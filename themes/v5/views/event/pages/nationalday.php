            <p>国庆户外旅游常见的损伤按<span class="color-yellow">创伤</span>类型分为<span class="color-yellow">皮肤</span>擦伤、撕裂伤、刺伤、异物插入、<span class="color-yellow">骨折</span>等;按照损伤的部位可分为头面部、颈部、胸部、腹部和四肢。在意外发生的现场，控制出血是非医疗专业人员所能做的少数几种影响后期救治效果的措施之一。</p>
