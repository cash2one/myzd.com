            <span class="round">压</span>
            <div class="text-content">
                <p><span class="step-header">压</span>：当看见伤口流血，最常做的急救动作就是用手按住出血区，这就是<span class="color-yellow">压迫止血法</span>。压迫止血法分两种：一种是伤口直接压迫，无论用干净纱布还是其他布类物品直接按在出血区，都能有效止血。另外一种是<span class="color-yellow">指压止血法</span>。用手指压在出血<span class="color-yellow">动脉近心端</span>的邻近骨头上，阻断血运来源，以达到止血目的。找压迫点时要用<span class="color-yellow">食指</span>或<span class="color-yellow">无名指</span>，不要用拇指，因为拇指中央有粗大的<span class="color-yellow">动脉</span>，容易造成误判断。当找到动脉压迫点后，再换拇指按压或几个指头同时按压。指压止血法虽然操作容易，但不经过系统培训，很难达到止血目的。</p>
            </div>
        </div>
        <div class="step">
            <span class="round">包</span>
            <div class="text-content">
                <p><span class="step-header">包</span>：无论什么样的出血，最终都要用<span class="color-yellow">包扎</span>来解决。包扎所用的材料是纱布、绷带、弹性绷带或干净的棉布或用棉织品做成的衬垫。包扎的原则是先盖后包，力度适中。先盖后包即先在伤口上盖上敷料(够大、够厚的棉织品衬垫)，然后再用绷带或三角巾包扎。这是因为常用的普通纱布容易粘伤口，给后续处理增加难度。力度适中指的是包扎后应止血有效，检查远端的动脉还在搏动;包扎过松，止血无效;包扎过紧，会造成远端组织<span class="color-yellow">缺血缺氧坏死</span>。</p>
            </div>
