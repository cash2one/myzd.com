                <div class="dis-desc">
                    指凡婚后夫妇有正常的性生活、未避孕、同居2年而未受孕的一种病症。近十几年来，关于不孕症在时间诊断标准上尚未统一，国内外关于婚后受孕时间的标准数据悬殊太大，曾在诊断不孕症的时限上有变动，如我国传统的不孕症诊断是：凡婚后夫妇同居3年，未避孕而未受孕称不孕症。近年来又有人主张：凡婚后夫妇同居1年，未避孕而不能受孕称不孕症。由于我国提倡晚婚，计划生育，故前者所定时间太长，拖延了治疗时机，而后者又因时间过短，不孕症的结论为时过早。因此，不孕症的时限标准定为2年比较符合我国国情。
                </div>
                <div class="dis-title"><i class="fa fa-star-o"></i>&nbsp;不育症(give not birth)</div>
                <div class="dis-desc">
