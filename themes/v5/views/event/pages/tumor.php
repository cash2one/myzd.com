                            外界致癌因素是引起癌症的重要刺激因素，大约80％～90％的癌症是由环境因素引起的。已知致癌因素有化学、物理、生物、营养等几种，较重要的有以下几项：
                        </p>
                        <p>
                            ① 吸烟与被动吸烟。肺癌病人中吸烟者是不吸烟者的10倍；吸烟者肺癌、喉癌、食管癌、膀胱癌、口咽癌的发病率也比不吸烟者高。吸烟量与癌症发病关系尚不明确，即使接触烟草的烟雾量不大也会发生癌症。近年来还发现，经常生活在嗜烟者烟雾环境中的不吸烟者，发生癌症的机会也多。
                        </p>
