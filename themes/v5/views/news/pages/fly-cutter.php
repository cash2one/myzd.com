                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">一方面，平台同医生、医院、患者签订三方协议，平台职责为信息发布、专家时间预约，床位信息对接，但不参与治疗过程的操作。一旦出现事故，由落地医院承担。同时，大部分专家出诊，专家自身也会关注术后患者恢复情况或者派专家团队成员进行24小时监护，落地医院每天会报告患者的各项指标。</span>
