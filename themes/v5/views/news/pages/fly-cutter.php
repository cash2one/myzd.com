                <p style="line-height: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;font-weight: bold;">名医主刀是一个将“手术”环节互联网化的平台。 &nbsp;</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">具体来说，平台像是一个名医经纪人，一方面，整合顶尖的医生资源，为其打造“名医工作室”，整合二、三甲医院和高端民营医院的空闲床位资源，为名医异地行医提供场所；另一方面，患者可以通过平台去预约专家，专家利用自己业余时间为其进行手术治疗。患者向医生、就诊医院支付手术、住院费用，名医主刀平台分别从中收取百分的“佣金”。</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">这种模式俗称“飞刀”，在看病难、三甲医院过热的医疗现状下，已经存在许久。传统模式下，患者通过熟人、基层医疗机构途径约到名医到当地进行手术。除去医院就诊费用，患者还需支付名医“飞刀”的费用。在“丁香园“网站 一项 &nbsp;3 0 0 0 多名医生参与的调显示示， 5 5 %的医生称”所在医院医生飞刀走穴普遍，近三成医生表明”曾“走穴”过。</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">长久以来，名医”飞刀走穴“并不合法，多是私下行为，医生获得的飞刀收入也被认为是其“灰色收入”，而其中存在的医疗责任问题很多也并不明确。如今，伴随着今年年初 《关于印发推进规范医师多点执业的若干意见的通知》的出台，医生多点执业成为医疗体制改革的重要一步，而医生“飞刀走穴”合法化似乎逐渐成为可能。</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">苏舒，名医主刀创始人，中国第一个移动“飞刀”平台尝试者。 8 7年生人，出身医生世家，毕业于新加坡国立大学电子工程专业，在硅谷一家创业团队做过、还在高盛新加坡做过三年投行业务。</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">&nbsp;</span><span style="font-family: 微软雅黑,Microsoft YaHei; font-size: 14px; line-height: 2em; text-indent: 2em;">2 0 1 4 年，由于外公被查处癌症，苏舒从高盛辞职，回国全程照顾。在这个过程中，他深刻体会了在中国就医有多难： 一个三甲医院的专家号需要等半年，看病却只有 5分钟，住院还要等 &nbsp;3个月的床位。最终，外公由于就医拖延离世，这个过程让他痛心疾首，并决心通过互联网的方式，不再让悲剧重演。在苏舒看来，彼时，移动医疗大火，但没有一个互联网医疗平台可以解决手术端的需求，互联网医疗服务没有真正落地。苏舒因此想要解决看病最后一公里的事情，“联系专家、安排专家出诊、解决医院床位，这三件事情，是我们专注做的。”&nbsp;&nbsp;</span>
                </p>
                <p style="line-height: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;font-weight: bold;">具体怎么做呢？</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">首先，医生端。苏舒之初是希望将三甲医院的主任、副主任医生均吸引进来，但之后他发现，看病不像打车，患者关注的是医生和平台的紧密程度，医生是否是业界顶尖，今年7月，他将方向改为建立100个名医专家团队。每个名医团队 5人，除去主刀名医，其他4人由专家自己选择，其中一人作为团队秘书长，同剩余3人负责同平台沟通、删选患者、协调医生就诊时间，同患者沟通病情、对接等，由名医主刀平台支付工资给团队。</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
