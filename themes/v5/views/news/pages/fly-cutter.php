                </p>
                <p style="line-height: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;font-weight: bold;">创业邦将其优势归结为三点：</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">第一，团队。苏舒的联合创始人有 3人， COO为南京医科大学校友会会长、医学博士（名字），首席医疗官是曾在上海华山医院有10几年工作经验的临床专家， CTO为苏舒大学同学，新加坡国立大学计算机专业毕业。目前，名医主刀团队共18人。</span>
