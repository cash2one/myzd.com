                </p>
                <p style="line-height: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">同时，同名医签订“独家协议”也是极为关键的一步。</span>
                </p>
                <p style="line-height: 2em; text-indent: 2em;">
                    <span style="font-size: 14px; font-family: 微软雅黑,Microsoft YaHei;">第三，顺势而为。国家多点执业政策的推进。“紧跟政策来，虽对现阶段步伐有制约，但这也是优势，可以抢先做，有时间窗口 。 ”</span>
