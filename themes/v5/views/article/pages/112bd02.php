		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我省开展新生儿聋病基因筛查（资讯）</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯 江苏省妇幼保健院昨日开展“新生儿遗传性耳聋筛查和诊断”培训，记者获悉，我省将在新生儿中推行遗传性耳聋基因筛查，以提前采取干预措施，避免发生耳聋或错过语言训练最佳时机。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据江苏省妇幼保健院副主任刘嘉茵介绍，我国每一千个新生儿中就有一位患有先天性耳聋，其中60％以上是由遗传因素引起的。还有许多迟发性听力下降与基因缺陷有关，这些基因缺陷可造成患儿对某些药物易感。如母系传递的线粒体基因突变，对氨基糖甙类药物极其敏感，只要用一针就可以导致耳聋。临床上常用的氨基糖苷类抗生素除了熟知的链霉素以外，还有卡那霉素、核糖霉素、庆大霉素、妥布霉素等近20种，一旦使用，极易引起永久性耳聋。如果新生儿筛查出有线粒体缺陷，早期远离这类抗生素，同时避免耳部外伤、噪声刺激和感染，可降低耳聋的发生率。据悉，我省将把遗传性耳聋的听力筛查和基因筛查结合起来，应用基因芯片技术，采集新生儿脐带血，对最常见的遗传性耳聋致病基因进行检测。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> WHO防聋顾问、江苏省人民医院耳鼻喉科卜行宽教授表示，卫生部要求设有产科和儿科的医疗机构开展新生儿听力初筛和复筛，一般情况新生儿出生48小时开始进行听力筛查，如果不达标，要在3个月内诊断，6个月内进行干预。</span>
</p>
