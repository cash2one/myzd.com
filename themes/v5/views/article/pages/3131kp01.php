<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">黄斑变性是与青光眼、糖尿病视网膜病变齐名，成为老年失明的三大元凶之一。数据统计显示，目前在全国50岁以上老年人中患病率高达15.5%，70岁以上患病率则达到20.2%。即50岁以上老年人中约每7人就有1人是老年黄斑变性患者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前老年黄斑变性的病因尚不清楚，但大量研究表明，老年黄斑变性发生可能与年龄、遗传、环境、吸烟等因素有关。女性比男性更易患病，50岁以上老年人、有糖尿病史、高血压史、高胆固醇血症人群都是老年黄斑变性的高发人群。因此，黄斑变性国际联盟建议55岁以上人群每年接受一次眼底检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;除了药物治疗之外，建议服用一些抗氧化剂，补充具有抗氧化能力和富含维生素C、维生素E的保健品，能防止自由基对细胞的损害，保护视细胞，起到视网膜组织营养剂的作用。</span>
