			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胆胰外科介绍</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 仁济医院胆胰外科有着悠久的历史传承，丰厚的文化底蕴，优秀的医疗团队，雄厚的临床实力，完善的科研体系。胆胰外科最早由我国外科学奠基人之一兰锡纯教授开创，历经邝耀麟教授、施维锦教授、吴志勇教授，2013年10月，仁济胆胰外科正式建科，王坚教授任科主任。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆胰外科是上海胆道疾</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">病会诊中心的所在单位、上海交通大学医学院胆道疾病诊治中心的牵头单位、上海交通大学医学院胰腺疾病诊治中心、仁济医院特色学科、仁济胆道恶性肿瘤学科群建设牵头单位、上海交通大学医学院疝与腹壁外科疾病诊治中心，2012年作为普外科的重要亚学科入选国家重点专科建设。 &nbsp;&nbsp;</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前胆胰外科拥有床位73张，在编医师23人，其中主任医师5人，副主任医师6人，博士生导师1人，硕士生导师6人，每年手术例数3000例，出院人数4000余人。拥有中华医学会外科学会青年委员1名，中华医学会胆道外科学组和脾脏外科学组委员各1名，中国医师协会胆道外科专业委员会常委1名，上海医学会普外科委员1名，上海医学会胆道外科学组和疝与腹壁外科学组副组长各1名，上海医学会胰腺外科学组委员1名，上海中西医结合学会外科分会副主任委员1名，委员3名。胆胰外科还与John Hopkins医院、 Mayo clinic医学院以及日本东京大学和名古屋大学等国际一流胆胰临床中心保持良好的学术交流关系。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">长期以来，我院胆胰外科在肝胆胰肿瘤的根治、肝内胆管结石的规范治疗、肝外胆道结石的微创治疗、胆胰肠结合部疾病的诊治、胆管损伤与狭窄的修复、胆总管囊肿的治疗、门脉高压合并胆道疾病的治疗和复杂多次胆道胰腺手术后的再手术处理方面形成特色。负责中华医学会胆道外科学组《胆胰十二指肠结合部损伤的诊治》和《急性胆道系统感染的诊断和治疗》两本指南的编写。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆胰外科目前能熟练开展各种肝胆胰超大型手术和复杂手术，包括肝右三叶切除、半肝联合尾状叶切除、联合半肝切除的肝门胆管癌扩大根治术、围肝门切除术、胆囊癌扩大根治术(联合肝胰十二指肠切除术)、十二指肠乳头肿瘤局部切除术、联合门静脉-肠系膜上静脉切除重建的扩大胰十二指肠切除术、全胰腺切除术、保留十二指肠的胰头切除术、胰腺节段切除术、保留脾脏的胰体尾部切除术、解剖肠系膜上动脉的胰颈部肿瘤切除术以及联合腹腔干切除的胰体肿瘤切除术等，同时胆胰外科拥有国内一流的胆道微创技术，能熟练运用腹腔镜、胆道镜和十二指肠镜对肝外胆道结石进行微创治疗，成功率在90%以上，能施行经皮经肝胆道穿刺(PTC)、逆行胰胆管造影(ERCP)、经皮经肝胆囊穿刺引流(PTGBD)和经窦道胆道介入治疗，每年完成胆道微创治疗1300例，并已开展腹腔镜下胰体尾切除、脾切除、肝部分切除和腹壁疝修补，每年行切口疝及各类腹股沟疝修补近600例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，胆胰外科努力提高胆胰肿瘤的根治性切除率，降低死亡率和并发症率。目前，胆胰肿瘤的根治性切除率和胆胰肠结合部损伤治疗存活率处于国内先进水平，每年会诊处理来自全国的疑难病例300余例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1989年创刊的由施维锦教授任主编至今的《肝胆胰外科杂志》是国内该领域最早的专业杂志，并且每年一次的“全国肝胆胰学术交流会”已连续举办了25届。近三年胆胰外科又成功主办了一系列国内和国际大型会议，如 “第14届全国胆道外科学术大会”和“第3、4届仁济肝胆胰外科论坛”，赢得全国同道的高度赞誉。同时，每年举办二个国家级I类学分学习班——肝胆胰外科新进展和新技术学习班以及腹股沟疝日间手术学习班，吸引大批全国学员参加，扩大了仁济医院胆胰外科在全国外科学术界的知名度和影响力。 &nbsp;&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆胰外科目前已形成胆道肿瘤、胰腺肿瘤、阻塞性黄疸和胆道动力学等系列科研方向，建立了临床标本库和数据库，开展了肝门胆管癌手术安全性与可切除性的评估、不同疝修补方式与疗效分析等各项临床研究。近五年来承担和已经完成的科研课题15项，包括国家自然科学基金2项，国家科技支撑计划课题子课题1项，上海市科委课题7项，上海市卫生局课题5项，总科研经费500多万元，五年来已发表SCI收录论文近30篇。在读博士研究生6名，硕士研究生7名。</span><br />
