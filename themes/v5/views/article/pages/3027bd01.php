		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>心脏起搏器迎来6.0时代：植入起搏器后能做核磁共振检查</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003753874125" title="" alt="demo.jpg"/>宿燕岗教授</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 68岁的王先生因心跳太慢，窦性心动过缓低于每分钟40次，必须安装心脏起搏器来恢复和维持正常的窦性心律。但是他同时还伴有其他慢性疾病，不能排除未来接受核磁共振检查的可能性。好在心脏起搏器进入了核磁兼容型起搏器时代。日前复旦大学附属中山医院心脏内科宿燕岗教授为王先生植入了由百多力公司研制的全身核磁兼容型起搏器。该起搏器相比传统起搏器除了核磁兼容外，还在体积和其他功能上都更胜一筹，在植入方法上与传统起搏器并无二致，手术取得圆满成功。将来王先生就可以安全地接受核磁共振检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宿燕岗教授介绍，全国每年新增7万多例心动过缓、传导阻滞的病人靠心脏起搏器维持心跳维系生命。心脏起搏器技术至今已有60余年，历经了无感知起搏器、单腔有感知起搏器、双腔有感知起搏器、频率应答起搏器、远程监测+频率应答起搏器、核磁兼容型起搏器、等多次技术飞跃，现在是全新升级的“心脏起搏器6.0时代”——核磁兼容型起搏器。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “快慢通吃”的常规方案</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 心脏起搏器是一种植入于体内的电子治疗仪器，很小、很轻，它时刻监控人体的心跳，必要时可以发出电刺激信号，以保证心脏按正常节律跳动，达到治疗由于某些心律失常（主要是缓慢性心律失常）所致的心脏停止跳动的目的。常见的起搏器分为单腔、双腔和三腔起搏器。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 除了治疗心动过缓以外，心脏起搏器还可运用于治疗心跳过快及心力衰竭等疾病。植入型心律转复除颤器（ICD）能全天候及时终止可致命的室性心动过速和心室颤动，有效预防心脏性猝死；而心脏再同步化疗法（CRT）是一种三腔（右心房+左心室+右心室）起搏器，通过优化心房-心室和左室-右室的起搏间期，使心脏协调收缩，从而治疗心衰，并能降低心衰患者的死亡率。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 合并其他心脏问题仍需规范治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 大多数情况下起搏器是为那些心率过慢而引起不适的人准备的，心脏停跳3秒以上或心率经常低于40次，尤其是出现眼前发黑、突然晕倒的患者，应该植入起搏器。宿燕岗教授指出，植入心脏起搏器只是针对一种心脏问题，如心动过缓，如果病人同时合并冠心病、高血压等时仍需要针对这些基础心血管疾病进行治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “解禁”核磁共振检查</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宿燕岗教授介绍，植入了起搏器的人完全可以像正常健康人一样生活，能够安全出入各种带有辐射的环境，如微波环境、拨打手机、机场车站安检、乘坐磁浮列车等，但如需接受MRI检查，则成为绝对禁忌证。这是因为传统起搏器和电极导线原有的材料和结构工艺在强磁场中会出现诸如发热、异常感知和起搏等现象，严重的可致电极头端发热灼烧心肌导致穿孔或使起搏感知异常导致的恶性心律失常，可致患者死亡。而核磁共振恰恰是通过强磁场对人体进行扫描诊断。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医学科技的进步带动了起搏器的一次次革新。现在通过对包括材料和工艺在内的一系列技术的改进，有了可以接受MRI的起搏器和导线，被称为核磁兼容型起搏器。有了这种起搏器，患者在植入后就可以有条件地安全接受MRI检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> MRI已被越来越多地应用到临床诊断中，尤其在退行性病变、软组织损伤、脑卒中、肿瘤等领域已获得了广泛认同并被列为了诊断的金标准。植入起搏器的多为中老年患者，是上述疾病的高发人群尤其可能需要MRI检查，所以患者对核磁共振兼容型起搏器的需求越来越迫切，这也注定了核磁兼容是未来起搏器的趋势。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 复旦大学附属中山医院心脏内科在葛均波院士的带领下，由宿燕岗教授领导的心脏起搏器小组年植入量已超过1000台，拥有丰富的经验和技术，软、硬件俱全，在国内外均享有很高声誉。</span><br />
