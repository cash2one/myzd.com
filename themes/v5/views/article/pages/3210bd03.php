<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胡海在健康报谈“三孔法隐瘢痕手术 胆囊切除新选项”</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着腹腔镜手术的不断成熟和手术器械的不断改进，各种径路的腹壁无瘢痕腔镜术式得以迅速开展。利用脐部的自然褶皱，经脐入路的腹壁隐瘢痕腹腔镜下手术得到了广泛应用，其中经脐入路的单孔腹腔镜下手术尤为突出。然而，由于单孔手术时手术器械容易互相干扰，加上手术视野较窄容易形成所谓的管状视野，使得这项技术的普及率受到较大影响。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在隐瘢痕的概念下，结合安全性、可行性、微创等要求，我们设计并开展了三孔法隐瘢痕技术。临床试验证实其既具有美容效果，手术难度及风险又明显降低，不失为易于临床推广的实用技术。在与经脐入路的单孔腹腔镜下胆囊切除术比较，我们认为三孔法隐瘢痕腹腔镜胆囊切除术几乎与常规腹腔镜胆囊切除术一样简单、安全、有效。两种隐瘢痕手术的实际切口总长度是基本相同的，单孔法是将25mm的单个切口隐藏于脐部，而三孔法是将1个10mm切口和2个5mm切口分别隐藏于脐部和阴毛内，其结果同样达到了腹壁无可见瘢痕的目的。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在三孔法隐瘢痕腹腔镜胆囊切除术中，我们体会到：1.只需在常规腹腔镜操作设备（普通5mm镜头）的基础上增加一个加长抓钳，无需增加其他特殊器械，所以较易推广。 2.三孔法的操作难点主要是胆囊管的处理，我们的经验是可采用先游离胆囊，后用圈套器套扎后离断，亦可以用hem-o-lok夹闭胆囊管先离断后再游离胆囊床。我们早期采用圈套器法，后期采用hem-o-lok法，后一种方法更具有安全性和易学性。3.脐部戳孔由于胆囊由脐部取出，故可置入10mm的trocar，由转换器置入5mm的镜头。下腹部由于只进入5mm镜头和5mm的手术操作器械，因此仅需置入5mm的trocar。4.下腹部两处戳孔都要在脐部内镜直视下穿刺，避免伤及下腹部肠道及腹壁小血管。5.戳孔位置的选择较为重要，三点戳孔投影到胆囊点要形成合适的夹角，容易形成比较舒服的腔镜操作空间立体感。6.在病例的选择上，尽量选择慢性或急性发作在72小时内的胆囊炎患者，而非萎缩性胆囊炎。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 总之，较之单孔法，三孔法隐瘢痕腹腔镜胆囊切除术是一种更为安全、简单、有效和美容效果好的腹壁隐瘢痕手术。&nbsp;</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">自2007年5月以来，我们先后为100多名患者成功施行三孔法腹腔镜胆囊、阑尾、疝气隐瘢痕切除术。其中60名女性胆囊病患者做了经脐经阴毛胆囊切除，平均手术时间25分钟，术后疼痛远比单孔经脐要轻，没有发生一例并发症。不仅病人对手术及美容效果满意，也引起了同行关注。</span>
