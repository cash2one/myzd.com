	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>认识椎管内神经鞘瘤</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">神经鞘瘤（Nerve&nbsp;Sheath Tumor)是起源于Schwann’s鞘的良性肿瘤，起病缓慢，多在中年时发现，占所有脊柱肿瘤的25%，以胸段为最多见，其次为颈段和腰段。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">【临床表现】&nbsp;&nbsp;一般神经鞘瘤发生于后根，多首先表现为单侧的根性痛。病人表现为不同程度的颈部及上肢疼痛、麻木感及放射痛，休息时不能缓解。肿瘤增大时可呈串珠状或哑铃状出现，发生于神经根出神经孔处，引起神经根压迫的相应症状。巨大神经鞘瘤可侵蚀椎体及侵及椎旁软组织。当肿瘤进一步增大压迫脊髓可出现脊髓半切综合征。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">【影像学检查】</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（1)X线片&nbsp;&nbsp;可见有60%左石的椎管内肿瘤可引起骨质改变，肿瘤压迫可使椎弓根内缘骨质吸收变薄或凹陷，两侧受累呈括弧状变形，重者可完全破坏。椎弓根距离增宽，即椎管的横径增宽。肿瘤向外延伸或发生神经根压迫时，可使椎间孔扩大，在斜位片可显示出来。当侵蚀椎体引起严重破坏时可出现病理性骨折。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（2)CT扫描&nbsp;&nbsp;CT扫描能显示肿瘤邻近组织关系，骨质破坏情况。CTM能在一定程度上反映脊髓受压情况。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（3）MR检查&nbsp;&nbsp;MR所见显示90％以上的神经鞘瘤位于椎管后外侧。其中居于硬膜下的占67％，骑跨在硬膜内外呈哑铃状的占17％，完全位于硬膜外者占17％。肿瘤通常仅数厘米，但位于腰骶段或盆腔者可长得很大。大的肿瘤容易囊变、出血，肿瘤易切除，无复发倾向。矢状面与轴面上神经鞘瘤在T1加权像上呈髓外低信号瘤灶，在T2加权像上呈高信号，脊髓被髓外肿瘤压迫变形移位。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">【治疗】</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">唯一的治疗是手术切除，根治性整体切除术应为手术方案选择的首选。单椎节内神经鞘瘤容易摘除，多椎节内的神经鞘瘤范围大，出血多，分界线不清楚不易全部切除，可以先部分切除肿瘤，减小肿瘤体积后，进一步将肿瘤与神经性成分分离。对于侵犯椎旁软组织的肿瘤，由于肿瘤可能没有完整包膜并富含血管，切除时可先暴露出肿瘤的上下两极的硬膜，明确解剖结构后再进一步切除。神经鞘瘤切除后易于复发，有时需多次手术。</span><br />
