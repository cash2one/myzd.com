<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 37岁的余先生来自河南，一年前出现腰部疼痛，当时没有特殊治疗，后来腰疼逐渐加重，半年前又出现双侧髋部、臀部及大腿发木，而且还伴有大便干燥，排尿费力、尿频尿少，咳嗽时尿失禁等症状，这时余先生才到当地医院检查，结果发现他的脊柱下端骶管内有个巨大的囊肿。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">因为囊肿巨大，手术有很高的风险，余先生来到北京三博脑科医院找到了脊髓脊柱专家范涛教授。范涛为他制定了周密的手术方案后，在神经电生理监测下，成功切除了囊肿。据范涛介绍，患者的囊肿位于脊柱骶管内，体积很大，囊肿的上方达第五腰椎上缘，下方在第五骶椎下缘处，骶管背侧骨质已经受压变薄，神经根压迫明显，且大部分被挤压在囊肿右侧，少部分在囊肿腹侧，粘连紧密，手术难度和危险性都非常高。</span> 
