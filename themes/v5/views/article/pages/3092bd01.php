<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 本报讯 （李亚静）近日，首都医科大学附属北京三博脑科医院成功切除了一例长在脊柱骶管内的巨大囊肿，囊肿大小约10厘米，在临床上很罕见。目前患者已经可以下床活动，症状明显好转，正在逐渐康复。</span> 
