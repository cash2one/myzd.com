			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腿部吸脂前后的注意事项</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们都知道腿部吸脂术可以帮助我们瘦腿，目前受到了很多人的喜爱。那么术前术后都需要注意什么呢？下面就来详细的为大家介绍一下。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腿部吸脂术前注意事项</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、手术前3天受术者应每天洗澡。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、术前停止正在用的药物、维他命及其他营养品，以防影响伤口愈合，增加术中出血；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、术前停止吸烟二周，吸烟降低血中氧气的浓度，影响伤口的愈合；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、术前24小时内停止饮酒，饮酒加速血液循环增加术中出血。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腿部吸脂术后注意事项</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、超声治疗：超声是通过热和非热效应，促进伤口愈合。超声的4种治疗效应是：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">①刺激组织愈合；②缓解术后疼痛、痉挛；③促进血肿吸收；④增加结缔组织可塑性，即软化硬块。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">超声对整形外科患者的术后水肿、创伤、粘连、瘢痕组织，以及肌肉分离治疗等均有益处。术后2~3天可开始进行超声治疗。治疗时间和剂量是不同的，取决于患者和治疗处。用超声器慢慢地在皮肤上移动（每分钟为155cm2），可持续3~5min，连续地治疗，每次可减少剂量，每周可进行3次。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、心理护理：心理护理包括术前和术后心理护理。如术前心理工作做得完善，术后受术者的心理状态稳定，对各种并发症的发生有心理准备，康复就会迅速。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、美容辅导：美容辅导是心理康复的主要组成部分。术前应说明术后发红、瘀斑、肿胀等常常需要3~4周或更长的时间才可消退，术后3~4周可恢复正常活动。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、避免阳光暴晒：手术后避免阳光暴晒4~6周，因为阳光可延迟伤口愈合和正常色素的恢复。手术后6周方可进行日光浴。手术后至少2个月应避免穿暗色游泳衣。</span><br />
