	<div align="center">
	<strong><span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;">湘雅医院团队成功切除世界罕见小儿颅内巨大肿瘤</span></strong><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<img src="<?php echo $baseUrlImage; ?>article/doctor/3168/1.png" title="" alt="demo.jpg"/><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中南大学湘雅医院小儿神经外科主任刘景平教授检查患儿情况</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 两岁幼儿颅内长有巨大脑膜瘤，肿瘤组织占据2/3颅内空间，为世界罕见。中南大学湘雅医院专攻小儿神经外科的刘景平教授率团队，先后通过两次手术，成功为患者全部切除了颅内肿瘤。术后检测，患者肿瘤总重量达1.1千克。患儿术后恢复顺利，无神经功能损害临床表现。通过检索近10年文献发现，如此巨大的幼儿颅内肿瘤切除术，国内外尚未见相关报道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 两岁幼儿颅内巨大肿瘤占颅脑2/3空间</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 患者余天(化名)，今年2岁，2013年9月出现“头围增大、行走不稳”等情况。随后，患者被父母带至广东省某大型医院求医。行头部磁共振检查发现：患者颅内巨大占位，肿瘤组织占据了整个颅内约2/3的空间，将正常脑组织压缩至原来的1/3。经查体后诊断为：颅内巨大脑膜瘤。患儿因年龄较小，耐受性较成人好，但各种高级中枢、脑干已严重受压，颅内情况已达临界点，随时有可能出现昏迷，甚至呼吸、心跳停止。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 四处求医均因“手术难度极大”导致无果</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 由于患者随时有生命危险，当地医院决定对患儿立即进行手术。但术中情况不容乐观。在取完骨瓣，取小部分肿瘤组织活检后，患者失血已超过1000毫升，这对于一个年仅两岁、体重15千克的患儿来说，其危险可想而知。术中患儿出现循环血量不足表现，医生当即决定终止手术。术后患儿右侧肢体肌力4级，较术前有下降。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 手术失败并未让患者父母退却。他们辗转北京，带孩子就诊于多家大型综合医院请求手术治疗，但均因“患儿颅内肿瘤体积过大、而年龄太小、手术难度极大”而建议保守治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在湘雅医院找到“救星”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 正当患者父母几乎绝望的时候，有朋友推荐中南大学湘雅医院神经外科。2014年2月，患者一家来到湘雅医院，找到了小儿神经外科主任刘景平教授。刘景平教授在对患儿详细的病史询问、体格检查，并仔细阅读了患儿的磁共振结果后认为：患者可以手术，但其颅内脑膜瘤体积巨大、血运丰富，加之患者本身年龄小，手术风险极大。“术中若稍有不慎，便可引发患者肢体活动障碍，偏瘫等后遗症，重则意识障碍、昏迷，危及生命”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 如何既要成功完成开颅手术又要避免后遗症，刘景平教授组织小儿神经外科团队，对患者病情进行了认真讨论、缜密分析。在充分考虑了患儿年龄及肿瘤特点后，制定了分两期手术的治疗方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 湘雅专家为患者成功全切肿瘤</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2014年2月25日，小儿神经外科刘景平教授、赵杰副教授等专家团队，历经5小时，将患者肿瘤大部分切除。考虑到术中出血量及患者自身身体状况，决定二期手术再行肿瘤全切。一期术后患儿一般情况可，经护脑、护胃等对症支持治疗后，于3月18日行二期手术，对肿瘤进行了全切除。手术对患者血管、神经保护良好，尤其对矢状窦达到了完全保留的水平。“患者术后恢复顺利，无神经功能损害临床表现。”湘雅医院小儿神经外科主任刘景平教授告诉记者，两次切除的肿瘤总重量达1.1Kg，占患儿体重的1/13，“患儿原来狭长的头颅外观也得到良好的矫正”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据刘景平教授介绍，脑膜瘤是一种常见疾病，人群发生率为十万分之二，占全部脑肿瘤的近20%。该病成年患者较多，老年与儿童较少，婴幼儿更少。两岁幼儿长有如此巨大的肿瘤，实属世界罕见。“此次余天的成功救治，体现了湘雅医院在小儿神经外科领域的综合救治能力”。</span>
</p>
