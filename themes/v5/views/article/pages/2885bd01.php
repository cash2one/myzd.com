			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>手术后，3男童终能正常排尿</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/2885/1.png" title="" alt="demo.jpg"/></span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 三个不同地区的孩子，却因为患有“尿道下裂”而聚在云南九洲医院。7月4日，来自上海交通大学医学院附属仁济医院教授、主任医师叶惟靖成功为这三个孩子进行了手术。不久的将来，这三个孩子就能像正常的男孩子一样，自信地“嘘嘘”了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">12岁男孩上厕所感觉没自信</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7月4日9时，记者来到云南九洲医院看到了准备当天下午手术的小亮（化名），他跳来跳去，没有一丝惧怕，但却不太爱讲话。小亮的爸爸杨志寿告诉记者，他们来自宜良，小亮今年12岁。在他几个月大的时候就发现他排尿时不太对劲，不是往前射出，而是向旁边洒出。但那个时候认为他年纪太小，不适合做手术，于是就没有带他就医。后来，随着他一天天长大，渐渐发现他没有小时候活泼了，而且他也曾问过家长为什么他尿尿与男同学不一样，家长也都没法解答。直到现在，才把他带到昆明来看，一检查才知道是“尿道下裂”。而与小亮有着同样遭遇的还有来自曲靖8岁的小枫（化名）和来自昆明13岁的小强（化名）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2岁以前手术是最佳时期</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当这三个小男孩分别来到云南九洲医院后，考虑到三个小男孩病例的特殊性（13岁的小强曾在北京儿童医院做过手术，但未能治愈），该院立即请尿道下裂手术专家、上海交通大学医学院附属仁济医院叶惟靖教授进行会诊。叶惟靖教授了解了三个孩子的情况后，决定亲自为他们做手术，并于7月3日飞到昆明。据叶惟靖介绍，尿道下裂是男孩的一种先天发育不全疾病，发生率较高，约4／1000。它的一般类型有四种，阴茎头型：最为常见，畸形较轻，尿道口位于包皮系带部，尿流仍可向前。阴茎体型：尿道口可位于阴茎体腹侧任何部位，阴茎有不同程度的向腹侧弯曲畸形。阴茎阴囊型：尿道口位于阴茎根部与阴囊交界处，阴茎严重弯曲畸形。会阴型：尿道口位于会阴部，阴茎极度向腹侧弯曲。这三个孩子所患的尿道下裂属于一类型的，即阴茎体型（在阴茎上开裂）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其致病的直接原因是胎盘内绒毛膜促性腺激素的分泌不足，但间接的原因很多，有遗传、多做人工流产使子宫受损等等。有的家长认为，孩子太小不适合做手术，而实际上，这种手术的最佳时期是18个月左右，或2岁以前。因为，2岁以前，生殖器还没有成型，这个时候修复好，基本不会影响到生殖器的正常发育，如果年龄大一些，有的就会发生弯曲，发育偏小，对婚后的性生活有很大影响。并且，孩子在3岁的时候就有性别区分的能力，随着年龄的增长，会对他的心理造成不同程度的影响。年龄越大，心理影响越重，有的直接形成心理疾病。因此，家长们一定要留心最佳治疗时期，当发现孩子阴茎发育、尿尿异常的时候，一定要及时带孩子到医院就诊。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三个孩子可正常排尿</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上午9点半，8岁的小枫被带入手术室，他是这三个孩子当中第一个接受手术的，也是年纪最小的一个。叶教授选择用国际上比较流行的 Snodgrass法为其手术。他说，这种手术方法1994年就在美国广泛应用，2002年被引入我国。其原理是将开裂的输尿管重新卷成一个完整的管子缝合。这种方法既简单成功率又高，在90%以上。很少有手术成功后又瘘的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">10：50，小枫的手术结束。10：50小亮的手术开始，并于13：00结束；14：30小强的手术开始，并于16：35结束。术后，叶教授宣布三个孩子的手术都很成功，可立即通过术后的尿道正常排尿，只不过会有一些疼，疼痛会随着时间的推移逐渐减轻至消除。10天内，如果不出现异常就可以出院了。恢复期间要穿宽松的裤子，不要碰到，少吃辛辣食物。出院后1-2个月内需来复查。如果恢复得好，对成年后的性生活基本没有影响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据云南九洲医院杨焕南院长介绍，九洲医院与具有上海十八家三甲医院专家资源的上海男性病专家会诊中心有着密切的学术交流，并于2005年3月成为上海男性病专家会诊中心定点会诊医院，上海专家每月都会到九洲医院进行坐诊、会诊、学术交流，让云南患者不出家门就可以享受到上海专家的治疗服务。此次，叶惟靖教授到九洲医院为三个小孩进行尿道下裂手术属于两地专家的正常交流。</span><br />
