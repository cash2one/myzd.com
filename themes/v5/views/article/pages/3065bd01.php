<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>清华长庚医院姜泊获2015何梁何利基金科学与技术进步奖</strong></span> 
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 11月4日，2015年何梁何利基金科学与技术奖颁奖典礼于北京钓鱼台国宾举行，北京清华长庚医院内科部部长、消化内科主任姜泊教授荣获何梁何利基金科学与技术进步奖。</span><br />
<img src="http://static.mingyizhudao.com/147004073602279" title="" alt="demo.jpg"/><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 何梁何利基金由香港爱国金融家何善衡、梁銶琚、何添、利国伟于1994年创立，旨在奖励中国杰出科学家。今年共有52位优秀科技工作者获得此项荣誉，其中医学领域仅有8位。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 姜泊长期从事大肠癌早期诊治的临床与基础研究，研发建立的大肠癌早诊早治的关键技术，解决了长期以来我国早期大肠癌和平坦型肿瘤内镜下难以发现并导致的漏诊问题，发现并报道了大肠侧向发育型肿瘤和锯齿状腺瘤，带领课题组建立了世界第一株大肠侧向发育型肿瘤细胞株，使我国内镜下早期大肠癌及癌前病变诊治水平明显提高。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">继续临床与医疗科研，姜泊已组建北京清华长庚医院消化中心团队，从日本、美国以及国内知名三甲医院引进精英人才，深入开展社区早癌筛查项目，完成国家科研项目——肠道微生态系统研究等。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 专家介绍：</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 姜泊：清华大学北京清华长庚医院消化中心主任; 中华医学会消化内镜分会副主任委员; 中华医学会消化病学分会常委，清华大学医学中心教授; 南方医科大学南方消化疾病研究所所长;广东省胃肠疾病重点实验室主任。大肠癌早期诊治，胃肠道微生态系统与肠粘膜屏障，炎性肠病和功能性胃肠疾病等疾病的研究，内镜下诊治早癌及癌前病变。（作者/北京清华长庚医院消化内科 汤小伟 特约主编/韩冬野）</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 相关阅读：</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 探索肠“道” 意气风发　——记北京清华长庚医院消化内科主任姜泊</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 有一条“道”，在人体里蜿蜒，它溶解着外来的一切，供应着全身的力量。这条“道”上有数不尽的成员，它们是“道”的维系者，也会是“道”的破坏者。这条“道”便是人的胃肠道。1984年被分到消化内科，他便走上了这条“道”，30年的人生里，埋头钻研，镜下微观，因此成为胃肠道疾病诊治的翘楚。他是北方人，却在南方贡献了青春，曾任南方医科大学南方消化疾病研究所所长，现在他荣归故里，担任清华长庚医院内科部部长、消化内科主任，他叫姜泊。</span><br />
<img src="http://static.mingyizhudao.com/147004074018844" title="" alt="demo.jpg"/><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南下北上的3段人生</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 1982年，姜泊毕业于白求恩医科大学，正赶上国家强化军队医疗，一车皮把连他在内的40位医学毕业生拉到了中国人民解放军总医院，他成了一名正式的军人。一心想成为一个外科医师的他，在军队一切行动听指挥的环境下，被分配再分配，姜泊开始了他从医生涯的第一段——与消化内科结缘。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 1987年，岳父被确诊为结肠癌，这正是姜泊的专科医疗范围，于是，岳父成了他接手的第一位肠道癌症患者。两次化疗，老人的身体每况愈下，姜泊决定运用临床所学，采取保守疗法，因此成功维系了岳父的健康至今。“因为早年父亲患肺癌去世，我走上了从医这条路，我的岳父患上大肠癌，则让我开始关注并钻研早癌筛查。”硕士毕业后，他报考了在大肠癌研究方面具有卓越影响力的周殿元教授，并以第一名的成绩被录取，由此开启他南下的第二段人生。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 博士毕业，姜泊被留在了南方，开始了他在南方奋力攀登的22年时光。作为新入人才，从驻守基层到临床、科研，姜泊接受了全面训练。在平均海拔4000米的青藏高原上，他驻守了一年，在各个兵站部之间巡诊。“我政治生涯的洗礼是在青藏线上。”青藏高原上的官兵最常见的肠胃疾病是幽门螺杆菌感染和溃疡性出血，为此，他频繁地穿梭在各个兵站部，采集检体，最终以此做成科研课题，获得了军队科技进步二等奖，并荣立了三等功。凭己之力，姜泊为青藏高原兵站捐送了一套内镜，那也是青藏兵站部医院的第一套电子内镜，让驻守高原的官兵确诊肠胃病有了支撑。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 从青藏高原回到临床，术业有专攻，姜泊一心扑在了大肠癌早期筛查的研究上，建立大肠癌诊治关键技术平台，确定大肠癌高危人群快速标定方法；建立大肠镜下诊治大肠肿瘤及早期系列平台；研发结肠胶囊机器人，推广临床应用。在“胃肠道”的临床、教学、科研上，攀着执着、勤奋的梯子，姜泊逐渐由一位主治医师，到执掌南方医院国家重点学科的学科带头人——消化内科主任和南方医科大学南方消化疾病研究所所长。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 无论工作如何繁忙, 家庭团聚仍是他的心病。自2004年原工作单位集体转制后，他的夫人和女儿先后回到北京工作，当清华大学新建医院——北京清华长庚医院向他伸出橄榄枝时，他说自己也算是圆满地完成了在南方的工作责任和医疗任务，在新平台的吸引和家人的期盼下, 他决定接过清华长庚这一棒，再创辉煌。他说：“这是人生的又一挑战。”</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 意犹未尽的肠道研究</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 在南方，姜泊率领团队将消化内科打造成了国家重点专科，他钻研的早癌筛查等项目也已收获从国家科技进步奖和多个广东省科技进步奖等各层面的大奖。来到清华长庚，梦想还有很多，他一口气就说了四个。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 清华长庚立足于号称亚洲最大的社区——天通苑，这也是姜泊看好的研究阵地，“做成社区早癌筛查这个大项目”是他首先提到的目标。继续早癌筛查，他希望能让老百姓用更实惠的价格刹住肠道癌症的闸。与此类似，炎性肠病如溃疡性结肠炎、克罗恩病，发病率逐渐上升，贴着“终身免疫性疾病”、“致残性疾病”的标签，让人不寒而栗，“建立临床患者群、长期随访制度，对患者安排个性化治疗和随访，保证稳定的疾病静止期状态。”每个梦想，姜泊都有一个明确的目标。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 来到清华长庚近一年时间，姜泊成功立项了国家卫计委的科研项目——肠道微生态系统研究，在国内这已是一个炙手可热的科研方向。作为人体最受瞩目的系统，胃肠道与外界相通，每种疾病都牵涉到胃肠菌群的变化，消化道肿瘤、肝脏疾病等，每一项拿出来都单独成病，而肠道微生态环境的改变就牵连着这诸多要人命的重症。为此，他联盟十几家单位，正在展开全国调研，“胃肠道是人生活的依靠，菌群则是胃肠道的依靠，”在前面的人生里，姜泊作为胃肠道研究的领军人物，对菌群中的成员进行了针对性研究，这一次他瞄准了整体，目标就是冲刺“建立肠道微生态诊治规范,，确立经济适宜的系列诊治技术。”</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 作为清华长庚医院优先发展专科，消化内科从空间到设备都走得超前，内镜中心配备了代表国际最先进水平的内镜，同时科室胃肠功能和动力紊乱等疾病诊治平台，也齐备了胃肠道动力检测设备、肠道疾病生物反馈治疗系统等。因此，他带领团队确立的第四个大方向就是把功能性胃肠病治好，最常见的如胃食管反流，功能性便秘，肠易激综合征等，这些疾病看似非重症，却深深地影响着患者的生活品质。而这个目标，姜泊已经带领他的团队走在了实现的路上。</span><br />
<img src="http://static.mingyizhudao.com/147004074034181" title="" alt="demo.jpg"/><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> “意识决定技术”</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> “我职业生涯的洗礼是在日本。”为了深入研究大肠癌，2000年，姜泊第一次到日本做访问学者，在一家规模不到500张床的医院，他见证了他的老师——工藤教授做出世界顶级水平的业绩。此后的每年他几乎都会去，他说他带回来最宝贵的是一股子精气神。“能够发现早期癌，是日本学者那种研究细微病变的专心、细心，那种坚韧不拔一定要发现的决心，这种意识就决定了技术。”</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 他凭借着这股精气神，带领课题组建立了世界第一株大肠侧向发育型肿瘤细胞株，是迄今为止最早期的大肠肿瘤癌前病变；带出了享誉全国的大肠癌、炎性肠病、胃肠微生态三支团队，将南方消化内科的发展带向了一个高峰，“是该交接的时候了。”而交接不是隐退，这股精气神像一簇不熄的火苗，姜泊将它带到了清华长庚，点燃了一支崭新的团队。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">说起手下的医师，他如数家珍一样，从日本、美国引进的海归人才，从协和招募的年轻而勤奋的医师，他组织了自己的“新家庭”。从顶尖的专科到一个全新的医院，要再带出一个全国顶尖的团队，“我有精力和信心做这件事。”姜泊坚定地说。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 走进姜泊的办公室，目之所及几乎都是书，或者证书，他将青春献给了临床科研攻关，所学所得，他也必将投入到医学教育当中去，“定期文献读书报告会”，重点培养团队的学术思维；利用查房，培养年轻医师的分析能力；联袂解决临床疑难病例，养成医师的职业价值观；“生命为上，这是医生最基本的职业道德。”这一点他以身作则，在消化内科的临床诊断上他将手把手教学。</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> “我非常喜欢医生这个职业，对于不满足现状的人来说，医生不啻于最好的工作。”在胃肠道诊疗和科研的这条道上，昨日的辉煌已放下，不满足的姜泊带领着他的新团队，向着更高的阶梯攀登，在胃肠道的探求路上，他意犹未尽，他们意气风发！（作者/韩冬野） &nbsp; &nbsp;</span> 
</p>
