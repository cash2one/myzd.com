			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>甲状腺手术不再留疤痕</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯（通讯员杜巍巍）以往甲状腺手术后，患者脖子上会留下一条触目惊心的疤痕；患者往往难以面对“抹脖子”、“自杀”等诸多猜疑。武汉大学人民医院专家采用先进的超声刀技术，将6至8厘米的拉链式创口化于无形，可使患者免除这一尴尬。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 40岁的王女士是高校教师，最近她总感觉憋气，颈前长出一肿物，被诊断为结节性甲状腺腺瘤，需手术治疗。王女士目睹了以往病友术后颈部留下的疤痕后，对手术一直犹豫不决。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 武汉大学人民医院耳鼻喉科华清泉教授解释，甲状腺腺瘤的高发年龄为25岁至45岁，女性多于男性。常规的外科甲状腺手术要切开颈部，术后颈部会留下6-8厘米的“自杀状”疤痕，患者往往“治了身病落下心病”。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经多次会诊，华清泉决定为其采用先进的“钥匙孔＋超声刀”术式。术中，医生在王女士胸骨上凹隐蔽处切开一个1厘米的小切口，然后运用超声刀在甲状腺周边进行“扫除”，把病变的结节统统切除，再将废弃组织从“钥匙孔”中排出。整个手术耗时1个多小时，手术刀口基本无出血。华清泉介绍，由于切口极小且藏于皮肤纹理中，术后即使近观也不易察觉，满足了患者对于微创和美观的两大要求。</span>
</p>
