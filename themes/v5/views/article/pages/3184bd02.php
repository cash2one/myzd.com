			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肿瘤微创治疗国际研讨会上海举行:破坏性治癌将成历史</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中新网上海11月8日电 (陈静达利敏)正在此间举行的“第十届中国肿瘤微创治疗学术大会”暨“第一届亚太影像导引下肿瘤微创治疗学术大会”上，自美国、日本、意大利、韩国、中国香港和台湾地区的知名学者聚焦肿瘤微创与综合治疗新理念：破坏性治疗将成为历史，而以微创与生物免疫治疗为核心的综合治疗有望成为未来肿瘤治疗的重要发展方向。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着现代医学影像学的进步，影像引导下的微创介入治疗技术以及生物免疫治疗发展突飞猛进。实体肿瘤治疗理念也在发生革命性的改变。当下以破坏性治疗为主导的治疗模式正逐渐向“建设性治疗”模式转变。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 本届大会执行主席、上海仁济医院专家翟博告诉记者，过去，对于实体肿瘤，如肝癌、肺癌、乳腺癌等的治疗，医学界以手术切除、放化疗等大面积破坏性治疗为主；而现在业界日益认识到，以微创与生物免疫治疗为核心的综合治疗，将大大避免为患者带来大创和巨创。这种建设性治疗方式未来将逐渐承担重要角色。他说，对肝癌中晚期、已失去手术机会的患者而言，接受微创介入法治疗，其3年生存率可大大提高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 亚太影像导引下肿瘤微创治疗协会共同主席吴沛宏教授8日接受记者采访时，也谈及肿瘤治疗的理念。吴沛宏说，对于一些实体肿瘤，患者常听到医生言及“根治术”，医学科技发展至今，“根治”的概念也不同以往的局部、区域或者全身灭活肿瘤系组织；联合免疫生物治疗“消灭”看不见的病变组织才可称为“根治”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据了解，当前，在发达国家和地区，微创治疗技术发展很快。本届大会上，美国专家同与会者分享了最新的“纳米刀技术”，即，不可逆电穿孔技术。吴沛宏介绍，纳米刀技术可实现以高电压选择性打开肿瘤细胞外壁的纳米孔，使之凋亡，而后诱导免疫系统吞噬凋亡的细胞。他说，该技术的最大优势在于在治疗肿瘤的过程中，保护正常血管、神经和管道系统。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，纳米刀技术已获得美国FDA批准用于临床治疗，中国仍在审批中，大陆地区目前正将此技术运用于动物实验。</span>
</p>
