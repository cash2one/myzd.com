		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腿软像面条原是先天颈椎后凸畸形作怪</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3072/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一比一的3岁女孩颈椎模型。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为提高手术精准度，医生术前用特殊仪器制作出一比一的女孩颈椎模型进行试验</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯 （记者饶贞 通讯员李冰洁、谢银龙摄影报道）因为罕见的先天颈椎畸形，一位3岁女孩从出生8个月起就无法向右侧旋转头部。2岁后的一天，刚学会走路的她突然无法站立，两条腿软得就像面条一样。辗转了多家医院后，女孩的家人带着她前来广州求医。由于病情十分罕见，广州的颈椎专家通过网络与美国颈椎专家一起对女孩进行了会诊，最终确立手术方案，成功挽救了女孩的生命。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3岁女孩</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">先天颈椎后凸畸形</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据广州军区广州总医院脊柱外科副主任医师艾福志介绍，3岁女孩芳芳出生8个月左右时，家人就发现她的脖子后出现一个硬结，而且伴有压痛。刚开始家人并没有特别在意，但不久后他们发现孩子无法向右侧旋转头部。女孩直到2岁多才学会走路，而且比同龄人走得都要慢。2岁多的一天，女孩突然不肯下地走路，两条腿软得仿佛面条一般，站也站不住。到医院检查后，医生发现孩子患的是一种十分罕见的先天颈椎后凸畸形。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">由于手术风险高、难度大，在当地医生建议下，芳芳的父亲带着女儿前往广州军区广州总医院脊柱外科求医。为了尽全力救治这个小生命，艾福志决定邀请美国专家一起为芳芳进行会诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">艾福志首先将芳芳的资料发送给美国颈椎外科学会主席、圣路易斯华盛顿大学邦斯医院著名颈椎外科专家丹尼尔·瑞尔，请他会诊，看看双方能否共同制定一个较安全有效的手术方案。“瑞尔教授很快提出了自己的治疗意见，但他同时表示如此小年龄的小儿先天性颈椎发育不良引起的颈椎后凸畸形手术，他本人也没有碰到过。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在瑞尔教授的推荐下，艾福志很快又与美国另一位权威小儿颈椎外科专家、宾夕法尼亚大学小儿骨科的约翰·多曼斯教授取得了联系，多曼斯教授也给出了一些治疗意见。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">用钛板</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">固定女孩颈椎</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">就这样，在短短几天时间里，中美两国医生经过十多封电子邮件的交流，共同为小芳芳制定了一个手术方案。为了提高手术的精准性，艾福志还使用最先进的数字骨科技术，在电脑上制作出芳芳的三维颈椎模型，并通过特殊的仪器，制作出一个和芳芳的真实颈椎一样大小的三维模型。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“由于孩子的年龄实在太小了，我们找不到专门用来固定这么小孩子颈椎的钛板。在美国专家的建议下，我们将平时用于固定成人四肢的钛板一个一个在这个模型上试验，最终发现平时用来固定成人脚指头的小钛板最适合用来固定芳芳的颈椎。”</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5月8日，经过6个小时的手术，芳芳被平安推出了手术室。经过一个多星期的康复，芳芳已经能抬起小手和小脚，并且很快就出院了。</span>
</p>
