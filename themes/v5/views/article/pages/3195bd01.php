		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>&nbsp;赵元立：救人为乐（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">早上7点，北京天坛医院住院部大楼就已挤满了人。赵元立忙碌的一天拉开了序幕。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">当医生完全是赵元立自己的选择。1988年，协和医科大学成为他理想的起点。学医是件苦差事。“哪块肌肉在哪，哪块骨头怎么长，都要背下来，那时天天看书看到夜里一两点。”赵元立说，“虽然辛苦，但这和能治病救人的快乐相比，就不算什么了。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">8年后，拿到博士学位的赵元立没有像大多数同学那样选择出国，而是接过了首都医科大学附属北京天坛医院伸来的橄榄枝。他希望自己能够尽快积累临床经验，为病人服务。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">起初，赵元立也只是个跟在老医生身后的“小尾巴”。“当医生需要经历学习、见习、实习等等许多过程才能逐渐独立，没有捷径可走。水平再高再聪明的新医生，也比不上在工作中积累了一些经验的医生。但是，个人的努力也很重要。”赵元立说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1997年底，赵元立被安排学习神经导航手术。此前，医生做开颅手术只能凭经验寻找病灶，而神经导航手术则通过计算机定位，术前勾画病灶位置。对于风险很大的开颅手术来说，这无疑是一个重要的进步。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">当时，国内对这项技术的研究还是一片空白，所有文献资料都是英文的。1998年，手术室改装完毕，新机器投入使用，赵元立把厚厚一本说明书一点一点看下来，再翻译成中文。经过半年努力，他掌握了导航定位技术，显著提高了开颅手术治疗颅内深部肿瘤和血管性病变的成功率。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">随后，赵元立被调到颅脑外伤病房，负责筹建低温治疗和重症监护病房。其间，他还曾出国短期学习。1999年，赵元立晋升为副主任医师，2001年，他又被调到脑血管外科，并且开始收“徒弟”。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">30岁的赵元立和他的学生几乎同龄。“我和学生就比较随便，大家一起讨论问题，每星期进行一次学术谈话。”赵元立把他从国外学来的医科学生培养模式用在教学过程中。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2004年，赵元立开始担任神经外科中心脑血管外科和老年肿瘤专业组主任。那一年，他刚刚34岁。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">对任何一位医生来说，失败都是不可避免的。赵元立记得，毕业时自己曾经想去肿瘤医院工作，被家人拦住了。“爷爷说那里病人得的都是癌症，再开刀也治不好。”而现在，赵元立所在的神经外科同样是与死神赛跑的竞技场。“我只是一个普普通通的人，肯定有我治不好的病。我能做到的只有不停地接触新事物，一直学下去。”赵元立说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">回首身穿白大褂的16年，赵元立坦言，也不是没有诱惑——比如出国。“到国外考个医生执照，在那里做医生，收入等等都很有吸引力。”赵元立说，“但中国的医疗业已经和国外接轨，我还是希望能在国内发展，通过我的努力让更多病人和病人家属满意。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">16年来，赵元立累计发表论文30余篇，参与多部著作编写，作为课题组成员3次获国家科技进步二等奖，还曾获得北京市卫生系统先进个人、全国卫生系统青年岗位能手、北京市优秀青年知识分子、茅以升青年科技奖等光荣称号。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“人一辈子不能体验太多。我这辈子就是要通过帮助别人来收获快乐。”赵元立笑道，“也许命运就是这样安排的”。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">微寄语</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">帮助别人，既能体现人生价值，也能收获快乐。</span>
</p>
