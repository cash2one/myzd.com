			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>哪些疾病适合人工髋、膝关节置换术？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、适合人工髋关节置换术的疾病：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、髋部骨折</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">①股骨颈骨折有移位，年龄较大者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">②股骨颈陈旧骨折，因各种原因延误治疗或保守治疗后出现骨折不愈合或股骨头缺血坏死者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">③外伤性股骨头缺血坏死、塌陷者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">④股骨颈骨折病人全身情况差，不宜久卧床者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">⑤髋臼骨折早期的人工髋关节置换仅适用于预期切开复位和内固定手术后效果较差的老年病人。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、髋关节骨关节炎（或骨质增生）出现夜间痛、服用止痛药效果不佳，影响工作和生活的病人。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、股骨头缺血坏死，股骨头已经塌陷，疼痛、功能障碍。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、髋关节发育不良或髋关节脱位的中老年人，出现患髋部疼痛或腰部疼痛者。可考虑行全髋关节置换术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、类风湿关节炎髋关节疼痛，药物效果不好或出现髋关节畸形、功能障碍者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、强直性脊柱炎髋关节病变，药物效果不好，或髋关节畸形、功能障碍者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7、髋关节感染、外科、手术后残留关节强直髋关节强直出现下腰痛。髋关节融合术后出现假性融合伴疼痛或非功能位融合。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8、髋关节的其他疾病，经常规治疗无效者。</span>
</p>
