</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、适合人工膝关节置换术的疾病：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、膝关节骨关节炎（或骨质增生）疼痛加重，药物效果不好者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、膝关节创伤性骨关节炎膝关节因外伤后或半月板切除术后出现疼痛，药物效果不佳。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、膝关节类风湿关节炎疼痛，药物效果不好或出现关节畸形、功能障碍。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、膝关节感染、外伤、手术后残留关节强直，出现下腰痛或健侧髋或膝关节出现疼痛；膝关节融合术后出现假性融合伴疼痛或非功能位融合者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、膝关节的其他疾病。</span><br />
