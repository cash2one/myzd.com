	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>腰椎间盘突出症手术发展史：何谓微创手术？(转载)</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我们脊柱外科医师在门诊，常常会遇到腰椎间盘突出的患者为微创所吸引，寻求解决自身疾病最佳治疗方法的渴求。诚然，患者到医院，寻求的是康宁，通过专家的精心医治，得以安乐的工作和生活。那么，我们应当如何为此类患者抉择呢？腰椎间盘突出症手术治疗的最佳选择如何呢？</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">结合笔者的相关研究、临床经验，广泛查阅文献，现将相关观点与诸君探讨。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">腰椎间盘突出症的手术可分为：开放与微创（Minimally Invasive Surgery，MIS），后侧与后外侧。对于腰椎间盘突出症的手术，最早由 Mixter 和 Barr 于 1934 年，在 NEJM 上报道了以全椎板切除和髓核摘除术治疗 19 例椎间盘突出症，开创了椎间盘突出症手术治疗的先河。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1955 年，陆裕朴教授放弃美国的行医执照和绿卡，与钱学森等著名科学家一起回国，在西安第四军医大学西京医院创立了骨科，经多年的摸索与实践，率先在上世纪九十年代，进行了国内最大样本腰椎间盘突出症手术治疗的报道，并创建了椎板小开窗、髓核摘除术的手术方式，在我们科室的脊柱组，而一直沿用至今。具体方法，在国内同道们都很熟悉的、极具临床指导意义的《实用骨科学》（1990 年版）上，有详细介绍。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我们来回顾椎间盘镜和椎间孔镜发展的历程：</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1997 年，Foley 和 Smith 引入了椎间盘镜下髓核摘除术（Microendoscopic Discectomy，MED），并报告了他们治疗 100 例患者的经验。此即 MED——椎间盘镜技术，亦是从后正中入路，于椎间盘镜下，摘除髓核组织。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1983 年，美国宾夕法尼亚大学医学院骨科 Kambin P 等于 Clin Orthop 上报道了经皮后外侧入路。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1990 年，美国匹兹堡长老会大学医院的 Onik 等，于 Neurosurgery 上报道了椎间孔镜后外侧治疗腰椎间盘突出症（Percutaneous&nbsp; Endoscopic&nbsp; Lumbar&nbsp; Discectomy，PELD）的前瞻性多中心研究结果，表明严格手术指征的 327 例患者，成功率 75.2%；不控制指征的 168 例患者，成功率 49.4%。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由此可见，国内外医者对腰椎间盘突出症手术治疗的改进，几乎同步；所不同的是，国内以陆裕朴教授为首的医者，提出的是椎板小开窗、直视下，彻底摘除突出和退变、松散的髓核组织；国外是后外侧，椎间孔镜下操作。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1997 年，美国亚利桑那骨科 Yeung 等于 Spine 报道了 YESS（Yeung Endoscopic Spine System）治疗 307 例患者手术后短期随访（平均 19 月）的经验，声称其总体成功率为 89.3%，结果差的占 10.7%，并发症率 3.5%，包括 2 例深部感染、2 例血栓性静脉炎、6 例感觉迟钝、1 例硬膜破裂。同期 Spine 上，美国梅奥诊所的 Deen 医生，发表评论，质疑该研究的设计：基于 1990 年 Onik 等的研究，Yeung 等只选择报道了 307 例患者，而采用同样技术治疗的另外 483 例患者效果如何却只字未提。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1994 年，德国 Hoogland 等报道了 Thomas Hoogland Endoscopic Spine System（THESSYS）系统、椎间孔成形治疗 280 例连续患者的经验。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">近年来，椎间盘镜和椎间孔镜技术，借由微创而甚嚣尘上，认为传统的开放髓核摘除术创伤大，切口长，问题多。我们科技工作者，静心而思考，往往对舆论会有全新的见地；正如 2010 年美国卫生保健质量和研究署所提出的疗效比较研究（Comparative Effectiveness Research，CER）的理念，对于一种替代治疗方法（Alternative methods），对其优点和缺点与经典方法进行比对，即 CER，此为替代治疗方法实施前的必需步骤。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">那么，正如 Lancet 发表论著的准则那样：可以改变我们临床实践的随机临床试验，其说服力最高；抑或是一项技术经 10 年以上的长期随访，为数代医者所推荐。这些技术，与经典的开放手术相比，具有说服力的依据吗？</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">经过检索，目前关于椎间盘镜或椎间孔镜，迄今为止，尚无发表于 NEJM 或 Lancet 等顶级杂志的随机临床试验，亦无 10 年以上的长期随访研究，表明其优于开放髓核摘除术。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2010 年，荷兰 VU 大学医学中心骨科的 Nellensteijn 等，在 Eur Spine J 发表了对 PELD 的系统回顾分析，在已发表的关于 PELD 与开放髓核摘除的比较研究（临床试验）中，PELD 与髓核摘除在下肢疼痛症状改善（89% 对 87%）、总体改善（84% 对 78%）、再手术率（6.8% 对 4.7%）和并发症率（1.5% 对 1%）等方面，无明显差异。因此，得出结论：对于腰椎间盘突出症，没有强有力的证据证明 PELD 优于开放手术、髓核摘除术。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">欧美对椎间孔镜技术大宗病例报道很少，对其认可程度，可见一斑；韩国 Wooridul 医院神经外科的 Lee 等，今年发表在 Neurosurgery 上的文章，回顾了他们 12 年间共 10228 例 PELD 治疗腰椎间盘突出症的经验。436 例（4.3%）的患者手术失败，失败的定义为 PELD 手术后 6 周内需要再手术（注：此定义是否能为患者所接受，PELD 后 2 月或半年复发，对于该文作者算是成功，对患者，是否算成功的手术，敬请评论）。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">失败的原因包括：283 例（2.8%）髓核摘除不彻底，78 例复发（0.8%），41 例（0.4%）持续疼痛（完全摘除髓核亦不能缓解），21 例切口相关疼痛（0.2%）。髓核摘除不彻底的原因包括工作通道位置不当（33.6%），91 例中央型突出（33.2%，图 1），63 例腋下型突出（22.3%，图 2），70 例髓核游离型（24.7%，图 3）。</span><br />
