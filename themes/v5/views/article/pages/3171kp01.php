<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">关于 PELD 手术的辐射问题，韩国学者报道透视时间为 2.5 分钟，一年之中，无透视铅衣保护下，291 例 PELD 手术即达透视安全界限。MED 亦需透视。脊柱手术的辐射风险，今年 Spine J 的文章已为丁香园骨科频道学术编辑所编译 「辐射风险：医生与患者谁更安全？」，敬请诸君参阅。而开窗手术不用透视。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">关于微创脊柱融合手术，同 MED 或 PELD 一样，作为传统脊柱融合的替代治疗，效果如何呢？2014 年，加拿大多伦多西部医院的医者，在骨科经典杂志 Clin Orthop Relat Res 上发表了关于微创脊柱融合与开放融合手术的系统回顾分析，目前的研究报道，都不能提供微创脊柱融合优于开放融合的可信依据。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">国外最新的观点，正如今年 3 月在美国拉斯维加斯举行的美国骨科医师年会（AAOS）上，加拿大学者 Evaniew 所报告的那样，证据表明使用微创手术技术总体神经根损伤、硬脑膜损伤及再次手术风险较高，目前的证据并不支持微创手术作为椎间盘切除的常规手术方式。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">腰椎间盘突出症患者，不乏年轻者，或为人父母，或为人子女，或为人丈夫或妻子，肩负家庭的期盼，社会工作的责任。一朝椎间盘突出严重，压迫神经，影响工作、生活，愁忧不安，躺于手术床上等待麻醉之时，身心不宁，家属在手术室外焦虑等待。所期待者，医术精湛、完成学习曲线之医者，为之手到病除，再不受此手术开刀之苦，再无复发之忧念。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脊柱外科医师，手下是芸芸众生和家属的福祉，肩负着众多患者和家属的期盼，无论椎板小开窗、MED 或 PELD，我希望，医者以仁心、娴熟之技能，为患者解除病患、尽量彻底的摘除髓核；我本人亦希望，国民注重劳逸结合，重体力劳动者、司机、财会人员、教师等职业者，注意保护颈椎腰椎，避免久坐、弯腰搬物，预防椎间盘疾病的发生；亦希望，腰椎间盘突出症患者，获得诊断和治疗的正知正见，获得最恰当的精准治疗。如是，当为此文成文之本愿。</span>
