<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">通过以上分析，对于有经验的脊柱外科医师，我们切开突出的纤维环后，摘除突出的髓核，并以髓核钳放置于椎间隙，于多个方向、手感感知松散的、退变的髓核组织并加以彻底摘除。尤其是外侧区域，应加以彻底摘除，此区域为最容易复发掉出的部分。而此区域，恰恰是 PELD 技术的盲区所在。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">关于学习曲线陡峭的问题，手术时间是个很好的体现。最近，南京东南大学医院骨科的医者，在 Int Orthop 上发表了他们关于采用 THESSYS 系统治疗 277 例腰椎间盘突出症患者的经验。他们的手术时间，随手术病例数增多而逐渐缩短，从刚开始的近 3 个小时到后来的 80 多分钟。韩国 45 名医生 12 年间 10228 例的报道，平均手术时间 50 分钟（30 分钟到 90 分钟）。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术费用研究中多未提及，南京东南大学医院骨科的医者，THESSYS 手术患者，45 岁以下的平均费用为 15480 元，45 岁以上者为 16381 元。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2014 年，美国田纳西大学手术医院骨科的医者，对比了他们采用 MED 和开放髓核摘除术的疗效，值得注意的是，时至今日，他们的开放手术，竟然是手术切口 8-10 厘米，半椎板切除以髓核摘除！而且，MED 和开放手术的平均手术时间：98.8 分钟对 97.3 分钟！</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">看来，无论是脊柱外科界，还是腰椎间盘突出者的患者，对于椎板开窗减压、髓核摘除术存在不小的误区，故在此展示我们近来手术的一例 19 岁腰 4/5、腰 5/ 骶 1 椎间盘突出、骨化的女性患者的手术及费用情况。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">此患者，我们手术团队（李新奎教授、王海强副教授和张军、王峰亮主治医师）采用双侧椎板开窗减压（开 4 个窗、特别是保留了每侧两个窗之间的骨桥，万一复发需要翻修，此骨桥可提供剥离瘢痕的重要标志）、神经根探查松解、髓核摘除（包括骨块）术，麻醉方式为硬膜外，手术切口 6.5 cm（若是一个节段，切口一般 3 cm），手术时间 100 分钟，所有费用 1 万元。术中无需透视定位。</span><br />
