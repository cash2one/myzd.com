			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>宝宝"大舌头"不能都怪舌系带短</strong></span>
	</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孩子进入学语阶段，一些细心的家长发现，孩子说话含混不清，伸舌头时显得有点费劲。“这是‘大舌头’，得去医院把舌系带剪开，否则会影响孩子的语言表达！”这种说法在家长中颇有市场，口腔外科专家却认为不可一概而论。“我们接诊的舌系带短的孩子主要集中在1~3岁，其中90%的孩子不需要做手术。”中山大学附属口腔医院口腔颌面外科主任廖贵清教授指出，家长在对待孩子舌系带短的问题上，普遍表现得“过度担忧”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 他指出，宝宝“大舌头”，不能都怪舌系带短，而轻度甚至部分中度舌系带过短患者完全没必要做手术，经过科学语音训练，他们也能做到吐字清楚、表达无碍。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 案例：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 三龄童话说不清 家长怪罪舌系带</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 三岁半的成成最近在幼儿园遭遇了“社交危机”。小朋友说听不懂他的话，不乐意跟他玩。老师告诉成成妈妈，孩子说话时好像故意藏着舌头，他会把出租车说成“嘟嘟车”，“干什么”说成“干横吗”，影响了准确表达。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “这是‘大舌头’。成成跟我家孙子一样，舌系带有点短，最好去医院做个手术。如果不把舌系带剪开，以后话都说不清楚。”懂行的邻居奶奶指给成成妈看，成成伸舌时，“舌筋”绷得紧紧的，受其牵扯，舌头只能刚刚伸出下嘴唇。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “很多家长对舌系带短有误解。孩子说不清楚话，不能都怪舌系带短。”廖贵清指出，舌系带短在人群中相当常见，也长期遭到人们的误解。 正常的舌系带可以让舌头活动自如，舌尖能轻松地伸到口外，或向上舔到上牙龈，舌尖呈尖尖的“V”形。舌系带短的人们伸舌时，舌尖由于受到系带牵拉，伸舌有不同程度的困难，且舌尖呈现“W”形凹陷。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 诊断：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 新生儿衔乳困难 小心舌系带短</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “从临床症状上看，舌系带短可分为轻、中、重三类。轻度被称为‘小W’，孩子伸舌可到下嘴唇和下巴之间的位置，但舌尖受舌系带轻微拉扯。中度则被称为‘中W’，患者的舌头可以伸出嘴唇，但舌尖呈现较明显的W型。”廖贵清说，这两类患儿比较常见，一般不需要手术干预。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 需要重视的是重度舌系带短患者。这类患者在刚出生时往往就会被助产士发现，因为一哭，舌尖被舌系带拉扯，呈现明显的W型。出生时舌尖表现得不是特别明显的新生儿，则多因衔乳困难而被发现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “新生儿吞咽奶头时，要用舌头将奶头裹起来，从而在口腔形成负压，顺利吮吸到乳汁。”廖贵清说，舌系带太短的新生儿无法完成这个动作，也就喝不到奶。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近几年，广州处于建国后第四次人口出生高峰，产房床位紧张，一些医院甚至要求顺产的产妇住院一天后即出院。一些哭泣时舌尖W形状不明显的新生儿很可能在产房来不及被发现，需要家长在家认真观察。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 妇产科专家提醒，衔乳困难的小宝宝哭泣时，家长除了要观察舌头是否受舌系带过度牵扯，舌尖形状呈W形，还应注意听哭声有无异常。舌系带短的宝宝哭泣声跟正常宝宝略有不同，前者近似“哀哀”、“恩恩”声，而正常宝宝的哭声更似“啊啊”、“哇哇”声。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 治疗：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 轻中度患儿 手术不是必选项</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “大‘W’患者需要尽快手术，越早越好，否则长大后，会导致语言障碍。”廖贵清指出，大“W”患者的手术最佳时机当然是新生儿期。出生一周内的孩子舌系带薄得像纸片，剪断后出血少，对孩子的影响最小。男婴咚咚出生三天时，就因舌系带太短、无法正常喝奶，而在产房挨了一剪。术后半小时，他就能正常喝奶了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 年纪越大，舌系带手术对患者的影响越大。年纪大的孩子做手术时需要采取“全身麻醉+局部麻醉”，切开舌根系带，然后在舌底进行缝合。术中出血不多，但有痛感，除了麻醉有一定风险，手术本身也会对孩子的心理造成一定冲击。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “做不做手术，目前最纠结的群体是小‘W’和中‘W’患儿的家长。”廖贵清指出，门诊遇到最多的是1~3岁的学语期儿童，仅仅从病情考虑，这些孩子并不需要做手术。受发音器官的肌肉力量不足、协调性不强以及发音“业务不熟练”等因素的影响，学语期儿童都会经历从说话不清楚到准确表达的过程。因此，1~3岁的轻度患儿口齿不清，往往并非舌系带短的错，不需手术。对中“W”患儿来说，手术也非必选项，今后可以通过有效的语音训练实现语言代偿，避免成为“大舌头”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 然而，许多家长并不这么想。对舌系带短的不理解和对孩子的担忧，常令他们更愿意选择手术。“家长的焦虑和不安写在脸上，这种心理会对孩子产生很不好的暗示。”廖贵清说，如果家长不能正确认识舌系带问题，总是耿耿于怀，孩子们善于察言观色，也会受到沉重的心理打击，为自己舌系带短而自卑，甚至影响语言表达能力，以后真的变成“大舌头”。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 提醒：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 经科学训练</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “大舌头”也能口齿清楚</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 廖贵清强调，导致口齿不清的因素有很多，舌系带过短只是其中一个小因素，不能把“大舌头”的罪名都扣在它上面。“在发音原因中，舌头扮演的是近似小喇叭的作用。”他说，牙齿、舌头、口腔构造、鼻窦等与发音有关的器官发育不良，都可能诱发口齿不清。发音技巧错误、听力障碍、某些智力疾病，也是可能的原因。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 口齿不清也可能与主管发音的肌肉运动不协调有关。当孩子出现“大舌头”症状时，除了排查器质性病变，家长不妨带孩子去做“语图仪”检查，检测是哪一块肌肉出了问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">靠谱的语音训练可改善发音问题。廖贵清建议，舌系带短的孩子不妨去学唱歌。不少患儿通过向专业发音老师学习科学的发声方法，能改善发音问题，也能做到口齿清楚，表达无碍。</span><br />
