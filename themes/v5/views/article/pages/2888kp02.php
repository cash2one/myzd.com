			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>注射宫颈癌疫苗有没有必要？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">提问</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所有的疫苗接种都有一定失败率及并发症，接种前需慎重。因此，不能盲目接种HPV疫苗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">身体周刊：妇科检查有什么需要注意的？什么情况下去看医生？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刘开江：女士体检比男士多一项妇科系统性检查，包括妇科检查和妇科B超检查。有人以为，做了B超，就没有必要再做妇科检查了。其实B超检查的是子宫和附件的情况，可以发现子宫肌瘤，卵巢囊肿等；而妇科检查的是阴道和子宫颈的情况，可以发现子宫颈病变，阴道炎等。所以，B超检查和妇科检查不能相互替代。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">出现以下情况需要看医生：分泌物增多，可能意味着炎症、肿瘤；不规则流血，可能意味着炎症、功能性子宫出血、肿瘤等；腹痛，可能意味着炎症、内异症、急腹症等；腹部包块，可能意味着肿瘤等。检查发现的包块，绝大多数是肿瘤（良性肿瘤和恶性肿瘤），但也有一些是炎性包块和包裹性的积液等非肿瘤性包块。对于女性来说，还要考虑妊娠增大的子宫以及与月经相关的卵巢黄体等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">身体周刊：腹腔镜可以做哪些妇科肿瘤手术？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刘开江：传统的妇科肿瘤手术，大多数手术方式都是在腹部切开一个刀口，特别是一些恶性肿瘤手术，如卵巢癌手术、子宫颈癌手术都需要在腹部切口20-30厘米的长刀口，手术后切口的疼痛，活动受限，以及术后明显的疤痕等均给患者造成恐惧的感觉。但是现在绝大多数的妇科肿瘤手术均可以在腹腔镜下完成，这种微创手术，术后疼痛轻、腹部无明显疤痕，使患者更容易接受。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">包括常见的良性肿瘤如子宫肌瘤、卵巢囊肿等可以在腹腔镜下完成，常见的恶性肿瘤如子宫颈癌、子宫内膜癌、早期卵巢癌也可以在腹腔镜下完成。目前腹腔镜开展较好的医院，95%以上的良性肿瘤，80%以上的恶性肿瘤可以在腹腔镜下完成。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">10年前，对于腹腔镜下妇科恶性肿瘤手术还存在争议，现在，随着腹腔镜手术技术的成熟，越来越多的妇科医生接受腹腔镜下妇科恶性肿瘤手术，特别是一些手术模式固定的、解剖性的手术，如宫颈癌、子宫内膜癌、早期卵巢癌手术，利用腹腔镜技术的优势，可以把肿瘤切除得规范、干净，而且具有微创手术的优点，损伤小、出血少、术后恢复快等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">需要强调的是，这并不是说腹腔镜下能做所有的恶性肿瘤手术，如晚期卵巢癌手术，为了保证肿瘤切除的完整性、规范性，就需要行开腹手术。所以说，任何一项技术都有自己的优势，再好的技术也不能、也不应该完全取代所有其他的技术，但随着腹腔镜技术的逐渐提高，过去一些禁忌证可以转变成该技术的适应证，这也说明该技术在不断得到改进和提高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">身体周刊：目前有不少中国内地女性前往中国香港地区及韩国等地注射HPV疫苗，你怎么看这种现象？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刘开江：检查发现子宫颈癌有两套简单、可靠、便宜的方法，那就是薄层液基细胞学(TCT)检测和HR-HPV-DNA(HC2)检测。而女性乳腺癌、宫颈癌的谱查方法也很简便可靠，同时准确性高，体检中的宫颈刮片就能查出宫颈癌，乳腺癌通过体表检查也能查出来。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于科普知识的宣传深入人心，大家都知道HPV病毒感染是导致宫颈癌的重要原因。美国食品药品管理局（FDA）已经批准了HPV疫苗用于宫颈癌的预防，在韩国和中国香港也已批准HPV疫苗的应用，但中国内地尚未批准。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原因可能是：第一，HPV病毒疫苗的临床应用只有阶段性的成果，并没有最终明确的结论。第</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">二，并不是所有的女性都可以接种这种疫苗，还存在许多限制，如年龄的限制，有些国家限定为18岁以前，有些国家限定12或13岁之前，无性生活史。第三，所有的疫苗接种都有一定失败率及并发症，接种前需慎重。因此，不能盲目接种HPV疫苗。</span>
</p>
