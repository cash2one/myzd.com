			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>光影：一根血管两条命，全程图文纪实</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主笔：大医生兵器谱戴志悦</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">怀孕18周，33岁的小凡（化名）刚刚开始感受幸福的胎动时，却开始出现胸痛和心衰的症状——她不得不面对随时可能猝死的危险。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">她来到了北京安贞医院妇产科，这里是北京市妊娠合并心脏病孕产妇转诊会诊中心。同时这里还汇聚了国内最优秀的心血管外科、麻醉、体外循环、新生儿、心外ICU专家，他们在妊娠合并心脏病的外科治疗上具有丰富的经验。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">小凡是马方氏综合征患者，怀孕后她的主动脉发生了撕裂，专业上称为妊娠合并主动脉夹层，十分凶险，如果不做手术，随时可能发生主动脉破裂而猝死。可是，做手术，肚子里的孩子能保住吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在我国，妊娠合并主动脉夹层第一例手术，是主动脉疾病治疗的领军人物孙立忠教授在1998年6月2日成功完成，15年来，他的团队已经治疗过22名此类患者，是目前全世界单中心完成临床病例最大的一组。这一次，担任小凡手术主刀医生是团队里著名的心血管外科专家朱俊明教授。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对小凡的病情，安贞主动脉团队医生和妇产科会诊意见很明确，以她目前的心功能状况和心血管病变，主动脉根部瘤直径已达6公分，主动脉瓣大量返流，如果不马上手术，要么主动脉瘤破裂，要么心力衰竭，等不到妊娠结束，最终结果都是母子双亡。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">小凡同意马上做手术，但态度也很坚决：保住胎儿。甚至说：“如果孩子没了，她也不活了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个孩子对小凡来之不易，是做了好几次试管婴儿才成功的。朱俊明作为主刀医生的挑战就是，既要体外循环下切除小凡身体里这个随时可能破裂的主动脉瘤炸弹，又要在手术中保证她仅18周的胎儿安全，让她能继续妊娠下去生下这个孩子。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">朱俊明说，面对妊娠合并主动脉疾病的患者，医生和患者的心愿一样——尽可能达到母子平安，但主动脉手术有其特殊性，在体外循环特别是深低温停循环下，这个愿望实现起来难度很大。在过去朱俊明经手的22例妊娠合并主动脉疾病的病例里，只有1例保胎成功。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那是5年前在桂林医学院，朱俊明给妊娠合并急性主动脉夹层的患者替换了主动脉根部，由于术中多方面的特殊处理和严密的术中胎儿监测，妈妈出死入生的整个过程，胎儿一直安全地在腹中，术后孕满8个月，剖宫产出生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于胎儿已经足月或接近足月，手术台上，先由妇产科医生做剖宫产手术把孩子取出来，朱俊明再接手给母亲做主动脉手术，但由于子宫出血和体外循环手术肝素抗凝将给手术医师带来极大的挑战。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">也有不幸的是，医生给母亲做完手术后，由于体外循环特别是深低温停循环影响胎儿心脏停跳了，最后只能引产。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还有一例，让朱俊明印象深刻。患者怀孕6个多月了，要求等胎儿满7个月后再做手术，这样孩子存活机率大一些。无论医生如何劝告，她的父母爱人甚至跪地肯求她赶紧手术，但孕妇坚决不同意。一直住在安贞心外大楼11层的5A病房，有一天早上，她和母亲正坐在床上吃早餐，她突然栽倒在地，半分钟就走了——主动脉瘤一旦破裂，医生根本来不及抢救。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">朱俊明说：“这类患者，妈妈能活下来，孩子才有活的希望。”立即手术是让妈妈活下来的唯一办法，而能否在手术中保住胎儿，患者的主动脉病变部位至关重要，也需要多学科的紧密协作，过程很复杂，简单的讲就是术中母亲心肺功能停止后，要继续保证胎儿足够的血液供应，代谢和内环境稳定，一定要避免体外循环低温、高钾等的心脏停跳。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很幸运小凡与朱俊明手术中保胎成功的那一例的病情很相似。术前朱俊明认为，小凡有希望成为安贞医院第一例保胎成功的妊娠合并主动脉夹层的患者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最终，朱俊明成功了，小凡成功了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这台手术，大医生兵器谱全程跟访，我们用文字和照片记录了这位妈妈和宝宝的手术过程，更见证了在心血管外科、妇产科、麻醉、体外循环、监护的团队协作下挽救了两个生命，见识了朱俊明主任精湛的技术，和他“笑料信口抖出”的功力。</span><br />
