			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>心脏搭桥适合那些人？哪些人适合做心脏搭桥？</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">介入和搭桥，是治疗心血管疾病的两种重要手段。其中支架，尤其是药物支架，作为后起之秀，以其创伤微小的特点得到了很多患者的青睐。据《纽约时报》2月25日消息称，美国2006年接受搭桥的患者约有36.5万，比10年前下降了1/3；而接受介入治疗的患者却一路猛涨，达到了100万左右。但该文章也指出：很多心血管病专家呼吁，尽管如此，人们不应忽视搭桥手术的优势，而只看重介入。那么，这二者到底孰轻孰重？在什么情况下，患者应该接受外科搭桥手术呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现任北京大学第三医院心脏外科主任告诉《生命时报》记者，具有以下几种病变特征的冠心病患者应该选择搭桥。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">左主干病变：按照国内外的指南，外科手术是左主干病变的首选。因为左主干一旦发生堵塞或再狭窄，可能会致命。为了缩小风险，最好选择搭桥。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三支病变：病变血管较多，如果选择介入治疗，要放很多支架，这会使再狭窄、发生血栓的几率大大增高。而且，患者的经济负担也较重。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">伴有心功能不全：这类患者需要完全性的血运重建以促进缺血心肌的恢复，介入很难做到。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">伴有糖尿病的患者：普通支架对糖尿病患者的再狭窄率较高，而药物支架问世时间还短，还没有明确证据表明介入治疗会比搭桥有更好的疗效。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">冠心病心肌梗死后并发症患者：心室破裂、室间隔穿孔、二尖瓣关闭不全等都必须选择外科搭桥手术治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">北京大学第一医院心内科副主任洪涛则表示，由于支架需要患者服用抗血小板药物，对此药物过敏的患者也应考虑搭桥手术。“然而，如果患者有呼吸系统的疾病，接受全面麻醉可能会有危险，不适合接受常规体外循环搭桥，而应选择介入治疗。”</span>
</p>
