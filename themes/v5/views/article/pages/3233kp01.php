<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>颅内的病很多由全身疾病引起</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/3233/1.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">名 医 名 片</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">陈劲草 神经外科教授，武汉大学中南医院神经外科主任，能熟练处理各种颅内疾病，显微手术技术娴熟，主要研究专业方向为脑血管疾病（动脉瘤、动静脉畸形、脑烟雾病等）的显微手术治疗及松果体区肿瘤的综合治疗。擅长各种疑难脑血管病、颅内肿瘤的诊治。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">专家门诊时间：周三上午（知名专家门诊） 周五上午</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“那么多的医生放弃了我们，你却给了我们希望；那么难、那么长的手术，你却给了我们成功的讯息。你真是个好医生！”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">打开武汉大学中南医院神经外科主任陈劲草教授的网页，患者留下的全是对他的赞扬：对疑难病不推诿不敷衍，还要千方百计成功。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由此，他获得了2013中国名医百强榜，领域是“脑动脉瘤及脑血管畸形手术”，获得这一殊荣的医生，迄今为止武汉地区不到10个。全湖北省疑难的脑动脉瘤和脑血管畸形的病人，几乎全部被送到了这里，因此，陈劲草教授的每一台手术都是走在生与死的钢丝绳上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脑动脉搭桥</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">将中风消灭在萌芽状态</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">陈劲草现阶段在临床研究和实践的重点是脑动脉搭桥手术。他介绍，脑梗塞、脑溢血（统称脑中风）导致的死亡已占到人类死亡的第三位，而脑中风的前提是脑血管狭窄和闭塞。早在上世纪70年代，世界医学界就开始探索用搭桥的方式来解决脑血管的狭窄和闭塞问题，但苦于当时的检查手段和不能评估血流量的局限，一直没有大的突破。近五年来，功能核磁共振检查运用于临床，它可以很好地评估脑血管血流量，从而尽早筛查出脑供血不足的病人。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术中，陈劲草取下原来给头皮供血的颞浅动脉，替代病人大脑中狭窄或闭塞的血管，从而使脑血管恢复正常，消除脑中风的隐患。作为湖北地区开展脑血管搭桥最多的医生，陈劲草用颅内血管搭桥治疗脑烟雾病的技术获得了湖北省科技进步奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">长时间打手机对脑有影响</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">但没有量化指标</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脑血管病是呈上升趋势的。陈劲草教授介绍，如脑动脉瘤，有先天畸形的，也有后天形成的，更有先天后天因素夹杂的。如动脉粥样硬化，可以引起心脏的动脉瘤，也可以引起脑动脉瘤，而血管粥样硬化的基础疾病有高血压、高血脂等基础疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">再往下深挖，不良生活方式也是脑血管瘤的祸根。陈劲草教授介绍，现代高脂、高糖、高热量的三高食物，作息没有规律，上网、打麻将、夜生活过于丰富，打乱了人体生物钟的节律，容易引发高血压、高血脂等一些代谢性疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">现代人离不开电子产品，“电辐射对人体大脑确实是有影响的。”陈劲草教授解释，手机等电子产品的电辐射对大脑影响有限，但影响是存在的，只是现在没有一个量化的指标。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">陈劲草教授最后介绍，现在人们脑血管病增加还与我国人口老龄化有关，再就是检查手段先进了，有问题能够及时发现。</span>
</p>
