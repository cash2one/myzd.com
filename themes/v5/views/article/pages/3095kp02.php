<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>闫长祥：三博医院耳病治疗将听、面神经保留很好&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[主持人]您给我们讲讲三博医院在治疗上耳病上有没有什么特色？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[闫长祥]我们三博医院最大的特色就是手术，尤其是涉及到听神经的一些病变，我们的手术治疗还是比较好的。因为我们现在对听神经瘤治疗，它面临着巨大的挑战：一是保留面神经，因为听神经和面神经是保留在一起的，而且是并在一起走，所以听神经瘤的问题，面神经、听神经是沾的非常紧的，就像原来一个正常条状的东西，长了瘤的时候，成为一个面状的东西，保留面神经是一个很大的挑战，因为很多人做完手术以后面瘫，瘫了以后没有任何表情了，这对心理打击是非常大的。所以我们现在就在研究怎么保留面神经，尤其是近十年，我们面神经保留率还是非常高的，因为我们做听神经的手术量还是非常大的，到目前来说，我本人已经在500例以上。这个时候保留面神经，尤其是最近十年，基本上是没问题了。我讲的是解剖保留。还有一个功能保留，就是做完以后，面神经保留住了，但是功能上可能会受到影响，有人是闭眼睛闭不全等，功能保留是一个很大的挑战。所以我们除了把解剖保留，还有就是功能保留，它的措施是仪器设备，监测面神经，面神经一旦受到扯拉给我提示。现在还有一个很大的挑战就是听力，听力保留是很大的挑战，所以它的保留和肿瘤的大小有很大的关系，所以我们一直想建一个听神经的中心，全国有很多中心医院、神经外科也好、耳科也好，让发现的早期治疗，肿瘤越小，保留听力的机会越大。但是现在说听力保留也就是二三十的样子，如何保留听力，术中也是监测，耳蜗神经一些电位监测，提示这个神经在什么地方，减少它的损伤，提高听力的保留。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; [主持人]您做了500多例的手术，您有没有印象深刻的患者，跟大家分享一下，也给大家一些建议。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; [闫长祥]比较高兴的就是我能把听、面神经保留的很好，没有任何表现，听力保住了，这种还是很多的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; [主持人]能占多大比例？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; [闫长祥]大的还是小的也好，听力保留基本上是90%以上。听力保留确实跟大小有关系，如果真的是很大的，比如4公分以上的，保留听力的难度就相当相当大了。小的，一两公分甚至一公分以下的，保留听力的机会就相对大，越小听力保留的机会越大。面神经也是这样，肿瘤越小，对面神经压迫的就轻，所以肿瘤越小，保留听神经和面神经的比例就很大。我印象最深就是广东深圳的一个小女孩，二十一二岁，长的很漂亮，双侧听神经瘤。这对我们有很大的挑战，小的一侧有听力，大的一侧没有听力，那我们做的时候就选择大的那一侧，大概在四五公分的样子，那时候面神经保留还是有比较大的把握的，听力是没有的，因为她本身就没有听力了。所以我们第一次把大的切完，面神经保留的挺好的，听力没有了。第二次做手术我们就有很大挑战了，因为第二次她有听力，如果这次保留不了听力，她可能就是一个无声世界了，那对她个人来说打击肯定是很大的。所以我们还是费了很大的功夫，研究了方案，怎么让她的听力保留，这是手术的一个重点。那一侧小，不到两公分，做完以后听力还能保留。</span>
</p>
<p>
