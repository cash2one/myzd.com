<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>化疗患者吃什么？这里有最详细的回答(转载)&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">化疗患者该吃什么？这是医生、患者、家属共同关心的问题。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胃肠道反应是化疗最常见的不良反应之一，75%的患者会产生恶心、呕吐，伴随周身不适感。化疗期间应该吃什么，同时是患者和家属普遍关注的问题。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">集中三个目标&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 患者对饮食的要求主要集中在三点：（1）什么样的饮食能够增强患者的体质？（2）能否通过饮食来对抗化疗的各种不良反应？（3）严重的胃肠道反应患者应该吃什么？&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 患者和家属最希望有食物能够使患者的身体获得“大补”，但是食物毕竟不是“兴奋剂”，改善患者体质亦不是在一朝一夕之间完成的，食物更不能替代药物，对抗化疗药物的毒副反应，但应尽量通过合理饮食来改善患者的营养状况及精神状态。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">注重整体原则&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 化疗患者的个体差异较大，难以有一个适合于所有患者的饮食方案。但总体原则应做到清洁、清淡、不油腻，食谱丰富多样，富含高蛋白质、维生素和微量元素，达到营养全面的标准。此外，化疗期间，患者应适度饮水，不能食用辛辣刺激性食物，不能吃生冷食物，应当戒酒。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 患者的胃肠道反应除受个体素质影响外，辅助化疗和姑息化疗的患者耐受性不同，有些伴有情况也影响患者的胃肠道反应，比如口腔粘膜炎、便秘、接受消化道手术。还有一些特殊情况需要特殊进食，比如贫血、电解质紊乱。不同情况的患者应采用不同的饮食方案。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">WHO恶心、呕吐分级标准&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 患者的恶心、呕吐程度不一，能够进餐的情况也就不同。根据WHO标准分级标准，恶心、呕吐分级标准大致如下：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 0级：无恶心、呕吐；Ⅰ级：只有恶心，能够吃适合的食物；Ⅱ级：一过性呕吐伴恶心，进食明显减少，但能够吃东西；Ⅲ级：呕吐需要治疗；Ⅳ级：顽固性呕吐，难以控制。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 根据上述情况，患者的进食情况分为几类：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ①对于没有胃肠道反应或反应不重的患者，饮食以加强营养为主，但食物也应易消化，避免油腻，不能吃辛辣刺激性食物。少吃油炸、烧烤、油煎等烹饪方式烹制的食品。饮食多样性，营养均衡，热量充足。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ②对于食欲受到影响，但无明显恶心、呕吐的患者，应以刺激患者食欲，选择患者喜爱的食物为主。患者可多喝粥、喝汤，流食中可加入营养较为丰富的食物，比如，将海参、肉、鲍鱼绞碎加入粥中，并加入盐等电解质。少食多餐。清晨往往是患者恶心、呕吐反应最轻的时候，因此在清晨应该为患者准备较为丰富的食物。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ③严重呕吐的患者可以考虑禁食水4-8小时，必要时可延长至24小时，禁食结束后由流食逐步过渡到普通饮食。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ④贫血患者可以使用含铁丰富的食物，比如猪肝、木耳、菠菜、山药、红枣、桂圆、藕粉等。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ⑤电解质紊乱的患者可以多芒果、苹果、柑橘、土豆等。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ⑥接受胃肠道手术的患者胃肠道反应往往比较严重，同时因吸收功能障碍，患者营养状况受到影响，应格外重视食物的营养。藕粉含有维生素B12和铁等造血必须的原料，并易于消化，也是胃肠道手术患者的理想食物之一。接受胃肠道手术的患者少食多餐。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4个方向进击化疗患者的饮食&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 应该强调的是，不能单纯地依赖于某一种食物，毕竟胃肠道的容量是有限的，单一的食物不仅会导致患者营养缺乏，还可能影响患者食欲。而化疗可能使患者厌食，因此化疗期间，最重要的是要增加患者的食欲。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 下面介绍一些适合化疗患者进食的食物：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ①水果：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 苹果：是营养成分最为全面的水果之一，富含碳水化合物、维生素和微量元素（富含钾、铁），富含膳食纤维、苹果酸、酒石酸、胡萝卜素。苹果也可以安神。如患者进食少，可以榨汁引用。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 芒果：热量高，营养成分全面，含有糖、蛋白质、粗纤维、维生素、脂肪等。芒果质地柔软，是化疗患者较为理想的食物。能够通便，具有一定的止吐功能。对芒果过敏者禁食。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 桃子：含有蛋白质、脂肪、碳水化合物、粗纤维、矿物质（钾、铁）、维生素B1、苹果酸、柠檬酸）、葡萄糖、果糖、蔗糖等。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 柑橘类：富含维生素C、胡萝卜素、钾、钙、铁，具有抗氧化功能，四季可食，可以榨汁引用。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ②蔬菜：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 土豆：土豆中所含的营养成分较为全面，包括：淀粉、蛋白质、脂肪、维生素、矿物质。土豆中含有18种氨基酸，其中包括人体不能合成的必需氨基酸。土豆中含钾量几乎是所有蔬菜中最高的。烹饪方式多样，可以作为主食，可以作为菜品。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 番茄：富含多种维生素和矿物质，还含有蛋白质、糖类、纤维素。不宜生吃。不宜高温长时间烹制，不宜与黄瓜同食。简单而有营养的烹制方式有番茄炒鸡蛋，番茄鸡蛋汤。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 西兰花：营养全面，富含蛋白质 、糖、脂肪、维生素、胡萝卜素和矿物质，本身具有抗癌作用，凉拌和清炒是适合化疗患者的烹制方式。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 胡萝卜：富含胡萝卜素，并含有蛋白质、脂肪、多种维生素及矿物质等，具有抗衰老、抗癌的作用，能够提高机体免疫力。建议切碎、用油炒熟后吃，有利于营养的全面吸收。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 花菜：富含多种维生素和粗纤维，但热量较低，也可作为化疗患者的食物之一。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ③富含蛋白质的食品：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 海胆：富含17种氨基酸组成，等质量的海胆黄的蛋白质含量是鸡肉、鱼类等的两倍，此外，海胆黄含有不饱和脂肪酸、磷脂、糖、钙、磷、维生素 A、维生素 D等，其中，钙、磷含量高于牛奶。推荐的做法为海胆蛋花汤，加工简单，清香味美不油腻，比较适合化疗患者。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 海参：蛋白质含量极高，含有18种氨基酸、牛磺酸、硫酸软骨素，多种矿物质及维生素等。但因养殖方式等问题，食用安全目前具有争议。建议化疗患者短期食用。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 鲍鱼的营养价值极为丰富，含有20种氨基酸 ，此外，含有脂肪、维生素、微量元素等。化疗患者可以选择鲍鱼粉碎后煮粥。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 其它富含蛋白质的食物有鱼、虾、肉、蛋、奶、豆类，可以根据患者喜欢的口味加工。肉类加工时可加压，使肉松软易消化。蒸蛋糕可以作为化疗患者的食品选择。磷虾富含蛋白质、欧米茄-3、DHA、胆碱，也是较好的选择。此外，豆腐富含蛋白质，是化疗患者不错的选择。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ④其它：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 花生：含有蛋白质、脂肪、糖类、多种维生素以及多种矿物质，并含有8种人体所需的氨基酸及不饱和脂肪酸、卵磷脂、胆碱、胡萝卜素和粗纤维等。具有防癌的功效。化疗患者可以选择盐水花生。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">香菇：富含维生素B族、铁 、钾、维生素D原等，建议晾晒后，切丁煮汤，可以加入胡萝卜丁、豆腐丁、葱花煮汤。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">竹笋：含有丰富的蛋白质、氨基酸、维生素、脂肪、糖类、矿物质、胡萝卜素等。也是化疗患者较好的选择之一。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">南瓜：含有多糖、胡萝卜素、矿物质、氨基酸、淀粉、维生素，具有防癌、通便的作用，可以煲汤熬粥，也是良好的食物之一。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 也有一些不适合化疗患者的食物，主要是含5-羟色胺的食物，常见食物有香蕉、核桃和茄子。也应少食含有色氨酸的食物，常见食物有小米、腐竹、豆腐皮、虾米、紫菜、黑芝麻等，但并非禁忌，如患者有意愿吃这些食物，可以少量给予。&nbsp;</span>
</p>
