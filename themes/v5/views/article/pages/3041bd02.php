<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3041/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">机器人系统的4只手臂</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四川新闻网3月19日讯（记者 刘佩佩）“机器人臂”代替人手，进入人的体腔，360度无死角转动。近日，川大华西医院泌尿外科团队使用达芬奇Si机器人系统，在泌尿外科主任魏强教授的带领下，完成了医院首台机器人辅助膀胱切除术。据魏强介绍，与常规的腹腔镜手术相比，机器人辅助的腹腔镜手术是3D视野，能够达到更精确、精细地效果，病人的创伤更小。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">韩先生今年50岁，云南昆明人，患有膀胱癌1年多。这次他的膀胱切除术就是机器人系统辅助完成的。3月17日，魏强和他的团队为韩先生进行了膀胱切除，整个手术持续2个小时左右，与没有机器人系统辅助相比，减少了近一半时间。“3到5天就可以出院了，病人的出院时间也有所减少。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">机器人手臂在整场手术中担任着怎样的角色？魏强打了一个形象的比喻，常规的腹腔镜手术是在微创的情况下，将医生的手延伸到患者体腔内。而机器人辅助下的腹腔镜手术则是把医生的手缩小，进入到患者的体腔。缩小的好处就在于留下更多的视角空间，并且更为灵活。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术过程中，魏强的助手和护士站在手术台前，而他则坐在一旁的3D平台上，通过手指精确地操控主机上的4只机械臂。在机械臂的末端安有可转腕器械，可以在人的体腔内的狭窄解剖区域中灵活的转动。“这样手术过程中的切割和术后的缝合都能更为精确和精细。”魏强说，医生通过3D平台，能够看到光学放大10倍的高清晰立体图像，使得手术的部位更为精准。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3041/3.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在机械臂的末端安有可转腕器械，可以在人的体腔内的狭窄解剖区域中灵活的转动。（图为用鸡腿缝合实验）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“精确精细地手术能够使出血量少并且保护患者体腔内的健康组织。”魏强告诉四川新闻网记者，使用机器人辅助腹腔镜手术对医生的要求更高，在手术前，需要到香港一家有资质的培训机构培训并取得执照，执照有两种，主刀证和助手证。“这套系统必须输入取得执照医生的ID才能启动，不是谁都能动，这样就保证了手术的安全性。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据魏强介绍，目前，机器人辅助腹腔镜手术主要运用在泌尿科以及普外科、妇科、肠外科等需要做腹腔镜手术的手术中。</span><br />
