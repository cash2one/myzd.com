<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>隐形胆囊取石术:一种更加人性化的治疗方法&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯（记者 鲁雁南）做过胆囊切除手术的人，常常笑称自己是“无胆英雄”，但实际上却并非如此“潇洒”：有的人术后容易出现腹泻、胃肠功能淆乱等症状，有的人则在心理上产生“少了点什么”的空虚感。东方医院微创医学部主任胡海告诉记者，患者内心都希望“保胆”，只是习惯了传统的“一刀切”疗法，“其实那些病发早期、功能尚好的胆囊，完全可以保留下来”，由其首创的隐形胆囊取石术今年已成功为20例病患保住了胆。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从“一刀切”除胆到“刀下留情”保胆，医生的理念转变了，患者得到的是更加个性化、人性化的服务。胡海说，现代人高脂、高油食物吃得多了，胆囊结石的病例日益增多、患者也日趋年轻化。“以往此类手术唯一的方法就是切除，每天一切就是10来个，几年里已切除了1.8万个胆囊，但在手术中发现，有的胆囊其实取出结石之后还是可以保留的，为什么不想办法保留呢？”胆囊的功能在于贮存、浓缩胆汁，平衡胆道压力、调节消化系统功能，有条件“保胆”的话，谁愿意做“无胆英雄”？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今年初，胡海在新成立的微创医学部大胆尝试隐形胆囊取石术，开全国先河。据介绍，这一创新疗法是将胆道镜与腹腔镜结合，在微创手段取出结石的同时确保胆囊完好，术后几乎没有疤痕。虽然不需要额外添置设备，但在技术上要求极高，也并非每一位患者都适合“保胆”，术前都需要经过彻底、细致的评估。对于那些多发性结石病患者，还是应采取切除疗法“以免后患”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胡海的电脑里，存放着许多“保胆”患者的照片，免做“无胆英雄”的他们个个笑容灿烂。今年30岁的杨小姐患有胆囊结石，家里人很早就劝她手术切除，但她却一直犹豫不决。经过咨询、检查和评估，胡海发现其胆囊体积无异常、轮廓清晰，符合保留条件，便为其实施隐形胆囊取石术，成功“保胆”的杨小姐连声感谢：“这下它又可以陪伴我几十年了！”</span>
</p>
