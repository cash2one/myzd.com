<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;"><strong>节前如何预防痔疮复发</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 随着中秋、国庆一天天临近，许多肛肠病患者都在寻求彻底摆脱痔瘘困扰，轻松、健康地度过黄金周假期。据济南肛肠医院门诊资料显示，痔疮、肛瘘、结肠炎患者猛增了六成以上。为此，医院特别开展了北京、南京专家大型会诊活动。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 祛除痔疮，轻松度中秋</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 鄄城的痔疮患者林先生告诉笔者，他患有重度混合痔六七年了，平时大便带血，排便时还有肿物脱出肛门外，剧痛难忍。不敢吃辣，更不敢喝酒，每次过年过节，都是自己的受难日。今年中秋节许多同事、朋友都商量着去外地旅游，可林先生却苦于有“痔”之身受不得旅途颠簸，思来想去，终于决定找一家专业的肛肠医院彻底祛除痔疮，好好享受一次轻松、健康的假期。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 据济南肛肠医院首席专家王保学院长介绍，临近中秋、国庆节，许多肛肠病患者选择了节前彻底治疗。为此，他提醒广大肛肠病患者，节假日期间应尽量避免吃辣喝酒，以免痔疮发作。但是，要彻底祛除肛肠病，进行专业、正规诊疗是关键。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 国内两大肛肠治疗技术汇聚济南</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 针对目前治疗痔疮、肛瘘扎堆现象，济南肛肠医院启动了大型专家会诊活动，即日起至10月8日，所有肛肠微创手术费、安氏中药剂减免10%；10月2日～4日北京中日友好医院王晏美教授、10月5日～6日南京市中医院王业皇教授将在济南免费会诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 据了解，会诊专家将采用卫生部“十年百项”计划推广技术安氏疗法以及国家“十五”项目丁氏疗法进行治疗，两大疗法均以中医治疗为主，中西医结合，突出微创、无痛、治疗彻底、随治随走的特色。据介绍，安氏、丁氏两大肛肠治疗技术的结合为国内首次！(深海)&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 随着中秋、国庆一天天临近，许多肛肠病患者都在寻求彻底摆脱痔瘘困扰，轻松、健康地度过黄金周假期。据济南肛肠医院门诊资料显示，痔疮、肛瘘、结肠炎患者猛增了六成以上。为此，医院特别开展了北京、南京专家大型会诊活动。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 节假日注意三大“节日病”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 据介绍，痔疮、肛瘘、结肠炎是三大“节日病”，患者一不注意就可能发作或加重。节日期间，人们往往都有很多聚会、酒宴，在酒精、辛辣的刺激下，很容易导致直肠黏膜血管曲张、扩大，从而导致痔疮突发，轻者便血、眩晕，重者可能导致痔疮急性嵌顿、坏死等严重后果。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 针对女性肛肠病患者的疑惑，女子肛肠病专家金清珍主任介绍说，由于女性特殊的体质和生理结构，需要更加注重肛肠病的防治。节假日、孕期是女性痔疮患者的高危期，辛辣、酒精刺激以及久坐久站等不但可导致痔疮加重，还会造成一系列妇科病，孕妇还会影响胎儿发育。医院针对女性患者专门成立了由女专家坐诊的女子肛肠病诊疗中心。</span><br />
