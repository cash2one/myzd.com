			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肝癌年轻化趋势令人忧虑</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2907/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">定期做血清甲胎蛋白含量检测和B超，有助及早发现肝癌 图/新华社</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 受访专家/中山大学肿瘤防治中心肝胆科主任 陈敏山教授</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 文/羊城晚报记者 陈映平</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 实习生 郝小书</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 35岁以上乙肝丙肝患者，须半年检查一次</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4月12日，年仅31岁的《南方都市报》记者过国亮因罹患晚期肝癌去世了。消息一出，人们在扼腕叹息的时候不禁疑惑，肝癌是如何找上门的呢？年轻人身体强壮，充满活力，按照常理应该具有很高的抵抗疾病的能力，怎么也被肝癌缠上呢？应当如何监测自己的身体健康，以便尽早发现癌变呢？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 年轻的肝癌患者</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 恶性化程度比老年患者高</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “目前肝癌有高危群体年轻化的倾向。年轻人由于细胞活力强，一旦罹患肝癌，癌细胞的增长速度会很快，肝癌的恶性化程度也就会比老年人更高。”中山大学肿瘤防治中心肝胆科主任陈敏山教授说。陈敏山自己也曾接诊过不少三十多岁的肝癌病人。他强调，年轻人也决不能自恃年轻强健而忽视对肝癌的防范。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 陈敏山介绍，肝癌在我国的严峻形势不容小觑。据2008年的统计数据，我国每年肝癌发病例数超过40万，占全球发病人口的一半多，而肝癌死亡例数为37.1万，占据了全球因肝癌死亡病例的53.36%，成为严重威胁人们健康的重大疾病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 压力大、熬夜、喝酒</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 会增加患肝癌几率</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 陈敏山说，目前在我国，大部分的肝癌都是由病毒性肝炎发展而来的。此外，酒精、黄曲霉素、亚硝酸盐、饮用水污染、寄生虫感染和遗传因素也会导致肝癌的发生。据《广州社区人群肝癌高危因素调查》显示，肝炎病毒、黄曲霉素污染、酗酒、饮用水污染和肝吸虫感染是诱发肝癌的五大高危因素。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有慢性肝炎病史（乙肝、丙肝）；家族中已有确诊肝癌患者；长期酗酒者；长期食用腌腊、烟熏、霉变等食品者；长期工作压力过大、经常熬夜或长期精神压抑者等，都是肝癌的高发人群。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 35-4O岁中青年</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 每年体检早发现</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据介绍，过国亮起初只是由于腹部疼痛不适而入院，一经检查发现已是肝癌晚期。那么，如何能及早发现肝癌呢？陈敏山介绍，由于早期肝癌基本没有什么明显的症状，患者自己很难察觉。而晚期的治疗又非常困难，所以早期筛查对于肝癌防治具有重大意义。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “在肝癌早期发现并进行手术治疗，病人的五年生存率可达到50%，治疗费用在5万元左右。但肝癌一旦发展为晚期，不仅治疗费用大大增加（可能达到20万左右），且5年生存率很低。所以公众一定要重视肝癌的早期筛查，尤其是高危人群。”</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我国目前的肝癌筛查方式主要是血清甲胎蛋白（AFP）含量检测和超声检测。陈敏山认为，鉴于肝癌发病年轻化的特点，对于35岁-40岁的人来说，如果本身是乙肝或丙肝病毒感染者，即为肝癌高危人群，应当每6个月进行一次相关检查。如果是有肝病家族史的人，至少每年应进行一次检查。而对于没有病毒性肝炎感染史和肝病家族史的人来说，最好每年做一次癌症相关检查，以便同时预防其他高发癌症。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 陈敏山说，高危人群做好早期筛查，同时养成健康生活方式，注意饮食和作息规律，不酗酒，不吃腌制食品等，都可以有效降低患上肝癌的风险。&nbsp;</span><br />
