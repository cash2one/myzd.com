			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>面部色斑的种类和治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">色斑是最让女性朋友烦恼的事情了，中国人以白为美，素有“一白遮百丑”之说。随着年龄的增长，皮肤表面会逐渐出现各种各样的色斑，如黄褐斑、老年斑、雀斑、褐青斑、炎症性色素沉着、太田痣等，对于不同的色斑，其治疗方法各有不同。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从深度上来说，有的色斑非常表浅，就在皮肤的最外面一层，比如雀斑、早期的老年斑，有的色斑就在皮肤的深层，比如黄褐斑、褐青斑等。从色斑的形状上来说，有的是细小或斑块状的，比如雀斑和老年斑，有的是成片成团边界模糊的，比如黄褐斑。不同的色斑，其预防和治疗方法就完全不一样，需要区别对待，而且，在中老年妇女，面部往往还不止一种色斑，需要按照次序依次治疗，不可随意处理。所以，您首先要到医院去看看，确定自己的色斑是哪一种或哪几种，然后再让医生为您设计一个综合的治疗方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、 &nbsp;雀斑。雀斑是一种遗传性的色素性疾病，表现为全面部尤其是面中部散在的大小不等的细小色斑，多见于白皙干燥的皮肤类型。雀斑在白种人中很常见，在西方并不认为雀斑是不美丽的表现，而在中国，素来以“洁白无瑕”为最高境界，所以求美者有治疗的要求。雀斑的色素是在皮肤的最外面一层，比较容易去除，以往采用激光的方法对雀斑一一进行烧灼，去除色素，治疗对皮肤有创伤而且耗时，由于雀斑细小，治疗时还难免会有遗漏。也有用化学剥脱的方法治疗，使用化学制剂（如酚类或果酸等）将皮肤最外面一层包括雀斑一并剥除，以达到治疗的效果。自从有了光子机以后，光子（强脉冲光）治疗已经成为去除雀斑的金标准，可以在不损伤正常皮肤的情况下轻松地将雀斑去除。但必须注意的是，由于雀斑是遗传性的色斑，所以在治疗后都可能会复发，需要重复治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、 &nbsp;色素性母斑。“色素性母斑”又叫做“咖啡牛奶斑”，是一种先天性的色素以异常性疾病，刚出生的时候一般比较淡或者不明显，随着年龄的增长慢慢地明显起来，颜色就像是加了牛奶的咖啡一样，边界非常清楚，好像地图上的国界线。这种色斑和雀斑一样，色素位于皮肤的最外层，可以使用激光、磨削、化学剥脱和光子等治疗手段去除，治疗似乎并不复杂，但色斑异常顽固，不易根治，大部分病人在治疗后都会有程度不同的复发，有的甚至在治疗后数月内就复发。所以建议在开始阶段可以先选用创伤较小的光子治疗，可以最大限度地减少对正常组织的损伤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、 &nbsp;褐青斑。是一类比较特殊的色斑，又叫做“获得性太田痣”，因为这种色斑看上去和一种名叫“太田痣”的先天性青胎记几乎完全一样，但并不是出生就有，而往往是20岁以后才出现的，部分病人的家族内有类似的疾病史。。褐青斑的面积通常比较小，而且常常是在面部的左右两侧对称性生长，不会像太田痣那样生长到眼睛里面去。这类色斑的色素位于真皮层内，其治疗方法和太田痣一样，需要使用一种名叫“调Q开关激光”进行多次治疗，只要治疗得当并坚持治疗，可以达到完全治愈的效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、 &nbsp;老年斑。老年斑顾名思义是随着年龄增长而出现的皮肤色斑，这是一种轻微的皮肤疾病，多发于面部或手臂暴露的部位，早期仅仅是皮肤表面的比雀斑大一些的咖啡色斑点，这些斑点可以逐渐增大并渐渐突出于皮肤表面，少数斑点最后可以变成一个较大的斑块，有时还会有轻度的发红发痒等炎症性的表现，高出皮面的老年斑在医学上又称作“脂溢性角化病”。老年斑对身体健康没有什么妨碍，但是会影响到容貌，给人以衰老的感觉，所以从美容角度出发应该进行治疗。对于不高出皮面的老年斑使用Q开关激光治疗，对于高出皮面的老年斑，可以选用CO2激光烧灼治疗，一般1次就可以达到完全消除的效果，如果是多发性的老年斑，可以分次治疗。</span><br />
<p>
