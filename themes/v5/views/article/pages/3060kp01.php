			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>关节置换不可怕</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">街道王大妈下楼梯不小心跌跤，拍片发现是股骨颈骨折，需要做髋关节置换手术。家属认为是一个大手术，战战兢兢，担心不已，结果手术后恢复的非常好。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着社会的老龄化和交通事故的增多，关节置换越来越普遍。通过采用金属或高分子的人工关节替换掉病变磨损的关节，称为关节置换术，它不仅可以减轻你的痛苦，还能让你轻松地从事日常活动。过去认为是大手术，现在县级医院都能开展了。近年来为90岁以上老人行髋关节置换术成功的报道也屡见不鲜，术后恢复良好。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前医学上能够开展髋关节、膝关节、肩关节及肘关节等关节置换术。第一例全髋关节置换术开展于1960年，以后随着手术技术和外科材料学的发展，全髋关节置换术治疗效果明显提高。据报道，美国每年大约要行168 000例全髋关节置换术。手术大约需要2～3个小时，将病变的骨及软骨去除掉，再安放上金属或塑料假体，以恢复下肢的力线及关节功能。主要手术指征有关节炎和髋部骨折，包括骨性关节炎、类风湿性关节炎、创伤性关节炎、股骨颈骨折等。最常见的关节置换是髋关节和膝关节。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 髋关节置换&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如果你的情况符合以下几点，一般来说你就应该进行髋关节置换了：(1)髋关节的疼痛已经限制了你的日常活动，如走路、弯腰等；(2)无论白天或黑夜休息时都存在髋关节疼痛；(3)髋关节僵硬使你不能行走或抬腿；(4)服消炎药治疗后髋关节疼痛减轻的程度很轻微；(5)服药后有严重的副作用或感到非常不适；(6)经过其他治疗仍无法减轻髋关节疼痛。&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 大约有90％以上的病人在全髋关节置换后，症状得到明显改善，髋关节疼痛明显减轻，从事日常活动的能力明显改善。大多数髋关节置换的患者，术后第二天就可以在医生的指导下，借助于助步器开始站立、行走等活动。全髋关节置换术后发生并发症的可能性很低。据报道，一些严重并发症的发生率低于2％。术后在家的活动至关重要，特别是在最初的几周内。一般说术后3～6周就能恢复到可以从事日常活动的程度。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 术后应该注意的问题：(1)预防血栓形成；(2)预防感染；(3)防止摔倒；(4)不要做盘腿的动作；(5)屈曲髋关节时不能超过90度。(6)不要过度的内翻或外翻你的脚；(7)晚上睡觉时在两腿之间放一个枕头。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 膝关节置换&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 膝关节置换是目前国内外治疗晚期骨性关节炎、类风湿性关节炎行之有效的方法之一。手术将病变的膝关节表面软骨及病变骨切除，换用生物和金属材料覆于膝关节表面，避免了活动时膝关节各骨之间直接产生摩擦，因而不会引起疼痛，达到止痛、增加膝关节活动范围、矫正畸形的目的，而且大大改善了膝关节的功能，提高病人的生活质量。人工膝关节的安放不会对人体产生不良作用，不会危害人体的健康。经过大量的临床实践证明，人工膝关节置换是治疗晚期骨性关节炎的最佳选择。</span>
</p>
