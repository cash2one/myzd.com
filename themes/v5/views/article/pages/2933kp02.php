		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>预防白内障从生活点滴开始（科普）</strong></span>
</p>
<p>
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">眼睛是心灵的窗户，它是我们认识世界和感受世界的很重要的一种途径。但随着岁月流逝，我们的“窗户”也会蒙上一层“纱”，这就是通常所说的白内障，这种随着年龄的增长而发生的白内障在医学上叫做老年性白内障。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">老年性白内障的主要症状是渐进性视觉模糊。在全球范围内，老年性白内障是致盲的主要眼病之一。国内的流行病学显示，在北京有5.04%～12.5％的盲人是因白内障所造成的；而60岁以上的老年人得白内障的病人数直线上升，80岁以上的老年人100％会得白内障。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">随着生活水平的不断提高，老年人也越来越重视自己的生活质量，当然也比较关心自己的视觉质量，很多人会问，老年性白内障能预防吗？如何预防？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">老年性白内障的发病原因到现在也不是完全明了，所以对它的预防一直是眼科医学界探索的重要问题。临床研究表明，可以从以下几个方面进行预防：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1、防止眼睛接受长期、大量紫外线的照射。太阳光紫外线照射时间越长，患白内障的可能性就越大，因此，老年人在太阳光强烈时出门可戴防紫外线的太阳镜。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2、防止脱水。人体脱水时，体内液体代谢紊乱，会产生一些异常的化学物质，损害晶状体，导致白内障发生，而对已有白内障患者，脱水可使病情加剧。因此，一旦遇到各种原因引起的腹泻、呕吐、大量出汗等，都应该及时补充水分，而平时只需喝白开水、茶水即可。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3、加强营养，宜多摄入富含维生素、微量元素、蛋白质的食物。如可多吃苹果、香蕉、圣女果等维生素含量丰富的水果，多吃菠菜、上海青、西兰花等深绿色系蔬菜，多吃鱼类等富含蛋白质的食物。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4、糖尿病是白内障发生的一个高危因素，控制糖尿病可以减少白内障的发生。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5、避免白内障易感因素，晶状体对各种电离辐射敏感，如X线、红外线、紫外线等。生活中各种化学毒物及药物均可引起白内障，如药物中的糖皮质激素、氯丙嗪等，化学制剂中三硝基甲苯（TNT）、氟化物及各种重金属离子等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">此外，还应保持情绪舒畅、心情愉快，戒烟，保证充足的睡眠，注意用眼卫生和保健，积极防治糖尿病等全身性慢性病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">从这些生活的点滴做起，将能阻止和延缓老年性白内障的发展，让老年人也能享受清晰的美好生活。</span>
</p>
