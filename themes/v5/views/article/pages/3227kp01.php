<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">事实上，普外科是一个专科，主要治疗肝胆胰脾、胃肠道、甲状腺乳腺、疝气等方面的疾病。有些医院将普外分成肝胆外科、胃肠外科和甲乳外科等几个分科。另外在一些医院，没有专门的血管外科、肛肠科，这些疾病也是由普外医生来诊治。因为，诊治的疾病比较多，很难用一个简单的词汇来概括，因此称为普通外科（General Surgery）。这些疾病中有些比较简单，有些很复杂，一点也不普通哦。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外有些简单的外科疾病，譬如体表肿块、体表感染、烫伤、甲沟炎等等，也是由我们来治疗。</span><br />
