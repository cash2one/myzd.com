<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病人就诊时往往会对普外科的名字感到疑问，是不是简单一点的外科问题都可以来找普外医生。而像骨科、眼科、泌尿外科这些名字通俗易懂，病人一看就知道是看哪一块疾病的。</span>
