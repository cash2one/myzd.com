<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病例1：一个发作性头晕恶心呕吐的患者，让患者做头颅CT，患者及家属拒绝，强行留院观察，予常规扩容，改善循环等处理。第二天早晨，下级医师说该患者唤不醒了，急复查头CT，提示大面积小脑梗塞，压迫第四脑室，梗阻性脑积水，急予侧脑室穿刺引流，最终患者呼吸心跳停止死亡。</span>
</p>
