<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">神经外科疾病中头晕的症状也很常见。对于头晕的患者来诊时，无论临床表现的轻重，一律按重患对待，高度重视，向家属交代好病情。因为此类患者病情变化快，尤其是椎基底动脉系统的梗塞或出血，病人来的时候神志清楚，一转身的功夫就可能出现昏迷、瞳孔散大，危及病人的生命。</span>
</p>
