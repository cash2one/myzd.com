	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>北京医生 赤峰摘瘤</strong></span><br />
</div>
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4天前，52岁的教师老严从北京回到赤峰市，昨天在家门口的赤峰市宁城县医院接受了北京知名专家实施的食管癌手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">受国家卫生计生委派遣，来自中国医学科学院肿瘤医院的12位国内最顶级肿瘤专家组成国家级医疗队，本月13日晚抵达内蒙古赤峰市，在宁城、林西等县进行为期一周的义诊。该院院长赫捷说：“我们这次组织了一支最顶级的专家队伍，十几位专家都是知名教授，还有好几位科主任。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一到宁城，专家组就马上进入医院病区进行查房。主任医师牟巨伟就是在查房时遇到的老严。“我前天就住进来等着您了。”老严说。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">不久前，老严发现自己得了食管癌，家人带着他匆匆忙忙赶到北京肿瘤医院，挂的就是牟巨伟的号。在肿瘤医院检查，牟巨伟发现老严需要手术治疗，但在北京手术不仅要排队等床位，而且老严和家人在北京的食宿也是一笔不小的开销。老严有点犯愁。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">牟巨伟看病历发现老严是赤峰人，突然说：“我们下周要到宁城义诊，正好，我到那儿去给你做手术。”老严一家当天就离开北京，第二天就住进宁城县医院，做好术前检查，等着北京的专家。昨天上午9时42分，在宁城县医院外科楼8号手术室，老严的手术开始了，牟巨伟一边手术，一边给宁城县医院的四五位年轻大夫讲解。1个多小时后，手术顺利完成。老严和家人很开心，在家门口做手术费用节省了一半多。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">与此同时，9号手术室里，肿瘤医院妇瘤科主任吴令英正在为一位19岁的女孩切除卵巢肿瘤。10时20分，一个长约20厘米、厚度14厘米、大小如同一只柚子的巨大肿瘤，从女孩体内取出，一旁观摩手术的基层医院医护人员长了见识。切除了一侧卵巢后，吴令英为这名年轻的患者保留了另一侧卵巢，这意味着患者今后能够正常生育。十来位女孩家属长舒一口气：“真没想到北京的专家能来这儿给我们做手术，真是不幸中的万幸。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在医院一层门诊大厅，还有几位来自北京的专家为患者义诊咨询，一上午就接待患者200人次。很多人拿着检查结果要寻求治疗方案，还有好几位肿瘤患者都是在北京做了手术，本来应该去北京复诊，听说有大专家来，直接来县医院复查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">不仅仅是在赤峰。国家卫生计生委本周启动60万医务人员参加的首次全国大型义诊，派出8支国家医疗队赴陕西、内蒙古、青海、甘肃等边远贫困、优质医疗资源缺乏的地区，开展疑难病例会诊、实施手术、技术管理培训等义诊活动。今后，每年9月的第三周都将举办“服务百姓健康行动”全国大型义诊周活动，为群众“送医、送药、送健康”。</span>
</p>
