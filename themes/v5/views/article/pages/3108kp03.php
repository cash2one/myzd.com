	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>非肌层浸润性膀胱癌的治疗及随访(转载)</strong></span><br />
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">非肌层浸润性膀胱癌:既往称为表浅性膀胱癌，占初发膀胱肿瘤的70%，手术是主要的治疗手段。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一、手术治疗：</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 1.&nbsp;经尿道膀胱肿瘤切除术&nbsp;经尿道膀胱肿瘤切除术( TURBT)&nbsp;既是非肌层浸润性膀胱癌的重要诊断方法，同时也是主要的治疗手段。膀胱肿瘤的确切病理分级、分期都需要根据首次&nbsp;TURBT&nbsp;后的病理结果确定。经尿道膀胱肿瘤切除术有两个目的:一是切除肉眼可见的全部肿瘤，二是切除组织进行病理分级和分期。&nbsp;TURBT&nbsp;术应将肿瘤完全切除直至露出正常的膀胱壁肌层。肿瘤切除后，建议进行基底部组织活检，便于病理分期和下一步治疗方案的确定。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2.&nbsp;经尿道激光手术&nbsp;激光手术可以凝固，也可以汽化，其疗效及复发率与经尿道手术相近，术前需进行肿瘤活检以便进行病理诊断。&nbsp;2μm连续激光的能量被组织中的水分完全吸收而达到汽化切割作用，可用于准确的汽化切割膀胱壁各层，不影响肿瘤病理分期，已有报道用于治疗非肌层浸润性膀胱癌。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3.&nbsp;其他治疗选择&nbsp;&nbsp;（1）光动力学治疗、（2）膀胱部分切除术、（3）根治性膀胱切除术</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二、术后辅助治疗：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">非肌层浸润性膀胱癌&nbsp;TURBT&nbsp;术后有很高的术后复发率，小部分患者甚至会进展为肌层浸润性膀胱癌。原位癌单纯&nbsp;TURBT&nbsp;手术并不能解决术后高复发率和疾病进展的问题。因此，推荐所有非肌层浸润性膀胱癌患者进行术后辅助性膀胱灌注治疗，包括膀胱灌注化疗和膀胱灌注免疫治疗。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、 膀胱灌注常用药物：吡柔比星、表柔比星、多柔比星、羟喜树碱、丝裂霉素，吉西他滨也可用于膀胱灌注化疗。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、免疫治疗常用药物：卡介苗(BCG)&nbsp;，其它还包括干扰素、匙孔虫戚血蓝蛋白等。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三、随访</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在非肌层浸润性膀胱癌的随访中，膀胱镜检查目前仍然是金标准，检查过程中一旦发现异常均应该行活检及病理检奈。超声学、尿脱落细胞学、&nbsp;IVU&nbsp;等检查也有一定的价值，但均不能完全代替膀胱镜检查的地位和作用。推荐所有非肌层浸润性膀胱癌患者在术后&nbsp;3个月时进行第一次膀胱镜检查，但如果存在手术切除不完全、肿瘤发展迅速可适当提前，以后的随访根据膀胱癌复发和进展的危险程度决定。高危患者推荐前&nbsp;2&nbsp;年每&nbsp;3&nbsp;个月行一次膀胱镜检查，第3&nbsp;年开始每&nbsp;6&nbsp;个月一次，第&nbsp;5&nbsp;年开始每年&nbsp;1&nbsp;次直到终身;低危患者如第一次膀胱镜检查阴性，建议术后&nbsp;1&nbsp;年时行第二次膀胱镜检查，之后每年&nbsp;1&nbsp;次直到第&nbsp;5&nbsp;年;中危患者随访方案介于两者之间，依据患者个体预后因素和一般情况决定。随访过程中，一旦出现复发，治疗后的随访方案按上述方案重新开始。</span>
</p>
