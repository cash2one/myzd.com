</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">胃癌的致病因素目前研究发现大致与以下情况有关：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1. 幽门螺旋杆菌（HP）感染：HP感染率高的国家和地区常有较高的胃癌发病率，且随着HP抗体滴度的升高胃癌的危险性也相应增加。但HP是如何诱导胃癌发生的机制尚未完全明了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2. 化学因素：亚硝胺类化合物及多环芳烃类化合物等均能致癌。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3. 饮食因素：是胃癌发生的主要病因，其中最常见的有亚硝盐类化合物。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4. 地域及遗传因素：东亚三国发病率高，欧美国家发病率相对较低。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5. 胃的癌前病变和癌前疾病：癌前病变指的是容易发生癌变的胃粘膜病理组织学变化，公认的是高级别上皮内瘤变。癌前疾病指发生胃癌危险性明显增加的临床情况，如胃溃疡，胃息肉，萎缩性胃炎，残胃等。</span><br />
