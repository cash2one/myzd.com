		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>8个月男婴重获健康心</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003645289412" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 本报讯（记者陈青）这是一次没有成功经验可参照的手术，唯一可确定的是，如果不手术，眼前这个8个月大的患有罕见胸骨裂五联症的男婴，将不会有生存的希望。昨天，复旦大学附属儿科医院胸外科主任贾兵教授与他的团队，经过4个小时的努力，成功完成了从心脏修补到给“裸心”穿上保护“外衣”的一系列根治术，为患儿重建了一颗健康的心脏。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 胸骨裂畸形心脏裸露</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 8月大的小明瑞来自山东省一个普通农民家庭。出生后不久，父母就发现孩子胸口骨头没有长好，胸骨下部少了一大段，一层如纸一样薄的皮肤包着一个鸭蛋大小突出的搏动性“肿块”，每遇哭闹后，“肿块”就会增大，并且剧烈跳动。</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">孩子的生长发育也特别缓慢，8个月时体重仅5.5千克，仅相当于正常孩子满月时的重量。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当地医院初步检查孩子患有先天性胸骨裂畸形，而那个搏动的“肿块”，竟是完全不受保护的心脏。如此复杂的心脏疾病，当地没有一家医院可以提供积极治疗。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 发病率百万分之五以下</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 7月16日，儿科医院接到来自长期对口援助单位山东省泰安市妇幼保健院的求救电话。听完简短的病情介绍后，胸外科主任贾兵教授当晚飞往泰安会诊，最终诊断孩子患上了“五联症”，这是一种极其罕见的先天性胸腹畸形，其发病率在百万分之五以下。由于其临床症状严重，大多患儿于新生儿期即夭折，最终可接受手术的患儿非常少。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术面临四大难题</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 由于患儿仅8个月大，心内畸形复杂，手术的耐受性很差，这给手术带来了极大的难度和风险。在术前讨论中，各科专家提出必须面对的四大难题：一是孩子心脏畸形复杂，体外循环时间长，心内操作范围广，心肌牵拉严重，手术后短期内可能出现心肌水肿、心脏扩大；二是婴儿胸腔本身偏小，同期完成胸壁重建及心脏的畸形纠治十分困难；三是要给“裸心”穿上“外衣”，“衣料”哪儿来？由于人工材料没有可生长性和存在感染的高风险性，选择自身材料修补胸骨缺损是必然的，而对于仅8月大的小明瑞而言，可选择的“材料”并不多；四是患儿同时存在胸骨缺失及心脏畸形，心脏修补后再回纳，能否适应新的胸腔环境，仍是个未知数。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术历经4个小时</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术于昨天上午八点施行。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 8：49贾兵划下了第一刀，手术正式开始。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 10：30手术组顺利完成心脏根治术，并停止体外循环，成功实现修补的心脏复跳。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 11：30在仔细观察后，采取双侧肋软骨离断转移重建胸壁的方法，为“裸心”穿上坚实的“外衣”。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 12：05关胸，手术宣告成功。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 截至发稿时，转入重症监护病房的小明瑞各项生命体征正常。</span>
</p>
