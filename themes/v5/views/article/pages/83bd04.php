		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>北京大学肿瘤医院顾晋谈“医患沟通的艺术”</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">北京 2012-02-22(中国商业电讯)－－ 医患沟通是永恒的话题，加强医患沟通是改善医患关系的重要手段，加强医患沟通技巧，构建和谐医患关系是摆在广大医务人员面前的重要课题。2012年2月21日下午，北京北亚骨科医院特别邀请了北京大学肿瘤医院结直肠肿瘤外科主任、中华医学会北京肿瘤专业委员会主任委员顾晋教授做了题为《医患沟通的艺术》的精彩专题讲座，医院领导及全院员工200余人参加了此次讲座。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">顾教授从中国医院目前面临的困境开始了医患沟通的艺术讲座。他从医生本人、社会两个不同视角引出了为何医生的社会角色不被社会认同的话题。顾教授从医学模式转变后，医生应负责任加大以及患者心理需求变化等方面分析了我国医患沟通不良的原因，指出医生加强人文素质和科学精神对改善医患关系的重要性，强调人文素养会直接影响医生在社会中的形象。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">顾教授还分析了什么是人文，作为医生应具备的人文素质和人格特征，讲授了医患面谈中的基本原则，即C-L-A-S-S原则(C：氛围，L：倾听，A：情感，S：对策，S：总结)，用“如何告诉病人坏消息”的实例阐述了医生在与病人面谈中的人格魅力、谈话艺术和内容，以及如何避免坏消息(即与病人的意愿背道而驰的消息)的不经意传播。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">顾教授将行医数十年的亲身经历和对医生应具备的人文素养的不断思索渗透整个讲座中，生动的事例和幽默的话语深深地吸引了在座的每位医务人员，让大家在笑声中对医生这个职业应具备的素质有了更深的理解和认识。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，北京北亚骨科医院作为北京市一家大型现代化骨病专科医院，以股骨头坏死、强直性脊柱炎的治疗为核心品牌，探索并实施了以疗效为核心、安全为底线、人文为追求的LAR质量管理体系，医院以其领先的医疗技术、和谐的医患关系赢得了国内外大批患者的信赖和支持，在社会上一直享有很高的盛誉。相信通过此次顾晋教授的精彩讲座，将进一步加强该院广大医务人员的医患沟通技巧，促进医患关系的和谐有序发展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">顾晋，现为北京大学肿瘤医院结直肠肿瘤外科主任、主任医师、博士生导师，中华医学会北京肿瘤专业委员会主任委员。自1987年以来，顾教授始终从事消化道肿瘤的临床和应用基础研究，始终坚持工作在临床第一线，对消化道肿瘤的诊断和治疗积累了丰富的经验。在教学方面培养了多名硕士和博士研究生，先后发表论文80余篇。在国际权威杂志发表多篇论文。2008年获北京市科技进步三等奖，中华医学三等奖。2009年获北京大学杨芙清-王阳元院士教学科研奖。</span>
</p>
