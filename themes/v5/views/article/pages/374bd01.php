			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>复旦大学附属肿瘤医院：早期食道癌可在内窥镜下根治</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早期食道癌手术治疗，是用“开膛破肚”式的切除术还是在内镜下切除？复旦大学附属肿瘤医院由学科带头人陈海泉教授带领胸外科主任相加庆教授以及团队成员，5年累计完成内窥镜下治疗食道癌200例左右，规范治疗后的复发率及生存率与传统腔镜和开放手术疗效相当。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肿瘤医院胸外科已建立起沪上数量较多、类型丰富的食道癌生物样本库。张杰副教授2009年开始在美国匹兹堡大学从事内镜下切除的培训和临床工作，共完成300余例各种食管内镜下手术。自2011年起，肿瘤医院便在全国胸外科范围内率先开展内窥镜下的食道癌切除术，术后并发症发生率1%，完整切除的患者随访后发现生存率达到100%，局部复发率3.3%，并发症和死亡率的发生率低于国际及国内同类报道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 今年德国学者在胃肠道杂志发表了内镜下切除治疗食管粘膜内癌的长期随访数据，内窥镜下的食道癌切除术已经成为国际公认的、安全的、有效的早期食道癌治疗方案。经过多年探索，肿瘤医院胸外科专门制定了内镜下食道癌切除术的流程，包括肿瘤诊断标准、适应症、禁忌症、病理标准、并发症防治、出院及随访标准等。在张杰副教授看来，肿瘤医院内窥镜下的食道癌根治术由胸外科医师直接负责，能够有效地根据患者的肿瘤大小、病灶所处位置、浸润程度、患者意愿等“个性”，严格把握治疗指证，有效筛选出最为适宜的治疗人群，最大程度地规避治疗方式选择中的偏差，对早期食道癌进行全程管理。</span>
