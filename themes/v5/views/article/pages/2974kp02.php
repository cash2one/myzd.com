			<h1 style="text-align:center;">
	<span style="font-size:18px;font-family:'Microsoft YaHei';line-height:2;">搭桥手术后什么时候可以正常喝水？</span>
</h1>
<p>
	<span style="font-size:18px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个问题病人经常会问到，但也是医生最不好回答的一个问题，因为没有统一标准的答案，受太多因素的影响。总的来说，搭桥手术本身在术后早期，对心脏肯定是个打击，会影响到心脏的功能。因此，我们在术后早期会根据各种医院内的监测指标，控制液体入量和饮水量，甚至在病人觉得非常渴的情况下，还严格控制饮水，减轻心脏负担，利于术后的恢复。但是出院后，没有了各种监测设备和指标，即使您去询问大夫，大夫也不可能给您一个标准答案。这里给大家一个大体原则，如果术前心脏的功能没有受到影响，出院复查时心脏的功能也是正常的，那么出院后，可以不用太严格的控制饮水，不必限水到连吃饭都难以下咽，舌头和嘴唇都干裂的地步，只要觉得微微有点渴就可以了。三个月复查时，如果心脏功能正常，就可以正常饮水，也不要短时间内大量饮水，按少量多次的原则就可以了。但是如果术前心脏就因为心肌梗死导致心脏的功能明显受损，或者合并有室壁瘤和瓣膜疾病，那么饮水量就需要相对严格的控制了。出院后，早期要保持有点渴的状态，缓慢的根据情况逐步增加饮水量，严格掌握一定量的水，分更多次，每次更少量饮用的原则。同时要严密观察自己是否有憋气、咳嗽加剧，咳稀薄的白色泡沫痰，如果出现上述现象，就不仅仅是限制饮水的问题了，很有可能要加强利尿药的使用，一旦出现这种情况，最好是去医院就诊。</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还有一种情况，病人也会经常问，说：“大夫，我水控制的很好，一点也没有偷着喝水，可就是尿少，怎么办？”这种情况也是跟很多因素有关，例如病人的血压、肾功能、心功能状态、药物等因素有关，您可以联系您的管床大夫，他可能对您的病情更为了解，可以给出适当的解决办法。有条件还是到医院进一步检查，更为稳妥。</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关于喝水，部分病人理解还是不够，也可以说医生交待的的确没有那么细致。出了问题，来医院了，告诉大夫出院回家后的确没有喝太多水，“子女孝顺，一天喝三碗鳖汤补一补”，或者“每天不吃饭，只喝稀饭”，还有的“嘴基本不闲着，吃水果”等等，让医生哭笑不得。还有的病人，在病房里家属管着不让喝，自己上厕所时在卫生间喝生自来水！举这些例子的目的，就是告诉大家心脏手术后的饮水控制，也是治疗以及康复的主要内容之一，绝对不可忽视。嘴里舒服了，心脏可能就要受罪了，到头来受苦的还是自己。作为家属，也要和我们一起，做合格的监督员。</span>
</p>
