			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑瘫介绍&nbsp;</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑性瘫痪简称脑瘫，是指从出生前到出生后1个月内由各种原因导致的非进行性脑损伤，主要表现为中枢性运动障碍及姿势异常。目前，该病病因不清，对患者的运动功能影响较大，致残率较高，严重影响患者的生活质量。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>流行病学</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据我国调查，在0～6岁小儿中，脑性瘫痪的患病率为1.2‰～2.7‰，男女比例在1.13～1.57：1，我国有600万脑瘫患者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>发病原因</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑瘫是一种综合症，病因尚不完全清楚，目前认为从出生前到出生后1个月内，凡是可以引起非进行性脑缺陷或损伤，继而引起中枢性运动障碍的因素都可视为危险因素。具体分为包括产前、产时、新生儿期等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1．产前因素 &nbsp;母亲孕期有放射线接触、工业污染、宫内感染、服用某些药物、某些合并症、异常妊娠史；胎盘早剥、胎盘功能不良、脐绕颈和宫内窘迫等因素；胎儿脑畸形；遗传因素。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．产时因素 &nbsp;如新生儿窒息及产伤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3．新生儿期 &nbsp;如早产儿、低出生体重儿、由缺氧缺血和高未结合胆红素血症所引起的新生儿脑病、病毒性脑膜脑炎、化脓性脑膜炎等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>临床表现</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1．运动发育落后，主动运动减少：患者的粗大运动和精细运动发育均落后。新生儿时期动作减少，吸吮觅食反应差。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．肌张力异常：肢体及躯干出现肌张力折刀样、齿轮样增高或肌张力减低。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3．姿势异常：患儿在俯卧位、仰卧位，由仰卧拉成坐、直立位出现多种异常姿势，如“剪刀”步态和“X腿”等表现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4．反射异常：原始反射延缓消失、保护性反射减弱或延缓出现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5．其他表现 &nbsp;常合并癫痫、智力低下、语言障碍、感知觉障碍、脑积水等。患者的受累肢体越多，预后越差。痉挛型双瘫和偏瘫预后较好，手足徐动型及痉挛型四瘫预后差。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>治疗方法</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑性瘫痪尚无根治方法，目前主要采取功能训练、药物与手术相结合的方法来缓解症状，治疗应及早进行。提倡家庭训练和医生指导相结合，对运动障碍及合并的语言、智力、癫痫及行为异常进行综合干预。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1．功能训练</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常见方法有躯体训练、技能训练、语言训练、水疗、功能性电刺激，生物反馈疗法及矫形器的使用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．药物治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗脑瘫的药物疗效还远未达到理想状况，但医生仍然可以通过不同的药物和给药途径缓解患者的运动功能障碍。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常用口服药物有安坦、巴氯芬片。口服巴氯芬片时，部分患者尽管可能起效，但有时所需剂量很大，带来力弱、昏睡、恶心及其他副作用，且多数不能达到理想效果，这时可尝试鞘内巴氯芬泵治疗。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A型肉毒素（botulinum toxin type A）可抑制突触前乙酰胆碱的释放，从而阻断神经肌肉传递，常应于缓解上肢、下肢、头面部及颈部局部肌肉痉挛。注射后72小时出现明显效果，持续6周～6月，结合康复治疗和矫形器具，可延长治疗效果，无明显副作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3．手术治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于严重的痉挛型脑瘫患者，可通过神经外科或骨科手术缓解过高的肌张力和痉挛，改善异常姿势，以便获得最大的功能恢复。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1） 选择性脊神经后根切断术（SPR） &nbsp;手术可在脊髓腰段及颈段进行，分别缓解下肢及上肢广泛肌群痉挛。术中通过对脊神经后根小束进行电刺激，观察肌肉反应分辨出与痉挛形成有关的异常后根小束，切断这些异常小束即可减轻痉挛，同时减少对感觉及运动的干扰。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2） 选择性周围神经切断术（SPN） &nbsp;当患者痉挛症状比较局限，保守治疗无效，无固定挛缩畸形时，可采用SPN术。该手术创伤小，部位灵活，效果肯定。如肌皮神经SPN手术可治疗肘部痉挛，尺神经、正中神经SPN手术可治疗腕部和手指痉挛，胫神经SPN手术可治疗踝痉挛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3） 立体定向神经外科手术 &nbsp;对震颤和肌张力障碍型效果较好。采用影像立体定向技术和微电极导向神经电生理技术分别对脑内核团进行解剖定位和功能定位，然后用射频电极加热毁损特定核团，定位准确，疗效肯定。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4） 鞘内巴氯芬泵 &nbsp;鞘内巴氯芬泵即持续鞘内巴氯芬灌注系统（CIBI）。它采用外科手术将程控泵和导管埋在人体内，通过程控泵和导管将巴氯芬注射剂按照设计剂量直接持续注入脑脊液中，对CP引起的肌肉痉挛有显著效果，对不自主运动也有改善。具有以下特点：剂量微小，副作用少；采用程控装置调节药物；剂量可调，满足个体需要。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（5） 矫形手术 &nbsp;神经科手术不能完全代替传统的矫形外科手术，特别是肢体已经出现明显的固定挛缩畸形时，矫形外科手术将成为前述手术的必要补充。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，脑瘫尚无根治方法，目前仍以综合治疗为主，为持久改善功能，上述各种外科手术治疗后都不能忽视正规持久的功能训练。</span>
</p>
