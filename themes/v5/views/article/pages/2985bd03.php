		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肺癌可能已经变成慢性病</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">南都讯 记者曾文琼 目前，2016年C SC O青年医师论坛在哈尔滨举行，记者从会上获悉，近年来，靶向等新的治疗药物出现了，一些联合化疗的优化治疗方案的出现，使晚期肺癌的生存期可达到三年，甚至达到五年的，因此出现了肺癌是慢性疾病的说法。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">广东省人民医院广东省肺癌研究所钟文昭教授在向记者介绍：“肺癌里面，晚期肺癌占了大概70%左右，只有20%- 30%是早期肺癌。过去十年，肺癌的治疗获得了众多突破，靶向治疗、免疫治疗这些新的手段推动了学科进步，显著延长了晚期肺癌患者的生存期。但是化疗仍然是不可忽略的，在整个治疗过程中的某个阶段发挥着很重要的作用。有研究提示，即使有驱动性靶点的肺癌，患者在整个治疗过程中如果没有接受化疗，生存期是短于有接受化疗的。化疗还可以和靶向、抗血管生成药物联合，起到协同作用。对于没有驱动基因的晚期肺癌患者，化疗可使30%- 40%的晚期肺癌患者肿瘤得到缓解，60%- 70%的病人肿瘤生长得到控制，并能缓解癌症引起的多种症状。此外，在晚期肺癌患者一线治疗进展后，有效的化疗仍能延长生存时间达50%，规范化疗可有效帮助患者获得更高生活质量并延长生存期。钟文昭教授强调，低剂量螺旋C T筛查早期诊断是肺癌早期发现的重要手段，肺癌如果早期发现治愈率会很高，对比晚期肺癌将会大大降低治疗花费，早发现一年，肺癌的整个治疗格局走向都会发生改变。</span>
</p>
