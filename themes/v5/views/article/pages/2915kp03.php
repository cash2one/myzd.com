		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻腔扩容术（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻腔扩容术针对鼻腔阻塞这一上呼吸道阻塞的源头性因素，借助于个性化鼻腔扩容手术，矫正鼻腔异常结构，对称性开放鼻窦，增加鼻腔有效通气容积，降低鼻阻力，缓解上气道阻塞，增加睡眠时呼吸潮气量，提高机体有氧代谢水平，降低咽腔手术的施行率，对于减控心脑血管病、老年代谢障碍病等重大疾病的发病率具有重要意义。其核心目的是扩大鼻腔有效通气容积，意义在于降低上呼吸道通气前阻力，纠正并改善咽腔塌陷，恢复睡眠时正常通气功能。内容包括鼻中隔三线减张成形术、中鼻甲内移固定术、双侧中鼻道鼻窦对称开放术及下鼻甲外移固定术等系列手术。</span>
</p>
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>（一）鼻中隔三线减张成形术</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">手术遵循尽量保留或维持软骨及骨性支架的原则。通过解剖鼻中隔偏曲的三条曲线（胚胎期生发点生长过程中产生异常张力导致），恢复鼻中隔应有的生物力学支撑性，避免后续性鼻背塌陷。去除上颌骨腭突及腭骨鼻嵴，保留方形软骨（明显偏曲者，可在凹面侧行网格形划痕减张）；对高位重度偏曲的筛骨垂直板可用剪刀或咬骨钳夹持使其骨折保持中线位而不切除（韩德民等，2009）。该术式可以保持鼻中隔的厚度和硬度、避免顺应性鼻梁塌陷、降低呼吸时鼻中隔的扇动、增加总鼻道通气容积、减少鼻中隔穿孔的发生（Wang等，2010）。</span>
</p>
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>（二）中鼻甲内移固定术</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">中鼻甲形态结构异常如泡状中鼻甲、中鼻甲反张、中鼻甲肥大，可直接影响中鼻道通气和鼻窦引流；同时窦口鼻道复合体引流不畅，局部慢性炎症致黏膜肿胀加重中鼻道狭窄阻塞。中鼻甲内移固定可以简化处理上述问题，包括拓宽中鼻道以利通气，同时减轻黏膜炎症状态。内镜下用剥离子将中鼻甲自根部向内按压移位，扩宽中鼻道。操作简单、病人耐受性好、效果显著。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong></strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>（三）双侧中鼻道鼻窦对称开放术</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">慢性炎症长期刺激局部黏膜水肿加重鼻腔阻塞，钩突增生及筛泡过度发育会导致中鼻道狭窄，阻碍相关鼻窦的通气引流。鼻腔流体动力学研究发现鼻腔的有效通气空间主要位于总鼻道及中鼻道，故双侧中鼻道鼻窦对称开放是鼻腔扩容的重要部分。术中切除钩突、对称性开放筛泡、前后组筛窦，扩大中鼻道增加鼻腔有效通气容积，改善鼻腔加湿加温功能。对上颌窦口必要性扩大会利于上颌窦内NO分子到达下呼吸道。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong></strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>（四）下鼻甲外移固定术</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">多种因素可以引起下鼻甲增生肥大，影响总鼻道通气。鼻阈是整个上呼吸道最狭窄的部位，而下鼻甲前端是构成鼻阈的重要结构之一，其微小变化会影响到整个上呼吸道阻力的变化。下鼻甲肥大是造成鼻腔阻力升高的重要因素。下鼻甲外移固定术既在一定程度上缓解鼻阈狭窄，另一方面也符合功能性鼻内镜手术保留正常鼻腔粘膜的要求，避免空鼻综合征的出现。手术方法将圆钝剥离器经总鼻道置入压迫下鼻甲前下端，由前向后骨折下鼻甲根部,&nbsp;将其鼻腔外侧壁移位，敞开总鼻道。该手术具有方法简单、创伤小、疗效明显的优点。</span>
</p>
