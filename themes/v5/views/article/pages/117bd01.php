		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>王学浩：江苏省人民医院肝脏外科主任、教授</strong></span>
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">核心提示：王学浩是江苏省自己培养的第一个卫生界院士，曾冒坐牢风险开展活体肝移植，开创中国大陆活体肝移植的历史。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 名家介绍</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 王学浩，主任医师，教授，博士生导师。现任江苏省人民医院(南京医科大学第一附属医院)肝脏外科主任，江苏省肝移植中心主任，卫生部活体肝脏移植重点实验室主任兼学术委员会主任，卫生部人体器官移植专家组成员，中华医学会微免分会移植免疫学组组长，江苏省医学会副会长等职。擅长肝移植和活体肝移植治疗终末期肝病、肝癌的外科手术和现代综合治疗等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 名家语录</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 只要看准方向，就要承担风险、勇于进取，不管有多大困难，都要坚定不移地一步一步走下去。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2011年12月8日，中国工程院公布当年院士增选结果，江苏省人民医院王学浩教授榜上有名。“他是我们江苏省自己培养的第一个卫生界院士!”医院领导骄傲地表示。可问起荣誉加身的感受，王学浩只是平淡而幽默地说：“我很高兴、激动，其余的你们自己发挥。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 许多人认为，扛起中国活体肝移植头块牌子的王学浩教授应该“霸气”得让人敬而生畏。然而当你和他身处一处，感受到的却只有一位普通医者的朴素、平和及从容;而他跋涉前行、攀登险峰的精神和斗志，却又令人折服。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 冒着坐牢风险，开展活体肝移植</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 出生在江苏盐城建湖县的王学浩，从小家境贫寒，但是学习却非常刻苦。上世纪60年代初，王学浩离开家乡去南京求学，以优异的成绩毕业于南京医科大学，成为一名外科医生。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 回忆起奋斗之路，王学浩永远忘不了那段最困难的岁月：“我们是冒着坐牢的可能，在做活体肝移植研究。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肝移植，被公认为是治疗终末期肝病最有效的方法。从上世纪60年代初开始，国际上便开始兴起相关研究;上世纪70年代末80年代初，国内的一些医院也纷纷投入这一领域。1983年到1985年，王学浩前往美国匹兹堡大学医院世界肝移植中心，师从世界“肝移植之父”斯达泽教授，研修肝移植及肝脏外科，回国后成立肝脏专业小组。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 然而，全球性的供肝来源匮乏，极大限制了肝移植的进一步发展。1989年，巴西报道了世界上第一例活体肝移植手术——“活体换肝”，也就是从病人活着的亲属身上切下一部分肝脏，移植给患者。随后，日本也成功开展了活体肝移植技术。#p#副标题#e#</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 活体换肝，虽然给晚期肝病患者带来了生命希望，但这一手术同时关系到两条人命，不仅技术要求相当高，而且风险很大，国内的很多同行把它视为“禁区”。可王学浩不这么认为，“活体肝移植供肝来源广、质量高、排斥发生率低，势必成为肝移植的发展方向。“外国人能做到的事，我们中国人也一定能做到!”天生喜欢挑战的王学浩坚定了自己的信念。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 上世纪90年代，王学浩率先开展了这方面的研究。当时，国内还没有相关法规，一旦供肝者出现生命危险，医生要担责任。他对学生说，“如果哪天我为此坐牢，你们记得给我送饭就行。”1995年1月5日，一位勇敢的妻子为身患肝癌晚期的丈夫献肝，王学浩酝酿已久的活体肝移植手术付诸实际，开创了中国大陆活体肝移植的历史。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 屡败屡战，创下活体肝移植的三个“最”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2005年，王学浩领导的“中国活体肝脏移植研究所”成立之时，上海复旦大学肝癌研究所所长汤钊猷院士曾表示，王学浩带领的班子有三个“最”——国内活体肝移植做得最早、手术例数最多、手术质量最好。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 可很少有人知道，三个“最”的背后，是“失败—成功—再失败—再成功”的曲折。他的学生张峰回忆，当年一台手术要十几个小时，用血一万多毫升，王学浩因此得了个外号叫“王一万”。记得时间最长的一台换肝手术，做了28个小时，用血两万多毫升。有时，术后病人生死不明，王学浩就带着团队整日整夜守在病人身边，最长的一次守了72个小时。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 那时，患者术后存活时间不理想，加上外界的各种议论，让王学浩承受着巨大的压力，经常彻夜难眠，反复琢磨手术的每个细节，甚至连家人都跟着他担惊受怕。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 但王学浩深知，作为一个学科带头人，如果自己退缩了，所有的努力将前功尽弃。他一遍遍地鼓励大家：“我们这是屡战屡败、屡败屡战，不达目的决不罢休。”最初开展手术，尽管知道不可能，他还是拍着胸脯，毅然决然地保证供肝者会百分之百地安全，好让病人和家属放心。#p#副标题#e#</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 就这样披荆斩棘，从1995年到现在，王学浩完成的肝移植手术已超过600例，从当年的“王一万”，到如今3—4个小时就能做完一台手术，1/4的病人不用输血，5年生存率达到75%，存活时间最长的患者已生存了10多年。他成了中国活体肝移植当之无愧的第一把好手，并创造了另外一连串纪录：成功为一例肝豆状核变性患儿实施移植术，6天内成功为一名6岁患儿实施活体肝移植、肝动脉重建、减体积再次肝移植3次大手术，成功实施中国大陆首例急诊成人间右叶供肝活体肝移植……</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当上院士不是终结，而是一个新的开始</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在同行眼里，王学浩是医学界的骄傲、耀眼的“明星”;可在患者和老百姓眼里，王学浩是一位亲切的医生、慈祥的长者。他从未忘记过老家的乡亲们，老家无论谁生了重病，不管是不是肝病，只要到了省里，都会找王学浩帮着联系。后来，建湖乃至盐城，甚至周边地区的居民都知道省人民医院有个肯帮忙的建湖籍专家，看病都找他。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 王学浩的热情和真诚，让很多患者把他当成亲人和好朋友。中国大陆第一个换肝后结婚生子的病人钱华就是这样。手术之后，他跟王学浩处得就像一家人一样，谈恋爱、结婚都是和王学浩商量的，平时家里一些大事也请他参谋。喜得千金后，他还带着老婆孩子来到医院，当面表示感谢。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在王学浩眼里，活体肝移植是一项浩大的生命工程，也是自己一生的梦想。当上院士的他，更是感到强烈的使命感，“这对我和我的团队不是一个终结，而是新的开始，我现在的感受是，压力大、责任大、使命大。”今天，他又给自己定下了新的目标：带领团队建设一个有国际影响力的肝脏外科中心;在移植免疫治疗上力争有新的突破;进一步规范肝癌治疗，让肝癌患者受益。在王学浩心里，肝移植发展靠的是团队精神和拼搏奉献，不管有多大困难，他都会坚定不移地带领大家一步一步走下去。(本报特约记者 成运芬 陈步伟)</span>
