		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>北京同仁医院：超10名危重眼爆炸伤者昨夜入院（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">本报讯（记者 刘洋）“宝贝儿，慢慢睁开眼睛试试，尽量向上看。”除夕夜，同仁医院眼外伤科主任卢海第10年坚守在眼科一线，为陆续送来的眼爆炸伤患者提供救治。截至昨晚11点30分左右，已有超过10名眼爆炸伤危重症患者被送至同仁医院接受救治。尽管数量上较去年有所下降，但多数需要接受手术，情况依旧不容忽视。据悉，猴年除夕，同仁医院加强眼爆炸伤救治力量，投入眼科18名医师备战门急诊值班工作。手术室安排6名眼外伤、眼整形专业的高年资医师进行急诊手术治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 据介绍，截至昨晚11点半，约有10位危重症患者陆续被送至同仁医院眼科急诊接受治疗。“从数量上看，比去年有了明显的下降，去年同一时间这个数字大概在十七八个左右。”据同仁医院眼外伤科主任卢海介绍，在这10名危重患者中，几乎都是来自外省市，特别是河北省的患者。“今天接诊的北京患者就一两个，都是轻伤。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 尽管数量上较去年有所下降，但危重情况仍然不容乐观。昨晚11点左右，一名来自河北衡水的12岁的男童因左眼被炸伤，在七八名家属的陪同下被送入诊室。据了解，他是在独自点燃二踢脚时不慎被炸伤，炸伤时间已经有七八个小时。尽管下午已经在石家庄接受过缝合治疗，但因伤势较为严重，仍然被送来同仁医院接受进一步的检查。由于爆炸距离近，炸伤可能影响视力，造成视神经挫伤。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在男童接受检查的同时，另一名40岁左右的中年男子也被紧急送入急诊室。据了解，这名男子来自河北省沧州，是在燃放礼花弹时由于烟花没有发出声响，上前查看时发生的炸伤。经过医生检查，这名男子眼内出血严重，且不排除眼内仍留有爆炸异物，被安排接受手术，进行眼球异物的探查。“虽然数量有了明显减少，但手术量与去年基本持平，重伤破裂缝合的压力仍然较大。今年从值守的医务人员数量上，基本与去年持平，不敢轻易减少人手，还是担心后半夜的救治压力会逐步加大，保证及时为患者做上手术。”</span>
</p>
