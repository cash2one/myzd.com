			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>眼眶痛都有什么原因？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>一、眶周痛的神经解剖学</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疼痛是一种主观感觉，发病机制为疼痛敏感组织本身病变或受伤害性刺激，神经发生异常兴奋而致痛。疼痛的敏感器官包括皮肤、血管、神经、肌肉、脑膜、骨膜等。眶周痛是由第Ⅴ、Ⅶ、Ⅸ、Ⅹ颅神经引起，这些神经均与三叉神经脊髓束相联系，C1、C2、C3组成此脊髓束的始端[1]。节前交感神经元脊髓胸索T1～T5的中、外侧核，伴有多数从T2和T3节段发出的纤维，这些纤维的轴突经过腹侧核和脊神经，进入交感神经干并发出分支与白质联系。这些轴突在交感神经干内转向上，最终到达颈神经节，神经节后纤维进入颈内动脉，形成血管周围丛。部分纤维沿颈外动脉支配面部汗腺，其余纤维支配瞳孔开大肌、提上睑肌的平滑肌和前额的汗腺。从颈神经节上方发出的节后纤维沿颈内动脉进入颈动脉管与颈动脉伴行，并分成两支，一支沿着颈动脉后外侧与颈内动脉吻合，另一支沿着颈动脉到达海绵窦，形成海绵窦丛。后一支的纤维与部分从颈内经脉丛发出的纤维进入第Ⅲ、Ⅳ、Ⅵ和第Ⅴ颅神经第一支。这些纤维的终末分支沿着眼动脉到达眼眶，沿着颈动脉前内侧到达这些动脉的终末支。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>二、眶周痛的病因诊断</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">引起眶周痛的眼眶病主要有炎症、肿瘤、血管畸形，通过眼眶超声检查和高分辨率CT扫描不难做出明确诊断。眼眶MRI扫描对定性诊断有更大帮助，而且有助于准确的定位诊断。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眶蜂窝织炎是眶周痛的常见原因之一，为急性化脓性感染，主要致病菌为金黄色葡萄球菌、溶血性乙型链球菌、酿脓链球菌、肺炎链球菌、流感嗜血杆菌等[2]。致痛机制可能为炎细胞释放血管活性物质如5-羟色胺、组织胺等刺激周围组织产生痛觉。常表现为剧烈的疼痛，眼球运动或压迫眼球可加重痛觉。伴有眼肌麻痹、眼球突出、结膜肿胀、视力减退。儿童多发，并常合并有副鼻窦炎。超声探查可见眶脂肪垫扩大，眼外肌轻度肿大和眼球外透声间隙。CT扫描可显示鼻窦粘膜肥厚和积液；眶内软组织肿胀和轻度密度增高。MRI显示眶内和鼻窦内炎症，T1加权像为中信号，T2加权像为高信号。合并脓肿，B超表现为不规则暗区或低回声区，CT表现为局限不规则高密度块影。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">炎性假瘤、甲状腺相关眼病二者同属免疫性疾病，均可表现为眼球突出、肿胀、疼痛。炎症常常侵犯眼外肌，可引起眼球运动障碍。炎性假瘤的受累眼外肌主要是肌腱和肌腹的肥大，形状不规则，肌肉附着点呈球形肿胀；甲状腺相关眼病引起的眼外肌肥大则以肌腹肥大为主，肌腱止点一般正常[3]。甲状腺相关眼病尚有特异性的眼睑体征，即上睑回缩和迟落；并有T3、T4的异常和甲亢病史。而炎性假瘤最多见为眶脂肪内的软组织肿块；如侵犯泪腺，为泪腺炎型；以眼外肌肥大为主的则为肥大性肌炎。炎性假瘤侵犯眶上裂或海绵窦名痛性眼肌麻痹，或称Tolosa-Hunt综合征。炎性假瘤、甲状腺相关眼病主要为眶区胀痛为主；而Tolosa-Hunt综合征则以眶后或眶上缘特异性“钻痛”为主[4]。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼眶良性肿瘤占眶周痛病因的很大比例，致痛原因可能与瘤体生长速度快而压迫神经和血管有关。血管性肿瘤尚可能与瘤体内血流速度改变冲击血管壁，刺激痛觉神经末梢有关。眼眶良性肿瘤多表现为无痛性的眼球突出，部分尚可触及肿块。眶周痛多为伴随症状，主要为胀痛、钝痛，部分与体位有关。根据CT和MRI扫描可见眶内占位性病变和眼球突出可以进行诊断和定位，但定性诊断需病理组织学检查。皮样囊肿是是眶周痛的常见病因，可能原因为囊壁发生自发性或创伤性破裂，诱发强烈的炎症反应和剧烈的痛觉反应[5]。皮样囊肿眶外上缘骨缝处多发，可刺激骨膜诱发疼痛。CT扫描可准确定位及定性诊断，特征性表现：（1）圆形、椭圆形或半圆形占位病变，周围绕以高密度影；（2）肿物内有负CT值区，高低密度间杂、水平面或全为低密度区；（3）骨膜外囊肿边缘骨增生，压迫眶壁形成凹陷或眶壁缺失，病变呈哑铃状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">恶性肿瘤呈浸润性生长，瘤体增长速度快，无包膜，极易破坏周围的血管、神经、肌肉及骨骼等痛觉敏感器官，因此，恶性肿瘤一般均有疼痛。引起眶周痛的常见眼眶恶性肿瘤为恶性淋巴瘤、泪腺恶性肿瘤和转移性肿瘤。恶性淋巴瘤以50～60岁的患者多发，主要表现为眼球突出、运动障碍和视力下降[5]。以泪腺区多见。瘤体呈橙红色鱼肉样外观，CT扫描可见铸造样特异性改变。泪腺恶性肿瘤，尤其是腺样囊性癌，有嗜骨膜和神经浸润的倾向，可导致眶深部的钻痛[2]。CT和MRI扫描可清晰显示肿瘤的大小、范围、邻近软组织或骨质破坏。约20％的眼眶转移性肿瘤的患者有眶区疼痛[2]。女性常见的眼眶转移癌为乳腺癌，男性常见的眼眶转移癌为前列腺癌。疼痛可能与骨膜浸润有关。早期既有疼痛、眼球突出、眼球运动障碍和骨破坏。乳腺硬癌侵犯眼眶则常表现为眼球内陷。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血管畸形主要致痛原因为血管壁受到伤害性刺激而诱发眶区疼痛。静脉曲张的患者多伴有眼球内陷和体位性眼球突出。B超探查，眼球突出前为正常声像图，突出时可见不规则的透声腔隙，压迫眼球可消失。CT扫描平卧正常或小片不规则、条带状高密度影，憋气或压迫颈内静脉，眼球突出、眶内高密度块影。颈动脉海绵窦瘘是眶周痛的常见原因[2]。由于眼上静脉回流受阻，颈动脉海绵窦瘘最特异、最常见的体征是眼球表面血管怒张和红眼，表现为血管高度迂曲扩张，呈螺丝状，色紫红或鲜红，以角膜为中心呈放射状排列。尚伴有搏动性眼球突出、眼球运动障碍。影像学表现主要为眼球突出、眼上静脉扩张、海绵窦扩张、多条眼外肌弥漫梭形肥厚、眶内软组织肿胀。海绵窦栓塞亦可引起眶周疼痛[5]。MRI对各个阶段的血栓均敏感，不失为极佳的诊断手段。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">引起眶周痛的眼眶病种类很多，单凭眶周疼痛不足以做出任何诊断，但结合病史、症状、体征及影像学检查手段可帮助我们进行正确的诊断，并选择合适的治疗方案。</span><br />
