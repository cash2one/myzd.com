			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肿瘤标志物高就是癌症吗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最近在临床工作中经常有一些患者会问我这样的问题“肿瘤标志物高是不是得了癌症啊”，其实，这个问题回答起来很简单，但是总是听得患者一头雾水，那么对于不是学习医学专业的人群，究竟应该怎样理解肿瘤标志这个专业的医学概念呢？又怎样正确认识肿瘤标志物增高意味着什么呢？今天我们做一个小小的科普来揭开肿瘤标志物这一神秘面纱！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肿瘤标志物从字面的理解应该是肿瘤疾病的一个标志，但事实上并不是所有的肿瘤疾病都要表现出肿瘤标志物增高。我先拿原发性肝癌的肿瘤标志物甲胎蛋白（AFP）举一个例子吧，甲胎蛋白在胚胎、婴儿、孕妇或成人中都可以检测的到或者检测到高于正常值，对于某些先天疾病、肝炎或肿瘤疾病中可以高浓度表达，很显然如果在婴儿、孕妇或肝炎等这些没有疾病或者良性疾病的人群在检查到了甲胎蛋白增高，我们是不能诊断她们得了癌症的。说到这里我们还得了解一个概念，就是特异性标志物这一概念，严格的特异性肿瘤标志物是指肿瘤患者中一定有或者检测的到，而没有肿瘤的人群是一定检查不到的，这才是真正的特异性肿瘤标志物，就像男性和女性的特异不同一定是性器官的区别，即便是变性人我们也不能用完全的女性或者男性去定义，而选择了人妖这个概念一样。那么肿瘤标志物高又应该怎样理解呢？我们看看下面几种情况吧。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单一肿瘤标志物增高：严格的讲，单一的肿瘤标志物增高是不能诊断任何一种肿瘤的，因为肿瘤标志物分为很多种，如经常检测的糖类蛋白（CA199、CA242、CA125等）、蛋白质类（AFP、CEA、TPA等）、酶类（ALP、LDH等）、激素类（ACTH、HCG等）、多胺类（5-HT等）很多种类，并且这些标志物存在于多种器官和组织中，所以就不能靠单一的标志物增高，就说哪个器官得了肿瘤。这个时候虽然不能说明什么，但是警钟长鸣，我们应该进一步去了解其它检查是否有变化，所以通常我们需要依靠超声、CT或核磁等检查来进一步明确是否有肿瘤，甚至需要病理检查或核酸扩增检查来证明。如果其它的检查并没有证明肿瘤的存在，我们是不是可以庆祝了呢，也不是，没有肿瘤固然是好，但是再高明的影像检查也只能发现12.5px以上的肿瘤，可是我们知道，肿瘤细胞是我们肉眼看不到的，真的等到长大了看到了，可能就已经晚了，所以我们提倡单一肿瘤标志物高的、家族有肿瘤相关疾病人群需要跟踪观察检测。对于没有肿瘤家族史的人群一般我们要跟踪检测最初的第4周、第8周和3个月、6个月，部分需要1年的肿瘤标志物跟踪来观察这个标志物是否持续升高，有肿瘤家族史的人群我们要观察至少1-3年的时间。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多个肿瘤标志物增高：如果是多个肿瘤标志物增高，这个可要十分小心了，必须到水平较好的医院做一个系统检测，来明确是否真的有肿瘤疾病存在，而且要对家族中高发的肿瘤疾病，要做全面的检测。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血清肿瘤标志物增高：多数情况下我们检测的肿瘤标志物均来自抽血检测的结果，如果肿瘤标志物增高，我们还是要看是单个还是多个肿瘤标志物增高，来决定进一步怎么办！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病理肿瘤标志物增高：如果是肿瘤标志物结果来自人体的组织，也就是我们说的病理免疫组织化学检查，这个结果高，基本就可以确定肿瘤诊断了，毕竟目前的医学水平，病理检查为诊断疾病的金标准。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于已经明确肿瘤诊断的患者也要定期检查肿瘤标志物，因为肿瘤标志物由治疗前的较高水平到治疗后的减低，是临床上肿瘤治疗有效的标准，如果不降或反高就应该考虑肿瘤治疗无效、复发或者转移了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">哪些人群应该检测肿瘤标志物：在我国每分钟就有6人确诊为癌症。因癌死亡的比率为12.94%。约每5个人中就可能有一个人会罹患癌症，约每8个人中就可能有一个人死于癌症！死亡率男性高于女性，50岁以上人群发病占全部发病的80%以上，60岁以上癌症发病率超过1%，80岁达到高峰；所以提醒以下几类人群：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、40岁以上的人群就应该开始肿瘤标志物的检查了；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、有肿瘤家族史的或某种肿瘤高发地区的人群依据近亲属肿瘤发病年龄，提前5-10年开始检查肿瘤标志物，3-5年开始影像补充检查；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、长期从事高污染、易与致癌物接触的人群：如印刷、厂矿、大量吸烟、嗜酒、喜焦烤食物等人群。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、目前患有慢性炎症疾病的人群：如肝炎、乳腺炎、宫颈炎等人群</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在这里我们也提示，对于那些高压力生活、长期独身，家族低寿命的人群也应重视肿瘤标志物。最后希望大家正确认识肿瘤标志物，有病早发现、早治疗。</span><br />
