<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> （转：科普）噪音让现代人越来越"聋" 专家推荐护耳八法&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 耳朵给我们带来了一个有声的世界，但生活中各种各样的噪音反过来却在慢慢吞噬它们的健康。研究表明，近四成的听力障碍与噪声伤害有关。3月3日是第13个"全国爱耳日"，今年的主题是减少噪音、保护听力。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 噪音是指能引起人们烦躁和危害听力的声音，这种伤害取决于声音的大小，以及人们暴露在噪音环境中的时间长短。短暂的强噪音和长期持续的噪声都会影响听力，甚至耳聋。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 那么，什么样的声音才算强噪音呢？举个例子来说，在公交车或地铁上，我们常能看到戴着耳机听音乐的人，如果旁人可以听到从他耳机里传出的声音，说明音量可能已经达到了115分贝。如此大强度的声音足以使人内耳的毛细胞损坏甚至死亡，会严重损害听觉器官。节假日燃放鞭炮声、飞机起降声、卡拉OK声等高强噪音，可能接触一次便会引起听力损害。研究发现，声音一旦超过80分贝，耳聋的可能性就会增加50％。另外，电脑主机的轰鸣声、电话及手机铃声等，人们可能感觉不到它声音大，但这对听力的伤害是长期的。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 由噪音引起的耳聋称为噪声性耳聋，是长期受噪声刺激而发生的一种缓慢的、进行性听觉损伤，损伤部位在内耳。人的内耳有1.8万个直径约0.01毫米的纤毛细胞，这些脆弱的细胞特别容易受噪声影响，一旦受损，不能再生。噪声性耳聋也是目前世界医学界面临的难题。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在我国0-17岁的孩子中，造成听力残疾的主要原因有：噪音、遗传、中耳炎、药物中毒、孕期感染等，但噪音是唯一一个能够通过自我调整就可改变的致病因素。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家推荐护耳八法&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 避免噪音伤害，专家推荐了8种方法，不妨一试。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1.听MP3掌握"60-60"原则。这是国际公认的保护听力的方法，用耳机听音乐时，音量不要超过最大音量的60%，连续听的时间不要超过60分钟。此外，耳机最好选头戴式的，它比耳塞式耳机对听力的损伤要小，尽量不要在嘈杂环境下用耳机听音乐。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2.噪音环境下用防护耳塞。有的人不得不在噪音环境下工作和生活，比如机场、建筑工地，可以戴一个防护耳塞，能有效阻隔一部分噪音。如果临时找不到耳塞，可用棉花球、纸球塞进耳朵，也有一定的保护作用。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 3.合理饮食、锻炼身体。维生素B1、B2、B6和维生素C能保护听觉细胞，可适当补充维生素补充剂，也可多吃粗粮、瘦肉、蛋类、新鲜蔬菜、水果等食物。现代人生活紧张、压力大，应适当体育锻炼，保持心情愉快。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 4.开快车别开窗。美国广播公司报道称，汽车车窗开得太大，开快车时的风噪声和开慢车时的交通噪音是损害听力的主要原因。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 5.别频繁掏耳朵。掏耳朵时最好用棉棒，轻轻在外耳道口转动，然后耳朵朝下，让耳屎自行排出。要避免使用指甲、卡子等尖锐物挖耳；不要频繁挖耳，一般一周一次。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 6.住在马路旁，别装推拉窗。国家室内环境质检中心主任宋广生指出，其隔音效果远远次于密封较好的平开窗，可以在窗边多摆绿植。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 7.电视机、耳机的音量不宜过大。以柔和、不刺耳为宜；骑车、走路时等噪声环境中不要听耳机。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 8.对于经常用耳机的人，如果出现头晕、耳痛等症状，应暂时不用耳机。如果出现耳鸣以及听力下降，应及时就医。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 我们每天用耳朵听着各种音乐，接收着各种信息，在它的"身上"打上好几个洞，以便让自己更加时尚。但却没有仔细关注过它的健康。很多人觉得，只要是可以听到外界的声音，耳朵的健康状况就一定良好！其实不然，耳朵的健康指数下降是在潜移默化间，需要你从细节中认真呵护。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 爱护耳朵，让耳朵帮我们感受多姿多彩的生活。&nbsp;</span><br />
