		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中国医生国际舞台展示内镜手术轰动全场</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2月2日上午8：45，德国Düsseldorf（杜塞尔多夫），第14届国际消化内镜大会会场内，1700余位世界代表济济一堂。与会代表期待已久的内镜手术操作演示准时开始，由日本、美国、德国、中国、荷兰等国医生于5个房间内先后进行的7台手术，将由卫星实况转播到大会现场。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;他的内镜手术“不可思议”，一连赢得四次掌声</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;中山医院内镜中心副主任周平红主任医师的最新高难度POEM（经口内镜肌切开术）第4个登台演示，大会主席Schumacher教授亲自配合手术和解说。只见周平红面带微笑，在对大会主席的邀请表示感谢后即开始操作演示，5分钟内顺利完成长达12厘米的食管黏膜下隧道的建立，操作娴熟、流畅，创面清晰（未出1滴血），顿时会场内爆发了热烈的掌声；当进行到手术关键步骤——食管贲门周围肌肉切开时，意外出现了，一动脉出血，由于与会代表大多未见过类似手术，都倒吸一口气。会场内一片寂静，鸦雀无声，而周医生却格外镇定，并在短时间内完成止血，此时会场内第2次掌声雷动；手术中周医生边操作边介绍手术方法和技巧，大会主持人要求转播人员紧盯周平红的双手转播，不能放过任何一点细微动作，当完成肌层全部切开，周平红介绍说中山医院去年内镜诊疗量超过7.1万例时，全场再次掌声如潮；当手术进行到第20分钟，周医生用5个金属夹顺利缝合食管伤口，而此时，美国、德国和日本等国专家已齐聚检查室，近距离地目睹这一手术过程。面对呈一直线、缝合间距一致的食管伤口，大会主持人宣布周平红25分钟成功完成POEM，整个手术过程“不可思议”时，会场掌声第4次响起。5分钟后病人苏醒，伸出右手作出“V”手势，手术室内一片欢呼，多国专家伸手表示祝贺。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;他的手术被命名为“POEM ZHOU”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;手术结束后，大会主席Schumacher教授在休息室内宣布周平红的POEM是上午最精彩、最成功的手术，是上午手术演示的冠军，其应用海博刀进行食管环状肌和纵形肌的全层切开手术方式为世界首创，手术出血少，操作时间短，复发率低，建议命名为“POEM ZHOU”。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;多国专家在休息室内将周平红医生团团围住，一边握住周平红的双手不断地用“Good job!”和“Congratulation!”表示祝贺，啧啧称奇周医生这双这么神奇、短时间内能完成如此高难度手术的双手，一边迫不及待地询问手术的各项要点。由于多个专家手术操作时未能目睹手术过程，强烈要求大会主席赶紧准备另1例病人安排第二天再次演示，并索要手术录像。还有医生仔细检查周医生的手术衣后背，发现竟毫无汗滴时连声称赞“Unbelievable！”“Amazing！”大多外国专家都表示有机会一定要到上海中山医院学习，参加中山医院内镜中心ESD大会和学习班。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 中国消化内镜医生走上国际舞台</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;在当晚大会组织的特别晚宴上，内镜中心姚礼庆主任和周平红主任医师应邀出席，这是受邀参加的唯一两位中国医生。晚宴中大会主席Neuhaus教授宣布上午POEM病人恢复很好，首先感谢来自中国的周平红医生，为德国病人带来福音，称赞周平红教授是消化内镜界的一颗新星，并邀请他参加2013年举办的第15届消化内镜大会，国际专家们共同举杯祝贺。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;面对手术过程中会场内4次雷鸣般的掌声，尤其是第4次全场掌声如潮水般阵阵响起，持续时间最长，中国代表们无比激动和自豪，姚礼庆主任也热泪盈眶。期待已久的中国消化内镜医生终于走上了国际舞台，成功展示中国内镜治疗的水平，一展中国内镜医生风采的一天终于来到。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;继2011年周平红医生出席意大利、韩国、香港、台湾等地消化内镜大会、登上国际消化内镜舞台后，本次大会2天时间内周平红成功完成2台内镜最新微创手术，代表中国医生出场5次，为国际同行所了解和瞩目，为中山医院和中国医生赢得荣誉和尊重。</span><br />
