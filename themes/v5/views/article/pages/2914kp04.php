		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>不塌鼻的鼻中隔成形术与鼻中隔矫正术的区别？（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻中隔矫正手术是耳鼻咽喉头颈外科的最常见的鼻科手术之一。其手术方式历经一百余年的不断发展和演变，大致分为三个阶段：鼻中隔次全切除术、黏膜下切除术、鼻中隔矫正术和鼻中隔成形术。黏膜下切除术能够满足各种类型的鼻中隔偏曲的矫正，时至今日仍然被广泛使用。然而现在的鼻中隔偏曲矫正手术真的至善至美了吗?&nbsp;鼻中隔成形手术与鼻中隔矫正手术一样吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">随着20世纪70年代鼻内镜的问世与发展，促进了鼻内镜下鼻中隔矫正手术的开展。在内镜直视下处理鼻中隔偏曲，术野清晰，扩大了传统手术适应证。但随之而来的问题是普遍存在过多切除鼻中隔支架的现象。这些术式虽能矫正鼻中隔偏曲，缓解症状，但由于大面积鼻中隔软骨和骨质被切除，造成鼻中隔主要支架缺失，使中隔黏膜过于松弛而摆动，并可导致鼻的缓慢变形，如鞍鼻、鼻背过宽、尖上区塌陷、鼻中隔穿孔等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻中隔偏曲产生的生物力学分析</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">如何通过解除导致鼻中隔偏曲的应力关系，纠正各种形式的鼻中隔偏曲的同时，保留鼻中隔软骨及正常骨结构支架，避免并发症，仍是一个十分值得探索的课题。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">从骨的生长发育角度来看，颅骨完成发育较早，而鼻中隔软骨发育完成较晚，即在额骨与上颌骨、腭骨发育完成时鼻中隔软骨还在生长。因此鼻中隔的上下均已固定，会因为无法延长而形成上下的应力关系，其主要集中于较薄弱的鼻中隔软骨与周边骨之间的连接部位，可能是鼻中隔偏曲形成的最主要因素。另外，鼻中隔有多个骨或软骨组成，各部分生长是不平衡的。如图1，开始时鼻中隔是软骨性的，而后开始部分骨化。下方的犁骨、上颌骨鼻嵴、腭突最先骨化；而后鼻中隔后部逐渐向前骨化，骨化的顺序是从头侧到尾侧。当后方和下方的筛骨垂直板和犁骨、上颌骨腭突骨化固定后，软骨部分还在延伸生长。因此这种发育不均衡所造成的张力，主要作用于鼻中隔软骨的前、下和后方的三条线上，即与已骨化部分的交联处，形成受力的三个核心区域，形成突起，严重时形成嵴，矩状突，造成严重的偏曲。如图2所示，第一条张力曲线：方形软骨尾侧端与鼻小柱大翼软骨内侧脚之间，形成前位偏曲；第二条张力曲线：鼻中隔软骨与筛骨垂直板结合处，形成高位偏曲；第三条张力曲线：方形软骨与犁骨、上颌骨鼻嵴和腭骨鼻嵴交界处，形成后位偏曲以及形态各异的鼻嵴和距状突。&nbsp;&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三线减张法鼻中隔成形手术不同于鼻中隔矫正术</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp; &nbsp;鼻中隔成形术是按照鼻中隔偏曲产生的生物力学规律，设计一种新的改良鼻中隔矫正手术术式，即三线减张鼻中隔成形手术。其特征如下：保留大部分鼻中隔软骨和正常骨性支架；只切除三条张力线区域部分少许软骨及骨质，解除导致鼻中隔偏曲的应力。对重度高位偏曲的筛骨垂直板可用咬骨钳夹持使其骨折而不切除，然后把方形软骨复位于正中，对合双侧黏软骨膜。该术式保留了偏曲的软骨和骨质，这也是与传统术式的主要区别之处。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三线减张鼻中隔成形手术的特征</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">该术式按照鼻中隔偏曲产生的生物力学规律，试图重新树立鼻中隔矫正的概念。通过解除产生三个张力的核心区域的方法，将发生变异的应力关系修正恢复正常，由新的应力关系来引起鼻中隔支架的重新塑型，以期在矫正鼻中隔偏曲的同时，减少手术并发疾病。它的意义在于：①保持中隔的厚度和硬度，否则黏膜愈合到一起会导致黏膜萎缩和变形，如中隔缺损太大，呼吸时中隔产生扇动及哨音；②有助于预防鼻锥和鼻尖的下塌，并可防止黏软骨膜层结缔组织的收缩；③减少鼻中隔穿孔的发生。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三线减张鼻中隔矫正术后的恢复与以往手术不同。传统完全切除中隔骨质手术后鼻腔恢复较快，但是如果保留软骨结构或术中施行骨折之后会出现肿胀时间延长、蓄骨愈合的时间，所以要保持耐心。术后要保证随访质量，避免鼻腔粘连和狭窄，并进行合理用药。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">手术年龄的选择</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">多年来由于手术观念的局限性，我们常常把中隔矫正手术锁定为一个年龄限制，强调中隔手术应在18岁以后。这种旧的手术观念影响了几代人，认为未发育成熟的患者接受中隔矫正手术，鼻腔发生塌陷的可能性较大。现在看来有不少的儿童青少年一侧鼻腔已经完全偏曲阻塞，如果等到18岁，他会长期张口呼吸引起下颌骨的顺应性重塑改变，进而对生长发育带来负面影响，增加成年后惟患阻塞性睡眠呼吸暂停低通气综合症的风险。对这些儿童已经不能用年龄来限制，早期恢复鼻腔的正常功能至关重要。El-Hakim等以26位年龄4.5~15.5岁的儿童（平均年龄9.5岁）为研究对象，行鼻中隔矫正手术。在平均时间为3.1年的随访后，认为轻度的鼻背长度和鼻尖高度下降并无临床意义。因此只要保留正常的鼻部骨性支架，对于儿童鼻中隔成形术并非禁忌。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">结论</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻内镜下用三线减张法进行鼻中隔成形术，简单易行，安全可靠，鼻中隔畸形矫正充分，有利于术后鼻腔代偿性改变向着生理状态转化，最终恢复鼻腔—鼻窦生理功能。尽管鼻中隔偏曲的特点和形式多样，但通过切除三线张力区域，消除中隔软骨和骨质之间的张力，可同时解决鼻中隔前位、后位、高位偏曲和嵴、距状突等局部畸形，符合鼻中隔偏曲产生的生物力学规律，将手术上升到微创水平。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">细节决定成与败，我们应当思考结构重塑过程中细微之处，重视功能恢复干预中的人性化原则。正确认识解剖结构、生理功能和临床症状三者之间的辩证关系，最大限度地保留正常组织结构，恢复其基本生理功能，提高患者的生活质量，实现微创外科技术的宗旨。</span>
