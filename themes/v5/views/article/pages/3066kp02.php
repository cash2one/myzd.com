			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>出现肝转移≠生命倒计时</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">结直肠癌（大肠癌）在我国是发病率很高的恶性肿瘤，它与胃癌、食管癌和肝癌并列消化系统恶性肿瘤的“四大杀手”。近年来结直肠癌根治性手术在外科医生的努力下已经做的越来越规范和精益求精，但术后能存活5年以上的患者仍只有60%左右，出现肝脏转移是影响结直肠癌患者长期存活的最主要问题。的确，有一半的患者手术后或早或晚都会出现肝转移，这一比例是很高的。因为胃肠道的血液都是流向肝脏的，所以如果肿瘤在生长过程中穿透了血管壁，就会有癌细胞随着血流跑到肝脏种植下来，很容易形成肝内的转移癌。但庆幸的是大肠癌肝转移灶的生长速度相对比较缓慢，而且在肝内一般只形成孤立的转移病灶，很少再在肝内继续扩散，这就为手术切除创造了条件。自1940年第一例直肠癌肝转移切除手术至今已有70年的历史了，实际疗效证明手术切除仍是目前治疗结直肠癌肝转移的最有效方法，而且也是唯一能够真正治愈肿瘤的办法。能够手术切除的肝转移患者中有40~50%可以获得长期存活。换句话说，大肠癌患者出现了肝转移，并不意味着世界末日，也不是开始了生命的倒计时。相反通过积极的科学的有针对性的治疗，我们和大家一起努力是完全可能彻底战胜大肠癌这个病魔的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 70年来由于外科技术的长足进步和我们肝胆外科专业医师经验的积累，肝转移癌的大小、数目、在肝内的生长部位都不再是影响患者是否能够手术的因素，很多以前的手术禁区已被一一突破。可以说，只要手术后能够保留足够的肝脏（一般要保留30%以上），大多数的肝转移癌都能够被切除。如果肝脏以外也有转移，能切除者也可以一并切除，比如肺转移灶、腹腔种植转移灶、肝门转移的淋巴结等等都是可以清除的。有些患者在发现罹患结直肠癌的同时也发现存在肝转移，这要根据情况分别对待。如果患者的肝功能和身体状况允许的话，按目前的技术水平完全可以一次手术同时切除。如果由于肿瘤堵塞了肠道而需要做急诊手术时，由于缺乏完善的术前检查资料和存在较高的手术感染机会，我们不推荐同时切除转移癌，应该优先确保原发结直肠癌的顺利切除和患者的迅速康复。肝转移癌切除之后2年内，可能会有60%的病例复发，约1/3仍然还是出现在肝脏。不要灰心更不要害怕，只要条件许可，还是能够再次手术治疗的，而且再次切除后总的生存期与初次肝切除相仿。</span>
</p>
