<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 当然手术也不是万能的，面对强悍的敌人我们外科医生也需要援军，那就是多个学科参与的综合治疗。比如术前和术后的放疗和化疗，针对不同的患者选择不同的方案。对那些无法开刀手术的患者还可以使用创伤较小的射频或微波消融方法，用一根长长的针在体外（不用开腹手术）就可以消灭某些部位的体积较小的肝转移肿瘤。所以，结直肠癌患者出现了肝转移的情况时，千万不要绝望和悲观，要知道这是一种有机会治愈的疾病。大家要做的不是有病乱投医，到处寻找偏方秘籍，而是应该及时到正规专业的肝胆外科医师处就诊，积极争取治疗机会。我们会帮你一起度过难关，你不是一个人在战斗！</span>
