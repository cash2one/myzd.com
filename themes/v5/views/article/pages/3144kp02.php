		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>单侧小耳畸形：治疗策略与时机（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">小耳畸形多为单侧发病。我国的统计数据表明，在小耳畸形患者中，75%为单侧患者，其中右侧多发，男女比例略高。小耳畸形患者常伴发耳道闭锁，形成中重度的传导性耳聋。因为一侧耳正常，此类患者的言语发育通常不会有障碍，所以既往对单侧小耳畸形患者的治疗，集中于耳廓的整形，而不重视对患侧耳的听力重建。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 随着对听觉研究的深入，对单侧小耳畸形患者进行听力重建的必要性，逐步得到耳科医生及听力师的认可。正常人拥有双侧听觉，因此具备了声源定位以及嘈杂环境中识别言语的能力。与此形成对照的是单侧小耳畸形患者，他们只具备一侧听觉，因此难以确定声音来源，并且在嘈杂环境中听觉表现较差。在临床上，很多单侧小耳畸形伴发耳道闭锁的患儿，能够正常地形成语言；但在复杂的听觉环境（上课、群体交谈、噪音背景）中，患者的听觉和沟通能力与正常人相比存在差距。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 因此，对于单侧小耳畸形患者，如果伴发了耳道闭锁（出现中重度传导性耳聋），治疗的策略应该是听力重建与耳廓整形并重。这一治疗策略在西方国家已普遍得到认可，但目前在国内，很多单侧小耳畸形患者（或患儿父母）仍不太了解听力重建。这一认识的滞后，使很多单侧患儿错过了听力重建的最佳时机；而且传统的听力重建手术，会损伤耳廓重建所需要的皮瓣区，影响日后的耳廓重建，因此医生通常也只能在耳廓重建后，再进行听力重建。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;听力重建应该在何时进行？单侧小耳畸形伴发耳道闭锁的患者，外界声音不能有效地传入内耳引发听觉，因此患者通常出现一侧传导性耳聋（听力损失大多在60分贝，为中重度耳聋）。此类患者的听觉发育，主要依赖正常侧的听力，即逐步形成单侧听觉模式。此类患者通过治疗，重建患侧听力后，听觉中枢需要一定的时期，对双侧的听觉信号进行整合。通常听力重建越早，患者听觉整合所需的适应期越短，两侧听觉的差异越小；反之则越长，差异越大。因此，对于单侧小耳畸形患儿，应及早进行听力重建，实现双侧听力的正常、平衡发育。目前随着麻醉技术和耳科手术技术的进步，一些单侧小耳畸形伴发外耳道闭锁的患儿，在2岁左右即植入振动声桥，在耳廓重建之前就完成了听力重建，获得了正常的双侧听觉发育。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 我院自2010年5月以来，已对数十名患者进行了振动声桥的植入，其中大部分是小耳畸形伴发外耳道闭锁的患者。目前对于此类患者的治疗，我们提倡听力重建优先的治疗策略：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; -&nbsp;3岁左右，单侧或双侧植入振动声桥，完成听觉重建。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; -&nbsp;10岁左右，取肋软骨，重建单侧或者双侧耳廓，完成整形。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">单需要注意的是，并非所有小耳畸形外耳道闭锁的患者，都适合植入振动声桥。声桥植入要求患者的中耳内有一定的结构；医生在术前也会根据患者的颞骨CT进行评估，以决定是否能够手术。</span>
</p>
