		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>技术达不到，这是医生最懊恼的事情（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">□星期日周刊记者　顾筝　实习生　杨立婷</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 医者，记者</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 从曾经的医者到记者，又以记者的视角去关注医者，我时而沉醉，时而徘徊，时而焦虑，时而释然……常常，我饱尝内心之起落。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 医者的心，是坚韧、是执着、是淡泊，是一颗满怀希望之心。然而，谁愿意说？谁愿意听？说什么？听什么？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 记者的笔，是耳朵，是眼睛，是嘴巴，是一支洋溢激情的笔。然而，谁可以写？谁可以看？写什么？看什么？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 《医者》专访医疗界，呼唤专业内涵下的医学人文情怀。每个星期天，我们在一起倾听。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 医学技术和医生自身的技术，都在随着时间，不断发展。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 第二军医大学附属长征医院骨科医院副院长肖建如回顾自己在脊柱肿瘤外科工作的近20年时间，他说：“有些手术，我现在倒过来看，会发现当时存在的缺陷，如果让我今天做，我可能会做得更好。在技术不断进步的同时，回过头来也会反思一些问题。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 当然，有的时候看到自己十多年前做的手术，还是觉得做得非常棒。就算现在把当时的影像资料拿出来，都不会觉得过时。”</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 病人的顾虑很正常</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 8月上旬的那个周一下午，肖建如在看专家门诊的时候遇到两个病人。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 一位病人肺癌转移压迫到了神经，在手术和保守治疗之中，病人选择了保守的放疗。而肖建如认为这个病人有手术适应症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 患者的家属很困惑，他们连续问了肖建如三个问题：“手术会不会导致肿瘤扩散？手术如果做得不好，会不会导致病人瘫痪？动手术，病人吃不吃得消？”面对这些问题，肖建如解释道：“患者现在已经发生椎体溶骨性破坏，有神经压迫了，外科手术方法是首选，它能够让他尽快地下床活动。”因为病人已经卧床一个月了，迎面而来的是各种问题：肌肉萎缩，心肺功能减退，大便不通畅，胃口不好等，全身都会面临崩溃。对于病人的这种状态，肖建如认为，通过手术，可以减轻或缓解病人疼痛，解除脊髓和神经根压迫，病人可尽早下地行走。在这样的情况下，他会有信心接受下一步的放化疗等综合治疗，对于提高病人生活质量乃至延长生存期会有帮助。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 另外一位患有椎体血管瘤的病人，肖建如建议他做微创手术。病人也有疑惑：“如果微创失败了，再塌陷压迫怎么办？”肖建如回答道：“如果发生了塌陷压迫，到时候再做开放手术，也是来得及的。”对待同一个部位的手术，类型不同，病情时段不一样，肖建如选择了不同的手术方法。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日周刊记者（以下简称“星期日”）：手术是治疗疾病的一个手段，有的需要进行手术，有的不需要，有的需要即刻动手术，有的需要等待观察……对于不同的病情，你是怎么进行评估的？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：一个好的医生终其一生都会琢磨手术的适应症，第一病人需不需要手术；第二根据病人全身和局部的情况，他适合做什么样的手术，适合大做、小做，或是微创；第三医生自身的技术水平，也要综合评估。最终医生的手术决策、治疗结果要让病人获益，这是医生真正要做的事情，也是病人最需要的。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 像前段时间，有一个脊柱转移癌病人转院到我们这里，她已经瘫痪了一个星期。在做术前检查时，发现她已经有肺部感染并且全身情况很差，这个时候我经过综合评估后的结果是，时机太晚，不能做手术了。其实，应该是在没有发生完全瘫痪的时候就进行手术，这个时候效果最好。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：但是在没有瘫痪的时候，病人大多不会想开刀。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：这就是一个理念问题。以前人们认为，只要是脊柱转移癌，中晚期就不用开刀了，开刀了也没用。现在概念有了改变，在病人发生病理性骨折后，神经压迫就有可能造成瘫痪，而且疼痛非常剧烈，此时的外科手术可以即刻改善这种神经压迫，绝大多数人就不会需要服用止痛药了。另一个好处在于，病人可以尽早进行离床活动，改善生活质量。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在病人肿瘤转移快要发生瘫痪的时候，外科一定要及时干预，这样神经功能就能得到解救，后面再做化疗和放疗就来得及。一旦病理性骨折发生了，脊髓都压迫了，这个时候还想保守治疗，是非常愚钝的。现在的研究表明瘫痪会加速病人的死亡进程，因为大多数脊柱转移癌的病人都是死于瘫痪所致的肺部感染、尿路感染、褥疮等相关并发症。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：我觉得患者的顾虑在于脊柱的特殊性，它是支撑人体的重要部位，患者会担心，如果手术失败，会有瘫痪的风险。你在看病的过程中，是不是也碰到很多患者，对于手术有很多的担心和犹豫？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：脊柱肿瘤手术面临几个风险，第一是出血的风险，因为许多类型脊柱瘤体血供丰富，容易出血；第二就是如你所说，手术所致瘫痪的风险。但瘫痪的概率在今天的技术条件下，发生率是非常低的。比如说我们去年做了九百多例脊柱肿瘤，症状加重的比例约1%，瘫痪的比例就更低，大约千分之一。因此一定要在高水平的脊柱肿瘤专病中心诊治，专病医生的丰富经验会减少手术相关并发症的发生。当然并不是所有的疾病都需要开刀，我们会进行评估后再给出诊疗意见。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：当你评估完，向病人建议手术的时候，你要怎么与他们沟通，来缓解病人的担忧呢？</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：病人的顾虑其实很正常，要增加沟通，要让他们了解：不要只看开刀的风险，你要倒过来看，如果不开刀的话会怎么样？如果不开刀，你的肿瘤能够不发展，处于稳定状态，那我建议你不要开刀；如果不开刀，你的瘫痪概率要远高于开刀的，那你觉得是等到你瘫痪了再做手术，还是及时做更好？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 如果病人能够理解这些的话，他们可能就会愿意接受治疗。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 感觉像当了逃兵一样</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 作为外科医生，对于病人病情的评估以及手术方法的选择是十分重要的，也就是通常所说的外科治疗策略。而手术疗效如何，同样重要的还有外科手术的技能。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：你刚刚提到：“一个好医生一辈子都在琢磨手术适应症的问题，一辈子都在想如何提高手术技能和优化手术方式。”你现在是一个成熟的医生，可以理性地进行评估，那你做小医生的时候呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：1990年代中后期，我开始专攻脊柱肿瘤外科，这是国际医学界公认的具有挑战性的临床难题，当时我们所有人都认为它是脊柱外科的技术制高点。早在1990年代中期，被誉为中国脊柱外科医师摇篮的长征医院骨科，其脊柱常规的创伤和退变技术日趋成熟，但很多脊柱肿瘤的手术做到一半做不下去，被迫关闭伤口，我们叫这种是“开关手术”。因为在手术中会伴随大量的出血，当时肿瘤切除手术技能匮乏，再加上也没有脊柱稳定重建的内固定器材。我们只有无奈地选择放弃，这是医生最懊恼和窝火的事情，就像打仗的时候发起冲锋，结果打到半山腰冲不上去一样。手术做到一半关掉了，感觉就像自己当了逃兵，自我感觉很不好。当时国内脊柱肿瘤的现状就是如此，我是带着接受挑战的心态进行攻坚的。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：在当时技术达不到的时候，有没有让你感到很无奈的病例？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：让我印象最深的是，1997年我遇到了两个病人，都是热恋中的女孩子，均患有胸椎骨巨细胞瘤。其中一个人的病灶相对比较小，经过我们前路、后路的两期手术，肿瘤切得比较干净。后来这个女孩子的病情也没有复发，现在已经组建了家庭，过着幸福的生活。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 另一个女孩子的肿瘤较大，当时我们竭尽了全力，但是当时外科技术有限，手术时间长出血多，肿瘤切除得不是很彻底。病人做完手术后进行了放疗，稳定了一段时间，后来病人陷入了复发、手术、再复发、再手术的恶性循环，她连续开了五刀，每一年都进行一次手术，最后肿瘤病灶仍无法控制，最终瘫痪和死亡。我觉得很遗憾的是，如果有现在这样的技术，结果肯定大不一样。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 这两个病例给我的触动很大，对于脊柱肿瘤外科医生来说，你必须具备超强的外科技术，它在很大程度上决定了病人的预后。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：技术达不到要求的时候，你非常困惑，也很懊恼。那什么时候，突然有种感觉：“我好像摸出点门道了”？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：2000年初我去欧洲进修，那年我回国后参加全国中青年优秀论文比赛的时候，当时比赛几乎芸集了国内最优秀的选手，我的论文是颈椎肿瘤外科治疗策略的文章，讲的是临床故事，最终进入决赛并获奖。我觉得我的技术逐渐走向成熟了，2002年根据不同的病例我可以自己设计方案了，在手术台上面对各种问题也更加自信了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 现在每年我都要出国到各个国家的医院去看看，保持技术交流。早期的时候，处处都很好奇，无论是设备还是技巧，觉得国外都比我们强。这两年出去，已经没有什么感觉了，说明我们和国际上已经逐步接轨了。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：2001年你去欧洲进修学到了什么，哪些东西让你觉得好奇？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：一个是他们手术切除的理念，一个是技术。1990年代以前就算你有钱，也没办法做那种内固定，因为国内几乎很少有脊柱系列的钢板和钉棒内固定系统。我们那时做枕颈内固定，都是自己到医院的车间去加工消毒，这种器材也因为便宜，都不收病人钱的。1990年代后期国外大量的脊柱内固定设备抢占中国市场，短短几年内就把中国的脊柱医疗水平带上了一个新的台阶。内固定器械的革命有力推动了脊柱外科技术的迅猛发展，最终让众多的病人获益。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 星期日：回顾你自己近20年在脊柱肿瘤外科工作的经历，你的感受是什么？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 肖建如：有些手术，我现在倒过来看，会发现当时存在的缺陷，如果让我今天做，我可能会做得更好。在技术不断进步的同时，回过头来也会反思一些问题。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 当然，有的时候看到自己十多年前做的手术，还是觉得做得非常棒。就算现在把当时的影像资料拿出来，都不会觉得过时。我记得2000年有一个上颈椎骨巨细胞瘤的案例，当时我们经口腔下颌骨入路做的，做了7个小时。那个时候我还处在探索时期，本来认为这个病人希望不大了，因为他的肿瘤很大且部位特殊，我担心切不干净。结果当时做得很好，到现在14年了都没有复发。这个时候，我会感到非常欣慰，感觉自己从死神手里捞回来了一条生命。当时病人及家属都哭了，他以为自己不行了，但迄今仍在正常地工作和生活，这让人感到无比得欣慰。</span>
</p>
