		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻子关乎健康，也需细心呵护</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="http://static.mingyizhudao.com/147004462990060" title="" alt="demo.jpg"/>
</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">采写/记者 陈俊珺</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">擤鼻涕时，你是否习惯按住双侧鼻腔，用力猛擤？挖鼻孔甚至拔鼻毛，是否成了你不经意间的习惯动作？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">复旦大学附属眼耳鼻喉科医院的鼻病专家王德辉教授提醒，正是这些看似平常的生活细节，影响着你的鼻部健康。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">明天是今年的“全国爱鼻日”，让我们听听王德辉教授关于“养成爱鼻护鼻六个好习惯”的提醒。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一,“善待”鼻毛</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">用手指挖鼻孔、拔除露出鼻孔的鼻毛，是不少人的习惯动作。王德辉教授提醒，鼻内的毛细血管其实非常发达，如同蜘蛛网一样丰富。鼻腔除了有通气功能外，鼻毛和鼻黏膜还有过滤、吸附空气中的灰尘和细菌的作用，以防止其进入呼吸道的深部。随意用手指挖鼻孔、拔鼻毛，很容易损伤鼻腔黏膜，或引起出血、鼻前庭炎症等。少数炎症还会通过面部三角区的静脉回流波及颅内而引发危险。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">如果鼻毛过长，露出鼻孔，可以先将鼻腔清洗干净，然后小心地用鼻毛修剪器将过长的鼻毛修剪掉。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">二，保持卫生</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">刷牙洗脸是人们日常生活中必不可少的卫生习惯，但鼻子的清洁却容易被忽视，成为被遗忘的“角落”。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">其实，鼻子是保证人体呼吸顺畅的“第一器官”，也最易受到细菌的感染。除了改掉抠鼻的坏习惯外，保持鼻腔卫生对鼻部健康也非常重要。鼻部有炎症或鼻部手术后的患者，应该在医生指导下使用生理盐水清洗鼻腔。具体方法如下：弯腰，面部朝下，双手捧水至鼻孔处；将水由鼻子轻轻吸入，而后呼出。也可用专门的鼻腔清洗器或鼻腔护理喷雾进行鼻腔清洗。鼻干燥者可在洗鼻后将1到2滴油剂滴于鼻腔。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三，正确擤鼻</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">擤鼻涕是生活中常见的动作之一，但能正确掌握擤鼻涕方法的人却并不多。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">擤鼻涕的正确方法应该为：以轻柔手法，先擤一侧再擤另外一侧，或者将鼻涕向后缩再经嘴巴吐出。切忌按压双侧鼻腔并用力猛擤，这样不仅容易造成头颅内压力不平衡，影响听力，并且易致鼻涕进入咽鼓管和鼓室，继发急性中耳炎。研究表明，经常不正确的擤鼻还会使鼻腔黏液进入鼻窦，令鼻窦成为病原菌滋生的温床，引起或加重鼻部炎症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">四，坚持按摩</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">王德辉教授建议，无论何种季节，都最好用冷水洗脸，并坚持按摩鼻部，这样能疏通经络、促进鼻部的血液循环，对鼻部健康大有益处。具体方法为，先用食指和拇指按揉鼻翼两侧、鼻唇沟内的迎香穴20到30次，然后用摩擦发热的手掌轻轻按摩鼻尖、鼻翼，顺时针、逆时针各10次，从而改善鼻内的血液循环，提高抗冷御寒能力，减少伤风感冒。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">加强锻炼，在空气新鲜的地方慢跑、散步等，也都能改善鼻部的健康。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">五，定期除螨防鼻炎</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">每到春季，都是过敏性鼻炎的高发时节。螨虫是我国过敏性疾病患者最主要的过敏原，其中以屋尘螨及粉尘螨最为普遍。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">王德辉教授推荐以下几种控制尘螨的方法：一，床上用品要定期清洗、烘干。普通洗衣粉在25℃、5分钟的条件下，即可去除绝大多数螨和猫毛等过敏原，如果使用杀螨虫的洗涤剂，去螨效果则更佳。二，过敏性鼻炎患者一定要加强室内的清洁和通风，空调的过滤网别忘定期清洗。三，尽量不要在地下室生活，因为地下室中的空气无法与外界形成较好的对流，且相对湿度较大，是螨虫绝佳的繁殖环境。四，最好使用防螨床垫、被套、枕套，并避免使用地毯。五，儿童不宜玩绒毛等织物玩具，尽量不穿羊毛内衣等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">王德辉教授特别指出，门诊中经常会遇到一些鼻炎患者，由于受到误导，非常担心长期患有鼻炎会导致恶性肿瘤，特别是鼻咽癌。事实上，无论是过敏性鼻炎还是慢性鼻炎、鼻窦炎，其转变为恶性肿瘤的概率都非常低。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">环境污染、不良生活习惯以及遗传才是造成鼻腔、鼻窦及鼻咽恶性肿瘤的主要原因。近年来，鼻炎患者在人群中的比例不断升高，得了鼻炎应该及时就医，在医生的指导下进行正确的预防和治疗，以缓解症状、防止并发症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">六，出鼻血时勿后仰</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在许多人看来，一旦出鼻血，第一反应就是头往后仰。殊不知，这样很容易导致鼻血倒流入咽喉、胃部等，对这些器官造成不良刺激，严重者还会误吸呛入气管及肺内，造成窒息危险。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">出鼻血后的正确处理方法应该为：人坐直，头部保持正常直立或稍向前倾的姿势，使已流出的血液自鼻孔向外排出，以免留在鼻腔内干扰到呼吸的气流。不要低头，而应让血液顺利地从鼻腔流出。同时，可将双侧鼻翼捏向鼻中隔，压迫5至10分钟。也可以填塞卫生棉球压迫止血，或用冰袋或冷毛巾敷鼻梁及前额。经上述处理后，如果鼻血仍不止或鼻出血反复发作者，需及时就医。</span>
