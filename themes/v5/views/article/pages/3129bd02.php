			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肖颖彬先进事迹报告会举行</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">9月5日，第三军医大学第二附属医院心血管外科主任肖颖彬同志先进事迹报告会在北京人民大会堂举行。报告会由国家卫生计生委和解放军总后勤部卫生部联合举办。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肖颖彬是中国人民解放军第三军医大学第二附属医院心血管外科主任，全军心血管外科研究所所长，二级教授，博士生导师。他先后完成1万8千余例心脏手术，领衔创造了国际首例高原不停跳心内直视手术、国内首例连体婴儿分离手术等医学奇迹。先后被评为中国十大杰出青年，中国医师协会“金刀奖”、重庆直辖10周年建设功臣，全军优秀共产党员、二等功荣立者。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">国家卫生计生委主任李斌号召全国医疗卫生系统要大力宣传肖颖彬同志的先进事迹，深入开展向肖颖彬同志学习的活动，大力推进医疗卫生队伍思想道德建设、作风建设和能力建设再上新台阶。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">李斌指出，肖颖彬同志不仅是军队医疗卫生战线的优秀代表，也是全国卫生计生工作者的光辉榜样。她强调，广大医务工作者要学习肖颖彬同志刻苦钻研突破医学难关的职业精神、心系患者敢于担当负责的大医情怀、立足本职践行为民宗旨的高尚医德，持续提高医疗服务质量，不断满足人民群众日益增长的医疗服务需求，努力办好人民满意的卫生计生事业，为全面建成小康社会、实现中华民族伟大复兴的中国梦作出新的更大的贡献。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">会上，报告团的6名成员分享了肖颖彬同志“为人、行医、授业”的点点滴滴，讲述了他心系患者、刻苦钻研医术、护佑军民健康的先进事迹。肖颖彬同志在紧急时刻坚守岗位不顾个人安危，平常时间救治生命责任高于一切的感人故事，令在场听众深受感动。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">国家卫生计生委副主任崔丽，总政宣传部部长周涛，总后政治部主任陶德平、副主任冯毅，总后卫生部副部长李清杰、第三军医大学政委高占虎出席了报告会。报告会由总后卫生部部长任国荃主持。国家卫生计生委办公厅、宣传司、医政医管局等相关司局，总政宣传部宣传局、总后政治部宣传部、总后卫生部医疗管理局领导及有关同志；地方、军队医务人员代表，共约700人参加了报告会。</span>
</p>
