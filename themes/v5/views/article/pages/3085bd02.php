<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>帕金森｜病or征？搞清楚了再治</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“目前帕金森治疗的主要方法是规范、系统性的药物治疗和微创的脑起搏器手术治疗两种，需要强调的是手术治疗时，必须认真区别患者是原发性帕金森病，还是帕金森综合征，因为两者的治疗方法和预后有相当大的差别。”解放军301医院功能神经外科凌至培教授接受寻医问药网编辑采访时说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3085/12.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 帕金森病≠帕金森综合征</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 首先，疾病起因不同。帕金森综合征一般由神经系统的其他疾病，如脑血管病、脑外伤、颅内炎症、脑肿瘤，或是由毒物、药物所引起，而帕金森病的病理改变主要为中脑黑质多巴胺神经元变性，以致不能产生足够的多巴胺而发病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 其次，治疗方法也不同。药物治疗方面，用左旋多巴替代药物疗法治疗帕金森病起到较好的效果，而对帕金森综合征的效果较差。手术治疗方面，凌至培表示，帕金森综合征患者一般不建议手术治疗，因为帕金森综合征患者即使手术也不会达到远期的治疗效果，只是暂时缓解，而帕金森病是适合手术治疗的，并且手术效果也非常显著！</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 药物治疗帕金森病仍然是最基础的治疗方法</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 对于帕金森病发病初期的患者，凌至培说，药物治疗仍然是最基础的治疗方法，药物治疗对治疗帕金森病有明显的效果，但是药物治疗有个两个很大的弊端，一个是由于人脑里面的血脑屏障会阻挡一部分的药物吸收，如果药量太大，又会对身体产生明显的毒副作用，久而久之会不耐受！</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 手术治疗帕金森病最好的方法是DBS（脑深部电刺激疗法）</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 对于中晚期患者药物治疗难以再控制病情时，就应该选择手术治疗。目前手术治疗最好的方法是DBS（脑深部电刺激疗法）治疗，即俗称的脑起搏器。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 这种方法是医生将一根比圆珠笔芯还细的高频刺激电极植入病人脑部特定的部位，通过微电流刺激，来抑制帕金森病人的颤抖，脑起搏器，对脑细胞没有损害，其刺激的微电流是可控、可调节。对人体结构没有任何破坏性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 手术中最为关键也最为困难的一步就是如何准确的找到定位点把电极放入。“脑袋就像一个很完整的西瓜，在西瓜中间的一粒西瓜子，如何在看不见的情况下只开一个小洞眼准确的把你所需要的那一粒西瓜子找到，如果没有找到精准的位置，一方面是没有什么效果，另外一方面就是带来其他的副作用。”凌至培非常形象的比喻说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 此外，为了找到精准的位置，手术前会用核磁共振技术确定手术的位置，这个位置不能超过1毫米的误差。由于人体的大脑被脑脊液所包裹，当在患者的头部打开1个多厘米的小孔时，常常会有少量的脑脊液流失，在重力的作用下，有的病人的大脑会有1至2毫米左右的移位，如果还按照原来的计划安装电极，就会有误差，手术也不会达到最佳的效果，为了避免这种误差，使用多通道的微电极记录系统进行术中的细胞水平的精准定位，应用了世界上最先进的术中核磁共振仪，初步安装好电极后，再次为病人做脑部扫描，观察植入位置，这使得手术的精确度极高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 对于精准医学在帕金森病治疗领域的应用，凌至培认为，其实精准医学的重点是在“精准”二字上，就像上面说的如何精准的找到电极放入的定位点这一精准得过程就是精准医学的最好体现。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 虽然DBS手术方法对脑结构没有破坏，并且对未来能够根治帕金森病的方法留下了很大的空间。但是这种手术仍然不是从根本上治疗了帕金森病。现在各种治疗帕金森病的方法都只是针对症状进行治疗，控制和改善症状，提高病人的生活质量。只有解决帕金森病的发病原理才是从根本上治疗了帕金森病。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 干细胞移植是根治帕金森病的一个方向，注意，是方向！</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 帕金森病的发病原理是神经功能细胞的受损，致使一种叫多巴胺的物质减少导致的。现在研究的通过干细胞移植来修复受损的细胞希望从根本上解决问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 凌至培认为，未来对于彻底根治帕金森病干细胞移植是一个方向，已经有了很大的进展，但是应用于临床还有一段距离。因为还有几个问题没有解决，首先是那些植入的原始干细胞刚好就分化成那些损害的黑质细胞并进行修复；其次是能否分泌需要的多巴胺量 ；最后是如何控制植入后的细胞的生长数量和速度。一旦这些问题解决，帕金森病以及中老年人神经系统退行性的疾病就都迎刃而解了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 由于这项新技术还处于研究之中并没有应用于临床治疗，凌至培强调，帕金森病患者不要被一些类似生物介入再生疗法、3N神经修复疗法等技术名称误导，在面对出现的新技术、新疗法时一定要选择正规医院去治疗，最新最好的治疗方法也一定是最先出现在正规医院，避免相信一些偏方、广告导致误诊、延误病情，对下一步治疗产生影响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
