	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 凌至培：解放军301医院主任医师，教授，硕士生导师，国内首先应用深部脑刺激（DBS）方法治疗帕金森病。主要负责立体定向和功能性神经外科疾病的治疗，擅长帕金森病、特发性震颤、肌张力障碍、痉挛性斜颈、舞蹈症、脑性瘫痪、抽动症等的微创手术治疗和难治性癫痫的评估和外科治疗。</span>
