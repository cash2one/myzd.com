		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>陈杰：把疝气修复作为 毕生事业的外科医生</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>陈杰：</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首都医科大学附属北京朝阳医院疝和腹壁外科主任，主任医师，临床医学博士。1998年2月开始从事无张力疝修补的临床应用、推广和基础研究，并率先在朝阳医院开展腹股沟疝的无张力疝修补术。2003年开始专攻疝气修复研究。2007年，创立了国内第一家疝与腹壁外科一级科室。如今，科室一个月的手术量相当于很多三甲医院同类疾病一年的手术量。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>谈工作：上紧发条的工作节奏</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多数时间里，陈杰的一天是这样度过的，早5：30从家里出来，6：30到单位，利用半个小时做一些非临床性工作，如为专业杂志审稿，准备讲课及培训资料等。7：30准时参与科室的查房与交接班工作，8点进手术室。通常情况下，手术做到中午才结束，遇到复杂病例时，就需要占用下午时间。下午，陈杰在朝阳医院的东院或西院出门诊，慕名找他的病人很多，15个号源对望眼欲穿的患者来讲就是杯水车薪，加号无法避免。“最多时加到过53号，看到第30个病人时就非常累了。”陈杰坦言，很多病人见他的第一句话经常是，陈教授，我是慕名来找您看病的，挂您的号非常不容易。正是这句话，让陈杰无法拒绝加号。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">精力旺盛是院里很多医生对陈杰的评价，长年睡眠时间不足6小时的他既要出诊，手术，培训，还要带队伍、参加国际交流。赶上手术日，就在两个手术中间打个盹，再接着上下一台手术。甚至还来不及体会倦怠的滋味，就要开始新一轮的工作。日复一日浸润在所钻研的领域内，他获得了同行的一致认同和患者的广泛好评。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>谈未来：主要任务是传帮带</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疝气，即人体组织或器官一部分离开了原来部位，通过人体间隙、缺损或薄弱部位进入另一部位，俗称“小肠串气”。就像穿久的衣服会破一样，人到了一定年龄后就会成为疝气的高发人群。中国一年需要疝气手术的病人有几百万，“每年都有因疝气治疗不及时而死亡的病人，非常可惜。”陈杰说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">传统的修复手术效果及预后并不理想。上个世纪八十年代，国内从国外引进了无张力疝修复手术技术，陈杰率先在朝阳医院开展，既减轻了手术痛苦，又缩短了住院时间，还节约了治疗费用，越来越多患者因此而受益。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疝气手术是外科医生的入门手术，难度不高，但面对疑难和复杂病例，做到逐一攻克并非易事。不仅需要扎实的理论基础，更少不了丰富的临床经验。“随着年龄的增长，医生的体力到达顶峰后逐渐开始走下坡路，但经验却成为最宝贵的财富。”陈杰说，未来主要任务是传帮带，把自己多年的临床实践经验传授给团队其它医生。努力带好徒弟，等到自己需要做疝修复手术时，让他做才放心。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>记者手记</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者的信任重如泰山</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">见到陈杰时，他刚从一台手术中下来。当天，5个手术室同时开台，他要参与22位病人的疝修复手术。5位病人局麻，16位病人全麻。陈杰说，一旦全麻，病人的生死就托付给了医生。非亲非故，病人以命相托，源自患者对医生的充分信任。“肩负着这份沉甸甸的信赖，医德应该是从医者的入门标准，其次才是技术。”陈杰说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者慕名找到陈杰，目的除了瞧病，还希望由他亲自做手术。陈杰告诉记者，为了满足更多患者的需求，手术的关键部分由他来做，但不能保证每例手术他都全程参与。科室梯队建设已经形成，同事们定期在一起讨论、交流业务，手术水平请患者务必放心。受疾病困扰，在治疗上，患者会寄予医生很高的期望，但现阶段的医学技术并不能包治百病。一部分患者会把因此而带来的负面情绪转嫁给医生。陈杰对此表示理解并直言，面对不同个体的患者时要保持平和心态，尽量在技术上追求完美，以弥补医学技术在发展进程中带来的缺憾。陈杰总结，自己对事业发自内心的热爱成就了今天的成绩，也因此收获了精神上的满足感和自豪感，给予他不断前行的动力。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现在，越来越多的外地疝气疑难、复杂病人被推荐到朝阳医院，对于朝阳医院疝与腹壁外科的外科医生来说，是机遇，也是挑战。</span>
</p>
