		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>广州军区总医院成功实施寰枢椎脱位手术</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一名刚满两岁的幼儿，因车祸造成创伤性寰枢椎脱位，患者辗转求治全国各大医院均因手术风险太大而放弃治疗。广州军区广州总医院骨科医院尹庆水院长凭借丰富的临床经验和独到的数字骨科技术迎难而上，战胜风险成功为患者实施高难度手术。据权威资料检索，为年仅两岁多的患者施行此类手术属世界首例。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2009年初，一场飞来横祸降临在江西南昌市年仅2岁的小患者刘亦菲身上，她不幸被疾驰的汽车撞到，车祸致小亦菲颅脑外伤以及右股骨骨折，昏迷不醒的小亦菲在江西老家医院和广州一家大医院治疗一个半月后终于苏醒，病情好转，但肢体肌力始终无法恢复正常。磁共振检查发现寰枢椎脱位导致脊髓压迫。治疗医生忧虑地告诉亦菲妈妈，如果无法恢复寰枢椎正常的对位关系，脊髓压迫发展到不可逆阶段，小亦菲将造成高位截瘫的永久性残疾，甚至可能因呼吸循环衰竭而危及生命。心急如焚的亦菲妈妈带着小亦菲辗转求医于北京、上海、广州等地的大医院，可是医院的专家都一致认为手术风险太大，无法顺利完成。最后，抱着最后一丝希望的亦菲妈妈找到了以上颈椎手术闻名国内的广州军区广州总医院骨科医院院长尹庆水教授，尹教授详细检查了小亦菲的病情，确认小亦菲患的是创伤性寰枢椎脱位，需要行“后路寰枢椎椎弓根钉棒复位内固定术”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 随后进一步的检查结果显示，小亦菲寰枢椎椎弓根最细的地方直径仅为3.6mm，而最小的椎弓根螺钉直径也达3.5mm，一根直径达3.5mm的螺钉置入直径只有3.6mm的椎弓根内，要求置钉技术必须非常精确，这也就意味着手术风险极大，术中置钉时如稍有不慎螺钉极易穿破椎弓根皮质，损伤椎动脉或脊髓，将导致灾难性的后果。手术是做还是不做？医院专家的意见一度出现了分歧，有“好心人”提醒尹庆水，像这样高风险、高难度的手术还是别做了，万一出了事，谁也负不起这个责任。面对压力和挑战，有着几十年丰富行医经验的尹庆水没有惧怕，他语气坚定地对全科医生说：“做医生的不能怕担风险，只要患者有一线希望就不能放弃！”在对患者进行了深入细致的检查，并查阅了大量相关资料后，尹庆水决定采用世界先进的计算机辅助设计和快速成型技术，配合骨科医院在寰枢椎治疗领域的先进经验，在降低手术风险的基础上，为患者施行“后路寰枢椎椎弓根钉棒复位内固定术”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 数字骨科是以骨科学为基础，计算机数字化技术为辅助的前沿交叉科学。简而言之，就是针对患者病情，先用计算机设计好手术整个过程，然后按计算机设计方案进行手术。这种人机交互的医疗技术，可为患者“量身定做”高精度的手术方案，把并发症发生的可能性降到最低。作为21世纪新兴的临床手术治疗方案，在其产生的十余年里已获得快速发展，尹庆水教授带领的广州军区广州总医院数字骨科研究团队攻克了数字骨科多项技术难关，在四肢骨折、骨关节发育异常、脊柱损伤、肢体矫形、骨肿瘤、骨缺损修复等领域广泛开展了数字骨科手术，成功完成数字骨科手术70余例。目前广州军区广州总医院骨科医院数字骨科技术已达到了与国际同步、国内遥遥领先的水平。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在尹庆水教授的带领下，由数名骨科专家组成的数字骨科团队加班加点废寝忘食，认真为小亦菲进行计算机测量和设计手术方案。经过详细评估，手术方案终于出炉。2009年4月的一天，尹院长和医护人员成功为小亦菲施行了手术。手术非常顺利，比原计划提前两个小时完成。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 5月8日，刘亦菲的父母手捧锦旗来到广州军区广州总医院面谢恩人，通过专科复查，医生告诉亦菲父母，目前小亦菲的智力、语言与运动功能恢复情况良好。</span>
