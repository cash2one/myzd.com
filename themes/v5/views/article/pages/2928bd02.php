<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003297745763" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">图片说明：医生紧急为伤员进行骨科手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 东方网通讯员王根华、实习记者孙晓菲5月18日报道：上海派出的6支医疗救援队自14日前往灾区之后，一直奋战在救援第一线。由于山体滑坡，路面严重被毁，道路不通，大批伤员积压在灾情严重的乡镇边远地区。上海长征医院医疗队在人员十分紧张的条件下，又抽调12人，分3个小组，分别前往晓坝、桑枣、沸水、映秀镇等地展开救治工作，在当地谱写了一首首医疗工作的感人篇章。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 截至16日下午6时，上海长征医院医疗队共收治了700余名伤员，开展了60多台大手术，其中不乏颈椎、开颅和开胸等难度较大的手术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 16日中午，从北川送来了一位名叫杨静的女患者，被营救出来后，全身已无法动弹，经过医生检查发现她的颈前路颈部椎体爆炸性骨折。此类病人已不可能后送，即使手术也有可能瘫痪的危险。长征医院骨科副教授史建刚斩钉截铁地说：“准备手术！”在微弱的灯光下，医生以顽强毅力争分夺秒，挽救伤员的生命。经过二个半小时的艰苦努力，手术获得圆满成功。简陋的手术里一片欢呼声，成功了！这是在整个灾区完成的第一例高难度、高风险的重大手术。术后病人双手已能活动，本已麻木的脸也露出了笑意。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 17日，地震后出生仅3天的一男婴被父母抱着，经过10来个小时的连续奔波跑到山外，被等候在山下的救护人员送到长征医院医疗队驻地。经检查后，母亲刘霞英因一路小跑出现了出血情况，小孩脐带有感染，头部有血肿。长征医院妇产科李文和刘晓军两位医生分别对他们进行了对症处理，并拿来了奶粉和食品，让婴儿和产妇食用。很快，原来一路啼哭的婴儿就平静地睡着了。后来当医疗队员问小孩叫什么名字时，父亲李兵说就取名“长征”吧，并解释说：“10个小时的连续奔波可谓是长征，后又被长征医院专家救治，取名长征意义重大呀。”</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 沸水镇57岁的付利华因被掉下的石块砸伤颈椎，导致肢体失去知觉。长征医院骨科副主任叶晓健介绍说，这种突发性外伤导致的不完全瘫痪，如不及时进行急诊手术，极可能导致高位截瘫。颈后入椎板减压手术，在平时算不上什么特别，但在医疗条件相当匮乏的野战救护所，能进行这种手术不仅要需要高超的技术，还要有过人的胆识。为了挽救付利华，叶晓健在认真评价了手术条件后，决定勇闯禁区。在麻醉科众医师的协助下，经过2个多小时的努力，成功地完成了手术。病人目前正在康复中。据文献资料显示，野战条件下全麻进行急诊颈椎手术还未见报道。</span>
