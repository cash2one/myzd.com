		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>成功接受眼科手术（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">102岁老人</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">成功接受眼科手术</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">近日，北京医院眼科、心内科、呼吸科、麻醉科等多科室，联合为一位102岁的高龄患者成功施行了白内障超声乳化、人工晶体植入术联合玻璃体腔注药术，患者术后良好。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">术前，这位102岁高龄患者因自觉视力迅速下降，查出双眼老年性白内障和一眼湿性老年黄斑变性，需要进行手术。这位患者老年性白内障，还合并有严重的眼底病变，手术风险大。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在这种情况下，北京医院眼科戴虹主任、郭晓萍主任医师联合心内科、呼吸科、麻醉科和手术室，为这位102岁高龄的患者，施行了白内障超声乳化、人工晶体植入术联合玻璃体腔注药术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">手术过程仅有10分钟，术后该患者不仅眼部感觉良好，其全身也并无不适反应。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">术前，各科室会诊多次，准备各种应急预案；术中，责成专科人员对重要器官进行专门监测；术后，因高龄老人睡眠中也会有潜在风险，所以护理人员还要对其进行睡眠监测。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">文/记者 范维</span>
