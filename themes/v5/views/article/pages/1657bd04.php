<div align="center">
	<span style="font-size:18px;" id="__kindeditor_bookmark_start_2109__"><span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>从“肾斗士”说肾移植</strong></span></span><br />
</div>
<span id="__kindeditor_bookmark_start_2109__"><br />
<br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/1657/6.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">克拉什尼奇接受肾移植后重返赛场，表现依然神勇图/新华社</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 受访专家/中山大学附属第一医院器官移植中心主任 王长希</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 文/羊城晚报记者 张华 通讯员 李绍斌 彭福祥</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 肾移植</span><br />
</span>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 四年一度的欧洲杯让球迷们为之疯狂。在观看精彩的足球赛事时，资深球迷常常会为一位足球场上的“肾斗士”感叹不已，他就是来自克罗地亚的球星克拉什尼奇。虽然本届欧洲杯他没有出现在首发名单，但做过两次肾移植手术竟然还能重返球场的克拉什尼奇的确创造了诸多奇迹。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 父母分别给他捐了一个肾</span><br />
</span>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 克拉什尼奇在足坛就是坚强的代名词。这名射手在不来梅有着大红大紫的经历，由于被查出肾功能障碍，他在2007年不得不进行肾移植手术。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2007年初，他的母亲为他捐肾，但术后5天，换上的肾出现严重的排异反应，他必须进行第二次换肾。没过多久，克拉什尼奇父亲的肾成功移植到了他身上。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 专家解析：目前，肾移植技术已十分成熟，主要的难点在于肾源相当匮乏，超过90%尿毒症患者只能靠透析治疗（腹透或血透）维持生命，等上两三年才能换肾的患者并非少见。目前，解决肾源问题主要有两条途径：1、亲属间器官捐献；2、脑死亡或心脏死亡的器官捐献，其中前者已占近50%的比例。根据遗传学的规律，亲属间捐献的成功率比普通人群要高。所以，克拉什尼奇的父母给他捐肾，而且手术终于成功了。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 按照我国的规定：严厉禁止非亲属之间的活体捐赠，活体器官捐献人与接受人仅限于以下亲属关系：1、配偶：仅限于结婚三年以上或者婚后已经育有子女的；2、直系血亲或者三代以内旁系血亲；3、因帮扶等形成亲情关系：仅限于养父母和养子女之间的关系、继父母与继子女之间的关系。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 术后八个月重返赛场</span><br />
</span>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 术后34天，克拉什尼奇开始了慢跑训练。2007年的10月，他重返赛场，这离他做完手术不到8个月的时间。作为足坛历史上第一位换肾后重返赛场的球星，“肾斗士”克拉什尼奇扬名天下。但很多人不知道，当克拉什尼奇在场上奔跑的时候，他的一侧下腹部带着一个用玻璃纤维制成的保护罩，这个保护罩的作用就是防止他的移植肾受到猛烈的撞击。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 专家解析：慢性肾功能不全的患者，既可以透析又可以换肾，但肾移植是最理想的治疗方法，因为肾移植后，极大地提高生活质量，患者可以像正常人一样生活。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 尿毒症患者在顺利完成移植手术后，可以立即有尿排出来，肌酐也能在2-3天后恢复正常，2-3周就能出院。肾移植的伤口大约有10厘米，在10天左右伤口就能愈合。一般来说，术后3个月就可以做一些有氧运动锻炼，如散步、慢跑，运动半小时至两小时。6个月之后就可以跑步、游泳、打球，跟正常人一样生活。所以，克拉什尼奇可以在换肾之后重返赛场。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span id="__kindeditor_bookmark_start_2109__"><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 运动对移植受者很有好处，可以增强抵抗力，促进消化，控制体重，达到提高长期存活率和维持良好生活质量的目的。但由于足球运动是一项对抗性极强的运动，克拉什尼奇为了避免移植肾区受到撞击，需要用保护罩保护自己。</span></span>
</p>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;" id="__kindeditor_bookmark_start_2109__"> 据了解，国内肾移植患者的肾脏移植物最长存活了32年，国外有超过40年的报道。若术后随访管理好的话，肾脏移植物10年存活率可以达到90%，绝大部分患者能够回到自己的工作岗位，开始正常的生活。不过，肾移植手术最大的副作用就是需要长期服用抗排斥药物，这些药物会增加感染和肿瘤的风险，这对于他们的生活也会造成一定的影响。而且肾脏移植物并不能永远存活，比较年轻的患者在一生中可能需要进行两次或者几次移植。如果移植失败，患者可以回到透析治疗中去，等待另一次移植。</span>
</p>
