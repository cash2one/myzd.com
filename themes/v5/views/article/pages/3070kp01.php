		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>3%广州孩子有睡眠障碍 医生提醒勿乱服安眠药</strong></span>
</p>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;2011年的数据显示，广州睡眠障碍发生率高达47%，2013年全国睡眠指数发布，广州成为全国最难入睡城市。今年，广州人均7.9分钟的起床时间，又一次成为起床最利索城市。明天是世界睡眠日，广东三九脑科医院睡眠监测中心主任郭沈昌教授表示，长期失眠不可小视，很可能是存在精神或躯体方面的疾病。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">关上了电视，37岁的广州白领张伟锋又打开了平板电脑发表了一通看完亚冠球赛后感想，不知不觉就到了凌晨1点。近3个月来，他的入睡时间基本都是这个点，随后是清晨7点起床。而不到6小时的睡眠，让他需要通过口服两粒舒乐安定来帮助入睡，服用该药物的副作用，就是白天不宜驾驶。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 2011年的数据显示，广州睡眠障碍发生率高达47%，2013年全国睡眠指数发布，广州成为全国最难入睡城市。今年，广州人均7.9分钟的起床时间，又一次成为起床最利索城市。明天是世界睡眠日，广东三九脑科医院睡眠监测中心主任郭沈昌教授表示，长期失眠不可小视，很可能是存在精神或躯体方面的疾病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 形形色色的睡眠障碍</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 30多岁的李先生于3年前睡觉时出现打鼾，如雷的鼾声经常把妻子和孩子吵醒，偶尔也会把自己憋醒。经多导睡眠脑电图监测，李先生夜间多次呼吸暂停，血氧浓度下降，每次呼吸暂停后会被憋醒。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 明明已经睡着，却总感觉自己整夜未睡。江女士被“失眠”折磨了十几年，最终被诊断为“主观性失眠”。其症状为患者坚信自己“失眠”，并能具体描述为入睡困难、睡眠不足或完全失眠，但多导睡眠图显示睡眠时间与睡眠结构基本正常。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 郭沈昌表示，睡眠障碍可引起冠心病、高血压、2型糖尿病、代谢综合征等疾病，甚至可引起脑卒中、夜间猝死等。需要通过睡眠监测及临床诊断，盲目服用诸如褪黑素、安定片类安眠药物的做法并不可取。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 孩子睡觉打鼾需注意</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 广州3%左右的儿童也会因为鼾症等因素出现严重睡眠障碍。“导致儿童睡眠障碍的主要原因，则是源于扁桃体、腺样体的肥大，阻塞了呼吸道”，广州市妇女儿童医疗中心耳鼻喉科主任刘大波表示。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 刘大波提醒，调查显示，在广州地区乃至广东省，每百名儿童中有3个孩子会发生类似的睡眠障碍。儿童睡眠如出现打鼾现象，往往提示孩子生长所需的氧气量不足。如不及时治疗，会严重影响孩子的智力、身体、记忆力等方面的发育。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 刘大波建议，家长应培养好孩子睡前养成良好习惯，多听音乐，注意不要剧烈运动，同时将温度、湿度、光亮度都调节到最为适宜的状态。“对于孩子而言，睡前过度饮食也影响睡眠。孩子可以在睡前喝一些热牛奶，但是最好在睡前半小时喝。”</span>
</p>
