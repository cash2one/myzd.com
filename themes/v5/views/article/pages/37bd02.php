		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>爱心接力点燃少女希望之光（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">14岁的女孩毕晨滢本应享受多姿多彩的校园生活，但一种叫做脊椎肿瘤的病却改变了她的生活——躺在医院的重症监护室内接受治疗。巨额的治疗费用使原本贫困的家几乎陷入绝境，但匿名的爱心捐款却点燃小滢滢的希望之光。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;患重病&nbsp;四肢异常</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;毕晨滢，是肥东县白龙镇青龙中学一名普通的初二女生。活泼可爱的她是家里的开心果，但现在，她却只能一动不动地待在医院重症监护室的病床上。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;今年5月，小滢滢正和哥哥一起玩耍，哥哥忽然发现她的动作似乎和正常的孩子有些不同。小滢滢走路时一瘸一拐，而且身体向一边倾斜。这时，她的父母才发现毕晨滢的四肢竟然不一样粗，右边的胳膊和腿明显的要比左边粗上一圈。经过医院确诊，毕晨滢得的是脊椎肿瘤。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“孩子之前也没说过哪里痛，或者身体不舒服，谁知道一检查就是这么大一个病。”毕妈妈流着眼泪说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;为求医&nbsp;四处奔波</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;为了治好孩子的病，小滢滢的父母带着她连续跑了好几家医院，但得到的答复都是“没把握”。“好几家大医院的医生都说，因为病情比较复杂，他们没有把握能够治好孩子。可是这个病要是不治，孩子肯定会有生命危险的。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;当他们带着小滢滢来到合肥一家医院的时候，该医院脊柱骨科楚主任告诉他们，他们医院和上海的一家医院有着合作关系，可以邀请上海的专家来合肥给毕晨滢看病。在小滢滢住院两个礼拜后，上海的专家肖建如为她做了肿瘤切除手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“我们现在每天只有半个小时能进监护室看看孩子。”&nbsp;小滢滢的爸爸告诉记者，“孩子现在很担心，害怕自己再也不能走了，害怕再也回不到校园，可我们却什么都做不了，只能在旁边陪着她，告诉她要坚强。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;为治病&nbsp;家徒四壁</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“肖医生说，由于是在脖子前后各开了一刀，情况比较复杂，特别容易出现术后并发症。”毕妈妈说，孩子在手术后果然出现了大面积水肿和重度肺部感染。“现在，她住在重症监护室，胳膊和腿几乎不能动。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;现在，毕妈妈在医院照顾滢滢，而毕爸爸在外打工筹钱。“孩子的手术费要15万元，对于我们这个农村的贫困家庭来说，简直是一个天文数字。”为了能给孩子尽快做手术，滢滢家里卖掉了唯一值钱的耕牛，还垫上了从亲戚家东拼西凑的10万元。“现在术后出现并发症，又需要治疗，这个钱我们实在没有了。”一边是生病的女儿、一边是高额的医药费，毕爸爸愁得满头白发。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;爱接力&nbsp;共盼康复</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;怀着仅有的希望，毕爸爸将孩子的情况发在了论坛上，没想到引起了许多人的关注。据毕妈妈介绍，前几天有人来医院看望滢滢，离开时留下了1200元钱爱心善款。“我希望他们能够留下自己的名字，可是他们不愿意，一直说这是自己的一番心意。”同时，也有不少爱心人士匿名往毕妈妈的银行卡里打钱。“这些好人从不留下自己的信息，只说希望孩子能好起来。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;虽然手术取得成功，但滢滢出现了术后肺部感染，只能静静地躺在重症监护室里。整个采访过程中，毕晨滢家人说得最多的话是“谢谢”。“谢谢那些帮助过滢滢的好心人，我们没有什么文化，不太懂得表达，但是这份情我们会一直记在心里。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;□何云&nbsp;见习记者&nbsp;汪洋&nbsp;乐天茵子</span>
</p>
