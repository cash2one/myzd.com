			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>重庆妹儿的骨头就是“不够硬”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“重庆美女别只想着美白瘦身，应该多晒晒太阳、喝喝牛奶、练练身体，年纪大了你才能依然魅力十足!”昨天是世界骨关节炎日，第三军医大学西南医院关节外科主任杨柳教授说，重庆是全国关节病的高发地，年轻时候就不合理的生活理念和习惯是患上骨关节病的主要原因。在关节病中，发病率最高的是膝关节，西南医院关节外科全年手术超过2000台，其中大部分都是膝关节类手术，手术患者近八成为女性。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骨关节病不是老年病，孩子也可能得</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“人们常常将骨关节疾病视作老年病，其实，所有年龄段的人，甚至包括儿童都有可能罹患此病。”杨教授说，对关节疾病应该早预防、早诊断、早治疗，身体素质较差的老年人，骨关节疾病会引起很多并发症，存在极大的生命危险。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">30岁妹子没国外60岁妇女骨头硬</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“重庆市民的骨头就是‘不够硬’，有些美女才30岁，骨头还没有国外一些60岁妇女骨头硬;别人都是硬的，掰都掰不动，她们的都是脆的，一折就断!”让杨教授感到忧虑的是，重庆中青年女性对关节病的认识几乎没有概念。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据统计，西南医院关节外科全年手术超过2000台，其中大部分都是膝关节类手术。“而膝关节手术男女比例是2∶8，女性患者又以中老年为主，并呈逐渐低龄化趋势。她们得关节病很大部分原因都是‘骨头质量’不行了。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为啥妹子们躺枪了?</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">重庆梯坎多</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杨教授分析了该科室近几年的收治情况，他认为，重庆女性这么多的关节病原因与重庆地域特点、女性生理结构、生活方式有重大关系。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骨关节病主要由“机械磨损”和“骨头质量”两方面引起。重庆是一座山城，市民长期爬坡上坎，关节“机械磨损”方面比其他地区严重。“而骨头质量说直白点就是骨头硬不硬，有没有骨质疏松。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">觉得自己还年轻</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杨教授说，重庆市民特别是女性，在青年时期普遍认为“养生保健”这些事情是中老年人的事情。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，女性在50岁前后绝经期，身体钙质流失特别明显。绝经以后由于内分泌相对失调、身体机能开始退化，很多女性就算绝经后开始锻炼身体、补钙、补充维生素，吸收能力已严重不足，预防骨关节病效果会大打折扣。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杨教授打了一个比方，年青女性就开始认识、预防关节病，就是往身体里“存钱”，存得越多以后老了能用的越多;但到了中老年再开始，身体里“存钱”这个功能已经没有了，你再怎么想存钱也存不进去。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“年纪大了发现有骨质疏松、骨关节病了，你来医院更多的就是‘治病’了;关节病预防的黄金期在中青年。”杨教授解释说，只有在年青时候让骨头少磨损、更坚硬，年纪大了骨关节的退化就会从较高起点开始，不至于很快患上骨关节病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">选保暖的打底裤、多晒太阳</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这些法子对“美”没帮助，但对你骨头好</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">晒太阳</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“年轻女孩子都想白一点瘦一点，造成很大一部分不喜欢锻炼、不喜欢晒太阳、不均衡营养。”杨教授说，美女们怕晒怕黑，很多人日晒过少，殊不知晒太阳是重要的一个补钙手段。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">少喝咖啡</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">年轻人把工间的一杯咖啡看做时髦，杨教授却建议，尝试少喝咖啡类刺激性饮品，多喝牛奶、补充胶原蛋白，这些都是增强骨头质量的好方法。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">少久坐</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现在很多年轻人大多从事电脑工作，每天面对电脑有10个小时左右，长时间用一个姿势久坐，长久的坐姿使得身体关节压力大，也会增加患骨关节病的风险。合理地进行体育锻炼对保持和改善关节活动也很有利。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">打底裤要保暖</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">天气逐渐转凉了，一些爱美女性还是穿着打底裤、短裙上班，殊不知，关节部位的脂肪和肌肉层比较薄，一不小心就会患上膝关节炎。</span>
