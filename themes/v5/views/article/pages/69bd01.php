<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>权威访谈—郭辉受邀做客江苏卫视：癫痫患者如何正确看病&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;权威访谈——</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;郭辉受邀江苏卫视：癫痫患者如何正确看病</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;癫痫，俗称：“羊羔疯”，到底该怎么治疗？那么癫痫到底有没有这么可怕，我们又该如何面对癫痫、治疗癫痫呢?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;6月24日，江苏卫视《万家灯火》节目组特邀了同济大学附属上海德济医院主任医师、教授郭辉老师为大家解读什么是癫痫病、癫痫病的诱发因素、癫痫病患者日常该注意些什么以及市场上假药泛滥、该如何选择正规医院进行治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;癫痫病 是一种由脑部不正常放电所引起的脑科疾病，在中国有九百多万患者，是一种比较普遍的脑科疾病。而郭教授20多年的临床经验告诉我们，通过目前技术80%的 患者都能完全治愈，癫痫病并没有那么可怕。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 癫痫的治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 1.药物治疗&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;70%的癫痫病患者可以通过药物治疗完全控制、治愈病情，但是一定要到正 规专业的医院诊治、开药，因为如果不慎买到假药， &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 不仅没有疗效，还会使病情恶化、引起反复发作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;2.手术治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;无法通过药物得到治疗的则称为难治性癫痫，而难治性癫痫里 又有80%的病患可以通过手术治疗得到治愈。近十年来，癫痫手术治疗已经非常先进，通过探测的电极精确定位找到不正常放电的病灶之后，可以通 过手术完全根治，且癫痫病手术的副作用很少，根据目前新技术，2500个癫痫手术只有3个有副作用，千分之一的概率都不到，基本上不存在死亡、植物人、偏 瘫等情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3.生酮饮食治疗癫痫</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 通过改变饮食中蛋白质、脂肪、碳水化合物的比例，让人体血液中酮体的浓度升 高，以此来控制、治疗癫痫。目前为止，已取得良好的疗效。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;4.神经调控技术</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 仅用于其它治疗方案无效后采取此方案，用刺激器，发送电冲动来刺激迷走神经，通过迷走神经抑制大脑放电这样一个机理，一般费用在20-22万左右，一般不采用这种方法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癫痫病患及家属在日常生活中需注意的事项：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 病患在家里发作时，不要搬弄病患，让其侧躺;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2 用筷子缠布让病患横咬，以免咬伤舌头;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3 病患不能从事危险活动，如驾车、炒菜，以免病情突发，伤害到自己;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4 如家中有癫痫患儿，不要对其大补，容易诱发病情;看电视等每小时要休息，每天最好不要超过3小时。</span><br />
