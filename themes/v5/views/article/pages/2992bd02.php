		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>广东省人民医院五科联动手术七小时创母女平安奇迹</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯（记者曾文琼 通讯员陈靖 周小英）近日，广东省人民医院 成功地为一妊娠33周的产妇同时施行了剖腹产和心脏再次换瓣手术，现母 女平安。据悉，施行这样的手术可以保住母女平安在我省属首例。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 来自惠州的小曹8年前因风湿性心脏病做过换瓣手术，其后一直情况良 好。但由于她的病生小孩有危险，她的婚事遭到了男方父母的反对。为此冲 破阻力结婚的小曹最大的愿望</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">就是生一个小孩。去年小曹怀孕了，但一直缺 乏合理的治疗，近期突然出现心悸气促、脚浮肿、不能平卧等症状，在当地 医院住院治疗后症状无改善便来到省人民医院妇产科求医。当时孕妇呼吸困 难，出现心衰症状，生命危在旦夕。妇产科马上请心脏内科、心脏外科专家 会诊，经诊断为风心换瓣后人工瓣膜血栓形成、瓣膜失功能和严重心功能不 全，必须立即手术，否则孕妇和胎儿都随时有生命危险。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 面对这种危险情况，省人民医院非常重视，成立了由妇产科、心脏外科 、心脏内科、麻醉科、新生儿科组成的医疗小组。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当天晚上10点多，手术开始，由心脏外科卢聪和妇产科钱德英副主任 医师主刀。卢聪插好管，开好胸，做好体外循环的一切准备工作。等妇产科 钱德英副主任实施剖腹产取出胎儿后，便立即在体外循环下做心脏再次换瓣 手术，做完换瓣术后将血液调到正常状态，钱主任再查看腹部伤口没有出血 后才将伤口缝合。手术持续7个小时，婴儿重2400克，母女平安。所有 在场的医护人员都说这是一个奇迹。 手术两大难度</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 孕妇小曹8年前因心脏病做过换瓣手术，近期再次病发，经专家会诊， 必须立即手术，否则孕妇和胎儿都随时有生命危险。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 难度一：先治心脏胎儿必死</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 假如先取出血栓重新换瓣，因心脏换瓣要在体外循环、低温麻醉、血液 肝素化等情形下进行，孕妇心跳一停止胎儿心跳也会停止，则胎儿必死于腹 中；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 难度二：先剖腹产孕妇难保</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 假如先剖腹产取出胎儿，孕妇回心血液猛然增加，本已心衰的心脏负荷 不了，则孕妇生命难保。</span>
</p>
