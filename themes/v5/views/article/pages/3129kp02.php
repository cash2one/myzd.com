			<p style="text-align:center;">
	<span style="line-height:2;font-size:18px;font-family:'Microsoft YaHei';"><strong>先天性心脏病的病因和预防</strong></span>
</p>
<p style="text-align:center;">
	<span style="line-height:1.5;"><strong><br />
</strong></span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;先天性心脏病的确切原因迄今尚不清楚，研究认为是遗传和环境等因素相互作用的结果，下列因素可能影响到胎儿的发育而产生先天性畸形。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 1.胎儿发育的环境因素: (1)感染，妊娠前三个月患病毒或细菌感染,尤其是风疹病毒，其次是柯萨奇病毒，其出生的婴儿先天性心脏病的发病率较高。(2)其它：如羊膜的病变，胎儿受压，妊娠早期先兆流产，母体营养不良、糖尿病、苯酮尿、高血钙，放射线和细胞毒性药物在妊娠早期的应用，母亲年龄过大等均有使胎儿发生先天性心脏病的可能。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 2.遗传因素：先天性心脏病具有一定程度的家族发病趋势，可能因父母生殖细胞、染色体畸变所引起的。遗传学研究认为，多数的先天性心脏病是由多个基因与环境因素相互作用所形成。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 3.其它：有些先天性心脏病在高原地区较多，有些先天性心脏病有显著的男女性别间发病差异，说明出生地海拔高度和性别也与本病的发生有关。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 在先天性心脏病患者中，能查到明确病因的是极少数，但加强孕妇的保健，特别是在妊娠早期积极预防风疹、流感等风疹病毒性疾病和避免与发病有关的一些因素，对预防先天性心脏病具有积极意义。</span>
</p>
