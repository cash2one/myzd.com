		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>宝宝常发呆，当心癫痫“小发作”</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">受访专家/南方癫痫研究所所长、南方医科大学第三附属医院癫痫外科主任 张新伟</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 文/羊城晚报记者 陈辉 实习生 李丽斯 &nbsp;通讯员 陈晓霖</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 癫痫宝宝在患病早期以“小发作”为主，表现好像发呆一样，常常被家长所忽视。但这种“小发作”往往很频繁，一天多达几十次。每次发作身体都处于缺氧状态，会使得脑细胞持续受损，也会影响宝宝的智力发育。医学专业人士提醒，如果发现宝宝有这种“小发作”，要及时就医，越早开始干预，癫痫对宝宝身心发育的影响也越小。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 别忽视宝宝“小发作”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 癫痫，即俗称的“羊角风”或“羊痫风”，是大脑神经元突发性异常放电，导致短暂的大脑功能障碍的一种慢性疾病。南方医科大学第三附属医院癫痫外科主任张新伟说：“在中国，癫痫已经成为神经科仅次于头痛的第二大常见病。癫痫在人群中发病率是千分之五到七，小孩的数据更高。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宝宝癫痫大发作时，可能会出现突然意识丧失，倒地、头后仰，甚至口吐白沫、面色青紫等表现，往往会把家人吓坏了。可是，宝宝在患病早期，常常以“小发作”为主，表现可能是突然呆住了，但往往只持续几秒钟。张新伟提醒说，这种“小发作”一次可能对宝宝的身心影响不大。但这种“小发作”往往很频繁，一天多达几十次。每次发作身体都处于缺氧状态，会使得脑细胞持续受损，也会影响宝宝的智力发育。如果发现宝宝有这种“小发作”要及时就医，如果出现倒地“大发作”时更要引起高度重视，及时就医。经过脑部核磁共振、24小时的视频脑电图检查后确诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当宝宝被确诊为癫痫后，家长不要觉得天都塌下来了。而是应该积极和医生一起制定治疗方案，接受规范治疗，减少癫痫发作的次数及频率。因为一次癫痫的发作意味着大脑一次高强度放电，造成数十万甚至上百万脑细胞的死亡。越早干预，对宝宝身心发展的影响越小。张新伟介绍说，接受规范治疗的癫痫患儿是有可能像正常孩子一样学习、工作、结婚、生子的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 癫痫宝宝少逛街避免劳累</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 张新伟介绍，目前癫痫患儿的治疗以药物治疗和手术治疗为主。有三分之一的患儿通过抗癫痫药物治疗就能够很好的控制癫痫的发作。要是2年内没有发作，脑电图正常，就可慢慢减药。不过通常吃药的时间至少要三四年。由于儿童癫痫病程长，需耐心长时间用药治疗。部分家长怕长期用药对患儿身体有损害，一见病情缓解，就自行停药，结果导致病情反复、加重，这种再用药物治疗可能效果就不再敏感了，发展成为顽固性癫痫。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宝宝的癫痫发作如果是药物控制不了的，则需要借助手术治疗了。一种手术是癫痫病灶切除术，有些患儿需要切断两侧大脑半球的联络，以减轻发作时症状从一侧传导至另一侧。另一种手术是迷走神经刺激术。迷走神经刺激器被埋藏在胸部肌肉下并通过金属丝延伸与迷走神经相连。这一装置被植入后，它就会按一定的强度和频度对迷走神经进行刺激。这一装置需要长期放置，适合发作次数频发的患儿。50%的患儿手术后都能有较大程度的症状改善。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对于癫痫宝宝，家长日常照顾也要特别注意，要留心观察，摸索规律，留意避免促成患儿发作的原因，如避免大脑兴奋过度，避免处于缺氧状态（如不要去高原和人多的商业区），避免过度劳累。另外，饮食要规律，暴食暴饮、腹泻都会增加肠胃负担，易诱发癫痫发作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 癫痫发病原因？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 癫痫的病因非常复杂，受遗传、环境等多种因素影响，极少数是遗传的，但大部分受后天因素影响而造成的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 大多数癫痫患儿与妈妈孕早期的身心状态关系最为密切。孕妇怀孕时，工作或生活环境太差，空气憋闷缺氧、或污染严重；怀孕早期，孕妇感冒、发烧等，都可增大宝宝患癫痫的风险。另外，宝宝在子宫里有脐带绕颈、出生时难产，造成缺氧的也都是宝宝患癫痫的致病因素。严重者一出生后就马上发作，轻者3—5岁时才出现第一次癫痫发作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> Tips</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宝宝抽搐家长不要用力压</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 张新伟提醒，宝宝癫痫发作，开始抽搐时，旁人绝对不能用力按压其身体，否则易骨折或造成肌肉损伤。正确的做法是——</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ① 孩子一发作，马上上前扶住他，然后慢慢地让其顺势倒下，避免摔伤引起脑出血。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ② 趁宝宝嘴巴未紧闭之前，迅速拿些东西塞进病人嘴里（不管什么东西都行），垫在病人的上下齿之间，防范牙关紧闭时咬破舌头。此时家长不用担心孩子难以呼吸，因为鼻子还是可以呼吸的。</span>
</p>
