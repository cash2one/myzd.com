<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>附属儿童医学中心医疗队援助孟加拉国心脏手术</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 应孟加拉国卫生部之邀，上海交通大学医学院附属上海儿童医学中心组织多科联合医疗七人小组，前往孟加拉国首都达卡(Dhaka)开展小儿先天性心脏病诊治及技术培训。日前，上海儿童医学中心收到了来自孟加拉国卫生部的感谢状。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 7人精锐医疗队 6天完成7台心脏手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 本次医疗援助活动为期6天，上海儿童医学中心心胸外科张海波医师、李志浩医生、心血管内科张玉奇医师、麻醉科白洁医师、体外循环张蔚医师，以及陈雅萍护士组成7人精锐医疗团队，克服艰难环境，成功完成7台复杂的先天性心脏病手术，包括大动脉转位手术、全腔肺静脉与肺动脉连接术、二期法四根治术，并向当地医护人员传授了小儿心脏手术的先进技术。</span><br />
<img src="http://static.mingyizhudao.com/147003822608047" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 先天性心脏病是先天性畸形中最常见的一类，发病率不容小视，占出生活婴的0.4%～1%。先天性心脏病谱系特别广，包括上百种具体分型，有些患者可以同时合并多种畸形，症状千差万别，最轻者可以终身无症状，重者出生即出现严重症状如缺氧、休克甚至夭折。然而先天性心脏病的诊治需要一支从诊断、麻醉、手术以及术后监护全能的医疗团队。上海儿童医学中心心血管专业是我国最早的先天性心脏病诊治团队，2005年亚洲最大的儿童心脏中心大楼建成启用以来，每年手术率超过3000例，其中复杂先天性心脏病手术占50%以上，堪称世界之最，中心还屡次创下国内最小年龄、最低体重的手术纪录，至今无人打破。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为确保此次援助活动顺利进行，世界健康基金会和上海儿童医学中心共同准备了小儿心脏手术器械一套、精细心脏手术器械数把，小儿心脏缝线，人造血管、深静脉穿刺等器材到孟加拉国，为手术做了精密的准备。世界健康基金会中国项目总监徐丽华女士也兼任医护成员参与到医疗工作中。作为一个医疗团队整体，他们展示儿童医学中心团队精神，注重团队建设，善于发挥集体的智慧和力量。</span><br />
<img src="http://static.mingyizhudao.com/147003822763646" title="" alt="demo.jpg"/><br />
<img src="http://static.mingyizhudao.com/147003822942387" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 医疗队员和当地医务人员同台手术，在艰难的医疗环境和条件下实施了多台高难度先天性心脏病手术。李志浩医生、陈雅萍护士与徐丽华女士每天看护病人到深夜，同时还要教导当地的监护室医护人员掌握小儿心脏手术后的监护要点，细到将液体补充及用药剂量以及呼吸机的调整都做了仔细的教导与交班，确保病人平稳、快速恢复。医疗队的医术和医德受到了当地医生的赞扬和肯定，他们评价道：“中国医生总是很努力地工作，他们的工作效率非常高。我们信任中国医生，他们不仅医术非常好，而且对我们非常友好”。患者的父母们也对医疗团队表示敬意和感谢。除完成手术外，医疗队还举办了各种专题讲座，通过培训授课、现场操作讲解，演示等方式，毫无保留地把自己的医术与护理知识传授给当地同行。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 局势动荡 每天乘坐救护车保证安全</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孟加拉湾是飓风经常肆虐沿海地区，内地经常遭受龙卷风袭击，很多地区还是“吃饭靠上树，穿衣一块布，用钱靠救助，说话不算数”的状态。同时，孟加拉国国内的局势很不稳定，罢工时有发生。医疗队在达卡期间经历了长时间的罢工，甚至目睹了爆炸事件的发生，以致于成员们每天只有乘救护车上下班才能保证人身安全。</span><br />
<img src="http://static.mingyizhudao.com/147003823086189" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 另一方面，当地的药械匮乏、卫生条件恶劣。在当地心脏中心，进行心脏手术所必须的医疗条件还不是很完善，手术室内经常漫天飞舞着绿豆蝇，严重影响着手术安全。医护人员除了要拿手术刀，还得挥赶手术室内这些“来之不尽，打之不绝”的不速之客。然而，就是在这样的环境里，医疗队成员从没有因为条件的艰苦而抱怨不满，更没有因此而影响了手术的安全进行。大家心中有着共同的目标和追求：通过医疗援外增进两国友好，为当地人民健康作出一点贡献。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 难得的经历 人生的财富</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这次经历无论得与失、喜与忧，还是感与悟，都是难得的财富。让医疗团队领略到不同民族的文化、不同地域的风情，将让我们学会用不同的视角看待、思考问题。通过短短几天的外援工作，丰富了大家的人生、锻炼了能力、开阔了视野，成为人生财富积累的重要环节，团队成员们在感到自豪的同时也深感责任重大。</span><br />
<img src="http://static.mingyizhudao.com/14700382317154" title="" alt="demo.jpg"/><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 站在第三世界国家或发展中国家的共同立场来看，医疗合作并不像其他领域的交流容易引起外界的关注和重视，但在两国关系中却扮演着极为重要的角色。国际援助通常被视为一种资源的转换，以维护贫穷国家人民的权益、促进经济成长及其基本福利需求为主要目的；而人道援助则更是以尊重人类价值，强调博爱精神，超越种族、国家、宗教的差异，追求人人平等为其宗旨。应该说中国医疗队与这一精神极为契合，团队愿以润物细无声的姿态，坚持不懈、为孟加拉国这个最需要帮助的地方提供无偿的服务。</span>
