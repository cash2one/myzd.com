		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>视网膜母细胞瘤可以采用激光治疗</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">视网膜母细胞瘤(Retinoblastoma , RB) 是儿童时期常见的眼内恶性肿瘤,晚期发现者可能威胁生命。所以，很多家长在得知孩子发生了视网膜母细胞瘤后，从开始的不能接受，到后来的积极寻求解决办法，往往需要经历一个很痛苦的过程。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">毫无疑问，现在对视网膜母细胞瘤的治疗主要是化学治疗，但是化学治疗也只是一种减容治疗，也就是把肿瘤的容积减小，然后再采用诸如冷凝、光凝等治疗，最终才能消灭肿瘤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在中国，由于缺少早期的筛查，早期发现的视网膜母细胞瘤不多，很多这类病人在发现时都已经处在晚期，此时化学治疗等治疗是首选的治疗措施。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">实际上，对于体积较小的肿瘤，很多开始就可以采用冷凝、光凝等治疗，并获得良好的治疗效果。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于有眼底疾病治疗经验的儿童眼科医生，视网膜母细胞瘤的激光光凝治疗并不复杂，甚至孩子越小，光凝治疗越简单。</span><br />
