		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>子宫肌瘤的动脉栓塞治疗（科普）</strong></span> 
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">子宫肌瘤是女性生殖系统最常见的良性肿瘤，来源于子宫平滑肌，通常认为与雌激素、生长激素、黄体酮有关。根据生长部位分为浆膜下肌瘤（15%）、黏膜下肌瘤（21%）及肌壁间肿瘤（62%）。育龄期妇女的发病率在20%-25%。无症状或症状轻微的年轻病人或者接近绝经期的病人，一般无需处理或仅用药物（激素）治疗，但激素治疗可引起内分泌紊乱，停药后肌瘤生长迅速。肌瘤剔除术（腔镜下或剖腹）虽然能保留子宫，但对于较大或数目较多的肌瘤，往往力不从心，且复发率高达20%以上。次全子宫切除可保持阴道解剖及功能上的完整，最大的缺点就是有可能发生宫颈残端癌的可能，并且对于女性心理、生理还是有一定的影响。全子宫切除对病人的心理、生理影响更大。子宫动脉栓塞术治疗子宫肌瘤是近十来年发展起来的新方法（美国前国务卿赖斯就接受过该手术），其原理是将双侧子宫动脉栓塞，使子宫肌瘤缺血坏死，从而使子宫肌瘤缩小或纤维化以达到治疗的目的，同时又可保存子宫。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">该方法适用于育龄期有症状的妇女，包括：（1）月经量过多，尤其是伴有贫血者；（2）子宫肌瘤引起的慢性盆腔、腿部、背部的疼痛，或者有其它不适；（3）子宫肌瘤压迫输尿管和膀胱引起泌尿系统症状（4）病人欲保留子宫和生育功能；（5）子宫肌瘤摘除术后复发。一般来说，肌瘤太小（&lt;3cm）或太大（&gt;10cm）、带蒂浆膜下子宫肌瘤及即将停经的患者不考虑该方法治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">基本的治疗方法是从一侧股动脉处用一根约1mm粗的导管依次插至双侧子宫动脉内，然后用聚乙烯醇（PVA）颗粒（直径300-500微米较为适宜）栓塞即可，一般1小时即可完成手术。还有其它栓塞剂，但不是非常理想。治疗时间以月经结束后1周内为宜。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">多数病人在子宫动脉栓塞后出现栓塞后综合症，包括盆腔疼痛、恶心、呕吐、发热，白细胞计数增高，尤以盆腔疼痛为突出，给予消炎、止痛对症处理即可（消炎痛栓纳肛止痛较好），症状多在3-5日内减轻或者消失。极少数病人可能出现月经量明显减少甚至过早闭经，可以不予处理或服用中药。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">据Ravina报道，200例子宫肌瘤患者采用子宫动脉栓塞术治疗，随访6年，临床成功率为83%，子宫肌瘤体积在3个月之内缩小达50%，1年缩小78%，整个子宫体积在三个月内缩小35%，在6个月内缩小50%，大多数病人在子宫动脉栓塞术后恢复正常月经，并有超过12例病人怀孕。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">基于同样的原理，子宫肌腺症也可采用子宫动脉栓塞术进行治疗。</span> 
</p>
