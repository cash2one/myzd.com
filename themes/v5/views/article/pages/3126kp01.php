			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>不容忽视老年男性“尿流不畅”背后隐藏的疾病</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">邻居家的张老伯已70多岁了，平时喜欢玩麻将，不太注意保养身体，小便时“尿流不畅”有一段时间了，自认为是年纪大，并未在意，近来“尿流不畅”情况有所加重，感到有些紧张，道听途说可能得了前列腺疾病，又怀疑自己是不是患上了“坏毛病”。后来张老伯去了一家三级医院泌尿外科就诊，做了一些检查，确诊为良性前列腺增生症。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “尿流不畅”并不是老年男性一种自然衰老的现象，常常会有一些疾病引起，应该引起足够的重视，及时诊断和治疗，引起“尿流不畅”主要有以下几种疾病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、前列腺增生，前列腺增生是一种老年男性常见病，虽不会威胁生命但却会影响生活质量。排尿困难为最主要的症状，典型表现为尿流变细无力，排尿时间延长，尿末滴沥，同时伴有尿频，夜尿次数增多，有时可伴有血尿、尿路感染，严重者可发生急性尿潴留、肾功能损害，并有可能引发腹股沟疝气、痔疮等。前列腺增生通过临床表现、直肠指检和B超等检查，可以明确诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、前列腺癌，近年来发病率逐年上升，早期可无临床症状，随着疾病进展，可出现尿流缓慢，排尿不尽，直肠指检前列腺有结节，质地坚硬，血清前列腺特异抗原（PSA）升高，可行B超、磁共振和系统前列腺穿刺活组织检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、尿道狭窄，有尿线细，排尿时间延长表现，病情严重患者可发生尿潴留，尿道狭窄一般多有尿道损伤及感染病史，行尿道膀胱造影检查与尿道镜检查，不难诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、神经源性膀胱功能障碍，临床表现与前列腺增生相似，有排尿困难，残余尿量较多，肾积水和肾功能不全，病人常有中枢或周围神经系统损害的病史和体征，如有下肢感觉和运动障碍，会阴部皮肤感觉及肛门括约肌张力减退或消失，尿流动力学检查可以明确诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、尿道结石，典型症状为排尿困难，点滴状排尿，伴尿痛，病情严重者可发生会阴部剧痛及急性尿潴留，尿道结石绝大多数来自肾脏和膀胱。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、尿道肿瘤，主要表现为进行性尿流变细，或排尿困难，有时尿道口有分泌液或鲜红色血液流出，尿道镜检查可明确诊断。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7、真眼包茎，可出现尿流细，排尿不畅表现，有包茎或包皮过长，及慢性包皮炎症史，体格检查容易诊断，包茎最大的危害是积聚的包皮垢，长期慢性刺激诱发阴茎癌。</span>
</p>
