		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>王学浩院士助力扬州健康产业发展</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4月28日上午，2014上海（扬州）国际医学园发展论坛在扬州智谷新闻发布厅举行。论坛由扬州市政府主办，扬州市经济技术开发区、市科协，上海国际医学园区、江苏省苏北人民医院等单位联合承办，200多名来自美国加州大学、中科院药研所、日本国际养老医疗协会等高校和科研院所，以及拜耳、辉瑞、GE医疗、奥林巴斯、联合利华、完美等国内外生物医药、营养保健企业和医学产业PE、VC等金融机构的代表济济一堂，探讨国际健康产业发展大趋势，展望健康医学技术新方向，会商集“医学制造产业、医疗服务、医学教育、金融支撑”于一体的扬州健康医学产业园区建设思路。中国工程院院士、江苏省人民医院肝脏移植中心主任王学浩应邀参加论坛开幕式并做学术报告。扬州市副市长孔令俊、开发区管委会主任季允丰、上海国际医学园区党委副书记周永刚，市科协党组书记、主席王华平等出席论坛。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">开幕式上，孔令俊和周永刚分别为论坛致辞，开发区和上海（扬州）国际医学园分别作健康产业发展战略交流和园区介绍。孔令俊在致辞中表示，目前扬州健康产业基础良好，扬州市政府将全力支持开发区与上海国际医学园区的合作，积极打造健康产业的集聚区、示范区。周永刚在致辞中表示，上海（扬州）国际医学园的发展思路是集两家所长，谋错位发展，将推动双方相得益彰、实现共赢。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/117/6.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">图为王学浩院士做学术报告</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">主题演讲环节中，王学浩院士以“中国肝脏移植现状与挑战”为题做学术报告。王院士围绕DCD是拓展供肝来源主要渠道，活体肝移植仍然是拓展供肝来源的重要方法，拓展供体来源的新探索，肝癌肝移植的思考，小肝综合征与移植免疫研究等五个方面展开论述。他指出，肝移植外科技术已日趋成熟，肝移植免疫排斥不是主要问题，主要挑战仍然是供肝来源匮乏问题。他强调，DCD是缓解供肝来源问题的重要研究方向，但是仍然存在诸多问题，它的发展任重而道远，需要医学界乃至全社会的关注。他呼吁要重视与肝移植相关基础应用研究，提高受者长期生存率和生活质量。最后，王学浩表示发展健康产业与扬州宜居城市建设是相当契合的，上海国际医学园与扬州的合作起点高，特色鲜明，好好发展将大有作为。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/117/7.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">现场嘉宾进行圆桌讨论</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">拜耳公司、GE医疗集团等企业负责人分别围绕“全球发展方向及中国策略”、“携手扬州共创美好明天”等先后发表主题演讲。现场嘉宾围绕“探讨大健康产业发展趋势，扬州如何抢占发展先机”为主题开展圆桌讨论。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">上海（扬州）国际医学园由扬州经济技术开发区与上海国际医学园区（集团）有限公司于今年2月正式签约合作，总体目标是打造以医学和健康为特色的亚洲一流的国际医学中心，按照规划将用七年时间，完成2平方公里核心区开发任务，总投资200亿元，完成建筑面积300万平米，形成300亿产值、50亿税收的康复医学产业规模。</span>
