			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“脉诊验孕”折射中西医隔阂</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不久前，一场“脉诊验孕”约战把中医脉诊推到聚光灯下，引来各方七嘴八舌。11月5日，国家中医药管理局专门就此表态：约战毫无意义，又招来各方热议。一番热闹过后，人们应该对此事进行冷静反思。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 微博约战激起千层浪</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 事件始于一则微博。9月11日，微博名人、北京积水潭医院烧伤科医师“烧伤超人阿宝”在微博发帖，提议随机盲法测试，验证中医号脉验孕准确性。在得到中、西医网友回应后，9月13日，他在微博向全国三甲医院副高及以上职称的中医发出《挑战中医脉诊大师公告》，称愿出奖金5万元，测试中医脉诊准确率是否能超过80%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 该微博进一步引起网友关注。随后，“赌注”加码至10万元，北京中医药大学教师杨桢应战，阿宝则委托媒体人王志安全权办理此事。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 10月11日，该事件经媒体报道后引发热议，一些知名网站也发起了关于“脉诊验孕”挑战的民意调查。还未敲定挑战的具体方案，10月13日，成都老中医吕即来加入战局，豪掷20万元摆擂，希望在癌症、肿瘤等多个方面与西医对决。其间，约战双方多次隔空喊话、反复协商。11月3日，挑战组织方对外发布挑战赛实施方案，并随后被媒体跟踪报道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 面对这一发酵多日，并愈演愈烈的挑战和随之而来的舆论热议，11月5日，国家中医药管理局正式给出官方意见：这场约战毫无意义。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “挑战结果并不能改变什么”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “中医诊断要求望闻问切、四诊合参，脉诊仅仅是中医诊疗技术之一。”对于国家中医药管理局的这一观点，多位受访中、西医专家都表示赞同。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中国中医科学院广安门医院呼吸科医生王蕾说：“我当时不太关注。原因就是在于约战本身并无意义。”王蕾说，中医诊断方法很多，连普通人都知道至少包括望闻问切，诊断要四诊合参，且脉诊本身也内容丰富。不管方案如何设计，结果如何，也只能说明某医生个人在脉诊验孕方面的准确率，不能代表脉诊的准确率和水平，更无从代表中医药的治疗效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 首都医科大学北京同仁医院外科专家梁存河也表示，虽然不了解中医，但当遇到现代医学暂时解决不了的病症时，他也会建议患者去看中医，多一个机会总是好的。“这场挑战的结果并不能改变什么。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 双方都不代表各自群体</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 广安门中医院呼吸科主任李光熙曾在美国梅奥诊所学习、工作。在他看来，此次事件只是极端个案，双方都不代表各自群体，因此也就不存在中西医打擂台的说法。但抛开挑战本身，中西医双方都应该看到的是，两个医学体系之间在相互尊重的大背景下，也确实存在需要消除的隔阂。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “有些西医不信任中医，我甚至是有点理解的。”李光熙坦言，自己在初入大学接触中医时，也有相同的想法，“中医阴阳五行、君臣佐使、理法方药，第一想法就是‘这都是些什么封建糟粕啊’。”李光熙笑称，这也是好多刚接触中医者的普遍印象。但随着学成体系，并在临床确见其效，这种想法逐渐消失。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 李光熙表示，近年来中医科研发展迅速，国内外不少科学研究在一步步证实中医的有效性。“我们学习了解现代医学，不对西医妄加恶评。也希望个别人能在抨击中医之前，先试着了解中医。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 应客观看待中西医长短</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 采访中，梁存河分析了一些西医同行对中医印象不佳的客观原因。一方面，人们经常会看到有些中医宣称“包治百病”，西医认为这种“无所不能”本身就是不客观的表现；另一方面，不少西医在临床中会碰到患者在中医处没有得到规范、准确的诊疗，贻误救治。“比如我们外科常见的阑尾炎、疝气等疾病，西医诊断明确、治疗精准，完全可以纯西医治疗。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 李光熙表示，确实有人利用中医的“神秘感”过分夸大疗效，但这与个人医德有关。而对于梁存河的后一种说法，李光熙表示，中医确需认清自身局限，认可西医在诊断、外科治疗等多方面的优势，要有所为有所不为；同时，西医应与中医多交流沟通，了解中医更多长处。“在美国，越来越多的高层次学术会议邀请中医专家参与交流。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 让人欣慰的是，事件发生期间，两个医学体系在某些领域都表现出了令人赞赏的姿态。10月29日，在一次应对埃博拉和登革热的中西医学术研讨会上，一位中医专家提出“在这时西医就没有办法了”，在场西医专家并未当场否定，而是在与中医专家沟通后私下向记者纠正：“不是西医没有办法，只是尚无法进行抗病毒治疗。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 11月6日，最早应战的中医杨桢也发出《别了，阿宝志安》的休战声明，称“中西医彼此之间的互相尊重是主流，更重要的是发展良性竞争符合大众的利益和福祉”。</span>
</p>
