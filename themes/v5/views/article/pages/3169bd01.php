	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>肿瘤压迫脑干致偏瘫 湘雅专家妙手切除一周后恢复正常行走</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">央广网长沙1月25日消息（记者傅蕾）脑干是人的生命中枢，如果这个中枢被肿瘤纠缠，生命便岌岌可危，66岁的赵奶奶正是因患上脑干脑膜瘤而不幸偏瘫。经湖南省颅底外科与神经肿瘤研究中心副主任、中南大学湘雅医院神经外科副主任、博士生导师刘庆教授团队成功实施手术，赵奶奶术后一周就能下床走路。1月15日，赵奶奶在家人的搀扶下开心地走出了医院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">去年8月，赵奶奶因有些头痛和肩膀痛，以为是患上了肩周炎，到张家界当地医院进行检查。然而，不幸的是，检查发现赵奶奶颅内长了一个脑膜瘤，并且是在危险的“生命禁区”脑干。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">原来，脑干是生命的中枢，几乎管辖着生命的一切。赵奶奶的肿瘤正好挤压了脑干延髓，而延髓是控制人体呼吸、心跳的神经中枢，大脑也是通过延髓传导到脊髓，控制手脚运动。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">据悉，颅底中央区脑膜瘤是颅底神经外科技术难度最大、最有代表性的手术之一，死亡率和致残率非常高。轻度的脑干刺激或损伤都可能造成患者瘫痪、呼吸及心跳改变，手术风险极大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由于手术风险太大，当地医院建议赵奶奶进行保守治疗。然而10月开始，赵奶奶开始慢慢出现了偏瘫，先是右侧身体不能动，之后左侧也开始偏瘫了，慢慢地就卧床不起，平时行动也只能坐轮椅。看着赵奶奶情况一天天恶化，家人心急如焚，即使冒着风险也希望能通过手术治疗。去年12月底，家人终于慕名辗转找到了刘庆教授就诊。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">刘庆教授介绍，赵奶奶颅内的肿瘤位于颅底深处，严重压迫延髓，周围密布重要神经、血管，显露及切除非常困难。为了保证对正常脑组织的损伤最小，尽量不过多牵拉脑组织，只能沿着脑组织的自然间隙进去，因此术前需要进行非常复杂的入路设计。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“肿瘤周围还被密集的‘神经网’紧紧包围，手术只能通过‘神经网’仅几毫米的间隙进行，避免对神经造成损伤。我们最担心的就是后组颅神经受影响引起咽反射迟钝，从而造成吞咽困难，甚至导致误吸或窒息死亡。”刘庆教授表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">更为棘手的是，肿瘤不仅压迫了脑干，更是把双侧椎动脉都包绕进去了，而椎动脉分支的重要功能就是给控制生命中枢的脑干，以及控制运动平衡的小脑供血。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“在分离肿瘤的过程中，可能出现血管痉挛，影响脑干和小脑的供血，从而影响脑干功能，这种影响可能导致术后灾难性的后果。可能出现比术前更严重的偏瘫，或是无法自主呼吸需要呼吸机维持，甚至成为植物人。”刘庆教授表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">然而，赵奶奶的严重的病情已刻不容缓，她和家人都迫切希望能够抓住手术这一线机会，重新站起来恢复正常生活。经过充分的术前准备，1月4日，刘庆教授、袁健博士团队为赵奶奶进行手术，小心地将血运丰富的脑膜瘤分块切除，术中出血量很少，不到3个小时就将顺利地将肿瘤切除干净。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">术后第2天，赵奶奶的肌力就明显改善了，并且没有出现呼吸、吞咽困难等并发症。赵奶奶醒来正好看见查房的刘庆教授等医护人员，她不禁开心地竖起大拇指说：“你们真棒！” 一个星期后，赵奶奶第一次在家人的搀扶下站了起来，高兴地在病房里迈出了卧床2个月以来的第一步。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“从传统的神经外科手术，到显微、微创手术，再到现在的‘无创’手术，我们希望能把手术的创伤降到最小，这就需要精细化程度更高的手术水平，才能保证这么好的手术效果。”刘庆教授表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">据悉，中南大学湘雅医院神经外科在湖南省神经外科主任委员袁贤瑞教授的带领下，自上世纪80年代末即开始开展颅底中央区肿瘤手术的研究，积累了丰富的经验，手术效果优于国际上同期报道的大宗病例研究结果。近年来，袁贤瑞教授和刘庆教授率领的团队，保持了这一领域的领先优势，进一步优化了复杂颅底中央区肿瘤的手术方案和手术技术，积极开展海绵窦区肿瘤、颈静脉孔区肿瘤等手术新技术，致力于以“无创”理念切除复杂难治性颅底肿瘤，实现了颅底、脑干肿瘤手术无“禁区”，且进一步提高了肿瘤全切除率，并发症少，死亡率降到0.5%以下。</span>
</p>
