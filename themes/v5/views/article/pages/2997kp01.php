		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>食管癌、贲门癌术后宣教</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1． &nbsp;心态：经过外科手术等治疗措施，食管癌、贲门癌是完全有可能治愈的，因此您对自己的病情和治疗期间的副反应要有正确的认识，务必保持乐观开朗的情绪，坚信自己一定能够战胜疾病。只有调整心态，树立信心，积极配合治疗，才能调动身体内部的抗病机制，消极悲观对康复是非常不利的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2． &nbsp;饮食：出院后可继续半流质饮食，如藕粉、蒸蛋、麦片粥、大米粥、烂糊面等，逐渐由稀变稠，术后一个月左右可过渡到软食乃至正常饮食。注意少食多餐，根据需要每天可进餐5-8顿，进食时要细嚼慢咽。不要忌口，各种食物只要是清淡、新鲜、富于营养、易于消化的都可以吃，不吃辛辣刺激和不易消化的食物，禁烟酒。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3． &nbsp;体位：不要躺着进食，饭后不要马上平卧，可适当散步约30分钟后再睡觉，睡觉时可将上半身垫高30度，尽量朝向手术的一侧睡觉。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4． &nbsp;不适：如果您有返酸、易饱胀、呛咳等不适感，您不必紧张，因为切除了贲门，加上胃肠排空功能减弱，所以胃肠内的食物和胃液有时会反流到食管引起不适，经过上述的饮食和体位的调整措施后，一般可以缓解，如仍不能缓解，您可以服用一些药物如奥美拉唑、吗叮林等加以控制。如果您有腹泻症状，往往与手术后胃肠功能紊乱有关，除了注意食物要清洁以外，应避免进食油腻食物，以免加重腹泻症状，经过饮食调理后，如仍不能控制腹泻，可服用一些止泻药物。如果您感觉手术伤口有针刺样疼痛和麻木感，与手术时切断了胸壁的神经有关，您要耐心，数月后，这种不适感才会慢慢消退。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5． &nbsp;随访：您应坚持长期定期随访。术后两年内每三～四月复查一次，之后每半年复查一次，至第五年后可延长至每年复查一次。医生会给您复查胸片、胸部CT、腹部B超等，根据需要还可能行全身骨扫描、磁共振等其它检查。如果您还有任何其它问题，随时欢迎您来咨询。我们胸外科普通门诊时间：每周一～六上午都有；徐松涛专家门诊时间：周二上午和周三上午。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">6． &nbsp;化疗：如果您需要接受术后化疗，一般于术后一月左右开始。化疗前半小时可注射止吐药物减少胃肠道反应。化疗一般需每月重复1次，视情况可能需要2-6次。每次化疗前应验血查血常规和肝、肾功能，若白细胞&lt;3500或肝肾功能异常，则应暂时中止化疗。每次化疗后间隔3～4天应复查血常规，至少查4次，如有异常随时复诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">7． &nbsp;放疗：如果医生告诉您需要接受放疗，您应听从放疗科医生的安排，每日坚持放疗，一般于术后3～4周开始，疗程大约需要2-6周时间。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">8． &nbsp;中药：您可以在放、化疗的同时服用中药，包括中成药和中草药。建议您在有经验的中医师的指导下用药，不要随便服用一些所谓的秘方或偏方，以免毒副作用的危害。请您注意，所谓中药没有副作用的说法是完全错误的。必要时，您也可以在医生的指导下应用一些免疫调节药物和生物制品如干扰素等，以增强您机体内的抗癌机制。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">9． &nbsp;滋补：您可以服用一些保健品来加快恢复，提高免疫力，减轻放、化疗的毒副作用。但请您注意，目前保健品市场较为混乱，请您不要轻信一些不法厂商的不实宣传，警惕上当受骗。如果您需要服用某些保健品，最好事先征求一下医生的意见。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">10. 工作：因手术创伤较大，术后常辅以化疗或放疗，需要一段时间的休养和恢复，待这些治疗结束，再休息2～3个月，可视体质情况逐步恢复工作，一般可以胜任除较重体力劳动以外的任何工作。</span><br />
