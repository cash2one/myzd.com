</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海交通大学附属第九人民医院骨科是集临床、科研与教学为一体的、在国内外享有较高声望的上海市关节外科临床医学中心，先后入选上海市医学领先学科（关节外科）、上海市教委重点学科、国家211工程重点学科建设计划。为硕士、博士学位予点和博士后流动站。学科附设上海交通大学骨与关节研究中心、上海交大医学内植物工程合研究所、关节镜培训中心、成份组织库及《医用生物力学》杂志编辑部等。科主任朱振安教授长期从事骨科医、教、研工作，主攻关节外科及老年骨质疏松症的防治。在人工关节置换、关节重建技术方面积累了丰富经验。每年实施数百例人工髋关节置换术，成功率极高，在国内外享有很高声望。欢迎患者前来就诊，我们将以最好的医疗服务质量为您解除痛苦。</span>
