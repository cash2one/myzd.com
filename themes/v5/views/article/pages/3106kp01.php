<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我需要接受人工髋关节置换术么？</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人工关节置换术已成为治疗严重关节疾患的首选方法，并在很多国家得到广泛应用，目前全世界每年进行的人工关节置换手术约180万-200万例。受多种因素限制，随着我国国民经济的发展和人民生活水平的提高，人工关节置换手术病例正在不断增加。据估计今后将以每年约15％-20%的速率增长，可达10万例/年。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是人工关节置换术在骨科领域属于高级别手术，要求手术医生对关节的生理、解剖、病理以及相关疾病有深刻的理解和认识，对人工关节的材料学、力学、设计理念和操作器械掌握熟练，同时还要具有高超的手术技巧。对于一名医生来说，需要10年以上的骨科和关节外科临床培养和学习，才能够很好的完成人工关节置换手术。而目前我国由于各种原因所限，关节外科医师的培训尚不完善，不可随意开展关节置换术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于患者来说，首先想要了解的是是否需要接受人工髋关节置换术，我们总结具体的情况包括：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp; 股骨头缺血性（无菌性）坏死</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. &nbsp; 先天性髋关节发育不良（脱位）或由于儿童时期各种原因造成的髋关节发育畸形</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. &nbsp; 髋关节退行性变（骨关节炎、骨质增生）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. &nbsp; 创伤骨折：髋臼或股骨头粉碎性骨折、</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">老年人股骨颈骨折、粗隆间骨折</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5. &nbsp; 感染性疾病：化脓性关节炎、髋关节结核等，需要感染控制，病灶稳定后再进行置换</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6. &nbsp; 髋关节类风湿性关节炎</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7. &nbsp; 强直性脊椎炎患者累及髋关节</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8. &nbsp; 滑膜性疾病：滑膜软骨瘤病、色素沉着绒毛结节性滑膜炎</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">9. &nbsp; 肿瘤：髋关节骨肿瘤及周围的肿瘤侵犯髋关节</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">过去认为年轻患者不应进行关节置换术，但随着人工关节材料、设计理念、手术技术的飞速发展，年龄已不再是衡量手术与否的严格标准。总的来说，如果你想获得一个无痛、有功能和稳定的髋关节，而且符合这些条件：①发育成熟，②髋关节疾病发展到了比较严重的程度，③疼痛或功能障碍影响生活和工作，④没有其他更好的治疗方法，都可以接受人工髋关节置换术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
