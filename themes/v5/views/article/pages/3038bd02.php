			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>以身试镜 大框眼镜 当心诳你眼睛</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大框眼镜比一般眼镜宽，镜片的光学中心点之间的距离也随之增宽，造成瞳孔与镜片中心对焦偏差</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如今，眼镜已不仅是矫正视力的工具，还成为年轻人装扮自己的时尚利器。在春熙路，随处可见售卖大框镜架的小饰品店。不近视的买副镜架修饰自己，近视的人买完镜架后，再去眼镜店里配副镜片。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">然而，这种大框近视眼镜在带来时尚感的同时，也在悄悄损害佩戴者的健康。成都商报记者近日采访专家并试验发现，大框近视眼镜的佩戴者容易感到视力模糊、视觉疲劳、头晕，严重的可导致斜视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病例</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">戴大框眼镜 头晕恶心</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今年25岁的袁女士最近觉得浑身不对劲儿：眼睛干涩、视力模糊、头晕恶心……到医院一查，眼科医生告诉她，病根就在她所戴的大框眼镜上。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原来，看到朋友戴大框眼镜后，觉得时尚又美观的她换下自己常规的近视眼镜，也去配了一副大框眼镜修饰脸型。没想到，大框眼镜戴了没多久，就出现了视力模糊、头晕恶心的症状。在医生的建议下，她又换回了自己原来的眼镜，所有不适的症状逐渐消失了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨日，成都商报记者从华西医院、市三医院了解到，类似袁女士的问题已不是个别现象，两家医院的眼科门诊经常会遇到这种病例。华西医院视光学中心主任刘陇黔解释，人的瞳距是固定的，两个眼睛的瞳孔要与各自镜片的中心在一条轴线上。而大框眼镜比一般眼镜要宽，两个镜片的光学中心点之间的距离也随之增宽。这样，就会造成眼睛瞳孔与镜片中心对焦偏差，形成棱镜效应，导致视力模糊、视疲劳，很容易引起近视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原理</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼镜光学中心须与瞳孔成一线</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">华西医院视光学中心主任刘陇黔告诉记者，人的晶状体是一个凸面镜。当光线射入晶状体时，从瞳孔经过的光线不会发生折射，直接落到视网膜上；而从晶状体其他位置经过的光线，会发生折射。晶状体对光线的折射起到的是聚拢作用。这样，光线最终落到视网膜上成像。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人近视后为什么看东西模糊呢？刘陇黔解释，人近视后，晶状体会被拉长，对光线的聚拢作用减弱。当平行光线经过晶状体，光线发生折射后所成的像就在视网膜之前，所以人看东西会模糊。近视镜片属于凹面镜。佩戴近视眼镜相当于光线先经过凹面镜发散，再经过晶状体，让光线折射后落在视网膜上，使近视的人看清楚。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刘陇黔强调，无论如何，眼镜的光学中心和瞳孔（也就是晶状体的光学中心）必须在一条轴线上，这样才能保证佩戴眼镜后能看清楚。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">试验</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现场佩戴</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">镜片光学中心偏6.5毫米</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在春熙路一家饰品店，摆放着很多大框装饰镜架。成都商报记者挑选了一副镜架后，来到玉沙路一家眼镜店，为大框镜架配镜片。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼镜店的老板表示，大框眼镜可以配镜片，只是刚开始戴会觉得头疼，习惯了就好了。这位老板告诉记者，不少年轻人到她店里配大框眼镜，“你还好，才200多度，（我给）近视几百到一千度的都配过，只是要降低度数。”配制完毕后，成都商报记者带上大框眼镜，感觉一阵眩晕。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为了更清楚地看到光学中心偏离的距离，医生让记者戴上大框眼镜，用激光笔照射瞳孔，用黑色笔在眼镜片上标注出了瞳孔在眼镜片上的落点。成都商报记者发现，瞳孔在镜片上的落点，明显靠上，而镜片的光学中心则明显向下、向外。测量发现，二者的距离为6.5毫米。</span><br />
