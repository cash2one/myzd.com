			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>程琳：癌症的认识及防治</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">记者：程琳博士您好！感谢您接受我们的采访。您特别指出了城市化进程与癌症的关系，同时中国癌症统计调查也显示，城市癌症率明显高于农村，背后的原因是什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：首先需要说明一点，中国的登记制度不是非常完善，在城市统计可能做的更好一些。在农村，因为中国太大了，很多偏远的农村可能就统计不到。但可以确认的一点，工业化、城市化进程同癌症发病率提升是有关系的。这跟城市生活方式有关系。比如说工业化污染，比如城市中生活节奏加快，人们的生活压力更大，等等。过去曾有人提出“城市五大癌症”的说法，这五大癌症分别指肺癌、乳腺癌、大肠癌、上消化道癌和肝癌。是有一定道理的。从现在的临床看，恐怕还要再加上一个前列腺癌。因为人们生存周期在延长，得前列腺癌的几率也会增加。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中国这些年的工业化进程，在某种程度上也在重复西方发达国家的老路。我们在统计上也能看到很多城市癌症发病率是在提高的。癌症发病率基本上和是经济发展带来的污染指数平行提高的。还有就是生活质量的提高。比如乳腺癌和大肠癌，肯定是跟脂肪摄入量提高有关的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：据统计，癌症发布率从年龄段上看，40岁以上年龄组发病率快速升高，这说明40岁以上人群成为癌症的“高危人群”，您认为主要原因是什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：其实过去我们都认为癌症是一个是“老年病”，通常都是 50岁以后的人得。20年以前，如果我们医院接收到一个三四十岁人的癌症患者，这都很罕见的。癌症是一个慢性病，它可能潜伏很长时间，中年以后接受到外界因素的刺激，引起细胞基因突变。所以这里也提醒广大中年人要特别注意癌症的预防。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：事实上，近年来癌症正在不断“低龄化”。2012年10月，《齐鲁晚报》曾报道过一例2岁婴儿体内检查出癌细胞的消息，该报道同时指出，80后、90后的癌症病人正逐渐增多。从您诊治的情况看是这样吗？这些患癌症的低龄化人群具有怎样的特质呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳： 我没有看过这个报道，不知道里边的具体指的什么癌。因为癌症是一个泛称，它包括各种年龄段易发生的癌症。比如白血病、视网膜母细胞瘤，这些癌症就是发生在小孩身上的情况多。所以并不奇怪。如果小孩身上检查出了肺癌、肝癌，这才是奇怪的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 不过癌症低龄化确实是值得注意的因素。我们现在几乎每个月都能接收到一个30岁左右的癌症患者，这在以前是不常见的。有时候甚至有20岁左右的大学生。根据我们的观察，这些患癌症的年轻人在得癌症之前都有较大的心理创伤。比如青春期抑郁症。我前段时间还遇到过一个家庭关系不和谐，是单亲家庭。还有人有家庭遗传史，家里有三四个亲属得了癌症。这些因素都和癌症低龄化有一定关系。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：最近媒体对于癌症的报道非常多。有媒体援引数据说，中国每分钟有6人被诊断为癌症，有5人死于癌症，人们一生中患癌概率为22%。癌症是否真的距离我们如此接近？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：如果说每分钟6个人患癌症，这个数字是靠谱的。毕竟中国十几亿人，人口基数大，换算成这个数字并不夸张。不过如果说人一生中患癌症几率为22%，每5个人就有一个得癌症，肯定是夸大其词，这可能和换算方法有关系。因为刚才说过，癌症是一个泛称，它包括各种肿瘤，从白血病、肝癌、前列腺癌等。种类非常多。但癌症发病率提高是不争的事实，我们不能忽视。特别是癌症需要早发现早治疗，越早治疗，越好治疗，花的钱也少，效果也更好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：我们知道情绪对于癌症有很大影响，社会上有“癌症性格”的说法，是说具有抑郁性格的人容易得癌症，据您分析，这种癌症性格是指什么？是否有科学依据？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：这个是从心理学上讲的，心理学把人分为ABCD四种类型的性格，A型追求完美，有事业心，对自己紧迫，这种人患心血管、高血压疾病的几率就比较大。你刚学说的“癌症性格”通常指C型，这种人内敛，没有宣泄渠道，生活处处取悦别人，就是通常我们所说的“老好人”，这种人有了不痛快会埋在心底。那这样也会对免疫系统有影响，造成你的免疫力下降。同时这种人常常会有较大的心理负担，可能会引发癌症。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 但对于这种人我不提倡去贴标签，生活中“老好人”性格类型的人我们很容易分辨出来，这种人就一定会得癌症吗？不尽然。我在临床中也遇到性格开朗的癌症病人。所以完全归结于性格是不全面的。特别是我们还说“江山易改本性难移”似乎性格是改变不了的，其实也不是，人的心态和思维模式是可以根据自己的意志调整的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 所以我不同意“癌症性格”这种说法，这会加重C型人格的人的心理压力，我们可以说这个人内向、外向，但贴上这样的标签，是很不好的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：根据英国癌症研究会1月29日的统计报告说，英国男性死于癌症的几率比女性高35%以上。如果将前列腺癌、睾丸癌和卵巢癌等性别相关性很高的癌症排除在外，英国男性死于癌症的几率要比女性高出67%。我们目前没有找到中国这方面最新的权威调查，据您的行医经验来讲，中国的情况是怎样的？这说明怎么的问题？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：从临床上看，男性患癌症的几率比女性高。男性癌症患者的死亡率也比女性高。从全世界看都是这样的。包括世界人的平均年龄，男性也比女性低。中国也是这样。这应该从两个方面来看，一是工作环境，比如在户外、在污染环境中工作的男性要比女性多，他们接受的不良环境的几率比女性高；二是生活环境，男性更容易沾染上吸烟、喝酒等不良生活习惯。所以患癌症的几率也高。此外，男性担负的生活压力更高，这也是重要因素。这也要求男性在个人健康方面更要注意。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：我们知道，中医和西医是两个完全不同的知识体系、技术体系，而中日友好医院将中西医结合起来治疗癌症，这是怎样实现的？效果如何？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：中医侧重整体，它更像一种文化或说哲学。而西医注重个体、局部以及微观世界。我们认为中医治疗癌症可以是必要的辅助。像我们医院的肿瘤科大夫就是中医出身，并受过西医的培训，根据辨症施治，效果还是不错的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 癌症我们现在更多把它视为一种慢性病。我们认为在慢性病上，中医可能更擅长，比如慢性咳嗽，西医办法就不太多，中医就比较好的效果。还有像脑血脂，中医针灸治疗也有比较好的效果。中西医是可以结合治疗的。现在一些医院的广告，中医的完全排斥西医，西医的完全排斥中医，其实都是不全面的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 记者：关于癌症的防治，您对于广大网友有什么具体的建议？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 程琳：癌症治疗，越早期发现越好治，所以我建议大家应该每年做一次体检。特别是40岁以上的人群，要更加关心自己的健康。癌症预防方面，我们目前没有找到明确致病根源，但影响因素是明确的，吸烟、喝酒都是重要的影响因素。饮食方式，不要吃太多荤腥的东西，要经常摄入绿色蔬菜、粗纤维类的食品。另外也要注意不良情绪的管理，保持健康、向上、快乐的心态。</span><br />
