			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>角膜屈光手术后干眼的预防及治疗</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">角膜屈光手术相关干眼的主要原因：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp; &nbsp; &nbsp; 术前干眼危险因素的影响.</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高达38％的近视者在术前即有干眼症状，LASIK术后更易出现严重干眼，且术前患有干眼会增加LASIK术中和术后发生并发症的危险，包括角膜上皮缺损、上皮内生、感染和屈光回退等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. &nbsp; &nbsp; &nbsp; 手术因素对干眼的影响</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多次和过量使用表面麻醉剂可对角膜上皮和基质细胞产生毒性，引起上皮缺损、弥漫性板层角膜炎和上皮内生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">LASIK术后1～2周干眼的发生率高达69％~85％，LASIK手术所使用的微角膜刀或飞秒激光切断角膜前基质内感觉神经丛，术后早期 (3～6 个月)角膜知觉减退，因此导致瞬目次数减少、泪液蒸发增多，角膜形态改变影响泪膜附着及稳定性。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. &nbsp;术后引起干眼的原因</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术后常规使用的糖皮质激素和抗生素眼药水大多含有防腐剂，在术后干眼状态下，防腐剂可产生对角膜上皮的毒性作用。配戴隐形眼镜、眼表面细胞损伤、泪液减少或成分变化也会导致干眼症的出现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. 其他影响因素：患者术前长期配戴隐形眼镜，年龄偏大及生活在气候干燥的环境、长期从事电脑工作等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">角膜屈光手术后干眼的主要特点：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术后1～6个月（个别为1年或以上），经常出现眼睛干涩、易疲劳、视力波动等症状，尤其在过度用眼之后更为严重。这些症状可随术后时间延长而逐渐减轻；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">结膜轻度充血，下方角膜或角膜瓣弥漫细点状染色；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">客观检查指标：泪液分泌试验及泪膜破裂时间异常，至术后3~6个月逐渐恢复至术前程度。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">角膜屈光手术后干眼的治疗：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. 眼局部点各种人工泪液，包括透明质酸钠、羟丙基甲基纤维素、羧甲基纤维素纳等，因为防腐剂可以造成或加重眼球表面的损伤，需长期使用时建议使用不含防腐剂的人工泪液。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 泪小管塞栓封闭术：通过部分封闭泪液排出管道，以增加眼球表面起润滑作用的自然泪液。阻塞泪小管就好比用塞子塞住了眼泪流出的“下水道”，能最大限度地在眼内保存自然泪液。对于单纯因手术导致的干眼，可选用半年可降解的临时性泪小管塞子。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">角膜屈光手术后干眼的预防：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 &nbsp;对于术前就存在干眼的患者，应该在手术之前就开始进行治疗，如热敷、使用人工泪液等，在角膜上皮修复后再进行手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2 &nbsp;避免眼球长时间在空气中暴露，看电视、电脑或阅读时，不要长时间瞪着眼，要不断眨眨眼，让眼皮上的泪液润滑一下眼球。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3 &nbsp;要改变长时间连续用眼的习惯，看书、看报、看电视、看电脑或做细致活，每隔1小时左右，要休息5~10分钟，或闭目养神，或眺望远处。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4 &nbsp;生活要有规律，早睡早起，不要熬夜，保证充分的睡眠，防止眼睛过度疲劳。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5 &nbsp;不要将化妆时使用的眼影粉、睫毛膏等弄入眼内，以免对眼睛产生刺激作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6 &nbsp;极个别人残留远视眼、近视眼、散光等，要戴合适度数的眼镜矫正，但不宜戴隐形眼镜，若必须戴隐形眼镜时，时间也不可过长。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7 &nbsp;若发生眼睛干涩不适，看东西模糊不清时，要到医院检查治疗。在医生指导下进行治疗，切莫乱用眼药水点眼，以免加重病情。</span><br />
