			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>为什么我国的恶性肿瘤治愈率那么低？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据统计，我国2010年新发恶性肿瘤309万例，当年因肿瘤死亡196万人，占所有死亡的27%而成为头号死亡原因。我国肿瘤的治愈率远低于美国，为什么这样？难道是我国治疗肿瘤的水平就那么差吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有人说肿瘤病人的死亡，1/3是吓死的，1/3是治死的，另外1/3才是肿瘤致死的。这是无稽之谈。以现在的医学水平，有些肿瘤是完全可以治愈的，有的则无法治愈。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">哪些肿瘤是可以治愈的？只要肿瘤处于早期，通过正确的外科治疗，绝大部分可以治愈。早期肿瘤一般没有任何症状或不舒服，如果不去主动做体检的话就根本发现不了。肿瘤一旦出现症状或不舒服才诊断出来，多半已到了晚期。绝大部分晚期恶性肿瘤在当前的医学水平之下是无法治愈的，尽管最近发明很多的新药和新疗法如靶向药物治疗、生物免疫疗法,除了可能减少一些痛苦，可能延长一点生存时间以外，根本无法改变晚期恶性肿瘤的进程，有些所谓的新技术和新疗法根本就是徒劳的，甚至还可能增加痛苦和加速死亡。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我国恶性肿瘤病人在诊断和开始治疗时大部分已到了晚期，所以总体治疗效果很差。例如中山大学肿瘤防治中心统计了2014年1-6月份在该该中心就诊的新诊断的肿瘤病人，发现早期病人只占38.80%（其中I期为16.50%，II期为22.30%），另外61.20%的病人肿瘤到了晚期（III期（即局部晚期）为29.80%，IV期（最晚期）为31.40%）。作为我国综合实力最强的肿瘤专科医院的病人情况尚且如此，全国的情况就可想而知了。仅从这个统计数据，我们就不难理解为什么我国恶性肿瘤的治疗效果差了，因为发现的时候大部分已是晚期了，到了晚期才治疗，那是回天乏术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如何才能改变这种状态？需要多方面的努力，国家、行业和个人三个层面一起努力才能改变这种局面，首先是国家医疗保障体系上要制定宏观政策，责成各种医疗或健康保险要将肿瘤早期筛查或体检纳入报销范围，媒体和医疗机构等要大力宣传健康体检理念，医疗行业要建立起一整套针对不同患癌风险人群的肿瘤体检或筛查的体系并提供足够的服务。由于肿瘤高发年龄一般在40-50岁后，因此40岁后应每年定期体检，每个公民应以对国家和家庭高度负责的态度认真对待自己的健康检查。如能这样，绝大部分恶性肿瘤可以早期发现、早期诊断和早期治疗，绝大部分可以治愈，而且也可以节约大量经费和医疗资源。花在治疗一个晚期恶性肿瘤病人身上的资源一般可治愈10个人早期肿瘤病人，有时甚至可治愈100个早期病人。这是多么大的财富节省啊！</span><br />
