	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>牟巨伟：胸腔镜手术敢“吃螃蟹”的人</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2014年6月27日凌晨2:34分，中国医学科学院肿瘤医院胸外科副主任牟巨伟教授用手机发了4张刚刚结束的手术照片，图片下面短短的一行字：VATS左肺下叶袖切成功。图片中的他，和同事们面对镜头，带着满意而又疲惫的笑容，这是当天他和同事们完成的第六台手术。</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/461/1.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">VATS，学名叫电视辅助胸腔镜手术。上世纪90年代初期， VATS开始应用于胸内疾病的诊断和治疗。随着医疗技术，特别是胸部肿瘤规范化诊治成果在全国的推广应用和国内外腔镜器械与微创技术的不断发展完善，近五六年，这项技术在中国医学科学院肿瘤医院胸外科获得了飞速的发展。目前全科近一半的手术都是通过腔镜完成的。而在这项技术的应用与推广中，牟巨伟教授自称“被幸运撞了一下腰”，先后完成了肿瘤医院胸外科第一例胸腔镜下左全肺切除、第一例支气管袖式切除、第一例解剖性肺段切除和第一例胸腹全腔镜食管癌切除食管胃颈部吻合术。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“水瓶座”，喜欢迎挑战</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">别看现在身为医科院肿瘤医院综合实力最强的科室之一——胸外科的副主任，牟巨伟教授最初的人生理想可是和医生没一点关系。1983年，他毫无思想准备地一脚踏进了中国医科大学的校门，1988年毕业时，他已成为全校成绩最优异的学生之一，来到了医科院肿瘤医院。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">选择肿瘤专业，他早有自己的打算：相比较综合医院，肿瘤医院的急诊量非常低，低年住院医生不至于过于疲于奔命，也就有了更多的时间可以专注地看专业书籍和文献，利于在执业之初就打好扎实的理论基础，并有机会在科室主任指导下参与基础和临床研究。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">而选择胸外科，则来自于东北汉子那满满的豪情：谁都知道，胸外科手术是所有手术中风险最大的手术之一，手术难度大，压力大，手术时间长，围手术期管理复杂，“绝对是个男人的工作。”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">胸腔镜的出现很大程度上改变了胸外科疾病传统外科治疗的理念，被认为是 20 世纪末胸外科手术技术的最重要进展。与传统开胸手术相比较，电视胸腔镜手术具有创伤小、术后疼痛轻、恢复快、住院时间短、符合美容要求等优点。尤其适用于年老体弱、心肺功能不良病人的胸外科手术，从而扩大了胸部疾病的手术范围和适应证。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">早在上世纪90年代末，现任院长赫捷院士，时任胸外科主任，留美学成归来就率先成功完成了中国医学科学院肿瘤医院第一例胸腔镜肺楔形切除术、肺叶切除术和胸壁肿瘤切除术。但他很快意识到，这项新技术的影像系统和手术器械还需要手术医生和生产厂家长时间的沟通与完善，特别是在国内肿瘤规范化诊治理念和技术尚未普及推广的特殊历史时期，盲目在胸部肿瘤外科治疗中推广应用胸腔镜微创手术势必以牺牲手术安全性尤其是根治性为代价，因而他高瞻远瞩的适时主导了胸部肿瘤规范化诊治理念与技术在我国的应用推广，这无疑在更高的层面为近年来胸腔镜微创技术在胸部肿瘤外科治疗的普及推广奠定了基础。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">之后10余年来，随着腔镜器械日益完善，规范化诊治理念的深入人心，胸腔镜微创技术越来越成为胸部肿瘤外科治疗的发展趋势。作为医院综合实力最强的科室之一，腔镜技术的应用推广已是水到渠成。谁将成为此时第一个吃螃蟹的幸运儿呢?</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“我喜欢挑战。”牟教授说。不管是读书，做研究还是外科手术，他都喜欢做一些有一定难度和挑战性的工作，对此他有足够的勇气、胆识和耐心。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">谈及动因，他狡黠地一笑：“因为我是典型的水瓶座。”</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/461/2.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在洛杉矶Cedar Sinai Medical Center手术室与Mekenna教授合影</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“幸运儿”，完成四个第一</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">谈及幸运的完成了肿瘤医院胸腔镜手术的四个第一，牟教授总是将其归功于胸外科几代人创建发展的良好平台和上级医生多年来无私帮带下练就的扎实外科基本功，更得益于赫院长的悉心指导和鼓励，特别是给予美国、新加坡和香港等地多次难得的培训学习机会。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">其实对于已熟练掌握胸部肿瘤开放手术技术的医生而言，腔镜手术最难的，不是技术，而是视觉习惯、思维习惯和操作习惯的重新建立。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">对于这点，牟教授有个精辟的比喻：“你开左舵车习惯了，现在让你去英国或者香港开右舵车，车还是会开走，一开始难免会觉得别扭，但是只要有足够的耐心适应一段时间后一样会驾轻就熟。”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">话虽如此，但镜下操作远比开车要求高，稍不留神，术中就可能发生各种意外。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">视觉习惯从开胸直视到看显示屏影像，还不是最难的。最要命的是，术中出现的问题尤其是大出血能否及时有效的控制。胸外科手术是肿瘤外科最凶险的手术之一，因为胸腔里有心脏、肺和纵隔大血管等脏器，所以出血是难免的。开放手术中，不论是止血钳还是手指按压，都能迅速止血，但镜下操作，手进不去，万一出血严重，很快就会镜头模糊，医生就像盲人一样了。所以牟大夫说，“此时最紧张的，是来自于心理的压力，而危急时刻术者的心里素质和定力往往决定着手术的成败和病人命运的转归。”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">他还清楚记得成功完成第一例胸腹全腔镜食管癌手术的日期：2009年3月19日。胸中上段食管癌传统术式又称三切口手术，是胸外科最复杂最耗时的手术。而胸腹全腔镜手术同样要经过胸腔镜游离食管、腹腔镜游离胃、食管和胃颈部吻合3个步骤。现在科里同事都做熟了，四五个小时就可以结束手术，但那毕竟是院内第一例，他从早9点做到晚7点，整整做了10个小时。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">业内人士都知道，高手的手术，其实都是风平浪静的，这源于术前方方面面的充分准备和术中一丝不苟的精细操作。这个“第一例”，没如人们想象那样充满了戏剧冲突，甚至让人有点小小失望，但牟教授还是将其归功于幸运。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">从他成了第一个“吃螃蟹”的幸运儿之后，胸腹腔镜技术迅速在肿瘤医院胸外科普及并日益规范成熟，短短几年时间，微创手术量已跃升为全国第一。</span>
</p>
