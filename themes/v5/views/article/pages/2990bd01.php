		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>北京协和医院第一批国家医疗队4月21日晚赴川（资讯）</strong></span>
</p>
<p>
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;<img src="http://static.mingyizhudao.com/147003585818051" title="" alt="demo.jpg"/> 4月21日下午5：30，北京协和医院骨科林进教授、基本外科刘跃武教授、重症医学科隆云副教授从医院出发到首都机场，乘当晚8:30的航班前往四川雅安地震灾区参加重症伤员救治工作。赵玉沛院长、姜玉新书记和于晓初副院长亲自送队员出发。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 国家卫生和计划生育委员会医政司王羽司长代表委领导前往机场为医疗专家组送行。王羽司长转达了委领导对医疗队员的感谢和慰问，希望医疗队员发扬救死扶伤的人道主义精神，保持召之即来、来之能战、战之能胜的作风和特别能吃苦、特别能战斗的优良传统，出色完成医疗救治任务。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 据悉，这是国家卫生和计划生育委员会选派的第二批国家医疗专家组。16名医疗专家分别从北京、上海和江苏出发赶赴成都，协助开展地震重症伤员的医疗救治工作。该批医疗专家的专业涉及重症医学科、骨科、神经外科、普通外科、胸外科、感染科、肾内科、小儿骨科和康复科等。（记者 郑文婷）</span><br />
