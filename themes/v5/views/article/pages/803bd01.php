			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>在成为整形外科医生之前你应该知道的10件事</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一位美国女性整形外科医生在网上发布了一篇文章(13 Things I Wish I Knew Before I Became a Plastic Surgeon)，还原了一名整形外科医生的工作和责任，医美圈编辑对此进行了整合并邀浙江省人民医院整形外科主任吴溯帆教授进行点评。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、他人眼中整形外科医生的工作似乎非常“有趣”，然而正式成为一名整形外科医生的道路并不容易，在此之前需要接受一系列及其枯燥的培训。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：医生本来就是一个辛苦的职业，其中外科医生更辛苦，这在国内外都一样。在国内，医科大学毕业后就可以做整形外科医生，但在美国，毕业后通过几年的实习后只能获得普通外科医生的资格，此后还要再经过3~5年的专科培训，考核通过后才能成为一名整形外科医生，所以在美国成为一名专科医生难度更大。通常美国一位有执照的整形外科医生年龄约在38~39岁左右。美国的“住院”医师真是“住在医院”里的，穿着手术室的衣裤连续几天值班不能回家是常态，这样的高强度训练出来的医生也是千锤百炼的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、即便身在好莱坞，为明星做整形治疗的情况也是少之又少。整形手术中并没有所谓的“典型”患者，更多的是不同年龄的普通人，需求也是多种多样的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：明星本来就少，就算做整形美容也是小概率事件。即便有也是去找一些有名的高年资医生，而且为了隐私，大家也会注意保密。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、经营自己的诊所有很多优势，但同时也将带来一系列挑战，你甚至需要亲自花时间完成所有的文书工作。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：挑战是必然的。美国医生在拿到医师执照后，通常先在公立医院接受锻炼，在40-45岁时“羽翼丰满”，各方面成熟了，才会出来开业，这也要求医生有更加健康的身体。一般美国医生年收入，普通公立医院整形外科医生约30-50万美元，开业医生则在百万美元以上，收益远超过公立医院，但付出的劳动自然也是不一样的。公立医院医生每周工作5天，一天工作8小时，但是自己开业的医生，一周工作6天是常态，甚至可能无休，而且不可能8小时后准时下班。对于开业医生，除了专业上必须精通，还要求懂交际、情商高，同时还要管理财务并领导诊所的员工。在美国开业还比较“单纯”，诊所不能做广告宣传，中国的开业医生面临的挑战可能更多，有各种行政规章制度，还要做广告宣传，并且面临来自民营医院的竞争等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、手术时间只占你和患者接触中的一小部分，更多的时间需要花在术前沟通咨询和术后的随访交流上。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：医患关系原本就不仅仅是纯医学的，人与人之间的交流是双向的，整形与其他专业又不一样，往往以外形的满意与否来评判，并没有客观指标，这只有一个标准，就是求美者是否满意。所以术前的沟通很重要，审美观每个人都不一样，因此术前必须认真沟通、签字、拍照。患者往往术前没有那么认真地看自己的五官，而术后天天照镜子。我碰到过一个案例，患者做的是眼整形手术，然而术后对方表示自己的鼻子歪了，患者并非“闹事”，是确实认为自己的鼻子歪了，于是我们把术前的照片调出来看，发现术前鼻子就是歪的。我们整形美容患者多看外表，术前说得再多，没有图像告知他/她，患者术后还是不认可，病人接受自己变得漂亮的速度是惊人的，只需要几分钟，但是更多的注意力会放在“寻找缺点”上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">五、你需要在患者“想要的”和实际上“最适合”他/她的之间做出平衡。分析患者的心理，确保对方没有不切实际的期望，否则必须果断拒绝并建议先由心理医生做咨询。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：有研究统计过，整形美容求美者中，至少有一半的人在心理上或多或少有些异常，但并非严重的问题，例如过分追求完美、以自我为中心等。如果发现求美者“想要的”效果实际上是做不到的，那么就需要果断告知并拒绝草率从事。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">六、总有患者对整形结果不满意，此时不必过分介意，你需要做的就是别忘记拍摄专业的术前术后对比照片，并与患者充分沟通，必要时，提供整形后一定期限内免费修复服务。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：处理不满意患者的经验也是需要日积月累的，年轻医生经验缺乏，往往一下子就“慌了神”，资历更深的医生则能够缓和面对。确实是在执业过程中经常会碰到不满意的患者，对方也会很懊恼和愤怒，当然在国内医患矛盾尖锐的当前，要看患者是什么样的人，然后再采取相应的应对方法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">七、有时候不得不为其他医生（非医生）的错误“埋单”，许多患者贪图便宜甚至找没有资质的人员接受整形手术，随后产生并发症，有一部分工作就是为这些患者进行修复。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：国内的主要问题是，很多操作微整形的人并不是医生，这属于“非法行医”范畴。对于真正医生同行之间，其实还是要互相帮助，不能“拆台”，很多医生碰到外院手术失败前来治疗的患者，讲话不注意会促使医患矛盾的产生。其实整形美容的操作是个“手工活儿”，没有人能保证每一个病人都成功，每个人都有不成功的病人去别的医生处再就诊，此时不能火上浇油，有的病人已经很生气了，这也是作为医生的一个基本操守。应该帮患者做些缓冲和开导，通常医生资历越高，反而越会帮别的医生说话。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">八、许多人难以相信为自己整形的主刀医生是一位女性。尽管医学院女性占半数，选择整形外科专业的很少。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：在中国，医科大学本科生的男女比例是2:1，可目前已经达到1:1，到研究生阶段则女生更多，所以今后可能医生有一半是女医生。我个人认为整形外科还是比较适合女医生从事的，女性对漂亮有更多的理解，同时知道如何跟同性别的求美者交往沟通。自己开业做小型美容手术，作为女性非常有优势，对于乳房、生殖器私密手术等，患者和女医生沟通往往更加轻松自在，我们科室来看生殖器整形的全部都由女医生来负责，女医生在治疗女性患者方面非常有优势。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">九、你的形象将影响你作为一名整形外科医生的个人品牌。因此，关注自己的容貌并注意日常着装，对自己进行形象管理很重要。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆：在公立医院只要头发梳整齐，衣帽整洁，穿上白大褂即可，因为公立医院来看病的病人并没有那么挑剔。可如果自己经营诊所，那么一个不修边幅的医生与一个每天整整齐齐的医生相比，患者的信任度肯定是不一样的。但也不必过分讲究高档甚至奢华，作为医生，干干净净很重要，通常开业医生更需要注重外表和着装。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">十、整形医生的工作比大众想象中更多，除了整形美容，急诊的创伤患者治疗、唇腭裂患儿修复、乳腺癌后乳房重建都是工作范畴。你的工作不仅仅在改变人的外貌，很多时候你改变了对方的人生。当你给患者带来更美的容颜，他们将更加自信并迎来更好的生活。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴溯帆： 整形外科本身从业范围就有两方面，一方面是“雪中送炭”，做修残补缺；另一方面是“锦上添花”，把正常人变漂亮。在我刚工作时还是前者较多，而从2000年开始，比例开始倒置，修残补缺的人数其实没变，但是需要锦上添花的人越来越多了，现在的倒置程度是8:2，10个病人中有8个是要“变漂亮”的，而对于自己开诊所的医生而言，可能就是9.9:0.1了，因为外伤和畸形通常都在公立医院就诊。所以很多老百姓以为整形医生就是给明星做整形，赚大钱。其实，整形外科医生也做畸形患者的修复重建，也给急诊创伤患者做救死扶伤，这些工作对患者心理以及人生机遇的改变是巨大的，因为他有异于正常人，会受到歧视，我们把他做到和正常人一样，对于患者回归社会起到很大作用，这是整形外科医生的职责所在。</span><br />
