	<span style="line-height:1.5;font-size:18px;"><strong>乳腺癌为什么需要化疗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>为什么乳腺癌患者要做化疗？</strong></span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">因为化疗可以预防癌症的复发和转移，特别是淋巴结有转移的、分级差的、恶性度偏高的乳腺癌患者，容易复发和转移，所以需要做化疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">但很多患者担心，化疗对免疫力有影响，而且化疗的副作用让人难以忍受，对化疗的恐惧远远超过对手术的恐惧，这是不对的。目前有非常明确的证据表明（50年的乳腺癌术后随访研究），做化疗的患者比不做化疗的患者生存时间明显延长了。虽然短暂的化疗期间，患者的生活质量确实会受到影响，化疗的不良反应会导致患者恶心、呕吐、脱发等等，但从长远看，做化疗防止了可能出现的复发和转移，获益远大于这些不良反应，这种付出是值得的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">乳腺癌是一种全身性疾病，作为乳腺癌治疗的重要手段——化疗，在某种程度上不亚于手术的作用，它是一种非常重要的全身性的治疗手段。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>但是不是所有的乳腺癌患者都需要术后化疗呢？</strong></span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">答案是否定的，并不是每个乳腺癌患者都需要化疗，只有具备化疗指征（比如有淋巴结转移、恶性程度高、分化差等情况，具体患者需要具体分析）的病人需要做：第一，必须要有可靠的病理检查结果，免疫组化检查结果是完整的，根据这个结果判断患者是不是要做化疗；第二，需要检查患者的肝肾功能、血常规、尿常规、心电图等，某些特殊的化疗可能还需要对患者的心脏功能做进一步的评估，需要做超声心动检查等等。符合这些条件的患者，才可以进行化疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">确定患者能做化疗后，医生会根据患者的具体情况，个性化地制定化疗方案（化疗方案因人而异，不会完全一致）。化疗方案不同，治疗的周期也会不同：比如常用的表阿霉素联合环磷酰胺的方案一般是4个疗程，间隔一般是3周；如果表阿霉素联合环磷酰胺、紫杉醇方案，一般有8个疗程，但疗程间的时间间隔可能会不同——普通化疗一般间隔3周，密集化疗一般间隔2周，这样一来，都是8个疗程，密集化疗的时间就会明显缩短。选择常规化疗或密集化疗是个专业性问题，应由医生根据病情和患者身体情况综合决定。</span><br />
