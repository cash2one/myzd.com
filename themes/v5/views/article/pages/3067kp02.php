			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>运动创伤治疗，康复很重要</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着人们健身意识的增强，体育锻炼已经成为我人民广泛接受的防病治病特别是预防慢性病的重要手段。但是，运动创伤往往使许多热衷于锻炼的无法正常进行训练。发生运动创伤的原因很多，除了一些意外因素致伤外，运动创伤的发生是有规律的，绝大多数运动创伤可以用“三多”来表示，那就是“慢性伤多、小创伤多、积累伤多”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">创伤手术治疗 &nbsp;避免过度积极干预</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">运动创伤医生在决定病人是否需要手术时，往往是以受伤部位是否影响运动功能为判断标准的。由于手术的肢体在术后个阶段内功能肯定受到影响，尽管有非常好的康复措施，仍会影响患者的锻炼，有些患者术后甚至永远不可能达到原有的功能水平。因此，运动创伤即使发生，只要不影响运动能力，运动医学医生也主张立即采取比较激进的手术方法来解决，而是通过康复手段尽量维持病人的运动能力，直到病人必须手术时才会考虑用手术来解决问题。从这个意义上讲，就要特别强调早期处理运动创伤，不要使小创伤积累到大创伤，更不要把运动创伤拖成一个慢性伤。有病早治，早干预，早康复，实际就是预防大的创伤发生，避免过多的手术干预。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，即使如此，总还是有不少病人由于种种原因不得不接受接受手术治疗。尽管手术可能不大，又采用微创的手术方法，但暂时性的功能丧失还是会出现的，这就是运动创伤病人康复必须解决的问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">围手术期 康复介入最佳时机</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">运动创伤的病人经过手术后，需要进一步打针吃药的情况极少，而全面恢复病人的运动能力上升到首位。康复手段不仅能让病人在接受医疗干预过程中最大限度保持身体其他部位的最佳功能状态，同时又能让接受手术或其他治疗的肢体部位尽快恢复功能，回到未受伤前的机能状态，很快地投入到体育锻炼中去。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">实际上，运动创伤发生部位主要在四肢，特别是关节部位。据统计，膝关节损伤占到所有运动创伤的50%左右，其他关节如肩、踝、肘、髋、腕关节等部位也有相当的发病率。这些关节部位损伤后对康复的要求非常高，需要在康复医师和康复师的指导下进行严格的训练和治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">运动创伤围手术期是康复切入的最好时机。由于患者多数是年富力强时受伤，因此在无论从心理和生理方面都不适应。康复师在这个阶段要帮助患者适应手术后的肢体不便，比如怎样扶拐行走，怎么照顾自己的生活，也包括怎么锻炼没有受伤部位的功能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于运动创伤病人来说，康复内容包括两部分：一是维持全身机能最佳状态，二是逐渐恢复受伤部位的功能。在对手术部位进行功能康复的时候，必须根据医嘱循序渐进，既不可操之过急，又不能听之任之。临床常见到一部位病人由于怕疼，手术后不活动，导致患肢功能丧失；也有一些病人术后过早活动，导致手术效果受到影响。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">运动创伤恢复 &nbsp;需要解决三大问题</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对受伤部位的康复，主要针对以下三个问题。这三个问题解决了，患肢基本功能就可以满足继续运动的要求。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第一是要恢复患肢的活动度。无论那个部位受伤，是否手术，术后有没有采用外固定，进入康复期的一个重要训练内容都是恢复活动范围。如果是关节部位，应该恢复到与对侧关节活动范围基本一致。手术过程中的出血，术后外固定等因素易造成关节粘连。因此，术后一段时间内关节活动度的恢复是康复的一重要内容。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第二是必须恢复关节周围肌群的力量，因为手术及外固定本身可以导致关节周围肌群的废用性萎缩，这对术后关节功能影响很大。由于关节周围的肌群不仅可以使关节正常活动起来以满足肢体活动的需要，同时也是稳定关节的重要结构。如果肌群萎缩太厉害，关节也不能满足运动的需要，非常容易导致再次受伤。一般来说，关节周围肌群的力量随着康复程序进行会逐渐加强，需要达到对侧相对应肌群70%的肌力以上，才可以逐渐开始恢复训练。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第三是在康复过程中要加强本体感觉的训练。由于损伤可能导致本体感觉功能减退，而手术后本感觉作为深感受又不能迅速恢复正常，需要在恢复过程中不断加强本体感觉的训练，以保证日后在体育锻炼中不至于由于本体感觉丧失而再受伤。这在一些关节韧带损伤病人的康复过程中尤其重要。</span><br />
