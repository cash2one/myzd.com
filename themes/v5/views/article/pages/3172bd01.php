<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">湖南省医学会泌尿外科学专业委员会主委、中南大学首届“湘雅名医”、湘雅医院泌尿外科主任齐琳教授获评中国泌尿外科界最高荣誉——“2015年吴阶平泌尿外科医学奖”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3172/2.png" title="" alt="demo.jpg"/><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">湖南省医学会泌尿外科学专业委员会主委、中南大学首届“湘雅名医”、湘雅医院泌尿外科主任齐琳教授获评中国泌尿外科界最高荣誉——“2015年吴阶平泌尿外科医学奖”。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 记者近日从吴阶平医学基金会获悉，湖南省医学会泌尿外科学专业委员会主委、中南大学首届“湘雅名医”、湘雅医院泌尿外科主任齐琳教授获评中国泌尿外科界最
高荣誉——“2015年吴阶平泌尿外科医学奖”。据悉，这是我省首位获此殊荣的专家，此举充分体现了中南大学湘雅医院乃至全省泌尿外科的学术水平和影响
力。2016年3月，在北京举行的纪念吴阶平院士学术活动中将为获奖者进行盛大的颁奖仪式。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 著名医学科学家吴阶平院士是我国泌尿外科的开拓者之一，由吴阶平医学基金会于2006年设立的“吴阶平医学奖”，是中国医药卫生领域最高级别的个人奖项。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 为了促进中国泌尿外科医学事业的发展、推动泌尿外科医学科学技术进步，根据《国家科学技术奖励条例》和科技部颁发的《社会力量设立科学技术奖励管理办
法》，经吴阶平医学基金会与中华医学会泌尿外科学分会商定，自2014年开始，“吴阶平泌尿外科医学奖”正式纳入科技部批准设立的“吴阶平医学奖”序列。
该奖项主要奖励在泌尿外科临床、教学、科研工作中做出突出贡献、取得显著成就的个人。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据了解，“吴阶平泌尿外科医学奖”每年颁发一次，每次颁发2人(如经终审未评出获奖者，可以空缺)。在今年8月徐州召开的2015年”吴阶平泌尿外科医学
奖“评审委员会终审会上，经由中华医学会泌尿外科学分会本届副主委以上专家及部分老专家组成的评审委员会进行无记名投票，湖南省医学会泌尿外科学专业委员
会主委、中南大学首届“湘雅名医”、湘雅医院泌尿外科主任齐琳教授等2人以高票获此殊荣。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 来自全国各地的知名专家纷纷对齐琳教授获此殊荣表示祝贺，并认为齐教授近年来在相关领域做出的卓越成绩是有目共睹的，由其带领的学科知名度不断提升，在行业内树立了典范，因而此番获奖乃实至名归。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 长期从事微创泌尿外科及泌尿男生殖系统肿瘤基础与临床研究的齐琳教授，先后主持和指导国家级科研项目10余项(国自8项)，获得省部级等奖项18项，共发
表论文180余篇(第一作者或通讯作者SCI论文30篇)。2012年，以齐琳教授为学科带头人的泌尿外科还获得了国家临床重点专科。此外，由齐琳教授首
次提出的利用GnRH-PRP对恶性肿瘤实行靶向的新治疗思路，成果发表在Cancer research及Urologucal 
Research等权威杂志上，被国际公认并多次引用。不仅如此，齐琳教授一直致力于泌尿生殖系肿瘤的规范化治疗，并且大力推广泌尿生殖系肿瘤的规范化诊
断及腔镜治疗技术。由他开展的腹腔镜下“中央型肾癌肾部分切除术”、“保留性神经的膀胱全切术”、“保留性神经的前列腺癌根治术”、“腹膜后淋巴结清扫
术”等疑难手术，填补了省内多项技术空白，并且在全国各地学术会议上进行现场手术演示。迄今为止，齐琳教授主刀实施了泌尿生殖系肿瘤的腔镜手术1000余
台，拥有丰富的腹腔镜泌尿外科手术病例的治疗经验，是国内较早开展泌尿生殖系肿瘤微创治疗和相关基础性研究的专家之一。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 多年来的工作成绩能够受到国内同仁们的广泛认可，齐琳教授对此感到十分的欣慰。他说，这项荣誉虽然是以奖励个人成就为主，但其中包含了很多前辈对我的悉心
培养以及我们团队的共同努力。其承载了大家对我的认同和鼓励，但更多的是对我今后工作的鞭策。吴老所弘扬的精神是“精湛的医术、高尚的医德、艺术的服
务”，我和我的团队将倍加努力，继承和发扬吴老的精神，为中国泌尿外科事业更上一级台阶，以及能更加全面优质地服务好患者，做出更大的努力和贡献。</span><br />
