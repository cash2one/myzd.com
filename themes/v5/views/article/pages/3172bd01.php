	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 首届中南大学“湘雅名医”，一级主任医师，博士研究生导师。现任卫计委国家临床重点建设专科学术带头人，中华医学会泌尿外科学分会常务委员，中国抗癌协会
泌尿男生殖系肿瘤专业委员会常务委员，湖南省泌尿外科疾病医疗质量控制中心主任，湖南省医学会泌尿外科学专业委员会主任委员，湖南省抗癌协会泌尿男生殖系
肿瘤专业委员会主任委员，《国际泌尿系统杂志》主编，卫计委泌尿外科医师准入专家委员会湖南专家组组长，中南大学泌尿外科研究所所长，湘雅医院泌尿外科主
任、泌尿男生殖系肿瘤亚专科主任。</span>
