			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>你手指甲上涂的颜色很漂亮</strong></span> 
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生有很多去国外医院交流，进修，培训的机会，由于文化差异及中外医疗制度，环境的不同，医生们总会对国外医患之间的相处很有感触，心生向往。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可是思及现实大量的门诊病人，繁重的工作量，不免觉得毫无可能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">真的毫无可能吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">或许，是可以有一些小小改变的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">星期日周刊记者　顾　筝讲述人：周晓兰</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">职业：复旦大学附属华山医院国际医疗中心内科医生</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">去年8月我去美国一家面对社区的医院进修，医院内的医生大部分是全科医生，我跟着他们一起工作。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于是社区性质的医院，所以来看门诊的病人全家可能都是这位医生的病人，彼此之间就像老朋友一样。病人对医生很尊重，而医生和病人说的第一句话并不是问：你有什么不舒服，而是会先拉家常：前段社区里举行了棒球比赛，你们去参加了吗？你家人最近怎么样？你体重控制得如何，睡眠好吗？</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">整个看诊过程让人感觉很和谐，即使医生脸上带着的是职业化的微笑，但作为患者来说，还是能深刻地感觉到，这位医生是很关注我的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有一次我跟着一名医生去病房会诊，那位病人由于病情严重一直卧床，不过医生关注到了一个细节，他对病人说：“你的指甲油颜色很漂亮。你真是一个热爱生活的人。”或许是文化的不同，我们更为内敛，不大会直接夸赞他人，而美国人的表达非常直接，我在那边工作的时候，常常会有同事或志愿者夸赞我：医生，你穿的裙子很漂亮。你今天背的包很好看。每每听到，心里总甜甜的，我想同样的，对于那位病人来说，医生这样一句小小的夸赞和鼓励，很快地拉近了彼此的距离。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">美国看病采用预约制，从客观上来说，医生确实有先天优势，能有很长时间和病人沟通。不过日本和国内情况类似，我2014年在京都大学附属医院进修，医院在市中心，一幢大楼，人很多。我跟着肾内科的医生去查房，查房时，即使是主任，也会在病床前蹲下来，视线水平地注视着病人，或是握着老年病人的手询问一些身体状况，有的医生会坐在病床上，询问病人。我想，面对这样的查房，病人的感觉会不太一样。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，说起来，我们的专业能力不比国外医生差，我们看的病人多，工作量大，临床实践能力很强。说实话，我们国内的医生很不容易，当然，患者也不容易，彼此需要更多的尊重。可现实是，医生很累，病人也不满意，双方都是很疲惫的状态。病人想我排了那么久的队，医生才两三句话，就给我开药了；而医生想，如果我和每个病人多说几句话，这么多的门诊病人，不知道要看到什么时候。我们在国外看到了那样的医患沟通，很有感触，但回到现实中，很多人都觉得不可能实现，后来我回过头想想，真的不可能实现吗？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从医这十年间，我的状态是有点变化的，改变或许是因为有了孩子，外加学了心理学。人在疲累状态中很难有精力去额外关注其它东西，也很难有创造力，只有在放松状态的时候才有可能去更多地关注病人。同样的，只有心里有爱的时候，才会去爱。生了孩子之后，我观察到，孩子总在关注我们习以为常的那些东西，而且不吝惜他们的赞美，这是因为孩子有赤子之心，不带任何价值判断。所以我也渐渐地有了一些转变，我发现病人身上有很多值得我们学习，能让我们成长的品质，比如有的病人很坚强，有的病人面对疾病时有从容的态度……我发现，当我面对病人时耐心地倾听，观察病人的角度不一样，我和他们的关系也会有微妙的变化。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病人来到医院时，对他来说是一个巨大的变化，他们本身内心就很紧张，如果碰到的医生比较冷漠，或者只是关注他的病，而不关注他的感受，那么他会有戒备心理，他会东问西问，去想方设法地寻找别的医生的意见，也会在网上查资料，先来挑战试探你，看你对这方面的医学知识是否了解。在过去，碰到这样的情况，我可能更多地会有无力感，心想：真没劲，因为我不是专科医生，所以你不信任我，开始怀疑我了。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而现在，我的想法会有转变，如果我发现病人质疑我了，或是莫名其妙发脾气了，那我知道这背后是有原因的，我会反省自己，今天有倾听他，共情他吗？如果病人问很多医学专业上的问题，那我也理解，这说明他对自己的健康很关注，他有权利知道这些。我会听听，他想了解什么，如果这部分知识，是我能够和他交流的，那么我们就坐下来好好交流一下，如果超出了我的专业范围，那我也很坦诚地告诉他：对不起，我可以请专科医生来和你交流。这并没有什么，不会减少他们对我的信任，相反，不懂装懂，才是大忌。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以说，医疗大环境或许我没有能力改变，但自己的状态改变了，就会有所改变。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">讲述人：王恩敏</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">职业：复旦大学附属华山医院神经外科主任医师</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">读大学的时候，母亲检查出得了胃癌，那是1980年代，家乡的医疗条件很差，也没有什么好的医生，所以我那时的志向是一定要做一名好医生，能治好令人痛苦的疾病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当了医生后，我分别去英国和美国的医院进修过一段时间，有了很大的感触，我发现，自己之前的志向还只停留在较低层次的理念上。美国医院内的一句宣传语让我印象深刻：好医生治疗疾病，伟大的医生治疗病人。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">英国是全民公费医疗，看病的时候，医生会握着病人的手问：你最近过得怎么样，情况还好吗？他们很轻松地和病人拉家常，倾听病人所述说的情况，然后为他们解决问题。在看病之后，他们会认真地对病人进行随访。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">英国的医院硬件条件并没有那么好，但很注重细节。我在那边的儿童医院看到，在病房的监护仪器旁安装了一个射灯，晚上护士查房时，只需要在门口打开射灯，看到仪器上显示的数字记录下来即可，不会走进房间打扰孩子休息。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在美国，我可以跟随医生一起看门诊。美国医生总会事先询问病人：今天有一位从上海来的医生要和我一起看诊，你是否同意？在得到同意的答复后，我才能进诊室和美国医生一起看诊。看诊时，医生和病人的沟通也非常多，态度很和善。在那边我看到一位90多岁的名医，有一次一个40多年前的病人又把自己的亲戚带来找他看病，老医生还能记得这位病人，还向他询问他们家人的情况。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不要急躁，慢慢来，真正对病人好，他们会感受到的。这是我在国外进修之后很大的感受，同时，我也在可能的情况下吸取了一些经验。现在，我一直坚持着对病人的随访，在随访中我们也积累了大量的经验，能更好地治疗疑难杂症。</span> 
</p>
