	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>眉山市人民医院刘睿当选市二届外科专委会委员</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3033/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">会议现场</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 四川新闻眉山7月21日讯(侯建明 许可)近日，眉山市人民医院承办的“市医学会第二届外科专委会学术年会及专委会委员会议” 在东坡国际大酒店举行，市医学会会长赵平，眉山市人民医院院长王建民及全市相关专业的医务人员100多人参加，该院刘睿被新增为委员。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在专委会委员会上，委员们分别介绍了自己所在医院、科室的发展情况和发展方向，讨论了下一次会议的时间、地点，主办的形式，主要课题等。本次会议还新增选了13名委员，为外科专委会注入了新的血液，我院普外一科主治医师刘睿担任委员。在会议上，参会的委员一致认为，今后外科专委会应该建立自己的qq群和微信群，搭建交流平台，相互学习交流 ，提高我市外科整体发展水平，保障医疗安全，减少差错和事故的纠纷，更好的满足我市百姓健康需求。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3033/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;专委会委员会议</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 眉山市人民医院王建民到会并对本次大会顺利召开表示热烈的祝贺。他说，专委会是各个医院之间相互学习交流的桥梁和平台，通过这个载体，使我们外科界同仁紧密的团结起来，同时，上级医院的专家教授在百忙之中亲临基层，对我们指点江山，传经送道是非常难得的机会，所以希望在座的各位认真聆听专家们的精彩讲演，对我们今后的工作都大有帮助。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">学术年会特邀请了四川省医学会外科专委会胃肠外科学组副组长、华西医院胃肠外科中心主任胡建昆教授，四川省肿瘤医院头颈肿瘤外科副主任医师王少新教授、四川大学华西医院普外肝胆胰外科主任医师、医学博士彭兵教授等省内知名专家分别带来了《胃癌外科治疗进展》、《甲状腺微小癌的外科治疗进展》、《腹腔镜在胰腺手术中的应用》等讲座。各位教授的精彩讲座，使与会人员学习到了外科领域的新进展，并将学到的知识运用到日常工作，提升自身的业务水平。</span><br />
