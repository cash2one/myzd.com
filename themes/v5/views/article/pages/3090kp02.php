			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>耳朵前“仓眼”是怎么回事？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在一些小儿的单耳或双耳的耳前，可以看到一个小眼儿,这就是我们常说的“仓眼”，医学上叫做先天性耳前瘘管。这种病与遗传有关。当然，在有家族病史的人中，有的人也会终身不感染的。如果耳前瘘管反复红肿甚至形成脓肿，就需要手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性耳前瘘管，是由于在胚胎发育期形成耳廓的组织发育不全所引起的。这种病可以单独发生而不伴有其他的耳朵畸形。但是，也有少数人可能同时伴有腭裂，副耳廓、耳廓发育不全，遗传性耳聋等先天性畸形。耳前瘘管一般开口于耳前，轻的仅在耳前有一凹痕；重者瘘管可以有广泛的分支，形成多个盲管甚至可以绕到耳后而造成耳后感染。有少数病人的瘘管开口于耳甲腔、外耳道或乳突皮肤上医学上称之为异位耳瘘。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性耳前瘘管常分泌一种像白色乳酪一样的分泌物，带有臭味。一般说来，患者平时自己不一不易察觉，如有继发感染，可引起局部皮肤红肿、疼痛甚至形成脓肿。如果反复感染破溃，可使瘘口周围的皮肤形成明显的瘢痕，影响美容。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于没有感染的耳前瘘管不必处理。对于继发感染的耳前瘘管，在应用抗生素的同时，还可局部外敷鱼石脂软膏。如有脓肿形成，必须切开引流放出脓液。在感染被控制后，再做手术，切除瘘管。手术应力求一次切除干净，必要时还得切除少许软骨，否则极易引起复发。</span><br />
