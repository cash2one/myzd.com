			<div style="text-align:center;">
	<strong>陆劲松教授：BRCA突变与预防性乳房切除等热点问题解析</strong>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">015年4月10～12日，备受瞩目的“第八届CSCO乳腺癌高峰论坛暨2015北京乳腺癌论坛”在北京隆重召开。在会场上，医脉通有幸邀请到了上海仁济医院的陆劲松教授，就辅助内分泌治疗的卵巢功能抑制问题，及预防性手术切除热点问题发表见解。访谈详情如下：</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医脉通：近期，安吉丽娜•朱莉预防性切除卵巢、输卵管引发争议，此前她曾预防性切除双乳，您认为家族性乳腺癌高危人群是否需要切除乳腺预防乳腺癌？或者他们最佳的外科处理方法是什么？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陆劲松教授：这个问题提的非常好，近期这一问题无论是在医生团队还是在患者群体中都引起了极大的关注。针对家族性乳腺癌高危人群，有没有必要进行预防性乳腺切除？或者同时做卵巢预防性切除？可能还存在一定的争议。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大家都知道，BRCA基因突变以后，患者的乳腺癌和卵巢癌发病率相对普通人群都会明显提高。如果能够提前知道这两个器官未来发生恶性肿瘤的概率非常高，个人认为可以进行预防性切除。然而，关键问题是，我们需要确切地了解某个基因位点的突变能引发乳腺癌的概率是多少，卵巢癌概率是多少。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而实际上，除此之外，还有很多问题需要讨论，比如BRCA1基因的突变可以大幅度提高患乳腺癌和卵巢癌的风险，同时也提高输卵管癌、子宫和宫颈癌、胃癌、肠癌等其他部位风险，这里面同时患上述不同类型癌的患者比例是多少？哪一种癌会在该患者身上发生？还是哪几种癌在该患者身上出现?出现的先后顺序？这些都是未知数，尚不能确定。大样本研究提示该基因突变者虽然乳腺癌、发病风险高达约40--50%，卵巢癌达约6--10%，但同时患乳腺癌和卵巢癌的概率为2－5%，而有约30--40%的人不患癌，那么有无必要同时切除乳房和卵巢，可能对很多人不是必须，所以目前尚不能推广这种手术，因为无法准确界定切除的器官就是那个要癌变的器官。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果在未来，能进一步积累大数据，更深入研究，能明确具体某一个致病突变导致具体那个部位的肿瘤，那么我们就可以建议针对性预防性切除，这也就是最近提出的新概念——“精准医疗”。目前可能还要进一步针对遗传基因进行深入研究。如果将来能够获得更加精准的数据，在未来可能会提倡预防性切除。可喜的是，已有一些报道有了这方面的成绩，明确提示BRCA的某突变更容易患乳腺癌，而另外某区域更容易患卵巢癌，相信精准预测从而精准预防时代终会到来。然而就目前而言，对于家族性乳腺癌高危人群，可能还需要针对不同的个体实施个体化的策略。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">研究详情：BRCA突变类型及位点影响乳腺癌、卵巢癌风险</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医脉通：作为此次St. Gallen会议讨论的热点之一，您认为乳腺癌内分泌治疗是否该加入卵巢功能抑制？哪些患者可能从卵巢功能抑制中获益？该如何辨别他们？&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陆劲松教授：这个问题也是受到患者和医生广泛关注的另一重要课题。可能对这一问题关注的时间更长。从去年TEXT试验和SOFT试验的结果发布之后，乳腺癌内分泌治疗又向前走了一步。但是，目前还缺乏对特定人群的独立样前瞻性研究，一些共识还是建立在亚组分析推理之上。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">卵巢抑制的应用可从三个层面来分析，其一，对于需要辅助化疗而不愿意接受辅助化疗的激素受体阳性的患者，单纯卵巢抑制（OFS）疗效与CMF方案疗效类似，而他莫昔芬加 OFS的疗效好于CMF。因此对于不愿接受化疗，或不能接受化疗的、具有化疗指征的激素受体阳性患者应建议卵巢抑制加他莫昔芬。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其二，对于已接受了辅助化疗的受体阳性患者，这个问题以前没有前瞻性证据确凿表明化疗后他莫昔芬和OFS联合好于单用他莫昔芬，很多混合用药组，尚不能明确建议所有的绝经前乳腺癌受体阳性都使用OFS，但相对大样本联合分析，对于年轻乳腺癌患者加用OFS可能明显改善预后，故一些共识和指南建议年轻患者可以考虑。去年报道的两个研究，SOFT和E-3193研究对这个问题有了进一步的认识，一方面二者同时提示对于总体而言，OFS加他莫昔芬和他莫昔芬的治疗效果没有统计学差别，而SOFT研究亚组分析数据来看，在具有高危风险的患者和化疗后仍处于绝经前的乳腺癌患者，加用卵巢功能抑制可较单药他莫昔芬提高治疗效果；这一点支持以往的共识中的建议，在今年的St. Gallen会议专家投票中也得到多数专家的支持，对于年轻小于35岁患者、有高危复发风险的患者，如分化三级，淋巴结转移多、多基因检测提示高风险者的等支持加用OFS；</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其三，就是如果使用OFS，是同时合用他莫昔芬，还是何用芳香化酶抑制剂，这个问题一直以来困惑着我们，OFS联合阿那曲唑的前瞻性临床试验ABCSG12，不支持二者合用，而TEXT和SOFT试验带来转机，OFS联合依西美坦好于联合他莫昔芬，在本次St. Gallen会议中，大部分专家认为，对于年轻、具有高危复发风险的患者应建议采用内分泌治疗联合OFS，并且更为推荐OFS联合芳香化酶抑制剂，从而提高治疗疗效。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，去年公布的POEMS研究提示，对激素受体阴性的乳腺癌患者使用卵巢功能抑制药物进行卵巢功能保护，可保护患者的卵巢功能、生育能力，使患者具有更好的生活质量，甚至还具有生存的优势。在本次St.Gallen会议中，绝大多数专家支持在接受化疗期间给予卵巢功能抑制药物来保护卵巢功能。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医脉通：感谢陆教授的精彩解答。</span><br />
