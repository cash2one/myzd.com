			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>一个都不能少</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/14701065379901" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在北京大学第三医院心脏外科住过院、接受过手术治疗的患者，往往都有一个共同的感受，就是在这里，时时刻刻都能感受到医生精湛的技术和对患者真真切切的关怀。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “病人把‘心’都交给你了，你怎么能不认真负责、竭尽全力呢？”北京大学第三医院心脏外科主任万峰说，患者进到手术室里，本来就很紧张，所以医生就更不能冷冰冰地对待他们，要让他们感觉到亲切、放心。“在我们心脏外科，绝对不能容忍对患者不耐烦或者态度不好的医生。”他经常教育科里的医生，要把“病人至上”当作核心理念。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据介绍，北京大学第三医院心脏外科是一个建立在全新理念上的高技术专业学科，拥有国内最好的住院环境、最先进的设备及最优秀的医生团队。学科带头人万峰是我国非体外循环冠脉搭桥手术（心脏不停跳搭桥手术）的奠基人，至今已完成一万余例手术，居全国领先地位。在国内外心脏不停跳搭桥手术平均实施率为30%—50%的背景下，万峰率领的团队心脏不停跳搭桥手术平均实施率大于99%，已做到常规进行手术,并在我国建立了该手术外科治疗标准，对推动我国冠心病外科的发展具有重要意义。“损伤小、风险低、恢复快是这个手术的特点，而且越是病情严重的患者受益就越多，特别是老年重症患者。”万峰表示，我们所做的一切努力和尝试，都是为了患者的健康和安全。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在不断提高自身水平的同时，万峰还致力于推广这一技术。他带领团队在全国建立了50多家协作单位的协作网，每年通过举办全国性技术培训班, 将顶级的医疗服务带到基层，推动冠心病外科技术在我国基层单位、特别是在西部地区的快速发展。非体外循环冠脉搭桥手术外科治疗标准的建立及在全国范围内的推广，提高了冠心病外科治疗的水平，使更多患者受益，特别是过去常规冠状动脉搭桥术中，被视为禁忌的患者，如今也重新获得了治疗的机会，而且相对于常规冠脉搭桥术，节省了大量医疗资源。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 万峰认为，手术难不难，关键是看医生的培训和技术是否到位。如果技术熟练的团队，只要是单纯的冠脉搭桥，心脏扩大、血管钙化、心律不齐以及心衰等因素都不是绝对的禁忌症，就连心脏射血分数9%的患者，我们也做成功过。但如果医生水平不过关，病情再单纯的患者也不该做。每个病人都满意，每个手术都成功，是一个医生最大的愿望和成就。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在万峰的带领下，北医三院心脏外科目前在冠心病治疗方面拥有全国领先的八大技术：一是微创非体外循环冠状动脉搭桥（OPCAB）手术的总例数全国领先；二是局部清醒状态下小切口冠状动脉搭桥（MIDCAB）术全国首例；三是急诊冠状动脉搭桥术总例数全国领先；四是非体外循环下二次冠状动脉搭桥（Re-Do OPCAB）术例数全国领先；五是激光心肌打孔血运重建（TMLR）术的总例数全国领先；六是严重左心室功能不全（低射血分数）的冠状动脉搭桥术全国领先；七是微创内窥镜方法获取冠状动脉搭桥血管移植物的总例数全国领先；八是应用“搭桥+激光+干细胞”的“三合一”手术治疗疑难重症冠心病全国第一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于众多成就和荣誉，万峰总说：“这是我们整个团队的功劳，我只是其中的一个小小分子。”但每当面临棘手的病例时，万峰总是冲在最前面。“希望有一天，北京大学第三医院心脏外科能成为国际一流的心脏中心。”万峰对未来充满信心。</span><br />
