			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>北京三博脑科医院成功为患者切除巨大脑瘤</strong></span>
	</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中国网北京11月21日讯（通讯员 张正尤）11月21日上午，来自广州的37岁脑癌患者张勇在北京三博脑科医院术后出院。20天前，该院首席专家石祥恩教授等成功为其切除一个78×75×68毫米的巨大脑胶质母细胞瘤。这是患者第三次做手术，肿瘤体积大，周围囊肿多，难度和风险很大。由于术前准备充分，手术治疗及时，方案制定周密，手术技术高超，手术十分成功。目前，患者身体状况恢复良好，四肢运动正常，可下地自由活动，食欲良好，语言流畅，思路清晰。</span>
</p>
<p>
	<span><span style="line-height:25.454544067382813px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2005年，患者张勇偶发倒地抽搐，以为仅仅是癫痫，自</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">行服药治疗，稍有好转，但好景不长，抽搐次数明显增多，并伴有严重头痛。2007年9月，患者在广州成功做了第一次开颅手术，病理诊断为脑星型细胞瘤Ⅱ级，患者术后无神经功能障碍，生活工作正常。2010年9月，患者脑瘤复发，来到北京成功做了第二次开颅手术，可是病理诊断结果已升级为恶性程度最高的脑胶质母细胞瘤，但是也正常生活了1年。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2011年9月，患者脑瘤再次复发，头疼难忍，每天依靠多次输入药物来维持生命。10月，患者病情进一步加剧，眼珠像被绳子拉住往外拽一样，8月至10月，先后4次癫痫大发作，头部和四肢抽搐，疼痛剧烈，异常痛苦难受。患者慕名来到北京一家全国知名大医院就诊，经专家诊断，脑瘤一处复发且体积很大，达78×75×68毫米，另一处转移，也有30×31×20毫米。专家认为手术风险很大，患者可能下不了手术台，或者成植物人，或者肢体瘫痪。专家考虑到患者病情高度危重，家在广州，路途遥远，建议尽快回家维持生命，否则可能回家途中发生不测。患者和家人感到非常绝望和痛苦，抱头痛哭。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">天有不测风云，人有旦夕祸福。11月2日上午，患者家人已经订好当天下午的飞机票，准备带其回家。此时，意外惊喜地得知我国著名神经外科专家王忠诚院士的博士、北京三博脑科医院首席专家石祥恩教授手术高超。患者家人抱着最后一线希望，立即找到石祥恩教授，他当即表示急诊入院，尽快手术，尽一切力量救治。</span>
</p>
<p>
	<span><span style="line-height:25.454544067382813px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 当日18时45分至23时15分，石祥恩教授与医师周忠清、王震及护士楚景涛、麻醉师穆锋连夜为患者施行手术，经过4个半小时的紧张手术，成功为患者切除一个78×75×68毫米的巨大脑胶质母细胞瘤。术后数小时，手术医师周忠清对患者家属说：“患者肢体活动正常，比我们预想的效果还要好，请放心！”</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术后，患者家人给石祥恩教授送去5000元钱，表示感谢。后来，患者家人发现该款被全部打入患者住院押金，让患者家人万分感动。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">出院当天，患者妻子陈海燕给该院院长张阳写来一封长达3页2000余字的感谢信，信中字字句句情真意切，感人肺腑，催人泪下，她高度称赞该院医务人员精湛的医术、高尚的医德和救命之恩。患者及其家人还给石祥恩教授及全体医护人员赠送一面锦旗，上书“医德高尚令人颂 医术精湛除病痛”。</span>
</p>
<p>
	<span><span style="line-height:25.454544067382813px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据石祥恩教授介绍，11月8日的核磁共振成像显示，患者目前颅内还有一个30×31×23毫米肿瘤，近期需做第四次手术切除，术后并需进行放化疗，才能进一步延长生存期。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者表示，先回家短暂休养，再视情手术和放化疗。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">11月21日上午，患者出院前，其家人将感谢信亲自送到北京三博脑科医院院长张阳办公室，亲手交给张阳院长。张阳院长表示，非常感谢患者及其家人的信任和褒扬，为患者服务好是我们应尽的职责。</span>
</p>
