<div style="text-align:center;">
	<span style="font-size:18px;line-height:2;font-family:'Microsoft YaHei';"><strong>鼻整形相关问题（原创）</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼻整形就是隆鼻吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊碰到很多求美者来要求做鼻子，可是就是不知道要做什么，如何做？而是问医生需要做什么，有的就只是要求隆鼻，如果接诊大夫也没有正确的审美观，就按求美者要求做隆鼻，可事实上可能并没有达到理想的效果。其实，鼻整形远非隆鼻手术那么简单，专业做鼻整形的专家可能会给出合适的建议，只有双方都达成一致认识方可施行手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么样的鼻子最美？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">适合自己的才是最美。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼻子审美如同人体其他部位审美一样，没有绝对统一的答案，所有的美学标准都是相对的，流动的，随着时代变迁、地域的变化、文化的差异而有所改变。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，鼻整形手术一定要根据自己的鼻形、脸型、自身条件、职业特点、性格爱好、文化背景等因素综合考量，当然还要考虑医学发展水平的限制。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般来说，美的鼻子特点高而挺，轮廓分明，比例匀称，与面型的比例搭配也很合理，肉感的脸型者，鼻子也相对圆润的，骨感的脸型者，鼻子的轮廓感也更强，鼻梁偏窄。东方人尤其是南方人多半是圆胖脸型，而且鼻子也相对塌陷，这时如果你要是垫一个高耸而细长的鼻子就很不协调，即使你做得比例协调，在整个都是塌鼻子的人群中也不一定能被广泛接受。笔者曾有一案例，为一中面型偏长脸型的美女，不胖也不瘦，下巴较尖，眉清目秀，就是鼻子相对塌陷、鼻翼宽大、鼻尖缺乏轮廓感，经过详细讨论、沟通，给她做了隆鼻加鼻尖鼻翼整形并加长鼻尖手术，手术后本人很满意，围观的医护人员都一致赞不绝口，可是回到家里周围有人说像外国人，她的看法也有些动摇，复诊时疑惑地问我到底好不好看，我根据他的面型特点给她做了详尽的分析，她算是接受了。可这个案例告诉我，地域文化的差异对人们的影响是我们整形医生不得不考量的内容。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般说来，高耸的鼻子</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">给人以高贵气质的感觉，眉宇间会自然透出一种盛气凌人的傲气与威严，比较适合长脸型尤其是相对清瘦而额骨突出者，如果你是做法官或管理者，你可能会更有优势；低矮的鼻子比较平民化，有亲和力，比较适合圆脸型者，圆脸型配上一个不太高（当然也不要太矮）、比例匀称的鼻子看起来很可爱，让人没有压力感，如果你是从事营销工作，而且性格也很温和，可能会有更多客户愿意与你打交道哦。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此要强调的是，我们反对毫无计划，毫无目标的盲目跟风的鼻整形要求。有些人鼻子已经长得很好看了，就是因为不符合流行标准或与心中偶像不一样而去整形或是一时冲动去整形都是不可取的，门诊实践中笔者总是喜欢在第一次交流后提出本人的参考意见，要求求美者回去后与自己的家人、男女朋友讨论取得支持并有确定的想法，才肯实施手术整形。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼻整形有些啥内容？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼻整形手术内容目前来说常见的可分为扩大、缩小、精致化及缺损修复四类手术，前三者主要涉及正常鼻子美容，后者则是畸形的修复，因为疾病、外伤造成全鼻或部分鼻缺损或组织位移，则需要鼻再造与修复，需要住院手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">扩大手术包括塌鼻垫高、短鼻延长、小尖鼻扩大甚至还包括鼻基底塌陷的垫高；缩小的手术包括肥大的鼻尖鼻翼缩小、宽鼻变窄，长鼻缩短；精致化手术则是更高要求的手术，如鼻轮廓感的修饰，歪鼻、驼峰鼻或鹰钩鼻的矫正、鼻翼弧度、鼻尖翘度及鼻唇角调整等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">扩大的手术往往需要植入材料，如硅胶、膨体等人工材料或自身的耳软骨、肋软骨、真皮。人工材料使用方便但有极少数人会有排异反应，自体材料有很好的相容性，但是要形成新的伤害。两者各有利弊，一般来说，严重的短鼻、鼻背皮肤菲薄者最好用自体软骨，轻度鞍鼻、鼻部皮肤厚而松者完全可以用硅胶等人工材料；缩小的手术则需要切除部分组织，如果是软骨切除倒好办，如果是皮肤切除则可能要遗留疤痕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼻整形能解决我的问题吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个问题似乎与鼻整形手术无关，可是这却是鼻整形医生与求美者不可回避的问题。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不管你承认与否，事实上门诊很多求美者并不是不美，而是出现了一种体像障碍的心理毛病，也可能是儿提时代亲戚邻居不经意的一句评论，也可能是班上同学取了一个带侮辱性的外号，从此在幼小的心灵里埋下疑惑、自卑甚至焦虑忧郁的负面心理，这种挥之不去的心理阴影如果长期反复加强，就会形成体像障碍甚至产生忧郁症、精神分裂症。体像障碍分为轻度的积极体像障碍和稍重的消极体像障碍，还有更严重的病态体像如合并有忧郁症、精神分裂症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般认为，积极的体像障碍通过手术常常能获得较好效果，消极体像则需要配合心理治疗甚至药物治疗；病态体像就不适合手术了。</span>
</p>
