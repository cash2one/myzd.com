			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>空调与足踝关节炎</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">李阿姨从凉爽的西南故乡来到炎热的广州,帮助照顾刚生产的女儿。可第二天早晨醒来，却发现自己变成了病人！李阿姨的左脚、踝关节红肿热痛，根本无法下地行走；是痛风急性发作吗？李阿姨告诉我，她既未吃海鲜和动物内脏类的食物，平时血尿酸也控制的很好；只是昨晚觉得炎热难忍，吹了一晚的空调，左脚就直对空调风口。检查的结果也确实发现血尿酸不高。难道真的是空调导致了足踝部的炎症？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">天气变化与疼痛</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有过外伤或手术史的某些人，经常会在天气变化前，出现伤处的隐隐不适，而准确地‘预报天气’。有学者观察到，当气温变化大于30C或湿度变化超过10%时，就医的关节痛患者明显增多。天气的变化其实与人体的疾病有着某种神秘而密切的联系！但这种联系到目前仍尚未被现代医学所揭秘。为什么在下雨潮湿的天气，关节疼痛的病人会更多？为什么吹空调，会诱发类风湿关节炎或其他关节病变？有人认为可能与地磁场、空气中的氧含量或激素分泌的生物钟有关。更多的人认为，这其中可能既有天气变化的原因，也有患者本身的体质、基础疾病等方面的原因。当外周冷觉感受器感受到皮肤温度下降较剧烈时，通过外周神经传导到中枢，中枢调节将反射性使局部血管收缩，血流速度下降；进一步当局部组织存在基础性疾病或外部的压迫时，在炎症部位或压迫以远的部位特别是足部或踝关节更容易渗出增加，出现急性炎症反应。李阿姨的足踝关节炎就是如此产生的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这种情况，中医认为是风寒入络所致。在酷暑高温的夏日，导致人体风寒的可能性虽然较春秋季小,但由于空调、风扇的过度应用，导致局部如踝足关节风寒的并不少见。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">哪些人应慎吹空调！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有痛风、糖尿病、类风湿等慢性病史者，足踝部慢性创伤后遗症者，吹空调时应小心！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有痛风、类风湿等慢性疾病史的患者，由于足踝部往往存在慢性踝关节炎、跖趾关节炎或踝足部滑膜炎，在空调环境下，如踝足部局部降温迅速而剧烈（如长时间将足踝部或关节部位对着空调或风扇吹），而周围组织由于天热而毛孔扩张，可能将导致血管神经功能紊乱，局部温度下降，血流速度减慢，代谢产物聚集在局部，从而诱发炎症急性发作。如李阿姨既往曾因血尿酸高被诊断为‘痛风’，有痛风病史；虽然此次检查血尿酸不高，但如踝足部局部尿酸升高，也会导致局部痛风急性发作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同样，有糖尿病病史的患者，由于踝足部血管壁增厚而狭窄，血流速度下降；更重要的是慢性糖尿病患者往往有外周神经病变，神经感觉障碍，无法感受到外周的伤害性刺激，在局部降温后，诱发足踝部的红肿疼痛。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而那些足踝部曾经严重扭伤或有创伤后遗症的伤者同样也要注意！在冷热迅速变化时，伤部的神经末梢也可能感知到异常，而诱发急性炎症发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如何避免？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">加强锻炼，增强体质，避免长时间直接接触空调风等可能是降低足踝部急性炎症发作的有力措施。而对于既往有足踝部慢性病史的患者，局部保暖（如佩戴护踝或外包毛巾被）防潮湿，是开着空调过夜的必需安全防护措施。而对那些喝着啤酒一直看球到深夜的球迷们，更要注意保护自己的双足和踝关节。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在足踝部急性炎症发作时，不要着急，马上到附近医院诊治。经医生指导应用阿司匹林或NSAID类药物，或针对病因应用降血糖、控制血尿酸的药物，或关节局部注射激素，都可能迅速地解决痛苦，恢复关节功能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">足踝部位于身体的最未端，白日承受身体体重奔波，接触大地，最容易受到细微劳损与伤害， 更应得到我们更多的关怀！</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个广州的夏天是如此的炎热与漫长！没有空调或风扇简直无法想象！但温柔而舒适的空调风啊，对于你的足踝来说，可能带给你的并不仅仅是快乐啊！</span>
</p>
