<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">目前贲门失弛缓患者更多的是接受经口内镜下肌肉切开术（POEM）治疗，有些不适合或暂时不适合POEM治疗的患者可以采用内镜下肉毒素注射或大球囊扩张治疗。POEM治疗的疗效在不同类型的贲门失弛缓患者疗效明显不同。贲门失弛缓的压力分型分为三种，各型均有下括约肌压力大于等于15mmHg，此外I型表现为100%的无效食管蠕动，II型表现为多于20%的吞咽时食管可见增压现象，III型表现为多于20%的吞咽时下段食管有阶段性提前收缩。其中II型做多见，治疗后效果也最好，其次是I型，最少的III型，I型和III型的治疗效果较差。因此治疗前做出准确的食管压力分型，可以预估治疗后疗效的好坏。此外根据不同分型的患者，在肌肉切开长度上要适当区分，特别是III型患者切开长度一定要充分，可能术后疗效会更好。因此建议患者在做贲门失迟缓内镜下治疗前有必要做食管测压检查，让医生有更充分的参考依据。</span>
