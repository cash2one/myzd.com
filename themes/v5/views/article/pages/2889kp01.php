			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是脑动静脉畸形（BAVM）？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑动静脉畸形（Brain Arteriovenous malformation, BAVM），是一种比较危险的先天性的脑血管病。BAVM是胚胎发育到45-60天时发生的动静脉血管的异常沟通，动脉与静脉之间没有正常的毛细血管网，而只有异常的畸形血管团。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/2889/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床医生就是根据AVM供血动脉、畸形团大小、部位和深度、引流静脉的深浅等对其加以分级。常采用Spetzler分级方法（畸形团大小每3厘米加一级，部位位于功能区加一级，引流静脉如是深部静脉引流加一级，共五级）。级别越低，相对就比较好治疗；级别越高，治疗难度就越高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑AVM通常在以下情况下被诊断出来：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1，运动时或情绪激动时突感剧烈头痛，常伴呕吐，神智可清醒，也可模糊甚至昏迷，可以有手脚麻木或者无力，严重时有偏瘫、讲不出话等神经功能障碍。多于青少年期发病。AVM第一次出血的病人80%-90%可以活下来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2，肢体抽搐，也就是癫痫发作。大约一半以上的BAVM可有癫痫发作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3，头痛，半数以上的病人有头痛史。头痛常偏一侧，可自行缓解。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4，逐渐加重的神经功能损害。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">出现上述情况时，医生会建议患者进行头颅CT、磁共振（MRI）检查、特别需行脑血管造影（DSA）检查，以明确AVM的供应动脉，畸形团的大小、位置，以及引流静脉的情况，即可加以确诊。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗方面，目前有三类治疗方法：1，开颅手术切除。2，血管内栓塞治疗也就是介入治疗，通过往畸形团内注射ONYX等胶的方法来使畸形团得到全部或部分闭塞（近年来发展很快）。3，立体定向放射外科治疗也就是伽玛刀和射波刀等。这些方法也可以结合应用，也就是综合治疗，通常会有更好的疗效。</span><br />
