<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>没有甲状腺疾病 也可能得甲状腺眼病</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;我们在日常生活中有时会发现，有的人眼球凸出变大了，这并没有让他们更加好看，反而会让他们感觉不舒服，眼睛干涩、疼痛，甚至怕光、流泪，睡觉的时候上下眼皮闭不全。这是怎么回事呢？原来，他们得了甲状腺眼病。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;甲状腺眼病：与甲状腺相关的一种自身免疫性疾病</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;复旦大学附属眼耳鼻喉科医院眼科副主任医师张锐介绍，甲状腺眼病是眼眶疾病中最常见的类型，是与甲状腺相关的一种自身免疫性疾病，可以伴发甲状腺机能亢进、桥本甲状腺炎、甲状腺机能减退等。这种疾病女性多见，多数双眼发病，双眼的严重程度可能不同。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;甲状腺眼病最常见的首发症状是眼睑退缩，其他的临床表现包括：上睑迟落（向下方注视时，上眼睑不能相应地随之下移），眼球凸出，眼睑、结膜充血水肿，泪阜（内眼角）肿胀。此外，还会出现斜视、眼压升高等表现。眼睑闭合不全还可能导致暴露性角膜病变，甚至角膜溃疡穿孔。眼外肌的肥大和纤维化严重者，会导致视神经受到压迫，引起视力的下降和视野的缺损。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;甲状腺眼病、甲状腺疾病可同时也可单个存在</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;可能有的人会问：我没有甲状腺疾病，是不是就不会得甲状腺眼病呢？非也。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;“甲状腺眼病和甲状腺疾病没有必然的因果关系，它们就像同胞兄弟，可以同时存在，也可以单个存在。”中山大学中山眼科中心著名眼眶病专家杨华胜教授指出，临床数据表明，甲状腺眼病患者中有60%~70%的人有临床性的甲亢或甲减，需要到内分泌科进行治疗。有20%~30%的人有亚临床的甲亢或甲减，可以暂时不在内分泌科治疗。还有10%~15%的人甲状腺功能是完全正常的。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;甲亢、甲减等甲状腺疾病患者应定期检查眼睛</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;为了及早发现甲状腺眼病，杨华胜教授建议：本身有甲亢或甲减等甲状腺疾病患者应该定期检查眼睛，一旦发现自己患了甲状腺眼病，要及时控制病情发展。没有患甲状腺疾病的人，如果发现自己出现眼肌变大、眼球凸出甚至视力下降、视物重影、畏光等症状，也应该及时到眼眶病专科就诊。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;一旦确诊为甲状腺眼病，张锐介绍，如果存在甲状腺功能异常等情况，治疗首先应在内分泌科控制甲状腺功能。眼部需根据病情进行个体化治疗。甲状腺眼病是一种自限性疾病，有自行好转的可能性。因此，轻度的甲状腺眼病不需要应用激素或放射治疗，副作用可能大于疗效，只需滴用人工泪液进行眼表的滋润。对于眼睑闭合不全者，应在睡前眼部涂眼药膏，避免睡觉时角膜长时间暴露引发角膜病变。睡眠时抬高头位，以减轻眼眶周围水肿。饮食以低盐为主，避免吸烟。对于急性期患者，眼部有明显水肿、充血、疼痛等炎症刺激表现，或有压迫性视神经病变的患者，应在全身情况允许的条件下，应用糖皮质激素静脉冲击治疗，必要时联合使用其他免疫抑制剂或放射治疗。在稳定期，甲状腺功能正常，眼部症状稳定6个月以上时，对于有眼球凸出、暴露性角膜病变、压迫性视神经病变或有美容需求的患者，应进行手术治疗。</span>
</p>
