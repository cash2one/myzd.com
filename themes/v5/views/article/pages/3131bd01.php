<span><span style="font-size:14px;line-height:2;">【摘要】 当我们形容对某种事物的珍惜时，往往会说“像爱护眼睛一样爱护它”。然而，现代生活方式带来了哪些眼病，我们又该如何防治，大家却知之甚少。3月4日，在首个“重庆志愿者日”到来之际，第三军医大学大坪医院眼科专科医院副院长白继教授及其团队在重庆联通公司本部大楼开展爱心义诊活动。&nbsp;</span></span><br />
<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; 华龙网讯（记者 蒋永劲）当我们形容对某种事物的珍惜时，往往会说“像爱护眼睛一样爱护它”。然而，现代生活方式带来了哪些眼病，我们又该如何防治，大家却知之甚少。3月4日，在首个“重庆志愿者日”到来之际，第三军医大学大坪医院眼科专科医院副院长白继教授及其团队在重庆联通公司本部大楼开展爱心义诊活动。</span></span><br />
