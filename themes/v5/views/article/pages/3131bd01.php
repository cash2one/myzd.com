<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; 医疗团队在重庆联通公司本部大楼开展爱心义诊活动。记者 蒋永劲 摄</span></span><br />
<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; 活动现场，白继教授提醒，预防和缓解眼干，要养成良好生活习惯，使用电子产品和驾车要控制好时间，切忌“目不转睛”，连续用眼一小时，需休息5—10分钟；看书半小时或看手机10分钟要给眼睛一点休息时间。同时，增加眨眼频率，营养滋润眼睛；另外，很多人出现眼睛干涩、刺痛等症状时，往往自己去药店购买眼药水，如果用药不合理不但不能缓解症状，反而会使干眼症加重，因此若出现眼部不适症状，应及时就医。</span></span><br />
<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; “重庆联通35岁以下的青年职工占60%左右，因为工作需要大家几乎每天都必须长时间使用电脑，给眼睛带来负担。重庆联通公司团委考虑到大家健康用眼，本着早发现早治疗的原则，特意请到了重庆最专业的大坪医院眼科专科医院白继教授率领青年志愿者一行为公司员工进行用眼知识宣传和义诊。”重庆联通公司团委书记刘国平说。</span></span><br />
<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; 记者在现场看到，医疗团队为青年员工们带来的三项免费关爱项目，包括眼底疾病诊治及预防、测视力及验光等都受到了现场青年们的一致好评。</span></span><br />
