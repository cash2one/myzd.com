<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; 讲座现场。记者 蒋永劲 摄</span></span><br />
<span><span style="font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;“眼干眼涩也是病，很多年轻人长时间玩电脑、智能手机等电子产品，是导致用眼过度，引发干眼的主要原因，而干眼、眼疲劳这些现代人眼病症状，常常被大家忽视。”讲座上，白继教授介绍，眼睛长期干涩会造成眼组织不可恢复的伤害，视力下降，甚至可造成失明，因此要加强预防。</span></span><br />
