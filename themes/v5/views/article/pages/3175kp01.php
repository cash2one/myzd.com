	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>帕金森病吞咽障碍</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">帕金森病患者的运动症状表现为静止时肢体不自主地震颤，肌强直、运动迟缓以及姿势平衡障碍等。随着疾病的缓慢进展，患者还会合并出现语言减少和声音低沉单调、吞咽困难、流涎、睡眠障碍、抑郁或痴呆等症状，绝大部分帕金森病患者将最终被禁锢于家中、病榻上，为家庭和社会增添了沉重的负担。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 最近5-6年，德国Braak教授通过病理切片发现帕金森病患者不仅在中脑黑质存在病变，在肠道的神经丛、迷走神经等处也发现了lewy小体。进一步观察发现，身体从上到下存着大量的lewy小体，很多病人在运动障碍之前就伴有嗅觉障碍、肠道障碍，睡眠障碍等。后期还会有认知障碍。现在来看，这个疾病一开始从周围神经丛，然后到脑干，最后到大脑皮层。因此，它是一个进展型的疾病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;伴有吞咽障碍的帕金森病患者生活质量明显降低，主要表现为流涎、进食困难和呛咳， Edwards报道有52%帕金森病患者存在吞咽障碍，而年龄匹配的正常对照组只有6%的发生率。吞咽障碍的患者由于食物误吸进入气管导致继发性化学性肺炎，经常反复出现肺部感染，严重者可发生呼吸衰竭或呼吸窘迫综合征，最终导致死亡；直接由于大块不易分解食物吸入气管造成的机械性窒息，继而出现心跳呼吸停止。此外，患者可因摄人不足，造成水和电解质紊乱及其他营养成分缺乏，白蛋白降低，严重消瘦。由于吞咽功能障碍导致的吸人性肺炎以及恶病质，往往又是PD病人高病死率的一个重要原因。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;摄食-吞咽是食物从被认知开始，经口腔、咽部、食管到达胃部的全部过程。这一过程以食块位置分为先行期（认知期）、准备期、口腔期、咽部期、食管期5个阶段。后3者相当于吞咽动作的口腔、咽、食道3个时相。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;对于存在吞咽困难的帕金森病患者，可以采用才藤氏吞咽障碍7级评价法进行分级评估，该方法是日本学者才藤荣一于1999年设计发展的，2001年高怀民将其引人我国，被我国康复界接受。此评价法在诊断吞咽障碍的同时对障碍程度分为7级，级别越高，吞咽障碍越轻，分级标准明确清晰，可指导各级患者的康复。此评价法在日本吞咽障碍诊断与康复治疗中广泛应用。</span><br />
