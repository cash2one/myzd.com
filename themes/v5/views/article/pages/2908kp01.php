			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>发现胃肠癌不要忙于外科手术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前很多患者一旦被诊断为胃肠癌就马上要求进行外科手术治疗，一些临床医生也是持有这种不正确的观念。其实现今的外科手术和30年前并无明显不同，为什么过去一说到癌就认为是不治之症，而现在很多胃肠癌可以治愈?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这有多方面的原因：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 一是目前发现的早期癌多了，因为人们重视了无症状体查和检查仪器先进了；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;二是化疗、放疗、生物治疗进步了；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;三是治疗观念发生了变化，其中新的辅助化疗起著重要作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;所谓新的辅助化疗就是指在手术前先做1-2个疗程的化疗再行手术治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 其实，外科手术切肿瘤就象是砸鬼子碉堡一样，碉堡再大也可以砸掉，有经验的外科医生是不怕肿块多大的，手术常可切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 癌只所以难治，是因为癌细胞会到处跑，就象碉堡里鬼子要是跑到碉堡外，砸了碉堡鬼子还会在别的地方建新碉堡，这就是指癌肿切除后部分会形成新的复发转移灶。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;这是癌症治不好的根本原因。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;发现癌肿不急忙手术有好处。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;一是因为病人一旦诊断为癌症，情绪上是一个巨大的打击，心理处于人生的最低谷，造成了免疫功能的紊乱，癌细胞容易跑；</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;二是马上手术，要禁食、麻醉、开刀也是一种生理上打击，生理功能也处于最低谷时，容易造成癌细胞的转移。更何况手术挤压是否会造成癌细胞转移也无定论。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;因此，在手术前先做化疗可以先将肿块附近的散兵游勇消失，同时也将那些杀灭不了的癌细胞打趴，之后再做手术，癌细胞就不会活嘣乱跃地到处跑，手术切除容易一锅端，术后不易发生转移从而可达到完全治愈肿瘤的目的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;确定治疗方案和策略具体情况当然应在有经验的医生指导下进行。</span>
</p>
