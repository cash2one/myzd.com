<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">孔小乾坤大，内镜显身手</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">介绍垂体瘤治疗的新方法----神经内镜手术治疗垂体瘤</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">垂体瘤是颅内最常见的肿瘤之一，根据不同的内分泌类型又可以分为泌乳素型、生长激素型、促肾上腺皮质激素型等等，许多垂体瘤需要通过手术才能解决问题。随着科学技术的不断发展，垂体瘤的手术进路从开颅手术到经口鼻蝶窦手术再到经鼻蝶窦手术，手术技术从传统技术到显微技术再到内镜技术，如今垂体腺瘤手术通过内窥镜下经鼻进路切除，手术创伤已经非常微小，成为世界神经外科典型的微创手术病例。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">鼻腔是天然的腔道，由于鼻腔较小而且内部鼻甲的不规则结构的影响，需要采用撑开器将鼻中隔骨折并撑开，显微镜下才能进行手术操作，由此造成的创伤较大；而且术后需要二侧鼻腔填塞止血，病人只能经口腔呼吸，且感觉不适，拔除填塞物时还容易引起鼻腔再出血，同时远期经常会出现鼻中隔穿孔、鼻腔感染和粘膜萎缩等并发症。正如胃镜可以插入胃内一样，纤细的神经内窥镜则可以由较小的鼻孔插入鼻腔，直接到达我们需要手术的部位进行照明和操作，最大程度上利用了自然的腔隙，减轻了对鼻腔正常结构的损伤，手术创伤微小，术后很快恢复。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">神经内镜的优势不仅仅在于对鼻腔的微创，更在于肿瘤切除过程中的安全和彻底。传统的显微镜下手术，只能在一定范围内直视手术，因而切除肿瘤很多时候凭的是手感。而神经内镜的观察原理，类似我们家门上的‘猫眼’，近距离可以获得广角视野。手术时，我们将内镜放置到离肿瘤尽可能近的部位，近距离广视野观察，同时选择不同角度的内镜，让许多在显微镜下无法看到的部位暴露无遗，肿瘤切除自然更彻底。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">由于神经内镜的微创效果，使得垂体瘤病人的住院时间较传统显微镜手术大大缩短，大大减轻了病人的经济负担和痛苦，在经济危机的今天不啻成为广大患者的福音。在发达国家，神经内窥镜经鼻蝶治疗垂体瘤这项新技术已经普遍开展，成为垂体瘤手术的标准术式之一而为广大神经外科医生和病人接受。由于此项技术需要特殊的训练和研究，目前国内仅有少数医院能够开展。复旦大学附属中山医院神经外科经过几年的不懈努力，已经熟练掌握了神经内窥镜经鼻蝶手术治疗垂体瘤的技术，成为我市唯一能够开展此项技术的医院。通过开展此项技术，病人的术后住院天数、术后的不适反应和费用大大降低，同时肿瘤的全切除率得到显著提高。相信随着技术的推广和普及，这项世界上公认的最新手术技术将不断造福广大垂体瘤患者。</span><br />
