			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>17岁女孩患帕金森病 专家称患者年轻化需重视</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中新网成都4月11日电(记者 胡敏)“我们发现帕金森病患者越来越年轻化，这很恐怖，原来大家以为都是老年人群发病”，11日，四川大学华西医院神经外科教授王伟接受记者采访时表示，年轻人需重视自己的身体，早发现早治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4月11日是世界帕金森日，帕金森病是一种严重危害人类健康的慢性进行性神经系统疾病，其确切致病原因至今未明。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在帕金森患者年轻化的病例中，患病已五年的陈芳(化名)就是特殊的一例。陈芳来自四川南充，今年17岁，她12岁时出现莫名的出现眼斜、走路向前冲的症状，周围人对她出现的变化觉得很怪异，都不愿意接近她。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 那时，小小年纪的陈芳因病离开了校园，父母带着她去各处检查治疗，在北京一家医院她被确诊为是帕金森病。在接下的4年时间里，陈芳的父母四处求医问药，她的爸爸为了给女儿挣得看病的钱而车祸身亡，这让一家人生活变得更加窘迫。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 今年年初，陈芳的妈妈带着女儿来到了四川省八一康复中心，向该中心主任(院长)邵明咨询脑深部电刺激术。最后，在八一康复中心医生以及脑起搏器专业人员多方面的评估下，确定了陈芳适合做脑深部电刺激术。后来，陈芳的手术很顺利，并且恢复情况良好。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在王伟看来，所治疗的患者虽还没有像陈芳那样小年纪的，但是这几年他明显感到了年轻患者的增加，“我们去年就做了两例手术，患者都是20多岁，今天还有4位年轻患者过来咨询治疗。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 王伟认为，年轻人患帕金森病是多因素的，其中与工作生活压力大、长期熬夜用脑过度、环境污染等因素有关联。他提醒，如果年轻人出现面部表情越来越少、手脚越来越慢并出现震颤、写字越来越不工整等情况时，需要格外注意，并到医院检查，早发现早治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据了解，目前帕金森病还不能治愈，主要通过药物和脑起搏器治疗，以此缓解患者症状。“患者应该早诊断，前三年是药物治疗最好的时候，如果治疗情况不满意，则采用脑起搏器的外科手术治疗”，王伟教授表示，对于年轻帕金森患者更适合外科手术治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，为预防帕金森病，王伟教授建议年轻人，每天要保证6个小时以上的睡眠时间，定时吃饭并保证营养多样化，工作需要劳逸结合，以及多吃干净食品，少吃包装食品。</span><br />
