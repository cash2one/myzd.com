			<div style="text-align:center;">
	<span style="line-height:1.5;font-size:18px;font-family:'Microsoft YaHei';"><strong>乱吃水果有时会夺命</strong></span>
</div>
<br />
<img src="http://static.mingyizhudao.com/147010487922469" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医学指导</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 南方医院消化内科主任医师 智发朝教授</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 杨桃可能会夺走生命、菠萝可能会引起窒息、吃柿子吃出胃结石、啖荔枝啖来低血糖晕倒……夏天是敞开肚子吃水果的好时节，殊不知“水果病”也纷纷找上门。专家提醒，吃水果需节制，结合自身体质选择水果，才能吃出健康。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这些水果可能夺命</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一位老先生吃了杨桃，两天后全身水肿，几天后竟去世了。询问起老先生的病史，才知道他有糖尿病肾病。南方医院消化内科主任医师智发朝告诉记者，老先生是中了杨桃毒，肾病病人不能吃杨桃。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “杨桃内有某种毒素，肾功能不全的人无法将毒素排出体外，会出现中毒症状。短短几天时间，就能夺走肾病病人的生命，目前医学技术尚无有效应对办法！”智发朝主任称，广东已有多个肾功能不全的病人命丧杨桃。这类病人一定不能吃杨桃，肾功能正常的人则可放心食用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 除了杨桃，菠萝食用不当亦可致命。当吃菠萝后，皮肤出现红斑、皮疹，则可能是菠萝过敏。“菠萝内含菠萝酶，能使人全身过敏。严重者甚至使喉咙水肿而窒息身亡。”智发朝主任提醒，食用菠萝前应将其切片浸泡于盐水中，这样可抑制菠萝酶的活力，破坏菠萝酶，防止与减少过敏症的发生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 此外，表皮带有绒毛的桃和杏若清洗不干净，也会使人产生皮肤潮红、拉肚子等过敏反应，绒毛里的细菌会像花粉般到处飘、刺激鼻腔，导致过敏性鼻炎，建议去皮后食用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这些水果不宜空腹吃</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据智发朝主任介绍，柿子、芒果、黑枣、荔枝、西瓜不仅不能吃过量，而且不能空腹吃。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有位老先生到外地游玩，肚子饿了就吃了很多柿子。三个月后，起立坐下时竟听到胃里头有东西滚来滚去的“咕咚咕咚”声，食欲下降，人也消瘦了。经医院诊治，发现胃里有颗十厘米的结石，且胃黏膜已被撞坏，长了三处溃疡。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “柿子黏性大，消化不了易变成胃结石。”智发朝主任说，通常胃结石在你起立时会有“咕咚”掉下去的声音，胃结石除了破坏胃黏膜，还会引起肚子痛、呕血、胃胀、贫血、黑便等症状。芒果和黑枣同样是黏性易滞水果，吃过量和空腹吃都容易引起胃结石。“尤其小孩和老人嚼不烂，更应注意。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “我们收治过不少从荔枝树上摔下来的小孩，不是他不小心摔跤，而是吃荔枝造成低血糖而昏倒。”荔枝那么甜，怎么会低血糖？智发朝主任告诉记者，最主要的原因是荔枝含有大量的果糖而不是葡萄糖。“尤其空腹的时候血糖偏低，需肝脏分解葡萄糖来促进血糖平衡。虽吃荔枝能使血糖上升，但抑制了肝脏葡萄糖分解，导致低血糖。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 除此，西瓜也不能空腹吃。空腹吃水果易刺激胃酸分泌，此时吃含糖高的西瓜会愈发刺激胃酸分泌，导致胃痛；冰镇西瓜会刺激胃黏膜，使胃痛加剧，或者拉肚子。胃病病人尤其要慎吃西瓜。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ●特别关注</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 拼装水果放冰箱 更易感染“冰箱病”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 人们喜欢将水果切开存放在冰箱，以确保水果的新鲜与冰凉的口感，可知如此吃水果，不仅易使水果的营养丧失、维生素氧化，而且食用后容易引起急性感染和腹泻。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “冰箱里头细菌多。”据智发朝主任介绍，冷藏室内的低温虽能抑制多数细菌繁殖，但一些嗜冷菌却最喜欢这样的环境。冰箱存放的食品多而杂，无论是切开的水果还是用水洗过的水果，更容易受到污染。“要提醒大家的是，随着冰箱的普及，一种叫克罗恩病的慢性肠道炎疾病在我国越来越多，它的可怕之处是使肠子糜烂，不得不手术切除，一年的治疗费高达20多万，而且容易复发，无法治愈。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 把水果洗干净后放冰箱总万无一失了吧？智发朝主任指出，洗过的水果放冰箱更不可靠，水果表面的水分更容易使细菌吸附。此外，拼装水果、水果沙拉将水果切得较小，细菌更易入侵。水果的鞣酸、维生素含量与品种不同，PH值也不一样，混吃水果比单吃更不利于营养吸收和消化道的保护。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 智发朝主任建议，已经切开的水果或者是洗过的水果放到冰箱后，如要再次拿出来食用记得一定要重新洗干净、切除表层后再食用；脾胃虚弱者少吃寒凉与拼装水果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ●专家提醒</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 水果应在饭后两小时吃</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 很多人在饭席应酬时，通常都会碰到这样的情况：饭一吃完就上水果，其实这样吃不健康。智发朝主任介绍说，最好在饭后1-2小时后吃水果，不仅营养吸收好，且减少肠胃消化负担。“饭前吃水果会刺激胃酸分泌过多易导致胃食管反流症等消化道疾病；饭后尤其酒后立即吃水果会影响消化与营养吸收。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 水果不能当正餐</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有不少人常将水果当正餐，亦或长期靠水果餐减肥，不仅会使人体营养偏差，还会影响肠胃功能。由于水果在胃里较快排空，使胃长期处于空状态，胃酸无法中和会刺激胃，引起胃隐痛、胃食道反流甚至胃炎、胃溃疡等。此外水果形成的渣少，不利于排便。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肠胃功能弱者要小心</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 智发朝主任指出，肠胃功能弱者吃水果应注意以下几点：食管、胃等上消化道系统不正常者，忌吃冰凉、刺激性强的水果，如橄榄等腌制水果、水果罐头等；胃病病人慎吃过甜的水果，空腹时尤其不能吃；易拉肚子的人少吃红薯、芋头、土豆等薯类食品和冰镇水果，否则易刺激胃结肠反射。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 区分水果色和出血</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 大便带红，别着急，先想想自己是否吃过西瓜和红色火龙果。据悉，西瓜、火龙果是易滞水果，食用西瓜、红色火龙果易使大便变红。两天后若大便仍带红，则需注意是否肠道出血。</span><br />
