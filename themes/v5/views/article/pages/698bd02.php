			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>加速康复理念让患者少疼痛少花销</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在大多数人印象中，手术很“伤人”：术前禁食禁饮甚至还要灌肠、术后卧床休息、排气后才能进食，术后的疼痛亦然。但在浙江大学医学院附属第二医院，越来越多的患者开始接受一种全新的医疗理念：“加速康复外科（ERAS）”。通过这样的一种理念，患者能够大大缩短住院时间，也能够有效减少术后疼痛对于身体各项生理机能恢复的阻碍。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 浙医二院院长王建安教授说，ERAS不是简单地让患者早点出去就完了，它需要外科、麻醉科、疼痛科、康复科、护理科等多个学科部门之间的相互合作，它更体现一个团队的工作。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 术前不再禁食 术后少卧床</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 上周末，“中国研究型医院学会加速康复外科专业委员会成立大会”在杭州举行。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 浙医二院副院长梁廷波教授介绍，“加速康复外科（ERAS）”是近年来国内外广为推行的一种医疗模式，其核心是在术前、术中及术后应用各种已被证实有效的方法以减少患者的手术应激反应和并发症的发生，缩短住院时间，提高满意度，减少住院费用。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “打个比方，一名胰头癌患者需要进行手术治疗，按照以前的理想，患者从术前一天开始禁食以及灌肠。第二天，因为灌肠而一晚不睡的患者进手术室，从手术出来，往往身上带有四五根插管，还有至少要四五天才能拔掉的导尿管，至于进食则在患者排气之后，大约5-7天时间。这样一名患者的住院标准时间是21天，而患者的生理机能从禁食灌肠开始就不断受到各类刺激，对于术后各项功能的恢复影响很大。但纳入ERAS后，他不需要灌肠，术后的插管大大减少，导尿管24小时即可拔掉。吃东西和下床活动都将在术后第二天得到实现，住院时间缩短为一周。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 梁廷波教授说，以前的观念是术前禁食禁饮甚至灌肠，术后要卧床休息，排气后才能进食，但是ERAS颠覆了这些传统理念。灌肠会导致患者肠道菌群生态环境和水电解质平衡的破坏，可能会延缓术后胃肠功能的恢复。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> ERAS还鼓励患者尽快恢复下床活动，因为长期卧床将会增加肌肉流失、损伤肺功能、加快血栓形成。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 疼痛管理是加速康复理念的重要核心</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 长期以来，手术后的疼痛被认为是正常现象，然而，术后的疼痛不仅给患者身体带来不适，对于术后各项功能的恢复也会造成阻碍。ERAS的重要核心，即是疼痛管理。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2015年初发表的《普通外科围手术期疼痛处理专家共识》中提到，术后的疼痛对于患者的心肺、胃肠功能都有不同程度的影响，还会导致焦虑、无助、不满等负面心理因素，并产生睡眠障碍，影响患者手术后的快速恢复。如果在初始阶段对疼痛进行有效控制，可以很大程度避免由于持续的疼痛刺激引起中枢神经系统发生病理性重构，从而避免急性疼痛发展为难以控制的慢性疼痛。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 梁廷波教授透露，浙医二院积极实践加速康复理念、疼痛规范化管理已有一年有余，目前该理念已在浙二普外科、麻醉科、护理科等多个科室中积极推广应用。从2015下半年起，纳入ERAS的患者已有约1000余名。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 根据浙医二院的数据，手术患者平均住院日从以前的11天下降到了8天，缩短3天。以往的手术和医药费用需要花30多万，现在能降低约1/3，节省10多万。这些颠覆性的改变正是由于“加速康复外科”和疼痛规范化理念的实施。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 本次大会进行了加速康复医学专业委员会委员选举，诞生了中国第一个“加速康复外科专业委员会”，浙医二院副院长梁廷波教授当选为主任委员。浙医二院肝胆胰外科副主任白雪莉教授当选为中国研究型医院学会加速康复外科专业委员会副主任委员兼秘书长，她说：“加速康复外科（ERAS），是一个颠覆性的理念革新，是一个朝阳学科，加速康复外科专委会的成立将有利于ERAS在中国真正落地，真正施惠于民，促进病人的快速平稳康复。”</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 浙医二院作为加速康复外科的探索者和先行者，要把“更高，更快，更强”的精神融入到全院外科医疗实践中，并将其推行于世，实现医疗资源更高效的利用，医院床位更快速的周转，及术后患者更安全的康复。</span>
