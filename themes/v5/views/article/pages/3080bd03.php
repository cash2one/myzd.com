		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>省内首家心血管 “杂交”手术室落户省医</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">南方日报讯 （记者/曹斯 通讯员/郝黎 靳婷）心血管科若遇到病情复杂的病人，往往需要既做外科手术，又做内科介入，这在过去得分两步走，耗时耗力，也增加了风险。笔者昨日从广东省人民医院获悉，广东首家心血管科“杂交”手术室落户该院，使外科医生在手术室内不仅可以进行常规外科手术，还能够直接进行血管造影和介入治疗，也可以与内科医生合作共同完成开放手术与介入手术，在影像学信息的辅助下大大提高了手术效率和成功率。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据该院心外成人二科主任范瑞新介绍，可杂交手术室（Hybrid Operation Room ， Hybrid－OR）又称“复合手术室”、“镶嵌手术室”。而DSA“杂交”手术室是指将数字减影血管造影（Digital Subtraction angiography，简称“DSA”）系统直接安装在手术室中，这样，由心外科、放射科、麻醉科、心内科、心儿科、手术室护士组成的介入手术治疗团队相互配合、相互协作，为患者实施“内外兼修”的手术。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 什么病人可以做“杂交”手术？范瑞新表示，比如说，主动脉夹层病人可以同期施行主动脉外科手术和主动脉腔内科隔绝手术；冠心病的病人可以同时施行冠状动脉造影、开胸冠状动脉搭桥及内科的冠状动脉支架置入手术等，真正体现了开放手术与介入手术一站式的服务理念。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “过去这些复杂的手术很可能要分两步进行，先做外科手术，然后过几天等状态稳定了，再做内科介入，耗时耗力耗金钱，而且等待过程中也可能增加了病情发展的风险，杂交手术这种“无缝”链接的方式大大方便了患者。”范瑞新说。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据悉，除心脏外科外，DSA杂交手术室还可广泛应用于心神经外科、血管外科、肝胆外科等介入手术的治疗。</span>
</p>
