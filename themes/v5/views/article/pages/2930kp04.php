		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>肝癌介入后二期切除</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">外科手术切除是原发性肝癌（PHCC）最有效的治疗，TACE（经皮肝动脉导管化疗栓塞术）是最为广泛的治疗措施，RAF、PEI等是肝癌治疗的有效补充；原发性肝癌治疗原则是序贯综合和合理使用各种方法。TACE优势在于可以控制肝癌病灶进展，治疗不能手术切除的肝癌，病灶缩小以达到二期手术切除的目的，介入后可二期切除的病例占TACE6.2%-11.2%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;介入治疗是目前公认的不可切除中、晚期肝癌的主要治疗手段,它在抑制肿瘤生长,提高患者生存率等方面取得了明显效果。研究表明:TACE 后肿瘤和正常组织缺血、缺氧可使肿瘤细胞及瘤周正常细胞分泌促血管生成物质如VEGF、FGF 等，使肿瘤血管侧支循环形成，增加了再次TACE治疗的难度。这些因子还有其他副作用如：①使血管通透性增加,增加转移可能性；②抑制肿瘤凋亡，使残存肿瘤细胞长寿；③促肿瘤增殖作用，易诱发复发。减少TACE 术后血管生成的策略包括: ①尽量做到栓塞治疗彻底，如行节段性栓塞；②在超选择插管的前提下使用强效栓塞剂：如碘油+无水乙醇等；③TACE 后加用局部消融治疗，特别要注意对肿瘤周边区域进行有效的局部治疗；④加用抗血管生成治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; 研究显示TACE 术后残存肝癌细胞及正常肝组织细胞的基因表达发生了变化，其中部分变化促进了对治疗的适应性，使部分肝癌细胞表现出“耐缺血性”、“耐栓塞性”，表现在以下几个方面: (1) TACE 可以促进残存肿瘤细胞的血管生成相关因子的表达，我们的动物实验研究表明TACE术后残存的肝肿瘤灶VEGF、bFGF 表达是增强的，微血管密度增加；国外学者研究发现肝癌TACE术后血液中VEGF 含量一过性升高，二期手术切除标本中的癌细胞和瘤周肝细胞VEGF表达是增强的；(2) TACE后未栓塞肿瘤细胞的增殖活性增强；(3) TACE 术后未栓塞部分肝脏出现代偿性增生,增殖活性亦增强。这些均说明TACE 术后残存肝癌的复发和转移潜能是增加的，同时正常的肝组织亦具备肝癌发生的土壤。目前对TACE 后肝癌残存灶生物学行为影响方面的研究刚开始，尚有许多的问题没有解决，对这方面进行深入研究会为我们的治疗带来新思路。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; 经过多年大量病例的治疗实践，国内外文献公认TACE 能提高患者的生活质量，延长生存期，且TACE 较安全、严重并发症少。TACE 治疗PHCC 患者的1～3 年生存率分别为60. 4 %、42. 9 %、28 %。经TACE 治疗后肿瘤缩小并获得二期切除约占TACE 总例数的6. 2 %～11. 2 % ，二期切除者5 年生存率与小肝癌切除者相近。有少数病例经多次TACE和综合疗法后肿瘤缩小、消失，有的至今生存10～20年以上，达到基本治愈的疗效。对于能手术切除的肝癌，术前是否行TACE？国内外都存在分歧意见，主张术前行TACE的研究者认为：TACE可使肿瘤体积缩小，减少术中出血量及术中因挤压而造成的癌细胞播散，从而主张对PHCC患者术前常规行TACE。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;研究表明经TACE 治疗后，虽有73 %的肝癌患者50 %以上的肿癌组织出现坏死，但仅有5 %是完全坏死，而残存的癌细胞会具有或产生更强的增殖和侵袭能力。如有的病例TACE 后短期内出现多处或弥漫性播散灶，有的出现肺内多发转移灶等；此外肝癌结节周围区或包膜下常有存活的癌细胞，加之周围侧支血供很快建立，残留癌组织很快增殖；肝癌患者多伴肝硬化、门静脉高压等, TACE 后绝大多数患者会出现肝功能损害，部分可发生上消化道大出血、肝功能衰竭等严重并发症；多次TACE 治疗后，可导致肝萎缩和严重肝功能失代偿，进而加重了病情；此外，肿瘤的大小、血供以及患者肝功能状况等均是影响TACE 疗效的因素。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; 综上所述，对不能手术的原发性肝癌，应首先全面考虑患者的病情，然后结合各种疗法的优缺点，实行综合治疗才能获得最佳疗效，综合介入治疗将成为治疗恶性肿瘤的趋势。治疗PHCC的各种介入方法要获得突破性进展，必须与分子生物学和临床高科技相结合，才能有效的提高肝癌治疗的临床疗效。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; 澳大利亚Chua等系统回顾了近年来发表的有关可切除肝癌术前给与TACE治疗的所有临床研究文献共18篇，文献共统计3927例患者，其中1293例在术前给与了TACE，分析两组患者的术后DFS（无瘤生存）、OS(总生存率)、病理标本的坏死率和手术死亡率等，得出结论：随即和非随机的临床试验均提示TACE作为可切除肝癌术前辅助治疗手段是安全有效并可提高病理标本的坏死率、不增加患者的手术死亡率，但是并不提高患者的DFS。然而更多的临床文献表明对于可切除肝癌不推荐术前给与TACE，尤其是早期肝癌和不伴有肝硬化背景的肝癌患者；可切除肝癌术前给与TACE降低了患者的远期生存率，还有可能导致患者肿瘤进展和肝功能衰竭。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; 我们的经验是肝脏储备功能不良（ICGR15 ＞25%)、肿瘤巨大切除可能会丧失较多肝脏体积使患者无法耐受手术、高龄无法耐受手术、怀疑肝内存在转移病灶等患者选择TACE治疗。需要重视的是TACE后肿瘤侧支循环的建立是肝癌复发和转移及肝癌患者预后不良的主要因素，TACE治疗后可促使肝外侧支循环建立，3-4次后为17.9%，5-6次后为56.4%；TACE难以使肝肿瘤组织完全坏死，二期切除肿瘤标本显示坏死率22%左右，同时要注意在二期手术切除时TACE患者具备以下特点：1.多次的TACE治疗后，术中在病灶周围会形成较为明显粘连；2.患者存在肝炎肝硬化背景，肝脏病灶周围炎症性粘连会更加明显；3.虽然经过TACE治疗后肿瘤体积明显缩小，但术中肿瘤的纤维包膜不明确；4. 经过TACE治疗后，肝脏组织更加松脆，创面更容易渗血；5. 经过TACE 治疗后肝脏功能状态在术后更加容易出现肝功能障碍并发症如腹水、胸水、低蛋白血症等；6.TACE 术后残存肝癌的复发和转移潜能是增加的，同时肝炎肝硬化背景的肝组织亦具备肝癌发生的土壤；7.重视肝癌残存灶生物学行为对患者远期生存的影响。因此如果经过TACE治疗后患者获得了二期手术的机会就应该积极选择外科切除或者是射频消融治疗等手段可以彻底治愈的方式，只有综合治疗才能提高肝癌的临床疗效。</span>
</p>
