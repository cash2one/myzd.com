			<div style="text-align:center;">
	<strong><span style="font-size:18px;">儿童气管或食管发生异物伤害高发 花生瓜子最多</span></strong>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨天，记者从北京儿童医院获悉，节日期间，儿童气管或食管发生异物伤害高发，九成异物都是花生米和瓜子等食物。北京儿童医院每年接诊的这类患儿已达到700例左右，五岁以下儿童比较容易被异物侵袭。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">每年春节都会有不少儿童发生气管或者是食管异物的伤害，到儿童医院来紧急救治。北京儿童医院耳鼻喉科主任医师张亚梅介绍，一般来说，气管异物90%以上都是瓜子、花生、开心果等食物，因为大人喜欢吃就会也喂给孩子，同时，还有些生活用品，如笔帽、口哨等，孩子如果习惯随手把东西塞在口中的话，就会容易出现气管被堵塞的情况，“一个小女孩的爸爸刚刚买了衬衫，把衬衫上卡衬衫的大头针拿下来，孩子就放到嘴里边，结果一叫她，就进入气管了”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张亚梅介绍，异物吸入后，孩子往往会咳嗽和急喘，家长须特别留意。如果较大的异物将孩子气管堵满的话，会导致孩子完全不能呼吸，这时候只要窒息五分钟，一般来讲就会出现脑死亡。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">记者获悉，目前北京儿童医院具备一整套完善的气管异物患儿的术前和术后的量化方法，能够做到快速诊断以及安全取出异物，20年来共取出呼吸道异物1万多例，近年来每年都取出700例左右。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>医生建议</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">家长应掌握海氏急救法</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张亚梅建议家长应掌握海氏急救法，如果觉得孩子吸入异物后，要赶快把他放在胸前，让他背冲着你的胸而坐，你双手重叠，使劲且有节奏地快速挤压他的上腹部，即胃的位置，目的是通过腹压的作用把胸腔里的气挤到横膈至气管里，最终把异物给冲出来，“让孩子远离危险源才是最关键的”。（记者李秋萌）</span>
</p>
