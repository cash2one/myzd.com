			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>早期肾癌患者都要行肾切除术吗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为什么早期（局限性）肾癌患者需要接受手术治疗？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于局限性肾癌，手术能使大部分患者达到治愈水平。目前手术方式主要有根治性肾切除和保留肾单位的部分切除，而且对于绝大多数患者都可以采用微创技术（腹腔镜）完成，效果达到开放水平，甚至好于开放手术。具体的治疗方式取决于多种因素，包括患者的年龄、健康状况、麻醉风险以及肿瘤大小、位置和浸润情况。通常肿瘤越大，根治性肾切除的可能性越大，除非完全切除患肾后患者可能出现肾功能下降需要透析，这种情况下尽可能保留更多的正常肾单位。但是具体如何选择手术方式必须根据患者的具体情况与医生协商决定。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么是根治性肾切除术？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根治性肾切除术包括开放式与腹腔镜下根治性肾切除术。开放式切口可以在腹部也可以在腰部，但通常手术切口较大，不仅美观欠佳，而且手术创伤也较大，愈合时间较长。腹腔镜下肾切除避免了上述不足，医生只要通过较小的通道就可以完成手术，除非有癌栓形成、肿瘤巨大和淋巴结较大的患者，否则都可以考虑腹腔镜下手术。在过去的10年中，腹腔镜手术已经发展成为根治性肾切除的标准方法，该方法侵袭性小，恢复迅速，而且手术禁忌也较少。但腹腔镜手术需要经验丰富、熟练的外科医师才能完成。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么是肾部分切除术？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果患者肿瘤较小，或者切除患肾会严重影响肾功能，考虑是否保留肾单位手术时必要的。如果肿瘤小于175px，有可能适合接受保留肾单位的手术。随着医疗技术的进步，对于许多患者来说切除整个肾脏是不必要的。所谓肾部分切除术指的是仅切除肿瘤和肿瘤边沿的小部分正常肾组织。随着对肾肿瘤的认识越来越清楚，经过多年论证，接受肾部分切除的患者肿瘤复发率并不比根治性肾切除患者高。随着经验的积累，腹腔镜水平的提高，无论是开放还是腹腔镜下肾部分切除术都已成为常规术式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为什么要肾部分切除术？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于孤立肾肿瘤或双肾肿瘤，如果采用根治性肾切除必然会导致患者要接受透析，因此部分切除是有必要的。如果患者有糖尿病、高血压、肾结石、慢性感染、狼疮性肾炎等目前或者将来有可能影响总肾功能的疾病，切除患肾可能导致残留的肾功能处于临界状态，将来有透析的风险。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于两侧肾功能正常，而且没有基础疾病的患者，当准备切除一侧肾时，外科医生和患者必须考虑术后有发生肾功能不全的可能性。尽管一个肾对于患者今后的生活没有影响，但是正常的肾组织当然越多越好。结石、感染、高血压、糖尿病、老年化等问题随之时间的推移都有可能发生，并可能对肾功能产生不利影响。因此最大限度保护正常肾组织对于年轻患者特别有益。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，对于因为肿瘤而切除一侧肾脏的患者，对侧肾再次发生肿瘤的风险也高于常人。因此，在某种程度上来说，肾部分切除已经成为一个标准的治疗方案而不仅仅是个选择性的方案。</span><br />
