			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>被诊断为乳腺癌后怎么办？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺癌已经成为女性最常见的恶性肿瘤。在美国，每8位女性就有1位会被诊断为乳腺癌。中国女性乳腺癌的发病率也在逐年增高，在北上广等大城市，乳腺癌的发病率正在接近美国的水平。而且，不容乐观的是，中国女性的乳腺癌发病年龄显著的年轻化，根据我们的数据，40~50岁是中国女性的发病高峰年龄。这个年龄的女性应该说正是一个家庭的顶梁柱，上有老下有小，所以经常会给一个家庭带来灾难。更为令人心痛的是，20~30岁的乳腺癌也不少见，甚至有10几岁就被诊断乳腺癌的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么万一真的不幸中招了，被诊断乳腺癌了，接下来应该怎么办呢？逃避不是办法！放弃治疗不理智！去吃“绿豆”更是愚昧！悲痛之后还是应该好好想一想接下来怎么办。下面我仅从医疗角度跟患者朋友简单交流一下。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先，还是传递一些正能量！乳腺癌总体而言，治愈率还是很高的，早期乳腺癌可以达到70~80%。而且，得了乳腺癌也不意味着就要失去乳房或者丧失美丽的外形。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果怀疑自己得了乳腺癌或者被确诊为乳腺癌，那么，首先需要就诊的科室应该是乳腺外科，先去找乳腺外科医生评估手术的可能性。对于可以手术的早期乳腺癌，建议尽快手术治疗；对于有保留乳房愿望的、肿瘤体积不大、单中心的病变，可以进行保留乳房的手术。许多患者担心保留乳房的手术不够彻底，其实不是这样的。保留乳房的手术会使局部复发的风险增加一些，而且需要进行放射治疗，但是不增加远处转移的风险，也不影响患者的存活时间。换句话说，如果保留乳房以后复发了，再做全切手术，照样有治愈的机会。当然，保留乳房手术，还是会增加一点的复发风险（约3%），而且也会增加一定的费用（放疗），所以对外形无所谓的患者也不用强求。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果乳腺外科医生评估患者已经局部晚期，也就是说手术切不下来的可能性很大，那么可以先进行药物治疗，包括化疗、内分泌治疗等。等肿瘤缩小到可以切除了，再进行手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术治疗结束后，医生会根据病理分期、肿瘤的生物学指标、患者的年龄、身体状况以及经济情况，跟患者或者家属沟通后制定下一步辅助治疗的方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可以说，乳腺癌的治疗是一个长期的过程，因此，患者朋友最好找一位自己信任的医生进行治疗。因为只有您的医生全面了解了您的情况，才能制定适合您自己的个体化治疗方案。乳腺癌的治疗没有适合所有人的“万金油”，每个人都有自己的特殊性，所以没有必要去跟别的病人比较治疗方案。每位医生对病人情况了解不一样全面，对乳腺癌的认识也不一样，开出来的处方也是不一样的。所以，也没有必要到处咨询，把所有可能接触到的医生都咨询一遍，否则，最终的结果必然是把自己搞晕，无所适从，最后频繁换方案，反而耽误了治疗，影响了疗效。</span><br />
