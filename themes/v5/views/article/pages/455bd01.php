	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>国际著名医学专家来聊城市人民医院访问交流</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2月6日，国际著名肝病及肿瘤学家Jack Wands教授、中国科学院北京基因组研究所顾明亮教授应邀来聊城市人民医院交流访问,并与聊城市人民医院专家进行学术座谈。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 作为美国肝病协会会长、美国肿瘤协会副会长、美国布朗大学消化中心主任，Jack Wands教授长期从事消化系统癌症及胰岛素代谢疾病的研究和教学工作，个人拥有100多项专利技术发明，在&nbsp;等国际学术期刊已发表SCI论文520余篇。Jack Wands教授此前曾多次来聊城市人民医院访问交流，此行的主要目的是对聊城市人民医院参与的国际多中心临床研究项目的前期试验结果进行讨论。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2014年，聊城市人民医院凭借雄厚的科研技术实力以及与中国科学院北京基因组研究所多年来深厚的合作基础，与北京大学肿瘤医院、中国医学科学院肿瘤中心、首都医科大学附属北京佑安医院一起，被评选为国际多中心临床试验项目参与单位。该项目以Jack教授的多项独家专利技术为研究基础，主要致力于对癌症恶性肿瘤的发病机制和治疗靶点实现新发现和新突破。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 作为该项国际多中心临床研究参加单位之一，聊城市人民医院承担了头颈部肿瘤、肾癌、前列腺癌等三个领域的试验研究。聊城市人民医院病理科、转化医学实验室、中心实验室、神经外科、眼耳鼻喉医院、肿瘤科、肾内科、两腺外科等多个科室和部门积极参与。通过与聊城市人民医院研究人员就前期试验数据和结果的交流讨论，Jack教授对试验的顺利开展给予了高度评价，预计研究成果2-3年即可应用于临床。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在座谈会上，张彬院长指出，聊城市人民医院参与的该项国际多中心临床研究，具有两大特点：一是合作层次高，参与单位均为国内知名医院，并运用了多项Jack教授个人专利研究;二是实用性强，临床应用前景广阔。项目预期能够实现的新型非化疗抗癌药物的研发，将是聊城市人民医院在转化医学研究方面取得的重大成果和突破。Jack教授和顾明亮教授均对聊城市人民医院的科研实力给予了极大的肯定，并期望医院与布朗大学能够开展进一步的广泛交流与深入合作。</span>
</p>
