	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>李朝辉:白内障手术要趁早</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">白内障是目前中国致盲率排第一位的眼病，但白内障并没有人们印象中的那么可怕，其可控性高，手术治愈率高，但国内白内障手术率相对较低，一方面是科普宣传力度不够，另一方面是大家平时接受到很多误导信息，并不知道白内障的最佳治疗方案就是手术，且白内障手术做得相对早，手术风险小，且术后效果好。本期记者来到解放军总医院（301医院）眼科，请李朝辉主任为广大白内障患者讲讲手术的益处，给大家普及科学知识。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3089/1.png" title="" alt="demo.jpg"/> &nbsp;&nbsp; <br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">李朝辉，男，解放军总医院眼科主任，白内障青光眼组组长。中国医师协会眼科医师分会委员会委员，中国医师协会眼科医师分会白内障专业及防盲专业委员会委员，中华医学会眼科学分会白内障学组委员，全军眼科专业委员会白内障学组副组长。主要从事眼显微外科。目前主攻各类复杂眼前段手术；如白内障超声乳化、青光眼、眼前段外伤的结构重建、青光眼白内障联合手术、各种原因造成的晶体脱位手术等。已做各类白内障手术4万余例。在白内障基础及临床研究中有独到的特色，《白内障系列研究》获军队医疗成果二等奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;白内障最好的治疗方式是手术</strong></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;目前，无论国产还是进口，白内障没有特别有效的药物可以治疗，初期、早期的白内障可以用药抑制，然而对于已经形成、影响视力的白内障是不能用药去除的，唯一有效治疗方法就是手术，这是需要大家明确的，虽然临床医生和科研人员一直在努力希望发明一种可以有效溶解白内障的药物，但并未有突破性进展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;白内障越早治疗效果越好</strong></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;现在国际、国内治疗白内障的最新技术是超声乳化手术，也就是用超声振动把白内障粉碎、乳化掉，然后吸除。因此白内障越在初期，越容易被粉碎掉，长得越“老”，越不容易。白内障长得越“老”，就相对越硬，做超声乳化的时候，用的能量越多，周围组织的损伤越重，所以白内障手术应趁早做。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;超声乳化手术并非每个医院都能开展，有些医院只能开展传统手术，但都可以帮助患者复明，无论哪种方式，白内障都要尽早治疗。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;但患者往往不清楚什么程度的白内障需要手术。医生建议只要白内障影响正常生活或工作了就可以手术。老年性白内障患者还需要进行一系列的检查，让医生帮忙明确是否合并其他眼病。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;白内障会让人看不清楚，暗环境下就容易发生危险，对于老年人最大的危害是跌倒、骨折，这种骨折是会影响寿命的，因为一旦骨折就要卧床或者手术，对老年人的心理和身体的打击都是非常大的。白内障患者因为看不清，活动范围受限，大部分的外界信息都因为看不清而缺失，逐渐就会感到孤独，这也是医生呼吁老年人有了白内障，不要拖着不治疗的原因。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;<strong>&nbsp;&nbsp;&nbsp;白内障手术不可怕</strong></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;白内障手术是比较容易做但又非常精细的手术，对于一个成熟的白内障医生来说，常规的白内障手术5-10分钟就能做完，也不需要特殊麻醉，点3滴眼药水，10分钟之内就可以开刀了，手术切口特别小，跟一元硬币的厚度差不多，不用缝线，手术时间短、恢复快，手术第二天就可以拆纱布，生活、饮食不受影响。另外，这种手术在大的医院可以不用住院，对于换环境睡不好觉的老年人来说是个好消息。解放军总医院门诊也开展此类白内障手术。对于外地患者或身体不方便的患者，也可以住院治疗。一般来说，门诊患者做完手术，休息半个小时到一个小时就可以回家，第二天就可以打开纱布，来医院复查，术后常规用半个月到20天眼药帮助恢复，一般情况下正常生活是不受影响的，只是早期要稍微注意用眼卫生，眼部不要受伤，完全稳定要在1-3个月。跟其他手术相比，方便、快捷。随着中国逐渐进入老年化社会，人们的生存年龄越来越长，白内障发生率越来越高，一定要及时解决，以提高生活质量和安全系数。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;<strong>人工晶体没有寿命限制</strong></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;白内障手术类似给相机换镜头，镜头用了好几十年，从透明玻璃变成毛玻璃，病人会感觉视力下降，看东西都是雾蒙蒙的，这时候换掉这个镜头，就要替换另一个镜头，也就是人工晶体。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;人类社会现代性白内障手术是从1949年开始的，那时候就出现了第一枚人工晶体。经过这么多年发展，人工晶体的发展到很高的水平，目前的白内障手术也称之为屈光性白内障手术。很多患者误以为手术植入的人工晶体只能用几年，因此一直拖着，想要晚点再换。但事实上，人工晶体是没有寿命的，其物理、化学性质非常稳定，可以终生使用。因此不要因为这样的认识误区而耽误手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><strong>&nbsp;&nbsp;&nbsp;&nbsp;人工晶体发展现状</strong></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;人工晶体分很多种，不同的人工晶体还附加了不同的功能，以前人工晶体只解决复明的问题，从看不见到看得见。而现在，人工晶体不仅解决了看不见的问题，还可以提高很多病人的视觉质量，比如解决近视、散光及老花等问题。医生可以根据患者需求及术前检查结果来帮助其选择合适的人工晶体。</span>
</p>
