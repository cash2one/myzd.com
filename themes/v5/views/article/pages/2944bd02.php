<div style="text-align:center;">
	<span style="font-size:18px;line-height:2;font-family:'Microsoft YaHei';"><strong>关爱女性 从“乳”开始&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在汕头大学医学院附属肿瘤医院中，有一支非常年轻但是充满活力的团队，科室成立不到5周年，医生平均年龄仅为32岁，但是他们兢兢业业、团结努力、奋勇拼搏。它就是粤东地区首个综合性乳腺肿瘤防治中心——乳腺中心。为了减少医疗资源的浪费，使患者得到最佳的治疗，促进多专业协作的工作模式发展，肿瘤医院成立了乳腺中心，该中心的特色是集外科、内科、放射治疗科、放射科、检验科和病理科等于一体的多学科综合诊治模式。科室目前在编医师7名，其中主任医师2名，副主任医师1名，住院医师4名。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该中心是广东省医学重点学科，人才梯队合理,技术力量雄厚；严格按照国家卫生部颁布的乳腺癌临床路径、诊疗规范、诊疗质量控制指标，参考国际最新研究动向，制订规范化、个体化、人性化的乳腺癌多学科综合诊治方案；主持并举办多届国家级及省级学术会议，促进乳腺癌诊治新技术新观点的交流。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该中心始终致力于转化性研究，目前已有20项在研及已结题的转化性研究课题，总资助金额近1500万元，培养博士后、博士研究生、硕士研究生多名；2012年获批“广东省乳腺癌诊治研究重点实验室”；在乳腺癌分子影像学研究方向以及临床应用具有雄厚基础，并在世界顶级医学期刊nature系列杂志发表论文。乳腺中心成立四年多来，已发表的学术论文共37篇，其中sci论文17篇。该中心率先开展了对家族性乳腺癌患者及家属进行易感遗传基因brca1、brca2的检测，目前已建立乳腺癌病例管理系统及乳腺癌标本库，为乳腺癌科学研究提供保障。该中心参加多项国际和国内乳腺癌多中心临床研究，获得同行认可。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个优秀的协作团队</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺外科技术难度最大的手术就是重建术了，没有一支团结协作的团队，是无法成功开展重建术的。乳腺中心虽然成立不到5年，但是在大家的共同努力下已经成功完成了近30例重建术。重建术手术时间是普通乳腺癌手术的3倍，也就是近7个小时，是个“体力活”，所以医生的体力至关重要。有了体力没技术也不行，技术更加重要。该中心在极度缺医生的情况下专门委派吴俊东主任医师于2008年8月赴天津医科大学附属肿瘤医院乳腺外科进修乳房重建术半年，问起科室开展第一台重建术的感受时，吴主任记忆犹新地告诉记者：“当时，我与黄主任并肩作战，我主要负责游离皮瓣（重建后的“新乳房”），黄主任一切除患病乳房，我就马上把游离好的皮瓣转移到胸部上去，而黄主任这时候又正好可以缝合我刚刚取皮瓣的地方，这样配合下来就能大大减少手术时间，使患者恢复更快，其他医生负责协助好主任手术，而科室的护士们早早准备好氧气、心电监护、止血药，等待接收术后病人。如果没有这个团队，少了任何一个人，手术都不能既快又好地完成”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该中心黄文河主任一直强调：“我们是一个团队在作战，每个人都很重要，少了谁也不行，每个人都有自己的方向与特色，每个人都有机会赴省内外交流与学习，甚至出国深造。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张国君教授，乳腺中心带头人</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张国君教授，享受国务院特殊津贴，汕头大学医学院附属肿瘤医院院长，乳腺中心学科带头人，毕业于中国医科大学，在日本国立癌症中心和美国哈佛医学院等国际一流研究机构长期从事乳腺癌基础和临床研究，是肿瘤学和外科学双博士。回国后怀着一颗报效祖国的忠心和服务社会的热心，成立了汕大医学院附属肿瘤医院乳腺中心，中心从最初的34张床到现在的51张床，每年新收治200多例乳腺癌病人。他坚持乳腺癌单病种模式，在肿瘤医院率先开展乳腺癌保乳术及保腋窝术，保乳率达20%，居国内先进水平；同时还开展乳腺癌术后乳房重建术，填补粤东地区空白；在国内首创应用自主研发的光学分子影像手术导航系统进行前哨淋巴结活检；他治学严谨，严于律己，在工作中对待病人热诚耐心，急病人之所急，想病人之所想，努力为病人提供高质量的医疗服务，深受患者的信赖。该中心在张国君教授的带领下已建立起基于分子分型及乳腺癌复发转移风险分级的个体化综合诊疗模式，使每一位患者都能获得基于循证医学证据的个体化最佳治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">黄文河主任医师，硕士生导师，汕头大学医学院附属肿瘤医院乳腺中心行政副主任。从事肿瘤外科临床医疗、科研和教学工作，具有丰富的临床经验和熟练的操作技能。擅长乳腺癌外科治疗及乳腺疾病微创手术治疗，特别对乳腺癌的保乳手术、前哨淋巴结活检、乳腺癌术后乳房重建及多学科综合治疗和乳腺良性疾病微创手术有较深的造诣，深得广大患者的好评。</span>
</p>
