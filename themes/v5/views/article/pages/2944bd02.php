<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴俊东主任医师，医学硕士，1998年6月毕业于汕头大学医学院临床医学系，2002年汕头大学医学院普外科硕士研究生毕业,2010年8月至2011年1月在天津医科大学附属肿瘤医院乳腺外科进修，主要擅长乳腺肿瘤的外科治疗和综合治疗，乳腺肿瘤微创手术以及乳腺癌术后乳房重建、乳头乳晕重建和缩乳手术。</span>
