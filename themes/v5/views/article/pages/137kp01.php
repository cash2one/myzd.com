			<p>
	<h1 style="text-align:center;">
		<span style="font-size:18px;font-family:'Microsoft YaHei';line-height:2;">腹主动脉瘤发生病因和诊治</span>
	</h1>
	<p>
		<span style="font-size:18px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腹主动脉瘤是腹主动脉壁的扩张膨出。腹主动脉瘤发生后可逐渐增大，最后破裂出血，导致病人死亡。腹主动脉瘤主要发生于60岁以上的老年人，男女之比为10：3。常伴有</span><a href="http://www.haodf.com/jibing/gaoxueya.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"></a><a href="http://www.haodf.com/jibing/gaoxueya.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高血压</span></a><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病和心脏疾病，但年轻人也偶尔可见。男性多于女性。腹主动脉瘤的发生主要与动脉硬化有关，其它少见原因是主动脉先天发育不良、梅毒、创伤、感染、大动脉炎、Marfan综合征等。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腹主动脉瘤的患病率占主动脉瘤的63％—79％，大多数腹主动脉瘤系动脉粥样硬化所引起，一般位于肾动脉远端，延伸至腹主动脉分叉处，常波及髂动脉、偶尔位于肾动脉以上部位，又称胸腹主动脉瘤，多侵犯肠系膜下动脉分支，在出现破裂和接近破裂前部分患者可没有症状。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其常见的病因有动脉粥样硬化，动脉中层囊性变性、梅毒性、先天性、创伤性及感染性。其中以动脉粥样硬化是最常见的病因。动脉发生动脉粥样硬化后，中层弹性纤维断裂，管壁薄弱，不能耐受主动脉内血流压力而发生局部膨大，形成主动脉瘤。由于动脉瘤承受的血流压力较大，使动脉瘤逐渐扩大，并可压迫临近器官，甚至侵蚀胸骨、肋骨或向体表膨出，成为搏动性肿块。在膨大的瘤部，血流减慢，形成涡流，可产生附壁血栓。患者可因动脉瘤严重压迫重要脏器或自行破裂而死亡，囊性的动脉瘤较梭形的更容易破裂。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">症状：50岁以前少见，最常见于60—80岁之间的男性。　 　1、多数患者无症状，常因其他原因查体而偶然发现。典型的腹主动脉瘤是一个向侧面和前后搏动的膨胀性肿块，约50％患者伴有血管杂音。2、疼痛：为破裂前的常见症状，多位于脐周及中上腹部。动脉瘤侵犯腰椎时，可有腰骶部疼痛，若近期出现腹部或腰部剧烈疼痛，往往预示瘤体濒临破裂。腹部包块：最重要的体征是脐周或上中腹部有膨胀性搏动的包块，除非患者</span><a href="http://www.haodf.com/jibing/feipangzheng.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"></a><a href="http://www.haodf.com/jibing/feipangzheng.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肥胖</span></a><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">，一般均可触及，有压痛及细震颤，还可听到收缩期杂音。股动脉或足背动脉搏动减弱或消失。　3、破裂：可为致命性并发症的初发症状，最常见为瘤破裂，血液从瘤破入腹腔，所幸破入腹膜后腔者更为常见，该部出血较为缓慢。腹痛及失血</span><a href="http://www.haodf.com/jibing/xiuke.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">休克</span></a><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可持续数小时或数天，患者多可就医，偶尔出血局限，患者可有腹痛，发热、轻至中度失血，往往再次破裂。还可破入下腔静脉，产生主动脉静脉瘘，出现连续性杂音，高心排出量及心力衰竭。偶尔可入十二指肠引起胃肠道出血。4、其他严重并发症：瘤内偶可形成急性血栓。腹主动脉瘤血栓或动脉粥样硬化碎片可造成下肢栓塞。十二指肠受压可发生肠梗阻，下腔静脉阻塞可引起周围水肿。继发性细菌感染罕见。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前探查腹主动脉瘤并估计其大小范围的方法有七种：1）腹部触诊，准确性最低。2）腹部X线片：若有典型的卵壳形钙化阴影，诊断多可确立，但至少有1/4的患者无此征象。3）二维超声检查：对腹主动脉瘤的诊断很有价值，操作简便，探查动脉瘤的准确性高，可清晰地显示其外形及附壁血栓等，为目前优选的诊断方法。4）腹主动脉造影：准确性不高，因动脉瘤的宽度可为透光性附壁血栓所掩盖。但造影结果常可提供有价值的资料，故仍为术前必须进行的检查。 5）DSA：其结果类似腹主动脉造影，而无需动脉内注射造影剂诊断经验正在积累中。6）CT：与二维超声波检查相比，可以更清晰地显示腹主动脉瘤及其与周围组织结构如肾动脉、腹膜后及脊柱的关系，以及腹膜后血肿等。但费用较高，操作时间较长。 MRI：其诊断价值与超声波及CT相仿，缺点是费用昂贵，操作费时，但新一代产品成像时间将大为缩短。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腹主动脉瘤不医者可能自愈，最严重本地的后果是破裂出血致死，主要治疗是手术。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疾病预防：首先应积极预防动脉粥样硬化的发生（一级预防）如已发生、应积极治疗，防止病变发展并争取其逆转（二级预防）。已发生并发症者，及时治疗，防止其恶化，延长病人寿命（三级预防）。</span>
	</p>
	<p class="MsoNormal">
		<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腹主动脉瘤主要发生于60岁以上的老年人群，常伴有</span><a href="http://www.haodf.com/jibing/gaoxueya.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"></a><a href="http://www.haodf.com/jibing/gaoxueya.htm" target="http://www.haodf.com/zhuanjiaguandian/_blank"><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高血压</span></a><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病和心脏疾病，但年轻人也偶尔可见。男性多于女性。　该病犹如体内的一颗定时炸弹，一旦破裂，死亡率高达50%-80%。患者多数是在体检时发现的，腹部出现搏动性包块。　手术治疗腹主动脉瘤的5年存活率可达60以上，主要的死亡原因为心脏病、恶性肿瘤以及脑血管意外等，而与腹主动脉的手术无关。近年来由于医疗技术的不断提高，手术的死亡率大大降低，术后并发症也大大减少，择期的腹主动脉瘤的切除手术也成为一种比较安全的手术。</span>
	</p>
