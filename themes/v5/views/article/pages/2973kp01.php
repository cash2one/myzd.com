<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>椎管内肿瘤和脊柱肿瘤有何不同？</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">经常腰酸背痛，人们的第一反应可能更多的是脊柱方面的疾病，比如腰椎间盘突出、腰脱等。但其实，椎管、脊髓也是和脊柱齿唇相依的结构，要是椎管，甚至脊髓长了肿瘤，侵犯到血管和神经了，同样令人痛苦不堪。今天特别邀请到北京301医院神经外科尚爱加大夫谈一谈椎管内肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">众所周知，脊柱是支撑起人体形态的最主要结构之一，脊柱主要由一节节椎骨构成，上下椎骨中间的椎孔相连，就形成了一种管状结构——椎管。椎管内衬有一层层的膜性结构，并有脑脊液浸润，共同将中间的脊髓包裹起来，保护脊髓。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">所谓的椎管内肿瘤就是椎管内的这些组织结构上长了肿瘤。一类椎管内肿瘤是先天形成的，多发生于婴幼儿，在胚胎生长发育时期，出现发育异常，就容易形成椎管内肿瘤。另一类是可能是由于环境污染、放射线等因素导致的，多见于成年人。或者其他部位的癌细胞转移而形成的椎管内恶性肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">然而，椎管内肿瘤往往容易被认为就是脊柱肿瘤，因为二者很难从症状上进行区分。但是，这两类肿瘤的生长位置有明显区别，脊柱肿瘤是指长在脊柱或者椎间盘等处的肿瘤，影响脊椎的正常结构和功能，一般要去骨科或者脊柱外科就诊；而椎管内肿瘤是长在椎管内壁或者椎管内的肿瘤，直接压迫脊髓造成脊髓功能损伤，或者压迫脊髓周围的血管，导致脊髓缺血，甚至坏死，较少影响到脊柱，少数的椎管内肿瘤有可能通过椎管间隙，突出到椎管外，侵犯到脊柱，但这与直接生长在脊柱上或者破坏脊柱的肿瘤，对骨质的破坏程度是不同的。椎管内肿瘤往往需要去神经外科，医生可以处理神经和血管方面的问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">虽然脊柱肿瘤和椎管内肿瘤所导致的症状很难区分，但是在症状出现的早晚上还是有区别的，椎管内肿瘤往往早期就会出现疼痛症状，而脊柱肿瘤运动障碍的情况更为常见，疼痛感出现得较晚一些。</span>
</p>
