			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何摆脱“开胸术后慢性疼痛”的困扰</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">相信许多胸部疾病的患者在经历了开胸手术（也包括胸腔镜手术）之后，多多少少要面临一个后续问题的困扰——切口周围慢性疼痛，令人烦不胜烦。它常常表现为沿伤口周围持续存在的钝痛或烧灼痛，在手术结束2个月后仍顽固地存在，或者虽有减轻但挥之不去；它在咳嗽、抬手（肩）、着床休息以及天气变化时会诱发加重，一部分老年女性患者因为下垂乳房的牵拉而导致该症状尤其明显。这个讨厌的家伙在医学中的术语叫做“开胸术后疼痛综合征”。在这里，作为胸外科医生，我将向大家剖析一下它发生的几率、产生的原因以及怎样预防和治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、 &nbsp; &nbsp; 它常见吗？有什么趋势或影响？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">非常遗憾地告诉您，开胸手术和截肢术一起，被认为是导致术后慢性疼痛最常见的手术。据文献报道，开胸术后慢性疼痛的发生率高达52%（其中轻度32%，中度16%，重度4%）。通俗地讲，如果有10个人开胸做手术，一半以上都会发生术后切口慢性疼痛。但是，随着时间的推移，一部分患者的症状可以自行消失或减轻，多数是在手术半年到1年以后，而其中少数人可被疼痛纠缠长达4-5年。慢性疼痛可因情绪、天气变化以及体位变动而诱发，严重时会影响患侧上肢活动，而且也会导致焦虑、抑郁等精神障碍，使患者的情绪和行为发生消极改变，显著影响患者术后的生活质量。因此，包括胸外科、麻醉科医师、患者及其家属，都非常有必要对开胸术后慢性疼痛加强关注，采取多种措施，包括药物治疗、积极预防，甚至是必要的人文关怀，以缓解它带来的诸多困扰。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、 它产生的原因是什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;继发于肋间神经损伤后的肋间神经瘤：是目前认为的最主要的疼痛原因。很多患者经历的，沿胸部切口呈半环形分布的感觉异常和自发性灼痛或钝痛，是典型的神经性疼痛，是由损伤的肋间神经皮支（感觉神经）愈合形成的神经瘤所致；肋间神经的损伤，一部分是和关胸技术相关的，如间断肋间缝合时将肋间神经困于其中，压轧神经造成持续性疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;正在愈合的肋骨骨折或骨裂（术中因病变侵犯或撑开器过度撑开造成肋骨损伤）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;局部感染与胸膜炎：由于胸膜神经分布的密集和特殊敏感性，胸膜表面形成的炎性粘连或胸膜炎，会在相当长的时间内引起疼痛症状，特点为间断、时好时坏，位置较深在的固定区域疼痛，用患者自己的话表达就是“在胸腔里面，哧哧拉拉地疼，持续存在”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;肋软骨炎与肋软骨脱位：也多与肋骨撑开过度有关，肋软骨位于前胸，故疼痛常位于胸壁靠前侧。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;“肌筋膜疼痛综合征”：原因是由于切口周围的肌肉、筋膜组织损伤后的不良修复，即局部炎症、神经异位增生及瘢痕等形成而导致。该疼痛的特点是：部位较固定、且胸壁表面有明显触痛点，轻微的抚摸都会带来针刺或灼烧样的痛感。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;遗传易感性和心理原因：研究发现，每个人在体质上对疼痛的敏感性是不同的，表现为痛阈较低，对疼痛反应强烈；另外，女性、焦虑性格等心理因素也会影响患者对术后疼痛有不同程度的主观感受。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7 &nbsp; 肿瘤局部复发：肿瘤生长会直接侵犯胸壁、肋骨、胸膜等，压迫肋间神经、刺激骨膜，造成持续的、进行性加重的痛感，也需要引起足够关注和警惕。</span><br />
