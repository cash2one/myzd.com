		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>眼内炎的早期诊断与治疗（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">眼内炎是一种少见但极为凶险的感染性致盲眼病，诊治不当往往会带来灾难性的后果，轻者丧失视力，重者摘除眼球甚至波及颅内，危及生命。从病因方面可分为外源性和内源性，前者主要发生于手术或外伤后，后者则为转移性。由于近年来抗生素和激素的普遍应用，眼内炎的病原微生物菌谱发生了一定的变化，耐药菌株不断出现，增加了眼内炎诊疗的困难。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">眼内炎是临床上极为凶险的致盲眼病。早期诊断，针对病原准确用药是关键。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">早期眼内炎的临床表现有以下几点：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">①不明原因患眼畏光，疼痛、流泪。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">②视物模糊，视力迅速下降至手动、光感。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">③眼压正常或降低。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">④角膜后弹力层皱襞，KP明显。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">⑤前房浮游细胞增多，房水闪光明显。前房纤维素性渗出，前房积脓。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">⑥眼底红光反射消失，双目间接检眼镜下不能辨认视乳头。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">⑦超声波影像学检查：可见均匀一致点状致密玻璃体混浊物。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">⑧房水或玻璃体的微生物学检查可检出病原菌。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">眼内炎是葡萄膜与视网膜的化脓性炎症，是十分严重的眼内感染。由于眼球对感染的自然抵抗能力差，眼内结构易受细菌、真菌、病毒及其毒素所致炎症的损害，各种方法常较难控制感染的发展，可致患眼失明。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">临床换衣眼内炎的患者，请及时到正规医院检查就诊，若基层医院没有条件，迅速转到有玻璃体手术条件的医院治疗。</span>
</p>
