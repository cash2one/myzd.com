			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>双侧人工耳蜗植入问答</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">双侧植入对听力的改善与单侧植入相比有多大差异？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根据现有的大量关于双侧植入的文献统计，客观听力学检查结果显示：1）双侧植入的听阈比单侧植入的更低，越低意味着听力越灵敏，双侧植入的听阈一般均可达到正常人的听力，即20分贝以内。2）、双侧植入者在安静环境下的言语识别比单侧植入高约18.7%，也就是说同样的环境下，100个词语里面，双侧植入者比单侧植入者多听懂18-19个。3）双侧植入者在噪音环境下的句子识别：当噪音和言语来自同一侧耳时，双侧植入比单侧植入的句子识别高31.1%，，当噪音和言语来自不同侧耳时，双侧植入比单侧植入的句子识别也高出10.7%。这意味着当处于嘈杂环境时，双侧植入更能实现真正意义上的自由交流。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3219/11.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">双侧植入是不是意味着将来无法享受未来更新技术？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早期都提倡先植入一侧，留着另一侧享受未来新技术。这在当时是非常合理的提法。但目前的情况看来，基因治疗等新技术仍需要等待至少10年，而10年后，无声音刺激的一侧神经功能会明显退化。因此，留下单耳不加干预等待新技术的提法是缺乏现实意义的（据统计，单侧植入者对侧长期佩戴的助听器的比例小于10%）。最新人工耳蜗都采用柔软技术设计的软电极、超软电极更加柔软、纤细和轻薄，就是专门为保护耳蜗微结构而设计的，现在临床的微创植入技术都很成熟，都可以采用圆窗进路进行微创植入，术后蜗内结构保存完好，不影响未来新技术的应用（科普常识：人工耳蜗电极植入在耳蜗的鼓阶腔，而毛细胞等听力相关结构在蜗阶腔的基底膜上，因此微创手术本身对基底膜和毛细胞等听力结构没有直接影响）。并且，只有现在通过声音刺激能保持听神经和中枢的活性，对未来技术的应用才有意义。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3219/12.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">双侧同期植入对孩子的损伤和手术风险很大吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人工耳蜗植入术属于中小创伤手术，组织损伤少、出血量少、麻醉时间短。最新人工耳蜗的植入体很小、切口3-4cm就能良好的植入。不管双侧植入还是单侧植入，对孩子没有特别的损伤。手术风险通过精细操作（Soft手术规范）也在有效的控制范围之内，因此手术技术发展到现在，是可控的也是安全的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3219/13.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">双侧先后植入间隔时间较长的两侧听觉能匹配吗？有意义吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">间隔时间较长的双侧植入与同期双侧植入相比，要达到两侧匹配需要一个过程。因为毕竟后植入侧较长时间没有听见过声音。后植入侧需要多长时间才能跟先前植入的一侧匹配呢？这跟第二侧的调机是有关系的。调机可以使两侧响度水平都调到患者能耐受的最大范围时，两侧响度水平相当，平均3-6个月的适应时间都能达到理想的听觉水平。双侧植入给使用者的生活、工作带来更大的助益，不管是噪音环境、安静环境，言语交流能力和音调识别、音乐鉴赏都有更大提高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3219/14.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、双侧植入之后两边都戴上体外机很难看吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人工耳蜗体外机的设计也更加小型化、美观化。最新的人工耳蜗处理器非常小巧的，多种人性化的佩戴方式可让处理器佩戴在其他位置，如女孩的发卡式，男孩的帽子式，都可以很好的隐蔽处理器。未来的体外机发展有两个方向：一体机、耳内机。等到孩子再大一些，对于美观和形象有更强烈需求时，正好可以赶上这些技术。双侧植入强调早期植入，以达到两侧最佳的匹配和最佳的效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3219/15.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除了听力得到更好的改善，双侧植入对孩子还有其他好处吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">研究证明：双侧听力的孩子除了在听力和语言能力上优于单侧听力的孩子之外，在社交、心理、情感、成才模式上也都优于单侧听力孩子。数据显示：单侧听力的儿童中，（这里指一侧耳完全正常，另一侧耳聋）35%以上的儿童，至少留级一年。60%以上的儿童，在学习上需要额外的辅助。此外，一侧大脑没有声音刺激，左右脑发育不均衡，这一人群整体的智力水平较双耳听力的孩子差。由此造成的社交心理情感障碍，成才模式上的区别是不可忽视的。双侧植入恢复的是孩子双耳的听力，学习和工作交流相对单侧还是要轻松很多，如果家长朋友对孩子的定位和要求比较高，及时的双侧植入对孩子的发展有明确的助益。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">双侧植入在植入后日常生活中有没有特殊注意事项？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">日常生活中，双侧植入的孩子的注意事项有两点：1）、两侧处理器的程序是不能交替使用的，家长要把两侧处理器要做好标记，不要佩戴错了。2）、双侧佩戴尽量置于双侧方向或双侧耳边，这样对孩子全方位听声、方向辨别更有好处。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8、双侧植入的孩子调机时有没有特殊注意？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 调机时，家长应向调机师分别反映两侧的听觉表现，如单耳的言语识别情况、听敏度情况等，并且反映孩子日常的声音定位情况。以辅助调机师调整程序。</span><br />
