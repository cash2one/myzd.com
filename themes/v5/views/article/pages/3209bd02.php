	<br />
</p>
<img src="http://static.mingyizhudao.com/147010447125260" title="" alt="demo.jpg"/><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">作者简介：王涛，解放军第306医院，神经外科。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><strong>疾病简介</strong></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脑囊虫病是中枢神经系统最常见的寄生虫感染疾病，是猪肉绦虫的幼虫（囊尾蚴）寄生于人脑内引起的疾病。脑囊虫发病率较高，约占囊虫病的60%～80%，潜伏期较长，数月到数十年不等。脑囊虫病常引起机体的神经功能障碍，临床表现有癫痫、精神症状、头痛、恶心呕吐、脑膜刺激症状等，复杂多样。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">要想通过外科手术的方法取出囊虫，如何精准地定位病灶、选择手术路径是医生面临的重要问题。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">通常，单纯凭借肉眼观察医学影像和粗略的数值计算得到的定位精度不够，导致开颅手术时间很长，病人创口大，后遗症多。对于病灶相对较深的病例，医生利用显微镜观察、用双极电凝切开脑皮质寻找病灶则会更加困难。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">新方法的出现</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1983年，Kelly首先提出立体定向开颅手术的概念，与传统开颅手术相比，这种方法精准度提高，神经功能障碍的发生率可由10%下降至4%，切口并发症发生率可由8%下降到4%，住院天数也相应缩短。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">随着科学的发展，结合计算机技术、机器人学和临床需求逐渐发展起来的神经外科医疗机器人可以根据CT、MRI等医学影像辅助医生进行更加精准的定位和导航，机械臂直接抵达医生在计算机软件内三维模型上选择的最佳入颅点，这意味着患者的开颅创口会尽可能地缩小，更有助于术后康复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><strong>病例解析</strong></span><br />
<img src="http://static.mingyizhudao.com/147010447159672" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">11月9日，在解放军第306医院展开了国内首例机器人辅助的立体定向脑囊虫摘除手术，患者为44岁男性，脑部MRI图像如下图，可清晰看见位于患者顶叶的囊虫。由于囊虫位于功能区，传统的开颅手术后易引发患者并发症，比如瘫痪。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术的主刀医生是解放军第306医院神经外科的主任医师赵全军主任，他从事神经外科治疗三十余年，除神经外科常见疾病，如颅内肿瘤手术切除以及脑出血、脑外伤、脑积水等外科治疗外，赵医生在难治性癫痫的外科治疗、 脑功能性疾病的立体定向手术治疗以及机器人辅助无框架定位脑手术方面尤为擅长。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">本次手术使用到的医疗机器人是我国自行研发的 Remebot 神经外科手术机器人。上午9时许，患者头戴 Remebot 标志点扫CT图像，文件导入机器人的计算机软件内后，自动生成患者头颅的三维模型，医生根据模型中所示的病灶位置选择好最佳入颅点和入颅角度。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">完成机器人和病人注册步骤后，病人与机械臂的相对关系建立，在计算机软件内操作控制机械臂分步骤完成走位，医生根据机械臂末端指向的靶点位置确定开颅创口的具体位置，并用笔标记在患者头皮。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">紧接着，针对患者头部、机械臂末端操作平台进行全面消毒，并做好开颅手术所需全部术前准备。机械臂再次走位，确认到达病灶点后，医生先利用机械臂末端的操作平台钻孔，确定病灶深度，然后将机械臂归位，开始实施小创口开颅手术。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">环钻去除颅骨作小直径骨窗，剪开脑硬膜后显微镜下沿脑沟回切开脑皮层，避开重要血管和功能区，找到脑囊虫病灶后完整摘除，残腔反复冲洗后，敷贴可吸收止血纱布，缝合脑硬膜后用颅骨锁固定颅骨骨瓣，缝合头皮层。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">此病例完成后，赵全军医生接受了中央电视台新闻栏目的采访，并在节目中介绍了医疗机器人未来在远程医疗上的更多可能性。</span>
</p>
