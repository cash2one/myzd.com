<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><strong>医生简介</strong></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">赵全军，主任医师，教授，博士，研究生导师。1981年进入第二军医大学军医系学习并入伍，后师从于我国癫痫外科四大创始人之一的刘宗惠大师，1992 年毕业获医学硕士学位并留海军总院神经外科工作。2012 年底至 2013 年初作为医院领军人才赴美国克利夫兰医院癫痫中心与梅奥医院神经外科学习，按期回国，现任解放军第306医院神经外科主任医师。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1998年起， Remebot 在海军总医院研制的时候，赵医生就参与了机器人系统的研制与临床应用，累计实施手术3000余例。2015年，解放军第306医院神经外科正式引进了 Remebot 医疗机器人，并与 Remebot 继续合作开展脑立体定向微创手术相关的临床研究。</span>
</p>
