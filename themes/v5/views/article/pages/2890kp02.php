			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肾脏错构瘤---值得重视的良性疾病</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;肾错构瘤又称肾血管平滑肌脂肪瘤（ANGIOMYOLIPOMA），为肾脏良性肿瘤，主要成分为壁厚的动脉瘤样血管、平滑肌、不同程度的成熟脂肪组织组成。在肾脏肿瘤中不到10%。发病年龄多为20-50岁，女性多见，大多数是体检发现后来就诊，少数患者是因为错构瘤自发性出血产生腰痛或休克等症状来就诊。近年来发病率有增高趋势，可能与诊断技术水平提高有关。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>肾错构瘤是如何诊断的？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 肾错构瘤大多数是体检B超发现再通过进一步的肾脏CT或MRI检查明确诊断，如果肾错构瘤伴有出血的发生有时诊断会产生困难。肾错构瘤的B超表现为高回声的团块，肾脏CT表现为低密度（CT值为负值）的占位性病变，肾脏MRI表现为含有脂肪成分的T1高信号，T2低信号。B超、MRI或CT检查提示瘤体内有脂肪成分者，基本可以确诊为肾错构瘤，因为肾癌无脂肪成分。肾错构瘤由于其成分含量不同，特别是脂肪含量，各肿瘤差异较大，从而导致术前误诊。以下几种情况易误诊为肾癌，应仔细鉴别：(1)瘤体内有较多出血，肿瘤密度增高，掩盖了脂肪组织；(2)肿瘤直径在1.0～1.5cm时，由于容积效应的影响，不能反映肿瘤的真实密度；(3)以血管平滑肌组织为主，含极微量脂肪组织的肾错构瘤。根据本人经验对可疑的肾错构瘤，可以采用CT薄层扫描，或MRI增加压脂序列，如果测到脂肪成分，则有助于诊断，以提高诊断的正确率</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp; 肾错构瘤如何分类？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;肾错构瘤主要分二类，一类是非遗传性的错构瘤，一般是独立的疾病，肿瘤大多是单发病灶，大多数患者均属于此类。另一类是遗传性的错构瘤，可伴结节性硬化，此系常染色体显性基因遗传的家族性疾病，80%病人脸部有蝴蝶状皮脂腺瘤，其他器官如脑、眼、骨、心、肺亦有病变。大脑发育迟缓、智力差、有癫痫发作也为其疾病的特点，肾脏瘤体临床特点为双肾多发病灶，80%为女性，出现病状在20－50岁，40岁以后占多数。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp; 如何选择肾错构瘤治疗？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;肾错构瘤的治疗方法包括观察等待、选择性肾动脉栓塞和外科手术。手术的目的在于切除肿瘤、解除肿瘤引起的症状、预防自发性破裂、保留肾单位、排除影像学怀疑恶性肿瘤的可能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.对于诊断明确的错构瘤直径&lt;4cm的无症状肿瘤患者，可进行观察而不一定要进行治疗，每年进行CT或B超检查了解肿瘤变化即可。如果有症状或有并发症者，如出血、疼痛、血尿、感染、积水等也应手术治疗。影像学怀疑有恶性肿瘤的可能而术前诊断困难者，应积极手术探查，可根据冷冻切片检查结果行手术治疗而不主张随访。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.对于肿瘤直径&gt;4cm，尤其是外向性生长的肿瘤而易发生自发性破裂，或肿瘤进行性增大有症状的肿瘤患者，均应采取积极的手术治疗措施。根据肿瘤大小、部位行肿瘤剜除、肾部分切除或肾切除术。因肾错构瘤的发生为多中心源性，双肾同时或先后发生的机会较多，应尽量行保肾手术，尽可能保留正常肾组织，手术时不宜轻易地切除肾脏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.大的错构瘤伴有出血者，往往与周围脏器紧密粘连，因难以行肿瘤剜除或肾部分切除术，大多数只能选择切除肾脏，所以对于易于出血倾向的错构瘤还是选择积极的治疗，避免出血后处理，增加保肾手术的难度</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.错构瘤伴有急性出血，生命体征不平稳者或者肿瘤多发、特别巨大不能手术切除肿瘤可以先考虑选择性肾动脉栓塞，栓塞多选用超选择性肾动脉分支栓塞，以保护部分肾功能，有急性出血者待栓塞后出血停止，血块机化与周围脏器粘连好转（一般出血后3月）再考虑手术切除肿瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.少数病例可有局部及淋巴结侵犯，甚至瘤栓侵入大静脉，呈恶性行为表现，应行根治性肾切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6. 对于双侧病变导致肾功能衰竭或肿瘤破裂出血而必须行双侧肾切除的患者可以选择血透或肾移植。</span><br />
