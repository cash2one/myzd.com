	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>心肺一起换 湖南省首例心肺联合移植手术成功</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">２０岁的郑先生现在的心脏和肺都曾经是别人的，但现在它们“贴心贴肺”</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;地为他服务了。中南大学湘雅二医院昨日宣告，我省首例心肺联合移植手术已经获得成功。郑先生是我国第１５例接受心肺联合移植手术的患者，而此前的１４例手术只有２例术后生存期超过１个月。郑先生这次手术的成功标志着我省胸心外科技术已达到国际先进水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 手术　９月２０日顺利进行</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 来自邵阳的郑先生在８年前就感到活动后经常心慌气促，这种情况近５年来日趋严重，即使不活动嘴唇也发紫，而手指和脚趾逐渐长成了杵状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 今年９月１０日，郑先生到湘雅二医院就诊，被确诊患有先天性心脏病中相当严重的“艾森曼格综合征”。其心功能和肺功能已非常差，极可能在短期内死亡，唯一有效的救治办法是进行心肺联合移植——将病变的心脏和肺取出来，换上健康的心脏和肺。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> ９月２０日，湘雅二医院成功地对郑先生实施了心肺联合移植手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 全球　此手术不到３０００例</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 临床首例成功的心肺联合移植手术１９８１年在美国斯坦福医学中心完成，目前全球接受该手术的患者不到３０００例。我国上世纪９０年代开始开展心肺移植手术，到目前已进行１４例，其中术后生存期超过１个月者只有２例，存活时间最长者到目前已有１１０多天。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 湘雅二医院李永国副院长介绍说，心肺联合移植术牵涉到多个学科，只有整体技术水平达到一定水准的医院才具备开展该手术的能力。该手术由该院胸心外科、呼吸科、心血管内科、移植中心、麻醉科等１２个医技科室紧密配合完成，每天必须完成的循环功能、呼吸功能等各项检查就有２０多项，手术成功说明湖南的胸心外科技术在国内领先、达到国际先进水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 细节　每个都要“零差错”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术组主要成员、湘雅二医院胸心外科周新民教授，向记者介绍了扣人心弦的移植手术过程：麻醉之后的患者躺在手术台上，他自己的心脏和肺还在“坚守最后一班岗”，所有的急救设备都已准备到位。此时，手术组的另一部分成员从一名已脑死亡的志愿者体内取出了心脏和肺，在特殊的容器和特殊的保护液中以最快的速度向手术室送来。当两名医生跑步将一个装着心肺供体的密封蓝色桶子抬入手术室的时候，手术台上医生刚好切开了患者的胸腔。近３个小时后，新的心肺顺利移植到了郑先生的胸腔内。３０多分钟后，移植心脏复跳，新老心肺顺利“交接班”。术后一小时，郑从麻醉中清醒。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 周新民说，手术过程的每一个细节都不容许出现任何闪失，必须做到“零差错”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 患者　“父母给了我力量”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 昨日，记者通过电话与尚在胸心外科特别监护病房接受治疗的郑先生通话。郑非常乐观，觉得他身上发生的是奇迹：他手术后４０小时就可进食；目前，他在监护病房看电视、看书，可以随意走动，他相信自己的身体会越来越好。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 郑先生说，他的父母给了他莫大的精神支持。手术后３８小时气管插管拔出后，郑先生讲的第一句话是“爸爸”。术后第四天，郑的母亲穿隔离衣进入病房，母亲的出现增强了他战胜疾病的信心。郑先生说：“感谢父母给了我力量，感谢所有为我操心的医护人员使我获得了新生。” （频道责编　李玉梅）</span>
</p>
