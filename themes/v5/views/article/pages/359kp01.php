			<div style="text-align:center;">
	<strong>体检发现肾囊肿，我该怎么办？</strong>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>一．肾囊肿是如何形成的？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大多数单纯性囊肿大多是后天形成，最初是由于肾小管梗阻堵塞形成小憩室，然后液体不断积聚，憩室不断增大而形成囊肿。而多囊肾则主要由于先天遗传性因素，存在基因缺陷，导致两侧肾脏出现无数个大小不等的囊肿。中医则认为：肾囊肿多是由于先天遗传或后天失养，肾气受损，络脉不和，致肾脏血水内聚而产生的内脏囊性疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>二．肾内出现囊肿危险吗？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">绝大多数肾囊肿无症状，对人体健康通常也不会有很大的影响。部分患者可因囊肿内压力增高、囊肿出血、伴发感染等诱因而出现以下症状：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1）腰、腹不适或疼痛：疼痛的特点为隐痛、钝痛，固定于一侧或两侧，向下腹部及腰背部放射。如有囊内出血或继发感染，则使疼痛突然加剧。如合并结石或出血后血块阻塞尿路，则可出现绞痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2）血尿：可为镜下血尿或肉眼血尿，可呈周期性。发作时腰痛加剧，剧烈运动、创伤、感染可诱发或加重。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3）腹部肿块：以前是患者就诊的主要原因，60％～80％的患者可触及肿大的肾脏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4）高血压：可因囊肿压迫肾脏，造成肾脏缺血而引起高血压，肾功能减退时，高血压的发生率更高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5）蛋白尿：可在部分病人中出现，一般尿蛋白的量不多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6）肾功能减退：由于囊肿长期压迫肾脏实质，使正常肾组织显著萎缩，肾功能进行性减退。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7）肾囊肿恶变，形成肾癌，约有3%～7%肾囊肿可能会恶变，需要及时发现并积极治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大部分小囊肿不需要特殊处理，但是任何以上症状在病情严重时都会对身体健康有影响。我们的意见是在专业医生的指导下决定是否该对您的囊肿“下手”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>三．肾囊肿需要治疗吗？治疗方法主要有哪些？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果囊肿较小，而且患者没有任何不适的症状，可以暂时不做任何治疗；但要定期复查，观察囊肿是否继续增大，以及增大的速度如何。有症状或囊肿较大（直径&gt;100px），在明确排除恶性肿瘤后，首选采用腹腔镜肾囊肿去顶减压术——将囊肿的“屋顶”掀掉，不让囊液积聚。其优点是创伤小，复发率低。在怀疑囊肿有恶变可能时，更应及时手术探查，如果有必要，应完整切除肾脏占位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>四．如何判断肾囊肿恶变？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床医生可以通过肾囊肿的B超和CT表现，对肾囊肿进行分级，该分级系统被称为“Bosniak分级系统”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">I级囊肿：单纯良性囊肿，囊壁薄、无分隔或钙化，囊液为水；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">II级囊肿：良性囊肿，伴有细小分隔和或钙化点，分隔无强化；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">III级囊肿：囊壁厚，囊内多分隔和钙化，分隔有强化，超过50%为恶性囊肿；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">IV级囊肿：囊肿内含有强化的软组织成分的恶性囊肿。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于I~II级囊肿，可以选择观察随访，而III~IV级囊肿，应该尽快手术探查和切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以，如果您在体检时发现自己肾脏上多出了一个囊肿，请一定要来我们医院让专业医生帮您读读片哦！</span><br />
