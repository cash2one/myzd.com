			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肾肿瘤治疗又一新选择----射频消融</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">采用射频消融技术治疗良性与恶性肿瘤是90年代初兴起的一种新技术。1990年McGahan和Rossi等先后报道了用射频消融术治疗肝肿瘤，从此这项技术就在世界范围内引起了广泛的兴趣。各国都先后开展了射频消融术的临床应用，我们科室已经开展近百例腹腔镜肾肿瘤射频消融手术，取得良好的手术效果，特别是对于小于3cm的小肾癌用普通手术有高度风险的患者尤其合适。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">射频消融治疗肾肿瘤的途径选择</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前根据射频电极达到肿瘤组织的途径不同分为开放、腹腔镜和经皮穿刺三种。其中开放和腹腔镜途径均在直视下进行操作，具有直观、定位精确、易于保护邻近脏器、可取病理活检等优点，经皮方法简便易于重复治疗、经济、恢复周期短、患者易于接受；但其局限性在于受肿瘤的位置及毗邻器官的限制，临床上通常借助影像技术，如CT、MRI、超声等引导下来完成此操作。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;射频消融治疗肾肿瘤的适应症与禁忌证</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 对患有肾癌者行一侧肾脏根治性切除或部分肾切除可导致肾功能不全的危险者，均可以考虑应用射频消融治疗，例如孤立肾性肾癌；一侧肾癌已切除，对侧肾有癌转移或新发癌；单发转移性肾癌；双侧肾癌（特别是具有家族遗传趋势的肾多发性肿瘤综合征患者，如Von-Hippel-Lindau疾病及遗传性乳头状肾癌）。近来国外学者研究表明小肾癌也可以行射频消融治疗，并且无明显肿瘤残留和复发。对于年老体弱，伴有严重心脏病、糖尿病等无法承受麻醉、手术创伤的肾癌患者，也可采用射频消融治疗，使这些以前“无药可治”的患者有了新的治疗方法。</span><br />
