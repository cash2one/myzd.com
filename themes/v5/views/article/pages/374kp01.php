<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">气胸术后可以坐飞机么？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;很多气胸的病人都会问到这样的问题，我可以做飞机么？我什么时候能坐飞机？虽然乘坐飞机本身并不增加气胸发生风险，但在高空上可加重气胸病情，后果严重，故对于未行胸腔闭式引流的气胸患者应免乘坐飞机，需经治疗或者影像学资料提示气胸吸收消失后方可乘坐。而对于既往有气胸发作史的人，则需根据气胸复发可能性，以及其对于气胸发作的耐受程度决定是否乘坐飞机。英国民用航空局允许气胸患者行胸腔闭式引流治疗成功两周后乘坐飞机。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">气胸术后可以潜水么？&nbsp;</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 由于水下活动可增加气胸复发率，且在潜水上升过程气胸量又会加大，增加张力性气胸发生风险，因此英国胸科学会发布的指南建议对于未行确切方法（如：肺大疱切除术）治疗的患者应终生避免潜水。而对于专业潜水员，气胸发作后需行肺大泡切除术，方可重新开始潜水。如果你发作过气胸，只进行过保守治疗，或者只做了胸腔闭式引流术，而没有做肺大疱切除术，尽量远离潜水这项活动吧。</span>
</p>
