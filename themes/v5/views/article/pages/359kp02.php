	<strong>肥胖真的会增加患肾癌的风险吗？</strong>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 很多人可能不知道，肥胖的人可能更容易发生肾癌。来自著名医学杂志《柳叶刀》的文献显示BMI（身体质量指数）每上升5kg/m2，男女性肾癌的发生率就会分别上升1.24和1.34倍。然而深入研究后就可以发现，我们并不能给所有“胖子”都打上容易得肿瘤这个标签。原来肥胖可分为中心型肥胖和外周型肥胖，其中中心型肥胖才是罪魁祸首，他的主要表现是内脏脂肪堆积。打个比方，看看您的周围，那些挺着啤酒肚的就是典型的一类中心型肥胖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">复旦大学附属肿瘤医院泌尿外科的医生们在对500多名患者的长期的观察中就发现，肾癌中最常见的透明细胞癌患者竟然有不少都是这种中心型肥胖患者。平均下来，每个患透明细胞癌的患者的内脏脂肪面积可以比患其他肾脏肿瘤的患者大25平方厘米，而他们之间皮下脂肪含量却相差无几。在校正了性别、年龄、肿瘤大小、BMI、吸烟史、高血压史、糖尿病史等混杂因素后，内脏脂肪仍然与肾脏透明细胞癌密切相关。巧合的是，内脏脂肪堆积的人往往同时并发糖尿病、高血压、高血脂等代谢性疾病，医学上称为代谢综合征。造成这个现象的原因可能是因为内脏脂肪除了储存能量外，还担负了一部分内分泌的功能，它们所分泌的很多细胞因子或许是造成这一系列结果甚至发生肾癌的罪魁祸首。我们科室的这些研究成果已经发表在国际泌尿外科杂志British Journal of Urology International上，获得了广泛的认可（2014）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着年龄的增长，似乎内脏脂肪的堆积在所难免。很多中年男性发现自己渐渐有了“啤酒肚”，很多中年女性发现自己曾经引以为傲的水蛇腰已无影无踪。可是，积极的锻炼，远离暴饮暴食，保持健康生活习惯仍然对保持正常身体代谢和一个健康的身材至关重要。在饮食方面，我们有如下建议：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1）多吃蔬菜、水果和粗加工的主食，多摄入富含维生素A的深色蔬菜，富含维生素C的水果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2）坚持适宜的体力活动，积极锻炼，保持体重。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3）严格戒烟，少饮酒。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4）限制高脂肪饮食的摄入，避免吃“肥肉”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5）远离油炸、油煎食品，减少黄油、奶油在食谱中的份额。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">远离癌症，从保持身材开始！</span>
