<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>30名中国军人 拯救一个国家</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>——观察者网专访解放军援塞医疗队</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><br />
</span>
</p>
<p align="center">
	<br />
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟访谈录</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟（解放军302医院肝胆外科主任、援非医疗队外科负责人、医院改建现场指挥）</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:您去之前对埃博拉是怎么看的？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:埃博拉的传染途径到现在还不明确。虽然国内还没有过埃博拉病例，但我们去之前经过充分的思想上理论上的准备。查阅文献资料，研究数据，我观察到埃博拉的扩散并不快，呼吸道感染的可能性不大;传染方式也不是成群传染，判定一般也不通过消化道传染。总的来说，只要不直接接触患者和体液，感染风险就不会太大。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">基于这个认识，我们到了驻地就动手改造医院，尽管不通过呼吸道。消化道传染，但我们还是按照最严重的情况考虑，兼顾预防呼吸道、消化道传染病的要求改建医院。那里本来有一个小型综合医院，现在我们要建一所初步符合收治各类烈性传染病的专科医院。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:改造之后的医院是理想的传染病医院吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:不能那么说，只能说是把我们的想法搬过去，对现实条件做了一定的妥协。从操作上说，我们做到了污染区、清洁区各占一栋楼，中间还有整整一栋楼做缓冲区，供我们穿脱防护服、洗澡、更衣、喷淋。简单点说就是:流程要合理。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三栋楼都有两层，理想做法是所有医务人员，包括中方和塞方，都只走单行线。从第三栋楼二层清洁区出发，经过第二栋楼二层缓冲区，到第一栋楼二层及一层污染区工作，工作结束再从第一栋楼一层污染区，经过第二栋楼一层缓冲区，再到第三栋楼清洁区回来，符合传染病房的设计规程。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">但实际操作中发现，塞方人员很难做到单向走行，经常不分清洁和污染来回穿梭。再加上宗教习惯，洗澡更衣的习惯等和我们不太一样，操作也不规范。为了避免交叉感染，也尊重他们的风俗习惯，我们只能把中塞双方的路线分开。从清洁区到污染区都走同一条路线，回来的时候，我们从二层沿原路返回缓冲区，再到清洁区。他们从一层按照原来的设计路线返回。实际上，我们把危险留给了自己，把方便和安全给了塞方。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一般设缓冲区，就是空间上分隔一下。这次考虑到传染方式不是很明确，我们把缓冲区还分了好几层。穿着防护服走到第一个缓冲区外面，先喷淋含氯消毒剂，然后脱掉第一层防护服，第一层手套，进入第一缓冲区。在第一缓冲区脱掉第二层防护服、面屏、鞋套和第二层手套。到第二缓冲区脱雨鞋、眼罩、帽子和第三层手套、换拖鞋。出了缓冲区再去洗澡间，脱内层防护服，洗澡更衣，进入清洁区。这几层缓冲区，每一层之间都有物理隔断，用木板和门隔开，每一步操作完成后，都要用含氯消毒剂重新洗手。从硬件上说，基本靠得住。可以说，设置了两个缓冲间，准确的说，加上第一缓冲间外面喷淋，我们有三个缓冲间。这是我们这次病房改建上的一个亮点，一个创举，突破了世界卫生组织及国家疾病控制中心有关传染病病房设置的规范，大大减少了传染病传播的可能性。简单的概括一下就是:分区分层穿脱防护服。</span>
</p>
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:改造医院的过程顺利吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:李进队长是总指挥，负责流程及施工图纸设计，我具体负责整个医院的具体改造过程，也就是监工。从打隔断到装喷头，一件件落实。总的来说还算顺利，中资机构帮了我们很多忙。但是当地人实在是靠不住。当时留守医院的人全跑了，我们只能把门全砸开，再重新配锁。那都是十几年前中国施工队安装的锁，换到合适的锁芯很不容易。但请的锁匠今天推明天，明天推后天，最后一天马上就要接病人了，这个医院原来的塞方院长亲自去找，才把锁匠找来。最后还是配不齐，只能有什么用什么，有的地方用挂锁，有的地方在门上开一个洞，用自行车的挂锁凑合锁上。我们给第二批交班的时候，也是交的这么一串千奇百怪的钥匙。</span>
</p>
