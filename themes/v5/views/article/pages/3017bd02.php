	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:管理这些病人不容易吧？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:这里也有很多细节问题。非洲人的脸在我们看来都一样，隔着防护服就更看不清。甚至他们的名字我们也念不出来，读音听起来都一样。更有意思的是，他们重名重姓的人很多，刚开始我们咋都对不上病人，费了好大劲才知道重名这回事。后来我们到旧货市场买了一个拍立得相机，入院就拍照，把大照片贴在病历上，每次查房都带上当地护士，让他们去认，这才搞清病人谁是谁。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">护理组刘丽英:从来没见过这么难管的病人。</span>
