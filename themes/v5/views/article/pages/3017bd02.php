<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">裸体乱走</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">这样的环境，很容易把没病的病人也传染上埃博拉。其他医院的硬件还未必有我们好，所以我们建议，3天验证没有埃博拉的病人就该出院。其他医院觉得我们的建议合理，也照着做。</span>
