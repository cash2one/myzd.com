<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">挥手进入病区</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">带着这么多东西，不要说干复杂的事情，就是拧一颗螺丝钉也费很大力气。有一次病人不服从管理，不愿被关在单间，把门踢坏了，我们去修门。呼出的水汽凝在面屏上，什么都看不见，又不能用手去擦，只能减慢呼吸让水汽自己慢慢干。越着急就越看不见，减慢呼吸又喘不过气来，只能强忍着。很多体力差一点的队员中间不得不出去休息一下，然后穿上防护服再进来。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">穿防护服的时候，听力也受影响，本来当地人的英语就混着许多当地方言，这下更听不懂，我们只能带一个当地的护士进去，要她来翻译。</span>
</p>
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:这些防护装备，穿起来有什么原则吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:根据我们的经验和当地的具体实践，我们提出这样一个原则:脱防护服比穿防护服还重要。穿防护服的时候，你是在清洁区，防护服本身也是干净的，所以错误不会太致命。两个人可以互相检查，互相帮助穿戴，有遮挡不严密，佩戴错误的地方可以互相纠正。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">脱防护服就麻烦了。你是从污染区走出来的，任何错误都可能导致感染。而且为了避免交叉感染，你只能一个人操作，不可能有人帮你。步骤错了、方法错了都不行。所以，我们一再强调，脱防护服才是真正的考验。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="http://static.mingyizhudao.com/147003666331765" title="" alt="demo.jpg"/></span>
