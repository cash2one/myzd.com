<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">培训当地人员</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="http://static.mingyizhudao.com/147003666502157" title="" alt="demo.jpg"/></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">手卫生考核</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">这套严密的规程是我们自己总结出来的，天气这么热，其他国家的医生往往会忽视安全隐患，扯开拉链就把衣服扔掉。很多国家的国人医护人员都这么干，结果就是被感染。20多家医院，只有两家做到了0感染，另外一家是个很小的留观诊所，我们是唯一0感染的大型医院，这不是偶然。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:辅助人员能习惯这些防护装备吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:培训他们很难。他们基本没有受过这方面训练，很多人连五指分开的塑胶手套都不知道怎么戴。培训了很久，在穿防护服方面勉强可以，脱防护服还是有很多不规范的地方。我们的工作人员比较有经验，知道自己快到体力极限了，就会及时退出污染区，脱掉防护服休息一阵。非洲的工作人员往往是到了体力极限，或是氧气不足快要晕厥了，才往外狂奔，跑到缓冲区一把扯掉面罩大口呼吸。就算是正常的下班退出缓冲区，他们也往往有不规范操作。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:当地人员不规范操作，这是不是很大的隐患？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:对，不能说到了清洁区，就掉以轻心。我们的另一个深深的体会，或者说是亮点，或者说是原则是:清洁区消毒比污染区还要重要。因为在污染区大家都穿着严密防护服，只要操作正确就不会有问题。但是到了清洁区，所有人都混在一起，不分中方、塞方，都只穿着普通的便装，免不了近距离接触乃至身体接触。如果有一个人。一个角落感染，后果都完全不可控。所以我们每天都要对清洁区进行反复消毒，控制人员出入，持有证件才能进来，进来之前先查体温。这才做到了塞方人员0感染。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">塞方人员还有一个问题，就是下班后不走，聚在办公区聊天。因为我们这里免费提供食品，为了这顿饭他们也不愿意回家。为了避免交叉感染，我们及时发现问题及时解决，规定不上班的不能来医院，不到上班时间的也不能早来，下了班的及时走人，工作期间不能扎堆聊天等，尽可能减少无关人员进入工作区，大大减少了互相污染的机会。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">我们总结的这两个原则:脱防护服比穿防护服还重要，清洁区消毒比污染区还要重要。各国来参观的都赞赏，表示要学习。当然学习归学习，能不能做到是另外一回事。</span>
</p>
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:我们还提出过其他的标准吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:有。原来塞拉利昂的埃博拉疑似病人，被确诊后要在医院留观6天才出院。这实际上是一个完全不考虑实际情况的标准。经我们结合实际情况考虑后，改为3天。现在美国CDC和WHO都接受了我们的标准，在整个塞拉利昂推广。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">为什么要修改这个标准呢。因为病人很难管理，交叉感染的概率太高了。我们的医院算是硬件条件比较好的，还是管不住病人往一块凑。病人们不肯老老实实住在病房，从监控里看，他们经常半裸。甚至裸体到处游荡，哪里凉快就在哪里睡，我们查房经常要跑到大树下面。有的时候病人忽然就会少了一个，哪里也找不到，查监控也找不到，然后第二天就冒出来了。还有一次，病人莫名其妙多了一个，那么高的墙，不开门，也不知道怎么就多了，然后这个人过几天又莫名其妙出去了，我们都怀疑自己数错了。后来才知道，一个小伙子翻墙进来看他的弟弟，然后又翻墙出去了。</span>
</p>
