<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:秦大夫说，如果早点按照中国这套治疗方法，很多人不会死。您怎么看？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:的确，许多病人实际上不是出血而死，主要是并发症衰竭而死。之前的治疗方法不注意防护，不注意支持治疗，不注意给病人补水、补充营养，我们纠正了这些问题，效果明显。所以，我们还提出一个观点，就是:支持治疗比药物治疗更重要。其后，我们才看到最新最权威的文献也这么说。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">记者:听说您在医疗队也算大厨了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">李志伟:我们只去了30人，医疗队内部什么都得自力更生。我是山西人，会做饭，也勤快，只要有空就会去厨房帮忙做饭。我还有个兼职，就是医疗队正式让我负责厨房的食品及环境卫生。每天我要保证餐具清洗消毒干净，厨房用品及时使用及时清洗消毒干净，生熟案板刀具分开，肉菜等清洗干净。两个月驻扎期，全队未发生一人次拉肚子等食物污染事件。</span>
</p>
