		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>椎管内肿瘤有哪几类？</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">椎管内肿瘤有很多种，想要给大家说明白很不容易，建议先看看文章《椎管内肿瘤和脊柱肿瘤有何不同？》，搞清楚什么是椎管内肿瘤。一般来说，这是一大类疾病，根据在椎管内的位置不同，分为三大类：脊髓髓外硬膜内肿瘤、脊髓内肿瘤和椎管内硬膜外肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">长在不同位置的肿瘤，所表现出现的典型症状也不同，如果肿瘤直接侵犯神经根，就容易出现神经根痛；如果肿瘤生在脊膜附近，疼痛就出现的较晚，而运动受限、麻木的感觉会出现。而且，肿瘤对血管和神经侵犯越剧烈，症状就越重；而不是肿瘤越靠近脊髓，症状越严重。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">这里边涉及到不同的解剖结构，先来说说大家较为熟知的脊髓，脊髓是细细的管束状神经结构，位于脊柱的椎管内且被脊椎保护，就像是连接大脑中枢神经和周围神经组织成束的电线（见下图），在脊柱的不同阶段，这束电线会逐渐分支出一条条感觉神经或者运动神经，从而分布在人体各处。如果肿瘤局限在脊髓内生长，就成为脊髓髓内肿瘤。比较常见的有室管膜瘤和星形细胞瘤，以及血管母细胞瘤、表皮样囊肿、囊性畸胎瘤等。其中室管膜瘤是最常见的髓内肿瘤，占髓内肿瘤的60%，30～60岁的人容易得，但是多数室管膜瘤属于良性肿瘤，生长比较缓慢。如果及时手术，尽可能做到全切，患者可以恢复，获得平稳生活的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">接下来脊髓外面有一层脊膜，其实这是由数层膜性结构组成的。由里到外，紧紧贴着脊髓的那一层，叫做软脊膜；软脊膜外边有一层蛛网膜，脑脊髓就在软脊膜和蛛网膜之间流动；蛛网膜之外又有一层像塑料布一样较厚的硬脊膜。我们常说的脊膜特指这种硬脊膜。（见下图）</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><img src="http://static.mingyizhudao.com/147003531596014" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脊髓、椎管结构（图片来源于网络）</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">如果肿瘤生长在脊髓外面，但是又被包裹在硬脊膜里边，就称作脊髓髓外硬膜内肿瘤。这是椎管内肿瘤中最常见的一类。其中主要包括神经鞘瘤和脊膜瘤。神经鞘瘤发生于神经根，一般属于良性肿瘤，典型的症状就是神经根痛，然后逐渐出现感觉过敏、减退或者运动受限。手术全部切除后，复发的可能性较小。而脊膜瘤大多从蛛网膜或者硬脊膜附近长出来的，患者会出现局部节段的疼痛或者麻木，往往神经根痛出现得较晚；肿瘤长大后压迫脊髓，就会出现容易肢体运动功能障碍，比如截瘫或者四肢瘫痪。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在硬脊膜之外，与椎管之间，还有一定的空间，如果肿瘤长在这个位置，就是椎管内硬膜外肿瘤，得过肺小细胞腺癌的人，癌细胞就喜欢在椎管内硬脊膜外安营扎寨。另外还有淋巴瘤、脂肪瘤等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><img src="http://static.mingyizhudao.com/147003531824031" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">不同位置的椎管内肿瘤（图片来源于网络）</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">如果医生怀疑是椎管内肿瘤，通常会采用MRI等检查，来进一步判断患者属于哪一类椎管内肿瘤。总之，椎管内肿瘤多数属于良性的，但是由于脊髓、椎管空间有限，若不及时切除，同样会由于压迫、炎症侵犯到神经和血管。因此，椎管内肿瘤应积极手术治疗，特别是采用显微神经外科技术，尽可能切除肿瘤，又能保护脊髓、椎管。</span>
</p>
