		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>颈椎病的预防与治疗</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着人们现代生活习惯的改变，低头看手机、工作的时间越来越长，越来越多，来笔者门诊看颈肩不适的患者也越来越多，年纪也越来越年轻。大家见我都会问：“肖主任，我是不是患了颈椎病？” 。那么劲椎病究竟是怎么回事？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">颈椎病是中、老年人常见病、多发病之一，是颈椎间盘退行性改变为基础，继发颈椎间盘突出、骨质增生和韧带增生病理改变，刺激或压迫邻近的神经和血管而引起相应临床症状的一种常见疾病。 其发病率在成人中约占10-15％，男性多于女性，约为3 : 1，常在中年以后发病，50 岁以上的人群约25％，60 岁以上的人群约50 ％患过颈椎病，其发病率随年龄升高而升高。在颈椎病的发生发展中，慢性劳损是首要罪魁祸首，长期的局部肌肉、韧带、 关节囊的损伤，可以引起局部出血水肿，发生炎症改变，在病变的部位逐渐出现炎症机化，并形成骨质增生，影响局部的神经及血管。 外伤是颈椎病发生的直接因素。往往，在外伤前人们已经有了不同程度的病变，使颈椎处于高度危险状态，外伤直接诱发症状发生。不良的姿势是颈椎损伤的另外一大原因。长时间低头工作，躺在床上看电视、看书，喜欢高枕，长时间操作电脑，剧烈的旋转颈部或头部，在行驶的车上睡觉，这些不良的姿势均会使颈部肌肉处于长期的疲劳状态，容易发生损伤。颈椎的发育不良或缺陷也是颈椎病发生不可忽视的原因之一，亚洲人种相对于欧美人来说椎管容积更小，更容易发生脊髓受压，产生症状。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;如下图所示，颈椎增生压迫脊髓神经根。 &nbsp;&nbsp;</span>
</p>
<p>
