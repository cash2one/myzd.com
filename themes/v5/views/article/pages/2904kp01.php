<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>乳房疼痛，是否有乳腺癌？(转载)</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">柳叶刀杂志根据疼痛规律将乳房疼痛分为周期性乳痛（约占70%）与非周期性乳痛（30%），后者又可以分为导管扩张、肋软骨炎、创伤、硬化性乳腺病和癌五个亚型。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>乳痛的病因 </strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 周期性乳痛是最常见的乳痛形式，是经前期综合征的一部分，多出现在月经的黄体期（第14-28天），在月经期后逐渐缓解，是由于乳腺组织增生复旧紊乱引起的，俗称为“小叶增生”。具有严重周期性乳痛的患者很多都存在内分泌异常，主要表现为月经的不规则，经期及经量变化，在血液循环中则表现为雌激素、黄体酮及泌乳素的升高。发生周期性乳痛后不必过于惊慌，因为大部分的周期性乳痛均为良性疾病，与癌变风险或真正的病理改变相关性很小。甚至有研究显示，当癌症被排除后，仅仅安慰就可以缓解86%的轻度疼痛和52%的严重疼痛症状。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 非周期性乳痛主要有乳腺原因及非乳腺原因的乳痛。乳腺原因的包括导管扩张、创伤、硬化性乳腺病及癌。导管扩张是常常发生在乳晕周围的乳房大导管，可伴随有乳头溢液等症状，导管的扩张程度常常与疼痛严重程度显著相关。而创伤，则与外力打击有关，比如不慎撞击、车祸、熊孩子的拳脚等，可伴发局部的肿块、淤青形成，有时候可以和乳腺癌的表现很相似哦，所以如果有外伤病史，一定要详细告知医生。硬化性乳腺病主要是由于乳腺间质增生引起，具体的机制目前仍然不明确。非乳腺原因的乳痛较常见的就是肋软骨炎了，专业术语叫提策氏病，主要是由于肋骨与胸骨关节的软骨炎症引起，压迫关节处可有较明显的疼痛，常常与乳痛混淆。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>乳痛与乳腺癌&nbsp;</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 接下来就是大家最关心的乳腺癌了。并非所有的乳腺癌患者都有乳痛的表现，但是乳腺癌的疼痛多为持续性隐痛或刺痛，部位常常固定一处，可牵扯至肩背部，如果还伴有乳房肿块或乳头溢液，那么您需要尽快就诊。当然，目前乳痛与乳癌的关系尚没有定论，有研究表明，有周期性乳痛的女性相比较无乳痛的女性，今后患乳腺癌的风险还是大大增高（RR=3.12 即患病风险增加3.12倍）；但也有研究显示，周期性乳痛的患者患乳癌的风险较无乳痛患者明显降低。尽管如此，无论是周期性乳痛还是持续性乳痛的患者，均应警惕乳腺癌的患病风险，如果您的乳痛伴有以下情况，需尽早就诊，按期复诊：&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1. 一级亲属（父母、子女以及兄弟姐妹）患有乳腺癌；&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2. 伴发肿块或乳头溢液/溢血；&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3. 月经初潮早于14岁，绝经时间晚于50岁，未生育或未哺乳；&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4.中老年妇女（大于40岁）。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>如何科学的预防和治疗乳痛？&nbsp;</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;首先，研究证实，减少膳食中的脂肪摄入可以明显的减轻周期性乳房疼痛。即减少对肥肉、奶油、全脂牛奶、油炸食物等高脂食物的摄入，可以减轻周期性的乳房疼痛，适当的运动和情绪的调整、休息等，对于减轻乳痛也有显著的帮助。&nbsp;</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其次，有临床试验证实，支持性更好的乳罩可以减轻乳房疼痛，比如运动型的乳罩，通过减少乳腺的移动与摩擦，起到减轻乳痛的作用。&nbsp;</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">当然，生活方式的调整可以对周期性乳痛起到一定的预防和治疗作用，但是严重的周期性乳痛以及非周期性乳痛仍然需要及时到医院专科就诊，明确病因，及时治疗，如需用药，则一定要咨询专科医师，切勿擅自用药，避免病情延误或加重。&nbsp;</span>
