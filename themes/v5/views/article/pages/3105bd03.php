		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>复旦大学附属眼耳鼻喉专家为白内障大熊猫除眼疾</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">新华社上海１１月１１日电（记者 仇逸）１１日下午，复旦大学附属眼耳鼻喉科医院的眼科医师来到上海动物园，为１０日晚成功接受白内障摘除术的大熊猫鹏鹏进行了术后首次眼科检查。鹏鹏术后康复情况良好，角膜稍有水肿但比较清晰透亮，属于术后正常现象，瞳孔可见，有少量瘀血正在吸收中，精神状态和进食都已逐渐恢复。待眼内瘀血完全吸收后，鹏鹏的视力也将有所恢复。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 今年５月，上海动物园工作人员发现１２岁（相当于人类４０岁左右）的鹏鹏双眼先后出现白色浑浊并逐渐加重，经诊断为白内障。用眼药水滴眼治疗后没有明显好转，鹏鹏的视力障碍日益严重，不仅对笼内的障碍物“视而不见”，也只能依靠嗅觉分辨饲养员所喂食物的方位来进食。为此，上海动物园特别邀请了复旦大学附属眼耳鼻喉科医院的眼科专家对鹏鹏进行会诊。经过检查确诊鹏鹏是白内障，且右眼因外伤导致晶体完全脱位，病情极其复杂。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 为了“国宝”的眼睛能重见光明，医院决定为鹏鹏进行眼科手术。在医院眼科主任卢奕教授的带领下，多位经验丰富的医护人员组成手术团队，为医院历史上首次为熊猫而做的眼科手术进行了一周的术前准备工作，确定了多套手术方案以应对各种突发情况，并安排了国际上最先进的手术显微镜、高清手术录像系统、超声乳化及玻璃体切割机器等眼科手术设备，克服许多困难将全套设备从复旦大学附属眼耳鼻喉科医院运送至提供手术场地的解放军上海１０８医院，确保手术顺利进行。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; １１月１０日晚，在全麻下对鹏鹏的双眼进行Ｂ超、眼压测试及眼底检查等眼部检查后，由复旦大学附属眼耳鼻喉科医院眼科主任卢奕教授主刀，成功摘除了鹏鹏右眼内的白内障。由于鹏鹏患有高血压，眼底视网膜已经存在出血等病变，晶体全脱位导致没有支撑人工晶体的位置，而且目前并没有适合熊猫的人工晶体，所以手术中未植入晶体。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 据悉，复旦大学附属眼耳鼻喉科医院为本次手术而专门准备的器械和耗材都是一次性使用，并根据鹏鹏术后一两个月的用药需要提供了足量的眼药水。下周一，医院还会对鹏鹏进行随访开展眼部检查，密切关注其眼部康复情况。</span>
</p>
