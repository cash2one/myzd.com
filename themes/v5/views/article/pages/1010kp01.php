			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>头颈部肿瘤放疗后的口腔并发症及其防治</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">放疗是头颈部癌的有效措施之一，但头颈部放疗除了可以达到预期的抗癌效果外，还会造成正常组织的损伤，出现一系列的口腔并发症，如:粘膜炎、口腔干燥症、放射性龋、牙关紧闭及放射性骨坏死等。这些口腔并发症可能会导致抗癌治疗的中断，影响患者术后生活质量。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>1　粘膜炎</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 粘膜炎又称口腔炎。放疗破坏快速分裂细胞，在口腔造成口腔粘膜的短期损伤。放疗第1周后，患者常出现口腔组织的触痛、肿胀，粘膜红斑、水肿，并逐渐加重。到第4周，口腔大部分组织肿胀。患者进食尤其是辛辣或粗糙食物时，口腔有疼痛，烧灼感。且放疗后口腔微生态环境的稳定性遭到破坏，粘膜炎若合并微生物感染，会使症状加重。一般粘膜炎持续到放疗结束后1~2周，感染、吸烟和饮酒会加重粘膜炎，延长愈合时间。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 放疗前患者应进行口腔卫生检查，磨光尖锐粗糙牙尖和充填物，去除不良修复体。放疗开始时，患者可使用碳酸盐溶液漱口1分钟，一天至少8~10次。该溶液能溶解粘液，松散食物残渣。出现严重粘膜炎症应到口腔科就诊，检出真菌感染可使用制霉菌素或酮康唑片剂控制感染，若患者因口干无法溶解片剂时，可使用相应的口腔喷雾剂。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2　口腔干燥症</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">口腔干燥症是头颈部放疗后最常见的并发症。放疗第1周，唾液流量即可减少将近50%，随着疗程的进展，流量可减少95%。在很多情况下，唾液减少是不可逆的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前对于放射性口干症的主要防治措施包括：利用三维适形或调强技术进行精确放疗、放疗前行唾液腺移位术以减少主要唾液腺所受到的照射剂量。目前在很多大医院，精确放疗技术已经广泛应用，明显减轻了放疗后口干的程度，但尚不能完全避免这一并发症；手术预防增加了额外的手术风险，很多患者不能接受。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于已出现放疗性口干症的患者，可采用的手段包括M胆碱受体激动剂、唾液代用品和中医药治疗等。M胆碱受体激动剂的代表药物是毛果芸香碱，能够显著增加静止性唾液流量，从而减少放射性口干症的发生，但对改善口腔黏膜炎及生活质量评分无任何帮助。该药物起效缓慢、需长期用药才能获得持续的疗效，费用昂贵，而且有一定的副作用，如出汗、尿频、哮喘和心律失常等。人工唾液的主要成分包括羧甲基纤维素、聚乙烯氧化物或动物黏蛋白等，多项研究证实了人工唾液为代表的唾液替代品能够明显改善患者的口干症状，但需要在全天（包括夜间）均保证极高的使用频率。中医药在预防和治疗方面均有一定作用，但作用较为温和缓慢，需要坚持用药一段时间。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3　放射性龋</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 放射性龋产生的原因主要为:1)头颈部离子化放射破坏了唾液腺功能，唾液的质量发生改变，唾液对碳水化合物和酸的清除能力、酸的缓冲能力降低，使得口腔正常菌群转化为高危度的致龋菌群，牙齿结构再矿化能力的破坏;2)癌症患者口腔保健意识及自我清洁能力差，易选择软食、富含碳水化合物的食物，或使用致龋性的唾液刺激剂。且放疗后釉质结构产生物理化学变化，溶解度和脱矿深度发生改变，龋敏感性增强。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 放射性龋的临床表现非常有特色:龋损可发生于牙颈部、牙尖、切缘及牙光滑面，且进展迅速，可在数周内累及大多数或全口牙。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术前应结合X线片进行牙列检查。能保留的龋齿及时治疗，无法治愈的患牙尽早拔除。放疗前患者应学习正确的刷牙方法、如何使用牙线或软木制牙签去除牙间菌斑等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>4　牙关紧闭</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当咀嚼肌或其他颞下颌关节周围软组织位于放射野时，放射线造成组织局部水肿，细胞破坏及纤维化，出现张口受限，受累肌肉系统活动度减少。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">放疗开始后，这些患者需做每日的开口训练，以保持最大张口度和颌动度。若发现开口度减小，可用适度厚度的楔形硬橡皮块或阶梯形木块作开口器，逐渐增加厚度，使开口度逐渐增大。一般张口受限(肌源性)可持续到放疗结束后3~6个月。因此，对于有牙关紧闭危险的患者，这段时间内继续加强开口训练，辅以理疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>5　放射性骨坏死</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 放射性骨坏死是头颈部放疗最严重的并发症，其发病率为5.4%，许多ORN患者早期无明显症状。放射性骨坏死的主要原因是头颈部癌根治性放射造成的血管损害和骨损害，在放疗过程中或放疗后拔牙手术、牙源性感染或损伤造成的继发感染等往往成为其诱因。且下颌骨因骨质致密，血供相对不丰富，较上颌骨更易发生坏死。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为预防放射性骨坏死应放疗前常规牙周清洁，注意口腔卫生;取出口腔内已有的金属假牙，活动假牙需在放疗终止一段时间后再行配戴，以免造成粘膜损伤。</span>
</p>
