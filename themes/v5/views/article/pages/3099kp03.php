<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腰椎间盘突出的原因&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多病人朋友问我，为什么腰椎间盘会突出。那我现在就大概的说说引起这类疾病的诱因。希望能对大家能有些许的帮助。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 随着社会的发展，腰椎病已经不再是中老年朋友的“专利”，随着电脑的普及和日常生活中小细节的忽视，现在越来越多的年轻人踏入了腰椎病的行列。有报道称，目前有半数的白领工作是腰椎病患者。那么腰椎病是由哪些原因引起的呢？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;大多数人都有腰痛的经历。引起腰痛的疾病有很多种，如急性腰扭伤、慢性腰肌劳损、腰椎间盘突出等，但不同病因引起的腰痛，其症状也不同。引起腰椎病的具体原因有以下几点。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 原因一：退行性病变：腰椎病的根本原因是脊柱的退行性病变。所谓退行性病变简单的说就是老化。脊柱的退变包括脊椎的退变和椎间盘的退变。脊椎的退变表现为：骨刺的形成、关节突肥大、椎板增厚、黄韧带肥厚最终导致椎管狭窄。椎间盘的退变包括：纤维环退变，表现为变薄、破裂。髓核的退变，表现为水分减少、失去原有的弹性和可流动性。最后导致椎间盘的膨出、突出和脱出。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;原因二：无菌性炎症：骨刺、突出的椎间盘压迫其周围的神经根、血管、脊髓，引起受压组织产生水肿、渗出、小的出血灶、粘连这些病变，医学上称之为无菌性炎症，它是引起临床症状的重要原因。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;原因三：骶棘肌痉挛：骨刺、突出的椎间盘压迫神经的后支，可导致骶棘肌痉挛，它会引剧烈的疼痛，并使椎间盘膨出、突出加重。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们在日常的生活工作中，一定要注意身体的锻炼，有效有意识的预防椎间盘突出的疾病。健康的身体是美好生活的基本。</span>
</p>
