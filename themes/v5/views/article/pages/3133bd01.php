			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>12岁男童生吃螃蟹险偏瘫 肺吸虫钻进大脑竟肉眼可见</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3133/1.png" title="" alt="demo.jpg"/></span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">恐怖的肺吸虫。 &nbsp;记者 &nbsp;陈丹 &nbsp;摄</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 华龙网1月19日16时讯（数字记者陈丹、通讯员袁儒青） &nbsp;12岁男孩5年来左侧肢体无力，险些偏瘫。神经外科医生在手术中惊讶发现，导致男童患病的竟然是一条肉眼可见的肺吸虫。重庆医科大学附属儿童医院医生表示，这种情况实为罕见。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 12岁男童洋洋（化名）来自贵州农村，父母长期在外打工，只剩他和爷爷奶奶相依为伴。原本乖巧可爱的他，却在5年前慢慢变得笨拙起来，左侧肢体使不上力，走路摇摇晃晃。父母带着他走了当地几家医院，始终没有找到病因。洋洋的病情时好时坏，直到最近愈发严重，记忆力下降，常喊头痛，连走路都成问题，只能辍学在家。父母才下定决定带他到重庆来治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 重庆医科大学附属儿童医院神经外科主任梁平教授经过洋洋头部CT照片发现，洋洋的头部右侧有一个血肿。引起血肿的原因有很多，外伤、血管畸形等因素。血肿导致洋洋左侧肢体麻木，梁平教授决定为其进行开颅手术清除血肿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在手术中，梁平教授惊讶地发现，洋洋颅内血肿里竟然有一条肺吸虫，“近几年，科室收治了200多例脑部肺吸虫患儿，这是第一次用肉眼看见虫体，虫体约1公分大小，肺吸虫又引起颅内血肿，很罕见。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 经过询问，洋洋称在7岁的时候与小伙伴在回家路上的小溪曾经吃过生螃蟹。去年春节，洋洋家的团年酒席上多了一样螃蟹，为了确保螃蟹鲜嫩，蒸的时间不长，蒸到半熟就端出了锅，洋洋也吃了。梁教授推测，患儿应该是吃了感染肺吸虫的螃蟹所致，肺吸虫经患儿肠部血管上行到脑部。目前，医生对洋洋进行药物排虫、止血等治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据介绍，肺吸虫一般只寄生在螃蟹身上，人们一旦生吃它，肺吸虫卵将随之进入体内，在身体里扎根安家形成肺吸虫。肺吸虫不仅可以侵入肺部，引起阵发性咳嗽、咳痰、咯血，还可侵入人体其他组织，如胸部、腹部、肝脏等处，引起胸痛、胸腔积液、腹痛、腹泻等症状。肺吸虫还可入侵脑部，在脑组织中移行和破坏组织，引起头昏、呕吐，少数患者有癫痫、抽搐、偏瘫、运动障碍等表现。肺吸虫一旦造成脑内大出血，将有生命危险。 对此，梁平教授提醒市民，过节吃螃蟹一定要煮熟。</span>
</p>
