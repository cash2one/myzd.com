		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>经常用电脑注意自我调节 到严重时麻烦大了</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">谢先生今年才30出头，可腿脚最近老不好使，如果走慢一点，跟正常人无异，如果走快一点，则身体会左右摇晃，两腿感觉在地上拖行一样，像“鸭步”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; “穿拖鞋，如果鞋子掉了，竟然会没感觉。”谢先生说，最近两个月来，两脚感觉变麻木，抬腿都有点困难，总感觉腿没力气，脚踩下去时经常感觉不到地面，因此总有“踩空”的感觉。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 在乡下医院看过医生，认为是腰椎间盘突出，就在按摩店按了几天，没效果。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 快两个月了，症状一直这样，昨天，谢先生到南京军区福州总医院找到神经外科王守森主任医师。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; “在谢先生的核磁共振片子上看到他颈部有椎间盘突出，而腰部情况比较好。”王主任说，而且这种突出也比较典型。但让王主任惊讶的是，就在谢先生颈椎间盘突出的水平面的颈髓出现异常信号，而且这种改变范围很长。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 难道这是肿瘤吗？这个想法在王主任脑海中一闪而过，但他又迅速否定了。“在临床上很少有在这个椎间盘突出的平面，颈髓长出这么奇特的肿瘤。”王主任说，一般是不会有这么巧的事出现，那它到底是什么呢？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 于是，王主任让谢先生再做一次核磁共振加波谱检查，结果跟王主任猜测的一致，谢先生颈髓大面积的病变，并非肿瘤，而是颈椎椎间盘突出后，压迫了颈髓，长时间压迫后导致颈髓脱髓鞘性病变。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 王主任说，髓鞘的作用是保护神经元并使神经冲动在神经元上得到很快的传递，所以，髓鞘的脱失会使神经冲动的传送受到影响。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 急性脱髓鞘性疾病的神经髓鞘可以再生，且速度较迅速，程度较完全，虽然再生的髓鞘较薄，但一般对功能恢复的影响不大。慢性脱髓鞘性疾病，由于反复脱髓鞘与髓鞘的再生细胞明显增殖，神经可变粗，并有轴突丧失，因此功能恢复不完全。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 显然，谢先生现在需要的就是先解除颈部压迫。南京军区福州总医院骨科徐皓主任医师说，像谢先生这种情况越快手术越好，因为只有解除了压迫，才会阻止这种病变进一步发展从而影响神经功能。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 在两位主任的临床实践过程中，像谢先生这样突出并引起颈髓病变的人还是比较少的。尽管谢先生有打篮球，但更多的时间是呆在办公室用电脑，这种颈髓压迫的时间很长，轻者只是手肩部有麻木感，重者还会有生命危险。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 所以长期用电脑或用固定姿势工作的人，一定要注意自我调节，如果发展到谢先生这样麻烦就大了。</span>
</p>
