	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;"><strong>笑脸墙 生命墙——医生袁贤瑞的故事</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3193/2.png" title="" alt="demo.jpg"/></span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3193/3.png" title="" alt="demo.jpg"/><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞25年主刀超过5000例手术，每一台手术以后，就多一张笑脸。灿烂的笑脸组成一道灿烂的生命的墙。（照片由受访者提供）</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/3193/4.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;">袁贤瑞要求学生和同事多从病人角度想问题，但绝容不得“想歪了”。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 中南大学湘雅医院神经外科35病室传出暖暖笑声。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 科室主任袁贤瑞俯身查看来自江西宜春的敖女士病情。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 敖女士患巨大型听神经瘤，经袁医生5个多小时努力，做了全切手术。暂时不能开口的她，一直用微笑回应着医生的关怀。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 患者女儿双手合十，眼眶湿润：“不知道妈妈面神经能恢复好不？”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “她笑得怎么样？笑得好就证明面神经保留、恢复得好！”袁医生自问自答。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 患者丈夫高兴地说：“看来我们从网上找医生，找对了！”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞是享受国务院特殊津贴的神经外科专家，中华医学会神经外科分会常任委员，神经肿瘤学组副组长。“我最高兴的事，就是看到他们术后的笑容！”袁贤瑞取出电脑，打开一个容量超过90G的患者数据库，从医25年，超过5000例的神经外科疾病患者的资料都在这里。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 记者注意到一份文档某几个页面，满满的，由一张张笑脸构成。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “这些照片是术后用来对比的。看，这是一个教师，手术后，他能够正常工作了。”每一张笑脸，袁贤瑞都能说出背后的故事。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞用二十五年筑起一道灿烂的笑脸墙。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “看到病人难受自己很难受的人，我才要他搞神经外科”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞的从医之路并不是从微笑开始的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 1953年，袁贤瑞出生在湖南汝城县农村。10岁时，父亲过世，但父亲善良、忠厚、勤劳、乐于助人的性格，在他身上打下烙印。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 父亲过世一年后，袁贤瑞患上了膀胱结石。那种钻心的痛，现在他都记得。经过医生,手术，袁贤瑞很快康复。重新活蹦乱跳的少年，觉得医生了不起。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞在湖南医学院医疗系毕业后，与来自湘雅医院的医生护士一起被派到洞庭湖边从事血吸虫病防治。一个护士得知这个小伙子将会成为自己的同事，告诫他千万不要分到搞开颅手术的神经外科去——“又苦又累还会死人。”当时的袁贤瑞，向往去骨科。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 1977年3月1日，是他到湘雅医院上班的第一天。门开了，居然偏偏就是神经外科。第一眼的感受，袁贤瑞刻骨铭心：医师用器械夹到一个肿瘤，恰好该肿瘤包绕着一根血管，一扯就把血管撕破了。血在涌，把脑组织往外挤压，脑组织在那一瞬间就像吹气球一样，一下子就开花了。那时候医疗条件差，医生只能用手指把出血点压住……家属撕心裂肺的哭声……</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 此情此景让袁贤瑞郁闷痛苦。他闹情绪，想转科室。老师王茂山跟他推心置腹聊了一次。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “为什么不愿意学神经外科？”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “看到病人那个样子，心里难受。”</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “看到病人难受自己很难受的人，我才要他搞神经外科。不把病人痛苦当回事的人，不够资格做神经外科医生。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “正因为困难，才更有发展空间。等着，我做几个漂亮的手术让你看看，开颅手术是可以让一些病人起死回生的。”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 不久，一位失语、偏瘫的脑膜瘤患者来了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 当时医学影像技术不发达，只能显示一个方向的颅内结构。王茂山教授就是根据这种影像，亲自主刀，手术非常成功。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 出院时，患者向王教授千恩万谢。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 看着患者出院的背影，给王教授打下手的袁贤瑞看到了希望。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 一位同学找到袁贤瑞，说可以对换科室。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 留，还是走？袁贤瑞犹豫着。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 他到自己的神经外科启蒙老师陆雪芬家中请教。</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “别换！”袁贤瑞第一篇译作的修改老师，两个字，稳定了袁贤瑞的军心。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 1984年，袁贤瑞硕士毕业，开始在病房做总住院医师。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;">16个月里，袁贤瑞每天几乎24小时待在病房、泡在手术室。他感觉慢慢摸到了神经外科的“门边”。“自己穿梭在病床、手术室之间的脚步，越来越觉得轻盈。神经外科很迷人，让我兴奋。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “在脑部做肿瘤手术，像在萝卜地里拔萝卜，只能拔出萝卜，绝不能带出泥”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 要让颅底肿瘤病人术后能够微笑，很难！</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 2011年，一位李姓患者因脑部颅咽管瘤住进湘雅医院。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “影像显示患者肿瘤位置很深，并对视神经造成严重挤压。”配合袁贤瑞开刀的彭泽峰副教授回忆，“完全切除的概率几乎没有。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 术前讨论时，有的医生说如果肿瘤不能“一刀切”，总归又会长。反复手术，病人就需要反复开颅。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “要全切，这么深，周边的重要结构盘根错杂，搞不好，命都保不住。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞很漂亮地做了全切，他是怎么做到的？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞的电脑上，有一段他做手术的视频。在一首古典钢琴曲的伴奏声中，记者见到三四种手术器械，非常协调、精准、稳健地在分离、切除一个肿瘤。</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “这个地方是颈内动脉血管，这一侧是视神经。这些都不能损伤。血管破裂了，血会像水管爆裂般喷涌；视神经损伤，患者会失明；即便是微小的分支血管损伤，亦可能造成术中出血或术后病人昏迷不醒、偏瘫。”袁贤瑞指着屏幕介绍手术情况。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “这个手术空隙有多大？”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “两公分。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 这个视频在学术会议上交流时，引得国外同行惊叹：这样将肿瘤几乎完整分离切除，却没有损伤周边重要组织结构，难以置信。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 1991年以来，袁贤瑞主刀完成颅咽管瘤手术200余例，全切除率85.0%，随访患者5年总体生存率达到96.5%，5年无瘤生存率为88%，治疗效果达到国际先进水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞又调出手术前后病人脑部医学影像资料，让记者对比着看。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “在脑部做肿瘤手术，就像在萝卜地里拔萝卜，只能拔出萝卜留下坑，绝不能带出泥。”这是记者听到的最有新意、最美妙的关于“拔出萝卜带出泥”的解释。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “手术，既要拯救患者的自然人的生命，更要拯救患者社会人的生命”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 对于病人，有时微笑与生命同样重要！</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 听神经瘤作为颅底肿瘤的一种，全切的手术难度和风险非常大。尤其对于大型、巨大型听神经瘤，全切肿瘤并保留面神经功能特别是听力，则更为艰难。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 2001年，一名18岁的女孩在医院做听神经瘤切除手术，手术很成功，肿瘤被完全切除。由于女孩的肿瘤巨大，当时又没有相应设备，在切除肿瘤的同时，面神经未能保留，女孩面瘫了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 术后回访中，女孩痛苦地告诉袁贤瑞，虽然身体康复了，但因为面瘫，不敢出门，不敢交友，一切梦想都成了泡影，简直是生不如死。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “当孩子告诉我她生不如死时，我心情很沉重，我一定要攻克‘大型、巨大型听神经瘤手术中很难保住面部神经’这个难题。”袁贤瑞告诉记者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 当时医学界普遍认为，对于大型、巨大型听神经瘤，肿瘤不可能全切除，面部神经不可能保留；对于直径3公分以上的听神经瘤，几乎不谈听力保留。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 2002年起，袁贤瑞和同事开始专注于听神经瘤。“手术，既要拯救患者的自然人的生命，更要拯救患者社会人的生命。”遇到困难时，袁贤瑞就拿这句话和同事共勉。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 要全切肿瘤，又要保住命，还要留好神经功能，这不仅是鱼和熊掌兼得，这是三者都得，还是在颅底深部，太难了。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 但他做到了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 十年来，袁贤瑞已成功为391名听神经瘤患者进行了手术，肿瘤全切率高达96.08%，面神经完整保留率达到了惊人的97.48%，面神经功能恢复和完全恢复达86%！更加难能可贵的是，袁贤瑞在对187名听神经瘤患者进行听力随访过程中发现，听力保留率达到了25.93%，其中具有语言分辨能力的患者占21.3%。这一结果，引起国际医学界的惊呼！</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;">“掌握显微神经外科技术，是一门无止境的艺术”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 在病人笑脸背后，是他，是他们对技艺尽善尽美的追求。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “当然，这样的手术在显微神经外科时代以前，想都不要想！”袁贤瑞解释说。显微技术能够将开颅手术视野放大，提高手术精确性，减少邻近组织的损害，使得之前被视为手术禁区的脑深部病变、脑干肿瘤等，得以逐步解决。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞与显微镜有着不解之缘。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 上世纪八十年代，时任湘雅医院神经外科主任的曹美鸿，力主引进医院第一台手术显微镜，立刻成了科室的宝贝。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 1987年，袁贤瑞开始做显微神经外科手术。“我的第一个手术病人姓曾，颅底脑膜瘤手术，效果很好。”之后，他开刀近百例，一年下来没有出现一例死亡。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 接下来，袁贤瑞瞄上了当时国内外风行的分子生物研究，跟曹美鸿教授提出，自己的博士论文要做胶质瘤耐药基因研究。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “分子生物学和临床之间距离很大，现在你的临床手术技术虽然有了提高，但掌握显微神经外科技术，实现精准微创手术，是一门无止境的艺术。”曹美鸿一席话，给赶时髦的袁贤瑞泼了冷水。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 就在他闷闷不乐时，一位护士，患中颅底神经鞘瘤住进了神经外科。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “姑娘身材高挑，来时，穿着一件浅蓝色连衣裙。第一次手术，肿瘤没有能够切下来，造成偏瘫后遗症；第二次手术结果更糟。”这次不理想的手术让他终身难忘，也因此觉悟“自己的价值应该还是用显微外科技术为患者解除病痛”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 1994年，袁贤瑞的博士论文《鞍上与鞍旁区脑膜瘤的显微外科手术与影响其疗效的若干因素》答辩。武汉协和医院朱先立教授，是国内唯一一位跟随显微神经外科开创人亚萨吉尔学习三年的医生，他在袁贤瑞的博士论文上写道：“这个结果是令人鼓舞的，切除率这么高，死亡率为零，几乎不可能，但你的数据说明这种可能性是存在的。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “是病人的信任，让我能够做出理性的必要的冒险”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞觉得患者术后的微笑，不完全是自己的功劳。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 2003年，一位外资公司高管住进医院。这位患者肿瘤所处位置非常复杂，长在神经中枢，重要的内分泌调节中枢这一部位。这个位置太深，不能完全显露出来。以当时的神经外科技术，对这种病例还处于初步探索阶段。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “第一次手术，我很想做到全切。可当时的情况，我没有这个胆量，给他再做下去，我担心很严重的问题会发生！”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 术后，肿瘤只残余了一点点，病人各项体征也很平稳。但袁贤瑞很为病人担心，知道肿瘤又会长起来，只是没有想到，不到三个月病人肿瘤就复发了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 怎么办？与肿瘤打持久战，病人消耗不起；来个一锅端，争取全切除肿瘤，有这种理论上的可能，但实践经验太少，万一……</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 病人看到袁贤瑞为难，笑着对袁贤瑞说：“袁教授，我相信你！你大胆做，不要有顾虑，就是死了，我也不会怪你！”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “好，我们一起努力！”正是有了病人的信任，袁贤瑞果断采取当时最新的手术方式，做到了对肿瘤的全切。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 术后，患者恢复了正常工作，一直到现在肿瘤没有复发。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “是病人的信任，让我能够做出理性的必要的冒险。”袁贤瑞说。在这个病人之前，袁贤瑞类似32例手术的全切率为52%；之后，同类型手术170多例，全切率超过95%，无瘤生存率达到88%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;">“第一台手术超过24小时”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 为了病人的笑容，袁贤瑞竭尽全力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 年近六旬的袁贤瑞，每天上下班都是爬楼梯。他还要求同事跟他一起爬。“一是湘雅医院求诊病人多，电梯留给病人；二是神经外科也是个体力活，工作之余要多锻炼。”袁贤瑞的同事解释。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 上世纪八九十年代，是袁贤瑞做颅底手术的起步时期。“第一台手术，我印象很深。从早上八点钟，做到第二天上午九点，超过二十四小时。”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 在将近十年的时间内，这样的手术时间，成为一种常态。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 有些手术需要其他科室配合，跟袁贤瑞做手术搭档真不容易。搞麻醉的医生觉得，跟袁贤瑞做一次手术下来，全身发麻，昏昏欲睡。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞自己也觉得每当手术持续做到三更半夜时，身心极度疲惫。“那时候我感觉，人世间最大的幸福是躺在床上睡觉。但手术没有做完，只能再坚持。要是只顾自己的感受中断手术，也许病人就没有治愈的机会了。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 儿子袁健，现在成了袁贤瑞的同事。小时候，妈妈做饭菜时，他和姐姐就去门口看爸爸什么时候回。“很少能够等到父亲一起吃晚饭。甚至我们第二天早上去学校了，还没有见到他回来。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁健告诉记者，那时，他和姐姐不会感到失望，还觉得好玩。“他回来时，常把手术间歇中用来充饥的桃酥、方便面一起带回来，来不及吃，就睡了。这些东西，就成了我们的零食。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “更好玩的是，他一起来，就会跟我们聊他的手术做得如何成功，病人恢复得如何好！这是我们小时候听得最多最好的开心故事。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 父亲的故事，让袁健感受到了医生的伟大。“我初中的时候，就立志学医，因为医生一辈子可以救很多人的命。”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “长大后我就成了你”的袁健才体会到父亲的辛苦。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “儿子上了我的当。”袁贤瑞觉得医生是在燃烧自己的生命，去点燃病人的生命；医生是在牺牲自己的健康，去拯救病人的健康。“只是我从不把工作上的不愉快带回家，我只和他们一起分享病人的快乐。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 尽善尽美的袁贤瑞，25年主刀超过5000例手术，颅底中央区脑膜瘤手术400余例，肿瘤全切率超过85%，死亡率低于0.5%；大型、巨大型听神经瘤近400例，肿瘤全切率超过96%，术后面神经功能正常或接近正常者达86%，听力保留率超过20%；复杂颈静脉孔区肿瘤100余例，肿瘤全切率超过90%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 这份不亚于或优于国际同期大宗病例报告的背后，是他超长的手术时间。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “做记录，搞科研，医生如果来假的，不仅是一辈子的耻辱，还会贻害性命”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞很少对学生和同事笑，有的人甚至为了不挨骂，选择躲着他。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “袁教授的手术，术后护理难度不大，因为手术很完美。”护士长徐德保说，“不过他的要求太高。手术台上那么多器械，每次都要放在固定位置；保护脑组织用的明胶海绵棉片，要做成统一的规格。时不时还考考护士‘如果输液回血，怎么办？’”看来护士们被袁贤瑞“刁难”过不少。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁健对此更有体会。一天，他在手术台下观摩父亲做复杂鞍区肿瘤手术视频。到了中午，手术还没有做完。一位师兄路过，看到后，喊袁健一起吃饭，袁健去了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 一回来，做完手术的父亲，等在门口，劈头盖脸来了一句：“你是来学做手术的，还是来学吃饭的？”袁健看着父亲生气，不敢吭声。“这么精彩的一个教学手术，你们跑去吃饭？以后怎么为病人主刀？”袁贤瑞嚷嚷着走了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 为了病人，袁贤瑞会和学生一起绞尽脑汁想办法。张家界山区的一位老年患者，在湘雅做完非典型鞍旁脑膜瘤后，两三年没来复查。袁贤瑞一直惦记着。“这种病，容易复发。一旦复发，死亡的可能性大。”袁贤瑞的博士生刘定阳说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 一开始，袁贤瑞叮嘱刘定阳给患者打电话，不料病人手机号换了，联系不上。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “按照地址写信。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 刘定阳前后写了三封信，每封袁贤瑞都会过目，并写上希望老人来复查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 信也石沉大海。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “查！根据地址，查能够联系上的电话号码。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 刘定阳按照袁贤瑞的要求，终于联系上病人村里的村支书。村支书说，老人收到信后，很感动，也想过来，但无奈年老体衰，来不了。“要到过年的时候，等他崽伢子回来了，才能来。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 过年前，老人果然在儿子陪同下，带着土鸡蛋、茶叶、茶油来了。袁贤瑞很高兴，像见到亲人一样，忙前忙后为老人做复查，问病情。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “那天科室安排搞个年底聚餐，袁老师为此没有参加。等我们吃完饭，他们还在聊。”刘定阳说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞要求学生和同事多从病人角度想问题，但绝容不得“想歪了”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞手术时，一位学生负责做病历记录。术后，袁贤瑞调看时，发现记录缺乏真实性，尽挑好的写。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “病历记录既是医学档案，又是法律文件，必须真实全面，不真实全面，发现不了疾病本身的规律特征；不真实，由此得出的结果就没有参考价值，结论就可能片面甚至错误。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “做记录，搞科研，医生如果来假的，不仅是一辈子的耻辱，还会贻害性命。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “不能为了名来假的，更不能为了利搞名堂！”袁贤瑞的话像炮弹一样炸响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 一位农村患者，因慢性硬膜下血肿来看病，身上只有2000元现金，在长沙东拼西借，凑齐8000元住院费。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 主管的进修医生，一下子就开了4000多元的治疗药物，导致手术做不成。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> “这是一种罪恶！”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 发完脾气，处理完当事医生，袁贤瑞在科里定下“铁令”：凡超过500元的处方必须经病房组长签字才能生效。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 当然，袁贤瑞也有温情的一面。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 看到学生在医院外买早餐吃，他会关心说一句：“小心地沟油。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 过年了，知道大家值班辛苦，他会将爱人做的油粑粑，带到医院犒劳大家。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;">“老师曾跟我说，可以通过一个人的字判断他的手术水平”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞的一些故事，有人觉得好笑。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 作为中南大学神经外科学一级教授、博士生导师，袁贤瑞曾经定了一个规矩：神经外科博士生毕业答辩前，要能够背诵一段《论语》，并结合专业讲体会，背不出，讲不好，不能答辩。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 这个规定让很多学生“为难”、“不解”，袁贤瑞却觉得传统文化的精髓，对于医生的人文修养有着重要影响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;"> 刘庆副教授记得，在一次和导师的谈话中，袁教授语重心长地以一段古语相告：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> “重为轻根，静为躁君。是以圣人终日行不离辎重。虽有荣观，燕处超然。奈何万乘之主，而以身轻天下？轻则失根，躁则失君。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;"> 刘庆查到这段话出自《老子》，袁老师意在告诫他们：在学习神经外科知识的过程中，更要磨砺出稳重、踏实、坚毅、勤勉的职业性格。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 在袁贤瑞的办公桌上，摆着一本《英汉医学辞典》。封面已经被翻得破损，是上海科技出版社1981年出版的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 办公室的角落，摆放着文房四宝。只要有空，袁贤瑞就会练习一下书法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 他出差的时候，看到有的同行写得一手好字，很是羡慕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> “老师曾跟我说，可以通过一个人的字判断这个人的手术水平。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 湘雅医院党委宣传办主任伍西明评价：“袁老师的字确实像他的手术一样，是那种稳健有力的风格。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 袁贤瑞常对学生讲：“术业有专攻。”他觉得学科细分才能让医生在某一领域更好地为病人服务。他要求年轻医生不要成为“多面手”，而要专注于练就“一招鲜”。每个人找到自己的主攻目标。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 在脑血管病的动脉瘤、动静脉畸形等方面，袁贤瑞有着丰富的临床经验。后来为了学科的健康发展，他忍痛割爱，将自己的方向定在了颅底肿瘤上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 十年磨一剑。目前，湘雅医院的神经外科形成了颅底脑干肿瘤、脑血管病、颅脑创伤、神经肿瘤综合治疗、立体定向和功能神经外科、儿童神经外科、脊髓脊柱神经外科7个特色鲜明、优势明显的亚专业学科和研究方向。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"> 采访中，袁贤瑞的手机不时响起，不断有人来访，都是咨询病情的。他一年要做300多台手术。每一台手术以后，就多一张笑脸。灿烂的笑脸组成一道灿烂的生命的墙。</span>
</p>
