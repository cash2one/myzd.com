			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>儿童颅咽管瘤的治疗趋势和结果</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据《Neruo-Oncol》2013年3月报道 题：1975~2011儿童颅咽管瘤的治疗趋势和结果</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 由于其与周围重要组织结构关系密切，颅咽管瘤本身及其治疗可导致长期的严重并发症。许多年来，其最适合的治疗方法一直存在争议。本研究回顾近十年来本院对儿童颅咽管瘤的治疗并讨论近30年来的治疗趋势（1975~1989，1990~2001，2001~2011）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">结果：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、 &nbsp;患者特征：33例儿童病人（15例女性），平均年龄10.7岁，随访年龄为4年（0.7~9.3）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2、临床表现：头痛28例(85%)，视力损害25例（76%），恶心呕吐14例（42%），肥胖7例（21%），体重不足5例（15%）。在垂体功能障碍方面主要为GH、ACTH和TSH异常，伴糖尿病。肿瘤平均直径为4.9cm(2.2~12,6)，多数有囊变和钙化。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、首选治疗：18例作囊肿减压术，辅助治疗为放疗或囊内化疗。由于博来霉素的神经毒性，到2005年，我们停止该药囊内化疗，改用囊内注射α-干扰素。放疗等用5400CGy,分30次完成。在10岁之前，尽可能避免放疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">14例（42%）患者接受放疗，8例（24%）接受囊内化疗（4例博来霉素，4例γ-干扰素）。3例从开始阶段就采用γ-干扰素化疗，2例在随访20和28个月无复发，第3例在治疗2个月后再次抽干囊液，41个月随访，不需要进一步治疗。第4例为复发病例，随访时间短。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、 &nbsp;长期治疗效果</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 7例患者肿瘤复发（52%），复发的平均时间为11.5M；其中4例患者是被认为全切的患者，2例需要第二次手术。7例首选放疗患者，2例复发，1例作囊液引流，另一例作肿瘤部分切除。7例囊内化疗病人中4例复发，3例作囊液引流，1例手术部分切除。我们发现年龄小的患者容易复发。只有一例患者因急性感染而死亡。生存率97%，29例患者至少有一种垂体激素缺乏，最常见的为GH,28例（85%），其中8例治疗前即存在。TSH缺乏24例（73%），ACTH缺乏21例（64%），LH和FSH缺乏20例（61%），ADH缺乏18例（55%）。在随访中，19例肥胖（其中7例发病时即肥胖）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、1975~2011年治疗趋势和结果</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在过去十年，颅咽管瘤GTR的病例显著减少，微侵袭部分切除病例明显增加。从1975年到1989年，最大程度切除肿瘤以期获得治愈。对于复发的肿瘤采用再手术或放疗结合。1990~2001年，向保守治疗转移，肿瘤部分切除，必要时辅以放疗，出现肿瘤腔内化疗。从1990年前0例，1990~2001年1例，到过去十年的8例。首选放疗患者病例也在增加，1975~2001年0例，过去十年为7例（21%）。辅助治疗从0到2%和现在的42%。过去30年肿瘤的复发率无明显改变。34%、30%到52%，糖尿病和皮质激素功能 在第一和第二时间分别为69%、89%和79%、93%。近十年分别为49%和64%。肥胖的发生率也有所下降。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，随着颅咽管瘤治疗从全切除到综合治疗的转变，患者内分泌损害有所改善，一方面达到延长患者生命的目的，也有利于改善患者的生活质量，肿瘤的复发率变化不大。</span><br />
