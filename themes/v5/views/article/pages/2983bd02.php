		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>矫正脊柱侧凸 女孩宜在初潮后</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 孩子脊柱长“歪”了怎么办？“做引体向上矫正呗。”相信不少家长都会这样回答。“未必。”骨科专家指出，引体向上、侧向体操等形体锻炼办法仅对轻度脊柱侧凸患者有一定效果，但并不能从根本上解决问题。尤其是各种结构性的脊柱侧凸，锻炼矫正的方法根本无效。&nbsp;</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 八成患儿发病机理不明</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“每年暑假都有很多孩子来治疗脊柱侧凸，有的孩子明显一肩高一肩低，身体向一侧扭。”广东省人民医院骨科柯雨洪副主任医师指出，青少年脊柱侧凸又叫“脊柱侧弯”，在医学上是指发生在青春发育期前后的脊柱结构性侧凸畸形，也就是俗称的“高低肩”。国内外统计到的发病率在1%～3%左右。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">有些孩子是因单侧背书包等造成的姿势性侧凸，这不属于结构性的侧凸，通过改变不良姿势能够纠正。当然，如果脊柱处于长期不良姿势下，极有可能发展为结构性的侧凸。“而结构性的脊柱侧凸有八成患者的具体发病机理不明。”省人民医院骨科肖丹副主任医师说，已知的可导致脊柱侧凸的疾病主要有先天性脊柱侧凸、神经纤维瘤病、马凡氏综合症、神经肌肉性脊柱侧凸、脊髓空洞源性脊柱侧凸等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">女孩发病年龄较男孩早两年</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">柯雨洪指出，脊柱侧凸更偏好女孩，男女比例为1：4。国外有研究认为，女孩容易发病和雌激素等因素相关。统计显示，女孩多在10至14岁发病，男孩一般在12至16岁发病，较女孩晚两年，但因发育期长，结果反而更严重。由于侧凸出现初期孩子不会有明显不适，往往到发病一两年后才有肌肉酸痛等表现，而家长通常在孩子出现明显的“高低肩”、影响形体美观时才会有所察觉。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肖丹说，轻度的脊柱侧凸一般不会导致严重的后果，但是随着年龄的增长，未经治疗的脊柱侧凸会进一步发展，不单使患儿因外形畸形而产生自卑心理、减少了户外活动和社会交往，严重的还会因脊柱向一侧弯曲，压迫影响心肺功能，而进一步影响身心健康。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">省医刘斌副主任医师指出，脊柱侧凸的治疗时机最好是在生长高峰期，即12至15岁，尤其是女孩最好在初潮来后再动手术，此时骨骼生长相对稳定，对治疗效果影响较小。在此年龄段，越早治疗效果越好，尤其是先天性侧凸且弯曲角度较大的患者，治疗不宜拖。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">戴支具矫正才是规范疗法</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">柯雨洪说，目前，青少年特发性脊柱侧凸在治疗上可分为以下几种情况：在X光照片上脊柱侧凸程度即Cobb’s角小于20°的轻度患者，可在医生指导下进行形体锻炼以矫正姿势和减轻畸形，但要定期观察、随诊,防止病情反复。吊单杠、做引体向上是一个不错的办法。还可经常向侧凸的反向做体操动作，睡觉时则身体可单向卧，例如左侧凸则睡觉时左侧卧，并在凸起的一侧垫一个小枕头。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于这类青少年患者还要警惕成年后病情的反复。有些人从事体力工作会加重病情，不仅会重新“启动”侧凸的进程，还会因受力线偏曲，椎体退化而腰背痛以及其他神经系统症状。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脊柱侧凸呈40°以下的患者可先行支具矫形的非手术治疗，定期随诊观察，一旦侧凸角度进展加快需考虑手术治疗。作为一种治“本”的方法，目前比较成熟的手术是向脊柱里打钛合金“钉”将其“扭”向正常形态，这种材料与人体组织相容性良好，一般一生无需更换，只有极少数人会有不适或者因金属疲劳断裂而需更换。&nbsp;</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肖丹还澄清，如今有些医院以“电刺激矫正脊柱侧凸”作为卖点，“可以肯定地说，这种办法对病理性侧凸无效。”他更强调，在治疗脊柱侧凸方面，佩戴支具是目前唯一被医学界公认为有效的非手术方法。（记者任珊珊 通讯员张玲玲）</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"></span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"></span>
