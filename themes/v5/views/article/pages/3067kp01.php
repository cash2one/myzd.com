			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>运动创伤后长期制动的弊端</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在发生运动损伤后，我们通常在保守治疗或手术治疗两种方案中选择其一，无论采用何种方式应对，都应该在恢复过程中适时适度的引入康复训练，以保证尽最大可能避免或是减轻因制动造成的诸多障碍。 &nbsp;无论采用何种方案，最初目标都是重建解剖结构，在手术/保守治疗后，不同程度的制动是难以避免的，通常情况下，制动会产生人体各种功能退行性改变，主要体现在这样一些方面：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.损伤相关关节活动度下降——主要体现为粘连和挛缩。长期制动使相关关节持续处于相对静止状态，关节周围肌肉、肌腱、韧带、关节囊、皮肤等软组织会产生明显的挛缩现象，在关节活动过程中，处于关节运动拮抗方向的软组织会产生大大高于正常水平的阻力，当该阻力超越产生运动的主动肌的最大力量时，该动作即不能完成。同时，由于损伤中出血，大量成纤维细胞释放入组织间隙，从而产生“胶水”一样的作用，在组织中形成广泛的粘连。另外，由于制动，肌原纤维中可自由滑动的肌丝间，会产生大量阻碍其滑动的横键，从而在肌肉内部形成粘连。在整个制动过程中，挛缩和粘连同时发生，共同作用，训练时间稍为不及即会产生严重的ROM障碍，为训练埋下了困难的种子。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.损伤相关关节所属软骨质量下降——关节软骨的营养主要来源于关节液，在正常运动中，软骨面间的相互滑动、挤压会使软骨像海绵吸水一样在其内外环境中形成物质交换，从而实现其营养过程，长时间制动严重阻碍软骨的新陈代谢功能，进而导致软骨萎缩，退变加速，在日后的正常活动中，会加速退行性骨性关节炎的发生速度及加重其严重程度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.影响受损组织愈合——不利于组织修复。无论骨组织还是其他软组织，其愈合离不开适当的应力。以关节韧带为例：韧带的愈合（外固定保守治疗或手术重建）在处置后2周会出现明显的胶原纤维连接，6周左右纤维连接已较可靠，但此时纤维排列顺序杂乱无章，强度只接近正常韧带应力水平的约一半，不能达到正常的功能则治疗宣告失败。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.全身整体功能水平下降——由于绝对制动可造成不同程度呼吸系统（肺活量下降、肺栓塞等），循环系统（循环血量减少、血栓形成等），消化系统（食欲不振等）等各个系统功能退化，同时除损伤相关部位外，运动系统也会有整体功能水平的下降，如此全面的全身功能退变，对于体弱及老年人尤其具有恐怖的杀伤力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.心里因素——长期脱离社会对患者心理造成的巨大压力也是不可忽视的方面</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6.经济因素——这个时代断绝经济来源后果是很严重的</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总而言之，运动创伤后，长期的制动会对患者产生巨大的影响，即便局部过度制动也可能造成严重的后果，所以应以科学的方法，适时适度的针对患者伤情施以康复训练，力争患者伤而不残，早日重返社会。</span><br />
