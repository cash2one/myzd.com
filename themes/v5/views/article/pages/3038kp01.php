			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是婴幼儿视力筛查？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常常有家长咨询：我的宝宝在妇幼保健院筛查发现视力低下或有散光，怎样治疗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先我们应该了解什么是“筛查”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">筛查，在医学是指，在一定的人群中通过一定的方法或仪器识别没有迹象或症状、看起来是正常的个体尚未确认的疾病的方法。所以，筛查应该具有较高的敏感性，也就是尽可能不要漏掉异常，但敏感性高就可能存在特异性不高或假阳性的问题，就是不正常的误报为异常。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">筛查的目的在于早期发现可能存在、但无任何征象的疾病或可能会患某种疾病的相关因素，再通过一定医学检查和手段达到早预防、早诊断、早治疗的。也就说筛查的目的在于发现“可能”发生某种疾病或存在可能发生某种疾病的风险，但不是诊断，更不可能是确诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">筛查发现异常后需要通过医学检查才能诊断“是否”患某种疾病，筛查的结果不能作为治疗的依据。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，我国的婴幼儿视力筛查实际上是屈光不正的筛查。屈光不正包括远视、近视和散光。由于婴幼儿时期异常高的屈光不正或双眼屈光度不等（屈光参差）是弱视、斜视的风险因素，筛查可能发现这些因素，并通过到正规医院检查确诊，早矫正以期预防或治疗弱视、斜视。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同时，视力筛查也不能代替儿童视力保健，儿童的视力保健是在一定的医学机构里有具有一定眼视光学专业知识技能的人为儿童进行系统的检查，包括眼部健康检查（外眼、眼前节和眼底）、眼屈光度、眼位和眼球运动等。而视力筛查仅仅是屈光度可能异常的识别。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，视力筛查可以早期发现可能的异常屈光不正、弱视和斜视的发病因素，但不能作为诊断、治疗的依据。</span><br />
