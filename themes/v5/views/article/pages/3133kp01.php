	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>怀疑脑积水后应该查什么？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一旦怀疑脑积水，以下是一些必查项目：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">头颅CT扫描，是检察头部疾病最简便快捷的手段。可以发现脑室扩张的情况，初步排除颅内出血以及是否有肿瘤等占位性疾病。缺点是，对于婴幼儿CT检查会有一定量的辐射，可能对发育有影响，目前并没有明确的结论证明这种损伤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">头颅核磁共振扫描，MRI对于脑积水的诊断可靠性更高，能发现一些头颅CT不能发现的情况，比如梗阻粘连的部位，是否有肿瘤等。而近几年发展的核磁共振放电影技术可以对脑脊液的流动情况作出判断，该技术可作为治疗前后对比的有效手段，来判断治疗效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰穿检查，是脑积水治疗前最重要的检查，可以测量出颅内压力，化验脑脊液情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑池造影，是在腰穿的同时向蛛网膜下腔注入造影剂，病人头低臀高位维持15分钟，做头颅CT扫描，检查造影剂分布的情况，发现梗阻的位置，以便决定下一步治疗方案。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">视力和眼底检查，是否有视神经乳头水肿，眼底有无出血等。</span><br />
