<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>11岁病重男孩在附属儿中心完成生命最后的奉献</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 6月14日晚8点40分，11岁的饶小兵（化名）躺在病床上，穿着蓝色的校服，离开了这个世界，但他却留下了光明。他的父母为没能等来心脏移植的他做了最后一个决定——捐献角膜。晚上11点半，在经过严谨的器官捐赠程序后，角膜摘取手术顺利完成，小兵完成了他生命最后的奉献。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 来自江西的小兵于5月29日突然发病，全身浮肿，前往当地医院医治。医院诊断为扩张性心肌病，由于小兵之前从未发病，或是发病时未得到重视，此时病情已是晚期。当地医院紧急转往上海交通大学医学院附属上海儿童医学中心，6月2日小兵入院，心内科进行了积极治疗，但由于病情过重，小兵的心脏功能极弱，6月13日开始昏迷。令人感动的是，此时小兵的父母主动表示，愿意捐献眼角膜给需要的人，让小兵的生命延续。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孩子父母的义举让守护他的医护人员非常动容，医护人员为小兵穿戴整齐，并让孩子的父母进入监护室，与孩子告别。看着悲痛的父母，医护人员深有感触，因为小兵本身也是一位等待心脏捐献的病人。上海儿童医学中心心胸外科主任张海波表示，小兵所患的扩张性心肌病是最常见的心肌病类型，是左心室或双心室心腔扩大和收缩功能障碍，产生充血性心力衰竭，常伴有心律失常。此病起病缓慢，患者早期可能无症状，但患者会有胸闷，气急、乏力的的临床症状。最初在劳动或劳累后气急，随着病情发展，会在轻度活动甚至休息时也有气急，但由于症状不明显，往往容易被忽视。张海波主任同时指出，如果有合适的心脏为小兵进行心脏移植术，或许小兵仍有生命的希望。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 儿中心重症监护室主任王莹医生表示，中国是世界上人口最多的国家，但也是等待器官移植患者数量最多的国家。数据显示，与国际比较，中国人体器官捐献的规模和需要器官的患者数比例严重偏低。目前，全国每年有150万左右患者需要器官移植，但能实现移植的仅有1%左右，供需矛盾十分突出。2011年上海儿童医学中心成为上海市心脏死亡捐献器官移植试点医院，获准开展心脏移植项目。儿中心每年实施3500例小儿心脏病手术，收治各种小儿先天性心脏病，治愈率达97%以上，具有非常丰富的临床经验。然而，对于复杂性或未期先心病患者来说，心脏移植最有效的方法之一，许多在儿中心治疗的复杂先心病患儿中不少正等待着这一线希望。同时医院已经成立专项技术队伍，包括心内科的诊断和心肌活检、外科的供体采集、小儿心脏移植小组、术后监护及抗排移反应等心脏移植团队，随时保证医疗工作顺利开展。然而，器官来源十分稀有，捐献心脏的更少，因此，到目前为止，医院仍未开展过心脏移植手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 小兵及其父母的义举，给予医护人员极大的信心，希望通过这股正能量的传播，令更多的人能转变理念，给患有危重症的孩子希望，让生命之花不再那么轻意地凋谢。</span>
</p>
