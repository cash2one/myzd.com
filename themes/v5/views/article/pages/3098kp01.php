<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>绒毛结节性滑膜炎&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">绒毛结节性滑膜炎，全称色素沉着绒毛结节性滑膜炎，是一种以滑膜增生、关节大量积液、伴有骨侵蚀的一种滑膜炎。1941年Jaffe对该病进行详细描述，病因不清。目前研究认为是滑膜炎性反应，尽管归属为良性肿瘤，但是病变为“恶性”增生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多发生于四肢大关节，以膝关节多见，也可发生于髋、踝、肘关节等。可单关节发病，也可多关节发病，根据发病部位以及病变反复，本病分为局限性和弥漫性。主要症状为患病关节明显进行性肿胀，并伴有局部疼痛、压痛、活动受限。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该病通过MRI检查可以得到初步诊断，但最终需要通过组织病理检查确认。色素沉着绒毛结节性滑膜炎的MRI表现具有明显的特征性。MRI能敏感而清晰显示滑膜的增厚和积液的程度，能显示滑膜的绒毛状或结节状的隆起以及关节间隙周围的软组织肿块。由于病变滑膜组织内含铁血黄素的沉积，故在T1加权像和T2加权像上均呈低信号。这是MRI诊断色素沉着绒毛结节性滑膜炎的特异性征象。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗主要是滑膜切除术，并对已经侵蚀的软骨也要进行刮除。对于弥漫性滑膜炎，治疗的关键彻底清理病变的</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">滑膜组织，但是往往常规手术难以清理干净，通过关节镜下可以达到关节的全面清理，需要术者具备良好的关节镜技术。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一些研究认为术后放疗可以防止复发，但治疗的关键还是彻底清理病变滑膜。</span>
</p>
