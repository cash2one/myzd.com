<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>李志伟教授专访：解读2015门静脉高压症高峰论坛（资讯）</strong></span> 
</p>
<p>
	<span style="font-family:Microsoft YaHei;font-size:18px;"><strong><br />
</strong></span> 
</p>
<p align="center">
	<br />
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2015年11月07日，2015门静脉高压症高峰论坛在解放军第302医院学术报告厅隆重召开。本次高峰论坛借“三O二医院门脉高压外科”成立5周年之际，由北京医师协会手术技艺研究会主办，解放军第302医院承办。来自全国30余家医院的200余位外科、内科、介入治疗科的医务工作者参加了论坛。</span> 
</p>
<p>
	<img src="<?php echo $baseUrlImage; ?>article/doctor/3017/1.png" title="" alt="demo.jpg"/>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">此次论坛推动了国内门静脉高压症诊疗的学术与发展，加强了区域和学科之间的交流合作，为致力于门静脉高压症诊治的同仁提供了一个相互交流，相互学习的平台。医脉通在会议期间有幸采访到了此次高峰论坛的执行主席李志伟教授，李教授为我们介绍了本次论坛的目的和门静脉高压症的治疗现状，详情如下：</span> 
</p>
<p>
	<img src="<?php echo $baseUrlImage; ?>article/doctor/3017/2.png" title="" alt="demo.jpg"/>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">门静脉高压症高峰论坛召开的意义</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">门静脉高压症的病情比较复杂。从我们国家来看，中国是个肝炎大国，大部分肝硬化是各种类型的肝炎病毒引起的，因此所涉及的病例非常多。肝炎发展到一定阶段是肝硬化，肝硬化的严重并发症之一是门脉高压消化道出血。然而，消化道出血病情很复杂，多个学科通过多种措施都会涉及治疗这种疾病，各有各的优缺点，而每种治疗都有各自的最佳适应症。以门静脉高压外科为例，汇集了内科、外科、介入科，甚至眼底这类交叉学科，通过多学科探讨一种疾病的诊断和治疗。在本次门静脉高压症高峰论坛上，汇集了行业内顶尖的相关专业的教授们，他们详细介绍了自己的治疗体会和国内进展。</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">其次，本届高峰论坛为何要在解放军第302医院召开？因为，302医院是一家历史久远的，全军唯一的传染病专科医院，也是全国最大规模的传染病专科医院，一年门诊量94万，住院量有6万多，其中85%以上都是各种类型的肝病，“三O二医院门脉高压外科”成立于2010年6月，也时国内唯一一家以“门脉高压外科”命名的、名副其实的专病专治的专科病房。所以三O二医院外科非常有特色，门脉高压一年最多能做358台，是全国不可想象的，累积3000多例手术。此外，肝癌，肝脏移植也是我们的长项。在治疗门静脉高压症上，我们医院有一定的治疗经验，但是毕竟是一家之言。所以，借此次机会把大家请来包括基层同仁，如实汇报一下我们方方面面的实际工作，以及涉及到这种疾病诊断治疗的各个方面，接受大家的建议、指导，有利于我们在这个学科向更好、更规范、更科学、更合理的方向发展，同时也给基层做个示范，相互学习交流，以期做的多更要做的好。</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">第三，希望通过我们多中心跨单位、跨学科的深度合作，找到制约学科发展的瓶颈问题、关键问题、疑难问题来联合公关，紧密结合临床解决实际问题。正如，北京医师协会手术技艺研究会提到的，我们一起借助这个平台，把全国有兴趣也有条件、有资源研究门静脉高压症的医生们，定期或不定期组织在一起，共同商讨这个病的最佳适应症，最佳治疗的策略，将整个疾病的诊疗水平提到更加科学合理的程度。本次高峰论坛上，我们邀请到了全国门脉高压主任委员的权威为我们把脉，一方面是鉴赏，一方面是检验，指导我们与同行集体协作，集体公关，让门静脉高压症更规范更合理更科学，也使我们有限的经验在兄弟单位相互交流。</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><br />
</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">门静脉高压性引起上消化道出血的防治现状</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">如何预防门静脉高压性出血？目前全世界范围内防治门静脉高压引起上消化道出血的方法一共有6种，药物、内镜、介入、常规手术、肝脏移植，还有一个是紧急情况下三腔管压迫止血。每种方法都有最佳的适应症，比如静脉曲张很轻时，可以观察，静脉曲张还不是很严重的时候，可以药物治疗，药物预防。尤其是对一些不适合手术的病人，年龄大，有禁忌症的药物治疗，内镜是金标准，是首选。不管是首选还是预防，内镜联合药物的效果很好。然而，内镜要求的技术含量以及设备比较高，小型城市医院一般不具有这个条件。手术，虽然不是最佳选择，但目前疗效最持久，止血最确切最可靠，相对费用比较低。内镜需要反复治疗，耗费时间长，效果相对差，花费高，像一个疗程的内镜花费在三万以上，做上几次得多少钱了，而手术费用几万块，在所有的治疗手段中算是最确切的。此外，还有一个治疗的次序问题，先药物还是先内镜？基本上遵循的是，出血止不住的话，肝功好的选择手术，肝功不好的选择TIPS，再不行就得换肝。如果手术前内镜治疗的话，手术完成后有可能问题不在脾上，也不在胃上，而是出现在肝上，那么肝硬化问题的没解决，门脉高压问题也没解决，还有再出现的可能，这时候也只能选择内镜治疗。因此，每种办法都有自己的最佳适应症，需要各科治疗措施的互相配合。</span> 
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2015门静脉高压症高峰论坛为我们提供了一个良好的学术交流平台，希望可以为我国门静脉高压症外科事业的发展贡献一份力量。</span> 
</p>
