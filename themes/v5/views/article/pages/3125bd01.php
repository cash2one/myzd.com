			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>达芬奇手术机器人的第555场胃肠癌手术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3125/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6月19日，重庆西南医院外科手术室，该院医生正在进行达芬奇手术机器人的第555例胃肠癌根治手术。本组图片由李相博拍摄</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 新华网重庆频道6月20日电（李相博）19日上午，56岁的老赵躺在西南医院外科楼11号手术室的床上，准备接受一场手术治疗。不久前，腹部持续出现疼痛的他被确诊患有胃癌。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 相比其他手术室的忙碌景象，11号手术室稍显“冷清”，偌大的手术室里，只有一名助理医生和护士站在手术台两边，主刀医生在坐在手术台不远处的机器边，埋头观看显示屏，双手握住摇杆不停舞动，还有一名护士在旁登记术前麻醉信息。另一个不同的是，手术台边，一台接近两米、名为达芬奇手术机器人的“傢伙”不时地挥动着 “手臂”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 11号手术室的“冷清”，与“达芬奇”密不可分。 对于“达芬奇”和主刀医生余佩武而言，正在进行的这场手术再普通不过了，可却也意义非凡。因为这是“达芬奇”来到余佩武担任主任的西南医院普外科后进行的第555例胃肠癌根治手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在余主任看来，和以往的外科医生操刀手术相比，达芬奇机器人的优势着实不少。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 余主任称，自从达芬奇机器人于2010年引进普外科后，该科室进行的胃肠癌手术所需的医生及护士人数明显下降，从以往的3-4名医生3名护士，变为目前的两名医生，两名护士。此外，主刀医生不再需要站在手术台上，而是坐在操作台前，看着3D立体显示屏，操控主控制器。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 由于“达芬奇”的机械臂采用微创技术，进行像老赵这样的胃癌手术，只需在其腹壁打上四个孔，将机械臂伸进去，便可进行手术。而传统的手术方法，则需在病患腹壁上划开一道近20公分的创口，不仅会导致患者出血多、承受巨大痛苦，而且术后恢复缓慢，还会留下难看的疤痕。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">与此同时，达芬奇机器人的四根机械臂都安装有稳定器，具有人手无法比拟的稳定性及精确度，动刀时快速而准确。“再有经验的医生，也不排除会因为精力状况、时间长短等而影响手术发挥，就比如手抖”，余主任说道，但对于“达芬奇”而言，这一切都不会存在。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 此外，相比腹腔镜手术中手术器械只能如“筷子”一样操作的不足，达芬奇机器人的机械臂更为灵活，不但能够伸进病患部位，还能进行540度旋转。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 另外，通过安装在其中一根机械臂上的高分辨率3D镜头，在伸进病患部位后，便能实时传输手术部位的3D立体图像，并将手术视野放大15-20倍，克服传统腹腔镜画面缺乏层次的不足。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">达芬奇机器人在手术治疗中的应用，不仅提升治疗水平、降低病人痛苦以及医务人员劳动强度，也让余佩武所在的团队再次走在微创外科的前沿。在完成老赵的胃癌手续后，余佩武带领的西南医院普外科累计成功完成555例胃肠癌根治手术，手术数量排名国际单中心最大宗病例第一名。</span><br />
