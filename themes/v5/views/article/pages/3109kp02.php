<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">癫痫是一组最常见且起因较复杂的临床症候群，俗称“羊痫疯”、“羊羔疯”。流行病学调查资料表明：癫痫的患病率为4‰-9‰，由世界卫生组织在我国组织的大规模的调查报告为8‰，我国现有癫痫病人1000多万人，同时，我国的新发病率为25/10万/年，年新增发病人数为35万人。其中70%-80%的病人在医师指导下经正规服用抗癫痫药治疗后可从事正常工作、学习或生活，但也有20%-30%的病人因长期服药无效或泛用、乱用各式各样抗癫痫药而导致成药物难治性癫痫，也有的发展为癫痫性精神病，这些病人极大的损害了病人的心身健康并造成社会的不安定，给家庭造成极大痛苦。由于癫痫是一种慢性疾病，绝大部分时间病人生活在家庭之中，所以治疗与恢复过程中家庭成员的护理非常重要。随着生物-心理-社会医学模式的转变，癫痫患者的家庭护理显得尤为重要。简单地讲癫痫的护理分为平时护理与发作时护理，平时的护理又可以分为治疗护理、心理护理、生活护理三部分。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一、治疗护理：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1．&nbsp;&nbsp;病情的观察：由于癫痫发作时大部分病人出现意识不清，不能正确讲述癫痫的发作情况，主要是家属的观察，特别是儿童病人。但由于病人一旦出现癫痫发作时家属往往十分紧张，忘记对病情的观察，所以80%的家属在到医院看病里并不能讲述病人的详细发作情况，在一定程度上会影响癫痫病情的判定与治疗。主要的观察要点是1）病人发作有无规律性，如是夜间多发还是晨起多发，女性病人与月经有没有关系，什么情况下容易多发，如休息不好、食用辛辣食物、情绪、感冒等；2）发作的情况：一般持续多长时间，发作前有无胃部不适、幻视、手脚发麻等感觉，发作时先开始抽搐的部位，头眼有无向一侧偏斜，小便失禁、摔倒等情况；3）发作后病人昏睡时间，有无头痛等。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2．&nbsp;&nbsp;服药观察：1）督促病人按时、按量服药，减少漏服、少服，增加病人对医嘱的依从性，癫痫病人由于长期服药，容易对服药产生厌烦心理，经常会漏服药物，特别是青少年，容易将药物扔掉，而病情一旦得到一定时间的控制后病人是最容易认为癫痫已经治好，忽视医生的劝告，放弃服药，往往使治疗功亏一篑。2）在换药、减药过程中注意缓慢减药，切忌突然停药，新换药物一般需要5个半衰期后才能发挥良好的抗痫作用，而原药物在一个半衰落期后就很少有治疗癫痫的作用，所有在相当长的时间内会出现有效药物的中断，所以在换药中突然停药往往容易发生癫痫持续状态，严重时有生命危险，而减药中速度过快可以引起癫痫复发，长期的治疗前功尽弃，同时也要注意防止病人在加药或换药中加量过快，如妥泰片等药物加量过快可以引起严重的副作用。</span><br />
