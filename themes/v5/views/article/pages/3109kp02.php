<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4．&nbsp;&nbsp;疗效观察与就诊建议：一般出现3次癫痫样发作后才能诊断癫痫，癫痫一旦诊断往往需要进行药物治疗，当前我国的癫痫治疗较为混乱，特别是一些单位在中药中添加大量多种便宜的西药或一些有害金属粉剂进行治疗，往往效果不佳或是有明显效果但容易引起药物中毒或其它毒性反应，所以家属要引导病人到有癫痫专科的大型医院进行服药治疗。服药中家属要如实、详尽地记录服药情况（包括加、减、调整药物情况）、药物的副作用和治疗效果，给医生提供第一手的资料，利于医生选择药物，病人可在1-2家大型的癫痫专科中心进行长期的随访治疗，同时家属要为病人保管好所有的化验检查资料，以便于进行前后对比，医生对病人服用药物剂量及种类也便于掌握，可以提高治疗效果，避免反复调整药物。对于3年以上的发作频繁或发作随时危及自身或家属生命的病人要劝期到外科癫痫中心接受外科手术治疗，其中约70%的病人可以得到根治或良好的效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二、心理护理：癫痫患者往往对其频繁发作产生恐惧、焦虑和紧张的心理，另外由于患者往往很难治愈，同时受到社会或周围人员的歧视而产生自卑心理与自闭现象，另外部分癫痫病人可以表现为精神异常性的发作或伴有各种精神障碍，所以家属要注意观察癫痫患者的异常精神表现与行为变化，同时要积极对病人从以下几个方面进行心理护理。1）帮助病人正确认识癫痫病：病人出于对癫痫病的高度紧张和自卑而拒绝承认自己是癫痫病人，这类病人往往很难配合医生的治疗，经常出现停药与漏药现象，另外一些病人是渴望一种正常人的生活，不能接受患病人的现实，身体与心理上都得不到很好的休息，经常诱发癫痫发作，这两类病人多是青年及中年病人，家属给予病人更多的关爱，多向病人宣传癫痫的可防可治，找病人谈心，与病人交朋友，帮助病人承认癫痫病，鼓励病人接受癫痫可能产生的社会影响。还有一类病人与家属对癫痫非常害怕，一旦诊断后就认为自己什么都不能做了，完全是一个病人，学生连体育科都不能上了，家务活什么也不能干了，再加上家属的迁就与保护，使病人基本上脱离社会，而且容易发展为性格古怪，甚至于严重的精神障碍，这种病人主要是青少年或老年发病者，主要的是鼓励病人尽可能走出家庭、回归社会，恢复正常的社会交往，在保证病人安全的情况下进行学习与工作，作一个对社会有用的人，在接受治疗的前提下去掉自己的病人心态。另外一些病人认识癫痫只是偶尔抽搐一下，并不能影响什么，发作后就看看医生，然后也不吃药，反复发作后往往病情加重，这些病人多是成年人就要向其讲述癫痫的危害性与危险性，说明癫痫长期治疗的必要性。2）给病人创造良好的心理环境：家属的心理状态往往严重影响病人的心理，所以家属要避免精神过度紧张，怕让别人知道了会没面子的心理对病人影响是最大的，一定不能有，要多与病人进行沟通，了解病人的痛苦，帮助解决困惑。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三、&nbsp;&nbsp;生活护理：生活护理是基本的内容，主要是建立良好的生活习惯，避免过度劳累、睡眠不足、高热等情况，避免刺激性食物，尽可能避开危险场所和危险品，不要单独上山或下水，儿童不要进行强刺激性游戏，成人避免开车、骑自行车等高度紧张的事情。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">四、&nbsp;&nbsp;发作时护理：1）家属要保持冷静，及时观察记录病人发作情况，同时不能强行搬动或按压病人，防止发生骨折。2）尽可能将病人放在一个平坦、松软的位置，避免摔伤；3）将病人腰带、领带、红领巾等松开，头偏一侧，将口内流出、吐出食物等及时擦静，防止吸入，同时不要进行灌水清洗口腔。3）对于部分表现为精神运动性发作的病人要保护好病人，防止到处走动，避免伤及自身或他人；4）抽搐停止后要尽可能让病人休息；5）如果病人出现出现误吸、连续抽搐达到30分钟以上或者两次抽搐间病人未恢复清醒时，要及时向医疗单位报告，寻求帮助。</span>
</p>
