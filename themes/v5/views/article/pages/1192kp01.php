		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>周华的病能治好吗?</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">看过电视连续剧《爱你没商量》的人都知道，周华是剧中的女主人公。整个剧情就是围绕着她患尿毒症而展开的，时而催人泪下，时而皆大欢喜……观赏之余，人们或许会问：尿毒症究竟是怎么回事?它有多大的危害?能否彻底治好?与周华同病相怜的尿毒症病人及其亲属，一定更为关心这些问题。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 1．肾“亡”人亦危</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 所谓尿毒症，又称慢性肾功能衰竭。其诱发病因很多，其中最主要的是肾脏的一些慢性疾病，逐步发展到不能有效治疗和无法收拾的地步，肾功能所剩无几，无法继续担负维持生命的重任。比较常见与容易发展到慢性肾功能衰竭的疾病是慢性肾炎、肾病综合征和先天性多囊肾。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 人体好像是一个大型化工厂，肾脏则好似“废水处理车间”。体内多余的水分和钠、钾等电解质，主要通过肾脏“制”成尿液排出体外。发生尿毒症时，水、钠潴留可引起“水灾”泛滥，不但会全身水肿，严重者连五脏六腑也会发生“水灾”。于是肺水肿、脑水肿等接踵而至。机体在新陈代谢过程中，不断产生许多有毒的废物，必须通过肾脏的处理，随尿液排出体外。有人形象地将这些废物称为“尿毒素”。肾功能丧失之后，这些尿毒素就会滞留在体内，毒害全身各组织器官，从而产生一系列症状。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 显而易见，尿毒症如果不及时治疗，后果不堪设想。绝不能等闲视之，必须积极采取有效治疗措施。目前，最有效的办法只有两个，一个是“洗肾”，一个是“换肾”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;2．洗肾可治“标”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;周华的病情就是通过洗肾而有所缓解的。洗’肾的“学名”叫血液透析疗法，就是利用人工肾机将病人的血液引流到透析器内，经过4～5小时的反复“洗涤”过滤，把血液中的“尿毒素”清洗出去。故又有血液净化之美称。由于体内每时每刻都在产生“尿毒素”。因此，每周必须透析2～3次，以解“燃眉之急”。靠这种疗法，病人亦可存活数年乃至数十年。不过，人工肾毕竟无法完全替代人的肾脏，它不具备天然肾脏各种复杂功能。所以，只是一种部分的替代疗法。用中医的话来说，它只能治“标”，不能治“本”。病人一旦开始接受洗肾，就必须长期依赖，不能摆脱。然而，对于已陷入绝境的病人来说，通过洗肾能够明显延长生命，改善生活质量。同时，可以为准备进行“换肾”治疗的病人争取宝贵的时间。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 3．换肾可治“本”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; “换肾”的医学名称是“同种异体。肾脏移植”，是把一个来自供体(亲属或尸体)的健康肾脏“安装”到尿毒症病人的身体里，让它发挥正常的肾功能，维持病人的生命。将肾移植说成是“换肾”，虽然比较形象，比较大众化，但并不确切，因为。肾移植并非把病肾切除，在原位换上一个新肾。通常两只病肾并不需要切除，除非病肾患有肿瘤、结核、结石、多囊肾伴感染等，非切除不可。不论病肾切除与否，移植的健康肾脏总是“安装”在病人的下腹髂窝部，各有所居，互不相干。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 近50年来，特别是新一代的免疫抑制剂环孢素A和FK506广泛应用以后，肾移植获得引人注目的发展。目前，全世界接受肾移植治疗的病人已近60万例次。到2009年底，我国肾移植总数已近10万例次，成功率已接近或达到世界先进水平。上海长征医院从1978年至今的30年间，共完成肾移植3600例次，居全国之首。肾移植1年人／肾存活率分别达到98％／96％以上。现有1373例已存活超过10年以上，近2 500余人恢复了正常生活，一般状况良好。有的还恢复了生育能力，或重返工作岗位。可见，只有肾移植才能从根本上消除尿毒症的危害，是最为理想的疗法。倘若有条件接受肾移植手术，那么周华(当然也包括所有的尿毒症病人)是完全有希望重获新生的。</span>
</p>
