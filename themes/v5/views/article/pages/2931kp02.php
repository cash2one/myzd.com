		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>家长：不要为宝宝包茎太操心</strong></span>
</p>
<p>
	<br />
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2931/4.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对幼儿来说，包茎大多数是一个生理过程。家长无需太担心。（资料图片）</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 目前，医学上对包茎的手术指征仍然存在着争议，只有包皮口瘢痕性狭窄这种情况才绝对需要手术。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 5岁的小虎前些日子在幼儿园常规体检中被查出患有包茎，父母带他到医院求治。医生建议先采用手法扩张治疗，也就是用手强行将孩子的包皮向后推开，外翻包皮，显露龟头。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这种治疗方法虽然有效，但会造成剧烈疼痛。小虎为此产生了强烈恐惧，每次治疗都拼命挣扎哭喊。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">心疼不已的父母商量着是否赶快做手术算了，但是，他们又听说如果手术将孩子的包皮完全切除，龟头将没有包皮的遮盖，这样阴茎头和裤子长期摩擦，会使敏感度下降，影响日后的性生活。这可如何是好？</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 家长：不要为包茎太操心</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 其实对幼儿来说，包茎大多数是一个生理过程。统计资料显示，在新生儿中，仅有4％的包皮可以外翻，另有54％的包皮通过轻柔的手法后推可以显露龟头顶部。以后随生长发育，包皮口将逐渐扩大后退，可以完全外翻显露龟头。绝大部分包茎并不需要过多的干预。就像婴儿咿呀学语和蹒跚起步的时间，个体间可以存在很大差异，但最终孩子都会完成这一过程。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医学上也没有任何证据提示这一过程完成的早晚，对今后的生长发育或婚姻生活有影响。被认为拥有可能是人类历史上最聪明头脑的爱因斯坦，直到4岁才开口说话，他的父母和体检医生当时也一定非常焦急。因此，有时我们最好耐心等待。包茎绝大多数时候不会是孩子的麻烦，也不应该成为家长的心病。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 包皮不应成为“替罪羊”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 幼儿中尿频、尿急常见，症状可以间歇性出现，好好停停，在男孩常常被归咎于包茎。其实它们并非由尿路感染引起，也没有包皮龟头炎的表现，大多呈一种自限性过程。只是家长和幼儿园老师往往非常担心，非专业的医生在家长的压力下往往将包皮作为“替罪羊”，甚至不惜对它动刀。可是包皮环切术并不能真正改变这些症状，其治愈大多还是需要我们的耐心等待。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 部分存在生理性包茎的幼儿排尿时，尿液可能聚集于包皮下，使局部包皮鼓成球形。这往往令家长很担心，不惜让孩子接受强行包皮扩张或手术。其实，幼儿本身对这一现象并不以为然。他们并不会感到任何不适。家长只需在平时洗澡和清洁时，向后轻轻推开孩子的包皮，显露龟头顶部，反复多次以后往往可以改变这一情况。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 需要当心的是包皮龟头炎</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当然，患有包茎的孩子在成长过程中也会出现一些与包皮有关的异常，最常见的就是包皮龟头炎，这在未进行包皮环切手术的男孩中的发生率是4％。包皮龟头炎表现为包皮红肿以及少量渗出，多见于2～5岁的男孩。其发生可能与感染、损伤、局部刺激和接触过敏有关。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对于多数患儿来说，包皮龟头炎是一个自限性的疾病，并且随年龄的增长而发生率下降。这也印证了随着生理成熟，包皮可以外翻使得局部易于清洁。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 包皮发生炎症时，可以尝试局部或全身的抗菌药物治疗。但很多时候仅需洗一个热水澡局部清洁，就能缓解不适。而反复发作的包皮龟头炎，是包皮环切术的相对手术指征。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术不会影响性功能</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 目前，医学上对包茎的手术指征仍然存在着争议。不同的医院甚至同一医院的不同医生，具体操作过程中都有差别。有时候家长在一家医院得到的建议是观察等待，而另一家医院却是立即手术，于是感到无所适从。其实，家长们自己心中应该有个谱：</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1.只有一种情况绝对需要手术，那就是包皮口瘢痕性狭窄。与生理性包茎不同，瘢痕性包茎的包皮口可见坚硬的瘢痕组织使包皮口失去弹性，不能扩张后退显露龟头，一般只能通过包皮环切术进行治疗，但瘢痕性包茎在5岁以下的儿童中较为少见。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2.即使是生理性包茎，而家长和医生都认为应该手术，也不需过于担心。虽然手术总有风险，但那种认为手术后阴茎头和裤子长期摩擦会使敏感度下降，影响日后性生活的观点，是缺乏依据的。试想，以色列等国家因为宗教原因，男孩都接受包皮环切手术，而北欧国家男孩接受包皮环切术的比例很低，但没有任何证据提示这两个地区成年男性的性功能障碍发生率有明显区别。</span>
</p>
