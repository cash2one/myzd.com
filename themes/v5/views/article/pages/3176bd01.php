<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>消化道癌发病率高</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;">&nbsp;&nbsp;&nbsp;&nbsp; 记者&nbsp;杨振东</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;“癌症早发现早治疗是业界共识，但实现早发现却并不是任何一名患者或医生所能做到的，这里面大有学问。”5月23日上午，在解放军第153中心医院参加会议间隙，解放军总医院消化科主任医师卢忠生接受大河健康报记者专访时坦言。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;为什么一发现就是癌症晚期？</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：对于早发现，患者为什么难以做到呢？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：在现实生活中，我们常常发现这一现象，很多消化道癌症患者，往往一发现就是癌症晚期。他们很多人是有了明显的消化道症状才去就诊，还有些人并没有什么特别不适，甚至能吃能喝，可是到医院一检查，竟然得了癌症，甚至是癌症晚期。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：消化道癌症发病率很高吗？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：食道癌、胃癌、大肠癌等消化道癌症，在我国发病率和死亡率一直居于前6位。其中胃癌在我国常见恶性肿瘤发病率居第二位。好发年龄一般在50岁以上，男女比例为2∶1。这些消化道癌症发病率高，但由于常规体检项目不做胃肠镜检查，很难发现。一旦出现症状或者被检查出来，一般都到了中晚期。手术、化疗、放疗，花费少则几万元，多则几十万元，却仍会复发或转移，5年生存率很低。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：对于患者来说，如何及早发现消化道癌症呢？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：要做“消化道体检”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：怎么做？</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp; 卢忠生：消化道癌若要早发现，最有效的方法是做胃肠检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：哪些人需要做“消化道体检”呢？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：根据我国国情和胃癌流行病学，以下符合第1项和2～6项中任一项者均应列为胃癌高危人群，建议及时做“消化道体检”。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;①年龄40岁以上，男女不限。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;②胃癌高发地区人群。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;③幽门螺旋杆菌感染者。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;④既往患有慢性萎缩性胃炎、胃溃疡、胃息肉、手术后残胃、肥厚性胃炎、恶性贫血等胃癌前疾病者。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;⑤胃癌患者一级亲属。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;⑥存在胃癌其他高危因素（高盐、腌制饮食，吸烟、重度饮酒等等）。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;早癌筛查医生须接受至少三个月专项技术的正规培训</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：如果这样的话，提醒这些人去附近能做胃镜的医院去做个胃镜不就行了吗？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：做内镜及内镜下活组织检查，这个建议是对的，因为这是目前诊断胃癌的金标准，尤其对平坦型和非溃疡性胃癌的检出率高于X线、钡餐等方法。然而，内镜诊断依赖内镜图像质量和内镜专家经验，并非所有的内镜设备和内镜医生都可以检出早期病变。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：为什么这么说？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：要想早发现，内镜是关键。早期癌症在内镜下的表现其实并不明显，如果内镜设备老旧，检查前准备不充分，无法获得清晰的内镜图像，医生就看不出早期癌症。而且，如果医生经验不丰富，也可能识别不出早期病变。对于可疑病变须做高清染色放大内镜检查，比如“窄波成像放大内镜”，窄波可以穿透黏膜，并被血液吸收，呈现的图像更加清晰，仿佛染色，却无需颜料，所以叫电子染色。电子染色内镜，同时配备放大内镜，可以放大100~200倍。既染色，又放大，诊断的精确度很高。同时，要求医生经过至少3个月的正规培训，积累了丰富的经验才行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：到哪儿培训呢？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：比如来我们医院培训，或者在我们远程指导下操作。像解放军第153中心医院消化内镜中心，就引进了最先进的胃肠镜设备，并全员接受过我们的专业培训。这样，早癌发现就有了保障。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;记者：有了最先进的设备，接受了正规培训，消化道癌症早发现就不成问题了，发现后如何治疗呢？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;卢忠生：发现消化道早癌后，就可以在胃肠镜下实施“内镜下黏膜剥离术”，将其扼杀于摇篮之中，消灭于萌芽状态。至于大肠癌，只要及时发现并掐掉息肉，就可预防并消除早癌，大肠无息肉，就不会有早癌。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;消化道早癌向中期、晚期发展很快，但是从癌前病变向早癌的发展却是缓慢的。食道癌、胃癌的癌前病变阶段可能长达好几年，叫“不典型增生”或者“上皮内瘤变”，最后发展到原位癌。原位癌发展为癌，又需要一年多。如果每年做一次新型内镜检查，只要医生技术熟练，一定可以发现癌变，消除癌变。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;总之，希望大家到了一定年龄，依据是否属于高危人群，定期接受胃肠镜检查。内镜医生也要努力提高早癌的诊断能力，正如解放军第153中心医院消化科黄锦主任提出的口号：“发现一个早癌，挽救一个家庭。”、</span>
</p>
