		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>【刨根问底】追问冬雷脑科医生集团受资本热捧之谜</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">宋冬雷创建的脑科医生集团(Brain Doctor Group)很受资本追捧，已经先后有四家投资机构踊跃掏钱，最新一家是专业医疗基金弘晖资本。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “用不了用不了，我们现在的规模还用不了这么多钱，况且我们本来就已经有收入了。” 关于这些融资，宋冬雷有些打趣的说。可实际上，按照他自己关于医生集团未来的很多发展设想，“到时候，可能就要花很多钱。” 当然，宋冬雷受追捧，肯定不只是因为一个大计划。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宋冬雷是名医， 既因为医术，他曾经是复旦大学附属华山医院神经外科教授、主任医生、博士生导师以及若干头衔，通常人们眼中的大专家；又因为魄力，他 2013年 离开体质自由执业，出任私立医院德济医院的院长。2015年9月 创办医生集团，是宋冬雷最新的一个大动作。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对很多医生来说，从公立医院到民营医院已经是职业生涯的巨大跨越。何况按照宋冬雷自己关于自由执业的理解，“只要走出体质，都是自由执业。” 可是，他为什么又要离开民营医院，在五十岁的年纪要去 “折腾” 一个具有很多不确定性的医生集团？</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “我要给患者提供最好的服务。” 尽管听到很多人在很多时候谈到这个话题，但真的有人愿意这么坚持的去追求这件事仍然很意外。“公立医院提供的服务很难满足患者的需求，民营医院虽然在某些方面做了改进，但整体还是处在一个比较低的水平上。” 宋冬雷说。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 如今，成立自己的医生集团后，宋冬雷终于有机会完全按照一个理想的模式，来亲手构建医疗服务体系。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这时已经是下午 5 点，上海因为一直在下雨，天色暗得要更早一些。经过一天的忙碌之后，宋冬雷依旧精神饱满。我们到的时候，刚好赶上他在手术，后来又随他一起查房。期间有一位外地来求医的患者，分娩中脑病发作，病情危重。宋冬雷帮她向与自己合作的浦南医院，争取了一个医院慈善项目的帮助。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宋冬雷说，医生集团已经聘请了职业经理人来打理日常运营事务，而他自己至少在未来五年内，主要精力都将放在临床一线。“我还要把团队里的年轻医生带起来。” 对宋冬雷来说，他选择的神经外科需要投入的精力会很大，因为这是个门槛很高、要求很高的学科，尤其是对人才的要求更好，所以也许发展却不会很快。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这些问题在宋冬雷当初决定要做医生集团的时候肯定已经考虑过了，而且从目前看，他的模式设计至少已经打动了众多投资机构，剩下的问题就是，能不能打动市惩未来。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 集中精力做最擅长的事</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 至少凭借精湛的医术，宋冬雷打动患者已经不成问题。在上海浦南医院的 A 等病房里，已经住满了 14 名来自全国各地找宋冬雷求医的患者，每个人都是单间。浦南医院是一家位于浦东新区的一家二甲综合医院，始建于 1983年，是冬雷脑科医生集团建立合作的第一家公立医院。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “浦南医院的条件不错，而且设备也非常好。” 宋冬雷说，目前已经建立合作关系的有三家医疗机构，“门诊基地是在瑞慈医疗集团上海静安诊所，还有两个临床基地，浦南医院是一个，另外一个是上海国际医学中心，由我们团队来帮他们组建神经外科。” 宋冬雷说，另外还有一家上海的私立医疗机构在谈。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 借鉴张强医生集团的经验，冬雷脑科医生集团与医院的合作也采取了类似 PHP 的模式(Physician Hospital Partnership)。同时，因为定价机制的关系，合作的主要对象同样是私立医院和公立医院的特需服务。目前的三家已经合作医疗机构全部位于上海。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 冬雷脑科医生集团的医生目前有 5 名，除了宋冬雷之外，另外三名医生包括两名主任医师、一名副主任医师和一名住院医师。“马上还会有两名新的医生加入，包括一名从海外进修回国的医生，这些医生全部都是自由执业。” 宋冬雷说，“我们招医生的标准是年富力强，有一定业务能力，常规手术能拿得下来，同时也会招一些年轻医生来培养。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在医生选拔方面，宋冬雷还特别强调了一点，“来我们这里工作的医生，一定要非常理解自由执业的意义，而不是像在传统医院那样去追求。” 宋冬雷说，“我们对医生强调的是技术加服务，并且尤其是强调对病人服务的重要性。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 也是从为患者提供更好服务的角度，冬雷医生集团目前已经配备了三名医疗助理，主要就是为患者提供院前、院中、出院后全方位的服务，帮助病人预约门诊、安排住院等。而且诊疗及后续随访期间，病人有任何疑问，都可以联系医疗助理咨询。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 关于这些服务，近年互联网医疗实际上讲了很多故事，有很多公司的产品就是致力于提升医生的工作效率，包括诊前、诊中和诊后。不过宋冬雷说，从满足神经外科的需求来讲，市场上的移动医疗产品还不够充分，所以他们的服务主要通过电话。“但我们正在跟人合作，共同开发这样一款移动端的工具，能够在服务中用得上。” 宋冬雷说。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在服务的领域上，“我们肯定是集中在手术上，这是我们最擅长的部分。” 宋冬雷说，“只有在最擅长的部分，我们才能真正树立自己的品牌。至于以后，我们可能会考虑延长服务链的事儿，比如健康管理、康复这些内容。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 除了医生、医疗助理外，另外一半团队则是运营团队。目前，整个冬雷医生集团的团队规模 14 个人左右，还处于相当初期的阶段。获得的首轮融资来自中卫基金、建信康颖基金、飞马旅和弘晖资本，而冬雷医生集团则由核心医生团队控股。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 神经外科 “慢” 的两面性</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 虽然冬雷脑科集团还没有到大规模花钱的时候，但实际上资本的加入已经可以为其发展带来一些帮助。比如在对接医疗机构上，弘晖资本就可以将自身已有的资源对接给冬雷脑科医生集团，帮助其在更多城市落地。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “这确实会在选择医疗机构方面节省我们很多精力。因为神经外科的要求非常高，各方面的设备要非常齐全，团队的能力要非常强，并不是所有医疗机构都能达到。有些找上门来的医院条件就不行。” 宋冬雷说，“有这些投资机构的帮助，我们可以更快找到合作医院。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 标准高恐怕是神经外科的最主要特征。从近些年医疗领域市朝的发展程度看，牙科、产科、眼科是市朝程度比较高的专科，已经实现了连锁发展，主要原因是这些专科的技术门槛相对较低且比较容易实现标准化。而像心血管、肿瘤、神外领域，虽然涌现了很多优秀的医院代表，比如亚心、三博，但这些医院都没有实现规模化连锁发展。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 原因有几个方面，一个是宋冬雷多次提到的神经外科对技术团队、医疗设备的要求比较高，资金投入会很大，另一个方面就是优秀人才的稀缺。快速扩张必然需要大量能够提供高质量标准化服务的人才，但在神经外科领域却很难做到这一点。总体而言，就是实现标准化的难度比较大。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宋冬雷对此非常清楚。“我们的扩张肯定没那么快。” 宋冬雷说，“所以，我们今年的计划主要就是做服务的标准化，招医生、培养医生，把上海已有的几个基地发展好，作为我们以后进行复制推广的范例。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 但门槛高的另一个侧面可能就是有一个广阔的市场等待开拓。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 从统计数字看，中国的神经外科医生数量并不算充裕。比如，截至 2009年，按照世界神经外科学会联合会的统计，中国的神经外科医生仅 3000 名；而按照中国医师协会神经外科分会的统计，这个数字是 13000 名。有人按照标准差异折算之后，认为 8000 名作为比较合理。即便如此，我国每 15 万人一名神外医生，而日本是每 1.7 万人一名神外医生。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不仅是资源总量并不宽裕，而且分布也并不平衡，包括北京天坛医院、上海华山医院、四川华西医院三大神外中心分别承担着华北、华南、西南地区的主要神外医疗工作。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> (上表是 2009年 统计数据，来源：张玉琪等《中国神经外科医师队伍的现状和发展》)</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医生资源的短缺和分布不平衡，都意味着市场需求尚未得到充分满足，尤其是在非核心城市，甚至连社会力量举办的民营医院短期内都难以快速成长。“现在的医疗资源难以满足这么大的需求，对我们有很大的空间。” 宋冬雷似乎并不担心慢，“只要我们能把自己的服务做好。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 关于未来的设计：轻重兼备</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “像我们这样完全脱离体制的医生集团，医生既要有过硬的技术，又重视对患者的服务，还懂得市场，肯定是非常需要的。” 宋冬雷非常有自信，“神经外科的供给很小，数得上来的大医院就那么几家，但需求非常大。所以，不仅民营医院需要我们，甚至公立医院也需要我们，尤其是中小城市。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宋冬雷清楚的看到了这个市场的需求在哪里，或者说，看到了冬雷脑科医生集团未来拓展的方向在哪里。但问题就是，他如何去做？虽然冬雷脑科医生集团刚刚创办运行不到半年，但宋冬雷对未来的这些事儿已经想了很多。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “外地已经有医生集团表示愿意加入我们，成为我们的子公司。” 宋冬雷说，这种品牌共享的轻资产模式是路径之一，而且他还认为，未来那种由几个医生组成的小型医生集团会是主流，因为这样的医生集团在形式上会更加灵活。“一旦扩张，我们对医生的需求量还是会比现在大，完全是自由执业的医生不可能，肯定会有来自多点执业的医生。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 轻资产模式扩张的突出特点就是速度快，但对服务标准程度的要求其实更高。不过既然是轻资产，对资金的需求量就不会太大，宋冬雷之所以提到未来的计划可能会花很多钱，主要是他还有建实体医疗机构的打算。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 关于实体医疗机构的建设实践中争议非常大，主要是因为这种模式的建设周期长、回报慢、风险高。但没有实体医疗机构，似乎也会面临一些问题，比如互联网医疗里面的变现问题。所以选择是多角度的，比如张强医生集团就选择轻模式，做自己最擅长的事儿；丁香园则决心开诊所，做一场全新的医疗实验。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 宋冬雷还没有到必须做出选择的时候，但他现在的思考是，未来建设实体医疗机构应该是需要的。“从为患者提供好的服务角度，还是医疗人才培养的角度，长远看，自建医疗机构恐怕都是必须的。” 宋冬雷说。如果真的开始建医院，那要花的钱确实就不是小数目了。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 有关建设实体医疗机构，值得一提的是，宋冬雷有一个很少有人具备优势是，他曾亲自管理过一家私立医院，拥有实体医疗机构的实践管理经验。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 与宋冬雷关于未来的大多数探讨，估计还需要一些时间才能落地。宋冬雷此时的主要精力，是在经营上海这个 “根据地”。但有些意外的是，虽然从公立医院到民营医院再到医生集团，宋冬雷个人的行为似乎越来越激进，但他关于自由执业的现状却并不乐观。“很多东西没有明确，大方向上甚至还有很多争论。” 宋冬雷说。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当然，所谓的困难其实是阻挡不了那些真正愿意创新者的脚步的。就像医生集团这个领域，包括张强、宋冬雷、龚晓明、张子谦、鲍宇克、王永春、卢旭光等医生都在积极探索尝试。近期，他们共同组建了中国医生集团联盟。“在这个联盟里，我们可以就各自的经验交流、探讨，甚至病人也可以互相转诊。” 宋冬雷说。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 聊了这么多有关未来的设计与畅想之后，我似乎有些理解宋冬雷为什么坚持对自由执业的追求，因为越多的自由其实会意味着越多的创造。只是，创造的人会比较辛苦。采访大约在 6 点多结束，宋冬雷背着双肩包回到办公室，接着去安排后面的工作了……</span>
</p>
