			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>患了甲状腺癌，真有想的那么糟吗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>飙升的甲状腺癌</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺癌是一种常见的恶性肿瘤，自2003年以来，新增病例上升了393%，并以每年17%的高增速增长，近年来，甲状腺癌更是偏向年轻化、女性化，男女患此癌比例甚至高达1:4。中国医学科学院肿瘤医院头颈干科的专家表示，甲状腺癌高速增长的最主要的原因就是随着体检普及率提高，“查出率”大幅上升。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>甲状腺癌=死刑？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实不然。虽然发病率大幅上升，但绝大部分都属于良性结节，即使不幸罹患甲状腺癌，也不用害怕，目前甲状腺癌的10年生存率高达95%以上，早发现、早治疗，便不会对患者的正常生活造成很大的影响。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>没事儿摸一摸</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺癌的临床表现一般为甲状腺肿大，并且肿块直径超过2厘米，闲着没事儿摸一摸，反正闲着也是闲着，如果发现肿块呈圆形、表面不光滑有实体感且肿块生长速度异常就应该及时去医院就诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>正常补碘觉到病除</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺癌和其他癌发病一样目前仍不清楚其具体发病原因，故尚不能很好预防其发病。但应避免或少接触放射线损伤，正常补碘，改善环境污染，少食致甲状腺肿大物质。同时应该少熬夜多睡觉，因为熬夜不仅让人眼袋发黑、面容枯槁，更危险的是它还会增加甲状腺癌的发病几率。习大大都曰过：年轻人不要老熬夜，做到12点就去睡大觉。所以要想远离甲状腺癌，最简单的方法就是不熬夜，觉到病除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>如何打败甲状腺癌</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着医学的发展以及治疗方法的更新与转变，医生会根据诊断结果是否为可疑结节、患者的体质、癌肿的病理类型和临床分期等因素选择不同的治疗方式，如外科治疗、化学治疗、内分泌治、放射治疗、cls生物细胞治疗等。通过以上方式，极大地提高了甲状腺癌患者的生存率，提高了患者的生存质量</span><br />
