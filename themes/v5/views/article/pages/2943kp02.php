<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>甲状腺结节或肿块诊治中的一些问题&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺结节或肿块诊治中的一些问题</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">头颈科 &nbsp;彭汉伟</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺结节或者肿块是甲状腺外科很常见的临床问题，然而并不是所有的外科医师都能对其进行深入的研究并为患者提供最合理有效的治疗的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">实际上，甲状腺结节或肿块的诊断并不是很容易的事，它可能是肿瘤性疾病，也有可能是非肿瘤疾病，可能是良性肿瘤也可能是恶性肿瘤，临床诊断决定治疗方案，各种疾患的治疗手段不尽相同，可能是需要手术的也可能是内科治疗就行的。那么，如何去合理地给甲状腺结节或肿块做出准确的诊断？诊断后如何合理地进行治疗？甲状腺外科手术该如何做才是合格的？术后如何进行随访观察？等等，均是甲状腺外科医师应该好好地去研究的课题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>一、</strong><strong>&nbsp;甲状腺结节或肿块的诊断</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于一个甲状腺肿块患者的诊断并不是件简单的事！绝对不是医师简单的触诊就能解决的。当然，一个有经验的临床医师的问诊和触诊往往可以作出初步诊断，但是某些常规的检查还是需要的，它能为临床医师提供更多的诊断信息。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最糟糕的情况就是把恶性肿瘤误认为良性肿瘤，从而进行不恰当的治疗，延误治疗，甚至造成不良后果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，就甲状腺肿块的定性而言，有几项检查是有帮助的：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医师触诊：质硬、表面粗糙、固定、边界不清往往是恶性肿瘤的征象，同侧颈部淋巴结触及肿大时更应考虑恶性肿瘤的可能性。甲状腺炎特别是桥本甲状腺炎有时也表现为质硬肿块，但是往往有急性上呼吸道感染史、触痛明显。多结节同时存在，质地不硬者多数为结节性甲状腺肿。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">间接喉镜检查或者电子喉镜检查：声带活动状况应作为常规检查之一，声带麻痹者几乎都是恶性肿瘤，当然也可能是既往手术伤及喉返神经所致。良性甲状腺结节极少出现声带麻痹，笔者从医十多年未曾见过！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">彩超：甲状腺彩超在甲状腺结节的定性诊断方面准确率最高可达85%左右，钙化、边界不清、实质性结节、气管食管旁淋巴结或颈侧淋巴结肿大是彩超诊断甲状腺恶性肿瘤的重要指征。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">CT：对甲状腺肿块的定性诊断甚至不如彩超，但是，它能清楚而直观地显示肿块的位置、周围关系，对肿块巨大、怀疑恶性的甲状腺肿瘤患者应作为常规检查，更重要的是CT平扫提供信息太少，意义不大，平扫+增强应作为常规。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">细针穿刺细胞学检查：在欧美多数作为常规检查，是术前定性诊断最为可靠的依据，然而，在国内，除了为数不多的几家细胞学水平较高的医院外，诊断的可靠性不高，这就是我院在编写《甲状腺结节和甲状腺癌诊疗指南》时明显不同于NCCN指南的原因</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺功能：既可以了解甲状腺功能，又有助于甲炎的鉴别。当然，单纯检查T3、T4意义不大，TSH、TG、TM的检查意义更为重大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺核素扫描：曾经被认为是诊断常规，然而，经过深入的研究之后，学者们发现，核素扫描在鉴别良恶性方面几乎没有任何价值，但是，它能为甲状腺功能状况提供一定的帮助，对具有吸碘功能的转移灶的诊断有较高价值。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">实际上，经过全面仔细的临床检查、甲状腺彩超、甲功五项，良恶性判断就已经初步形成。我们建议上述检查应作为甲状腺肿块患者的常规检查项目。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当我们把甲癌的诊断指征适当放宽的时候，甲状腺恶性肿瘤漏诊的可能性就变得很小了。我院头颈科年收治甲状腺肿瘤患者250例左右，其中甲癌约60例，漏诊的可能性只有1%左右。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>二、甲状腺结节和肿块的治疗手段</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">并不是所有的甲状腺结节或肿块都需要手术治疗的，甲炎和最大直径小于1.5cm的结节性甲状腺肿是没有必要手术的，然而，事实是很多普外科医师对几乎所有的甲状腺结节患者施行手术治疗，这是非常不负责任的做法。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，甲炎诊断不明确，试验性内分泌治疗无效或者形成明显结节并有钙化者也应该行诊断手术，并且术中冰冻，根据结果做相应的术式。结节性甲状腺肿肿块大于1.5cm，或者不排除有恶变，或者肿块巨大有压迫症状者均应手术治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">任何有恶性可能的甲状腺肿瘤均应行手术治疗，这是毫无疑问的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们不主张滥用手术治疗甲状腺结节，毕竟手术是有创的，其实，很多临床医生都明白手术是不能治疗结节性甲状腺肿疾病本身的，它只能切除肿大的结节，解除压迫或者预防可能出现的压迫，当然，还有一个目的就是防止恶变。因此，我们认为良性甲状腺结节的非手术治疗是值得探索的，比如说无水酒精囊内注射，生理需要量左旋甲状腺素替代治疗等等，前提是诊断把握性较高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>三、甲状腺外科手术怎么做</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正是由于甲状腺肿块术前定性诊断存在困难，加上甲状腺外科手术对精细解剖的要求非常高，使得甲状腺外科手术并不是想象那么简单。一味追求手术快是不负责任的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于甲状腺周围有喉返神经、甲状旁腺等重要器官，而这些器官本身较小，解剖显露有难度，只有具备甲状腺包膜精细解剖的技术才能完美地施行甲状腺手术。2007年全国头颈肿瘤大会正式地把甲状腺包膜的精细解剖提上议事日程。我们主张利用小型手术放大镜辅助下行甲状腺手术进行甲状腺包膜的精细解剖，每侧甲状腺至少显露并保护一个旁腺，喉返神经的解剖显露应该作为常规。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">切多少也是一个值得探讨的问题，结节性甲状腺肿施行甲状腺全切术显然是过度的（累及整个甲状腺例外），和大剂量I131治疗甲亢，待甲状腺功能低下后在长期行甲状腺素替代治疗如出一辙，但是在欧美却乐此不疲，令人百思不得其解。一侧甲状腺癌行常规甲状腺全切除术的观念近来已经有所转变。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">按照目前循证医学证据提示：①有手术指征的结节性甲状腺肿应尽量保留正常的甲状腺组织，当保留的组织量不足时术后应给予替代治疗；②一侧甲状腺分化型癌颈部无淋巴结肿大者行一侧叶+峡部切除的同时常规清除VI区淋巴结；③一侧甲状腺癌有颈部淋巴结肿大者行改良型颈清扫术，甲状腺处理同前；④改良型颈清扫应尽量保留颈丛神经；⑤特殊类型的甲状腺癌按照其病理性质处理。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当术前怀疑恶性时，术中行冰冻检查，根据病理结果做决定。当术中探查高度怀疑恶性而冰冻病理提示为良性肿瘤时，应在保留甲状旁腺和喉返神经的前提下按照恶性处理。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>四、术后怎么办</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺肿块手术切除后并不是就万事大吉了，在多数情况下，还需要做进一步治疗：①结节性甲状腺肿残留甲状腺组织过少者应行替代治疗，不断的检测TSH水平，调整用药，防止残留甲状腺组织再次形成结节；②有高危因素的甲癌患者术后应行TSH抑制治疗，在可以耐受的前提下尽量将TSH水平抑制在1.0uIU/ml以下，用药时间至少在五年以上；③分化型甲癌手术切除不干净，或者有肺转移、骨转移等，术后尚需行I131内放射治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于不同甲状腺疾病术后处理不尽相同，因此，临床医生必需很好的把握各种不同情况的处理原则，给每个患者建立随访档案，嘱咐患者术后密切随访，作为患者也应积极配合医生的治疗，按照常规接受检查。</span>
</p>
