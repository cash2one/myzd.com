		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼾症的诊治知识（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一、什么是阻塞性睡眠呼吸暂停低通气综合征？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">阻塞性睡眠呼吸暂停低通气综合征（OSAHS）是一种病因不明的睡眠呼吸疾病，临床表现有夜间睡眠打鼾伴呼吸暂停和白天嗜睡。由于呼吸暂停引起反复发作的夜间低氧和高碳酸血症，可导致高血压，冠心病，糖尿病和脑血管疾病等并发症及交通事故，甚至出现夜间猝死。因此阻塞性睡眠呼吸暂停低通气综合征是一种有潜在致死性的睡眠呼吸疾病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">二、引起发病的主要原因？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">OSAHS的直接发病机制是上气道的狭窄和阻塞，但其发病并非简单的气道阻塞，实际是上气道塌陷，并伴有呼吸中枢神经调节因素障碍。引起上气道狭窄和阻塞的原因很多，包括鼻中隔弯曲、扁桃体肥大、软腭过长、下颌弓狭窄、下颌后缩畸形、颞下颌关节强直，少数情况下出现的两侧关节强直继发的小颌畸形，巨舌症，舌骨后移等。此外，肥胖、上气道组织黏液性水肿，以及口咽或下咽部肿瘤等也均可引起OSAHS。关于OSAHS的病因和发病机制，需进一步研究。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三、温馨小告示</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">警惕阻塞性睡眠呼吸暂停低通气综合征症状？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.打鼾：睡眠中打鼾是由于空气通过口咽部时使软腭振动引起。打鼾意味着气道有部分狭窄和阻塞，打鼾是OSAHS的特征性表现。这种打鼾和单纯打鼾不同，音量大，十分响亮；鼾声不规则，时而间断。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.白天嗜睡：表现为白天乏力或嗜睡。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.睡眠中发生呼吸暂停：较重的常常夜间出现憋气，甚至突然坐起，大汗淋漓，有濒死感。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.夜尿增多：夜间由于呼吸暂停导致夜尿增多，个别患者出现遗尿。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5.头痛：由于缺氧，患者出现晨起头痛。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6.性格变化和其他系统并发症:包括脾气暴躁，智力和记忆力减退以及性功能障碍等，严重者可引起高血压，冠心病，糖尿病和脑血管疾病.</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">四、饮食指导</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">宜清淡为主，多吃蔬果，合理搭配膳食，注意营养充足。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">五、有效治疗方法？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">OSAHS的治疗除侧卧，戒烟酒，肥胖者减重，分为非手术治疗和手术治疗两类。</span>
