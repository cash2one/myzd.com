			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海仁济医院成立全国首个“国际盆底手术培训中心”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2015年11月29日，上海仁济医院盆底中心与北美医学教育基金会（NAMEF）共同成立“北美教育基金会尿失禁及盆底手术培训中心”。该中心是全国唯一的国际尿失禁及盆底手术培训中心，今后将为全国盆底疾病诊疗培训更多专业的医生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院盆底中心主任吕坚伟教授介绍，培训中心将对学员采取一对一、手把手的现场手术操作指导，以提高基层医院医生对于盆底疾病的诊疗水平，规范盆底的标准手术流程。中心还将定期邀请北美盆底专家、全国著名盆底尿控专家授课，并提供各种手术的光盘演示，尿失禁手术、盆底重建手术、盆底整形手术关键技巧讲解，尿失禁及盆底手术并发症处理技巧，尿动力学的实际应用，盆底功能评估的意义，以及盆底康复训练等。每位学员经毕业考核合格后，可以获得由北美盆底手术培训中心和仁济盆底中心共同颁发的培训证书。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海仁济医院盆底疾病诊治中心成立于2010年初，是我国首个由泌尿科与妇产科两个学科交叉融合成立的盆底疾病专病诊治中心，开设泌尿科和妇科联合门诊和联合病房，构建了真正意义上的MDT多科室协作诊疗模式。盆底中心为盆底疾病（尿失禁、盆腔脱垂、盆腔疼痛以及膀胱阴道瘘等）患者提供“一站式”的诊疗服务，避免患者在不同科室间多次就诊，节省了时间，更避免了多次手术带来的痛苦和经济负担。比如，一位既有子宫脱垂又有尿失禁的女性患者，如果在仁济南院就医，只需看一次泌尿妇科联合门诊，由泌尿科和妇科专家联合做一次手术，就能解决所有问题，且能避免二次手术的痛苦，大大节省了治疗时间和费用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，该盆底中心先后派出多名医师参加国内外盆底康复及微创手术的培训，并主办了多届国家级继续教育——尿失禁和盆底功能障碍性疾病的学习班。该中心掌握多种盆底整复微创手术技术和康复治疗技术，拥有多台生物反馈治疗仪具有国外留学交流的经历、盆底肌肉评估仪、电刺激治疗仪、电针灸治疗仪、尿流率测定仪、尿动力测试仪等设备，拥有膀胱药物灌注、交感神经神经阻滞、膀胱电灼法等特色治疗方法，真正实现了多方法、多层次的特色治疗手段。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">哪些病可以去盆底中心诊治？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. 尿频、尿急、尿失禁、产后盆底肌松弛以及盆腔脏器脱垂患者</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">采用生物反馈电刺激理疗、电针灸理疗以及膀胱药物灌注等特色康复治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 中重度尿失禁、盆腔脏器脱垂以及膀胱阴道瘘患者</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">采用腹腔镜技术治疗盆底脏器脱垂和膀胱阴道瘘。仁济医院盆底中心是国内最早（2000年）开展经阴道无张力尿道中段悬吊（TVT）术治疗压力性尿失禁的单位之一，至今已开展超过1800例的尿失禁微创手术，手术效果达到98%以上。2015年1月，该中心开展了Needleless单切口无针吊带技术，使尿失禁手术更微创，术后恢复更快，使其成为真正的日间手术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. 间质性膀胱炎（IC）患者</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院盆底中心于2012年首创经皮穿刺交感神经调节法治疗顽固性IC的疼痛，取得良好疗效。2014年首创经尿道激光电灼术治疗难治性IC，疗效显著。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. 涉及多个科室的盆底疾病患者</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脏器脱垂（子宫脱垂、膀胱脱垂、阴道残端脱垂）、宫颈癌IIb期以上拟行广泛子宫切除+盆腔淋巴结清扫术、膀胱阴道瘘、晚期妇科肿瘤继发梗阻性肾衰等患者，以往需要在泌尿科、妇产科多次就诊，联合门诊及联合病房可以“一站式”搞定，由妇产科和泌尿科医生共同参与，以微创手术为主要治疗手段，共同完成盆底手术。</span><br />
