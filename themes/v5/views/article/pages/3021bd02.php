<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>军医李幼生：生命“接力”是场永无止境的马拉松</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3021/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">满面春风，目光炯炯，耳朵白里透红，轮廓分明。今年50岁的他，履历可以总结为一句话——入伍34年，参加工作30年。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">故事倒回1年前，他作为黎院士首位“两栖医生”的培育者，敢于挑战自我，义不容辞救治大洋彼岸的飞机失事患者；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">故事倒回5年前，他沉着冷静，秉承不放弃、不抛弃的“马拉松”精神，全力接治沈阳危重患者张建宇，使其重获新生；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">故事倒回33年前，一盆“凉水”，让他全然冲锋在医学的“前沿阵地”；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">故事倒回1个月前，他在合肥耗时4小时56分42秒跑完全程马拉松……</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">春夏秋冬，四季轮回。30载岁月悠悠，30载秉持锐意进取、勇往直前，他用实际行动刻画出新一代革命军人在医疗战线上救死扶伤、全心全意为人民服务的时代肖像。走进南京军区总医院普外科李幼生教授，一位医生执著的人生追求直击心灵——隐忍，持久，且充满蓬勃。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大洋彼岸虽远千里，生命接力却近咫尺</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在李幼生的记忆中，那是一场长达199个日夜的生命接力赛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2014年10月，移美华人王蕾在美国天宁岛飞往塞班岛的途中，因飞机失事致她严重腹内外伤，经当地医院全力救治，仍生命垂危。无奈只有通过“ＳＯＳ”国际救援中心求助，用专机将她送到几千公里外的南京军区总医院接受治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2014年10月23日凌晨2点飞机降落南京。当医院了解到王蕾的病情后，李幼生作为黎介寿院士首位“两栖医生”的培育者，义不容辞地担起此项极具挑战性的医治任务。经他细心检查发现王蕾在美国的多次手术导致她的十二指肠和多处消化道瘘口发生严重感染，肠功能基本散失。李幼生看在眼里，急在心里，迅即拿出一套特殊的治疗方案——遵循损伤控制性外科的原则，利用微创技术控制严重的腹腔感染。经过一夜的“鏖战”，暂时把王蕾从生死线上拉了回来，可是他却累趴下了…… 10月23日下午15时，李幼生苏醒后迅速拔掉自己手上的输液针管，召集医疗团队对王蕾的肠功能恢复进行专业会诊。由于病人情况特殊，经过大家一番激烈的探讨，李幼生最终决定把他所掌握的小肠移植经验应用到王蕾身上，从而避免了她的小肠、结肠和十二指肠被切除。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在王蕾的治疗期间，李幼生视病人如亲人，每天下班回家前去看望她，并以开玩笑的方式，缓解病人的心理压力。1天、2天、3天……经过199个日夜的精心医治，王蕾重获新生。泪，落地无声，爱，润物有声。“当时我在美国的治疗让我很怀疑……如今，健全出院，这真的是个奇迹。谢谢李教授！”2014月5月26日王蕾出院时拥抱李幼生含泪感慨道。王蕾带着灿烂的笑容，带着新生飞回大洋彼岸，李幼生继续一袭白衣，穿梭病区，低头弯腰细语呢喃，目光里全是满满慈爱。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“老李：你是我的‘再生父母’”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">李幼生有着一双“妙手”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为了保持双手的灵活度，他从医30年，每天吃早饭坚持用筷子剥鸡蛋。这些年他先后发表论著74篇，其中被SCI收录15篇，曾获军队科技进步一等奖1项、二等奖2项、三等奖2项；国家教育部科技进步一等奖1项，自然科学二等奖1项，中华医学科技进步一等奖1项等，是江苏省优秀医学重点人才。他还同时担任南京大学和第二军医大学教授、博士生导师，《中华医学杂志》《中华实验外科杂志》的编委或特约编委。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2011年10月30日，沈阳患者张建宇被某医院告知生命最多只能维持24小时时，他彻底绝望了，家人甚至为其准备好了后事。上帝关闭了一扇门，同时也打开一扇窗。后来张建宇的朋友几经周折联系到李幼生，他得知情况后，不顾病房满员，不顾患者可能在转院途中遭遇死亡的危险，全力接治张建宇。据李幼生的学生小刘回忆，当时有人劝李幼生放弃接收病情危重的张建宇，可他仍沉着冷静，秉承不放弃、不抛弃的职业操守，当即召集医疗团队的医师，动员一个病情相对较轻的患者转入分院治疗。后又设法联系国际红十字会，采用医疗救援直升机，当晚将张建宇转运至南京。 </span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">
<div>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</div>
</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2011年11月１日凌晨2点钟，张建宇转到南京军区总医院后，李幼生立即为他进行了两次手术——清除腹腔感染及控制腹腔出血。手术之后紧接着又为他进行了胆红素吸附及血液过滤，替代部分肝脏和肾脏功能。据李幼生事后介绍，他们经过48小时的“鏖战”，终于将张建宇从死亡线上暂时拉了回来。尽管如此，李幼生丝毫不敢松懈，时时叮嘱管床医生：每天至少查看张建宇三十次，并要及时汇报，即使在他出差期间也要远程指导每一个治疗环节。随着日子一天天过去，张建宇的病情渐渐好转，他们一家人又重新看到了希望。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张建宇似乎仍要给李幼生更大的考验。2011年12月20到2012年1月18日，在不到一个月的时间内，张建宇出现了腹腔脓肿、腹腔高压、结肠瘘、胆漏等一系列并发症。此时李幼生一方面做好病人的心理辅导工作，一方面为张建宇进行腹腔冲洗、腹腔敞开、肠造口、胆囊穿刺引流等大大小小7次手术。据张建宇妻子回忆，病魔不仅摧垮了她丈夫的身体，同时也摧垮了他的意志。张建宇第一次清醒时，颤抖着右手写道：“给我点药，让我走吧！”。然后他极其不配合治疗，不愿意输液，经常把身上的治疗管子拔掉。李幼生为此耐心地对张建宇说：“困难是短暂的，我们还年轻，要有足够的信心战胜病魔，你能行。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">针对病人轻生的念头，李幼生每天查房坚持跟张建宇解释病情及当天的治疗意义；一向严肃的他还特意和张建宇开玩笑打气，缓解张建宇的焦虑。李幼生从张建宇妻子口中得知他丈夫爱好美食，李幼生每天查房时用“欺骗”法鼓励张建宇说，如果他今天能锻炼上肢100次并且配合治疗就允许他喝一口汤……经过这样特殊的沟通方式，张建宇终于重拾了活下去的勇气，病情逐渐好转，此时李幼生悬着的心也逐渐放了下来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2012年大年初五，李幼生完成春节总值班和家人刚回到山东老家过年时，科室这边打来电话说张建宇突发呼吸衰竭，肾功能衰竭，病情危重。病情就是命令！他立即驱车赶回南京组织抢救，因为没有人比他更了解张建宇的整个治疗过程。为此，李幼生的妻子和孩子表示不理解，难免有时抱怨他为了工作连家都不顾。然而，他却对记者说，选择了医生这个职业就选择了患者的生命，不能轻易放弃。 2012年3月12日，很多人都会记住这一天，久经病痛的张建宇可以试着说话了，说的第一句话竟是：老李，你是我的“再生父母”。李幼生却连说了三个“不敢当”。2012年6月张建宇带着新生回沈阳了，而李幼生依旧一袭白衣，脖挂听诊器，穿梭在病区，为更多的患者进行生命接力……</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今年10月30日，李幼生又收到了张建宇发来的微信——五年前的今天，是我人生最黑暗的开始，是您不放弃、不抛弃，使我重获新生！五年后的今天，再次祝您工作顺利！我一切安好，家父也很好，感谢！记者采访时问李幼生作何感想，他微笑着说：“我的价值在于带给患者健康或新生，为此我很开心，我会继努力奔跑下去。”继而追问李幼生，为什么张建宇在微信还说了一句：“家父的身体状况也很好”？原来十天前他刚刚为患直肠癌的张父进行了手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">只有坚持不懈，才能使生命‘接力’更稳妥 </span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当记者走进李幼生的办公室，只见锦旗挂满半间屋子，他笑着说这应该感谢33年前的那盆“凉水”。在李幼生的记忆中，那是一个终生难忘的秋天。 1981年8月，他考入上海第二军医大。入校不到1年时，由于身材比其他学员较瘦小，有个习过武术的同学陡然把凉水泼向他，为此两人在田径场上针锋相对一番，事实表明他输了——5000米不及格。一传十，十传百，“泼水”事件给他心灵蒙上一层阴霾。李幼生回忆说，在他人生最暗淡的时光想起了最初的梦想。在李幼生看来，求学是一条崎岖坎坷的路，既然选择了，便只顾风雨兼程。为此在接下来的几年时间里他一边加强文化课学习，一边加强体育锻炼…… </span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">
<div>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</div>
</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">时光回溯到1984年4月，眼看临近毕业。他天天从“海绵”堆里挤时间，别人午休他看书；别人打球他看老师搞研究；他还总结出一套“三不”学习法——有问题不过夜，有问题不上台（人体标本解剖台），有问题不睡觉。经过3个月的淬火加钢，李幼生的头脑变“硬实”了，但身体却变“柔弱”了，身边的同学甚至说他简直就是“书呆子”，然而他却一笑置之。功夫不负有心人，李幼生在众多毕业生中脱颖而出。当老师和同学们问他是什么信念支撑他一直拼搏下去，李幼生回答，医学事业好比马拉松运动，只有挑战自我、超越极限、永不放弃，将来才能使手中的“接力棒”更稳妥。再次把时光转至1984年7月，李幼生离开第二军医大学，来到南京军区总医院普外科工作。这一干就是30年。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">工作初期，由于他的英语水平比其他课程相对落后，当时掌握的英语词汇不过600，其中还包括大部分医学英语词汇。于是他在工作空隙间一边听翻录磁带，一边练习语法。据李幼生回忆，那时在医院的工作不是很忙。他通常早上6点起床，每天坚持围着医院操场跑完7公里。饭后带上录音机来到门诊，有病人就给病人看病，没病人时就边看书边听英语磁带。天天如此，在自学了3年的英语后，他的英语词汇量竟突破了3000个。随着工作的深入，他的心理也随之变化——依靠自己目前仅有的知识、技术是难以应对当今新生的医学难题，于是他决定报考研究生。为了实现自己的人生目标，敢于挑战极限，超越自我，他一边工作一边复习政治、专业基础课、专业课（器官移植学）、西医综合，经过４年的努力，1990年9月获得研究生学位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“只有与时俱进，头脑不断更新才能有更大作为。”李幼生告诉记者，他从1990年开始着重对小肠移植进行深入研究，他逐渐掌握了器官移植（小肠和胰腺）、肠衰竭和营养支持等方面的“话语权”。自2001年以来，先后承担国家重点基础研究发展规划、国家自然科学资金资助项目课题等省部级课题共11项。回想军校时光，李幼生说应感谢那段尘封的“泼水”插曲，因为让他奋不顾身爱上运动。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">只有永不止步，才能使人生更精彩</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“人生和事业是场永无止境的马拉松，无论前方有多崎岖，只有秉承坚持不懈、挑战自我的体育精神，才能使生命更精彩。”1个月前李幼生跑完合肥市的马拉松告诉记者，他能有今天的成就，完全归结于他的“马拉”精神，也归根于黎介寿院士的精心栽培。为此，他常常对身边的人说，最有意义的事，就是帮患者摆脱病痛，就是从患者手中接过生命的“接力棒”！ 熟悉李幼生的人都说他是——长跑教授。要说这个“绰号”的来源，又得追溯到2005年5月的一天。当时多名退休老兵联系李幼生，邀请他参加一个马拉松活动。他为了参加此项赛事，除了值夜班外，每天晚上十一点前就寝，清晨五点起床，进行相关训练，在训练中李幼生注重配速和身体的调控，为此还买了许多训练设备……活动结束后主办方为他颁发“最佳精神奖”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此后，李幼生对马拉松更加热爱，每年和老兵们到各大城市报名，多次利用轮休时间，乘飞机参加全国各地的活动。他最近参加的一次活动是在合肥，耗时4小时56分42秒，要知道10年前，跑完全程他花了5小时19分09秒。如今，他总是自豪地对身边的朋友说：“跑步跟年纪大小没有关系，我就是例子，没想到50岁了越跑越快。” &nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">晨曦微露之时，一身麻灰色的运动装，朴素、干练、活力，人们又看见李幼生奔跑在医院的田径场上，继续追求他的多彩人生。</span>
</p>
