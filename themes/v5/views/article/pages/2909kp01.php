<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">许多胆囊结石患者，发现结石后不治疗，而是选择忍，这样真的好吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆囊结石最典型的表现就是疼痛，还可能伴有体温升高，严重时甚至引起血压下降导致休克，这种情况是非常危险的。如果出现剧痛，一定要及时就医，因为可能是发生了胆道梗阻，此时，仅仅止痛是不够的，应该尽快解除梗阻、消除炎症反应。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆囊结石造成胆管梗阻后，患者的巩膜和皮肤会发黄（黄疸），有些患者还可能有皮肤瘙痒，虽然看似没有什么大的影响，但是这表明患者的胆道被堵住，胆汁淤积对肝功能的影响很大。如果胆囊结石造成梗阻，还会让患者恶心呕吐，不能正常进食；即使没有梗阻，结石时间长了，产生慢性炎症，患者的右上腹会有隐痛，最终也会影响到进食（尤其是饭后胆囊收缩时会引起疼痛）。</span>
