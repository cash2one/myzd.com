			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>不要歧视身边的肝炎朋友</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们身边的朋友有很多，我们也会把自己的朋友按照自己的想法去把朋友分类，如有钱的朋友、吃喝的朋友、干事的朋友、异性的朋友、发小的朋友，但是朋友最应该的是交心的朋友！但是有时候我们也会把朋友按是否有那种疾病来划分，如果朋友生病了我们会去看望，会去关心，甚至去照料，如果你知道你的朋友患有的是肝炎、艾滋病这样的传染病，您还会这么做吗？今天我们就聊一聊身边的肝炎朋友吧！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实肝炎是一种常见的传染病，很多人听到传染病就已经很是害怕了，因为从我们知道的731，十年前的非典，到现在的埃博拉、禽流感、还有刚刚在我国发现的寨卡病毒，都会让我们心惊肉跳，如果一个现实的传染病就在我们身边，可能头脑中马上想的是可怕、可怕，唯恐避之不及！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还是先了解以下什么是肝炎吧，也许了解之后您就不会再有那么大的恐慌！肝炎其实有好多种，如病毒、细菌、药物、酒精、毒物、自身免疫等很多因素都可引发肝炎发生。传染性肝炎通常就是病毒引起的，如常见的甲肝、乙肝、丙肝、丁肝、戊肝还有少见的巨细胞病毒及单纯疱疹病毒型肝炎等。其实我们害怕肝炎主要就是怕传染我们，但是这些肝炎真的就一定会传染我们吗？让我们看看它们都是通过什么途径传染的吧！甲肝和戊肝是通过消化道传染，也就是说吃东西就可以传染，所以我国有吃毛蚶导致甲肝大流行的病例，戊肝目前尚没有大面积流行的病例，但是生活中我们也会有不洁饮食的个例发生，这两种肝炎可以说如果我们避免生食食物和不洁进食习惯是很容易避免的，因为肝炎病毒在体外很是脆弱，56℃水中30分钟就可以让它失去传染性，100℃水中1分钟就可以让肝炎病毒失去传染性，15-20分钟几乎所有肝炎病毒灭亡。而且甲肝和戊肝是可治愈性疾病，大部分人群自己得了这两在肝炎并不知道，因为他们通过自身的免疫力可以自愈，部分表现出症状，病程在半个月左右的时间，系统治疗均可以治愈。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">再看看乙肝、丙肝和丁肝吧，丁肝不会单独感染我们的，它需要和乙肝这样的病毒伴随才能被人体感染。乙肝和丙肝传播途径基本一致，主要通过：血液、母婴、性，这三种途径传播，现在的医疗条件好了，因输血、拔牙、纹身、纹眉等这样的传播机会很少，即便是有也是发生在不正规，没有卫生监管的私人诊所或美容机构中，所以我们要避免在没有资质的这些机构进行可能会有侵入性损伤的操作就可以切断这条传播途径。母婴传播也不用担心，现在母婴阻断技术十分成熟，即便目前患有肝炎，孩子得肝炎得几率也不到1%。这里强调的是丙肝目前可以完全治愈，索菲布韦及其二代产品已在全世界推发使用，相信不久的将来，丙肝将是这个世界上消失的疾病。性传播这个不用多说了，洁身自好才能好自为之的道理谁都懂得！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">说了这么多好像只有乙肝很吓人，不是的。在我国有10%的人群是乙肝携带者，但是真正的乙肝患者有3000多万，是不是每位乙肝携带者或乙肝患者都会传染呢，不是的，乙肝患者通常会检测一项叫HBVDNA定量的检测，如果这个检测结果是低于检测标准的，那么无论这个人是大三阳还是小三阳都不会通过血液、母婴或者性行为传染给我们。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实我们被传染上肝炎的几率比我们患上某种恶性肿瘤的几率还低，所以完全没有必要谈虎色变。在一项调查中一半以上的人都会歧视肝炎患者，因此影响他们上学、就业，目前国家2009年至今已有多部委出台相关法律禁止这些歧视行为了，并且视为违法行为。但是2015年的另一项调查仍让我们堪忧，仅有不到1%的人对乙肝没有任何歧视。所以普及肝病常识，让百姓了解肝炎传播途径是改变歧视观念的始动因素。每年的3月18日是全国的爱肝日，让我们行动起来呵护肝脏，远离歧视，这不仅仅是医务工作者义务，更是全社会的责任。</span><br />
