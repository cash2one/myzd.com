	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>胆囊癌诊治常识</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">胆囊癌主要病因可能是慢性胆囊炎症，同时诱发胆囊癌的其他高危因素还包括:肥胖、高碳水化合物饮食、吸烟和酗酒等。胆囊结石通常是慢性胆囊炎症的主要诱因，因此有相当一部分患者初次就诊是因为胆囊炎急性发作出现的发热、畏寒等胆道系统炎症表现。胆囊癌的临床表现很难与胆囊炎胆绞痛区分，一些相对特异性的表现如持续性疼痛、体重下降和黄疸常常提示肿瘤已经无法切除。通常在有胆绞痛病史的老年患者突然转变为持续性无法缓解的钝痛时应警惕胆囊癌可能性，尤其是同时伴有体重下降或右上腹部肿物时更应注意。急性胆道炎症期患者可能会出现恶心、呕吐等消化道症状，严重时可导致患者出现肝源性或肝后性黄疸，晚期胆囊癌侵犯胆道或肝门部时也会导致梗阻性黄疸的出现，或者肝门部转移性肿大淋巴结压迫胆道导致黄疸。晚期胆囊癌会引发患者出现体重下降等营养不良表现。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">体格检查可发现胆囊癌合并胆囊炎时会出现右侧缘下方局部的压痛，可触及肿大的胆囊、腹肌紧张、Murphy征阳性、皮肤巩膜黄染等。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">实时超声检查是一种无创、操作简便、患者耐受性良好、及时能得到结果的检查方式。可以评估疾病的范围及程度（胆囊癌的临床分期），以选择合理的治疗方案；如果怀疑胆囊癌就应该选择腹腔强化CT扫描，它能够提供比超声检查更加精细的图像资料，可以仔细观察有无肝转移和腹腔、肝周、腹主动脉和腹腔静脉间淋巴结肿大。核磁共振和核磁共振胆道造影检查是较为理想的检查方法，除了显示有无胆道受侵外还能很好显示肝脏的小转移灶；核磁共振胆道造影还能很好显示门静脉和肝总动脉受侵，从而协助排除不可手术的病例。如果怀疑胆囊癌病期较晚可以选后两者检查方式。部分胆囊癌患者会出现血清癌胚抗原CEA、CA199升高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">胆囊癌患者常合并出现贫血、碱性磷酸酶升高和胆红素升高的现象，因此其他检查项目包括血常规、肝肾功能全项、凝血功能等；胸片检查了解患者双肺情况外还可以排除肺部转移协助肿瘤分期；心电图、动态心电图和肺功能检测可以了解老年患者的重要脏器功能能否耐受手术。头颅核磁共振检查可以排除是否有颅内转移。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">胆囊癌的分期是用来判断预后的最佳标准，优于组织学分型、分级以及其他生物学指标。胆囊癌有多种分期系统，在AJCC/UICC TNM系统中</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1期（T1，N0）指肿瘤局限于胆囊壁的粘膜层或肌层；来源于Rokitansky-Aschoff窦的肿瘤虽然位于浆膜下但是还是归入1期。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2期（T2，N0）是肿瘤侵犯肌层周围组织但是没有穿透浆膜层或侵犯肝脏，淋巴结没有转移。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3期（T3,N0,Tx,N1）指（a）肿瘤穿透浆膜层，肝脏侵犯小于2cm；（b）肿瘤转移至十二指肠韧带上的一级淋巴结。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4A期（T4，N0.1）指肿瘤侵犯肝脏大于2cm；4B期（Tx，N2）指二级淋巴结受侵或有远处转移，肝十二指肠韧带以上的淋巴结转移归入远处转移。单纯的肝脏受侵预后要优于远处转移和血性转移，应归入3期。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">胆囊癌的治疗方案取决于胆囊癌的临床分期和胆囊的淋巴引流特点，胆囊癌淋巴引流特点如下：胆囊的淋巴引流通常是沿胆管下行，先到达胆囊管淋巴结和胆总管周围淋巴结，然后进入胰腺后方淋巴结、门静脉淋巴结和肝总动脉淋巴结，最后到达腹主动脉和下腔静脉间区域淋巴结、腹主动脉淋巴结和肠系膜上动脉淋巴结。依据胆囊癌临床分期，1期（T1，N0）患者用单纯胆囊切除术即可达到治愈目的；T1期的肿瘤几乎不存在淋巴结转移，但是在术中一定要保证胆囊管的切缘必须阴性，如为阳性必须进一步切除。2期（T2，N0）应选用扩大胆囊切除术（也叫根治性胆囊切除术），手术包括胆囊切除加胆囊床的肝脏组织楔形切除和肝十二指肠淋巴结清扫术；3期（T3,N0,Tx,N1）推荐的治疗方案也是扩大胆囊切除术，由于汇管区淋巴结阳性率较高，通常同时切除胆总管以彻底清扫该区淋巴结。4A期（T4，N0.1）也可以选择扩大胆囊切除术实现患者长期生存；4B期（Tx，N2）患者不适宜手术治疗，只能选择减状治疗。</span>
</p>
