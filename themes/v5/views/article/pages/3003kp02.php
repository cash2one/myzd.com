		<div style="text-align:center;">
	<span style="line-height:2;font-family:'Microsoft YaHei';font-size:18px;"><strong>莫到老来丧失中心视力</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">6月6日，爱眼日。今年的主题是“防盲治盲，共同参与。”在所有的致盲性眼病中，老年性黄斑变性由于治疗棘手、发病率高而逐渐受到关注。上海市复旦大学附属眼耳鼻喉科医院眼视网膜、玻璃体及眼底病学科副主任医师江睿指出，定期的眼底检查是预防此病的关键。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 记者：据统计，老年性黄斑变性已经成为发达国家50岁以上人群的第一致盲原因，我国情况如何？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 江睿：虽说在我国老年性黄斑变性还不是最重要的致盲原因，但随着人口老龄化的进程，它对视力的威胁正日益凸现。按照目前的医学水平，白内障、青光眼这些“致盲大户”是完全可以攻克的，但对于老年性黄斑变性还没有好的治疗手段。该病患者大多在50岁以上，其患病率随年龄增长而增高，常常是双眼同时或先后发病，70岁以上的老年人患病率高达34.11％。通常10年内就可能造成中心视力完全丧失，从而严重影响生活质量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 记者：何种原因会导致老年性黄斑变性？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 江睿：该病的病因尚不明确，可能与遗传、先天性缺损、慢性光损害、营养不良、中毒、药物作用、免疫异常以及心血管系统及呼吸系统等全身性疾病有关。现大多数人认为该病可能是多种原因综合作用的结果。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 记者：老年性黄斑变性有哪些症状？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 江睿：老年黄斑变性，为黄斑区结构的衰老性改变。患者常主诉中心视力减退、视物模糊或视物变形。萎缩型者视力下降缓慢，甚至达数月。渗出型者视力急剧下降，伴有出血、眼前黑影或视力障碍。视物变形和近距离阅读困难，是老年黄斑变性眼病最早期的症状，出现上述自觉症状患者，要及时上医院检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 记者：发现了老年性黄斑变性怎么应对？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 江睿：目前尚无特效疗法。手术治疗已经不再主张，对于在视网膜中心凹以外500微米处的病变可以激光治疗，用激光所产生的热能，摧毁异常新生血管。而中心凹处的治疗就比较棘手，现在有一种光动力学治疗方法，虽不能彻底阻止病情发展，但根据2年后的随访，视力出现严重下降的较少。然而这种方法也只适用于早期出血、渗出和疤痕反应不严重的病人，且价格非常昂贵。另外美国目前已经研制出抗新生血管药，经过前期实验，效果喜人，相信很快会用于临床。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 除此，一般还会有些辅助治疗，如服用微量元素锌剂、维生素C、维生素E等抗氧化剂帮助延缓病情。中药有抗衰老及改善微循环的作用，对萎缩型的黄斑变性有较好疗效，对阻止渗出型瘢痕修复后复发、防止另眼病情发展方面也有一定作用。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 记者：该病有预防之法吗？</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 江睿：戒烟很必要。一般来说，吸烟者或曾经吸过烟的人得老年性黄斑变性的可能性要大大高于不吸烟的人。另外，要避免强光刺激，外出时配戴遮阳帽或变色镜可减少黄斑区损伤。大多数病人发现得病时往往已经视力严重丧失，因此老年人应定期检查视力及眼底，以争取尽早治疗，防止失明。</span>
</p>
