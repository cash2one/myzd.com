<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>男性健康&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">曾经有一句广告词说得好，“有家的男人真幸福，谁知养家的男人真辛苦”。据调查由于激烈的社会竞争，男人工作紧张，思想负担重，致使精神疾患、心血管疾病以及阳痿、遗精、性功能低下明显增多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">传统观念认为：男人应在社会和家庭中保持一个强者的形象。因此，一些男性朋友在生活中遇到挫折不愿找人诉说，身体遇到不适也不愿到医院就诊，采取“忍”和“扛”的方式，久而久之，影响了男性的健康。另外再加上一些男性朋友饮食不节制，饥饱无常，任凭喜好，饮食偏嗜，特别是对嗜烟酒等不良生活习惯使得男性身体健康更为堪忧。在世界大部分地区，男性寿命明显短于女性，大量与男性相关的健康问题，如体质和脂肪分布的改变，肌肉无力，尿失禁，认知能力丧失，健康状况下降等。如果医师和社会对这些问题认识更广泛的话，都可以在早期发现和治疗，就可以有效地降低发病率，提高生活质量并减少医疗费用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1． &nbsp; &nbsp;男性健康的标准是什么?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) &nbsp; &nbsp; &nbsp;有充沛的精力，能从容不迫地担负日常生活和繁重工作，而且不感到过分紧张与疲劳。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2) &nbsp; &nbsp; &nbsp;处事乐观，态度积极，乐于承担责任，事无大小不挑剔。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3) &nbsp; &nbsp; &nbsp;善于休息，睡眠好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4) &nbsp; &nbsp; &nbsp;应变能力强，能适应外界环境的各种变化。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5) &nbsp; &nbsp; &nbsp;能够抵抗一般性感冒和传染病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6)   体重适当，身体匀称，站立时，头、肩、臂位置协调。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7)   眼睛明亮，反映敏捷，眼睑不是发炎。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8)   牙齿清洁，无龋齿，不疼痛；牙龈颜色正常，无出血现象。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">9)   头发有光泽，无头屑。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">10) &nbsp; &nbsp;肌肉丰满，皮肤有弹性。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2． 影响男性健康的主要因素是什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">世界卫生组织（WHO）早在1948年对人类“健康”一词所做的定义就非常明确：健康——是指生理、心理和社会适应能力三方面的完善状态，而并不仅仅是反映没有疾病和身体残缺。影响男性健康的因素，以内外环境分，则有内因和外因之不同；以先后天分，则有先天禀赋因素和后天调养因素之不同；以其性质分，又可分为精神因素和生物因素以及外伤、房劳因素等等。总之，影响男性健康的因素多种多样，既可单独为病，也可联合致病，损害健康。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[先天因素]   男子自身的健康，与父母先天禀赋关系极大。父母的禀赋在很大程度上决定了新生儿的强健乃至一生的健康水平。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[后天因素]</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1)      社会因素：由于激烈的社会竞争，男人们工作紧张、思想负担重，致使精神疾患、心血管疾病以及阳痿、遗精、性功能低下明显增多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2)      情志因素：男子常因七情不节、内伤脏腑，导致高血压、冠心病等。西医学发现，当男子的大脑皮层处于正常工作的情况下，全身的神经、内分泌功能稳定，睾丸的生精功能以及性功能都很正常。如果精神处于长期压抑、悲观、忧愁状态、大脑皮层以及全身神经、内分泌功能便会失调，睾丸的生精功能以及性机能也会发生障碍，不育的可能性就会增加。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3)      气候因素：健康的不利影响，主要表现为风、寒、暑、湿、燥、火六淫邪气和疫疠之气侵袭机体为病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4)      饮食因素：饮食因素对男性健康的影响，主要表现为：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">①饮食不节，饥饱无常，尤其是暴饮暴食，在当今男子更为多见。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">②任凭喜好，饮食偏嗜，影响健康。特别是过嗜烟酒，对健康影响更大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">③不知食物偏性与身体相宜与否。有些男子不明饮食物的性能是否于体有益，常不加选择，随便乱吃，明明是阳虚之体，却常食寒凉之品；明明性功能低下，却误食对性功能不利的食品等等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5)      起居因素：起居因素是影响男性健康的重要原因之一。主要表现为：不按时起卧、生活不规律，过劳和过逸等。睡眠对于健康十分重要。因此，为了自己的健康，必须养成良好的生活习惯。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6)      性生活因素：对于男子来说，性生活既不可无，更不可过。性生活受到障碍，则会影响身心健康，出现生理和心理的不平衡而发生病变。过度而无节制的房事生活，势必损伤肾精，耗气伤神。只有适度的性生活才于身心健康有益。临床中常见精力充沛、貌显年轻之人，常是具有良好性生活之人。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3． 男性健康应遵循什么原则？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">增强保健意识，加强健康维护。积极的生活方式首先来自积极的保健意识。有规律地从事体育锻炼会令你充满活力，促进大脑及人体其他部位供氧，提高睡眠质量等等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">减少健康威胁因素，改变不良生活习惯。以饮食和起居为主要因素的不良的生活习惯是健康的最大威胁，如嗜烟酗酒、饮食、作息不规律、休息不充分等，都会直接导致男性健康状况的下降，成为健康隐患。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">树立生活信心，寻求有效帮助。有时候最难做到的事情恰恰是什么事情也别做。而这正式我们有些人偶尔所需的东西，以便彻底给自己的身体一个休养生息的机会。但是放弃与放松却绝对是两回事。逃避与放弃会让患者陷入深深的绝望中。然而如果鼓起勇气，到医院去，他们往往会发现克服障碍并非难事。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">消除陈旧的保健观念，矫正错误的健康认识。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4． 都说女性有更年期，而男性有无更年期？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关于男性有没有更年期至今还没有个准确的定论，虽然存在分歧，但已有不少专家作出了肯定的答案。男性更年期的年龄特征与女性相比略晚一点，大约发生在50岁左右，男性到了50岁，睾丸酮水平降低，造成一时的内分泌失调，出现生理变化引起暂时性的异常阶段。所以男性更年期确切地说应该称为中老年男性睾酮部分缺乏（PADAM）。典型的症状包括潮热、出汗、心悸（心血管），健忘、注意力不集中，焦虑，缺乏自信，效率低。（精神），失眠，食欲不振，骨骼和关节疼痛（生理体能），性兴趣降低，性欲降低，阳痿等</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">研究表明，男性更年期通常表现为体力欠佳、精力不足、思维欠灵活和记忆力减退等。过去历来是雷厉风行的爽快人，可能会变得优柔寡断、缺乏主见；过去一直是好学、敏感的人，可能会变得对许多事提不起兴趣。还会出现疲倦、失眠、郁郁寡欢、容易激动和性欲低下等男性更年期的一些常见症状。但是，对于大多数男性来讲，由于男性更年期的影响而引起的生理和心理上的状态变化不是很典型，只有少数男性到了这个年龄阶段，会出现各种症状。</span><br />
<br />
