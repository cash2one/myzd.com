<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肩周炎/冻结肩/挛缩性关节囊炎的手术治疗&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先要确认是肩周炎而不是其它能引起肩关节疼痛的疾病，比如肩袖损伤、肩峰撞击综合症。这需要专科医生的检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肩周炎又叫冻结肩，或者挛缩性关节囊炎。患者大部分没有明显外伤或者只有轻微外伤而发病。症状主要是疼痛和肩关节全方位活动受限。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 以前认为肩周炎是一种自限性疾病，病程12-36个月。但是近期的研究发现，在相当一部分患者，肩周炎并不会自然好转，患者终生都会有症状。一部分患者尽管最终自然缓解了，但是病程过长、症状过重，对生活质量造成了严重影响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肩周炎的治疗整体看对日常生活的影响。首先要采取保守治疗。如果正常关节功能是100分，而患肩因为肩周炎引起的疼痛和僵硬，功能评分在60分以下，称为严重功能障碍，保守治疗3个月无效，则建议手术治疗。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前，关节镜下肩关节全方位松解术是唯一能够在可以预料的时间内终止冻结肩病程的方法。绝大部分患者术后3个月功能得到最大程度恢复，也就是病情终止。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如果肩关节功能影响不大，比如整体功能评分在80分以上，建议保守治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 冻结肩从病程上分三期：始冻期、冻固期、解冻期。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在始冻期，大部分医生不愿给患者做手术，尽管患者肩关节功能可能已经受到严重影响。这主要是因为医生心里也没数，怕做过手术患者还是会进入冻固期，等于手术无效。但是我们的研究表明，绝大部分患者术后3-4个月后病程就终止了，而不再进入冻固期。因此在始冻期行手术治疗有其价值。始冻期手术的另外一个优势是关节没有严重挛缩，手术操作比较简单。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在冻固期和解冻期前期，大多数医生也不愿意为患者做手术。这主要因为这个阶段的患者有严重关节挛缩，关节镜可能根本插不进关节内，也就没法完成手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我们根据肩周炎患者肩关节挛缩的程度分为四度：轻度挛缩、中度挛缩，中度挛缩、极度挛缩。针对不同的挛缩程度，设计了不同的松解方法。即便对极度挛缩的患者，也能够通过特殊的松解方法进行高效率、安全的松解。因此，对于冻固期和解冻期前期的患者，因为他们的功能往往有严重影响，而又不存在技术障碍，我们采取更积极的治疗态度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肩周炎患者手术治疗的效果和手术方式有极大关系。我们的经验：保证治疗效果需要完成以下几部分手术：（1）肩关节360度松解；（2）肩袖间隙炎症组织的完全切除而不是简单松解；（3）肩胛下肌的270度松解；（4）肱二头肌腱的固定；（5，关节内、肩峰和三角肌下滑囊内炎症滑膜的彻底切除。</span>
</p>
