		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>互联网+能给医生和患者带来什么？</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">最近《互联网+》非常冲击我们的眼球。互联网+能给我们带来什么？</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">给患者带来的变化</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">互联网+可使患者就医的过程更加便捷、有效</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、通过互联网医院增加了挂号的方法，患者不需要盲目排队挂号。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、大量的医生利用休息的时间，进行了大量相关科学知识的普及，对自己所患疾病做到了心中有数。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、医疗管理机构使许多省份之间的医疗保险报销互联互通。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4、医生借助互联网进行远程会诊，降低了患者长距离远程面诊的费用。节约了时间。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5、知悉了诊治方向，避免盲目求医。知悉治疗方案提高了与医生交流的通畅性。通过与医生的院前交流，患者节约了在医院与医生面诊的时间。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6、方便患者长期与医生进行（交流、）治疗后的随访、以及进一步健康指导。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">给医生带来的变化</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">互联网+不是简单的医学+互联网，是一次医学创新。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、创新还是创业是摆在医生面前的难题。互联网是非常有用的工具，借助这个有用的工具，通过创新缓解了医生创业的压力。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、医生借助互联网进行学术交流，提高了医疗技术水平。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、医生通过在互联网的帮助，帮助了自己执业地域外的患者。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4、预先与患者进行交流，将院内的工作向院前拓展，提高了的工作效率和有效性。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5、医生之间进行疑难疾病讨论，提高治疗质量。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6、通过互联网，医生之间进行治疗推荐，方便患者就地治疗。</span>
