			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>深入虎穴直击病灶</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2883/3.png" title="" alt="demo.jpg"/></span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在人类通过手术探查癫痫病灶的过程中，总是试图不累及正常脑功能区而尽可能精准定位一举拿下，就像驾车人在智能化的GPS指引下不走弯路、绕开拥堵，直达目的地。立体定向脑电图（SEEG），如同医生手里的新式GPS装备，再隐匿的癫痫病灶也躲不过它的火眼金睛。文献显示，SEEG是近年来国际上新兴的一种癫痫病灶查找方法，可以进一步明确癫痫病灶的部位，达到准确切除病灶的目的。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 看上去很完美的临床新技术，实际运用情况究竟如何呢？上海交通大学医学院附属仁济医院功能神经外科徐纪文教授介绍，立体定向脑电图近一两年开始逐渐被国内专业医院引入临床。从专业角度来讲，这是一种借助立体定向微创技术，将深部电极植入到大脑不同部位，按照三维空间配布，记录大脑皮层及深部组织的脑电变化、癫痫发生以及传播途径，以达到精确定位致痫灶的方法。基于SEEG技术，以“临床症状—脑电生理—脑内解剖结构”为依据的致痫灶定位理念，已为欧美大部分癫痫专科医生接受和奉行，被认为癫痫外科史上一次革命性的飞跃。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 功能神经外科学普遍认为，癫痫手术效果高度依赖于术前的准确定位。然而，一些位于皮层下、位置深在或者颅内较为隐秘部位的癫痫病灶，无法使用头皮电极和硬膜下皮层电极定位。据统计，头皮脑电仅可记录到皮层1/5-1/10的电位变化，皮层脑电图仍是一个从表面推测病变部位的过程。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “不入虎穴，焉得虎子”。SEEG可以直接放至颅内病变部位，如额叶深部、大脑内侧面、扣带回、颞叶内侧等无法常规放置皮层电极的部位，SEEG误差小于1毫米。每位患者的致痫灶不同，定位点随之个体化，电极的每个触点对应不同的解剖部位。如果在定位过程中发现微小病灶，可直接进行热凝，避免二次手术和开颅手术。徐纪文教授指出，虽然手术需要全身麻醉下完成，但是微创技术可以做到不开颅行SEEG电极植入。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有些癫痫的发作表现及脑电图提示的病灶区与磁共振等神经影像学检查结果不在同一区域，也就是颅内原发起始区和外在癫痫表现不一致。此时，运用SEEG技术可及时捕捉癫痫起始区的真正所在，无论部位多小或者位置多深，而且可追踪其他部位存在的脑电图异常。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> SEEG技术对医生的专业要求较高，还需结合Leksell立体定向系统、BrainLab工作站和血管成像融合技术。根据临床运用实践，徐纪文教授评价，目前手术风险小，疗效确切。适用的癫痫病人经过治疗后，可明显改善生活质量，回归正常学习、工作和生活。&nbsp;</span><br />
