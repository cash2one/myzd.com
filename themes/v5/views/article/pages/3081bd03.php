		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>一名中国医生的追梦人生——记解放军总医院泌尿外科主任张旭</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">６月６日，北京解放军总医院国际会议中心，为期３天的国际微创泌尿外科盛事——第十届“欧洲腹腔镜手术大会”拉开帷幕。</span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">大会主席由解放军总医院泌尿外科主任张旭担任，这也是大会主席首次由欧美国家以外的专家担任。</span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">所有这些改变，皆因为中国微创泌尿外科１０余年来的发展——正是张旭持之以恒的逐梦之旅，开创了一片令世界瞩目的泌尿外科新天地。</span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">１９９１年，一名美国医生将腹部外科的腹腔镜技术“移植”到泌尿外科领域，开启了世界泌尿外科手术的一场变革。当时，做开放式手术已臻化境的张旭，敏锐地洞察到了大洋彼岸的微创腹腔镜手术在泌尿外科的前景——</span>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">泌尿外科的开放手术需要在病人腰腹部切一个２０至３０厘米的口子，有时还需要切掉一根肋骨，病人术后一个月都直不起腰；而腹腔镜手术则只需要打三个小孔，每个孔直径０.５至１厘米，患者术后第二天即可下床，一周痊愈出院。</span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">于是，张旭毅然放弃了传统手术，从零开始探索腹腔镜手术。</span>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">张旭介绍，在欧美等国家，泌尿外科的腹腔镜手术都是从腹腔开孔，因为腹腔的解剖标志明显，操作空间大，但缺点是容易损伤其他器官，并发症较多。“我们是泌尿科的医生，为什么非要经过腹腔开刀呢？从前腹部进入需要经过肠、胰腺等脏器才能到达腹膜后，不仅费事，操作不好还对其他脏器造成影响。所以我想到直接从腹膜后打孔。”张旭说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">张旭重新从最简单的腹腔镜体外模拟训练到动物实验开始，重新认识腹腔镜下解剖结构，重新设计腹腔镜手术步骤。２０００年，张旭成功实施了中国第一例泌尿外科的腹腔镜手术——他创建了一整套与欧美国家完全不同的、以“后腹膜入路”为特色的泌尿外科腹腔镜技术体系。这套另辟蹊径的“后腹膜入路”技术体系，能最大程度减少对腹腔内脏器的干扰，直接进入腹膜后腔手术，从而减少相关并发症，彻底改变了我国泌尿外科传统手术模式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">如今，张旭已先后创立腹腔镜手术术式３２种，涵盖泌尿外科所有体腔内的脏器；完成手术７０００余例，没有一例出现意外。他主持的“腹腔镜技术在泌尿外科的应用研究及推广”项目，获２００６年度国家科学进步二等奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">为了能让腹腔镜技术更多更好地服务于病人，张旭先后通过学术会议、手术演示卫星转播、专题报告和培训班的形式，在全国５００多家大型医院和大学附属医院进行推广普及，培训了７３００名微创泌尿外科专科医生，培养了５５０多名博士生、研究生和进修生。</span>
</p>
