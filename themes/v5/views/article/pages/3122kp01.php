<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>头痛莫忘看眼科</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">日前，一位刚退休的张阿姨因头部剧痛，右眼视力骤降，还伴有恶心、呕吐，来到厦门中医院急诊内科就诊。张阿姨回忆说，以前曾多次头痛，伴恶心、呕吐，都是自己随便吃几片止痛药。急诊内科医生做了几项检查后，马上把张阿姨送到眼科。经检查发现张阿姨右眼视力仅为光感，眼压极高达到65mmHg，诊断为青光眼。后经积极治疗，待眼压稳定后，厦门中医院眼科林媛主任成功为张阿姨进行了青光眼小梁切除手术。术后患者在眼压得到满意控制的同时，视力也有明显的提高，出院时检查达到0.4，现已痊愈出院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 头痛是青光眼常见的临床症状，大部分患头痛兼恶心、呕吐的病人会很自然的去找内科医生治疗。孰不知眼病也会引起头痛，因此提醒广大患者朋友如果出现头痛、眼痛伴视力下降时应立即到眼科诊治，以免延误病情，造成不可挽回的后果。同时，提醒广大市民出现以下情况时，你可能已患上了青光眼，要赶快到医院进行检查。①经常性眼球胀痛，鼻根眉弓痛而又无近视、远视和散光或其它疾病；②经常出现看灯或其它光源时可见其周围有彩虹样的光环、看东西好象前面有一层雾遮挡；③眼红充血，视力模糊，疼痛明显但无分泌物；④远视眼者及近视眼者近期内视力明显下降；⑤ 40岁以上经常更换老花镜，仍觉不合适；⑥家族中发现有人患青光眼者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">青光眼是中国致盲的主要眼病之一，当病人出现症状而就医时，往往病变已发展到一定阶段，出现视力减退、视神经萎缩、视野缩小等无法挽救的视功能损害。因此，学会自我检测，若能早发现、早治疗，将会大大提高治疗效果。</span><br />
