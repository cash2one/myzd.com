<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">人民网北京6月24日电 （王亚微）今日上午 ，北京天坛医院神经病学中心癫痫科、功能神经外科、小儿科联合在该院门诊大厅举办了“国际癫痫关爱日”大型义诊咨询活动。癫痫科主任医师王群、功能神经外科主任医师张建国、小儿科主任医师高宝勤以及众多知名专家齐聚现场，为癫痫患者提供了义诊咨询服务。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6月28日是第九个“国际癫痫关爱日”，今年关爱日的主题为“用心照护，康复身心”。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">癫痫这一慢性神经系统疾病从新生儿期至老年期均可发病。该病病程长、致残率高，被世界卫生组织作为重点防控的神经精神疾病之一。我国癫痫患病率为7‰，活动性癫痫患病率为4.6‰，即约有600万癫痫发作没有控制的癫痫患者。由于癫痫发作的突然性和不可预知性，以及癫痫可能造成的认知功能损害和心理负担，与其他慢性病相比对患者生活质量的影响更大。而长期照料患者、社会歧视等因素使其家人面临着沉重的经济负担和巨大的心理压力。“用心照护，康复身心”旨在呼吁广大医务工作者、患者和其家属以及社会公众，在关注癫痫这一疾病的同时，能够重视疾病带来的心理影响，做到心对心的呵护，帮助患者及其家庭在控制癫痫发作的同时，达到身与心共同健康。</span>
