<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>先拆肾动脉瘤再做自体肾移植 十二小时保命保肾</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">日前，中山大学附属第一医院多个学科联合作战，通过一期手术即完成了一例左侧肾动脉瘤旷置、右肾切取、右肾动脉瘤切除、体外血管重建、自体肾移植等手术步骤在内的复杂双侧肾动脉瘤手术。据了解，这在国内属于首例同期完成此类手术的案例。</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/1657/5.png" title="" alt="demo.jpg"/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中山大学附属第一医院器官移植科主任王长希教授</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">双侧肾动脉瘤随时破裂 患者急需保命保肾</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">该患者是一名38岁的男性，因为活动后有下腹会隐隐作痛到医院查明情况，医生通过算肾动脉CT、造影和肾动态想想检查措施发现，患者右肾动脉真性动脉瘤，双肾动脉假性动脉瘤。其辗转多家大医院就诊，均因病情复杂、手术风险极大而未被收治，无奈之下最后到了中山大学附属第一医院就医。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">医生经过进一步检查发现，患者的双侧肾动脉瘤都较大，患者左肾动脉假性动脉瘤直径超过了10厘米，随时有破裂大出血死亡可能，左肾被假性动脉瘤压迫已经萎缩，功能明显下降，而右肾动脉主干也有了一直径达4cm的症型动脉瘤，供应肾实质的三条分支血管均由该动脉瘤发出，而供应右肾上级分支血管远端右形成一直径达3.5厘米的假性动脉瘤，无法采用血管支架介入治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中山大学附属第一医院器官移植科主任王长希教授强调，像这种动脉病变的患者，瘤体在长到这个程度下随时会有破裂大出血的可能尽快手术是挽救患者生命的唯一选择。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">但是手术面临十分棘手的问题。王长希教授表示，手术要保留患者的右肾功能，否则就要终身透析治疗。但该患者右侧肾动脉瘤已经深入右肾的肾门，瘤体的位置较大且较深，肾动脉处血管壁机器脆弱。而如要切除动脉瘤及保住右肾，即便采用髂内动脉体外重建肾动脉后行自体肾移植术，手术难度也较大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">多科室合作同期克服五道手术难关</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由于患者病情复杂我元召集了血管外科、器官移植科、泌尿外科、显微外科等多科会诊，进群体专家讨论最终拟定手术方案，决定一期手术同时处理双侧肾动脉瘤，行“左侧肾动脉瘤旷值置+右肾切取、右肾动脉瘤切除、体外利用髂内动脉重建肾动脉+自体肾移植”术，以降低手术风险。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6月3日，在综合中山大学附属第一医院院长、血管外科首席专家王深明教授，副院长、器官移植科首席专家何晓顺教授指导意见的情况下，中山大学附属第一医院血管外科、器官移植科、显微外科同力合作，有血管外科常光其教授、器官移植科王长希教授、显微外科戚剑副教授共同实施该手术。在各科教授通力合作下，手术历时10余个小时梳理完成。术中同期顺利完成了左侧肾动脉瘤旷置、右肾切取、右肾动脉瘤切除、体外利用髂内动脉重建肾动脉、自体肾移植五道高难度手术。术中术后无任何并发症。患者顺利康复，自体移植肾已经恢复正常功能。尽管该手术十分复杂，但器官移植科已经有400多例活体肾移植经验，加上显微外科的密切配合，经过精细操作，最终去的手术成功。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">值得注意的是，在外科手术专家的精细操作下，术中出血很少，患者无需输血，术中术后无任何外科并发症，自体肾移植顺利完成，术中即刻有尿排除，肾已经恢复正常功能，避免了患者术后靠透析维持生活。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">王长希教授指出，此例手术若分期进行，手术难度将会更大，病人也存在大出血的可能，一次完成动脉瘤旷置、右肾切取、体外利用髂内动脉重建肾动脉和自体肾移植术，消除了再等待二次手术期间潜在的动脉瘤破裂的风险。根据现有文献检索，通过同期手术完成双侧肾动脉处理加自体肾移植手术，消除了等待二次手术期间潜在的动脉瘤破裂的风险。根据现有文献检索，通过同期手术完成双侧肾动脉处理加上自体生移植手术，这在国内尚属首例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">王长希教授还强调，像这种高难度的大手术在术前要充分考虑医院整体综合实力以及多学科协同作战能力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">双侧巨大肾动脉瘤极其罕见，死亡率高</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">肾动脉瘤是一种罕见肾血管性疾病，国内外报道发病率0.01%-1%，双侧巨大肾动脉瘤更加罕见。发病原因主要是动脉肌纤维先天发育不良，动脉粥样硬化，或继发于创伤和感染。临床表现多样，缺乏特异性，多因为顽固性高血压被发现，诊断依靠双肾动脉CT和造影。处理方式包括肾切除、肾部分切除、介入手术和自体肾移植等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在双侧肾动脉瘤处理中，自体肾移植占有尤其重要的地位。肾动脉瘤一旦破裂，死亡率高达70%以上。动脉瘤周边血供丰富、易粘连，手术分离易出血，操作难度大。因此及时发现、恰当处理显得至关重要。此案例中，专家借助先进检测手段，凭借丰富的外科经验，发现及时，处理得当，采用一期手术同时处理双侧肾动脉瘤以及字体肾移植，避免了病人的疾病进一步发展导致肾功能丧失，挽救了有功能的肾脏，髂内动脉体外血管重建的手术方法，应用与字体肾移植显示出了明显的优越性。</span>
</p>
