<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">舒马赫主刀医生Stephan Chabardes（左二）在车王病情发布会上介绍情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/535/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王伟教授（右）与Stephan Chabardes在上月交流时合影。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> @华西都市报:关于车王的情况，或许只有权威专家给出的说法才比较具有参考价值。为此，华西都市报记者独家专访了四川大学华西医院神经外科教授王伟，听这位曾与舒马赫的主刀医生有过交流的神经外科专家对这一类病况的解读。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近两个月的时间过去了，舒马赫依然处在昏迷之中，而全世界车迷对他的关心并没有随着时间的逝去而消减。因此当德国新闻杂志Focus耸人听闻地报道称医生已经放弃唤醒车王时，无数人都为车王的现状揪心不已。好在舒马赫的经纪人随后及时辟谣，才让大家心中的大石头稍微放了下来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 舒马赫手术后主刀医生上月访华</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 今年1月，舒马赫的一名主刀医生、法国知名脑外科专家Stephan Chabardes教授曾来中国参加一个神经外科的国际学术交流活动。在舒马赫治疗中，Stephan Chabardes的作用至关重要。据美联社报道，自舒马赫被送入医院之后，Stephan Chabardes教授就全程参与了舒马赫的手术，他主要负责清除其脑内淤血和减轻颅内挫伤。可以说，Stephan Chabardes对于舒马赫的病情非常了解，但是出于对病人隐私的考虑，他从不轻易在私下透露车王的病情。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 不过，在此前医院方面召开的几次新闻发布会上，车王的伤情主要都是由Stephan Chabardes来对外公布：“受伤之后舒马赫就被直升机迅速送往了医院。大脑扫描显示，有脑内出血情况并且还伴随着脑挫伤和水肿。我们立即进行了手术，试图清除淤血，遗憾的是，淤血虽然可以清除，但又出现了并发症。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 受伤不足三月放弃唤醒为时过早</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Stephan Chabardes来中国参加学术交流活动时，遇到了四川大学华西医院神经外科教授王伟。同为这个领域的专家，两人饶有兴致地做了一些学术上的讨论。但出 于 对 患 者 隐 私 的 保 护 ，Stephan Chabardes和王伟教授均未透露任何舒马赫伤后细节和治疗情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 昨天，华西都市报记者电话采访了王伟教授，他表示现在放弃舒马赫的唤醒治疗的消息听起来不太可信：“植物人唤醒术，一般是在患者呈植物人状态的三个月后才能开始实施，但舒马赫现在受伤才2个月，现在说放弃为时过早。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 当然，这并不表明对于舒马赫的治疗一定会无限期地进行下去，王伟教授同时也提到，“如果到时舒马赫的各项指标达不到做植物人唤醒术的标准，也只能放弃。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 植物人唤醒术唤醒率为20%左右</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 作为一名神经外科专家，王伟教授在去年年底刚刚做了一例植物人唤醒术，而且非常成功，这名男性患者目前正在逐渐恢复中。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据了解，该病人因为一起意外车祸导致严重颅脑损伤，并出现脑出血、脑梗塞、右侧肢体瘫痪的症状。在四川大学华西医院进行了两次手术后，生命虽然保住了，但却始终处于植物人状态。医院神经外科专家为病人做了一个名为“脊髓电刺激疗法”的植物人唤醒术，让他逐渐恢复了意识，并使其重新建立起语言中枢系统。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 王伟教授介绍说：“全世界接受唤醒手术的有200多例，唤醒率在20%左右。”事实上，四川大学华西神经调控中心作为西南地区唯一一个神经调控中心，自去年上半年开展“脊髓电刺激疗法”以来，已经成功地将3例植物人唤醒。背景新闻</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 放弃唤醒车王？原来是假消息！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 日前，德国新闻杂志Focus 一篇报道让所有关心舒马赫病情的人们伤心不已。该报道称，因为舒马赫目前的病情没有根本性进展，因此医生已经停止唤醒昏迷中的车王。很多人担心，按照这篇报道的说法，车王是不是永远都不会醒来了。好在车王的经纪人及时出来澄清，表示舒马赫的治疗依然在按步骤进行，只是唤醒程序的持续时间比较长，想要苏醒还需要等待。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前几日，马萨说他嘴唇动了</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 去年年底舒马赫在法国滑雪严重受伤，之后一直处于昏迷状态。本月初医生开始对他展开唤醒程序，但是舒马赫一直没有反应。长时间的昏迷让人担心舒马赫可能会就此成为植物人，但是探视过舒马赫的人都表示舒马赫的恢复前景乐观。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 尤其是在几天前，舒马赫的前队友、巴西车手马萨还给了大家很多希望。他在巴林试车之前前往法国医院探望了舒马赫，马萨表示他看到了舒马赫的嘴唇在动：“他在睡觉，看上去相当正常，他的嘴唇甚至还动过几次。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 家人发声明：不会选择放弃</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于舒马赫的病情，他的家人一直没有放弃过。2月19日，家人在舒马赫的个人主页上发表了一份声明：“非常感谢大家！每天能收到如此多来自全世界的祝福，这令我们很感动，你们的支持给予我们力量。我们都相信：舒马赫是一名战士，他不会选择放弃。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 而经纪人萨宾娜•科姆也表示，舒马赫的治疗是一个漫长的过程，想要苏醒还需要一段时间，在此期间她希望媒体和车迷都不要打扰到舒马赫及其家人：“像迈克尔这样的情况，不可能每一天都会有很大的改善，舒马赫的家人希望大家能够理解，给他们更多的私人空间。”</span><br />
