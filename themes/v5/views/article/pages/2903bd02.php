			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>节日聚餐多，谨防胆道疾病</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中秋佳节和国庆节即将接踵而至，亲朋好友往往趁此机会聚聚，有时难免要大餐一顿，甚至多喝几杯。某公司经理黄先生连着几天赴宴，吃了不少油腻食物，又喝了很多酒。这天晚上应酬之后他刚回到家中，就感到右上腹疼痛，持续性加重，同时出现发烧、恶心、呕吐等症状，被家人送到医院后确诊为急性胆囊炎。医生说黄先生的胆囊结石是细小结石，如果落入胆总管，可引起胆管炎、胰腺炎，严重者甚至有性命之忧。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着人们生活水平的提高，胆囊结石的发病率与发达国家日趋接近，是典型的“富贵病”。现在国内人群普查胆石症的发病率大约在7%左右，中年女性的发病率可达12%。复旦大学附属华山医院普外科副主任医师、医学博士殷保兵提醒大家：胆石症患者节假日期间饮食尤其要注意，饮食不当会直接诱发胆囊炎。因此控制饮食是预防胆囊炎急性发作的关键。脂肪类食物可促进胆囊收缩素的产生而增加胆囊的收缩排空胆汁，但是胆囊内结石使胆汁排空障碍并可引起胆道痉挛，引起胆绞痛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>哪些人更需警惕胆石症</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">殷保兵介绍说，随着人们生活水平的提高和饮食结构的变化，胆石症的发病率呈上升趋势，胆道结石的种类亦由胆道炎症引起的胆色素结石向高脂、高蛋白引起的胆固醇结石演变，胆囊结石比例上升而胆管结石下降。凡有家族史、肥胖、40岁以上的女性是胆结石的高发人群。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、中老年人：罹患胆石症以中老年人居多，且女性是男性的两倍。中老年人一般运动减少，控制胆道系统排出胆汁的神经功能也日趋衰退，胆囊、胆管的收缩力减弱，容易使胆汁淤滞，导致其中的胆固醇或胆色素等成分淤积而形成结石；其次，中老年人身体发胖，体内脂肪代谢紊乱，造成胆汁内胆固醇增加，女性尤为明显，故而中年妇女是胆石症的高危人群。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、家族史：流行病学研究发现胆石症的发生与遗传亦有密切的关系。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、经常不吃早餐者：由于空腹时间长，胆汁长期贮存在胆囊内形成淤积，导致胆汁中的胆固醇在胆囊内沉积，逐渐形成结石。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、嗜甜食者：经常吃甜食，过量的糖分会刺激胰岛素的分泌，使糖原和脂肪合成增加，同时胆固醇合成与积累也增加，造成胆汁内胆固醇增加，易导致胆结石。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、高脂饮食者：过量食用脂肪食物，使胆汁中的胆色素、胆固醇含量增加并发生沉淀，逐渐形成结石。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、长期素食者：结石的形成固然与脂肪过多、胆固醇过高有关，但更主要的取决于胆固醇的溶解度。正常人的胆固醇与胆盐、卵磷脂以一定的比例混合呈微胶粒状悬浮胆汁中，当这一比例破坏，便会形成胆结石。一些素食者摄入卵磷脂不足，且素食中过多的纤维素妨碍了胆汁酸的吸收，使胆汁中的胆盐浓度下降。卵磷脂与胆盐不足，导致比例失调，造成结石的形成。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7、其他：某些溶血性疾病或肝硬变时也可导致胆囊结石，但这种结石多半是黑色结石。胃大部分切除或迷走神经干切断术后，也可使胆囊排空延迟，利于胆石的形成。肥胖的人，如果在减肥后，又恢复原状，也是诱发胆石症的高危人群。糖尿病患者的胆结石较正常人高出一倍。甲状腺功能低下的人：甲状腺功能差，导致机体基础代谢率下降，消化功能、胆囊、胆道的收缩功能便相应减弱，引起胆汁流出变缓、滞留淤积和浓缩而析出沉淀，从而引起结石。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>如何预防胆道疾病</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 殷保兵告诉记者，预防胆道疾病，建立良好的生活习惯是关键，建议每天进食早餐和饮食规律，少食高胆固醇食物和动物内脏，控制饮酒，宜多食各种新鲜水果、蔬菜。社会生活及工作的压力较大者，要自我调节，保持良好心态，还要避免经常熬夜及生活不规律。节假日期间也应该遵循如上的生活习惯，切忌暴饮暴食，饮食无节制。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1.规律的饮食是预防胆结石的最好办法，不能暴饮暴食或饥一顿饿一顿。因为长时间空腹胆囊中充满了胆汁，胆汁浓缩淤滞，此时容易形成胆泥和胆固醇结晶。如果按时规律进食，食物刺激分泌胆囊收缩激素，使胆囊收缩，有利于胆囊排空，这时粘稠的和有少量胆泥的胆汁均被排到肠内帮助消化，可以预防胆结石的形成。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2.提倡早饭吃饱，切忌早饭不吃，因为一夜浓缩的胆汁需要排空，避免胆固醇沉淀形成胆泥。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3.合理的饮食结构，食物要荤素搭配，食入蛋白和脂肪要适中，增加食物中纤维成分，多吃粗粮，少吃胆固醇含量高的食物。适当食用纤维素丰富的饮食，以改善胆固醇的排泄，另外含有少量油脂食物促进胆汁排泌，有利于预防胆结石的形成。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 4.治疗能引起胆囊结石的一些原发病，如溶血性贫血和肝硬变，因为这些病易诱发胆囊胆色素类结石，预防胆结石。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 5.加强体育锻炼，要改变长期静坐生活方式，多运动，久坐不动导致胆固醇过多、胆汁淤积，有利于胆结石的形成。勤于锻炼、适当运动，能对胆汁起到促排作用。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6. 要养成良好的生活习惯，早起一杯水,每天饮水八杯，有利于稀释、促排胆汁。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>胆囊炎急性发作怎么办？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">殷保兵提醒读者，节假日聚餐首先应该适当控制脂肪类食物，不要饱餐和过量饮酒，而多进食纤维素和水果。如果出现腹胀不适等症状出现，考虑少吃一顿的饥饿疗法或者清淡流质，同时口服利胆药物，以免引起病症的急性发作和加重。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般认为急性胆囊炎是由于胆囊结石阻塞胆囊管，引起，如果结石落入胆总管则可引起胆管炎、胰腺炎 ；而较大的结石常无明显的腹部绞痛，但可引起慢性胆囊炎的表现。急性胆囊炎起病多与饱食、吃油腻食物、劳累及精神因素等有关，结石嵌顿于胆囊颈部或胆囊管内，使胆囊肿胀，浓缩的胆汁排不出去，这种浓胆汁对胆囊壁产生强烈的化学刺激，在这基础上较易并发细菌感染。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">急性胆囊炎大多采用抗炎解痉等对症治疗后好转，部分病人胆囊肿胀炎症控制不了并发化脓性感染，则需要急诊手术。急诊手术的风险较大，并发症较多，因此对于有手术指征的胆囊结石建议在炎症控制后择期手术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;那么哪些胆囊结石需要手术呢？①反复出现右上腹疼痛、胀痛者；②胆囊内为0.2-10px细小结石的患者，因为细小结石容易掉落到胆总管造成胆管炎和胰腺炎；③胆囊结石直径&gt;62.5px的患者，巨大结石长时间能摩擦可导致胆囊癌；④充满型胆囊结石患者，此类结石患者胆囊功能已经基本丧失。对于绝大多数胆囊结石需要手术的患者都能通过腹腔镜完成胆囊切除。现在随着腹腔镜技术的不断成熟发展和减少手术疤痕，腹腔镜胆囊切除从最初的四孔发展到现在常规的三孔技术，甚至在少数大医院减少至单孔，进而达到无疤痕手术，满足女性患者追求的美容效果，病人术后恢复快。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于结石落入胆总管合并胆道感染严重者，随着微创技术的成熟应用，先行经十二指肠镜乳头切开取石或仅引流胆汁，术后7-10天行腹腔镜胆囊切除加胆总管探查。未感染的胆总管结石可以直接行腹腔镜下胆囊切除加胆总管切开取石，由于其创伤小、恢复快、术后疼痛轻、手术安全和并发症少，但是对技术要求较高，需要扎实的腹腔镜技术和胆道镜技术。由于胆总管结石的情况较复杂容易造成残余结石, 术前应该做B超及磁共振等检查, 充分了解患者胆道解剖是否存在变异和结石位置。胆道结石是外科的常见病和多发病，对于诊断明确并且有手术指征的患者尽早实施腹腔镜手术，对出现急性发作的患者应在控制炎症后，1到2个月后择期手术。</span><br />
