	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>换心肺十年了，长贵生活怎么样</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">嘴角含着笑，眼角噙着泪。12月17日，亚洲心肺移植术后最长生存纪录保持者、29岁小伙郑长贵，在中南大学湘雅二医院为他举办的出院10周年纪念大会上满怀感恩。为郑长贵做手术的该院胡建国教授、周新民教授来了，悉心照顾他的护士们来了，他所工作的医院图书馆馆长也来了。掌声中，长贵吹灭了“生日”蛋糕上的蜡烛，默默地许了个愿望（本报12月18日第1版刊发图片报道）。后来他向记者透露，他的愿望是：努力工作多挣钱，找个贤惠女朋友。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">医院精心准备了一本相册，记录着长贵十年来的精彩瞬间。一份体检报告更是让长贵吃了一颗定心丸：所有指标正常。</span>
</p>
<p>
	<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">10位专家上阵“换心换肺”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1998年9月，由于长期肺动脉高压，年仅14岁的湖南邵阳农村男孩郑长贵在休息状态下也会出现心衰，危及生命。他辗转来到湘雅二医院胸心外科寻求治疗。该院胸心外科主任胡建国和团队经过讨论，最终找到对策：心肺联合移植手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2003年8月，等待近5年的长贵终于等到了合适的供体。经过精心筹备，当年9月20日，胡建国、周新民等10位专家为他成功实施了心肺联合移植手术。但专家们知道，更困难的是术后恢复。由于免疫抑制药物的作用，长贵的抵抗力比常人差很多。医生每天为他的伤口换药并根据情况调整药物剂量，护士则做好一切基础护理。同时，医院还专门为长贵成立了心理护理小组。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">术后第3天下床活动；术后280天，能从事轻体力活动；术后1年，可以像正常人一样生活……每一个进步都激励着医护人员，也让长贵对未来更有信心。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">关爱和责任一起“植入”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">10周年纪念大会这天，湘雅二医院胸心外科主任周新民给长贵测量了肺活量（见右图）。“吐气，吸气，再来，预备起，加油！”做完呼吸训练，护士又拿出精神和心理评估测试表给长贵进行心理测试。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">该院护士长魏芳小心地把床摇到合适的高度，先测心跳。可能是由于紧张的缘故，长贵的心跳有点快，魏芳连忙握住他的手安抚他。血压等几项指标都很正常，大家舒了口气。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">周新民告诉记者，全科近200名医生、护士都把长贵当成科里的“宝贝”。这天，长贵刚从胸心外科病房出来，迎面遇上的一位保洁阿姨就亲热招呼：“长贵……”记者很惊诧：“您也认识长贵？”阿姨笑容满面地说：“大家都认识他呀！”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">幸福，就是快乐地工作和生活</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">长贵坐车从邵阳老家到长沙大概要5个小时。2005年6月，考虑到他每次随诊都要来回奔波，家庭条件又差，医院为他安排了新工作――湘雅二医院图书馆管理员。长贵很喜欢这份来之不易的工作。</span>
</p>
