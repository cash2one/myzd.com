<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肩锁关节损伤与疼痛&nbsp;</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、什么是肩锁关节？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩锁关节，即肩峰-锁骨关节，是肩膀上方连接锁骨和肩胛骨（肩胛带）的关节。肩峰是肩胛骨的一部分，是肩膀上方能够触摸到的骨性突起。&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在肩锁关节处，有较强的韧带把肩峰连接至锁骨上；在其间有一个软骨盘，起着衬垫的作用。肩锁关节非常重要，因为它把肩胛带与躯体的其它骨骼连在一起。这里是肩关节疼痛的常见部位，也常遭到损伤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、什么原因导致肩锁关节疼痛？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正常情况下，在肩锁关节的肩峰和锁骨端之间存在着一个软骨盘。在肩关节活动时，该软骨盘像是一个衬垫，允许肩峰和锁骨相对旋转。但是，有时该软骨盘会退变，导致衬垫的缺失；有时，因为肩部着地跌倒、反复过头运动或者举重之类的创伤，会造成软骨盘损伤。这就会导致疼痛，有时会有弹响或者卡别之类的机械症状。肩锁关节炎症可能会发展成一种叫骨溶解的病症，这是一种关节内骨端吸收退行性变。这种病症常出现在举重人群中。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有时候疼痛来源于疼痛性关节炎。关节炎可能是年老的正常表现，也可能起因于创伤或者反复的过度关节负荷。后者常被称为创伤性关节炎。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、什么情况下会造成肩锁关节损伤，肩锁关节如何会分离?</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肩锁关节是经常遭受损伤的部位。常见的损伤机制是肩部上方着地摔伤，或者起因于特殊的运动，如北美橄榄球运动中球员遭受的肩关节上方的阻挡冲击，或者曲棍球运动中肩部与防护板的撞击。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩锁关节有很强的韧带连接。这些韧带走行于肩峰和锁骨（肩锁韧带），以及喙突与锁骨之间（喙锁韧带）。这些韧带可能遭到拉伸而未断裂（Ⅰ型损伤），可能部分撕裂（Ⅱ型损伤）或者完全撕裂（Ⅲ、Ⅳ、Ⅴ型损伤）。肩锁关节受伤以后疼痛可能来源于遭到牵拉或者撕裂的韧带，也可能来源于关节内软骨盘的损伤。如果韧带撕裂严重，会导致肩锁关节的畸形，也就是锁骨外侧端翘起。这是因为锁骨对手臂正常的悬吊支持丧失，肩部和手臂下坠，锁骨外侧端就显得突起，俗称“肩锁关节分离”。有肩锁关节分离的患者会感到肩关节的急性或者钝性疼痛，感受到肩锁关节的错动，或者不稳定。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、肩锁关节疼痛如何治疗?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有很多方法来治疗来自肩锁关节炎症或者骨溶解的疼痛。首先要采用保守治疗，包括休息、规避能够造成关节疼痛的活动，服用消炎镇痛药，采用局部的冷热处理，以及包括超声在内的正规理疗措施。如果保守治疗不能有效缓解疼痛，在肩锁关节内注射激素和麻醉药会很有帮助。局部注射一般有两个目的：首先，局部注射能够减轻炎症，许多患者因此得到疼痛的明显缓解；其次，如果注射后疼痛缓解了，则能够证实肩锁关节是疼痛的产生部位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果尽管采用了保守治疗，仍然有疼痛，并且影响到了患者的生活质量，就需要手术治疗。手术包括去除锁骨外侧端和肩峰内侧缘的一小部分，并去除软骨盘，这样创造一个空间，使肩锁关节的关节面没有刺激性接触。手术可以在关节镜下完成，也可以通过切开手术进行，两者效果相同。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">五、如何治疗肩锁关节分离?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有关肩锁关节分离的治疗始终有争议。大多数肩锁关节分离可以采用保守治疗。Ⅰ型和Ⅱ型损伤最适合采用保守治疗。Ⅲ型损伤的患者，如果患者能够接受锁骨端的突、疼痛不严重且功能不大受影响的话，也可以采用保守治疗。有些医生会建议患者马上手术，用螺钉或者缝线来稳定肩锁关节；但是对于该处理方式，在文献上缺乏临床经验总结支持。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">较为严重的肩锁关节分离，如Ⅳ型、Ⅴ型损伤，保守治疗产生疼痛和功能障碍的几率非常大，合理的选择是立即手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果肩锁关节分离是陈旧性的（数月之久），一般需要进行肩胛骨和锁骨之间的韧带重建。手术可采用肩关节局部的韧带，也可以采用异体韧带，联合缝线应用。该手术可以在关节镜辅助下完成，也可以作为完全切开的手术完成。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">六、术后康复是什么样的?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关节镜下或者切开肩锁关节切除后：该手术可以作为日间手术进行，一般在术后当天或者第二天患者可以回家。术后前两天需要佩戴一个吊带，肩关节局部可以进行冰敷，以缓解疼痛。随后去除吊带，可以使用手臂进行日常生活活动，比如洗澡、穿衣和开车。有关理疗和恢复运动的时机，手术医生会有特别关照。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩锁关节重建术后：肩锁关节重建术一般住院进行，术后第二天可以回家。在术后需要佩戴吊带，前几天需要定期使用冰敷以减轻肿胀。吊带一般使用6周，以使重建的韧带愈合。术后1周后，可以去除吊带洗澡，但是不能去除吊带自由使用手臂。可以用患侧手吃饭或者打电脑。更具体的康复计划需要咨询手术医生。</span>
</p>
