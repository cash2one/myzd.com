<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>专家提醒：老年人应从四方面预防骨质疏松症（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">日前，在中华医学会骨科学分会成立30周年之际,学会发起了“关爱骨骼健康,拥抱和谐生活”大型义诊和科普活动，活动中发现，公众对骨质疏松症存在很多误区。很多人认为，中老年人腰背痛、腿脚痛是因为“缺钙”，甚至认为这种疼痛不用治疗，挺一挺就能过去，这种观点是错误的，公众需要更多的专业性指导和建议。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;骨质疏松症根据病因可分为原发性骨质疏松症，如老年性骨质疏松症、绝经后骨质疏松症等；继发性骨质疏松症，如甲亢性骨质疏松症、糖尿病性骨质疏松症等；原因不明的特发性骨质疏松症。其主要症状有：疼痛，疼痛是骨质疏松症最常见、最主要的症状，以腰背部多见。驼背，它是由于骨质疏松造成椎体变形而引起的，是临床上的重要体征之一。骨折，骨质疏松症最严重的后果是骨折。常见部位包括髋部、胸腰椎、桡骨远端、肱骨近端及踝部。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;老年人本身骨质疏松，容易发生骨折。由于老年人对疼痛敏感性差，有的已经发生了骨折，但未感到明显疼痛，不及时就诊，容易耽误治疗。因此，老人跌倒，不能认为没有疼痛就不会发生骨折，只要跌倒就要及时就诊。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;一般来说，骨质疏松症一旦出现明显的疼痛症状时，骨骼中的骨量已经丢失30%—50%以上。此时骨破坏大于新骨的生成，骨骼中的矿物质减少，骨骼中的骨小梁变细﹑变脆或发生断裂从而导致上述症状。此时单纯服用钙剂效果往往不太理想,需要综合治疗骨代谢失衡，不仅要促进骨生成，同时也要阻止骨流失，这样才能有效控制骨质疏松。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;如何预防骨质疏松症，延缓其进程呢？建议老年人做到以下四点：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;1、生活有规律，戒烟限酒；</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;2、不偏食，要摄入足量的含钙质以及各种维生素的食物，多喝牛奶非常有必要；</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;3、加强锻炼，平时经常有计划地参加户外活动，如打太极拳、散步、慢跑等，运动量要根据自身的体质来定，同时要经常晒太阳；</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;4、服用一些预防性的药物，一个人每天需求钙的量是1000毫克，而根据我国饮食习惯，每天摄入钙的量是400毫克，远远低于正常需求，因此可适当地服用补钙药物和维生素D。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;此外，过去只发生在中老年人身上的骨质疏松症近年来正在年轻化，这和不少年轻人的不良生活方式有关。例如经常喝咖啡、抽烟、喝酒、喝碳酸饮料等都是骨质疏松的诱发因素，同时，年轻女性还普遍存在容易引起骨质疏松的两大不良生活习惯：缺乏运动与缺乏日晒。年轻时的这些不良习惯会给骨质健康埋下很大的隐患，直接导致不少女性到了中年或绝经后出现骨质疏松症。因此，骨质疏松症的预防要尽早开始。(中华医学会骨科学分会主任委员、中国工程院院士　邱贵兴)</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;热门解读</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 姜保国　北京大学医学部副主任</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 翁习生　北京协和医院骨科副主任</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;老年人为什么会变矮？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;人老变矮的主要原因是因为脊柱缩短。由于椎间盘变性导致椎间盘变薄，如果累及数个椎间盘即可导致身高的降低。椎体压缩性骨折，椎体发生楔形改变导致高度降低而引起，最终可能导致驼背或畸形。如果中老年人的身高比前1年降低2厘米,或是比年轻时低了3厘米—5厘米,就需警惕是否患了骨质疏松症，发生了椎体压缩性骨折。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;补钙会导致肾结石吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">补钙与肾结石没有必然联系。肾结石的发生受遗传、环境、营养及尿钙浓度等多因素影响。目前，专家推荐的常规补钙剂量不会增加肾结石发生的危险。当然在过多补钙时，通过肾脏滤过排出的钙较多，在泌尿系统中，尿钙含量较高，会增加泌尿系统形成结石的机会。因此，对于那些易发生肾结石的患者，在补充高剂量钙和维生素D前，应适当监测血钙和尿钙水平，若尿钙正常，可以适当补钙。</span>
