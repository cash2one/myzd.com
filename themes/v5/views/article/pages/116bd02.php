		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>抵御癌症应从“吃”做起</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1日上午，江苏省人民医院胰腺中心举行的特殊病友会在内科楼阶梯教室举行，该中心主任苗毅教授和病友们一起互动讨论——“胰腺肿瘤，共同的敌人”。这一切，正如前阵热播的电影《滚蛋吧，肿瘤君》里描述的医患关系那样暖心。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 胰腺癌被称为“癌中之王”，凶险性和死亡率极高，目前美国的术后5年生存率仅为6%，上海的总体手术后5年生存率仅4.4%，而江苏省人民医院胰腺中心的目前术后生存率达到20%-30%。苗毅教授以通俗易懂的语言给大家讲解了胰腺的工作机制，然后又给大家讲解了一些容易导致胰腺肿瘤的不良习惯，“我们胰腺中心去年全年手术量是520例之多，医生非常忙，除了手术还要进行学术交流、研究论文，但是我们依然认为，与病友的交流不可或缺，不该遗忘。病人出院，医患共同抗击病魔的战争仍未结束。胰腺肿瘤术后的患者关心后续治疗方案，医生再忙也要关心术后出院的病人，指导他们配合术后治疗，树立良好的生活方式。”</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 喝着老酒，吃着大肉，人生快事，却差点酿出人生惨剧。江苏省人民医院普外科、胰腺中心副主任蒋奎荣也提醒病友以及来参加病友会的一些市民，六成胰腺炎起源在“胆”，六成胰腺炎与胆囊疾病有关，三成胰腺炎与饮酒有关，与高脂血症有关的胰腺炎也占了将近10%。预防要命的胰腺炎很重要的秘诀是“管住嘴”，而术后膳食也要合理搭配，注意碳水化合物、脂肪和蛋白质的比例，要以碳水化合物为主，脂肪和蛋白质的量要适宜，防止胰腺过度的分泌胰腺。 （沈峥嵘）</span>
</p>
