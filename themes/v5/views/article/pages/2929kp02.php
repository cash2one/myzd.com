			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>膝关节置换术后 哪些人容易发生感染？</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人工膝关节置换术后的感染问题，越来越受到医生、患者的关注。在医院，医生从手术前的防护到手术中的无菌操作，都是非常严格地进行把控，力求把感染率降到最低。目前，国际上人工膝关节置换术后感染发生率为1%，而在北京301医院术后感染率已经降到了0.1%，远远低于国际水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然感染率下降明显，但也只是一个概率，对于具体某个患者而言，要么感染要么没感染，是0和1的问题。那么，哪些人在膝关节置换术后容易发生感染呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第一，抵抗力弱的患者。当患者全身免疫能力比较差，抵抗力比较弱时，一旦细菌进入机体就毫无“还手之力”，抵抗不住这些细菌的攻击，就十分容易出现感染。这种情况比较少见</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第二，手术过程中，身体其他部位存在感染的患者。这种情况属于患者自身抵抗力还不错，但是面对大举进攻的细菌部队，感染的风险还是比较高的。例如，患者手术前有溃疡，手术时就容易感染。因此，在进行膝关节置换术前，要先消灭这些炎症细菌；</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第三，体质属于容易被感染的患者。这种情况属于自身抵抗力虽然好，但患者的体质很容易让细菌繁殖壮大，最常见的就是糖尿病患者。由于糖尿病患者体内血糖浓度高，这些血糖对于细菌而言，是重要的营养品。因此，糖尿病患者如果没有很好地控制血糖，也十分容易发生感染。</span>
</p>
