<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>耳聋的基本知识</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">第二次全国残疾人抽样调查显示，我国共有听力残疾人2780万，人数居五大残疾之首，并且每年有2到3万患有先天性严重听力障碍的新生儿出生。耳聋已成为现代社会最为常见的慢性疾病之一，应该得到我们充分的重视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一、耳聋分类</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 临床上将各种听力损失统称为耳聋，耳聋可按病变的性质分为器质性聋、功能性聋及伪聋三类，其中器质性聋多按病变部位分为传导性聋、感音神经性聋与混合性聋3类。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 传导性聋：发生在外耳、中耳或内耳声音传导径路上的任何结构或功能障碍都能致传导性聋。传导性聋的气导听力损失一般不超过60dB，而骨导听力基本在正常范围。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 感音神经性聋：由于Corti器毛细胞、听神经、听觉传到径路或各级听力中枢受损害，致声音感觉或神经冲动传到等发生障碍，称感音神经性聋，其气、骨导听力皆下降。其中噪声、感染、耳毒性药物、遗传等因素致毛细胞受损者称感音性聋（耳蜗性聋），常有重振现象。病变位于听神经及其传导径路者分别称为神经性聋、中枢性聋（统称蜗后性聋），如听神经瘤、听神经病等，其言语识别率常有明显下降，患者诉说能听到声音，但不能辨别其意；病变发生于大脑听觉皮层者称皮层聋。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;混合性聋：既有外耳和（或）中耳病变，又有Corti器毛细胞或听神经病变而引起的同时具有传导性聋与感音神经性聋者为混合性聋，如耳硬化症同时累及听骨链何耳蜗，颞骨混哈性骨折，慢性化脓性中耳炎，胆脂瘤，中耳肿瘤等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二、耳聋程度分级</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 世界卫生组织（WHO ) 1997&nbsp;年根据500Hz&nbsp;、1000Hz&nbsp;、2000Hz及4000Hz气导平均阈值，将听力损失分为以下几级：轻度听力损失（26 ~ 40dBHL），中度听力损失（41 ~ 55dBHL），中重度听力损失（56 ~ 70dBHL），重度听力损失（71 ~ 90dBHL），极重度听力损失（≥&nbsp;91dBHL）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三、评估耳聋病因、病变部位的常用方法</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">①纯音听阈测试（定向条件反应测试） 纯音听阈测试反应受试耳对各种频率纯音的听敏度，估计听觉损害程度。普通纯音听阈测试适合于成人，12岁以上儿童，6个月以上儿童进行定向条件反射测试（视觉强化测听或条件化游戏测听）</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">②声导抗测试&nbsp; 声导抗测试是客观测试中耳传导系统、内耳功能、听神经以及脑干听觉通路功能的方法，适合于各类人群。一般而言，凡中耳功能正常者曲线为A型；As型常见于耳樱花、听骨链固定或鼓膜明显增厚等中耳传音系统活动度受限时；若其活动度增高，如听骨链中断、鼓膜萎缩、愈合性穿孔以及咽鼓管异常开放时，则曲线可呈Ad型；B型曲线多见于鼓室积液和中耳明显粘连者；C型曲线表示咽鼓管功能障碍、鼓室负压。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">③听性脑干反应测听（ABR）&nbsp; 分为听性脑干反应阈值、听性脑干反应潜伏期。ABR是指检测声刺激诱发的脑干生物电反应，ABR可用于判定高频听阈、新生儿及幼儿听力筛查、鉴别器质性与功能性聋、诊断脑小角占位性病变等。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">④畸变耳声发射（DPOAE）&nbsp; 是一种客观听觉功能检测手段，它依赖于耳蜗整体功能的完整，与耳蜗外毛细胞的功能密切相关。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑤40Hz听觉相关电位&nbsp; 为听稳态诱发电位，属于中潜伏期反应的一种。主要用于对听阈阈值的客观评估，尤其对1000Hz以下频率的听阈确定更有价值。可以帮助了解新生儿听力筛查及ABR反应未引出者的低频情况。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑥稳态听觉诱发电位（ASSR）&nbsp; 是有连续的或稳态的声音刺激信号诱发产生的脑电反应。ASSR属于客观测听方法，可以检测各个频率阈值，在不能进行行为测听或行为测听得不到满意结果人群的听力测量中（尤其≤6个月的婴幼儿），是很重要的。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑦言语识别率&nbsp;&nbsp;指受试耳能够听懂所测词汇中的百分率。结合听力学评估可初略判定耳聋的种类。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑧新生儿听力筛查：新生儿出生后6～48小时内接受听性脑干反应或耳声发射筛查，若未通过一个月内复筛，三个月内确诊，在6个月前进行干预。</span><br />
