		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腹股沟鼓“包包”快看看是否长疝气！</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 目前我国每年有460多万人患疝气，但普遍存在拖而不治或听信广告偏方乱治的现象。对此，广东省人民医院血管甲状腺腹壁疝外科主任彭林教授呼吁，一旦发现患有疝气，应早采用手术治疗。他告诉新快报记者，受到广告误导，大多数患者对于疝气的治疗存在误区，认为疝气不痛不痒，不需要治疗，有些患者听信民间偏方，采取保守治疗而非手术治疗，因而延误最佳救治时机。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 67岁的刘大爷最近2年走路时下腹部会鼓出来一个小包包，有人提醒刘大爷，这是疝气，但他不以为然，直到日前肿块突然变成平常的5倍大，感觉剧烈疼痛，刘大爷这才去看医生。医生检查时发现，鼓起的包块就是被挤出来的肠子，而且已经因为缺血而坏死，只能切掉。广东省人民医院血管甲状腺腹壁疝外科副主任医师胡世雄表示，如果刘大爷能尽早就医，实时复位和小手术就能轻松康复，不至于出现肠坏死。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据彭林教授介绍，他遇到一位34岁的郑先生，脐眼附近长了疝气，但他一直拖了8年，直到疝越长越大甚至影响到夫妻性生活的正常进行才来求治。彭林教授强调说，手术是治疗疝气的唯一方法，现在用得最多的方法是开放式无张力腹膜前疝修补术，该手术具有时间短、疼痛度低、复原速度快等优点，门诊就可完成。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 小知识</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1.疝气有哪些症状？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> A：腹部用力如咳嗽、站立或工作时，或是小孩于哭闹、游戏或者洗澡时，在腹股沟(鼠蹊部)或阴囊部位有隆起肿大，而症状在不用力或睡觉的时候就消失。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2.为什么会得疝气？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> A：胎儿在母体内的发育期间有一腹膜鞘状突由腹腔内延伸至腹股沟、阴囊，使得腹腔和阴囊相通，而女性的腹膜鞘状突则是由腹腔延伸至大阴唇处。大部分婴儿出生后此构造会封闭，但有些人没有封闭，腹内器官组织经由此构造进入腹股沟、阴囊，则形成疝气。或因老化、受伤、用力过度(如提拿重物、咳嗽、大小便时腹部用力)、抽烟、怀孕、肥胖等等因素，造成腹壁薄弱、撕裂、腹压，导致疝气发生。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 3.哪些人会得疝气？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> A：位于腹股沟的疝气大多发生于男性。主要原因为在睪丸下降至阴囊后，腹股沟内留下来的无支撑空间。而女性大多因怀孕或生产而导致，部位大多于大腿上端。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 4.未开刀前，如何缓解疝气的症状或不适感？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> A：减少活动或减少体重可暂时舒缓症状。穿戴疝气托带(束腹带)也可暂时舒缓症状。</span><br />
