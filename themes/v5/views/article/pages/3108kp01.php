<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">精索静脉曲张（Varicocele，VC）是精索静脉回流受阻而扩张或静脉瓣膜功能紊乱使血液返流致精索内蔓状静脉丛异常扩张、伸长和迂曲的渐进性疾病，精索静脉曲张是引起睾丸生殖功能障碍导致男性不育症最常见的原因，也是男性不育症各类病因中可矫正的因素。精索静脉曲张通常可经体格检查和辅助检查来临床确诊。精索静脉曲张保守治疗效果不佳，精索静脉曲张伴精液参数异常的不育症患者、或临床症状明显者、或青少年患者伴有睾丸体积明显小于对侧应当推荐手术治疗。临床采取的手术方式主要包括开放式手术、腹腔镜手术、显微外科手术、血管栓塞术等，主要手术并发症包括精索静脉曲张术后复发、睾丸鞘膜积液及睾丸萎缩等。虽然对各种手术疗法治疗精索静脉曲张长期安全性和有效性的比较研究，有资料表明，与其它手术方式报告结果相比显微外科精索静脉曲张修复术对生殖功能改善较显著，患者精液参数改善率提高65%，配偶受孕几率提高40%，而精索静脉曲张手术后复发率和并发症最少。精索静脉曲张显微外科手术治疗效果最佳并逐渐普及应用。尽管如此，精索静脉曲张各种手术治疗对于男性不育症的疗效，目前还缺乏符合循证医学设计合理的研究方案及大样本比较研究，评估各种手术治疗的安全性和有效性。一些研究表明，配伍使用一些辅助药物及中药制剂可能对有助于精索静脉曲张提高术后精液质量和受孕率，有待于深入研究。</span>
