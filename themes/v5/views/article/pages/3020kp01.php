			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脚痛，不可轻视</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在平常生活中存在着一种有趣而发人深思的现象：如果是肚子痛，哪怕是轻微的小疼痛，足以让人紧张不已，于是四处求医。但是换成了脚痛，大多数人都是能忍就忍，觉得脚痛而已，没什么关系。其实，这是一种错误的观念，人体的任何一个地方出现疼痛，无论是腹痛，或是脚疼，都不容忽视，我们应该重视脚痛，爱护自己的脚，就像我们重视腹痛，爱护自己的胃肠一样。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先，我要向大家解释一下疼痛的定义，别以为这是一个显浅的词语，就能认为，自己对疼痛很了解。其实不然：汉语的“疼”是指余痛；“痛”是指病人身体内部的伤害性感觉。现代医学所谓的疼痛，是一种复杂的生理心理活动，是临床上最常见的症状之一。它包括伤害性刺激作用于机体所引起的痛感觉，以及机体对伤害性刺激的痛反应。痛觉可作为机体受到伤害的一种警告，引起机体一系列防御性保护反应。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以，我想大家都应该了解到，疼痛，是一种人体健康出现问题时的警告，疼痛是上帝给予人类最美的礼物之一，我们怎能置上帝的警告不顾而继续忽视你的“脚痛”呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">千里之行，始于足下，当你的足部发生疾病时，我们的人生又如何能走得更远、更精彩呢？足踝部的病变有很多类，不可能三言两语地在此栏目说得清楚，然而，大部分足部的疾病都可表现为足踝部的疼痛。下面，我将列举足踝部的一些常见疾病分类：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">发育与畸形性疾病：主要为扁平足、高弓足、蜡样骨病等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">炎症性疾病：炎症性疾病又可分为系统性与局部性的炎症。系统性的炎症性疾病包括类风湿关节炎、强直性脊柱炎早期表现、银屑病性关节炎等；局部炎症性疾病包括跟腱炎、跖筯膜炎、跟骨后滑囊炎等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">神经源性疾病：踝管综合症、前跗管综合征、趾间神经瘤等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肿瘤：色素沉着绒毛结节性滑膜炎、神经纤维瘤等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其他：踝关节撞击综合症、足部感染、踇外翻及踇囊炎、鸡眼、跖骨头骨软骨病等等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在这里，我们要强调的是，上述的疾病分类不可能囊括所有的足踝部疾病，但是，以上的疾病都可以表现为足踝部的急性或慢性疼痛。出现了足踝部疼痛时，既不能置之不理，又不能过于紧张，胡乱投医，应该到正规的医疗机构就诊。特别是一些足部恶性肿瘤如足部滑膜肉瘤，如不早期诊断，很可能危及生命。如果当地没有足够经验的足踝外科医生，也许会出现漏诊或治疗不到位的情况，此时，大家应咨询一些专业的足踝外科专家，以免耽误病情而出现灾难性的后果。当然，大家也不必过于担心，因为足部的恶性疾病，机率极小。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最后，我在这里介绍一种在年轻并且爱穿高跟鞋或尖头鞋女性中相当常见的一种疾病：踇外翻。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">后天因素引起的踇外翻与穿高跟鞋或尖头鞋有很高的相关性，外翻的原因是因穿了不合适的鞋子所造成的，通常因穿的鞋跟太高、过尖及过窄的鞋，使脚跟不易固定，对脚趾造成挤压摩擦及压迫，不但影响脚趾的伸展与活动，造成不适及疼痛，还会破坏了原本三个立足点的功能，而行走时全身重量落在足部前端，脚趾会因身体重量压迫逐渐变形，就会造成踇外翻的现象。踇外翻并非像其名字暗示的那样是一个单一的病变，而常常是伴有其余足趾畸形和症状的足踇趾序列的复合畸形。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在早期，踇外翻可表现为足踇趾关节周围的红肿、疼痛，此时大多患者可能会发现自己的踇趾关节部位稍稍增大，影响外观，此时若及早进行手术治疗，只需做一些软组织或简单的截骨手术就可以解决疼痛与外形的问题。如果忽视了早期的这种程度较轻的疼痛，任由疾病进展，最终可能造成大踇趾关节的脱位、第二足趾关节的骑跨畸形、踇趾疼痛加剧、足底出现疼痛而无法行走等等，当发展成为严重的踇外翻时，简单的截骨手术与软组织手术已经不能纠正畸形，恢复足趾正常的负重，就要进行关节的融手术，最终会丢失了一个甚至是两个足趾的关节，到那时，就无法再穿上美丽的高跟鞋了，更重要的是，足趾的运动功能受到严重的影响，降低生活的质量！！</span><br />
