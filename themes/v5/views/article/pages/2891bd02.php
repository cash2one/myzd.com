			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>术中门静脉压力动态测定在门静脉高压症术式选择中的意义</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝硬化门静脉高压症（portal hypertension, PHT）的手术与腹部任何其它手术不同，手术适应证掌握不严、术式选择不合理，将显著增加术后并发症、再出血以及死亡的机会，手术风险极大。PHT手术方式的合理选择须依据病人门静脉血流动力学变化（1）。门静脉高压时血流动力学最突出的表现是门静脉压力升高，而这是食道胃底静脉曲张形成的主要因素。常用的术前血流动力学研究方法有多普勒超声(DUS)，内镜检查，螺旋CT血管成像（CTA）、磁共振门静脉系血管成像(MRPVG)，经动脉门静脉造影及肝静脉插管测压、造影等。仔细评估上述研究结果，术前对术式大致可做出正确选择。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大多数病人依据术前详尽的血流动力学研究对治疗PHT的术式可作出合理的选择。但是，由于每个病人发生血流动力学变化的病因和发生因素不尽相同，因此依据术中门静脉压力的变化结合术前血流动力学研究结果使术式选择更科学、合理，手术治疗效果更好。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术中可阻断门静脉主干后分别测定肝侧门静脉闭锁压（HOPP）和脏侧门静脉闭锁压（SOPP）并计算最大灌注压（MPP），MPP=SOPP-HOPP。MPP不依赖于门静脉血流，主要反映门静脉灌注量，此值越大，门脉灌注量越大，向肝的门脉血流越多；该值如呈负值则表示门静脉血流有倒流现象，即出现离肝血流，故该值对PHT的手术选择有重要参考价值（2）。由于上述测压方法有一定难度和风险，特别是测HOPP必须阻断门静脉后肝侧门静脉穿刺测压，限制了该方法在临床上的应用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术中直接测定自由门静脉压（free portal pressure，FPP）是反映门静脉压力最可靠的方法，是客观、量化的指标，一般认为门静脉压力≥ 22mmHg（30cmH2O）才有可能发生曲张静脉破裂出血（3，4）。而西方文献常以肝静脉梯度压（HVPG）≥ 12mmHg为发生静脉曲张和出血的阈值（5），HVPG是肝静脉嵌入压（WHVP）和游离肝静脉压（FHVP）的差值，约等于自由门静脉压（FPP）减去下腔静脉压，下腔静脉压平均约7.35 mmHg（10cmH2O），因此国内门静脉高压症病人出血阈值比西方文献报道高2~3 mmHg。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果依据术中FPP压力变化选择手术方式，则必须使测压结果准确。传统的经胃网膜右静脉穿刺插管导管不易进入肠系膜上静脉-门静脉，而且每次取零点和用水柱法读取数据的方法不可靠，结果误差较大，准确性大受影响。为此，我们采用经结肠中静脉右支插管至肠系膜上静脉-门静脉，插管十分容易，导管插入至肠系膜上静脉-门静脉的长度容易控制，大约15cm长。此外，我们将导管连接压力换能器，零点固定在下腔静脉水平，可以动态观察压力变化，除零点可能稍有误差外，不同手术阶段的变化结果不受影响。FPP测定时段是开腹后、脾动脉结扎后、脾切除后、分流后和断流后。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为了探讨术后再出血和脑病的发生与术中门静脉压力的关系，以及术中门静脉压力变化在手术方式选择中的指导作用，我们总结了我院近7年来170例施行贲门周围血管离断术和联合手术的病例资料，并按照术后FPP值将断流病例分成高压组（术后FPP值≥ 22mmHg）和低压组（术后FPP值&lt; 22mmHg）（6）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根据术中FPP值的动态变化，我们发现开腹后高压组、低压组和联合手术组三组病人的FPP值差异无统计学意义。三组病人脾动脉结扎后FPP值均比开腹后显著下降，脾切除后三组病人的FPP值变化均不明显。断流后高压组的FPP值平均上升幅度较大，与开腹后比较已无显著性差异；而低压组的FPP值无明显变化。联合组在完成分流术后FPP值下降明显，断流后有所上升，但最终FPP指仍显著低于开腹后，与低压组断流后比较无明显差异。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从术后随访结果（4~69月）来看，断流组共有15例发生再出血，其中高压组再出血率显著高于低压组和联合组。联合组有3例术后发生再出血，均发生于术后1年。出血控制后MRPVG检查发现脾肾静脉吻合口栓塞，食管胃底曲张静脉再形成。从术后脑病发生率来看，低压组、高压组和联合组三组无统计学差异。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">贲门周围血管离断术是目前治疗门静脉高压症常用的术式，但术后再出血发生率高。其原因可能与断流术后持续的门静脉高压状态导致了门静脉系统侧支循环的重新出现，再次发生食管胃底曲张静脉破裂出血以及门高压胃病（PHG）有关。我们的研究显示断流术后门静脉压力的高低很大程度上决定了术后是否再出血。断流术后FPP≥ 22mmHg的病例术后再出血的风险明显高于FPP&lt; 22mmHg的病例。因此，无论采用何种手术，目标是FPP降至22mmHg以下。断流术后FPP降到出血的阈值水平以下时可考虑不行分流手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝硬化门静脉高压症病人的FPP主要取决于肝内阻力、侧支血管阻力和腹腔内脏血流量。脾动脉结扎、脾切除术的实质是一种减流手术，在纠正脾功能亢进的同时，可减少25% ~ 40%的门静脉血流。实际上，脾动脉结扎后FPP下降是整个门静脉高压症手术过程中压力下降最明显的过程，在切除脾脏后与结扎脾动脉后相比，FPP的变化不明显。如果肝内阻力增加的程度较轻，仍有较多的向肝血流时，脾动脉结扎后FPP下降较明显，表明脾动脉血流量增加在FPP中所占比重较大，在脾切除及断流后FPP基本上无变化，断流术后FPP值多 &lt; 22mmHg，通常不需再加行分流术；如果肝内阻力较大，自发性门体分流较多，则脾动脉结扎后FPP下降幅度较小，断流术后FPP值多≥ 22mmHg。脾动脉结扎后FPP值对术式选择起重要作用，FPP值较开腹后下降非常明显或低于出血阈值（22mmHg) 2~3 mmHg，可行断流手术，否则通常需行分流加断流的联合手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关于断流术和分流术的操作先后顺序各家报道不一，由于脾动脉结扎后FPP下降是整个手术过程中压力下降最明显的过程，如果结扎脾动脉后FPP没有能降到出血阈值内，特别是脾切除后FPP不变或反有升高时，就应该考虑加行脾肾静脉分流术，因为以后的操作不仅不会降低门静脉压力，反而会小幅上升。我们通常先行脾肾静脉吻合术，因为FPP降低可使断流术操作较容易；若反之，断流操作时易出血，而且如断流操作时间较长，有可能导致脾静脉血栓形成。如果脾切除后不能判断是否需行分流术时，可先作断流术，然后根据FPP值和脾静脉张力的变化决定是否加行分流术。</span>
</p>
