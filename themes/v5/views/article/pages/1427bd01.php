		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>大夫提醒：成年人疝气需手术</strong></span>
</p>
<p>
	<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">受访专家/广东省人民医院副主任医师　彭林&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">文/记者　张华　通讯员　张玲玲&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“疝气”从儿童到成年人都可发生，它不是一股“气”，而是人体正常的组织如肠子、膀胱等突破了腹腔的薄弱区域，移位到腹股沟或其他部位。疝发病率最高是老年人，尤其是老年男性。广东省人民医院普外科副主任医师彭林认&nbsp;</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为，随着日常行走、活动，形成习惯性下坠，一旦卡在环口处，不能复位，会造成肠坏死等。所以疝一定要治疗，成人疝还需要手术治疗。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">咳嗽便秘易致疝&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">疝好发于腹股沟，是中老年人的常见病，发病率为1.5%-2%。这和老年人有咳嗽、便秘、前列腺增生肥大等有关，因为长期的慢性咳嗽、排尿困难、排便费力，会使腹腔内压力增高，排挤、压迫腹内脏器向腹壁薄弱的区域移位。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">刚开始有“疝”时，症状不突出，几乎看不到，手摸的感觉好像有东西向外顶，慢慢症状明显时，有一块貌似肿胀的东西，稍一活动就鼓起来，躺下去就消失了，不痛也不痒，这种情况几乎可以肯定就是疝，要赶快去医院进行治疗。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/1427/1.png" title="" alt="demo.jpg"/>
</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">成年人疝需要手术&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">小孩出生后一年内，因腹股沟管尚未发育完善，所以在6个月以内穿疝带或疝托是有可能自愈的。而成年人则不可能，唯一能根治的办法只能手术。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">彭林介绍，目前，保守估计我国每年有200万例以上的腹股沟疝病人需要手术治疗，而全国疝治疗中心为数不多，大多数由普外科或泌尿外科医师完成。现在各种治疗疝气的方法也五花八门，其实疝气这个说法本身就不科学，因为鼓出来的不是气而是内脏。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">局部麻醉打“补丁”&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">以前，大多数疝气患者都需住院5-8天。因此，很多患者担心手术而迟迟不愿意接受治疗。彭林说，目前采用局麻下个性化无张力修补术，根据患者疝的具体情况打“补丁”。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这些“补片”好比“鱼网”，能让腹壁薄弱的区域的组织变得结实。这种治疗方法简单、快速、并发症少，伤口大约为4-6厘米，病人痛苦小，能迅速恢复体力。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">最后要提醒的是，虽然采用麻下个性化无张力修补术复发率只有1%以下，但仍要积极治疗前列腺增生、慢性咳嗽、习惯性便秘等，以免复发。&nbsp;</span>
</p>
