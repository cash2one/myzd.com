			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“眩晕哥”单希征：眩晕根源不在头在耳朵</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">生活中经常碰到有人“头晕”、“头昏”或者“晕倒”，而大家对于“眩晕”一词也并不陌生，但不同于一般头晕只觉得昏昏沉沉、头重重的，眩晕的人常会感到天旋地转、站立不稳，甚至恶心呕吐，严重影响到正常生活。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 70岁的邢阿姨就有过类似的遭遇，那天起床后她突然感觉一阵头晕脑胀，眼前的家具、周围的墙壁都跟着旋转起来。一时间她晕得不敢睁眼，脚下一软差点栽到地上，紧接着恶心、呕吐的症状伴随而来，发作数分钟后稍有缓解，但是一变换位置天旋地转的感觉就又来了。家里人忙把邢阿姨送去医院，可是到了医院却犯了难，这到底该挂哪个科？有没有办法治好啊？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 其实类似邢阿姨这种情况并不少见。统计数据显示，全国每年有780万人会患上眩晕症，北京每年就新增10万多患者。其中有些患者发病后并未得到及时有效的治疗，往往求诊一圈，花了不少钱，走了不少弯路，病也没有治好。为此，记者请来武警总医院中美技术合作眩晕病研究所所长单希征，这位潜心研究眩晕病20多载，为无数受眩晕之苦的病人解除病痛，被大家亲切地称为“眩晕哥”的专家来谈一谈“眩晕究竟是怎么回事儿？”“究竟该如何对症治疗”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="http://static.mingyizhudao.com/147003658333237" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眩晕是一种症状，背后有诱因</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 很多前来求助的患者都存在类似的困惑：什么是眩晕症？眩晕是不是就是头晕得厉害？其实眩晕与头晕是有本质区别的，头晕是病人发病时感觉头昏脑胀，而眩晕发病时则有天旋地转的感觉，就好像是划着小舟行驶在大海中一样，感觉摇摆不稳、晃动、头重脚轻。症状严重时，患者两眼紧闭，双手紧握床沿，唯恐从床上摔下来，且伴有恶心呕吐、腹痛腹泻、面色苍白、出冷汗等。眩晕症状虽严重，但病人意识清醒。有些病人也会感到周围景物左右摆动，或上下浮动，这是内耳疾病所特有的症状。严格地说，头晕包括眩晕，而眩晕不能反过来说成是头晕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 其实，眩晕就像咳嗽、打喷嚏、头痛一样是一种症状，并不是疾病的名称。但有一点很重要，眩晕之所以发作，它背后一定是某种潜在疾病引起的。引起眩晕的疾病很多，在还不能明确其引发因素时，就把它统称为眩晕症。例如耳石移位、上呼吸道感染、颈部血液循环不良、内耳积水、肿瘤等，同时还存在心理、精神等诱发因素。英国的一项调查显示，年轻人眩晕常与内耳不平衡或是良性姿势性眩晕有关；老年人则以脑血管病变、颈椎病变及药物的副作用最为常见。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003658355632" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 单希征和他的团队自主研发的SRM-IV型前庭功能诊断治疗系统</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 眩晕大部分根源不在头部在耳朵</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 说起眩晕，大家通常认为病根在头部，单主任却表示并非如此，有近乎三分之二的眩晕症病根藏在耳朵里，这就是前庭周围性眩晕也叫耳源性眩晕。其病变在耳蜗前庭器官，起病突然，眩晕伴有明显的恶心呕吐，持续数分钟或数小时，可呈阵发性，有眼震症状。如：梅尼埃病，良性阵发性位置性眩晕，前庭神经元炎，伴有眩晕的突发性耳聋，变压性眩晕，前庭迷路震荡等。第二大类就是前庭中枢性眩晕也叫脑源性眩晕，全身某些病变累及前庭中枢引起的眩晕。起病缓慢，呈持续性，可持续数日数月。眩晕较轻，眼震方向无规律性或无眼震，体检中常有脑干损害体征。如脑供血障碍、小脑出血、脑膜炎、脑瘤、脑脓肿等。最后一类就是非前庭性眩晕，包括：眼源性眩晕，颈源性眩晕，血液和心血管系统性疾病引起的眩晕，消化和植物神经系统引起的眩晕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 耳内小沙粒也可“兴风作浪”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 单主任表示，在眩晕病中良性阵发性位置性眩晕占整个眩晕病的三分之一，发病率最高，俗称“耳石症”，这种病好发于老年人。由于我国仪器设备和整体诊断水平的限制，该病能够得到准确诊断和有效治疗的人只是少数，大多数患者经常在临床的多个科室之间辗转，得不到适当的诊断与治疗，当眩晕发作时，总是怀疑是不是心脏和脑血管出了什么问题？或是脑子里长了瘤子？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 其实这种眩晕诱发的病因在我们的耳朵里，在人体的内耳里，有一套专门掌管人体平衡功能和位置感觉功能的精密结构，如半规管和前庭部分、内淋巴、嵴帽、小耳砂等，如果这些结构中任何一处出现问题，就会引起平衡位置感觉异常，眩晕随之而来。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 良性阵发性位置性眩晕的发病原因是内耳“负责”平衡的耳石，因为某种因素脱落，游离的“耳石”进入半规管内，这是它不该去的地方，因而改变了半规管内流体力学特性，导致头部转动感觉异常。即当头运动到某一特定位置时出现短暂眩晕及眼震，伴恶心、呕吐。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 针对这类眩晕，只要做耳石复位就可以解决。耳石复位可以采用手法复位和仪器复位，目前很多医院采用的是医生的手法复位，经过单纯的变换体位，将掉进半规管的耳石复位至前庭后，眩晕症状即得到治愈。一次复位成功率达80%，两次复位者还有10%以上可以成功，因此治愈率可达90%以上。但是，手法复位与医生经验有关，如复位速度不易统一，复位角度不准确等，因此手法复位并不易普及。目前研究所采用的是国内先进的全自动化耳石复位仪，有效率可以达到95%以上，一般一到两次复位就能彻底解决。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 眩晕别大意 及时对症问诊</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前庭性眩晕有明显的外物或自身旋转，是由耳科和神经科疾病引起的，可以选择耳鼻喉科或神经内科。而非前庭性眩晕是由于全身系统性疾病引起的，有轻重不等的头晕症状，没有明确的旋转感，病人可选择内科就诊，医生会根据情况作相应的检查。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 不少人认为眩晕症是小事，熬一下子就过去了。孰不知眩晕症也可能引起生命危险，特别是神经科疾病引起的眩晕，因为这类眩晕可能是脑中风的先兆。尤其是中老年人多少都有颈椎病，加上动脉硬化，很容易引起脑干、小脑缺血，甚至梗塞，从而导致眩晕。因此，建议中老年人出现眩晕，首先选择看神经内科。在排除了中枢性病变后，可以考虑是前庭周围性眩晕，此时要及时到耳鼻喉科进行前庭功能检查。如果是耳源性的眩晕，是不会瘫痪，更不会有生命危险，并且很大一部分良性阵发性位置性眩晕的病人可以通过复位很快痊愈。如果怀疑颈椎病，还应到骨科进一步确诊。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 由于眩晕病的病因复杂多样，涉及到多个学科领域，如耳鼻喉科、神经内科、神经外科、眼科、骨科(颈椎病)、老年病科、血液科、中医科等，因此，患有眩晕的患者常常是今天看这个科，明天看那个科，形成了眩晕病“多科论治”现状，而治疗效果也不能令人满意。因此，武警总医院建立了国内第一家设备最全的眩晕病研究所，引进了美国先进的全套前庭功能检查及治疗设备，并配备了全自动化耳石复位仪，研究所还汇集包括耳鼻喉科、神经内科、中医科、骨科、心理科等多科眩晕病知名专家同时会诊，使眩晕病症的治疗更具针对性、系统性。</span>
</p>
