		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>胡小吾：关注帕金森患者衣食住行（资讯）</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">随着社会的日益老年化，帕金森病已越来越多地出现在我们身边。关爱帕金森病病人，了解帕金森病的基本常识是全社会刻不容缓的事情。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3103/2.png" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">上海长海医院神经外科胡小吾</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 指导医师：胡小吾，第二军医大学附属长海医院神经外科主任医师、教授，现任上海神经外科学会立体定向和功能神经外科学组副组长等职。擅长立体定向功能神经外科，在帕金森病、特发性震颤、扭转痉挛等疾病脑深部刺激术（脑起搏器）治疗和毁损术治疗方面具有较高知名度。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 如何发现帕金森病</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 要想及时发现帕金森病，平时要对老人多一份关爱，及时发现帕金森病的初期症状。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病初期症状不像瘫痪、疼痛、抽风那样容易引起病人和家属的感知。除了比较典型的的肢体不自主抖动、僵硬及动作缓慢外，也有许多病人起病很隐匿，如表现一侧肢体活动不便、乏力、酸胀，足部易痉挛、拖步、脚趾屈曲、腰背部紧张感、走路前冲、驼背、“未老先衰”、写字越写越小、油脂脸、嗅觉减退、夜里多梦、抑郁、焦虑、便秘等。因此，患者和亲属应当对这些细微表现应有所认识和警惕，如有表现要及时到医院就诊。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 正确对待帕金森病</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 如果得了帕金森病，就应该帮助病人正确认识帕金森病，认识得越清楚，恐惧感就会越少，也越能配合医生的指导，进行科学正规的治疗，延缓疾病的发展。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病病情进展并不快，也不直接危及生命，目前内科药物和外科手术治疗的已取得较大进展，有十几年病史的病人还能生活自理、运动自如的病人比比皆是。因此我们要多关心帕金森病病人，树立起战胜疾病的信心，使他融入社会、融入家庭，尽可能的享受生活乐趣，提高生活质量。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 关爱帕金森病病人衣食住行</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 饮食均衡对帕金森病病人非常重要，平时多喝水，多吃水果蔬菜，限量吃肉，经常吃豆制品。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 中晚期帕金森病人随着疾病加重，慢慢丧失了自我照顾能力，生活上需要家人更多的协助和支持。家人应尽可能在居家设计上照顾帕金森病人：比如卫生间适当的地方装一些扶手，让病人可以自己上厕所。准备一把平直靠背的带扶手的椅子专门给帕金森病人，坐下去以后再站立困难的病人需要硬的支撑和扶手的帮助。由于帕金森病人有平衡障碍容易摔倒，家里的木地板最好能平整一点，或者铺地毯。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 得了帕金森病，人会不爱动，也忽视运动。家人应该监督病人多进行日常锻炼，散步、体操、朗读、唱歌、游泳、太极拳等活动能让各部位的肌肉动起来，对病人都有好处。对于卧床病人，家人可以帮他做一些被动运动，活动关节。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">合理进行药物治疗</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病病人往往有记忆力下降、容易遗忘等症状，家人要及时提醒其按规定时间吃药。吃饭和吃药要有时间差，服药半小时后进餐是最好的，因为食物中有些蛋白质会影响药效。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病不是吃几个疗程药后症状缓解就可停服，而是一旦吃上，需要终生吃药。然而，药物治疗最有效的时间一般只有4～6年，随着吃药时间延长，疗效越来越低，剂量越来越大。长期吃药后会出现肢体不自主舞蹈样动作和药效作用来去无踪的运动副作用。因此，过早或过晚服用药物都是错误的，如果症状轻微，可以暂时不吃药，尽量推迟吃药时期，但如果症状明显，就应该及时吃药，以控制症状。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 有的病人一开始吃药就选择效果最明显的多巴胺制剂。但殊不知，该类药物效果明显，但可能让副作用提早出现。因此，对年龄较轻的早期病人，应选择多巴胺激动剂，该类药物虽然效果欠佳，副作用明显，但可推迟药物副作用出现；对年龄较大的早期病人，可应用多巴类药物。随着病情发展，需要的药物剂量越来越大，因此吃药需要做到“细水长流，不求全效”，即剂量尽可能小，长期规律地服用，能达到80%左右效果即可。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 及时选择外科治疗</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 到了中晚期，吃药效果不理想，即使药量加大，药物疗效也不满意，症状影响到工作和生活，或药物副作用较大，应考虑用外科治疗。外科治疗通常在发病5年后进行，对以震颤为主要症状的病人因为震颤对外科治疗反应比较好，在发病3年后也可以进行手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 脑起搏器，即脑深部电刺激（DBS），是目前帕金森病最佳手术治疗方法。它是通过立体定向技术在患者脑内异常兴奋的神经核团植入一根直径为1.27毫米的电极，脉冲发生器埋藏在胸部皮下，电极和脉冲发生器通过皮下延伸导线相连，脉冲发生器发出的电刺激通过脑内电极作用于异常兴奋的神经核团，调控运动相关的神经核团兴奋性，使大脑的神经功能重新达到平衡，从而控制帕金森病症状，使患者恢复原有活动能力。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 脑起搏器植入是微创手术，具有手术创伤小，并发症少，术后恢复快，效果持久、显著等优点，可以有效地控制帕金森病患者的震颤、僵直、运动迟缓等病状，还能减少抗帕金森病药物的剂量，解决了药物治疗过程中出现的运动并发症，大大提高了患者的生活质量。由于不破坏组织，为患者保留了接受其他新的治疗的机会。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 最后要强调的是：多关爱帕金森病患者心灵，在治疗上多听医生的建议，该病治疗多因人而异，因病程而异，要到正规的医疗机构接受正规的治疗。</span>
