			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>强直性脊柱炎</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疾病名称： 强直性脊柱炎（骨科）&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">别　　名： 血清阴性脊柱关节病&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">简　　介： 强直性脊柱炎为慢性进行性脊椎关节病，主要侵犯中轴骨，其特点表现为脊椎的韧带附着处发生骨化，最终导致脊椎的强直、僵硬和畸形，过去混同在类风湿关节范围内，自70年代检出血清组织相溶性抗原HLA-B27后，强直性脊柱类刁脱离类风湿性关节炎的范畴，成为一种独立疾病，其患者类风湿因子95%为阴性，故又名血清阴性脊柱关节病，发病在15~30岁，男多于女，男：女=14：1。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">发病机理： 病因不明，可能与遗传、感染、损伤有关。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床症状： 起病缓慢，早期可表现为臀部或膝髋部关节慢性非对称性不适疼痛，关节发硬，早期疼痛可沿坐骨神经分布放散，服用止痛药可缓解，绝大多数患者先累及骶髂关节，约半年到1年后，出现腰椎疼痛，逐渐感弯腰困难，行走不灵便且易疲劳，病变继续向胸部及颈部发展，胸廓活动受限，严重者只能靠腹式呼吸，最终出现强直性驼背，不能立直平视，回头时需整个身躯连轴转动，此时疼痛消失，是成严重残疾，生活不能自理，卧床不起。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊　　断： 早期诊断困难，下腰部、臀部疼痛，脊柱活动受限为主要特征，继而胸椎后凸，胸部扩展缩小≤2.5cm。X线检查有重要意义，双侧骶髂关节经历增宽→侵蚀→硬化及强直改变，脊柱呈竹节样骨化改变，实验室检查患者多有轻度贫血，它沉快，类风湿因子阴性，HLA-B27阳性。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">辅助检查： X线检查，早期病变累及骶髂关节，关节轮廓模糊，关节软骨下有毛玻璃样骨质密度增加带，接着出现关节骨折增生，关节间隙狭窄、消失，发生骨性强直，脊柱早期有骨顶疏松，然后有小关节腔隙模糊、变窄、韧带钙化，椎间盘纤维骨化，椎体间骨桥形成，广泛韧带钙化，脊柱呈竹节样脊柱，实验室检查有轻度贫血，白细胞轻度升高，90%患者红细胞沿降率增高，类风湿因子95%阴性，90%患者HLA-B27阳性。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治　　疗： 1、药物治疗：阿司匹林治疗无效，可用保泰松、消炎痛、芬必得等，使用柳氮磺腰吡啶司控制症状，并逆转关节损害，氨甲喋呤5mg每周1次，雷公藤等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、封闭治疗用1%利多卡因5ml加强的松龙25mg，疼点封闭，5天一次。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、针刺、按摩、理疗神经阻滞疗法：常采用硬膜外疗法等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、手术治疗：一般不需手术，当髋关节功能障碍及脊柱强直影响运动时，行髋关节人工置换术及脊柱截骨术。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">预　　防： 因病因不明，不易预防，一旦发病应尽早治疗，减慢病程发展。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">相关药物： 阿司匹林 、吲哚美辛 、萘普生 、保泰松&nbsp;</span><br />
