	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>射频消融治疗肝癌</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">射频肿瘤治疗系统的基本原理是通过射频波/交变电流波，引起组织内极性分子的震动摩擦，产生热能。使细胞内温度提高，并逐渐向外周传导，当温度超过60ºC时, 肿瘤细胞迅速发生蛋白变性并凝固坏死，达到治疗肿瘤的目的。射频肿瘤治疗系统是现代高科技向医学领域渗透的新技术，该治疗技术具有创伤小，疗效确切，恢复快，住院时间短，可借助影像技术精确观察治疗效果等优势，同时也是目前国内外肿瘤治疗的热点，是肿瘤综合治疗中的实用、有效手段。射频消融治疗与手术相比，在治疗小肝癌方面，1、2、3年生存率和复发率没有显著差异，而在安全性、并发症和生存质量方面，射频消融疗效更佳，特别是对于肿瘤小于3cm的Ⅰa期患者，射频治疗可以替代传统的手术切除。对于那些不能手术切除、或因年龄大身体虚弱等各种原因不愿手术肿瘤患者，不愿承受放化疗副作用或放化疗及介入治疗等治疗效果不好肿瘤患者，复发、转移的中晚期肿瘤患者，射频消融也是首选的治疗手段。射频治疗的途径包括经皮、术中和腹腔镜下三种方式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我院腹部外科肝脏病区现开展了新技术项目“肝癌术中射频消融治疗”，该项目已经顺利通过北京市医学会立项鉴定和审批。该项目具有以下特点：术中射频治疗选择小切口进腹探查，在术中超声导引和监测下直视操作，不仅拓宽了肝脏肿瘤射频治疗范围，更重要的是使治疗更加确切，避免了射频治疗带来的周围组织脏器的误消融、出血、针道转移等严重并发症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">射频消融治疗适应了新世纪疾病治疗微创化的趋势，使肝癌的治疗方法增多范围增宽，它能使我院大约15%-20%肝癌和肝转移癌患者受益。它打击小，疗效确凿，病人损伤小恢复快，给肝癌病人的治疗及康复带来了新的希望，同时也填补了肝癌治疗一项空白。</span>
