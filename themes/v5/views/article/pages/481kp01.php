		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>刘厚宝答：胆囊肿瘤性息肉与饮酒没有明确的相关性</strong></span>
</p>
<p>
	<br />
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">胆囊肿瘤性息肉与饮酒没有明确的相关性，但饮酒时高脂饮食摄入过多和过量饮酒造成的肝脏损害可能与胆囊胆固醇性息肉发生有一定关系。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">胆囊息肉，又称“胆囊隆起性病变”，是对胆囊腔内局限性隆起的一类性病变的统称，它包括了多种胆囊良性的或早期的恶性病变，如胆囊良性肿瘤、假性肿瘤和早期胆囊癌等。其发病原因尚不明确。胆囊息肉在病理上有非肿瘤性息肉和肿瘤性息肉之分。非肿瘤性息肉包括胆固醇息肉、炎性息肉、胆囊腺肌增生症等；而肿瘤性息肉包括腺瘤、平滑肌瘤、脂肪瘤等。腺瘤和腺肌瘤有恶变倾向，是胆囊癌的癌前病变，常称之为胆囊癌相关性息肉，而胆固醇性息肉和炎性息肉则为非胆囊癌相关性息肉。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">大部分胆囊息肉患者没有特异性临床症状，往往在体检时被发现。其中以胆固醇性息肉最为常见，约占90%以上，常为多发，直径多小于1cm；而腺瘤样息肉多为单发，直径大于1cm时易恶变。超声是首选的检查方法，它能清晰地显示胆囊息肉的部位、大小、数目以及局部胆囊壁的变化，是一种简便、无创的诊断方法。彩超和超声造影能探查息肉有无血流，对鉴别胆固醇性息肉和肿瘤性息肉具有较高价值。而CT、MRI对微小隆起样病变意义不大。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">诊治的关键是如何从众多的胆囊息肉样病变中鉴别出胆囊的“肿瘤性息肉”，并识别出癌前病变或早期胆囊癌。临床上要从两方面把关：一是严格掌握手术指征，既不能因担心胆囊息肉有癌变可能，而盲目扩大手术指征，使很多胆囊功能正常的非肿瘤性息肉病人切除了胆囊，给病人带来不必要的损失。又要防止肿瘤性息肉发生癌变而错失手术良机。胆囊息肉样病变的手术指征为：①单发，直径1cm以上者；②年龄50岁以上，广基而单发的病变；③病变在短期内基底变宽,有增大趋势或病灶周围的黏膜有浸润、增厚的表现；④合并胆囊疾病，如胆囊结石、急性或慢性胆囊炎，有明显临床症状者； ⑤息肉较大、长蒂或胆囊颈部息肉，影响胆囊排空，有胆绞痛发作史者；⑥合并胆囊壁不规则增厚者。对于暂无手术指征的，因其仍有潜在恶变的可能，应进行定期随访观察，如发现病变发生变化，有手术指征时，应及时手术治疗。二是术中正确处理：凡因胆囊息肉样病变而施行手术者，胆囊切下后应立即剖开检查，如病变像肿瘤者，均应送冰冻切片检查，不但要确定有无癌变，还要确定癌变的部位是腺瘤顶端还是基底部，以及是否发展为浸润性癌。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">总之，胆囊息肉虽然不是什么大病，但也不可对其掉以轻心。如果发现患有胆囊息肉，应到医院相关专科进行诊治。</span>
</p>
