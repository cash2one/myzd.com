			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>难治性癫痫难治的关键在于</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">简单地说是采用正规的药物治疗，通过多种努力仍不能控制其发作的癫痫。目前我国癫痫病的患病率为7‰，约70%的癫痫病人能通过服药达到治愈目的，而30%的顽固性癫痫病（即难治性癫痫病），虽长期服用抗癫痫药物仍不能阻止病症发作，这部分病人及家庭常年生活在痛苦中。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1、特殊病因引起的症状性癫痫，如发育异常、脑肿瘤、脑外伤、脑血管病、各种代谢性疾病、缺氧、感染、寄生虫。这是因为颅内异常病灶的持续存在，导致癫痫灶异常放电不能用药物消除，药物无法控制癫痫而成为难治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2、难治性癫痫综合症，如婴儿痉挛症、颞叶内侧癫痫综合症等，这与病人的遗传和代谢有相当大的关系。或严重的癫痫导致颅内某些结构如海马、杏仁核等的异常而导致癫痫难治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3、由于医治不当或人为因素造成，主要有三方面，一是医师指导不力、失误或患者不能够积极配合，导致一些患者发作了就吃药，不发作了就停药，吃吃停停，不正规系统的治疗，使本来有效的药物也变成无效，最后造成难治性癫痫；二是服用药物成份不明的药，没有国家批准文号的药，看象中药或中成药，里面掺杂有多种西药，不按用药规范办事，成份交叉，重复用药，给以后的药物调整带来困难，造成难治性癫痫；三是听信偏方、秘方及江湖游医的误导，延误了有效的治疗，造成癫痫反复发作，对脑的伤害不断加重，产生了精神和智能的障碍，形成难治性癫痫。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 以上原因导致药物不能消除癫痫病灶的异常放电、或不能阻断异常放电的传播途径以及不能降低大脑皮层的兴奋性而使癫痫成为难治性癫痫。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 随着医疗条件的改善和医学技术的进步，越来越多的以前的难治性癫痫现在变的不再难治，有一大部分的以往人们认为的难治性癫痫，现在可以通过手术等方法治疗，术后大部分效果也都是非常理想的。</span><br />
