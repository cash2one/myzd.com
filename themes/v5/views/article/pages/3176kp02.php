<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>消化内镜治疗常见问题解答</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我在网上和门诊经常遇到患者带着内镜报告询问各种内镜下发现的病变是否需要内镜下或外科手术，这是一个患者最关心的问题，也是一个具有一定共性的问题。我在我的空间里将陆续针对一些问题用通俗易懂的语言讲解，希望患者可以及时理解自己的病情，并选择恰当的治疗方法。具体问题可能无法解释透彻，希望具体问题必要时当面咨询专科医生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">问题1、发现消化道粘膜下病变怎么办？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">消化道管壁壁大致上可以分为粘膜层、固有层、粘膜肌层、粘膜下层、固有肌层。食管粘膜下病变是指固有层以下的病变，多数是在无症状情况下内镜检查时偶然发现。有些患者的不适主诉也与所发现的病变无关。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">食管粘膜下病变多数是粘膜肌起源的平滑肌瘤，其次是固有肌起源的平滑肌瘤，少见的包括囊肿、血管瘤、脂肪瘤、神经鞘瘤等等。原发恶性的粘膜下病变非常少见。胃粘膜下病变种类比食管多，包括平滑肌瘤、间质瘤、异位胰腺、脂肪瘤、囊肿、血管球瘤、深囊性胃炎，…….等等。肠道的粘膜下病变种类比较少，主要包括脂肪瘤、囊肿、血管瘤、间质瘤等等。消化道粘膜下病变大小不等，多数是从几毫米到几厘米，极少数可以超过5厘米甚至达到十几厘米。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">良性粘膜下病变内镜下的表现是光滑隆起，根据内镜下表现可以初步估计病变的起源，但是确诊要依靠超声胃镜来明确其位于管壁的那一层。并根据回声的强弱和是否均匀为判断病变性质提供参考信息。如果决定行内镜下治疗，对于一些病变还需要行增强CT检查。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">粘膜下病变可以通过内镜切除。起源于粘膜下层以上的病变可以直接圈套切除或粘膜下剥离切除。位于粘膜下层的病变需要粘膜下剥离切除。食管贲门固有肌起源的病变需要粘膜下隧道内切除，胃固有肌起源的病变可以原位挖出或胃壁全层切除。肠道固有肌起源的病变首先考虑外科治疗，尽管个别病变有尝试内镜治疗的成功病例，但总体上不建议内镜治疗。 </span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">对于每个病变是否需要切除需要从多方面分析评估，主要考虑病变大小和起源、初步诊断、患者的年龄和身体状况、患者了解病情后的心态。目前还没有粘膜下病变内镜治疗的共识意见，基本的倾向是对于可以肯定良性诊断的较小病变可以随诊观察，除非患者强烈要求治疗。对于恶性可能性很低的较小病变，如果是老年患者不建议内镜下治疗。对于中青年患者的粘膜下病变治疗指征可以放宽，但是对于不存在恶变倾向的较小病变还是建议随诊观察。对于有潜在恶性倾向的、没有良性诊断依据的，患者治疗要求强烈的，可以考虑内镜下治疗，但是针对每个粘膜下病变的治疗都需要充分评估、权衡利弊。对于考虑恶性可能的粘膜下病变首选外科手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">问题2、贲门失迟缓怎么治疗</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">贲门失迟缓是一种由于贲门括约肌反常收缩导致的食物通过不畅或不能通过。随着生活节奏的加快和精神压力的增大，发病率有增加的趋势。虽然是个良性疾病，但是严重影响生活质量，一旦确诊需要尽快治疗。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">传统的内镜治疗方法主要包括大球囊扩张、肉毒素注射。扩张治疗多数有效，但是效果依赖扩张的直径，如果扩张直径过小则起不到撕裂内环肌的效果，但是扩张直径增大的同时穿孔的风险也会增加。注射治疗安全，也可以起到缓解症状的效果，但是疗效及维持时间差别较大。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">近几年发展起来的经口内镜下食管内环肌切开（POEM）治疗贲门失迟缓已经证实疗效肯定，复发率低，并发症可控。越来越多的患者接受了这项治疗。在接受POEM治疗前需要明确诊断，排除其他疾病引起的进食不畅。检查项目包括胃镜、钡餐造影、食管测压，以及其他手术必备的检查项目。手术是在气管插管全麻下进行。影响疗效的因素有内环肌切开的长度，下端是否超过了下食管括约肌；内环肌切开的深度，是否做到了内环肌全部切开。此外动力检查诊断的不同类型的患者术后在疗效上会有差别。并发症主要包括皮下气肿、纵隔气肿和气胸，但是封闭了粘膜切口后这些并发症都很好处理。目前对于熟练的术者已经很少出现这些并发症。少见的并发症包括食管纵隔瘘，隧道内感染，术后出血等等，但是只要确实保护好粘膜层的完整，隧道入口封闭可靠，隧道内血管凝固充分，这些并发症几乎不会出现。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三种治疗方法比较优缺点很明显，患者在术前要充分了解，并在医生分析帮助下选择。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">问题3、发现可疑癌变的粘膜病变怎么办？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我国是消化道癌的高发国家，与发达国家相比，主要问题是早期发现的癌前病变和早癌比较少，进展期的癌所占比例较高。癌症的治愈与发现的早晚密切相关，越早发现越早治疗效果越好，早发现早治疗的癌症是完全可以治愈的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">消化道早癌的发现依赖于内镜检查。需要有经验的医生应用高清晰内镜才能更多发现早期粘膜病变，并借助于染色放大观察技术、超声内镜技术来评估病变，通过精确活检确定病变性质。对不同性质、不同大小、不同深度病变采取不同的治疗方法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一旦发现了消化道粘膜高级别瘤变或分化良好的粘膜内癌，尽量咨询有消化内镜治疗经验的医生，探讨是否有内镜下微创切除的可能，不要在恐癌心理作用下首先接受外科手术治疗。有经验的内镜医生对粘膜病变的识别能力好，不但能发现病变，而且能识别病变，活检的准确度高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">粘膜病变的内镜治疗方法主要有粘膜大块切除术（EMR）、粘膜下剥离术(ESD)，二种治疗方法的根本区别在于EMR对于面积较大的病变只能分块切除，术后残留复发比率相对高些，并且术后病理诊断信息有限。ESD可以完整切除病变粘膜，获得准确的术后病理诊断，指导我们采取正确的后续治疗方法。对于适合内镜下治疗的粘膜病变术前会通过染色、放大等手段精确评估，在保证完整切除的前提下提高治愈性切除的比率，只有这样患者才能真正获益。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">需要明确的使内镜下粘膜切除是一种治疗方法也是一种诊断方法，对于适应症范围内的病变可以治愈，对于术后病理诊断为超出适应症范围的病变需要进一步外科手术或补充放化疗。</span>
</p>
