<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">12月17日上午，市卫计委科教处张勘处长率市卫计委及市医师协会专家组成的督导小组，来华山医院进行专科医师规范化培训督导，第一批获批的14个专科基地主任及秘书在花园大厅接受了督导。督导结果将作为培训基地资格复评审和增设专科医师规范化培训培训基地的主要依据之一。</span><br />
