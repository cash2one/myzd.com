<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>开颅术进行中唤醒患者</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">打开患者头盖骨，准备完整切除脑肿瘤时，医生将处于麻醉状态的患者唤醒，一边谈心聊天、一边指挥患者进行肢体活动……武汉大学中南医院日前为一名脑瘤患者实施了“术中唤醒”手术，该患者术后恢复良好，并于去年年末康复出院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">去年12月初，26岁的马先生因反复头痛到医院就诊，经检查发现其大脑左侧有一个3cm×4cm的肿瘤，若不尽快手术，很容易引起失语、瘫痪等严重后果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由于肿瘤位于大脑功能区，手术过程中稍有偏差，就会造成难以恢复的语言障碍和偏瘫。医院经过会诊，决定实施“术中唤醒”术，即在手术过程中，通过控制麻醉的剂量将马先生从麻醉状态下唤醒，通过对话交流判断手术切割区域，一边切除肿瘤一边对马先生肢体活动和语言进行检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“手术前一天医生与我进行沟通，并训练我的配合动作。”马先生表示，由于心里准备充分，手术中被唤醒后并没有感到疼痛，也没有紧张恐惧，不但顺利完成了医生的指示动作，还与医生轻松讲述了自己生活中的趣事。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术总共持续2个多小时，在进行到第30分钟时，主刀医生成功将马先生唤醒，一边聊天一边让马先生活动手脚，以此判断切除肿瘤的范围和程度，使患者的语言、肢体运动功能不受影响。马先生术后完全清醒时，他立即活动了一下四肢，看到一切正常，他知道手术成功了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“脑部功能区肿瘤都可以通过‘术中唤醒术’切除，既切除肿瘤，又能保留患者神经功能，而且该手术对患者年龄和病史没有特别要求。”医院神经外科教授陈劲草介绍。</span>
</p>
