		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>韩东一：规范化是人工耳蜗手术发展关键</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">由澳科利耳医疗器械（北京）有限公司主办的第五届科利耳植入式听力解决方案新进展峰会在北京隆重举行，包括中华医学会耳鼻咽喉头颈外科分会后任主任委员、中国人民解放军耳鼻喉科研究所所长韩东一教授、北京协和医院人工耳蜗中心主任曹克利教授、解放军总医院耳鼻咽喉科主任杨仕明教授、墨尔本大学维多利亚皇家眼、耳科医院Robert Briggs教授等国内外知名医疗专家、学者共同参与本次盛会。会上，专家们就植入式听力解决方案的手术经验、临床技术新进展、人工耳蜗适应症的选择、预防和治疗手术引起的感染等多方面进行分享及交流。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 植入式听力解决方案主要指人工耳蜗植入手术。人工耳蜗被认为是目前最成功的人造器官，也是临床应用最为成功的电子生物科技产品。外部声音处理器捕获声音，将其转换为数字信号，这些信号通过磁感线圈发送到内部植入体，植入体将信号转换为电脉冲，然后沿着耳蜗内的电极序列进行电刺激，通过电流直接刺激听神经并传递到大脑，从而可以听到声音，通过这种方式可以帮助重度、极重度听障患者重返有声世界。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 目前人工耳蜗植入手术在中国地区的临床应用已有数十年成功经验，中国地区仅植入澳科利耳人工耳蜗的听障患者就超过10000人。然而伴随手术例数的增长，手术操作中缺乏规范的问题开始逐渐显现。在本次会上，中国人民解放军耳鼻喉科研究所副所长韩东一教授表示：“促进中国人工耳蜗植入技术与国际技术的接轨，适应症选择、实现操作流程、术后康复护理等的规范化将是中国人工耳蜗手术发展的关键，也将成为建立有强大竞争力的人工耳蜗中心的关键。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 另一方面，与会的众多专家就国际领先的人工耳蜗手术技术及其发展趋势进行了广泛交流和深入探讨，其中曹克利教授与会者分享了电极设计及未来发展趋势的专题报告；澳科利耳公司邀请的美国弗吉尼亚州的美林布洛德尔听力研究中心的主任Jay Rubinstein教授介绍了国际最新的前庭植入技术、还有澳科利耳公司总部的技术和临床专家分别就TICI-全埋式人工耳蜗技术的更新及澳科利耳电极设计的发展等方面进行介绍。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 临床研究表明，澳科利耳的人工耳蜗产品是目前最可靠的产品，作为最早进入中国市场的人工耳蜗制造企业，澳科利耳占据了超过70%人工耳蜗市场份额。另外会上获悉，澳科利耳已经成为2012-2013年度“中国残联贫困聋儿人工耳蜗助听器抢救性康复项目”指定供应商，该项目将使中国地区的数千贫困聋儿通过澳科利耳人工耳蜗重返有声世界。</span>
</p>
