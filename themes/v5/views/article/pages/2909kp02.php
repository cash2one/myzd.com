			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胆囊结石患者的顾虑</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆囊结石患者大都有这样的顾虑——切除胆囊后，消化会不会受影响？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先，需要切除的是病态的胆囊,而不是正常胆囊. 某些病人的胆囊功能已经严重受损或则丧失. 胆囊虽然是个消化器官，起到储存以及排出胆汁的作用（胆汁是由肝脏分泌的）。具体来说，由肝脏分泌的胆汁会预先储存在胆囊中，进食后，胆囊收缩将胆汁排入肠道。其次，如果切除了胆囊，没有储存胆汁的地方，进食后胆汁仍然能够排入肠道，在术后头两个月，消化高脂食物时可能稍有影响，但胆管会慢慢地代偿性扩张，某些病人会出现腹泻,这种不良反应会逐渐减轻、消失。所以，胆囊并没有大家想象的那么不可或缺，在术后2~3个月的时间内，只要避免高脂饮食，消化是不会受到太大影响的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果患者做了胆囊切除术需要调整生活方式，控制体重和饮食——尽量少吃含脂肪多的食物，多吃蔬菜、水果等清淡的食物；辛辣或者口味重一点的食物对胆道没什么影响，但会刺激胃，从这个角度讲，患者也应该少吃。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，饮酒也对肝脏有比较大的影响，进而会影响人的胆道系统。建议不论是不是胆囊结石患者，不论术前术后，都应该远离烟酒。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果手术顺利，患者恢复得好，术后一周左右就可以上班工作。如果患者身体素质较差，恢复时间可能较长，这就因人而异了。关于术后能否运动，胆囊结石患者术后的运动应该循序渐进，可以先做普通的运动，例如散步等，再逐步过度到正常的运动，如果患者平时喜欢做一些激烈的运动，在术后得停一停。</span><br />
