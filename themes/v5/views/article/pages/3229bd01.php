			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>陶凉：以病人为中心提供最优治疗术式</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">亚心年会的会议规模虽然越来越大，但是我们还是注重很多细节：第一，确保论坛设置上能够预留充分时间开展学术交流；第二，要结合临床，以非常实际和实用的学术内容为主。本次邀约的无论国外专家还是国内专家，他们都有非常丰富的临床经验，我们在学术设置上，会结合专家自身的擅长点，给他们一个发言题目方向，从疾病的认识、治疗等方面给国内各位参会者一个更高、更专业化的交流平台。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同时，为让每位参会者都有一些收获，也会做一些动物实验来介绍新的术式。让参会医生在回到医院进入临床手术之前有操作动手的体会。因为我们现在国内的学术会非常地多，题目也非常多，但交流的机会和实际操作的机会并不多，希望亚心年会给各位参会者有一个实质性的收获。这也是我们办会的宗旨。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着现代医学的飞速发展，生物—心理医学模式的推广，以患者为中心的服务理念的逐步深入，已经使心脏外科和心脏内科的界限逐渐模糊，为一位病人的治疗，心内科、心外科同时进行的“杂交手术（Hybird）”在世界范围内迅速流行。请您介绍下亚心医院在杂交手术方面的开展情况？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陶凉教授：“杂交手术（Hybird）”是国际趋势，其实这背后体现的是对患者更个性化的指导——现在越来越以病人为中心，病人能够得到什么最好的治疗方法最为重要。有关专业分化越来越细，每个大夫只能从事一个专业的深入研究，当然某一类疾病深入研究会推动疾病治疗的发展。但是这类专家往往只能看到病变而看不到病人。所以真正以病人为中心，而不是说以某个疾病治疗为中心，从疾病治疗转化到对病人的治疗，其实是理念上的改变。医疗服务本质也针对的是病人而不是某一类疾病。同一个疾病，可能有几种甚至十几种治疗办法，但是哪一种更适合一个病人，就需要统一协调。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原来心内科心外科界线很明显，现在由于治疗方法越来越多，而且内科和外科又有所谓的介入治疗，包括介入与内科，内科与介入之间，界线也越来越模糊，这样就需要针对病人的治疗来统一协调。我们也开展了很多的所谓的杂交手术，就通过内外科的联合治疗，创伤最小，惠及更多病人。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所谓的Hybird手术，现在在心脏主要是三大方面：</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个就是先心病方面，包括原有紫绀的病人、一些做手术风险很大或者操作很复杂的病人，现在通过联合治疗，如介入封堵，介入侧枝封堵等复合手术，把手术简化，把病人的创伤变小；</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另一个就是所谓的瓣膜手术和搭桥，这属于成人更多一些。成人类疾病主要是血管硬化的问题，搭桥放支架或是搭桥和PCI同时完成。目前，外科越来越倾向于微创化，内科越来越倾向于追求有效，如介入支架进步也很快，效果也会越来越好。在瓣膜方面，传统的换瓣手术尤其对重病人来讲，虽然换瓣手术是有获益，但是手术对于患者身体伤害也比较大。现在还有各种介入的办法来做瓣膜置换减少对病人的创伤，但是介入瓣膜不可能有外科做的瓣膜修复这样符合生理，所以要根据病人的实际情况、病人的身体条件来决定选择一个更适合的治疗方式。再有，有一些疾病治疗需要外科术后再封堵，或者介入以后又外科手术，更体现了这两种手术的互补。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第三类就是大血管手术，很多都倾向于内外科合作，先归到外科来解决一部分问题，剩下的问题由介入解决。这样的话使病人创伤更小，存活率更高，因为大血管风险很大，以前单做外科，死亡率很高，现在复合手术以后相对来讲死亡率并发症都明显地降低。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总结来说主要是这三大类，也有术前内外科合作，也有术后联合，这些都体现了“以病人为中心”的理念。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据了解，您独创的主动脉瓣单、双、三瓣叶置换的成形技术及某些复杂先心创新手术术种，手术量和效果得到国内外专家的广泛赞誉。请您介绍一下，这些技术与传统的手术方式以及手术效果上有和不同？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陶凉教授：本身成型手术就是尽量地保留病人自己更多的组织，这样的话，远期效果和心脏功能会好一些。本着这个理念，我们在实际临床中，对于能够修复的瓣膜尽量不去置换，病人受益，花费也少，因为修复瓣膜手术难度主要是在医生——技术难度高、责任更大，但是大部分的病人是有更好地结果和获益，所以我们一直在努力地尝试和实践这方面的工作。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在国内乃至国际上，我们主动脉瓣成形术都是领先的。我们跟国外专家有很多的交流，其中特点之一就是要从本质上说，能够伴随我们一辈子的是父母给我们的瓣膜，置换人工的瓣膜都有寿命周期，会衰败的。所以瓣膜成形术理念是没问题的，具体方式上我们只是把病人坏的不能够再用的地方用一些材料来修补，尽可能地保留病人自己的组织，希望有一个更好的效果，这是我们的初衷。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">外科手术毕竟有一定难度，上面我说的这种所谓的责任挑战，可能在很多医院的医生不愿意开展这方面工作，但是在亚心医院，我们还是在努力尝试，我觉得这会是未来的方向——因为大原则(建立以病人为中心，站在患者角度考虑治疗方式)没有错，只是说我们要克服一些技术上、材料上的难关，通过这个瓶颈后，我想这肯定会引领未来心脏外科的发展方向。</span>
</p>
