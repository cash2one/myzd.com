	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>疝与腹壁外科疾病</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疾病介绍</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疝是一种古老而常见的疾病，俗称“小肠气”或“疝气”，不论男女老少均有可能发生。绝大多数疝发生在腹壁，是由于腹壁组织薄弱，致使腹腔内脏器（如小肠等）从薄弱处突出而在腹壁上形成一个肿块。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床表现</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腹股沟疝是最常见的疝，突出的包块发生在腹股沟处，有时可进入阴囊；脐疝的突出包块发生在脐周；切口疝患者常有手术史，突出的包块常在原手术切口部位；造口旁疝的突出包块在肠造口旁，常合并造口向外隆起，导致造口袋难以粘贴。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊断</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 根据病史，经专科医生仔细体检，结合超声检查可以明确诊断。对于一些复杂的切口疝、造口旁疝，还需要进一步结合CT检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 手术是治疗疝的唯一可靠方法，俗话说“小洞不补，大洞难补”，出现疝以后应尽早手术，避免发生疝嵌顿与绞窄导致肠穿孔、腹膜炎等严重并发症。手术治疗又分为组织对组织有张力缝合修补术和网片无张力修补术，目前网片无张力疝修补术是各种疝修补的金标准术式，包括开放术式和腹腔镜术式。</span><br />
