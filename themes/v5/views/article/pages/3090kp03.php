			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>婴幼儿外耳湿疹的治疗&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">外耳湿疹是指发生在耳廓，外耳道及其周围皮肤的多形性皮疹。婴幼儿多见。本病主要特征为瘙痒，伴有渗出。患儿家长多因孩子烦躁不安，不能入睡就诊。也有因孩子耳朵流有味儿的分泌物来看病的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那外耳湿疹怎样治疗呢?首先我们应先对湿疹的病因进行一定的了解。儿童湿疹多与过敏反应有关，牛奶及鱼、虾等是可能的过敏原。因此找到过敏原是治本的方法。但在实际治疗过程中找到明确的过敏原是不容易的。对儿童外耳湿疹病因不明的治疗首先注意调整饮食，对哺乳期婴儿我们鼓励母乳喂养，并在适当时候断奶。不要随意更改奶制品，减少其对胃肠道的刺激;对大一点的儿童应避免进食容易引起过敏的食品，如鱼、虾、蛋白等。避免搔抓，切忌用热水，肥皂及酒精等清洗，禁用刺激性药物。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">局部治疗依“湿以湿治，干以干治”的原则。1）对比较干燥无渗出者，可用10%氧化锌膏，抗生素可的松软膏等外涂，保护创面，以便结痂脱落愈合；干痂较多时，先用3%双氧水清洗后再用药。2）渗出较少者，先涂擦2%龙胆紫液，干燥后涂氧化锌糊剂。3）渗出液较多者，用3%双氧水或炉甘石洗剂清洗渗出液及痂皮，再用3%硼酸溶液湿敷，待渗出液减少后，再用上述药物治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全身治疗1）继发感染时，全身和局部应用抗生素。2）服用抗过敏药物，如扑尔敏或克敏能等。3）渗出特别多时，可静脉注射10%葡萄糖酸钙，补充维生素C。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿童外耳湿疹作为一种常见病，应及时去医院就诊，在医生指导下注意饮食，局部予以适当处理，是很容易治好的。</span>
</p>
