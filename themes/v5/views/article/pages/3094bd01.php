<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>民营医院首获国家自然基金项目资助</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近日，国家自然科学基金委员会公布了2011年度国家自然基金资助项目。北京三博脑科医院的栾国明教授、于春江教授分别获得两项国家自然基金项目资助。据国家自然科学基金委员会工作人员介绍，这是自医改新政《关于进一步鼓励和引导社会资本举办医疗机构的意见》出台后，这些科研人员所在的民营医院首次成为国家自然基金科研项目的执行单位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 此次北京三博脑科医院通过评审的两项国家自然基金资助项目分别是栾国明教授的“局灶性皮质发育不良所致药物难治性癫痫中甘丙肽的作用机制”和于春江教授的“miRNA调控NKG2D途径介导免疫逃逸与儿童型和成人型干胶质瘤差异性形成的关系研究”。两项课题都与临床医学联系紧密。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
