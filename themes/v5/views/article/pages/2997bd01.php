		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中山医院成功救治气管断裂女孩</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯 （通讯员 陈惠芬 记者 顾泳）安徽女生小韩高考前突遭飞来横祸，夜骑电瓶车撞上铁丝线，导致气管断裂，差点命丧黄泉。所幸，经过复旦大学附属中山医院悉心救治，目前女孩断裂的气管已经合拢，并恢复了自主呼吸，情况有所好转。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中山医院胸外科医生徐松涛、薛亮接诊小韩后，立即将她收入重症监护病房。此时患者已出现严重感染症状，错过了手术修复最佳时机。对于这类气管严重缺损和感染的患者，传统治疗是进行气管造口维持呼吸。但如果装了气管造口，患者颈部将终身留有疤痕，且无法说话。这对一名花季少女来说无疑是晴天霹雳。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">如何最大限度提高患者的生活质量？中山医院胸外科专家与重症监护室、耳鼻喉科、麻醉科等多科专家共同进行会诊，最终明确了气管损伤范围，并制定严密周全的修复手术方案。手术当日，胸外科专家徐正浪、徐松涛为女孩实施修补，最终让断裂一周的气管“一气呵成”成功对接。术后不久，女孩便恢复了自主呼吸，如今已能轻声说些简单语句。专家表示，小韩还将经历三个月的恢复期，届时才能确定她的语言功能会否恢复。</span>
</p>
