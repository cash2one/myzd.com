			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>【广州日报】神经电刺激取代用药 靠谱吗？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一直以来，“生病就吃药”为大众所认可，但药品带来的各种副作用同样令人担忧。在科技高速发展的今天，人类发明了一种全新且没有副作用的治疗手段——电刺激疗法，用一根电极直接刺激某些神经，就可以调控免疫系统，完全摆脱药物，从根源上控制疾病。电刺激疗法属于生物电子科学，目前，这种治疗方法在炎症、糖尿病甚至癌症方面均取得重大进展。它能否在未来全面取代药物成为主流？记者采访了中山医神经科学研究中心的蒋斌教授和省人民医院的吴佩娜主任医师，为大家揭开这一新兴学科的神秘面纱。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 从经皮电刺激 到植入式电刺激</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 早在上世纪，查尔斯•谢灵顿就提出“反射回路”的说法，即神经元产生电信号，“传话”给运动神经元，运动神经元再将电信号传至身体肌肉和器官，触发各种人类行为。美国范因斯坦医学院提出的一项研究也表明，药物在这个过程中，唯一作用就是与目标细胞发生反应，担任“传话员”的角色。由于药物的不稳定性，如果有一种装置可以取代药物，直接作用于神经，岂不是更高效且安全？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中山大学医学院神经科学研究中心的蒋斌教授介绍道：“电刺激疗法主要是用外加电流刺激特定神经，产生和释放有药物功效的神经递质，比如乙酰胆碱、肾上腺素等，提高机体免疫功能。外科手术中将心脏起搏器植入人体，就是利用脉冲发生器定时发放一定频率的脉冲电流，通过导线和电极传输到心肌（心房或心室），即利用外加电信号帮助心脏恢复正常跳动。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如今，神经电刺激已从传统的经皮电刺激逐步发展到植入式电刺激的阶段。植入式电刺激由具有刺激定位准、治疗效果好、刺激电流小、不适疼痛感轻等突出优点。目前植入式神经电刺激在治疗控制帕金森病、癫痫、慢性疼痛、尿失禁、听视觉障碍等多个方面已取得了较大进展，在脊髓电刺激、胃肠电刺激，以及痉挛、痉挛性斜颈、老年痴呆症、镇痛及戒毒等方面也有着诱人的前景。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 治疗癫痫和帕金森有奇效</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 人体分布着一对很重要的迷走神经，支配呼吸、消化两个系统的绝大部分器官以及心脏的感觉、运动以及腺体的分泌。因此，迷走神经损伤可引起循环、消化和呼吸系统功能失调。蒋斌介绍道，“目前，对于帕金森、糖尿病、癫痫等，药物还是主要治疗手段，但长期服用药物带来副作用。相对来说，植入式电刺激疗法虽然需要手术，但比较安全且副作用小，当然，电脉冲刺激的次数和疗效等还需要深入临床研究。”蒋斌告诉记者，迷走神经电刺激术用于治疗癫痫，目前世界各国临床治疗已超过5万例，我国约有150例。手术原理十分简单，将脉冲发生器埋于左锁骨下区皮下组织中，并将电极经皮下隧道引入颈下部，缠绕在迷走神经上，术后两周即可开始刺激治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于帕金森病的治疗也是一样，蒋斌介绍道，帕金森为退行性疾病，随着病情的加重，用药量也应增加，药物副作用成为不少人担心的问题。除了药物治疗，目前主要有两种方法：一种是核团毁损术，另一种是脑深部电刺激术（简称DBS）。前者是在异常活跃的神经细胞群制造一个小的直径约3毫米的毁损灶，后者则是埋植脑起搏器，通过高频电刺激达到类似的效果。“从外科手术操作技术上讲，两者差别不大，疗效也近似，但DBS手术更加引人注目，因为传统手术其实是一种治疗性的毁损，以破坏部分细胞为治疗方式，而脑深部电刺激是一种重建性的治疗，在不破坏大脑细胞的同时，通过高频电刺激的抑制作用产生功能性的毁损，但这种抑制作用会随电刺激的停止而消失，这个过程是可逆的，而且这种抑制作用可以通过调节电刺激的大小进行调节。”简单来说，之前的手术治疗更像是一锤子买卖，还与医生的熟练程度相关。脑深部电刺激的治疗现在已经作为一种趋势，正在取代传统的毁损手术，手术之后，如果患者的恢复情况比较理想，能够达到与常人接近的水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 电刺激治疗的“前世今生”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 电刺激疗法经历了三个发展历程：古罗马时，医师用电鳗来治疗人的头痛及关节炎，属于体表刺激；一直到数十年前发明机械后，人们才开始使用较稳定的电流输出，埋入经皮神经刺激阶段，用来处理淋巴的再生、促进伤口复原及控制疼痛。近年来经皮神经刺激疗法也被逐渐家用化，催生了“哪里疼痛点哪里”的脉冲止痛笔，它是一种形似圆珠笔的止痛仪，显效迅速，轻便易携，30秒有效缓解疼痛。止痛笔是通过激活大脑和脊髓释放内啡肽，从而提供持久有效的疼痛缓解。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 电刺激人工耳蜗帮助摆脱无声世界</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 声音能够被人耳感知，要经过很多关卡，外耳道、骨膜、正常的中耳腔、内耳、神经、中枢……其中，任何一项功能障碍都可能导致耳聋。广东省人民医院耳科主任吴佩娜告诉记者，耳聋可分传导性和神经性，助听器适用于轻中度耳聋患者，但对重度以上神经性耳聋患者来说，植入人工耳蜗是最好的选择。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 人工耳蜗的植入体部分包含电极组和接收/刺激器两部分，由组织相容性好的钛合金与硅胶等材料所支撑。电极组上面规律排列着独立的刺激电极，插入弯曲的内耳耳蜗，与听神经末梢紧密相连。接收/刺激器为扁平的近椭圆形，置于耳后皮下。体外部分由头件和声音处理器组成，还包括供电电池。“手术后一个月才可以开机使用，患者还要经过培训，逐渐恢复听力功能，平时睡觉时可以将外部的声音处理器取下来，就像摘眼镜一样。”吴佩娜说。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 全球范围已有60万人植入人工电子耳蜗，这项电刺激疗法已在临床取得了成功实验。目前，做一个人工耳蜗要十来万元，具体价格视材料而定，最好根据医生建议选择最适合的。“在中国，做这个手术的小孩占了九成以上，手术年龄越小，效果越好。以前很多家庭并不知道人工耳蜗可以治疗重度耳聋，因此延误了最佳治疗时机，而现在，耳聋是有办法治的。”吴佩娜建议人工耳蜗手术一定要去正规大医院做，手术过程涉及其他科室的配合，为避免手术引发的并发症，需要完善的配套。“科学总是在不断发展，人工耳蜗是一种终生设计，所以我们尽量保留原始结构，如果有一天，科学有了更大的进步，那么原有的装置还有改进的机会。”吴佩娜说道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;耐药菌带来的反思</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 尽管电刺激疗法逐渐普及应用，但蒋斌指出，药物治疗手段仍不能完全被代替。“目前电刺激只对少量疾病有效，作为一种新型的治疗手段，更多的应用还需要临床实践。”蒋斌说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 回顾之前的药物使用历史，疫苗使人类可以调动免疫系统，抗生素帮助抵抗病原体，但过度依赖药物使耐药菌问题逐渐突出。去年4月，世界卫生组织发布了首份全球抗生素耐药性报告，数据来自114个国家，显示所有地区都发现存在抗生素耐药。据美国官方统计，仅美国每年就有至少200万人感染耐药细菌，约2.3万人因此死亡，耐药细菌感染每年给美国增加高达200亿美元的直接医疗费用和高达350亿美元的生产损失。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 正因药物的种种局限，如今电刺激疗法就如一道光，照亮了医疗事业前进发展的方向，为医疗界的未来提供了无限可能。</span>
</p>
