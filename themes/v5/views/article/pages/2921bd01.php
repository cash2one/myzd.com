			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“红颜杀手”——乳腺癌</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">香港素有“时装女皇”之称的时尚作家黎坚惠，在3月12日因乳癌恶化离世，终年才46岁，这突如其来的消息让时尚界人士唏嘘不已。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 乳腺癌无疑是女性排名第一的“红颜杀手”。在我国乳腺癌的平均发病年龄在48岁，近年来还有不断年轻化的趋势。乳腺癌专家指出，乳腺癌是最好治疗的肿瘤，在早期发现时完全可以</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">.治愈，建议女性在35岁之后，应该学会乳腺自查，有条件的话，定期去医院筛查乳腺癌，以便早期发现、早期治疗。</span>
</p>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 发病年龄平均在48岁，比国外早10余岁</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 盘点这几年因为乳癌而红颜薄命的明星，记者发现她们都在女人最好的年龄：42岁陈晓旭、33岁的李珏、34岁的阿桑、黎坚惠也才46岁。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “相对于国外，我国乳腺癌人群发病年龄较为年轻，平均年龄只有48岁。”中山大学附属肿瘤医院乳腺癌诊治首席专家杨名添指出，“在美国，女性乳腺癌的发生年龄一般在58-61岁，乳腺癌发病的年龄我国比国外要提前10-13岁。这一点尤其要引起年轻女性的注意。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 乳腺癌出现了年轻化的趋势。杨名添说，目前在医院里有不少二三十岁的女性患上乳腺癌，原因是多方面的，比如在大城市里，生活条件好，女孩子10岁左右就来月经了，女性绝经也不迟于55岁，35岁以后才生育第一胎，生了之后又不哺乳，这使得雌激素持续作用于人体，内分泌的不平衡，增加了乳腺癌的风险。另外，一些年轻女性，由于生活就业等竞争压力大，往往忽略了自身健康。再加上环境污染、饮食污染等对人体体内激素造成的不利改变，也使年轻女性罹患乳腺癌的风险加大。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 能发现早期乳腺癌的患者只占20%</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 临床上医生能接诊到的乳腺癌早期病人很少。杨名添指出，即便是中山大学附属肿瘤医院的乳腺科，大约也只有20%的病人是1型乳腺癌，即肿瘤直径小于2厘米。实际上，早期的乳腺癌大多数是可以通过手摸出来的，通过B超和乳腺照片还可以发现更小的乳腺癌。他曾经做过的乳腺癌手术，乳癌病灶只有0.3厘米，像这样的病患，5年生存率在90%以上。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 但临床上也经常会见到顶着巨大的肿瘤才来手术的患者。最近，他们就接诊了一位巨大乳癌的患者，去年年初摸到右边乳房里有一个豌豆那么大的肿块，当时去佛山某医院就诊，医生认为是乳癌，建议她立即手术。但这位47岁的大婶因为害怕手术，在朋友的介绍下，竟然找人进行按摩治疗，以为按一按就能消肿散结。结果按了三个月，肿瘤不但没有变小，而且长得鸡蛋那么大，等到去年12月肿瘤已经有一个婴儿头那么大（25×18厘米）。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一谈到这个病人，杨教授气得直跺脚。“早一点来正规医院治疗，肿瘤完全可以治愈，现在病人虽然在化疗后手术了，但已经有严重贫血，虽然肿瘤还没有转移，但手术后5年生存率也只有60%”。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 坚持自查，35岁以上每年做乳腺B超</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 乳腺癌是一个长在体表的肿瘤，用手摸是完全可以摸出来的。杨名添指出，女性应该坚持自查，因为很多乳腺癌病人是自己或丈夫无意之间给“摸”出来的。所以不要以为乳腺自查不重要。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 但是，不少女性虽然心里害怕自己患上乳腺癌，但对乳腺癌筛查的重视程度却让人相当失望。在凤凰网上，有一项1520人参与的乳腺癌网络调查，其中有一道问题是“你知道女性如何预防乳腺癌吗？”有23.56%的人表示“不知道，从未关注过”。65.89%的人表示“了解过，但不是很清楚”。只有10.55%的人认为自己“完全掌握，并且运用到生活中”。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 可见，对于预防乳腺癌，近九成的人都“蒙查查”或者没重视。杨名添说，我们提倡女性在35岁以后，最好选择乳腺B超和乳腺照片（比如钼靶X射线）来筛查乳腺癌。当然，有家族史或者晚育未育的女性，在35岁以后，必须一年到医院做一次乳腺检查，像乳腺照片能发现小于1厘米的结节性病灶。如果条件不允许，做不到每年到医院定期检查乳腺，那么至少你要每天自查乳房，这是最基本、最经济、最方便的筛查办法。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Tips</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 乳腺自查，女人你要懂</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 虽然不能依赖乳腺自查来发现早期乳癌，但能帮助我们更早发现异常，防患于未然。自查一般有以下两个步骤—</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “视”诊：先脱去外衣，站在镜子前面，两臂自然下垂，观察乳房外形和皮肤的改变；再后双手叉腰，身体做左右旋转状，继续观察。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “触”诊：平躺在床上，仰卧，左手放在头后方，用右手检查左乳房，手指要并拢，从乳房上方顺时针逐渐移动检查，最后用食指和中指轻轻挤压乳头。注意不要用指尖压或挤捏，更不要用手满把抓捏，否则可能误诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 自查中需注意，如果是肿瘤，肿块一般多出现在单侧乳房，质地较硬，一般不能活动。乳房表面皮肤会出现“橘皮样”改变。乳头一侧会发生移位或下降或是乳头凹陷。</span><br />
