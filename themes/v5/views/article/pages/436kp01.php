<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>良性阵发性位置性眩晕（耳石症）——旋转性眩晕的第一大疾病</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“哎约，我晕的厉害，一躺在床上，向左一翻身就房子转，转到右边就好了。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在临床上，经常有病患抱怨在某个特殊的体位出现位置性眩晕，视物旋转感，换一个体位，眩晕即好转甚至消失。如果您也有上述的症状，那么，您极有可能是得了位置性眩晕。良性阵发性位置性眩晕、偏头痛、脑干或小脑损伤等均可出现位置性眩晕。其中良性阵发性位置性眩晕发病率最高，约占位置性眩晕的90%。约占到眩晕/头晕就诊人数的25%。良性阵发性位置性眩晕的患病率随年龄增长而增多，女性患病率大约是男性的两倍。那么，是什么原因引起了良性阵发性位置性眩晕，又可采用什么方法治疗？</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>耳石脱落移位是良性阵发性位置性眩晕的罪魁祸首</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">正常平衡功能的维持，需要机体完整的前庭觉，本体觉和视觉，以及它们在中枢神经系统内的整合。任何一处的病变或者功能障碍都将影响人的平衡感。前庭器官就在我们的耳朵里，一个耳朵有三根半规管和两个耳石器（椭圆囊和球囊）。良性阵发性位置性眩晕可分为管结石和嵴帽结石型。管结石是由于椭圆囊内的耳石脱落碎片进入半规管，并可能聚集成团块，当头位改变时会引起内淋巴液流动的畅通性不足，就如同一条小溪，中间有石头，溪水遇到石头时流动受阻一样。嵴帽结石型是由于耳石颗粒粘附在半规管壶腹的嵴帽上，提高了嵴帽对重力作用的敏感度而引起的眩晕。头部外伤，既往内耳病史，偏头痛，骨质疏松以及耳科手术史患者均较易出现良性阵发性位置性眩晕。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>手法复位是良性阵发性位置性眩晕的有效治疗方法</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">尽管良性阵发性位置性眩晕的发病率很高，但是可喜的是它也是一治愈率很高的疾病。手法复位治疗被认为是最有效的治疗方法。一次治疗完全恢复的病人可达50－70%。它的原理是通过头部位置的调整，让脱落的耳石碎片回到椭圆囊内或使耳石从嵴帽上脱离。依据美国耳鼻咽喉－头颈外科协会良性阵发性位置性眩晕的诊疗指南，不推荐常规使用抑制眩晕的药物。除非症状严重，无法体检诊断及治疗的病患，用以暂时的缓解其恶心及呕吐，但不能长期使用。因此，若您出现了位置性眩晕的症状，还请您及时就诊！</span><br />
