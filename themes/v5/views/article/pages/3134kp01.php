<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>走出乳腺增生病认识误区</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、乳腺增生病不是肿瘤：乳腺增生病是一种既非炎症又非肿瘤的乳腺常见病，是在雌激素刺激下乳腺发生生理性增生之后，组织复旧不全所造成的结构紊乱。故有人称之为"乳腺结构不良"。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、发病与内分泌失调关系密切：尽管乳腺增生病的病因尚不十分明了。但有一点是肯定的，此病与卵巢内分泌失衡有密切关系。随着月经周期卵巢功能的变化，人体内的雌、孕激素水平也出现周期性变化，随着月经周期雌、孕激素水平变化，乳腺组织结构发生着生理性增生与复旧的周期性变化。如雌激素水平过高或孕激素过少或二者间不协调，便可引起乳腺复旧不全、结构紊乱，即：乳腺增生病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、疼痛是最常见的症状：该病最常见的表现为乳房疼痛，这种疼痛多为周期性的经前胀痛或针刺样疼痛，可牵涉到腋窝，月经来潮后可自行减轻或消退。也有部分患者疼痛不随月经周期而改变。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、肿块并非常见表现：大部分患者都会觉得自己有肿块。实际上，乳腺增生病常表现为乳房多处呈团片状、条索状、棉絮状或颗粒状腺体组织增厚。扪起来象肿块，但并非肿块。不过，少数病情严重者会在乳房中形成多发性囊肿，则可扪到界限十分清楚的肿块。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、药物治疗与心理治疗相结合：该病西医没有特效治疗。呈慢性过程、反复发作为其特点。而外科手术治疗也对本病无效，只有在不能排除恶性可能的情况下用作确诊的手段。本病与情绪相关，多在暴怒、忧思后症状加重，因此患者应当重视心理上的自我调节，消除心理障碍。症状较重者可予中药治疗。我院运用“消癖1、2号”系列方剂治疗乳腺增生病，既顾及中医的理法方药，又兼收西医学成果，辨证与辨病相结合，讲究用药时机，按月经周期用药，调整激素代谢水平，故能调整脏腑功能，使气血调和，癖消痛除，临床疗效显著。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、生活中的自我调节很重要：注意进食低脂、富含纤维素、维生素的饮食。尽可能保持有规律的生活方式，适当的体育锻炼均有利于病情恢复。尤其对于乳痛症患者，避免恐惧、紧张和忧郁心理，保持良好的精神状态是很重要的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7、乳腺增生病会恶变的说法没有依据：关于本病是否会恶变，在医学界一直没有定论。但至少到目前为止，尚没有充足的证据表明，乳腺增生病与乳腺癌有明确的关系。一般认为，乳腺增生病只是乳腺癌的多种危险因素之一。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8、注意定期复查是关键：乳腺增生病一般不以乳腺肿块为主要表现，且一般也不会转变成乳腺癌。但值得注意的是，乳腺癌在早期阶段所表现的肿块较小，不容易扪到，且可能与乳腺增生病混杂在一起，有时就连医生也不容易鉴别。因此，提醒患者一定要注意定期到医院复查，以防乳腺癌的漏诊。</span>
