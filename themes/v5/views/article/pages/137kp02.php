<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>什么是腹主动脉瘤？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp;也许大家对腹主动脉瘤还比较陌生，但实际上这种病并不少见。爱因斯坦、李四光得的病就是腹主动脉瘤。腹主动脉，就是人体腹部的大动脉。而腹主动脉瘤是人体的腹主动脉由于某些原因而扩张并膨胀达到一定程度所引起的一种疾病。动脉瘤和我们通常理解的肿瘤不一样。一般人理解的肿瘤就是体内长了一块东西，而且会越长越大。长到一定程度的时候，肿瘤还可以侵犯到周围的器官或者转移到远处的器官。而腹主动脉瘤则不同，它是腹主动脉出现了扩大，就像吹气球一样。它本身是一种良性疾病，但是却非常凶险。如果腹主动脉突然增大并扩张到一定程度，很有可能破裂并引起致命的大出血，就好像是把气球吹破了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">而腹主动脉是人体大动脉里发生动脉瘤机率相对高的部位。其发病多见于老年人，患有动脉粥样硬化的人。在65岁老年人中，发生率大约8%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>多大的动脉瘤会有危险？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">到底多大可称之为动脉瘤，而瘤体具体到多大会有危险呢？一般来说，腹主动脉瘤的管径要比周围正常的血管大一倍。正常的腹主动脉血管大概有1.5cM到1.8cm的直径，大于该数值的一倍就称之为动脉瘤了。如果继续扩大，接近4、5cm以上，那么危险性就很大了。目前国际上的标准是，如果动脉瘤接近5cm，或者说动脉瘤不到5cm，但扩大速度比较快，半年增加7mm，那么就比较危险了。因此如果动脉瘤增长速度比较快，或者大小接近5cm，就应该及时求医。另外，女性患者即使动脉瘤直径&lt;5cm，也需要积极治疗。</span><br />
