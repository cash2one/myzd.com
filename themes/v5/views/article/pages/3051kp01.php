			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>神经外科的微创理念探讨：功能区巨大脑膜瘤手术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，外科各个专科都掀起了微创的外科理念，影响相当之深远。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前一般的微创概念是小切口、腔镜的应用。毋庸置疑这在神经外科也是微创的主要目标。但是，神经外科学界一直有把减少术中不必要的脑损伤作为微创的一个重要概念，神经外科专科的一个重要杂志起名叫做“中国微侵袭神经外科杂志”而非“微创神经外科杂志”，可能正是承袭了上述理念。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">功能区巨大脑膜瘤一直是神经外科的一个挑战，由于肿瘤巨大、供血丰富、毗邻不可损伤的重要血管等因素，手术损伤邻近重要功能结构导致患者重残甚至死亡的例子时有出现。对于功能区巨大脑膜瘤的手术，贯彻微创、微侵袭理念可以显著改善疗效。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于此类手术，个人有以下体会，按照手术的步骤探讨如下。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先是精确定位，无论是术前CT或者术中导航，目的都是从手术切口划定阶段就能够合理确定手术显露范围，即足够又要避免过分。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其次是麻醉配合，术前和术中与麻醉医生充分的沟通，术中适当时机合理采用脱水、过度换气、血压控制等措施，均有助于减少术中牵拉、减少术中出血。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第三，尽可能减少脑持续性牵拉。以往我们做大型脑膜瘤手术，为了减少出血，经常采取整块切除的方式，但是分块切除、瘤内切除缩小瘤体的方法可以有效减少分离瘤周时对周边脑组织的牵拉，借助先进手术器械（例如Ellman射频刀）的帮助是可以很好实现的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第四，显微镜下仔细分辨瘤周蛛网膜界面，保护重要血管，特别要重视回流静脉的保护。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上面所述是我的一些实际体会，和前辈、专家的理念是一致的。当然还有很多重要因素，例如神经功能检测的应用虽非必需但价值是巨大的。</span><br />
