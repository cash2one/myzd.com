			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脾切除断流术后门静脉系统血栓形成</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有关断流手术和脾静脉、门静脉系统血栓发生已有很多报道，但其结果不一致, 血栓发生率为13.4% ~ 35.5%。我们在术后通过磁共振检查发现脾切除、断流手术后门静脉系统血栓发生率高达91.06%，远高于其他报道，并且所有血栓形成病人均有脾静脉血栓，门静脉主干血栓形成占其中25.89%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门静脉高压症手术后门静脉系统血栓形成</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原因复杂, 多认为与门静脉系统血流动力学变化、凝血状态、门静脉系统局部血管病理变化、手术对局部血管机械损伤、手术结扎时形成血管盲端、不合理使用凝血药物及局部区域炎症有关。过去认为脾切除后血液高凝状态与血栓形成有关。事实上，脾切除后血液有形成分增加和凝血功能改变大多未超过正常水平，因此我们不认为这是形成血栓的主要因素。脾切除后脾静脉呈一盲端, 血流缓慢和湍流形成，术中钳夹、挤压造成脾静脉内膜损伤, 胶原纤维暴露, 激活凝血系统形成脾静脉血栓, 并可向门静脉主干蔓延。因此，脾切除后血流动力学改变，即血流缓慢和湍流在门静脉系统血栓形成中起最重要的作用。而脾肾分流术后脾静脉内血流速度较快, 不易形成血栓, 如有吻合口栓塞则脾静脉较易形成血栓。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血流动力学改变导致血栓形成的又一证据是在脾肾静脉分流加断流后一部分病人门静脉内可有血栓。这是由于当脾肾静脉分流口通畅时，肠系膜上静脉内的血液经脾静脉分流入体循环, 而向上经门静脉入肝血流明显减少，同时又由于肝内阻力较高，可使门静脉血流更缓慢、瘀滞，并可产生湍流，因而形成血栓,</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在肝硬化门静脉高压症行脾切除和断流术后，基本上不能预防血栓形成。但是，术后应严密监测患者的凝血状态和血小板计数, 特别是后者。脾切除后绝大多数病人血小板迅速升高, 在2周左右达到峰值, 以后逐渐降低。如果血小板计数大于 800 ×109/ L，应行抗凝祛聚疗法，包括口服潘生丁和阿司匹林，静脉滴注低分子右旋糖酐、丹参和皮下注射小分子肝素等，可防止血栓形成或血栓进一步增大。脾静脉和门静脉血栓在形成过程中，常伴有侧支循环形成，除非发生急性血栓，否则引起肠坏死罕见。</span><br />
