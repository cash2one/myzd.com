		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>甲状腺癌术后，【生活上的注意事项】</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">恭喜您出院了。出院后的日常生活没有特别的禁忌。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 积极运动没有问题，饮食上也无需特别忌口。至于海藻类等含碘食品，除甲状腺腺素不足的人士以外，不必过多考虑。 碘类摄取对甲状腺癌术后随访并无不利影响。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 出院后门诊随访很重要。随访中应注意切口情况、血钙及甲状腺激素的水平、以及复发的有无。虽然甲状腺癌大多生长缓慢，但仍有可能经过多年以后再复发，必须终生随访。随访的时间为术后2周、2个月及6个月，前3次随访目的不同，请务必本人到场。随访就诊包括触诊、适当进行血液、超声及心电图、骨密度等检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 与胸、腹部相比，颈部感觉敏锐，颈部手术后难免出现不适，包括喉部堵塞感、勒紧感，吞咽困难，声音改变（即使无喉返神经麻癖)、颈部牵拉、肩部酸痛等各种症状。术后感觉迟纯，因此这些症状在 术后1~2周，也就是出院时开始出现，至少持续2个月。进行颈侧部淋己结清扫手术的患者，不适感可能持续数年。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 实际上，这些都是感觉上的问题，与病变性质的好坏、手术的成功与否无关。尽早行颈部牵引或按摩可能有一定效果。由于害怕疼痛，不敢运动，切口周围就会有瘢痕形成，症状很难消失。这些症状不必过分在意，请早点回归正常生活。</span>
</p>
