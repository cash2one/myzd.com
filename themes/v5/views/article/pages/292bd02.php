			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>没有症状的胆囊结石需要手术吗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">没有症状的胆囊结石通常被称为安静结石，原则上可以每年复查随访。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但在以下情况下胆囊恶变机会相对增大，所以建议手术：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.结石直径大于2.5厘米，其恶变率增高8-10倍；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.胆囊内充满结石，胆囊已没有功能，且恶变率大幅上升；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.胆囊萎缩；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.胆囊壁钙化，因在腹部摄片时钙化的胆囊壁围成一个瓶状故俗称“瓷瓶样胆囊”；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.同时合并有胆囊息肉，息肉在结石刺激下易于癌变；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6.合并有胆囊腺肌症者；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7.病史长，尤其是大于10年者；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其次，另外一些情况下也应考虑手术：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.有糖尿病者，因一旦发作急性胆囊炎，感染不易控制，故建议可以考虑手术治疗；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.患者年龄偏大且合并有较多系统疾病如高血压、心脏病等，年龄越大手术风险越高者；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.胆囊内有较多小于5毫米的小结石者，因结石一旦移位至胆总管将使病情严重化和治疗复杂化，可以考虑手术。</span><br />
