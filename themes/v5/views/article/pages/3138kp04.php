		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>半数鼻咽癌患者中晚期才就诊</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="http://static.mingyizhudao.com/147004463104171" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">晚报记者 许沁 报道</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 近日，香港演员成奎安因鼻咽癌去世引发关注。记者了解到，目前我国南方地区尤其是广东、广西、福建等地的鼻咽癌发病率在世界范围内居前列。与此同时，从复旦大学附属眼耳鼻喉科医院、仁济、新华医院耳鼻喉科传出消息，超过半数以上的鼻咽癌患者往往到了中晚期才来就诊。其实，早期鼻咽癌的生存率很高，专家提醒，当出现鼻涕带血、耳鸣、颈部有肿块时，要及时到医院检查，以做到鼻咽癌早发现、早治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 中国南部鼻咽癌发病世界居高</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在我国，鼻咽癌的发病有明显的地域分布，南方明显比东北偏高，尤以广东、广西、福建等地居多。“不仅在国内，这些区域的鼻咽癌发病率在世界范围内，也都居前列！”仁济医院耳鼻喉科主任王家东教授说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 目前，在上海，鼻咽癌已是常见的恶性肿瘤之一。其发病年龄大多集中在40岁至60岁的中年人和早期老年，但也有青少年患病者，以男性多于女性。“虽然，鼻咽癌的真正发病原因至今不明，但据研究发现，鼻咽癌的发病原因可能与EB病毒感染、遗传因素、饮食烹饪等有关。”王家东教授说，研究表明，EB病毒感染与鼻咽癌的发生密切相关，但并非所有EB病毒感染者都是鼻咽癌患者，还需到医院排查。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 此外，环境因素、饮食习惯等，也可能是诱发鼻咽癌的另外原因。复旦大学附属眼耳鼻喉科医院耳鼻喉科主任王德辉教授说，也有报道称，食用咸鱼和腌制食物可能是鼻咽癌的高危因素。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 涕血、头痛、中耳炎等是早期症状</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 不少人不把鼻涕中带血丝当回事，其实不少蛛丝马迹的变化，都表明可能已经患上鼻咽癌。昨天，记者从复旦大学附属眼耳鼻喉科医院、仁济、新华医院耳鼻喉科了解到，在临床上，大约超过半数以上的鼻咽癌患者来医院就诊时，往往已是中晚期，错过了最佳治疗期。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王家东教授说，虽然鼻咽癌的死亡率较高，但如果鼻咽癌病人能尽量早发现、早确诊、早治疗的话，对于1期的早期鼻咽癌患者，其5年生存率可达到90%至95%。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 为此，专家提醒，当出现回吸性的涕血，即吸鼻后痰中带血或鼻涕中带血丝；耳鸣、听力减退、耳内闭塞感、分泌性中耳炎；经常头痛，早期可能是头痛部位不固定且呈间歇性，晚期则为持续性偏头痛，以及颈部出现淋巴结等，都可能是鼻咽癌的早期症状，需及时到医院检查确诊。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 高危人群应尽早做鼻咽镜检查</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “目前在常规体检的五官科检查中，一般都有通过压舌鼻咽镜检查鼻咽部，但不少体检者容易在检查时出现恶心、想吐等反应，因此拒绝检查。”新华医院耳鼻喉科头颈外科副主任杨军教授说，其实在每年的体检中，总有鼻咽癌患者被检出。市民在体检中，千万别忽视了这一小小的检查项目。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 杨军副主任提醒，由于鼻咽部位较隐蔽，检查不易进行，早期症状也不明显。如果因为恶心、想吐等拒绝检查，万一漏诊，可谓得不偿失。建议可到医院门诊接受鼻咽镜检查，尤其是有回吸鼻涕中带血、一侧头痛、颈上部有肿块、有家族遗传史的中年以上临床可疑病人，更应尽早到医院检查，以早发现、早治疗，避免耽误诊断和治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 此外，专家还提醒，应注意气候变化，预防感冒，保持鼻和咽喉卫生，避免病毒感染。饮食宜均衡，多吃蔬菜和水果，少吃或不吃咸鱼、咸菜、熏肉、腊味等含亚硝胺的食物，不宜过量食用辛燥刺激食品，不宜过量饮酒，饮食宜清淡，应选易消化、营养丰富的新鲜食物。</span>
