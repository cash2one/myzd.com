	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>在大脑上动刀好比走钢丝</strong></span><br />
</div>
<br />
<br />
<img src="http://static.mingyizhudao.com/147010408131581" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 名家介绍</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 袁贤瑞，中南大学湘雅医院神经外科主任、主任医师、博士生导师。任中华医学会神经外科分会副主任委员，肿瘤组副组长，湖南省神经外科专业委员会主委，中国神经科学会神经外科专业委员会委员，中国医师协会神经外科分会神经肿瘤专家委员会委员。担任《国外医学神经病学神经外科学分册》杂志的副主编和多种相关专业杂志的编委。享受国务院特殊津贴。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 名家语录</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 治疗过程一定要严谨，要认真，对病人我们不能讲有百分之百的好结果，但当医生一定尽百分之百的努力，竭尽全力不留下遗憾。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 采访真难。袁贤瑞教授每天的工作总是被安排得满满当当。他除了要经常参加国际国内的学术会议，平均每年还要做300多台手术。袁贤瑞教授的手提电脑里有一个他建的患者数据库，每个病人的基本资料、手术时间、手术前后图片到各种检查的前后对比，都详细地存在里面，走到哪儿带到哪儿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 袁贤瑞教授给记者的第一印象就是尽善尽美。他步履轻快、思维敏捷、记忆力惊人，他对患者有着发自内心的同情，对医疗事业无限热爱。在人类的“生命中枢”里，他创造着奇迹，也创造着自己充实而丰富的人生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 神经外科，让他从痛苦到着迷</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 袁贤瑞是一名出生在郴州汝城县的农村娃，经过多年的摸爬奋斗，他骨子里依然保留着朴实和倔强的品性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 谈到家庭，袁贤瑞说自己的父亲早在他10岁时就过世了，从邻居亲戚的口中，他知道了父亲是一位善良、忠厚、勤劳和乐于助人的好人。家庭不殷实的袁贤瑞很早就品尝了生活的艰辛，11岁时，他因为膀胱结石而住院开刀，发病时那种钻心的痛，和手术后很快康复之间的对比，让他觉得医生真的是非常神奇。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1975年，从湖南医学院医疗系毕业后，袁贤瑞还未到湘雅医院报到，就和其他人一道，被派到乡下的血防医疗队，一直到77年3月份，才回到医院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 到医院神经外科上班的开始半年，袁贤瑞坦言自己是非常郁闷和痛苦的。因为他最想从事的是骨科，而神经外科的高死亡率让他感觉不到工作的快乐，更无法接受失去亲人后那一声声撕心裂肺的哭泣，正好一位搞肿瘤的同学提出和他对换。老师陆学风推心置腹地跟他说：“开颅在很多人看来是一件恐怖的事情，应该看到一些疾病通过开颅手术起死回生，也正因为难，才更有发展的空间。神经外科就是要你这种人来做，一个看死人无动于衷的医生根本就不适合做神经外科！”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1984年袁贤瑞硕士研究生毕业后开始在病房做总住院医，也就是这段24小时呆在病房、泡在手术室的时间，让他开始感觉摸到这个学科的一点儿“门边”，逐渐摸索到一些规律和思路，穿梭在病床之间，他的脚步越来越轻盈，因为此时的袁贤瑞觉得：神经外科是一个令他觉得多么迷人、多么兴奋的好专业。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 没有显微镜不开刀</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1987年，担任科室医疗秘书的袁贤瑞被导师曹美鸿教授派往武汉协和医院学习显微镜手术。这次学习成为袁贤瑞从医生涯中最重要的里程碑，而他也开始了自此“没有显微镜不开刀”的新起点。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 八十年代末期，医学界掀起一股做研究就要做分子生物之风，在选择自己博士论文和研究方向时，袁贤瑞经过反复思索，把题目“胶质瘤耐药基因”送到导师曹美洪手上，曹导沉吟良久后说：“你的这个想法是可以的，但外科医生的长处毕竟是临床、手术，而不是分子生物学。基础研究和临床距离很大，你一旦陷进去，现有的专业就有可能荒废。”也正是此时，一件事情深深地触动了犹豫未决的他。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 当时，一位邵阳市中心医院的护士因患中颅窝底神经鞘瘤住进了神经外科，袁贤瑞现在还清晰地记得这位姑娘进院时的情景：姑娘高挑的身材，穿着一件浅蓝色连衣裙，右手提着一个住院时用的铁桶，放着一些日常用品，左手挎一个包，神采奕奕地走进办公室。第一次手术肿瘤没能切下来，并造成了偏瘫后遗症；第二次手术后没多久姑娘就告别了人世。这件事带给袁贤瑞深深的震撼和无比的痛惜，他想，当病人找到你，而你却无力为她解除病痛，我当医生的价值在哪里呢？这件事情不仅让他感受到了沉甸甸的责任，更坚定了用毕生精力为患者解除病痛的信念。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1989年在国外医学神经外科分册担任一年编辑的袁贤瑞，有幸接触到了国外不少关于颅底外科的文章，在医院显微镜设备落后、器械差的条件下，他完成了34例高难度颅底外科显微手术，无一例死亡，全切率达到79.4%，大部分患者术后恢复到患病前的劳动，他的博士论文一经发表在学科界引起了不小的轰动。与此同时，在实践摸索中，他派生出术后患者采用代谢平衡治疗和营养治疗的概念，一改以往神经外科术后患者大量脱水、上皮质激素的常规治疗。显微技术的创伤小，加上术后治疗的合理，神经外科死亡率大大下降，预后效果明显好转。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 让老百姓看病少走弯路</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 人类有着无限的思维空间，然而这个承担思维的特有器官——大脑，却是神经血管密布，娇嫩敏感，稍有差池就会危急生命。天天走在这根精细的“钢丝绳”上，唯有认真，才能克服一个个困难。在采访中，袁贤瑞说得最多的两个词是：竭尽全力，尽善尽美。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 袁贤瑞经常这样教导自己的学生，治疗过程一定要严谨，要认真，对病人我们不能讲有百分之百的好结果，但尽百分之百的努力，做到自己没有遗憾还是可以的。这位骨子里透着倔强的汉子说：“认真是我的一种习惯。我坚持的东西不会改变，要做的竭尽全力去做，绝不会随随便便应付。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 作为湘雅医院首屈一指的国家重点学科，神经外科在国内也颇有影响和名气。这离不开袁贤瑞的努力。作为队伍的领头人，袁贤瑞不仅在技术和设备上精益求精，在对待患者上也是毫不马虎。他曾在科室交班会上发过一次很大的脾气。一位农村患者因慢性硬膜下血肿来湘雅看病，当时身上只有2000元，在长沙东拼西借勉强凑齐8000元住院费。可主管的进修医生却将4000多元开了菌必治，导致手术不能做。袁贤瑞在交班会上非常严厉地说：“医生的职责是为病人寻求一条正确、合理的治疗方法，如果不是因为病情而是为某种利益来开药，这就是一种罪恶。”他严肃处理了该名医生，并在科里制订相关制度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">袁贤瑞总是强调，我希望湘雅神经外科这支团队，是一支互相尊重、品格诚实、和谐敬业的队伍，希望他们做到我所没有做到的事，让老百姓看病少走弯路，让更多病人受益。</span>
</p>
