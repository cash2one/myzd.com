			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肝细胞是怎样恶变的</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我是一个肝细胞，我每天的工作是很累的，我主要从事高危的化工行业，我的主人每天吃喝的所有东西都要经过我们加工一下，才能变成有用的东西，我还要把加工所产生的废品和垃圾全部转运到下游的部门进行无害化处理。我们的工作有多重要，如果我们不是因为集体生病休息恐怕人类永远都不知道。人类吃进来的所有糖类（碳水化合物）、脂肪、蛋白质都要经过我们的加工。如果我们不去加工和储存糖类，你们就会血糖忽高忽低，心慌气短；如果我们不去处理好脂肪，你们的血管里就会塞满垃圾，不是心肌梗死就是中风；如果我们不处理好氨基酸，半天就足以让你昏迷（肝性脑病）。哈哈，我这么一说你们大概知道我的重要性了吧。还有你们吃的药呢，不管有多毒我都得冒死冲上去，分解出有用的成分去治病，把没用的垃圾运送走。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可是你们人类对得起我们肝细胞吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于你们的母亲不懂医学知识，在你出生时就把病毒安排到我们的队伍中（病毒母婴传播），由于我们那时候都比较幼稚，也分不清敌我，导致这些可恶的病毒长期与我们为伍，他们还要强奸我们，把讨厌的DNA整合到我们的体内，让我们子子孙孙不得清白。还有些人，不懂得洁身自好，去干各种不该干的事——乱吃、乱喝、纹身、吸毒、嫖娼，把本来清白的我们给染上病毒。我们是无辜的啊。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果我们侥幸没有 染上病毒，你们也不会放过我们啊。你们经常大吃大喝，难道你们真的需要吃喝那么多东西吗？那么多的东西吃进来，让我们根本忙不过来。到处都是脂肪，让我们实在透不过气来。你们还动不动就喝酒，我们是最讨厌喝酒的，这些可恶的酒精，如果我们稍微处理不过来，多余的酒精就会把我们毒死。你们人类现在生活条件也不错了，可是为什么还要吃带有黄曲霉毒毒的东西呢，过去你们生活困难，什么都吃这我可以理解，现在条件好了，可不应该再乱吃了，你们是否知道黄曲霉毒素的毒性可是比砒霜还要厉害很多倍啊。可能你们也是无辜的，那些发霉的东西被黑心商贩加工一下就闻不出来了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们已经够累了，你总该让我们休息一下吧。11点以后这段时间，是我们需要休整的时间，我们需要较好的血液循环来休养生息，可是你们为什么都不睡觉呢？中医老祖宗不是告诉你们“人卧血归于肝”吗！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还好，我们肝细胞除了吃苦耐劳，前仆后继地干活，我们还有一个优点，我们繁殖地快，老子牺牲了，儿子顶上去，这是我们肝细胞的光荣传统。可是这些年我们的生存状况也太差了。乙肝病毒经常强奸我们，改变了我们的基因。我们的生存环境中充满了毒素，你们就不能少吃点药吗，那么多药会把我们毒死的，感冒药、避孕药你以为就没事吗，有些保健品也害得我们不轻啊。我们为了适应环境就加快生孩子，还经常早产。这些没有发育完善的幼稚细胞，可跟我们不一样啊，他们越来越习惯于早早生孩子，你们人类说我们这叫什么“分化不完全”，我也看这些家伙跟我们长得有点不一样了。你们把他们叫肝癌细胞，以区别于我们这些良民。现在坏细胞越来越多了，他们占山为王、聚众闹事，不但不干活，有点营养都被他们霸占了，害得我们饥寒交迫，经常被饿死。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现在你们终于知道肝癌细胞的厉害了，你们已经开始打击这些可恶的家伙了。我刚要高兴一点儿，又发现不对，你们怎么经常好坏不分啊。你们动刀子（外科手术），要把他们一网打尽，可是为什么伤了我们那么多无辜的弟兄。你们搞什么化疗栓塞，可是为什么经常给我们又断粮、又下毒。你们最近流行搞射频，那玩意儿可是经常把我们的弟兄给烫死啊。</span>
</p>
<p>
