		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胸腔镜肺癌射频消融术</strong></span>
</p>
<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong><br />
</strong></span>
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>&nbsp; 射频消融的工作原理:</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 1、射频是一种中、高频电磁波</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 2、其生物学效应分为非热效应和热效应，临床治疗肿瘤为其热效应</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 3、 射频消融相关的主要元素</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;1) 通过病人形成的电流回路 (单针/双针)</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;2) 针周围组织中的热量产生</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;3) 从热量产生区向外传导</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;4) 凝固性坏死的产生</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 4、高温导致的组织坏死</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>&nbsp; 射频治疗肺部恶性肿瘤的状况（国外）：</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 1、1990年Hiratsuka－J首次报道利用单极射频针治疗肺转移肿瘤</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 2、2000年Dupuy等报导3例经皮射频消融治疗肺部恶性肿瘤病例，揭开序幕——Dupuy DE, AJR 2000</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 3、 Lencioni 用RFA治疗106例（NSCLC、肺部转移性肿瘤）的186个病灶，CT随访3个月，93％的病灶无进展。NSCLC患者1年生存率69％，2年生存率49％；结肠癌肺转移者，1年生存率88％，2年生存率72％</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">— SOCIETY OF INTERVENTIONAL RADIOLOGY 2005</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>&nbsp; &nbsp;射频消融治疗肺部肿瘤的应用范围</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 1、失去手术机会的中晚期肺癌</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 2、拒绝手术者</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 3、综合治疗后复发者</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 4、肺部转移性肿瘤</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 5、恶性胸腔积液</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 6、气管、支气管腔内肿瘤</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>&nbsp; &nbsp;我科情况</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 2005年10月，我科引入RITA射频肿瘤消融治疗系统，开始主要用于剖胸探查后发现丧失手术切除机会的病例。11月起我们开始探索在胸腔镜下应用射频消融治疗肺恶性肿瘤。主要应用于：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ①胸腔镜探查发现肿瘤异叶肺内播散或胸膜腔种植播散，而单纯肿块切除困难者；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ②恶性胸腔积液的病例，行胸腔镜探查、胸膜活检和射频治疗；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ③肺部多发转移性肿瘤；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ④影像学检查提示晚期病变但无明确病理证实，胸腔镜活检后行射频治疗；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ⑤无法耐受手术治疗的肺癌病例（如肺功能差等）在胸腔镜下射频治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 我们认为在胸腔镜下应用射频消融治疗失去手术治疗机会的肺部肿瘤具有以下特点：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ①对于术前未确诊的无手术切除机会的晚期病例，可经胸腔镜获取病理诊断，同时进行射频治疗；&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ②经胸腔镜探查发现失去进一步手术治疗机会的病例（如病理证实胸膜种植）提供一种新的治疗方法，探查同时即给予射频消融，时间短，避免术后局部病灶的放疗，节省治疗费用；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ③治疗过程直观，定位确切，可以避免CT引导治疗时经常存在的呼吸运动和穿刺针伪影等的影响，治疗效果良好；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ④减少了经皮穿刺射频消融治疗的一些常见并发症；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; ⑤治疗过程在全身麻醉下进行，可避免经皮穿刺治疗过程中患者体位不适、配合欠佳、感觉发热、胸痛等不适。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 我们认为，胸腔镜下射频消融治疗失去手术机会的肺恶性肿瘤短期疗效显著，安全可行，特点明显，但必须强调,肺癌是一种全身性疾病,RFA 仅是局部治疗手段之一,有其特定应用范围，应该结合手术、放疗、化疗和生物治疗等方法进行肺癌的多学科综合治疗。</span><br />
