		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>怪病：痉挛性斜颈的症状和治疗</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">痉挛性斜颈，顾名思义，就是颈部肌肉的不自主发生痉挛，从而引起颈部的歪斜，导致持续头部及颈部偏斜，不能摆正，或者摆正后颈部肌肉疼痛，发生痉挛的疾病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">事实上，痉挛性斜颈的定义是：痉挛性斜颈是颈部肌肉不随意收缩引起的颈肌扭转或阵挛性倾斜为特征的锥体外系器质性疾患。发病率较低，男女并无明显差异。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">痉挛性斜颈的症状</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">痉挛性斜颈的发病需要一个缓慢加重的过程，患者往往是首先觉得颈部肌肉发硬、颤动或者活动不灵活，渐渐的发生向一个方向持续偏斜，表现在颈部肌肉不自主的牵拉头部，或者产生不自主的头部抖动，医学上分类较为复杂，主要分成：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">（）旋转型：头绕身体红包纵轴向一侧做痉挛性或阵挛性旋转</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">（）后仰型：患者原因头部痉挛性或阵挛性后仰面部朝天</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">（）前屈型：患者头部向胸前做痉挛性或阵挛性前屈</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; （）侧挛型：别人患者头部偏离纵轴向左或右侧转重症情况患者的耳颞部可与肩膀逼近或贴紧并常伴同侧肩膀上抬傲慢现象</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; （）混合型：颈项多处肌肉痉挛并有颜面口眼手及躯干部位的肌肉痉挛信任患者头颈歪斜呈多变状态伴复发其他部位肌肉痉挛出现相应表现的痉挛性斜颈</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">痉挛性斜颈除了以上几种类型之外，多数患者常伴有颈肩背等部位因痉挛造成神经受压引起的疼痛</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">痉挛性斜颈的治疗：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">痉挛性斜颈的治疗方法很多，总体治疗效果并不十分乐观，纵观医学史，治疗此疾病的方法有：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1.A型肉毒素注射：最为简单的保守治疗方法，通过肉毒素的毒性作用在局部注射产生肌肉神经接头兴奋性的传导障碍，导致暂时的肌肉-神经分离，从而缓解神经对肌肉的过度兴奋作用，缓解其痉挛的症状，是肌肉得到放松，从而最终缓解颈部痉挛的症状，有效率能达到30~50%。肉毒素注射的缺点在于易产生过敏反应，部分患者还会有中毒症状，肉毒素的神经肌肉阻滞作用一般在2~3月，因为周围神经的再生过程往往需要这么长时间，也就是过了这么长时间之后，常常需要再次注射，肉毒素注射3次之后其疗效将会大大下降。患者常常会首先选择这种方法，但是最终会因为它的逐渐下降的疗效和越来越严重的副作用而不能继续治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2.药物治疗：内科治疗，主要使用的药物有肌肉松解剂，镇静剂，多巴胺受体阻滞剂短时多巴胺排除剂抗胆碱能制剂GABA能药等，药物的作用往往局限，部分患者出现嗜睡、精神不佳等副作用，常作为手术治疗的辅助治疗方法。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3.手术治疗：手术治疗历史悠久，从19世纪的颈部肌群切断+紧缩，到后来的颈部神经（副神经+颈丛神经）的切断+颈部肌群切断+紧缩术，再到现在我科主要首选的副神经神经减压+颈神经根的超选择切断，手术的创伤已经越来越小，手术的副作用也越来越少见，而于此同时手术的疗效也越来越高。在所有的治疗方法中，先进的手术治疗方法已经渐渐成为任何一个有责任的医生必须介绍给每一位痉挛性斜颈的治疗首选方法。颈部肌群切断+紧缩手术主要是切断颈部痉挛的肌群，双侧手术，使外观上得到一致，但没有从根本上降低神经的兴奋性，手术复发率很高。颈部神经的完全切断机颈部肌群的切断在前一种手术方式的基础上减少了术后复发的可能性，但是术后患者极易出现头部活动不能，患者术后常年呈低头状，不能自主转头，同样影响了患者的生活。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">我科目前主要采用个体化采取“副神经神经减压+颈神经根的超选择切断术”或“脑脑深部电刺激术”治疗，术前行局部注射麻药以明确主要的痉挛肌肉，为的是手术中能够准确操作需要减压的神经，手术中再进行神经电生理检测，最大程度的明确责任神经，术中进行副神经减压+颈神经根的超选择切断，既保证了手术的有效性，又最大程度上保留了颈部肌肉的原有正常功能，达到较为满意的治疗效果，总体有效率在90%以上。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">需要说明的是，手术治疗并不能立刻改善患者的痉挛性斜颈的症状，患者手术之后需要进行有意识的功能锻炼，促进颈部肌肉正常功能的恢复。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前功能性神经外科疾病的手术治疗已成为南京市鼓楼医院神经外科的一项特色专科，痉挛性斜颈和面肌痉挛、三叉神经痛、舌咽神经痛一样，治疗的效果越来越好，手术后的并发症和复发率也越来越小，相信通过我们的努力，根治痉挛性斜颈这一功能性神经外科顽疾将在不久的将来完全实现。</span>
