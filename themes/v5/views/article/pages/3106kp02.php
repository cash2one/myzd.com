<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>不开刀的方法—震波治疗股骨头坏死和骨折不愈合&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;股骨头坏死全称股骨头无菌性坏死，或股骨头缺血性坏死，是由于多种原因导致的股骨头局部血运不良，从而引起骨组织进一步缺血、坏死、骨小梁断裂、股骨头塌陷的一种病变。据不完全统计，目前全世界患此病者约3000万人，我国约有400万人。尤其是激素的问世及广泛应用以来，股骨头坏死的发病率逐渐上升。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 上海二医大九院骨科将国际领先的骨科体外震波技术和OssaTron震波治疗仪引进国内，应用此项非手术方法向股骨头坏死这一骨科顽症发起冲击。初步随访结果证实这种方法安全、经济、有效。2005年1月28日，通过上海市医学会临床准入论证。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 震波治疗在局部麻醉或硬膜外麻醉下进行，每个部位选择3-5个冲击点，每点冲击1000次，做1次治疗历时一小时，住院1-2天。震波治疗尚可用于骨不连、肩周炎、肌肉附着点炎等疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;冲击波治疗骨不连</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 骨不连，即骨折不愈合，是骨折的常见并发症。交通事故的增多导致严重骨损伤病例的增加，骨不连发生随之增多。一旦发生骨不连，骨愈合过程将停止，骨骼失去连续性，无法进行正常活动，造成病人肢体的功能病废。病人的主要症状为疼痛、活动受限，骨折部位异常活动等。传统保守疗法对于这些疾病束手无策，而反复手术给病人带来很大的痛苦和巨大的经济负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 骨科体外震波技术最先来源于泌尿外科的体外震波碎石术。20多年以前，当泌尿外科医生用震波碎石机治疗输尿管和膀胱结石时，由于震波要穿过病人的髂骨，所以加大了震波的能量。此时出现了一个非常有意义的现象，所有病人在治疗后出现了髂骨增厚，这种现象在震波经过的区域最明显。很显然，骨组织的迅速生长的原因是震波的刺激。科学家们并没有忽略这种碎石术的“副作用”，随后展开的关于震波对骨和软组织生长影响的基础研究表明，震波对骨和软组织愈合有很大的促进作用，这种作用不只是来自于震波的机械刺激，而是对成骨细胞“唤醒”的生物学作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 骨科体外震波技术已经在欧洲大陆的大部分国家、美国、韩国、台湾等地广泛开展并取得了举世瞩目的成功。据报告此技术治疗骨不连的成功率高达79%，优于传统的手术方法。该技术的无创伤特点得到了患者的普遍欢迎，震波治疗术根本就不是手术，更象传统的理疗，只不过在需要的时候采用局部麻醉而已。我们的初步结果也表明，震波治疗对于骨不连有较好的效果。一般住院2-3天，在臂丛或腰麻下进行震波治疗，每个治疗部位取3-4个点，每点冲击1000次，持续时间约15分钟，治疗过程无明显不适感觉。</span>
</p>
