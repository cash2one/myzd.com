		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中老年人锻炼悠着点</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中老年人不宜爬山锻炼</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">广州军区广州总医院骨科医院副院黄华扬教授告诉记者，骨科医生经常会遇到一些中老年患者，因为过度锻炼而伤了关节，其中，膝关节损伤最为多见，而致病原因竟是天天爬山。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">黄华扬指出，爬山是一项很好的有氧运动，既能锻炼身体，又可以享受大自然的风光，吸收清新空气，令人心情愉悦。然而，爬山对中老年人的膝关节弊多利少。膝关节是一个负重关节，容易劳损。特别是下山，除了要负荷身体重量，还要承受下山时的冲力，对膝关节损伤更大。而且，一般过了35岁，骨骼和关节就开始退变，有些中老年人患有骨关节增生、骨关节炎或骨质疏松，如果再经常进行诸如爬山、爬楼梯等对膝关节压力强度较大的运动，容易加速膝关节软骨的磨损老化，引发膝关节方面的毛病。所以，中老年人不宜进行爬山运动。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">老人打太极拳，不要蹲太低</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">打太极拳以安全舒适、修身养性而著称，被认为是最适宜老人的运动项目，然而，黄华扬提醒，老年人打太极拳也要“偷偷懒”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“虽然打太极拳强度不大，动作也较缓慢，但时常要保持半蹲位，此时如果身体重心压得太低，对于膝关节的负重会较大，久而久之，对膝关节也不利。”黄华扬建议，老人在打太极拳时，不妨稍微提高身体重心，膝盖弯曲不要超过30°，这样可减少膝关节负荷，避免膝关节损伤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">如果已经患有关节炎的中老年人，要做到“一禁三不要”。“一禁”是禁止下蹲，“三不要”是不要爬山、不要爬楼梯、不要坐矮凳子，这些都是增加膝关节压力的动作。对于老年人来说，最好在温和的阳光下，在户外步行，或者气温适宜时游泳。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">特别提醒</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">运动损伤，正确诊治是关键</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">运动损伤很常见，然而，由于百姓对运动损伤的认识不足以及运动医学专科医生匮乏，导致大量的运动损伤误诊、漏诊，延误了患者的治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">黄华扬说，他曾接诊一名中年患者，平时喜欢羽毛球等运动，近年来被反复出现的右肩关节活动后疼痛困扰，有医生告诉他这是“肩周炎”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为了治疗“肩周炎”，他跑了几家医院，治疗了两三个月，又是按摩又是理疗，每天还不断地做爬墙、梳头等动作，但还是没效果，不仅抬肩无力，夜间还疼痛难忍。后转到广州军区广州总医院骨科就诊，黄华扬主任给他详细检查并建议做了核磁共振（MR）检查，结果发现该患者并不是肩周炎，而是 “肩袖损伤”，是因运动损伤所致。通过肩关节镜手术治疗，术后症状得到很好的控制。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为何被医生认定的“肩周炎”最后却被确诊为“肩袖损伤”？黄华扬说：“目前对于运动损伤的认识还处于相对滞后的阶段，比如，不少市民甚至普通的骨科医生都常常误认为肩痛就是肩周炎，进行不恰当的按摩、理疗及功能锻炼，很难起到治疗效果。”</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为了普及运动医学的相关知识，提高广东整体的运动医学诊治能力，由广州军区广州总医院承办的2013广东省医学会运动医学学术年会暨广东省医学会关节镜与运动医学新技术研讨会日前在广州举行，与会医生就运动医学及关节镜技术等进行了交流学习。</span>
</p>
