<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>咽部不适感可能为下咽癌征兆&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人一生中一般都会出现几次咽部不适。有时感觉喉咙口像被什么东西堵住了，咽不下去又吐不出来；有时感觉吞咽食物时有粘附感，不顺畅。然而，许多人常常会忽视这种咽部不适感，误以为只是慢性咽炎发作而已。对此，上海市耳鼻喉学会副主任委员、上海市第一人民医院耳鼻喉-头颈外科的董频主任特别提醒：“对于咽部不适，切莫大意！如果这种不适感觉偶然发生又很快消失，往往同进食不当有关，不必过分在意；如果这种不适感觉反复出现，或迁延加重，则最好及时到医院耳鼻喉科就诊检查，因为下咽癌早期症状就表现为咽部梗阻感与进食粘滞感。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>什么是下咽及下咽癌？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般而言，下咽即喉咽，即食物自口腔向食道转移的通道，是口腔向食道过度的重要组成部分。它位于发声器官—喉的后方，与喉的关系比较紧密，且位置隐蔽，功能极为重要。所谓下咽癌即下咽部发生的上皮源性恶性肿瘤。临床上，下咽癌患者并不多见,每年发病率约为0.17～0.8/10万,占头颈部恶性肿瘤的1.4%～5.0%,占全身恶性肿瘤的0.5%。董主任介绍说：“下咽癌作为一种恶性程度最高的上消化呼吸道肿瘤,其5年生存率约为25%～40%。近年来，下咽癌发病率呈逐年上升趋势，由于其症状不明显，易被混淆或忽视，所以大部分患者确诊时已属于中晚期。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>下咽癌的症状有哪些？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下咽部解剖位置较为隐蔽，其主要功能为进食和吞咽。因此，下咽部出现肿瘤后首先产生的症状就与吞咽有关，表现为进食的粘滞感，但不一定有进食梗阻、进食困难。此外，在未进食的时候则表现为咽部梗阻感，这种梗阻的感觉同慢性咽炎的表现极为相似，故容易不受人们重视。病情一旦发展就可表现出咽喉部的疼痛，但咽痛仍不是特异性的症状，容易与咽炎引起的咽痛相混淆，亦不易引起人们重视。病情发展到后期往往影响喉腔，则会表现出声嘶；颈部淋巴结转移时则出现颈部包块。后期的症状容易受到人们重视，但此时疾病往往已属于晚期，治疗上具有很大难度。尽管如此，有咽部不适的患者在检查确诊前大可不必过分担忧。因为中老年女性患者中慢性咽炎相对多见，而下咽癌一般在40岁以上并有长期烟酒史的男性相对多发。有调查表明，下咽癌在男女中的发病比例为12.6:1。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>下咽癌怎样诊断和治疗？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“慢性咽炎的患者应当加强自我保护意识，定期至医院的耳鼻咽喉科行专科体检。中老年男性又具有长期烟酒史者应当密切关注咽喉部的症状变化，一旦咽部梗阻感、进食粘滞感有明显加重或伴发咽痛，应及时至医院耳鼻喉科就诊。如专科医生建议需要行进一步的纤维喉镜或硬管喉镜检查时（拍一张照片就可发现肿瘤），且不可盲目以为慢性咽炎而自行服药治疗，却不行专科检查。另外，对于咽部梗阻感、进食粘滞感反复发作的患者，一旦出现声音嘶哑或颈部出现包块则需格外注意，及早来院就医。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，上海市第一人民医院耳鼻喉-头颈外科目前所开展的保留喉功能下咽肿瘤切除术在国内处于领先水平，具有丰富的临床诊治经验。董频主任强调指出：“手术加放疗的综合治疗模式是目前治疗下咽癌的最佳选择,而手术彻底切除肿瘤是提高疗效的关键。虽然下咽癌的恶性程度较高，但只要早期发现肿瘤，此时肿瘤局限，手术仍然可以取得较为良好的效果。上面已经提到下咽部同喉腔的关系密切，晚期下咽癌会侵犯喉腔，此时手术切除肿瘤以后会不同程度地影响喉的呼吸、发声功能；同时，一般术后需行气管切开术，有可能需终身带气管套管。反之，如能够早期发现病情，肿瘤局限于下咽部，没有侵犯到喉腔，那么手术后喉腔的功能就可以得到保留；某些患者甚至可以不行气管切开术，大大提高患者的生活质量。由于肿瘤的部位较复杂，其切除与成形的方法很关键。不同的肿瘤部位采用不同的手术入路，不同的修补方法。常用的修复材料有：胸舌骨肌瓣、胸乳肌皮瓣、游离组织、人工材料等。下咽癌治疗原则为彻底切除肿瘤，然后才考虑保留功能；有条件的患者可行术前诱导化疗，术后补充放疗。</span>
</p>
