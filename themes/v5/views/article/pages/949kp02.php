	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:1.5;">前列腺癌根治术后家属护理注意事项有哪些？</span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">术后一周是患者恢复的关键期，需注意各种引流液量、尿量，有无并发症，进食、活动等身体恢复情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 1. 患者术后常规留置引流管，家属应观察引流液量和颜色的变化，如引流液量逐步减少，颜色变淡则表示恢复良好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 2. 尿量在术后应维持在1500ml-4000ml/日，术后几天可能是淡血性，如能逐步变成淡黄色为恢复良好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 3. 术后一周内常见并发症：出血、发热、疼痛、感染、淋巴瘘、淋巴囊肿、尿瘘、下肢深静脉血栓，家属应及时观察患者变化，及时和医生护士沟通。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 4. 常规在肛门排气后（一般术后3天左右）可考虑少量多次进食流质饮食，包括米汤、菜汤和少油的鱼肉汤等等，之后根据病情恢复，逐步过渡到半流质饮食，包括米糊、稀饭、烂糊面、馄饨、藕粉等等，可以加入适量蔬菜和少量荤菜，一周后是否进食普食，应咨询医生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 5. 应按医生要求，鼓励患者适当下地活动，活动时注意安全，避免摔倒；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 6. 患者长时间卧床，需要定期翻身、拍痰以防褥疮和肺炎，不能下地活动时应加强下肢小腿到大腿的肌肉按摩，避免下肢静脉血栓。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 7. 在床上翻身、适度活动和上下床时注意避免牵拉引流管和导尿管，避免滑脱。</span><br />
