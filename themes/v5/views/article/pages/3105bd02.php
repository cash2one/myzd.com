		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>爱心接力抢救眼球 江西女孩慧慧今出院</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">东方网记者刘轶琳6月18日报道：今天是江西女孩慧慧（化名）右眼受伤住院治疗的第六天，经过术后几天的治疗，已度过外伤后继发感染关，她将在父母的陪同下出院继续治疗和养护。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">6月12日下午6时许，慧慧在家和另一名孩子玩耍时，不慎被对方的竹签插入右眼，于6月13日下午4时通过爱心接力到达上海的复旦大学附属眼耳鼻喉科医院急诊室，并于当晚施行眼眶探查联合眼球破裂伤修补手术。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">周一早晨，医院大眼科主任卢奕教授、副主任钱江医师以及玻璃体视网膜学科主任常青医师等一起来到她的床前查房会诊。小慧慧依偎在妈妈的怀里，看上去比之前来院时的精神好了许多。经过几位眼科专家的专业检查和全面分析讨论，认为现在小慧慧的外伤眼手术后病情稳定，但由于伤势过重，右眼伤后一直没有光感，目前进一步复明手术治疗的可能性不大，专家们会诊意见建议慧慧先出院继续术后的眼局部药物治疗，门诊定期随访观察。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">同时，专家们关照慧慧妈妈一定要注意对孩子左眼的观察，一旦有视力下降、眼红和眼痛等就要立即到医院就诊，警惕交感性眼炎的发生；同时也告知慧慧父母，随着时间的推移，小慧慧的外伤眼很有可能发生眼球萎缩。院方也表示，小慧慧出院后仍会与他们保持联系。</span>
