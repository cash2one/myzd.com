			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>先天性后鼻孔闭锁的表现有哪些</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性后鼻孔狭窄的主要症状是鼻塞、流白粘涕、嗅觉障碍及呼吸困难。病人症状的轻重缓解，与闭锁的程度和年龄有关。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因出生时新生儿一般在3周之内只会用鼻呼吸而难以用口呼吸，所以先天性双侧完全性后鼻孔闭锁的患儿，出生后即出现严重的呼吸困难、紫绀甚至窒息。有些患儿虽然症状不如上述严重，但每于吃奶或闭口时呼吸困难加重，并出现紫绀，从而拒绝吃奶。当张口啼哭时，呼吸困难和紫绀反而显著改善或消失。再次吃奶或闭口时，上述症状再次出现。故呼吸困难常呈周期性发作。因患儿吃奶不便而致营养障碍，加之不能经鼻呼 吸，失去对吸入气体的过滤及加温加湿作用，从而易罹患肺炎甚至夭折。若能幸存下来，新生儿需经历约3周才逐渐习惯用口呼吸，但吃奶时仍有憋气。随着患儿年 龄的增长，憋气或呼吸困难症状会日趋减轻。因此时患儿思维和沟通能力增强，此时患儿可主诉嗅觉丧失或不能擤出鼻涕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性单侧后鼻孔闭锁的患儿，吃奶时可出现气急，平时可无明显症状。但有一种情况需加重视，即已习惯用健侧鼻孔呼吸的先天性单侧后鼻孔闭锁婴幼儿，如健侧偶然堵塞，可能会突发呼吸困难或窒息。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因后鼻孔闭锁，鼻腔分泌物不能自后鼻孔排入咽部，所以患儿表现为白粘涕自前鼻孔流出。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因单侧或不完全的后鼻孔闭锁的患儿鼻腔尚可通气，气味分子可进入鼻腔，刺激嗅神经，所以这些患儿多不出现嗅觉功能障碍。嗅觉丧失见于双侧后鼻孔闭锁的患儿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性后鼻孔闭锁的患儿常伴发其他畸形。据Evans（1971）报道先天性后鼻孔闭锁65 例，合并其他先天性畸形者28例，占43%，其中有多发性先天性畸形（Charge综合畸形）即先天性虹膜缺损、先天性心脏病、先天性后鼻孔闭锁、生长停 滞、殖器发育不良、耳廓畸形等，此外还可能有其他畸形，如硬腭高拱、扁平鼻、先天性耳前瘘管、斜视、虹膜麻痹、颌面成骨不全综合征、外耳道闭锁、回肠憩 室、肠道异位、泌尿系统畸形、多指畸形等。而且有遗传倾向。&nbsp;</span><br />
