	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>河南弘大心血管病医院用起搏器打开患者“心”门</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147010298900392" title="" alt="demo.jpg"/>　心内科专家正在为心脏病患者实施手术&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在医疗市场竞争日趋激烈的形势下，河南弘大心血管病医院已经从省内知名的心血管病专科医院迈入全国知名心血管病专科医院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 近一年多来，河南弘大心血管病医院成功开展心内科手术近千例，尤其是心血管内科的高难度手术，如冠脉造影、支架植入、心脏起搏器、射频消融术等。目前，该院心内科在刘怀霖院长的带领下，在心内科手术的成功率、治愈率方面，已响亮地打出了中原地区心血管内科的专科品牌。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 技术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 为2岁半患儿安装心脏起搏器</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 全国仅3例</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 作为省内知名的心内科医生，河南弘大心血管病医院副院长刘怀霖是介入方面的专家，每天都有患者慕名而来。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 清丰县的张先生就是其中的一位患者。“我特意找刘院长，让他给我更换已用了十年的心脏起搏器。”张先生说，他今年已经63岁，之前，他就因心脏电传导系统故障而多次昏厥。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “他是三度房室传导阻滞患者，当年因为频繁昏厥而被当地医院当做癫痫治疗过。”刘怀霖清楚地记得，当时24小时动态心电图记录中，张先生因传导阻滞昏厥两次，结合其胸闷等症状，排除了癫痫。确诊后，给其安装了永久心脏起搏器，张先生的心脏又健康地跳动了10年，其间没有发生过一次昏厥。刘怀霖在心脏起搏器的植入术方面，积累了丰富经验。6年前，他曾经为一位老方丈安装了永久起搏器，重启生命之旅。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当时老方丈已逾百岁，因病态窦房结综合征心律失常，反复昏厥、抽搐。刘怀霖克服了患者心肌老化起搏电极不易固定、并发症多等困难，为其安装了永久性心脏起搏器，挽救了老方丈的生命，创下省内起搏器治疗患者年龄最大的纪录。</span>
