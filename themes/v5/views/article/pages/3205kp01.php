<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>关节镜变身肛瘘镜？这个可以有</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王业皇，南京市中医院肛肠科副主任，主任中医师，江苏省名中医，南京中医药大学兼职教授，硕士生指导老师；中华中医药学会肛肠分会副会长，世界中医药联合会肛肠病专业委员会副会长，江苏省中医药学会肛肠专业委员会秘书长，南京市中医药学会肛肠专业委员会主任委员。长期跟随丁泽民从事临床、教学及科研工作，并系统总结了丁老的临证经验及学术思想，在丁老的指导下，对高位复杂性肛瘘、环状痔、顽固性便秘、炎性肠病、骶尾部肿瘤等肛肠病进行了研究，并取得进展。在国内首先开展了IVS吊带加桥式修补治疗盆底功能障碍所致便秘，取得比较满意的疗效。主持研究国家“十五”攻关课题1项，先后获得4项市（厅）级科技进步及新技术引进奖。先后撰写了十余篇学术论文；参与编写20余本肛肠类专著。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">严重的肛周脓肿如果不及时治疗，会引发肛门部不适，甚至危及生命，治疗不恰当会继发高位复杂性肛瘘，而说到肛瘘手术，很多有过这类疾病的患者都会皱眉痛苦，治疗不彻底会反复发作，对肛门括约肌影响特别大，且手术的创面也会比较大，术后换药痛苦较大，如何提升患者的长期生存质量，减少手术痛苦，让身体恢复得快一点？成了很多治疗高位复杂性肛瘘专家所追求和探寻的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>反复肛瘘治疗“伤不起”</strong></span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于高位复杂性肛瘘的人群来讲，很多人都吃过好几刀。有的患者几个月就得手术一次，想到每一次伤口，每一次换药，都觉得伤不起。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这毛病不能断根吗？南京市中医院的王业皇教授告诉记者，高位复杂性肛瘘和别的肛瘘不同，它的那个“洞”穿越了外括约肌深层以上，而且多有支管，常常这里好了那里出事，让人防不胜防。要治疗这个毛病，就必须对肛瘘的形态、走向以及与括约肌的关系作出准确的判断，特别是细小支管一定得弄清楚。这些因素综合在一起，就导致了病情反复、患者“被虐”的凄惨现状。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这其中，以细小支管的情况最难摸清。到底有没有办法来解决呢？王业皇琢磨出一个办法——用肛瘘镜。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>关节镜变身解决难题</strong></span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">咱们现在都知道内镜很威武。在内镜下，我们能看到以前看不到的部位，能到达以前到不了的部位，创口虽然小，可就算是螺狮壳里它也能做道场，大展拳脚。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么，在高位复杂性肛瘘这种毛病上，内镜有没有用武之地呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然可以有。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王业皇教授说，他们根据高位复杂性肛瘘的特点，结合以往的治疗经验，把过去用于关节手术的关节镜拿来，用在肛瘘手术中。在内镜下，医生可以对肛瘘形态掌握更加清楚，同时深入患处，先把一个海绵一样的东西伸进去引流，再注入一些中药，接着用刨刀切除瘘管壁硬结组织，最后将内口切闭。过段日子，患者的肛瘘就会慢慢好了。而且因为内镜下看得透彻，处理得也更到位，解决了目前治疗中的一些不足之处。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王教授有话对你说</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于肛瘘，预防也远远重于治疗。王业皇建议，一般市民没事应该做一些保健工作，对于术后患者来说这一点尤其重要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>饮食保健：</strong>肛瘘的发生与湿热有密切关系，所以油腻生湿热的食物应有所节制，同时要戒除烟酒及嗜茶的习惯。应多食清淡并含有丰富维生素的食物，如冬瓜、丝瓜、绿豆、萝卜等。经久不愈的肛瘘多属虚型，饮食上应食含有丰富蛋白质类等食品，如瘦肉、牛肉、蘑菇、大枣、芝麻。另外，预防肛瘘，可以多吃些增加皮肤弹性的食物，比如猪皮、猪蹄、鸡爪等富含胶原蛋白的食物，核桃仁、花生、大豆富含维生素E的食物。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>生活保健：提肛运动</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、夹腿提肛法：仰卧，双腿交叉，臀部及大腿用力夹紧，肛门逐渐用力上提，持续5秒钟左右，还原，可逐渐延长提肛时间。重复10-20次，每日2-3遍。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、坐立提肛法：坐位，双足交叉，然后双手叉腰起立，同时肛门收缩上提，持续5秒钟，再放松坐下。重复10-15次，每日2-3遍。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、踮足提肛法：站位，双手叉腰，两脚交叉，踮起足尖，同时肛门上提，持续5秒钟，还原。重复10-15次，每日2-3遍。</span>
</p>
