	<div align="center">
	<span style="font-family:Microsoft YaHei;font-size:18px;line-height:2;"><strong>中南大学湘雅医院成功实施断肢寄养再植手术</strong></span><br />
</div>
<br />
<br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">科学网 成舸 蒋运高 罗闻报道） 当25岁的小伟带着血肉模糊的断肢前来中南大学湘雅医院求救时，这情景令医护人员都不寒而栗，仿佛置身于恐怖片中。悲剧发生在一个月前，小伟在务工时由于操作失误，导致右上肢不慎卷入钻床机，整个右手瞬间被机器完全离断，顿时血流如注，“当时我都吓傻了……”一股剧痛伴随着恐惧迅速攫住了小伟。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">据小伟的主治医师、湘雅医院手显微外科主任唐举玉教授介绍，此类断肢属于肢体离断最严重的一种，所幸患者的右手、腕关节、肘关节本身未被破坏，采用血管移植联合组合皮瓣移植桥接一期再植接活断肢，从技术层面来看是可行的。难题在于，常温下断指必须在10小时内恢复血供，否则一旦缺血时间过长将无法再植，而此类断肢耐受缺血的时间更短，必须争分夺秒。断肢寄养再植较普通的断肢再植对技术的要求也更高。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">经反复论证，唐举玉为小伟制定了分期手术挽救断肢的方案，即一期先将断肢彻底清创后“寄养”于小腿，待患者寄养肢体成活、全身情况恢复良好后，二期再开展寄养肢体原位回植术。很快，一期手术仅用了半个多小时，小伟的断手便被顺利接到了左小腿上。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">唐举玉告诉记者，就手术难度而言，寄养再植相对简单，更难的是原位回植。由于患者断肢组织水肿且粘连，不仅分离有难度，重建和修复更是有着相当严格的要求。“断肢血液循环的重建、组织缺损的修复和后期的功能重建是寄养再植成功的三大要素。”</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">12月4日，二期回植手术开始。受区清创，血管、神经、肌腱、骨的显露，寄养手解剖分离，骨支架重建、肌腱与神经修复、动静脉吻合，旋股外侧动脉降支嵌合穿支皮瓣切取、皮瓣移植、皮肤移植，受区创面闭合、小腿与大腿供区创面闭合......湘雅专家们在显微镜下有条不紊地“穿针走线”，历经9个小时，红润的右手终于顺利“回归”，“寄养”于小伟左小腿上长达一个月之久的右手终于被成功回植到他的右前臂上。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;line-height:2;">回植手术虽然成功了，但让患者重获一支有感觉、运动功能超越假肢的手，才是医者追求的最终目标。因此，未来还需通过功能康复锻炼和多次功能重建手术才能实现。“这就像盖房子一样，目前只是把主体建起来，未来还要进行内部装修。”唐玉举说。</span>
</p>
