<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯（记者顾泳）昨天，记者从首届“健康心跳，健康心”中国心律失常宣传周活动上获悉：我国每年约有54万人死于心脏性猝死，近九成猝死原因是心律失常。权威专家表示：预防心脏性猝死，医疗机构应及早识别高危人群，并进行科学救治和干预。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">心律失常是指心跳节律或速率发生异常改变，包括心跳过快、过慢或者心跳不整齐。复旦大学附属中山医院心内科主任葛均波教授表示，心律失常是一种常见病。临床数据表明，心律失常是发生心脏性猝死的最主要原因，99%的心脏性猝死患者还未来得及赶到医院，已不治身亡。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中山医院心内科宿燕岗副教授明确，房颤患者、心衰患者、冠心病患者以及有家族史的人群是心律失常的高危人群。大多数心律失常可以通过症状和心电图来明确诊断，患者可选择药物、导管介入术或植入心脏起搏器、体内除颤器等，来达到预防心脏性猝死发生的目的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据悉，首届中国心律失常宣传周由卫生部心血管疾病防治研究中心主办。宣传周将在包括北京、上海等九个城市内，开展免费健康讲座和专家义诊，提高群众对心律失常的自我防治意识，减少心脏性猝死发生率。</span><br />
