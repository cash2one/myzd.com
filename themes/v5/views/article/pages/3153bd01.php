			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>颅内异物伤，孩子伤不起</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我是陈若平，上海市儿童医院小儿神经外科主任。作为一名从业20余年的职业医师，不仅治疗了许多病人，也有了许多故事，有幸福的，有悲伤的，还有惋惜的。希望我的故事能为大家提供些经验，尽可能避免那些令人痛心的故事。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个平常的晚上，我休息在家，我的电话响起，是医院来了急诊外伤病人需要我到医院。于是，我匆匆来到医院。作为一名神经外科医师，我见过许多颅脑创伤病人，但还是惊诧于眼前的情景：一个可爱的三岁小男孩静静地躺在病床上，但其脑门上赫然扎了只金属挂钩。原来趁大人们都在关注自己的事，小男孩溜到桌子上玩耍，结果一不小心从桌子上摔下来，脑门正好撞到墙上的一个金属挂钩上。大人们急忙将挂钩从墙上铲下来，急诊送至医院。经过仔细检查发现，该挂钩已经穿破颅骨进入颅腔，但幸运的是挂钩没有刺伤眼睛，也仅轻微刺伤脑组织，所以孩子的意识还清醒，没有偏瘫症状。由于挂钩已经刺伤脑组织，简单地拔出不仅可能导致大出血，还可能遗留异物和细菌，导致颅内感染，开颅手术，取出挂钩，清洗和处理创面成为唯一的选择。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经过五个小时的紧张开颅手术，挂钩被顺利拔出，彻底清洗了被刺伤的脑组织及周围组织，刺穿的脑膜进行了修复，总之手术非常顺利。手术后经过抗感染治疗，小男孩顺利出院了，恢复了健康。尽管结果令人满意，但这是可以完全避免的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在以后的几个月中，还发生了二起类似的创伤。一个九岁的女孩在玩钩针时不慎摔倒，结果钩针从眼眶沿颅底插入颅腔中。也很幸运钩针未损伤眼球，仅轻微损伤脑组织。经过六个小时的手术，钩针被顺利拔出。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个八岁的男孩抢了支同学的活动铅笔，在追逃过程中不慎摔倒，结果铅笔也是从眼眶沿颅底插入颅腔中，也很幸运孩子没有发生严重损伤。但其早期处理有误，结果活动铅笔头段留在了颅腔内。经过我们五个小时的手术，取出了留在颅腔内的铅笔头，清除了脑组织的血肿，修复了受损的脑膜。孩子顺利出院</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在不到半年的时间内，就遇到了三起颅内异物损伤的孩子，尽管非常幸运，孩子们都恢复了健康，但每每想起这些孩子，总是心惊胆颤的：幸运女神不会总是眷顾；这些损伤是可以完全避免的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">孩子生性活泼好动，又对周围的危险缺乏足够和清醒地认识，因此无论是孩子们的监护人，还是老师，抑或是整个社会，都应该为避免孩子外伤努力。就这三个孩子，如果不在桌子上玩，如果有大人在旁监护，如果不玩尖锐物，如果不拿着尖锐物奔跑，损伤是完完全全可以避免。创伤是导致18岁前儿童死亡和残疾的最主要原因[1]。儿童创伤的年发生率可达200/100000，而且随着全球机动车辆的迅速增加，儿童因交通事故而导致的死亡和残疾率也有明显的增加，发展中国家可达21.5/10000台车辆[2]。同时为了治疗儿童创伤，社会和家庭投入了巨大的资金，美国在2000年时此项金额就高达346亿美元。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">听了我的故事，希望整个社会都能行动起来：保护好孩子，监护好孩子，没有什么比孩子更重要了；教育和培训孩子，建立安全意识和自我保护能力。最大限度地避免孩子受伤。</span><br />
