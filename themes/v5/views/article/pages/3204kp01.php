		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>为啥别人不痛就我痛？</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">三叉神经痛，这毛病听起来不大，治起来却麻烦。经常看见一些患者被折磨得痛不欲生，却始终解决不了自己的疼痛难题。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">南京鼓楼医院神经外科主任、主任医师梁维邦教授告诉我们，为什么有些人没有如此容易出现这类问题呢？其实这得看颅内结构说话，而弄清楚这一点，可以帮助许多患者脱离“痛”海。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">□通讯员 柳辉艳 金陵晚报记者 朱菁菁</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一痛就是十多年</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">都是“颅内”惹的祸</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">梁维邦教授曾经接诊过两个患者。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">其中一位45岁的女性病人，因额面部疼痛7年多，连饭都没办法吃，只能靠进食流质维生；人家吃饭用筷子，她得用吸管；最后来就医时，患者异常消瘦，得靠别人帮忙抬进医院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">另一位患者也是女性，59岁，面部疼痛十几年，药物止痛、打封闭、射频治疗全都试过，每次都是略有好转，不久后就再度复发。因为疼痛无法忍受，她先后三次试图自杀。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">其实这两位患者都是三叉神经痛。这个毛病听起来不算大问题，疼起来也很要命。可为什么临床有些人不疼，有些人就是莫名其妙的疼，还一疼就是十多年呢？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“秘密就藏在患者的颅内。”梁维邦说。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原来，在我们每个人的颅脑内部，分布着大量的神经和血管，其中有不少神经和血管的走向是并行的。一般情况下，这些血管和神经彼此相安无事。可那些不幸患上三叉神经痛的患者，他们的颅内结构有些特殊，部分神经和血管不仅挨得紧，角度还有些别扭，结果就惹祸了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“咱们每个人的心脏都在不停跳动，动脉在向全身输送血液的同时，搏动时的力量也在传递。可这些患者由于颅内血管、神经之间的结构特殊，这种搏动的力量都被传递到了神经上。”更糟糕的是，我们的神经纤维外部有一层叫做髓鞘的保护套，就好像电线外的绝缘套一样保护着神经，可这些患者被撞击的区域，恰好又在中枢神经与外周神经的接口处，是一段没有髓鞘保护的神经裸露区，于是就撞出问题来了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这个学说叫做“颅内神经血管冲突学说”。梁维邦教授告诉我们，这种学说已经得到了核磁共振等影像学检查的证实。它不仅可以解释三叉神经痛的发生，许多面肌痉挛的患者其实也是因为相似的原因才会患病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“5角硬币”空间里搞定</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">微创手术给神经减压</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">既然对病因有所了解，在治疗上也就明确了思路：咱们不是因为神经老被撞、还老被撞在不能撞的地方才生病的么？那咱们把这种撞击的力量给分流掉，帮神经“减负”！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">不过说起来简单，做起来却并不容易。手术都存在风险，何况是神经外科手术，那是和大脑打交道，稍有差池就可能酿成巨大遗憾。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">梁维邦告诉我们，以前传统手术方案中，这样的手术需要在患者头上开一个至少5厘米的骨窗，手术中还要切掉1／3的小脑，这听起来就很恐怖，手术风险当然也不小。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">不过如今就不同了。梁维邦本人在外出学习后，又根据学习成果不断改进，形成了自己一套独特的技术方法。如今这种锁孔显微血管减压术，在手术中只需要在患者耳后打开一个5角硬币大小的骨窗，不用切除小脑，就可以利用显微镜操作，有效分离对颅内神经构成压迫的血管，并通过特殊的“减压”材料将动脉传递过来的搏动力量成果分流。患者手术前不需要剃发，术中出血一般不超过10毫升，大大提高了手术的安全性和准确性，同时也起到了根治疾病的目的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">光是这样，梁维邦还不满足。他进一步探索这类疾病的发生机理，结合各类影像学报告，力求在手术前，就能够通过患者的颅内结构特点，预先评估患者的手术难度、风险，以及术后的恢复情况，然后再根据患者个人情况作出个体化的手术方案，进一步改善患者的预后。目前他们每年都要开展相关手术三四百例，累积已完成超过2000例，同时他们发表的大量研究成果，也获得过省市新技术引进奖等多个奖项，发表了40多篇相关文章，去年相关研究还获得国家自然科学基金。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">不仅能治疗帕金森</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">还用于脑起搏器治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在现有基础上，梁维邦也在涉足脑起搏器治疗领域。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">其实所谓脑起搏器，就是一种脑深部电刺激术(DBS)，它通过在脑内特定区域置入一个细微的电极，释放高频电刺激，从而达到治疗目的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">过去，这个方法主要用于治疗帕金森类疾病。不过梁维邦做了一个大胆的尝试：他利用这个技术，治好了一位年轻姑娘的抽动症。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这个姑娘从6岁时开始，就出现手脚不自主的抽动，不仅动作幅度大，随着年龄的增长，症状出现的频率还变得更加频繁。病情严重的时候，她不仅吃饭能把碗筷甩出去很远，嘴里常发出“呃、呃”的怪声，甚至连思维模式也改变了，整天为一些简单的日常小事而内心纠结，无法控制。虽然患者也接受了诸多治疗方法，但一直收效甚微。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在这种情况下，梁维邦和科室中的倪红斌医生一起，为这个患者实施了脑起搏器治疗，通过电刺激调控，患者不仅控制了身体症状，心情也好了很多，不再纠结于一些小事情，变得豁达大方，逐渐恢复了自信。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“现在发现不少疾病和大脑中某些神经团的病变、异常放电有关，而脑起搏器可以很好地治疗这些疾病。不过脑起搏器的费用比较昂贵，所以这些治疗还不能迅速推广，但对于一些深受痛苦的患者来说，这为我们多提供了一项治疗的思路。” 梁维邦说。</span>
</p>
