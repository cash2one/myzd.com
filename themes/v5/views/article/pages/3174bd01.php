		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>用药丸“拍照” 做胃镜不痛了（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">□晚报记者 肖波 制图 刘京</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “一根长长的管子伸入口中，进入食管、胃内，像翻江倒海一样，恶心呕吐，发誓再也不想做了。 ”这就是做胃镜检查后患者的感受。但是，昨天在第二军医大学长海医院启动的全球最大、技术最先进的消化内镜诊疗中心之一，记者看到了像孙悟空一样变成小药丸潜入人体的“消化道摄影师”，能够发现早期胃癌的窄带成像技术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胃肠道肿瘤发病率高检出率低</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “近年来人们生活水平提高，但生活习惯和饮食习惯却存在不少误区，导致消化道疾病尤其是胃肠道肿瘤发病人数上升，发病者也呈现年轻化的趋势。 ”该中心负责人、消化内科主任李兆申说。胃癌是最常见的恶性肿瘤之一，仅次于肺癌居第二位。我国是胃癌的高发区，每年新发现40万胃癌患者，占世界胃癌发病人数的42%。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 日本人和韩国人由于常年食用腌制的海鲜产品，所以胃肠道肿瘤发病率一直排在世界前列，但是在过去的10年里，由于政府投入和国民的重视，胃镜普及率大大提高，普通人每隔几年就要做一次胃镜检查，这使得胃肠道肿瘤早期诊出率成倍提高。眼下日本的胃癌早期诊出率超过70%，韩国也超过了40%，而中国，甚至还不到10%。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 其实，胃镜、肠镜等传统技术已经老树开新花，窄带成像术、放大内镜等技术提高了胃镜、肠镜对微小病变的诊断能力；内镜下黏膜切除术、内镜下黏膜剥离术能较好地治疗早期肿瘤。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 智能胶囊一次带回上万张照片</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 参观过世博园以色列馆的很多市民都对胶囊摄影技术留下了深刻的印象，就像孙悟空缩小身体，进入铁扇公主身体漫游一样。现在，这种包裹在胶囊内的小摄影机在临床上成为一种先进的胃镜技术，辅助治疗疾病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 被检查者吞服一粒胶囊后，可以照常工作生活，约6-8小时后把胶囊排出体外，就完成了整个检查过程。因为在这6-8小时期间，智能胶囊随着肠胃的蠕动，按照胃——十二指肠——空肠与回肠——结肠——直肠的路线做了一次消化道全程“旅行”，这位“消化道摄影师”拍下成千上万张照片，这些数据最后被医生导入到影像工作站，对患者病情进行精确的诊断。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 麻醉+心理抚慰=无痛胃镜</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “这个胶囊好是好，不过我们每隔两三年就要做一次胃镜复查，有没有便宜一点的胃镜啊？ ”在昨天下午的市民开放日活动上，有市民这样问医生。医生表示，胶囊内镜虽然具有无创伤、无痛苦、无交叉感染等优点，不过由于其采用世界先进的技术和微型摄影器材，目前价格还是偏高。而麻醉内镜则属于价廉物美的胃镜，但又不是每个医生都会做。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 大部分做过胃镜的患者，从手术台上下来几乎可以用“仓皇逃离”来形容，但长海医院内镜诊疗中心副主任医师施新岗每次给病人做好胃镜，患者不仅不会呕吐、痛苦，而且能当场自然地说话。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 施医生透露，秘诀是充分的准备工作、医患沟通和喷射麻醉药，“做胃镜的患者喉咙里被插根管子已经很不舒服，多数人都会紧张，一紧张，更容易气管痉挛、恶心呕吐。 ”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 于是，他和团队里的医护人员每次给患者做胃镜检查前，总要把每一步会怎么做原原本本告诉患者，检查手术开始以后，医生先对着患者咽喉部位喷洒麻药，被麻痹后，胃镜管插入时就不会显得特别难受。但即使这样，医生仍然会在胃镜管上涂抹像牙膏一样的麻醉剂，起到润滑和麻醉作用，再加上医生小心翼翼的操作以及每次操作前都跟患者“打招呼”，这样，持续半个小时的胃镜术舒适感就大大提升。</span>
