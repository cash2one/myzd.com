	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>喉癌科普问答</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一.&nbsp;&nbsp;什么是喉癌？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉癌是指发生于喉腔内的恶性瘤子。喉癌由于早期可出现声音嘶哑或咽部不舒服的状况，故多数易早期发现，是一组治愈率较高的癌症。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉癌主要发生在50-65岁的人群，其中男性明显多于女性。喉癌发病率在我国上海为3/10万，辽宁省部分市为5/10万。即每年全国约有4万人得喉癌。由于喉是人的发音和呼吸器官，同时起吞咽保护作用。过去治疗主要强调根治癌症，随着治疗水平提高，目前则可达到根治癌症同时，保留喉的功能。“让每一个喉癌患者都能讲话”已在一些大的治疗中心成为现实。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二.&nbsp;为什么会得喉癌？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">科学的讲，喉癌发生的真正原因尚不完全清楚，但抽烟已基本上肯定与喉癌发生有关系，在医院发现的喉癌患者几乎均为吸烟者。每天吸烟次数多与烟龄长的人患喉癌的机会就大。据统计，吸烟者患喉癌的危险是不吸烟者的3—39倍，重度吸烟者喉癌死亡率是不吸烟者的20倍。戒烟者的危险度有下降。又据调查喉癌与饮酒也有关，但同吸烟相比，饮酒只是较弱的相关因素，排除吸烟的危险度，只饮酒不吸烟的人患喉癌的危险度同不饮酒的人相比，危险度是1.5-4.4倍，长期抽烟加上嗜饮白酒的患喉癌机会更多。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">有人会问，抽烟的人为什么并不都得癌症。我们讲抽烟只是外因，内因是人不同的易感性，现已发现一些癌基因和抑癌基因与喉癌有关。相信在21世纪，科学家能通过分析每个人的基因来预测容易得喉癌的危险人群，有针对性的预防，收到事半功倍的效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三.&nbsp;&nbsp;喉癌有那些表现，如何早期发现？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">早期主要有声音嘶哑和咽部不适，如吞咽不适，咽部阻挡感，食后咽部异物感。稍后有的人会感觉到颈上份出现肿块。后期症状有喉内出血，呼吸困难，吞咽困难，喉头明显肿大。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">医生首先用喉镜（间接喉镜、光导纤维喉镜）检查，可看见病变。一般需要行肿瘤活体检查，即喉内喷上麻醉药后，夹取一小块瘤体做切片化验，3-5天后可以明确诊断。此外，医生还要做颈部触诊，检查颈部是否有肿大淋巴结，以确定颈部有无转移。根据情况，进一步要拍喉的X 照相，CT或MRI扫描，目的是为了解喉癌的大小和准确位置。作为治疗前准备，医生还要开出胸片，血液化验，心电图等检查。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉癌的早晚期分法比较复杂，而且国际上每隔几年更新一次，你不必仔细了解，向你的医生询问即可。大体上讲，综合喉部肿瘤大小和颈部淋巴结转移情况，分为I，II，III，IV期。I，II期称为早期，III期为中期，IV期为晚期。早期治疗效果很好，中期次之，晚期效果较差而且保留喉的可能性很小。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">四.&nbsp;&nbsp;治疗喉癌有那些方法，我该如何选择？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉癌的有效治疗至今仍为外科手术与放疗（俗称烤电）。化学药物治疗单独应用无根治效果。中药到目前为止尚无十分肯定的疗效。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">单一应用放疗或手术治疗，从治疗后5年生存率比较，早期喉癌国外文献报告两者疗效相同，均在95％左右。但国内资料仅外科治疗可以达到这一水平；国内几个医院放疗治愈率均在70－80％左右，原因不详[1]。较早期（II期）以上的喉癌放疗治愈率逐步不如手术治疗，差别很大。从根治目的出发，II期以上喉癌，应首选手术治疗。近年来激光手术治疗喉癌，避免了从脖子开刀，但主要限于早期喉癌。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术配合放疗的效果意见不一。多数医生认为与单纯手术相比，并无优越性。但很晚期或复发的喉癌，应用术前或术后放疗，则有希望提高治好的几率。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">从治疗后生活质量来说，放疗和手术各有优缺点：放疗控制癌症后发声效果佳，但咽喉部干燥不适，有的出现甲状腺功能低下，治疗所需时间长，约2-3个月；手术有一定创伤，手术后发音效果差别较大，有的近乎正常，有的则会失去发音功能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">五.&nbsp;&nbsp;喉癌的手术方法有那几种？术后我是否还能讲话？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">简单讲，喉癌手术分为喉全切除术和喉部分切除术。外科医生根据检查结果，手术前大致可判断手术方式，但最终要在手术台上根据术中所见来决定。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉全切除术已有100余年历史。由于喉全切除术后患者要由颈部气管造口呼吸，口腔没有气流，不能发声语言，对患者生活造成很大困难。几十年来很多喉科专家想方设法，发展了多种喉部分切除术，可以在术后基本上保留喉功能，使患者能讲话，恢复正常生活，回到社会。喉癌手术治疗目的与手术成功标准在于治疗后的高治愈率和相当的生存质量。保留喉功能的喉部分切除术，治愈率并不低于喉全切除术，易于为患者接受。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在各医院喉癌部分切除术手术数目差距很大，有的报告喉部分切除术占全部喉癌手术的75％，但有的只有5％。这由该单位医师对喉癌的认识和手术技术水平所决定。根据目前情况，一个省市级医院的耳鼻咽喉科，喉部分手术应占全部喉癌手术的50－70％，5年生存率应达到70％。从生存质量看，没有长期进食呛咳；气管套管的拔管率应在70－80％；大部分患者术后语言应达到应用水平，在社会上交流无困难。这一要求是经过努力可以达到的[2]。喉部分切除术至今没有得到医师们充分应用，国内外不少喉科医师仍热衷于做全喉手术。主要是两个原因：一是长期受旧有概念影响，不分场合的应用“肿瘤外科手术要广泛切除”的原则，怕手术范围缩小会造成肿瘤复发；二是喉部分切除术在手术技术上要求较高，全喉手术比部分喉手术好做，外科医师有一个熟悉过程，而且需要足够病例的支持医师技术的锻炼。国内头颈外科及耳鼻咽喉科医师，近年来在改革开放形势驱使下，学习国外经验，努力实践，在喉癌喉功能保留手术上已做了不少工作。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">对一些晚期喉癌、喉内已全部为肿瘤所占、并侵至喉外的患者，目前唯一的治疗方法仍为喉全切除术。喉全切除后如何恢复语言，目前有很多方法，需要医师的指导。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">六.&nbsp;&nbsp;什么是颈清扫术，对我术后会有什么影响</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">颈清扫术就是将一侧或双侧颈部淋巴结全部清扫除的手术，包括已有转移的淋巴结和有可能转移的淋巴结。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉癌颈部淋巴结转移率较高，有的类型（声门上型喉癌）转移率可达56%-62%，两侧颈部淋巴结均转移的情况也很常见。即使颈部没有发现淋巴结肿大，而实际上隐藏的转移一般也在30%左右。因此需要做预防性颈清扫术。过去传统的颈清扫术叫根治性或改良根治性颈清扫手术，要同时切除颈部一些重要的结构，如胸锁乳突肌，颈内静脉，颈外静脉，副神经，颈丛神经。对患者造成生理损伤，尤其是双侧清扫手术损伤明显。可出现颈部（包括耳朵）麻木，面部长期肿胀，抬肩困难，颈部外观凹陷。近十多年来，国外逐渐开展起新的局限性颈清扫术，手术保留了上述颈部所有重要的结构，患者术后除了遗留不甚明显的沿皮纹的手术刀痕外，并无其它明显功能损害。国内少数医院也开展了这一新的手术。临床资料证实只要不是广泛的淋巴结转移，局限性颈清扫术根治效果与传统的颈清扫术相似[3]。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">资料显示，喉癌治疗失败的主要原因为颈部转移，主要出现在颈清扫的对侧颈部，病期越晚转移率越高[4]，有的类型（声门上型喉癌）双颈转移的潜在可能性大，应做双侧保留功能的颈清扫手术，如局限性颈清扫术或改良根治性颈清扫手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">七.&nbsp;喉切除术后有那些生理改变，如何适应和锻炼？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（一）喉全切除术：一些晚期喉癌，目前唯一的治疗方法仍为喉全切除术。除了不能发声及语言功能外，由于喉全切除术后患者要由颈部气管造口呼吸，口腔和鼻腔没有气流，因此失去了大部分嗅觉功能，即闻不到气味，影响食欲和消化功能。可服用帮助消化药物，吃一些刺激味觉的食物。此外，由于缺少声门的关闭功能，不能屏住气，爬高和抬携重物吃力；大便易干燥，可吃通便的药物或食物。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（二）喉部分切除术：包括约10种不同的手术术式，医生根据肿瘤的部位和范围来决定喉组织保留的多少，从简单的声带切除到喉次全切除[5]。因此造成的生理改变严重程度不同。一般来讲有以下一些改变</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1．&nbsp;&nbsp;发音：手术后发音效果差别较大，主要由喉部分切除手术术式决定。好的近乎正常，差的要在耳边才能听见。但大部分患者术后语言可达到应用水平，在社会上交流无困难。另外，术后讲话有一个恢复和适应过程，一开始由于喉内炎性水肿和戴气管套管，发音的量小，清晰度不够。应尽早和积极锻炼说话，3个月左右一般可达到应有的发音效果。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2．&nbsp;&nbsp;呼吸：由于喉内手术后水肿，绝大多数患者手术的同时要做气管切开，术后通过放入的气管套管呼吸一段时间，鼻腔暂时没有气流。这种状态从数天到数月不等，医生根据堵住气管套管后患者是否能正常呼吸24小时以上，决定拔除气管套管的时机。气管套管拔除后留下的颈前伤口一般可自行愈合，少数人需要做一个小手术来关闭伤口。需要说明的是，并不是所有患者都能取掉气管套管，原因一是手术后喉腔过于狭小，不能堵住气管套管后正常呼吸；另外长期进食呛咳也是主要原因之一。但总的来讲，气管套管的拔管率在70－80％以上。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3．&nbsp;&nbsp;进食：喉部分切除术后，喉的括约功能暂时失控，所以术后都会有不同程度的误吸，会厌切除术后尤为明显。但此症为暂时性，锻炼后基本都能逐渐适应，症状消失。应向患者进行解释，开始宜进较粘稠的食物，小量多餐，逐步适应。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">八.&nbsp;我是否适合做激光手术治疗喉癌，激光治疗有何好处</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">主要采用CO2激光，由于组织吸收激光能量迅速和完全，数毫秒产生蒸发，从而达到气化、切割、凝固的作用。CO2激光止血作用好，术后水肿轻，不必做气管切开和放置鼻胃管，避免了颈部手术疤痕。应用于早期喉癌，省时又较经济，符合现代“微创”外科原则，越来越得到重视，适应症有逐步扩大的趋势。激光治疗后的患者生活质量较高，发音质量好。与单纯放疗相比，节约了时间和费用，免除了放疗的损伤和并发症，发音质量与放疗后患者无明显差异。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">适应于早期声门型（T1和T2a）和声门上型（T1-T2）肿瘤，肿瘤治愈效果与传统手术类似。但声门上型喉癌由于颈部转移可能性大，颈部仍然要手术。适应证应当由有经验的医生严格掌握。虽然美国等西方国家此项技术已成熟，目前国内仍只在几家大医院开展激光手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">九.&nbsp;手术后护理应注意事项</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术后主要是气管切开口及安放的套管护理，护士会帮助和教会你及家属护理事项，以便你回家后能自理。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（1）吸痰及更换气管套管内管：术后气管内分泌物增多，特别吸烟者及慢性气管炎者。利用吸痰器产生的负压，经常吸出气管内的痰，气管套管的内管至少每日刷洗更换4次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（2）保持气管造瘘口伤口清洁：每日用盐水纱球清除附着于创面上的血痂，痰痂，争取近早愈合。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（3）术后室内空气应保持90%以上的湿度，以防止肺内感染及气管内痰液浓缩结成干痂，特别在北方冬天空气干燥时，干痂阻塞气管套管导致呼吸困难。可采用加湿器或蒸汽吸入方法，并定时通过气管套管滴入数滴生理盐水。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（4）气管造瘘口周围出现的不健康肉芽时，应及时剪除以利于伤口早期愈合。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十.&nbsp;放射是如何治疗喉癌的？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">放疗适应症：（1）I 期病变，特别是声门型I期病变，可首先选择放射治疗。（2）喉癌侵及范围较广时，为配合手术根治可行手术前或手术后的放疗。（3）因故不能手术治疗者可采用放疗姑息，解除痛苦，延长生命。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">放疗机器主要选用60Co治疗机，直线加速器。利用其发出的放射线杀灭肿瘤细胞。医生在你的脖子上画上框，代表放疗野的范围，一般为8x6cm2-13x11cm2面积不等。喉癌放疗的适宜剂量为6000-8000cGy，每周5次，每次照射约几分钟。共需6-8周。喉癌治疗过程中可出现颈部肿胀，喉头水肿，嗓子疼，声音嘶哑加重，口干咽干等均属正常。治疗结束后多数症状可逐渐缓解。放疗结束后应勤复查。如肿瘤复发及时发现，还可通过做手术挽救，但一般要将喉全部切除。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十一.手术并发症有那些？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">各种并发症的发生主要与患者身体状况及个体差异有关，另外与手术大小，放疗与否，外科医生经验，设施好坏均有关系。发生率10%-30%不等。下面是一些常见的并发症。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1感染：&nbsp; 有颈部伤口感染和喉的感染。术前放疗、贫血及糖尿病的患者容易发生。感染的处理是切开引流，清除坏死组织以及异物。一般经过一段时间换药，感染创面会自行愈合。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2伤口皮肤坏死：伤口皮肤坏死可表现为刀口交叉处皮肤发黑，由以下原因造成：1）伤口缘张力过大；2）切口设计不当，引起远端血运不足；3）粗暴手术操作；4）高剂量放疗；5）其它如血肿，感染等因素。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">皮肤坏死的处理根据轻重，可采取清创换药，植皮，各种皮瓣和肌皮瓣修复，要视坏死面积大小，医生经验和医院条件而定。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3咽瘘&nbsp; 是喉手术中最重的并发症，因为咽瘘造成的生理损伤最重，患者不能经口进食，要长期依赖鼻胃管喂养。唾液和分泌物易向下流入气管，造成肺炎，咽瘘还可引起颈部大血管暴露，不愈合，造成颈总动脉破裂，大出血死亡的危险。经多因素分析，咽瘘发生与以下因素有关：1）肿瘤范围大，粘膜切除范围广，缝合后有张力；2）肿瘤未切净；3）营养状况差，如贫血，术前体重明显下降；4）以前曾放疗过。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">咽瘘多出现于手术后3周以内，迟发性的咽瘘要考虑肿瘤复发所致。最初表现为咽部周围皮肤发红，继而有波动感，打开颈部伤口后见到唾液。轻度咽瘘，一般引流换药后可愈合。严重伴感染坏死的咽瘘，需通畅引流和清理伤口。不愈合的咽瘘可考虑手术修复，时机因人而异，咽瘘发生后1-6个月不等。咽瘘修复视情况选用颈部皮瓣，或胸大肌皮瓣修复。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4气管造瘘口狭窄&nbsp; 发生于全喉或近全喉切除术后，主要因为造瘘时气管粘膜与颈部皮肤吻合不严，或吻合处张力过大，引起瘢痕生长；术后环行瘢痕收缩，与个体瘢痕体质有关；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">处理：轻度的狭窄可采用逐渐增加喉管直径的方法，但去除喉管后，狭窄大多会复发。应采用手术扩大造瘘口的方法。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5．喉狭窄&nbsp;&nbsp; 发生在部分喉切除的患者，发生原因：1）早期喉狭窄是由于声门创面肉芽组织形成。数月后自然消退；2）长期喉狭窄，患者多有放疗和喉感染史，引起软骨坏死和粘膜缺损，造成喉蹼形成，喉腔瘢痕或缺乏足够的喉软骨支架。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">治疗 早期肉芽引起的狭窄可用激素预防以后瘢痕形成，待肉芽消退后可以尝试拔除气管套管。单纯的喉蹼，最好用CO2激光烧灼，也可再次手术放置喉支架或喉模扩张。复杂的喉狭窄修复很困难，应十分慎重。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6．进食呛咳&nbsp;&nbsp; 喉软骨切除后，喉上神经切除或损伤，声带关闭不良等原因，均可导致食物误入气管引起呛咳，严重可导致吸入性肺炎。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">治疗：早期的误吸可锻炼克服。进食前深一口吸气，用手指盖气管套管口以便能憋住气，然后分吞咽小团软食，咳嗽后再吞咽。软食较液体更易吞咽。大约85-90%的患者经过进食训练能恢复正常进食。不能恢复进食的患者只能外科手术，有严重吸入性肺炎的需要做全喉或近全喉切除术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十二.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全喉切除术后的无喉患者如何重新获得语音功能</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉全切除术后，患者丧失了说话和经鼻呼吸的功能。为了恢复无喉者的语言功能，目前已有多种重建语音的方法应用于临床。这些方法归纳起来，大致可分为：手术发音，咽食管发音和配带人工喉3大类。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;手术发音</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">临床主要有气管食管瘘发音重建术，是在气管和食管（或下咽腔）之间，形成一个通道，使呼气所产生的气流经此通道进入食管或下咽腔，冲击粘膜而发音。再经过舌，腭，唇，齿等构音器官的协调作用而构成语音。术后说话一般不需特殊训练，音质音量均可达到近似正常发音的程度。缺点是要定期更换人造发音钮。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">可于全喉切除手术同时做I期气管食管造瘘，或手术后II期在表面麻醉下进行。于颈前气管造瘘口后壁处穿刺入食管腔，插入硅胶管，大约三周时间形成瘘道后，安放人造发音钮。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;咽食管发音</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 为不需借助工具或手术而恢复发音的方法。其基本机理为利用食管储存一定量空气，借助胸内压力，如同打嗝一样，将空气从食管内逼出，冲击食管上端或咽部粘膜而发音。这种咽食管发音法需经过一段时间的训练，逐渐掌握自如。因此，必须使患者增强信心，以促使积极练习。常用效果较好的练习方法为在吸气时利用食管内负压，并通过舌向后方运动，将空气压入食管，然后练习腹肌收缩，使膈肌上升，增加胸内压力，压缩食管，将空气由上口排出而发音。成功率在90%以上[6]。最快者几天即可掌握要领，发出简单语言。一般通过2—3周训练，绝大多数都能掌握。有的可达到相当理想的程度。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 本法优点为：音色较好，接近人喉发出的声音。无电子喉，机械人工喉不悦耳的怪声；不需任何设备，工具，张口就能说话，所以十分方便。缺点为声时短，每次发音说话的字数较少，连贯性较差，一句话常须停顿几次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;配带人工喉</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（1）电子喉&nbsp;&nbsp; 用简单的电子装置发出持续的蜂鸣音，将此装置贴于无喉者的颏部或颈上部，使声音从口腔传出，即可构成语音。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 电子喉的优点为使用方便，不需特殊训练即可说话，而且声时长，联贯性强，可作长篇发言。在电话中声音反而更为清晰。缺点是语音和人喉的发音有较大差别，听来有“怪音”之感，甚不悦耳。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（2）人工喉&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 其原理是将呼出的气流引出，冲击橡皮膜使之振动而发音。这种声音从口腔传出，即可构成语音。最简单的人工喉可以用一根粗橡皮管来代替，一端接气管造口，另一端经一侧口角插入口腔后部，稍加练习即可发出比耳语稍响的声音。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 人工喉的优点为声时长，吐字尚清晰，可作长篇发言，在电话中也很清晰。缺点也和电子喉一样，是一种与人声不同的怪声，听来很不悦耳。另外，使用上亦不甚方便。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十三. 喉癌能够治好吗？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">喉癌能够治好，而且与其他癌症相比，治愈率较高。所谓治愈，就是终身不再复发和转移，不影响你的正常寿命。在医学上，为了便于总结，通常采用5年无瘤生存率来代表治愈。因为喉癌复发和转移多发生在治疗后的2年之内，5年后再复发和转移的情况极少见。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 总的来讲，接受正规根治性治疗的喉癌治愈率在50-70%。但肿瘤的早晚期不同，差别会很大。如早期喉癌治愈率可达到90%，中期约50-60%，晚期只有30%左右。因此，早期诊断，早期治疗是提高治愈率的关键。需要强调的是治疗的手段一定要正确，否则延误了病情，其效果较差。最常见的例子是不少喉癌患者不是立即接受手术或放疗，而是服用长达数月的中药，结果耽误了有效和恰当的治疗，失去了保留喉和根治的机会。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十四.&nbsp; 我的家人和朋友如何预防喉癌的发生？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">首先，喉癌肯定不会传染，家人和朋友不必有任何担心。戒烟和控制污染可能是有效的方法。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一个正常细胞转变成癌细胞的过程相当漫长，一旦发生则不容易逆转。因此最好在年轻时就戒烟，患喉癌的危险才下降接近正常。中年以后戒烟，虽然能降低患喉癌的可能性，但仍然高于非吸烟者。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">控制大气污染固然重要，但防止室内污染更为有效。我国东北地区喉癌发病率明显高于其它地区，一个重要的原因可能是烧煤取暖造成的室内污染。因此，保持房间内新鲜空气流通可能有助于预防喉癌的发生。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">饮食方面还没有证据发现某种食物会诱发喉癌，或者能防止喉癌的发生。所以不必忌食。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">化学药物预防肿瘤的研究正在进行，还没有明确的结论。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十五.&nbsp; 患者治疗结束回家后，应注意些什么？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1. 复查：喉癌复发和转移多发生在治疗后的2年之内，因此要警惕肿瘤复发，勤到医院复查。最好去接受治疗的医院检查，因为那里保留有完整的病历资料，又有熟悉你的病情的医生。当然，如路途遥远，也可就近医院复查，但别忘了带上治疗医院出具的病情介绍。另外，大的肿瘤医院有正规的随诊信制度，每年会给你寄去一封病情调查表，请你如实填好后寄回。复查的时间一般为出院后的第3个月，第6个月，第12个月。2年以后每间隔一年复查一次。当然，如有异常情况，应随时去医院检查。比如出现颈部肿块，脖子肿胀不减轻反而加重，声音嘶哑加重，呼吸困难，咳血，口腔臭味等。复查的内容主要有喉镜检查，颈部触诊和胸部X片。有时还要做B型超声波和CT扫描。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2．护理：喉全切除术或喉部分切除术后暂时未能拔除气管套管的患者，应注意气管瘘口套管的护理（见手术后护理应注意事项）</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3. 心理健康：在心理上要保持平衡，避免闭门独自在家，要积极参加工作和社会活动。一是体现了现代医学治疗目的，即治好疾病的同时，保证患者生活质量；二是转移了心理压力，有利于康复。此外，找心理医生治疗也很有帮助。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4.语音康复：喉全切除术患者应尽早恢复发音功能（见全喉切除术后的无喉患者如何重新获得语音功能一问）。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5. 喉癌复发的挽救治疗：要是能及时发现喉癌复发，仍有治愈的机会，但是与第一次治疗相比，总的治愈几率减少。主要依靠手术挽救治疗，并辅助以放疗。手术切除的范围较大，保留喉的可能性很小，多数要做喉全切除术，甚至下咽，食管和气管切除。留下大的缺损还必须进行同期手术修复，如利用自己的空肠，胃或肌皮瓣修复。如复发后发现太晚，比如累及颈部大的动脉血管，出现肺和骨转移，则失去了根治的机会，只能姑息治疗。</span>
</p>
