		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>南医大王学浩团队探索出“管控”人体免疫功能新方法</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">患者即使在器官移植手术成功之后也有可能因为排异反应而丧命。日前，一项令人振奋的研究攻克了这一难题。南京医科大学王学浩院士团队在最近一期的《科学》杂志子刊《科学·转化医学》上，撰文介绍了一种新方法——从接受移植者自身提取出一种细胞，在体外培养之后，再注入患者体内，可以让患者免疫系统和新植入器官和平相处，避免排异反应。该技术已获国家发明专利。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">南京医科大学一附院肝移植中心吕凌教授介绍说，人体的免疫系统中存在着一种调节性T细胞，其作用相当于“宪兵部队”；还存着一种效应性T细胞，相当于“战斗部队”；“宪兵”可以管控“战斗兵”。“可是人体天然的调节性T细胞数量太少、功能不稳定，而且并不能聪明地识别移植器官这样有益的‘外来户’。”吕凌说。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147001920412445"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">由此研究人员引入DC细胞，相当于“侦察兵”，先侦测到器官提供者的抗原信息，再把“侦察兵”和“宪兵”放在一起体外培养，“侦察兵”就会将器官提供者的抗原信息传递给“宪兵”，即培训出能执行医生意愿的“聪明宪兵”，将其注入患者体内，再遇到移植器官时，就会通知“战斗兵”不要开火。然而“宪兵”并不是越多越好，注入这种调节性T细胞量有严格的控制。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">此外，经培训的“宪兵”在人体内还具有“教育”功能，它们能够“吸收发展”一些其他种类的细胞加入自己的队伍，培养“新一代宪兵”，“老一代宪兵”生命周期结束后，也能实现患者自身的免疫系统的新平衡。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">吕凌介绍，目前，南医大一附院肝移植中心正在进行健康志愿者实验，结果显示该群细胞具有很好的安全性。这一研究的医学应用前景非常广阔。“除了解决排异反应带来的问题，我们还可以将这种方法用于治疗许多自身免疫性疾病，例如红斑狼疮、Ⅰ型糖尿病、关节炎等。”吕凌说。</span>
</p>
