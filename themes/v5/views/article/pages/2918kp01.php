<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">ICL 常见问题详解</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问1： Visian ICL手术安全吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术是微创的近视治疗手术，全世界已经有600,000患者接受了ICL植入手术。在美国FDA的临床试验中，超过99%的患者对手术效果满意。像欧洲宇航员和飞行员都可以接受这项手术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问2： Visian ICL手术与其他近视激光手术的区别在哪？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术的切口只有3mm，由于手术无需切削角膜，对高度近视和薄角膜的低中度近视的治疗效果尤为明显，而且术后视觉效果也是非常完美，眼睛干的症状会更轻。ICL术后患者极少发生不良反应，一旦发生，可在必要时取出和/或更换晶体。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问3： Visian ICL手术痛苦吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术过程是无痛的，术后4个小时内可能会有流泪，异物感，4小时后就没有什么不适的感觉了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问4： Visian ICL手术会反弹吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答： ICL手术和近视激光手术不同，不需要去除或破坏角膜组织，保持了眼球的完整结构，回退的几率很低。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问5： Visian ICL手术后多长时间可以工作？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术后第二天即可看清，但是术后第一天应保持适当休息，第二天可以上班，术后早期会有看近处不适感，一周即可恢复。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问6： Visian ICL手术后多久可以游泳？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术后一月即可游泳，但需要佩戴泳镜，避免池水污染。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问7： Visian ICL手术后多久可以坐飞机？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术后一周就可以坐飞机。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问8： Visian ICL手术可以同时矫正近视和散光吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：可以。ICL可全矫-0.5D—-18.0D的近视和-0.5—-6.0D的散光。ICL晶体分为不加散光的ICL晶体和加散光的TICL晶体。术前散光低于-0.75D选择ICL晶体即可达到满意视力，散光超过-1.50D需要选择TICL晶体以纠正散光，散光位于两者之间可根据患者要求而选择晶体类型，考虑到TICL晶体位置可能旋转等原因，对于该区间散光患者，医生一般建议选择ICL晶体亦可达到满意效果。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问9： Visian ICL手术时间需要多久？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术时间一只眼睛十分钟即可完成。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问10： Visian ICL手术后视力可以恢复到多少？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术后视力即可以恢复到术前的最佳戴镜视力，恢复时间会因个体差异有所区别。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问11： Visian ICL手术后会有眩光吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：如果患者瞳孔大小较正常，一般没有这方面主诉。一些眼睛大瞳孔大的患者，在术后早期可能夜间有眩光表现，一般术后半年左右会适应而消失。一般不会影响到正常生活。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问12： Visian ICL手术后有何复诊要求？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：建议ICL术后第二天、第一周、第一月、第三月复诊。ICL手术患者多数是高度近视，高度近视患者建议每半年到一年复查眼底，并观察ICL晶体情况。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问13： Visian ICL手术后可以生育和顺产吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术对生育没有任何影响，但是术前术后需要滴用眼药水，眼药水对生育会有影响，因此建议术后停药一个月后再做怀孕计划。至于能否顺产跟ICL手术没有太大关系，而是取决于患者的近视度数，若患者术前近视超过六百度，又合并有高度近视的眼底病变，就有剖腹产的手术指征了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问14：哺乳期可以做Visian ICL手术吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：哺乳期后一个月可以行ICL手术，哺乳期间不可以。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问15： Visian ICL手术后老了还可以做白内障吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL植入于后房，位于虹膜之后，自身晶体之前。到老了长出白内障时，只需在白内障手术时将ICL晶体取出，再行白内障摘除联合人工晶体植入手术即可。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问16： Visian ICL手术对眼底有影响吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">答：ICL手术前会对眼底进行详细评估，手术不涉及眼底，对眼底没有影响。</span><br />
