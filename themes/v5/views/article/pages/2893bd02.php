	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;传统搭桥手术是在体外循环下进行的，心脏需要停止跳动。而传统的搭桥材料是用左侧乳内动脉和腿上的大隐静脉。大隐静脉远期的通常率约60%，不如乳内动脉。为了减少手术带给病人的创伤，并最大程度改善术后远期效果。、</span>
