			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何减少术后并发症？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在手术前，病人及家属问的最多的问题大概是：医生，我们这个手术有风险吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">的确，接受手术的患者可能会在术后发生并发症，而这些并发症不仅会加重病人痛苦、增加治疗费用、延长住院时间甚至还会导致死亡，因此，术前对这些风险进行评估非常重要，然而目前并没有统一的方法对患者进行术前风险评估。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近日，伯明翰大学医学院的外科团队设计了一种简单易行的方案来预测术后并发症——测量患者爬楼梯的时间。研究报告发表在《美国外科医师学会杂志》（J Am Coll Surg）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这些医生收集了342名普外科择期手术的患者来开展试验，其中包括88名结肠手术患者，58名胰腺手术患者，41名胃食管手术患者，38名肝脏手术患者，34名小肠手术患者，30名腹膜后手术患者，以及53名其他类型手术患者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在这次测试中，他们使用的是单阶楼梯，让患者踏上去再下来，往复7次，记录患者所消耗的时间。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">结果发现，在测试中用时少于15秒的患者，其并发症发生率很低，小于5%。花时超过25秒的患者，并发症发生率高于55%——也就是有超过一半的几率会发生并发症。花时间少的患者，其住院时间延长也少，而花时间多的患者可能需要多住3天院。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该项研究表明，爬台阶所花的时间是独立的、不受其他因素影响的、且非常强力的预测术后并发症发生率的要素。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">好吧，如果您或者您的家人即将要接受手术治疗，不妨找一级台阶做一下测试，看看15秒内能否上下7次。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果您不需要做手术，也可以试试看，因为在一些体能测试中，台阶测试是检验心肺功能的常用手段。</span>
</p>
