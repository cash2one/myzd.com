<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼球震颤</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">童双眼不能固视，不能同时注视目标。其中一眼偏斜，则出现复视。部分斜视儿童能形成单视，但已不再使用斜视眼，则形成弱视。有时一眼不停地震颤，或当疲</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">劳、患病、看近侧物体时震颤。婴儿的双眼常会出现震颤，但若2-3月大时震颤仍存在，则应看眼科医生了。治疗措施包括遮盖法、滴眼水、配镜和进行手术等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">视力障碍</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">某些儿童不能看清远处的物体（近视）或是近处的物体（远视）。若小儿眼角膜表面曲率不规则（散光眼），则任何距离的物体都是模糊的。以上常见的儿童视力障碍通常可配镜、配戴角膜接触镜等予以矫正。若未进行有效矫正，则可导致弱视。</span><br />
