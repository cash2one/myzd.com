			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>长期便秘，直肠癌高发</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">调查显示，中国每年新增结直肠癌患者40万，增长率近5%，远高于全球平均水平。在日前赛诺菲主办的全国肿瘤防治宣传周结直肠癌专题专家采访会上，中山大学附属肿瘤医院主任医师潘志忠教授表示，我国目前的结直肠癌治疗水平不容乐观，与发达国家有较大差距，美国的结直肠癌患者五年生存率达到65%，而我国仅47%，结直肠癌防治工作相当严峻。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp;高危人群需及时筛查</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据潘志忠教授介绍，早期结直肠癌的治疗效果很好，有统计显示，大约90%的患者患者经过手术治疗可实现临床治愈，死亡率也会大幅度降低。“为什么我国结直肠癌的治疗效果会比美国差那么多，很直接的一点就是目前就诊的患者中，早期结直肠癌患者发现率不到10%，而美国早期结直肠癌诊断率可达25%”，潘教授表示对结直肠癌高危患者进行筛查工作十分关键，通过筛查发现癌前病变并将之处理，便可终止结直肠癌的发生。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我国结直肠癌的高发年龄在40岁左右，家族有肠癌、肠息肉病史、患有消化道症状（如慢性腹泻、慢性便秘）或有慢性胆囊切除史、慢性阑尾炎病史的人群全部都是高危人群，是主要筛查的对象。据了解，从今年开始广州市将在50岁以上人群中逐步推广结直肠癌的筛查工作。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 结直肠癌专科体检主要包括肛门指检和大便潜血实验两项，潘教授表示，肛门指检非常重要，对于诊断直肠癌十分敏感，患者误诊往往都是因为没有做肛门指检，目前我国直肠癌误诊率高达70%左右，患者有出血症状时才就医，医生没做肛门指检便简单判断为痔疮，从而造成误诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<strong>治疗手段须规范化</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 潘教授表示，结直肠癌的治疗手段不规范也是导致我国与国外治疗效果相差甚远的原因之一。治疗包括外科手术治疗和术后化疗两方面，手术切除、淋巴结清扫、化疗、药物使用等都是影响治疗规范的关键。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 治疗有哪些不规范？“早期的结直肠癌患者术后临床治愈，本无需化疗但却继续化疗，这就属于过度治疗。三期的患者需要化疗，但未进行化疗或药物剂量不足便是治疗不足，另外还有一些辅助类靶向药物的不合理使用，靶向药物一般用于早期治疗才有效，根治手术后不应使用这些药物，这些都是治疗不规范，但却常常出现”，潘教授介绍道。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 针对上述治疗不规范的问题，潘教授表示治疗结直肠癌要进行多学科综合治疗，这种诊疗方式能够规避不规范治疗，可将患者的五年生存率从一般的5%提升到25%。多学科综合团队的会诊制度，称为MDT，多个学科的医生共同讨论病情，可克服专科医生对专科以外知识的局限，对病情的评估更准确，根据患者肿瘤的生物学行为、分期、经济情况等制定出个体化的治疗方案，是现代肿瘤治疗的最佳模式。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<strong>多吃粗粮防止便秘</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 潘教授认为，便秘也是结直肠癌的一种高危因素，长期便秘，致癌物质未得到抑制并在肠道长期停留，可能也会促进肠癌的发生，注重饮食习惯，尤其要多吃粗粮，对于预防结直肠癌十分关键。粗粮中富含纤维素，可稀释结直肠癌的致癌物质浓度，促进致癌物的排泄，抑制抗癌物的产生，对肠癌的预防起到一种保护作用。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 饮食要注重“加减法”并用。“减法”是减少摄入高动物蛋白、高脂肪的食品，减少食用精粮，减少腌制、烟熏类食物，控制烟酒，控制体重。“加法”则相反，要多吃新鲜的蔬菜、水果，多食用粗粮，多吃维生素D、钙，有研究发现维生素D可以提高结直肠癌患者存活率。</span><br />
