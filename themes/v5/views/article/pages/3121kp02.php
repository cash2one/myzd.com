			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>认识骨科运动医学：更小损伤，更好恢复</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骨科运动医学是一门新兴的临床学科，专门研究人体运动系统急慢性损伤的防治，包括肌肉、肌腱、关节、韧带、软骨损伤等。由于这类损伤好发于专业运动员或业余运动爱好者，我们常常将这类疾病称为“运动损伤”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 然而这类疾病并不一定发生在运动人群，可能在车祸、摔跤等意外伤害中出现，如交叉韧带损伤、半月板损伤、肩关节脱位等;也可能由于漫长人生道路中的反复磨损引起，因而也常见于老年人，如肩袖损伤、半月板损伤、关节软骨损伤等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在医学临床中从事这类疾病防治工作的主要是骨科医生，因而又称为“骨科运动医学”。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 常见的运动损伤有哪些?</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 运动创伤，不仅发生在专业运动员中，在普通百姓中也非常多见。它包括急性损伤和慢性劳损伤，好发于关节或引起关节活动的肌肉、肌腱、韧带等软组织。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 最常见的有膝关节半月板损伤、关节软骨损伤、交叉韧带损伤，髌股关节损伤;肩关节不稳或复发性脱位、肩袖损伤、肩峰撞击症;肘关节的网球肘;腕关节;髋关节;踝关节等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 精细化的微创外科+个体化的康复</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 所有受伤的运动员都希望通过尽可能小的创伤，治疗伤病，恢复运动能力和竞技水平，并重返赛场。这就对传统的骨科学中创伤治疗方法，提出了新的考验。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 因此，如何采用微创手段，最大限度地恢复伤者(包括运动员和普通群众)的运动能力，恢复竞技水平或生活质量，成为骨科医生和运动医学医生共同关心的问题!</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 因而，在国外一部分受到过骨科专业训练的医生，吸收运动医学的治疗理念，采用先进的治疗方法，专门从事肌肉、肌腱、韧带、关节急慢性运动损伤诊疗，从而发展起“骨科运动医学”这一新学科。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 本着“损伤小、疗效好、恢复快、功能佳”的治疗理念，“骨科运动医学”为传统医学注入了新的活力，也提出了很多新的课题，因此这一领域近年来发展迅速。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前来说，“关节镜微创外科”是“骨科运动医学”的主要武器之一，其微创外科的优势是显而易见的。因此，关节镜外科也逐渐得到了蓬勃的发展，相关手术器械不断开发出来，造福于广大患者。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除了关节镜微创外科以外，“骨科运动医学”还拥有关节腔局部注射治疗、冲击波物理治疗和功能康复锻炼等几大“武器”。</span><br />
