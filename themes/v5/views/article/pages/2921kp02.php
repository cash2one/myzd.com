			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何应对乳房疼痛</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;乳房疼痛是临床上最常见的良性乳腺疾病，常见于30-50岁的育龄妇女，是中年妇女最常见的乳腺疾病，青春期及绝经期少见。在美国进行的一项有1171位妇女参与的研究发现，有69%的妇女经历了规则的经前乳房不适，其中11%有每月超过7天的中到重度乳房疼痛。乳房疼痛的病因尚未完全明了，以下几种因素可能与其发病有关</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（一）性激素异常：卵巢分泌的雌激素增加，孕激素产生不足使得血清中雌激素水平升高，孕激素水平下降或雌孕激素比例失衡，可导致乳腺腺体过度增生或复旧不全，从而发生纤维化引发乳痛，组织结构发生紊乱，乳腺导管上皮和纤维组织不同程度的增生和末梢腺管或腺泡形成囊肿</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;(二）乳腺组织对性激素敏感性增高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;(三）其它：如不合理的孕哺史造成乳房复旧不全，口服避孕药，滥用丰乳药等外源性激素的使用以及现代人生活工作压力大、不合理的饮食起居等均可能导致内分泌失衡，引发乳房疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 乳房疼痛，通常可分为周期性乳房疼痛，非周期性乳房疼痛及其它原因所致乳房疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、周期性乳房疼痛是与月经周期相关的乳房疼痛。很多妇女在月经前2-3天都会经历乳房触痛或胀痛，并且乳房内可触及小的结节，月经后即消失，这其实可算是一种正常现象。真正的周期性乳房疼痛应是指每一周期乳房重度疼痛的时间超过一周。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、非周期性乳房疼痛</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">非周期性乳房疼痛与月经周期没有明显的关系。与周期性乳房疼痛不同，真正的非周期性乳房疼痛既可在绝经前妇女中发生，也可发生于绝经后妇女，</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、其它因素所致乳房疼痛：常见有Tietze’s综合症，又称痛性肋胸关节综合症，其疼痛常定位于覆盖于肋软骨表面的乳腺，患者常感到乳房中间象限的疼痛，按压患病软骨时疼痛常加重。此外手术创伤也可引起乳房疼痛，乳房手术后患者有1/3到1/2的患者术后一年仍有乳房疼痛。乳腺癌伴有乳房疼痛的并不常见。大约有10-13%的乳房疼痛来源于乳房外因素，如胆结石，颈椎病等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 大多数乳房疼痛表现为周期性乳房疼痛，以经前期明显，经后症状或减轻或消失。如发生乳房疼痛，首先要排除紧张及恐惧心理，因为一般来讲乳腺癌是不会引起乳房疼痛的。接下来仍需去医院做相应检查。一般首选彩色超声检查，因为本病多数患者年龄小于40岁， 腺体丰富致密，超声检查对致密腺体中的结节和囊、 实性肿物的分辨率远优于钼靶 Ｘ线检查。对年龄较大且腺体并不丰富者宜首选钼靶Ｘ线检查， 必要时可行二者联合检查。当然， 对部分病例因肿块或结节形成， 不易与纤维腺瘤和乳腺癌相鉴别， 需结合必要的病理组织学检查（ 空芯针穿刺活检、 细针穿刺细胞学检查或手术活检）进行确诊。当通过这些检查排除了乳腺癌及其它乳房肿瘤时，医生会对患者进行心理治疗，大多数患者可消除恐惧和顾虑，这样虽然不能缓解其疼痛，但可以改变其对疼痛的态度，从而使得疼痛不再是一个严重的问题。 &nbsp; 国外有报道约85%的患者可以通过心理治疗得到治愈。选择合适的胸罩对于缓解乳痛也有帮助。此外，还有一些患者乳痛是由于焦虑所致，对这类患者采用放松疗法有一定效果。如果经过这些治疗后，患者症状未能缓解，则可以考虑药物治疗。中医药治疗是目前国内治疗乳房疼痛的主要手段之一，特别是对一些年轻患者。常用药物有乳康片、乳增宁、逍遥丸、小金丸等，均有一定疗效。如中药治疗效不佳，可考虑使用他莫昔芬，这是一种抗雌激素药物，每天10mg可有效改善乳房疼痛，对周期性乳痛其有效率可达98%，对非周期性乳痛则为56%。其它治疗如低脂饮食、减少咖啡因、可乐、巧克力等的摄入等均可能起到缓解疼痛的作用。</span>
</p>
