<p class="MsoNormal" align="left">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院妇科主任刘开江认为，目前在国际上，没有关于卵巢</span><span><a href="http://money.163.com/keywords/9/8/988496326027/1.html" target="http://money.163.com/15/0404/08/_blank"><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">预防性</span></a></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">切除的手术指南，这纯属个人选择。“朱莉的抉择”在国内看来有些激进。卵巢是女性分泌雌孕激素的重要器官，不能轻易切除。在国内，确实有很多罹患乳腺癌的患者为了预防卵巢癌发生和乳腺癌转移到卵巢而预防性切除卵巢，但仅仅因为基因检测而去做预防性切除的非常少。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">身体周刊记者 肖蓓</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">安吉丽娜·朱莉在切除乳腺两年后再接受卵巢</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">-</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">输卵管摘除手术，引发大众与医疗界的争论。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院妇科肿瘤科主任刘开江表示，预防性切除双侧卵巢手术在国外并不新鲜，但在中国，仅仅因为基因检测而进行预防性切除的例子非常少见。不过，国内确实有很多乳腺癌患者为了预防卵巢癌和乳腺癌转移到卵巢而预防性切除卵巢。至于手术的副作用，可以通过口服激素来替代，与罹患癌症的风险相比，不能生育、提前绝经等副作用都是可以忍受的。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">瑞金医院放疗科主任陈佳艺则强调，目前预防性切除卵巢在国内还面临着医学伦理、学术要求和准入资质三大制约，类似预防性切除卵巢这样的重大治疗决策，需要建立在精确的基因检测、合理的遗传学诊断基础上，这个团队还需要专业医疗行政机构共同努力来完善，但是由此而衍生的对肿瘤早期诊断、生活质量的关注，却是不能忽略的。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">国内的“去势手术”</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">朱莉并没有罹患癌症，仅仅因为家族史和基因检测而预防性切除卵巢和乳腺，很多人怀疑，朱莉的做法是否过于激进？</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院刘开江教授认为，朱莉切除乳腺和卵巢之所以能引起轰动，是因为名人效应。由于</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA1/2</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因与乳腺癌和卵巢癌的关系，已经是国际共识，因此在国外，由于检测出这一基因而预防性切除的例子并不鲜见。在美国，很多年前就提出预防性切除的概念，他曾看到美国预防性切除卵巢的</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">140</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多个病例报告。报告显示，五年后，预防性切除卵巢的确降低了卵巢癌的发病率。当然，由于预防性切除会导致生育功能丧失，因此一般手术对象是完成生育的妇女。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">瑞金医院乳腺疾病诊治中心的外国专家、美国科罗拉多大学肿瘤学教授、美国医学会注册肿瘤专家大卫·加菲尔德曾在接受采访时表示对预防性手术的支持，他说：“（在美国）对于</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因突变的女性，这样的手术非常普通。美国女性患乳腺癌的几率本身就很高。通过预防性双侧乳房</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">+</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">卵巢切除，几乎可使乳腺癌和卵巢癌风险下降到几个百分点。”</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">事实上，这位美国教授的妻子就曾接受预防性手术。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">20</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">年前，加菲尔德太太的母亲因为乳腺癌去世，她的姐姐也发现罹患乳腺癌。因为有两个直系亲属患乳腺癌，加菲尔德太太也担心遭此厄运。虽然头颅扫描、乳房钼靶检查，结果都是阴性，但她还是决定做预防性双侧乳腺切除手术。几年后，加菲尔德太太在</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">50</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">岁时做胆囊切除手术的时候，还做了预防性的双侧卵巢切除手术。虽然最终基因检测结果证实她并没有</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA1/2</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因突变，但是她仍然不后悔。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而在中国，因为</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因突变而预防性切除乳腺的案例相当少，预防性卵巢和输卵管切除的案例就更稀少。陈佳艺认为，一个原因是，</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因突变发生率在白人、犹太人群中高，在亚洲人群中属低发，因此最早提出预防性切除的都是欧美国家。另一个原因，朱莉的治疗决策，是基于基因检测。而目前国内尚缺乏有资质的</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因检测机构，同时国内也没有能做到精确诊断的遗传学门诊。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“目前基因检测在国内并不普及，由于各类基因检测公司的乱象，卫生部曾专门发文强调基因检测不能列为治疗依据，只能作为科研依据。因为这里面有一个医学伦理的问题，由于没有规范和手术指南，医生不会轻易因为基因检测结果而为患者进行预防性切除，否则会引起争议的。”
刘开江表示。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在国际上，其实也没有关于卵巢预防性切除的手术指南，刘开江表示，是否手术纯属个人选择。朱莉并没有罹患癌症，仅仅因为家族史和基因检测而预防性切除卵巢和乳腺，在国内看来有些激进。卵巢是女性分泌雌孕激素的重要器官，不能轻易切除。在国内，只有罹患乳腺癌、卵巢癌、内膜癌的女性才会切除卵巢，仅仅因为基因检测结果而去做预防性切除的非常少。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刘开江说，“在临床中，我只碰到一例</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">42</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">岁的女患者，母亲是卵巢癌，她自己在北京做基因检测是</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">BRCA</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因阳性，由于担心罹患卵巢癌，在她的强烈要求下，充分沟通后，才为她做双侧卵巢和输卵管切除手术。”</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不过，由于乳腺癌有一定的卵巢转移率，再加上乳腺癌的治疗效果与女性激素有关，因此很多国内的乳腺癌患者，为了预防卵巢癌，也会选择预防性切除卵巢，也被称为“去势手术”。常是乳腺癌患者在外科接受乳腺切除手术后，被推荐到妇科做卵巢切除手术。现在的技术可以通过腹腔镜切除卵巢手术，属于小型的微创手术。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">复旦大学附属妇产科医院康玉副主任医师也表示，一般对于有家族史以及乳腺癌病史的患者，在完成生育功能后、在做妇科其他手术的时候，如子宫肌瘤剥出时，进行预防性切除输卵管，可能降低卵巢癌的发生几率。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术后的替代疗法</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">就在大家为朱莉的坚强和勇气感叹的时候，也有人疑问，切除卵巢和输卵管后，</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">39</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">岁的朱莉提前进入更年期，我们还能看到风情万种的朱莉吗？对于已经拥有六个孩子的安吉丽娜·朱莉来说，丧失生育能力损失不算大，手术也不及乳腺切除手术复杂，但副作用更大——强制绝经。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院刘开江教授说，卵巢是女性分泌雌激素和孕激素的器官，而雌激素和孕激素则调节着女性身材、皮肤等女性化特征。切除卵巢后，体内的雌激素水平会急速下降，出现潮热、出汗、烦躁等更年期症状，而且，更年期的激素水平是缓慢下降，切除卵巢后的激素水平是急速下降，所以，这些症状会更明显。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所幸的是，现在可以采取雌激素替代疗法，通过药物来补充体内的雌激素。刘开江说，朱莉的预防性切除背后是一个专业团队的强大支持，有一整套完备的后续人工雌孕激素的替代疗法。朱莉切除手术后，在子宫内安置了一个释放孕激素的装置，并在皮下埋植了一个释放雌激素的装置，可以模拟正常女性生理周期，规律性的释放雌、孕激素，保持女性特征。朱莉的手术及术后激素管理要比一般人精确得多。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而在国内，一般预防性切除卵巢的患者，通过术后服用口服雌孕激素来维持女性特征，为了防止复发，一般要等疾病稳定两三年后才开始补充雌性激素。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陈佳艺表示，在乳腺癌患者中，以 “抗雌激素”为主要机制的内分泌治疗疗效已经非常肯定，但是专家们仍然非常关注治疗副作用，希望能让患者安全地用足疗程，从而有效控制肿瘤。而对于切除卵巢后的小剂量激素替代治疗，激素剂量必须低于正常卵巢释放的激素量，对于没有切除乳房的患者，不合理的雌激素治疗反而会刺激乳腺癌的发生。</span><span></span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">是否会失去女人味？这个问题要看个人的感受。刘开江说，“预防性切除卵巢最大的意义在于降低卵巢癌的发生率，至于对于皮肤、身材、脾气的改变，则有个体差异。女性在一定年龄会自动绝经，这一手术只是提前了绝经的时间，因此对于消除患癌恐惧来讲，这些变化是能够忍受的。”</span><span></span>
</p>
