<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>食管癌病理分型与手术&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床上常根据食管癌的早期症状和中期症状分为如下几种类型。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一．早期食管癌（原位癌和早期浸润癌）常分为四型。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1） 隐伏型：占7.3%~11.8%，病变黏膜局部轻度充血，镜下均为原位癌。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）糜烂型：占33%~51.2%，病变黏膜轻度糜烂充血，形态不规则，与正常的粉白色黏膜混杂如地图状，镜下原位癌和早期浸润癌各占一半。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）斑块型：占24.4%~51.3%，病变处黏膜略高起呈斑块状，肿胀增厚，表面粗糙不平，黏膜的皱襞变粗或中断，组织学原位癌占1/3，早期浸润癌占2/3.</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）乳头型：占8%~12.6%，病变如乳头或息肉，凸向腔内，表面黏膜大多光滑，偶有轻度糜烂，镜下大部是早期浸润癌。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二．中晚期食管癌常分为五型。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）髓质型（巨块型）：占56.8%，癌体积较大，受累段食管壁呈不对称增厚，累及食管周径的大部或全周，癌上`下方均呈坡状隆起，常伴溃疡，此型癌组织向腔内</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）蕈伞形：占18.5%，癌组织像蘑菇状突入管腔，其边界清晰隆起外翻，中有较大而浅的溃疡，往往累及食管壁的一侧，外侵较少。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）溃疡型：占13.3%，癌组织较薄，累及部分食管周径，表现为较深溃疡，食管腔梗阻较轻，但易发生穿孔。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）缩窄型：占8.5%，病变为短的环状狭窄，通常累及全周，表面糜烂，多无溃疡，长度多不超过5cm，缩窄上方高度扩张。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（5）腔内型：占3%，瘤体呈巨大肿块，常为圆形或卵圆形，凸向食管腔，有粗细不等的蒂与食管壁下相连，多数瘤体表面有不规则的浅糜烂区，肿瘤只侵占食管周径一部分，外侵较少，吞咽困难症状常不严重。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三．食管癌的手术治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1） 早期食管癌的手术主要有三种手术方法：内镜下食管病变局灶切除术，这种方法病人痛苦小，恢复快。但复发率较高。另一种方法胸腔镜辅助下食管病变切除，创伤小，疼痛轻。再一种为传统开胸手术方法，手术创伤较大。早期食管癌手术效果好，长期生存率高。具体采用哪种方法要综合考虑患者情况和医生技术水平。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）中晚期食管癌手术方法主要有：左胸一切口、右胸-腹二切口、及颈胸腹三切口。长期生存率取决于肿瘤的病理类型、术中淋巴系统清扫、术后的综合治疗。</span>
</p>
