<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>让高龄患者也能做前列腺手术 王行环获我省科技进步一等奖</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">长江商报消息&nbsp;本报讯（记者 胡梦 通讯员 高翔）武汉大学中南医院泌尿外科王行环教授在国际上率先将前列腺等离子双极电切术用于男性前列腺增生手术，使该手术告别年龄限制，适用于所有男性患者。在昨日举行的湖北科技奖励大会，该术式的应用和研究获得湖北省科技进步一等奖。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">目前已使5000余位患者受益</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">50岁以上男性良性前列腺增生的发生率高达50%，其中约两成患者需要手术治疗。近半个世纪以来，经尿道前列腺电切术一直是治疗前列腺增生的“金标准”，但该手术存在不容易止血、易发生水中毒等缺陷，只有大型综合医院才能开展此项手术，且超过80岁的患者都被拒绝手术。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中南医院泌尿外科王行环教授经过反复摸索和攻关，于14年前在国际率先将等离子双极电切术用于男性前列腺增生手术，大幅降低了手术风险，目前已使5000余位患者受益。</span>
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1个小时为92岁老人做完了手术</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">王行环教授介绍，等离子双极电切术是在高清可视内窥镜的引导下，通过等离子刀产生的不同形式的高密度电流，使增生组织快速无痛汽化。该方法由于在手术过程中电循环不经过人体，所以出血很少。这一新术式成为国内外手术指南推荐术式。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在中南医院泌尿外科病房，92岁的汪爹爹说，他曾因患前列腺增生痛苦不堪，尿频导致每晚要起7次床，几乎无法睡个安稳觉。跑了全国好几家大医院，但这些医院都因为他年龄大、心脏又不好而拒绝做手术。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">汪爹爹找到王行环教授，不到1个小时就完成了手术。</span>
</p>
