	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>鬼压床，惹“鬼”的缘由</strong></span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong><span style="font-size:18px;"></span></strong><strong><span style="font-size:18px;"></span></strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生手记</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 文/广州市妇女儿童医疗中心五官病区 刘大波教授 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有的人在睡觉时，会出现一种睡眠障碍：睡眠处于半睡半醒的状况，同时还出现各种各样的幻觉，甚至能听见周围的声音。但奇怪的是，无论自己再怎样用力，却都使不上力来，想大叫也叫不出声，想睁开眼或翻身起床，却一动也不能动。拼命挣扎数分钟后，才终于醒来，人会觉得全身很累，有时甚至满身大汗。这就是“睡眠瘫痪症”，在民间被俗称为“鬼压床”或“鬼压身”，在午休和晚间睡眠时都可能发生。其实，这种情形跟鬼怪无关，但让人惊悚恐怖。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 那么，为什么会遇“鬼”呢？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主因：脑子醒了，身体“赖床”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 尽管原因还没有完全清楚，但目前医学界普遍认为，睡瘫症是大脑皮质与运动神经元不同步的结果。也就是说，是在大脑皮质已经苏醒，但运动神经元并没有同步苏醒的情况下发生的。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 大脑从睡眠休息中复苏过来，还来不及和身体重新连接，人处于半睡半醒状态，梦境与现实互相交错，导致身体与大脑发生不协调的情况。此时全身肌肉张力最低，所以会造成自己想要起来，却起不来，想发声，却喊不出声音的状况。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 诱因：过度疲劳，易惹“鬼”&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据研究报告，有40%-50%的人一生中至少会出现一次睡眠瘫痪，大多数病例是散发的，也有家族遗传的，可发生于任何年龄(初次发作通常在青春期)。它的发作与心理因素有很大关系，压力过大、太过焦虑紧张都可能诱发。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我们知道，人的睡眠是有一定周期的，分为清醒期、浅睡期(Ⅰ、Ⅱ期)、深睡期(Ⅲ、Ⅳ期)、快速动眼期(REM期)，正常的睡眠周期应该从清醒期入睡进入浅睡眠，再遵循浅睡期—深睡期—浅睡期—REM期的睡眠规律进行循环。睡眠瘫痪发生在REM睡眠期，是暂时性的睡眠障碍，通常发生在刚入睡时(睡前型)或醒来时(睡后型)。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 可能有人马上会问：不是说睡眠首先进入浅睡期吗？怎么会发生睡瘫症呢?&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 是的，正常状态下，睡眠从浅睡期开始，但在睡眠严重不足或病理状态下，睡眠首先进入REM期，就可能出现睡眠瘫痪的现象。这也解释了多发性睡病(一种原因不明的睡眠障碍)的病人为什么会有睡眠瘫痪的症状。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 因此，过度疲劳、睡眠不足就可能会诱发睡瘫症。比如一些医生，值班时睡眠不足，睡眠质量差，心理紧张，总担心随时有急诊，就容易惹“鬼”上身。&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 此外，有些药物也可以引起睡眠瘫痪的发生，曾有服用抗焦虑药后出现睡眠瘫痪的报道。</span>
</p>
