<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>轻症前列腺增生无需手术</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">荆楚网消息 (楚天金报副刊) 5月28日晚“金报专家热线”由武汉大学中南医院泌尿外科王行环主任主持，主题是关于“前列腺增生的治疗”，下面是此次热点电话的摘要记录。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 问：2年前因排尿困难，被诊断为前列腺增生，现在生活质量大受影响，想做手术解除增生，但因心脏病安了心脏血管支架，听说不能做手术，该怎么办？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 答：因为您安装了心脏血管支架，长期服用抗凝药物，术中容易大出血，所以常规手术不能做。但采取双极等离子电切术就可以避免这个问题。因为该手术是从患者尿道插入电切镜，对前列腺增生部分进行准确定位后，在高清可视内窥镜的引导下，通过等离子刀产生的不同形式的高密度电流，使增生组织快速无痛汽化。仅需十分钟就可将增生部分汽化切除。由于该方法在手术过程中电循环不经过人体，因此术中不会出现大出血现象。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 问：我父亲患有轻度前列腺增生，需要做手术吗？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 答：良性前列腺增生也分轻重缓急，医学上有一个专业的评分标准。症状轻的根本不需要治疗，只要定期体检，长期观察就可以了。中度的可以通过吃药进行治疗。只有那些严重的，已经出现了并发症的，才必须接受手术治疗。有中等程度以上症状、对生活有影响的，可以选择服用药物治疗，改善症状。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 良性前列腺增生患者，如果反复出现尿潴留；出现前列腺增生导致的其他严重问题，如肾功能损害、膀胱结石、反复尿路感染、反复血尿、疝气等；服药效果不好或不愿继续服药，而愿意通过手术缓解症状者，可以考虑选择手术治疗。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 问：前列腺增生有哪些有效治疗药物？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp; 答：前列腺增生患者有中等程度以上症状、对生活有影响的，可以选择药物治疗。药物治疗主要是改善症状。目前治疗前列腺增生的药物主要有三大类：一是松弛前列腺的药物。这类药物专业名称为α-受体阻滞剂，无论前列腺大小如何，都可以选择这类药物。服药初期要1个月复查1次，此后每年复查1次。吃这类药能中等程度缓解排尿症状。如果有明显副作用，就应马上停药，及时就诊。二是缩小前列腺的药物。这类药物专业名称为5α-还原酶抑制剂，适合于前列腺体积较大的患者，服药初期要6个月复查1次，此后每年复查1次。吃药后可减缓前列腺增生，降低尿潴留和手术风险；能轻度缓解排尿症状。但是，这类药通常需服药3个月以上才起效，部分患者出现性欲下降、勃起功能减退、精液量减少等副作用。三是联合用药，即同时服用上述两类药物。同时服药能中等程度缓解排尿症状，但是两类药的副作用兼有，费用也较高。</span>
</p>
