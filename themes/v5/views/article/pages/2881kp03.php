<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>药物难治性疼痛的有效外科治疗手段----DREZ切开术&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在20世纪60年代，研究发现脊髓背根入髓区（dorsal root entry zone, DREZ）与痛觉的整合调节和传导有关。1972年Sindou等在法国首先将DREZ切开术应用于治疗乳癌侵犯臂丛神经所引起的疼痛（Pancoast’s syndrome），取得了满意疗效。之后，DREZ切开术逐渐被广泛应用于其他多种药物难治性疼痛的治疗，取得了较好效果。经过40多年的临床实践，DREZ切开术已经成为疼痛治疗最有效的手段之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">DREZ切开术主要适用于臂丛或腰丛神经撕脱后疼痛、脊髓或马尾神经损伤后疼痛、周围神经损伤后疼痛（其中包括幻肢痛、残肢痛）、肿瘤侵犯神经丛或神经根以及周围神经所致的神经源性疼痛、疼痛合并痉挛状态等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">DREZ切开术并不复杂，全身麻醉，俯卧位，显微镜下进行。在相应疼痛节段打开椎板，显露相对应脊髓节段的后外侧面，在脊髓后根小分支进入后外侧沟入口的腹外侧纵向切开软脊膜，应用双极电凝镊子在显微镜下毁损切开DREZ区，深度一般不超过3mm。除了显微外科技术切开之外，也可以采用射频毁损、激光毁损等方法。手术的关键是准确定位相关DREZ区以及术中避免影响DREZ区周围其他结构和功能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">DREZ切开术安全性较高，主要副作用是与毁损切开区域相对应的节段性感觉减退或缺失，由于大多数患者术前该区域已经存在不同程度的感觉障碍，因此这一副作用对术后患者的影响较小。其他可能发生的副作用包括手术侧深感觉障碍、肌力减退、括约肌障碍、感染、出血、脑脊液漏等，临床上发生率都比较低，通常小于5%，大多数可以逐渐恢复。</span>
</p>
