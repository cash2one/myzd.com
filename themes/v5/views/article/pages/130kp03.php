		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>顽固性头痛、偏头痛如何治疗</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">多种原因可以导致顽固性头痛，如脑肿瘤、脑炎、外伤、血管痉挛、血管狭窄、血液循环障碍、鼻炎、神经受压、颈椎病等。临床上必须事先借助检查明确头痛的确切原因才可能彻底治愈头痛，因此当头痛发生时，首先应当想想有什么原因，不明确时应当到医院检查，如头部CT，头部核磁共振、脑血管造影、脑血管超声等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">最常见的顽固性头痛有两种：血管性头痛和神经性头痛。血管性头痛是由于头部血管结构上的异常所致，表现为不定期发作的头痛，以胀痛为主，有时呈搏动样头痛，可伴有头晕恶心甚至呕吐，可持续数小时甚至数天，严重时需要卧床休息，口服散利痛片可以暂时缓解，但不能根治。目前脑血管造影检查可以明确病变的血管和部位，在此基础上采用介入治疗或微创手术治疗就可以治愈头痛，因此对怀疑为血管性头痛的患者，应当尽早到正规的医院接受检查，以便彻底早日根治头痛。神经性头痛表现为头部某一区域针刺样的疼痛，可以呈条状或片状头部区域疼痛，疼痛多有放射性特征，每次发作持续时间很短，但会反复发作，剧烈难忍，大多不伴有恶心呕吐，去痛片和卡马西平可以暂时缓解疼痛。这种疼痛多因神经受压所致，因此明确疼痛的神经和受压的部位最为关键，借助医生的检查和影像学检查可以确诊，治疗上以去除病因为首选。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">常见误区：（1）不重视：认为头痛不会有大事，失去了最佳的治疗时机；（2）盲目治疗：当头痛发生时，在不明确诊断和病因的前提下随意采用多种治疗措施，不仅治不好还容易掩盖真正的病情；（3）误认为得了不治之症，轻信别人的说法，不再采取积极的治疗，长期忍受疼痛的折磨；（4）很多患者疼痛已经十多年，但从来都没有去检查过真正的病因，所以不但治不好病还丧失了信心，甚至会出现精神异常，因此建议所有的头痛患者在接受治疗前先问问自己得的究竟是什么病? 是什么原因? ，怎样才可能彻底治好?</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">不盲目治疗、对因治疗才是关键。</span>
</p>
