		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>马芙蓉：显微镜下盛开的“芙蓉花”</strong></span> 
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">芙蓉花，又名拒霜花，木莲等，因其美丽、高洁、柔韧，历来被文人墨客所钟爱，古有“堪与菊英称晚节，爱他含雨拒清霜”、“谁怜冷落清秋后，能把柔姿独拒霜”的诗句。而在北医三院耳鼻喉科，就有这样一朵芙蓉花，她悄悄地绽放、用自己精湛的医术、温情的话语，想患者之所想，急患者之所急，给每一位患者带来美和正能量——她便是北京大学第三医院耳鼻喉头颈外科主任马芙蓉。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/912/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “话疗”和医术同样重要</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 一身素色白大褂，和蔼的面庞上总挂着微笑，一开口温暖的话语很自然地就拉近了和患者的距离。很多年轻的患者都亲切地称马芙蓉为“知心妈妈”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “你真心对待患者，患者也会真心对你好。我从1985年参加工作至今，患者给我的更多是赞扬和肯定。”这让记者不禁想到“好大夫”等网站上，患者对她全篇的赞评。说到其中的秘诀，马芙蓉告诉记者：“作为一个医生，要想获得患者的信任，过硬的医疗技术是最基本要素。此外和患者沟通的能力也很重要，它是一名医生的‘软实力’。”</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在耳鼻喉科，大夫遇到的不仅仅是患者生理上的各种病症，有时还需要缓解他们心理上的痛苦。例如，一些耳鸣、鼻塞的患者可能是由于心理原因如焦虑、抑郁而出现这些症状，这个时候，马芙蓉还需要对他们进行心理疏导，用她自己的话叫做“话疗”。“你给病人一个肯定的眼神、一句鼓励的话语、给他们的一丝希望，对他们的心理和病情都有很大的帮助。”</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 谈到医患关系，马芙蓉说：“其实大部分患者都是很好的，自己为患者做了一点应该做的事情，患者都会感激万分。”在她看来，患者和医生不应该有矛盾，医生是患者的守护神，他们需要如战友一般并肩做战，共同对付藏在患者身后的病魔。现在有些患者，受到不良媒体的蛊惑，戴着有色眼镜儿，对医护人员一百个不信任，怀疑、猜忌，甚至打骂、侮辱、砍杀，实在让人痛心啊！这不是一个文明社会所应有的现象，最终受害的还是患者本人和大众的健康。我们应该号召全社会都尊重医生和护士，让这种尊医爱护的良好风气蔚然成风。医护人员应该换位思考，设身处地的去理解患者，多替患者着想，真心的去爱护、关心、帮助患者，耐心的去解释和进行有效沟通。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 显微镜下一“爬”就是15小时</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 耳科手术因其血管众多、神经密布，并且与众多脑神经相连，因此被称为“绣花手术”，对医术有很高的要求。而马芙蓉经常要做从耳朵进入颅底的手术，切除颅底交界性和沟通性肿瘤，因此她常常被誉为“颞骨解剖大师”。她说：“一个女外科医生，在显微镜下纹丝不动地‘一爬’就是十几个小时，苦累对我都不算什么，主要是要修得一副扛得住那些来自危害患者生命安全的巨大压力的坚强神经和顽强作风。”</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 去年底，从青岛来的一位瘦弱中年女性患者，因为面瘫找到马芙蓉，希望能够给她恢复面容。这位患者十五年前曾经在当地进行过耳内肿瘤切除，在术前耳部和颅脑检查中发现，患者耳内肿瘤复发，通过中颅窝和后颅窝侵入颅内并压迫大脑和小脑，肿瘤周围环绕的是颅内的几条大静脉窦与之粘连紧密。面神经包绕在肿瘤内。肿瘤触之极易出血，加之与薄如窗户纸的静脉壁紧密相连而广泛渗血和出血，手术几乎在出血不止的情况下完成的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 而马芙蓉却不畏艰难，用自己精湛的技术，稳稳地做了15个小时的显微手术，终于完整切除耳内和颅内的肿瘤，并成功进行了面神经修复。患者被平安地送出手术室，回到重症监护病房。当马芙蓉拖着疲惫的身子离开手术室时，心里想着的却是感谢，她说：“真是万分地感谢患者，她没有让我下不了手术台，毕竟手术的十多个小时里，我和患者共同闯过了一道又一道难关和鬼门关啊！”</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 医者仁心，马芙蓉这种精神，也感染着她的学生。从1997年接力颞骨解剖学习班以来，她培养了一批又一批耳科学人才。她的学生，很多分布在北京市各大医院耳鼻喉科，还有的远赴国外进行深造。无论他们走到哪里，都给所在医院留下了科室良好的培养所带来的高素质、吃苦耐劳、踏踏实实的印象。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 最幸福的时刻是看着患者康复出院</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 奉献，是医者救死扶伤的义无反顾，也是看见病人重新走向健康的幸福。医生把时间给了病人，但对于他们的至亲却挤不出时间。医生把健康给了病人，却无暇关注自己的身体状况。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 如今，拥有“四重身份”的马芙蓉经常忙得分身无术，她既是一名临床医生，一名科室主任，又是一名博士生导师，还是一名母亲。“我首先是一名医生。”马芙蓉说，她始终将诊断疾病、服务病人作为自己的首要工作。其次，在临床诊断的基础上再带领学生开展科研。同时，作为一名科室主任，她着力打造学习型科室。从2006年开始，马芙蓉组织年轻医生和学生坚持早读，学习英文文献，并进行一周要闻的英语和汉语的双语汇报。她还从内容是否先进、重点是否突出、层次是否分明、英语发音及语气是否流畅和标准、翻译是否达到“信达雅”、幻灯片制作是否规范、所配图表是否合理、形象举止是否得当、自信心和冷静程度等多角度进行点评，进行科室长期的教学积累和人才培养。经过十年的锻炼，科室已经有多人在院级、校级和北京市高校青年教师教学基本功授课比赛中获得多项奖项。除了早读，她还建起早交班、疑难病例讨论平台，临床新技术讲解等多个学习平台，帮助团队成员不断学习进步。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “现在我最头疼的事情就是时间不够用。”白天在医院做手术，晚上回家做科研，像很多医生一样，马芙蓉留给家人的时间少之又少，为此她满怀歉疚。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 记得女儿高考那年，马芙蓉早上一大早就开车把她送到学校，由于太早学校的大门都没有开，只好让女儿在学校门口等着，而她则必须在早晨6:30赶回科室组织科室年轻医生和学生学习。有一次，周五的中午，马芙蓉正在给科室开会，女儿因高考模拟考试不理想哭泣着打来电话，她只能开完会后，赶紧开车去学校接上女儿，把她放在一个大学的自习室，再匆匆赶回医院，好赶在下午1:30能够准时出门诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 晚上接女儿放学回家的时候，母女俩经常被堵在路上，“你知道我俩有多‘聪明’，女儿趁堵车的时候钻进快餐店买点便当，再匆忙跳上车，抓紧吃着晚餐的同时，还要不停地给堵车的我‘喂’上一些，等我们到家时，晚饭也就解决了。这样我们回家就可以省下时间学习了。”马芙蓉感慨，女儿也抱怨过她，但更加担心的是她的身体。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “没当医生时候，对医生的辛苦只是一种很模糊的概念。当了医生之后，才发现这份辛苦也是一份喜悦。将患者从‘死神’手边拉回，看着他们康复出院，所有的辛苦都是值得的。”说到这些，马芙蓉脸上露出了灿烂的微笑。</span> 
</p>
