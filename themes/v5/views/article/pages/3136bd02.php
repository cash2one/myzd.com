		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>爱护好身体里的那条“生命之河”</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">人体内的血管如同这个星球上纵横交错的河流，分布在身体内的每个角落，这条由血管交织而成的网，就是“生命之河”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 如何爱护好这条“生命之河”、当“河道”出现了阻塞该如何治理？复旦大学附属中山医院血管外科主任符伟国一直在这条全世界最复杂的“微河流”上钻研探索着。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医者简介</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 符伟国医学博士，复旦大学附属中山医院血管外科主任，复旦大学血管外科研究所所长、血管外科主动脉亚专科负责人，亚洲血管学会委员。国际腔内血管外科学会委员。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 几乎每天都在走钢丝</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 中午11点半，符伟国比约定的采访时间迟到了近半个小时。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对一名外科医生来说，一上手术台就说不准时间，似乎是再平常不过的事。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在普通人眼中颇为陌生的血管外科其实要负责除了心、脑之外的全身所有的血管疾病，其中难度最高的就是主动脉夹层和腹主动脉瘤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 符伟国所在的中山医院血管外科全国知名，他们每年要进行四百余台主动脉夹层手术以及腹主脉瘤手术，前者的累计手术量全球第一，而后者则位居全国第一。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 符伟国坦言：“几乎每天都在走钢丝。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 血管外科疾病发作时常常十分凶险，而且特别容易误诊。最典型的就是主动脉夹层，它常常会毫无预警地突然发生在中壮年身上，患者会感到急性胸痛、后背有撕裂感或者大汗淋漓。而这些症状很容易被误诊为心梗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 有些患者在胸痛之后则会出现腿痛、发麻、发冷，这种症状又极易被当作下肢动脉栓塞，没有经验或没有仔细询问病史的医生以为只需立即手术取出血栓就行，最可怕的就是等到上了手术台，才发现是主动脉夹层。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 动脉瘤也是一种平时悄无声息的血管疾病，它有可能会发生在全身任意动脉，又以腹主动脉较为常见。由于动脉瘤并非实体肿瘤，而是血管的结构向外膨出，因此它不痛也不痒，可一旦突然破裂，也有可能会危及生命。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “过去我们做一台腹主动脉瘤手术，往往一开就是十几个小时，这个过程就好比挖开路面，修理地下水管，工程极大。现在微创手术的切口越来越小，只需要在路面上找一个点挖下去，在地下把隧道做好，不需要破坏路面就能修理管道。”符伟国说，“创伤虽然小了，但医生的压力一点也不小”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 压力就是动力。作为我国第一位血管外科博士、第一批从事血管腔内微创手术的专家，符伟国几十年来不断提升手术水平，他以精湛的技术赢得了许多国内外专业奖项和同行们的赞誉。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 有时候治的是心病</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 每一次手术前，符伟国都十分慎重，他从来不会为了做而做。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 动脉瘤是血管外科的常见疾病，一般来说5.5厘米以下的动脉瘤如果没有症状，患者只需随访，无需手术。但一旦动脉瘤超过5.5厘米，就达到了手术适应证。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 然而这种病平时并没有什么症状，多数患者都是在体检中发现或自己无意中摸到的，突然间要面临手术的抉择，往往会犹豫不决甚至纠结再三。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “突然面临生死抉择，谁都会感到彷徨，要让病人安心地把生命交给我们，我们就必须换位思考，理解病人的心情。”符伟国坚持，对每一位病人都要全面评估，仔细权衡手术的风险与获益，到底是手术的风险大，还是动脉瘤破裂的风险大。有时候尽管没有到必须手术的地步，但病人的心结很严重，时时担心这枚“不定时炸弹”会破裂，那就要把这种心病对生活质量的影响也考虑进去，再进行衡量，最终决定是否手术。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在复杂的医学知识面前，医患之间存在着很大的信息不对称，此时，医生的判断和引导在很大程度上决定着患者的生命抉择，如何沟通就显得特别重要。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在符伟国看来，很多医患矛盾归根到底都是沟通问题。当病人和家属反反复复来问同一个问题，而门诊的时间又十分有限时，如何做到耐心回答，如何进行有效的沟通，这其中的“艺术”有时候比医术更难掌握。“一句话能宽慰病人，也足以伤人。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 多年来的经验告诉符伟国，医生治病，有时候治的是心病。治心病并不是一种额外的负担，相反，只要让病人信任医生、让病人安心，病往往就能好得快。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 而病人的心病有时候还不只是担心手术风险那么简单，面对高昂的手术费用，经济拮据的病人会有更多的顾虑。“遇到这样的病人，我们要帮他算好账，不能让病人看完病，就弹尽粮绝了，要想办法既为他省钱，又达到治疗效果。”这是符伟国除了手术与看病之外经常要思考的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">医者建言</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 爱护血管从年轻时开始</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 爱护血管，要从日常生活习惯做起，从年轻时做起：坚持清淡的饮食和适当的运动，积极预防糖尿病和高血压。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 一旦出现走路时走走停停、小腿疼痛、走路走不长的情况，可能就是下肢缺血的表现，应该尽早到医院血管外科就诊，进行超声检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 如果血管出现堵塞，可以根据医生的建议，及时改变生活习惯，改善饮食、增强锻炼。如有必要可先用药物治疗。一旦病情严重，则不要忌讳手术。随着医学技术的进步，目前大多数血管外科手术都能够借助微创技术完成。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 要想预防主动脉夹层的“偷袭”，平时要积极控制血压，尤其是顽固难治的高血压更要引起警惕。马凡氏综合征也是主动脉夹层的危险因素，有家族病史的人，要定期去医院检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 关于动脉瘤到底要不要手术的问题，符伟国的建议是：首先看动脉瘤的大小。如果超过5.5厘米，就应积极治疗，女性患者则需更加谨慎，达到5厘米就可以考虑手术，因为女性尤其是东方女性的血管相对较细，动脉瘤发生破裂的风险也相应增大。此外，还要根据动脉瘤生长的速度来判断，如果半年扩张5毫米、一年扩张1厘米，也要及时治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 假如通过医生的评估，可以暂时不手术，平时则要注意定期随访、积极控制血压、减少剧烈运动、控制饮食，不要因为担心动脉瘤会破裂，而陷入恐惧和焦虑中。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 符伟国主任特别提醒那些手术后的患者，手术结束并不意味着治疗就结束了，许多血管疾病都需要终身治疗。手术后一定要改变不健康的饮食习惯，戒烟戒酒，因为手术只能缓解一时的病痛，却不能保证一辈子的健康。</span><br />
