			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>被患儿妈妈信任是我的幸福—— 感动医生的故事</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大圆脸大眼睛的6岁小男孩，我们都亲切地称他“小可爱”。 看到他从病痛折磨的痛苦恢复到快乐健康，我忍不住内心的喜悦。可爱的孩子，可敬的家长，在医患关系紧张的年代，使我在从医急诊手术29年的风风雨雨中再次感受到真爱。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">孩子咳嗽吼喘一年，一直奔波于各大医院，住院打针吃药反反复复，可拍胸片总是肺不张而且是上肺叶，反复会诊没有医生愿意为他做支气管肺泡灌洗手术。原因很简单，高风险、高手术费、效果差，医学教科书说理论上肺不张时间超过3个月就不可逆恢复，更为关键的是孩子是单肾，用药抢救麻醉这么多的难关怎么闯过去呀。医生不是神仙，医闹正虎视眈眈，谁都不会越雷池半步。孩子的家长在无望中来到了我院呼吸科，王莹主任亲自打电话给我，无论如何来看看孩子，我会诊的意见同所有的医生一样，不同意做手术。我和孩子妈妈的交谈很沉重，她无助的眼睛在流泪，我冷峻外表下的心在痛，妈妈不停的说“医生你救救孩子吧，他才只有六岁，我知道无论是对孩子还是医生，手术的风险都很大，但是我要尽最大的努力帮我的孩子恢复健康，手术不成功我也不怪你们。”手术本身不违背医疗原则，但在当前医患关系紧张的态势下，创新手术谈何容易？回到科室与全体医生讨论手术方案，请麻醉科会诊制定单肾用药方案，重大手术报请医务科审批，认真做好围手术期的各项准备，确保手术安全。家长的充分信任给了我们冒风险的决心，在得到院领导的支持和各科协作的情况下，我们决定手术，无论效果如何，我们都要尽心尽力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一切按诊疗计划进行，耳鼻喉科全体医务人员进入紧张而有序的流程。在手术室麻醉科的配合下，我们从孩子的右上肺灌洗出大量粘脓，直至灌洗液清亮为止，手术安全顺利结束。孩子送回病区，护士给予特别护理，使孩子没有恐惧，使妈妈没有包袱。一周后复查胸片，奇迹出现了，肺不张竟然全部恢复。孩子妈妈激动万分，热泪满面，紧紧握住我的手，千恩万谢，说“感谢你们救了孩子，也救了我们全家，您不知道一年来我们家长面对多大的精神压力，我们甚至做好了再做一次手术的心里准备。”我也抑制不住内心的激动，与妈妈一样热泪盈眶，感触万千：被人感谢是司空见惯，救人一命胜造七级浮屠，这是医生的职责所系；被人理解是难上加难，医学科学瞬间万变，循证医学需要时间印证，为了医学发展要不断学习、不断创新，为了孩子要勇挑重担、敢冒风险。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“小可爱”你要感谢你的妈妈，是她给了你生命；我也要感谢你妈妈，是她的信任，给了我幸福。无论是写这篇文章还是再次阅读，我都在流泪。其实，作为医生，我做了应该做的事，一点都不值得去渲染，但是被理解和被信任是多么不容易啊。还有妈妈对孩子的爱，家长曲折求医的执着，深深地打动了我，使我在今后的从医生涯中要给孩子更多的爱，要勇于挑重担，要敢于挑战自我，创新未来，做每个家庭及孩子健康的卫士。</span>
</p>
