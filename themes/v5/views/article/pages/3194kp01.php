		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>癫痫反复发作会引起智力下降</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">首都医科大学附属天坛医院神经外科张建国教授介绍告诉记者，癫痫是大脑神经元突发性异常放电，导致短暂的大脑功能失调的一种慢性综合征。而癫痫发作是指脑内某些神经元的异常持续兴奋性增高和阵发性放电所造成的临床现象。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“最常见的表现就是意识丧失，四肢抽搐，口吐白沫，双眼上翻等，也有一些特殊的症状表现。” 张建国教授称，致痫灶在脑部位置不同，临床表现各异，如致痫灶在运动、感觉功能区或附近，患者会出现短时间上肢或下肢麻木、触电样感觉、运动受限等；如致痫灶在视觉功能区或附近，患者会出现短时间幻视、视觉障碍等；如致痫灶在颞、额区，患者会出现各种精神症状，如莫名心慌、恐惧、似曾相识感、错觉、幻觉等。癫痫危害十分严重，首先是对脑神经的损害，癫痫反复发作会引起智力下降，患者逐渐丧失学习、工作甚至生活自理能力；并且还要忍受他人异样的眼光，给患者的工作、婚姻和生活都带来影响。</span>
</p>
