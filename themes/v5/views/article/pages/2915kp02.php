		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻腔在上气道通气功能障碍发病的作用（科普）</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻腔是呼吸系统的门户，具有通气、加温加湿、清洁过滤、免疫防御和嗅觉等重要的生理功能。近年来，随着上气道影像学、鼻腔鼻窦解剖学、睡眠病理生理学等系列研究的开展，发现鼻阻力异常升高具有重要的病理生理学意义。影像学研究发现多数OSAHS患者存在鼻腔解剖结构异常，如鼻中隔偏曲、鼻甲肥大等，构成了鼻阻力升高的解剖学基础。鼻腔狭窄时，鼻阻力升高，咽腔负压增高，塌陷性增加。临床实践发现鼻阻力的升高与睡眠打鼾和睡眠呼吸暂停有密切的关系，且鼻堵是打鼾和OSAHS患者的较常见症状。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻阈是鼻腔最为狭窄的部位，其产生的阻力占上气道总阻力的50%左右。仰卧位时流体静压升高，鼻腔黏膜血管充血，加重鼻腔阻塞，鼻阻力升高。咽腔相对于鼻腔来说为软性结构，其较易塌陷的特性使得咽腔横截面积可能会随鼻阻力变化而变化。为克服鼻腔狭窄所造成的上气道阻力升高，生理状态下存在一种补偿机制：膈肌和肋间外肌用力收缩，加大胸腔负压，气流速度加快，以保证获得足够量的潮气量。但同时该补偿机制又出现负面作用：加大了咽腔的负压，使径向咽腔内的吸引力（管腔内负压）加大，进而咽腔有效通气横截面积降低，如伴有扁桃体肥大、软腭脂肪堆积和悬雍垂水肿的患者，咽腔阻塞则会更加严重。鼻阻力升高还可诱发睡眠时经鼻呼吸转换成经口呼吸方式，这种转换是有代价的。它使舌根后缩、舌后间隙减小，流经口腔的气流不稳定，震动软腭和咽腔壅余的组织，产生鼾声。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在上述系列研究的基础上，韩德民教授等提出了鼻腔扩容技术。它是在不断深入理解睡眠呼吸障碍疾病的病理生理机制的基础上，伴随着鼻内镜外科技术的不断成熟逐渐孕育发展起来的，源于解决鼻腔阻塞源头因素的睡眠呼吸障碍疾病外科干预。</span>
</p>
