			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肺癌为何脑转移？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肺癌是呼吸系统中最常见的一种恶性肿瘤。肺癌的脑转移发生率较高，可以达到35％～50％，常以头痛、呕吐、面神经麻痹、偏瘫、视物模糊、失语、肌肉无力等为首发症状而被误诊为脑血管病、原发性脑肿瘤、结核性脑膜炎或颅内高压等。因此，应提高对肺癌脑转移的认识，防患于未然。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<img src="http://static.mingyizhudao.com/147004399360659" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肺癌脑转移是临床常见而严重的病情，也是肺癌治疗失败的常见原因之一。小细胞肺癌在作出诊断时约有20%的患者已有脑转移，而在小细胞肺癌患者的死亡病例尸检中脑转移发生率高达80%.非小细胞肺癌患者在病程中约有30%左右发生脑转移，其中以大细胞未分化癌和腺癌较多见，鳞癌次之。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肺癌何以容易发生脑转移呢？这是因为脑血管与供应大脑的椎动脉、静动脉丛之间存在大量的吻合支，致使肺癌细胞可以不经肺毛细血管的过滤作用，直接经心脏、颈动脉至脑而发生血液转移。据临床资料分析，出现下列情况之一者，应高度警惕肺癌在开始或已经脑转移：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一、中老年人或长期吸烟者，出现难以用一般的脑血管病解释的神经精神症状者，如头晕、目眩、烦躁、嗜睡、失语、肌无力、面部神经麻痹等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 二、已确定为肺癌，即使没有任何肺癌脑转移的征兆，也应进行常规的CT检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 三、有颅内高压或局部性脑损害体征，如剧烈头痛、呕吐、瞳孔不等大、偏瘫等，若同时伴有呼吸道症状或淋巴结肿大、声音嘶哑者，也应当进行常规x光胸片检查、查痰寻找癌细胞、淋巴结活检以及纤维支气管镜检查，以期早期发现，早期治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肺癌脑转移的治疗肺癌脑转移除积极治疗原发灶肺癌以外，脑部转移瘤的控制极为重要，根本的治疗为外科手术配合脑部放射治疗。</span><br />
