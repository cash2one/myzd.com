		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>“今年的八一最有意义”（资讯）</strong></span>
</p>
<p>
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;新华网消息&nbsp;：</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 8月1日一大早，记者就赶往青海省湟中县医院，采访第二军医大学赴青海医疗服务博士团的同志。医院大厅里，人来人往，都是慕名而来的农民患者。在内科病房里，记者见到了林毅勇、杜奕奇、陈倩3位博士，杜奕奇笑着说：“往年八一都放假休息，今年不休息。在青海高原过这个为患者服务的八一，是最有意义的。”&nbsp;</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 这个医疗服务博士团的领队陈琛介绍说，6月初，二军医大组建了一支作风、业务过硬的队伍，准备到缺医少药的青海高原开展医疗服务。7月10日，由第二军医大学所属的长海医院和长征医院8位博士带着价值18万元的仪器药品奔赴青海。他们驱车200公里，行程超过1000公里，到达平均海拔3000米的海北藏族自治州，在两个县的4个镇和8个乡，诊治患者4700多人，为基层医院的医务人员讲课25次，做白内障、胆囊、子宫手术25例，培训医务人员470多人次。消化科的杜奕奇博士，还为海北州第二医院组建了电子胃镜室。在海北的医疗服务结束后，博士团的同志没有休息一天，又到了湟中县开展工作。&nbsp;</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在内科办公室，杜奕奇对青海自然和生活条件的艰苦感触颇深，他说：“高原的常见病和多发病怎样治疗，又激发了自己努力学习专业知识和科学研究的积极性，增强了为患者服务的意识”。林毅勇说：“通过这次到艰苦地区的服务活动，我对江总书记提出的‘三个代表’的重要思想有了更深刻的认识。昨天晚上，我写了一夜入党申请书，今天早上把申请书正式交给了临时党支部，我要争取前线入党，做‘三个代表’的积极实践者。”&nbsp;</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在眼科手术室里，眼科博士李由副教授正在为患者做白内障复明手术，昨天刚做完白内障复明手术、今天眼睛已经能看见东西的83岁的回族老阿奶韩玉莲不停的对记者说：“感谢解放军大夫，感谢解放军大夫……”&nbsp;</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;（来源：《光明日报》/刘鹏）</span>
