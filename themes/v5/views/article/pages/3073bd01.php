		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>粤千名儿童中有6人患癫痫</strong></span>
</p>
<p>
	<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">信息时报讯 （记者 袁建彰 通讯员 孙健 荣华）受出生时缺氧、婴幼儿感染高发影响，广东每千名儿童中有6人患癫痫。其中能够系统、正规治疗的不足四成，大多数小患者们不得不在抽搐、脑功能下降中生活。记者前日从南方医科大学第三附属医院南方癫痫研究所成立大会上获悉，通过精确诊断、定位，药物治疗无效的孩子经手术后可有效根治。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 今年10岁的小静从2个月大开始就出现严重癫痫发作，高峰时每天要抽搐200多次。长期癫痫发作，使得其生长发育晚于同龄孩子，四肢纤细，非常瘦弱。在手术切除了致使癫痫发作的组织并配合药物治疗后，她已经10多天没有发生抽搐了。只要药物控制1年时间，小静完全可以摆脱周而复始抽搐、口吐白沫的生活。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 研究所所长、医院神经外科教授张新伟表示，据调查广东儿童的癫痫发病率为千分之六，对于儿童癫痫患者而言，如不及时控制病情、减少抽搐，患儿的脑功能发育将日益变差，影响智力和今后的生活。在目前影像设备发展、颅内手术日益精细的情况下，手术治疗已能实现最大程度切除病灶的同时保护孩子的功能区不受损害。“纠正脑发育不良还与及早发现疾病有关，儿童癫痫并非只表现为抽搐。低龄儿童频繁无故栽倒、发呆、傻笑都可能是癫痫，需及时治疗”。</span>
</p>
