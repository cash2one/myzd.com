		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>我国专家质疑癌症早期筛查无用说法(资讯)</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">前列腺癌筛查不能废</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;如果说上面这个“突破”，只是建议筛查乳腺癌推迟，那么另一个“突破”更为惊人：建议叫停前列腺癌的一切筛查和早期检测。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;2009年3月，美国国家癌症研究所公布了一项研究成果：一半志愿者被分配到筛查组，或手动检查，或前列腺抗原检查(PSA)。另一半志愿者不接受筛查，自行决定是否接受年度体检。结果证明，筛查组有50名男子死于前列腺癌，而另一组中则有44人死亡——这说明筛查和早期检测并不能降低前列腺癌死亡率。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;在美国，PSA筛查前列腺癌一直受到质疑。其原因是PSA检测的敏感性和特异性不高。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;“这不能否定前列腺筛查的意义。”北京协和医院泌尿外科严维刚副教授说，检查肿瘤有很多标志物，比起其他癌症，PSA筛查前列腺癌的敏感性是最高的了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;严维刚建议：只要超过50岁，就应该每年定期做前列腺癌的相关筛查，如PSA，很方便，抽点血就行了，现在不少大单位在体检中，已经为达到年龄标准的人选择了这项检查。如果体检没选，自己去检查，医保也是能报销的。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;前列腺癌临床治愈率较高，因此早期筛查非常必要。严维刚指出：70岁以下的早期前列腺癌患者可以采用根治手术或化疗。75岁以上身体不能耐受手术的，可以选择内分泌治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;一位不愿透露姓名的专家说：这两个“突破”也许可以给国人一个提示，那就是不要外国人说什么我们就信什么，特别是在健康问题上，“癌症筛查，有比无好，早比晚好——这是铁律。”</span>
</p>
