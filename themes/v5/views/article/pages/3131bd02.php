			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>成人适度做对眼可缓解视疲劳</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯 人们90%的信息来自于眼睛，而如今电视、电脑、手机等的生活方式往往会带来一些眼病，但这该如何防治？3月4日，在首个“重庆志愿者日”到来之际，第三军医大学大坪医院前往重庆联通公司本部大楼开展了爱心义诊活动，讲解眼的疾病诊治及预防、测视力及验光等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“公司35岁以下青年员工约占60%，因为工作需要长时间面对电脑，加上平时长时间使用手机，给眼睛带来了极大的负荷，现在不少员工都带上了眼镜。”重庆联通公司团委书记刘国平说，此次义诊也是为了让员工们合理、正确用眼。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据第三军医大学大坪医院眼科专科医院副院长、教授白继介绍，我国约有70%的青壮年有视疲劳现象，干眼症也是视疲劳的一种，长期看电视、电脑会诱发干眼症，出现眼睛干涩、痛、痒等症状。“虽然绝大多数的干眼症均不会致盲，但少数严重的干眼症，尤其是合并有全身免疫性疾病的干眼症有可能发生角膜炎、角膜溃疡，导致视力下降。如果不加以注意，会使视力严重受损，甚至失明。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在活动现场，白继教授现场分享了一个辨别干眼的小游戏：打开秒表，眼睛眨两下之后保持睁开动作，计算睁眼时间。分别以5秒以内、5秒至10秒、10秒至20秒、20秒以上为界，干眼现象依次递减，其中5秒内干眼现象严重。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">白继教授提醒，锻炼平时不使用或少使用的眼部肌肉有助于预防缓解视疲劳，比如成人适度做对眼；经常上下左右多方向让眼球移动；远部眺望时多看绿色及淡绿色景物；做眼保健操等。但市民若发现眼部不适，还应及时到医院检查就医。</span>
</p>
