<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海首次为复杂先天性心脏病患儿实施镶嵌式手术</strong></span> 
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 新华网上海９月２８日电（记者仇逸）上海儿童医学中心２８日推出小儿先天性心脏病“一站式”诊疗新模式，并打破专业壁垒，整合学科优势，在全市首次运用“镶嵌式治疗”（Ｈｙｂｒｉｄ）新技术为一名复杂先天性心脏病患儿成功开展心脏手术。&nbsp;</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据悉，来自上海儿童福利院的患儿康康已经１１个月，体重却只有５公斤，远不及正常健康男婴的体重。孩子呼吸无力，脸色煞白。检查显示康康患有肺动脉闭锁伴室间隔缺损，体肺动脉侧支血管形成、室间隔缺损、卵圆孔未闭等多种先天性心脏疾病。&nbsp;</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 虽然上海儿童医学中心是国内外开展小儿先天性心脏病手术量最多的儿童医院，然而，面对康康的病情，专家们却皱起了眉头。原来，如果运用传统的治疗方法分两步手术，那将会是个两难的选择。由于康康的肺动脉闭锁，右心室的血流无法进入肺动脉，肺血流灌注主要依赖于侧支血管，因此在外科根治术前，体肺动脉侧支血管的存在对于患儿非常必要，是维持肺血流量和血液氧合所必需的。如果先行外科根治术，将闭锁的肺动脉打通，那肺动脉就拥有了足够的血流，若仍保留这一侧支血管，可导致肺血过多，从而引起充血性心力衰竭。反过来，如果先行内科介入治疗将引起侧支血管封堵，那就等于将唯一一条维持康康的生命通道堵上了。只有打破内外专科的壁垒，联合内科介入治疗与外科手术，在外科手术中运用介入方式对侧支血管进行分离和结扎，才能达到最理想的效果。&nbsp;</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; ９月２８日，手术室迎来了两个学科的主刀医师、心脏内外科医师共同站上为镶嵌手术特别设置的手术台。两位医师默契配合，由心内科李奋主任采用经导管介入治疗的方法对侧支血管进行封堵；胸外科张海波主任则在血管封堵之时施行外科手术，开放闭锁的肺动脉，复杂心脏病在刹那间获得同时纠正，手术非常成功。&nbsp;</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“镶嵌式治疗”技术是当今国际上先天性心脏病治疗技术发展的新动态，即在同一空间和时间内运用心血管介入与外科手术的复合技术，充分发挥心血管介入治疗和外科手术的优势，治疗复杂的心血管疾病。这一新技术以患者为中心，融合多学科知识和理念，针对患者的不同病理生理状态，设计最佳治疗组合方案，从而以最小代价获得最佳的疗效。专家指出，随着介入治疗技术和外科治疗技术的发展，两种技术的优势和缺陷也逐渐被人们所认识。介入治疗尽管已经广泛运用于治疗先天性心脏病，但仍有很大局限性，如新生儿和小婴儿血管太细，导管无法进入。外科治疗仍然是复杂先心病主要的治疗方式，但手术创伤大，并发症多。内外科镶嵌治疗将打破以往心血管内外科各行其是的格局，实现优势互补，可使高风险患者得到更为有效安全的治疗，降低术后并发症，并明显减少医疗资源消耗。</span> 
</p>
