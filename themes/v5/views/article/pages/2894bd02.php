<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近年来，国人的癌症发病率连年增长，连我们身边的至爱亲朋也不能幸免。早在2013年,马云便在亚布力中国企业家论坛上大胆预言：十年后中国的每一个家庭都将被肝癌、肺癌、胃癌三大癌症困扰。这些令人痛心的事实也引发了沃恩斯医疗团队的深思：如何引发更多人对癌症的重视？如何传播癌症预防知识？如何从预防阶段降低罹患癌症的机率？如何帮助癌症患者进行术后康复？今年6月，沃恩斯抗衰老医疗中心得知客户张怡女士一直在默默帮助大凉山贫困地区的骨肉瘤患儿，当即决定加入这个行列，向更多不幸的孩子伸出援手。“OWNS沃恩斯2025行动”应运而生，这项计划将与包括瑞金医院在内的知名三甲医院合作，发挥双方优势，实现资源互补，在未来十年建立起癌症预防、治疗和康复的整体链条。</span><br />
