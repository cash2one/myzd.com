	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>晕厥抽搐并非都是癫痫</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">56岁的林女士生活在山东农村，5年前无明显诱因突然出现眼前发黑，持续约10秒钟左右，症状自动缓解。开始她以为是过度劳累引起的，没有在意。后来发作越来越频繁，突然晕厥、摔倒、抽搐、口吐白沫，最多时一天可发病10多次，常常摔得鼻青脸肿。曾在当地多家医院求治，均诊断为“癫痫”，口服“苯妥英钠片”，症状不见缓解，而且越来越重。春节后，慕名到解放军总医院第一附属医院癫痫门诊求治。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">神经外科副主任梁树立博士检查发现，林女士头上有多处血肿，眼部黑青，面部多处伤痕，口唇摔裂，有的伤口还在流血。经详细询问林女士发病时的症状并查看以往各种检查结果，认为很可能是心源性疾病。于是给林女士做了24小时动态心电图监测，结果显示，林女士心跳最慢时每分钟仅有30次，且有多次间歇性停跳，停跳最长时间达16秒。原来导致林女士经常晕厥、抽搐的元凶是高度房室传导阻滞。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">许多疾病都可以引起晕厥、抽搐</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">梁树立介绍说，像林女士这样的患者在门诊经常碰到，尤其是家长，发现孩子有晕厥、抽搐症状就想到可能是癫痫，到处检查，花了很多钱，非要查出是癫痫才放心。其实仅仅有晕厥、抽搐等症状并不一定就是癫痫，许多疾病都可以引起晕厥、抽搐。如低血糖、过度哭闹或呼吸引起的抽搐，低血镁、低血钙引起的手足抽搦症，青霉素、可卡因等药物中毒或有机磷农药中毒相关的全身抽搐。另外长期酗酒、吸毒者在戒酒、戒毒过程中也会出现抽搐，女性妊娠过程中出现的子痫、破伤风患者的主要临床症状也是抽搐，像林女士这样的心动过缓、高度房室传导阻滞以及心跳骤停、心源性晕厥、血压突然升高导致的高血压脑病、肝肾功能衰竭等引起的各种脑病均可出现全身的抽搐。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">儿童在生长发育过程中会出现一些生理性抽搐。如其神经系统发育不健全而引起的小儿生理性的睡眠肌阵挛发作，这种抽搐发作对其生长发育并没有影响，会随着其生长发育神经系统完善后逐渐消失，不必处理。还有的家长发现小孩睡觉的时候，突然抽搐着踹了一下腿，其实这是一种生理现象，成人也会有，只是不注意。另外还有热性惊厥，多在体温上升期和38.5℃以上出现抽搐发作，可以全身或部分抽搐，大部分患者在6岁后抽搐发作消失。但也有一部分患者在6岁以后转变为癫痫发作，需要特别关注。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">突发性、短暂性、反复性、刻板性是癫痫性抽搐的主要特征</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">梁树立说，抽搐是癫痫发作的典型症状之一，除此之外，癫痫发作的形式有多种多样，如全身抽搐、局部抽搐、手口部不自主动作、心慌、幻觉、全身无力等等，可以有意识障碍，也可以没有意识障碍，甚至可以部分意识障碍，也可以同时或者先后在一次发作中出现多个症状。癫痫是一种慢性脑部疾病，癫痫性抽搐往往具有与其他疾病引起的抽搐所不同的根本特点，即突发性、短暂性、反复性和刻板性。突然发病、突然结束，发作时间短，往往可以自行结束，每次发作的症状相似且反复多次出现，而且通常较难模仿。局限性运动性癫痫发作时患者往往仅为局部的抽搐，早期或整个过程中没有意识障碍。全面性发作的抽搐包括肌阵挛、痉挛、阵挛、强直-阵挛等多种形式，往往合并意识障碍，甚至摔倒、摔伤。所以，当发现患者有抽搐症状发作时，家人要特别留心观察是否有上述特点，最好能用手机做下记录供医生参考。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">要诊断是否癫痫性抽搐发作还需要脑电图的证据，特别是全面性癫痫发作的脑电图会有特异性诊断价值。如果有抽搐且有以下几种情况时要多考虑癫痫发作：在睡眠中发作、有摔伤等意外伤害、时间多数在1分钟之内且脑电图有异常。文/张献怀 杨帆</span>
</p>
