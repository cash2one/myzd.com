		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>你了解周围血管疾病吗？</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">周围血管疾病的发病率有逐年上升的趋势，但大众对这类疾病的了解却不一致，甚至一些非专科医师也会出现误诊和漏诊，如将典型的间歇性跛行当作腰椎间盘突出治疗，或是将下肢深静脉血栓形成误认为是丹毒。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">周围血管疾病，临床上是心脑血管病以外的血管疾病的统称，包括动脉、静脉及淋巴三大系统的疾病。动脉疾病常见的有：动脉狭窄闭塞性疾病，如老年人常见的动脉硬化性闭塞症，动脉栓塞；动脉扩张性病变，如动脉瘤；动脉炎症，如多发性大动脉炎、血栓闭塞性脉管炎、结节性动脉炎；外压性病变，如胸廓出口综合征、动脉挤压综合征；末梢动脉功能紊乱，如雷诺氏病（现象）；其他还包括动静脉瘘、糖尿病的周围血管病变等。静脉疾病有：浅静脉的静脉曲张和血栓性浅静脉炎；深静脉系统的则有：深静脉血栓形成、深静脉血栓形成后综合征、原发性深静脉瓣膜功能不全，深静脉血栓并发的肺栓塞；另外也包括布加氏综合征、K-T综合征等。淋巴系统疾病以炎症和回流受阻为主，如淋巴管炎、丹毒、淋巴水肿等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着发病率的增高，周围血管疾病的危害也逐渐为人们所重视。如各种部位的动脉瘤，就像隐藏在身体中的“定时炸弹”，一旦破裂处理起来十分棘手；动脉硬化闭塞症、动脉栓塞、糖尿病坏疽等可导致肢体缺血，不但会引起难以忍受的肢体疼痛，严重者甚至需要截肢；而颈动脉硬化狭窄则会导致中风、瘫痪；一些静脉和淋巴系统回流性疾病，如深静脉瓣膜功能不全、淋巴水肿可致常年下肢肿胀，甚至溃疡形成，形成俗称的“老烂脚”，严重地影响了病人的日常生活。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">周围血管疾病的症状因疾病的性质不同而不同。常见的下肢动脉狭窄闭塞性疾病，如动脉硬化性闭塞症，由于肢体远端供血不足，表现为初期的间歇性跛行，即行走一段距离后下肢的酸胀、乏力或疼痛，而休息后可缓解，后期症状加重，并会出现静息痛、肢端坏疽；而由于心房栓子脱落导致的下肢动脉栓塞，则病情变化要迅速的多，因患肢急性缺血会出现下肢凉、麻、痛，一旦出现不可逆的缺血极易导致大范围的肢体坏疽，很多患者不得不截肢以挽救生命。动脉瘤通常没有症状，除了位于表浅部位的可能偶然被患者本人摸到，深部的如锁骨下动脉瘤、髂动脉瘤和内脏动脉瘤则往往不容易发现，但它的危害却常常是致命的——未经治疗的动脉瘤增长到一定程度即会破裂。大隐静脉曲张的症状多数人耳熟能详，即下肢“青筋显露”、肿胀酸痛、色素沉着、瘙痒等，但这些慢性症状未必能引起重视，常常是到血栓性静脉炎或溃疡形成才来就诊。我们建议，一方面，大家应适当增强自我保健意识，有条件者应定期体检；另一方面，当怀疑有周围血管疾病可能时，应尽可能去血管外科专科就诊，以免贻误治疗时机。</span><br />
