		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>勿做粗心家长，贻误孩子“先天性白内障”治疗</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">季樱红（复旦大学附属眼耳鼻喉科医院眼科副主任医师）</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">卢　奕（复旦大学附属眼耳鼻喉科医院眼科主任医师）</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">先天性白内障是严重影响婴幼儿视力发育的常见眼病。主要表现是白瞳征，也就是瞳孔区出现白色反光。也可以伴有或表现为眼球震颤，眼球快速往复的摆动。家长如发现宝宝出现瞳孔发白、斜视、眼球抖动或摆动以及宝宝视力差或怕光等症状，需及时到医院就诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">白内障目前没有有效的非手术治疗。一旦诊断为先天性白内障，医生一般会综合患儿全身情况做出是否手术的决定。在全身条件允许的情况下，手术越早效果越好。因此通过对新生儿和婴幼儿的筛查，早期发现先天性白内障至关重要。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">先天性白内障的发病可以是单眼的，只影响患眼的视力，另一只眼睛视力正常，在此情况下，孩子双眼同时视物还是能看得见的。婴幼儿早期不会表达自己的视力是否清晰，家长如果比较粗心，不能及早发现孩子眼部异常或视力欠佳，致使某些先天性白内障患儿不能及早被发现。另外，有些已经被诊断患有先天性白内障的孩子家长，认为孩子还小，不适宜手术，同样会贻误该病的治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于先天性白内障，手术只是治疗的第一步，之后的屈光矫正、弱视训练以及定期检查是一项艰巨而意义重大的任务，需要家长的高度理解和配合。孩子术后视力和视功能的重建、提高，需要眼科医生和家长的共同努力。</span><br />
