<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>附属上海儿童医学中心张海波获评首届上海市“仁心医师”</strong></span> 
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在6.26中国医师节之际，首届上海“仁心医者”大型医护评选活动揭晓，上海交通大学医学院附属上海儿童医学中心心胸外科主任张海波医师获得“上海市仁心医师奖”。&nbsp;</span> 
<p>
	<br />
</p>
<p>
	<span><span style="line-height:19.09090805053711px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据介绍，以构建和谐医患关系、传播医护正能量为出发点，此项评选由市卫计委指导，市医师学会、护理学会、浦东新区广播电视台共同主办，邀请了知名作家六六、“老娘舅”柏万青等作为大众评审参与活动评鉴。评选活动历时七个月，在经历医疗卫生机构推荐、网络投票、医护单位实地走访拍摄、电视展播等环节层层筛选后，最终产生沪上10名“仁心医师”和10名“仁心护士”。&nbsp;</span><br />
<span style="line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3032/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张海波是上海儿童医学中心心胸外科主任，在他的从业生涯中，已主刀手术5,000余例，其中危重复杂先心病手术例数为3,500余例，成功率超过98%，最小年龄生后18小时，达到国际先进水平，曾获得中国医师协会心血管外科医师奖（金刀奖）。在他的带领下，心胸外科发扬了前辈们技术创新，管理创新的传统，自2009年心脏中心在国内率先开展先心病的“镶嵌手术”。2014年，心脏中心年门诊量超过20,000人次，手术量达3,800例，为世界儿童医院之最，扬名中外。2013年11月，应孟加拉国卫生部的邀请，张海波主任带领7人组成的小儿心胸外科精英团队，前往孟加拉国首都达开展小儿先天性心脏病诊治及技术培训的国际医疗援助，受到当地人民的赞誉。张海波曾获得许多重大的医疗科研奖项，然而，他对几十年来获得的具有重量级意义的荣誉淡然处之。他经常说，给生命以时间，还生命以笑容。对他而言，任何事物都没有人的生命来得珍贵。&nbsp;</span> 
</p>
<p>
	<span><span style="line-height:19.09090805053711px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海医师协会方面表示，上海首次表彰仁心医师标志着该协会在行业“自律、维权”道路上爱开崭新一步。今后该协会将积极协调政府与行业间的良性互动，增强医师职业自律，权利维护医师合法权益，加强建立行业内部监督机制，科学管理执业医师队伍。&nbsp;</span> 
</p>
