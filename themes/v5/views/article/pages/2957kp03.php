		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻窦炎的危害（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻窦炎的并发症有哪些?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(1)颅内并发症：并发硬脑膜外脓肿时症状并不典型，诊断比较困难，除鼻部症状外可有头痛加重(卧位尤甚)、恶心呕吐、脉缓等症。并发硬脑膜下脓肿时自觉症状有头痛、发热和脑膜刺激征。严重者可出现对侧面部及上肢肌肉瘫痪或抽搐，脓肿破溃后可引起化脓性脑膜炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(2)骨髓炎：慢性化脓性鼻窦炎经久不愈者易引起额、蝶、筛、上颌骨骨髓炎。以额骨骨髓炎常见，除鼻窦区压痛外，头痛性质是闷胀样钝痛。骨髓炎经久不愈还可有黄绿色鼻涕，x线拍片显示骨质结构不清、死骨形成和鼻窦骨壁缺损。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(3)病灶和下行感染：鼻窦化脓性分泌物从鼻咽部向下流注，可引起咽炎、扁桃体炎、中耳炎、气管炎等病。化脓性鼻窦炎作为感染病灶，还可引起感染性关节炎、腱鞘炎和皮肤病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻窦炎对人体的危害有哪些?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(1)直接感染：鼻腔与口腔、咽喉以及下呼吸道与消化道直接相通，鼻炎、鼻窦炎分泌物向下引流，直接刺激这些组织，引起各种病变。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(2)经血行感染：鼻腔和鼻窦具有丰富的血管网，并且与眶内、颅内有复杂的联系，所以鼻炎与鼻窦炎的细菌和毒素，先破坏血管内膜形成血栓，栓子随血行传至其他器官，引起并发症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(3)经淋巴管感染：鼻腔或鼻窦的淋巴管直接把炎性物质传布到其他器官或组织发病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(4)经解剖通道：鼻窦炎症可以从筛孔中传入颅内发病。此外鼻腔与鼻窦的许多血管也直接与眶内、颅内相通，炎症可沿血管通道引起并发症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(5)鼻窦外伤：引起骨折、穿孔伤或手术损伤等因骨壁断裂、组织破坏，使感染易于侵入而发病。</span>
</p>
