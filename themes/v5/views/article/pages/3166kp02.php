<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑胶质瘤兄弟姐妹一大家 厉害程度各不同</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这依旧是一个“谈癌色变”的年代，特别是颅脑的肿瘤，现在我们扒一扒大脑里边最常见的一种恶性肿瘤——脑胶质瘤。脑胶质瘤是大脑中枢神经系统的一种肿瘤，得了胶质瘤后，会出现头痛、癫痫、肢体瘫痪麻木、言语障碍、视力下降、恶心呕吐以及颅内出血等表现，不及时治疗可导致昏迷死亡。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正常情况下，大脑里边有许多各种各样的细胞，其中神经元是构成中枢神经系统的基本单元，也是大脑中枢正常运转的基本元件。除了神经元以外，其他的细胞可以统称为神经胶质细胞，这些胶质细胞就相当于神经元的养分“搬运工”，主要是为神经元供给血氧，以保证其正常工作。（如下图所示）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3166/1.png" title="" alt="demo.jpg"/>
</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果哪天中枢的胶质细胞不好好生长繁殖，而是发生了可怕的“恶变”，那问题就严重了。由于大脑中枢的细胞有许多兄弟姐妹，于是人们按照细胞种类也给肿瘤分了类，比如星形细胞瘤、少枝（或少突）胶质瘤、室管膜瘤、混合性胶质瘤、脉络丛乳头状瘤、来源不肯定的神经上皮组织瘤、神经元及神经元神经胶质混合瘤、松果体实质肿瘤、胚胎性肿瘤、神经母细胞瘤……这么多分类，数一数也是醉了。为了更好地进行诊断和治疗，人们又将这些不安分的兄弟姐妹按照厉害程度分了类，不同的分类对患者寿命的影响也不同。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">分类如下：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1级（I级）：比较少见。但治疗效果比较好，如果能够全切，可以治愈不复发。这算是最为温柔的小妹妹了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2级（Ⅱ级）：主要包括星形细胞瘤（星形细胞胶质瘤）、少突细胞胶质瘤、少突-星形细胞胶质瘤、室管膜瘤、粘液型毛细胞星形细胞瘤。得了这种脑胶质瘤，如果切除肿瘤的风险比较低，能够全切，那么术后的平均寿命可以达到8~9年；如果没法全切，肿瘤风险值比较高，那么复发的可能性就更大，术后平均寿命会下降至3年左右。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3级（Ⅲ级）：主要包括间变型星形细胞瘤（星形细胞胶质瘤）、间变型少突细胞胶质瘤、间变型少突-星形细胞胶质瘤、间变型室管膜瘤。基本上都是“间变型”的，这就更为厉害了，它意味着患者可能只有1年半到3年的寿命了，术后的复发情况比较猛烈的。如果不手术，生存期就更短了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4级（Ⅳ级）：胶质母细胞瘤（又称多形性胶质母细胞瘤）、胶质肉瘤，这是最为厉害的一种脑胶质瘤了，得了这种瘤往往意味着只有平均14个月的寿命了，术后14个月左右很快就出现反攻了。如果不治疗，生存期就更短了，一般是几个月。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;由于细胞的分化程度越低，肿瘤的恶性程度就越高。所以这四种脑胶质瘤的分类中，3、4级的非常厉害，也非常难以对付，称为高级别的脑胶质瘤；1、2级的一般没那么厉害，及时对付还是可以有效控制的，就称为低级别的脑胶质瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外决定寿命的不仅仅在于肿瘤的厉害程度，还要看肿瘤长在大脑的什么位置和手术切除的程度，以及术后是否接受了正确的放化疗。肿瘤切除得越彻底，越不容易复发。如果肿瘤生长在支配肢体活动的脑中央运动区、重要的生命中枢脑干等部位，那么为了保护正常的神经功能，就没法做到彻底切除了，肿瘤就容易复发；如果术后进行了正确的放化疗，可以有效地延缓复发，疾病发展的结果就比较好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但并不是说所有胶质瘤都要先“切”为快。有的体积很小、没有长在功能区的肿瘤，可以考虑带瘤生活。如果肿瘤位置深，且毗邻重要的血管神经，但肿瘤直径不大，患者也没有任何症状，暂时也可以不选择手术切除；如果肿瘤是颅内多发型，大脑里边到处都是，那么就丧失了手术机会，这类患者可以在活检明确病理类型后，考虑放化疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然在查出脑胶质瘤的那一刻，患者无法决定肿瘤到底厉害到什么程度，也把握不了肿瘤生长在什么位置，但我们可以把握的是及早诊治，认真听取大夫的建议，能够及早切除的就及早切除，并针对情况做好术后的放化疗；不能切除的就要积极采取综合治疗的办法，不能听之任之，但也不能病急乱投医。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">瘤子有时就像野草，野火烧不尽春风吹又生，所以一定要尽量将可能带来复发的肿瘤细胞扼杀在摇篮中，才能活得长活得好。</span>
</p>
