<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;胰腺癌患者常有恶液质，对感染及创伤的抵抗力明显下降，易发生术后感染、伤口愈合不良等并发症。这类患者单靠饮食很难改善其营养状况，必须依靠肠内或肠外营养。应尽可能选用肠内营养，留置鼻肠营养管，滴注肠内营养制剂和PTCD回收的胆汁，一般应用10～14日，同时纠正水电解质失衡、贫血和低蛋白血症，以维持机体血流动力学的稳定，增强耐受手术的能力。</span><br />
