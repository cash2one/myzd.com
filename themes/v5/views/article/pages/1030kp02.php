<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 肠外营养适用于围手术期、胃肠道功能障碍、高分解状态及无法耐受肠内营养的病例，肠内营养可防止肠道菌群易位，同等热卡及含氮量肠内营养吸收优于肠外营养，故应尽早开始肠内营养。</span><br />
