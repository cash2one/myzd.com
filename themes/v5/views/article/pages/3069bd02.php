		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>两成早产儿视网膜病变</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">摘要：受大龄孕产妇增多、社会压力等因素的影响，新生儿中早产儿所占比例越来越高，部分医院甚至占到了40%以上的超高比例。其中20%左右会因视网膜发育不健全及出世后吸氧浓度过高而导致视网膜病变。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 本报讯 (记者王道斌 通讯员 冯光强)受大龄孕产妇增多、社会压力等因素的影响，新生儿中早产儿所占比例越来越高，部分医院甚至占到了40%以上的超高比例。其中20%左右会因视网膜发育不健全及出世后吸氧浓度过高而导致视网膜病变。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 记者昨日从广州市早产儿视网膜病变筛查中心获悉，该中心开展这一专项筛查工作以来，已累计为数千名早产儿进行了筛查、干预，其中通过治疗和手术得以恢复光明的就有140多人。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据筛查中心主任、广州市妇女儿童医疗中心眼科主任项道满介绍，妇儿医疗中心也曾专门前往广州市盲校进行过相应的调查，几年前还是遗传性、先天性疾病或后天意外伤害导致的盲童排名靠前，但最近几年，因为早产儿视网膜病变而进盲校的盲童明显增多，已经成为导致盲童产生的第一大因素。不过，“只要早期发现，95%以上的早产儿都能获得良好的治疗效果避免失明。”项道满表示。</span>
</p>
