<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">颈椎病的治疗</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">唐家广主任说，颈椎病，是一种以退行性改变为病理基础的疾患，退变引起的椎间盘突出、骨赘增生或者韧带钙化等，压迫了神经或者脊髓而产生症状。临床上以神经根型、脊髓型颈椎病最为常见。颈椎病的临床表现较为复杂，主要有颈部僵硬、活动受限、上肢放射性疼痛或麻木、下肢乏力、行走不稳或者有踏棉感，部分严重的患者可能会出现行走困难、大小便功能障碍等，如不处理的话，是一种致残率较高的疾病。颈椎病的治疗，大体可分为保守治疗和手术治疗。保守治疗适用于大部分的神经根、交感神经型和早期症状较轻微的脊髓型颈椎病患者。绝大多数的脊髓型颈椎病及长期保守治疗效果不佳的患者，比较适合选择手术治疗，来去除神经或者脊髓的致压因素，患者的症状才会得到较好的恢复。</span>
</p>
