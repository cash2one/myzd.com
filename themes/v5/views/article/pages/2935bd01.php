<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">唐家广，医学博士，主任医师，骨科副主任，硕士生导师。1990年就读于第四军医大学。2005年至2007年在第二军医大学攻读博士后。2010年12月赴美国巴尔的摩St.Joseph医疗中心，进修脊柱生物力学和脊柱非融合技术专业半年。师从解放军总医院第一附属医院著名骨科专家、全军骨科研究所所长侯树勋教授，现任解放军总医院第一附属医院（原为解放军第304医院）脊柱脊髓损伤科（骨五科）主任。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">病例治疗情况分享</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1 颈椎病、颈椎退行性骨关节病</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">刘阿姨，患颈椎病十年了，双手持物不稳、手指麻木，一直没有重视，直到多次出现走路时摔倒才到医院就诊。一年里去过多家三甲医院，因为害怕手术风险均未接受医院的手术建议，试过了很多种保守治疗的方法，不见起效反而症状逐渐加重。抱着试试看的想法，经过别人的推荐，刘阿姨找到了唐家广主任。唐主任看了病人的资料后，说手术虽存在一定的风险，但还是比较有把握的，恢复的希望也很大。唐主任很快为刘阿姨安排做了手术。经过近1个小时，唐主任成功的完成了三个节段的减压固定融合手术。术后第一天，刘阿姨佩戴好颈托后下床活动，明显感觉到足底踩棉感消失，手指的麻木感也明显减轻了。术后的刘阿姨兴奋地说，终于可以踏踏实实地走路了。</span><br />
<img src="http://static.mingyizhudao.com/147003328336888" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">唐家广主任在做手术</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2 颈椎外伤性骨折脱位</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一位河北的患者，遭遇车祸、从昏迷中醒来后发现颈部剧痛，四肢不听使唤。他人救起后转到解放军总医院第一附属医院急诊，唐家广主任接到急诊通知后，立即赶往急诊查看患者。综合检查后很快诊断为颈椎外伤、颈脊髓损伤、颈3椎体滑脱伴骨折等。经过缜密的术前准备，唐主任很快为患者安排做了颈椎的减压固定融合手术。手术很快顺利完成，术后第二天患者的四肢活动就明显好转，并经过一段时间的功能训练和康复治疗，患者的四肢活动功能恢复的很好。出院时，患者家属送了一面“医术精湛，情暖人心”的锦旗以表达对唐主任的感激之情。术后三个月患者来复查，健步走进科里的时候，喜悦之情溢于言表，科里的医生护士也为他感到高兴。看到患者术后恢复的如此之好，唐主任也倍感欣慰。</span><br />
