		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我国烟雾病治疗的领跑者</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于许多人来说，烟雾病是陌生的，甚至在早几年，很多医生对这个病都不太了解。但是现在，一提起治疗烟雾病，就要提到解放军307医院全军脑血管病中心主任段炼。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病最早是日本人发现并命名的。由于该病在做血管造影检查时，大脑里的血管形态如同烟囱里冒出的袅袅炊烟，故形象地称之为烟雾病。由于烟雾病临床症状复杂多样，所以极易造成误诊或漏诊。患者如果得不到正确治疗，轻则偏瘫致残，重者危及生命。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这种病在最初发现时，曾被认为是日本特有的一种疑难疾病，后经研究证实，各国都有该病患者，但发病率最高的是日本、韩国等地区，近年来我国烟雾病的发病率也逐年增高。但当时我国的医生对该病缺乏了解，往往当作脑炎、癫痫来治疗，很多教科书上对这种病的描述也是“不可治”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1996年，段炼赴日本攻读神经外科博士学位，师从日本著名烟雾病专家松岛善治教授。正是跟着这位老师，段炼系统地学习了烟雾病的发病机理、流行状况、诊断方法和治疗技术。毕业后，他踏上了归国之路，并暗下决心——一定要“拯救”我国烟雾病患者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 段炼清楚地记得，接诊的首例烟雾病患者是一位浙江中年男子。患病前他是一家企业老板，而长期的病痛使他半身瘫痪、偏盲、不会讲话、不会认字。由于四处求医无果，深感绝望的他在遇到段炼后，对段炼提出的手术治疗非常怀疑。段炼耐心地与他和家属沟通了三个多小时，详细讲解了烟雾病的来龙去脉、治疗机理以及国际上最新的治疗方案。最终，患者重新燃起希望，同意接受手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 经过周密的准备，段炼成功地为这个患者实施了手术。术后没几天，患者的视力就有所恢复。“有一天我去查房，一进屋就看到他把报纸贴在窗户上，正在练习阅读。”段炼说，当时他已经能够清楚地看到报纸上的大标题，之后，患者的说话、记忆功能也逐渐恢复。出院后，他又能正常工作和生活了，还能骑着摩托车到处“跑”。十年过去了，这个患者仍与段炼保持着密切联系。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这次手术的成功，标志着我国烟雾病治疗获得突破。也正是这次救治，段炼被业界称为烟雾病治疗领域的领跑者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “怎样让更多烟雾病患者得到及时有效的治疗？”段炼引发了新的思考。当时，国内还没有人对烟雾病领域进行深入系统研究，段炼开始将我国烟雾病患者发病特点、发病地域、男女比例等进行系统研究分析，并探索出明确的诊断标准和有效治疗方法。不仅如此，段炼还自觉担当起烟雾病治疗的“布道者”。无论是在全国学术会议上，还是在专业培训中，他都找机会对烟雾病进行宣讲、交流，同时经常在学术期刊上发表相关文章。他的学术论文“烟雾病在中国”发表在国际脑血管病权威杂志《卒中》上，引起国内外同行的关注。他还代表中国神经外科领域学者，先后应邀在美国神经外科年会、欧洲神经外科年会上做专题报告。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着越来越多医生对烟雾病认识的提高，国内各大医院也纷纷开展烟雾病手术，很多患者及时得到治疗。“曾因烟雾病辍学的女孩如今考上清华大学；曾瘫痪在床的年轻患者如今已结婚生子……每当看到患者恢复健康，我都感到说不出的欣慰。”段炼说，他们是我快乐的源泉和不竭的动力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前，解放军307医院全军脑血管病中心在段炼的带领下，已成功为3100例患者实施颅内外血管重建手术。“不但要让我国的烟雾病治疗普及化，更要让我国烟雾病治疗世界领先。”在众多成就面前，段炼肩上的担子更重了，“目前烟雾病的发病原因、发病机理还不清楚，因此无法预防，也没有药物可以治疗，未来还有许多难题需要攻克，我有信心！”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
