<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>男性更年期&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>起因：</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">男性更年期是近几年才被人们意识到的一个新的名称，它的“学名”叫“老年男性雄激素部分缺乏”。 男性更年期综合征是指男子在一定年龄内，骤然发生的各种反常心理状态，并由此产生的各种各样、轻重不同的临床表现。本病多发于50～60岁的男性，程度的轻重也很不相同，轻者可没有感觉，重者反应较明显。常见的临床表现有：在精神心理方面，注意力不集中，办事缺乏信心，工作能力减弱，记忆力、应变力均较差，处理事情优柔寡断，陷于悲伤、焦虑、猜疑、偏执、烦恼状态中。自觉体力不支，需求更多的休息才能应付日常工作。性功能方面，患者性欲、性反应、性能力持续减弱，***不应期延长，精液量减少，精子质量下降，有时出现阳痿、早泄。在其它方面，患者还可出现头晕耳鸣、失眠多梦、食欲不振、大便秘结或稀溏，小便短少或清长等多脏腑功能失调的症状。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着年龄的增加，男性雄激素水平逐渐降低，身体和心理出现相应的变化，产生与女性更年期部分类似的症状，以前称之为“男性更年期综合症”；实际上，男性与女性的情况不尽相同，例如男性不存在绝经等更年期开始的信号，症状发展不明显，内分泌代谢机制也不同于女性，故现在把这组症状命为“老年男性雄激素部分缺乏（PADAM）”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">与女性不同，男性性腺的衷退有较大的个体差异。部分男性从40岁开始出现血浆睾酮缓慢减少。老年男性除睾酮绝对水平降低外，其分泌的节律也消失，且血清性激素结合蛋白增加使游离睾酮相对减少，总的说来组织可以利用的雄激素减少了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据国外统计，40岁至70岁男性约有40％发生该病的某些症状。有趣的是并非所有雄激素减低的中老年人都有上述症状，而有症状者血睾酮也不一定都减低，其中许多还有待于进一步研究。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>症状：</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该病的症状多而复杂。患者多数表现为性欲减低、勃起功能障碍，心有余力不足，非常沮丧和苦恼。实际上，正是性功能的变化才使一些患者头晕心慌、四肢发凉，有说不清部位的疼痛，体检却没有异常发现。抑郁、焦虑、易怒、神经质也比较常见，常常影响患者与家人或同事的关系。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、精神症状：主要是性情改变，如情绪低落、忧愁伤感、沉闷欲哭、或精神紧张、神经过敏、喜怒无常，或胡思乱想、捕风捉影，缺乏信任感等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、植物神经功能紊乱：主要是心血管系统症状，如心悸怔忡、心前区不适，或血压波动、头晕耳鸣、烘热汗出；胃肠道症状，如食欲不振、腹脘胀闷、大便时秘时泄；神经衰弱表现，如失眠、少寐多梦、易惊醒、记忆力减退、健忘、反应迟钝等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、性功能障碍：常见性欲减退、阳痿、早泄、精液量少等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、体态变化：全身肌肉开始松驰，皮下脂肪较以前丰富，身体变胖，显出“福态”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>诊断：</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊断该病需要根据典型的临床表现和血睾酮水平，需要特别注意除心脑血管疾病外，高血压、糖尿病，恶性肿瘤等也可能有类似表现的躯体症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>治疗：</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">改善症状首先要加强体育锻炼，增强体质，振奋精神，保持平和乐观的情绪，养成良好的生活习惯。家人、同事的关心和理解也很重要。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">补充睾酮的疗法在理论上有其合理性。实践证明，睾酮治疗可以改善总的健康状态和情绪，提高性欲，增加肌力和骨质密度。目前较为理想的药物是口服睾酮类药物。　实践证明，睾酮治疗可以改善总的健康状态和情绪，提高性欲，增加肌力和骨质密度。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前较为理想的药物是口服睾酮类药物。但必须注意补充睾酮可能加重潜伏的前列腺疾病，如前列腺增生和前列腺癌。因此，在开始治疗前应详细检查。但必须注意补充睾酮可能加重潜伏的前列腺疾病，如前列腺增生和前列腺癌。因此，在开始治疗前应详细检查，如前列腺癌和前列腺增生造成的下尿路梗阻。此外，高龄男性发生前列腺疾病的可能性更大，应慎用。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中医理论对此症有一定的见解，根据不同的分型，选择相应的治疗加以调理，如滋肝养肾等。补充睾酮的疗法在理论上有其合理性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>预 防：</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">要从小事情做起首先是不能手淫。其次是过了40岁就要间断服用袋鼠精，海狗油，复合维生素等等保健药物其次性生活也要适度。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本病主要以功能衰退为特征，一般来说，病人症状虽很明显，但临床体检及辅助检查均无异常表现，因此，家庭调护时，应注意以下几个方面：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正视疾病，增强信心男性更年期综合征是男性生命过程中正常的生理反应，有一定的必然性和阶段性，其反应程度因人而异，只要重视疾病，积极治疗，建立良好的心态，是完全可以安全度过更年期的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物治疗，必不可少本病以功能衰退为主要表现，因此，适当的药物治疗是必不可少的。焦虑、忧郁、影响正常生活者，可口服泰尔登或丙米嗪；猜疑、紧张为主者，可用利眠宁或安定等中枢镇静剂；有幻觉、举止失常者，可用氯丙嗪治疗；性功能障碍严重者，可肌注雄性激素。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">适当调护，十分重要本病光靠药疗难以取得持久疗效，因此，应适当调节生活习惯，加强身体锻练，参加体育活动，提高身体素质，调理饮食，戒除烟酒，遵守作息时间，节制房事，保持心情愉快，避免劳累，减少烦恼。也可通过寄情山水、丹青等，修身养性，愉悦身心。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中药调节，疗效独到中医认为，本病多为肾阴阳失调，脏腑功能紊乱所致，当以平补阴阳，调理脏腑为主。一些补肾的中药及药膳食疗方如枸杞、肉苁蓉、何首乌、淫羊藿、冬虫草、灵芝、山药、核桃、狗肾及首乌、小米、猪肾粥，虫草炖鸡，鹿茸虫草鸡，锁阳子鸡，狗肾炖鸡，龙虾子鸡，牛鞭乌鸡等，也可根据病情常用。</span>
</p>
