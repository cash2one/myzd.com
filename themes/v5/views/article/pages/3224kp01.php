			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>内镜鼻眼相关外科</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主要用于治疗慢性泪囊炎、外伤性视神经病、某些眶内异物和肿瘤、重度球后视神经炎和恶性突眼（Grave,s病）等</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上世纪90年代之前，慢性泪囊炎、某些眶内异物和肿瘤、外伤性视神经病、重度球后视神经炎以及恶性突眼（Grave,s病）的手术入路是经面部切口进行的。这种手术方式的缺点是远离疾病部位、创伤大、恢复期长和术后面部遗留疤痕。内镜鼻眼相关手术是上世纪90年代建立的外科新技术。这项新技术是以鼻内镜微创外科技术为基础，经鼻腔鼻窦进路，进行眼眶疾病手术的外科技术。该技术最重要进步是：耳鼻咽喉头颈外科医生利用熟悉鼻腔鼻窦与眼眶仅一板之隔的解剖学特点，创建了经鼻腔鼻窦抵达眼眶和视神经骨管的进路。因此，内镜鼻眼相关手术是进路最捷近、微损伤、痛苦最小、恢复期短、面部无疤痕、疗效和传统手术方式相同的新技术，但内镜鼻眼相关手术难度较高，风险较大。经10余年的临床实践，内镜鼻眼相关手术已得到眼科学界和眼科医生的赞同。我科在具有精湛的内镜鼻腔鼻窦手术技术的基础上，拟开展鼻眼相关性疾病的诊断和治疗。我本人自2001年起在国内率先开展这一新技术，已经诊治鼻眼相关性疾病100余例，取得了良好的效果和丰富的经验。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">内镜鼻眼相关手术包括五种术式：经鼻内镜视神经减压术，经鼻内镜泪囊鼻腔开放术，经鼻内镜眶内异物取出术，经鼻内镜眶内肿瘤切除术，经鼻内镜眶减压术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、外伤性视神经病：这种疾病是因头部外伤后引起的，临床表现是视力骤减或失明。绝大多数是因不慎车祸撞击头部所致（多数是骑摩托车）。头部被猛烈撞击后，鼻窦骨多会发生骨折，视神经正位于鼻窦旁而受到损伤，一般是因骨折的骨片压迫视神经，或者是视神经同时被拉伤水肿，导致视力减退或者失明。因此需要采取经鼻内镜视神经减压术。视神经损伤需要急诊手术，受伤后7天内接受手术效果较好，超过10天后手术效果较差。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、慢性泪囊炎：是眼科常见病。传统手术方式是需要在颜面部眼内侧做切口，手术损伤较大，手术后容易遗留面部疤痕。采用经鼻内镜泪囊鼻腔开放术，可避免面部切口，在鼻内镜指引下手术，达到精细、准确的效果，疗效可靠。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、重度球后视神经炎：球后视神经炎是鼻窦积脓、巨大鼻窦囊肿、或不明原因引起的视神经炎症，导致视力减退，严重者失明。重度球后视神经炎可采取经鼻内镜视神经减压术进行治疗，以挽救视力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、眶内异物和眶内肿瘤：眶内异物是指外部物体因各种原因进入眼眶内，多数是枪弹误伤所致。眶内肿瘤是指原发于眼眶内的良性肿瘤。传统手术方式治疗眶内良性肿瘤需要在颜面部眼周做切口，手术损伤大，手术后遗留面部癍痕。采取经鼻内镜眶内异物取出术和经鼻内镜眶内肿瘤切除术，避免了面部切口，在鼻内镜指引下手术，达到精细、准确、并发症少的效果。但是，并非所有的病例都能采取这种手术方式。只有异物或肿瘤位于视神经内侧、靠近鼻窦的病例才适合选择这种手术方式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、恶性突眼：也称Grave,s病，是毒性甲状腺肿合并的眼球严重突出的疾病。由于眼球剧烈突出，不能闭眼，使角膜长期暴露在空气中，最终导致角膜干燥坏死而失明。内科治疗无效或效果不佳者可采取眶减压术使眼球回纳。传统手术方式需要在颜面部眼周做切口，而采用经鼻内镜眶减压术，可避免面部切口，术后不遗留面部癍痕，手术创伤小。鼻内镜指引下手术更精细、准确、并发症发生率低。</span><br />
