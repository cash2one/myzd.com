			<div style="text-align:center;">
	<span style="line-height:1.5;font-size:18px;font-family:'Microsoft YaHei';"><strong>肾囊肿会恶变成囊性肾癌吗？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随著体检影像学检查（B超、CT 、甚至是MRI 或PET-CT）的广泛应用，肾囊肿被发现的越来越多。对这些囊肿如何处理，很是让当事者心烦。虽说肾囊肿绝大部份是良性的，但哪些又是恶性的？那些囊性肾癌是不是囊肿恶变而来？这是经常被问及的问题，有时候不能说清楚，因为一说清楚就会伤到人，更多的时候则是没法说清楚，因为其中许多科学问题现在还没搞清楚。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先是肾囊肿会不会恶变？囊性肾癌是不是由囊肿恶变而来？到目前为此找不到任何科学证据来回答这个问题。就个人的知识和经验来看，囊肿就是囊肿，囊性肾癌就是囊性肾癌，它们之间没关系，如果一定要说有关系的话就好像羊和披著羊皮的狼一样。这样说一定会有人举例反驳，说“某某某好多年前发现肾囊肿，后来恶变了，变成囊性肾癌了不是吗？”。临床上也确实会见到“肾囊肿恶变为囊性肾癌”的病例，但仔细回顾其中一些影像资料完整的病例（从一开始就有平扫+增强CT扫描的病人）后发现，在发现肾囊肿之初，那囊肿其实就是囊性肾癌，而根本不是什么囊肿，是误将囊性肾癌当成肾囊肿了。有个病人发现“肾囊肿”5年，偶然的机会通过他的朋友（也是我的朋友）要我看看他的CT片子，一看吓一跳，这不是囊性肾癌吗？而且是如此之大局部还有一个肿大的淋巴结（即很可能有淋巴结转移）。当即告诉他，囊肿恶变成囊性肾癌（非常典型的那种）了，需尽快手术治疗。他接受了建议，作了经典的肾癌根治术即整块切除了患肾和其周围的肾脂肪囊、肾周筋膜、肾上腺和区域淋巴结。术后详病理检查证实是肾透明细胞癌，有一个淋巴结转移。他至今活著，10多年了。如果当时告诉他，囊性肾癌被误诊了5年，对他有什么影响？对一直诊断他为肾囊肿的医生有什么影响？对当下的治疗又有什么影响？恐怕除了负面的影响之外没有任何积极的意义。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">囊性肾癌为什么会被误诊为肾囊肿？如何区别肾囊肿与囊性肾癌？这也是常常被问到的问题。其实三言两语说不清楚。囊性肾癌为什么会被误诊为肾囊肿？简单一点说吧，就有些囊性肾癌太像肾囊肿了，所以被误诊了。如何区别肾囊肿与囊性肾癌？有两点要特别注意，第一要弄清楚肾囊肿与囊性肾癌的特点，第二诊断和区别肾囊肿与囊性肾癌一定要做平扫+增强薄层CT扫描。根据CT扫描的特点，将肾脏的囊性占位病变分为单纯性肾囊肿和复杂性肾囊肿，前者内壁光滑无分隔，后者有分隔、内壁不光滑或有结节，特别是内壁不规则有结节并在增强CT扫面上结节有增强表现，要高度怀疑是囊性肾癌，事实上有这样表现的肾囊肿，手术切下来作病理检查70-80%是囊性肾癌。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">发现肾囊肿后怎么办？先做双肾平扫+增强薄层CT扫描(没做这项检查的话)，明确囊肿是单纯性还是复杂性，如果是单纯性肾囊肿，囊肿不大、不影响肾功能则可定期观察（每年作一次彩超检查），如囊肿比较大或可能影响肾功能，行囊肿去顶或囊肿穿刺注射硬化剂处理均可。如为复杂性囊肿，处理要非常慎重，最安全的做法是像对待肾癌一样将囊性病变完整地作整块切除，穿刺或去顶手术可能引发灾难性后果。如果囊肿比较小（如2厘米以内），一时无法确切判断，定期作彩超检查和CT扫描密切观察可能好过匆忙处理。</span>
</p>
