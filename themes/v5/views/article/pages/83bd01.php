<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全国人大代表、北京大学首钢医院院长顾晋对提高全民医疗保障的议题非常感兴趣，建议将肿瘤类疾病的医保向早期预防筛查和晚期护理两端延伸。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“目前，癌症发病率是十万分之三百，概率并不低。但医保范围内的肿瘤病治疗主要集中在中期，也就是化疗、放疗等阶段，而早期的筛查和晚期的护理都没有涵盖其中。”顾晋代表说，应逐步改变这一思路，通过早晚两端的全覆盖减少患者的痛苦，提高患者的生存几率。同时，此举也能在一定程度上缓解国家的资金保障压力，可谓是利国利民的双赢举措。</span>
