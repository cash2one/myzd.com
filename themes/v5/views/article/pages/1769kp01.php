			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>先天性外耳、中耳畸形</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性外耳、中耳畸形，亦称“先天性小耳”。由第一、第二鳃弓及第一鳃沟发育异常引起，可发生于单侧或双侧。与遗传因素有关，也可能与妊娠期病毒感染、服用某些药物（如水扬酸类）、内分泌异常、胚胎局部缺氧、接触放射线有关。先天性小耳，特别是双耳者，常伴听力障碍。除影响患儿学习语言外，亦影响患儿的心理、智力发育，这点必须引起足够重视。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根据畸形程度分3级</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Ⅰ级：耳廓小而畸形，各部尚可分辨；外耳道狭窄或部分闭锁，鼓膜存在，听力基本正常。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Ⅱ级：耳廓呈条索状突起，相当于耳廓或仅有耳垂，外耳道闭锁，多伴中耳发育异常，呈传导性聋，此型最为常见。 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Ⅲ级：耳廓残缺，仅有零星而不规则的突起，外耳道闭锁，听骨链畸形,可伴有内耳功能障碍,耳聋严重，约占据2%。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗方法：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">包括：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp;听力补偿：双耳畸形者,一般听力比正常人差,在1岁左右应选配助听器,以免影响其语言发育和学习。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 手术治疗：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">⑴听力重建，行外耳道、鼓室成形术。 &nbsp; &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">⑵耳廓再造或义耳。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;手术时机：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;（1）双耳畸形者：在6岁左右 &nbsp; &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 对畸形较轻耳手术，以提高听力，促使病儿语言、</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 智力发育正常。 &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;（2）单耳畸形者：一般在18岁时手术。行听力重建和耳</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 廓再造术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术后注意事项：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）患者术后要定期复查，换药，防止术腔感染及耳道再狭窄。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）定期行皮下水囊注水扩张皮肤，为Ⅱ期耳廓再造术作准备。</span><br />
