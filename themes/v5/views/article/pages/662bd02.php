			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>郑树森：攻克终末期肝病难题 圆器官移植“中国梦”</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“很多病人要庆祝生日时也邀请我们去。他们说第一个生日是父母给的，第二个是我们这个团队给的。”中国工程院院士，浙江大学附属第一医院院长郑树森说。到现在，他已做了1850例肝移植，将很多以前被认为无法治疗的肝病患者从死亡线上拉回来。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在过去，肝衰竭一旦发作，死亡率高达80%以上，肝癌患者即便做完手术，他的三年或五年存活率仅在45%左右。这些可怕的疾病都属于终末期肝病范畴。在70、80年代，郑树森就发现了这个严峻的医学难题，之后，在中国这个肝病大国提高终末期肝病患者的存活率便成为他一直为之奋斗的目标。通过郑树森和妻子李兰娟带领团队的努力，肝衰竭病人3年5年的存活率达到80%以上，肝癌患者3年存活率可达到75.7%-78%。他们攻克了这个国际医学难题，创造了一个个奇迹。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如今，这个以“提高终末期肝病诊治水平，降低重症肝病患者死亡率”为目标的团队，已为终末期肝病的诊治默默耕耘了30年，帮助了大量濒临死亡的病人重获新生。2016年1月，郑树森和李兰娟共同领衔的“终末期肝病综合诊治创新团队”项目，荣膺2015年国家科技进步奖创新团队。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 只要把人救回来，比什么都好</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 郑树森和李兰娟这对科学界少有的院士伉俪被学生称为“医学狂人”，工作到半夜一、两点是家常便饭。在常人看来十分平常的准点下班，对于郑树森和妻子来说却是件奢侈的事。他说，“医生其实24小时都在上班。就算我回到家里，我的手机还开着，经常是半夜两点钟我科里年轻医生跟我讲，有一个病人要抢救，叫我到医院去做手术，我披上衣服就回医院。这对于我和李院士来说，已经习惯了。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 从70年代在医院工作到今天，郑树森基本上没有节假日，本该他休息时间，他都奉献给国家和医院了。“这里主要是我们对事业有足足的追求和兴趣。既然走上做医生这个道路，一定要付出的。”郑树森说，“要做一个有水平的医生，必须要付出很多，兢兢业业，不怕苦不怕累。实际上病人就是我们的老师。从治疗病的过程中，我可以了解到、探索到这个疾病的规律。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1993年4月29日，是郑树森做第一例肝脏移植手术的日子，而这次手术也是他印象最为深刻的一次。他要为有一个患6.7公斤巨大肝癌的病人做手术。这个手术一做做了19个小时，郑树森没有吃饭，也没有休息，汗水从额头上不停地滴下来，他的精力高度集中，只隐约记得有人给他喂了一些椰子汁来。最后手术圆满成功，这个病人终于摆脱了肝病带来的巨大痛苦和大的惊人的肚子，重获了新生。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “虽然手术有时候做的时间很长，很辛苦，但内心是非常开心的。只要这个人救回来了，那比什么都好。”这就是作为医生的郑树森最朴素却也最真挚的愿望。事实上，郑树森从医救人的梦想，在童年已经根植于心了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 上学路过的诊所成为他“从医梦”的开始</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 郑树森小时候上学的路上有一个诊所，里面总是飘出各种药味。他就爱跑去那里看老中医给人看病。那里有一个胡子很长的老中医在里面切中药，给别人搭脉看病，用毛笔写药名。那些人吃了药病真的好了。还在念小学的郑树森觉得非常神奇，对医生非常崇拜。他对自己说“有一天我能不能成为像他这样的医生？”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 从小就立下志愿的郑树森，通过自己的努力考上了浙江医科大学，最终走上了自己梦寐以求的从医之路。如今的郑树森凭借精湛的技术，冷静的头脑，已成为中国乃至世界的肝移植领域的塔尖行者。1993年，他成功完成的那例意义重大的肝移植手术，直接推动了我国第二次肝移植浪潮；他刷新了数个该领域在亚洲的纪录，并在2006年提出了具有重大国际影响的“杭州标准”，将肝移植病人的范围扩大了50%，更多肝癌病人因此获得了生存机会。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 外国同行也对郑树森团队的技术赞叹不已。2010年郑树森带领的团队首次走出国门，受印度尼西亚大学医学院的邀请去印尼做活体肝脏移植手术。他们一个礼拜做了四例活体肝脏移植，每一例都非常成功，而且现场直播。印尼大学医学院曾邀请过新加坡、日本的医生过去，但都不是很成功。因此该院院长感到很好奇，问是这个技术是从哪来的。郑树森说，“这个技术就是中国人自己的技术。”原本认为该技术是中国从别国学来的院长感到十分惊讶，说，想不到中国的医学技术也这么高超。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “画家的作品是画，作为医生来说，救活一个病人，做好一个复杂的手术，这就是我的作品了。”郑树森说，“经过我做的手术，我要求并发症少，基本上做到不输血，少输血，这就是一种底气，一种技术。”</span><br />
