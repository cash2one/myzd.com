			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>结直肠癌治疗 告别“单兵作战”时代</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003137338134" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（Gettyimages供图）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我国启动结直肠癌多学科诊疗（MDT）推广试点工程，打破学科“门派之见”可令患者生存率提高</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 医学指导/中国抗癌协会大肠癌专业委员会副主委、中山大学肿瘤防治中心结直肠科主任潘志忠教授</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 今年起，广州正式在常住人口中开展大肠癌（又称结直肠癌）筛查项目。作为一种治疗效果较为理想的肿瘤，结直肠癌有多种治疗手段，也因此常令患者陷入纠结：确诊后，应该先求助于外科一刀切除，还是先到内科做化疗？晚期结直肠癌常发生肝转移，此时是先到结直肠科求助，还是先去对付肝脏的转移病灶？不同的选择，不仅决定了治疗的经济成本和时间成本，更影响着治疗效果和术后生存机会。日前，由国家卫生计生委医药卫生科技发展研究中心发起的“全国大肠癌多学科综合治疗技术推广试点工程”(简称为“MDT工程”)在北京启动。该工程旨在推广结直肠癌MDT模式，通过打破学科“门派之见”，让病人以最小的就医成本，获得最优的个体化诊疗方案，从而延长生存期。这也意味着，结直肠癌的治疗将告别“单兵作战”时代。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 文/广州日报记者任珊珊 通讯员黄金娟、欧晓芳</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 初诊医生的“门派”决定治疗方向？不科学！</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 首批列入“MDT工程”推广基地的有中山大学肿瘤防治中心、北京大学肿瘤医院、复旦大学附属肿瘤医院、浙江大学医学院附属第二医院等。其中，中山大学肿瘤防治中心在全国率先开展结直肠癌多学科诊疗模式，每周二下午，各科专家组成的医疗团队会对结直肠癌病人的诊疗方案进行集中讨论。“在我们医院，接受MDT诊疗的结直肠癌患者超过了50%。”中山大学肿瘤防治中心结直肠科主任潘志忠教授指出，MDT模式具有不可比拟的优势。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 受制于国内医疗环境的特殊性，初诊病人一开始到哪个科就诊，通常决定了治疗的方向。如结直肠癌症的肝转移，可采取手术切除、射频消融、冷冻等局部毁损治疗、化疗及靶向治疗等多种手段，病人先找到哪个科的专家，一般就会跟着专家熟悉的节奏走。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 潘志忠指出，医学发展日新月异，分科也越来越细，加上医生日常工作的繁忙，每个学科的医生或许比较了解本“门派”的最新进展，却未必知道“隔壁邻居家里的新鲜事”。记者获悉，结直肠癌领域最经典的案例是，有些外科医生并不知道内科医生采用化疗和放疗，可以在某一阶段治愈某些类型的肠癌，而内科医生也未必清楚，晚期患者并非完全丧失手术切除机会，在特定阶段仍有机会实现治愈。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在这种传统诊疗模式下，初诊主治医生的个人经验和诊疗水平，往往左右着患者的命运。如果诊疗方案不适合病人，只能请其他科医生来会诊，一方面浪费了宝贵的时间，另一方面，“边治边调整”的形式导致医生对治疗缺乏全盘考虑，无法以最低的就医成本获得最佳疗效。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 多学科诊疗须舍“分段式”而用“一站式”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在多学科综合诊疗模式下，医生“单兵作战”和“门派之见”的局限性得到克服。中大肿瘤防治中心每次参与结直肠癌MDT讨论的各科医生有十几人之多。“病人可以接受外科、内科、放射科、影像科、内镜科、介入科、病理科、生物治疗、造口护士的联合诊疗。”潘志忠表示，由于结直肠癌患者容易发生肝转移以及肺转移，肝胆科和胸外科医生也要参与。医生们经过充分讨论，比较各科治疗思路的优缺点，从而得到一个最为妥当的治疗计划。如果患者需要转诊多个科室，参与讨论的医生们可依据治疗计划，追踪病人在院内的去向，做好治疗的衔接。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在MDT模式下，医生个人经验的不足，被集体智慧所弥补，但这些都要建立在对诊疗指南的合理使用和规范治疗的基础上。“诊疗指南本身是在不断修订的，医生在制定治疗方案时，要考虑不同病人的个体情况。”潘志忠表示，例如病人通过做放化疗，肿瘤明显缩小，出现了手术切除的机会，但究竟何时才是手术的最佳机会，指南上没有明确规定，需要多学科团队的医生们集思广益来评估判断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 潘志忠强调，结直肠癌的多学科诊疗不应该采取“分段式”，而应该采取“一站式”。有的医院采取的多学科诊疗模式是先让内科医生接诊，再让外科医生或放疗科医生接手。这种“分段式诊疗”，从表面上看，病人同样得到多科医生的诊断。但实际上，各科医生还是“各扫门前雪”，在制定诊疗“起步计划”时，缺乏全盘考虑。由于各科之间的衔接往往不够理想，最终也没有为患者节省诊疗时间。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 开刀前先放化疗 明显降低局部复发率</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> MDT模式适合结直肠癌初诊病人、复诊病人，尤其是复发或出现多个转移部位的病人。记者获悉，一项历时十年的对照研究结果显示，采用多学科诊疗模式治疗的结直肠癌肝转移病人，术后三年和五年存活率，明显高于直接转诊到专科治疗的病人，三年存活率分别是67.5%和54.1%，五年存活率是49.9%和43.3%。而老年结直肠癌病人采取多学科治疗模式，其术后5年生存率可提升20%左右，达到50%~70%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 以结直肠癌的肝转移为例，手术切除是唯一的根治机会。而多学科综合治疗模式为手术创造了更好的条件：放射诊断学尤其是术中超声的应用，可以发现更多肝转移灶，从而让手术切得更干净。术前化疗可以使原本不可切除的病人获得手术机会，术后辅助治疗降低了术后复发率和转移率。肿瘤射频消融与手术联合应用，则可以让原本无法手术切除的病人获得较好的治疗效果，从而长期生存。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “以直肠癌为例，很多外科医生倾向于马上进行手术切除，但这是不恰当的。”潘志忠说，尽管能做全直肠系膜切除，但是局部复发率是10%。而这类患者经过放化疗之后，特别是手术之前进行的放化疗是最敏感的，局部复发率可降到2%左右，效果显著。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 特别提醒</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 申请MDT 要带“大片”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> MDT模式下，影像学评估的作用非常重要。“病情的分期对治疗方案的制定至关重要。”潘志忠说，准确的分期有助于各科医生更好地评估病人的病情，预测肿瘤的生物学行为，从而对肿瘤的发展过程进行判断。结直肠癌的术前影像学评估，需要参考MR诊断甚至局部MR检查，内镜超声，再结合胸部的CT等等，或者是直肠腔内的B超等的检查结果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 他特别提醒，在该院门诊申请MDT的外院病人要特别注意影像资料的清晰度是否符合要求。“有些医院提供的CT片、MR片，都不是单张大图，而是一张片子里放几十张小图，不够清晰，不利于做出准确的分期，往往需要重新进行检查。”</span><br />
