		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腹股沟疝手术方式的选择</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">腹股沟疝是较常见的疾病，对于成人来说，药物治疗无效，唯一治愈的方法只有手术。与以前的破洞缝合方式相比，目前的主流是采用补的无张力修补术。因此种方式复发率低，在1%以下，患者术后无明显不适感，所以已取代了传统的修补术，目前广泛采用的方式是这种利用补片的无张力修补。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">常见的术式包括开放式和腹腔镜两种腹股沟疝无张力修补术，如何才能合理地选择呢？下面就此简单介绍。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一、开放式 无张力疝修补术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这种方式即所谓的“开刀”，在局部麻醉的条件下就可以完成。我们的切口长度在100px左右，与传统的7-200px的切口相比明显缩小。且出血量可以控制在1-2ml以内，基本无出血。我院可以实现术后24小时内出院，即今日手术，明天就可以回家，术后不需要特殊处理，只需注意休息。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这种方式优点是局麻即可，体内创伤面小，操作具微创性，适应症范围广，适用于绝大多数人，特别是老年人。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 二、 腹腔镜 疝修补术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这种方式即所谓的“微创”，需要全麻，但其实未必真微创。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">腹腔镜手术是通过在肚子上打三个0.5-25px的小孔，在机器放大直视下完成的，这一点使得操作更精确，对腹壁和精索的创伤更小，对神经损伤的可能性更小。因为补片是从内到外修补，相对开放性手术，人体腹壁受到的压强更小，从而复发率更低，而且患者术后恢复日常体育锻炼的时间通常较开刀快，一般在两周后即可运动。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">同时，腹腔镜非常适用于双侧疝和复发疝。腹腔镜打的三个孔是能够同时完成两侧手术的，而开放手术则不具备。对于复发疝患者，第一次手术部位的结构已经遭到破坏，若再采用开刀式手术，则可能会造成副损伤，而腹腔镜可以减少这种副损伤的发生率。另外，腹腔镜对那些要求不留疤的患者不失为一个可供选择的美容方法。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">尽管腹腔镜有那么多优势，但并不是所有病人都适合，也非严格意义上的微创手术。因为不是直接在患处落刀，手术路径较长，腹腔镜会在人体内造成较大的游离创伤面，手术潜在风险较开放性高，所以医生要根据病人实际情况进行个体化操作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于一些老年人尤其是那些合并有较严重心肺疾病的患者，全麻的腹腔镜就不适合。此外，腹腔镜的费用也相对较高。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">总体而言，开放术式和腔镜术式都是目前国际认可的治疗手段，也广泛被采用。前者简单快捷，创伤面小，经济实惠，局部麻醉扩大了手术适应症范围；后者适用于双侧或者复发疝，术后患者疼痛感更轻、恢复更快，但是手术潜在风险较高，有心肺疾病的人群慎用，费用也较高。</span><br />
<p>
