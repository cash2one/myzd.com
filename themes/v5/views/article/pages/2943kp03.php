<div style="text-align:center;">
	<span style="font-size:18px;line-height:2;font-family:'Microsoft YaHei';"><strong>耳垂周围肿块慎防腮腺肿瘤可能&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">耳垂周围，包括耳垂前、耳垂下、耳垂后肿块并不罕见，由于症状不明显，容易被忽略，也比较容易被非涎腺外科医师所误诊。笔者曾经收治过一些没有得到及时正确诊治的病例，有些是由于患者自己忽略，有些是接诊医师漏诊误诊所致。值得深思。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病例1，女性，17岁，澄海人，发现左耳垂下前方肿块3个月，缓慢增长，自诉如花生米大小，没有任何不适，无口面歪斜、流涎、眼睛干涩等不适，到当地医院就诊，一位该院最“优秀”的普外科主任医师告诉她：“简单，门诊手术切除就行”。手术结束后患者发现左侧无法闭眼、额纹消失、鼻唇沟变浅、口角偏斜。病理提示：腮腺粘液表皮样癌。送到上级医院进一步诊治，CT检查提示肿瘤可能残留。乃经过术前准备后送手术室在全麻下行左腮腺探查术，解剖面神经总干后发现，面神经总干进入腮腺后极处已被切断，肿瘤尚有残留，乃行全腮腺切除＋II区淋巴清扫，术后病理提示有肿瘤残留。这是1997年的事情了，虽则患者病治好了，但是，对于一个花季少女，遗留下一张歪把子脸的这个事实未免太残酷了！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病例2，女性，58岁，普宁人迁居广州，发现耳垂下肿块3个月，增大迅速，伴有局部疼痛，皮肤潮红，局部有轻压痛，到基层医院门诊部口腔科就诊，拟为“腮腺炎症”给予抗炎治疗，2周未愈，未作进一步治疗，2个月后疼痛加剧，伴有口面歪斜，到中山大学肿瘤医院就诊，MRI检查发现为腮腺来源实质性肿块，突入咽旁间隙，乳突骨质有破坏，考虑为腮腺恶性肿瘤，细针穿刺细胞学检查提示：见恶性肿瘤细胞。行手术治疗，术中发现面神经总干及颞面干、颈面干均被肿瘤浸润，而且肿瘤沿着面神经总干浸润至茎乳孔，磨开骨质后发现骨内段也受累，肿瘤突入咽旁间隙，上达颞下窝，作肿瘤广泛切除，术后行放射治疗。至今5年未见复发征象。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这仅仅是举几个例子而已，临床上遇到的象上述未能得到及时正确治疗造成不良后果的病例远远不止这些。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">实际上，耳垂周围肿块容易被忽略，主要是老百姓对它缺乏认识，非专科医师则比较容易把它当作淋巴结肿大看待，由于其发展往往较慢，且无任何症状，容易误认为是慢性淋巴结炎而仅仅给予消炎治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床上单纯耳前耳后淋巴结炎症性肿大并不多见，没有进一步检查的情况下不能轻易下结论。应该到口腔颌面外科或头颈外科就诊，绝不可在未进行仔细检查的情况下轻易在门诊手术切除。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般来讲，耳垂周围的肿块多数与腮腺有关，由于面神经穿行腮腺深浅叶之间，因此腮腺肿瘤切除时应解剖面神经，否则容易损伤该神经，出现面瘫症状，故凡是考虑为腮腺来源的肿瘤不管深浅大小均应住院手术，而不能简单草率地门诊切除，否则极易造成面神经损伤。当然，对于腮腺恶性肿瘤，当肿瘤累及面神经时，也只好连同神经一并切除，术后也会出现面神经瘫痪表现，但是这种面神经切除和面神经损伤是完全不同性质的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，确认耳垂周围肿块究竟是否来自腮腺除了临床检查之外，腮腺彩超检查往往可以准确的分辨，如果还不能确定，腮腺增强CT扫描或者MRI检查则可以进一步确定肿块的与腮腺的关系，诊断并没有太大难度。关键在于患者不要忽视，临床医生对耳垂周围肿块有充分的认识。</span>
</p>
