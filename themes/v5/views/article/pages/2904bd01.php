	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小叶增生未必是乳腺癌的“前奏”(转载)&nbsp;</strong></span>
</div>
<br />
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003098105171" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">优医汇</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">红房子丁昂乳腺外科工作室</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">例教职员工增加了一次妇科体检，徐老师和好几个同事都检出有小叶增生，因为最近两年一直看到都市女性要注意预防乳腺癌的宣传，她颇为担心，怕自己的小叶增生会发生癌变小叶增生不是一种疾病</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">严格地说，乳腺小叶增生不是一种疾病，而是一种乳腺生理性的变化。绝经期以前的乳腺受卵巢分泌的雌激素和孕激素的调控，所以会出现周期性的乳房胀痛，有些不舒服。雌激素会使乳腺的腺体、腺管增生，而孕激素会促使其复旧，所以乳腺每个月受雌激素、孕激素调控，会发生增生和复旧的过程。但内分泌还会受机体其他因素影响，如疲劳、心理状态、工作劳累等而发生改变，造成上一个周期复旧还没有完全过去，而下一个周期增生已经来了。同时可能伴有其他刺激因素使乳腺细胞不定期不规则的增生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">小叶增生在当代年轻女性中颇为常见，也很容易引起一些女性的恐慌。但事实上，如果确诊为小叶增生并不需要什么治疗，因为这只是一个生理性过程。小叶增生的癌变几率很低。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">女性在月经来潮前后乳房一直有胀痛或有肿块，应该及时去医院检查。35岁以上有小叶增生者应该每年做一次钼靶检查，能及时发现疾病，及早治疗，并提高治疗效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“丁克”女性小心乳腺癌恋上你</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">相比上述小叶增生的女性，事业型的“丁克”女性往往是乳腺癌的高危人群。女性独身，或年龄超过40岁未婚、未孕或第一胎生育年龄大于30岁者，其乳腺癌的发病率明显高于正常婚育的妇女。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">专家建议，40周岁以上的女性，应每一到两年进行一次乳腺X光检查。乳腺X光检查可以在乳腺癌患者还未有任何蛛丝马迹之时，就能准确地检测到病灶。许多患者就是凭借这一早期筛查利器，第一时间发现了很小、手还触摸不到的乳腺癌，为治疗赢得了宝贵的时间。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">40岁以下的女性，由于其乳腺组织致密，X光射线对其穿透力不够，年轻女性中大约有30%～40%的乳腺癌患者很难借助X光射线查出病灶。因此，邵志敏教授希望，这批高危人群能将乳房B超检查作为早期乳腺癌筛查的首选项目。</span><br />
