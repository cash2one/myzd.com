<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>三博脑科医院首次举办小儿神经外科先天性疾病学习班&nbsp;</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近日，由首都医科大学三博脑科医院林志雄教授主办的第一届小儿神经外科先天性疾病学习班成功举办。三博脑科医院作为全国神经外科医师培训基地，几年来已举办各种专业学习班百余期，每年培训各地神经外科医生百余名，此次小儿神经外科先天性疾病学习班为首次举办，共有来自全国11个省的34名医生报名参加。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 林志雄教授等就小儿神经外科先天性疾病本身特点，结合现代医学最新成就，从小儿神经外科先天性疾病的遗传学背景与分子生物学特征角度出发，分别就影像学特征、病理生理、自然病史、临床表现、病情评估、治疗原则，包括手术指征以及手术方式、手术要点及并发症等内容进行了精彩演讲。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 学员们不仅在短期内了解国内、外本专业领域内的最新动态，还掌握小儿神经外科先天性疾病相关问题的基本观点、治疗程序及手术技巧。本次学习，提升了参会医院的小儿神经外科诊治能力和医疗质量，建立了一支稳定有素的小儿神经外科专业队伍，有力促进了我国小儿神经外科诊疗水平的整体提高。</span>
