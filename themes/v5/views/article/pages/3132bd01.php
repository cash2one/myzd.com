			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>警惕儿童听力障碍 重医大儿童医院去年确诊1600多儿童耳聋&nbsp;</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">华龙网3月3日14时30分讯（记者 周晓雪） 儿童期是语言发育的黄金时期，今（3）日，记者从重庆医科大学附属儿童医院了解到，去年该院全年完成儿童听力检查3万例，确诊感音神经性耳聋1600多例。耳鼻喉科主任姚红兵提醒，听力功能在胎儿期已发育完全，儿童听力障碍早发现、早诊断、早干预至关重要。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 事件：感觉总听不清说话 长期戴耳机是元凶</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近日，初一学生欢欢感觉听不清别人说话，别人要说好几遍自己才能听清楚。家人带她到儿童医院耳鼻喉科检查，经检查，欢欢的听力有损伤。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 医生询问得知，欢欢平时喜欢戴着耳机听音乐，不管坐车的还是走路的时候，都戴着耳机。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “长时间用耳塞式耳机听音乐，声音在封闭的耳道中传播，鼓膜与声源极近，受到的刺激相对较大。”姚红兵表示，耳朵连续几小时、甚至更长时间的受到刺激，非常容易出现鼓膜劳损，不仅耳朵会痛，还会造成听力下降，引起噪音性耳聋和耳鸣。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 数据：全球每年新生聋儿3万多 胎儿期听力功能已发育完全</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 数据显示，目前，全球共有3.6亿人听力残疾，我国有听力语言障碍的残疾人达2057万。其中，7岁以下聋儿达80万。由于药物、遗传、感染、疾病、环境噪声污染、意外事故等原因，我国每年新生聋儿3万余名。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 儿童期是语言发育的黄金时期，如果这一时期听力障碍未被及时发现并有效干预，则可导致儿童语言发育障碍，甚至影响儿童的心理和社交能力，给家庭及社会照成沉重负担。因此，儿童听力障碍早发现、早诊断、早干预至关重要。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 很多人不知道，听觉功能在胎儿期就已发育完全。据了解，重医大儿童医院耳鼻喉科每年新生儿听力筛查超过5000例，正常新生儿发生严重听力障碍的几率为1‰—2‰，高危新生儿可达2%—4%。2015年，该院全年儿童听力检查3万例，确诊感音神经性耳聋1600多例。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 建议：孕期禁用耳毒性药物 新生儿必须做听力筛查&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “1岁以内儿童的听力障碍，家长往往很难发现。”姚红兵提醒家长，每个新生儿都必须做新生儿听力筛查，有听力损失高危因素的新生儿更应该长期随访听力。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 除了尽早发现新生儿的听力障碍，预防也同样重要。姚红兵建议，首先要加强孕前检查，减少遗传性耳聋患儿的出生率。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 同时，孕期妈妈需要注意避免孕早期风疹、巨细胞等病毒的感染，禁用氨基糖甙类抗生素、利尿剂等耳毒性药物。一旦发现糖尿病、甲低等孕高症，要及时治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “感染、核黄疸、早产儿等都是新生儿听力损失的高危因素。”姚红兵指出，积极治疗原发病、合理用药、减少中耳炎的发生率都很重要。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">姚红兵还提醒家长，一旦孩子被确诊为听力障碍，应根据孩子的听力损失性质及程度早期干预，这是孩子听力康复的关键。</span>
</p>
