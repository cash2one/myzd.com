			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小儿遗尿</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊经常遇到健康、活泼，但略显紧张的大孩子来就诊，一询问往往是遗尿问题。这些孩子多数7-9岁，一般从小开始尿床，白天小便控制良好，没有便秘及污粪。详细检查可以发现绝大部分孩子存在隐性脊柱裂，但不存在脊髓栓系，泌尿系统和直肠肛门检查也均正常。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽说遗尿不是什么严重情况，但也颇令家长和孩子本人烦恼。尤其是孩子上学后，由于怕被其他孩子耻笑而难以参加学校组织的一些集体活动，而导致孤独、怕羞、不自信等不良心理反应。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正常情况下，人有2个排尿中枢，脊髓的低级中枢和大脑的高级中枢。膀胱中贮存一定数量的尿液后，使膀胱内压力开始上升，产生尿意。脊髓低级中枢感知到尿意后，将尿意信号上传至大脑高级中枢。大脑高级中枢在确定时间、地点等合适的情况下，就反馈脊髓低级中枢，然后脊髓低级中枢就指挥膀胱开始排尿。正常人睡眠时，如果出现尿意，由脊髓低级中枢上传的尿意信号完全可以唤醒大脑，人觉醒后，完成小便动作，再入眠。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但对于经常性遗尿孩子来说，其脊髓低级中枢可能存在小小的不足，其上传的尿意信号略弱于正常。白天，人处于觉醒状态，大脑完全可以感知脊髓低级中枢上传的、相对微弱的尿意信号，良好地控制小便。但入眠后，由于大脑转入抑制状态，就难以感知脊髓低级中枢上传的、相对微弱的尿意信号。脊髓低级中枢在迟迟无法得到大脑高级中枢反馈指令的情况下，在膀胱内压力上升到一定程度时就自己行动，完成排尿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着孩子的成长，其睡眠深度将逐渐下降，大脑高级中枢也将逐步适应由脊髓低级中枢上传的、相对微弱的尿意信号，从而可以在夜间正常起床小便。这也是尿床的孩子，随着年龄的增大，症状可以逐步好转，甚至完全消失的原因。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以，对于这部分孩子，不需要过多的医疗干预。如果孩子存在紧张等不良心理状况，可以考虑试用一些可以提高中枢神经系统兴奋性的药物，如氯酯醒。这类药物可以提高大脑高级中枢的兴奋性，从而感知由脊髓低级中枢上传、相对微弱尿意信号。孩子就可以自己觉醒、或在家长的帮助下觉醒去小便。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这类药物的不良反应是孩子可能出现入眠困难，但只要减小药物用量，入眠困难情况即可小时。同时，人对这类药物的耐受性非常好，药物的作用会随着时间而逐渐减弱。不同的个体间的差别也相当大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，对于这类孩子，我的建议是：晚餐后控制液体的摄入；临睡前服用药物，如氯酯醒。剂量可以从半片开始，逐步增加。理想的效果为，孩子可以自己觉醒或比较容易被唤醒去小便，但又不至于出现入眠困难。如果孩子出现入眠困难说明药物剂量过大，如果仍非常困难被唤醒说明药物剂量过小。因此，药物的剂量可以自行调整。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，对于白天也存在小便控制不利、出现污粪、突然出现的遗尿、遗尿已经消失后再次出现者，或者存在下肢粗细不对称者，则需要非常详细检查，包括腰椎磁共振扫描、尿流动力学、肛门直肠测压、肌电图，以确定是否存在神经系统疾患，然后采取必要的治疗措施。</span><br />
<p>
