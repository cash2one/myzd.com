	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>都是垂体瘤惹的祸</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现代人生活节奏加快、社会工作压力大，很多白领出现易疲乏、眼睛看不清、上班没精神、下班就想睡等“亚健康”状态，有些女性会有月经失调、稀少甚至不孕现象，男性出现性欲减退、不长胡须等症状。被目前社会上充斥的“补肾壮阳”、“不孕不育专科”所蛊惑，很多人开始盲目服用补药或辗转于各式各样的“专科诊所”之间。孰不知，所有这些症状却可能源自同一个“罪魁祸首” ——都是垂体瘤惹的祸！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 垂体瘤是颅内常见良性肿瘤，占颅内肿瘤的10%，。但由于早期症状隐蔽，一般常易忽视。垂体瘤实际患病率近年来有上升趋势，国外报道亚临床垂体微腺瘤尸检率更可高达5～20%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">垂体腺位于大脑底部正中，虽然只有小粒花生米大小，但它却是调节人体正常生理功能最重要的腺体，可谓人体内分泌功能的“司令部”。一旦患了垂体瘤，就好比在“司令部”里安放了一枚“定时炸弹”，干扰了人体内分泌激素的正常调节，从而引起一系列内分泌紊乱症状，主要表现为以下几种：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1．闭经-泌乳-不孕症：由垂体泌乳素细胞腺瘤引起，为最常见一种，约占垂体瘤总数的40%-60%。育龄期女性好发。由于一些女性对自己的生理情况“羞于启齿”，直到婚后多年不孕才感问题严重，造成夫妻关系紧张。虽然调经治疗能做成人工周期造成治疗有效的假像，但不能根本解决问题，且耽误了真正病因-垂体瘤的治疗。男性也可能罹患泌乳素细胞腺瘤，临床上以阳痿、性功能减退为主要表现。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．巨人症-肢端肥大症：为垂体生长激素细胞腺瘤引起，约占垂体瘤的20%-30%。前段时间就诊于我院的全球第一女巨人就是典型的这种病症，这种类型的垂体瘤如发生在青春期以前即表现为巨人症，成年后患病则表现为肢端肥大症，即颜面变形增大，鼻唇舌肥大，手足肥厚宽大，常需不断加大鞋码。由于升糖激素升高，患者易患糖尿病。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3．肥胖症：为促肾上腺皮质激素细胞腺瘤引起，约占垂体瘤的5%-15%。这种病症表现为特征性的肥胖，即向心性肥胖，医学上称为 “库欣氏综合症”，患者面部和躯干脂肪堆积，而四肢相对瘦小，呈“满月脸”、“水牛背”体貌。面部痤苍以及顽固性高血压，低血钾等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 另一些没有内分泌功能的垂体瘤早期缺乏特征性症状，压迫正常垂体组织而引起乏力、嗜睡、毛发脱落等垂体功能低下表现。后期压迫视神经，阻塞脑脊液循环而导致视力进行性下降，颅高压症状直至危及生命。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;一旦得了垂体瘤这种病，也不必惊慌，现代医学的发展已可做到安全地摘除这枚“定时炸弹”，使患者恢复正常生活。早期治疗多能根治。目前治疗垂体瘤的方法很多，首选还是手术治疗，大多采用单鼻孔经蝶窦入路，创伤小、手术时间短、术后恢复快，体表不会残留任何切口瘢痕，疗效满意。瑞金医院神经外科开展经蝶垂体瘤手术已有20余年，积累1000余例成功经验。对于不适于或不愿手术者也可以采用针对性药物治疗或伽马刀治疗等方法。治疗效果好坏的关键是早期发现、早期诊断、早期治疗。所以建议广大患者朋友如出现上述症状，应及时到正规医院进行科学的检查，获得准确的诊断，进行有针对性的治疗。不要轻信街头大小所谓的“专科诊所”的虚假广告，也不要随意吃什么补药，否则，花了冤枉钱治不好病不说，还可能贻误病情，影响健康。</span>
</p>
