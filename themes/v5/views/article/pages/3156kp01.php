		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>细菌生物膜与假体周围感染</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">假体周围感染是人工关节置换术后的一种严重并发症，即使最终得到有效处理，其治疗期限也通常较长，同时患者也会经历长时期的关节功能丧失并且难以继续参与日常工作，使得总体治疗结果较差。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">造成疗效较差的主要原因在于往往难以对假体周围感染早期作出准确的诊断，并由此给予正确的处理。对假体周围感染基本病理变化的理解有助于指导对其作出正确的诊断和处理。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前针对包括假体周围感染在内各种感染的诊断标准主要还是基于1884年由Koch提出的推断及其各种改良。这些标准均包括首先从组织和体液中分离出病原菌，然后鉴定其种类并进行药敏试验以选择恰当的抗生素进行治疗。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这样一种简单的措施已被证明对大多数感染病例有效。然而，由于多数细菌在包括琼脂在内的实验室常用细菌培养基上并不会自然生长为简单的菌落，而是形成为细菌膜。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">研究已提出并深入研究了微生物生长的生物膜理论，并已经形成了坚实的科学基础。目前在海洋污染、水处理、以及食品工业等领域已经广泛接受了生物膜理论。美国学者Arnold WV等在一份AAOS教程中复习了假体周围感染中的生物膜相关知识。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">生物膜理论认为，细菌按两种不同的方式存活及生长（图1）。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">其一，这些单细胞细菌能够在一种与细胞外基质结构和功能类似的复杂生物膜基质上存活并生长，细胞外基质是高等级多细胞生物的重要标志。细菌生物膜由细菌本身产生，它既为细菌提供保护，也为其提供生存的组织构架，该组织构架有利于细菌的代谢活动以及不同细菌之间的信号传递。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">其二，细菌也可以以浮游形式存在，这是一种常见的传统单细胞生物生存形式。浮游状态生存的细菌单体之间并无组织结构存在，也不会产生化学介质梯度和相应的微生态环境。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">细菌的存在形式对于相应细菌所导致感染的治疗极为重要。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">浮游状态有利于感染的扩散，但同时容易被机体免疫系统和抗菌素所攻击。而以生物膜形式存在的细菌不容易形成感染扩散，但同时也受到保护而难以被免疫系统攻击，对抗菌素的治疗也不敏感。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3156/3.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">图1 &nbsp;根据Boles和Horswill、Otto、Resch等的研究所作的示意图以葡萄球菌为例表明生物膜的典型特点。其中蓝框表示生物膜形成的主要步骤，黄框表示细菌所处的化学环境，红框表示细菌的不同表型。浮游细胞通过附属基因调节因子（agr）系统产生致病因子、降低其粘附性、并提高细菌分散度抑制生物膜聚集。EPS：细胞外聚合物。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">值得注意的是，真菌，如念珠菌感染后，也可能以生物膜形式存在。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">感染的生物学特点</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">要发生感染，细菌首先应接种于条件适宜的部位。正常情况下，葡萄球菌是人体表面的一种共生菌，在接受手术的时候可能通过手术切口进入体内成为病原菌。此时的细菌通常被认为是以浮游形式存在的。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">进入体内后，这些细菌必需粘附于假体周围组织或假体表面。葡萄球菌粘连的分子生物学机理如下：该类细菌分泌出归属于MSCRAMM（microbial surface components recognizing adhesive matrix molecules，微生物表面成分识别胶粘剂基质分子）家族的粘附素，从而有利于这类细菌附着于各种细胞包基质蛋白。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">金黄色葡萄球菌（S. aureus）具有超过20种以上粘附素的基因编码。此外，与细胞外基质中的纤维连接蛋白相接合的粘附素可能有助于介导这些S. aureus进入人体细胞内部，在宿主细胞内这些细菌也可能进行复制。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">当细菌粘附成功后即进入复制阶段，而在此阶段内细菌可能是最脆弱的。但对于免疫机能正常的宿主来说，是否发生感染依赖于宿主免疫系统对侵入体内细菌的清除能力。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">单纯依靠抗菌素本身是不能清除感染的，但它显然能协助机体对抗定植的细菌。同样，任何有助于细菌逃脱免疫系统攻击，或对抗抗菌药物或机体免疫系统攻击的机制均有利于病原菌引发感染。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">生物膜的形成正是病原菌抵抗机体清除能力的一种方式。S. aureus一旦粘附于假体表面既开始进入生长和定植阶段，并释放出对宿主有毒性作用的化脓性因子。此阶段中细菌在环境因素的刺激下，以极为精准协调的步骤启动或关闭特定的基因表达。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">此外，研究者还发现在不同的细菌之间还存在一种被称为群体感应（quorum sensing）的联络系统紧密联系进行信息通讯，从而可能有助于菌落的总体生长并协调生物膜的形成。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">细菌最终被由多糖、糖蛋白和细胞外DNA（eDNA）组成的细菌生物膜所包裹。生物膜内存在的细菌与浮游状态细菌的生长方式从本质上是不相同的，可以认为两者是同一细菌的不同表型。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在生物膜内的细菌可以耐受百倍于杀灭浮游细菌所需浓度的抗生素，而且也更能抵抗机体免疫系统的攻击。但白细胞仍然具有侵入生物膜的能力。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">生物膜同样有利于营养物质的交换，而且细菌能从生物膜上脱落重新进入浮游状态，或者以生物膜片段的形式源源不断地到达身体其它部位，甚至引起急性系统性感染。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">细菌在生物膜内可能以毒力较弱的相对静止状态存在，但生物膜仍然会激发一系列的炎性反应，持续地破坏周围组织并最终导致包括疼痛在内的临床症状，而且在病程较长的慢性感染病例形成假体松动。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">腹腔导管、血管导管、隐形眼镜、骨科器械、人工关节假体等医疗性植入器材表面都已经检测到生物膜的形成。此外，在许多非内植物相关的慢性感染性病变，如：前列腺炎、囊性纤维化、心内膜炎、中耳炎、以及骨髓炎等均发现生物膜的形成。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">生物膜理论下假体周围感染的诊断</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">慢性假体周围感染的诊断通常相当困难，一般是通过包括红细胞沉降率（ESR）、C反应蛋白（CRP）、关节液细胞计数、以及关节液白细胞比例等间接指标来进行判断的。最新的一些研究则进一步检测关节液中白细胞酯酶水平以协助诊断。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">所有上述诊断手段从本质上讲都是针对机体免疫系统对假体周围感染的清除反应的检测，而非鉴定致病菌的直接手段。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨骼肌肉感染学会（Musculoskeletal Infection Society）和美国骨科医师学会（American Academy of Orthopaedic Surgeons，AAOS）均发布了假体周围感染的诊断方案和路径。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">无法从疑似慢性假体周围感染病例的关节穿刺液中分离出致病菌的机会十分多见。如果将生物膜因素考虑进来，则诊断中的困难就不难理解了。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">浮游状态的细菌可以很容易地通过传统的实验室检测技术成功分离和培养，但生物膜中存在的细菌则难以用同样的办法成功检测出来。而慢性感染病例的细菌绝大多数是存在于生物膜中的。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一些诊断假体周围感染的新方法采用了包括聚合酶链反应（PCR）在内的分子生物学技术。尽管PCR可以通过检测到细菌特异性核糖体RNA证明细菌的存在，但却需要以细菌特异性引物进行扩增以确定致病菌种类。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一种综合了PCR和质谱的被称为Ibis技术的检测手段已经被用于鉴定致病菌，并显示出了良好的应用前景。最近一项研究采用Ibis技术在培养阴性的假体周围感染病例以及部分原本认为是假体无菌性松动而翻修的病例中成功地检测到致病菌的存在。其中，在57例原本认为是假体无菌性松动的翻修病例中，有15例被Ibis技术检测出了致病菌。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这样的结果在一定程度上支持此前有研究者提出的猜测，即许多无菌性假体松动实际上存在低度慢性感染。这种新型分子生物学技术可能有助于假体周围感染的诊断，以及在准备再次置入假体之前确认感染已被清除。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">生物膜理论下假体周围感染的处理</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">通常在确定处理方案之前，首先将假体周围感染划分为急性和慢性：初次手术后4周内发生的感染为急性感染，而术后4周以后发生的感染则为慢性感染。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">术后早期感染通常伴有疼痛、伤口愈合不良、局部红肿、以及伤口长时间渗液等表现。这还包括原本功能良好的人工关节突然出现的急性感染，通常是在术后1年以后出现。一般认为这种急性感染是来自于身体其它部位感染病灶的血源性继发感染，通常表现为关节肿胀疼痛。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">慢性假体周围感染通常表现不明显，可能仅仅表现为慢性疼痛。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">急性感染通常采用手术治疗，包括清创冲洗保留假体、清创冲洗更换假体（一期翻修）、清创冲洗取除原假体后置入抗生素骨水泥spacer，并在感染控制后重新置入假体（二期翻修）。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">慢性感染则既可以一期翻修，也可以二期翻修。简单地进行清创冲洗并保留假体的方法处理慢性假体周围感染的失败率极高。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前文献中有关急性假体周围感染病例经清创冲洗并保留假体处理的疗效差异极大，这在一定程度上可以由生物膜的存在得到解释。如果不能彻底清除感染部位的生物膜，那么任何手术方式均会最终失败。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在假体表面的生物膜形成之前及时手术，或者手术中充分清除生物膜，则简单的清创冲洗并保留假体的治疗方式就能获得成功。对于慢性感染病例而言，可能需要术中更为彻底地清创以清除干净可能在假体周围骨组织中形成的生物膜。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">尽管二期翻修被认为效果更为可靠，但如果病灶中的生物膜没有被彻底清除，则最终失败的命运仍然不可避免。二期翻修和一期翻修的本质都是去除假体组件包括其表面所附着的所有生物膜，从而可以清除病灶周围组织中的生物膜。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">未来的研究方向</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">未来的研究目标包括阐明细菌感染和生物膜形成的基本过程。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">显然，预防感染形成的首要步骤在于防止细菌粘附。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">从假体的角度来看，研发不会吸引细菌驻留的假体表面结构，以及对假体表面进行抗菌涂层处理等措施可以降低细菌生物膜形成。尽管有研究发现表面共价结合有万古霉素的假体在有效抑制金黄色葡萄球菌生长的同时仍能促进骨愈合，但这样做可能也容易导致细菌对相应抗生素产生耐药性。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">另一个方法是用能够粘附于假体的生物表面活性剂处理假体表面。一些细菌，如乳酸杆菌即能合成具有双重亲和力的复合物，并可能用于处理硅基表面涂层。最近研究发现，归属于柑橘类衍生醇的金合欢醇能够抑制钛合金表面金黄色葡萄球菌生物膜的形成。其它一些材料可能并不适于进行假体表面涂层处理，但它们可以用于清除或破坏生物膜。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">阻断细菌之间的群体感应是另一种可能的治疗手段。对群体感应现象的分子机理有了更深入的研究，使得破坏细菌之间的信息交通更加切实可行。有研究者发现使用核糖核酸III抑制肽能有效防止由多种葡萄球菌属细菌，包括耐甲氧西林菌株引起的移植物相关感染。相关研究的最终目标是找到能够主动穿过多种而非特定细菌壁的类似抑制剂。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">还有研究专注于研发针对诸如金黄色葡萄球菌在内的常见致病菌的疫苗。动物实验已初步证实针对特定生物膜抗原的疫苗来治疗金黄色葡萄球菌性慢性骨髓炎。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">联合应用疫苗和万古霉素显著降低了实验动物的感染率，甚至能有效对抗耐甲氧西林金葡菌引起的感染。该模型同时也强调了区分细菌不同表型的重要性：万古霉素用于清除浮游形式的细菌，而疫苗则用于清除生物膜形式的细菌。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">如果能分离出特定细菌的抗原并进一步生产出相应的抗体，则可能找到一种诊断和治疗生物膜感染的有效方式。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">结论</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前在慢性假体周围感染的诊断和治疗中所面临的很多难题都能以生物膜理论得到解释。进一步的基础研究有助于深入了解生物膜的生物学特点，从而有助于提高这些慢性感染的诊断和治疗成功率。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">找到一种不仅仅针对某一特定细菌或真菌菌株，而是多种病原菌的治疗方案仍然是一个巨大的挑战。</span><br />
