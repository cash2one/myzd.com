			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>神经外科术后的家庭护理</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">心理安慰，树立信心</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 帮助恢复病人的正常情绪 时常注意观察病人的思想情绪，多作解释和安慰工作，使其增强战胜疾病的信心。可根据病情适当安排一些力所能及的劳动，使病人觉得自己是一个有用的人，也可适当参加一些如乐活动。对于有猜疑、强迫观念或迫害妄想等表现的病人，应加强照顾，需有人陪伴，严防自伤或伤人等意外情况发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;观察药物的疗效及不良反应</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 颅脑损伤病人一般需常服一些神经功能恢复的药，但常服用可能出现锥体外系症状，如吃饭时手振颤，走路时迈不开步，如出现此种情况要及时到医院就诊。如术后在医院内就发生癫痫的病人，要在医生指导下长期服用抗癫痫药物，并需注意按时服药不能遗漏，否则可能使癫痫复发。并要按时随访，调整药量。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 运动及理疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术后康复期，可采用运动或理疗的措施促进病人早日生活自理。按摩和热敷 指导病人用健康的肢体给瘫肢按摩，或帮助病人按摩热敷，以促进肢体的血液循环，防止肌肉萎缩。热敷时注意水温不能过高，以免烫伤，一般在６０－６５℃。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;物理治疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 根据病情可到附近医院理疗科就诊。按照医生的治疗方案，可用超声波按摩仪等，也可到健身房做肢体功能锻炼。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;语言训练</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 颅脑疾病、损伤可引起失语或发音障碍。当病人病情好转，神智清醒后，应抓紧训练病人，恢复功能。一般来说语言训练越早越好。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;饮食的调理</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 病人若无吞咽困难和其他伴随症状，可给以正常的饮食。如有吞咽困难者，可进半流质或软食，如面条、粥、蛋糕等。菜做得要细，如肉要吃肉糜，鱼要去骨，菜要切得细小。颅脑损伤的病人一般不需要忌口。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;生活护理</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 要保持会阴部清洁，大小便后要擦洗干净，防止尿路感染。有便秘和解便困难者，要训练定时排便，并给以含纤维素的饮食。必要时可服用润肠剂如麻油等。对于行走困难者一方面给予搀扶，另一方面要鼓励自我锻炼，防止病人有依赖感。对长期卧床的病人尤其要注意皮肤及预防并发症的护理。要保持被褥的整洁、干燥。定期翻身，拍背，并在骨隆突处予以按摩、擦洗，促使血液循环，防止褥疮发生。夏天室内要通风，冬天要注意保暖。</span>
</p>
