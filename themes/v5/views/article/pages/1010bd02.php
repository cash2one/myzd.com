			<div style="text-align:center;">
	<strong>肖水芳：“空鼻症”是伪命题 部分患者有心理障碍</strong>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近期，北京大学第一医院耳鼻咽喉头颈外科主任肖水芳教授在对话中新网健康频道时表示，医学界主流观点认为“空鼻症”是一个伪命题，所谓“空鼻症”患者，多由于慢性鼻部疾病反复发作，多次就诊均无立竿见影效果，加之对治疗效果有较高的期望，没有达到预期要求时出现严重的失落感，甚至导致心理、精神方面的障碍；少数患者则将这种异常的情绪转嫁于医生，进而出现纠纷和伤医事件。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “空鼻症”是伪命题 部分患者有心理障碍</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼻塞、鼻干、头晕、咽喉干燥或有异物感、睡眠质量差、胸闷、沮丧等，是网络上盛传的“空鼻症”症状。对此，肖水芳主任对中新网健康频道表示：“实际上‘空鼻症’只在网上流行而已，从专业角度讲，根本不存在这种疾病。很多疾病都可引起鼻干、不通气等不适症状，这些非特异症状不能成为‘空鼻症’的诊断依据，国内外的学术刊物，没有任何关于‘空鼻症’循证医学证据的研究和说明。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据了解，目前临床中导致所谓“空鼻症”的原因有二：一是精神心理因素，二是医源性因素。医源性因素里又包含两方面，疾病本身的影响和医疗技术水平的影响。不可否认的是，医生们的水平确有高低之分，鼻甲切除或消融不够，改善症状效果不明显；鼻甲切除或消融过多，则会导致过度通气等一系列副作用，“这是一个度的问题，考验医生的功力。”肖水芳主任表示。事实上，一些患有鼻腔鼻窦肿瘤的患者，即使鼻腔内的组织被大部分切除，也不会出现所谓“空鼻”症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床中，“空鼻症”患者反复向医生描述自己的痛苦，可是所有的检查结果都表明，患者的鼻腔功能基本正常，即使存在一些小问题，但是也绝达不到患者所说的严重程度。为什么主观描述和客观检查不一致？肖水芳主任解释：“病人的不适感肯定是有的，而且不能忽视。现在的经验是，一方面关注疾病的治疗，一方面多跟患者沟通，了解他们的心理状态和精神状态，因为尚有部分患者实际上存在心理、精神方面的障碍。如果发现异常，就会推荐到精神科或者心理科，有一些患者接受了建议，通过心理咨询或治疗，鼻子和心理的问题都解决了。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肖水芳主任介绍，有一位自认为得了“空鼻症”的患者，在北大医院耳鼻咽喉头颈外科就诊时，被怀疑有抑郁症。肖水芳和患者及家属沟通后，把患者推荐至一家专科精神类医院，后果然被确诊抑郁症，几个疗程治疗之后，患者抑郁症状消失，鼻子不舒服的情况也没有了。据肖水芳主任介绍，这样的患者，并不是个例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不过，是否接受看心理医生的建议，最终还是取决于患者。“所谓‘空鼻症’群体最突出的一个特征，就是十分固执，如果我们只是客观陈述没有病的事实，他就会抱怨我都这样难受了，你还说我没病，就会跟我们急、不听从建议。如果建议看精神科或者心理科，更可能出现逆反，亦很有可能发生纠纷。”肖水芳主任表示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所谓“空鼻症”群体的鼻部手术，大多涉及鼻腔粘膜、鼻甲或鼻中隔等手术方式，这正是众多媒体宣传的焦点，由此也导致很多需要做此类手术的患者，在“空鼻症”阴影的笼罩下犹豫不决。有的人甚至直接告诉医生：宁愿鼻子难受也不做手术，怕得更严重的“空鼻症”。大多数人经过医生的解释和宣教，最终接受手术，仍有一少数部分患者，因为“怕得空鼻症”放弃手术治疗，自行忍受不必要的痛苦。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为什么鼻子问题恰好和心理问题重叠？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为什么鼻子有问题的人，和存在心理障碍的人恰好有若干重叠？针对这个问题，肖水芳主任表示，可能和鼻子这一器官的特殊性有关。首先鼻子位于颜面部的正中间，最醒目，也最容易引起人的关注。鼻腔内含有丰富的血管、神经、腺体，其与眼、耳、口、咽喉等各部分相关联，鼻部不适易引起其他部位的不适感。鼻腔是呼吸道的门户，天气的反常变化、空气的污染等，都可引起鼻部的不适感，且稍微的不舒服，就有可能被无限扩大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，每时每刻的呼吸都与鼻腔有关。鼻甲粘膜下的海绵状血窦，可供调节鼻内气温所需热量，鼻腔粘膜腺体可分泌大量液体，用来提高吸入空气的湿度，防止呼吸道粘膜干燥。如果粘膜缺失过多，呼吸时会感觉到空气直接进入肺部，出现鼻干、咽干等不舒服的症状，长期受困扰的患者有可能产生焦虑、抑郁等心理问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“流鼻涕、不通气、鼻子干确实很不舒服，影响生活质量，但是症状本身通常不会导致严重的躯体疾病，有的患者因此自杀，或做出砍杀医生等伤医事件，其实还是因为存在心理、精神上的障碍。至于为什么恰好是鼻子的问题和心理问题纠缠在一起，耳鼻咽喉头颈外科学界也一直在探索，可能跟以上所讲的诸多原因有关。”肖水芳介绍。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “持证上岗”或可避免不规范手术</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">任何手术都有风险，鼻部相关手术也不例外。一般来说，涉及鼻甲切除或消融减容手术的疾病，有肥厚性鼻炎、过敏性鼻炎导致的鼻甲肿大、药物性鼻炎和鼻中隔偏曲等，这些患者需要部分切除、微创消融等方式，以达到鼻甲减容、改善通气的效果、。尽管这些手术有几十年的临床经验，开展得相当成熟，但是由于医生水平的参差不齐，患者依然可能面临着鼻甲过多切除的风险。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“年轻医生和地方医院的医生，由于经验不足或没有接受过规范的培训，很容易做的过度。”肖水芳主任介绍。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，对于地方医院的医生来说，他们大多数情况下更倾向于过度切除，因为患者花了钱，如果没有感受到预期的效果，很可能投诉医生、甚至与医生发生严重冲突。不过，医生选择有限的过度切除也并非毫无根据：鼻子发干、头疼、咽干等切除过度副作用，多数病人术后都可慢慢调整、恢复和代偿，如果没有精神上的障碍，绝不至于难受到自杀或者杀人的程度。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">面对“高危科室”的称谓，耳鼻咽喉头颈外科学界的专家也在思考，如何从自身努力，改善这种现状。据了解，国家卫计委相关部门在该领域开展了全国性的内镜培训工作，以提高内镜手术的准入门槛，提升医生水平，杜绝不规范的操作。不过，目前的“ 内镜上岗证”尚未普及。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肖水芳主任告诉中新网健康频道：“理想的状态是先培训后考试，通过考试才有资格做手术，但是达到这种水平还需要很长时间。”据中新网健康频道了解，耳鼻咽喉头颈外科内镜手术的培训标准、评审标准、准入标准等已被多次讨论、修改，预计最早今年能够出台，之后将陆续实施。届时，所有操作内镜手术的医生都要接受培训，已经有丰富经验的大夫可直接获得资格，无经验的大夫要通过考试获取资格。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“医生、 患者、媒体、政府和社会各界的共同努力，或许才是解决所谓‘空鼻症’顽疾的终极办法。”肖水芳主任如是说。</span><br />
