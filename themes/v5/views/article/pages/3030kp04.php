			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>预防癌症我们怎么吃？</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癌症病例在我们周围频频发生，积极治疗是最好的办法。但是在日常生活饮食中稍加注意，是否可以预防癌症的发生呢？回答是肯定的。在这里我给大家介绍一些可以预防癌症的蔬菜，根据自己的口味喜好经常选择性的吃一些，也可以达到预防癌症的作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3030/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刀豆 &nbsp; &nbsp;适宜食管癌、贲门癌、胃癌病人反胃呃逆时食用，有降气止呃的效果。民间常用老刀豆研粉，每次冲服30克，日服2~3次，每30天为一疗程。对肾癌腰酸痛者，民间有用刀豆子50克，同猪腰1个，切成腰花煮汤服用，于1日内分次服完。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">菜豆&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 别名四季豆。适宜白血病、鼻咽癌、甲状腺癌、骨肉瘤、恶性葡萄胎、绒毛膜上皮癌、乳房癌、子宫颈痛、卵巢痛、睾丸癌、肠癌等病人食用。这是因为菜豆的种子含糖蛋白、胰蛋白酶抑制物，并含有对人体血红细胞有很强活性的PHA，由于PHA有一定的抗癌作用，所以，上述癌症患者宜食之。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">黄豆芽 <img src="<?php echo $baseUrlImage; ?>article/doctor/3030/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 是一种很好的抗癌蔬菜，这不仅因为它的营养丰富，含有具有抗癌作用的多量维生素C和胡萝卜素。更重要的是，豆芽莱中含有一种酶，可阻碍致癌物亚硝胺在体内的合成。同时，豆芽中的叶绿素、纤维素也能防治大肠癌和其他一些癌变，癌症病人常吃豆芽，对增进营养，控制病情的发展有一定益处。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">海带&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 中国传统医学认为，海带有化痰、软坚、散结的作用，是中医常用的治疗肿瘤药物，多用之于瘿瘤、噎膈、瘰疬、痰核等，相当于现代医学所说的甲状腺、食管、胃、乳房、淋巴结系统的各种良性或恶性肿瘤，是一味理想的抗癌防癌食品，适宜甲状腺癌、肺癌、乳房癌、食管癌、贲门癌、子宫癌、恶性淋巴瘤等病人服食。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">紫菜&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 性寒，味甘咸，有清热、软坚、化痰的功用，是癌症患者的理想抗癌食品，适宜脑肿瘤、乳腺癌、甲状腺癌、恶性淋巴瘤，以及各种肿瘤伴有淋巴结转移者经常服食，这对抑制肿瘤生长、缩小肿块、缓解病情的发展有一定效果。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">海藻&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 现代药理研究证实，海藻有一定的抗癌功能，也是一种抗癌防癌食物，中医认为海藻性寒，能软坚、散结、消痰，适宜头颈部、甲状腺、消化道、肺部以及淋巴系统各种恶性肿瘤之人服用。通常与海带同食，更为适宜。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3030/3.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">黑木耳&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 性平，味甘，能益气、养阴。近代研究发现，黑木耳中含有一种多糖体，具有一定的抗癌活性。这种抗癌作用不是直接抑制或破坏癌细胞，而是通过提高人体的免疫力来抗癌的，这就是中医所说的扶正祛邪的效果。所以，黑木耳适宜各种癌症病人及放疗、化疗或术后体质衰弱者食用。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">白木耳&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 性平，味甘淡，有滋阴、润肺、养胃、生津的功用，是一种补虚扶正抗癌的营养滋补品。现代药理研究表明，银耳能促进机体淋巴细胞的转化，提高免疫功能。银耳的抗肿瘤多糖对癌细胞有一定的抑制作用，对各种癌症病人及化疗、放疗之后体虚者，尤为适宜。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">猴头菌&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;或称猴头菇，是一种营养丰富的食用真菌，适宜各种癌症病人，尤其是消化系统癌症患者食用。猴头菌不仅营养价值高，它所含的多糖体、多肽类对癌细胞有较强的抑制作用，同时能使机体内蛋白组成成分有明显增加，产生了干扰素，增强了抗癌效果。所以，癌症病人常食猴头菌，有助于升高人体的免疫球蛋白，提高淋巴细胞转化率，升提白细胞，增强人体免疫功能，起到防癌治癌的作用。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">金针菇&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;据日本学者研究，金针菇中含有一种朴菇素，这种朴菇素能有效地抑制肿瘤的生长，具有明显的抗癌作用。日本的长野县是生产金针菇最多的地方，该县居民正是由于经常服食这种食用菌而很少得癌症，该地区的肿瘤发病率也明显较其他地区为低。可见，金针菇也是各种癌症患者的理想食品。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">草菇&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;性寒，味甘，能清热生津养阴。它除了含有丰富的维生素C具有较好的防治癌症作用外，草菇的子实体中还含有一种异种蛋白，也具有一定的抗癌作用。适宜各种癌症病人服食，可以增进人体的防癌治癌能力。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">平菇&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;近年来，我国医学工作者研究发现，平菇也是一种抗癌食品，它有直接的抗癌防癌作用，适宜胃癌、鼻咽癌、宫颈癌等患者服食。在一定程度上，可以稳定病情，控制癌症的发展，并可减少术后的转移和复发。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">香蕈&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 俗称香菇。美国、日本科学工作人员，在香菇中发现一种1，3—β—葡萄糖苷酶，经抗癌试验表明，癌症病人用此物质治疗后，能提高机体抑制癌瘤的能力，加强抗癌作用。我国医药工作者也发现香菇中所含的香菇多糖能增强抗肿瘤药物的效果，虽不能直接杀死肿瘤细胞，但能刺激网状内皮系统。所以，各种癌症病人多食香菇，可提高自身的免疫功能及抗癌能力，增强放疗、化疗的疗效，防止化疗放疗后所致的白细胞下降。手术后坚持长期食用香菇，对预防癌症复发、转移也颇有裨益。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鱼鳔&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 又称鱼胶、白鳔。性平，味甘，能补肾、益精、消肿、散瘀。《本草纲目》曾说它“散瘀血，消肿毒”。《常见药用动物》中介绍：“试治恶性肿瘤：干鱼鳔40克，灶心土40克，共研细末，日服3次，每次10克。”内蒙古民间治食道癌胃癌，有用鱼鳔以香油炸酥，压碎，每服5克，日服3次的方法。癌症患者宜食之。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">黄芽菜&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;俗称大白菜，是冬季人们食用的主要蔬菜之一。具有解毒除热、通利肠胃的功效。现代认为大白菜有抗癌作用，这是因为白菜中含有微量元素铜，可以抑制人体内致癌物质亚硝胺的吸收与合成。所以说，癌症患者常食颇宜。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">花菜&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;近代科学家们发现，花菜等甘蓝类蔬菜中，含有多种吲哚类衍生物，这些物质能增加机体对致癌物质苯并芘和甲基苯蒽的抵抗能力，因而具有抗癌功效。所以，癌症病人宜食之。&nbsp;</span><br />
