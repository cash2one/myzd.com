<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;脑是人体的“司令部”，头部外伤常可以引起严重后果。对于头部外伤而言，预防是最重要的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前，造成外伤最常见的原因是交通事故，头部外伤也不例外。特别是骑摩托车或者电动车的人，一定要注意戴头盔。佩戴头盔还要规范，一定要在下巴处系紧，不然一旦发生碰撞，没有系紧的头盔可能飞掉，起不到对头部的保护作用。坐在摩托车或者电动车的后座的人，也必须按照规定佩戴头盔。摩托车和电动车一定要按照有关的交通规则，不要在高架桥上行驶。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 对于驾驶汽车等机动车的司机，要注意按交通规则佩戴安全带。正确佩戴安全带后，一旦发生交通事故时，安全带可以有效地将司机固定，减少身体的前后剧烈移位，包括减少头颈部的剧烈移位，减少头部碰撞，达到减少头部外伤的可能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 行人也必须遵守交通规则，要注意过马路走人行横道，注意避让车辆，减少意外伤害的发生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 头部外伤另一个常见原因是工地的事故，包括坠落伤、高空重物砸伤等。一定要加强工地的安全管理，配置安全网、安全带等设施。</span><br />
