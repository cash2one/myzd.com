			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胃肠胰神经内分泌肿瘤的内科治疗</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.内科治疗原则：内科治疗分为控制症状以及抗增殖两部分。控制症状主要是对症治疗，缓解由于肿瘤激素分泌导致的相关症状；而抗增殖主要包括生物治疗、靶向药物至治疗以及化疗。针对G1/G2的患者，应当根据其肿瘤负荷大小以及肿瘤侵袭性的强弱选择药物，对于生长缓慢且负荷较小的肿瘤甚至可以选择观察等待。针对G3的患者应当积极采取化疗为主的治疗。总之，应当综合原发灶，转移部位以及上述特点，制定个体化的治疗方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 控制症状：症状控制可首选生长抑素类似物（SSA）及干扰素的生物治疗。另外针对不同的激素可考虑相应的拮抗剂抑制其分泌，例如胃泌素瘤可使用PPI抑制胃酸分泌，ACTH瘤导致的库欣综合征可选择肾上腺阻断剂如酮康唑或美替拉酮治疗，胰岛素瘤可应用二氮嗪控制胰岛素分泌，还可选择维拉帕米、苯妥英及糖皮质激素等。除此外，局部治疗（如肝脏介入或射频治疗）也可达到较好的控制症状的效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.抗增殖：抗增殖的药物包括：生物治疗、靶向药物及化疗。首先，应当明确病理分级。对于G1/G2的患者，如果肿瘤负荷相对较小，生长较缓慢，可考虑生长抑素类似物的治疗。如果肿瘤负荷较大或者生长十分迅速，则首先考虑化疗，而介于二者之间的情况可考虑靶向药物（见图1）。而对于G3的患者，应以化疗为主，即依托泊苷、伊立替康、铂类、替莫唑胺、氟尿嘧啶类、贝伐珠单抗等均是可选择的药物。对于奥曲肽显像阳性的患者，也可考虑联合SSA治疗。下面我们就对这几大类药物一一进行介绍：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">生物治疗：如前所述，生物治疗包括SSA及干扰素α，生长抑素类似物主要包括醋酸奥曲肽微球以及兰瑞肽。SSA可应用于所有的GEP-NETs，其特点为副反应小，患者耐受好，对于一般状况较差，脏器功能欠佳或者高龄的患者均可使用，并且当肿瘤出现进展趋势时可考虑加量或者缩短用药时间，对于一部分患者仍可达到继续控制肿瘤生长的作用。干扰素目前国内应用不多，可单独使用或联合奥曲肽治疗，但通常被用于二线及以上治疗。生物治疗总体上有效率较低（&lt;10%），但患者耐受较好。&lt; span=""&gt;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">化疗：化疗主要应用于pNETs、转移性前肠NETs和NECs，而对于中肠的NETs化疗的疗效还有待进一步研究。针对pNETs国外首选脲链霉素±氟尿嘧啶的方案，但由于该药我国没有上市，针对G2的NETs国内常用的方案为替莫唑胺±卡陪他滨，该方案目前尚无大规模的临床研究数据支持，但多个小样本的研究证实尤其在pNETs中，可以获得较高的肿瘤缓解率（最高达70%），PFS可达到18个月。目前尚无有效的标志物筛选适合应用替莫唑胺±卡陪他滨的患者，对于标准治疗失败的GI-NETs也可尝试该方案。针对NECs，一线推荐铂类为基础的方案（EP或IP），二线则可考虑替莫唑胺＋卡陪他滨±贝伐珠单抗的方案，除此外也可尝试伊立体康及奥沙利铂为主的方案。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">靶向治疗：靶向药物包括舒尼替尼及依维莫司，目前均仅批准用于pNETs。这两个药物均有大型的III期临床研究证实其有效性，二药的PFS均近1年。靶向药物的有效率虽未超过10%，但治疗后肿瘤缩小的比例均明显高于安慰机组，目前将RECIST标准用于评效靶向药物的疗效尚存在争议，期待今后推出更加科学的评价体系来评估靶向药物的疗效。对于标准治疗失败的GI-NETs，可考虑依维莫司±SSA的方案。</span><br />
