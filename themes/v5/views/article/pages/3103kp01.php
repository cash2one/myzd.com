		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>帕金森病：每天活动比偶尔剧烈运动更有益（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">密歇根大学的最新研究发现，帕金森病患者可考虑尝试洗碗、叠衣服和在家附近散步以控制其症状。该研究的主要研究者，U-M功能影像学、认知和运动实验室主任Nicolaas Bohnen博士称：“由于运动症状如步态、平衡问题或摔倒，帕金森患者往往容易久坐。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一旦患者的脚感觉不稳定，他们可能会害怕摔倒，继而害怕做任何活动。Bohnen的团队观察到受试者参与运动，如游泳、健美操，可以帮助他们减轻那些使他们宁愿久坐的运动症状。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">Bohnen说道：“我们发现，并不是过多运动，而只是日常生活中的日常活动可保护患者的运动技能。久坐对任何人都不好，但对帕金森患者来说更糟。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">已在线发表的帕金森和相关疾病的影像学研究是由U-M团队进行的，他们同时在影像学和神经病学领域任职。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">研究人员观察48例帕金森病患者的非锻炼性和锻炼性躯体活动和运动症状严重程度，共持续4周。进行PET脑成像以测量多巴胺水平，并使用一份问卷以了解患者的身体活动，包括锻炼性和非锻炼性活动。他们发现非锻炼性体力活动与运动症状较轻相关。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">虽然多巴胺缺失是帕金森病患者的主要大脑改变，并认为是帕金森患者越来越久坐不动的主要原因，研究人员发现，非锻炼性体力活动可保护运动技能，包括不同水平多巴胺的患者。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“这可能对帕金森病患者产生重大影响，”共同作者、密歇根大学神经病学临床讲师Jonathan Snider博士说。“越来越久坐不动的行为不仅可解释帕金森病的恶化，也可解释晚期帕金森病患者的运动症状越来越重。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">密歇根大学放射学和神经病学教授、VA Ann Arbor医疗系统的医生、以卓越的帕金森病研究著称的U-M's Udall中心的研究员Bohnen指出：“我告诉我的患者，要站起来，少坐，多活动。”</span>
</p>
