			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>垂体瘤问答</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：我女儿前期月经不规则，经常延期，量少。最近甚至连续几个月没有来月经。去妇科检查没有查出问题，妇科医生怀疑她是垂体瘤。我们该如何就诊？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴劲松：“停经、溢乳和不孕”是比较典型的垂体泌乳素腺瘤的临床表现。如果你女儿同时有双侧乳房溢乳的话，的确有可能患上了垂体瘤。建议她去妇科或神经外科就诊，查血液泌乳素指标。如果泌乳素高于正常，再做头颅磁共振检查。这样就基本上可以确诊是否患有垂体瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：我患有垂体瘤。请问垂体瘤是良性还是恶性肿瘤？会转移吗？会遗传吗？手术以后会复发吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴劲松：垂体瘤是良性肿瘤，罕见有家族性遗传史。垂体瘤一般不会转移到全身其他部位。但有些垂体瘤也会在局部侵袭性生长，使得手术切除困难。垂体瘤如果能够彻底切除，复发率很低。如果切除不彻底，还是可能会复发。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：我女儿未婚，因月经紊乱，泌乳素很高，查出患有垂体瘤。目前正在服用溴隐亭药物，需要注意些什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴劲松：对于垂体泌乳素腺瘤，溴隐亭药物治疗是有效的。尤其是对于育龄期女性，溴隐亭药物治疗更是首选。溴隐亭的治疗需要长期坚持，方可获得稳定的临床益处。由于不同个体对溴隐亭的疗效和不良反应有相当大的差别，故在服药期间，患者应从小剂量开始，逐步加量。患者应定期随诊复查血泌乳素值，据此调整服药剂量。摸索一个适合自己的最低维持剂量。溴隐亭药物治疗需长期维持，一旦停药，泌乳素很快就会反跳性升高。用药初期，患者可能出现胃部不适，恶心，精神倦怠，便秘等症状。但随着用药时间的延长，多数症状可以慢慢缓解。建议患者饭后服药，非工作时间段服药。还应积极调整饮食习惯和生活方式，避免高危作业，以降低药物的副作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：听人说，伽马刀可以代替手术，治疗垂体瘤，是这样吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴劲松：对于部分类型的垂体瘤，可以采用伽马刀治疗。例如，垂体微腺瘤、垂体瘤术后残余病灶、身体情况不适合手术的垂体瘤等。但对于育龄期女性、垂体大腺瘤、垂体瘤术后残余病灶紧邻视神经等情况，伽马刀未必合适。因此，垂体瘤患者接受手术、伽马刀还是药物治疗？应当由专业的垂体瘤专病医生提供专业指导。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：服用溴隐亭期间怀孕了怎合办？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴劲松：对于育龄期女性垂体泌乳素腺瘤患者，服用溴隐亭后月经恢复正常，可以怀孕。虽然目前并无确切证据表明溴隐亭会提升服药人群的胎儿致畸率，但溴隐亭与其他药物一样，并非完全没有副作用。因此，处于胎儿安全考虑，可以在确认怀孕后，停止服用溴隐亭。但是，必须强调的是随着药物的撤离，患者的泌乳素会升高，可能导致胎儿流产。同时孕妇在妊媜期间，由于内分泌需求旺盛，肿瘤有可能会增大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：我多年未见的老同学都说我现在脸面变形了。我自己也觉得眉骨高耸，颧骨突起，下巴拉长，手脚粗大，鞋码增大了几号。不知身体出了什么问题？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">吴劲松：您很有可能是患了垂体生长激素腺瘤，导致了肢端肥大症。这种疾病事由于垂体瘤分泌过多的生长激素，导致骨骼末端、骨关节、软组织以及内脏器官异常增生。如果患者是未成年的儿童则可能导致巨人症，而对于骨骺已经闭合的成年人来说，主要表现为您所描述的症状。除了这些显性的症状，患者还会出现一些外表看不出的脏器与生理代谢病变。例如，糖尿病、肥厚型心肌病、结肠息肉、睡眠呼吸暂停综合症等。患者早期由于骨骼肌肉肥大，多精力旺盛，力气大。后期由于内脏器官多度肥大导致功能减退，出现全身乏力，骨关节酸痛，呼吸不畅等，严重者可以在熟睡时窒息。还有值得一提的是，肢端肥大症病人的癌症发病率也比一般人要高。</span>
</p>
