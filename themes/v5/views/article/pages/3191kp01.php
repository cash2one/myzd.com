<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>颅内未破裂动脉瘤的治疗原则</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;对于颅内未破裂动脉瘤，保守的内科治疗与积极的介入栓塞或外科治疗孰优孰劣，目前尚存在分歧。日前公布的国际上最大的多中心研究结果，也许能够给我们一些启发。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp;1 &nbsp;影响出血的危险因素</strong></span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 目前国际上最大的多中心研究——国际未破裂颅内动脉瘤研究报道了其研究结果。I期回顾性对于无蛛网膜下腔出血史的无症状性动脉瘤患者，直径小于10毫米的动脉瘤年破裂率为0.05％，而直径10～25毫米和大于25毫米者分别为1％和6％以上。II期前瞻性研究结果，以美国、加拿大、欧洲等超过60个治疗中心的4060人为研究对象，观察7年以上，分为未接受治疗组、接受显微外科手术组和接受介入治疗组。结果显示，总的未破裂颅内动脉瘤破裂出血年发生率约为1.9％。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 进一步分析显示，动脉瘤的体积、位置以及形状是否规则、有无家族史、是否为多发、近期生长速度等都是动脉瘤是否会出血的影响因素。此外，高龄、高血压、吸烟、女性患者都是动脉瘤易破裂人群。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2 &nbsp;开颅手术风险不应忽视</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 国际未破裂颅内动脉瘤研究小组公布了2621名未破裂颅内动脉瘤患者自然史资料和前瞻性手术相关性残疾率和病死率等资料，结果表明部分患者手术治疗风险不容忽视。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 比如，年龄是影响病人手术结果的重要因素，尽管＜45岁患者残死率为6.5％，但＞64岁患者的残死率则高达32％；≤5毫米的动脉瘤致残率＜3％，巨大动脉瘤死亡率和致残率则为20％左右；后循环的动脉瘤手术效果较前循环动脉瘤差，后循环巨大动脉瘤手术死亡率是9.6％，致残率是37.9％。另外，医生手术验也明显影响着治疗结果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3 &nbsp;栓塞治疗优势初步显现</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 手术与栓塞治疗的比较结果显示，未破裂出血组经栓塞治疗相对风险降低22.3％；有破裂出血组栓塞治疗相对风险降低29.7％。这初步显示了血管内栓塞治疗优于开颅手术，明显降低了患者术后近期的致残率与死亡率。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 可见，上述两个国际性多中心研究结果更支持栓塞治疗，而且大多数学者认为，如果患者年龄超过65岁，或者患者伴有其他疾病，则未破裂颅内动脉瘤血管内栓塞治疗的优势更加明显，相信随着栓塞新材料与技术的不断完善，会进一步改善未破裂颅内动脉瘤个体患者进行干预治疗的“危险－效益比”。但专家同时指出，相对于开颅手术，颅内动脉瘤的栓塞治疗可能存在完全栓塞率低，栓塞后再出血率、再通率较高的不足，其长期疗效也有待进一步的随访研究。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>4 &nbsp;该如何选择治疗策略</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 观察、内科辅助治疗、显微手术夹闭和血管内栓塞治疗是未破裂颅内动脉瘤的四种对策，目前未破裂颅内动脉瘤是否该保守治疗或者积极治疗尚存在争议，较一致看法是对大量影响因素进行个体化分析权衡“危险－效益比”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前对于未破裂动脉瘤的处理，较为公认的有关指导原则如下：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.1 &nbsp;小的偶然发现的海绵窦内动脉瘤无需处理；大的症状性海绵窦内动脉瘤，如果年龄允许，症状严重或发展的话，应该积极处理。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.2 &nbsp;所有颅内症状性动脉瘤均应考虑处理；如果属于急症，应紧急处理；对于大的和巨大的症状性动脉瘤，手术风险高，处理应该中心化和个体化。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.3 &nbsp;有蛛网膜下腔出血史的动脉瘤无论大小均应处理，尤其是位于基底动脉顶部者；病人的年龄、健康状况和治疗的风险可能会影响动脉瘤的处理，当保守治疗时应严密观察。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.4 &nbsp;无蛛网膜下腔出血史的无症状动脉瘤应该观察，除非患者年轻、有子瘤或有其他独特的血液动力学特点等需要考虑治疗；有蛛网膜下腔出血家族史的也应考虑积极处理。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.5 &nbsp;在考虑年龄、健康状况以及动脉瘤破裂风险的基础上，大于10毫米的动脉瘤应该积极处理。</span>
</p>
