<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原标题：鞭炮伤人更甚 塑料填充物是元凶</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“爆竹伤人11人眼球被摘除”后续</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">今年，四川大学华西医院收治烟花爆竹炸伤病例总量变化不大，部分医院收治烟花爆竹炸伤病例数有所下降，但伤情比往年严重。眼科医生分析，烟花爆竹的填充物太杂，是造成这种情况的原因之一，一些伤者的眼球中甚至取出了塑料硬片之类的异物。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">华西都市报记者从四川大学华西医院了解到，今年春节期间，该院收治烟花爆竹炸伤病例在百例左右。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">成都烟花少了伤者多来自郊县等</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在医院接受住院治疗和手术的伤员当中，几乎没有成都市区的市民，大多来自郊县和二级城市。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据华西医院急诊科统计，2012年春节期间，仅有3名烟花爆竹炸伤病人在该院实施眼球摘除手术。2013年春节期间，该院眼科接到被烟花爆竹炸伤的病人21名。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">成都市属医院收治的病例也在减少，据统计，2012年春节，全市各大医院收治的烟花爆竹炸伤病例都在10人以上。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">伤情为何更重？鞭炮填充物多又杂</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">四川大学华西医院眼科主任邓应平教授分析了从伤者眼球中取出的异物，“烟花爆竹炸伤者中伤势严重的比往年多。”他说，在这类伤者的治疗和手术中我们发现，烟花爆竹中的填充物比较杂，造成的伤势也相对严重。在进行的手术中，从伤者的眼球中取出的异物大多都是塑料块。“鞭炮里的填充物太多太杂，一旦炸开后就像散弹一样穿进眼睛，造成眼球破裂伤。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5成伤者系误伤多为老人和小孩</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">眼科专家还透露，伤者中，被火炮误伤的占50%以上。“从我们多年了解的情况来看，被烟花爆竹所伤的患者，不一定都是自己燃放时被炸伤的。特别是老人和小孩大多是观看他人燃放时被炮屑和烟火误伤。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">记者在采访中发现，燃放鞭炮时操作不当也是造成伤者受伤的重要原因。在11位摘除眼球的伤者中，有3人是因为点燃鞭炮后，鞭炮没有反应，凑上去一探究竟时，鞭炮却爆炸了。还有人直接用手捏着火炮燃放被炸伤或烧伤。建议被炸伤时勿水冲勿手揉</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">华西医院眼科住院部主管护士王荣提醒，鞭炮炸伤眼睛后，必须马上送医。“眼睛是很敏感的地方，必须小心对待。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">同时她提醒，受伤后，千万不能用水冲，因为水容易使鞭炮中的化学物质产生化学反应，会造成眼部酸碱烧伤，也不能用手揉眼睛，否则眼部伤口受压后可能会扩大。即使在没有明显外伤情况下，家长也要特别注意并进行观察治疗。案例7岁男童看哑炮受伤</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">（龙龙男7岁眉山）</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">和其他伤者不同的是，龙龙并不是玩的需要明火点燃的花炮，而是擦炮。龙龙像划火柴那样，把擦炮甩了出去，可擦炮并没有爆炸。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">好奇的龙龙慢慢靠近擦炮，弯腰查看时，擦炮突然爆炸。医生说，龙龙只有光感的右眼必须更换眼角膜。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">华西都市报记者程渝相关新闻</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">烟花爆竹经营点 较去年少200多个</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">华西都市报讯（记者席秦岭）昨日，记者从成都市安监局获悉，1月30日（除夕）至2月7日（正月初八），成都市中心城区共有307个烟花爆竹零售经营点，比去年春节期间的点位减少了两百多个。在此期间，成都市安监部门通过执法，收缴各类烟花527组、鞭炮1109饼、孔明灯500个，排查和整治各类安全隐患512处，取缔1户非法烟花爆竹零售经营点，挡获非法运输车辆2台。春节期间，成都市中心城区未发生烟花爆竹经营事故。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“今年，我们也特别提出对孔明灯安全隐患的重视。”工作人员告诉记者，由于城区高楼大厦密度较高，点放孔明灯不可控因素较多，因而造成的火灾隐患必须加以重视。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">春节期间暂时未接到关于烟花爆竹的安全隐患举报。“按照往年惯例，节后还将进行一次排查工作，并对春节期间的烟花爆竹燃放情况进行统计、对比。”同步播报</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">成都主城区 正月十五限时放烟花</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">根据今年成都市中心城区烟花爆竹临时销售点销售时间安排，1月27日（农历腊月二十七）至2月14日（农历正月十五）市民均可以到“黄房子”购买烟花爆竹，但需要注意的是，可以购买不等于可以燃放。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">根据《成都市烟花爆竹燃放安全管理规定》，成都市燃放烟花爆竹实行分区域分时段管理制度。三环路以内（含三环路）的地区为限制燃放烟花爆竹的区域，农历除夕和正月初一全天，正月初二至初六和正月十五每日的七时至二十四时，可以燃放烟花爆竹，其他时间不得燃放烟花爆竹。</span><br />
