		<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2013年1月28日国家科学技术奖励大会于人民大会堂隆重举行，习近平总书记主持会议并发表重要讲话，胡锦涛、温家宝、李克强等党和国家领导人均出席了会议。北京大学人民医院胸外科王俊主任作为2012年国家科技进步二等奖项目“中国肺癌微创综合诊疗体系的建立、临床基础研究和应用推广”的第一完成人参加了此次盛会并领取奖状。自1979年我国设立国家科技进步奖以来，胸外科界仅有三项课题获奖，这也是10年以来我国肺癌诊疗领域唯一的一项国家级奖项。此次获奖既是对王俊教授领导的团队20年来所进行的长期大量临床工作的认可，又是对北京大学人民医院胸外科在肺癌微创诊疗领域所取得创新成果的肯定，其意义深远，将进一步有力推动我国肺癌微创诊疗技术的快速发展。</span>
