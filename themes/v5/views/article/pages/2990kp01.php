		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>协和医院成功实施首例同期四关节置换术（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;日前，北京协和医院骨科林进教授等为一位重度类风湿性关节炎患者成功实施同期四关节置换手术。患者术后一周下地行走，两周出院。该项成果荣获北京协和医院2011年度医疗成果奖二等奖。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 3年前，年仅53岁的邱女士因患有类风湿性关节炎，双髋和双膝关节严重病损畸形，不得不与轮椅长期为伴，仅是去一趟卫生间，也需两个人搀扶。疼痛无时无刻不在折磨着她。这3年来，重新用自己的双腿站起来几乎成了她的一种奢望。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 2011年7月，邱女士抱着一线希望来到了北京协和医院骨科。林进教授带领王炜等医师组成的关节医疗小组通过详细的术前检查、充分的术前准备和周密的手术设计，并在麻醉科、ICU及免疫内科等科室的大力支持和密切配合下，成功地为邱女士实施了双髋和双膝共4个关节的同期置换手术。短短5个小时，邱女士就拥有了一副“全新”的双腿。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在骨科医护人员的精心治疗和护理下，术后仅仅2周时间，邱女士的双髋、双膝基本活动自如，能丢掉双拐自己行走了。虽然此时看起来她的步伐还有些像蹒跚学步的孩子，但这是她走向自立、自强、自信的良好开端。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 2000年至2011年的11年间，协和骨科共实施了14例多关节同期置换手术。患者年龄为22-65岁，平均42.7岁，置换关节数达到45个。所有患者均无严重并发症，常规出院；术后生活基本自理，明显提高了生活质量；长期随访均获得了良好的关节功能。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 多关节同期置换手术的成功实施，需要充分的术前评估和术中策略选择。术前必须对患者的自身条件进行评估，凭借北京协和医院强大的综合实力，骨科、麻醉科、风湿免疫科、ICU和血液科共同为患者保驾护航，为选择手术时机、麻醉方式、骨质疏松应对方案奠定了坚实的基础；术中充分考虑患者的主观诉求，首先解决“最痛的那条腿”，根据患者耐受情况灵活选择是否开始下一关节的手术。“手术能不能一次成功，关键不在于手术大夫，而是患者给不给力。”林进教授幽默地解释道。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 国内外文献大多集中于单或双关节置换，鲜有三个及三个以上关节同期置换的报道。多关节置换具有更好的社会效益和经济效益。与传统的分4次实施双膝和双髋关节置换相比，同期四关节置换可使邱女士避免多次麻醉的风险，同时可以减少重复的术前检查和术前准备，从而缩短约70%的住院时间，节省了约75%的麻醉费、用药费、床位费等医疗费用。从社会医疗保障角度来说，还可以大大节约医疗资源。</span>
</p>
