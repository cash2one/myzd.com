<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>毛克亚：腰椎间盘突出症的防治（资讯）</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><img src="http://static.mingyizhudao.com/147010321048098" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">毛克亚教授</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">毛克亚，男， 1970 年生，汉族，博士。解放军总医院脊柱外科副主任医师、副教授、硕士研究生导师。毛克亚教授毕业于解放军总医院骨科，并先后赴法国、美国、德国、加拿大等脊柱外科中心学习。长期致力于脊柱外科临床工作和研究，主要从事腰椎间盘突出症、颈椎病、腰椎滑脱、腰椎管狭窄、脊柱侧凸、脊柱结核及肿瘤的开放及微创手术治疗，年手术量达 500 台以上，达到国际和国内先进水平。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">腰椎间盘突出症是骨科的常见病和多发病，虽然常规手术治疗可达到神经根的松解与解压的目的，但有创伤大、手术恢复时间长和术后并发症较多的缺点，随着脊柱外科的发展，微创手术治疗腰椎间盘突出症越来越为人们所接受。我们邀请到了解放军总医院脊柱外科副主任医师毛克亚教授做客“专家访谈录”，谈一谈有关腰椎间盘突出症的最新研究进展，访谈内容主要围绕以下几点展开：</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、腰椎间盘突出症微创手术有哪些？它们的适应症和禁忌症分别是什么？</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、与常规手术相比，微创手术治疗腰椎间盘突出症有什么优势？</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、微创手术治疗腰椎间盘突出症前景如何？</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">针对以上问题，毛教授进行了详细的解答和分析。腰椎间盘突出症微创手术可分为彻底微创手术和姑息微创手术手术治疗，无论哪种手术，其并发症最常见的有神经损伤、出血、感染等，其禁忌症与切开手术的禁忌症类似。一些有其他合并症的患者不能耐受切开手术，此时可做微创手术治疗，例如合并有肾功能不全患者。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">姑息性微创手术具有费用相对低，创伤小，保留了椎间盘的功能的优势，但因其保留了大部分椎间盘，所以日后有可能会复发。彻底微创手术因其将髓核彻底摘除解决了复发的问题，但术后腰椎功能活动有轻微受限。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">腰椎间盘突出症微创手术要求临床医生要有一定的经验积累，医院要有较高级的设备, 因此临床尚不能广泛推广应用。临床医生要在考虑到需要和可能的基础上, 以提高治愈率、改善患者的生存质量、使患者获得最佳疗效为目标来制订手术方案。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">随着腰椎间盘突出症手术治疗技术的不断发展与创新, 以及人们对疾病更加深入的认识，腰椎间盘突出症的手术治疗将有着广阔的发展前景, 从传统的有创手术到微创手术, 甚至无创手术是发展的必然趋势。</span>
</p>
