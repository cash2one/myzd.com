		<p align="center">
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>老人膝盖疼，儿女心疼（科普）</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">作者：翁习生 </span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">日期：2015-12-13 23:04</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">链接：</span><a href="http://www.haodf.com/zhuanjiaguandian/wengxisheng_1971938484.htm"><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">http://www.haodf.com/zhuanjiaguandian/wengxisheng_1971938484.htm</span></a><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">随着入冬和降温，许多中老年人都会感到膝关节疼痛，尤其上下楼、买菜、做家务、散步、上厕所等都非常不方便，女性也不敢穿高跟鞋。这其中绝大部分是因为得了骨关节炎，他们很想知道有没有什么好的治疗办法。由于门诊就诊病人多，医生无法跟每个患者都详细解释，因此我想借用网络这一现代化传播手段，跟大家谈谈这话题，以飨读者，正所谓：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">老人膝疼儿女愁，骨关节炎是祸首。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">寻医问药求良方，专家科普把忙帮。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">正规治疗快步走，一解全家心头忧。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：什么是骨关节炎呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">答：骨关节炎又称骨关节病，俗称骨质增生，主要表现为关节软骨退行性变和关节周缘继发骨质增生（也就是常说的“骨刺”），中老年人尤其是女性比较多见。具体发病原因尚未得到全面阐释，肥胖、重体力活、过度运动、不合适的锻炼、高龄、遗传因素等都是发病的危险因素。全身多关节都可累及，主要是膝关节、髋关节、手指小关节、肩关节、脊柱小关节。在我国，膝关节骨关节炎最常见。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：什么是关节软骨呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">就是我们平时吃鸡鸭时，吃到的那层白色的薄薄的脆骨。人体这台机器经过几十年的运转，关节内的部分软骨磨没了，软骨与骨头不同，软骨一旦磨损后很难再生（骨头可以再生，哪怕九十几岁的老人摔骨折了，也可以再长上）。没有了软骨的保护，走路时骨头磨骨头，骨膜及周围的滑膜上神经丰富，所以十分疼痛。长此以往膝关节会变形，成O形腿或X形腿。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：关节软骨为什么会退变呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">这一方面是由于机械因素，即长期反复的磨损，而肥胖、过度运动等又会加速磨损，一开始表现为局灶性软化，软骨表面粗糙，逐渐失去正常弹性，继而出现小片脱落、不规则小凹陷或线条样小沟，多见于负荷较大部位，然后进一步出现微小裂隙甚至糜烂、溃疡，软骨大片脱落可致软骨下骨板裸露，进而出现硬化和囊性变；另一方面与个人体质有关，高龄或肥胖的女性更容易得骨关节炎。从微观的病因学来看，目前探明的主要机制有：软骨基质合成和分解代谢失调，软骨下骨板损害使软骨失去缓冲作用，以及关节内局灶性炎症。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：得了膝关节骨关节炎，有什么表现呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">有人表现为膝关节持续钝痛，有的还有关节肿胀，有人表现为活动时突然剧痛，出现“打软腿”。关节疼痛一般都是活动多后加重，休息则减轻。关节痛最初为发作性，诱因常常是受凉、过度劳累或轻微扭伤等。有的人最初一两年才犯一次，随着病情进展发作越来越频繁，最后变成持续性疼痛。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">如果关节在某一位置保持长时间静止不动，比如久坐几小时后，开始起身活动时感到关节僵硬，感觉关节像是被冻住了或粘住了，并且伴有疼痛，短时间活动以后这种“粘滞感”才消失。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">同时大多数人还伴有下蹲困难。长年累月还会出现关节变形，以“O型腿”最多见。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：得了骨关节炎，该如何治疗呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">答：早期的骨关节炎，应采取保守治疗，可有效减轻症状并延缓病情发展。首先，注意保护关节软骨，尽量避免剧烈、过度及负重的活动，如反复蹲起、爬楼梯、走远路、提重物、爬山等，肥胖者要控制好体重。同时，加强关节周围的肌肉力量：比如平躺在床上练习抬腿，伸直腿，勾住脚，将一条腿抬高到45°至60°左右（脚离床面40~50厘米左右），坚持10~15秒后再放下，两腿交替进行，每次做3~4个，每天早晚都做几次，以此加强股四头肌力量。如果关节疼痛比较明显，可以口服消炎镇痛药、贴膏药以及抹药膏，也可行关节腔药物注射。如果关节肿胀比较明显，可以进行高频理疗。另外，营养软骨的药物也适用于早中期的骨关节炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">但是，如果骨关节炎到了晚期，软骨磨损十分严重，保守治疗很难再奏效。要想解除病痛的折磨，唯有手术这一招了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：针对严重的膝关节骨关节炎，是做什么样的手术呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">这种手术被称为膝关节成形术，简单地说，就是把坏掉的那层软骨薄薄地去掉，换成人造的，中间再加上一个特殊塑料制成的耐磨垫片。打个比方，假设有张桌子，桌面变得坑坑洼洼了，我们没有必要把整张桌子扔掉换张新的，而是把坏掉的桌面去掉，用新桌面取而代之，别的部分都原封不动地保留。有的病人病情比较特殊，仅仅一侧关节软骨磨损比较严重，此时我们可以选择单髁表面成形术，以更小的创伤获得同样的效果。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：需要住院多长时间，做完手术能像正常人一样活动吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一般来说住院5到7天，术后两三天便可以下地走动、上厕所，经过两三个月的康复训练后，便基本和正常人一样了。上下楼、做家务、散步、骑车、游泳、跳舞、旅游等都没有问题，完全可以满足中老年人的活动要求。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：手术风险大吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">只要手术技术过关、术后不感染并且进行科学有效的康复训练，手术效果很好。同时，手术风险与患者自身的身体条件也有很大关系。高血压、糖尿病、冠心病只要控制好了，便可以耐受手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">问：人工关节可以用多长时间呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">只要不感染、不摔折了，可以用15~20年以上，据文献报道，有的最长用了40几年。由于假体使用寿命的限制，年轻人做这个手术要特别慎重，但是对于患有严重骨关节炎的老年人，平常生活受病痛折磨很痛苦，越早做这个手术就越早享福，有不少老年人术后生活质量得到很大改善，人也越活越年轻了。</span>
</p>	
			
