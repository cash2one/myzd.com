			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>出现下列症状来神经外科</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">头痛</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这是神经外科最常见的症状之一。临床上经常看到病人一有头痛就怀疑是不是头脑里长肿瘤了，其实日常生活中大部分的头痛都是功能性的，没有脑的器质性病变，只有下列情况要及时到神经外科就诊：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）急性发作的头痛：病人平时无头痛症状，突然出现剧烈的头痛甚至伴呕吐，可见于颅内出血、急性炎症、肿瘤卒中等;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2)反复阵发性头痛：多见于血管性头痛、癫痫性头痛、枕大神经痛或颈椎病等;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）逐渐加重的头痛：往往提示颅内压力逐渐增高，如果在头痛加重时视力逐渐下降，并在头痛时伴有喷射性的呕吐，则是典型的颅高压“三主征”，多为神经外科的疾病所致。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">面痛</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 包括面部、眼睛、鼻、口腔和牙齿等的疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(1)如果出现一侧面部突然发作的短暂而剧烈的疼痛，感觉像刀割、电击、烧灼或针刺一样，在洗脸、刷牙、吃饭或说话时容易突然出现，那就要考虑三叉神经痛的可能。特别要指出的是，三叉神经痛有时表现为难以忍受的牙痛，患者常先到口腔科就诊，迫不及待的要求拔牙，结果往往被拔掉半边的大牙，疼痛仍无丝毫缓解，最后才到神经外科。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(2)如果有面部持续隐隐作痛、钝痛或胀痛，无明显的间歇期，则要考虑三叉神经周围的病变，应到神经外科就诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(3)有舌根、口腔深部、外耳道深部的一过性剧痛，吞咽时常可诱发疼痛，则可能是舌咽神经痛，亦应及时就诊神经外科。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">抽搐：脑部疾病引起的抽搐，其发作有突发性，一过性和反复性的特点。常表现为突然发作的肢体抽搐，可扩展到全身，可伴意识障碍或大小便失禁，每次持续数秒到数分钟，发作间期完全正常。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肢体无力：可逐渐加重或突然出现。较轻的往往早期表现为握筷子不灵、易掉，持物困难，如果是下肢无力，则表现为走路拖沓，膝盖容易发软弯曲甚至跌到，患病一侧鞋底磨损明显比健康一侧的严重。如果手或脚不能抬起，甚至意识无法控制手脚的运动，就是所谓的“瘫痪”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不自主运动</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(1)抽动：表现为不固定的四肢、面部或胸腹部肌肉的不自主抽动，突然发生和停止。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(2)震颤麻痹：身体的一部分或全身不自主的节律性抖动，伴有肌肉的僵硬、运动迟缓。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">感觉障碍：人体的感觉有浅感觉和深感觉之分。浅感觉有痛觉、冷热觉、轻触觉和压力觉，深感觉是指人在闭幕时对肢体所处位置的感觉、震动的感觉和运动的感觉。神经系统的疾病可能引起这些感觉异常。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">视力视野障碍：视力突然或逐渐下降，视物范围缩小，如果到眼科检查没发现眼科的疾病或发现眼底有水肿表现，应到神经外科就诊，这些病症常常是颅内的病变引起的。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多尿：每天尿量在4000毫升以上，尿液颜色淡或无色透明，病人感觉口渴，每天需要大量饮水。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">失语：主要指后天性语言功能障碍或丧失，就是说原来没有聋、哑，后来才出现语言功能的问题。多数为左侧大脑高级语言中枢受病变影响所致。可表现为下列几种情况：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(1)运动性失语：表现为能听懂别人的话，能发音，但不能说话或只能说出个别单字，无法复述。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(2)感觉性失语：表现为听懂别人和自己的言语，虽然发音正常，说话连贯，但经常用词错误，答非所问。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(3)命名性失语：表现为知道一个东西的用途，却无法说出这个东西的名称，但当别人说出某物的名称时。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(4)完全性失语：表达和理解语言的能力都丧失，既不理解别人说的话，也不能说出来话。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(5)失读：视力正常，原来也识字，但现在阅读文字的能力丧失。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(6)失写：手部活动正常，但不能书写。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">月经异常和泌乳：月经不规则，每次来的时间不固定，量少，甚至完全不来月经，在没有生育时出现乳房分泌乳汁，量少的需挤压乳房才溢出，量多者可自动溢出多为双侧性，多为垂体瘤所致，可伴有视力下降。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿童巨人症、成人肢端肥大、面容改变：为垂体生长激素腺瘤表现。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">生长发育异常、性发育异常：多为三脑室前部或后部病变如颅咽管瘤、畸胎瘤、生殖细胞瘤、松果体瘤等引起。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">走路不稳：向一侧偏斜，多为小脑受累表现。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">嗜睡，性格改变、智力下降等：为额叶、颞叶或下丘脑等部位病变。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑膜刺激症状：突发头痛、呕吐、颈项强直，为蛛网膜下腔出血的典型表现。颅内的急性感染。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">躯体疼痛：放射性，一侧或双侧肢体无力、麻木，逐渐向上或向下发展，大小便障碍，为脊髓病变的表现。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">颅神经麻痹症状：如看东西有重影、上眼皮抬不起来、面部麻木、口眼歪斜、口角流口水、听力下降、吃东西吞咽不下去、饮水呛咳、发音异常、声音嘶哑等，为脑干、颅神经肿瘤及颅底邻近部位病变如动脉瘤、脑膜瘤、脊索瘤、颅咽管瘤、垂体瘤、神经鞘瘤等所致。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其他症状：眩晕，突发昏迷，记忆力、判断力下降等。</span><br />
