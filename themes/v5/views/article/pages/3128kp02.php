			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑干肿瘤能否手术？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人类的脑干约相当于大拇指大小，仅占全部脑的小部分，但它有着生命悠关的重要性。 在结构上，脑干居于脑的“中心”，上擎大脑，背负小脑，下连着脊髓，担负着小脑、脊髓与大脑的相互联系，同时是除嗅神经和视神经之外所有脑神经的出入处，另外，脑干是生命的基本中枢所在，如呼吸中枢、心跳中枢、睡眼中枢及意识中枢等均位于脑干。因此，脑干是神经系统的中心枢纽。脑干一旦有病变，如脑干出血，往往症状严重，引起四肢瘫痪、不能吞咽，眼球固定等，严重者呼吸停止，需用呼吸机维持生命，有时病情危急时，来不及抢救而呼吸、心跳停止死亡。因此，在现代神经外科中长期认为脑干上长病是不能手术的，为手术的禁区。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;但近20年来，由于对脑干解剖功能的深入研究，加之各种检查手段（如磁共振）、手术设备（如高清晰手术显微镜、超声吸引、神经导航等）的革命性发展，以及在显微神经外技术方面得到系统培训的新一代神经外科医师的成熟，脑干手术已经在一般省级医院成为比较普通的手术，脑干手术的神秘感早已淡去。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑干结构复杂，汇居众多的神经核和神经纤维，仅仅记着这些结构的名称和基本位置，就令一个优秀的医学生失眠多梦，但事实上，脑干内重要结构之间并非密不可透，其内有一些“缝隙”可供手术。目前高清晰磁共振可显示出神经核、神经纤维，及病变与这些结构的关系，因此，可用于指导医师决定脑干上病变能否手术及如何做手术，手术时神经外科医师已可“按图索骥”，达到切除病变而对脑干内重要的功能结构损伤最轻的目的。高清晰的手术显微镜和精细的显微手术器械为脑干手术提供了强有力手段，使脑干手术成为基本的现实。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;因此，当被诊断为脑干肿瘤时，不要轻易放弃。当脑干肿瘤为一些良性病变如海绵状血管瘤(为血管畸形而非真正肿瘤)、血管网织细胞瘤（也称血管母细胞瘤）等时，更应当持积极态度，因为这些病变如手术成功可以治愈。如笔者曾有一患者，女，30岁，诊断为延髓血管网织细胞瘤，来时由于肿瘤压迫呼吸中枢出现呼吸停止，及时采用呼吸机维持呼吸和手术切除肿瘤，术后呼吸恢复，意识清楚，四肢活动正常，术后已4年余，目前工作如常人。</span>
</p>
