			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>交感神经手术的历史与国内外现状</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">应用交感神经链切除手术治疗手汗症这一方法最早出现在1920年。由于这一结构解剖位置特殊，那时侯只能用开胸手术的办法完成这一手术，手术创伤显然很大。所以当时出现的这一方法并没有被病人和医生所广泛接受。但由于这样的切除交感神经链的手术确实能够有效地治疗双手多汗这一病症，所以人们并没有放弃这一手术，而是从此开始了改良并使其更加微创化的探索。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先后发明的手术方法有锁骨上切口、背部脊柱旁切口、腋下切口等等，但都没有明显降低手术创伤，反而增加了一系列手术并发症。也有人提出通过体外注射一些特殊的药物来破坏交感神经链从而达到治疗手汗症的目的。这样的手段尽管创伤确实减小了很多，但一个重要的问题就是疗效很不肯定，因为体外穿刺很难准确的达到预想的神经节段。现在社会上有人又在大肆宣扬地做这样的手术，其实是早就被隶属否定过了的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">20世纪90年代电视胸腔镜技术的出现给了这一手术方法探索上的一盏黑暗中的明灯。这是一项基于电视成像和冷光源技术的现代医疗科技。医生通过胸壁的一个0.5-1cm大小的小切口和置入其中的电视胸腔镜就可以明晰地看到位于脊柱旁的交感神经链，再通过一些特殊器械便可以完成交感神经链切除或切断这样的手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于手术大大简化，安全性和创伤性明显提高，使得在最近的20多年中，世界各地这一手术开展的例数呈数十倍甚至上百的增加。在过去的十多年中，国际上每年报道这一手术的文献达数百篇，涉及的病例上万人。电视胸腔镜技术的应用已经使这一手术真正成为了一项成熟的，并且常规开展的诊疗技术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但在十多年前，人们还多采用T2切断术或切除术，造成很多病人术后出现严重的代偿性多汗。尤其是在我国台湾地区，这样的病例很多，严重的代偿性多汗让病人很痛苦，由此造成了很多对这种手术不满的情绪。新近的研究在手术方式上做了新的探索和改良。尤其是目前我们建议采用的T4切断术，基本避免了代偿出现的问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腋汗症、上肢雷诺综合征，还有一种心脏病，叫长QT综合征，也都是这种交感神经手术很好的适应证。以往这种手术还用于头面多汗症和面红症的治疗，现在看来副作用太大，目前大医院极少开展了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在我国，包括北京大学人民医院在内的两三家大的诊疗中心在这一手术及交感神经链结构及功能方面做出了一系列深入研究，受到了国际学者的瞩目，极大地保证了临床手术的安全性和有效性。一些胸腔镜手术经验较少的单位也在尝试开展这种手术，技术上还有待规范，尤其是在规避手术并发症方面，还需要一些深入的理论知识和娴熟的技能作为支持。</span><br />
