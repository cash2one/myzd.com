<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>贺石生 上海市第十人民医院 脊柱微创中心主任、教授</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">直立行走的人类，解放了双手以从事创造性的劳动。但是，这样的进步是要付出代价的，脊柱疾病就是其中之一。用四肢行走的动物，脊柱与地面平行，无需承担体重；而人类就不同了，身体的重量都压在脊柱这一中轴线上，时间久了易引发椎间盘、脊柱韧带等结缔组织的损伤，产生局部酸、麻、胀、痛等症状。如大家所熟知的，长期久坐不动易引发腰椎疾病，而长时间保持同一姿势伏案工作，则是颈椎病变的推手。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">颈部是整个人体的交通要道，大血管、气管、神经等重要组织从中穿过。而增生、移位或膨出的椎间盘、脊柱韧带等，除了压迫神经引发局部疼痛、麻木、感觉障碍，还会有其他多种多样的表现，有些症状甚至貌似和颈椎病没有任何干系。比如，颈椎病中一种特殊的类型——交感型，即增生的颈椎结缔组织压迫交感神经，可引发心动过速、心律失常、血压升高、视物模糊等表现。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，还有神经根型（临床最为常见，以肢体疼痛麻木、感觉障碍为主要症状）、椎动脉型（因椎动脉受压，脑部供血不足而导致头晕）和脊髓型（由脊髓受压引发四肢无力、行走困难甚至排便障碍）等类型。颈椎病无疑是擅演“变脸术”的高手，如果出现常规检查无法解释的上述症状，不妨关注一下颈椎。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">颈椎疾病患者应首选内科保守疗法，但如果常规的牵引、理疗、服用解热镇痛药无法缓解症状，同时又有严重的全身性症状，就应该考虑接受手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一提到手术，很多患者心中就直犯嘀咕——担心手术中万一有个闪失会导致神经损伤甚至瘫痪。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">传统的颈椎前路手术，手术入路比较狭小，视野受限，如果要在如此狭窄的空间中对压迫脊髓的椎间盘、骨赘、后纵韧带等进行彻底切除，同时又不能累及一线之隔的脊髓，对医师的手术技能要求非常之高。不仅如此，在椎体后缘还常有丰富的静脉丛，极少量的出血就会模糊视野，从而极大地影响手术操作和脊髓保护。就算是经验丰富的老医师，碰到颈椎手术视野出血也得花上一段时间来仔细止血，如果止血不彻底只能用明胶海绵等填塞止血，但这会给脊髓带来人为的压迫。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">十院脊柱外科成功地将手术显微镜应用于颈椎手术中，所应用的手术显微镜，放大倍数在10-40倍，而且镜头可以变焦。手术医生对微小组织的辨认非常有把握，可清楚地判断局部病变的严重程度。据文献统计，显微镜下手术的神经组织损伤发生率在0.1%，大大低于常规手术3%的损伤率。</span>
</p>
