			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>狭颅症和小头畸形是一回事么？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊上经常碰到家长带着孩子来看“小头畸形”，甚至有些专业医生也说一年里小头畸形手术有多少多少例。这里要说明一点，小头畸形和狭颅症是两回事，不仅发病原因和机理不同，治疗的方式方法也不同，预后也有相当大的不同。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们医学中所说的狭颅症，也可以称为颅缝早闭。正常人颅骨上有很多条骨缝，常见的是前后的矢状缝和额缝，两侧对称生长的冠状缝和人字缝，在颅骨的生长扩大过程中，骨缝起着重要的作用，即使囟门闭掉了，只要骨缝存在，颅骨就会随着年龄的生长、脑组织的发育而不断扩大。但是如果某些原因，包括基因的、环境的、遗传的，反正现在还搞不清楚的，这些原因导致某一条或多条骨缝闭合了，那么骨缝相邻的两块颅骨就不会生长，而其余的颅骨就会代偿性扩大，导致头型的异常。最常见的是矢状缝早闭导致的舟状头，另外还有扁头、斜头及塔形头等等。颅骨的不生长就导致其下面的脑组织发育受限，慢慢会产生颅内压增高的表现，有的孩子会眼球突出，智力发育落后。因此，如果手术把骨缝重新开放或者颅骨打开重新塑形，就会缓解畸形颅骨对脑组织的压迫，促进脑组织的发育，而且改善外观的畸形，其预后还是非常不错的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而小头畸形则相反，多数是宫内缺氧、感染、颅内出血等等各种原因导致的脑组织发育不良。因为脑子不长，颅骨也就失去了扩大的动力，导致头颅偏小。这种情况下孩子的骨缝是存在的，其根本原因是脑发育的问题。因此，这种情况做颅骨手术是没有作用的，只有促进和改善脑组织的发育才能解决问题。但是，目前对于脑发育没有特效的治疗方法，而且很多神经的损伤是不可逆的，所以这种孩子的预后往往不好。</span><br />
<p>
