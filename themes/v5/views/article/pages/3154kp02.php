			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小儿眼睛手术，需要怎样的麻醉呢？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于孩子的眼睛要做手术，家长已经很忧虑了，眼睛上做手术，很可怕！而且医生说还要麻醉，这就更可怕呢，会不会影响到孩子的智力呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿童的眼科手术多采用全身麻醉，许多家长都担心全身麻醉会不会对孩子的大脑产生负面影响，是否影响智力、记忆力等，其实这种担心是不必要的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然任何医疗操作都有风险性，但是全身麻醉的风险很低，目前针对儿童使用的吸入式麻醉药物起效快、苏醒也快、体内代谢也很快，如果孩子肝肾功能正常的话，就更加的安全。而且全麻对大脑产生的麻醉影响也是短时间的，当麻醉结束药物迅速排出体外后，大脑的功能就会完全恢复正常。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，真正影响大脑功能的并不取决于麻醉，而直接取决于是否发生了脑缺氧。因为大脑对氧气是很敏感，但在整个全麻过程中，孩子都会得到充足的氧气供应，有全程的血氧饱和度的监测，同时还有现代化高科技的生命监测仪的看护，麻醉医生能随时了解到孩子体内氧气含量和其他生命指标的变化，保证不会出现缺氧的情况发生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然家长也要注意：麻醉之前必须禁饮食6小时以上，必须完善相关的血液和心电图、影像学的检查，手术前一周不能有感冒发烧的情况出现等等，孩子的安全靠我们一起来保障。</span><br />
