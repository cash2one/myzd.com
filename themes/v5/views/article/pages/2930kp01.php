<div align="center">
	<p>
		<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>胃肠道间质瘤的临床诊治</strong></span>
	</p>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><span style="line-height:2;">胃肠道间质瘤(gastrointesti-nal stromal tumor，GIST)为原发于胃肠道和腹腔的间叶组织肿瘤，细胞为梭形或上皮形，免疫组织化学c-kit染色阳性；与其他常见的胃肠道恶性</span><span style="line-height:2;">肿瘤不同</span><span style="line-height:2;">的是，GIST是一种恶性潜能未定的肿瘤。GIST发病率约(1-2)/10万人，占全部胃肠道肿瘤的0.1%-3%；发病中位年龄在55-65岁之间, 40岁以前发病很少，儿童更为罕见；男女比相当（1.2：1）。最常发生于胃(60% -70% )，其次是小肠(20% -30% )，结肠和直肠仅占5%，食道则占0% -5%，偶有发生在网膜和肠系膜者。GIST可发生于各年龄段，以50岁-70岁为高峰，发病年龄越小，恶性程度越大。</span></span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp; 以往对胃肠道发生的、以梭形细胞或上皮样细胞为主的肿瘤几乎都归为平滑肌肿瘤，曾被称为平滑肌瘤、平滑肌肉瘤或平滑肌母细胞瘤；目前共识，GIST是一种有着独特生物学特性的间叶组织肿瘤，不同于典型的平滑肌及神经源性肿瘤，不具有经典的平滑肌瘤和神经鞘瘤分化特征。统计近几年国内外文献大约700例外科手术切除病例，手术切除率40%-92%，5年生存率47.2%-57%。免疫组化方法显示近95%的GIST中CD117呈阳性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp; 胃肠道间质瘤的外科治疗原则因肿瘤的部位不同而不同：⑴胃GIST：一般认为至少应距肿瘤边缘2 cm以上。⑵十二指肠GIST：部位特殊，对于老人、低度恶性、肿瘤较小者，为避免增加手术并发症可只作局部切除，但要求切缘镜下无肿瘤细胞残余(冰冻切片)；青年患者、肿瘤较大但无播散者，不管其恶性度高低均应考虑行胰十二指肠切除，因为其局部复发的治疗效果极差。⑶直肠GIST：涉及到保肛若肿瘤直径≤2 cm则行肿瘤摘除术； 2-5 cm则作局部切除或考虑作前切除或Miles手术；≥5 cm时应施行前切除或Miles手术。需要强调的是，对于GIST患者，手术仍是唯一的根治性手段，能手术应尽量行手术切除。对GIST不主张常规行扩大切除或系统化广泛淋巴结清扫。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp; 如果肿瘤侵犯或浸润邻近器官，为了能完整切除肿瘤不宜勉强分离，只能行联合脏器切除；决定多脏器切除前，必须确认无腹腔播散和远处转移。如已有腹腔播散，多脏器切除意义不大。完全切除是GIST的标准治疗方法，传统化疗、放疗效果差。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp; GIST术后中位生存期48-66月；复发率达40%-90%，常发生肝、肺、骨转移等部位。临床数据显示对于术后复发中危及高危的患者，使用伊马替尼辅助治疗可以明显提高无病生存率，术后辅助治疗对于高危患者应至少要达到两年，建议终生服用。在临床工作中，对于已知的术后复发高危患者，应尽早使用伊马替尼辅助治疗。对于已经复发的患者，应全面评估疾病状况，争取手术机会，术后应给予伊马替尼辅助治疗。</span>
</p>
