			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>一岁半男孩告别“鱼肚子”仁济医院肾脏巨大肿瘤切除术获成功</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯（通讯员谭姗记者施嘉奇）年仅一岁半的小鹏鹏（化名）上周五挺着个“鱼肚子”，躺在仁济医院的手术台上。经过近3个小时的艰难手术后，泌尿外科医生们从孩子身上取下了重达近5斤的肾脏肿瘤，使他从此摆脱了因肿瘤迅速恶化而导致的生命危机。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一个半月前，鹏鹏出现反复发热的情况，且呼吸短促，确诊为巨大左肾母细胞肿瘤压迫肺部导致肺不张、肺部感染。1月2日入院后CT等显示：鹏鹏左肾一巨大肾母肿瘤已占据腹腔80%—90%，胃、脾、胰、肺等器官被严重压迫移位，同时伴有肺部感染、贫血。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术中，主刀医生叶惟靖、刘毅东发现肿瘤包裹着鹏鹏主要的血管，局部已开始溃破并与脾脏、胰腺、空肠、结肠粘连致密。叶惟靖教授首先分离出一个相对疏松的界面，局部突破，随后迅速控制支配肿瘤的大血管，再逐渐把瘤体与重要脏器分离，最终把瘤体完整切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 叶惟靖术后表示，鹏鹏将可以完全恢复到正常人的健康状态。他同时提醒市民，小儿日常生活中的腹痛、腹胀、呕吐、发热等往往是泌尿疾病的先兆，常规检查时不要忘了做尿检、泌尿系统B超。</span><br />
