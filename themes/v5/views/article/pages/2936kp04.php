	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>有效的缓解腰痛的方法&nbsp;(转载)</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 很多新妈妈在生完宝宝之后，平时只要做一些家务或者平时站着的时间过长，就会出现腰部的疼痛感，这与没有做好月子是有很直接的关系，如果你也会出现腰痛的正在，可以试试下面的方法，可以有效的缓解腰痛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 产后腰部保养的方法有：</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 揉筋结：用拇指指腹仔细在腰、骶部触摸，如发现有压痛的硬结时，则以指腹压其上，每结揉1分钟。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 揉摩腰背：工作之余，晨起或晚睡前都可以双手掌揉按摩擦腰背肌肉，上下揉摩50-100次，同时扭动腰部，有舒筋活血，促进局部血液循环，改善腰痛的作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 掌压腰骶部：俯卧位，双掌重叠压在疼处腰椎上，以不引起疼痛为度。一呼一吸为1次，做10-15次。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 推下肢：旁人帮助，俯卧位，固定胯部，以掌根从骶部开始，经臀部沿大腿外侧、小腿外侧，至另一侧肢体。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 滚腰操：仰卧，保持屈曲双腿的姿势，把身体蜷曲成团状，前后滚动10-20次。仰卧位，以热湿毛巾，不流水为度，于腰间疼痛部和稍下面的骶部，上面再放一热水袋，湿热约10分钟，然后再分别在腰两侧各约10分钟。最后，仰卧同法腹部。尤适用于经期腰痛。注意温度以不烫伤为难。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 另外，小编还要提醒大家，除了上述提醒的动作可以有效缓解产后的腰痛。但是如果做完这些运动后，还是没有很明显的疗效时，还可以去做一些专业的腰部按摩，也可以有效缓解疼痛。</span>
</p>
