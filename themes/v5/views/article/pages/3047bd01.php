			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>晚饭后不活动 易患胆结石</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3047/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，10月5日，沈殿霞已由重症监护室（ICU）转到普通病房供图/CFP</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 国庆前，香港艺人肥肥（沈殿霞）因胆管炎复发入院治疗。近日，又传出她因肝癌和胰脏癌而病危的小道消息。肥肥在20年前就查出患有胆结石，并伴有胆管炎。4年前，她经手术取出大小36块结石。“胆管结石可引起胆管炎，而肥胖与胆结石密切相关。”昨天，解放军302医院肝胆外科张培瑞副主任医师证实说，40岁以上的中年肥胖女性是胆结石的高发人群。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 绝经使女性形成结石</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 女性雌激素会使胆固醇更多地聚集在胆汁中，而40岁以上女性面临绝经，体内雌激素会相应减少，代谢水平也会下降，故容易形成结石。而肥胖女性的雌激素水平相对标准体重者会显得异常，所以形成结石的概率也高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 另外，中年女性有晚饭后长时间看电视的习惯，这也是形成胆结石的因素。宁夏中医研究院王学新主任医师经过10年研究发现，晚饭后睡觉过早，或晚上摄入高脂肪餐后不活动等导致的晚上临睡前胃及十二指肠积食，是引起胆结石形成的最主要原因。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 王学新认为，晚饭后常躺着看电视、看报刊、睡觉，使人体位处于仰卧或半仰卧，胆汁引流就不会通畅，便会在胆管内郁积、浓缩，胆结石就会形成。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 胆固醇代谢紊乱诱发胆结石</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据了解，我国胆结石症的发病率在8％－10％，且在逐年上升。“结石的主要成分是胆固醇结石，它是因胆固醇代谢紊乱，致使胆固醇从胆汁中析出、结晶形成的。”张培瑞说，胖人出现胆固醇代谢紊乱的机会通常比正常人要高很多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 排石、溶石治疗要慎重</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前，一些瞄准胆结石病人的排石、溶石治疗的广告满天飞。实验证明，所谓的溶石疗法根本不能溶化人体内的结石。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 张培瑞提醒人们，对待这些非手术治疗胆结石症的方法要慎重。结石若卡在胆管必须及时手术取出，否则就会引发急性胆管炎或胰腺炎，从而带来生命危险。他指出，目前胆结石的有效治疗就是手术取出，服用消炎利胆类药物只能控制症状，缓解病情，并不能去石排石。所以查出胆结石一定要接受正规治疗，切勿轻信广告。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 如何预防胆结石？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1.避免胆道感染，预防胆道蛔虫；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2.遵循高维生素、低脂肪饮食原则，多吃水果及纤维类蔬菜，少吃肥肉、蛋黄、动物内脏等高脂类食物；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3.早餐不可不吃，胆囊定时收缩，排空，可减少胆汁在胆囊中的停留时间；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 4.科学运动，控制体重，但切忌过快减肥，体重下降过快胆结石的发病率也会增加；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 5.如果怀疑自己有结石，可以用B超来检查胆囊内有没有石头、胆囊壁和胆管有没有增厚；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 6.胆结石尚无药物预防措施。</span><br />
