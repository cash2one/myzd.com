<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>武汉大学中南医院完成首例单孔腹腔镜手术</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">湖北新闻网10月28日电 (高翔 陈菁)武汉大学中南医院泌尿外科28日成功为一名肾囊肿患者实施了我省首台单孔腹腔镜手术，使术后腹部不留痕迹成为现实，标志湖北省微创治疗向无创迈进。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 王女士因腹部胀痛到医院看病，B超检查发现其左肾有一巨大囊肿，需要做手术。王女士听说即使采取腹腔镜这一微创方法来做手术，也要在腹部留下3个孔，这让爱美的王女士感觉难以接受。一月前，她找到在中国腹腔镜领域颇有建树的中南医院泌尿外科王行环教授，王教授决定采取国外最先进的单孔腹腔镜来为其摘瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在手术台上，王行环教授在患者肚脐上开一个隐蔽性小孔，插入自制的穿刺器，放入腹腔镜等微型手术设备，在腹腔镜的引导下，发现囊肿足有9cm大，将囊肿切开，排除其中的水分后，将囊肿从肚脐取出。手术仅用30分钟。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 术后王女士看到手术刀口隐藏在肚脐处，很难发现，像没做过手术一样，十分开心。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 王行环介绍，开展单孔腹腔镜有非常高的要求，首先由于只有一个操作孔，不象传统三孔腹腔镜手术形成操作三角，存在操作空间小、距离远、操作角度小等特点，对手术医生的技术要求非常高。其次，做该手术需要单孔三通道穿刺器，而该设备的专利在国外，中国没有生产厂家。为求得该设备，王行环找到国外相关单位，但其以技术保护为由拒绝提供。王行环自己动脑筋发明出了单孔三通道穿刺器，保证了手术的顺利进行。</span>
</p>
