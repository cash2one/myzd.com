<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">近期关于近视手术安全性的问题引起了广泛的思考，作为一个从事近20年近视手术的医生有自己的一些观点。华西医院近视手术做到现在大概有近6万例了，我们遇到两例较为严重的并发症，一例是早期手术的病人发生角膜扩张，通过带硬性隐形眼镜视力得以矫正；另外一例是近期出现的术后角膜感染，通过积极治疗目前矫正视力可以达到0.4，虽然都没有导致很严重的后果，但也提醒我们时时刻刻关注手术安全性。从统计学来看，这种手术的安全性还是很高的，当然这需要建立在很好的术前沟通、严格的适应症掌握、仔细的术前检查和评估、性能优异而稳定的设备、精湛的手术技术、严密的术后随访和良好的并发症处理能力的基础上。</span><br />
