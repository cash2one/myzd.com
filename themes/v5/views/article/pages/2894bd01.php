			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>张伟滨：骨肿瘤保肢和保命</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2008年北京奥运会前夕，19岁的国家队小将徐咏在美国训练时被查出股骨远端恶性骨肉瘤。徐咏的朋友、同时也是姚明的经纪人章明基走访国内多家医院，搜集各方名医的治疗方案送回美国，咨询3位美国权威骨肿瘤专家。最终，3位专家一致认可了其中一套保肢方案。按此方案术后不到4周，徐咏就能下地行走。尽管他的运动生涯不可避免地终结，但术后生活正常，至今五年病情没有复发。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这套治疗方案的设计者，正是上海交通大学医学院附属瑞金医院骨科主任张伟滨。骨肿瘤虽然发病率并不高，但是恶性骨肿瘤的死亡率却很高，患者往往面临截肢的命运，能保肢的不足50%。如何在抢救生命的同时，保住患肢？张伟滨通过自学摸索出一整套保肢手术方法，使四肢骨肉瘤的保肢率达到80%~90%，5年生存率达70%，术后肢体肿瘤复发率不到5%，这一生存率达到了国际一流水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">49岁的张伟滨，擅长治疗骨病、骨肿瘤和肩关节外科，尤其在骨肿瘤方面建树颇丰。他说，“骨肿瘤是骨科中的亚学科，涉及学科较多，一个好的骨肿瘤医生必须既具备骨科医生的手术技能，也必须具有内科医生的素质，熟悉化疗、放疗、生物治疗等各种恶性肿瘤综合治疗知识。因此医生在手术时不能只看到骨头，更要看到肿瘤整体，把病人放在中心位置，进行全面的考虑。尽可能地通过医疗手段帮助患者攻克病魔，减轻病人的痛苦，使他们活得体面、有尊严。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“金手指”探肿瘤</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骨科教授张伟滨的一天，从紧张而忙碌的手术开始。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">周一上午9点半，张伟滨再次查看病人的麻醉情况，戴好手套、口罩，走上手术台。手术台上一名中年女性患者的盆骨深处，隐藏着一个小西瓜大小的肿瘤。两年前，病人在外地被诊断为妇科肿瘤，可到了手术台上才发现肿瘤位于尾椎骨和直肠之间，这个部位布满了脏器、血管、神经，手术风险非常大，稍有不慎就会大出血。于是，手术中又匆忙找来外科医生，可手术进行到一半的时候，手术部位大出血，外科医生也不敢再深入，切了一块肿瘤送病理检查后草草塞了止血棉结束手术，留下了肿瘤残余在病人体内。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">两年过去了，病人体内的肿瘤不断长大，病人带着当时手术唯一的成果——一张写着“皮样囊肿伴癌变”的病理检查报告，辗转江西、广州四处求医，都没人敢收。最后，瑞金医院普外科教授尹路接收了她。由于肿瘤位于骶尾骨前方并对其发生侵袭破坏，且压迫推移直肠和肛门，经腹手术无法将肿瘤完整切除，于是尹路教授找来骨科主任张伟滨，一起为患者实施手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术台上，张伟滨一双粗壮有力的手稳稳地握住手术刀，从患者臀部后面切开一个小口，灵巧的食指向内试探着肿瘤的边缘，一边尽量避免伤及直肠，一边要当心周围密布的神经和血管。在一旁观摩手术的同事说，“这个部位的手术风险很大，稍不留神就会伤及大动脉，导致大出血，或者伤及直肠影响排便功能，虽然术前有CT扫描定位，但是术中复杂的三维结构，肿瘤的具体位置仍难以捉摸，张教授在无法看清肿瘤深部及周边组织结构的情况下，全凭一双手的手感，堪称‘金手指’。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">时间一分一秒过去，“金手指”一寸一寸地摸索着，终于找到深藏的肿瘤，背面的骶尾骨、周围的直肠、阴道被一点点与肿瘤分离开来，截断被肿瘤侵犯的骶骨，再一点点切除肿瘤组织……两个多小时后，患者体内的“定时炸弹”终于被清除。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">每天，张伟滨都要面对各种类型的骨科疾病，尤以骨肿瘤最为凶险。《韩非子》扁鹊见蔡桓公的故事里，曾有言：“疾在骨髓，司命之所属，无奈何也。”恶性骨肿瘤就是这种“病入膏肓”的恶疾。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骨肿瘤范畴很广，它包括所有来源于骨组织及其附属器官的肿瘤，分原发性与转移性。原发性骨肿瘤就是一开始就从骨头里长出来的肿瘤，如骨肉瘤。另一类称为转移性骨肿瘤，如肺癌、乳腺癌、甲状腺癌、肾癌、前列腺癌等的骨转移，临床上转移性骨肿瘤发病率占整个骨肿瘤的90%以上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">与肺癌、肝癌或者其他常见恶性肿瘤比，原发性骨肉瘤发病率非常低，仅为百万分之三左右，这也是为什么骨头的肿瘤比较难治的原因——因为医生见得不多。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发性骨肿瘤以骨肉瘤最多见，高发人群主要是青少年。目前研究认为，骨肉瘤的发病原因和青少年的骨骼生长有密切关系。典型骨肉瘤是高度恶性疾病，单纯手术死亡率很高，以往多仅行截肢术，5年生存率不足20%。随着化疗结合手术的综合治疗的普遍开展，初次就诊时尚未发生远处转移的骨肉瘤患者的五年生存率可达到60%~70%，已有很大的进步。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除原发性骨肉瘤外，还有一类继发性骨肉瘤，即骨头原来有一些非肿瘤或良性肿瘤性疾病，时间长了后慢慢发生恶性变化，变成了骨肉瘤。譬如有一种病叫畸形性骨炎，它是一种骨的特殊炎症，患病后有20%患者可能发生骨肉瘤变。中国人比较少得这个病，白种人比较多。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">保肢还是保命</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般恶性骨肿瘤，患者往往面临截肢的命运，国内病人能保肢的不足50%。而张伟滨主刀的四肢骨肉瘤保肢率已能达到80%~90%。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前骨肿瘤常用的手术方法有刮除术、瘤段骨切除术、截肢术。良性骨肿瘤或瘤样病变以手术刮除或切除为主。手术力求彻底干净，以免肿瘤复发或转移等一系列恶变，但应尽量保留肢体功能。张伟滨说，保肢手术的骨缺损重建主要有肿瘤型人工关节置换、大段异体骨移植、瘤段骨灭活再植等方法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着医疗技术的发展以及人们对生活质量的重视，恶性骨肿瘤的保肢手术治疗越来越成为患者和医生的共同追求，但能否保肢也要视情况而定。张伟滨反对保肢技术的滥用：“很多医院没有保肢手术的能力还热衷于给病人做手术，不加以全面的考虑，反而给病人造成了伤害。”他强调，保肢需要具备一定的条件，有时患者需要先做化疗，使肿瘤范围缩小、边界清楚，便于手术时将肿瘤完整切除。此外，进行保肢手术后的局部复发率要尽可能不比截肢术高，肢体功能也应比假肢好，使病人能保持正常的行为功能，确保生活质量。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“骨肿瘤手术不能只看骨头，更要看到肿瘤整体。”在张伟滨看来，骨肿瘤手术的首要问题是“肿瘤要切清楚，以防病情复发和转移，而不是肢体重建工作。如果不能大范围根除肿瘤，就相当于‘在肿瘤里切肿瘤’，可能刺激癌细胞进一步增殖、扩散，反而促使肿瘤生长和转移。因此对于恶性肿瘤，拯救生命永远是第一位的。当保命和保肢冲突的时候，应该毫不犹豫地选择保命。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张伟滨犹记得曾收治了一名20多岁的女孩，右胫骨近端查出肿瘤，做了穿刺诊断后，病理显示为良性。多年经验告诉张伟滨，病情远没有这么简单。然而，再次肿瘤切开活检的报告依旧显示为良性，于是，张伟滨只能按照良性肿瘤的治疗方法，对肿瘤进行刮除。“第二年肿瘤长大，切除下来还是良性。直到第三次，肿瘤再次长大，这次才被诊断为骨肉瘤。”经过化疗，女孩的肿瘤明显变小，但由于与血管黏合过紧，保肢困难，张伟滨最终为女孩做了截肢手术。这个病例充分说明有时骨肿瘤的诊断是很困难的，但一旦确诊后即应给予正确的治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">融会内外科</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">谈及瑞金医院骨科诊疗，张伟滨不无自豪，“目前我们瑞金医院的骨肉瘤保肢手术后局部复发率在5%左右，世界范围内的先进标准是在10%以下，可以说我们已经达到了国际的先进水平。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">作为国内为数不多的骨肿瘤治疗中心之一，瑞金医院的骨病专业组已完成6000余例骨与软组织肿瘤的治疗、800余例各类恶性或侵袭性骨肿瘤保肢术，并与胸外科、普外科及泌尿外科等科室通力协作，成功实施了数十例巨大骨盆恶性肿瘤切除肢体重建、胸椎骨巨细胞瘤病椎全切除等治疗，完成了国内首例恶性肿瘤骨盆以下截肢术（即海豚人手术）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“近十年来，在我国大的中心城市，骨肿瘤学科的发展已基本与国际同步，同时因为病人数量较国外更多，也给了临床研究以新的机遇。”不过他也坦言，不同于发达国家之间（如北欧国家）建立联合的骨肿瘤学科中心进行联合临床研究，我国的骨肿瘤学科发展尚未形成优势规模，优秀医生也并不多。“骨肿瘤手术技术难度高、风险大，往往一台手术就需要几个甚至十多个小时，十分辛苦。很多医生不愿意从事这个并不热门的领域。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在张伟滨看来，一个好的骨肿瘤医生，需要兼具内、外科医生的良好素质。“骨肿瘤涉及学科较多，如骨盆、脊柱、腹腔、泌尿、胸腔等系统，四肢还与丰富的血管、神经密切相关。很多骨科病变实际是内分泌的毛病，如甲状旁腺亢进形成的棕色瘤等代谢性骨病，很容易与骨肿瘤混淆。临床还经常会有女性因胸锁关节、肋锁关节软骨炎，而被误诊为肿瘤，这时就需要一名骨肿瘤医生具有全面的医学知识和素养，才能找到真正的病根。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术台上情况瞬息万变，需要医生拥有强大的心理承受能力和冷静的头脑。“有些极端高风险手术确实是在拿患者生命赌一把，但如我们怕承担医疗风险和责任，怕可能引起医疗纠纷而不敢赌这把，意味着患者一点生的希望都没有。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">作家陆幼青在生命最后阶段写就了震撼人心的《死亡日记》，阅读此书后，张伟滨也更加关注对肿瘤</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">病人的心理疏导：“虽然目前我们医生没有办法做到把所有恶性肿瘤都治好，但我相信除了医疗外，真情地与患者及其家属进行心灵沟通，尽可能缓解患者对疾病甚至死亡的恐惧，让患者处于一种乐观的和疾病抗争的心态中，积极配合医务人员的治疗也很重要。让病人有希望、有尊严地活着，应该是医生职业道德的体现吧”。</span>
</p>
