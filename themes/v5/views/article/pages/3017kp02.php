<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肝癌射频</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 国内肝癌的射频治疗已有十几年的历史，其适应症、具体操作技巧、疗效等已得到同行业普遍认可，并纳入了中华医学会制定的肝癌诊治规范。我院肝胆外科2000年开展肝癌射频治疗，积累了较为丰富的临床经验，适应症掌握严格，并发症少，疗效较好，现就有关问题简单向广大患者做一介绍。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、射频（RF）</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;射频（ ridiofrequendcy ablation,RF）是近年来应用于肝癌治疗的一种新技术，通过影像方法将电极针导入肿瘤组织，通过射频产生离子震荡发热，使电极周围组织坏死来达到治疗肿瘤的目的。由于其定位准确创伤小，毁损彻底痛苦轻，疗效确实恢复快，因此成为肝癌辅助治疗中的重要手段。解放军第302医院肝胆外科科2000年开展此项技术，治疗数量较多，迄今已行射频治疗肝癌1000余例，积累了丰富的治疗经验，获得满意的治疗效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、治疗原理</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 癌细胞比正常组织细胞耐热性差，局部加温至39－40℃可使癌细胞停止分裂，41－42℃时就可引起癌细胞的DNA破坏甚至细胞死亡。高频率的射频波（460kHz）能激发组织细胞进行等离子振荡，离子互相撞击能使局部产生80－100℃的热量，可迅速有效地杀死肿瘤细胞。当温度不足以引起坏死时，可以引起另一种形式的细胞死亡－调亡。经高温灭活的肿瘤组织，由于细胞免疫表型的改变而具有瘤苗的作用，从而发挥特殊的抗肿瘤作用。RF治疗后还可使肿瘤周围组织凝固坏死形成一个反应带，能阻断肿瘤血供并防止肿瘤转移。射频的波长较短，穿透力不强，组织的温度随与电极距离的增加而迅速降低，且RF的整个治疗过程由电脑控制，并在电视屏幕（B超、CT、MRI）监视下进行，对癌灶周围正常组织损伤较小，最大限度地保存了正常肝组织，尤其适合晚期肝硬化合并肝癌且不能耐受手术的病例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、操作方法</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;行RF治疗时常规备全麻及开腹器械以防急用。穿刺前4小时禁饮食，前半小时肌注度冷丁50mg、非那更25mg。B超先确定穿刺点，1%龙胆紫做标志，消毒、铺无菌巾、1%利多卡因局麻，视肿瘤大小在B超引导下穿入一根或两到三根穿刺针。射频能量预先设定好，计算机监控能量输出、组织阻抗及治疗时间。对直径小于5cm肿瘤，一到两次即可完成。直径大于5cm肿瘤，酌情在不同层面、不同方向、多次穿刺治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、 疗效判定</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 以术后2月复查（1）病人一般情况，如乏力、纳差、肝区疼痛不适得到改善。（2）化验AFP结果正常。（3）彩超检查病灶内近期呈高回声、无明显血流信号及治疗后高信号无回声病灶大于治疗前。（4）螺旋CT平扫病灶呈低密度，增强扫描后病灶无增强及治疗后低密度、无增强病灶大于治疗前等四项标准为治疗效果良好的判定标准，依次分为效果良好、部分有效及无效，以后每2个月复查一次。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">五、治疗效果</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;小肝癌的治疗效果可以与手术相媲美。一些特殊部位，比如临近大血管、胆管、胆囊及肠管等脏器的肿瘤，开腹射频具有独特的优势，主要是定位准确，操作安全，疗效可靠。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">六、并发症</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;RF以及其他各种经皮穿刺进行的微创治疗手段最严重的两大并发症为：穿刺部位大出血及腹腔脏器损伤。这两种情况均需及时开腹探察，进行止血或修复损伤。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;其它如皮肤烫伤、局部疼痛、胸腔积液、术后发热等轻微并发症均可经对症治疗后好转。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">七、止痛措施</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;初期手术病例仅用1％利多卡因局麻，大多数病人能忍受，但较为痛苦，有时影响治疗。后期总结经验，术前常规肌注度冷丁50mg、非那更25mg，术中给予充分吸氧、心电监护，必要时缓慢持续静点异丙酚。出现恶心、呕吐、高血压、低血压、心率失常时加强对症治疗，病人疼痛不适明显减轻，均能保证手术顺利进行。术后疼痛一般能忍受，不予止痛，以免掩盖病情，延误治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">八、提高疗效</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;1．准确定位是提高疗效，防止腹内并发症发生的关键。术前仔细阅读CT/MRI片，观察胆囊、大血管、胆管、膈肌、肾脏位置及与肿瘤的关系，尤其观察硬化变小的肝脏与胸壁、膈肌之间是否有肠管存在，术中B超仔细辨别腹腔内各种组织结构，选择合适的部位、方向、深度进针，避免副损伤。另外，经过RF治疗后产生的凝固性坏死及蒸气造成的强回声，其范围超过实际治疗面积，易过高估计治疗效果，也影响B超对周围病灶的观察，因此，对于较大或多处病灶，可从不同部位同时进2－4个穿刺针，治疗一个平面或一个部位后，按预先设计好的次序，逐层或逐个部位进行，实践证明穿刺范围准确、完整、穿刺时间缩短，效果较好。对于较大肿瘤，应当由肿瘤与正常肝组织交界处开始逐渐向肿瘤中心进行，有望预防穿刺过程中肿瘤细胞发生转移。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;2．在治疗过程中，能量要逐渐增加，使电极周围组织的加热固化过程缓慢进行，有利于热量的传递，减少电极周围组织碳化结痂而阻抗上升，提高疗效。也可在B超引导下瘤体内注射生理盐水或高渗盐水，利用高温盐水作介质向远处传导热量，扩大治疗区，或采用尖端冷却的电极以防止温度过高造成组织碳化阻碍热量的传导。如行开腹RF，可暂时阻断肝门，减少热量散失，也可提高疗效，缩短治疗时间。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 3．电极板放置位置。电极板必须放在腰背部，尽可能接近治疗部位，电极板尽量紧贴皮肤，从而减小阻抗，可以在最短的时间内达到最佳治疗效果。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 4．防止术后腹腔感染。由于肝癌合并肝硬化患者抵抗力低下，常常合并少量腹水，且癌组织液化坏死、少量血液及胆汁漏入腹腔，细菌可经穿刺针、血行或胆道引起腹腔感染。术中术后常规短期应用抗菌素，以预防感染发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">九、方法选择</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 手术切除仍然是公认的治疗肝癌的最有效手段，其无瘤生存期、治愈率、3－5年生存率是所有治疗方法中最高的。但部分肝癌患者已愈晚期，或肿瘤靠近第一、二肝门、肝实质中央等情况，手术无法切除。这就促使其他如无水酒精局部注射、射频（RF）、激光、微波、冷冻、超声聚焦、氩氦刀、TACE、PVE、CIK等治疗手段不断发展，在不能手术切除病例的综合治疗中，取得一定疗效。RF是其中的一个突出代表,尤其适用于合并肝硬化的原发性肝癌，因为肝硬化后纤维组织增多，肿瘤周围被致密的、导电性能差的多量纤维组织包围，热量不易散发，形成“烤箱效应”，疗效确实可靠。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">十、治疗途径</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;RF可经皮、开腹、腹腔镜下及手助腹腔镜下进行。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; （一）经皮治疗肝癌的主要适应症：①肿瘤巨大或靠近肝门，无法切除；②患者不接受开腹手术；③靠近肝实质中央或靠近腔静脉的小肝癌；④肝内多发病灶或肝转移灶；⑥肝功差或存在其他严重合并症，不能耐受麻醉及开腹手术；⑦晚期巨块型肝癌可减轻瘤负荷；⑧配合手术或介入等其他综合治疗措施。禁忌症：①弥漫性肝癌；②肝功能严重受损，伴明显黄胆、腹水、凝血功能障碍；③腹腔广泛转移，发热，恶液质。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;优点：创伤小、痛苦轻、不用开刀、恢复快、安全可靠、并发症少、近期疗效好，易于通过影像学观察疗效。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;缺点：①影像学未发现的病灶易于遗漏；②巨大肿瘤易于残留病灶；③有损伤腹内脏器可能；④不利于腹内其他病灶的探查及处理；⑤不能同时放入门静脉或肝动脉化疗泵；⑥治疗效果受术者经验和技术的影响。⑦近期疗效确切，远期疗效有待于进一步观察。⑧RF有导致肿瘤转移可能。肝硬化病人肝功能差，合并脾功能亢进时外周血白细胞、血小板计数低下，不能耐受放化疗，有可能在术中反复穿刺过程中造成肿瘤扩散。本组有2例术后2月内肝内广泛转移，考虑与此有关。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;（二）开腹RF与经皮RF相比，具有以下优点：①在麻醉状态下进行，患者手术时无疼痛；②在直视下进行，操作方便，定位准确，治疗彻底，不易出现腹内脏器损伤，不易遗漏病灶，能最大程度地保存正常肝组织；③配合手术切除，在肝断面治疗后可减少肿瘤复发，减少术中出血，能提高肝癌的手术切除率；④同时放入化疗泵，利于术后综合治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;开腹RF缺点：①创伤大，痛苦长，恢复慢，费用高；②对于肝功能差、有严重合并症的病人或不接受手术的病例不适用。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;（三）腹腔镜及手助腹腔镜RF则具有经皮及开腹手术的优点，最大限度地克服它们各自的缺点。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">十一<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:26.6667px;">、</span>发展前景</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;RF以及其它局部肿瘤毁损技术均是近十余年才发展起来的新技术，不可否认，其具有独特的优点及适应症，但毕竟无法比拟以及替代经典的手术切除，它的滥用会给医务人员及患者留下永不磨灭的阴影和遗憾。经此类治疗后的病灶是否完全坏死及在影像上的表现与癌组织残留如何鉴别，反复穿刺有无可能造成肿瘤扩散，巨大肝癌的局部毁损是否有疗效，毁损范围应该如何界定，首次治疗不彻底是否延误病情等，均是目前困扰RF等此类技术广泛应用、阻碍其进一步发展的严重问题，均需我们在今后临床工作中进一步深入研究和探索。但RF作为以手术切除为主的综合治疗措施之一，作为手术切除的补充和完善，作为一种姑息性的局部治疗手段，尤其作为一种微创技术，具有损伤小，适用范围广，疗效肯定，易于被病人接受等自身优点，值得进一步在临床上推广应用。</span>
</p>
