			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>高端技术擒病魔，仁心仁术济患者</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;——仁济医院胆胰外科在王坚教授带领下进入“机器人时代”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 日前，上海交通大学医学院附属仁济医院胆胰外科主任王坚教授带领团队完成我院首例达芬奇机器人胰十二指肠切除术。胰十二指肠切除术由于手术切除脏器多、解剖复杂、术后并发症多，被认为是普外科最具难度、最具挑战和创伤最大的手术。即使是传统开腹手术也常常困难重重，只有经验极为丰富的普外科医师才能完成，患者的切口长达750px以上。由于胰腺的解剖位置深，周围血管丰富，切除难度大，缝合精度高，所以一直是微创手术的瓶颈，国内外胰十二指肠切除的微创手术也一直处于缓慢发展与探索阶段，目前只有少数几个中心能开展腹腔镜胰十二指肠切除术。近一年来，胆胰外科在王坚教授带领下，勇闯手术禁区，不断挑战高难度手术，已成功开展了腹腔镜胰十二指肠切除术。但腹腔镜下的胰肠、胆肠等吻合口重建一直是腹腔镜胰十二指肠切除术的难点，需要较长的学习曲线，开始阶段医务人员需花费大量精力与时间，对医务人员来讲是种身心与意志的挑战。随着达芬奇手术机器人系统的出现，腹腔镜技术存在的诸多问题得到了解决，机器人系统的机械臂可以360度旋转，使得缝合变得精确和便捷，同时提供更清晰的手术视野、更准确的影像系统，使得手术更加的精准和精细，术中出血更少，最终达到术后快速康复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该患者是一名65岁男性，由于腹痛难忍1月入院，通过CT诊断为十二指肠乳头部肿瘤。十二指肠乳头部肿瘤早期无明显症状，起病隐匿，需要通过胰十二指肠切除术进行治疗，传统开腹手术的创伤非常大、术后恢复慢，经过完善的术前评估与精心的术前准备，王坚主任决定通过机器人技术为该患者进行手术。手术在王坚主任娴熟地操作和助手的协力配合下顺利实施，通过精细游离、精准解剖，达到手术几乎不出血，完成我院首例机器人下十二指肠乳头部肿瘤的胰十二指肠切除，开启了仁济医院胆胰外科的“机器人时代”。术后一天患者即转出监护室，状态平稳。目前患者术后恢复良好，已经出院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院胆胰外科成立一年以来，在科主任王坚教授的带领下，将“微创为核心的精准外科理念”运用于复杂的肝胆胰手术中，全面开展各项微创手术与技术，在治愈患者的同时，最大程度的减少患者的创伤，为患者的快速康复提供保证。目前已经开展了腹腔镜下胰十二指肠切除、腹腔镜下胰体尾切除、腹腔镜下胆总管囊肿切除胆肠吻合、腹腔镜下肝段切除、腹腔镜下胆道探查、单孔腹腔镜胆囊切除等高难度手术。随着我院首例机器人辅助胰十二指肠切除术即获得成功，这标志着胆胰外科已经全面进入了微创时代，以微创为核心的精准外科理念，将通过全面结合机器人技术与腹腔镜技术，给越来越多的患者带来福音。</span>
</p>
