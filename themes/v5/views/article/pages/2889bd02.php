			<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">他们的乐观感动着我</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨天在交大闵行校区观看奥运圣火传递的人群里，有位曾参与四川灾区第一线救助的医疗志愿者，他就是交大医学院附属仁济医院的医生潘耀华。他偶然得知奥运火炬将在母校传递，一大早乘着校车从徐汇赶到闵行。站在人群里，听着学生们吹着喇叭、挥着手高喊“奥运加油，中国加油，四川坚强”，他忍不住激动起来。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在第一时间赶赴四川灾区参与医疗救治，牵动潘耀华心的，是受灾群众严重的伤势。“尽管我们尽量想办法保住他们的肢体健全，但有些人因为被压时间很长，得了挤压综合征，肢体损坏严重，必须面对截肢。”他和其他医务工作者在那里几乎是没日没夜地工作，平均每天只睡4个小时左右。4级5级的小余震，他们根本无暇去感觉，心思全在手术台上和病房里。只有电视台预报晚上可能有6.1级余震的那天，他们才从屋子里暂时撤离，躲到室外空地的帐篷里。屋外下大雨、积水，他们坚持着过了一夜，第二天继续工作。与此同时，更让潘医生感动的是，他的同事徐欣辉在面临大余震时，坚守在医院的重症监护室里值班，守着那些无法被转移到室外的病人。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 爱是互相的，感动也是互相的。受灾孩子的乐观坚强，让奋战在第一线的医务人员有了更大的信心和力量去救助更多人。潘医生说，他见到病房里北川中学的三个女孩，她们被救出后，三人第一次见面时抱头痛哭，但不一会儿就好了，聊天、唱歌，还时不时有笑声，并不是整天愁容满面，悲痛无助。“一方面可能她们还小，童真比忧虑多，另一方面，这也说明我们国家灾后保障做得好。据我所知，很多灾区的患者都将转移到北京、上海、重庆、广州等地继续治疗，不用过于为未来担心。”</span>
