		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑动脉瘤的诊断和治疗</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>脑动脉瘤破裂出血后具有极高的死亡率和致残率！到底什么是脑动脉瘤呢？它的治疗除了我们最常接触到的开颅手术外还有其它方法呢？在这里进行一个简单的介绍。</strong></span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">因脑动脉管壁局部缺陷和腔内压力增高，脑动脉管壁逐渐变薄并异常膨出，在临床上称之为脑动脉瘤。脑动脉瘤的发生和先天性因素有关（有一定的家族倾向）,但更多的和高血压动脉硬化、感染、外伤等有关。本病好发于40-60岁中老年人，女性发病率比男性略高。在脑血管意外中，脑动脉瘤仅次于脑血栓和高血压脑出血，位居第三。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脑动脉瘤好发于脑动脉分叉和主干的分支处，由于此处动脉壁的肌层发育薄弱，又是受到血液冲击力最大的地方，在长期血流的压力和冲击力的作用下，此处动脉壁的薄弱点向外膨出，最终形成动脉瘤。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脑动脉瘤就像是在脑血管壁上吹起的一个气球，随时都有可能破裂。当在精神紧张、情绪激动、劳累、头部剧烈摆动、猛弯腰、急起身、饮酒、用力排便、举重物、上楼、性生活等诱因下，引起血压突然增高，很容易引发破裂出血，对病人的生命造成威胁。据统计，脑动脉瘤第1次破裂后死亡率为30%，它还会发生第2次、第3次破裂。据统计40-50%的病人会在破裂后的1个月内发生第2次破裂，第2次破裂后死亡率约为70%，第3次破裂后死亡率为100%。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">有时候，动脉瘤虽然没有破裂，但因为体积的增大，可以压迫附近的神经，从而造成相应的症状，如一侧眼睛睁不开、复视、偏盲、视力丧失以及面部疼痛等。</span>
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">因此，一旦出现剧烈的头痛，或者不明原因的脑神经症状，就应该考虑患有脑动脉瘤的可能。应立即去医院进行脑血管的检查（如CTA、MRA、DSA等），排除或明确脑动脉瘤，并去神经外科就诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">内科治疗目前只用于未破裂的微小动脉瘤，主要措施是控制各种能够诱发动脉瘤破裂的危险因素，如控制血压、戒烟、避免紧张或者通过各种方法来放松，还要定期进行影像学检查，监测动脉瘤的大小和发展的情况。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于破裂的动脉瘤和较大的未破裂动脉瘤，外科治疗是唯一正确的选择。除了传统的开颅手术夹闭外，还有血管内介入治疗可供选择。后者属于微创的治疗方法，仅是在病人的大腿根部血管上穿刺一个小洞，通过微导管的输送和操作，将动脉瘤进行填塞，达到止血和预防再出血的目的。病人痛苦小，恢复快。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">至于每个病人的动脉瘤选择开颅手术好，还是血管内介入治疗好，应该由专业的神经外科医生根据病人的具体情况来决定。</span>
</p>
