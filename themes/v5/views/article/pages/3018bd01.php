	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>中日友好医院刘德若谈肺癌的科学诊疗（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 11月26日下午14:30，中日友好医院大外科主任、胸外科主任、肺癌中心主任刘德若做客人民网，就肺癌的防治与网友进行交流。敬请关注。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">【嘉宾简介】</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 刘德若，医学博士，主任医师，硕士生导师。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 现任中日友好医院大外科主任、胸外科主任、肺癌中心主任。中华医学会北京胸外科学会副主任委员，中国医师协会胸外科医师分会常委，中华医学会第三届胸腔镜外科学组委员，中华医学会北京胸外科分会微创学组组长。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 学习工作经历：1982年毕业于白求恩医科大学，1995年获中国协和医科大学医学博士。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 专业特长：开展了大量胸外科手术，在肺外科、气管外科造诣较深。开展的胸外科复杂手术如气管支气成形术，人工气管置换手术，心包内肺切除术，肺血管成形术和上腔静脉手术；人工胸壁置换转移肌皮瓣重建术，游离空肠代食管术。近年来开展了肺减容手术，并在切除病变的判断、肺漏气的预防、支气管镜下肺减容进行了新的探索；在支气管镜冷冻治疗气管支气管肿瘤方面积累了丰富的经验。目前开展胸腔镜下肺叶切除术和肺癌根治术、肺移植手术等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 承担国家级及院级科研课题4项，国内外发表论文30余篇，参加编写胸外科专著4部，译著1部。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 2004年获北京市人民政府颁发2002-2003年度“首都精神文明建设奖”，近年来获北京市科技进步三等奖1次，多次获得院级科技进步奖，获国家专利2项。</span><br />
