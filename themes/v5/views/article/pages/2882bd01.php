		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海医院成功救治中国年龄最小脑干肿瘤患者</strong></span>
</div>
<br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中新网上海四月二十一日电 (记者 陈静)中国国内患脑干肿瘤并行手术治疗的年龄最小、体重最低的病人新近在上海交通大学医学院附属新华医院成功获救。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这名病人是个年仅七个月，体重四点七公斤的男婴。家长告诉记者，患病的男宝宝在出生后三个月常常用手反复抓头，平时极易哭吵，且吃奶后经常呕吐。同时，体重增长速度明显较同龄正常儿童缓慢。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 家长曾带该男婴到医院就诊，通过头颅核磁共振检查发现，该男婴颅内有一巨大占位性病变，侵犯脑干，同时合并有梗阻性脑积水。因为患儿年龄很小，手术风险巨大，该院建议转院治疗。家长到其他儿童医院求救，尽管做了治疗，但只暂时缓解了患儿的脑积水问题。孩子脑中的肿瘤依旧像一颗定时炸弹，随时危及生命。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 近乎绝望的家长从北京赶到上海新华医院小儿神经外科。新华医院小儿神经外科专家立即组织医院麻醉科、小儿神经内科、护理部等手术相关科室，就该患儿的病情和手术等问题进行了细致的分析和探讨，为其制定出详细的肿瘤切除方案。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 小儿神经外科主任马杰教授说，肿瘤起源于脑桥和延髓背侧并覆盖四脑室底和导水管下口，肿瘤血供极为丰富，整个手术在显微镜下微雕细刻地持续了三个小时，最后，五×六×七立方厘米的肿瘤被完整切除。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前，该男婴术后身体恢复良好，体重也有所增加。专家指出，假如孩子出现不明原因的头痛、敲头、用头撞墙、脾气突变、易烦躁、不明原因的呕吐、头围比周围儿童大等情况时，家长一定要当心，千万不要错过“脑肿瘤”的早期警报。</span>
</p>
