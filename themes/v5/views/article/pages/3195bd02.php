		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;">赵元立谈蛛网膜下腔出血（资讯）</span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">视频介绍</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 12月13日15时，首都医科大学附属北京天坛医院神经外科教授、主任医师赵元立做客人民网健康访谈，围绕着“危重病：蛛网膜下腔出血”这个话题与网友展开深入的交流，欢迎广大网友收看。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">【嘉宾简介】</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 赵元立　1996年毕业于中国协和医科大学八年制医学专业，获博士学位，此后一直在首都医科大学附属北京天坛医院神经外科工作。目前担任首都医科大学附属北京天坛医院神经外科教授、主任医师、博士生导师。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 1997年起在国内率先开展神经导航手术，显著提高颅内深部病变手术精确性。1998年入选北京市科技新星计划，2001年起参与脑血管病外科国家“十五”攻关课题，采取创新手术方式治疗颅内动脉瘤和脑动静脉畸形，2004年起担任神经外科中心脑血管外科和老年肿瘤专业组主任，2005年晋升为主任医师，2007年起获得首都医科大学博士生导师资格，已招收博士研究生4人。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 目前主要从事脑血管病和颅内肿瘤的诊断和治疗工作，担任专业组主任以来，每年完成各种神经外科开颅手术200例以上，系本单位中青年学术带头人之一。兼任中华医学会神经外科学分会工作秘书、青年委员会副主任委员。目前还主持2项国家自然科学基金、国家十二五支撑计划分课题研究。参加工作以来累计发表论文60余篇，参与编写多部专著。2003年、2005、2009年三次获国家科技进步二等奖，多次获部、市级科技奖励。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 兼任全国青联委员、东城区政协常委，中华医学会神经外科学分会秘书，美国Neurosurgery杂志国际编委等。</span><br />
