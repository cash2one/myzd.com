			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>应《中国青年报》之约谈医患关系</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“医患关系紧张，最终受伤的是病人。因为它可能引导一些医生有意无意地‘躲避’危重病人，倾向于很保守的治疗，并且看病盯住‘短平快’。有时，可能会让治病成了‘但求无过’的表面工程、短期工程。”刘彦国医生这样概括他眼中医患对立的“毒果”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刘彦国医生告诉记者，医患关系不好，首先会引导一些医生“挑病人”。“当手里同时有几十个病人的住院条时，那些危重的、年纪大的、身体不好的、并发症多的、手术难度高的病人，就可能被‘婉言相拒’。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 他表示，在一些专科医院，这种现象相对更明显，由于其本身的辅助科室不好，更倾向于接收简单的病人。“所谓‘简单’，就是手术好做的、偏早期的、病变比较小的、年龄比较轻的、身体底子好的。这样，病人能很快出院，家属高兴，医生也没有被骂、被打的危险。医患双方‘皆大欢喜’，问题是被‘挑出去’的病人怎么办？”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 让治疗变得小心翼翼，是刘彦国医生眼中的第二个“毒果”。因为“医生常常会把医疗的风险性搁在第一位，而将疗效搁在第二位，这不正常”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 他举例告诉记者，在给肺癌病人做化疗时，如果严格按体表面积算，某种药用到150毫克，疗效是非常确切的，但150毫克显然提高了严重毒副作用的风险系数。“这时，医生会怎么选？如果医患双方很好沟通，对于毒副作用彼此有‘共担’意识，那么，肯定要首先保证疗效；但如果遇到比较‘矫情’、难以沟通的家属，很多医生会以‘不出事’为首要原则，把剂量适当降下来些。”在刘彦国医生看来，医疗的首要目的应该是疗效，“为了所谓的‘安全’而减少剂量，对病人是好是坏？难说。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 保守不止体现在用药上，刘彦国认为，它甚至让医生对病人说话，都越来越像“外交辞令”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “比如放在10年前，当你肺里有个小结节时，如果有90%的把握是良性的，医生会告诉你‘没事，记得3个月后再做个CT，观察一下就行’。这对病人是极大的宽慰。”但在医患关系紧张时，越来越多的医生不再用这种“担风险”的说法了，他们会考虑到哪怕1%的可能性，把一切小概率事件都说给你听，以免被“算后账”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “当你检查了一大通，医生还只用‘可能是……也可能是……’的句式时，病人会怎么想？其实，医生比病人更怕‘误诊’，为了不惹麻烦，只有用排除法‘买安心’。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 然而，刘彦国医生眼中最大的医患对立“毒果”，恐怕是治病在一些医生那里，正在变成面子工程、短期工程。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “拿我们常做的肺癌手术举例吧。医生在给一个肿瘤病人做手术时，如果肿瘤与周围结构‘粘’得比较紧，一个职业精神比较强的医生，会非常仔细地剥离肿瘤，一定要把它切除干净。但代价是手术时间长、创伤面大，而且会增加术中大出血和术后迟发出血的风险。这时，如果病人家属不好说话，对这些副作用和风险性全然不理解、不接受，有的医生宁愿安安全全地把肿瘤剔除下来，手术时间短，病人创伤面小，术后恢复还快，病人和家属都能高高兴兴地回家。”但是，后者可能过3个月肿瘤就复发了，而前者，也许3年都不会复发。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “3个月还是3年，对病人性命攸关。当治疗被迫成为面子工程、短期工程时，谁之过？谁受害？”刘彦国忧心忡忡地反问。</span>
</p>
