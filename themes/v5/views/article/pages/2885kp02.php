	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 尿道上裂的发生率要明显低于尿道下裂的发生率，因此，就是一些小儿泌尿外科专科医师也可能很少碰到尿道上裂或没有碰到尿道上裂的病例。尽管如此，尿道上裂的手术难度并不比尿道下裂大，关键在于是否存在有尿道括约肌发育异常、阴茎上弯的程度是否严重，如果小朋友阴茎严重上弯，可能需要分期手术，首先解决阴茎上弯的问题，当然，根据手术中的情况，我们大多同时进行阴茎伸直和尿道成形；如果小朋友有尿道上裂，同时合并小便无法控制（尿失禁），则治疗方案可能不仅仅是尿道修复的问题，还可能需要进行控尿手术；如果单纯仅是尿道上裂的问题，根据小朋友阴茎发育的情况，择期手术，对于阴茎发育好的病例，在小朋友6个月大时即可进行手术。尿道上裂的小朋友必须通过手术进行生殖器矫形，一方面进行形态和功能的纠治，另一方面，可避免以后对小朋友心理的影响。至于麻醉方面，儿童专科医院都有专门的小儿麻醉科医师，对尿道上裂这类手术的麻醉而言，应该是很安全的，一般不会对小朋友有影响。手术的目的，就是是小朋友的阴茎外观类似于正常小朋友包皮环切手术后的样子，同时，也能像正常小朋友那样正常排尿。</span>
