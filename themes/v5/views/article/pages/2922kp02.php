			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>神经内分泌肿瘤的诊治进展</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胃肠胰神经内分泌肿瘤( gastroenteropancreatic-neuroendocrine tumors，GEP-NETs) 起源于消化道的神经内分泌细胞，包含</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">了一大类生物学行为及临床特征不完全相同的肿瘤，其原发灶大部分位于胃、小肠、结直肠及胰腺。在西方，胃肠胰神经内分</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">泌肿瘤的发生占到全部恶性肿瘤的1% ～ 2%，并且近几十年来其发病率呈明显上升趋势［1］。许多GEP-NETs 能够分泌激素</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">或多肽从而产生相应症状，例如类癌综合征、Zollinger-Ellison 综合征或低血糖综合征等，称为功能性肿瘤，而其他一些GEPNETs</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">则不分泌引起临床症状的激素，称为非功能性肿瘤。虽然大部分的GEP-NETs 都是非良性的，但其生长相对缓慢，因此</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">预后可能好于腺上皮来源的胃肠道肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前最常用的GEP-NETs 分类方法是2000 年WHO 推荐的分类标准［2］，其中将GEP-NETs 分为高分化神经内分泌肿瘤、</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高分化神经内分泌癌、低分化神经内分泌癌及内、外分泌混合性肿瘤等类型。近年，欧洲内分泌肿瘤学会( European Neuroendocrine</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Tumor Society，ENETS) 提出了GEP-NETs 的TNM 分期及肿瘤分级系统［3-4］，对WHO 分类标准做了更进一步的补充。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">按照肿瘤原发部位的不同，GEP-NETs 的TNM 分期方法不尽相同，而按照有丝分裂计数和Ki-67 指数进行肿瘤分级的标准是</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">完全一致的( 表1) 。高分化肿瘤不但在细胞形态上与低分化肿瘤存在差异，并且其生物学行为及临床特性与低分化肿瘤也明</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">显不同。低分化肿瘤具有较强的侵袭性，并且对以铂类药物为基础的化疗相对敏感，而高分化的GEP-NETs 则对化疗相对耐</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药。因此，对GEP-NETs 进行科学合理的分期与分级，对于指导GEP-NETs 的治疗具有非常重要的意义。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">表1 GEP-NETs 的分级标准( ENETS)</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">分级有丝分裂计数( 10 HPF) Ki-67 指数( %)</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">G1 ＜ 2 ≤2</span><br />
