<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">G3 ＞ 20 ＞ 20</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于GEP-NETs 包含了一大类生物学行为及临床特征不完全相同的肿瘤，而不同的GEP-NETs 对于治疗的反应性有所不</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同，因此如何选择最佳的治疗模式面临着巨大的挑战。通常如果患者条件允许，外科手术切除是GEP-NETs 首选的治疗方式，</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">之后多种治疗手段可作为考虑，例如传统的生物治疗( 生长抑素类似物、INF-α) 、化疗及靶向药物治疗等。特别是随着人类对</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">于GEP-NETs 生物特性的深入了解，目前越来越多针对GEP-NETs 信号传导通路中上调信号的新型靶向药物不断应用于临</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">床，为GEP-NETs 的治疗提供了更多选择，也为取得更好的疗效带来希望。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、外科手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术是目前惟一能治愈GEP-NETs 的方法，通常根治性手术适用于患者一般情况好，肿瘤局限在原发灶或仅伴有区域淋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">巴结的转移。同其他胃肠道恶性肿瘤一样，肝脏是GEP-NETs 最容易出现远处转移的部位，但与其他肿瘤不同的是，如果能将</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">GEP-NETs 的原发灶和肝转移灶同期或分期切除，远期治疗效果是非常令人满意的，并且患者可获得较好的生活质量。研究</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">表明GEP-NETs 在将肝转移灶切除后，其5 年生存率可高达87%，但大部分患者肿瘤最终还将复发［5-6］。对于一些晚期患者，</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">减瘤手术可用于控制由于激素分泌过多或肿瘤局部压迫、梗阻引发的症状。对于较为年轻，肿瘤原发灶已切除，同时不伴有</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝外远处转移的患者，当临床症状通过其他手段难以控制时，肝移植可作为一种治疗选择。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、局部治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于已无手术机会或无法耐受手术的GEP-NETs 患者，通过射频消融、激光消融及冷冻治疗等先进技术控制肿瘤的肝转</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">移灶，可有效减轻肿瘤负荷，减少激素分泌，从而改善患者的生活质量。Berber 等［7］报道在腹腔镜下对神经内分泌肿瘤( neuroendocrine</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">tumors，NETs) 行肝转移灶射频消融后，其症状改善率可达80% ～ 95%，影像学显效率高达97%，作用时间可持续6</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">～ 24 个月。与正常肝脏组织不同，肝转移瘤主要通过肝动脉供血，因此肝动脉栓塞常用于控制GEP-NETs 的肝转移病灶，其</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有效率超过50%。对于肝转移灶无法切除，并伴有明显症状的患者，经导管肝动脉内化疗栓塞作为一线治疗也是合理的，尤</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其是在患者的全身症状无法继续通过生长抑素类似物控制时。化疗栓塞通常使用的化疗药物为多柔比星或顺铂，化疗栓塞</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">后患者症状改善率可达63% ～ 100%，肿瘤客观有效率为33% ～ 80%［8］。</span>
</p>
