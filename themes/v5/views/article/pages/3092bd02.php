<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>巨大脊柱内骶管囊肿在三博脑科成功切除</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 千龙网讯(通讯员 李亚静) 近日，首都医科大学附属北京三博脑科医院范涛教授成功切除了一例长在脊柱骶管内的巨大囊肿，囊肿大小约10厘米，在临床上很罕见。目前患者已经可以下床活动，症状明显好转，正在逐渐康复。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 37岁的余先生来自河南，1年前出现腰部疼痛，当时没有特殊治疗，后来腰疼逐渐加重，半年前又出现双侧髋部、臀部及大腿发木，而且还伴有大便干燥，排小便费力、尿频尿少，咳嗽时尿失禁等症状，这时余先生才到当地医院检查，结果发现他的脊柱下端骶管内有个巨大的囊肿。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 因为囊肿巨大，手术有很高的风险，余先生来到北京三博脑科医院找到了脊髓脊柱专家范涛教授，范教授为他制定了周密的手术方案后，在神经电生理监测下，成功切除了囊肿。据范涛教授介绍，患者的囊肿位于脊柱骶管内，体积很大，囊肿的上方达第五腰椎上缘，下方在第五骶椎下缘处，骶管背侧骨质已经受压变薄，神经根压迫明显，且大部分被挤压在囊肿右侧，少部分在囊肿腹侧，粘连紧密，手术难度和危险性都非常高。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 范涛教授说，骶管囊肿不是肿瘤，没有恶变可能。囊肿内脑脊液压力增高，压迫周围骶神经、骨质，严重者可引起骨质破坏。还会出现感觉、运动功能障碍、甚至大小便功能异常。有些骶管囊肿没有症状，一般不需要处理，可先行观察。如果患者出现腰腿疼或间歇性跛行保守治疗无效，影响正常生活或工作；腰腿疼伴下肢肌力、感觉减退；会阴部疼痛或感觉减退，大小便或性功能障碍，就要提早积极手术治疗。</span>
</p>
