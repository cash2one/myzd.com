			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中午带饭易致癌？胡说！</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近日广州有媒体报道，白领中午带饭因多是隔夜饭菜，在细菌分解作用下，食物中会形成亚硝酸盐，有致癌作用，加热也不能去除。记者就此向国内长期从事食品安全和相关医学研究的专家求证。 &nbsp;&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 长期从事食品安全研究和监督管理的上海市药品食品监督所所长李洁表示，中午带饭与胃癌没有必然联系。因为从理论上说，亚硝酸盐要变成亚硝氨才会致癌。隔夜菜虽然亚硝酸盐含量升高了，但不会都变成亚硝酸胺，亚硝酸胺的形成是需要许多条件的，比如要有微生物的作用，要有酸碱度PH值的作用，还要有温度、时间的要求等。长期从事消化科临床医疗的仁济医院戈之铮教授也表示，临床试验表明亚硝胺的确会导致胃癌，但必须是大剂量的长期服用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 因此，专家提醒不要长期、大量食用腌腊制品。总体饮食上，当然是吃新鲜食物最佳，要带饭的话，要特别注意防止细菌污染。对于现在城市中胃癌发病出现年轻化倾向，专家认为，除了与饮食有关外，睡眠不足、精神紧张也是重要致因，培养良好的生活习惯、放松心态都很重要。</span>
</p>
