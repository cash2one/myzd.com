<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>千万别被过时的“新技术”忽悠</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<img src="http://static.mingyizhudao.com/147010538304329" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;王行环，武汉大学中南医院泌尿外科主任、教授、博导，中华医学会泌尿外科学分会常委，在泌尿外科微创手术，特别是在前列腺微创手术、腹腔镜手术方面有较高的造诣。如腹腔镜下肾盂成形术、肾癌根治术、前列腺癌根治术等。特别擅长经尿道前列腺等离子电切术。在国内很早开展经尿道前列腺电汽化术，经尿道等离子切割技术等。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;检查前列腺</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;还要常规武器</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;前列腺增生患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;易两极分化别被过时的</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;“新技术”忽悠</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;作为中华医学会泌尿外科学会分会常委，王行环教授指出，目前各医院对良性前列腺增生的诊断、治疗水平参差不齐，有的医生在诊断时，该做的检查不做，不该做的检查却做一大堆，而部分患者盲目相信大型检查设备。其实诊断前列腺增生主要依靠以下“常规武器”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;直肠指诊。对下尿路症状典型者，医生首先推荐直肠指诊，检查方法是经肛门用手指检查前列腺，可以了解前列腺的大小、形态、质地、有无结节及压痛、中央沟是否变浅或消失以及肛门括约肌张力情况。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;尿常规。对于疑似良性前列腺增生，尿常规也是最好的检查项目，做尿常规可以确定下尿路症状患者是否有血尿、蛋白尿、脓尿和尿糖等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;查血清。良性前列腺增生可以使血清值升高，查血清也是推荐项目，抽血化验立刻可有结果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;超声检查。超声检查可以了解前列腺形态、大小、有无异常回声、突入膀胱的程度，以及残余尿量，经直肠超声检查还可以精确测定前列腺体积，因此，超声检查被较多的推荐。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;尿流率检查也常被推荐，像通常排尿一样排一次尿，让机器记录下尿流率，不过，尿量在150~200ml时进行检查较为准确。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;王行环特别指出，CT和磁共振成像一般不用来检查良性前列腺增生。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;前列腺增生患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;易两极分化</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;前列腺增生患者两极分化趋势明显：有的患者得知病情后不当一回事，而有的患者因害怕而要求过度治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;今年57岁的黄爹爹在体检中被诊断为患有轻度的前列腺增生，他找到中南医院泌尿外科王行环教授，非要医生为其实施手术不可。医生告诉他只需吃点药，生活当中注意点就行了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;王行环教授说，前列腺增生在40-79岁的男性中发病率约为50%，80岁以上的男性发病率则高达80%。而大多数患者对前列腺增生存在认识误区，一方面是像黄爹爹这样要求医生过度治疗，另一方面也有患者认为前列腺增生是自然现象，没有在早期进行治疗，直到出现急性尿潴留、脱肛、肾积水等严重并发症，生活质量受到严重影响时才到医院看病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;王行环教授介绍，对于轻度前列腺增生患者，可服用药物控制病情的发展，在生活上密切注意，保持健康的生活方式，定期检查。而当出现严重影响生活行为及出现血尿、尿潴留等症状时，则要考虑手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp; 别被过时的</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“新技术”忽悠</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 专家指出，良性前列腺增生也分轻重缓急，医学上有一个专业的评分标准。症状轻的根本不需要治疗，只要定期体检，长期观察就可以了。中度的可以通过吃药进行治疗。只有那些严重的，已经出现了并发症的，才必须接受手术治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;在中南医院手术室，记者目睹了一台目前方法最先进的前列腺增生手术--双极等离电切术。无影灯下，只见王行环教授从患者尿道插入电切镜，对前列腺增生部分进行准确定位后，仅用十分钟就将增生部分汽化切除。术中出血很少。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 治疗前列腺增生目前在方法的选择上非常混乱，有的地方甚至把落后的、甚至已经被淘汰的技术当作新方法进行推广。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;王行环讲了这样一个例子。70岁高龄的林老伯因严重的前列腺肥大，在某家医院接受了前列腺支架手术。但没多久，因支架放置后发生移位，他得了尿失禁。林老伯只好再次到医院治疗。本来，林老伯可以接受一种切除前列腺的微创手术，但由于尿道里放的支架已经长到了肉里，如果要切除前列腺，必须开刀把支架取出。林老伯已经70岁高龄，心脏又有点问题，无法承受开刀手术。于是，他只能忍受尿失禁的痛苦，在医院里住了三个月。就拿林老伯的遭遇来说，在尿道里放入金属支架，虽然有效，但出现并发症的几率却高达35％，其中最常见的就是尿失禁。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;王行环说，还有一种球囊扩张手术。它是把球囊导管通过尿道置于尿道前列腺部，经球囊扩张后，把前列腺撑开，排尿就通畅了。按理说，这种方法是挺合理的，好几年前医学界也曾把它作为一种新技术加以推广，很多医疗器械商人也纷纷推销。但临床证明，这种手术的远期效果并不好，容易造成尿道损伤，对于部分患者来说，完全是得不偿失的。目前，学术界已经认为它是一种应被淘汰的技术。王行环还举例说，现在有几十种热疗的方法，其中有不少也是应该被淘汰的，但仍有不少医院把它当成新技术推广使用。</span><br />
