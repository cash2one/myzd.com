	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胃癌的早期诊断</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">胃癌是世界范围内常见的恶性肿瘤，其临床症状通常与良性疾病没有区别，因此，大多数胃癌患者一旦发现即为中晚期（进展期）阶段。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">早期胃癌的手术治疗效果5年生存率可达90%以上，因此早期诊断和治疗十分重要。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前，日本通过气钡双重造影及胃镜来筛查，早期胃癌的发现比例可达50-60%，韩国早期胃癌的比例已达30-40%。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">我国目前早期胃癌的比例约为10%，在少数几个大的医疗中心可达20%左右。因此，重视早期胃癌的筛查，尽早发现、及时治疗，对于提高胃癌患者的5年生存率十分重要。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">早期胃癌的检出，目前仍然依靠胃镜及组织活检，临床医师的警惕、患者的重视以及内镜医师和病理科医师的经验对于提高早期胃癌的检出率在目前来讲十分重要。</span><br />
