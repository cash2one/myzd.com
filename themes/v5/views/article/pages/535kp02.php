<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实脑起搏器用于特发性震颤治疗已经有相当长的历史了，国内外已大量成功病例报道，被认为是特发性震颤最理想的治疗方法。我们也发现特发性震颤脑起搏器治疗效果满意，无明显手术并发症和副作用。</span><br />
