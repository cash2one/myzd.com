	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑起搏器治疗特发性震颤</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发性震颤(essentialtrem，ET)是一种常见的运动障碍。又称为特发性震颤、良性震颤。常有家族史，故又称为遗传性震颤或家族性震颤。1887年Dana首次描述了该病，100多年来，尽管在其病因、发病机制、临床特征、诊断与治疗诸方面进行了大量研究，目前仍确诊困难，治疗无非常有效办法。为提高对本病的认识，今就本病发生机制、临床特征、诊断、鉴别、治疗及基础研究等方面简要概述。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发震颤与帕金森的区别和诊断：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床上最容易与ET相混淆的疾病是PD或帕金森综合征，有学者研究发现PD患者的家族中有高比例的ET患病率；同时ET中有高比例的ET-PD，且往往ET的发病先于PD；于是有学者认为ET是一种PD的顿挫型。但不少学者研究认为ET与PD是两种不同疾病。临床鉴别主要依靠震颤形式及神经系统体征。PD可有动作性震颤，不过静止性震颤更典型。静止性震颤可见于身体各个不同部位，常不对称，其中最典型的表现为搓丸样动作。是由肘部屈伸运动，前臂的前旋后旋运动和拇指运动所致，其频率为4-6Hz，典型静止性震颤随运动开始而消失，病程后期亦可转为姿势性。多巴胺能药物治疗通常可改善震颤。除震颤形式不同外，PD还有肌僵直、运动减少等神经系统体征。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">生理性震颤正常情况下仅在维持某种姿势时出现，在某些情况以及使用特殊药物时可加重并成为一种症状，如焦虑、紧张、恐惧、锻炼、低血糖、甲状腺毒症、酒精戒断及某些药物，频率手部为6～12Hz，一般有相应的心理或病史特点，去除触发因素症状即可消失。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，本病还需与其他疾病引起的震颤如小脑病变、脱髓鞘疾病、多发性硬化等进行鉴别，结合相应疾病特点鉴别不难。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如何治疗特发性震颤 ?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对症状较轻，不影响到工作和生活，可临床观察，不需要治疗；对症状较明显，影响工作，可服用心得安治疗；如症状影响到工作和生活，脑起搏器是首选治疗方法。脑起搏器治疗特发性震颤的手术方法与治疗帕金森病几乎相同。既往也采用丘脑腹外侧核毁损术治疗，但副作用和并发症较多，并且通常只能作一侧；由于脑起搏器的发明，目前已很少采用。</span><br />
<br />
