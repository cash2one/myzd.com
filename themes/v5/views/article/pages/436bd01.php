		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>医院5天拔出3根筷子 医生疾呼家长要时刻注意孩子安全</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这是新华医院短短5天以来，拔出的第3根筷子。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“真是逆天了，要开筷子特色门诊的节奏。”院方负责人忍不住吐槽。3起“筷子惹祸”事件令人胆战心惊，耳鼻咽喉头颈外科主任杨军忍不住再次疾呼，“请各位家长时刻注意孩子的安全，杜绝意外伤害！”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">昨天下午1时，新华医院又接收一例被筷子误伤的患儿。20个月的小姑娘姗姗（化名）生性顽皮，午餐时趁大人不注意抓起筷子玩。不小心跌倒后，悲剧发生：筷子插入左侧口腔侧壁！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">家长心急如焚，从松江带着孩子辗转两家医院来到新华医院。万幸的是，较之医院之前接收的两例“筷子兄弟”，姗姗的筷子因为斜插入口，没有刺入咽腔或者颅腔。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">口腔科主任周瑞庆马上主持组织多学科会诊，由住院总医师魏天佑进行手术。一小时不到，筷子顺利拔出，目前小姗姗仍处于耳鼻咽喉头颈外科病房密切观察中。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">不妨回顾前两例“筷子惹祸”事件。2月26日傍晚，家住奉贤的小阳阳（化名）因吃插在筷子上的紫薯时不慎摔跤，筷子通过口腔插入颅内，直至第二天下午2时才终将孩子体内足足7厘米的筷子取出；2月28日，刚过完3岁生日的常州小男孩航航手持筷子追逐打闹，筷子插入头部，尖端顶住了颈内动脉和静脉的交汇点，一天之后筷子被顺利拔出，航航现正在禁食的愈合观察中。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为何都是筷子惹的祸？孩子和父母在一起应最安全，怎么接二连三发生意外？院方表示，虽然短短5天遇到3起极端病例实属罕见，但儿童意外伤害的悲剧几乎天天都在上演，“特别是春节前后，单是在新华医院取出的气道异物集中在一起，南货铺都可以开了。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">杨军提醒家长，“尽量不要让孩子接触到尖锐物品，在活动奔跑的过程中应尤其注意。”他还表示，如果尖锐物品已经造成伤害，千万不能自行拔出，否则后果不堪设想。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">家是安全的地方，但也不是家长可以疏忽大意的理由。“作为家长与医务工作者，儿童意外伤害是我们最不愿意看到的，而且大部分也是可以避免。”杨军说，“尤其是低龄儿童，为了他们的健康安全，家长必须从日常行为做起！”</span>
</p>
