		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>专家王德辉分析鼻窦炎为何高发于青少年</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻炎的事情，可大可小，小到患者忽略它，也能一两天后痊愈;严重的的时候呢?却需要上医院开刀做手术。这不仅同鼻炎患者的病情有关，更与我们对于“鼻炎”概念的差异有关。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 这些差异，复旦大学附属眼耳鼻喉科医院耳鼻喉科主任王德辉教授为网友们一一做了解答，希望大家明白其中的误区。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3138/1.png" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">王德辉教授谈青少年是否最容易引发鼻窦炎</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 主持人：何种情况下鼻炎是“小事”?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王德辉教授：鼻炎分为急性鼻炎与慢性鼻炎两种，急性鼻炎一般都在感冒以后发生。急性鼻炎本身就是感冒的一部分，就是平常所说的病毒性鼻炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 感冒常发于日常生活，一般成人一年平均要感冒1-2次，儿童的次数是3-8次。感冒通常是5-7天就会痊愈，病毒性鼻炎也是如此，一般不超过10天就会康复，感冒好了，它就好了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 如果没有康复，则会转变为细菌性鼻炎，也是急性鼻炎的一种。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王德辉教授：细菌性鼻炎的治疗，一般都用抗生素类药就可以治愈，需要时要配合一些鼻腔冲洗和化痰化鼻涕的的药，鼻内鼻用的激素类药。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 抗生素类：阿莫西林，罗红霉素，阿奇霉素等</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 激素类药物：地塞米松眼液，伯克钠，雷诺考特</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 急性中，病毒性鼻炎不用抗生素治疗就可以，就对症治疗，支持疗法，多用一些收缩血管，鼻子不通用点药，多喝水，注意休息，慢慢就好。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 主持人：什么程度的鼻炎需要动手术?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王德辉教授：动手术的鼻炎患者一般都是因为慢性鼻炎，慢性鼻炎的情况要相对复杂，病因很多，与细菌感染、霉菌感染、环境等等因素都有关。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 慢性鼻炎诊断中，患者首先会有鼻塞、浓鼻涕、闻不到味道甚至头痛的症状出现，在医生诊看鼻腔内有分泌物，或者鼻息肉时，便可确诊是否是慢性鼻炎。如今还可以做更仔细全面的鼻内窥镜和CT检查。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王德辉教授：如果患者鼻子里有明显的息肉，脓鼻涕很多，在通过药物治疗后效果不好，医生根据患者的情况看鼻炎对患者的全身影响大不大来诊断是否要进行手术，如果很大，比如经常头痛、精力不好到影响工作、学习和生活质量，就要考虑做手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 做完手术病人的情况也会有差异，有人会完全好了，有人还是会有一些症状，但做了手术之后相比不做要好很多。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">青少年是不是最容易引发鼻窦炎疾病呢?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 主持人：青少年是不是最容易引发鼻窦炎疾病呢?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王德辉教授：鼻窦炎是青少年继近视之后的另一高发病，常高发于10—18岁之间的青少年，且大多数以学生为主。临床上，医生对在一般看来健康的儿童中进行鼻窦X线拍片检查，发现大约50%孩子存在鼻窦病变，如果对这些孩子进行上颌窦穿刺冲洗，竟有1/3左右可以冲洗出脓液。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 然而，不少家长总是以为鼻窦炎只是流鼻涕的小问题，殊不知小问题下竟然藏着巨大的健康危害。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 那鼻窦炎对青少年学生而言，到底会产生哪些健康危害呢?专家对此问题做了相关科学详细地讲解。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 鼻窦炎有急性和慢性两种。急性鼻窦炎早期症状与感冒相似，鼻塞、脓涕多、精神萎蘼、拒食、甚至抽搐等症状，同时伴有咽痛、咳嗽;也可伴发急性中耳炎、鼻 出血等;还可能引发脑膜炎。而慢性鼻窦炎主要表现间歇性或经常性鼻塞、黏脓性鼻涕，常发生鼻出血。病重者表现有精神、食欲不振，体重下降或低势，甚至可继 发贫血，风湿，关节痛，感冒，胃肠或肾脏疾病等全身性疾病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 青少年患鼻窦炎后会导致氧气吸入受阻，引起血氧饱和度下降，血氧供应不足则 会引起全身各组织器官不同程度缺氧，出现四肢乏力、昏昏欲睡、大周期性头痛头昏、视力下降、记忆力减退等现象，长期以往，大脑的连续缺氧势必影响到孩子的 思考能力、认知能力和精神运动能力，导致孩子学习成绩下降，影响孩子智力发育，让孩子输在人生的起跑线上……</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 更为严重的是，鼻窦炎长期不愈还会进一步影响青少年的身体发育，给以后的健康埋下隐患。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 鼻窦炎引起长期的鼻塞、张口呼吸等也会影响面部和胸部的发育，时间长了会出现痴呆样面容，包括上门齿前凸，下颌后退、鼻梁下陷、鼻翼萎缩、嘴唇变厚等“鼻炎面容”，阻碍孩子的身体健康发育。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 此外，鼻窦炎不及时治疗还容易并发中耳炎、鼻窦炎性支气管炎、上颌骨髓炎、眼眶蜂窝组织炎、脑膜炎、视神经炎等重症并发症，危害青少年的身心健康发展。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 因此，专家呼吁：身体健康是才是革命的本钱。所以，为了让广大青少年学生有一个健康的体魄应对学习压力，家长应该帮助孩子提高爱鼻护鼻意识，养成良好的生活习惯，别让自己输在了起跑线上。</span>
</p>
