<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>四伤员被送回沪治疗 医院开通绿色通道</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">东方网4月24日消息：据《新闻晨报》报道，昨日下午5点50分左右，车牌为“沪FR6177”的救护车驶入上海市第六人民医院，在“4·22”上海旅游大巴重大交通事故中受重伤的60岁王来娣，被第一个送到上海治疗。经初步诊断，王来娣颈椎骨折、脊椎损伤，伤势较重。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随后，每隔一个小时，上海市第六人民医院的绿色通道就开启一次:19时、20时10分、22时10分，此次事故的另外三位伤者相继被送到医院救治。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首名回沪伤员神志清楚</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“回上海真好……我就放心了。”躺在上海市第六人民医院住院部六楼的重症监护室里，王来娣全身被绷带包裹着，但她的神志很清楚，“救护车驶入上海的高速路，心里就松了口气”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据介绍，昨日下午3点50分，由上海派出的救护车从常熟出发。2个小时后，救护车终于抵达上海。据救护车上的工作人员介绍，考虑到伤者伤势较重，特别是颈椎、脊椎受伤，一路上不能受到大的颠簸，因此救护车开得很慢，“所幸，一路上没有任何闪失”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">回沪救治经严格评估</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在第一辆救护车离开常熟一个小时后，经评估，51岁的丁丽娟被抬上救护车并启程前往上海。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨晚7点，丁丽娟被安全运至上海市第六人民医院。经初步诊断，丁丽娟腰椎骨折，属于中重度伤势，可能需要进行手术。20时10分，51岁的潘女士被第三个送到上海市第六人民医院。据介绍，她右肱骨骨折、伴有腰椎间盘突出，伤势相对较轻。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">就在大家以为当天可能只有3名伤者被送回上海时，上海市第六人民医院副院长谭申生再次接到常熟的电话，第四位病人周开清22时10分将送抵医院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">谭申生透露，昨日一早，上海市第六人民医院紧急成立了大巴事故医疗抢救小组，由骨科主任、重症监护室主任、护理部主任等最顶尖的专家组成。医院同时开启了抢救绿色通道，“每一位被转回上海救治的病人都是经过专家组反复评估后决定的”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另据赶赴常熟参加救援工作的第六人民医院骨科主治医师周祖彬介绍，抢救中发现，伤员大多数为多发性骨折。</span>
</p>
