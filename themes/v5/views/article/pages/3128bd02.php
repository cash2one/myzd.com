			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>重庆新桥医院完成国内首例复杂脑肿瘤切除术</strong></span>
	</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">新华网重庆频道7月28日电　　在患者口中开一扇“门”，从而切除大脑深部的肿瘤。24日，第三军医大学新桥医院杨辉教授、任先军教授和谭颖徽教授等3位“海归”博士经过17个小时的协同作战，顺利完成了国内首例经口腔上颌骨切除颅底巨大恶性肿瘤的手术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 昨天，记者在新桥医院神经外科重症监护</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;室看见了手术后的曾先生。目前他呼吸、心跳、血压等都比较正常。曾先生今年45岁，年初出现头颈部疼痛，到5月份时，疼痛加重，并开始出现说话口齿不清，往往一句话要重复数次旁人才能听得明白，6月份他到新桥医院神经外科求治。经全方位的头部检查，医生确诊其脑干前方长了一“桔子”大小的肿瘤，并且已侵犯到第1、2节颈椎，需要及时切除，否则会威胁生命。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据该院神经外科主任、留美归国博士杨辉教授介绍，根据患者病情，如采用传统的手术方法，施行开颅手术，无法切除病变部位。在收治曾先生后，他们联合该院骨科及口腔颌面外科经过周密的术前讨论，三个科室的3位归国留学博士决定采用目前国际领先的手术方法，先将口腔的上颌骨切开，暴露“藏”在大脑深部的“桔子”，再完全摘除。经过17个小时的手术，杨辉教授告诉记者手术取得圆满成功。&nbsp;</span><br />
