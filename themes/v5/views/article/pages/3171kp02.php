<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;现代社会电脑已经成了工作、生活不可或缺的工具，为人们带来了极大的方便。但是随着电脑的普及，使用电脑的时间越来越长，尤其是坐办公室的工作人员、学生等，长时间低头、保持一个姿势，对颈椎的损伤很大，一定程度上加速了颈椎退变这一自然过程。很多人年纪轻轻就出现了颈部酸痛等不适，甚至出现了上肢疼痛、手麻等颈椎病症状。</span>
