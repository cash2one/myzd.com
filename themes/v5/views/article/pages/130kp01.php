		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>新技术使面肌痉挛和三叉神经痛的治疗更好更安全</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">事实上面肌痉挛、三叉神经痛、舌咽神经痛都属于血管压迫神经导致的神经功能障碍，治疗上将压迫神经的血管远离神经是最有效的方法，也就是说微血管减压手术是迄今为止首选的治疗方案，也是唯一一种有望彻底治愈疾病的微创技术。但是，并非所有的患者都能够获得预期的效果，也不是所有手术都绝对安全，因此不断应用的新技术能够弥补这一不足，使手术疗效更高，手术更安全。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>究竟有哪些新技术呢?</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1、病因血管的识别与定位技术。过去手术无法知道究竟哪一根血管是真正的病因，因此手术比较盲目。但是，应用现在的电生理学监测技术就可以解决这一难题。主要技术包括：AMR监测，只有将责任血管分离移位后才能出现AMR消失，否则AMR会持续存在；ZLR监测，监测探针只有放置在病因血管上才会出现，而放置在其他非病因血管上时不会出现；EMG监测，可以帮助手术医生判断真正的病变部位，间接判断病因血管的位置，对于位于外侧端的病因血管识别有很大的帮助。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2、手术是否彻底的监测技术。减压手术是否彻底直接决定了手术的效果，过去主要依赖手术医生的经验进行判断，但不够准确。目前主张依据科学准确的方法进行手术进程评估。包括区域证据、血管证据和电生理学证据。区域证据是指手术必须探查所有可能存在病因血管的部位，不能遗漏；血管证据是指必须对所有与颅神经接触的血管进行分离和移位，不能有遗漏；电生理学证据是指手术只有在AMR和ZLR都消失后才能结束手术，否则可能手术后无效。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3、减少术中血管意外损伤的技术。比如经小脑裂入路手术技术，可以明显改变手术的暴露，扩大手术视野，减少对小脑的牵拉，能够从多角度进行手术操作，减少了对岩静脉的直接操作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4、增加手术安全性的技术。包括BAEP监测技术、BTEP监测技术、SEP监测技术、无牵拉显露技术、温水冲洗技术、围手术期应用激素和扩血管药物技术、保暖技术等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">以上这些技术目前在国内还没有广泛应用，甚至还完全没有应用，我们期待更多关注技术的发展和进步，并在临床实践中创造条件应用，最大限度提高手术的效果和手术的安全性，造福于广大患者。</span><br />
