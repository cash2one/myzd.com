			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>心脏瓣膜病的五种症状</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;心脏瓣膜病的症状有以下五种，比如瓣膜病的病人主要是无缘无故出现乏力，本来平常能干一定强度的活儿，现在可能胜任不了，容易累等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 心脏瓣膜病的症状</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 一：瓣膜病的病人主要是无缘无故出现乏力，本来平常能干一定强度的活儿，现在可能胜任不了，容易累。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 二：气短，一干活儿就心慌，上不来气;也有的人出现腿肿。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 三：食欲不振，也就是说一段时间吃饭不好，胃肠道瘀血可能消化不好，出现肚子胀。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 　四：二尖瓣膜听诊区可闻舒张期隆隆样杂音或收缩吹风样杂音，主动脉听诊区可闻收缩期或舒张期样杂音。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 　五：肝大、肝颈静脉回流征阳性、腹水等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 常见病变分类</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 1、二尖瓣狭窄：如果瓣叶活动良好，仅为交界部粘连或轻度瓣下损坏，可争取行闭式扩张术或直视成形术。如果瓣膜钙化或漏斗样改变，则需要实行瓣膜替换手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 2、二尖瓣关闭不全：二尖瓣瓣环扩大或交界部局限的瓣叶卷曲者，可以争取实施直视成形手术。瓣叶穿孔、腱索断裂等、若成形手术难以完全矫正或成形手术失败，宜实施二尖瓣替换手术。二尖瓣狭窄合并二尖瓣关闭不全，大多数需要换瓣。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 3、三尖瓣损坏：通常三尖瓣不做换瓣手术。只有病变严重时才实施瓣膜替换手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 4、主动脉瓣狭窄：先天性主动脉瓣狭窄常可在青少年时期实施直视切开手术，中老年主动脉瓣狭窄多为先天性主动脉瓣二瓣化畸形的基础上钙化所致。需要实施主动脉瓣替换手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp; 5、主动脉瓣关闭不全：主动脉瓣关闭不全可由瓣环扩大、瓣叶撕裂穿孔、卷曲或脱垂等引起。通常应实施瓣膜替换手术。只有主动脉瓣轻度脱垂才可能做成形手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;　6、肺动脉瓣病变：多为先天性畸形，很少需要换瓣，常需实施带瓣管道右心室-肺动脉转流术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人造心脏瓣膜替换手术的相对禁忌症：风湿活动未被控制或控制不足3个月;心力衰竭合并心肌缺血损坏者如主动脉瓣狭窄的晚期病人。如心功能有所改善，仍争取手术。肝、肾功能或全身情况太差而不能经受手术的患者。细菌性心内膜炎病人已出现败血症并多处感染者不宜手术。</span><br />
