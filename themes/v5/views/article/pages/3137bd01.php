<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 东方网通讯员蒲欣9月15日报道，“创伤骨科学术研讨会”日前在浦南医院举行，共襄上海“创伤骨科”的临床进展与前景展望。</span>
