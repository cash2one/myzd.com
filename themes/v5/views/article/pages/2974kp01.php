			<div style="text-align:center;">
	<strong>心脏病患者门诊就医注意事项</strong>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者来到安贞医院心外科挂号就诊，有的人一两天就检查完毕、满意而归；但也有的患者花了一周还没做完检查。除了病情需要以外，其实、就我观察，绝大多数是由于患者准备不充分。原因大多如下：</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、忘带或者认为原先的检查不重要。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这种情况是最多见的。很多患者认为当地的检查不一定准确，因此希望来安贞医院进一步检查。但要知道，医生判断病情是需要连续性的，要了解您的病情变化，只能通过您当地的各项检查作参考。譬如一名中度主动脉瓣关闭不全的患者，若超声心动提示近5年左房、左室变化不大，完全可以等等再做手术。近几年的超声心动结果让医生看一眼，就大致能推断出是否有必要即刻手术或者择期手术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">冠心病患者的心电图尤其重要。门诊上我不止一次大光其火、却又无可奈何，就算是一些患者来京前已经与你联系过，你反复告知要带好所有检查，是“所有的”，他（她）们依然笑呵呵地对你说：没事，再做一张不就完了吗？但是患者犯病时才会有心电图和肌钙蛋白变化，现在是做不出来的啊！就算再花钱和花时间预约检查，医疗上的费用你有思想准备，但两三个人吃饭、住宿的钱有准备吗？与其这时求医生能不能帮忙提前预约，不如带齐资料。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、不重视影像学资料。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多患者只带纸质报告，认为片子太大，带起来不方便。普及一下，专科医生看片子时基本不看放射科医生的纸质报告，看片子得到的信息会远远多于报告所出示的内容。你认为不就是大夫花5秒钟扫了一眼片子吗？但这一眼，我们可是凝聚了几十年的临床功底的。不信的话，自己找副扑克牌，花二、三十年，每天找两张扑克牌的不同，结果你也会让别人大吃一惊。因此，没有片子的你，十有八九得再重新预约拍一张。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、复查时告知医生你的所有资料电脑里都有。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">你试试看，那是多个不同的界面，需来回转换。面对一堆门诊患者，可能马上要上的手术和病房里着急见医生的患者，听完这话，踹你的心都有。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那应该如何做呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、将所有的看病资料装到 片子口袋里，并按时间顺序排列。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、将医保卡也放到口袋里，这点尤其重要。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、若您不想带太多东西，可以将所有资料用IPAD或智能手机按顺序拍下来，门诊时出示给医生。但有时效果会差一些。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、若是急诊就医，赶紧让家人将原先的看病资料拍照并用微信 发送到你的手机里。</span><br />
