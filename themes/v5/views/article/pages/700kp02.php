			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>血尿怎么办？警惕泌尿系肿瘤的危害！</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">惨痛的教训：老王今年75岁了，3个月前小便时发现尿液呈现鲜红色，从小便开始到结束都是一样的鲜红，而且排尿时没有疼痛感，也没有其他不适的症状。然而尿了2次后血尿又很快消失。由于知道自己患有前列腺增生症，得知隔壁老张前列腺增生也尿过血，所以就没在意。然而1周前王大爷再次出现之前相类似的血尿症状时，在家人的一再劝说下来到苏大附一院泌尿外科就诊，经过相关检查后确诊为膀胱癌，且肿瘤长得相当大，侵犯到膀胱肌肉里，最后只能把膀胱切除，生活质量急剧下降。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这样的案例在临床上并不少见，由于患者或是家属的“大意”，时常有病人因此而延误病情，造成严重的后果。在泌尿外科的临床工作中发现，由于社会变迁、生还环境的变化，膀胱癌、肾癌等泌尿系肿瘤发病率呈升高趋势，且发病年龄有年轻化趋势。在众多泌尿系肿瘤患者中，除了体检发现的病灶，临床上膀胱癌、肾盂输尿管癌、肾癌等患者最常见的首发症状均为血尿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">何为血尿：血尿顾名思义，即尿中带血，呈鲜红色。在普通老百姓眼中的血尿，主要是指肉眼血尿，即眼睛能直接分辨出的血尿。然而，在医生的眼中，血尿还包括镜下血尿，即显微镜底下能看到血红细胞而肉眼观察尿色正常的血尿。通常，只有当每升尿液中有1ml血液时，尿色才能显现出肉眼可辨的红色。老百姓除了体检时查了小便常规能发现的镜下血尿，大部分患者的就诊时都是无明显诱因下的肉眼血尿。 &nbsp; &nbsp; &nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血尿的常见原因：事实上，引起血尿的原因有很多，大部分患者血尿的原因也并非肿瘤，所以突发血尿并不必给自己造成特别大的心理压力，血尿的原因大致有：炎症（急慢性肾小球肾炎、急慢性肾盂肾炎、急性膀胱炎、尿道炎、泌尿系统结核、泌尿系统霉菌感染等。）结石（肾盂、输尿管、膀胱、尿道，任何部位结石，当结石移动时划破尿路上皮，既容易引起血尿亦容易继发感染。）肿瘤（泌尿系统任何部位的恶性肿瘤或邻近器官的恶性肿瘤侵及泌尿道时均可引起血尿。）外伤（是指暴力伤及泌尿系统。）（5）先天畸形及全身性疾病等。 &nbsp; &nbsp; &nbsp;&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">导致血尿的肿瘤及其诊治：以上众多疾病中，后果最严重也容易漏诊的往往是泌尿系肿瘤，而且没有其他症状的血尿，往往来源于一些早期的肿瘤病灶，早期发现和治疗可以使患者省去大量的治疗费用、显著提高生活质量，因此及时和正规的诊治显得尤为重要。如果自己或者家人突然发现血尿，尤其是中老年群体发生的无痛性全程肉眼血尿，应当及早到正规医院就诊，泌尿外科的专业医生询问病史后，通过B超、CT、核磁共振以及泌尿道造影等影像学检查往往能减少漏诊的机会，而尿找脱落细胞（即在尿液中通过显微镜寻找异常细胞的方法）、FISH和NMP（大型医院开展的早期膀胱癌、尿路上皮癌的早期诊断指标）等实验室检查指标也对肿瘤有较好的提示作用。有时通过这些检查，会发现一些影像学可疑的膀胱、输尿管、肾盂病灶，通过膀胱镜、输尿管镜、输尿管软镜等先进的腔内镜技术不仅能够直观的观察到可疑部位，还能对一些部位进行活组织检查，在显微镜下观察组织结构，从而达到术前确诊的目的，进一步指导治疗方案。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">泌尿外科医生不仅在诊断上可运用的武器众多，在治疗上也是拥有十八般武艺。针对不同部位、不同发展程度的肿瘤，医生可采取各种先进的技术采取处理，微创技术在泌尿外科领域已经发展的相当成熟，经尿道膀胱电切镜、输尿管镜、输尿管软镜、腹腔镜、3D腹腔镜等在苏大附一院泌尿外科均已开展多年，给广大患者带来了福音，术后效果良好。而更为先进的达芬奇机器人也是即将在泌尿外科大展宏图的技术之一。与其他系统相比，如肺癌、胰腺癌、肝癌等，泌尿系统肿瘤预后相对较好，且随着医疗和生活水平提高，大多数患者发现时均为早中期肿瘤，具有手术指证与手术机会，通过以上微创技术切除病灶，不仅创伤小、恢复快，术后效果也极佳，很多泌尿外科肿瘤患者术后多年仍正常生活，预后良好。即使是晚期肿瘤，如晚期肾癌等，不同于其他肿瘤，切除病灶仍具有延缓病情进展、延长生存期的预期效果。总而言之，发现症状时及早就诊，诊断明确后及早治疗，是对自己和家人健康最大的负责，重视好个人的健康状况，生活才会更加明媚。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下面，简单介绍两种最容易导致血尿的两种常见泌尿系肿瘤：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血尿的第一大元凶——膀胱肿瘤：膀胱癌导致的血尿通常为排尿过程中全程红色的血尿，具有如下特点：1、无痛：患者无疼痛及其他不适症状。但若癌肿坏死、溃疡和合并感染时，可出现尿频、尿急、尿痛等膀胱刺激症状。2、间歇性：即血尿间歇出现，两次血尿可间隔数天或数月，甚至半年，容易给人造成血尿没有了的错觉，未能及时检查。若能抓住无痛血尿这一信号，及时检查，就能做到早期发现、早期治疗，取得较好的疗效。若像有些病人一样，可能失去治疗的最佳时机。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除了B超、CT等常规影像学检查，泌尿外科有着一样独到的“武器”——膀胱镜。膀胱镜检查操作方便快捷，就像众所周知的胃镜检查，既能够在门诊局麻下进行，惧怕疼痛的患者也能够住院全麻下行无痛膀胱镜。有了这样武器，医生能够在直视下观察膀胱肿瘤的大小、位置及生长的范围等，还能用特制的“钳子”夹取可疑组织作活组织检查，从而确诊。总之，膀胱镜是诊断膀胱肿瘤的最佳手段，能够发现B超、CT等影像学检查无法发现的病灶。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早期膀胱癌能够通过微创手段切除肿瘤组织，取得良好的治疗效果，从而避免膀胱切除，大大提高生活质量。血尿患者千万不能够因为惧怕心理逃避膀胱镜检查，导致病情延误。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同时需要指出的是，除了遗传、化学污染等不可控因素，今年来的研究已发现吸烟与膀胱肿瘤有明显关系，吸烟者比不吸烟者膀胱癌发病率高4倍。膀胱癌的男性发病率也明显高于女性，因此，烟龄较长的男性应尤其警惕膀胱肿瘤，为了家人和自己的健康，尽早戒烟，定期体检。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰痛血尿需警惕——肾脏肿瘤</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肾脏肿瘤的表现多种多样，其中最常见的是血尿和腰痛。在以往的医学书籍中，肾癌的经典临床症状即血尿、腰痛、腹部肿块，也称“肾癌三联征”。 肾脏在人体内的位置很深，周围比邻腹部的其它脏器，所以早期的肾肿瘤不易出现症状而被忽视。近年来，随着医学技术的发展以及老百姓健康意识的提高，越来越多的患者由于健康体检或因为其他系统的疾病去检查身体而偶然间发现了较小的和早期的肾脏肿瘤。早期的较小的肿瘤也并不会引起血尿和腰痛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肾脏发现肿瘤，一定就是癌吗？肾肿瘤分良性和恶性肿瘤两类。尽管多数资料显示肾脏恶性肿瘤占所有肾肿瘤的96-98%，而其中又以癌为主，但仍有部分病人患的是肾腺瘤、肾囊肿、肾血管瘤、肾错构瘤、肾脂肪瘤等良性肿瘤，有些良性肿瘤很难与恶性肿瘤相鉴别，需要有经验的肿瘤专科医生诊治。复旦大学附属肿瘤医院泌尿外科张海梁</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人的肾脏为什么会长肿瘤？到目前为止，肾癌的病因还不清楚，可能与很多因素有关，遗传因素可能是其中之一，调查发现肾癌有家族患病的倾向，如兄弟二人都患上肾癌，或一个家族中有三人甚至五人先后患上肾癌。某些遗传性疾病如结节性硬化症、多发性神经纤维瘤等可合并肾细胞癌；有视网膜血管瘤家族性肾癌，可为多病灶癌或囊内癌，VHL基因突变也是肾癌发病的原因之一。同时，吸烟者患病率明显高于不吸烟者。肾癌与工业致癌物的关系还没有肯定的答案，但男性吸烟者如果经常接触镉工业环境，发生肾癌者高于常人。另外，也有些报道说某些工业物质、黄曲霉素、激素、放射线、造影剂等可能导致肾癌，还有说咖啡可能增加女性发生肾癌的危险性，危险性与咖啡用量无关。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为什么肾癌越早发现越好？肾癌患者越早期治疗，其生存期获益越大。经统计，肾细胞癌的生存率为：总体5年生存率为60％，I期90~100%，II期65~75%，III期40~70%，IV期10~20%。近30年来肾细胞癌患者的生存率有所改善，这很大程度上归功于B超和CT扫描的普及，导致偶发性肾细胞癌增多，更多患者能得到早期发现和治疗。手术是治疗肾癌最有效、最根本的方式，最经典的方式是根治性肾切除术，在当前的大型医院，大部分肾脏肿瘤都可行腹腔镜微创手术治疗，创伤和恢复都较以前得到了显著的进步。更为可贵的是，对于早期肿瘤、肿瘤较小、肿瘤包膜完整、外生型的患者，可行腹腔镜下肾脏部分切除术，即只切除肿瘤，保留正常肾组织的手术。有人可能会反问：与肾脏全切手术相比，保留肾脏的手术是否会切除不彻底，从而影响生存率?事实上，从大规模的临床回顾性分析来看，两种手术方式在五年生存率上并无明显差异。正规医院的泌尿外科医生会根据患者的具体情况，选择最适合的手术方式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果一个长肿瘤的肾脏被切除了，会因为肾功能衰竭而威胁生命吗？答案是不会。因为一个人正常情况下有两个肾脏，每个肾脏有200万个肾单位。而维持一个成年人正常生活只需要100万个正常肾单位即可，也就是1/2个肾脏组织。因此，大多数情况下，只有一个正常肾脏还是可以正常生活，不会肾功能衰竭，但平时要注意多加注意和保护肾功能。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为什么说放疗和化疗对肾癌都没什么效果？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因为，肾细胞癌对化疗高度耐受，所以，一般不推荐采用化疗治疗晚期肾癌患者。吉西他滨与5-FU联用有一定的效果，但缓解率并不比采用白介素-2或干扰素治疗更好。放射治疗对肾癌的治疗尚无重要作用。多数学者不主张肾癌进行放射治疗，仅可作为手术前后的辅助治疗。肿瘤短期内增长快，毒性症状明显者，术前放疗可使肿瘤体积缩小，减少术中癌细胞扩散，有利于手术操作。对Ⅱ、Ⅲ期肾癌或病变已扩散到邻近器官，或肿瘤切除不彻底的病例，术后放疗可减轻局部复发。同时对于不能手术切除的晚期肾癌，放疗可减轻骨转移的局部疼痛、血尿和缓解毒性症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此文仅针对临床常见症状之血尿进行简单的讲解，如前文所述，大部分血尿患者也并非得了肿瘤，结石、泌尿道感染等其他良性疾病同样需要早期诊治，希望读者能够重视临床症状，管理好自己的健康。即使不幸确诊为肿瘤，也应当了解泌尿外科各种先进的治疗策略，积极配合治疗，只有医患之间的互相配合，才能使患者得到的治疗更加有效。同时注重定期体检，体检能够在症状出现之前发现健康问题，得到更为及时的处理，从而使病人及家属得到更有效的治疗，花费更少的成本。</span><br />
