		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>6年 他帮上千驼背患者挺直腰杆（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">手术台上的奇迹</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 术前不足1米3的患者，术后站起来竟有1米78；强直性脊柱炎14年，患者脊柱弯曲成96°，生活无法自理，三场大手术后已能下地走路……6年来，梁益建主刀的手术超过1000例，难度高、风险大，是许多医生不愿闯的“禁区”，而他成功挑战不可能，让众多“驼背”患者挺直了腰杆。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 手术台下的用心</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 为帮经济困难的患者减轻负担，梁益建不仅积极联系厂家，争取免费治疗器械，还找上身边的朋友，募集医疗费，连在茶馆喝茶时，都会倡议老板和其他客人捐款。“我身边有钱的朋友，都被我‘化缘’过了，有的要过几次了，小气点的都不愿意理我了，”梁益建笑道。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 成都商报记者 张漫</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 摄影记者 鲍泰良</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 实习生 朱天牧</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 90°、135°、180°、190°……梁益建不断挑战着新的难度。从2008年以来，一个个脊柱严重畸形的患者，从全国各地慕名而来，在梁益建的治疗下，找回了挺直腰杆的感觉。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 早上7点，成都市第三人民医院骨科，梁益建到达办公室后，打开电脑，再次将病人资料翻出来反复研究，准备即将进行的手术。尽管他是国内首屈一指的脊柱外科专家，主刀做过的脊柱畸形矫正手术超过1000例，但每场手术前，他都会做好万全准备，力求尽善尽美。梁益建知道，这关系到一条生命的尊严、一个家庭的幸福，容不得任何闪失。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 可以说，他开辟了一个“驼背神话”。有一次，某外省专家看到梁益建的一位患者手术前后的对比照片——从一个不可思议的扭曲角度到能够正常直立，竟以为是电脑合成。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 挑战不可能</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 不足1.3米的患者 术后1.78米</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 2008年，梁益建顺利帮脊柱后凸畸形患者伍才林实施了手术，术前不足1.3米的患者，站起来竟然是1.78米的高个子！伍才林的命运被改写，而梁益建也从患者及家人的欣喜若狂中，找到了自己作为医生的成就感和使命感。从这一年起，他更加致力于脊柱畸形矫正研究，6年以来，他主刀的手术超过1000例，其中一半以上的患者在治疗后，效果可以用脱胎换骨来形容。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在1个小时的采访时间内，梁益建接了8通电话，均是工作事宜。第一通电话，对方与他确定到重庆为一儿童患者做手术的时间，排在6月初；第二通电话，是患者家属咨询手术注意事项；期间，他还签署了两份病情说明。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 骨科走廊里新贴的一份感谢信，是一位江苏患者家属写的。这个28岁的患者，14年前诊断出患有强直性脊柱炎，家属带他走遍了北京、上海数家医院，均未能控制病情，后发展成双髋坏死、脊柱弯曲成96°，走路吃力，生活无法自理。今年2月，这家人来到成都找到了梁益建。经过三个月治疗、三场大手术，目前，患者已能下地走路，更重要的是，他的腰杆完全直立了起来。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 有人形容，这些“驼背”患者家庭像“追星”一样追逐着梁益建，他在哪里，他们就奔到哪里。他做的手术，难度系数高、风险太大，被许多医院医生称为“禁区”，因而不愿意去闯。但梁益建一次次用实际行动证明，禁区并非是绝对的，他挑战着一个又一个看似不可能成功的手术，让一个又一个的患者挺直了腰杆。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 茶馆里“募捐”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 他为患者争取数十万捐赠</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 梁益建没有因为被病人“追逐”而骄傲。他说，这些病患家庭被经年累月的病痛阴影追逐着，一个患者直不起腰杆，一个家庭的“脊梁”都沉甸甸地负重着。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “腰杆直立起来，你会发现，一个人的气质都变了，”梁益建说，经过手术后才发现，原来这些患者都是漂漂亮亮的男孩子、女孩子，或许因为直起腰杆后消化系统等功能的改善，患者的皮肤也会变好，五官也有了变化，这让他也觉得神奇。一个患者直立起腰杆，一个家庭的命运都产生了变化。他说，就是这股成就感支撑他不断进取，不断地勇闯禁区。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 不少患者家庭经济困难，手术和治疗费用对他们来说，也是沉重的负担。得知后，梁益建总是难以袖手旁观，积极地联系厂家，希望他们为贫困患者提供免费治疗器械，还动用自己的人脉关系，希望为病人募集医疗费。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “我身边有钱的朋友，都被我‘化缘’过了，有的要过几次了，小气点的都不愿意理我了，”梁益建笑道。他说，自己空闲时喜欢打太极、喝茶，在经常坐的茶馆里，都会翻出手机里的照片给老板和其他客人看，再讲讲病人家庭的困境，倡议他们捐款。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “善款不经过我的手，都是直接捐赠给指定的病人，”梁益建说，这些年来，他为经济困难的病人家庭募集的费用达到数十万。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 同一屋檐下</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 与儿子几天碰不到面</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 每周2至3台手术，每周抽一天去市三医院结对帮扶的大邑县中医医院，周末还要开会……梁益建随时在赶时间，连轴转。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> “儿子说，无论如何都不当医生，”谈及家庭，他说，自己也为不能多抽时间陪伴妻儿而内疚，但也无可奈何。有时他加夜班，儿子也要上晚自习，一个回家时，一个已经睡了，虽然在同一屋檐下，可有时两三天都碰不到面。儿子虽然没有抱怨，但也表示，自己无论如何不会当医生。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 自己为什么选择做医生？梁益建出生在煤矿工人家庭。小时候，一次煤矿事故让父亲全身多处骨折，病卧床上两年之后离世。由于得不到良好治疗，父亲承受了许多痛苦，目睹这一切的梁益建当时就发誓，长大之后一定要成为一名优秀的医生。1986年，他大学毕业后从医，后又经自己努力考取硕士研究生、博士研究生，到美国做访问学者，至今已经有28年从医史。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 一名好医生的标准是什么？梁益建说，专业医术和医疗道德，这二者缺一不可。精湛的医术是为病患解除痛苦的“硬件条件”，同时，还必须有为病人服务的医疗道德，才算得上是合格的医生。“读大学时，我给自己立下的目标就是当一名好医生，28年过去了，现在想想，应该算是做到了吧，”梁益建若有所思地说，旁人都说做医生辛苦，但想想那些成就感，他就觉得，值得坚持下去。</span><br />
