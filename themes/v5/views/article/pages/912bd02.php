		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我国每年新增3万聋儿 全国能植耳蜗医生不足百人</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 今年3月3日是第17个爱耳日。近日，北医三院成立了人工耳蜗植入患者俱乐部。40多位来自全国各地的听障儿童在北医三院接受了免费听力学检查和人工耳蜗调机服务。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据统计，我国7岁以下聋儿大约有80万，并且每年还在以3万人的速度递增。北医三院耳科主任马芙蓉说，新生儿耳聋与先天因素有关，比如基因缺陷等；也与后天因素相关，出生时低体重、高胆红素血症、出生时缺氧、出生时外伤等都有可能对听力造成不可逆的损害。轻中度的聋儿可以佩戴助听器，重度耳聋患儿则可通过植入人工耳蜗来感受外面的声音。“耳科手术特别精细，相当于在显微镜下绣花。”马芙蓉说，人的面神经从耳朵里面穿过，一旦手术不够细致，伤害了面神经，就会导致患者歪歪嘴，“本来耳聋就听不见，再歪歪嘴，患者该有多痛苦！”马芙蓉说，目前，我国能够称职地完成人工耳蜗植入手术的外科医生只有80人，而培养一个合格的耳科医生至少需要10到15年。北医三院去年成立了“芙蓉基金”，帮助贫困聋儿植入耳蜗，帮助耳科医生提高水平，“力争培养更多合格的耳外科医生，让更多的患儿可以在当地完成手术。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马芙蓉说，一个家庭有个聋儿，意味着整个家庭甚至整个家族的生活都要发生改变。幸运的是，现在的聋儿已经可以通过植入耳蜗重返有声世界，“一般来说，越早植入、效果越好。”</span>
</p>
