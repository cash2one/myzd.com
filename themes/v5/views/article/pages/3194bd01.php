		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>医界精英榜 张建国</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">张建国现任北京天坛医院功能神经外科主任，北京神经外科学院功能神外研究室主任，北京天坛医院癫痫中心副主任，北京天坛医院帕金森病中心主任，神经外科主任医师，研究生导师。担任《功能性与立体定向神经外科杂志》、《中国微侵袭神经外科杂志》和《中华神经医学杂志》编委、《中华神经外科杂志》特约编委。1998年获中国博士后奖励基金二等奖一项。1998年入选北京市科技新星计划，2000年承担国家计委课题“帕金森病的外科治疗”，2001年获王忠诚医学奖励基金二等奖，2002年获省级科技进步三等奖等。近年来参编和编译的论著有六部，发表文章二十余篇。主要从事功能神经外科疾病（癫痫、帕金森病和疼痛等）的基础和临床研究，率先在国内开展脑深部电刺激（DBS）治疗帕金森病。医疗纠偏：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病是好发于中老年人的常见病多发病，60岁以上人群患病率为千分之一，发病率高达1％，主要症状是肢体的震颤、僵直、运动迟缓和姿势平衡障碍，疾病早期一般从一侧肢体开始，最后发展到全身症状。目前治疗方法主要是药物和手术。早期药物治疗具有良好的效果，但随着疾病的进展，尽管增加药量，也达不到理想的治疗效果，而且还会出现一些副作用和并发症，如：“开关”现象、剂末现象、异动症等。手术治疗包括：毁损手术和脑深部电刺激手术（DBS）。以往多采用毁损手术，用射频的方法加热破坏大脑内的神经核团，缓解帕金森病症状，但该方法副作用和并发症较多，而且疾病还会“复发”，目前欧美发达国家已应用DBS手术取代了毁损手术。医界前沿：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> DBS是采用立体定向微创手术的方法，在头部安装一个定位头架，通过MRI扫描定位，确定大脑内的神经核团为靶点，头部钻一个小孔后，将直径为0.1厘米的电极准确无误地植入到靶点，然后连接一个刺激装置，打开开关就可以通过高频电刺激，来有效控制患者的症状。该方法具有可逆性、可调节性，副作用小，并发症少，提高病人的生存质量等优点。患者在局麻、完全清醒的状态下接受手术，术中患者可以随时与医生进行交流，医生也可以随时观察患者的反应和治疗效果。手术后，通过程序控制患者的症状可以立即消失。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 张建国博士目前已成功为200多位帕金森病患者解除病痛，无论数量和质量均居国内领先地位。此外，DBS手术对其他运动障碍性疾病也有良好的效果。朱丽丽12</span><br />
