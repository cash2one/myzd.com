	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>超声引导肝癌精准消融治疗系列讲座</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">第一节&nbsp;&nbsp; 肝癌的化学消融治疗</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（一）&nbsp;&nbsp;&nbsp;概述（历史和发展） </span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">化学消融是在肿瘤内注射无水酒精或50%～75%醋酸杀灭癌组织，前者远比后者应用广泛。1983年日本杉浦等人报告在超声引导下经皮肝癌穿刺注射无水乙醇，开拓了影像引导肝癌消融治疗的先河，它以侵袭性小、依从性好、简便易行、费用低廉等临床意义十分显著的优势，迅速在全球得到推广普及。经过20年的发展，肝癌的外科治疗(肝切除和肝移植)、区域治疗(经肝动脉治疗)和局部治疗(消融治疗)已成为当前肝癌治疗的三大主要手段。消融途径有经皮、经腹腔镜手术和经开腹手术三种途径，经皮消融最常用。优点是无需住院，局麻或附加静脉镇痛下即可完成操作，创伤微小，而且便于反复治疗。影像学引导是消融治疗过程中必不可少的条件和关键性技术之一。经皮消融的穿刺大多数在超声引导下完成，优点是实时显像、准确度良好、轻便灵活，但有时肿瘤可被肺气、肠气遮挡或病人体表组织较厚而显像不清，影响穿刺定位。 <br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（二）&nbsp;&nbsp;&nbsp;临床特点</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">肝炎病毒、黄曲霉毒素和饮用水污染是肝癌发生的三大相关因素。肝癌的大体病理分型可分为巨块型、结节型和弥漫型三种。其临床表现主要有：右上腹疼痛、胀满，上腹部包块，食欲减退，乏力和消瘦等。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">肝切除仍为肝癌治疗的首选方法。肝癌病人就诊时多数为进展期或合并肝硬化，仅10%～37%能够接受肝切除。术后2年内30%～50%的肿瘤复发，5年复发率可高达80%，复发性肝癌能获得再切除者仅占10%～20%。肝移植虽能同时治疗并存的终末期肝病，但供体来源困难及费用昂贵。由于外科治疗的应用受到限制，事实上绝大多数的病人需要接受区域或局部治疗，其中作为局部治疗的各种消融疗法在临床工作中可成为除手术刀之外的另一有力武器。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（三）&nbsp;&nbsp;&nbsp;介入治疗适应症、禁忌症；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">适应症：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1. 小肝癌：直径≤3cm、数目不超过3 个的小肝癌是消融治疗的最佳对象。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">今年，MP-PEIT的应用，可消融直径达5cm的肿瘤。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2. 治疗复发性肝癌：病人经过外科切除等手段治疗后，肝功能已有一定程度的损害，消融疗法有利于以后复发时能够为耐受再治疗而保存肝储备力，便于反复治疗。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3. 与手术治疗并用扩大切除的适应证：如主瘤位于一侧肝叶，对侧仅有数个小子灶，这种情况下便可以采用主瘤手术切除、子灶术中消融治疗。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4. 与TACE并用提高局部和远期疗效：TACE 对进展期肝癌的局部有效率平均为35 %左右，如加用消融治疗，能够进一步杀灭残癌，有力地遏制病情发展。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5. 作为肝移植的桥梁：由于供体来源不足，受体需要等候手术，及时对肿瘤采用消融治疗可使等待时间超过1～2年。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">禁忌证:病灶紧贴肝门部、胆囊、心脏、膈肌或胃肠等重要组织器官，或者病人凝血机能明显障碍、肝功能较差已达Child C级者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（四）&nbsp;&nbsp;&nbsp;术后处理和疗效判断</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">术后观察病人至少0.5h，无异常者可回家休息。如一次治疗穿刺针数较多者最好住院留观一夜。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">消融后1周～1个月判断肿瘤是否完全被灭活，有以下几种方法：(1) 超声检查。二维灰阶超声及彩色多普勒超声判断疗效不甚可靠，现今已发展成熟的实时超声造影大大改善了超声判断的准确性。超声造影下病灶无增强是肿瘤已灭活的确切证据。(2)动态CT扫描病灶无增强。(3)术前血清AFP升高者，治疗后经一段时间AFP值应恢复正常。(4)必要时可行肿瘤组织活检确认有无存活的癌细胞，注意在肿瘤边缘取材，以保证结果的真实性。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">影响局部疗效的两个主要因素是肿瘤的大小和肿瘤的位置。如肿瘤位于膈顶或靠近胃肠、胆囊等脏器，由于肺内气体或消化道气体的干扰，病灶难以达到最满意的显示，为避免损伤邻近的脏器而被迫放弃消融肿瘤边缘以外的组织，容易导致消融不全或局部复发。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（五）&nbsp;&nbsp;&nbsp;介入治疗并发症处理原则和预防</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">酒精消融并发症发生率约为1.7%～3.2%，主要并发症有以下几种：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">发热：是最常见的副反应，发生频率约44%-65%，原因可能与机体对创伤的反应和肿瘤组织坏死吸收有关。一般出现在治疗当天或治疗后第1天，持续2周。一般无需特殊处理，超过38℃者可口服解热镇痛类药物，多能缓解。如出现持续高热应注意细菌性炎症，及时行血化验和肝脏超声检查，确定有无肝脓肿等感染灶。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">腹内脏器组织损伤：在影像学引导下穿刺时，消融针的径路应避开腹腔内重要组织结构，必须在确认针尖的位置后才开始消融。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">出血：大多数原发性肝癌的病人合并肝硬化，可能存在凝血机能障碍的问题，消融治疗前应予以认真地评估和采取适当的纠正措施。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">肝功能损害：酒精用量过大或反复使用会加重原有的肝脏损害。对肝功能Child C级者在消融前使用药物消除腹水、纠正低蛋白血症等，能有效地提高治疗的安全性。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">胸腔积液：大部分无自觉症状，需要处理的病例约为2%，措施为胸腔穿刺抽液或闭式引流。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">感染：经超声引导脓肿穿刺抽液及抗感染治疗。严格的无菌操作有助于减少感染的并发症。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">针道种植：应特别重视肿瘤消融的完全性。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">疼痛：酒精刺激性较大，治疗时引起疼痛，有的术后仍需要镇痛处理。肿瘤靠近肝包膜者疼痛尤为显著，在制剂中加入少量局麻药可减轻症状。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">其他少见的并发症包括气胸或血胸、心律失常、肾衰和肌红蛋白尿等。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">掌握适当的治疗指征，遵从严格的无菌原则，准确的影像引导和规范的消融操作对于预防并发症有重要意义。治疗室内应常规配备急救药物以及氧气、吸引和除颤器等物品。治疗中良好的麻醉和镇痛有利于减少心血管合并症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（六）&nbsp;&nbsp;&nbsp;典型病例报告</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">患者男性，53岁，乙肝、肝硬化病史二十余年，体检超声发现肝S4占位病变，直径2.1cm。实验室检查AFP升高至890ug/L。行超声造影检查示病变动脉期呈均匀高增强，门脉期及延迟期消退为低增强(，考虑为肝细胞癌。增强CT检查亦提示肝细胞癌。病例经临床确诊后，在超声引导下行多极无水酒精注射消融术，共注入无水酒精25ml。术后一月复查超声造影示病灶内三期均为无增强，判断为消融完全(图7-1-5和图7-1-6)。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">患者男性，50岁，乙肝、肝硬化病史十五年，肝癌消融治疗后两年，超声发现肝S5占位病变，直径3.3cm。实验室检查AFP正常。行超声造影检查示病变动脉期呈均匀高增强，门脉期及延迟期消退为低增强，符合肝细胞癌。在超声引导下行多极无水酒精注射消融术，共注入无水酒精38ml。术后一月复查超声造影示病灶内三期均为无增强，判断为消融完全(图7-1-9)，增强CT动脉期无增强。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（七）&nbsp;&nbsp;&nbsp;结语/小结</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">酒精依靠药剂本身的毒性杀癌，效果比较确切。对≤3cm结节的完全消融率(肿瘤完全灭活率)为70%～80%。局部复发率15%～20%。远期疗效1、3、5、10年生存率分别达92%～97%、65%～74%、38%～48%和23%，与其他区域或局部治疗手段乃至手术切除相比差异无显著意义。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">以往化学消融一般只适用于直径不超过3cm的病灶，这是它的一大局限。其主要缺点是注射剂在瘤内浸润的程度难以监控，有些因素将影响液剂的完全弥散而降低灭瘤效果，如肿瘤组织的间质成分较多即质地较硬、瘤内的组织间隙液压比较高、存在纤维间隔、液剂随血管逸漏到瘤外等。近年来随着热消融技术的进步，酒精消融在国外已有被取代的趋势，适应证限于不宜热消融者或用于治疗血管癌栓。几年，由于MP-PEIT的应用，在一定程度上克服传统PEIT的缺点，可消融5cm大小的肿瘤。且因消融手段中，化学消融的创伤最小，操作最简便，费用也最低，因而临床依从性最好，仍然具有很大的实用价值和发展前景。</span>
</p>
