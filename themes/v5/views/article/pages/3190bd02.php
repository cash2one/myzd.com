	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>杨仕明:人工耳蜗——重度耳聋患者的福音</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我国已经成功自主研发人工耳蜗，让很多耳聋患者恢复了听力，那么人工耳蜗是怎么起作用的，它适用于所有耳聋患者吗？记者来到解放军总医院鼻咽喉头颈外科，请杨仕明主任为我们介绍人工耳蜗这个在医学界很出名但对大众来说比较陌生的技术。</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;<img src="http://static.mingyizhudao.com/147010401998469" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">杨仕明，男，主任医师，教授，中国人民解放军总医院耳鼻咽喉头颈外科主任，国家重点学科带头人，聋病教育部重点实验室主任，全军声损伤防护重点实验室主任，国家人工耳蜗项目技术指导专家，国家973项目首席科学家，入选科技北京百名领军人才。擅长听觉植入手术（人工耳蜗植入、振动声桥和BAHA植入），对聋病临床与基础研究（传导性聋、遗传性聋、先天性聋、药物性聋、噪声性聋、老年性聋等）具有很高造诣。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;杨仕明说，耳聋患者因为听不见，不仅比正常人少一个接受外界信息的渠道，而且语言能力会受到影响，如果没有有效的语言沟通，无法正常学习和工作，并且逐渐与社会隔绝，生活在孤寂的无声世界。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;后天性的耳聋患者因为经历了听力从有到无的反差，很容易失去生活的乐趣，往往痛苦不堪。先天性耳聋患者如果不早期接受治疗，智力发育和语言能力都会受到很大影响，最终失去语言能力成为聋哑人，无疑对家庭和社会都是沉重负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;按照耳聋性质和程度，可以有不同的干预治疗技术。大部分传导性聋通过中耳显微手术可以改善听力，但对于感音神经性聋患者中除了突发性聋之外一般药物治疗效果不好。传统助听器可以补偿一部分耳聋患者的听力问题，但不能解决所有耳聋问题。正常人听力阈值10到25分贝，轻度耳聋30分贝，到中度耳聋60分贝，70分贝重度耳聋，极重度聋90分贝。对于重度或极重度耳聋，传统助听器效果很差，特别需要人工耳蜗植入解决听力问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;人工耳蜗如何解决重度耳聋</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;感受声音的细胞称听毛细胞，声波刺激内耳听毛细胞纤毛运动，产生神经电活动传至中枢，人就能听到声音。如果听毛细胞纤毛或神经细胞因药物、老化、遗传等原因造成损坏，导致感音神经性耳聋。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;人工耳蜗就是把声信号转换为电信号，把复杂的语言信息进行编码，相当于把大型计算机变成微小芯片，通过电信号直接刺激神经产生听力，恢复患者的听觉。人工耳蜗具有划时代的意义，是现代科技革命性的技术进步。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;当然人工耳蜗还仅仅是神经植入假体，如果要根本性解决听力问题还需要研究听毛细胞和神经再生。杨仕明研究团队发现毛细胞在损伤两周之内还有机会修复，研究组从毛细胞再生、研发新药、基因诊断、治疗和干细胞移植等方面进行多方位系统研究，争取帮助更多的耳聋患者尽早恢复听力，让他们享受到现代科技给他们带来的福音。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;人工耳蜗普及的难点</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;杨仕明说，人工耳蜗新技术实现了聋哑人可以开口说话的神话，的确带来了耳聋治疗革命性的突破，而且99%的重度耳聋患者都是可以有可能获得改善听力的机会，但对耳聋患者是否需要人工耳蜗植入，何时进行人工耳蜗植入，如何保证手术安全等等，都需要高级耳科专家把握分寸和提高显微手术技术。人工耳蜗植入需要精准技术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;2006年残联数据表明，中国听力残疾人数2780万，位居所有残疾第一位，而且耳聋病人数量逐年增加，特别是新生儿听力障碍和老年人耳聋将是未来10年内我国必须面临的挑战，因为重度耳聋病人中可能潜在的人工耳蜗植入患者高达100万，急需植入者至少30万，但国内迄今为止植入者为3万多人，还有大约90%耳聋患者等待耳蜗植入。我国目前仅有69家医院共100多位医生被认定为人工耳蜗植入指定机构和具备手术医生资质。解放军总医院耳鼻咽喉头颈外科作为国家重点学科，是我国耳科领头羊，培养了大批中国耳科专家，也培养了大批中耳人工耳蜗植入专家，是国家耳科技术培训基地。301听觉植入中心拥有国内外最大之一的技术团队，目前已经成功完成人工耳蜗植入2000多例，手术技术和研究水平在国际上也非常有影响力。面对国内庞大的耳聋患者数量，我们感觉到紧迫感，我们需要培养更多的掌握这项精准技术的专科医生，的确任重道远。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;为什么人工耳蜗植入精准技术面临紧迫感？因为先天性耳聋的孩子每天都在成长，如果错过了1到3岁的植入黄金期，学说话就很慢了。同时每天又有新的耳聋婴儿降生，老龄化也会带来更多老年耳聋患者，单靠几个中心去做人工耳蜗是完全不够用的。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;解放军总医院耳鼻咽喉头颈外科在杨仕明教授带领下，逐步建立了我国人工耳蜗植入技术的标准化和流程化，并且在国内三甲医院广泛推广，未来将培训更多中国医生掌握人工耳蜗精准植入技术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;一直以来人工耳蜗被国外技术垄断，虽然上个世纪七八十年代国内开始研究人工耳蜗，但一直到2011年国产诺尔康人工耳蜗取得中国SFDA认证，并且在临床大量应用验证了安全性和稳定性，2012年获得欧盟认证，出口15个国家，成为第一个被国际认可的中国人工耳蜗产品。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;国产诺尔康人工耳蜗已经成功在国内外患者中植入2000多例，取得与国外人工耳蜗一样的植入效果，更为重要的是，中国诺尔康人工耳蜗最大的特点是相对于进口耳蜗对汉语的识别更好，因为汉语是声调语言，必须靠中国人自己解决这个问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;由于国外人工耳蜗的技术垄断导致产品的价格垄断，一直以来，进口人工耳蜗价格昂贵，植入费用高达15-30万，另外术后还需要长期的康复费用，一般家庭难以承受，所以没有被广泛接受。2010年之后国家人工耳蜗抢救项目为贫困聋儿解决了一部分难题，另外有社会慈善基金比如台湾王永庆资助的免费人工耳蜗项目、宋庆龄基金会以及中国少年儿童基金会组织爱心人士与企业提供的人工耳蜗资助项目。但是仍有众多耳聋病人还没有得到救助的机会。国产人工耳蜗大大降低了费用，一般在10万以内，将大大减低了医疗成本，让更多耳聋患者受益。当然，我们仍然大力呼吁尽快推进医保和新农合等多种政策性引导以及社会爱心人士参与慈善事业，最终解决耳聋救治问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;耳聋康复要成体系</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;杨仕明介绍说解放军总医院的听觉植入中心人工耳蜗在已经完成的2000多例手术成功率为99.5%，其中还包括相当部分的疑难复杂植入病例，手术一般历时一两个小时就可完成。杨仕明带领团队建立了标准流程，手术安全性和精准植入技术位于领先水平。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;除此之外，术后康复也是非常重要。正常孩子3岁之前都能说话了，但先天性的聋哑儿童人工耳蜗植入之后需要相当长的聆听习惯和漫长的语言学习过程，大部分孩子需要1-3年时间，言语效果与植入年龄和耳聋时间关系密切，一般是越早植入越好，国内一般是出生之后7个月以上即可手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;我们国家干预并没有像发达国家一样很成体系，现在国内还没有一个针对聋病从研发、生产、诊断、治疗到康复一体的国家基地，虽然我们离完善的体系还有一段距离，但建立完善、科学的体系是医疗的大方向。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;推动学科发展瞄准国际最前沿</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;解放军总医院刚建院时，五官科没有细分，在四代人长达半个多世纪的努力，这个学科快速成长、进步，从只有13张床位的耳鼻咽喉的小学科，发展成为拥有180张床位的大学科，成为首批国家重点学科。目前，耳鼻咽喉头颈外科已经建成解放军总医院这个医学航母的最大专科门诊，门诊量一直处于各学科之前列，年门诊量超过20万人次，特别是在耳科学更具优势，建立了亚洲首屈一指的耳聋患者DNA数据库、听力数据库和颞骨组织病理库。20世纪90年代中期解放军总医院耳鼻咽喉头颈外科在国内率先开展人工耳蜗植入技术，经过多年临床积累，找到了能根本性解决感应神经性耳聋的方法，让聋哑人能听见声音并开口说话不再是神话。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;2014年杨仕明教授入选“科技北京百名领军人才”，他正在带领团队从耳鼻咽喉科学国家重点学科为起点，瞄准国际发展最前沿，把学科建设成为覆盖 “预防、治疗、康复、研发、产业” 全链条五位一体聋病研究国家基地和国际科技合作研究基地。有理想才能有梦想，有梦想才有更远的未来。</span>
</p>
