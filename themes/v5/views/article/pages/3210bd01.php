<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>破解世界性技术难题 东方医院创新腹腔镜手术方式</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">东方网记者刘轶琳12月18日报道：腹腔镜微创手术技术已开展多年，以创伤小、手术时间短，术后恢复时间短、效果好及并发症少而闻名遐迩。但是腹腔镜微创手术目前有两个技术性难题尚未攻破，一直困扰着腹腔镜医学专家。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 日前，由同济大学附属东方医院胡海教授团队发明的一种免气腹单通道装置成功运用于微创单孔腹腔镜手术。此国际创新之举，被认为将克服需向腹腔灌注二氧化碳、增加部分患者风险，或采用物理“提拉”方式可能造成腹壁牵拉伤等腹腔镜技术“瓶颈”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 现任东方医院微创外科和胆石病科主任的胡海教授，自1992年以来，已主刀完成腹腔镜胆囊手术近6万例。近年来，胡海教授团队将单孔腹腔镜技术与免气腹技术相结合，并参阅了大量国内外相关技术革新资料，发明免气腹单通道手术装置——通过体外充气，使手术装置在体内膨胀成形，呈一个“中空”的三角形，为医生创建手术器械操作的足够空间。整个装置由支撑部、延伸部、操作部和进气部组成，已申请到国家专利技术证书。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 新装置本月4日第一次应用于临床，由胡海教授亲自主刀。受术的男性患者40多岁，为多发胆囊壁息肉。手术时，胡海教授首先在其脐部开一个2厘米的切口，将一个如格尺般的软长条状“气袋”（即免气腹单通道手术装置）从这个切口放置进入需操作的病灶部位，并与体外的“充气泵”连接。充气后，长条“气袋”在患者脐部和胆囊之间支撑出一个边长约10厘米的三角形有效空间。微创胆囊息肉切除术很快地“手到病除”，结束时，胡海慢慢抽离腹腔支架，缝合伤口，新型技术的微创手术大功告成。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术结束后，胡海教授介绍说：“在临床工作中，我一直在想一个问题，如何为腹腔镜微创医生建立一个“自由贸易区”，可以让他们有自由的手术操作空间，并在此基础上避免给病人带来更多的痛苦。在这样的初衷下，我们开始了这项新技术的研发。期间我们做了大量的临床试验，最终我们研发出的免气腹单通道微创术在整体上实现了既定的初衷。“</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 胡海教授说，免气腹单通道手术装置目前主要应用于单孔免气腹腹腔镜胆囊切除术和单孔免气腹腹腔镜保胆取石术，研发成果在整体上实现了小创口、单孔、无腹膜损伤，视野开阔，置放撤离方便。下一步的设想是继续扩大装置的生产线，待临床应用成熟后，努力把这项技术推到普外科，如阑尾炎、卵巢囊肿等的手术应用中，让更多需要患者受益。</span>
</p>
