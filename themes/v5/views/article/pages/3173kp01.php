		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>注重肝癌的合理治疗</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肝癌病人特别要更注重接受合理的治疗，原因在于肝癌往往伴有肝炎和肝硬化，肝功能好坏不一；肝癌发现的早晚及生长的部位对治疗的选择要求很高；近年来肝癌的治疗方法变得较多；肝癌疗效的提高更依赖于多种方法的综合治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;在临床上常常见到的不合理治疗很多，主要表现为两个极端即治疗不足和治疗过度，前者病人接受的治疗不足于有效控制肿瘤，留有后患，后者更为多见，治疗太多并不一定使肿瘤问题解决，反而导致肝功能进一步受损，而得不偿失。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;对病人而言，接受合理治疗的关键还是在于避免治治算了和治疗越多越好两种观点。达到合理治疗更多依赖于医务工作者的努力，在面对病情复杂的病人，在又有很多治疗方法和技术的现今，将最合理的治疗给每个病人个体并非轻而易举就能达到。将现今国内外的治疗规范或专家共识，与个人经验和技术条件有机结合才能达到。当然，医生的高度责任心也是关键。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;今后，肝癌的治疗将从现今的“规范化”发展为“个体化”，“一把钥匙开一把锁“，将更为合理有效。现在各种治疗研究无不围绕与此，目前有些方案已初露端倪，达到目标还需时间。当然不止是肝癌，现代医学所有研究几乎都以此为追求。</span>
</p>
