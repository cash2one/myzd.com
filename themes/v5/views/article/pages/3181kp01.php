			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肱骨外科颈骨折的分型</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根据Neer 的分类法基本能反应临床情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 第一型 单一外科颈骨折，或合并有大结节骨折，移位小于1cm ，成角小于45度骨折稳定，又称为“一部分骨折”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 第二型 骨折位于解剖颈，移位大于1cm ，或成角畸形大于45度，肱骨头与肱骨端成为分离的两部分，又称为“二部分骨折”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 第三型 在第二型的基础合并有大结节或小结节骨折，又称为“三部分骨折”。如果合并大结节和小结节同时骨折，又称为“四部分骨折”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 第四型 在第一型的基础上，合并大结节撕脱骨折，伴有明显移位或大结节的面骨折，常伴有肩袖损伤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 第五型 有小结节骨折并有移位。 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 第六型 肱骨上端骨折合并肱盂关节脱位。</span>
