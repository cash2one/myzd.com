<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>闫长祥谈不孕不育的另类隐情:垂体瘤</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 5月28日上午8:30-9:30，北京三博脑科医院副院长闫长祥教授做客人民网，谈不孕不育的另类隐情——垂体瘤的科学诊治。欢迎广大网友关注，并向专家提问咨询。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp; &nbsp;【嘉宾简介】</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">北京三博脑科医院副院长闫长祥&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 闫长祥，北京三博脑科医院副院长，神经外科五、六病区主任，主任医师、教授、硕士研究生导师、医学博士。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专业特长：各种颅内肿瘤（垂体瘤、神经鞘瘤、颅咽管瘤、胶质瘤、脑膜瘤）和动脉瘤等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1989年7月毕业于山东潍坊医学院临床医学专业，获医学学士学位，1997年先后师从我国著名神经外科专家、首都医科大学神经外科学院三系主任于春江教授，中国工程院院士王忠诚教授，获首都医科大学医学硕士和博士学位。曾在北京天坛医院、北京市神经外科研究所、首都医科大学宣武医院、附属复兴医院等多家单位学习和工作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2003年先后担任华北煤炭医学院和首都医科大学硕士研究生导师。目前培养硕士研究生、协助培养博士研究生十余名。近几年来，承担多项国家、省、市级科研课题，获得北京市科技进步奖3项。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 闫长祥主要致力于颅内肿瘤和脑血管病的临床治疗和基础研究。目前共完成各类颅脑手术近5000例，其中各类颅内肿瘤手术近4000例，脑动脉瘤、脊髓疾病及各类颅脑外伤等手术1000余例。在垂体瘤的诊治方面积累了丰富经验；特别对垂体瘤导致的不孕不育进行了深入研究；完成听神经瘤手术300余例，肿瘤全切除率95%，面神经解剖保留率大于90%，面神经功能保留率86%。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在《中华神经外科杂志》、《中华神经医学杂志》、《中国临床康复》、《中国康复理论与实践》等国家级期刊杂志上发表学术论文30余篇，参编、参译专著5部。现任中国中西医结合学会围手术期专业委员会委员、并担任国家级核心期刊《中国组织工程研究与临床康复》编委、《医学研究杂志》审稿专家。</span><br />
