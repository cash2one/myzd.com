	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>曾志伟：“器官捐献 成就不平凡”之旅</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">014年3月18日晚上七点，由中山大学团委宣传部、中山大学附属第一医院、国际扶轮3450区、中国器官分配与共享计算机系统研究中心共同主办的“曾志伟《器官捐献，成就不平凡》之旅”讲座活动，在中山大学东校区隆重举行。著名影星、器官捐献大使曾志伟先生、中山大学附属第一医院器官移植科主任王长希教授、国际扶轮3450区总监王国林先生、中国器官分配与共享系统研究中心主任王海波教授作为特邀嘉宾参加了本次活动，此外，还有600多名高校学生参加了此次讲座。</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/1657/1.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中山大学附属第一医院器官移植科主任王长希教授</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">王长希：希望通过借助多方力量带动器官捐献</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中山大学附属第一医院器官移植在全国的地位是举足轻重的，根据中山一院器官移植科主任王长希教授介绍，我国器官移植的现状就是“器官紧缺，形势严峻”，所以提倡器官捐献是非常有必要和有价值的事情。但是，在倡导的过程中需要一位有影响力的形象代言人，谁是器官捐献的最佳代言人？王长希教授说，曾志伟先生的爱心和号召力应当仁不让成为器官捐献的代言人。在将来器官捐献的发展路上，需要通过“大使”的大力宣传，加上医护人员的努力以及广大媒体朋友的积极正确引导，让更多的人参与到器官捐献之中来，使更多的生命得到延续。</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/1657/2.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中国器官分配与共享系统研究中心主任 王海波</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">王海波：谁有决定器官去哪里的权力？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">很多人都会有这样的一个疑问：器官将会去哪里？是国家卫生和计划生育委员会？还是中国人体器官分配与共享系统？都不是。王海波教授介绍，决定捐献器官去向的是国家分配政策。怎样才能就更多的人？什么样的人能得到优先救治？评分标准有哪些？王海波教授介绍了MELD评分标准，这是一个国际通用的标准，而在同分的情况下，小孩能得到优先获得器官移植的机会。</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/1657/3.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">著名影星 &nbsp;器官移植捐献大使 曾志伟先生</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">曾志伟：“器官给谁”不用担心 分配机制很公平</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">作为演艺界的“大哥”，曾志伟先生在慈善公益事业方面的关注也是非常多，而且更给力的是他每件慈善事业都是亲力亲为，在了解了器官捐献的分配机制之后，他深深感到这个机制的公平所在。“没有人名，根据分数来分配器官，能公平、公正地帮助有需要的人”这是曾志伟先生的亲身感受，也是他做这项事业的动力和意义所在。由于在中国还有很多人都不清楚器官捐献是怎么回事，大学生正是有知识有文化的一群人，所以通过大学生去宣传，去让更多的人了解和参与进来，这是本次校园演讲的初衷。</span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/1657/4.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">来自香港的器官捐献专家分享经验</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“你们的意愿（捐献器官）应该告诉你的家人”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在现实生活中，不乏这样的一些例子：有些是器官捐献本人已经同意了捐献器官，但是家人却不同意，这样一来，“爱与伤害”就成为了一组矛盾，那么，应该如何平衡呢？来自香港的器官捐献专家为我们分享了她的宝贵经验：在决定捐献器官之前，要把你的意愿告知你的家人。</span>
</p>
