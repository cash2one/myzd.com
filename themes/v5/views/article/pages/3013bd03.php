		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>儿科医院纠治先天心脏畸形 鸽蛋大心脏成功修复</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3013/4.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医护人员精心护理术后的袖珍宝宝。（儿科医院供图）</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">复旦大学附属儿科医院昨天传出消息：该院心血管中心专家为体重仅为1.5千克的复杂先心病早产儿实施心内隧道术，袖珍宝宝的迷你心脏得到完好修复。这例复杂先心病一期根治手术的成功，标志医院在早产儿、极低体重儿危重先心病体外循环领域再上新台阶。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">术后的患儿小羽躺在监护室床上，小胸脯一上一下，像是在用力感受这美好的世界。小羽父亲36岁、母亲34岁,历经艰辛，尝试各种办法，才迎来一对试管婴儿双胞胎，小羽便是其中之一。双胞胎提前1个月来到人世，小羽体重仅1.5千克、身长不过30厘米，属于极低体重早产儿。胎龄过小，导致患儿组织器官发育很不成熟，出生后不久，孩子就住进儿科医院新生儿监护室，接受生命支持治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">治疗过程中，小羽病情日渐加重。心超检查证实患儿存在复杂危重先心病——右室双出口、肺动脉高压。专家解释，正常状态下，左右心室分别连接主动脉、肺动脉，可患儿主动脉及肺动脉均从右心室发出，左心室血流只能通过室间隔缺损到达右心室，然后部分注入主动脉，大量分流至肺动脉。肺部的严重充血伴淤血，导致患儿肺动脉高压，出现严重心肺功能衰竭。专家称这是一种极其危重复杂的心脏解剖畸形，患儿在新生儿期间就可能夭折。早期手术成为挽救孩子生命的唯一方法。小羽的监护医生第一时间为孩子使用持续性气道正压辅助通气，改善患儿呼吸衰竭症状；营养支持、心功能支持的药物，也为手术创造了条件。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">修复心脏畸形，需要在全身麻醉、体外循环、心脏停跳下才能进行。儿科医院心血管中心内外科专家们在仔细分析小羽病情后，一致认为患儿心脏过小，很可能需要暂停循环来完成心内隧道，同时修复室间隔缺损。可对如此低体重早产试管婴儿来说，手术风险非常大，死亡率较高，目前国内外尚无相关文献报道。这样的高难度手术，无疑为手术技术、体外循环、监护水准提出极限挑战。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在经过充分术前准备后，心血管中心主任贾兵教授主刀，为小羽实施心内隧道及心内畸形纠治手术。袖珍宝宝的胳膊只有成人的拇指粗细，所用的气管插管不过2.5毫米内径，心脏仅鸽蛋大小，整个手术视野才两指宽度。可就在如此迷你的操作空间内，医生成功修复袖珍宝宝的迷你心脏。贾兵表示，患儿实在太小，组织十分脆嫩，吻合要求非常高，难度大，不过手术相当成功，达到心脏畸形完全纠正的目的，预计远期小羽可以像其他孩子一样健康成长。</span>
</p>
