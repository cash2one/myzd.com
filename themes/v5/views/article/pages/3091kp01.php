			<div style="text-align:center;">
	<strong><span style="font-size:18px;">北京三博专家为失明幼儿抢回视力</span></strong>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1岁10个月的失明儿牛牛在首医大三博脑科医院成功接受颅咽管瘤手术后，视力恢复至1.0，又能看见五彩斑斓的世界了。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">半个月前妈妈指着广告牌让牛牛辨认，牛牛竟然说看不清，妈妈以为孩子淘气故意不认。有天晚上，牛牛要求妈妈开灯，说太黑。这才引起警觉，医院检查发现已失明，核磁共振检查显示脑部有一肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 家人焦急带着牛牛赶到中国治疗颅咽管瘤综合水平最高的首医大三博脑科医院。周忠清主任诊断牛牛的失明是因脑瘤压迫所致，而且半个月时间就失明了可见视力下降明显。紧急完善检查，周忠清主任为牛牛全切除了荔枝大的肿瘤，严重受到肿瘤推挤压迫的双侧视神经及视交叉得到充分减压，失明的视力获得恢复。三博脑科医院颅咽管瘤专业组曾被《美国神经外科杂志》评价为全球手术治疗颅咽管瘤例数最多、肿瘤全切率最高和手术效果最好。目前已治疗600余例，肿瘤全切除率95%以上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 周忠清主任介绍，像牛牛一样的患儿不少，因年龄小表述困难，导致病情不能及时发现，直至严重下降甚至近似失明才被家长注意。周忠清主任提醒家长需留意儿童视力问题。若孩子的视力在短时间内莫名不断下降，不要只考虑用眼过度、近视，需到眼科或神经外科查看眼底，如发现有“视神经萎缩”，应排查脑肿瘤。</span>
</p>
