			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肾脏肿瘤一定要切除肾脏吗？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 肾脏肿瘤是泌尿系统的常见疾病，早期进展缓慢，患者无任何临床不适，但肿瘤进展长大直至出现腹痛，包块及血尿即所谓的三主征时，病情己失去最佳的根治机会，随着人们生活水平的提高及健康体检，越来越的早期小肾癌被发现，小肾癌的病人一定要切掉肾脏进行治疗吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;上世纪40年代西方著名的外科学家Robson提出肾癌根治的几条原则，即包括肾周筋膜，肾周脂肪，肾上腺，肾脏及肿瘤一并切除，输尿管切骼血管水平，肾血管周围淋巴结清扫，术中首先处理肾动脉，勿挤压肿瘤预防远处转移，以上总称Robson原则。该方法疗效确切而在数十年内在肾肿瘤的治疗中占据主要地位。然而对于孤立肾患者，对侧肾功能不全，慢性肾炎患者，甚至高血压，糖尿病或双肾肿瘤患者患肾的切除则意味着术后即时或未来慢性肾功能不全甚至尿毒症的发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近年来的临床研究证明，多数早期肾脏肿瘤往往将门围肾脏组织挤压形成一个假性包膜，只要在包膜外切除肿瘤术后很少出现肿瘤复发及转移，而且能够保留大部分肾脏功能，保留肾脏再发肿瘤的机率仅为1/%，保肾手术患者庭长期生存率甚至高于根治手术的患者，主要原因是避免了远期尿毒症的发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来保留肾脏功能的手术发生了巨大的变化，勿容置疑，开放手术在复杂肾肿瘤的保肾中具有非常重要的地位，微创腹腔镜手术的普及使大部分病人避免开放手术的创伤，仅需腰部或腹部数个钥匙孔大小的洞就能完成手术，患者创伤小恢复快，疗效确切。达达芬奇机器人手术的使用使腹腔镜技术的巨大进步，达芬奇机器人手术系统其实就是更为先进的腹腔镜，同传统的腹腔镜相比有二大方面优势：一、图像为三维立体显示，放大15倍左右异常清晰显露体内的组织结构，利于精准操作。二、所用器械如剪刀，钳子等可以向各个方向活动，就如灵巧的小手伸体内，非常易于缝合。在熟练掌握常规腹腔镜的基础上，利用我院新购置的国际先进的达芬奇手术系统si在一年时间左右已经完成手术200余例，均为大手术。不出河南就享受到全世界最先进技术。目前收费为仅收取器械耗材费用，每例较传统腹腔镜多1.5到2万元左右，仅为北京广州等地费用一半。相信随着人们对医疗卫生服务需求的提高，机器人手术必将造福更多患者。</span><br />
