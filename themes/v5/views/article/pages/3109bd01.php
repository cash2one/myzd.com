		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>吃生肉，脑里长虫（健康警报）</strong></span><br />
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 20岁的江苏女大学生张某患癫痫半年，最后医生从她脑内取出了一条寄生虫。中国抗癫痫协会青年委员会副主任委员、解放军总医院第一附属医院（304医院）神经外科副主任梁树立解释说，这多是因为吃进了寄生虫虫卵或幼虫导致的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “人吃了没有烧熟的肉，喝了不干净的水，吃了带有虫卵的生蔬菜，或者有不洗手的习惯等，都有可能感染上虫卵。”梁树立说，虫卵只有几十微米大小，肉眼是看不到的，虫卵进入人体后进入血液系统，并可随血液循环到人体的任何部位。人的大脑结构异常敏感，虫卵一旦进入大脑，会慢慢发育成幼虫并逐渐长大，对周围脑组织造成压迫、破坏，并诱发头疼、浑身无力、肢体运动障碍等一系列症状，最严重的就是诱发癫痫、视物不清，甚至失明等。即便这些虫体最后被杀死了，也会在脑内形成纤维结节或钙化，同样会诱发癫痫等脑病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 他提醒，预防寄生虫脑病，关键是要注意饮食卫生，不食用半生不熟的肉、鱼、虾等；买回来的蔬菜、水果最好用盐水浸泡并用清水洗干净后再吃；不吃蛙类、蛇类以及田螺等野生动物；要养成饭前便后，外出回来洗手的好习惯；不要喝生水，尤其是饮用池塘水、河水的地区，水一定要烧开后再喝。</span>
</p>
