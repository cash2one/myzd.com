			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>膝关节置换术后感染，不手术行不行？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">众所周知，人工膝关节置换术后发生感染是一场灾难，然而对于患者来说，更关心的是，他们该如何渡过这场灾难？关节置换感染后，一定要手术治疗吗？能不能不手术？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">事实上，人工膝关节置换术后发生感染，首先要了解感染的细菌是什么。目前最常见的感染细菌是革兰式阳性球菌，如表皮葡萄球菌、金黄色葡萄球菌、链球菌等。其次，要了解目前已有的药物中，是否有对该细菌治疗有效的抗生素。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">至于要不要手术，则要医生判断感染的细菌是否独立且强大。如果通过细菌培养发现它并不强大，有很多抗生素能对付它。而且是非常及时的发现了患者的感染，如术后1个月内，这时可以考虑保守治疗。但一直靠抗生素治疗的方式并不可取，它只是在患者的身体状况或经济条件不允许手术的情况下，一种无奈的选择。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">要知道，手术才是目前治疗膝关节置换术后感染最好的方法。手术治疗膝关节置换术后感染的方式有很多，其中一种就是切开清创治疗。简单来说，就是打开原来的手术切口，像打扫房间一样，把关节内所有可能残留细菌角落都冲洗清除干净，同时更换关节假体中的聚乙烯衬垫，手术后再用抗生素治疗。这种治疗方式的优势就是能够将关节假体保住，不用更换关节假体。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但值得注意的是，仅切开清创冲洗这种治疗方式适合少数患者，这些患者需要满足以下三点，缺一不可：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.膝关节置换术后的感染发现及时，一般在术后4周内，即感染属于早期感染或者急性期感染。如果术后感染的症状和体征持续时间超过4周，清创冲洗治疗失败率很高；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.术前已经明确了感染细菌是什么，而且感染细菌并不十分强大；</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.有多种抗生素能够对付已经明确的感染菌。</span>
</p>
