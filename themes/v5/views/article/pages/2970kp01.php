		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>儿童骨折“可吸收钉”并非首选</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2970/1.png" title="" alt="demo.jpg"/></span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 儿童骨折治疗方法与成人有别，复位不当容易发生骨折并发症“肘内翻”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨骺线是儿童骨骺最薄弱和最易骨折的部位，骨折手术中使用“可吸收钉”，容易“打坏”骨骺线，影响生长发育</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3岁的小妍不慎摔倒后，肘部疼痛肿胀。爸爸张先生带小妍到某小医院就诊，诊断为肱骨髁上骨折，医生给她进行手法复位和打石膏后，就让她回家了。张先生不放心，第二天送女儿到大医院复诊，拍片发现骨折移位了。经过手术切开复位钢钉内固定手术，小妍的肘关节恢复正常。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">小李今年21岁。5岁时她因摔跤而跌伤肘关节，当时进行了简单的打石膏治疗，肘关节能够活动后，家人也就没再管她。但之后，小李的手肘发生了“肘内翻”，手臂垂下时，总是不听话地往内拐30度，提东西不顺手，生活极不方便。去年，小李接受了截骨矫形钢板固定手术。一年后，她取下钢板，过上了正常生活。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">专家指出，由于儿童处于成长发育期，在组织解剖、生理等方面均与成人有很大的区别。一些成人治疗骨折的方法并不适合儿童。若治疗不当，会造成骨骺线损伤，引起骨骼生长延迟、骨关节畸形、肢体生长障碍，在成年后或出现后遗症和并发症。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">文/记者莫冠婷 通讯员李冰洁</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">医学指导/广州军区广州总医院骨科医院副院长、创伤骨科主任章莹博士</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肘部髁上骨折</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">治疗不当易发生肘内翻</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">儿童上肢骨折多于下肢骨折，肘、手腕、前臂的骨折比较常见，其中，肘关节受伤最常见。章莹介绍，儿童骨折门诊中，约有30%属于肱骨髁上骨折，也就是肘部上方2~3厘米处发生骨折。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肘内翻是儿童肱骨髁上骨折最常见的并发症。章莹说，儿童骨骼处于生长发育阶段，一旦发生骨折，容易引起骨骺受伤。若未经复位或复位不良，会导致骨生长机能紊乱，诱发继发性畸形，“其中，肘内翻最常见；当然极少数也可能出现肘外翻。”什么是肘内翻？章莹解释，正常情况下，肘关节自然肘外翻的角度称为“提携角”，女性的提携角正常范围一般在10~20度；男性为5~10度。当提携角减小消失甚至成为负角，就称之为肘内翻。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">章莹介绍，儿童骨折的诊断，首先是进行物理检查，医生通过观察和触摸，判断骨折的存在，如异常活动、畸形以及咔咔作响的“骨擦音”，是骨折的特有体征。此外，X线检查不可缺少，可明确骨折类型、移位情况、是否存在原发病变。“但要注意，特别是打着石膏做检查的患者，X线检查容易把骨骺线误诊为骨折线。”章莹说，此时可以通过对两侧作比照进行判断，或者进一步做CT、MRI提高诊断水平。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">保守治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">手法复位+ 固定</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“保守治疗和手术治疗是儿童骨折的常见治疗方法。”章莹说，肘部髁上骨折、前臂内侧的尺桡骨骨折、小腿的胫腓骨骨折都可以使用手法复位。复位以后，还需要进行固定。中医一般用夹板固定，西医使用石膏固定。“但固定时要注意防止压迫，绑得太紧或导致骨筋膜间隙综合征，轻则造成缺血性挛缩，重则导致肢体缺血性坏死。”章莹提醒说，防紧也要防松，要避免消肿后，骨折由于石膏松脱而“跑位”，因此，要定期拍片进行复查。一般在固定后的3天是水肿的最高峰，受伤部位有可能出现颜色发紫、麻木、肿痛以及压迫加重的表现，应进行第一次复查。随着肿胀慢慢消退，建议一周后复查拍片。接下来，半个月拍一次片。此后则按月拍片跟踪。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在上述治疗不成功的情况下，可以考虑牵引治疗进行复位和固定。5岁以下儿童的股骨干骨折，可进行悬吊牵引治疗。“牵引治疗可以作为终极治疗，也可以为手术前缓解疼痛做准备。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">手术治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">最好使用钢针固定</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">章莹说，手术方式一般能达到解剖复位的良好效果，减少骨折后的并发症和后遗症的发生，因此，越来越得到大众的认可，但是要求比较高。“儿童骨折手术，一般使用钢针和钢板固定，不使用髓内钉固定。”他介绍，钢针比螺钉细、损伤小、占位少，对需要穿过骨骺的小块骨折，用钢针固定效果较好。发生肱骨髁上骨折、膝关节骨折等关节内骨折，一般用直径为1.5毫米左右粗的钢针对骨折进行固定。“如用钢板固定，手术中要注意尽量避免螺钉进入骨骺线。在必须用钢针穿过骨骺线时，要求尽量一次成功，避免反复穿过。如骨折块较大，可互成角度穿入两根针，加强固定。”儿童肱骨髁上骨折进行钢针固定手术，一般术后4~5周可将钢针取出。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">虽然近年来不少医院在儿童骨折手术中使用可吸收钉，认为可吸收钉具有无须二次手术取出的“优势”。但章莹认为，从专业角度而言，可吸收钉在儿童骨折的治疗中并不值得推广。他解释说，一般使用两根钢针就能对骨折进行固定，但如果使用可吸收钉，因其比较粗，直径一般为2.7~3.5毫米，容易对骨骺线造成伤害。而且，可吸收钉螺纹小，把持力不好，易造成固定失效。此外，使用可吸收钉的后果难以预料，因为可吸收钉的材料在人体内降解的过程中会产生酸性物质，有可能对儿童骨骼发育产生不良影响。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨骺线</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">儿童独具的生长功能结构</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据去年卫生部统计，跌倒和溺水、道路交通伤害（车祸）被列为我国儿童的“三大杀手”。广州军区广州总医院骨科医院副院长、创伤骨科主任章莹表示，儿童骨折与成人骨折不同。儿童的骨骼处于生长阶段，成骨细胞、破骨细胞丰富且活跃，骨折愈合速度快。一个月大的婴儿2个星期就能愈合，而成人至少需要100天。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨骺线是儿童骨骼的重要组织，与生长发育密切相关。章莹介绍，成人的皮质骨坚硬，呈板层排列。儿童的骨骼柔韧性和弹性好，但无明显的板层结构，硬度不够。儿童的长管骨两端存在骨骺线。骨骺线在X光片中呈现近似波浪的线，实质是一层软骨样组织，而且是用于生长的软骨，年龄越小骨骺线越宽越明显。一般14年后，长骨仅由骨干和骨端构成，骨骺线逐渐闭合消失，人也不再长高了。“因此，如果在发育过程中，骨骺线受到损伤，会影响骨骼发育。”章莹提到，除了生长功能，儿童骨骼还是造血器官之一。</span>
</p>
