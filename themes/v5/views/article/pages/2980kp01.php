			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:16px;line-height:2;"><strong>达芬奇”机器人是泌尿生殖系肿瘤外科治疗中的利器</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前全世界约有3500台达芬奇手术机器人系统在临床上应用，其中在我国大陆地区约有50台该手术机器人在使用。据统计2014年全球应用达芬奇机器人进行的手术超过了57万例， 2015年估计会超过64万例。达芬奇机器人主要用泌尿外科、胸外科、妇科、肝胆外科、心外科和胃肠外科手术。2006年至2015年12月中国大陆达芬奇机器人手术共22917例，其中9316例为泌尿外科的手术，而在泌尿外科手术中主要为前列腺癌根治术、肾癌保留肾单位手术和膀胱癌根治性膀胱切除术。可见达芬奇机器人在泌尿生殖系肿瘤外科治疗中具有重要地位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">广东省目前已有三台达芬奇机器人，分别安装在中山大学的第一附属医院、附属肿瘤医院和第二附属医院。中山大学附属肿瘤医院的达芬奇机器人于2016年1月4日装机并于当天由泌尿外科主任周芳坚教授成功实施了前列腺癌根治术，春节前成功实施了包括肾癌保留肾脏手术、前列腺癌根治术和胰腺癌根治术共20例，术中出血很少，不用输血，没有全部病例术后第一天即下床活动，均恢复良好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">达芬奇手术机器人系统是现代高科技技术的结晶，她突破了医生肉眼和手的局限，将微创外科手术精准化。她由三个部分构成：医生操作系统、手术台旁机械臂系统和视频处理系统。视频处理系统提供放大10-15倍的高清晰度三维立体视频，能超越肉眼的局限，使手术视野更清晰，医生操作更精准。机械臂系统有7个自由度，手术器械可以模拟人手腕的灵活操作，可在人手不能触及的狭小空间进行精细操作，超越人手局限，且能滤除人手在操作过程中的颤抖。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随著我国经济的发展必将引入更多达芬奇手术机器人手术系统，必将大大促进我国外科特别是肿瘤外科的进步，提高肿瘤外科治疗效果。</span><br />
