	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>识破复发转移性乳腺癌的“变脸”伎俩</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;乳腺癌患者最担心的莫过于疾病的复发和转移了，但复发和转移的确是一部分患者要面对的问题。复发和转移性乳腺癌治疗比较复杂，更需要综合治疗、精准治疗、维持治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
