<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 乳腺癌是一组疾病，里面可以分很多类，不同类型的乳腺癌其治疗策略是完全不同的。复发转移的部位、发病时间、患者年龄以及以前的治疗方案都是复发转移后治疗方案设计中需参考的方面。讲一个病人的例子，这是一个中年女性，手术已经5年了，这5年间按时吃药定期复查一直很好。在5年的时候，她总感觉腰背部疼痛，在复查后考虑是腰椎的骨转移，在常规复查中肝脏也发现了2个1.5cm左右的病灶，根据常规呢这是一个绝经后、受体阳性（所以她手术后一直服用内分泌治疗药物）、术后5年出现的骨转移且肝病灶很小无症状，根据指南可以考虑内分泌治疗。而内分泌治疗的疗效依赖于病灶表达雌孕激素受体，为预防腰椎骨折患者做了腰椎固定手术，手术取出的转移病灶雌孕激素受体表达并不高，这就给医生的内分泌治疗决策带来了困难，为了明确肝脏病灶特点我建议患者做了肝病灶的穿刺病理检查。我很庆幸我建议患者做了这个检查，也很庆幸患者非常信任我，接受了这个检查，检查结果显示这是一个受体阴性、Her2阳性的乳腺癌，这种类型对内分泌治疗并不敏感，而对针对Her2的靶向治疗和化疗较为敏感。这样我们选择了化疗联合曲妥珠单抗的治疗，经过6个周期的治疗，肝脏病灶几乎完全消失，初战告捷！接下来我们会进入一个相对长期的维持治疗阶段。</span>
</p>
