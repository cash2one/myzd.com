<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 常流产可考虑做宫颈环扎术</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 连续两次怀孕5个月6天都莫名其妙地流产，后来检查发现居然是宫颈松弛，当患者周女士准备第三次怀孕之前，她做了腹腔镜下宫颈环扎术。15年不孕的徐女士好不容易怀孕了，却也在5个月左右宫口开了近5公分，流产症兆明显，经腹腔镜下宫颈环扎术后脱离险境，并经后期保胎产下足月胎儿。近日，第七届南京国际微创技术新进展研讨会在南医大二附院召开，会上宫颈环扎术的现场播放演示吸引了所有妇产科专家的眼球。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 据了解，正常人的宫颈口呈Y字形，直至分娩前，宫颈口才打开。但是宫颈机能不全的患者，其宫颈口呈U形，就像一个口袋没有扎紧一样。随着胎儿越长越大，子宫兜不住，就会以流产告终了。而运用宫颈环扎术，通俗一点讲，就是把宫颈口像面粉袋口一样用绳子系起来，让宝宝在里面继续生长。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; “这种手术可以在孕前和孕中实施。手术时间不长，而且手术对治疗宫颈机能不全的患者成功率可达87%以上。”南京医科大学第二附属医院妇产科主任应小燕教授介绍，“过去只是对宫颈机能不全的病人进行环扎，通过实践发现，在一定的孕周以前，在没有明显宫缩的情况下出现了流产的征象（羊膜囊未破）也可行紧急环扎术，有可能使你得到一个健康的宝宝，否则肯定会流产。”</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 专家介绍说，临床上，连续3次或以上意外流产就被称为“习惯性流产”，而引起习惯性流产最常见的原因就是宫颈内口松弛。它是导致妊娠中期以后习惯性流产与早产的主要原因之一。由于子宫颈内口难以承受逐渐增大胎囊的重量，巨大的胎囊压力导致自然破水、流产。应教授认为，对于宫颈内口松弛的孕妇，或者在孕12至24周之间有过流产史的孕妇，可以尽快进行手术将宫颈口扎紧，这样可以保证子宫颈口闭合，延长孕周，防范流产或早产。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 烤瓷牙本身是一种很成熟先进的技术，它对牙齿缺损、牙列不齐、牙缝、四环素牙、氟牙症、畸形牙及各种不能充填的牙根都可进行修复。它的特点是生物相容性好，颜色可由自己选择且色泽稳定，对牙龈无刺激，耐磨损等。不过面对名目繁多的烤瓷牙材料，不少患者觉得难以选择。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 南京市口腔医院修复科副主任医师张洁介绍，根据所用材料不同，烤瓷牙可以分为不同类型，比如全瓷牙、贵金属烤瓷牙等。目前最被大众接受的是贵金属合金瓷牙。贵金属对口腔刺激更小、毒性更低、使用年限更长久，看起来更美观，适合于广大患者。镍铬合金的烤瓷牙价格比较便宜，但它和组织的相容性差，刺激性较大，约有3%的人因此而过敏。纯钛烤瓷曾被炒得火热，但是纯钛与瓷的结合并不理想，纯钛易氧化，加工困难，配套瓷粉少而不成熟。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp; 制作烤瓷牙需要磨除一些牙体组织，有些患者为了这个会有点害怕。其实医生磨去的只是凹凸不平、已经伤痕累累的牙釉质而不是牙本质，因此不会伤害牙齿，相反却能恢复坏牙的咀嚼功能、给牙齿穿上坚固的“防弹衣”，让它避免受到外界细菌的侵害。 口腔科医生提醒，要获得理想效果，选择正规的医院和医生是关键，另外，装烤瓷牙要注意牙齿颜色和皮肤颜色的协调、牙齿外形要与形体五官大小比例协调，这样才会有完美的效果。烤瓷牙需要保养和爱护，有了牙周病要及时治疗、防止牙龈萎缩。&nbsp;</span>
</p>
