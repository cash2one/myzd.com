<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">特发性脊柱侧弯是青少年的常见脊柱畸形，发病原因不明。由于脊柱畸形，影响美观，常常由家长或本人洗澡或游泳时发现。往往出现一侧的肩胛骨高耸，呈现“剃刀背”畸形，由于脊柱弯曲，身高比正常儿童矮小，有时一侧肩膀高于对侧，严重时躯干偏移，外观明显不平衡，对儿童的外观及心理健康产生严重影响。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 一般手术时机选择12～14岁比较理想，青春期发育了，女孩月经已来，此时手术效果为最好。如果等到成人期手术时，往往手术矫形效果没有青少年时期效果理想。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 手术矫形时一般选择侧弯角度大于40度，Bending位大于25度的结构性侧弯矫形，往往可以取得很好的矫形效果，手术风险较低。</span><br />
