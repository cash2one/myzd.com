<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脊柱肿瘤影响预后的因素&nbsp;</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脊柱肿瘤影响预后的因素：</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. 病变的部位：出现在脊柱不同部位的脊柱恶性肿瘤 , 于术切除的难易程度不同 .下颈段、下胸 段、胸腰段及腰段脊椎手术显露相对容易 , 手术切除相对容易进行,那么相应复发率低。而位于上颈段、上胸段 脊椎或慨骨区的肿瘤,手术显露相对困难,手术切除相对困哪,那么复发率较高 , 预后相对较差。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 移瘤侵及的范围：据脊柱恶性肿瘤导致的脊柱畸形的 MRI 矢状位分类,属I 、 型的脊柱肿瘤 , 病变尚局限在椎体内,手术切除相对容易 , 复发率相对较低。田型的脊柱恶性肿瘤,肿瘤病变已突破椎体界限,手术切除相对复杂,复发率相对较高。凹型的脊柱恶性肿瘤,椎体内骨髓和骨小梁完全为转移瘤所取代,肿瘤进一步发展,浸润破坏累及椎板,手术切除更加困难,复发率较高,那么预后较差。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. 病理分级：于脊柱原发肿瘤的恶性程度及生物学行为的不同,其预后也存在着很大区别。肿瘤恶性程度相对较低,则预后较好;肿瘤的恶性程度相对较高,预后则较差。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. 转移瘤灶的数目转移瘤的数量及有无合并其他器官转移,对预后影响较大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5. 于术治疗脊柱恶性肿瘤的手术治疗由于其部位深在、解剖结构复杂、手术难度大 , 肿瘤的切除 常存在一定的危险 , 所以很难进行根治性切除。 一般病段切除,清除肿瘤较为彻底,预后要比刮除植骨术为好。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6. 心理因素。拥有良好的心态，利于术后的康复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
