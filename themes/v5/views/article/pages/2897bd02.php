<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>个体化治疗让中老年肺癌患者疗效更佳&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，中老年人的肺癌发病率在快速上升，据统计我国每年约有50万新增病例。由于老年患者心理、生理及代谢上的变化，身体状态没有年轻患者那么佳，以及部分患者和家属对肺癌认识不足导致老年患者较难接受积极的治疗，往往治疗效果不尽如人意。东方医院胸外科主任陈国涵指出，“尽管老年人体质状况相对较差，但这并不意味着只能消极治疗。临床经验显示，积极的个体化治疗措施能使中老年肺癌患者获得更佳的疗效。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“进入二十世纪后，随着基因学和蛋白组学的发展，尤其是小分子络氨酸激酶抑制剂的出现，将分子靶向治疗推向了历史舞台，而EGFR基因突变对TKI疗效预测的作用开启了‘个体化’治疗的大门。癌症的治疗模式正在从‘One size fit all’的粗放型治疗向‘Pensonalization of cancer care’的个体化转变。在基因和蛋白组学上选择合适人群采用合适的治疗是个体化治疗的真谛。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“肺癌分非小细胞肺癌与小细胞肺癌，其中80%--85％为非小细胞肺癌。65岁以上肺癌病人占非小细胞肺癌患者的50%；约30％的非小细胞肺癌和25%的小细胞肺癌患者在70岁以上，被称为老年肺癌。”陈主任介绍说，“由于老年肺癌患者年龄较大，体质相对弱，并多伴有各种慢性病，心脏、肺、肾脏、肝脏、骨髓等功能较差差，故而在治疗方法选择及疗效上和年轻患者存在一定的差异。单纯的外科手术治疗如今已无法进一步提高生存率，因此应该推广以外科为主的综合治疗模式，特别是老年肺癌的治疗应讲究‘同病异治’的个体化治疗。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“拥有较好的体力状态和病情处于早期阶段的老年患者如接受积极的个性化临床治疗，其生存时间丝毫不逊色于年轻患者。”陈主任强调指出，“所以，希望老年肺癌患者要相信科学，切忌有病乱投医、迷信偏方及虚假宣传报道。要用科学的态度，良好的心态配合医生的个性化治疗，才能提高康复率。”</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早期非小细胞肺癌老年人如身体状态良好，手术仍然是最好的治疗方法。可切除的手术患者与未接受手术者相比生存时间显著延长。选择合适的手术方式以及加强围手术期处理是防治术后并发症的关键。对没有扩散转移又不能接受手术的患者应考虑进行根治性放疗，以达到最佳治疗效果；局部晚期的老年患者应尽量接受正确的化疗或放疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于晚期非小细胞肺癌的老年患者，治疗原则是在保证生存质量的基础上进行综合性的个性化治疗。此期患者可以考虑接受单药化疗、非铂联合化疗、含铂联合化疗、以及靶向治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陈主任介绍说，目前临床主要使用的药物有以健择、泰素或泰索蒂、诺维本为代表的三代化疗药物，第三代化疗药物的不良反应与前两代药物相比已大大减轻，且多数老年病人的化疗是以这些药物单种药治疗为主，因此毒副反应轻微。新一代止吐剂的广泛应用，使一部分接受含顺铂或卡铂联合化疗方案的患者的恶心、呕吐反应明显减轻，已使老年肺癌患者的化疗不再是令人望而却步的治疗；而是明显提高生活质最，延长生存期的治疗方案。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">以易瑞沙、特罗凯为代表的靶向药物治疗，目前已明确证实对一线治疗失败者有良好疗效，尤其对东方人群、不吸烟者和女性、腺癌及肺泡癌有效，使晚期肺癌治疗取得突破性进展。它们以服用方便，不良反应相对较轻微，疗效明确，成为标准二三线治疗用药方案。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最后，陈主任强调：“总之，肺癌的治疗必须根据患者实际情况、肿瘤分期、病理分型等来进行。尤其需要提醒广大患者、家属和医生注意的是，在局部晚期非小细胞肺癌的治疗上，对有潜在治愈可能的患者采用完全不能治愈的姑息化疗进行治疗是不正确的；另外，过度治疗，尤其是过度化疗严重影响肺癌患者的生存期，甚至危及生命，造成人财两空的不当治疗也应该避免。” </span>
</p>
