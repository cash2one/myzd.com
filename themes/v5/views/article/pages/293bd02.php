			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>夏强：探索儿童活体肝移植</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003489443228" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">夏强说，与普通手术不同，肝移植病人术后可能出现排异等一系列问题。身体周刊记者 雍凯 图</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 十年前，年仅三十八岁的夏强带领平均年龄仅三十四岁的仁济医院肝脏外科团队几乎从零起步，而今，他们已完成肝移植手术一千八百余例，连续四年肝移植手术位居全国首位，尤其在儿童肝移植领域一直领跑全国，连续四年位居全球首位。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 夏强</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1966年出生，主任医师，教授，安徽安庆人，上海交通大学医学院附属仁济医院肝脏外科主任、主任医师、教授、博士生导师。上海市医学会理事、上海免疫学会移植免疫分会副主任委员、上海医学会器官移植分会副主任委员、上海市肝移植质量控制中心专家委员会委员和上海医学会肠外与肠内营养学分会委员、中国医师协会器官移植分会常委、中华医学会器官移植分会委员。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在仁济医院肝脏外科团队每天早晨交班的会议室墙上，悬挂着一排医学名家的照片，几乎涵盖了世界肝移植发展史中每一个里程碑式人物。其中，世界肝移植之父托马斯•斯塔泽（Thomas E.Starzl）的这句话“医学在昨天是不可能的，今天看来很困难，明天就是常规。”是夏强的座右铭。夏强就是带着这种探索精神，用10年时间，把仁济医院肝脏外科带领到了国际领先行列，让曾经看起来不可能、很困难的肝移植成为常规。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 10年前，年仅38岁的夏强带领平均年龄仅34岁的肝脏外科团队几乎从零起步，而今，他们已完成肝移植手术1800余例，连续4年肝移植手术位居全国首位，尤其在儿童肝移植领域一直领跑全国，连续4年位居全球首位。2014年，肝脏外科共完成111例儿童肝脏移植手术，移植数再创新高。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在回顾十余年的探索之路时，夏强感叹，“医学在不断发展，只有不断对新技术进行探索，才能有更多机会和能力挽救病人。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 睡在病人旁边观察</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 夏强的父母都是医生，他笑称从小闻着消毒药水长大。一次青霉素过敏休克被抢救过来的经历让他对医学有了初步的感触。在高考填志愿的时候，夏强自然而然选择了医学。1987年，他于安徽医科大学医疗系毕业，1997年12月提前获得复旦大学医学博士学位后，成为一名外科医生。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1999年，夏强作为访问学者赴奥地利格拉兹大学工作、学习。在那里，他第一次亲眼目睹了肝移植手术的全过程。作为外科医生的他，当即抑制不住内心的激动。夏强很喜欢世界肝移植之父托马斯•斯洛泽所写的《组装人》，其中描述了作者开始学习肝移植到肝移植怎么发展成功的整个心路历程，夏强从中获得了非常多的启示，这本书成为他事业的指明灯。他回忆说，“当时国内还没有开展过肝移植，第一次亲眼看到肝移植手术觉得非常神奇，这个手术非常复杂，是外科医生的梦想和追求。那时候刚刚三十多岁，正是想做事情的时候。因此就把肝移植当作自己的奋斗目标。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2000年回国后，夏强所在的上海市第一人民医院正好准备启动肝移植项目，他如愿进入项目组，从动物实验开始做起。2001年第一例肝移植手术开始，夏强一发不可收拾，他们团队三年内做了一百多例肝移植。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2004年，38岁的夏强迎来了他医学生涯的转折点—来仁济医院组建新的肝脏移植中心。一晃十年过去了，仁济医院肝脏外科肝移植数量每年达到200例，连续4年全国第一，去年完成265例肝移植手术，占上海地区手术量的一半。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “肝脏外科的医生往往是救命的。”夏强还记得，2005年，一名20岁左右的小姑娘从无锡被送至上海，几度昏迷，情况紧急，在幸运地获得匹配的肝源后，夏强对其实施了肝移植手术。三天后她醒来时，恍如隔世，丝毫记不起抢救及手术过程，仿佛“重生”了一样，是肝移植技术让她获得了第二次生命。渐渐地，夏强把肝移植作为自己未来的攻坚方向。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于一个外科医生来说，肝移植手术无疑代表着外科手术的最高峰，是外科医生的追求和梦想。夏强说，“其他手术，只需要把病灶切除，进行修补缝合就可以了。而肝移植手术不仅要把病肝切除，移植一个新的肝脏，还要保证新的肝脏成活。肝脏上血管密布，取肝时血管的分离、移植时血管的吻合，都考验着医者的水平。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肝移植手术是一种极其复杂、精细的手术，从取肝到移植，需要天衣无缝的协作配合，只有每一个程序都衔接得当，才能够使一台耗时耗力的肝移植手术顺利进行。与普通外科手术截然不同的是，肝移植病人虽然度过了手术危险期，可后续的排异、感染等一系列问题，随时都会出现，必须每天早查房晚查房，夏强说：“有时候就睡在病人旁边观察。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 母亲肝脏移给孩子</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肝移植手术最大的瓶颈就是肝源。夏强渐渐发觉普通尸体肝移植面临供肝缺乏的瓶颈，便决定向难度更高的活体肝移植领域进军。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2006年，他带领仁济医院肝脏团队在上海率先大规模开展活体肝移植手术，迄今已可进行包括左外侧叶、左半肝、右半肝、带肝中静脉扩大右半肝活体肝移植、原位辅助性肝移植和两供一受在内等各种高难度活体肝移植手术。然而活体肝移植的风险，常人无法想象，活体肝移植需要同时对一个患者和一个健康人进行手术，不仅要医治好患者，而且还要保证肝脏捐献者今后能够正常生活。他是如何面对这一医学挑战的呢？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在繁重的肝移植工作之余，夏强带着一批年轻医生，利用周末的时间，在外科训练基地从早上8点到晚上8点，十几个小时，在猪身上进行活体肝移植实验。并赴美国、日本、中国香港等地多家医院学习。在了解学术前沿信息动态的同时，邀请台湾长庚医院的顶尖专家陈肇隆院士来院联合诊治，为活体肝移植做技术上的准备。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 夏强至今难忘，2006年他带领团队第一次开展儿童活体肝移植。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这个来自河南的9个月大男孩，患先天性胆道闭锁，黄疸指数严重超标，只有做肝移植才能挽救幼小的生命。为救孩子，母亲毅然决定捐肝救子，第一例手术异常艰难，经过前期充分的准备及预案，长达13个小时的活体肝移植手术，最终将母亲的约1/6肝脏成功地移植给了患儿。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 现在这个孩子已经上小学三年级，非常健康。去年仁济肝脏外科建科十周年的时候，他还来参加活动。夏强回忆说，“由于孩子肝硬化导致门静脉高压，门静脉长期压迫血管壁，使血管壁变得很细，只有3毫米。手术中血管吻合花了很长时间，因为肝移植后要保证血管畅通，这是手术成功的关键。希望肝移植的孩子都能正常生长发育，这样肝移植的意义才大。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 儿童亲体供肝移植手术是在儿童和成人之间进行，面临的最大技术难题就是怎么把成人的血管接到儿童的血管上，怎么把孩子的器官和成人的器官进行匹配，这个对医生来说极具挑战性，考验着医生的耐心和仔细。同时，考虑到孩子未来的成长因素，用什么线缝，怎么缝，也是需要攻克的难题。夏强从细节中找到了突破口，用可吸收的缝线，采取间断的方式，预留扩张的空间，这种极其细微的技巧，大大增强了活体肝移植手术的质量，这极大鼓舞了夏强，他边实践边摸索新技术，一步步规范活体肝移植手术操作。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 仁济医院肝移植中心，自2006年实施第一例儿童肝移植手术至今，已经累计完成了323例儿童肝移植手术，无论是数量还是质量都位居全球前列。2014年，肝脏外科儿童肝脏移植数再创新高，全年完成111例儿童肝脏移植手术，年手术量连续4年居全球首位。截至去年，仁济医院完成的儿童肝移植手术已经涵盖了14种儿童肝脏及遗传代谢性疾病。这些接受手术治疗的患儿年龄最小不到5个月，最大为15岁；他们中有110位分别来自全国20个省和自治区，还有1位是马来西亚籍患儿。在这些手术中，亲体肝移植手术占76例、公民心脏死亡后器官捐献（DCD）肝移植手术30例、劈离式肝移植5例，手术成功率超过95%。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 婴幼儿肝移植技术成果被《胃肠病学（Gastroenterology）》、《肝脏病杂志（Journal of Hepatology）》、《移植（Transplantation）》等业内顶级杂志引用，并被写入《Schiff肝脏病学》、美国临床胃肠与肝脏病学教材及德国肝脏疾病治疗指南。夏强还向全国12个省16家三级甲等医院或医学院附属医院进行技术推广，协助国内多个省市的三级甲等医院开展了当地的首例儿童肝移植手术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 经过夏强的钻研，仁济医院的肝移植水平，已经走在全国的前列。当初一台活体肝移植手术需要十几个小时，如今缩减到了七八个小时，准备10000毫升血液，如今只需准备1000毫升，手术成功率达到90%以上，达到了国际先进水平。活体肝移植基于肝静脉解剖结构的流出道重建技术、婴幼儿肝移植肝动脉重建技术改进、婴幼儿肝移植胆道重建技术改进、肝移植术后门脉血栓介入治疗以及肝移植术后免疫监控技术……更是填补了国内空白。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肝脏外科精准化</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在夏强的带领下，仁济医院肝脏外科已形成以肝移植为临床技术特色、儿童肝移植为标志性技术、集终末期肝病和肝癌的临床治疗与基础研究为一体的特色学科。学科先后成为国家临床重点专科、国家“211 工程”重点建设学科、国家临床药理机构等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2014年，由夏强主任领衔的《儿童肝移植关键技术的建立及临床推广应用》项目荣获高等学校科学研究优秀成果奖科学技术进步一等奖、华夏医学科技奖一等奖、上海医学科技奖一等奖。去年6月26日，夏强获得中国医师行业的最高奖项“中国医师奖”。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 回顾十几年来肝移植的探索之路，夏强坦言，肝移植领域的发展与技术追求有很大关系，每一个进步都是在不断突破。“以前传统的肝脏来源少了，我们就要寻找技术突破，全肝不行，那做半肝手术，也就是劈离式肝移植，一肝两用，这样虽然给技术带来挑战，但带来器官有效利用。活体肝移植根据患者需求量体裁衣，精确计算，在不影响供者生存的情况下，如何做到精确切除，这需要影像学、血管重建等技术的支撑。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 而肝移植的技术探索，也是对肝脏外科发展的巨大推动。活体肝移植能够做到如此精准的计算，那么常规肝脏手术的精准就更不在话下。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 把活体肝移植的技术突破用于常规肝脏外科手术，使以前的高风险手术更安全。比如活体肝移植对于肝脏解剖精确评估，肝动脉、肝静脉、门静脉、胆管都要保持通畅，切除时就要把影像学技术做好。这一技术用在肿瘤切除上，使得肿瘤切除更加精准。</span><br />
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 此外，肝移植推进肝功能储备功能的研究，比如，肝脏切多少给病人用，剩多少给供者用，要做到分毫不差。现在对肝功能储备功能的数字化计算，可以让肿瘤切除更安全。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 再者，肝移植的血管吻合技术被用于肿瘤切除上，肝移植的一个血管库可以做搭桥手术，使很多原先不能做的手术变成可能。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 夏强说，“在肝移植的技术带动下，我们推进了精准的肝脏外科，肿瘤切除更精准，肝脏保护更完善，恢复得更快。目前30%~40%的肝切除可以用腹腔镜，去年开展了达芬奇机器人的肝肿瘤切除手术。”</span><br />
