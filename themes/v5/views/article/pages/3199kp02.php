			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>浅谈消化内科的营养治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">消化内科对于相关疾病范围的归纳并不准确，顾名思义，就是消化只是占据消化内科疾病的一部分，另一重要的部分就是吸收。如果为消化内科写一幅对联的话，上联应该是“消化五谷美味”，下联是“吸收天地精华”，横批应该是“平衡营养”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">食物通过口腔牙齿的咀嚼吞咽进入食管就开始了消化吸收征途的开始。食管有腺体分泌各种粘液，但是对于普通食物的消化作用较弱，更重要的是润滑。一旦进入胃这个巨大的皮囊，胃就开始分泌胃液，最重要的是胃酸、还有胃蛋白酶。蛋白的消化首先是从胃开始的。食物进入小肠还会有肠液、胆汁、胰液的分泌，这些消化液就像铺天盖地的锋利的小剪刀，把大块食物剪切成小块，因为肠道的腺体就像是一张张纤细的小口，只有细小的营养颗粒才能被这些樱桃小口吞下，这就是吸收。当然广义的吸收 还要包括细微的营养颗粒进入血液在运转到全身，最终进入细胞内。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以消化系疾病，如果病人消化不良，哪怕每天进食</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">凤髓龙肝也难转变成足够的营养。所以新的科技就能通过科学技术生产的消化酶将食物分解成细微的足以让肠道的纤维绒毛直接吸收入血液的微小营养颗粒，这就是短肽型肠内营养剂。病人通过口服，鼻饲等方法，将流质的营养剂饮入消化道，就能达到将难以吸收的龙肝凤胆送入血液的较好效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
