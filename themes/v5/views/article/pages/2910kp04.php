		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腔镜辅助小切口甲状腺手术，Miccoli 术式介绍</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">Miccoli 术式为美容微创的手术，即在腔镜辅助下行小切口甲状腺手术。此手术方式由意大利医生Miccoli P于1998年首次报道，又称为Minimally invasive video-assisted thyroidectomy(MIVAT)。经过10余年的发展，该术式在世界范围内已经完成了数万例手术，包括单侧腺叶部分切除、双侧腺叶全切除以及中央组淋巴结清扫术等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 手术时在胸骨切迹上方2cm做一长约1.5～2.5cm的水平切口，从此切口置入内镜及手术器械。Miccoli等总结1320例MIVAT平均时间：单侧腺叶切除为32.3分钟（20-120分钟）、双侧腺叶全切除术为44.1分钟（30-130分钟）、21例双侧腺叶全切除+中央组淋巴结清扫术为57分钟；MIVAT手术并发症与传统手术无差别。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 1996年Miccoli等提出手术适应症包括：1）最大径小于3cm的甲状腺结节；2）有131I治疗禁忌症的Grave’s病患者，但要求甲状腺小于20ml；3）没有甲状腺炎病史；4）颈部无手术史或放射史；5）低危甲状腺乳头状癌。</span>
</p>
