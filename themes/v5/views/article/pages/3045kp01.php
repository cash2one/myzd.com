<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是烟雾病，怎么治疗烟雾病</strong></span>
</p>
<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong><br />
</strong></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">烟雾病（Moyamoya病）又称自发性基底动脉环闭塞症，是一种以颈内动脉末端及大脑前、大脑中动脉起始部动脉内膜缓慢增厚，动脉管腔逐渐狭窄以至闭塞，脑底穿通动脉代偿性扩张为特征的疾病。造影时扩张血管的形态如同烟囱里的袅袅炊烟，故日本人形象地称之为烟雾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 烟雾病是日本人最早发现的。随着1962年Subirana报告第1例非日本人烟雾病患者以来，世界各地均有烟雾病的报道，但主要发生于黄种人。发病率最高的是日本，其次是韩国和中国等东南亚地区。发病年龄有两个高峰，一是4岁左右的儿童期，二是30-40岁的中年期。儿童与成人发病率之比为5：2。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 烟雾病的病因尚不十分清楚。同胞间发病率比普通人高出42倍，患者子女发病率高出37倍。最近有学者发现烟雾病可能与遗传有关。烟雾病的主要病理改变是颈内动脉内膜弹力纤维增生，逐渐使颈内动脉管腔狭窄，最终闭塞。血管内膜的病变多发生在颈内动脉的末端，大脑前及大脑中动脉的起始段，偶尔波及到大脑前和大脑中动脉主干及颈外动脉，乃至身体其他部位的血管。因这些颅内供血动脉血管内膜增厚、官腔狭窄导致脑血流量减少，为补偿脑血流量，脑底及脑表面的细小血管代偿性扩张，形成了烟雾状血管。当代偿性血管扩张形成增加脑血流的速度小于因脑供血动脉狭窄脑血流减少的速度时，即产生了脑缺血症状，出现脑梗塞、脑萎缩。脑软化等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 烟雾病的临床表现从轻到重分为6型，轻者以短暂性一过性脑缺血（TIA）发病，表现为头疼、癫痫、肢体无力、感觉异常及视力视野改变等，重者则以脑梗塞或脑出血起病而危及生命。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 烟雾病的诊断主要根据DSA、MRA影像学检查，可参考1995年日本厚生省制定的烟雾病及烟雾现象的诊断标准：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; （1）血管造影特征：1）一侧或双侧颈内动脉末端或大脑前、大脑中动脉的起始段狭窄或闭塞。2）血管造影动脉期可见颅底异常的烟雾血管网。3）以上发现为双侧性病变。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; （2）如果MRA和MRI有如下发现时可不必行血管造影：1）MRA上可见颈内动脉末端和大脑前、大脑中动脉起始端狭窄或闭塞。2）MRA上可见基底节区异常血管网（MRI上基底结区出现两个以上的流空现象亦可诊断异常血管网）。3）1）和2）呈双侧性改变。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; （3）病因学特点：病因不明和排除其它基础性疾病（如动脉硬化、脑膜炎、肿瘤、外伤、放疗、Down综合征、Von Reclinghausen氏病）。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; （4）病理结构学特点（尸检病例替代血管影像学改变）：1）颈内动脉末端内膜增厚致管腔狭窄或闭塞，通常是双侧性变伴脂质沉积。2）构成基地动脉环的主要动脉（大脑前、大脑中及后交通动脉）因内膜增厚致管腔不同程度的狭窄或闭塞。3）基地动脉环周围可见很多细小的穿动脉和吻合支。4）细小的血管在软脑膜上聚积成网。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 根据以上特点确定烟雾病人诊断标准如下（尸检病人血管造影参见（4）病理结构学特点）：1、确诊烟雾病：全部符合（1）或（2）和（3）；小儿符合（3）和（1）或（2）的1）2），对侧末端颈内动脉狭窄。2、可疑烟雾病：符合（3）和（1）或（2）的1）和2）。3、烟雾综合征：符合一部分或全部（1）和（2），但不符合（3）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 烟雾病的治疗目前国内还没有十分理想的方法。内科以血管扩张药以及抗菌素等进行对症治疗，但疗效不理想。外科以手术治疗为主，大多数学者采用由日本学者松岛善治先生1979年发明EDAS的间接吻合法，即将颅外血管越过颅骨和硬脑膜屏障引至颅内而促进脑皮层的供血，该法并发症少，《YONMANS神经外科学》中进行了详细地介绍。笔者从上世纪70年代末开始，在著名神经外科专家段国升教授的指导下，采用颅内外血管融通术及颅内外血管的直接吻合等方法治疗了1000余例烟雾病。同时笔者还结合我国烟雾病人的发病特点，进一步改进了烟雾病的手术疗法，使一些较为复杂的病例获得满意疗效。</span>
</p>
