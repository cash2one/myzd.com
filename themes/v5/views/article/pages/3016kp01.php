			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>科普-耳石症</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、什么是良性阵发性位置性眩晕</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">良性阵发性位置性眩晕（BPPV）是因特定头位改变而诱发的阵发性短暂眩晕，为最常见的前庭末梢器官病变，亦称为耳石症。发病机制为椭圆囊耳石膜上的耳石脱落到半规管里。此病多见于中老年患者，青年人亦可发生，多数病例发病并无明显诱因，可能的诱发因素有头部外伤、情绪波动和劳累等，也可由其他内耳疾病合并发生，如突发性聋，梅尼埃病，前庭神经元炎等，此病预后良好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、良性阵发性位置性眩晕临床特点有哪些 &nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;1）症状的发生常与某种头位或体位活动有关：激发头位（如左右翻身、起床或患耳向下）时出现眩晕症状，眼震发生于头位变化后3～10秒之内，眩晕则常持续于60秒之内，可伴恶心及呕吐。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2）眼震特点：确诊BPPV，医生一般要做变位试验，一个叫滚转试验，一个叫DIX-HALLPIKE试验，类似于我们平常生活中的床上左右翻身和起床，左右滚转翻身时，会出现水平性的向地或背地眼震；从坐位倒下至激发头位时会出现一种旋转性的短暂的易疲劳的眼球震动，当左耳向下时眼震为顺时针方向，右耳向下时为逆时针方向，眼震持续过程中，先是逐渐增强，其后逐渐减弱，当从卧位回至坐位时，出现方向相反的眼震。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个试验以前在检查床上操作，现在已有全自动的前庭功能诊疗系统，可以依靠设备帮助患者完成上述动作，这对于体形肥胖、患有颈椎病的患者来说是一个更佳的选择，并且检查比人工操作更精准。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3）BPPV病程可为数小时至数周，个别可达数月或数年，眩晕可周期性加重或缓解，严重者于头轻微活动时即出现眩晕，间歇期可无任何不适、或眩晕发作后可有较长时间的头重脚轻及飘浮感。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、良性阵发性位置性眩晕的治疗和预后怎样 </span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">良性阵发性位置性眩晕是一种良性的自限性的疾病，但其自愈的时间有时可达数月或数年，严重的可使患者丧失工作能力，故应尽可能地治疗，治疗以复位治疗为主。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）耳石复位 目的是使沉积在半规管的耳石复位。根据耳石异位的半规管的不同，手法不同。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）心理治疗 本病为良性过程，无严重的后遗症，患者不应有过重的精神负担。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）体位和头位的保健 当眩晕发作剧烈时，尽量避免采用可引起眩晕发作的体位和头位。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）抗眩晕药 可以酌情服用改善内耳微循环的药物，如西比灵（氟桂利嗪）、敏使朗（甲磺酸倍他司汀）以及辩证使用中医中药等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（5）前庭康复操和前庭习服疗法 目的是促进前庭功能代偿和恢复，增加对眩晕的耐受能力，可在医生指导下进行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、良性阵发性位置性眩晕有哪些复位治疗方法</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">复位治疗在BPPV的治疗中起重要的作用，传统复位方法主要有以下几种：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）后半规管BPPV的治疗 该方法由Epley于1992年提出，其依据为半规管结石症学说，目的是借助定向的头位活动及摆动使管石依靠自身重力作用逐步从后半规管重新回到椭圆囊。具体治疗分五步进行（以右后半规管为例）：①患者快速头右转450，悬头仰卧；②将头向左回转450；③头与躯干同时向左转1350，使脸朝下450；④保持头和身体向右转，扶患者坐起；⑤头转向正前方，低头200。每种头位需30s，或至眼震完全消失，每完成一步要观察和记录眼震，待眼震终止后方可做下一步。治疗结束后嘱患者保持头部相对垂直24h以减小耳石又回到后半规管的可能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(2) 外半规管BPPV的治疗—Lempert法（Barbecue翻滚法） &nbsp;由Lempert等（1996）设计的复位方法较为常用，具体方法如下：①嘱病人仰卧；②头向健侧转动900；③身体转动1800由仰卧变为俯卧而头位保持不变；④继续转头900至面部向下；⑤继续转头900至患耳向下；⑥恢复直立。每次头位变换需迅速在0.5s内完成，每一体位保持30~60s直至眼震消失，整个过程头部共转动2700。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除传统复位外，目前国内已经推出了仪器复位，如SRM良性阵发性位置性眩晕诊疗系统复位治疗，这种仪器复位治疗更精准，疗效更确切，复发率更低。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、 BPPV的习服练习是怎样做的</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者迅速向患侧卧位，眩晕消失后保持30秒，然后坐起等待眩晕消失。患者应向对侧重复以上运动，停留30秒，坐起。整个治疗练习重复10～20遍。每天3次，如果连续2天无眩晕出现，可停止治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、复位后有哪些注意事项，复位后还会复发吗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">复位之后，患者尽应量休息，保证充足的睡眠，并避免头位向患侧倾斜，最好24小时内相对固定头位，严禁剧烈活动头部、作颈部按摩等。</span><br />
