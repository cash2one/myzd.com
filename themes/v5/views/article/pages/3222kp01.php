	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>超声内镜指导消化道粘膜下病变诊疗的应用</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">内镜结合组织活检能使许多消化道疾病（主要是粘膜表浅病变）得到明确的诊断，但对于粘膜深层及以下的病变和消化道外脏器或病变压迫的消化道隆起性病变，内镜检查往往无法作出诊断。超声内镜（Endoscopic Ultrasonography，EUS）：是经内镜导入超声探头，通过体腔在内镜直视下对消化管管壁或邻近脏器进行断层扫描，获得消化管管壁粘膜以下各层次和周围邻近脏器的超声图像。它能在内镜直视下直接对可疑病变及其周围进行超声扫描检查，从而获得清晰的超声图像，准确显示病变在消化道壁的层次部位或与消化道管壁的关系。EUS可根据其起源层次和病变回声强弱、均匀与否、边缘情况及周围淋巴结等影像特点提示病变的性质，是目前诊断消化道粘膜下病变的最好方法。应用超声内镜对粘膜下病变进行诊断，根据内镜诊断结果进一步指导治疗如：内镜下微创治疗或手术治疗，以及术后随访。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
