		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>骨关节手术迈入“针孔”时代</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">通过体表约5毫米的微小切口将关节镜放入关节腔内，经与关节镜相连的摄像系统将关节内图像直接显示在屏幕上，医生通过屏幕直观地了解患者关节内病情，从而做出正确诊断，同时，在开孔处插入检查或手术器械可以直接在病变处进行治疗。目前，在威海市中心医院关节外科，通过先进的关节镜技术及先进的设备，肩、肘、腕、髋、膝、踝关节的各类疾病，医生们均能实现微创治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147004093547628" title="" alt="demo.jpg"/>于晓峰（中）与同事一起为患者做手术。威海晚报记者 王茂忠 摄</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “不到外地看专家，外地专家来看你”是威海市中心医院近年来为广大患者提供的一项便民服务。近年来，关节外科先后与北医三院运动医学研究所、北京积水潭医院等国内著名医院建立良好的技术业务关系，每年邀请专家不定期来院手术、讲课，并选派优秀年轻医师前往进修学习，通过“走出去、请进来”的发展模式，不断提高技术水平，取得了良好效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专业技术 精益求精造福患者</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 日前，一名22岁的男青年因左侧髋关节疼痛到医院就诊，经过CT及MRI等检查后，关节外科主任于晓峰确诊其为髋关节滑膜软骨瘤病，并立即为其进行了髋关节的关节镜微创手术。“在他髋关节间开了两个5毫米的小切口，通过这两个像针孔一样的切口，取出了20多个游离体。”于晓峰说，与传统手术需要开刀将伤口完全暴露出来、创伤较大、恢复时间久相比，关节镜微创手术刀口小，手术中出血量极少，不仅术后感染几率大大减少，并且手术后基本没有明显疤痕。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 关节镜微创技术的明显优势让越来越多的患者愿意选择关节镜手术进行治疗。尤其是一些年轻人，对于工作、运动的要求相对较高，更愿意接受微创手术。“从各种类型的关节炎、半月板损伤、交叉韧带损伤、滑膜炎、髌骨软化症、关节内游离体，到复发性肩关节脱位、肩袖损伤、关节粘连、关节内骨折，我们都能通过关节镜进行检查、治疗，并且达到省级医院同等水平。”于晓峰说，中心医院关节外科日益成熟的关节镜微创技术已实现全身所有关节的“全覆盖”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 虽然关节外科的关节镜技术日趋成熟，但于晓峰每年仍会带领科室的业务骨干到北京大学第三医院运动医学研究所、山东省千佛山医院、中国人民解放军总医院、北京积水潭医院等全国知名医院学习先进的理念和技术，应用于临床，让更多的患者受益。该科还注重功能重建，在骨关节损伤早期和择期手术前后，将薰蒸泡洗、手法松解、体疗理疗等康复治疗尽早介入，进一步提高了疗效。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 先进设备 精准定位全新体验</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2月19日，于晓峰熟练操作施乐辉关节镜，为一名32岁的男性患者完成了关节镜下前交叉韧带的重建手术。“通过两个小切口，把关节镜放进关节腔内，患者膝关节内的情况马上清楚地显示出来。”于晓峰说，通过施乐辉关节镜，他可以清晰地看出患者膝关节前交叉韧带完全断裂。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 施乐辉关节镜是美国进口、目前国内外最先进的内镜设备之一，该设备兼有检查、诊断、治疗等多重功能，能让医生在手术过程中直视进行半月板缝合修补、前/后交叉韧带修复重建，具有创伤小、切口美观、费用低、康复快、恢复好等优点。关节外科还拥有术中三维CT、术中导航仪等先进设备，精准定位患处，能提供更加安全有效的治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 值得一提的是，威海市中心医院还积极发展自主创新技术，经过大量的实验研究，发明了一套髌骨运动轨迹摄片装置，并于2013年获得国家发明型专利，填补了技术空白，得到了国内同仁的高度评价，极大地方便了患者检查。过去，髌股关节运动轨迹方面的摄片方式是盲区，拍片是在静止的状态下看髌骨和股骨的轴位像，无法了解髌骨关节的运动轨迹状况。髌骨运动轨迹摄片装置通过了解病人屈膝过程中不同角度下髌骨运动的轨迹情况，将屈膝过程中多角度的多个连续图像上各种数值测量描绘出，在髌骨轨迹异常的诊断及鉴别诊断方面有重要作用。若发现运动轨迹有异常，即可通过关节镜进行微创治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专家云集 经验丰富保障有力</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “手术后感觉非常好，北京专家做的手术，我放心。”2月23日，于晓峰查房时，74岁的患者张先生脸上满是笑容地说着自己的术后感受。原来，2月21日恰逢周日，关节外科邀请北京大学运动医学研究所崔国庆教授来到医院，为患有痛风性关节炎的张先生进行了手术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “不管患者想让本院专家还是外地专家做手术，我们都能满足。”于晓峰介绍，关节外科共有医护人员17名，其中正高职称1名，副高职称1名，中级职称3名，硕士研究生3名。学科带头人丛海波教授能熟练开展四肢骨与关节的各种手术技术。于晓峰到北京大学第三医院进修后，回院后率先在威海地区开展肩关节镜手术，可熟练开展肩、肘、膝、踝、髋、腕关节镜下各种微创技术操作，该类技术处于威海地区领先水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 从2012年开始，关节外科与北京大学运动医学研究所合作，成立了威海地区关节镜微创技术协作推广基地，专家每个月来院为患者诊疗、手术，良好的效果叫响了医院“不到外地看专家，外地专家来看你”的服务品牌，慕名前来手术的外地患者已占到患者总数的30%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为了提高患者的就医体验，减轻负担，威海市中心医院关节外科以下病种实现就医零负担，职工医保病人费用“零负担”，居民医保病人报销60%：单侧人工股骨头置换手术治疗，单、双侧全膝关节置换手术治疗，单、双侧全髋关节置换手术治疗，关节镜下前、后交叉韧带重建术，关节镜下单、双侧髌骨外侧支持带松解术，关节镜下肩袖修补术。</span><br />
