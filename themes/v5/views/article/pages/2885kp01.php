			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小儿尿道下裂一般知识</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 对于大众而言，尿道下裂这一个疾病很陌生，主要有两个原因。其一，尿道下裂并不像感冒、腹泻那样多见；其二，由于这个病发生在生殖器上，受国人传统隐私观念的影响，不宜外传或张扬。事实上尿道下裂是小男孩常见的生殖器畸形，我国每一年新增大约3万例。也是小儿泌尿外科医师经常面对的疾病。之所以多发，与遗传、宫内因素（包括环境污染即所谓环境激素）等有关。我科收治的尿道下裂小孩除了来自深圳及周边地区外，还有来自四川、江西、湖南、湖北、贵州、海南等地的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尿道下裂并不是尿道口裂开了或者是尿道裂开了，而是胚胎时期尿道发育障碍，导致一段尿道没长出来，出生后看到的是尿道开口异常，可能尿道缺损只是在龟头那一段，只差几毫米，也可能缺损从龟头至阴茎体甚至更远，这时就会缺损几厘米。临床上凭外观常分为龟头型、冠状沟型、阴茎体型、阴茎阴囊交界型、会阴型等，也有的为了方便描述，把它分为轻型、中型、重型（轻型并不意为好治）。不仅如此，尿道下裂往往同时伴有阴茎向下弯曲畸形，甚至呈钩状，也没有包皮系带，包皮的分布也不正常（背侧多腹侧少）而呈头巾样覆盖在龟头上。还有的伴随着阴茎阴囊转位（即阴囊包围着阴茎四周），阴囊分裂为左右两半。所以尿道下裂这个病是阴茎和尿道发育最严重的一种畸形。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尿道下裂不会像家长所期待的那样自己长好，而必须通过手术治疗。专科医生通过巧妙的设计和手术方法，把弯曲的阴茎弄直，把缺损的尿道做出来，同时把外形整好尽可能地接近正常，从而满足小儿站立位排尿以及将来性生活的功能要求。这其中最难的是做一段代替尿道的皮管。医生会利用小儿自己的材料，如尿道板、包皮、阴囊皮肤，甚至口腔粘膜等。用这些材料做成的皮管在愈合过程中有可能产生尿瘘、尿道狭窄，也可能在术后很长时间由于皮管不能够像正常尿道那样抵抗尿流压力而形成扩张或形成憩室。如果出现了尿瘘、尿道狭窄或尿道憩室就需要再次手术治疗。到目前为止，在国内外医学领域，尿道下裂的治疗仍然是有相当难度的，需要有经验的专科医生治疗，有部分病人需要两次或多次手术才能达到最终目的。还有极少数病儿由于体质（如瘢痕体质）等原因，可能形成难以纠正的缺陷，医学上称之为尿道下裂残废。值得告诉家长的是尿道下裂手术十分复杂，国际上对尿道下裂治疗的观念和方法也是不断进步的。过去强调一次手术成功率，现在更强调手术后排尿功能及远期的阴茎形态和成年后性生活是否满意。手术计划有一期和分期两种方案。分期手术就是有计划地在第一期手术时把阴茎弄直；第二期手术时再做皮管代替尿道，以达到从龟头处开口排尿的目的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有研究表明一部分孩子采取二期手术方案，其最终效果好于一期手术方案。因此建议家长不要过于要求一次手术成功，而应由手术医生来做最适宜的决策。以最终效果最好为目标，而不是以手术次数最少为第一考量。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尿道下裂何时治疗，并没有特别年龄限制，手术多在2-3岁之间进行。随着技术的进步以及麻醉和护理的完善，现在手术年龄倾向更早。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还要提醒家长：尿道下裂术后需要几年的时间随访观察，尤其是在术后1-2年内更是如此，以便及时发现和处理并发症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，虽然尿道下裂是一种严重的畸形，是一个治疗过程麻烦的疾病，但是是可以治疗的疾病，家长的配合也是获得良好效果的重要因素。</span><br />
