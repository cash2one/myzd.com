			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>浙医二院成全国首批前列腺癌规范化诊疗基地</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯 由中国健康促进与教育协会主办，浙江大学医学院附属第二医院协办的“前腺卫士”前列腺癌患者健康教育公益讲座，上周在浙医二院举行。同时，浙医二院正式挂牌成为全国首批50家前列腺癌规范化诊疗和患者管理示范基地之一。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">浙医二院泌尿外科主任杜传军教授说，所谓前列腺癌的规范化诊疗，就是按照中国泌尿外科诊疗指南及美国泌尿外科、欧洲泌尿外科诊疗指南，首先对疾病及程度做出诊断，指导下一步治疗。基地必须具备前列腺癌治疗的所有设备和治疗方法，如放疗、化疗、生物治疗、手术治疗等。其次，这些治疗方法不是单独运用，而是联合诊断与治疗。另外，筛查前列腺癌所需检查是有先后顺序的，是有所选择的，一开始并不需要一股脑儿都做掉。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“有些医院一上来就叫你做很多检查。其实，直肠指检、超声及血PSA指标是筛查的基本手段，对筛查高度怀疑的前列腺癌患者，才需要进一步进行经直肠前列腺穿刺活检以及CT、MRI检查。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杜传军教授说，前列腺癌不同于其它肿瘤，早发现早诊断早治疗的话，完全可以治愈，不影响寿命，目前早期前列腺癌患者5年以上生存率能达到90%以上。所以50岁以上的男性，要把PSA检查列入每年一次的常规体检项目，有家族遗传史的高危人群，从45岁开始。记者 谢谨忆 通讯员 方序 鲁青</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多吃番茄多喝绿茶多晒太阳</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有利于保护前列腺</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据流行病学调查研究显示，我国前列腺癌的发病率近年来有明显的上升趋势。根据国家癌症中心的最新数据，前列腺癌自2008年起成为泌尿系统中发病率最高的肿瘤，2009年的发病率达到9.92/10万人，在所有男性恶性肿瘤发病率中排第6位。死亡率达到4.19/10万人。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杜传军教授从事泌尿外科工作32年，前15年，他总共才做了4例前列腺癌根治手术。而现在，浙医二院泌尿外科几乎每周都要做4例以上。“近20年，前列腺癌的发病率几乎与GDP是一个增长速度，每年都要增长7%-8%。”他说。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌的发病率之所以飞快增长，和几个原因有关：</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一是饮食结构的西方化。高动物脂肪饮食是前列腺癌危险因素。美国曾调查过一群日裔美国人，发现他们从日本去美国后，饮食习惯从在日本时的较清淡，变成到美国的高脂高热的西方饮食，结果前列腺癌的发病率大大增加。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，环境的污染、遗传基因、人口老龄化、前列腺癌早期的筛查诊断，都是发病率增加的原因。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杜教授说，关于前列腺癌的发病因素，有些我们控制不了，有些我们是可以注意的。比如平时多吃番茄、多喝绿茶，降低动物脂肪的摄入，多晒太阳，这些都有益于保护前列腺。维生素E摄入不足、硒缺乏、木脂素类、异黄酮摄入不足则会增加前列腺癌的发病风险。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">直肠指检、PSA检查</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可以筛查前列腺癌</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“前列腺癌40岁以上就有发病，高发人群在70岁以上。直肠指诊、超声及PSA指标是筛查前列腺癌的基本手段，对筛查高度怀疑的前列腺癌患者，才需要进一步做穿刺活检及其他检查。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杜教授说，12年前为一位72岁的老人做过前列腺癌根治性手术。那位老人平时身体蛮好的，体检时发现PSA值为6.8ng/ml，高于正常值，直肠指检发现前列腺结节，后来再做进一步检查，确诊为前列腺癌早期，骨扫描提示未见转移。“做了手术后，那位老人现在健健康康地活着，天天早上去城隍山上遛个鸟。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">过度性生活，会增加前列腺癌风险？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌和前列腺增生有关吗？杜教授说，这个问题目前尚未定论，这两种都是老年男性易患疾病，发病都和雄性激素的分泌有关。他最近做了一项研究，发现有前列腺增生的患者，患了前列腺癌后，死亡率是一般的前列腺癌患者的2倍。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“另外，之前有研究说性欲亢进的人，过度频繁的性生活，是前列腺癌的危险因素。因为听了这句话，很多人为了健康就刻意克制自己的性生活次数了。但最新有个研究，发现僧侣中患前列腺癌的也不在少数。所以，过于清心寡欲的生活习惯可能对保护前列腺并没有明显的帮助，适度的性生活可能才是最有益于前列腺健康的，普通人不需要刻意控制性欲。”</span>
</p>
