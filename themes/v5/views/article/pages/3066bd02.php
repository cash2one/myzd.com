			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>兄弟情深 捐肝救命</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">12月1日，北京大学人民医院肝胆外科病房。周文质撩起上衣让记者看他肚子上的伤口。“医生在我肚子上开了个‘奔驰’标志。”他笑了。那个样子完全不像刚做了肝移植手术的人——22天前，他把自己的右半肝切给了自己的二哥周文定，后者当时因为肝功能衰竭正面临死亡的风险。“为了我哥，那还有啥说的。”周文质说。事实上，他为哥哥付出的不仅是右半肝，还有他的前程。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 周家四兄弟，三个都在部队，唯有老二周文定是个“文人”。他毕业于中南大学，后在长沙保险职业学院任教，今年考上了中国社会科学院经济学博士。一家人非常欢喜。可是，由于劳累和休息不好，今年6月中旬，周文定的慢性肝炎突然发病，并且在一周内出现了失控状态。在当地医院治疗3个月后，他的黄疸仍然高达590多，转氨酶50多，处于医学上最难治的“酶疸分离”状态。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 后来，周文定从长沙转到北京三○二医院。在医生的努力下，周文定的病情得到短时间的缓解。可是，好转状况并没有维持多久，病情又再次急转直下，而且被确诊为“进行性肝脏衰竭”。主治医生后来不得不向他们下达病危通知书。“医生告诉我们，除非做肝移植手术，不然我二弟必死无疑。”11月5日，周文定转院到肝移植技术更为出色的北京大学人民医院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 问题是，周家在付出了17万元医疗费后已经没有更多的钱来支付巨额的手术费用，而且更要命的是，供体肝从哪儿来？虽然周家找遍了北京各大医院，甚至是其他省份的也都打听了，仍然没有任何收获。一家人急得要命，而他们的父亲周宝生也跟着抹眼泪。老人在去年刚刚失去老伴，眼看着儿子也要撒手人寰，哪能受得了。老人想过把自己的肝给儿子，可是医生说，人太老了，肝脏老化，不理想。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 正在绝望之际，老四周文质站了出来。“把我的肝切给二哥”，周宝生被小儿子的决定惊住了。“手心手背都是肉，我下不了那个决心。文质献肝后还得退伍，没有了工作。这个牺牲太大了。”况且，手术的风险性很大，如果一下子失去两个儿子，老人怎么能扛得住？周文定自己也骂四弟太傻，不同意他捐肝给自己。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据人民医院肝胆外科黄磊副教授介绍，虽然肝移植在技术上已经比较成熟，但是术前从CT上看，周文质的左半肝生长得较小，不足以达到手术要求，如此只能切主要的右半肝。“独立完成成人对成人的右半肝移植，以前我们科室还没有做过。”这就意味着一旦手术不成功，两兄弟都将面临生命危险。另一个难题是，周文质的肝动脉存在变异，而且右肝有两根胆管，移植时该怎么和周文定的血管和胆管对接？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 周宝生一家犯难了。可是，周文质决心已定。他给家里人和主刀医生说了4个理由：一是二哥文定这么年轻，又是高学历，人才难得，要不惜一切代价保命；二是小侄博成（文定小孩）不到两岁，需要父爱，二嫂待他又好，需要家庭；三是父亲年老，去年母亲去世打击已很大，如果今年二哥再遭不测，怕老爷子接受不了事实；四是兄弟之间情深义重，感情很深，不忍离散。周宝生最后同意了小儿子的决定。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 11月9日，周文质、周文定被先后推进了手术室。周文质在进手术室的那一刻笑着向家人打出了“V”的手势，让他们放心。然而，手术比预想的还要复杂。黄磊在取周文质右半肝时发现，其肝右动脉异常导致取下的右肝动脉又短又细，无法与周文定的肝动脉吻合，他们只好利用心脏外科技术，从周文定的右手取了一段约8厘米的桡动脉血管实施搭桥术，光解决这两个难题就用了3个小时的时间。整台手术持续了24个小时。“好几年没有做这么长时间的手术了。”黄磊说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在手术后，周文质醒过来的第一句话是：“我二哥怎么样？”护士告诉他手术挺成功的，他二哥没有太大的危险，他才高兴得笑起来。两位兄弟再次见面是在监护室。已经能够走动的周文质来到二哥的病床前问，“二哥，感觉怎么样？”周文定拉着弟弟的手说，“兄弟你救了我一命!”周文质又笑了，“哥，不用这么说。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 截至目前，从弟弟周文质身上取出的750克肝已经在哥哥体内长到1100克。作为</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肝脏提供</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">者，周文质体内剩余的肝脏也从原来的700多克生长到980克左右。“兄弟两人肝肾功能正常，都已脱离危险期。”主刀医生、中华外科学会副主任委员、北京大学人民医院外科主任冷希圣教授说。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这次手术，周文定除了要感谢手足之外，还要感谢许许多多的好心人。他告诉记者，他们并非大富之家，自己上学读书已经让家里负债累累，这次治病前后已花了38万余元。除了依靠单位大病统筹的十几万元，其他的钱都是从单位、亲朋好友及老乡处借来的；人民医院也为他们减免了几万元的费用，还替他们向亲体器官移植基金会申请了5000元。很多素不相识的人也通过网站为他们捐款。“没有我弟弟为我披肝沥胆，没有好心人为我捐款，我哪儿能活到今天！感谢所有的人!”</span><br />
