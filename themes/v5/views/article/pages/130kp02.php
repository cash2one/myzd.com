		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>眼睑痉挛究竟该如何治疗？</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">双侧眼睑痉挛的治疗目前仍然是医学界的难题，根本原因是目前还不清楚双侧眼睑痉挛发病的真正原因，也不明确该疾病的发病机制,目前大多学者认为是不明原因导致的中枢神经系统的功能紊乱。因此很多的治疗都是对症治疗，而不是对因治疗，因此很难获得治愈的效果。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 目前的治疗包括三种：（1）药物治疗。这也是眼睑痉挛的首选治疗方法，常用的药物包括氯硝安定、氟哌啶醇、硫必利、苯海索等，部分病人在疾病的开始阶段会有一定效果，但是随着病程延长，疗效会逐渐减退，或者是因为不能耐受药物的副作用而停药；（2）肉毒素治疗。这是目前治疗眼睑痉挛较为有效的方法，通过在上下眼睑内注射一定量的肉毒素而减弱痉挛的程度，肉毒素注射后效果明显，但是疗效持续的时间因人而异，多数在一到数月之间，失效后可以反复注射。目前应用肉毒素治疗最大的问题是部分人对肉毒素过敏、不能接受肉毒素带来的副作用或者疗效不明显，因此肉毒素注射治疗也应用有限；（3）外科治疗。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前可以肯定的是双侧眼睑痉挛患者中，来自面神经的下行神经冲动明显亢进，这也是导致眼睑痉挛的直接原因，因此针对面神经的外科手术试图减弱明显亢进的下行神经传导，在部分病人中可以观察到较好的效果，只不过并非所有的患者都能通过手术获益，因此目前的外科治疗只是作为药物治疗和肉毒素治疗无效患者的补充治疗方案，选择需要慎重。</span>
</p>
