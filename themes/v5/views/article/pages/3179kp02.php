<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 东方网5月24日消息：一颗脂肪肉瘤“囤”在“腰眼”廿余年，从一粒花生米大小长到橄榄球大小，令年近八旬的金老先生苦不堪言。近日，复旦大学附属华东医院为老人顺利切除了巨型脂肪瘤，一解老人多年困扰。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 二十年前，金先生的右侧腰间长了一粒结节，近三年小结节迅速膨大，最终长成橄榄球般大小的巨型肿块，严重影响起居生活。无可奈何下，金先生来到华东医院求助。普外科主任唐健雄教授亲自操刀，手术先沿肿块自上而下切开皮肤，皮下组织发现，巨型肿块由一个大肿块包裹另一个小肿块，挤占整个肋骨至腰腹部，有的组织甚至挤进肋骨之间。医生小心翼翼分离肿块边缘，半个多小时后，将肿块完整切除，术后病理诊断为脂肪肉瘤。经测量，肿块重量约3780克，相当于足月新生儿体重。术后，老人腹壁留下巨大的椭圆形缺损，为恢复腹壁正常功能，医生再将猪小肠粘膜生物补片重新覆盖缺损腹壁，防止术后腹壁疝的出现。日前，金先生完全康复并顺利出院。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
