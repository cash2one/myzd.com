<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海骨科专家带病赴灾区 远程会诊定手术方案</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今天一早，作为中华医学会指定医疗专家、仁济医院神经外科主任江基尧教授，将奔赴四川雅安重灾区，指导当地伤员，尤其是颅脑外伤伤员的抢救工作。对于颅脑受伤的患者，及时送到有条件的、有能力处理的大医院专科诊治，可最大限度抢救每个人的生命，最大程度帮助伤员恢复功能，实现科学抢救。上海增派的5位骨科专家，在四川省人民医院通过与上海六院远程会诊，为伤员制定最佳手术方案。与此同时，随着重症病人转往成都，灾区工作还要重“防疫”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>重灾区重伤员立刻转运</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今天一早，仁济医院神经外科主任江基尧教授将奔赴雅安，指导当地颅脑外伤伤员的抢救。“在华西医院的伤员中，约四分之一至三分之一的伤员为颅脑外伤病人！ ”昨天傍晚刚抵达四川的江基尧教授下了飞机后，就直奔华西医院，经过对该院重症颅脑伤员的病情排查后，他告诉记者，此次地震救援和转运及时，不少颅脑外伤或复合伤病人，能在第一时间从地震重灾区转入条件比较好的大医院专科接受救治，使得危重病人得以挽回性命。在华西医院，颅脑外伤的伤员特别多，仅脑外伤伤员就达到50多名，在重症监护室里的危重病人有10多名，重症患者还一度出现昏迷等危重状况。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今天，江基尧教授随同中华医学会指定医疗专家组进入雅安重灾区。同时，仁济医院已紧急组织成立四川雅安抗震救灾医疗预备队，以骨科、神经外科、重症监护、麻醉、护理等创伤相关专业的医疗救援应急队伍，已备好药械等必备物资，随时待命前往灾区救援。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>专家远程会诊定方案</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨天，作为国家卫计委增派的上海第六人民医院骨科专家孙玉强主任医师、杨庆诚、韩培副主任医师等一到四川省人民医院，就投入到紧张的医疗工作中。一上午检查了25名病人，主要为四肢骨折病人。对于诸如陈旧性骨折畸形愈合，合并远端骨折、粉碎骨折等复杂伤情的伤员，前方专家还通过远程会诊，与六院张长青副院长、骨科副主任柴益民教授网上交流，制定最佳手术方案。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在当地的临时办公室，虽然缺少办公家具，但专家们因地制宜，或趴在窗台上，或站立倚着墙壁，迅速吃完简餐、盒饭补充体力，又投入到连续作战的大量手术中。手术一台接着一台……5岁小女孩在全麻下接受骨折闭合复位石膏固定术，孙玉强主任医师亲自为女孩复位，短短半小时完成。由于孩子年纪小，正处于生长发育阶段，今后愈合快，恢复好，不会影响功能。另一位肱骨下端骨折伤员，在专家组指导下，由当地医院医生完成，手术获得成功。据悉，明天专家组还将继续为多位病人进行双侧跟骨骨折、骨盆骨折等手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>骨科专家带病救援手术</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仅在四川省人民医院，大约就有100名创伤病人，且多为外伤，不少还表现出明显的肿胀。“确定合适的手术时间最为关键，太早或太晚都不好！ ”第九人民医院骨科副主任医师干耀恺说，专家组将视伤员的具体情况，或给予牵引，或拆掉石膏以松解，再确定最为合适的手术时间。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">记者了解到，干耀恺两年前还曾患过白细胞减少症，当时血小板和白细胞急剧下降，甚至出现40度高热等重症。经骨穿刺，诊断为骨髓增生。在用药之后，虽然病情有所好转，但至今，他的血小板和白细胞依然低于人体正常值。此次医院紧急选派，干医生正好又是灾区急需的创伤骨科方面的专家，不仅踊跃报名，还奔赴灾区救援第一线。干医生家中还有个6岁的孩子，他援驰灾区一线救援，更得到了家人的支持。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>灾区工作还要重防疫</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">国家卫计委首批医疗专家组成员、中山医院胸外科专家王群教授昨天进入雅安重灾区，在第一线筛查伤员，以防重伤员误判、误治等。对雅安当地的住院伤员经过排查后，目前当地绝大部分的重症病人已转往成都。目前医疗工作的重点在于院内救治，力争院内伤员“零死亡”，以最大限度地降低伤残率，同时积极开展伤员的心理辅导。此外，灾区工作还要重视“防疫”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此前，国家卫计委发出食品安全提示，尽可能选择新鲜的食品，不食用腐败、变质或霉变的食品；不采摘当地没有食用习惯的野菜及野生蘑菇，防止误食中毒；要选用清洁、安全的水源；不喝生水，尽量喝开水或瓶装水。在简易条件下，饭菜应现做现吃，尽量不存放熟食品。烹调方法以煮、蒸等彻底加热的方法为主，尽可能不加工和食用冷荤类食品；要讲究个人卫生，婴幼儿、孕妇和老人等特殊人群容易出现营养不良，造成人群免疫力降低，需要特别关注。一旦出现呕吐、腹泻等食物中毒症状，要立即停止食用可疑中毒食物并进行自救，并尽快就医等。</span>
</p>
