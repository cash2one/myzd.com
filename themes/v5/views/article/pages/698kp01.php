			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肝胆胰外科在腔镜手术的发展及建议</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如今腹腔镜胆囊切除术已取代传统的开腹胆囊切除而成为治疗良性胆囊疾病的金标准手术方式，同时腹腔镜手术也从早期治疗良性疾病向着恶性肿瘤根治性手术方向发展。尤其是在消化道肿瘤的微创治疗方面进展迅速，不仅经过了技术层面的突破，而且还根据标准化的术式制定了相应的规范和指南， 然而，作为最先点燃微创技术之星火的肝胆胰外科在腔镜手术的发展和规范方面远远落后于胃肠外科。其主要原因是：1）除胆囊切除术外，肝胆胰手术即使在开腹情况下技术要求很高，手术风险大。2）肝脏、胰腺为深在的实质性器官，缺乏明显的解剖间隙和解剖标志，增加了腹腔镜操作的难度。3）肝脏、胆道、胰腺均为非常重要的消化器官，发生病理改变时常表现形态上的变化和功能受损，一旦手术失败，再次手术补救的难度非常大，常导致患者的死亡和残疾。4）针对腔镜肝胆手术特点的专用摄像系统和特殊分离实质性器官的器械还不能满足临床需要，并且腹腔镜肝胆手术所需设备加多，价格昂贵，限制了基层和不发达地区此项技术的开展。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 具有代表性的成果包括：1）腹腔镜结合胆道镜和纤维十二指肠镜（所谓“二镜”或“三镜”联合）可以治疗大多数的胆囊结石合并胆总管结石。2）绝大部分的脾切除可以经腹腔镜手术安全顺利完成。3）在严格控制适应症的情况小，腹腔镜肝切除治疗肝脏良恶性疾病可以达到安全、有效和微创的目的。4）腹腔镜下胰体尾良性肿瘤切除，急性胰腺炎经腹腔镜引流、腹腔镜下胰腺内分泌肿瘤局部切除等手术均取得良好效果。5）腹腔镜下右半肝切除、右三叶切除、尾状叶切除、肝门部胆管癌切除、先天性胆管囊肿切除、门奇断流术、胰十二指肠切除术甚至活体供肝的切除等高难度手术均有成功经验的报道。广大人民群众不但要求治好病，对快速康复和提高生活质量的要求越来高。微创已完全不限于纯技术的意义，而是进化成医生和患者共同追求的一种理念——最佳治疗效果、最小的机体创伤、最大限度保持机体内环境稳定。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;措施：技术层面；针对技术难点进行多学科多中心的联合攻关，以期在腹腔镜下断肝技术、胆肠吻合和胰肠吻合技术、淋巴结清扫技术方面取得突破 &nbsp;管理规范层面：以专业学会牵头，组织多个大的医疗中心对各种腹腔镜肝胆胰手术进行技术规范。对于涉及观念改变的问题设计多中心的随机对照研究以充实证据和制定指南。如腹腔镜左肝外叶切除能否代替开腹手术、腹腔镜肝切除治疗肝癌是否具有优势、腹腔镜/胆道镜/十二指肠镜在治疗胆管结石中如何优化组合、腹腔镜下胆道探查一期缝合胆管能否代替传统的开腹胆道探查T 管引流术等问题。同时对一些技术含量很高、手术风险大的术式如腹腔镜下右半肝切除、右三叶切除、尾状叶切除、肝门部胆管癌切除、先天性胆管囊肿切除、门奇断流术、胰十二指肠切除术等应建立准入门槛，从而保证病人的利益、保障医疗安全。 &nbsp;</span>
</p>
