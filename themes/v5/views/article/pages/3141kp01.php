	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>面肌痉挛和三叉神经痛治疗的新技术</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;面肌痉挛，即脸部抽筋，是由于一侧面神经受到在颅内受到血管的压迫，长期和反复的血管搏动刺激，而产生的面部肌肉阵发性不自主抽动、眼睑紧闭和口角歪斜等。三叉神经痛也是由于同样的原因，产生其支配的面部的反复发作性剧烈疼痛，疼痛如针刺、刀割、烧灼或撕裂样，持续数秒至数分钟，突然停止，不发作时如正常人。疼痛的发作常由一触发点或称“扳机点”引起，触发点多数在上、下唇、鼻翼、口角或牙齿等处。刷牙、洗脸、讲话、吃饭或吹风等可以诱发疼痛发作，以至于病人不敢刷牙、洗脸和触碰面部。患者长期通过手压面部来减轻疼痛，以至于造成面部皮肤粗糙、增厚和眉毛脱落等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 由于患者发病的症状具有明显的特征，比较容易诊断。通常在明确诊断后，先选择药物等保守治疗，一旦保守治疗无效或副作用太大，即可选择微创手术治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 采用显微镜进行的微血管减压术已经成为国际神经外科公认的最理想的微创手术治疗方法，中山医院神经外科在此方面的治疗取得了非常理想的疗效。近年来，中山医院神经外科大胆创新、积极探索，引进国际最先进的神经内镜系统和技术，运用于微血管减压手术中，不仅进一步减少了手术创伤，而且疗效更加有效，使得面肌痉挛和三叉神经痛的手术治疗实现了突破性的进展。</span><br />
