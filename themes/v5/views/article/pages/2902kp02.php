			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>听神经瘤手术并发症及处理</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">神经外科的临床实践中，听神经瘤是一种常见的良性肿瘤，因为其位置较深，在桥脑小脑角夹角处，周围有脑干（桥脑）、小脑、三叉神经、面听神经、后组颅神经（第5-12颅神经）和椎动脉、基底动脉、岩静脉等重要结构，因此该部位手术可能导致的并发症，基本上与上述结构的损伤影响其功能有关。桥小脑角常见的肿瘤为听神经瘤、脑膜瘤和胆脂瘤（上皮样囊肿）等，该部位的手术并发症基本相似，这里以听神经瘤为例，简述手术可能遇到的一些并发症，供大家参考（靠近该区域的其他肿瘤，如小脑、脑干胶质瘤等也可参考此文）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp; &nbsp; 一. 术中并发症</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1.较大动脉血管损伤出血：术中切除肿瘤不可避免多多少少要出血，但有时出现肿瘤周围大血管损伤，如基底动脉、椎动脉或其较大的分支，会造成术中出血较多，导致术野不清楚，在止血过程中容易进一步损伤其他结构如脑干或肿瘤周围神经，引起严重后果。如果出血量大，会引起休克。由于现在绝大部分都是显微手术，这类大出血发生率很低。 &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2.脑干损伤导致心率血压影响：脑干是生命中枢所在，掌管呼吸血压和心率等，也是颅神经起源地及支配对侧肢体运动的中枢。术中分离肿瘤和脑干边界时，可能会伤及脑干，导致术中血压快速下降或上升/心率快速变慢或快，此时医生只有暂停手术，等血压心率恢复后再继续小心谨慎手术，如果再次出现类似表现，则可能就此终止手术，这样肿瘤可能有残留！极个别的，在术中就出现心率血压难以恢复，出现生命危险！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3.小脑挫伤脑肿胀：术中需要牵拉小脑向内侧以暴露肿瘤，有时会出现小脑挫伤出血/脑肿胀，影响术中肿瘤的暴露与切除，主刀医生可能会切除部分挫伤小脑组织，即使没有挫伤，有时为了切除肿瘤需要也会切除部分小脑组织。少数患者术后会出现小脑共济失调，如行走不稳/手指不灵活等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 4.远隔部位血肿：由于肿瘤被切除，体积逐渐减小，颅内压会有降低，极少数情况会出现手术远隔部位因颅内压降低移位所致颅内出血！发生率很低，但一旦出现处理会非常棘手！一方面手术部位的肿瘤尚未切除，另一方面颅内其它部位又在出血压迫脑组织向手术部位移动，使手术部位空间越来越小，无法继续手术！手术医生不得不匆忙停止正在进行的手术（有时甚至止血也无法满意止好），紧急关颅，紧急复查头颅CT，发现远处出血部位后，再进手术室先开颅清除远处血肿后，再重新从原入路进入继续肿瘤手术！！因此发生这种情况，十分被动和棘手！尽管医生会采取措施（如术中尽量避免快速下降颅内压）预防，但仍有极少数患者会发生。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 5.颅神经损伤，尤其是面神经损伤(面瘫)：由于肿瘤所在的桥小脑角及附近区域，是第5-12对颅神经的发源地。肿瘤与面听神经及三叉神经外展神经及后组颅神经关系密切，术中可能有不同程度的损伤，尤其是面神经。面神经损伤在术中并无严重的后果显现，但术后却出现明显的面瘫，影响外貌！</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp; &nbsp; 二.术后并发症</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1.术后出血再次手术：是术后十分紧急凶险的并发症，也是听神经瘤死亡的重要原因之一！术后颅内再次出血往往病情急，需要急诊再次手术，部分患者甚至来不及再次手术即已出现生命危险！出血可出现在手术部位，亦可能在远隔手术的部位出现，但是以前者更凶险，因为血肿近邻脑干，容易引起呼吸停止危及生命。如出现呼吸停止，应紧急气管插管，直接送手术室或行CT后直接送手术室。尽管手术医生在术中已确切止血彻底，但术后仍有出血发生的机率，具体出血原因很难确切查明。患者有高血压/糖尿病/血脂高/血管硬化或术后血压急剧波动，术后出血的机率加大，最易术后当天发生，亦有术后3天内发生者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2.脑干继发性损伤：术后再出血压迫脑干（如上述）或脑干水肿或小脑水肿压迫脑干等 ，导致脑干继发性损伤，严重者可能导致偏瘫/昏迷/呼吸和血压不稳，有的需要行气管切开，有的还需应用呼吸机辅助呼吸，是一种严重的并发症。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;3.脑积水：由于小脑水肿导致脑脊液循环受阻可能引起阻塞性脑积水，也有术后脑脊液吸收障碍导致的交通性脑积水，前者往往在术后数天出现，需要急诊行脑室外引流术，如水肿消失后拔出外引流，仍有脑积水，则需要再次行脑积水内引流术（多为脑室腹腔引流术）；后者多为术后2周后出现，有的甚至术后数月才出现，可以直接行脑积水内引流术。应重视术后急性脑积水的及时处理，如延误，可能出现病情突变形成脑疝，危及生命。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 4.面瘫：是听神经瘤术后最常见的并发症，表现为嘴歪眼睛闭合不全，影响外貌！即使术中保留了面神经，但仍难以避免术后不同程度的面瘫，因为解剖保留了面神经，但功能却可能有受损。有的患者对面瘫难以接受，提出可以部分切除肿瘤以保住面神经，残留肿瘤再行伽玛刀治疗，但即使这样术中也不能绝对保证面神经的不受损，因为术中情况千变万化，有时肿瘤血供很丰富，很难及时停止手术，不得不多切除肿瘤甚至全切除，这样术后就有可能出现面瘫；再者即使如愿部分切除肿瘤术后没有面瘫，残留肿瘤亦可以伽玛刀治疗，但残留肿瘤并没有根除，仍在颅内，需要长期复查，大部分可以长期控制，但仍有部分肿瘤再长大再次手术，第二次手术保面神经更难，因为有第一次手术的粘连，伽玛刀后也有增加粘连的可能。面瘫的危险并不在于影响外貌，而在于眼睛因闭合不全，容易感染发炎，严重者出现角膜溃疡穿孔，因此严重面瘫如出现眼睑闭合不全，应及时采取措施保护角膜，必要时行眼睑缝合术！面瘫后遗症有许多综合康复治疗手段，但基本上很难痊愈。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 5.声音嘶哑/饮水呛咳：是后组颅神经受损后所致，术后饮水呛咳，需要插胃管进食，一般在术后2周到3月可以拔出胃管，依靠对侧神经的代偿可以正常进食。声嘶往往较难改善。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 6.小脑共济失调：小脑受损出现行走不稳，手指不灵活。术后需康复锻炼，大部分会有不同程度的好转。少数少儿患者出现术后不说话，呼之不应，不吃不喝，或者烦躁不安，可能是术后缄默症的表现，与损伤小脑亦有关，多数症状会逐渐好转。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;7. 感染：包括颅内感染或肺部感染/泌尿系统感染，有的需要行腰穿脑脊液引流，有的需要气管切开吸痰。肺部感染以老年患者/术前长期吸烟者易发。如出现术后长期昏迷（如脑干伤/术后出血再手术），肺部感染难以避免，应及早行气管切开。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;看了以上的内容，患者朋友也不要害怕，不是每个听神经瘤手术都肯定会有上述并发症！这里描述的是常见的并发症总结，每位主刀医生都会尽最大努力、尽最大的心血去完成每一例生命的托付和信任，尽最大可能减少各种并发症的发生。</span><br />
