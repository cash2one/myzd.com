	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>湖南：贫困白内障患者可免费手术</strong></span><br />
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">新华网长沙６月６日专电（记者帅才）６月６日是全国“爱眼日”。记者６日从湖南省卫生厅获悉，湖南省今年将开展“贫困白内障患者复明工程项目”，为２．５万名贫困白内障患者实施免费手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据中南大学湘雅医院眼科主任夏晓波教授介绍，白内障是我国首位致盲眼病。每年新生白内障患者约４０万人。早期白内障一般不影响视力，但随着视力无痛性、渐进性减退，最终导致失明。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 夏晓波指出，目前白内障患者呈现年轻化趋势。白内障是一种可逆的致盲眼病，只要发现及时，可以通过手术完全治愈。全民认识白内障，发现白内障，防治白内障是防盲治盲道路上最重要的一步。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 夏晓波指出，白内障预防要注意生活小细节，做好预防工作，可以有效延缓白内障的发展和成熟。如佩戴深色防紫外线眼镜，减少紫外线对眼睛的照射；补充适量的维生素，养成良好用眼习惯；控制血糖、血压；防止机械性因素、放射线对眼睛的损伤；积极治疗其他相关眼病等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据中南大学湘雅二医院眼科贾松柏副教授介绍，白内障治疗可分为保守治疗即药物治疗和手术治疗。对早期白内障病人，以药物治疗为宜，但目前用于防治白内障的所有药物都只能延缓白内障的进展速度，而不能阻止白内障的发展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 湖南省卫生厅要求，湖南省各地要严格落实医疗费用补助和减免政策，充分利用居民医保和新农合基金、民政的特殊对象医疗补助资金、定点医院援助等，对贫困患者的基本手术费用减免，并积极争取落实配套工作经费。各地贫困白内障患者可凭单位或居委会、村委会出具的身份、经济状况和医保证明，到就近的定点医院接受筛查。</span>
</p>
