<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>赵金忠：关节镜修复损伤</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147004321619615" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 赵金忠不主张中年人进行健美类训练。身体周刊记者 徐晓林 图 　　1966年出生，博士学位，教授，主任医师，博士研究生导师。上海市第六人民医院运动医学中心主任。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵金忠擅长肩、肘、髋、膝、踝等关节病损及运动损伤的微创治疗，目前年手术量一千余台。就在日前，医学界普遍认同的“琅琊榜”——中国名医百强榜之骨科系列显示，赵金忠是中国唯一同时入选“运动医学与关节镜外科”肩关节镜手术和膝关节镜手术的名医。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2016年1月20日，上海迎来了30年来最强寒潮。上海市第六人民医院，11楼病房，29岁的杨磊躺在病床上。刚刚做完膝关节手术，他还不能自由行动。杨磊望向窗外，寒潮裹挟着冷雨，打在玻璃窗上，但他心里却是暖和的，这一天他已经等待了7个月，或者说更长。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2015年6月18日，杨磊从江苏扬州前往上海。此前，他查阅了大量关节镜手术和运动医学方面的资料，随后预约挂号。“2009年我踢球受伤，在当地医院做了膝关节的切开手术，但术后膝盖并不太平，每年都会发生一到两次剧烈疼痛。”自2010年起，杨磊疼了就去各地的医院检查，5年拍了四次片子，但情况并无好转。“一直到2015年5月，扬州的一名医生给我推荐了赵教授，他告诉我，以前手术并不成功，术后的膝盖，前十字韧带伴有撕裂。他说， 赵教授说能动手术就有救，赵教授说不行，那你的膝盖就被判 死刑 了。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个月后，杨磊前往上海求医；半年多后，他排期成功，进行手术；未来，他的膝盖功能有望恢复到受伤前的90%～95%，可以急停急转，甚至可以打羽毛球。杨磊求访的医生名叫赵金忠，他是上海市第六人民医院运动医学中心主任，也是六院关节镜外科和运动医学科创始人。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵金忠擅长肩、肘、髋、膝、踝等关节病损及运动损伤的微创治疗，目前年手术量1000余台，处于国际领先地位。就在日前，医学界普遍认同的“琅琊榜”——中国名医百强榜之骨科系列显示，赵金忠是中国唯一一名同时入选“运动医学与关节镜外科”肩关节镜手术和膝关节镜手术的名医。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">留学带回先进技术</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1966年出生的赵金忠本科就读河南医科大学，随后在上海医科大学攻读研究生、博士。1995年，博士毕业后，他进入上海市第六人民医院骨科工作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当时，医学界的国际交流已经频繁。1998年年底，赵金忠作为进修人员前往奥地利，其间他第一次接触到了关节镜手术。关节镜手术是通过切开皮肤数个“筷子”大小或更小的孔（5-10毫米），将摄像头、手术器具伸入人关节内，在显示器监视下，由医生操作，诊断和治疗各种关节疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，中国关节镜手术的基本技术，如半月板切除与缝合、韧带重建、滑膜清理等已得到普及，可以诊治的关节包括膝、肩、踝、肘、腕、髋等。不过，彼时是上世纪90年代，中国的关节镜技术基本处于空白阶段。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“出国交流期间，在学习了关节创伤等科目后，我首次接触到了关节镜，也找到了自己的兴趣。”赵金忠说，那时能申请工作签证，他人又勤快，奥地利老师带着他一起，做了一些简单的关节镜手术，“我基础的关节镜技术培养是在奥地利完成的。这一次短暂的留学经历，让我看到了未来的方向，微创是一种发展趋势。至今，我依然庆幸当时的选择，这个方向是对的。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此后，赵金忠又先后前往德国、韩国和美国等地工作和学习，掌握先进的运动损伤治疗理念和关节镜治疗技术。2000年，赵金忠成为上海市第六人民医院骨科关节镜和运动医学特色专业的创始人，开始在国内进行技术普及和概念普及。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">每天一两百个病人</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">以周为单位，赵金忠的工作量非常大。星期一全天和周四上午门诊，周二周三是手术日，周四下午和周五则是科研、教学和总结一周的工作。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多年以来，赵金忠保持着固定的作息规律。正常情况下，每天清晨5点45分起床，7点就到单位，巡视病房，提前准备。“我家距离单位只有4公里路程，路上时间花很多，也是一种浪费。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊日8点开诊，一般赵金忠早上要看80-100名病人，中午五分钟吃一个盒饭，下午再看60-80名病人，傍晚5点门诊结束。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“我看病人很快，一般3分钟左右。来找我的病人，全国各地都有，不少人甚至在国内兜了好几圈了，他们病例材料齐全，我需要做就是明确诊断和治疗方案。”赵金忠建议，病人来找他之前，可以提前在六院初诊，该做的检查如X光、核磁共振等提前完成，“病人来了，检查材料齐全，可以最大限度地利用时间，我也能为更多的病人服务。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊结束后，赵金忠返回病房查房，与病人术前谈话，收集病人功能评价的资料，研究手术方案，回家一般都是晚上7点以后。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术室没有停歇。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关节老化治疗、韧带重建、髌骨错位、肩袖损伤……赵金忠关节镜手术的门类繁多，每年1000多台的手术量，很多外国同行都用“amazing”来形容。但其实，排队等候赵金忠的病人更多，一年里有3000多个病人慕名而来。也正是因此，非紧急病人往往需要等待3个月、6个月，甚至更长时间才能手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“速度快”、“稳定性强”和“成功率高”是很多业内人士对赵金忠手术的评价。1月19日，记者跟随赵金忠进入手术室采访，当天他将进行13台手术。8点35分，赵金忠进入1号手术室准备。8点45分，第一台手术开始。病人半月板损伤、内侧滑膜皱襞增生。关节镜插入、内侧滑膜皱襞切除、关节清理、半月板修整，9点手术结束。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">没有停歇，赵金忠转入2号手术室，此时第二位病人已经就位。9点02分，手术开始，病人肩关节滑囊有炎症、严重的纤维化和挛缩，肱二头肌腱部分断裂，肩袖冈上肌部分撕裂……9点10分，赵金忠开始关节囊挛缩松解，9点16分，磨肩峰下骨赘，9点24分，修复缝线在病人的肩关节内部穿梭，肩二头肌腱固定，肩袖撕裂修补。9点40分，第二台手术完成。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">依然没有休息，赵金忠返回1号手术室。9点46分，第三台手术开始，这是一台复杂肩袖损伤Trans-Tendon修复，赵金忠为了保留病人原有的连接不打断，关节镜入路采取了冈上窝入路，这一新通路增加了手术的技术难度。约2个小时，11点40分，手术成功结束，不过这一手术时长仍是世界领先。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随后，赵金忠进入休息区，吃了一个盒饭，费时5分钟。他又返回2号手术室开始手术。这样的节奏一直持续，晚上7点32分，当天最后一台手术完成。近11个小时，手术期间，赵金忠一直是站着，除了午饭的5分钟，不曾有坐下的机会。“今天还算结束得早，遇到复杂的手术，做到晚上十一二点都有过。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“铁人”是学生们私下对赵金忠的评语。戚文潇是赵金忠的研究生，跟随他已有半年，“赵主任工作敬业，体力也超好。我们跟着他一起做手术，是要用车轮战的。一般上午跟下来，很多人都站不住了，下午换一拨继续上，但他从头至尾都是一个人，连坐都不坐一下。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">没有音乐，电刀刨削、电钻打入和偶发的钉锤声，赵金忠的手术室安静而高效。从业至今，赵金忠已经完成了超过1万台关节镜手术，成功率达到95%以上。在手术日，开刀10-12个小时是常态，“这么多年，体力上都练出来了。我是越开越兴奋，特别是遇到高难度的手术，外科医生的兴奋点，探索高精尖技术，做到别人做不到的，这也是我和我的团队在关节镜手术和运动医学领域，始终站在国际前沿原因之一。”科研和临床同行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">精准医疗、基因测序、3D打印技术……都是未来中国医疗发展的方向。赵金忠告诉记者，他们的团队也在进行各种研究和创新，“关节外科中软骨细胞一层一层，不同层有不同的细胞构架，这都是我们研究的方向。现在国际上也有将干细胞直接注入关节，90%的软骨能够重新生长，但是这些都还在动物实验的阶段，离我们的临床还有很远。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“科研和临床，我们需要两条腿走路。”为了解决病人痛苦，赵金忠开创性地建立了多种关节镜手术方法。创立复杂性肩关节脱位的“满汉全席”治疗技术，使有严重骨缺损、关节囊韧带缺陷或者脱位术后复发的患者有了更可靠的治疗措施；创立肩袖重建技术，针对巨大不可修复肩袖损伤，成功使许多严重肩袖损伤的患者恢复了肩关节功能；对于因肩部严重骨质疏松而不能使用锚钉修补的肩袖损伤，创立Fix-to-Suture Base修复技术，不使用锚钉即能规避局部骨质疏松完成肩袖修复；对于严重的肩关节粘连挛缩，创立自外向内的关节镜下松解技术，成功规避了腋神经损伤的风险。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在膝关节领域，赵金忠擅长各种复杂膝关节韧带损伤和髌股关节紊乱的治疗，膝关节稳定性重建的水平处于国际领先地位，其一系列创新技术荟萃于《膝关节重建外科学》。他还是国际关节镜外科杂志《关节镜检查（Arthroscopy）》和北美运动医学杂志的编委。2005年因“膝关节稳定性重建”方面成就，他分别获得上海市医学科技三等奖、上海科技进步三等奖。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最值得称道的是，赵金忠创立的《膝关节僵硬的Zhao松解术》是中国骨科界唯一被收入“骨科圣经”《坎贝尔骨科手术大全》的技术。翻开书页，赵金忠说，他要特别感谢一个人——她的太太杨敏，“我的太太很早以前是中山医院骨科的护士长，后来为了我放弃了自己的事业，照顾家庭，照顾儿子，同时也成了我的 秘书 。我的每一个病人都有一个数据库，临床资料的收集、输入、整理，都是太太帮我完成的。”赵金忠说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">志同道合的团队</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除了家人，志同道合的伙伴，还有他的团队。上海市第六人民医院关节镜运动医学技术始于1990年，但作为尝试和摸索，每年的手术量并不大。2000年，赵金忠成为上海市第六人民医院骨科关节镜和运动医学特色专业的创始人，开始在国内推广关节镜技术。2003年，六院关节镜外科成立。2013年，关节镜外科更名为运动医学科，并成为首批国家级临床重点专科。目前，科室拥有临床医师7人，护士20多人，所有医师均有博士学位或者在读。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">护士长秦瑜在关节镜外科已经工作6年，她说这份职业很辛苦，但也干得无悔。“赵主任治学严谨、工作务实、手术敬业，而严谨、务实和敬业也是我们这支团队的风格。去年4月27日，我们搬了病房，床位从47张扩到68张，但人员上并没有太大的增幅。就这个搬家准备就花了近两个月，所有的活，我都要跟赵主任商量，然后把细节做好。氧气管道、床位安排、幕帘、叫铃，每一个环节都要注意到。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">秦瑜顿了一顿说：“对了，赵主任吃饭是出了名的快，你知道吗？其实手术室的医生都吃得很快，团队就是这样，等待对病人或者家属而言都很漫长，为了减少他们的等待，我们科室医护人员一直在努力，最后这些努力变成了我们的生活习惯。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在赵金忠的带领下，六院运动医学科声名远播，关节镜下微创修复肩、膝、肘、髋、踝等大关节的运动损伤，尤其是肩、膝关节修复重建技术，居于国际前列。赵金忠说，2016年他们科室的收治病人和手术量将再增加约30%，预计收治病人数达4000人，手术3900例，“2016年，我对各个医生也有要求，不是为了开刀而开刀，而是要始终站在制高点上。临床研究和技术研究，最终变成医疗成果，最终体现在患者对治疗结果的满意度上。”&nbsp;</span>
