		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>江苏省首例“冻卵胎儿”长势喜人 发育正常（资讯）</strong></span>
</p>
<p>
	<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一名正常怀孕困难女士的卵子，在零下196度低温下保存3个月后，经过“解冻”成功地与精子结合，发育成正常胎儿，且为双胞胎。江苏首例“冻卵胎儿”17日在江苏省人民医院宣告成功，“冻卵”孕育成功给更多生育困难女性带来了当妈妈的希望。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 据了解，“冻卵胎儿”的母亲是一名来自北方城市的31岁女士，结婚6年未孕，检查发现排卵障碍，输卵管通而不畅，经促排卵+人工授精治疗4次未成功。2005年11月，该女士来到江苏省人民医院生殖中心，要求施行体外受精-胚胎移植治疗，男方例行检查后未发现异常。在夫妇双方知情同意下，2005年11月如期进入治疗流程，当女方卵泡成熟需要取卵时，其军人丈夫因特殊公务不能如期到中心取精，为此中心建议其采取“冻卵”的方式保存卵子，夫妇双方同意后完成“冻卵”，并于2006年2月顺利进行冻卵复苏+体外受精+胚胎移植术，现已妊娠5个月，胎儿发育正常。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 江苏省人民医院生殖中心主任刘嘉茵介绍，卵子冷冻是辅助生殖技术领域中一项前沿技术，冷冻技术分慢冻和速冻两种，速冻又称作玻璃化卵子冷冻，在不到1秒的时间内就要将冷冻温度降到零下196度，以保证对卵子内部结构的损害降至最低。世界首例玻璃化卵子冷冻试管婴儿1999年诞生，美国首例玻璃化卵子冷冻试管婴儿于2005年7月诞生。目前，我国能开展此项技术的医院只有4家，省人民医院是江苏第一家开展此项技术并已成功妊娠胎儿的医院。</span>
</p>
