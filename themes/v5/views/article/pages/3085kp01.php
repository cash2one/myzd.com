<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>帕金森病科普</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">帕金森病科普问答-手术治疗 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一、帕金森病（PD)是否都要手术？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">并不是所有PD都要手术。立体定向手术对震颤和僵直效果较好，对运动缓慢效果较差。一般认为其手术对应症：年龄在70岁以下，长期服药无效或副作用明显，工作和生活能力受到较大限制；临床分级Ⅱ～Ⅳ，且无明显手术禁忌症者--即严重高血压、心、肝、肾、肺、糖尿病患者，不能手术。北京301医院功能神经外科凌至培</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二、帕金森病术前需要做哪些检查？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（1）帕金森病术前需要进行头颅CT检查，最好是核磁共振检查；了解有无脑萎缩；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（2）心电图；</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（3）血生化检查--了解有无糖尿病、肝、肾功能等情况。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三、治疗帕金森病有几种手术方式？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">目前帕金森病手术治疗方式包括：⒈立体定向靶点射频毁损术（包括所谓细胞刀）；⒉深部脑核团刺激术（DBS）；⒊脑组织移植术。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">四、帕金森病怎样选择手术方式？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">帕金森病选择立体定向靶点毁损术，对于双侧手术，可以先一侧行毁损术，另外一侧选择深部脑核团刺激术或者双侧都选择脑深部刺激术。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">五、帕金森病手术有无危险性？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">帕金森病手术如同其它脑外科手术一样具有一定的手术并发症，只要手术适应症选择适当，手术并发症发生率较低。最大的并发症是颅内出血引发偏瘫。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">六、帕金森病手术后能发生哪些并发症？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">帕金森病手术可能发生颅内出血，偏瘫，语言障碍，吞咽困难，一侧肢体麻木，平衡障碍等并发症。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">七、帕金森病手术后是否需要再服药？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">帕金森病手术只是缓解其大部分症状，不是根治，另外手术只进行一侧，所以手术后仍需要服药，但可减少药物的用量。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">八、有无“细胞刀”治疗帕金森病？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">没有所谓“细胞刀”，只是一种微电极记录系统，他是一种帮助手术者验证所选择的靶点是否正确的电生理仪器。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">九、什么叫做微电极记录系统？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">采用立体定向技术将一根很细电极送入脑内靶区，这根电极末端直径一般为1-2微米，尖端裸露长度为 15-140微米，外层镀铂或铱；电极杆为环氧树脂绝缘，放入25号不锈钢套内。用螺旋微型推进器推进微电极。此电极入脑内将各处细胞生物电引出，放大、显示、记录等步骤。帮助外科医生对目标区进行精确定位，并了解其电生理状态。所以不是什么细胞刀。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十、微电极记录与"细胞刀"二者的关系</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">微电极记录系统="所谓细胞刀"，二者是一个检查仪器。这种仪器不是用来去除脑内病灶的一种类似手术的"刀"。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十一、微电极记录对治疗PD有多大价值？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">微电极记录在治疗PD中的意义在于：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（1）了解脑深部某些核团的边界及其周围的主要的结构（如视束、内囊等）。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">（2）由于深部核团内不同部位细胞有不同放电形式，在手术定位中有一定价值。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十二、微电极记录在治疗PD中有无危险性？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">微电极记录在治疗PD中是有危险的，因为要达到微电极制图的目的，就需要多次多方向穿刺脑组织，一般均采用3-8个针道穿刺，平均3-5个针道，最多有报道13个针道穿刺。有可能出现穿刺道和目标区域的出血或脑组织损伤。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十三、什么叫射频毁损治疗帕金森病？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">射频毁损治疗帕金森病，已有五十年历史，将电解转换成热能，通过热敏电阻或热敏电偶方式，把医疗上所用的温度控制在37℃～90℃之间，平时所用的温度60℃～80℃，阻断与PD相关的部位的通路， 达到治疗的的目的这种仪器叫温控射频仪。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十四、什么慢性脑刺激治疗PD？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">深部脑刺激（deep brain stimulation），简写为DBS，就是向大脑深部核团植入电极，传送高频电刺激来控制帕金森病等疾病症状的一种方法，可通过体外调控刺激频率（赫兹），电流，电压来获得最佳治疗效果。这种方法具有脑损伤小，副作用少，可恢复性，可调节性及可行双侧手术等优点。对年老和双侧帕金森患者更适用，大大地提高了手术的安全性和治疗效果。DBS和射频治疗的根据区别在于后者DBS可逆性。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十五、DBS可治疗那些疾病？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">DBS可用来治疗帕金森病、原发性震颤、慢性疼痛等。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十六、什么样帕金森病选择用DBS？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">帕金森病经药物治疗无效或药物的副作用较大而不能继续服用者；震颤伴言语障碍者；双侧帕金森病症状，已经有过一侧毁损术；需要进行双侧手术的病人等均为DBS的治疗对象。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十七、DBS治疗帕金森病效果如何？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">临床研究显示DBS治疗帕金森病（PD）具有显著的治疗效果：⑴可控制帕金森病的震颤、僵直、运动缓慢或行动不能、平衡障碍等症状；⑵减少口服药物的剂量；⑶能明显地提高患者日常生活质量和能力。经验表明对左旋多巴反应良好的患者效果显著，对左旋多巴反应差的患者可以行DBS治疗，效果略差些。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十八、DBS植入病人后注意什么？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">大多数医疗设备检查是安全的，以下几点应注意：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑴不宜直接对着脉冲发生器（IPG）植入处热疗；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑵不宜直接放射治疗；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑶有强磁场不安全。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">⑷飞机场安全检查通道不要通过。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">大部分家用电器不影响DBS，如电视机，电冰箱，微波炉，短波收音机等。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">十九、植入DBS有无后遗症？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">DBS治疗目前尚无明显的后遗症，但手术有一定的风险如脑内出血、癫痫、感染等。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二十、植入DBS是否一劳永逸？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">植入DBS后的随访非常重要，通过调节刺激频率、电流、电压，以达到最佳疾病控制和最少副作用。每年随访1-3次，进行调控检测。由于该治疗方法不是PD的病因性治疗，帕金森病病人病程仍在进展，仍在自然恶化，所以DBS不是一劳永逸。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二十一、植入DBS数年后无电怎么办？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">DBS的脉冲发生器的电池一般使用5-10年，无电时，仅需更换皮下的脉冲发生器即可，电极和导线不需更换。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二十二、什么叫脑组织移植治疗PD？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">PD病因主要是脑内某些部位的多巴胺神经细胞缺少，多巴胺的含量减少，而出现的一系列症状。将含多巴胺较丰富胚胎组织，经过处理移植入PD病人脑内，使其成活并具有分泌功能， 从而达到治疗P.D。（已经过近20年实践，很不理想，只有短暂效果）</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二十三、什么叫基因治疗PD?</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">就是向脑内缺乏某些物质蛋白引入外源基因，来纠正或补偿其基因缺陷，从而达到治疗目的叫PD 的基因治疗。它是通过分子、基因水平上操作，提高脑内多巴胺的浓度，而达到治疗PD目的。目前尚处于动物实验阶段。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二十四、PD手术治疗可根治吗？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">目前PD的手术治疗只能使症状得到改善，而不是根治。将来随着科学技术的发展，脑组织移植及基因治疗有望根治PD。</span> 
</p>
