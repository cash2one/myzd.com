			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>现有条件下手术时机可适当提前</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术时机对帕金森患者十分重要</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于帕金森病患者来说，手术时机的选择是比较重要的，“鉴于对手术的顾虑，不少患者都选择在中末期进行手术，这并不可取，过早进行手术或者盲目延迟手术都是不明智的，手术时间过晚，患者的脑部损伤也比较大，细胞的代偿能力较差，选择手术为时已晚，我个人认为可以适当提前，不宜过晚”，张振馨介绍，早期进行手术，借助细胞的代偿能力，也有利于患者的恢复</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，年龄和病程也是选择治疗方式的重要因素。年龄较轻的患者有更多的生活质量和僵直症状改善，更少的认知并发症以及更为缓慢的中轴症状的恶化，病程达5年以上，特别是出现药物疗效已明显下降或出现严重的运动波动或异动症，是考虑接受脑深部电刺激（脑起搏器）植入的最佳时机。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">之所以建议将手术时机适当提前，张振馨告诉记者，从目前的临床经验来看，在一些水平较高的医院，目前的技术水平已经允许将手术时机适当提前，如结合核磁共振、同位素检查、嗅觉检查等多种方式，而且通过内外科的配合，能够为患者提供更准确的诊断。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊断准确性是影响手术时机关键因素</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张振馨表示，之所以建议患者在病程达到一定时间后再进行手术，诊断水平是关键因素。“诊断的准确性是影响疗效的关键因素，诊断准确的病人在手术后的疗效一般都比较令人满意，从临床经验来看，患者疗效不理想的情况多与诊断相关。如多系统萎缩，病症表现与帕金森病极为相似，都是从身体一侧发展至另一侧、初期服药效果明显等，但对于多系统萎缩患者来说，药物维持疗效的时间比较短（有些药物的药效维持在半年左右），如果医生经验不足，容易误诊为帕金森病。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">DBS或具神经保护功效</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于可适当提前进行手术的建议，张振馨还表示，“我在临床中体会到DBS（脑深部刺激疗法）能够达到比较好的疗效，有些患者在拔掉后生活状况依然比较理想，这可能与DBS具有神经保护作用有关，而且有患者可以在手术后逐渐减少用药量，这可能也说明DBS具有一定的神经保护作用。”</span><br />
