			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>刘迎龙建议鼓励医师开设儿科诊所缓解看病难</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;大众网北京3月8日讯儿科医生少、工作压力大，短时间内如何缓解？在8日上午的全国政协十二届四次会议医卫组联组讨论中，全国政协常委、北京安贞医院小儿心脏外科主任刘迎龙结合工作实际支了一招，他建议政府鼓励在职副主任医师以上的医生，尤其是儿科医师开设儿科私人诊所，或挂靠到社区医疗机构坐诊，缓解儿童病患看病难的问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “我有个媒体的朋友，大年初一去给孩子看病，挂了特需号还排到200多号，从早晨一直等到晚上5点才看上病。孩子一直发烧40度。”在发言中，刘英龙先给大家讲了个故事。他认为，儿科医生待遇低、工作强度大、职业风险高并且医患矛盾多，这直接导致很多毕业生不愿意到儿科医院找工作，登记在册的儿科医生流失现象也很严重。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刘迎龙建议，国家应该支持和鼓励在职副主任医师以上的医生，特别是儿科医师，开设儿科私人诊所，或者挂靠到社区卫生机构开办工作室，利用休息时间为孩子们进行治疗。“可以在晚上7点到9点半，或者周末，都可以坐诊”。刘迎龙说，在医生指导下，大多数儿童常见病在家门口就可以治疗。医生和患者是邻居，彼此了解情况，医生不仅治病，还可以与孩子、家长在感情上交流。如果病情有变化，可以及时转到大医院。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刘迎龙还建议政府对医生开办儿科诊所出台相应的扶持政策。如，收取30元的诊疗费，政府再配套30元，将这部分收入免税，并在医保报销、开处方药等方面给予支持。</span>
</p>
