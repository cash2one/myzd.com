	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>超声引导消融治肝癌 精准治愈率高</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<img src="<?php echo $baseUrlImage; ?>article/doctor/3079/1.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">医学指导：中山大学第一附属医院超声科谢晓燕 教授</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">肝癌是“癌中之王”，是临床上最常见的恶性肿瘤之一，全球发病率逐年增长，位居肿瘤相关死亡的第3位。我国每年因患肝癌死亡的人数超过10万人。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">对于肝癌患者，毫无疑问，手术治疗是目前治疗肝癌最为有效的方法，但目前突出的问题是肝癌的远期疗效并不理想，术后复发再切除、再复发与转移。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">超声引导消融可“精准定位”治疗肿瘤</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">超声引导消融治肝癌是在超声图像的实时引导下用专门的治疗针经皮肤穿刺进入肝脏肿瘤内进行治疗，实际上是热效应原理，使肝癌组织局部在几分钟内达到70℃-110℃的温度，引起肿瘤靶组织发生凝固性坏死，而周围组织极少或不受损伤，达到“烧死”肿瘤细胞的目的。这种微创治疗方式的最大特点是精准有效，但创伤小，对患者全身影响小，不破坏患者的免疫力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">超声引导消融治肝癌 一次性治愈率可高达97%</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3公分以下的肿瘤的一次性肿瘤灭活率高达95%～97%，3至5公分的肿瘤的一次性肿瘤灭活率达85%～90%。“超声引导消融是复发性肝癌的首选。肿瘤不超过5公分，肿瘤数量不超过5个适宜于消融治疗。”谢晓燕主任补充道，有肝癌患者在1992年做超声消融手术后至今未复发，同时，手术病人最小仅9个月大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">哪些肝癌患者适合消融治疗呢？谢晓燕教授谈道：“主要治疗5cm以下复发性肝癌患者，3cm以下的原发性肝癌，特别适宜于不适合手术的肝癌患者”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">超声消融治疗肝癌有哪些优势？谢晓燕教授指出：微创， “针”在超声引导下精确进入肿瘤内，无疤痕，无缝线，避免了大手术对全身“元气”的伤害；.治疗时间短，疗效高，可局部麻醉或全身麻醉下，单发病灶≤5cm的肿瘤有希望一次灭活，一次治疗时间约12～36分钟；对于无法手术切除，血管癌栓行成或者是肝功能差的肝癌，可以达到姑息治疗，减缓疼痛、延长生命，提高生活质量的作用；可以反复多次治疗，且费用便宜，相对于手术费用低。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">据悉，手术中90%的患者局麻，静脉镇痛，患者术中可对话。小孩、75岁以上的老人、肿瘤较大者需进行全麻，以免影响血压和呼吸。超声消融手术需12～36分钟，患者检查、消融、观察，只需三天便可出院，明显缩短了病人的住院时间。治疗后机体恢复快，清淡饮食即可。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">专家建议每年至少体检一次 特别是45岁以上者</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">谢晓燕教授建议每年至少进行一次体检，年纪越大检查频率应越高，密切关注甲胎蛋白的变化，通过CT或MRI（磁共振成像）和超声造影等影像综合分析，若发现肿瘤则根据实际情况采用手术治疗或消融治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">至今，中山大学附属第一医院介入超声科已经完成数千例超声消融手术，其介入超声科、肝胆外科等几个科室共同组成一个团队，专门为患者提供超声消融技术。超声消融治疗费用大概1.5～2.5万，可用医保报销。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">谢晓燕教授希望这项技术能推向社会，能更广发的推广和普及，能真正造福大众。</span>
</p>
