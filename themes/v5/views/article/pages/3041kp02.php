			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>前列腺癌的筛查</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">自 1979 年前列腺特异性抗原（PSA）从前列腺组织内分离并提纯，且于 1986 年首次应用于临床后，血清肿瘤标志物 PSA 已逐渐成为颇具临床价值的前列腺癌检测指标。30 余年来，其在前列腺癌早期筛查、风险分层、诊疗和预后评估中的应用越来越广泛。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">日前，在北京举办的肿瘤标志物大师班交流会上，西班牙巴塞罗那临床医院生化实验室癌症研究中心主任 Rafael Molina 教授就 PSA 的研究发展、医学价值及其对前列腺癌管理的应用进行了深入探讨。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">合理应用 PSA 检测辅助前列腺癌诊断与风险评估</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">作为人类组织激肽释放酶基因家族中的一员，PSA 是由前列腺上皮细胞分泌的一种丝氨酸蛋白酶。但 PSA 并非前列腺组织特异性，其在良性前列腺增生（BPH）、前列腺炎和前列腺癌等前列腺疾病，以及其他非前列腺组织中也均可分泌产生。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">研究发现，在女性的胎儿羊水、支气管肺泡灌洗液、乳腺囊肿、乳汁和卵巢囊肿等部分体液中同样可检测到 PSA，且在女性血清中，PSA 处于非常低的浓度水平。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">PSA 在血清中主要存在结合与非结合两种形式，血液中绝大多数 PSA 与多种内源性蛋白酶抑制剂相结合形成复合 PSA，如与α1 - 抗糜蛋白酶（ACT）结合构成 PSA-ACT，这是血清中复合 PSA 的主要构成形式，也是目前免疫学方法能检测到的主要成分，因其他几种结合形式的抗原决定簇被人α-2 巨球蛋白所掩蔽，不能为 PSA 检测抗体所识别。剩余少部分 PSA 在血清中处于游离状态，成为游离 PSA（fPSA）。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，临床检测的总 PSA（tPSA）事实上仅是 fPSA 与 PSA-ACT。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">tPSA 和 fPSA 在血清中的半衰期只有几个小时，且随着时间的推移，稳定性下降，尤其是 fPSA 相较更不稳定，因此，PSA 标志物的实验室检测必须注意样本保存以保持 PSA 的稳定性。欧洲肿瘤标志物组织（EGTM）明确提出了实验室检测中样本保存的推荐操作方法。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">用于肿瘤标志物检测的血液标本，应在采集后 3 小时内离心分离血清，并根据进行检测的时间，选择正确保存条件。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于 tPSA 而言，其在 2～8 ℃ 下可维持 5 天短时间稳定，在 - 20℃ 以下可保存 6 个月；而对于 fPSA，拟放置 24 小时内检测的标本应保存在冷藏温度以防 PSA 降解，如标本分析需延迟超过 24 小时，需至少在 - 20℃ 下冷冻保存，而长期保存的样本最好储存于- 70℃ 以下。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，临床正在研究 tPSA 与 fPSA 在血浆中的稳定性，初步结果显示它们可在 2～8 ℃ 中稳定保存 60 小时。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">世界卫生组织（WHO）于 1999 年发布的 PSA 校准方法（WHO 96 / 670）将截断（cut-off）值定为 3.1ng/mL，一般超过 10ng/mL 就要考虑罹患前列腺癌的可能。但研究发现，因血清 PSA 浓度在前列腺增生和前列腺炎中均有升高，前列腺良性疾病与前列腺癌在 2～10ng/mL 时有交叉情况，由此存在很高比例的假阴性和假阳性结果。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">若假阴性出现在 2～4 ng/mL，可导致 15% 癌症漏检；当假阳性在 4～10 ng/mL 出现时，被认为是判断前列腺癌的灰区，65% 的良性疾病会被错判为癌症。此时，可通过结合 fPSA 计算游离前列腺指数（%fPSA），以提高前列腺癌检出率。在 tPSA 水平为 4～10 ng/mL 的患者中，以%fPSA 作为标准可减少 35% 不必要的活检，且前列腺癌检出敏感性可达 94%。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，因 PSA 与雄激素水平调节密切相关，在前列腺增生的治疗中使用 5 - α还原酶抑制剂等抗雄激素治疗可降低 PSA 40%～50% 的浓度，且治疗结束后其影响时间最多可保持 6 个月，但不影响 f/tPSA 指标，对于这些患者的 PSA 水平评估需做相应调整。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除了辅助诊断外，前列腺癌应根据血清 PSA 联合 Gleason 评分和临床分期分为低、中、高三个风险等级，以指导预后和治疗。Gleason 评分由国际泌尿病理协会于 2005 年颁布，目前已成为前列腺癌病理分级标准。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">通过 Gleason 评分形成癌组织分级常数，其波动范围为 2～10 分，2 分提示侵袭性最小，10 分侵袭性最强。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当 Gleason 评分 ≤ 6 且 PSA＜10ng/mL 及临床分期 ≤ T2a 时，判断为低危；若 Gleason 评分 ≤ 7 或 PSA 为 10～20ng/mL 或临床分期 ≤ T2b，则属于中危组；如 Gleason 评分＞7 或 PSA＞20ng/mL 或临床分期为 T2c，说明为高危组。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主动监测成为国际 PSA 诊疗指南推荐的保守治疗策略</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，国际上 EGTM、欧洲泌尿外科学会（EAU）、美国临床生物化学学会（NACB）、美国泌尿外科协会（AUA）等组织均发布了前列腺癌的相关指南，以指导临床诊疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">美国癌症协会（ACS）推荐医生每年对所有 50 岁以上的男性进行 PSA 检测和直肠指检（DRE），尤其是有非洲裔血统和有前列腺癌家族史的 65 岁以下高危男性，则需从 45 岁开始每年进行两项检测；而对于家庭诸多成员都有罹患前列腺癌家族史的超高危男性，则需从 40 岁开始每年进行两项检测。当 PSA 水平 ＜ 2.5 ng/mL 时，仅需要每 2 年复检一次，但若高于 2.5 ng/mL，则须每年都进行复检。而对预计生存期低于 10 年的患者，筛查益处不大。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，Molina 教授介绍了一项覆盖欧洲八个国家、入组 50～74 岁男性人群的欧洲前列腺癌随机筛查研究（ERSPC）。该研究将人群随机分为定期进行 PSA 筛查组与不限定是否进行 PSA 检查的对照组，筛查组进行 PSA 检查的间隔为 4 年，以统计核心年龄段为 55～69 岁受试者的前列腺癌死亡率。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">结果发现，随访至第 9 年时，核心年龄段定期筛查组与对照组的前列腺癌死亡率之比为 0.85；随访至第 11 年时，比值为 0.78。近期，在《柳叶刀》杂志上公布的 13 年随访结果显示，在 55～69 岁男性中，PSA 筛查使前列腺癌死亡率下降了 21%，而且与随访 9 年和 11 年的结果相比，这种获益呈增长趋势。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尽管如此，EGTM 指南指出 PSA 筛查也存在潜在伤害，包括过度诊断和随之而来的过度治疗。对其缺陷和对死亡率降低的进一步量化评估仍是决定人群是否进行 PSA 筛查的先决条件。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为降低临床诊断为低级别风险前列腺癌患者过度治疗的比率，采取定期 DRE、PSA 检测以及定期活检等手段的主动监测成为当前保守治疗的主要策略，低风险组患者可以选择观察等待病情进展情况而延迟干预治疗方案。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">EAU 2011 年指南建议了进行主动监测的患者指征，主要包括临床分期 T1～T2 分化良好的前列腺癌患者，Gleason 评分 ≤ 6、PSA&lt;10 ng/mL、阳性活检数 ≤ 3 或每条穿刺标本肿瘤 ≤ 50% 的低危患者。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Molina 教授表示：「在筛查前，建议医生和患者讨论检测项目的选择、益处和可能的副作用，以便患者根据情况做出自己的选择，对有早期诊断要求的人群不应拒绝进行 PSA 筛查。」</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">然而，由于 tPSA 检测方法多样，不同厂商试剂、不同检测方法之间差异水平达 10%～40%。Molina 教授指出：「PSA 检测方法的不一致使其价值长久存在争议。由于不同检测方法产生的结果不同，PSA 就会失去其在诊断和预后方面的重要价值。」</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为提高 PSA 检测的特异性、增强 PSA 应用价值，临床医生需结合年龄特异性参考范围、PSA 抗原密度（PSA/D）、速率（PSAV）、游离与总 PSA 比（f/t）、外周带 PSA（PSA-TZD）检测等附加指标综合判断。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然当前不同的 PSA 检测方法之间差异仍较大，但随着诊断技术的改进，PSA 检测精准度正在不断提高。罗氏诊断 Elecsys? PSA 和 fPSA 检测拥有 WHO 的标准溯源，只需一管血，18 分钟就能为临床提供准确结果和高医学价值信息，同时为患者长期随访提供了可靠依据，有助于实现前列腺癌的高效管理。</span><br />
