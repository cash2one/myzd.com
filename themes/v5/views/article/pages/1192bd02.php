		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>遍地开花令人忧 专家吁器官移植不能"无序竞争"</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中新网9月23日电新闻晨报报道，针对国内目前器官移植领域的无序竞争状态，卫生部副部长黄洁夫日前明确表示，中国将对器官移植实施准入制度。上海相关专家在接受记者采访时也纷纷呼吁：器官移植技术含量高，不宜“遍地开花”，应尽快给器官移植建立门槛，对已有的器官移植中心要进行质量监控，每年进行检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 有近10家医院开展器官移植</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 器官移植作为一个新兴学科正日益受到综合性大医院的青睐，上海目前有近10家医院开展器官移植。中国自2000年以来，器官移植数量每年以5000例以上的速度递增，上海目前每年开展逾600例器官移植。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 并非所有医院都有充足经验</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家指出，目前器官移植的现状令人担忧。中国人民解放军器官移植研究所、第二军医大学附属长征医院器官移植中心主任朱有华教授透露，沪上不少医院经常收治转来的器官移植术后病人，有些就是由于移植进入体内的器官失去功能，医院只好重新手术切除器官，其中不少都与某些医院处理不当、移植不规范有关。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 个别医院一年只开展几例器官移植，经验积累显然不够。有些小医院甚至聘请专家去做手术，专家一走，患者的术后治疗也就没有了保障。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 利益驱使某些医院强拉手术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 更有些医院由于利益驱使，勉强拉病人进行器官移植手术。上海市第一人民医院副院长彭志海教授指出，器官移植对医生的技术要求高，病人花费也多，一般都需要十几万元到几十万元，其他治疗方法相比更加经济、实惠，器官移植只能作为最后的治疗手段。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家举例说，目前部分病人做肝移植手术非常勉强，比如有些肝功能A级肝硬化消化道出血患者没有必要再做肝移植，某些种类的肝癌只需切除就可以了。(陈里予崔颖)</span>
</p>
