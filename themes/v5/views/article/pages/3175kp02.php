			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肌张力障碍只能做脑起搏器手术吗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么是肌张力障碍？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者的身体出现不自主的持续性的肌肉收缩，引起的扭曲、重复运动或姿势异常，严重干扰日常生活及活动。肌张力障碍是常见的运动障碍病，每10万个人中约有39个肌张力障碍患者，中国患病人数约46万。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肌张力障碍分几类？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根据患者身体受累的肌群的不同分为</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp; &nbsp;局灶型肌张力障碍：眼睑痉挛、痉挛性构音障碍、痉挛性斜颈、书写痉挛和鼠标手等，为单一部位肌群受累。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. &nbsp; &nbsp;节段型肌张力障碍：Meige综合症，轴性肌张力障碍，包含两个或两个以上临近的肌群受累。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. &nbsp; &nbsp;偏身型肌张力障碍：半侧身体受累，一般都是继发性肌张力障碍。多个不相邻肌群受累。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. &nbsp; &nbsp;全身型肌张力障碍：下肢与其他任何节段肌群的组合，伴有轴线方向的不随意性扭转的肌张力障碍，是最为多见的表现形式，称为扭转痉挛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全身性肌张力障碍约占17%；局限性肌张力障碍约占50%，其中痉挛性斜颈最为常见；其余为偏身或节段性肌张力障碍。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">根据病因分型</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp; &nbsp;原发性肌张力障碍：病因不明确，该类病人没有明确的中枢神经系统结构异常，如书写痉挛、眼睑痉挛和痉挛性斜颈等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. &nbsp; &nbsp;继发性肌张力障碍：有明确病因，有的是遗传性疾病，如肝豆状核变性、亨廷顿舞蹈病，棘红细胞增多症，Hallervorden-Spatz综合征和神经节苷脂病；有的是由外源性因素引起的，如围生期损伤引起的脑瘫，由于服用精神类药物引起的迟发性肌张力障碍等，由于脑损伤和脊髓损伤引起肢体痉挛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肌张力障碍的非手术治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物通过影响神经系统中的神经化学递质而起到治疗作用，而神经系统通过递质来执行大脑所下达的控制肌肉运动的指令。肌张力障碍没有根治性的药物，镇静类药物可在缓解患者紧张情绪的时候，部分地改善症状；可供选择的药物有安坦、氯硝安定、硝基安定、巴氯芬、氟哌啶醇和丁苯那嗪等，效果均不十分理想，有的副作用还很大。例如大剂量服用安坦会出现口干、眼花、记忆力下降等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于口服药物治疗无效的局造型肌张力障碍和部分节段型肌张力障碍可以进行肉毒素注射，部分患者有效。但是有些患者效果维持的时间有限，即使反复注射仍然不能满意地控制症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对多数肌张力障碍病的患者，功能神经外科手术可能是唯一的症状治疗手段，特别是全身性的肌张力障碍、扭转痉挛等。治疗方法包括：周围神经或肌肉离断术，脑深部电刺激术，丘脑或苍白球毁损术和鞘内巴氯酚输注。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肌张力障碍手术的适应症</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp; 谁适合周围神经或肌肉离断术？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">适合痉挛性斜颈的患者。离断术前进行肌电图检测，术中通过神经电刺激，判断痉挛收缩的肌肉，选择性地切断部分导致颈部痉挛的神经，或离断部分痉挛收缩的肌肉。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. &nbsp; 谁适合脑深部电刺激术（Deep brain stimulation, DBS）？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">年龄大于7岁的原发性肌张力障碍的患者，包括全身性和节段性的肌张力障碍、偏身肌张力障碍与颈部肌张力障碍，DBS可有效改善症状40~90%，目前常用的刺激靶点是内侧苍白球或丘脑底核。肌张力障碍患者对持续DBS的治疗反应较延迟，肌张力障碍性姿势在手术治疗后要经过长达1年或更长时间逐步好转。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于某些继发性肌张力障碍的患者，也可以考虑脑深部电刺激术，如亨廷顿舞蹈病，棘红细胞增多症，Hallervorden-Spatz综合征，以及服用精神类药物导致的迟发型肌张力障碍。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">DBS使用类似心脏起搏器的植入式医疗装置，对脑内的目标区域进行精确的电刺激。这种疗法是可逆转和调整的。它使用植入的装置来刺激大脑的特定区域，帮助控制运动的回路更順利运作。即此缓解运动症状。比如肌肉的痉挛、扭曲、不自主收缩、不正常姿势与不受控制的运动。需要了解的是，肌张力障碍的 DBS疗法可望控制您的症状，但无法根治。开启 DBS系统时，它会开始提供刺激来减缓您部份或所有的症状。但症状在关闭系统后复现。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. &nbsp; 谁适合丘脑或苍白球毁损术：该手术破坏大脑运动环路中的特定部分，安全性较DBS差，但具有便利，经济和易照料的优势。毁损手术一定要在经验丰富的，专门从事立体定向手术的中心进行。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) &nbsp; 对于书写痉挛，鼠标手等单纯局限性肌张力障碍，年轻病人适合立体定向丘脑毁损手术，费用少，没有植入物的限制，病人的生活更加方便。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2) &nbsp; 对于原发性肌张力障碍，如果病人无法承受双侧脑起搏器的费用，可以采用单侧苍白球毁损手术，另外一侧脑起搏器，效果很好，安全性很高。但对于单侧脑起搏器的费用任然无法承受，可以采用双侧苍白球毁损手术，手术的风险较安装脑起搏器相对较高，然而经验丰富的功能神经外科医生仍然可以有效控制风险，保证安全性和长期良好的疗效。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3) 对于患有全身性肌张力障碍，对植入物有排斥反应、无法耐受的患者，也可以采用毁损手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. &nbsp; 谁适合鞘内巴氯酚输注系统？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">巴氯酚注入到脑脊液中是由一个泵来实验的，将泵与一个小的细管连通，通过手术植入到患者腹部皮下，将细管环绕到背部，与另一植入背部皮下更细的管连结并通过连于细管一端的针进入脑脊液中，由于巴氯酚可随脑脊液向上流动至脑，因此可以松弛整个机体，包括颈、上肢、躯干和腿，一些个体尚可改善抖动、吞咽、交流能力、上肢和手的功能，改善躯干控制和步态。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">巴氯酚泵改善痉挛的作用是肯定的，包括脑瘫引起的双下肢痉挛，脊髓损伤后下肢痉挛，脑源性痉挛和遗传性痉挛性截瘫（HSP）等。</span><br />
