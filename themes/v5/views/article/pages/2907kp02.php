			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肝癌化疗后出现副作用怎么办？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">化疗都会产生一些副作用，但不是刚开始用药就显示出来，一般在第二、三次用药时才会慢慢地出现，因为化疗药往往有一个“累积效应”。下面是患者经常咨询的一些关于化疗副作用的问题，在这里做出解答，希望能帮助广大病友，如果有问题可以在网上或者医院咨询我。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">消化道症状——肝癌化疗的副作用，最主要的体现就是化疗期间恶心、呕吐、食欲不振，更严重的患者甚至会出现腹泻等症状，这些患者可能对化疗药比较敏感。这种胃肠道的不良反应往往在用药过后3~5天就会慢慢缓解，如果患者呕吐比较剧烈，可以在咨询医生后使用一些止吐药，如果是腹泻比较严重，也可以用一些止泻药。有些患者会出现便秘，使用一些润肠通便药物即可。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">出血——如果是皮下出血，首先要考虑患者有凝血功能障碍，需要检查肝功能，看看凝血因子合成能力是否正常；如果是大便带血，则需要考虑患者是否有消化道溃疡、炎症甚至是肿瘤，需要做一些相关检查；如果是消化道大量出血，甚至呕血的，这可能是由于化疗加重了患者自身的疾病造成的（化疗对肝功能会有影响，肝硬化食管静脉曲张有可能破裂出血），一旦出现这种情况需要立刻送到医院处理。针对有这方面风险的患者，需要在化疗前或者化疗期间预防性的用药（比如使用抑酸药，具体请咨询医生）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">感染——这是化疗出现的比较严重的副作用。因为化疗对患者的骨髓有一定的抑制作用，会导致患者的白细胞数量降低，免疫力低下。如果化验白细胞降低，则需要咨询医生，可能要打升白针。对于免疫力低下的问题，可以适当使用提高免疫力的药物（不是必须，但用了没坏处）。如果患者是因为营养不良引起的免疫力低下，就需要改善饮食状况了。有些患者觉得吃得多了，正常细胞长得好，癌细胞也会一起生长，其实不是的。手术后癌细胞的危害程度是很低的，吃一些高营养的食物对癌细胞基本没影响。所以，在手术后的早期，患者还是应该把营养状况补上来，如果恢复得快，一段时间后恢复正常饮食就行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手脚蜕皮——肝癌患者化疗很少出现手脚蜕皮的情况，这种现象在使用靶向药物时比较常见。如果患者出现了手脚蜕皮，一般也不会很严重，我建议患者在蜕皮处涂抹一些维生素E乳膏，尽量不要自己撕脱即可。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关节红肿——只有极少数患者反应化疗期间出现了关节红肿，这与化疗的副作用关系甚微。如果患者出现了相关症状，有可能是免疫系统疾病，需要到免疫科做检查。</span><br />
