			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何区别生长痛和骨瘤痛？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>身体周刊：骨肿瘤发病原因是什么？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张伟滨：骨肿瘤发病因素很复杂，一般内因条件先存在，外因通过内因而发生。内因有人体素质学说、基因学说、内分泌学说等；外因有化学毒素、病毒感染和各种射线照射等环境刺激学说等。多发性骨软骨瘤和纤维异样增殖症均与家族遗传有关。骨肉瘤多发生于10-30岁之间，说明骨骼发育生长的旺盛，与骨肿瘤的发生可能有关。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一些骨的良性肿瘤可以发生恶性病变，如多发性骨软骨瘤、纤维异常增殖症等。有些骨肿瘤病人常回忆起患处有外伤病史，如扭伤、碰伤等，实际上，这类外伤不至于引起骨质恶性变化，在骨折部位发生的骨肉瘤极为罕见，很可能是肿瘤发展到一定程度时，外伤促使症状明显才引起注意。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我曾有一个湖北的病人，距今已有八九年的时间了，他们学校给学生统一发保健品，这个保健品是用来长身高的，整个年级每个学生都吃，结果他们这个年级全部100多人中有3个学生发生了骨肉瘤，骨肉瘤发病率高达3%，比骨肉瘤自然发生率（百万分之三）足足高了一万倍。二者之间有没有直接关系，我没有进一步的研究，但值得警惕，小孩使用促进增高药物需谨慎，建议在专业医生指导下使用为好。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>身体周刊：小孩子在生长期也会有生长痛，跟骨肉瘤的痛怎么区别？</strong><strong></strong></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张伟滨：第一，生长痛的小孩一般年龄比较小，多是五六岁的时候，很少有十几岁的孩子还有生长痛。当然五六岁的孩子也有得骨肉瘤的，这需要医生来判断。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第二，生长痛是在长骨头的地方痛，这也是骨肉瘤比较易生的部位。区别是生长痛主要表现为阵发性，很少为持续性疼痛，而后者疼痛持续加重则是骨肉瘤疼痛的特点。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">恶性程度高的骨肉瘤可能在一个月左右就会发生很明显的变化，痛得厉害，肿得很快，这种病人比较容易诊断。比较麻烦的是一些恶性程度相对比较低的患者，症状比较隐匿，肿瘤缓慢地生长，痛得不是非常严重，可能只是钝痛，感觉不舒服，好像腿越来越没地方放的感觉。肿块的凸现也比较晚，这种情况比较难做到早期诊断。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">建议大家如果连续两周一直感觉不舒服，很注意休息还是解决不了问题的时候，就要去看医生。骨肉瘤病人还会有另外一些症状，比如有些患者会有持续低烧，37.5 左右。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">身体周刊：骨肉瘤的确诊方法是什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张伟滨：肿瘤的诊断主要是三种方法，一是临床检查，也就是医生问诊和体格检查；二是拍片子，如X光片、CT、磁共振、骨扫描等；最重要的方法是病理诊断。通过三种方法结合基本可以达到百分之百的诊断。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般的体检很少去查骨肉瘤。其实骨肉瘤有一个可参考的标志物，就是碱性磷酸酶升高，但是比较麻烦的是儿童青少年本身处在骨骼生长期，他们血中的碱性磷酸酶本身就比正常人高，所以有时很难用此标志来确诊骨肉瘤。而且还有一部分骨肉瘤病人碱性磷酸酶并不增高，所以单纯靠指标来诊断不如影像、病理的方法准确。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>身体周刊：骨肉瘤的治疗方法和术后注意有哪些？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张伟滨：现在标准的治疗模式是综合治疗——手术加化疗。化疗又分为术前化疗和术后化疗，术前化疗有几个作用，其一是判断化疗方案和药物对病人有无效果。因为目前三分之一的病人经过化疗和手术，还是控制不了肿瘤的生长和转移，重要原因就是肿瘤细胞特殊的耐药特点。同时耐药的骨肉瘤细胞还喜欢转移，很快发生肺等远处转移。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不同的手术，术后康复要求是不一样的，要根据医生要求来做。譬如采用异体骨重建，肿瘤切掉以后用一大段别人的骨头来重建骨缺损，要等骨头长好后才能做较强的主动关节功能操练。但如果用肿瘤假体的话就比较快，每个部位不一样，如股骨远端可能术后两到三周就能开始恢复主动功能操练。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现在越来越推崇尽早进行康复训练，一般手术后当天肌肉的康复训练就开始了，一方面是避免肌肉萎缩，另一方面也能够促进血液循环，利于伤口的愈合。</span><br />
