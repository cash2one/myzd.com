<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑AVM的血管内介入治疗&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;一般来说，所有颅内 AVM均是血管内介入治疗的适应症，尤其对于脑深部、重要功能区、高血流量或大型AVM，血管内介入治疗是首选考虑的治疗方法。其优点是：①对一部分能通过栓塞而达到治愈的病例避免了手术；②即使不能完全栓塞畸形血管团，由于闭塞了主要供血动脉和大部分畸形血管团，使 AVM范围缩小、血流减少、缓慢、盗血现象减轻，有利于手术切除或放射治疗，使一部分传统认为不能手术的病例变为能够手术；③并发症和危险性较外科手术小。 &nbsp; &nbsp;&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">通过栓塞能达到治愈的病例主要是有 1-2支供血动脉的小、中型AVM，病灶越小，栓塞后畸形血管团消失越完全。通过栓塞术，能使20％的病例达到治愈。对只能做到部分栓塞的病例，栓塞应针对畸形血管伴发的脑动脉瘤、引流静脉不通畅、脑动静脉瘘等畸形血管出血性因素进行；应在病灶缩小，血流减少、缓慢后及时给予手术或放疗。目前常用的栓塞材料有 Onyx胶和 NBCA胶，辅助材料有弹簧圈和球囊。对于高流量的AVM，虽然静脉引流通畅，血管内栓塞降低流量后可避免动力性动脉瘤的发生。&nbsp;</span>
</p>
