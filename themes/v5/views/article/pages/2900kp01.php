	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>射波刀简介</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1988年，美国斯坦福大学从事放射外科治疗与研究的神经外科医生John Adler Jr博士提出了影像引导无框架立体定向放射外科的概念（iamge-guided frameless stereotactic radiosurgery）。随着计算机技术、神经导航技术和直线加速器放射外科的发展，1992年Adler及同事研制出最原始的无框架立体定向放射外科治疗设备，当时Adler将这种设备注册为Beta system (射波刀的雏形)。1994年射波刀开始治疗脑转移瘤病人。在以后的临床试用阶段，射波刀得到了进一步改进和完善，1999年美国FDA正式批准射波刀治疗系统 (Iamge-guided Cyberknife Radiosurgery / Radiotherapy System) 可用于治疗头部疾病，2001年美国FDA批准射波刀用于治疗全身肿瘤。尽管射波刀技术成熟，但是仍有诸多不完美之处。2002-2006年间，射波刀控制系统、剂量计划系统、靶区追踪系统、脊柱追踪技术、呼吸追踪技术进一步升级完善，使射波刀的临床治疗日臻完善。射波刀无须使用金属头架或体架，它采用计算机立体定位导航和自动跟踪靶区技术，是新型的大型立体定向放射治疗设备。射波刀由直线加速器、机器人机械臂（Robot Arm）、治疗床、治疗计划系统、靶区定位追踪系统（target localization system）、呼吸追踪系统、计算机网络集成与控制系统组成。2006年9月，射波刀的治疗床改进为机器人控制床，准直器的更换从人工改为电脑自动辨认与更换，特别是Multiplan剂量计划系统的问世，使射波刀治疗进入一个崭新的时代，Accuray公司（由Adler博士的创立）把新一代的射波刀称为第四代（G4）。射波刀的问世使放射外科治疗的解剖范围从脑部扩展到全身，它不仅可以治疗颅内肿瘤，还可以治疗颅底深部肿瘤、头颈部肿瘤、脊髓肿瘤、脊柱肿瘤、肺部肿瘤、胰腺肿瘤、肝脏肿瘤、肾脏肿瘤、前列腺肿瘤、妇科肿瘤、骨科肿瘤。</span><br />
