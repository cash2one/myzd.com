<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>眼角开大——手术之前要三思&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼角开大手术是在亚洲地区非常流行的手术之一，将内外眼角进行手术开大，使眼睛看起来更大更动人是无数求美者的梦想，那么眼角开大手术的实质是什么？它真的像宣传的那么好吗？这个手术有哪些风险呢？本文将就以上问题进行一一解答。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">首先来讲内眼角，在进行内眼角手术讲解之前需要了解一个概念——内眦赘皮，内眦赘皮是东亚人的特征性结构，指遮盖泪阜前的皮肤组织。因为这一结构的存在，导致了东亚人内眼角处不如高加索人开阔，显的眼睛横径较短。解除内眦赘皮需要通过手术的方法，这就是内眦开大手术。内眦开大后会使眼睛横径变大，再结合重睑术的话，会使眼睛的外观效果更佳。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是需要指出的是，内眦开大手术最大的问题是内眼角处术后的瘢痕形成，和重睑手术不同，内眼角的术后瘢痕无法被皮肤覆盖而是直接暴露在外，因为个人体质原因，部分人瘢痕生成比较明显，术后更是不仅没有达到美容的效果而造成影响外形的瘢痕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，医生建议，内眦开大适合比较严重的中度以上的内眦赘皮，对于轻度的内眦赘皮，医生不建议进行，因为眼角的改善效果反而不及瘢痕造成的影响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下面讲外眼角，这里需要郑重指出的是，外眼角手术的瘢痕较内眼角更加明显，因此该手术仅适合于病理性的眼部畸形如小眼症等，在美容范畴一定要非常慎重，我院接诊了一大批在外进行外眼角开大的患者，需要进行瘢痕整复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">综上所述，眼角手术并非想象当中的神奇，完美，手术前一定要三思而行。</span>
</p>
