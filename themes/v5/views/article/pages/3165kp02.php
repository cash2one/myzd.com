	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人体出现骨质疏松主要是两个原因，第一：自身的骨钙不足；第二：骨钙流失得过快。一般来讲，从幼年期，人体的骨钙逐步增加，到一定年龄开始，骨骼的总重量以每年1％的速度递减，50岁后骨总重量已减轻30％。随着年龄的增长，身体的活动量减少，钙的吸收与储存逐渐减少。到了老年，体内钙的溶解排出更超过吸收与储存，因而老年的骨密度会逐渐下降，以至于出现骨质疏松，其症状是容易骨折，最易折断的部位是股骨和脊椎。</span>
