<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>军医就要着眼治疗战创伤</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">吴阶平医学奖、第五届中国医师奖、第17届美国赛克勒中国医师年度奖和王忠诚学术成就奖……一份份沉甸甸的荣誉，是对解放军总医院神经外科主任余新光的最高褒奖和充分肯定，以此来证明他从事神经外科工作30年的成绩。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">姓军为兵，姓军为战！“习主席要求军队‘能打仗、打胜仗’。作为军医，就是能治病、治好病。所谓治病，就是要着眼治疗战创伤，为军服务。”科技日报记者采访时，余新光如是说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">特殊情结，志当一名好军医</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">余新光祖籍广东梅县，祖父曾是粤军总部参谋长邓仲元（与廖仲恺同为孙中山的左膀右臂）家庭教师。邓仲元牺牲后，祖父被迫下了南洋。抗日战争爆发后，余氏家族又以极大的爱国热情支援着这场反侵略战争。他祖父曾因积极组织募捐抗日，被日军列入“黑名单”被捕。父亲利用家传的皮鞋作坊从事地下抗日宣传工作，后又在印尼一家进步华人报纸做编辑。这样的家族基因，让余新光身上多了一份沉甸甸的责任，也为他成为军医奠定了思想根基。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1979年，余新光考上了第二军医大学军医系，毕业后进入部队医院，成为一名军医。从穿上军装的那一天起，至今已是45年。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1998年长江抗洪时，余新光正好在武汉某军医院代职。面对滔天洪水，无数官兵奔赴抗灾前线，而他作为代职人员只能留在医院值守。面对越来越多的伤病员，他再也坐不住了，“作为总后下来代职的专家，我应该以身作则，而不是坐在后方。”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在他的强烈要求下，上级同意了他们奔赴前线的请求。坐上冲锋舟，穿上救生衣，他和几位专家奔赴灾区。那次为期一周的任务中，他先后救治了几百名受灾群众和救灾官兵。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“那一刻，我觉得自己真正成了一名军人。”余新光激动地说道。更为重要的是，从那个时候开始，他开始认真思考灾难医学和战创伤这个全新的概念。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">赤子忠心，救灾一线谋创新</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2008年，四川汶川大地震时，解放军总医院派出了9个医疗队赶赴灾区一线，余新光带领其中一个医疗队，在成都452医院对后送伤员实施援救。在那里，灾难救治中如何真正发挥好军医的作用，一直是他深深思考的重点问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">那些日子里，他带领的医疗队提出了“降低死亡率，提高存活率；降低致残率，提高保质率”的救治理念。在这一理念指导下，医疗队建立起一整套完整的救治流程，从到机场接伤员，到伤员的分类，到专科救治，到康复，到后送，涵盖了救治的整个流程。后来，这一理念被新华社记者发表到内参上，受到中央领导的充分肯定。也正是在他们医疗队，治愈了“汶川地震站起来的第一人”——李科。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2010年，玉树地震临近结束时，余新光所在的医疗队返回期限已到。这时，医院突然接到一个藏族地震伤员。这是一种罕见病例——高位颈椎脱位损伤！损伤部位靠近生命中枢，若不迅速救治，患者的生命随时可能停止！</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">这种病例，国内外只有2—3例公开报道，缺乏成熟的治疗经验，手术风险很大。他当即决定推迟返回时间，立即通过远程视频会诊，与相关专家一起商讨救治方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">是军人就要敢于冲锋！余新光下定决心，采用一种几乎全新的治疗方案：在一个麻醉下同一天做了两期手术，先经口腔实施脱位骨质切除手术，后进行颈椎固定手术。由于处于特殊时期，医疗器材装备有所欠缺，军医余新光在没有穿防护服的情况下，亲自做了这个手术。从早上9点开始一直到下午4点顺利完成手术，他一直暴露在X射线机下。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“比起能成功救活一个病人，这一点点辐射又算得了什么。”回忆起这段经历，余新光欣慰地说道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">瞄准战场，开创海战救治新模式</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">从玉树抗震救灾回来后，余新光又做了件令很多人记忆犹新的事。在总后召开的总结大会上，他代表医院专家医疗队做汇报发言。很多人没有想到，他抛开早已准备好的发言稿，汇报了自己在灾区一线的体会和感受。他提出，作为医务工作者，不应该只关注宣传报道，而应该更多地思考在灾难面前如何救治。因此，不能只是做形式上的宣传，而要加强灾难医学中重症现场急救的研究，应该强调科学救治。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在全总后的大会上这样提，确实很需要勇气，但军医的责任心使余新光顾不了太多。结果，这个很是“尖锐”的发言，获得了与会首长、军事专家、院士的充分肯定。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我国是临海国家，有相当长的海岸线。近年来，不断有临近国家在我国周边挑起争端。因此，海战将成为我国未来可能面临的最主要战争之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2011年年底，余新光调到解放军总医院新建立的海南分院，担任神经外科主任兼大外科主任。两年任职期间，他十分注重医院“为军服务”的重要功能。期间，他多次参加医疗队，亲赴西沙8个岛屿，为守岛官兵提供医疗服务。在这个过程中，他了解到，对海战伤的伤员如何高效地进行运送救治理念尚在研究探索之中。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“平时展开医疗救治都这么困难，那么爆发战争之后怎么办？”一直关注灾难医学和战创伤救治的余新光陷入沉思。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">传统陆地战条件下，主要依靠医疗后送、分级救治来保障大量伤病员的救治。这种理念下，医疗后送大致要经历就地治疗、后送治疗和分级救治三个阶段。我军现行的医疗后送模式是“三区七级”的救治体系，即连抢救组，到营救护所，到团救护所，到师救护中心，到一线医院，到二线医院，到后方医院。这种理念在传统陆战条件下确实取得了很好的效果，并对维护和恢复部队的战斗力做出了一定贡献。但是这种救治理念显然不适合海战条件，无法保证救治的“黄金3小时”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">结合海南分院军事卫勤任务，余新光做足了调研，开创性地提出了现代海战条件下伤员的运送救治模式，为沿海战略后方医院早期综合救治指明了新的要求。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">他认为，在海南海棠湾建立分院，实际可以更好扶助沿海地带的三甲医院，而这正好构成了一个沿海地带综合医院的医疗网，这个医疗网如同一个医疗阵地，实际上是现代战略后方医院的前移。而随着先进检查设备逐渐配备到这些医院里，实际上提高整个医疗网的救治水平。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“各种先进运送设备的应用，高效的转运模式和沿海战略后方医院前移阵地化，使得我们能突破传统‘三区七级’的救治模式，开启了现代海战中战创伤患者在沿海战略后方医院的早期综合救治的理念，启动了卫勤保障的新模式，更好地为广大伤病员服务，最大限度地恢复部队的有生战斗力。”他充满信心地说道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">作为一名军人，国防安全令余新光始终魂牵梦绕；作为一名军医，他用自己独特的方式，书写着对祖国的无限忠诚，吹响了新形势下战创伤救治前移的第一声号角。在他的带领下，这个科室正朝着国家重点学科的方向稳步前行，朝着提升战创伤救治和灾难医学更高标准大步迈进！</span>
</p>
