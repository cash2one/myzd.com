		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>专访长海医院胡小吾教授：帕金森症怎么治最科学（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">帕金森病又名震颤麻痹，是一种常见的中老年神经系统变性疾病。其运动症状表现为静止时肢体不自主地震颤、肌强直、运动迟缓以及姿势平衡障碍等，病情晚期会导致患者生活不能自理。目前中国帕金森患者数已达200万，占全球帕金森患者总数的一半。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 由于帕金森病至今尚无法治愈，不少患者对晚期生活质量的严重下降担忧不已，甚至失去治疗的信心，给患者及家属带来了很大的心理的负担。针对这一现状，三九健康网编辑请来了第二军医大学附属长海医院神经外科胡小吾教授，向大家仔细介绍应如何科学治疗帕金森病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3103/1.png" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 专家介绍:胡小吾现任中华医学会上海市神经外科学会功能和立体定向学组副组长，上海市医疗质量鉴定委员会专家，中华医师学会神经调控学会委员，世界神经调控学会会员，国家自然科学基金项目评议人。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 专业特长:立体定向功能神经外科，在帕金森病、特发性震颤、扭转痉挛等疾病脑深部刺激术（脑起搏器）治疗和毁损术治疗方面具有较高知名度。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 早期应用药物治疗 把握剂量很关键</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胡小吾教授介绍，帕金森病情早期症状较轻，吃药效果较好，医学上称之为“蜜月期”，此时即使手术，效果也不理想，建议早期患者尽量药物治疗，不要急于做手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病在药物治疗上要坚持八字方针：“细水长流，不求全效”。药物治疗作用有限、具有时效性，任何一种药随着吃药时间的延长，效果会越来越小，且会出现副作用。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胡小吾教授建议：患者应尽量推迟吃药时间，若患者症状比较轻，可服用神经营养药、受体激动剂等药物，不建议一开始就服用最好的药物，也不建议追求最好的药效，因为达到最佳效果，药物服用剂量会很大，会缩小今后的治疗空间，用药效果只要达到七八成就可以了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 中晚期外科手术最好选择脑起搏器</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胡小吾教授说，外科手术一般适合中晚期患者，患者病情发展到中晚期，药物效果不理想，药物副作用较大，症状影响到工作和生活，此时应考虑外科治疗。外科治疗通常在病发后5年进行，对以震颤为主要症状的病人因为震颤对外科治疗反应比较好，在病发3年后也可以进行手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胡小吾介绍，目前外科手术常用到的两种方式是毁损术和脑起搏器。毁损术即通过射频热凝脑内异常兴奋神经核团，来消除帕金森病症状。胡小吾教授作了一个很形象的比喻：大脑就像个很复杂的电路，帕金森病导致大脑的一部分电路出现短路，引起周边电压过高，脑细胞异常兴奋，需要通过手术将电压降下来，损毁术就是通过破坏脑细胞打到降低电压的目的。做损毁术时，对手术质量要求很高，若是把不该破坏的脑细胞破坏掉，会产生较严重的并发症，若是该破坏的脑细胞没被破坏，会出现病情复发，而且术中可能会出现脑出血、靶点偏差等并发症。约4%～10%病人术后出现毁损术一般只能做一侧，如果做双侧，并发症高达30%。毁损术比较适合经济原因不能进行脑起搏器治疗的单侧症状帕金森病患者。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 脑起搏器是自左旋多巴药物之后治疗帕金森病的第二大进展，也是目前世界上最好的外科治疗方法。脑起搏器即脑深部电刺激术 (DBS)，手术在脑内神经核团植入电极，把脉冲发生器埋藏在胸部皮下，脉冲发生器发出电刺激，通过皮下导线，传到脑内电极，抑制不正常的神经放电，使其消除症状。起搏器电池耗完后（一般在6年左右）需要更换脉冲发生器，即在胸口皮下组织取出电池更换新的便可。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 脑起搏器治疗安全性高，是一种可逆性的神经调节治疗，如果今后有更好的药物被研发出来，可停用脑起搏器，并不破坏脑组织。但是脑起搏器的费用较高，大概10万人民币左右，建议经济条件允许的帕金森晚期患者安装使用。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森治疗误区多家属应当心</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胡小吾教授指出，患者在治疗中应注意避免以下误区：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 关于吃药。帕金森病患者需要终身吃药，而不是吃几个疗程后症状缓解就停服。在病情早期，吃什么药、怎么吃、剂量多少，需要在正规医院医生的指导下进行。总的来说，用药原则是“细水长流，不求全效”。患者应尽量推迟吃药时间，如果症状轻微，可以暂时不吃药，但如果症状明显，则应及时吃药。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 关于药物选择。不要一开始就选择效果最好的药物。药物治疗最有效的时间一般只有4～6年，随着吃药时间延长，疗效越来越低，剂量越来越大。因此用药时剂量要尽可能小，尽量长期规律地服用，能达到约80%效果即可。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 掌握好手术时机。外科治疗不能太早，也不能太晚，太早了药物治疗效果还很好，手术效果不明显；手术是通过外力调节内因缓解病情，若手术时间太晚，或病人年龄过高，手术风险较大，收益较小。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 术后也要吃药。目前，帕金森病无法根治，所有针对帕金森病的治疗都是控制症状。外科手术是为了解决药物治疗差、副作用大的问题，术后患者可降低药物剂量，同时药物的副作用大大降低，但是术后还要继续吃药。胡小吾教授称，中晚期病人最佳的治疗手段应是“一手拿着多巴胺药物，一手拿着脑起搏器”。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 康复治疗同样重要</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 帕金森病常见的功能障碍包括：震颤、肌强直、运动功能障碍、吞咽功能和语言障碍、自主神经系统功能障碍等。通过药物或外科治疗，症状改善的明显程度依次为：肌强直&gt;震颤&gt;语言及吞咽障碍&gt;平衡障碍及姿势反射，并且药物或外科治疗还会带来继发性功能障碍。因此，单纯依靠药物或外科治疗无法解决帕金森病的所有问题，这时就需要康复治疗的参与，即通过对帕金森病患者予以功能训练为主的多种有效综合手段，配合正在进行的药物或外科治疗，使之尽可能地保持独立的日常生活活动能力，提高生活质量。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 胡小吾建议帕金森患者多做一些力所能及的运动，如做家务、散步等，尽量多参加社会活动，融入社会，克服心理障碍。患者家属对其应该多鼓励多支持，使其融入家庭，以利于病情的恢复。家属要了解患者的病情波动性，认识到患者何时需要帮助；还要保持患者的独立性，不可事事亲为。</span>
</p>
