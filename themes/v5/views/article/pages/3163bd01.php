		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>应酬一族急性胰腺炎高发（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">□晚报记者 肖波 报道 摄影 何雯亚</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 近日，第二军医大学附属长海医院连续收治急性胰腺炎发作病人，由于饮酒、高脂食物、缺乏运动、肥胖等是造成急性胰腺炎发病的重要原因，发病人群越来越多地集中在私人老板、公司高管等应酬一族中。长海医院抢救的病人中，50岁以下最为多见，其中最年轻的仅20多岁。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 220斤中年妇女突发急性胰腺炎</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 40多岁的陈女士（化名）今年7月19日入院，躺在长海医院急性胰腺炎ICU病房已近一个月，刚刚脱离危险，至今仍不能说话，和医生交流只能通过点头摇头和眼神。消化内科副主任医师杜奕奇表示，患者目前还未完全脱离危险期，虽然肾衰竭已经恢复，但目前出现发热、感染，治疗还需要相当一段时间。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 身高1.65米，体重220斤的陈女士血清甘油三酯是正常指标的十倍，护士为她抽血时，肉眼就能看到浑浊血液中的油脂。 7月17日晚餐后，陈女士出现腹痛、呕吐、低烧症状，在当地医院诊断为急性胰腺炎后病情恶化，出现并发症，立刻转至长海医院。当时陈女士神志不清，出现呼吸衰竭和急性肾衰竭，浑身浮肿。经过一个月的抢救、重症监护治疗才暂时脱离危险。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 三个荷包蛋要了一个富翁的命</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 和陈女士相比，另一位来自常熟的病人何先生（化名，杜奕奇教授提供病历）就没有那么幸运。 40多岁的他因为连吃三个荷包蛋出现急性胰腺炎症状，在当地医院住院后，无法控制病情，最后转到长海医院，可惜因为耽误了就诊时间，没有被抢救过来，身家数千万的他倒在暴发性胰腺炎面前。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 长海医院消化内科副主任医师杜奕奇接受采访时表示，近年来重症急性胰腺炎发病有所上升，该院急性胰腺炎ICU病房今年以来从没有空床位。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 重症急性胰腺炎是十分凶险的疾病，前几年死亡率高达50%，近几年长海医院的抢救成功率提高到九成左右，但医生仍然为无法抢救过来的这些病人惋惜。“相当一部分病人是不良生活习惯导致发病的。在病人中，中青年肥胖体型者占绝大多数，我们抢救过最年轻的一位病人只有20多岁，他们大多有高血脂病史，不少人好饮酒。 ”杜医生说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 而女性患者中，胆源性引起的急性胰腺炎占一定的比例，即原先有胆囊炎、胆囊结石的女性患者，得急性胰腺炎的风险相对较高。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 急性胰腺炎发病时，具有强大消化功能的胰液像洪水一样涌到其他内脏，各种毒素（炎性介质）进入血液循环，激发强大的破坏性的“瀑布机制”，可以使远离胰腺的脏器如心肺收到损害。医生提醒，如果饭后出现莫名腹部疼痛，并不断加剧和有放射症状，最好去医院检查。</span>
</p>
