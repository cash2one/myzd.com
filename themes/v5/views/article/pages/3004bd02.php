			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>爱耳日：老年性聋，最易被忽视的慢病</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3月3日是第14个全国爱耳日。今年的主题为：健康听力，幸福人生关注老年人听力健康，旨在唤起全社会关注老年人听力健康，造福广大老年人及其家庭。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为了能听到和理解语言，需要听觉系统三个基本过程相互配合完成，即外周听觉系统能接受外界声音的刺激，听神经能将这些声音传到中枢听觉系统，中枢听觉系统能对声音进行分析和认知。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 然而，随着年龄的增加，人的听觉系统将出现退化现象，并导致听觉系统功能障碍（包括外周和中枢部分）。这种因年龄相关导致的听觉系统退化出现的听力下降，在排除噪音、耳毒性药物和中耳感染等因素的参与被称为老年性聋。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 老年性聋是指随着年龄增长逐渐发生的进行性听力减退，重者可致极重度聋的一种老年性疾病，是人体老化过程在听觉器官中的表现。2005年，我国65岁以上老年人的比例为7.69%，到2050年，这一比例将会达到25%。听力下降在老年人群中是一个非常普遍的现象。相关流行病学调查表明：以纯音听阈大于25dB为标准，在55-64岁人群中的发生率为10.1%，随着年龄进一步增加，在65-74岁人群中，其听力下降的比例也增加到26.2%。老年性聋已成为现代社会最为常见的慢性疾病之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 老年性聋不仅可因外周听觉器官损害引起，还包括整个听觉传导通路的机能障碍。临床上分为中枢性和周围性两类，但以混合类型居多。老年性聋患者听觉中枢的病理变化是由于年龄相关导致听觉中枢退行性变、或由于外周对中枢的输入减少或消失有关。另外，随着年龄的增加，耵聍易积聚外耳道，导致耵聍的栓塞。外耳道塌陷，特别是皮肤萎缩、弹性降低、脱水干燥。这些变化导致外耳道对损伤的抵抗能力降低，以及对硬性材料的耐受性较差。当患者配戴耳道式助听器或需耳模时，应考虑对外耳道皮肤的损伤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着年龄的增加，中耳的改变表现为：鼓膜的血管减少、变薄、弹性降低、砧镫关节、砧槌关节老化以及中耳肌肉和韧带萎缩和变性，软骨钙化、骨化。总之，由于年龄的关系，出现的上述中耳和外耳的变化很常见，然而他们对声音的传导影响较少，但会影响助听器的使用效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 老年性聋的临床表现</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 以双侧对称性感音神经性耳聋为主。少数可表现为双侧耳聋程度可不完全一致，另有少数合并有外耳或中耳退行性病变者可表现为混合型听力下降。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 听力下降呈缓慢进行性加重。早期多未引起注意。当下降到一定程度时，患者会突感听力下降，交流困难，被误诊断为突发性聋。多数老年人听力下降开始时多以高频听力下降为主，患者仅对门铃声、电话铃声、鸟叫声等高频声响不敏感。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 言语识别率低是第一症状。这个症状开始仅出现于特殊环境中，如公共场合很多人同时谈话时，以后逐渐发生交谈困难。多数老人有一定程度的耳鸣，多为高调性，开始时仅在夜深人静时出现，逐渐加重，持续终日。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中枢性老年性聋为大脑老化所致。对老年中枢性聋的研究往往又受到周围性听觉器官老化的影响，临床上可表现为对外界声音的感受下降、对外界声音的感受失真、中枢对外界信息处理紊乱、在噪声环境下听力障碍明显、声音的定位障碍等等，并使得对老年人中枢性听觉功能的检测、判断变得复杂。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 助听器，老年性聋患者的生活伴侣</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前老年性聋的治疗缺乏有效的药物，其干预的有效策略主要为配戴助听器，极少数患者出现极重度听力下降时，可考虑人工耳蜗植入。一般而言，佩戴助听器有适应的过程，一般1～2个月才能习惯。另外，戴助听器在室内辨别各种语言效果较好，但在公共场合及嘈杂环境效果不好，因为噪声也会被放大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 轻度耳聋一般不需配戴助听器；一般听力损失35～85分贝者可配戴助听器。听力损失在60分贝左右，效果最好。重度耳聋也可配戴，但助听器失真度增加，往往达不到理想的效果。对于极重度聋者，助听器虽能增加音响，但其语言识别率很低，故无实用价值，此时可考虑人工耳蜗植入。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前国外对重度、极重度耳聋的老年性聋患者开展人工耳蜗植入并取得较好的效果，患者的言语康复取得明显的提高，患者的生活质量也得到明显改善。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在日常生活中同老年性聋患者交流时说话应清晰，自然，不应大声叫喊；而应尽量靠近对方，特别是在噪音环境下，面对老人，让老人利用唇读更便于理解谈话内容；在谈话前提醒听者注意，考虑噪音环境状况长时间使用助听器易产生听觉疲劳，交谈时要有耐心。此外，同老人交谈时，应避免竞争性声源的存在，因此应关电视和收音机等声源。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如何预防老年性聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 人体老化是大自然给予的恩赐，我们无法拒绝，但是我们可以采取一些措施推迟这恩赐的到来。这就需要我们对老年性聋的发病原因有所了解。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 老年性聋的病因复杂，其发病机制目前尚未完全阐明。可能的发病原因不仅包含听觉系统衰老过程,还受到生活中环境与社会因素的影响。老年性聋的发病年龄及其发展速度，与遗传因素有关。笔者目前正从事相关研究，以期为老年性耳聋的防治提供新的策略。另外，老年性聋还与环境因素有关，如环境噪声、动脉硬化、高血压、高脂肪饮食、吸烟酗酒、耳毒性药物或化学试剂、感染、思想生活压力加重等。在日常生活中避免噪声，应从小做起，避免对听觉器官的慢性损伤。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 老年性聋的预防还应注意劳逸结合，保持心情舒畅；注意饮食卫生，减少脂类食物</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">；戒烟酒，防治心血管疾病等全身疾病；避免接触噪声；慎用耳毒性药物；进行适当的体育锻炼。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 此外应引起重视的是，老年性聋患者由于长期听力障碍，同周围人的交流减少可产生社会孤立感，生活质量下降。这种情感上的障碍值得引起家庭和社会的重视。</span><br />
