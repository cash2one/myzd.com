		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>广州李逊教授领衔，三名省泌尿外科名医加盟昆仑</strong></span>
</p>
<p>
	<br />
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">日前，广东省泌尿外科名医加盟仪式取得圆满成功。三名蜚声省内外的泌尿外科专家：广州医科大学附属第五医院原院长/微创经皮肾手术创始人/金膀胱镜奖荣誉获得者李逊教授、省泌尿生殖协会总顾问董诚教授及省泌尿生殖协会秘书长夏明义教授慕名加盟深圳昆仑泌尿外科医院，分别荣膺深圳昆仑泌尿外科医院专家团总顾问、技术顾问及顾问荣誉证书。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 他是第一位全世界用经皮肾镜微创术取石超过一万例的专家；每天都有人从全国甚至全世界坐飞机、坐火车来到他所在的医院找他治病；在病人眼里，他是一个温柔、负责的好大夫；在业内，他是一个大名鼎鼎的人物，他发明和改良的手术器械“李逊微创肾镜”等系列手术器械获得国家专利并成功转化为医疗产品造福病人。他负责的国家级培训基地坐落在羊城，他教授过的医生遍及全国，人数过千。他就是广州医科大学附属第五医院原院长、深圳昆仑泌尿外科医院技术顾问李逊教授。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 据介绍，李逊教授自1984年起从事泌尿外科微创新技术的临床研究及推广应用，微创经皮肾手术创始人。在输尿管镜取石术、前列腺微创手术及泌尿腹腔镜手术等领域造诣颇高。广东省有突出贡献专家，享受国务院政府特殊津贴，曾获国内腔内泌尿外科最高荣誉奖“金膀胱镜奖”、第十六届“恩德思”国际内镜奖———泌尿外科奖。卫生部有突出贡献资深专家，多次获国家、省部级、市级科技成果奖。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 器材的设计缺陷也增加了医生操作手术的难度和劳动强度。李逊回忆，最长的一次输尿管取石微创手术从早上9时一直做到晚上10时，一共做了13个小时。当时没吃没喝没拉一直做，下了手术台整个人都快累虚脱了。日复一日的手术一台又一台，转眼四五年过去了。李逊开始动脑筋琢磨，“设备能不能改进一下，用得顺手一点，起码密封性好点？”学过一年机械的他开始试图改进这些器材。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 此后，一有机会，李逊就与德国医疗器械厂商沟通，提出器械在手术使用过程中的不足之处，但对方根本不理会。2004年至2005年间，李逊凭借自己对机械的了解，开始画图设计适合国人尺寸的微创肾镜，后来再找来中国的工程技术人员去制作，成功改良了原来的手术器械，并一直不断将使用的意见向德方反馈。新器械更好地解决手术中通水、灌注和冲洗中的一系列问题。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 机会总是青睐有准备的人。这一年，李逊应邀参加一个国际学术会议，会上德国人希望真实地看一看：到底哪一种器械手术效果好。于是，一场精彩的同台比武展开：李逊与来自日本、德国、中国台湾三位著名的泌尿外科专家“同台演出”。其他专家用原有的器材，李逊用的是自己改良过的器材，同时进行经皮肾镜碎石术。结果当李逊手术完成了三台时，其他三位专家第一台手术还没做完。全场掌声雷动。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 2006年，李逊代表我国出席美国泌尿外科年会以及世界腔内泌尿外科会议，并在大会上介绍了其创立的中国微创经皮肾取石术的方法及经验，得到国内外学者的充分肯定，使该技术在国内外得到推广。“他们只要认同你了，佩服你了，就会完全相信你！”德国设计师开始按照李逊的意见设计器材，并命名为“李逊微创肾镜”。2008年，李逊获得实用新型国家专利，开始在世界广泛推广应用。并凭借此项技术，荣获国内腔内泌尿外科最高荣誉奖“金膀胱镜奖”、第十六届“恩德思”国际内镜奖———泌尿外科奖。2009年，李逊采用多种方法加多种机械“软硬兼施、上下沟通、长短结合、双管齐下”的理念，设计出一款更标准的新式“李逊微创肾镜”。这种设计缩短了手术时间，减少了病人痛苦，同时也减少了医生的劳动强度。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 董诚教授兼任北大等多所高校教授，广东省泌尿生殖协会工作委员会常委及总顾问。精通泌尿外科各种疑难病症的诊治以及泌尿外科疾病的腔内微创治疗，尤其在泌尿系结石、前列腺增生、膀胱肿瘤等领域造诣极高。在《中华医学》、《中国性科学》、《男科学》等国家级刊物上发表论文百余篇。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 夏明义教授是广州医学院附属第一医院的主任医师，从事临床工作30余年，泌尿外科、显微外科学科带头人，在男科疾病、不孕不育诊治上有较高造诣，特别是在性功能障碍、泌尿感染、泌尿系结石、肾病等方面的治疗上有深入研究，疗效显著。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 深圳昆仑泌尿外科医院院长表示，为进一步拓宽临床诊疗范围，加强与泌尿外科名医名院深度交流与协作，搭建更加良性的泌尿外科名医行医平台，在即将到来的2015年，医院将继续以高、精、尖技术力量为核心，重点围绕四中全会及2015年《医改蓝皮书》关于进一步落实医生职业化的坚实路线，并在强化深圳市“三名工程”名院名医名校人才政策下构建名医健康堡垒，持续为深港男性同胞健康服务，并以此为起点，为深圳乃至全国创新医疗模式树立典范。</span>
</p>
