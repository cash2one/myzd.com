</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3054/2.png" title="" alt="demo.jpg"/>
</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 编者按：6月5日，近六万人参加的美国临床肿瘤学会年会在美国结束。健康时报在931期焦点版对大会上的中国医生做了专题报道。除北京肿瘤医院郭军教授所做黑色素瘤主题演讲外，四川大学华西口腔医（学）院的李龙江教授也与国际同行分享了他关于口腔癌研究进展的报告。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 名词解释：肿瘤基因治疗——将目的基因用基因转移技术导入靶细胞，使其表达此基因而获得特定的功能，继而执行或介导对肿瘤的杀伤和抑制作用，从而达到治疗目的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家简介：李龙江，教授、主任医师，四川大学华西口腔医（学）院院长助理、头颈肿瘤外科主任，四川省卫生系统学术带头人。担任全国头颈肿瘤专委会常委，四川省及成都市有突出贡献优秀专家。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 擅长：长期从事头颈肿瘤防治及修复重建的研究及临床工作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 数字说会：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2744——2012ASCO会议共采纳论文2744篇，比2011年的2574篇多出140篇，高出5.4%，其中口头报告257篇。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 152——中国这次共提交论文152篇，占提交总数的2.89%，提交论文数比去年多出5篇，提高了3.4%。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 8——本届大会中国专家提交论文数位列美国（2346）、日本（253）、法国（248）、意大利（245）、德国（241）、西班牙（201）、加拿大（175）之后第八位，与去年持平。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 相关数据来自ASCO官方报告&nbsp;</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 参会就像来“朝圣”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “这已经是我第三次参加ASCO会议了，这个会对于研究肿瘤的医学家来说，就像是一个圣地，全世界的业内人士如果不来这个会议朝拜一番，是不够虔诚的。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 谈到刚刚结束的ASCO会议，四川大学华西口腔医院的李龙江教授意犹未尽。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这次参会，李教授带来的是“选择性动脉灌注重组人p53腺病毒基因联合化疗对晚期口腔癌的II期随机双盲临床试验”项目报告。在McCormick Place会展中心能容纳2005人的E354b会议室，8个世界头颈部肿瘤研究领域的最新成果依次展示，而李教授的这个项目，作为惟一中国入选项目位列其中。原定发言的李龙江教授，为了与芝加哥大学探讨合作计划，把这次机会让给了自己的学生。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 因为全球经济危机影响，欧美国家对头颈部肿瘤基因治疗的研究停滞了三四年，而中国这次带来的新的研究成果无疑是一场世界范围的“及时雨”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 中国为肿瘤基因治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 树立标杆</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “十多年以前，我作为一个外科医生，在治疗头颈部肿瘤的过程中，发现传统的治疗方法进步空间狭小，并且难根治，易复发，就萌生了寻找其他治疗手段的想法。这时候国际上肿瘤基因治疗开始起步，我就选择了这个方向，跟上步伐，在国内进行研究。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 打个简单的比方，肿瘤细胞是人体里极其狡猾的犯罪分子，清除起来十分困难。传统的治疗手段可以称做宁可错杀一千，也不放过一个，临床上通过手术、化疗、放疗等方法，以达到消灭肿瘤的目的。但是在治疗同时，也给机体造成了极大的损害，疗效有限。而肿瘤基因治疗技术则是专门针对这些犯罪分子，进行教育改造，降低控制肿瘤的恶性程度，使肿瘤低害甚至无害，对于奉公守法的正常细胞是不会去骚扰的。这种新型的治疗手段，极大的减轻了病人的痛苦。目前看来惟一的副作用就是治疗期间会带来发烧。可是这种发烧不仅对杀死肿瘤细胞有促进作用，而且使用小剂量的退烧药和简单的物理降温就能控制，减轻痛苦，提高了患者的生活质量。李教授解释道。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 目前，中国是世界上惟一拥有已上市肿瘤基因治疗药物（今又生）的国家。“现在对于这类药物的用法、用量和安全性等的研究，中国是最有发言权的，东南亚国家、日本、韩国都是参照我们制定的标准来进行临床应用，其他国家使用这个药也必须从我们国家进口。”李教授自豪地说。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 70家三甲医院开展</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 试验治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 谈到下一步的工作，李教授说：“我们在基因治疗头颈肿瘤领域内进行了成功的探索。本次会议上公布的99例口腔癌患者经5年以上随访，疗效满意。这只是我们整个实验数据库的一部分，实验预计的样本总数应该在1500例左右。目前我们联合了国内70多家三甲医院，已完成2/3，其中对照组300多例，治疗组700多例。这也是国内惟一在北美注册的临床试验。但因为治疗费用比较昂贵，国内广泛开展此项技术存在一定难度。我们希望通过努力，尽可能的为国内患者制造治疗条件。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 关于ASCO会议，李教授说：“西方医学界制定的治疗指南和治疗规范，对于完善我国医疗体系有很大的借鉴意义，ASCO会议就是一个非常好的学习机会，就目前而言，我们还是应当向先进国家看齐，用句四川话来讲就是‘学习不能蒙着脑壳’。”</span><br />
