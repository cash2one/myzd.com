		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>神经短路　 姑娘半边脸抽动十多年</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 新华报业网讯26岁的何丽（化名）患上了一种怪病，紧张或见到陌生人时，半边脸顿时不听使唤，抽动不停，十年来饱受折磨。日前她去医院再次检查才发现，原来是面部神经短路了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> &nbsp; &nbsp; 何丽说，她从小就体弱多病，平均半个月就感冒一次，小时还因感冒并发心肌炎，由于治疗不及时，留下了后遗症。就在15岁中考前夕，她又因感冒发烧患了肺炎，治疗期间，她发现自己的左边脸有时会不由自主地跳动，一病就是十多年。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> &nbsp; &nbsp; 害怕别人看到她跳动的左脸，整个大学期间，何丽不愿与人交往，也没有谈恋爱。大学毕业后，凭借努力和爱心，何丽赢得了学生和同事的认可，但她左半边脸抽动的症状越来越严重，严重影响了何丽的生活，至今没有谈恋爱。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> &nbsp; &nbsp; 在鼓楼医院，她被查出是一种面肌痉挛的疾病。南京鼓楼医院神经外科梁维邦主任介绍，面肌痉挛的病因目前尚未完全明确，研究表明可能是面神经受压损伤后，互相接触，神经冲动发生短路引起。昨天，何丽在鼓楼医院神经外科成功接受了手术治疗，经过微创手术，专家为何丽被压迫的神经进行了减压，小何摆脱了左脸抽动的烦恼。</span>
</p>
