<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>难治性癫痫新疗法--迷走神经刺激术</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 癫痫是一种常见病，与卒中并列成为影响人类中枢神经系统健康的最常见病症，引起了世界卫生组织的高度重视。目前，全球共有癫痫患者5000万，其中我国将近有1000万，而且至少有一半病人在儿童或青少年时期发病，严重影响患者的正常学习、工作、和生活，成为社会和家庭的沉重负担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大约70%的癫痫发作可以用药物完全控制，但是癫痫是一种慢性疾病，需要坚持多年长期规律服药；尽管采用了各种各样的新型抗癫痫药物，仍然大约30%的患者不能满意控制发作，就需要借助外科手段进行治疗了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">外科手术治疗癫痫，是现代医学的一大进步，是药物难治性癫痫患者的有效治疗方法，可以使一半以上患者的癫痫发作完全消失。但是，相当一部分患者、家属、甚至是医生，对手术心存疑虑和恐惧，宁可历经多年反复更换或添加药物进行治疗尝试，或企图寻找神医、秘方等来达到目的，结果往往事与愿违，劳财伤身，耽误病情。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癫痫外科手术治疗，有严格的手术适应症。手术之前需要进行严格的评估，对于适合手术的患者，还要根据患者的具体情况，制订个体化的手术方案。手术大体上可以分为两类：切除性手术(根治性手术)和姑息性手术（减轻症状的手术）。当引起癫痫发作的病灶很局限、能够明确定位并且不在功能区时就可进行切除性手术；当病灶多发或无法确定时可以进行姑息性手术，这种手术主要包括胼胝体切开术和迷走神经刺激术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">初次发作的癫痫患者，应该到</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">正规医疗单位神经内外科就诊，接受包括核磁共振和脑电图等在内的多种必要检查，对没有发现颅内病变(脑肿瘤和血管病、皮层发育不良等)的患者给予药物治疗；最新研究结果显示，对于颞叶内侧癫痫等手术效果较好的患者，如果两种单药不能有效控制发作者，应尽早手术，避免长期反复发作对脑功能的损害，因为临床研究已经显示，更换第三种药物时完全控制率低于3%，而颞叶内侧癫痫手术后完全控制率超过70%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">迷走神经刺激术（vagus nerve stimulation,VNS）是一种新的治疗难治性癫痫的有效外科手段，它具有不用开颅、不损伤脑神经组织、微创、可逆、安全性高等优点，自应用于临床以来受到广泛欢迎。不但可以用于药物无效的患者，对于手术后无效或复发者也适合进行此手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">迷走神经刺激术（VNS）是美国食品及药品管理局批准作为难治性癫痫的辅助疗法，其原理是将刺激器植入左锁骨下的胸大肌浅层，刺激器电极固定于颈部迷走神经，胸壁刺激器不断发射微弱电脉冲，刺激迷走神经，从而引起脑电活动及神经递质变化，达到治疗癫痫的目的。临床研究结果显示，迷走神经刺激术可以使大约10%难治性癫痫患者获得完全控制，随着植入时间的延长，通过不断调整刺激参数，大约70%患者发作次数可减少50%以上。值得一提的是，这种疗法对于改善患者的生活质量也有作用，可以使患者的警觉性、语言技巧、思维能力、独立性等方面，获得不同程度的进步。目前国内外迷走神经刺激术(VNS)在儿童以及成人难治性癫痫患者中的应用越来越广泛，对儿童的记忆力、情志改善尤为明显。随着研究的深入，VNS已经广泛应用于无法进行切除性手术的各种年龄段难治性癫痫患者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全球已有超过13万例迷走神经植入术治疗难治性癫痫，中国已超过800例患者接受了迷走神经刺激术治疗，目前在上海华山医院、北京天坛医院等全国多家神经外科中心均开展了此项技术，为广大难治性癫痫患者带来了福音。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，癫痫是一种常见的神经系统发作性疾病，患病后要正确对待，患者及监护人最好能对发作过程进行详细记录，然后提供给医生，将对确定诊断和调整治疗非常有帮助。患病后应该到正规医疗单位的专科就诊，以免耽误病情。要严格按照医嘱服药，不可随意增减或停换药物。除了药物治疗以外，可以酌情采用包括迷走神经刺激术在内的外科治疗手段，大多数可以取得良好疗效。</span>
</p>
