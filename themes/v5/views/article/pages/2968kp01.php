<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肩膀疼不一定就是肩周炎</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肩袖损伤多发于青壮年的运动外伤</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 为何被医生认定的“肩周炎”最后却被确诊为“肩袖损伤”？两者又有何不同呢？广州军区广州总医院骨科医院副院长、骨病关节科主任黄华扬主任医师说道；“目前对于肩袖损伤的认识还处于相对滞后的阶段，不少市民甚至普通的骨科医生都常常误认为肩痛就是肩周炎，进行不恰当的按摩、理疗及功能锻炼，很难起到治疗效果。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “肩袖损伤主要是指由于旋转肩袖肌腱外伤性损伤或退变基础上解剖学结构异常导致的损伤断裂，发病率大概占到肩关节疾病的17%，最常见的病人主要是青壮年的运动造成的外伤和老年人退行性病变。”黄华扬主任表示：“肩袖损伤的主要表现有三大方面，首先是疼痛，运动时（尤其是患肢高举超过头顶时）疼痛和夜间痛多见；其次表现为肩关节无力：主要为外展、外旋和内旋力量的减弱，常洗脸、梳头、穿衣、拿放高处的物品以及驾驶等日常活动的困难；最后是肩关节活动受限：主要为主动上举（包括外展和屈曲）、外旋和内旋活动度的降低。但被动活动范围通常无明显受限。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肩袖损伤若不治疗病情易恶化</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肩周炎是自限性疾病，通常通过外部的一些理疗就会痊愈，且痊愈后一般不会复发，但是若是肩袖损伤，若不治疗患者会出现上举没有力且没有办法另双肩外伸或扩展，且病变会进一步发展，导致肩袖更严重的撕裂，影响关节的功能，因此如何早诊断早治疗就显得尤为关键。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 黄华扬主任称：“由于肩周炎和肩袖损伤都存在肩关节疼痛和活动受限，鉴别存在一定的困难，如果患者出现患肢上举无力，高举过头顶时疼痛明显，被活动范围大于主动活动范围情况就得高度怀疑肩袖损伤，需到专业的关节科或运动医学科就诊，做磁共振（MR）以明确有没有肩袖损伤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肩关节镜手术是肩袖损伤的首选手术方法</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对于肩袖损伤的治疗一般有两种，如果病情较轻可采用保守治疗，但是如果冰枪一直无法缓解就需要手术治疗，现在对于肩袖损伤的手术一般都采用肩关节镜检手术，一种微创对于病人恢复更快的手术方法。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 保守治疗：轻度的肩袖损伤可以口服非甾体类抗炎药和肩峰下间隙封闭注射，封闭注射一般不超过3-5次，以免出现肌腱断裂。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术治疗：对接受系统的保守治疗三个月至半年，病情无明显缓解甚至加重的患者需要采用手术治疗。具体要根据患者的年龄、活动要求及断裂部位进行综合考虑。分为开放手术和肩关节镜手术。</span><br />
<p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 黄华扬主任介绍肩关节镜手术时说道，主要是通过标准的前方、后方和外侧通路插入关节镜和器械进行肩峰下减压和用缝合锚进行肩袖的修复。医生在关节镜的监视下，视野宽阔，能清楚地看到相关结构，手术显得空前的安全，侵袭性小，且避免了开放手术所带来的潜在的危险的并发症，尤其是减少了三角肌和肱三头肌的损伤，能诊断并评价撕裂的类型及对损伤进行治疗；肩关节镜手术创伤小，术后康复快，是治疗肩袖损伤的首选手术方法。目前广州军区广州总医院骨科医院骨病关节科已广泛开展了肩关节镜手术治疗肩袖损伤，优良率在90%以上。</span>
</p>
