		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>慢性鼻窦炎的诊治和饮食管理（科普）</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"></span>&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>慢性鼻窦炎</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一、鼻窦炎有哪些表现？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">局部症状：脓涕多、鼻塞、头痛、嗅觉减退或消失。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.流涕：多为脓性，黄、绿色或灰绿色，病程长者鼻涕可有息气，脓涕常可经后鼻孔流至咽喉，病人自觉咽部有痰，并常经咽部抽吸后吐出。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.鼻塞：因鼻粘膜充血，鼻甲肥大或鼻息肉所引起，有时亦可因脓涕太多，于擤出鼻涕后鼻堵减轻。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.嗅觉下降：多为两种原因所致，一为鼻粘膜肿胀、鼻塞，气流不能进入嗅觉区域，二为嗅区粘膜受慢性炎症长期刺激，嗅觉功能减退或消失。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.头昏、头痛：慢性鼻窦炎多表现为头沉重感，急性发作时可有头痛，均为鼻窦内引流不畅所致。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">全身症状：精神不振、易倦、头昏、记忆力减退。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">二、慢性鼻窦炎可引起哪些并发症？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.由于后组筛窦及蝶窦与视神经距离很近，可发生视神经炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.额窦或小儿上颌骨发生骨髓炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.可发生眼眶骨膜炎或骨膜下脓肿，重者可能出现眼眶蜂窝组织炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.颅内合并症虽发病率低，但非常危险，可危及生命。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5.鼻窦炎，可向下行感染，引起咽炎、扁桃体炎、喉炎、气管炎、支气管扩张等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6.因脓液流人胃，可引起胃功能紊乱。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">7.可引起关节炎、心、肾等脏器的合并症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三、慢性鼻窦炎的诱因有哪些？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.全身原因，如疲劳、爱凉、营养不良、变态反应体质、烟酒过度、全身疾患如结核、梅毒等，以致身体抵抗力减弱。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.患急性鼻炎（即感冒）时感染蔓延至鼻窦。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.鼻腔疾病妨碍鼻窦通气与引流，如鼻中隔偏曲、鼻甲肥大、鼻腔变态反应性病变、鼻腔肿瘤、异物。以上疾病均可阻塞中鼻道或上鼻道，妨碍鼻窦通气与引流。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.慢性扁桃体炎及儿童患增殖腺者常使鼻腔阻塞，易诱发鼻窦炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5.鼻腔手术或治疗鼻衄时鼻腔填塞物置用时间太久。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6.游泳跳水，污水吸入鼻窦而生炎症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">7.鼻窦外伤骨折或异物存留。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">8.上颌窦炎常因牙病而发生。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">因以上种种原因，细菌侵入鼻窦，破坏组织，发生急性或慢性炎症四、如何预防慢性鼻窦炎？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.加强体育锻炼，增强体质，预防感冒。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.应积极治疗急性鼻炎(感冒)和牙痛。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.及时、彻底治疗鼻腔的急性炎症和矫正鼻腔畸型，治疗慢性鼻炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.慢性鼻窦炎的病人由于病程日久，常伴有全身不适及容易烦躁等表现，居住室内应保持空气新鲜，冬季气温变化不应太大，注意休息，坚持治疗。小儿患病应按时用药。患者多做低头、侧头动作，以利鼻窦内脓涕排出。清洁鼻腔，去除积留的脓涕，保持鼻腔通畅。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5.积极预防感冒，在上呼吸道感染期及时治疗，因为“上感”治疗不彻底，常是慢性鼻炎及慢性鼻窦炎的诱因。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6.工作环境粉尘、污染较重的地方，应戴口罩，避免细菌进入鼻腔。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">7.注意不用力擤鼻，脓涕多者可先滴药、再擤鼻，以免单个鼻窦炎因擤鼻不当，将脓涕压人其他鼻窦而导致多个鼻窦发炎。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">8.禁食辛辣、肥腻刺激性食品，戒除烟酒。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">五、如何治疗慢性鼻窦炎？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">原则为通畅鼻窦引流，去除病因。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.滴鼻药：血管收缩剂能收缩鼻腔肿胀的粘膜，以利引流。常用1%麻黄素液或呋喃西林麻黄素液、氯霉素麻黄素液滴鼻。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2..理疗 &nbsp;一般用超短波透热疗法，以辅助治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">六、饮食指导</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">患者宜多吃：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1、新鲜水果和蔬菜，以摄取维生素c和生物类黄酮;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2、贝类和坚果，以摄取锌;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3、全谷类和豆类，以摄取维生素b;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4、葵花子、种子油，以摄取维生素e;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5、具减充血作用的草药和调味品，例如大蒜、洋葱和姜、接骨木花、麝香草。</span>
