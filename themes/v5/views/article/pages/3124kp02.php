			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我理解的微创神经外科</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">外科是手术刀下的艺术，从本质上来讲，它是通过可接受的创伤来为患者祛除躯体的病痛。为了让这种可接受的创伤越来越小，自诞生之日起，外科医生就没有停止过对微创的追求。现代条件下，每位患者都希望获得最为微创的治疗。这就在客观上对外科医师提出了要求。那么什么是微创？外科医生如何才能做到微创？下面我就从医生的角度谈一下自己理解中微创。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 微创，其实是一个相对的概念。简单地讲就是要用最小的代价为患者的健康获得最大的利益。比如，对一个巨大脑膜瘤患者，不行手术，医生肯定不会给患者带来创伤，但患者的肿瘤却得不到治疗。这应该不符合微创理念。那么，为了全切占位，却让患者神经功能严重受损甚至付出生命的代价，这也不符合微创理念。微创，就是要求医生根据患者的病情和实际情况，在尽力保证患者安全的前提下最大限度的切除肿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 为了做到微创，首先客观上要求医生要对患者所患疾病有非常深刻的理解。同样的疾病，不同的患者，情况可能会千差万别。就像同样是四只腿的猫，但你很难找到两只长相完全相同的猫一样。同一种疾病你也很难遇到两个情况完全相同的患者。所以，只有深刻的理解了疾病的病理本质，才有可能为每个独立的患者提供个体化的治疗方案，才可能将治疗的创伤降到最低。其次，好的医生一定要拥有精湛的技艺。现代微创神经外科是在显微神经外科基础上发展而来的。很难说一个不会应用显微镜的神经外科医生可以将手术做到微创。而学会应用显微镜并非仅仅是拥有一台显微镜那么简单，而是要经过在显微镜下严格的操作训练才能做到。只有通过训练，医生才能在显微镜下做到游刃有余。也就为实现自己理想中的手术操作提供了技术保证。再次，疾病的治疗过程中会涉及诸多部门、诸多医护人员。如接诊医生、经治医师、手术主刀医师、手术助手、麻醉医师、主管护士以及其他许许多多的相关辅助医护人员，只有这些人有机的整合，通力协作，才能将各种可能的危险因素消除在萌芽状态，患者才会安全。因此，一个团结协作的团队是保证疾病治疗微创化不可忽视的条件。以上是从人的角度来理解微创治疗的要求。从物的角度来讲，必须要有足够的外部条件来支撑一个优秀的团队来完成他们理想中的手术治疗，赤手空拳肯定做不了手术。这就是老百姓常说的硬件设备的支撑。其实除了以上各种因素之外，医者的一颗“佛心”也是应该是将“微创”做到极致的关键！</span>
</p>
