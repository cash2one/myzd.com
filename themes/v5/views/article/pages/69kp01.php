<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>癫痫病人日常生活注意事项</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.绝大多数癫痫患者在正规合理地服用抗癫痫药物后均可得到有效控制。请遵医嘱按时规律服药，不要漏服或自行增减药物。当疗效不好时请及时到医院复诊，遵医嘱调整治疗方案。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.日常生活中需养成良好的生活习惯，严禁熬夜，作息要规律；适当锻炼身体，避免劳累、体力透支等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.饮食方面：正常吃饭，一般食物都没有问题。不要暴饮暴食，不要“大补”，如甲鱼、人参、大量食用牛、羊肉等。不要喝碳酸饮料及甜饮料；不要喝浓茶、咖啡、等兴奋性饮料；严禁吸烟、喝酒。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.严禁驾驶、登高、近水等危险性工作。不要长时间看电脑、电视、手机，减少使用屏幕的时间；不要长时间玩电脑游戏、搓麻将、打扑克、下棋等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.遵医嘱定期复查脑电图、血常规、肝功能、肾功能、凝血功能、血药浓度等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6.禁用药物：A 青霉素类；B 喹诺酮类；C 甲硝唑、替硝唑；D 凡药物说明书写明癫痫病人慎用或禁用的药物均不能使用。需要同时服用其他药物时，请务必咨询医生。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"></span>
