			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>谈谈老花眼</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">老花眼多出现在40至45岁以后</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“与白内障、青光眼等眼部疾病不同，严格来讲，老花眼不能被称为疾病。它是一种由年龄增大而导致的生理上的眼调节能力下降，就像每个人的头发都会变白，一般表现为容易出现眼部疲劳，看近处不清楚，就像相机不能聚焦一样。”北京大学第三医院眼科中心主任医师陈跃国介绍。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 老花眼的发病年龄一般在40至45岁以后，从原理上讲，所有人都会发生老花眼。但是，老花眼症状的出现也可能提前或延后，这跟他以前伴有的远视或近视的状态有关。年轻时患近视的人，出现老花眼的年龄会延后，因为近视度数可以抵消一部分的老花度数，而年轻时视力特别好或者患有远视的人，出现老花眼的年龄则会提前。另外，精神紧张、忧郁等情绪问题和全身疲劳的状况，也会造成眼睛调节能力的下降，导致老花眼出现过早。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 老花镜需专业验配并定期更换</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">老花眼不是疾病，不需要治疗，但它会给生活带来不便，而减轻这种不便的最好方式就是配戴老花镜，陈跃国医师说，但是，很多人在配戴老花镜上存在着误区。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">配老花镜时应该去正规医院验光和检查，而不是去眼镜店随便挑一副。老花镜也是有度数的，原来有近视、远视以及散光的情况，也会影响到对配镜的要求。曾患近视或远视的人，可能会需要配两副眼镜，一副看远，一副看近。比较简单的方法是配双光镜，即每个眼镜片上都有两个度数，当视线往上时，度数浅一点，适合看远，当视线往下时，度数深一点，适合看近。另外还有一种渐变焦的老花镜，视线从上往下时，度数逐渐变深，可以避免双光镜的视觉跳跃现象。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 很多老年人喜欢用放大镜帮助阅读，其实，放大镜的放大倍数过高，长期使用，会让眼睛产生依赖性，以后就不容易配到合适的老花镜了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，老花的度数会随着年龄的增长而变化，往往还会加深，一般每隔两三年，老花镜也需要调换一下。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 青光眼、白内障也会出现老花假象</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">老花眼主要的症状表现是看近处（比如阅读书报）的视力下降、容易出现眼疲劳、眼睛胀痛等，但是出现这些症状时，千万不要理所当然地认为一定是老花眼，陈跃国医师介绍。因为青光眼早期也会出现眼睛胀痛，白内障早期也会出现视力下降，糖尿病引发的眼病也会有类似的表现，这个时候就需要鉴别。如果草率地把这些症状当做是老花眼的表现，去眼镜店随便挑一副老花眼镜，很有可能会掩盖病情错过治疗时机，所以，出现类似症状时，一定要先去专业医院验光和检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 激光手术也可矫正老花眼</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">陈跃国说，很多白领尤其是女性比较爱美，不想显得老态，不想过早地戴老花镜，也可以进行激光手术矫正老花眼，跟远视矫正的原理差不多，就是通过激光的方法，让眼睛的角膜形态变得陡峭一些。以前进行过近视矫正的人，也仍然可以做。这项手术从技术层面上讲是成熟的，不过还不流行，医生也不会重点推荐，因为配戴老花镜毕竟是一种最为安全、简单而且是有效的方法。出现老花症状 &nbsp;需去医院验光检查</span><br />
