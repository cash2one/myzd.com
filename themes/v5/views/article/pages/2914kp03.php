		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻息肉的诊治和饮食管理（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>鼻息肉</strong></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">一、什么是鼻息肉？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻息肉是鼻部常见疾病，也与某些全身疾病有关。它是由于鼻粘膜长期炎性反应引起组织水肿的结果。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">二、鼻息肉的症状有哪些？&nbsp;&nbsp;&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">由于鼻粘膜是整个呼吸道粘膜的一部分，而且鼻与气道之间存在鼻肺反射，故鼻粘膜病变可借助某种机制与呼吸道其他疾病相关联。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.支气管哮喘&nbsp;&nbsp;大量临床资料统计发现，鼻息肉患者有较高哮喘发病率。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.阿斯匹林耐受不良和Widal三联症</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.呼吸道粘膜先天性异常&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.高血压病 Granstrom（1990）调查了224名鼻息肉患者，发现其中78名（34.7%）患有高血压，这类患者鼻息肉病史均在10年以上。该氏认为，就像睡眠呼吸困难综合征一样，鼻息肉引起的长期息塞可促发高血压。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">三、鼻息肉应该如何预防？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.应积极防治各种慢性鼻病，如鼻鼽、鼻渊等，以预防变生息肉。锻炼身体，增强机体抗病力，预防伤风感冒，以免症状加重。增强体质是一个很关键的问题，只有体质增强了才会减少各种疾病的发病率，特别是鼻部疾病。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.长时间的感冒不愈最容易发生鼻息肉。对于息肉较大者，长久吃药是不好的，手术是最好的治疗方法，很多患者在手术后就开始肆无忌惮，这样也是一种错误的表现，手术后的复查和预防也是关键之处。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;3.鼻息肉在饮食营养治疗方面也有一定的讲究，在饮食预防方面，少吃油腻肥厚之物，避免过食生冷、鱼虾等腥荤之物，戒除烟酒，忌食辛辣刺激性食物，多吃蔬菜、水果、动物肝脏等食物。加强卫生宣传教育工作：在冬春寒冷季节或感冒流行期间，外出须带口罩，避免公众集会，尽量少去公共场所，对发病者作好隔离工作。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.避免导致人体抵抗力下降的各种因素：如过度疲劳、睡眠不足、受凉、饮酒、吸烟等;这是因为，当人体抵抗力下降时，鼻粘膜调节功能差，防御功能低下，病毒乘虚入侵导致发病。增强抵抗力：坚持体育锻炼，增强体质，提高人体对不良条件的适应能力，如晨跑、冷水浴或冷水洗脸等，提高人体对寒冷的耐受力，并积极治疗上呼吸道疾病及全身其他慢性疾患。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">四、如何治疗鼻息肉 ？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.对初诊患者，如息肉较大，严重影响鼻功能，应先行外科治疗；息肉较小，鼻塞症状较轻者，可行内科治疗，1个月后疗效不佳者再行手术治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.对伴有鼻窦感染者，宜先抗感染，或行鼻息肉摘除，以利开放鼻窦引流。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.对复发性鼻息肉，应行筛窦切除术，术后鼻内应用皮质类固醇1～2年。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">五、饮食护理</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.鼻息肉患者不适宜吃什么?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">少吃油(油食品)腻肥厚之物，避免过食生冷、鱼虾等腥荤之物，戒除烟酒，忌食辛辣刺激性食物。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.鼻息肉患者适宜吃什么?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">多吃蔬菜(蔬菜食品)、水果(水果食品)、动物肝脏等食物。</span>
</p>
