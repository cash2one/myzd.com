			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>早期食管癌根治可尝试锁孔手术</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">新闻中心讯 近年来，复旦大学附属肿瘤医院胸外科主任相加庆教授和张杰副教授领衔的临床科研团队，应用内窥镜下根治术治疗早期食管癌，5年来已累计完成200例，术后死亡率为零。随访发现，接受内窥镜下根治术的患者术后并发症发生率为1%，局部复发率为3.3%，50%的早期食管癌患者得到根治。该成果论文已发表在国际著名期刊《临床胃肠外科杂志》上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">相加庆介绍，以往对早期食管癌均采用开放式手术或腔镜手术方法治疗，创伤大、恢复慢、费用高。内窥镜下根治术具有创伤小、恢复快、费用低等优势。临床医生需严格把握内窥镜下根治术的治疗指征，筛选出最为适宜的治疗人群。该院临床实践证明，淋巴结未出现转移，最大直径小于3厘米、扁平型、局限在黏膜层的早期食管癌患者，是此项治疗的适宜人群。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经过多年探索，复旦大学附属肿瘤医院胸外科制定了严格的内窥镜下食管癌切除术流程，包括肿瘤大小、病灶所处位置、浸润程度、适应证、禁忌证、病理标准、并发症防治、出院及随访标准等。同时，该院在治疗中如发现病理诊断为黏膜下癌或者食管穿孔等患者，胸外科医师会及时干预，避免“二次治疗”所需的等候时间，让患者尽快得到治疗。</span>
</p>
