			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>治疗烟雾病还需手术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">北京有位上四年级的小女孩，聪明漂亮，爱好芭蕾。今年4月的一天，小女孩在练习芭蕾舞时突然晕倒在地。家长带着女孩四处求医，最后经人介绍，找到北京解放军307医院神经外科主任段炼教授求治。段炼主任为女孩做脑血管造影检查，发现女孩双侧颈内动脉闭塞，出现脑供血不足，导致晕倒，诊断为烟雾病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病是缺血性脑血管病</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病听起来比较陌生。这种病别说是普通百姓没听说过，即使是临床医生也不见得都能认识清楚。其实，烟雾病属于一种缺血性脑血管病。有些病人由于颈内和大脑的动脉内膜缓慢增厚，造成动脉的管腔逐渐狭窄以致闭塞，为了补偿脑血流量的减少，脑底及脑表层的细小血管就会出现扩张，形成烟雾状血管。当代偿性血管扩张形成增加脑血流的速度，小于因脑供血动脉狭窄脑血流减少的速度时，即产生了脑缺血症状，出现脑梗塞、脑萎缩、脑软化等。因为扩张的血管在血管造影时的形态像烟囱里冒出的袅袅炊烟，所以被形象地称为烟雾病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病是全身性血管病变的局部反映，主要病变是颈内动脉内膜弹力纤维增生，逐渐使颈内动脉管腔破穿，最终发生闭塞。烟雾病变多发生在颈内动脉的末端，大脑前及大脑中的起始段，偶尔波及大脑前和大脑中动脉主干以及动颈外动脉，乃至身体其他部位的血管。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病的症状复杂，主要表现为头疼、癫痫、突发性肢体麻木无力，感觉异常、失读、失语、视物不清、偏瘫、偏盲、智力减退等。一般发作数分钟至数小时后可自行缓解，上述症状可反复发作，交替进行，缓慢加重。烟雾病临床表现因脑缺血的轻重而有所不同，轻者主要表现为头疼、癫痫、肢体无力及视力改变等，重者则以脑梗塞或脑出血起病而危及生命。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有一定的遗传倾向</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病发病主要有两种类型，一种是由于颈内动脉末端闭塞引起的脑缺血，另一种是脑缺血后代偿扩张的脑血管破裂造成的脑出血。发病年龄有两个高峰，一是在4岁左右的儿童期，二是在30～40岁的中年期。相比来说，儿童更多见，儿童与成人发病之比为52。儿童患者主要症状多以脑缺血发病，表现在哭闹或疲累后出现一过性肢麻木瘫痪，有时两侧肢体交替麻木瘫痪。而30多岁的成年人，多以急性颅内出血起病，表现为脑梗塞或脑出血。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病有一定的遗传倾向。国外研究表明，同胞间的发生率比普通人群要高出42倍，烟雾病患者子女的发病率比正常人高出37倍。307医院神经外科曾收治了一对可能与先天性遗传缺陷有关的烟雾病母子，患者是北京房山人。那天5岁的小男孩哭闹一阵后，突然出现一侧肢体瘫痪，不能活动，诊断为烟雾病。同时询问男孩母亲以往病史时，得知其母曾有过脑出血，经核磁共振检查证实也患有烟雾病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 确诊后应及时手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病治疗相当困难。过去由于检查、治疗手段的局限，对烟雾病缺乏正确的认识，诊断不清，有时被当做癫痫、动脉炎或格林巴利综合征治疗，延误了病情。内科以血管扩张药以及抗菌药进行对症治疗，但效果不明显。外科过去通常采用颊肌贴敷、大网膜颅内移植贴敷及颅外血管的直接吻合，虽然有一定疗效，但仍不理想。如今，随着影像技术的发展，烟雾病的诊断不成问题，通过核磁共振就能够得到正确诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病确诊后，主要是手术治疗，将颅外血管越过颅骨和硬脑膜屏障，引至颅内而促进脑皮层的供血，从而改善患者脑组织血液循环，使其缺血症状得到明显缓解。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 由于是双侧颅内缺血，一般得做两次手术，第一次手术10天内就可出院。休息3个月后，再做一次手术，才能完成全部治疗。手术治疗费用约5万余元。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于烟雾病患者来说，早发现早诊断早治疗是关键。首先应做PET和功能核磁检查，进行科学正确诊断，确诊后及时手术。早期手术的病人大多能完全恢复正常生活。如果延误治疗，一旦发生了脑出血或脑梗塞，有可能导致偏瘫、残疾，甚至危及生命。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 相关链接</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 烟雾病最早是日本人发现的，曾被认为是日本特有的一种疑难疾病。早在1960年，日本学者已经注意到烟雾病的出现，在1961年正式报告了第1例烟雾病。到1965年，日本共发现96名烟雾病患者，并对该病的临床特征和症状进行了描述。1969年，日本学者铃木和高久以MOYAMOYA为名发表了第一篇论文，烟雾病从此得到正式确认。烟雾病发病率最高的是日本、韩国和中国等东南亚地区，男女发病率为1:1.6倍，男性略高于女性。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 提示</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 女性烟雾病患者最好在治疗之后妊娠，分娩时应由产科、神经外科、麻醉科的医生共同制订安全方案。烟雾病患者治疗前要避免高空作业、驾驭汽车、游泳等运动，以免发生危险。(2007.11.21 健康报)</span>
</p>
