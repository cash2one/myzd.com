<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么，脑积水如何治疗呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑积水大多需要手术治疗,手术方式较多。如1减少脑脊液分泌的手术：脉络丛切除术后灼烧术，现已少用; 2解除脑室梗阻病因手术：如大脑导水管形成术或扩张术，正中孔切开术及颅内占位病变摘除术等; 3脑脊液分流术：手术目的是建立脑脊液循环通路，解除脑脊液的积蓄，兼用于交通性或非交通性脑积水。常用的分流术有侧脑室-小脑延髓池分流术，第三脑室造瘘术，侧脑室-腹腔、上矢状窦、心房、颈外静脉等分流术等。其中,脑室-腹腔分流术以其操作简单、疗效可靠得到广泛应用。但脑室-腹腔分流术并发症较多,如分流系统堵塞、感染、分流过度或不足、裂脑室综合征、癫痫、视神经损伤等。腹腔端管的并发症，包括分流管移位、断裂、脏器穿孔、肠梗阻、腹部积液等。近年来，微创分流术把微创外科新技术应用到脑室-腹腔分流术中，具有创伤小、对腹腔干扰少，减少腹腔粘连甚至能够松解轻微腹腔粘连，术后瘢痕不明显且隐蔽，疼痛轻、恢复快等诸多优点。需要指出的是，导管对患者正常生活、工作的影响很小，与术前相比，意识不清、胡言乱语等症状全部消失，生活质量可得到极大改善和提高&nbsp;</span><br />
