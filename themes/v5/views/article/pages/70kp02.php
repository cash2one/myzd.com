	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑积水是怎么回事</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑积水是神经外科常见病之一,是指脑脊液生成或循环吸收过程发生障碍而致脑脊液量过多，压力增高，扩大了正常脑脊液所占有的空间，从而继发颅压增高，脑室扩大的总称。 多继发于颅内感染、颅脑损伤、蛛网膜下腔出血等疾病或原因不明。临床上分为交通性脑积水和非交通性脑积水。如不及时处理，会导致智能障碍。可仅有轻度记忆力及计算力减退，常伴有迟钝、淡漠、缄默等。重者可呈痴呆。少数可有激动、易怒、哭笑无常、幻觉、谵妄等；导致行动障碍。常在精神症状出现后逐渐开始，有起步困难，行走缓慢不稳。肌张力和腱反射常增高，反射阳性；有时呈现轻偏瘫;尿便障碍。尿便频繁、失禁或困难，有时仅在晚期出现。此外，尚可有眩晕、一过性意识障碍、眼球震颤、帕鑫森综合征等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
