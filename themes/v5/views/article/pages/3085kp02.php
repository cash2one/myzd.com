<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>帕金森非运动症状之睡眠障碍(转载)</strong></span><br />
</div>
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">睡眠障碍存在于至少60%-90%的帕金森病患者中，严重影响患者的生活质量。帕金森病患者中常见的睡眠障碍包括失眠、睡眠片断化、快速动眼期睡眠行为障碍(REM&nbsp;behavior&nbsp;disorder,&nbsp;RBD)、白天过度嗜睡(excessive&nbsp;daytime&nbsp;sleepiness,&nbsp;EDS)或睡眠发作、睡眠异常呼吸等。其中，快速动眼期睡眠行为障碍(RBD)与帕金森病关系最为密切，目前很多研究都表明RBD可在帕金森病发病前十余年就出现，因此可以看作是帕金森病的前驱表现，对帕金森病的诊断和早期预测有重要价值。此外，RBD的发生还可预测帕金森病病情的快速进展、认知功能损害、精神症状的出现，对帕金森病研究具有重要意义。&nbsp;那什么是RBD呢？RBD是以REM睡眠期肌肉弛缓现象消失并出现与梦境相关的复杂运动为特征的发作性疾病，可导致自伤及同床者受伤并使睡眠中断。患者常将恶梦付诸于行动，表现为咀嚼，大声喊叫，或窃窃私语，与人交谈，大笑，哭泣，骂骂咧咧或唱歌，严重者则出现拳打脚踢，击倒床头柜，从床上坐起，甚至从床上跳起来或滚到床下，伤及自己或同眠者。部分患者可同时存在某些非暴力行为，如吹口哨，扮鬼脸等怪异行为，或作出某些似有目的的动作。一般夜间暴力的行为与白天的性格并不一致。RBD患者被唤醒时常迅速清醒，并可回忆起生动、令人不快或恐怖暴力的梦境。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 除了前面说到的RBD外，帕金森病患者还可以出现其他的睡眠问题，比较常见的是白日过度嗜睡（EDS）。人们常说，春困秋乏夏打盹，睡不醒的冬三月，似乎一年四季都有可能白天犯困。其实白天总想睡觉，也有可能是一种疾病，关键在于是否存在“过度”嗜睡的现象。如果一个人晚上睡眠的时间充足，没有药物的影响，也没有新陈代谢或内分泌的问题，白天还是太想睡觉，那可能就属于白日过度嗜睡了。白日过度嗜睡，轻者只是精神恍惚，注意力不集中，工作还能照样进行；中度者则是在精神放松时，比如吃过饭后、看电视、听音乐时容易睡着。重度者就比较麻烦了，在一些必须全神贯注的场合，比如开车、与重要客户会谈、吃饭等时候也会无法控制地睡着，这样往往会造成比较严重的后果，甚至危险。当然，白日过度嗜睡只是一种临床症状，常见的临床疾病包括睡眠呼吸暂停综合征、发作性睡病、夜间肌肉挛缩症等。具体需要通过临床医生检查，结合睡眠监测情况，考虑中枢神经、新陈代谢、内分泌等问题，综合之后作出判断。因此，如果出现了白日过度嗜睡的情况，建议引起重视，及早就医。</span>
</p>
