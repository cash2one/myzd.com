			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>结直肠癌肺转移的微创外科治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着医学的不断发展，过去的一些我们曾经认为晚期的、没有治疗希望的疾病也逐步获得了更好的疗效，比如结直肠癌肝肺转移。复旦大学附属中山医院许剑民教授领衔的多学科团队在这方面作出了杰出的贡献，并赢得了世界性的声誉。作为这个团队的重要一员和一名胸外科医生，我负责这个多学科团队的结直肠癌肺转移的微创外科治疗已经3年多了，现在和大家分享一下我对结直肠癌肺转移的一些认识和体会。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>结直肠癌肺转移的外科手术指征</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同时性发现的肝肺转移灶，与原发灶同时存在。结直肠癌的肝转移的处理已经非常成熟，大家也已经形成了一定的共识，比如我们中山医院结直肠癌MDT团队就出版过我们的肝转移的诊治指南；同期存在的转移灶，只要数目有限，局部切除，都可以和原发灶一起切除，只要患者的身体情况允许；肺作为结直肠癌第二位的远处转移病灶，可以和肝转移一起存在，也可以单独的肺转移，同样我们认为只要数目有限、最好位于同侧胸腔、最好是位于肺野外周三分之一可以楔形切除等等病例可以通过微创的肺切除联合原发灶的切除，伴或者不伴肝脏转移灶的切除来获得一个很好的疗效；当然如果患者的情况比较复杂，比如肝脏的手术难度很大或者肺的手术需要做肺叶或者双侧的胸腔镜等等，我们会选择分期对肝肺转移灶进行处理。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">异时性发现的肺转移灶。文献回顾告诉我们，对原发灶切除术后随访过程中出现的肺转移灶，有一部分病人通过手术切除可以获得很好的疗效；一般认为术后出现肺转移灶的间隔时间、血液中肿瘤指标的情况、肺门淋巴结有没有肿大或者转移；肺转移灶的分布和大小数目等等都是我们考虑是否会给患者进行手术切除的重要依据。当然还有一个重要的问题就是肺内结节到底是原发的肺癌还是结直肠癌的肺转移，或者根本就不是恶性病变，为肺内的一些慢性炎症或特殊感染等等，而这需要一个成熟的胸外科医生基于丰富的临床经验做出自己的判断，如果诊断遇到困难，甚至可以考虑进行经皮肺穿刺或者气管镜TBLB、EBUS-TBNA等等检查以帮助判断，制定合理的诊疗计划。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>微创技术的发展与结直肠癌肺转移的外科处理</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近10年以来，国内胸外科的微创技术取得了长足的进步，中山医院胸外科作为国内胸外科微创技术的倡导者和技术领先者，培养了全国各地的胸外科医生无数。结直肠的肺转移是最适合进行微创手术的，因为手术创伤小，留置一根胸管3天左右就可以拔除，可以和原发灶和肝脏一起手术，而且可以进行多次的胸腔手术。微创技术用于肺转移灶的切除技术非常成熟，也是我们培养低年资住院医生的主要术式之一。有鉴于此，我们在结直肠癌肺转移灶的切除方面是比较积极的。我们这些年的病例，还没有病人围手术期出过并发症，因此也是非常安全的术式。胸腔镜手术是我们目前最常用的技术，达芬奇机器人也可以用于肺转移灶的外科切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们需要考虑的是，有鉴于肠癌患者良好的生存和对化疗或者靶向药物的良好反应率，患者的治疗周期会很长，因此我们要尽可能的微创，尽可能的保护肺功能，为治疗创造条件。因此我们在行肺转移灶切除的时候，需要注意尽量避免行肺叶切除术，除非实在无法行肺楔形切除。如果不是为了抢救，坚决反对全肺切除。</span><br />
