	<div align="center">
	<p>
		<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>男人平时如何呵护自己的“精子”(转载)</strong></span>
	</p>
	<p>
		<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong><br />
</strong></span>
	</p>
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">中国学者的研究显示，中国男性的精子质量正以每年1%的速度下降，精子数量降幅达40%以上。工业化程度越高的地区，精子质量下降速度越快。引起精子质量下降的原因，有些是先天或后天的疾病，有些则是生活中一些人为因素，比如汽车尾气、医疗药品、烟酒、噪音、毒品等造成的。男性最好从以下几个方面多关照自己，提高精子质量。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、给“精子”一个稳固的后方</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在男性生殖器官中，睾丸是制造精子的“工厂”，附睾是储存精子的“仓库”，输精管是“交通枢纽”，精索动脉和静脉是后勤供应的“运输线”，前列腺液则是运送精子必需的“溶剂”。前列腺炎、精索静脉曲张、结核、梅毒、淋病等疾病会影响精子的生成、发育和、运输等过程，可造成男性不育。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、别闷坏了你的“种子”</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">精子对温度的要求比较严格，必须在低于体温的条件下才能正常发育，温度过高会杀死精子，或不利于精子生长，甚至会使精子活力大幅度下降而导致不育。有关资料统计，男子不育症中有相当一部分人是由于睾丸温度高于正常温度所致。所以要尽量避免导致睾丸温度升高的因素，如长时间骑车、久坐不动、穿紧身牛仔裤等。而在备孕期间，洗桑拿浴、蒸汽浴、过热的热水澡这样的行为更要节制或避免。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、戒除不良嗜好</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">吸烟、酗酒、吸毒不仅影响身体健康，而且可使精子质量下降。饮酒过度可使精子发生形态和活动度的改变，甚至会杀死精子，从而影响受孕和胚胎发育，使先天智力低下和畸形儿的发生率相对增高。酒精是一种性腺毒素，长年累月地大量饮酒，会给男性的性功能造成难以恢复的损伤。一般情况下，精子的存活时间为2个月，如果不射精，源源不断积累的精子会老化、死亡、重新被身体吸收。因此，如果准备怀孕，男性需在孕前2～3个月戒烟禁酒，这样才能有足够的时间产生优质的精子，保证最有生命力的精子与卵子结合。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4、防止身体肥胖</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">男性过于肥胖，会使体内雄性激素减少，雌性激素相对增加，从而妨碍精子生成，影响精子质量。另外，过于肥胖还可诱发高血压、糖尿病，这些病症都有可能引发性功能障碍。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5、远离辐射和有害物质</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">现已确定辐射对人体的健康有明确的影响。小剂量的辐射会影响机体发育，大剂量的辐射可引起睾丸组织结构的改变，增加精子的畸形率，降低精子数量、密度。日常生活中，辐射源很多，微波炉、电脑、电视机、空调、手机等，都会产生辐射。因此，男性平时应尽量减少与辐射源的接触，比如卧室内不要放置电脑、电视一类的电器;不要用电热毯取暖；要尽量避开电磁波、核辐射、X射线照射。许多化学、生物因素会使精子畸形或染色体异常，如铅、苯、二甲苯、汽油、氯乙烯、农药、除草剂、麻醉药等均可致胎儿畸形。如果接触农药、杀虫剂、二氧化硫、铜、镉、汞、锌等有害物质过久，体内残留量一般在停止接触后6个月～1年才能基本消除，在此期间也不宜受孕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6、多吃富含锌的食物</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">与男性生育相关的微量元素主要包括锌、硒、铜、钙和镁等。锌是生殖系统内重要的元素，锌直接参与精子的生成、成熟、激活和获能。缺锌会影响青春期男性生殖器官和第二性征发育，降低精子的活动能力，影响精子的密度，削弱机体的免疫功能，使男性容易患前列腺炎、附睾炎等感染性疾病。因此，男性平时应该多吃含锌的食物。富含锌的植物类食物主要有豆类、花生、小米、萝卜、大白菜;动物类食物以牡蛎最佳，其次是牛肉、鸡肝、蛋类、羊排、猪肉。</span>
</p>
