		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>事情做完就忘 一颗脑瘤让她记忆力持续下降</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">红网长沙6月5日讯（记者 董雷 通讯员 彭萍）不久前刚刚做过的事情，过两天就忘得一干二净，41岁的湘潭市民李女士，最近半年来发现自己的记忆力越来越差，右侧肢体也越来越乏力。这一切，都是她颅内一颗鸭蛋大小的肿瘤造成的，如果不做切除，将随时危及她的性命。近日，湖南省肿瘤医院神经外科为她成功切除了这颗“定时炸弹”，目前患者已经康复出院。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 湖南省肿瘤医院神经外科主任何正文博士介绍，颅内肿瘤并不罕见，罕见的是李女士的这颗肿瘤不仅个头很大，位置还极其特殊，它深藏于颅内中央核心位置，周边被一系列重要血管和神经包围，一不小心就会造成颅内大出血，手术难度非常之大。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 何正文介绍，行医20多年来，他还是头次遇上如此棘手的一颗颅内肿瘤。李女士也表示，她曾在省内多家医院就诊，均以“风险太大”为由遭遇拒绝，无奈下才来到省肿瘤医院。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 经过多科专家联合会诊，医院为李女士制定了详细的手术方案。一个星期前，手术顺利实施。尽管有了充足的准备，手术中李女士仍旧出血高达3000毫升，几乎相当于普通人10次献血的总量。不过最终李女士的肿瘤被成功切除，术后她恢复良好，两三天便能正常说话、走路，昨晚因家中突发急事顺利出院。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 何正文提醒，如果您有不能用感冒或其他疾病解释的头痛，且成断断续续持续加重的趋势，或者清晨时易恶心呕吐，或者视力下降模糊，当上述症状同时出现2个或2个以上时，就要怀疑可能是患上颅内肿瘤。颅内肿瘤可发生于任何年龄，以20-50岁为最多见。</span>
</p>
