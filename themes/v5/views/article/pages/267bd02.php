<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>用心做合格的医生&nbsp;</strong></span><span style="font-family:'Microsoft YaHei';font-size:18px;line-height:25.454544067382813px;"><strong>——访华山医院普外科主任钦伦秀教授</strong></span><span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong></strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">钦伦秀：师从汤钊猷院士获博士学位。主要研究领域为肝癌外科治疗、转移复发防治等。获国家杰出青年基金，长江学者特聘教授和教育部创新团队带头人，973首席科学家。2010年获国家自然科学二等奖，去年获“第十三届上海市科技精英”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“人生轨迹因一颗小石子改变”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在那个“学好数理化，走遍天下都不怕”的年代，钦教授从小怀抱当科学家的梦想：“我对科学家尤其是物理学家的向往到了膜拜的地步，理想是中国科技大学物理系少年班。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">16 岁的钦教授在高考前夕出现意外。在“跳箱”测试中跌落，导致肘部关节“撕脱性骨折”，但医生误诊为脱位，使他经受了不该有的二次手术，折腾了两个多月，出院时离高考只有一周了。“这次经历让我体会到俗话说的良医救人、庸医害人，医生的一点点疏忽误判就会给病人增加无端的痛苦。”填志愿时，钦教授决然将原来的梦想全都换成了医学院。“开始我只是简单地想，至少我自己的命要掌握在自己手里。但慢慢体会到做合格的医生是一件很不容易的事，医生承担了太多的社会责任。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“将学术之魂融入外科技术的骨架”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">钦教授1993年师从著名肝癌外科专家汤钊猷院士，这二十年间，他根据汤院士的要求，始终坚持外科临床与学术研究双栖发展，在完成肝癌病人诊治和外科手术等临床工作的同时，坚持研究转移复发的机理，取得了多项新发现。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经典理论认为肿瘤早期不会转移，只在晚期发生转移。但钦教授团队发现这个理论解释不了医疗实践中的很多现象，有些看似凶恶的大肿瘤在彻底切除后并不会转移复发，而一些看似微不足道的小肿瘤却会早期广泛转移。他们比对无转移和有转移的肿瘤，发现两者的基因表达谱差别很大；而有转移的病人原发肿瘤与其转移瘤间无明显差异，背离传统经典理论。由此他们首次揭示了“肝癌转移能力始于原发瘤，受环境因素影响而改变”，获得了2010年国家自然科学二等奖，这是少有的以外科医生为主要成员的奖项。他们创建了两个可用于预测肝癌转移的新方法，正努力将它们转化为有效的预测诊断手段，争取在早期知道哪些病人容易转移复发，如何进行有效的干预预防，以及该从哪个阶段开始进行干预。“几十年的研究不是为了发论文，而是为了寻找对病人有用的工具。”将研究应用于临床，是钦教授自行医之日起追求的目标。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“我尽力做合格的医生”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癌症还没有被攻克，医生总要面对很多无奈。“只要有一线希望，我就会尽全力延长病人的生命。我们不仅同情病人也同情他们的家属。即使不能治愈，也要帮他们度过这个难关。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“医生最大的欣慰是通过努力给病人带来希望，帮病人家庭重拾幸福美满”。10多年前，钦教授为大学毕业才2年就患上早期肝癌的小伙子做手术，小伙子有个女友，“她不可能等这个身患重病的他”——很多人这么想。然而，手术后两个月小伙子来复查时，他的女友也跟在一旁，问钦教授“我们能不能结婚？能不能要孩子？”再1年后，他们给钦教授发来女儿的照片，开玩笑地跟了句：“我们能不能再生一个？”说到故事的结尾，钦教授会心一笑，随即又说：“但是，外科医生只能解决一小部分问题，临床问题需要科研解答。”这也是钦教授一直注重将科研应用于临床，将临床服务于科研的原因。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“钦教授教我们怎样做好医生”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">兴趣和内心的宁静是通往学术道路的不二法门。几乎每晚，钦教授都在办公室里看文献。“有兴趣才会投入。晚上，我会看文献，关注各个研究方向进展的情况，或是与学生讨论课题进展与困难, 修改学生的论文。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">每过一两周，钦教授就会召集学生们开组会，让他们交流讨论各自这个阶段的研究成果，钦教授则提出自己的指导性意见。“教授给我们研究的大方向，他给我们的研究空间从来都是很自由的。”二年级博士生高小妹这样说。基础医学专业博士三年级的盛媛媛则说：“他会直接指出我们的错误，但是否要根据他的意见修改则完全由我们自己决定。如果我们临时要换课题，他也不会反对。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">钦教授不只关注学生们的研究内容，对他们报告时的PPT 设计、用语等细节也会一一提出建议，尤其注重培养学生的英语能力，亲自修改每个学生的每一篇英文论文。最近，他组织研究生们翻译一本癌转移方面的书，每晚都熬夜校对和指导学生们的翻译。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">钦教授常常与学生们交流自己的感悟：“学医要悟性，不管是理论还是技术方面都需要悟性。但更重要的是投入，只有你感兴趣了，才会全身心地投入到你的研究和工作中，才会做个有心人，仔细观察，主动思考。”</span>
</p>
