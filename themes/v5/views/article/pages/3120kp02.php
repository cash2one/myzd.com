			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>年轻患者如何考虑关节置换的手术时机？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">与骨关节病通常在老年人中发病率较高不同，强直性脊柱炎、类风湿性关节炎、先天性髋臼发育不良的患者，往往发病年龄早，关节的破坏严重，很多患者常常到了40岁左右就开始经常为疾病所困扰。一部分患者关节开始融合、关节间隙变窄甚至消失，关节的活动范围逐渐减小；另一部分患者由于髋臼发育不良，可能出现疼痛、双下肢不等长、患肢短缩、跛行、行走困难、骨盆倾斜、脊柱侧凸等等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 事实上，部分研究已经证实，对于强直性脊柱炎的患者，如果手术以前已经出现严重的疼痛，那么手术后6-8个月，疼痛的缓解情况要比术前疼痛不严重的患者要差。这也就提示我们，这部分患者如果及时手术，治疗的效果可能更好。而且对于患肢的及时治疗，还可以延缓对侧肢体的退变、进展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 对于年轻患者（40岁左右）而言，如果选择手术，根据目前的假体设计寿命，则很可能在一生中要接受第二次手术（关节翻修），但如果选择保守治疗，疼痛、行走障碍又明显的影响生活、工作和事业。而保守治疗只是对症，疾病本身仍在持续进展，越往后手术的难度也就越大、风险也就越高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 所以患者应该清楚的了解这两方面的利与弊，做出选择。一旦选择手术治疗，也应该到正规的有关节专科的大医院接受治疗，确保手术后达到满意的效果。</span><br />
