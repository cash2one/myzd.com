	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>日间手术是腰椎间盘突出症患者的福音</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">腰椎疾患有许多种，腰椎间盘突出症仅仅是其中发生率最高的、最普通的一种。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">名称解释：门诊手术、日间手术、住院手术。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">门诊手术是指在门诊即可完成的手术，做完后,患者无需住院,当天来当天走。基本都是病情较轻的小手术,如无特殊情况不需要做任何随访。住院手术是指需要办理住院，在医院住下后接受的手术,一般是比较大的手术,患者术后需要比较细致的护理，保证患者的早日康复。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">日间手术最早源自欧美发达国家，是指过去需要接受住院治疗的患者，在医疗水平提高后，安排在一至二个工作日内完成对患者的住院、手术，在手术后进行短暂观察、恢复即可办理出院。患者不在医院过夜。这是一种安全可靠的手术模式。能够开展日间手术的医疗机构一般配备有比传统大医院更细致的术前评估流程、更先进的手术室条件和设备，更专业、经验更丰富的手术医生和麻醉医生、更科学的就医流程和更完善的术后随访系统及康复指导。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">我科从2009年开始申报开展日间手术模式并获得了批准。至2014年这个模式逐渐成熟，开展了大量的日间手术，缓解患者了住院难的问题。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">看病难是一个综合性的问题。患者方面是病人太多，而且是越来越多。医生方面是医生的数量和精力是有限的。2014年301医院完成脊柱外科手术2594台，2015年是3647台，增加了1053台。2015年每个手术日平均增加了5台手术，可见医生已经非常努力了，依然有许多患者在等待床位中。仅上月底医院周会统计骨科每天平均等待手术床位的患者是360人，这就是现实中的住院难。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">众多方案中最有效方法之一是提高技术水平，大量开展日间手术。说起来简单，实施起来就没有那么简单。开放的腰椎间盘突出症手术能否变成日间手术，怎样变成日间手术就是我一直思索的问题。2003年我跟随美国医生Anthony Yeung学习时，他已经是日间手术了，因为只有手术水平达到一定的高度，才敢于让患者当天出院。如果没有这个技术把握，就无法解决这个困局。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">值得欣喜的是，这个问题已经获得解决。下面的2015年工作总结，告诉大家我们过去一年的工作数据，来阐述我们是如何为患者缓解住院难这个困局的。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2015年我院共完成907台PELD手术，其中838（92.4%）名患者是术后2小时内出院。由此可见，我们的技术已经可以满足很大一部分患者在经过很短时间的观察后出院，大大缓解了患者住院难的情况。除此之外，日间手术最重要的工作是如何处理其余的69例（7.6%）住院超过24小时的患者，这些患者才是保证日间手术顺利进行，技术进一步提高的关键。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">完成日间手术的整个流程给医生的诊疗水平和医院的综合能力提出了更高的要求。涉及院前检查科室，放射科、临检科、门诊部窗口服务能力。涉及住院部、病案科、门诊日间病房、手术室、骨科的协调能力。在完成日间手术的同时，必须有备选方案，保证日间手术的顺利进行。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">日间手术说白了首先是医疗水平的问题，其次体现到医疗效率的问题。美国等发达国家极少有上千张床位的医院，而我们国家有大量千张床以上的医院。而且这些像肿瘤一样，医院越来越大，并且发生转移到处都是分院。世界上著名的TSRH医院仅仅只有几十张床，就可以满足医院的运行，并且几十年都没有扩张的动作。而我们国内一个普通教学医院骨科的脊柱外科就近百张床。由此可见，中国医疗的一个严重问题是效率问题。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在没有明显增加床位的基础上，我们2015年脊柱外科手术量增加了1053台。全年完成日间手术1368台。可见我们脊柱外科手术量的增加，得益于效率的提高。目前的脊柱外科手术中，只有PELD手术可以实施日间手术的管理模式。因此为了缓解腰椎疾患患者住院难、减轻患者的手术创伤，PELD是未来发展的方向。日间手术是医院管理模式的改革和发展方向，</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">日间手术的意义：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、远程医疗方便患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、当地进行术前检查</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、异地提前预约手术</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4、患者术中痛苦小</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5、术后卧床时间短</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6、康复时间明显短</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">7、复发的发生率低（&lt;1%&lt; span=""&gt;）</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">8、复发补救手段多</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">9、符合外科治疗原则</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">10、家属陪伴负担轻</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术适应症：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、症状严重VAS疼痛评分大于6分的单纯腰椎间盘突出症者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、有手术指证但不适合开放手术的高龄和多种合并症者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、症状严重的远处游离者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4、症状严重的钙化型腰椎疾病者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5、症状严重的腰椎管狭窄症患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6、开放手术后再次出现坐骨神经痛的患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">7、融合手术后再次出现坐骨神经痛的患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">8、腰椎滑脱II度以内出现坐骨神经痛为主的患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">9、出现症状的椎小关节囊肿</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">10、各种腰椎开放术后椎管内异物出现症状的患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">11、TLIF术后对侧出现症状的患者</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">12、多间隙腰椎疾患</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">相对禁忌症</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1、血液系统疾病合并有手术指证的腰椎疾患</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2、单纯腰痛症状的腰椎疾患</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3、盘源性腰痛（有争议）</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4、VAS疼痛评分在5分以下</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">小结：微创PELD手术已经涵盖了大部分的腰椎疾患，所有腰椎疾患中大约85%的患者可以应用微创的手术方法进行治疗。日间手术缓解了医院住院难的问题，减小了患者手术的创伤、缩短了康复的时间、减轻了家属的护理负担。2015年301医院大约25%的患者，接受的是日间手术模式的住院管理模式，所以说PELD日间手术是腰椎疾患患者的福音。</span>
</p>
