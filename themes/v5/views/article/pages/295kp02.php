			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>复发、转移了的乳腺癌怎么办？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 乳腺癌患者们最关心的，就是乳腺癌能不能治愈、治疗后会不会转移、转移了怎么办等问题，希望这篇文章能为患者朋友们答疑解惑。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关于乳腺癌能否治愈的问题，可以明确地说：大部分原发性乳腺癌，通过标准手术治疗，以及采取化疗、放疗、内分泌治疗、靶向药物等手段后，在5年内没有复发和转移，则可以称之为治愈</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近些年关于乳腺癌的新闻很多，“知名歌手罹患乳腺癌，最终病逝”，“好莱坞影星切除乳腺组织预防乳腺癌”等消息不断地挑动着大家的敏感神经，很多人产生了“恐乳癌”的情绪，甚至人人自危，其实这大可不必。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">只要平时定期体检，做好预防工作，早发现，早治疗，乳腺癌是可以治愈的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于做过乳腺癌治疗的患者，最担心的就是复发。针对新长出来的肿瘤，患者首先要找到专业的医生诊断，不要瞎猜瞎想，给患者造成心理压力；确定是乳腺癌复发后，还需要确定复发的状态——是只有原来长肿瘤的乳腺部位复发了，还是另一侧的乳腺里也长了肿瘤，或者淋巴结甚至肝、肺等脏器里出现了转移等；确定复发的状态后，需要对复发的肿瘤取活检，确定诊断、判断分型，最后由医生根据这些检查结果制定下一步的诊疗计划。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么乳腺癌复发后还能再次手术治疗吗？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个要分情况。如果仅是在乳腺内或局部软组织的复发，全身其他部位都没发现癌症病灶，如具备完全切除的可能时首选手术；如果脏器有转移或者再发，可能不会首选手术治疗。复发或转移患者往往需要全身药物治疗，是化疗还是内分泌治疗、抑或靶向治疗，这需医生综合判断。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">重点解释一下化疗，复发后的化疗与首次术后化疗的目的是不同的。首次手术后的化疗称为“辅助化疗”，其目的是为了彻底治愈乳腺癌；复发以后的化疗称为“解救化疗”，目前来说，解救化疗还不能达到治愈乳腺癌的目的，但是可以控制病情的发展，延长患者的生存时间。关于复发后的治疗效果，医生需要了解肿瘤的分型是不是发生了变化、与第一次治疗间隔的时间（间隔时间长，可能肿瘤的恶性程度低）、以及首次治疗时肿瘤对治疗的敏感程度等问题，每个患者的病情不同，疗效也不尽相同。</span>
</p>
