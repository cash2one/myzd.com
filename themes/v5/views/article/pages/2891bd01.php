			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>门体分流术在肝硬化门静脉高压症外科治疗中的地位</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门静脉高压症（portal hypertension，PHT）所致食管胃底静脉曲张出血是肝硬化病人的主要死亡原因之一。在20世纪70年代以前主要以手术治疗为主，80年代开始了内镜治疗，90年代以后内镜治疗，如食管曲张静脉内镜套扎（esophageal variceal ligation，EVL）和内镜下注射硬化剂（endoscopic injection sclerotherapy，EIS）以及介入治疗（经颈内静脉肝内门体分流术，TIPS）逐渐占据主导地位，特别是为最终需行肝移植的病人争取了时间。在我国，21世纪初肝移植蓬勃发展，一度曾认为在肝移植时代，肝硬化门静脉高压症的传统手术，包括各种断流术和分流术已不再重要。事实上，我国乙型肝炎肝硬化的发生率较高，供肝严重短缺且并不是所有肝硬化食管胃底静脉曲张出血是终末期肝病的表现。因此可以预见，就外科治疗而言，传统手术是现在及今后几十年内仍然是主流手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">传统手术主要有三类：各种门奇静脉断流术、门体静脉分流术及分流加断流的联合手术。过去曾认为，门静脉高压症的手术方式繁多，术式选择主要依据术者的经验，即术者所熟悉的手术方式。其实不然，肝硬化所致的门静脉系统血流动力学变化极为复杂。众所周知，影响门静脉压力的主要因素有两个，一个是门静脉血流量，另一个是作用于血流的血管阻力，可以用Ohm定律表示三者的关系：P = QÎR。P是门静脉压力，Q是整个门静脉系统包括侧支血管循环的血流量；R是门静脉系统血管阻力。由此可见，门静脉压力与血管阻力和血流量相关，肝硬化时肝内血管阻力增加时造成门静脉血流动力学改变的基础，而全身和内脏的高血流动力循环，对门静脉压力增高的维持和加重起重要作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门静脉压力升高后，形成很多侧支循环，其中最</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">具临床</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">意义的是食管胃底静脉曲张，也可发生在胃的其他部位和肠道。曲张静脉壁的张力越高，则越容易破裂出血。曲张静脉壁的张力受若干因素相互作用的调节，按照Laplace定律可用下式表示：曲张静脉壁张力=(P1-P2)×r/w。上述公式中，P1为曲张静脉内压力，P2为食管腔中的压力，r为曲张静脉的半径，w为曲张静脉壁的厚度。曲张静脉内的压力直接受门静脉压力的影响，虽然侧支循环的建立有助于降低门静脉压力，但事实上，随着肝硬化的进展，门静脉压力仍持续升高，曲张静脉中的压力不断增加，管壁变薄，血管半径增大，成为曲张静脉破裂的基本条件。另外，曲张静脉周围的组织支持具有重要意义，血管曲张到一定程度，如其周围有坚强的组织支持，则不至于破裂。但支持组织黏膜面可因炎症、糜烂等局部因素所损伤，使组织支持力量减弱而易于破裂。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由此可以看出，门静脉压力直接决定曲张静脉内的压力。如果门静脉压力升高的主要原因是门静脉血流量增加，那么断流术可以取得较好的效果；如果是肝内阻力增加，那么只有门体分流术才能取得满意的结果，仅行断流术的手术并发症较多，远期再出血率高，有时甚至导致死亡的灾难性结果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门奇静脉断流术是针对自然形成的分流侧支血管，采取阻断、结扎的方法以达止血或预防出血目的（1）。断流术的合理性主要表现在两个方面：①维持门静脉的向肝血流。②断流术直接针对可造成曲张静脉破裂出血的胃底贲门区的侧支血管，手术目的明确，可即刻确切止血。然而，断流术也存在一些缺点：①某些情况下断流不易彻底，遗漏曲张的血管，尤其是高位食管支，而造成复发出血。②术后远期再出血率较高，可达10%~20%（2）。③脾切除断流术后的患者，几乎不可避免地发生脾静脉血栓形成，并向门静脉主干蔓延。断流术后的其它并发症如腹水、腹水感染、发热以及胃肠功能紊乱（胃动力差或腹泻）的发生率远较分流术高，特别是术中发现胃肠壁厚（水肿）的病人上述并发症的发生率更高。因此，断流术适合于肝脏门静脉血流灌注减少量为中等或以下、脾动脉结扎后自由门静脉压值≤20 mmHg者，在没有准确的血流动力学信息时，不能盲目地行断流术。裘法祖教授倡导的贲门周围血管离断术已经在我国成为主流术式，选择性贲门周围血管离断术（简称选择性断流术）对食管胃底区的危险血运彻底阻断的同时，维持了机体部分门静脉血的自发性分流，理论上有其合理性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着门静脉高压症血流动力学研究的进展，依据门静脉血流动力学状况选择分流术或断流术逐渐获得广大学者的认同。分流术在国内虽不如以前风行，但仍被使用，且有一些改进。分流术可分为全门体分流术、部分性门体分流术和选择性门体分流术三大类。门体分流术是以降低门静脉压力达到止血的目的，虽然有效的门体分流必将导致肝血流的锐减，从而可能引起肝功能的进一步恶化（3），可是分流术有利于减轻门脉高压性胃病，分流术后胃粘膜血运循环得到改善，胃粘膜屏障功能得以恢复，加强曲张静脉周围的组织支持，降低曲张静脉破裂的几率。分流术具有降压效果满意，食管静脉曲张的消失率、止血率以及腹水消失率高等优点，适合门静脉高压性胃病出血和断流术后再次出血的病人，在传统手术领域仍占有一席之地。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然全门体分流术使门静脉血完全转流入腔静脉，术后脑病和肝功能衰竭发生率高，但该术式中的大部分适合于门静脉成为流出道者，因为此时门静脉系统的血流非但不流入肝脏反而全部经侧支分流，且经肝动脉流入肝脏的血液经门静脉逆流入体循环，这样即使行全门体分流术，也不会降低入肝血流。因此可以施行如门腔、肠腔静脉大口径侧侧分流或架桥分流术等全门体分流术，但不能行端侧门腔分流术，虽然此术式也属于全门体分流术，否则肝窦压急剧升高引起肝功能恶化和难治性腹水；不能施行肠系膜上-下腔静脉端侧分流术，否则使小内脏的门静脉压力明显升高致术后再出血；也不能行选择性远端脾肾或脾腔静脉分流术，因这不符合该术式必须有向肝血流的原理，且可加重肠系膜上-门静脉的高压状态，使其与小内脏区域迅速建立侧支；更不能施行断流术，否则可使门静脉压力更显著地增高，易引起门静脉高压性胃病和异位静脉曲张出血及顽固性腹水等（4）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">部分门体分流术指在远离肝门处、较小口径的分流术。在充分降低门静脉压力、控制出血的同时，通过限制分流道口径（8~10mm），在于适当降低门静脉压的同时，仍保持一定的门静脉向肝血流，可减少肝衰竭和肝性脑病的发生率。主要术式包括限制性门腔侧侧、肠腔或门腔“桥式”分流和传统脾肾静脉分流术等，适合于门静脉入肝血流很少、脾动脉结扎后FPP值&gt;20 mmHg者。传统脾肾静脉分流术也可用于门静脉完全离肝血流的病人。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">选择性门体分流术通过选择性降低胃脾区门静脉压力达到控制食管胃底静脉曲张破裂出血的目的，具有对肝功能、全身血流动力学的影响较小的优点，代表术式有远端脾肾静脉分流术和冠腔分流术。1967年，warren提出远端脾肾分流术，其原理是门静脉系统存在相对高压的胃脾区和相对低压的肠系膜区，通过结扎胃冠状静脉、胃右静脉和胃网膜右静脉，将胃脾区和肠系膜区分开，保留脾脏，结扎脾静脉近端，脾静脉远端与左肾静脉吻合，从而选择性降低食管胃底曲张静脉的压力。由于操作复杂，术中出血多，也不能解决脾大、脾功能亢进和腹水等问题，又必须存在向肝性门静脉血流以及无腹水或仅有少量腹水，所以该术式在国内尚未普及该手术。冠腔分流术是Inokuchi于1967年设计的，它是将胃冠状静脉与下腔静脉进行吻合分流，同时切除脾脏解决脾功能亢进问题。冠腔分流是真正的高选择性门腔分流术，但因冠腔分流手术过程复杂，失败率高，广泛应用受到一定的限制，需自体大隐静脉或人工血管作桥式吻合，手术操作复杂、难度大，远期疗效也不理想（5）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">分流加断流的联合手术结合了分流和断流手术的特点，既能有效降低门静脉压力，又能维持一定的门静脉向肝血流。目前常用的联合手术包括肠腔静脉H 形分流加断流术，肠系膜下静脉肾静脉分流加断流术， TIPS加改良Sugiura 术以及传统的脾肾静脉分流术加断流术等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在各种分流加断流的联合手术中，我们认为脾切除脾肾静脉分流加贲门周围血管离断术最合理，应成为首选。首先因为切除脾脏可纠正中、重度脾功能亢进；其次我们研究门静脉高压症术后门静脉系血栓形成的机制，结果发现脾切除断流或分流术后，大多数病人门静脉主干及左右分支有血栓形成，表明血栓形成主要是血流动力学紊乱所致。同时我们还发现脾切除脾肾静脉分流加断流联合手术后，内脏血流经肠系膜上静脉-脾静脉-脾肾静脉吻合口至体循环，如果血管吻合技术满意者，吻合口通畅率极高（95%左右），分流口通畅者几无术后再出血。而其他联合手术如TIPS加断流术或肠腔静脉分流加断流术等均难以达到上述结果。虽然脾切除脾肾静脉分流加断流联合手术存在门体分流，但术后脑病率不高，约10%左右，且容易纠正，同时考虑到我国的门静脉高压症大多由乙型肝炎肝硬化所致，除非急诊手术，否则我们常规将HBV-DNA值控制在103拷贝/ml以下时再手术，这大致上需要抗病毒治疗2~4周左右，术后继续抗病毒治疗2~3年，这样进一步降低了手术后的脑病发生率。脾切除脾肾静脉分流加断流联合手术适合于没有全面的血流动力学资料，以及自由门静脉压力在脾切除后断流前&gt;20mmHg者。如无条件准确测定自由门静脉压力，如果脾切除后脾静脉张力较高者，也需加行脾肾静脉分流术（6）。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然，我们平时提到术式选择主要根据术者的习惯和经验，但这是指对于选用何种分流术或何种断流术而言，不能违背依据门静脉血流动力学状况进行术式选择原则，如果施行的手术是错误的，可导致灾难性结果。因此，在门静脉高压症术式选择中，不存在一种术式如断流术适合所有的病人，需根据具体血流动力变化而定。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，即使内镜下反复行EVL或EIS或两者结合，仍有部分病例术后再出血。显然，这种病人并不适合行断流术，门体分流术才能达到预期的治疗效果。针对内镜或药物疗法失败的门静脉高压症出血病人(Child A或B级)，一项大型的多中心随机对照研究结果显示TIPS与远端脾肾分流术对预防再出血和肝性脑病方面疗效相近，死亡率也近似。鉴于两种方法疗效相似，考虑到分流手术在国内一直是较为成熟的临床技术，而TIPS技术目前尚难普及。因此外科分流手术目前仍应作为复发出血尤其是标准内镜治疗失败病人的主要治疗手段（7）。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，由肝硬化门静脉高压症曲张静脉破裂出血机制中得出，门静脉压力直接决定曲张静脉内的压力，门静脉压力升高的原因不同，决定了有的可采用断流术，有点必需施行分流术。因此，门体分流术在肝硬化门静脉高压症外科治疗中扔占据重要地位，一方面手术方式的选择必须依据门静脉血流动力学状况，另一方面内镜治疗失败的病人再行断流术疗效不佳，应选用门体分流术。</span>
</p>
