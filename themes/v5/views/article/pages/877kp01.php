			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>食管裂孔疝的治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">食管裂孔疝(hiatus hernia)是指腹腔内脏器(主要是胃)通过膈食管裂孔进入胸腔所致的疾病。食管裂孔疝是膈疝的一种，也是膈疝中最常见者，达90%以上。食管裂孔疝患者可以无症状或症状轻微，其症状轻重与疝囊大小、食管炎症的严重程度无关。裂孔疝和返流性食管炎可同时也可分别存在，区别此二者，对临床工作十分重要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>疾病病因</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1.食管发育不全的先天因素。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2.食管裂孔部位结构如肌肉有萎缩或肌肉张力减弱。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3.长期腹腔压力增高的后天因素，如妊娠、腹水、慢性咳嗽、习惯性便秘等可使胃体疝入膈肌之上而形成食管裂孔疝。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4.手术后裂孔疝，如胃上部或贲门部手术，破坏了正常的结构亦可引起疝。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 5.创伤性裂孔疝。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 形成食管裂孔疝的病因尚有争议，少数发病于幼年的患者有先天性发育障碍的因素，形成较大的食管裂孔和裂孔周围组织薄弱；近年来多认为后天性因素是主要的，与肥胖及慢性腹内压力升高有关。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 食管胃接合部的生理作用仍不太清楚，食管胃接合部功能健全时具有活瓣作用，液体或固体物咽下入胃，但不返流，只当打嗝或呕吐时，才能少量返流。保证此正常功能的因素有：①膈肌对食管的夹挤作用；②食管胃接合部粘膜皱襞的作用；③食管与胃底在解剖上呈锐角状相接；④腹内食管段参与了食管下段的瓣膜作用；⑤食管下段生理性高压区的内括约肌作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 多数人认为上述因素第5项是防止返流的主要因素，附近的正常解剖关系对此有支持作用。防止胃液返流的作用受迷走神经的支配，切除迷走神经后此作用即消失。胃内压力增加时，胃液易返流入食管。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 食管粘膜的鳞状上皮细胞对胃酸无抵抗力，长期受返流的胃酸侵蚀可引起返流性食管炎，轻者粘膜水肿和充血重者形成表浅溃疡，呈斑点分布或融合成片，粘膜下组织水肿，粘膜受损而为假膜覆盖，较易出血。炎症可浸透至肌层及纤维外膜，甚至累及纵隔，使组织增厚，变脆，附近淋巴结增大。在后期食管壁纤维化，瘢痕性狭窄，食管变短。在某些病例，可发现膈食管膜被牵拉至主动脉弓下，可达第9胸椎水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 返流性食管炎的严重程度可因下列因素而异：胃液的返流量，返流液的酸度，存在时间长短和个体抵抗力的差异。返流性食管炎的病理改变多数是可以恢复的，矫正食管裂孔疝后，粘膜病变有可能修复。</span><br />
