			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>2014亚太疝外科年会掠影-朝阳医院疝外科走出国门</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;2014年11月6-8日，2014年度亚太疝外科年会在粉红之城—印度斋普尔顺利举办，朝阳医院西院疝外科主任陈杰、医务办副主任王明刚应邀参加本次学术盛会。本次会议吸引了亚太地区以及欧洲、澳洲等近20多个国家和地区的超过500名疝外科医生参会。大会进行了现场手术演示(Workshop)、大会主题发言、专题讨论等学术交流内容，针对疝外科医生所关注的多项学术问题展开了深入而广泛的交流和讨论。应大会组委会和印度疝外科医师协会邀请，我院王明刚主任作为中国疝外科中青年医师代表，访问了印度斋普尔M.G甘地纪念医院并参与了本届大会的现场手术演示(Workshop)环节，在活动中与印度及参会的各国医师代表现场进行手术技巧的交流和探讨，向参会医师介绍了北京朝阳医院以及北京朝阳医院疝外科的发展历史和现状，尤其介绍了朝阳医院疝外科开展的各项先进的手术项目和丰富的临床经验，赢得参会代表的广泛赞誉，现场有多位医师表示希望到我院参观访问。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王明刚医师目前担任首都医科大学附属北京朝阳医院（西区）医务办副主任兼门诊办公室主任，外科副主任医师、疝和腹壁外科腹腔镜组组长，大中华腔镜疝外科学院秘书长，《腹腔镜外科杂志》编委，中国医师协会疝外科医师培训中心副主任，是中国医师协会微创医师委员会和疝外科医师委员会全国中青年委员。在陈杰教授支持下，率先在北京朝阳医院开展腹腔镜疝修补技术，填补了我院这一领域空白，后专注于腹腔镜下各种腹外疝的治疗，主持撰写：中国腔镜腹股沟疝手术操作指南（中国实用外科2013.7），先后在美国《Surgical Endoscopy》、《中华普通外科杂志》、《中国实用外科杂志》、《腹腔镜外科杂志》、《中华疝和腹壁外科杂志》、《中华全科医师杂志》、《首医学报》等专业核心期刊上发表近二十篇专业论文，作为课题参与者完成国家自然科学基金一项，吴阶平医学专项基金一项，于2010年获得北京市科技进步奖一项。近年来多次在国家级学术大会进行专题演讲和手术演示，在全国100余家省、市级大型综合医院进行手术演示和教学，是目前腔镜（微创）疝外科治疗领域国内知名的中青年专家。</span>
</p>
