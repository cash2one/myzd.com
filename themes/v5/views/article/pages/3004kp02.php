			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是急性中耳炎</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">急性化脓性中耳炎是中耳黏膜的急性化脓性炎症。病变主要位于鼓室，但中耳其他各部也常受累。主要致病菌为肺炎链球菌、流感嗜血杆菌、乙型溶血性链球菌、葡萄球菌、铜绿假单胞菌等。本病好发于儿童，多继发于急性上呼吸道感染、临近部位的炎症病灶及急性传染病，如麻疹、猩红热等。婴幼儿免疫力低下、中耳解剖生理特点以及哺乳姿势不当等，是其易经咽鼓管途径罹患中耳感染的重要原因。鼓膜外伤致细菌经外耳道进入鼓室或血行感染亦可引起本病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>急性中耳炎有哪些表现呢？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.全身症状：轻重不一，可有畏寒、发热、倦怠、食欲减退等。小儿全身症状较重，常伴呕吐、腹泻等消化道症状。鼓膜一旦穿孔，体温即逐渐下降，全身症状明显减轻。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.耳痛：耳深部疼痛，逐渐加重，如为搏动性跳痛或刺痛，可向同侧头部和牙齿放射，吞咽及咳嗽时耳痛加重。鼓膜穿破流脓后，耳痛顿减。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.听力减退及耳鸣：始感耳闷，继则听力减退，伴耳鸣。耳痛剧烈者，耳聋可不被觉察。如病变侵入内耳，可出现眩晕。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.耳漏：鼓膜穿孔后耳内有液体流出，初为浆液血性，以后为黏液脓性或脓性。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>急性中耳炎需做哪些检查呢？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.耳内镜检查：早期鼓膜松弛部充血，锤骨柄及紧张部周边可见放射状扩张的血管。继之鼓膜弥漫性充血、肿胀、向外膨出、正常标志难以辨识。鼓膜穿孔时，局部出现小黄点，开始穿孔一般甚小，不易看清，彻底清洁外耳道后方见穿孔处之鼓膜有闪烁搏动的亮点，或见脓液从该处涌出。坏死型者鼓膜迅速破溃，形成大穿孔。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.听力检查：呈传导性聋，听阈提高至40-50dB。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.血液分析：白细胞总数增多，多型核白细胞比例升高，鼓膜穿孔后逐渐恢复正常。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>急性中耳炎怎样诊断及治疗？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">急性中耳炎的诊断主要是通过患者有感冒或流涕的诱因，再加耳痛的症状和鼓膜的检查基本就可以确诊。对于轻度到中度的患者做个耳内镜检查就可以了，严重的患者可加做听力学检查及血液分析等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于急性中耳炎的治疗一般以药物治疗为主，主要是口服抗生素及一些的辅助对症药物。对于特别严重的患者需行鼓膜切开置管手术。</span><br />
