	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>天气转凉，高血压多发！教你如何防治血压飙升</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">天气转凉，如何防止血压飙升?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1、掌握好天气转凉后的进补与饮食原则</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 科学制定进补方案：采用低盐食疗法，每日盐的摄入量≤5克。多吃木耳、蘑菇和黑豆、黄豆等清补食物。海参属于滋阴类补品，既可增强体力，又不会引起高血压，也是上佳的滋补品。同时，高血压男性在进补时还要注意少吃辛辣的食物。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 该类人群应该选择清淡类的饮食。天冷人们喜欢各种火锅，包括麻辣香锅、麻辣烫、刷肉等等，这些食物对于高血压患者来说，盐分含量太高，应该尽量少吃甚至不吃。如果觉得日常饮食口味清淡，建议多以醋、芥末、香油、洋葱等调味品增加味道，逐味精、酱油、辣酱等则不要用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2、特别注意做好自我预防</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 要次注意保暖防寒。寒冷刺激血管收缩，易造成血压升高;要注意外出穿戴，防止寒冷刺激，避免迎风走路;要注意晨练前一定要先服降压药并做好保暖，血压不稳定的患者不宜在天气寒冷时外出晨练。总之，做好保暖工作很重要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3、一定要做好血压的监测</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 建议高血压患者一天中测量三次血压，即早晨、中午和晚上各一次。这样就可以掌握自己的血压变化规律，了解病情。在血压稳定后，可以延长测量的间隔时间，测量血压需要注意：在活动以后应至少休息5分钟以上，环境要安静舒适，座椅适当，手臂与心脏位置等高;传统的水银血压是国际上医生通用的血压测量仪器。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 天气寒冷，血压一旦升高怎么办?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 天冷血压高一些不是药物不管用，患者千万别慌张，急着换药。只要血压持续升高在20毫米汞柱以上，才必须调整用药。建议在20毫米汞柱范围以内的患者，可以咨询医生采取加量或者加药的办法，避免血压波动更大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 秋冬季节温度下降，人体内的肾上腺素水平上升，体表血管收缩以减少热量的散发，同时肾上腺素又能使心率加快，心输出量增加，这样就会导致血压的升高。有些高血压患者常会因寒冷刺激导致血压急剧上升而发生脑卒中。因此，天气转凉了，建议有高血压的朋友及时到医院就诊，检测血压，及时合理调整药物。一旦有了疾病征兆应该尽早治疗，如果贻误了最佳治疗时期，就会增大治疗的难度。</span><br />
