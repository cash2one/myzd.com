			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>颅内肿瘤的早期症状</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 我在门诊中发现，有的患者朋友发现肿瘤就诊时，肿瘤已经长很大了，给手术带来更多的风险和并发症，也可能会让病家花更多的金钱。其实，大部分颅内肿瘤在发病初期还是有一些“苗头”的，只是因为大家可能没有这方面的知识而忽视了其早期的“预警”，以致错过了及时发现肿瘤及时诊治的时机。这里浅谈几个常见的颅内肿瘤的症状，希望对大家有帮助。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 1.进行性加重的头痛</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 看到这里大家不要怕，以为有头痛就生脑瘤了。其实人的一生，出现头痛症状是非常常见的！感冒受凉会头痛；睡眠不好，压力太大会头痛；心情不好可能也有头痛。而许多脑瘤一开始并不一定有头痛。与脑瘤有关的头痛主要是颅内压增高引起的，随着肿瘤的长大（或脑积水加重），头痛会进行性加重；而其他原因引起的头痛，在原发病因好转后（如感冒痊愈后）会自行消失。如头痛同时伴随其他一些症状,如呕吐、视力下降、记忆力差反应变慢或以下第2到第10条症状之一，则高度怀疑颅内有肿瘤或其他占位性病变。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 2.单侧耳鸣或听力下降</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 单侧听力下降，因为不影响日常生活，往往不易察觉。但如果在单侧听力下降前，有较长时间的单侧耳鸣，应该高度警惕！单侧耳鸣是听神经瘤最早最常见的”预警”，如果早期发现，肿瘤很小，则可以行伽马刀治疗，免去开刀之苦（也有部分小听神经瘤，伽马刀后长期随访再生长，再手术）。在临床中接诊的听神经瘤患者，很多肿瘤都较大了，甚至压迫脑干和小脑，出现脑积水或共济失调（手脚不协调）。在我向他们追问病史中，基本上都有生长肿瘤的一侧有早期出现耳鸣，随后逐渐听力下降，以致出现走路不稳了才去就医。因此，如果出现单侧耳鸣或听力下降，应及早就医。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 3.视力下降或伴复视</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 有些患者出现视力下降，以为是近视眼或老花眼，忽视了进一步检查，有的去看眼科，有经验的眼科医生可能会想到颅内问题，做一个核磁共振，结果发现颅内肿瘤。有的甚至在眼科反复就诊，视力越来越差，甚至一侧眼失明了，才想起做颅内的检查，此时肿瘤已经很大，可能包绕周围的神经血管，手术风险大增，而且有的还失去了全切除机会。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 哪些颅内肿瘤会影响视力下降呢？最常见的是鞍区肿瘤，如垂体瘤、颅咽管瘤、脑膜瘤，少见的如胆脂瘤、蛛网膜囊肿等，由于压迫双侧视神经，会导致双侧视力下降，有的是一侧明显些。蝶骨嵴脑膜瘤往往影响一侧视神经。其他的如前颅底肿瘤，下视丘肿瘤也会导致视力下降。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 还有的患者视力检查正常，但自觉视物不适，有闪光或重影(复视)、变形，除了到眼科就诊外，也要考虑排除一下颅内疾病，常见的有视觉中枢附近枕叶或视觉传导通路上的肿瘤，最常见的为胶质瘤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 4.性功能下降或月经紊乱/泌乳</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 正常的男性成人如出现性功能下降，部分患者由于害羞不去就诊,有的到男性科去看性门诊,但症状却没什么好转，一直到出现视力下降，才到眼科或神经外科就诊，拍片子发现垂体瘤或鞍区其他肿瘤。当然不是每个垂体瘤都有性功能下降的，这里说的是泌乳素型垂体瘤，男性出现性欲下降，毛发稀少，女性患者常见月经失调或伴泌乳，甚至停经。肿瘤长大后，还会压迫视神经致视力下降。因此，如果出现上述性方面的问题，不要犹豫害羞，到医院就诊，要警惕垂体瘤或其他鞍区肿瘤！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;5.嗅觉下降或幻嗅</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 嗅觉下降一般很难发现，如果偶然发现一侧或双侧嗅觉下降，除了去看五官科外，应高度怀疑颅内累及嗅神经的病变，如前颅底/嗅沟脑膜瘤，嗅母细胞瘤，或累及前颅底的脊索瘤等。如果周围本来没有异味而闻到有怪味（幻嗅），可能是一种癫痫的特殊表现，应高度警惕颞叶内侧面的肿瘤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 6.记忆力下降或反应迟钝</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 随着年龄的增大，有的会出现记忆力下降或反应迟钝些，这也比较常见。但如果在较短的时间内（如半年）出现明显的进行性记忆力减退或反应力下降，或年纪轻轻就出现上述症状，应该重视颅内有病变了。较大范围的额颞或胼胝体的病变（如胶质瘤）或慢性增高的颅内压（如各种占位病变，慢慢长大或脑积水），不一定有早期的明确定位性症状或体征，却逐渐表现为记忆力或反应的迟钝，计算力下降（简单的加减法也算错，如计算100-7等于多少也算不出，有的可以算出答案为93，让他再减7等于多少就算不出）。这类患者的症状往往由与之密切接触的亲人最先发现并送医就诊，有的被当作老年痴呆症而误诊！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 7.行走不稳</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 行走不稳的原因很多，与颅内占位病变有关的最常见的是小脑肿瘤，表现为直线行走不稳，手指运动不协调等，如出现上述症状，及时到神经外科或神经内科就诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 8.一侧肌力减退或发麻</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 单侧肢体无力或发麻可能是病变累及颅内运动功能区或感觉功能区，也可能是脊髓病变所致，建议及早到神经外科或神经内科进行专科检查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;9.继发性癫痫</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;指成人出现的癫痫，如排除颅脑外伤所致，则多数为颅内占位引起，如颅内肿瘤/脑血管畸形/寄生虫肉芽肿等，往往需要外科治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 10.发现上述症状如何就诊</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 如果出现上述症状，除了到相应症状的科室就诊检查外（如视力下降到眼科，嗅觉/听力不好到五官科等），建议到神经外科或神经内科（神经科），进行专科检查，最好先做一个核磁共振MRI平扫检查，如果发现颅内占位性病变再作增强扫描，这样绝大部分颅内病变都能发现。有的病家只做CT平扫，可能会漏诊，因为部分病灶在CT平扫上不能显示，特别是后颅窝病变！</span>
</p>
