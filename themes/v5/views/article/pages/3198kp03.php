		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>肝海绵状血管瘤都要治疗吗？（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在门诊经常会碰到病人焦急地询问：“医生，我肝脏上长了血管瘤，要紧吗，需要开刀吗？”其实平常大家说的肝脏血管瘤主要指海绵状血管瘤，其本质并不是真正的肿瘤，而是一种错构性质的“瘤”，属于一种发育异常，在成年后一般就不再生长了，通常并不需要治疗。在一些特殊的情况下，需要考虑治疗：（1）血管瘤较大（一般要&gt;5cm），造成周围组织的压迫或引起肝区疼痛（靠肝脏边缘时可以有症状）；（2）血管瘤不断地缓慢生长；（3）血管瘤突然增大，怀疑有恶变（罕见）。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp; &nbsp; 治疗方法有2种，即手术切除与介入治疗。除了怀疑有恶变的血管瘤一定要手术治疗外，绝大部分的肝血管瘤可以通过介入手段治疗，具体方法是经股动脉穿刺，将一根细导管插到肝脏相应的病变部位，将药物注入血管瘤的血窦内即可，以后血管瘤就会慢慢萎缩并纤维化。少数患者因血管瘤的血供较少，还需要在B超或CT引导下再局部穿刺，直接在瘤内注射药物1～2次。效果明确，副作用较少。极少数病人可能会发生胆囊炎或一过性肝损伤，但只要认真操作一般可以避免。</span>
</p>
