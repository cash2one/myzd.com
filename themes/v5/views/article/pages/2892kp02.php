<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>明天你是否依然疼痛</strong></span> 
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>1、谁应该读这些系列科普知识？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们都知道疼痛是什么。我们也都经历过疼痛。有时我们很难注意到它，有时它难以忍受。通常它以自有的方式消逝，有时它经过治疗处理后消失，但确实有很少的比例疼痛感难以消除，从而成为持续性疼痛（我们有时称为慢性疼痛，我统一称为持续性疼痛）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有很多种原因能够产生持续性疼痛，我一系列的科普文章目的是帮助你理解持续性疼痛的治疗困难，以及如何更好的对待它。在这系列文章中，我并没有特别的涉及癌症疼痛，但我所描述的内容部分也适用于癌症疼痛。</span> 
<p>
	<br />
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">无论你是最近还是很久前，无论是严重还是轻微，系列的疼痛科普文章是帮助你寻求最好的方式去管理疼痛，以及我们如何看待它，我们能做什么，谁能帮助你，你怎样能够进行自我恢复，以及怎样得到最好的医治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2、为什们疼痛挥之不去？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一些疼痛很容易理解(看到)，因为有明显的伤害如创伤或者淤青等。但有一些疼痛就不那么明显，如你不能“看”到阑尾炎的疼痛，但是得过此病的人能很真切的告诉那种疼痛的真实存在。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康专家们用各种不同的术语来区别不同的疼痛类型。短期疼痛，如扭伤脚踝成为剧烈疼痛；长期疼痛，如背疼，成为持续或者慢性疼痛；有些疼痛来了又去，比如头痛被称为周期性疼痛。几种类型疼痛，或者在身体多处同时存在疼痛也并不是罕见之事。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多数剧烈疼痛是警示你某些地方出现问题一个非常有用的信号。大部分不严重的疼痛能够自愈或者经过简单的处理就能自愈。有些疼痛是更严重疾病如腿骨折的重要征兆，这种疼痛是有用的，因为它提示你需治疗并且充分休息，使得骨头的折裂能够有机会复原。但是，若成为持续性（慢性）的疼痛并没有什么有用的目的，而且它给很多人的生活带来了巨大的影响。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“我不得不放弃工作，隔绝了几乎所有的社交关系，疼痛导致了我家庭生活的困境，我丈夫和孩子不仅仅要承担本属于我的家庭工作，还要照顾我。虽然我才三十多岁，但就要依靠别人的帮助才能移动和穿衣。你可以想象，我的自尊降低到何种程度，疼痛限制我能够到哪里，我能够做什么.......”——患者自述。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疼痛能来自于身体的任何部分：皮肤，肌肉，韧带，关节，骨头（伤害性疼痛），受损组织（炎症性疼痛），神经（神经性疼痛），内部器官（内脏疼痛）或者是这些不同种疼痛的组合（混合疼痛）。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疼痛感受通常来自身体伤害部位的成千上万的神经纤维，经过脊髓，传导至大脑。在某些情况下（如，中风后痛），伤害本就来自大脑和脊髓本身，所以此时大脑和脊髓就成为疼痛感觉的起点。疼痛信号需要首先经过脊髓，然后到达大脑，大脑同时也是诸如焦虑、情感、睡觉、欲望、记忆的“交换局”。（图：红色上行部分）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2892/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">也可以说每个人的大脑创造了个人独一无二的疼痛体验。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大脑又将信号传回给脊髓，脊髓反过来即进一步减少又增加疼痛感。由于持续的疼痛信号输入在脊髓和大脑的神经末梢细胞变得过度敏感，这种兴奋是持续疼痛难以轻易消除的一个原因，即使造成疼痛的原因已经发现并得到处理。（图：蓝色下行部分）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">通俗的说，是身体的警告系统变得过于敏感，增强了疼痛的感觉，以至于身体的持续性伤害已经完全消除，疼痛依然存在，从而导致疼痛这种疾病。这导致了长期且令人沮丧的问题，你和你的医生需要富有技巧、给予时间和耐心的紧密合作才能够改善这个问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尽管医学技术不断提高，但是疼痛非常复杂，它包含太多要素，我们无法通过现有的医疗技术能够描绘出各个疼痛要素，也不能通过试验等各种手段来显示疼痛。然而，不知道疼痛的原因并不意味着它不是真实存在的。只有疼痛的人才能真正的告诉你他如何的疼痛。因为疼痛是非常个体化的感受，没有两个人的疼痛是完全相同的，这也就意味着疼痛的定义和治疗都将非常困难。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">国际疼痛联合会对于疼痛的定义是:“疼痛是一种令人不快的感觉和情绪上的感受，伴随着现有的或潜在的组织损伤。”疼痛经常是主观的，每个人在生命的早期就通过损伤的经历学会了表达疼痛的确切词汇，无疑这是身体局部或整体的感觉，而且也总是令人不愉快的一种情绪上的感受。这个定义非常重要，因为将疼痛明确为感觉体验，这就意味着判断是否有疼痛的唯一方法就是问或者观察患者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">非常不幸的是，持续性疼痛要比简单的组织损伤还要麻烦。疼痛感已经足够让人痛苦了，但如果它持续足够的时间，它将影响你生活的方方面面，包括影响你的工作能力，你的家庭、朋友关系，你的活动水平甚至你的睡眠。所有的这些扑面而来时将导致更加疼痛感受和负面情绪的恶性循环。</span> 
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疼痛不是简单的存在于意念或者存在于肉体之中，它是一种非常复杂的来自于身体和大脑如何解读的混合信号。你知道你的疼痛感受，尽管它不能看到和不能得到外部客观的诊断，正确理解持续疼痛的复杂本质才能更好的应对疼痛。</span> 
</p>
