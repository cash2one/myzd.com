<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>罕见癫痫顽症在北京三博脑科医院治愈</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">千龙网北京讯（通讯员 程丹丹）14岁的小孔在家是个乖乖女，在学校是个成绩优秀的学生，但就在两年前出现的一个“魔咒”，开始左右着小孔的正常生活。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刚开始，小孔自称看见了某某人，但她所看见的某某人，全家人谁都不清楚是谁。又称听见远处有隐隐约约的音乐声，但其他人完全听不到。而且时不时咳嗽，经常饭后出现呕吐。渐渐地，小孔的咳嗽症状开始频繁，一度持续20余天。到当地医院诊治，疑为呼吸系统疾病，给予雾化吸入治疗，咳嗽症状缓解。但是前述的幻觉症状又随之加重，还自称闻到一股臭味，并在幻觉后很快出现恶心，严重时呕吐。到当地大医院诊治，胃肠造影显示“肠系膜压迫综合症”，服用多种药物后也不见症状好转。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 面对复杂的病情，医生困惑，家长担心。当地医院怀疑小孔得了癫痫，并用癫痫安给小孔进行试探性治疗。遵医嘱服药一个疗程后，小孔的呕吐和幻觉症状基本消失。于是，家长擅自减少癫痫安的剂量，半个月后小孔再次出现持续性干咳，严重时声音嘶哑，连话都说不出来。同时手部、面部出现明显的麻木感。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近日，为了确诊小孔的病症，家人慕名赶到国内著名的脑外科医院北京三博脑科医院，该院的特色之一是其癫痫中心采用“内外科联合干预”与“多学科协作”来治疗难治性癫痫。通过几天全面、系统的检查之后，三博脑科医院神经外科一病区周健主任综合各种临床资料，诊断小孔患的是癫痫。周健主任介绍称，以干咳、呕吐、幻觉为主要发作症状的癫痫病例在世界范围内极为罕见，极易误诊，但这种类型的癫痫可以通过精确的术前评估和脑立体定向技术定位来手术切除癫痫病灶，从而达到根治效果。终于，两年前出现的“魔咒”在三博脑科医院被锁定，小孔脑内的致痫灶得到完全切除。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 周健主任提醒，因癫痫发作的表现多种多样，极易误诊而耽误最佳就诊时机，需及时明确诊断。如果怀疑自己或家人患了癫痫，应主动就医，咨询有关诊断治疗事宜，积极治疗。</span>
</p>
