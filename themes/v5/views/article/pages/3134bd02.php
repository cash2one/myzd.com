</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3134/4.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;护士们在广场上跳乳房保健操 记者 龙在全 摄</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 10月是国际乳腺癌防治月。乳腺癌是女性最常见的肿瘤之一，且发病率逐年上升，目前在我市肿瘤排行榜上居于前六位。重医附一院乳腺外科专家表示，年轻女性最好每年定期进行乳腺彩超检查；而40岁以上女性最好通过乳腺钼靶筛查乳腺癌，做到早诊断早治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 伸展手臂、摆动肩颈……昨日，在重医附一院门诊大楼前的广场上，来自重医附一院乳腺外科的男医生与女护士一起，跳起了乳房保健操，拉开了10月“国际乳腺癌防治月”的活动序幕。据重医附一院乳腺外科主任厉红元介绍，乳腺癌发病率逐年上升，目前在我市肿瘤排行榜上居于前六位。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 历红元表示，乳腺癌的发生因素包括年龄、遗传因素、第一次怀孕年龄、口服避孕药、膳食高脂肪、饮酒、情绪等。现在不少女性都有工作压力大、生活节奏快、休息不好、常熬夜，饮食不健康、少锻炼等特点，这样更容易患病。历红元建议，年轻女性最好每年定期进行乳腺彩超检查，而40岁以上女性最好通过乳腺钼靶筛查乳腺癌，做到早诊断早治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 历红元还强调，乳房肿块并不是乳腺癌的唯一症状，早期症状还有乳房的对称性改变，如皮肤变厚、乳房皮肤橘皮样变、乳房溢水或溢血等，有这些情况一定要到医院检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 支招</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 乳腺疾病咋自查</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 厉红元称，自我检查对及早发现乳腺疾病能起到关键作用，最好在月经后的7～10天，可在沐浴时检测，最好每月1次。一般从以下4个方面来检查：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1.看镜子两手自然下垂，仔细观察乳房两边是否大小对称、有无不正常突起、表皮凹陷或隆起、脱屑、红肿、起皱纹等；再注意乳头有无分泌物、变形或糜烂、凹陷；再将双手放在腰部，上身往前倾，肩膀及手肘稍往前压，观察镜中乳房有无异常。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2.双手抬高，手稍往前压，注意观察镜中乳房外形、轮廓是否有上述异常之情形。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3.抬高手臂，左手将指头并拢，仔细检查右边乳房，用手指指腹轻压乳房，感觉是否有肿块，由乳头开始做环状顺时针方向检查，逐渐向外（约3—4圈），至全部乳房检查完为止；注意乳房外上1／4及腋窝处，摸摸是否有不寻常的皮下肿块；同时轻轻挤乳头，注意有无分泌物。检查完后，换另一边。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 4.卧平躺，左手指头并拢，将乳房看成以乳头为中心的圆，由外往内一圈圈按压检查，再轻挤乳头，注意是否有分泌物。</span>
