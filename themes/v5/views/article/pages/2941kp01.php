			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小孩手术麻醉会影响“脑子”吗？</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">孩子聪明不聪明？尤其是与同龄人比较，有无优势可以发挥，弱势需要弥补，这些都是我们关心的“智力”问题。智力是人所特有的一种综合性智慧和才能的心理活动，是人的认识能力，包括感知事物的能力、记忆能力、想象能力、思维能力和创造力等，也就是我们通常所说的聪明程度。儿童智力的发展是参差不齐的，这些个体差异只是少数，绝大多数儿童基本上都按着差不多的速度发展。生前3个月至生后3岁是脑发育的关键时期，也是智能发育的重要时期。外界环境对3岁到18岁成年的智能发育影响更大一些，如果遇到重大事件如家庭变故、创伤、手术都可能对孩子的智力发育产生影响。评价儿童智力是根据各阶段儿童智力发育特点进行的，评价儿童智力发育的方法用智能测试。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">评价儿童智力发育有如下意义：①为早期教育提供依据，儿童生长发育有其规律性，早期教育应依据其规律性，不能超越其实际水平和能力，不能要求过高、过急。②挖掘发现儿童某一方面的“强项”，充分利用早期神经系统可塑性强的时机，改善环境进行训练，掌握特长技能。③及早发现儿童某一方面的“弱项”， 及早进行干预，以促进其智力发育，如有的小儿动作发育较迟钝，有的语言发育较晚，可根据每个儿童存在的具体问题加强教育和训练。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿童智能结构一般概括为四个方面：①动作能：反映小儿的姿态、头的平衡、坐、立、爬、走、跑、跳及使用手指的能力，这些运动能力构成了对小儿成熟程度估计的起始点。②应物能：反映小儿对外界事物的分析和综合的能力，也就是运用过去的经验来解决新的问题。③ 言语能：反映小儿听、理解和语言的表达能力。④应人能：反映小儿生活能力和与人交往的能力，与神经运动和智力的完整性有关。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿童智力测验是通过动作、作业、回答问题、做算术题、拼图、理解词义等方面进行的，是间接的测定方法，有一定局限性。单一智能检查方法，不能作为评价儿童智力水平的唯一标准，必须结合儿童在日常生活中所表现出来的各种能力，以及学校老师、家长及邻居对孩子智力的评价，由专业医师通过分析和综合，才能作出较客观的结论。</span>
</p>
