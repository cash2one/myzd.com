			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>甲状腺出问题 少女“停”在12岁</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯 本报迅（记者 刘迅 通讯员 王琛）1米45的身高，12岁女童的面相,19岁女生张芹一走进大学校门，就被周围同学视为“异类”。昨日，妈妈带着张芹到医院复查，医生发现，张芹经过一年治疗逐渐有了女性特征，但因治疗过晚，身高已经无法改变。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">发育停步，女孩变得沉默</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“我知道，我和其他正常女生不一样。”张芹老家在孝感，她说自己懂事后就感觉到周围人异样的眼神。张妈妈说，女儿读到初中，就不再长个子了，起初以为是晚发育，全家人没当回事。可见着班上的女同学的个子一个个往上蹿，张芹仍只有145厘米，明显比同龄人矮了一截，一家人这才着急了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">进入高中后，张芹的身高劣势越发明显。令张妈妈更忧心的是，这个年龄段的女孩都逐渐开始发育：胸部微微凸起，每月也有例假，可女儿15岁了身体发育仍像小孩子，什么女性特征都没有，身材看起来和10岁的幼童差不多。“孩子发现了自己的异样，从学校回家后常常沉默不语，一个人躲在房间。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“不差吃，不差喝，女儿怎么就不发育了呢?”张妈妈带着女儿在当地四处求医，一直没有找到病因，药吃了不少，也不见有效果。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">补甲状腺素，恢复性特征</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">无奈之下，张妈妈带着女儿赶到武汉，在市妇幼检查时，医生发现张芹不仅甲状腺功能低下，而且子宫发育也明显不好，卵巢不排卵，这些都是张芹身材矮小，女性特征发育严重滞后的重要原因。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“身体缺什么，就补充什么。”市妇幼保健院为张芹开出了治疗方案，持续补充甲状腺素、雌激素、孕激素。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨日，张芹经过一年治疗后来复查。医生惊喜地发现，经过一年治疗，张芹已慢慢甩掉了一张“娃娃脸”，逐渐有了年轻女性特征，乳房有明显变化，同时也有了月经，内分泌水平在逐渐建立平衡。但遗憾的是，因为张芹耽误了最佳治疗时间，身高问题已经无法改变。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">专家提醒，武汉市对新生儿有专项筛查，如果发现有“甲低”，应积极治疗，以免影响孩子发育；家长发现孩子生长发育严重滞后，别抱侥幸心理，有可能是甲状腺在“作怪”。</span><br />
<br />
