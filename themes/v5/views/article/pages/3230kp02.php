<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不用再留“自刎疤痕”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯(记者 胡梦 通讯员 杜巍巍）以往甲状腺手术后，脖子上会留下一条触目惊心的横向疤痕，患者往往难以面对“曾抹脖子自杀”等诸多猜疑。武大人民医院耳鼻咽喉科—头颈外科专家采用先进的超声刀技术，将6～8厘米的拉链式创口化于无形，使患者免除了这一尴尬。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">武大人民医院耳鼻喉科华清泉教授介绍，新技术将在患者胸骨上隐蔽处切开一个1厘米的小切口，运用超声刀在甲状腺周边进行“扫除”，把病变的结节统统切除，再将废弃组织从“钥匙孔”中排出。“由于切口极小且藏于皮肤纹理中，术后即使近观也不易察觉。”</span>
