<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>儿童颅内肿瘤当心被误诊</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">核心提示：小儿颅内肿瘤的诊断常常比成人困难，在早期由于缺乏有效的检查手段，儿童脑瘤误诊率一度较高。随着对小儿脑肿瘤的认知逐渐增加，加之CT、核磁MRI等检查手段的普及，现在被误诊的几率已经大大降低，但是我们在临床依然可以见到被误诊的病例，这是为何呢?。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 张玉琪教授，清华大学玉泉医院副院长，教授，博士生导师。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 小儿颅内肿瘤的诊断常常比成人困难，在早期由于缺乏有效的检查手段，儿童脑瘤误诊率一度较高。随着对小儿脑肿瘤的认知逐渐增加，加之CT、核磁MRI等检查手段的普及，现在被误诊的几率已经大大降低，但是我们在临床依然可以见到被误诊的病例，这是为何呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 儿童颅内肿瘤被误诊，考虑与如下因素有关：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1、幼儿不会正确表达自己的症状，常常依赖家属的回忆与推测来描述，增加了医生判断上的困难;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2、儿童在进行神经系统的检查时，不能很好的配合，低龄幼儿常因哭闹使得阳性体征不容易被外科医生发现;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3、颅缝或者前囟门未闭合，脑瘤使得颅压增高后，往往通过头颅增大来代偿，使得症状出现较迟;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4、小儿颅内肿瘤有时症状表现不典型，常与身体其他疾病所混淆，特别是头部外伤或者其他疾病之后出现的症状，常被误诊为与脑瘤无关的疾病;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 5、我国小儿神经外科还没有完全普及，很多医院的小儿脑肿瘤患者分散在其神经外科科室进行治疗，这些医院没有独立的小儿神经外科专业病房，也缺乏对小儿脑肿瘤特殊性的认识，故对早期的诊断比较困难。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在我们临床常见的小儿脑肿瘤被误诊的疾病中，按照误诊疾病出现的频率，依次为脑炎或脑膜炎、癫痫、肠胃疾病、中枢性尿崩、先天性脑积水。避免误诊是我们给予孩子正确治疗的第一步，家长们在碰到这些诊断时，最好能寻求专业小儿神经外科的确认诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1、误诊为脑膜炎：由于病儿头疼、发热，颈部抵抗(又称作颈部强直)未作腰穿而按脑膜炎治疗，这类患儿常在基层医院被误诊为脑膜炎并治疗一段时间无效后才会转到上级医院。这种颈部抵抗是慢性小脑扁桃体下疝的结果，进行腰穿时很危险，会诱发小儿的脑疝;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2、误诊为癫痫：儿童如果出现癫痫，一定要找到癫痫发作的真正原因，切不可盲目的进行抗癫痫治疗，患儿只要做个CT检查即可明确。有一类特殊的“痴笑性癫痫”的儿童，要注意是否有下丘脑错构瘤，做MRI检查会比较清楚显示;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3、误诊为肠胃道疾病：有些儿童脑肿瘤的患者脑瘤长在第四脑室，没有颅压增高，而是肿瘤直接刺激呕吐中枢，一般医生会误诊为胃病或“幽门梗阻”，治疗无效并出现严重头疼时才做颅脑方面的检查;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4、误诊为中枢性尿崩：鞍上生殖细胞瘤的首发症状就是多饮多尿，这类患儿一定要进行CT和MRI的检查，特别是MRI，了解下丘脑有无肿物。CT对于微小病灶常会漏诊，一定要重视MRI检查;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 5、误诊为先天性脑积水：先天性脑积水的患儿出生时头围就较大，出生后头围增加快，头皮易见静脉曲张，前囟巨大且张力较高，但是不会出现头痛和呕吐。而脑瘤儿童出生时头颅大小一般正常，从几个月～2岁，家长会发现头围增加迅速，常伴随头痛和呕吐。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 以上是临床仍然会不时遇到的误诊情况，重视儿童脑健康，加强儿童颅内肿瘤的鉴别诊断，为颅脑肿瘤的患儿争取到宝贵的早期治疗时间与机会，是我们小儿神经外科医生在诊断时要注意的重要事项。</span>
</p>
