		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>[CIT2013]宿燕岗:经心内膜植入左室导线可行吗？</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前常规的CRT是将LV通过冠状静脉送达静脉分支，从心外膜起搏左室。但由于窦开口畸形等心脏解剖结构的问题，有5%-10%的患者不能通过静脉途径植入左室电极。在中国介入心脏病学大会（CIT2013）上，复旦大学附属中山医院的宿燕岗教授介绍了左室心内膜起搏的优势及应用前景。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003754003139" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">宿燕岗教授在作报告</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;经静脉途径植入LV电极不成功后的选择</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;1）放弃手术：医生和患者均不甘心</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;2）改为植入ICD：虽然都符合指南，因不改善症状，患者多不接受。</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;3）心外科将LV缝在心外膜：创伤大、风险高、患者不能耐受手术等弊端。</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;4）左室心内膜起搏</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;左室心内膜起搏的植入途径</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;1）经主动脉途径</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;2）经心尖部途径</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;3）直视下心肌内缝合</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;4）经穿刺房间隔途径</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;左室心内膜起搏的优势</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;与通过心脏静脉行左室心外膜CRT相比，心内膜起搏具有以下优势：</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;1）较低的左室起搏阈值</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;2）几乎无膈神经刺激风险：经静脉起搏发生膈肌刺激的比例高达20%左右。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;3）室壁激动顺序更符合生理：心肌透壁激动顺序逆转（心外膜起搏时）会增加多源传到和透壁复极的离散度，延长Q-T间期，增加尖端扭转性室速的风险；而左室心内膜起搏的CRT患者QT离散度明显降低。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;4）选择左室起搏点更方便：经灌装静脉植入方式因受解剖、与之、膈肌刺激等限制，很多情况下无法根据术前左室最晚激动部位选择左室起搏点；而左室心内膜电极植入方式使选择性左室不同起搏位点变得比较容易。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;5）有助于提高心室同步化效率：心内膜刺激通过心内膜更快速的传导和更短的新呢莫州长，能进一步缩短心室激动间期。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;左室心内膜起搏潜在的并发症</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;1）长期抗凝剂体循环栓塞：长期抗凝带来的并发症；左心室血栓可以导致包括脑血管栓塞事件在内的严重的体循环系统并发症。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;2）点击对二尖瓣闭合的影响：加重二尖瓣关闭不全和增加感染性心内膜炎的几率，继发于二尖瓣的感染性心内膜炎赘生物较右心系统并发症严重。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;左室心内膜起搏的应用前景</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;临床上需要大样本对照临床试验探讨该技术的安全性和有效性。左室心内膜起搏的CRT技术毕竟为不能经静脉途径植入的患者提供了一种相对无创、前景值得期待的一种新方法</span><br />
