			<div style="text-align:center;">
	<strong><span style="line-height:2;">脊髓栓系综合症&nbsp;</span></strong>
</div>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脊髓栓系综合征 (tethered cord syndrome, TCS)是指由于先天或后天的因素使脊髓受牵拉、圆锥低位、造成脊髓出现缺血、缺氧、神经组织变性等病理改变，临床上出现下肢感觉、运动功能障碍或畸形、大小便障碍等神经损害的症候群。TCS可于任何年龄段发病，由于病理类型及年龄的不同，其临床表现各异。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">造成脊髓栓系的原因有多种，如先天性脊柱裂，硬脊膜内、外脂肪瘤，脊髓脊膜膨出，腰骶手术后脊髓粘连、脊髓纵裂畸形等原因。脊髓栓系的部位，多数是脊髓圆锥或终丝末端。共同特点是病变对脊髓远端和神经根的固定效应。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发性TCS的病因：在胚胎发育初期，脊髓和椎管等长，随后脊柱生长快于脊髓，由于脊髓头侧固定，故脊髓相对向上移动，在胚胎20 w时，脊髓圆锥上移达L3~4椎体水平， 40 w时位于L3椎体水平，出生婴儿脊髓末端位于L1-2水平。出生3个月后脊髓升至成人水平，即圆锥末端位于L1水平。脊髓圆锥变细移行为终丝(成人直径小于2 mm)。在脊髓上移过程中，如存在神经管闭合不全、椎管内脂肪瘤、脊髓圆锥皮样囊肿或畸胎瘤、脊髓纵裂等原因导致脊髓牵拉、圆锥低位等病理改变，就会造成脊髓末端回缩不良，马尾终丝被粘连、束缚而导致发育不良，称为原发性脊髓栓系综合征。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">继发性TCS的病因：多为腰骶部脊柱裂修补术后或椎管硬膜内手术后,该部位瘢痕组织与脊髓和马尾粘连,疤痕收缩导致脊髓受牵拉,也可见于蛛网膜炎局部出血后形成的粘连，称为继发性脊髓栓系综合征。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脊髓被栓系、固定后，引起脊髓或神经的血循环障碍而发生缺血、缺氧，逐渐变性坏死或呈退性行改变。脂肪瘤型的脂肪组织经缺损的椎板、硬脊膜栓系脊髓，脂肪组织浸润至脊髓，神经纤维深入脂肪瘤内，两者之间常无明确界限。血流障碍导致栓系部位代谢率低，产生进行性神经损害，且约20%脊髓栓系合并脑积水；长期处于这种状态使脊髓出现变性、 坏死、软化、萎缩和脊髓空洞等改变；其MRI表现多样，且多发生于近栓系部位。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脊髓栓系综合征患者常有特征性的皮肤表现，如腰骶部皮肤出现小的凹陷、皮肤窦道，局部多毛或皮毛窦，腰部中线部位血管瘤，不对称臀裂等。腰骶皮下脂肪瘤则提示脂肪脊髓脊膜膨出(lipomyelomeningocele)。</span>
</p>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>MRI：</strong></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">MRI是诊断TCS的首选方法，MRI能清晰显示脊髓圆锥的位置和增粗的终丝，一般认为，脊髓圆锥低于腰2椎体下缘和终丝直径&gt;2mm为异常，对脂肪瘤和终丝脂肪浸润的分辨率高，它们在T1加权像和T2加权像呈高信号，矢状面成像可确定圆锥与脂肪瘤的关系，MRI还能发现脊柱裂，分裂脊髓畸形，脊髓空洞等其他异常。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">X线平片：可了解有无脊柱裂，脊柱侧弯及椎体分节不全等畸形，对TCS的诊断具有提示作用，但仅能显示骨质异常，不能直接显示神经畸形和异常。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>CT：</strong></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">CT脊髓造影能显示脂肪瘤、脊髓圆锥、马尾神经和硬脊膜之间的关系。另外，CT能显示骨骼畸形、脊柱裂、椎管内肿瘤等。但是CT诊断脊髓栓系综合征的敏感性和可靠性不如MRI， CT椎管造影又属有创性检查，对典型脊髓栓系综合征病人，MRI诊断已足够。对复杂脊髓栓系综合征尤其是合并复杂的骨性畸形者，可联合应用MRI和CT检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>脊柱裂手术治疗：</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">显性脊柱裂，均需手术治疗，手术时机在出生后1-3个月。单纯脊膜膨出，或神经症状轻微的其他类型，应尽早手术。如因全身情况等原因推迟手术，应对局部加以保护，尤其是脊髓外露者，防止感染。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术原则是，分裂松解四周的粘连，将后突的脊髓或神经根回纳入椎管，切除多余硬膜囊，严密缝合脊膜开口，并将裂孔两旁的筋膜翻转重叠覆盖加以修补，修补椎板缺损处（植骨等）。对有脊髓栓系综合征的患者，可行椎管探查，松解粘连及切断终丝。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>加强皮肤护理：</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患儿皮肤角质层较薄，易擦伤导致皮肤细菌感染；另一方面，由于患儿皮肤的屏障功能脆弱，且皮肤中含水量较多，pH值较高，利于病原菌的生长，因此应加强患儿皮肤护理，勤翻身每l～2小时一次，翻身时动作应轻柔，避免拖拉等，注意保持床单位的干净整洁，无褶皱、对骨突出的部位每次翻身后给予按摩，在翻身时要注意轴线翻身，避免躯体过度扭曲。</span>
</p>
