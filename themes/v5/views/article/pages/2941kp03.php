</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 尿道下裂是个常见病，但是手术治疗对许多医生、患者、家属来说都是梦魇，因为“失败率”太高了。尿瘘、尿道狭窄、尿道憩室、外观难看……</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;其实，严格意义上讲“尿瘘、尿道狭窄、尿道憩室”都属于手术并发症，并发症是可以再手术纠正的，属于“好事多磨”。如果仅仅排尿正常，但是外观难以接受，成年性生活不舒服，也算是手术成功吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 对于大部分的尿道下裂，排尿功能本身是正常的，仅仅是尿道口位置不正常，随着阴茎长大，不处理也可以站立排尿。就像开重睑手术，即划双眼皮，视力是不影响的，外观改善更重要，而且都不希望看出来是“开过的”。异常的术后外观，是对患者、家属持久的暗示：“尿道下裂患者”。结合国外的长期随访经验，患者在幼年完成手术治疗，成年以后的不满意主要集中在阴茎整体外观和性生活的舒适度方面，而且在告知可以再次手术改善时的就医愿望迫切。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;因此，在选择治疗方法的时候，家属、患者、医生都要有更高、更久远的要求目标。</span><br />
