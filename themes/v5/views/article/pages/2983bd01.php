		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>患强直性脊柱炎，也能结婚生子</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">受访专家/广东省人民医院骨科副主任医师 肖丹</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 文/羊城晚报记者 陈辉 通讯员 郝黎 靳婷</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 近日，“周董”大婚，一些热心的读者产生了这样的疑问：周杰伦患有强直性脊柱炎，此病有“不死的癌症”之称，是否会对生育有影响？强直性脊柱炎患者以年轻男性居多，他们都将面对结婚生育问题，因此生育问题也是患者普遍关心的问题。广东省人民医院骨科副主任医师肖丹介绍，强直性脊柱炎患者在病情稳定时是可以生育的，不过，最好停药3个月以上。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 停药3个月再考虑受孕</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肖丹副主任医师介绍，强直性脊柱炎是一种血清阴性（血清中的类风湿因子阴性）的风湿免疫疾病，致病的机理还不明确。目前的研究认为与遗传有密切关系，男性病人多于女性病人，而且还多见于年轻的男性。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 强直性脊柱炎并不能治愈，但早期用药可以减缓病情的发展，使得关节、脊柱畸形、僵硬不会出现或者推迟出现，患者仍可以像其他人一样正常工作、生活。就如周杰伦，他从发现患强直性脊柱炎到现在已多年，仍能在舞台上又唱又跳，这正得益于早期发现、早期干预。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 生育问题是否会因患病受到影响？肖丹给出的回答是“多数情况不会影响”。他介绍，强直性脊柱炎患者的病情是会反复的，在发作期不宜生育。如果有生育计划的话，可以在病情稳定期，在医生的指导下停用影响生育的药物足够时间，根据药物的半衰期（通常是在3个月以上），再考虑受孕问题，以避免药物影响生殖细胞的健康，所幸作为一线用药的非甾体类药物并不影响生育。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 腰背痛是主要特点</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肖丹提醒说，如果疾病发现得晚，加之控制得不好，已经出现脊柱或髋关节畸形，生活都可能无法自理，更不要说怀孕了。那么，该如何尽早发现强直性脊柱炎？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据介绍，强直性脊柱炎最早的病理表现之一就是骶髂关节炎，患者往往感到下腰背疼痛。很多强直性脊柱炎病人起病时腰背痛，以为是自己不小心扭伤或拉伤，会自己买些活络油、红花油来擦，或贴药膏。持续一段时间不见好，可能会去做针灸、推拿。再不好，才考虑到医院就医，往往已经贻误了病情。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">强直性脊柱炎的腰背痛有以下几个特点：40岁前发病；药物止痛效果好，但容易反复发作；缓慢发病，而不是突然起病；休息后疼痛不能减轻，活动后疼痛反而可以减轻；有时晚上会痛醒，而且多是下半夜；经常会有屁股痛，但大腿的放射痛较少（即坐骨神经痛）。如果你的腰背痛符合以上大多数特点，建议尽早就医。</span>
</p>
