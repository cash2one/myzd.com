		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中山医院胸外科主办手术卫星直播活动——“胸术”</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4月20日，由中山医院胸外科主办、强生医疗器械有限公司协办的胸外科手术系列直播活动“胸术”开展了首期手术演示，手术实况通过卫星向全国19个城市的胸外科同行直播，取得了广泛而深远的影响。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中山医院胸外科王群主任和谭黎杰主任医师分别演示了通过左、右胸两种径路以及开放、微创等不同方法实施食管癌的根治性切除，向全国同道展示了中山医院胸外科精湛的手术技巧和全面的食管癌诊疗技术。随后，王群主任又演示了胸腔镜左下肺癌根治术，以精彩的淋巴结清扫完美收官。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">转播期间，四川雅安地震消息传来，王群主任通过卫星直播表达了对灾区人民的深情关怀以及对抗震救灾医务工作者的崇高敬意。大灾当前，在直播手术台上，王主任当即报名成为抗震医疗队的一员，随时候命，奔赴灾区。王群主任说：“作为一名外科医生，将完美的手术技艺作为最重要的抢险物资送到震区，送给灾民，是我们的最大心愿！”侠之大者，为国为民。作为胸外科手术演示品牌活动之一，“胸术”的成功举办为中山医院医务工作者的仁心仁术做出了最好的注解。</span><br />
