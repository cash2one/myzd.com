	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>减肥降糖释重负</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2988/1.png" title="" alt="demo.jpg"/>
</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">11月14日是“世界糖尿病日”。2型糖尿病是一种常见的内分泌代谢性疾病，主要是因体内胰岛素分泌不足或作用障碍引起的代谢紊乱的一种疾病，目前认为肥胖是糖尿病的主要危险因素。在所有类型糖尿病中，2型糖尿病病人约占90%。而肥胖人群中患有糖尿病的人数占41.5%。而且，发生肥胖的时间越长，患糖尿病的机会就越大。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">近日，上海复旦大学附属中山医院普外科的楼文晖教授和吴海福教授就公众关心的减重和糖尿病领域话题进行科普讲解，呼吁重视肥胖以及糖尿病，选择正确的诊疗方式。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“发福”诱发糖尿病</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">如今，全球有近三分之一的人超重或肥胖。在中国，肥胖或超重人群呈爆炸式增长。按体重指数，目前约有43.6%，即6亿的中国人超重或者肥胖。楼文晖教授介绍，“在临床上，诊断肥胖以体重指数（BMI）为标准，BMI超过25为超重，超过27.5即为肥胖。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">全球范围内，糖尿病已经成为严重威胁人类健康的慢性疾病之一，会引起冠心病、肾病、视网膜病变及神经病变等并发症，而这些并发症会造成患者致死、致残，严重影响患者的生活质量甚至生命。糖尿病通常与肥胖并存。调查显示在糖尿病患者中65%的人超重或者肥胖，可见“发福”不是福，甚至可能导致健康之“祸”，肥胖患者亟需正确看待肥胖导致的健康风险，尽早控制体重。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“微创”减肥降糖</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">说到减肥，很多人马上会想到饮食控制和锻炼。但是对于超重和已经深受糖尿病困扰的肥胖患者来说，这些方法见效慢、难以坚持甚至容易反弹。严格的饮食控制和反复的血糖波动还可能会对患者造成持续精神压力，影响生活质量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">敖先生是接受减重手术的患者，胖乎乎的他一直是人们眼中的“开心果”，但2013年的体检结果让他怎么也开心不起来。原以为人到中年发胖是常事，但他的各项身体指标都出现异常，还成为了2型糖尿病的患者，空腹血糖值已经高达18毫摩尔/升。敖先生四处求医问药，也开始锻炼身体控制体重，但是效果并不明显，稍不注意血糖又上去了，直到今年夏天，在医生的建议下，他接受了减重及糖尿病微创手术。“两个月之内敖先生不仅身体轻盈了不少，血糖、血压等指标都恢复到了正常水平，他的心里也轻松了不少。”吴海福教授谈道。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">减重与糖尿病手术不仅能迅速减轻患者的体重，也能有效改善大部分患者并存的血糖代谢紊乱，从而稳定血糖、远离糖尿病的困扰。“和传统治疗方案相比，减重手术可以采用腹腔镜微创手术，能显著减少手术创伤和并发症，相比不做手术的患者，83%的手术患者能有效降低糖尿病风险。在美国，减重与糖尿病手术已经有了几十年的治疗经验，是一种非常成熟的治疗方式。”吴海福教授介绍道。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">楼文晖教授进一步介绍，病人的BMI是判断是否适合手术的重要临床标准。只要年龄在16至65岁，BMI超过27.5，患2型糖尿病时间小于15年，经调整生活方式和药物治疗难以控制血糖，且存在代谢综合征或者合并症的情况就可以考虑减重与糖尿病手术。当BMI超过32.5的时候，无论是否患有2型糖尿病，都应积极进行减重手术。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">楼文晖教授还介绍，为了保证减重及糖尿病外科手术的质量，上海市普通外科临床质量控制中心在今年启动了上海市糖尿病手术治疗质控标准的制定，并已经在9月份开展可糖尿病手术的质量督查，这是我国第一个地方糖尿病手术质量标准，通过督查和宣教，规范了上海糖尿病外科治疗，确保了手术规范，造福广大患者。</span><br />
