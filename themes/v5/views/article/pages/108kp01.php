			<div style="text-align:center;">
	<span style="line-height:1.5;font-size:18px;"><strong>肝门胆管癌的诊断与治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;line-height:2;">&nbsp; &nbsp;疾病介绍：肝门部胆管癌也称Klatskin瘤，是胆道系统常见的恶性肿瘤。因其发生部位特殊、呈浸润性生长及与肝门部血管关系密切等特点给手术切除造成极大的困难。长期以来，肝门部胆管癌被认为是无法根治性切除的肿瘤。近20年来，随着影像学和手术技术的进步，使肝门部胆管癌的诊断和治疗取得重大进步，手术切除率逐步提高，生存率明显改善。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;line-height:2;">&nbsp; &nbsp; 临床表现：早期无特异性临床表现，无痛性黄疸是肝门胆管癌最早也是最重要的症状，呈进行性加重，伴有恶心、呕吐、消瘦，患者尿色深黄、大便色浅甚至呈陶土色等，常产生梗阻性胆管炎的表现，如寒战、发热伴肝功能损害。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;line-height:2;">诊断：依靠B超、CT、MRCP、PTC、ERCP等影像学检查，再结合血清肿瘤标志物如CEA、CA199、CA125等能做出明确诊断。增强CT能显示肿瘤与周围血管之间的关系；MRCP可以清楚地显示整个胆管的情况，对临床分型更有指导意义；PTC能直接显示肿瘤的部位和受累肝管的范围，并可通过PTCD术前减黄。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;">治疗：肝门部胆管癌只有根治性切除，才有彻底治愈的机会，根治性切除手术包括切除肝外胆道、骨骼化清扫肝十二指肠淋巴结和胆肠R-Y吻合，Bismuth III型以上的肝门胆管癌还需切除一侧肝叶与尾状叶，IV型肝门胆管癌还可考虑肝移植术。对于无法切除的病例可选择胆道支架内引流术或肝内胆管穿刺外引流术。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;">我科特色：受市科委课题资助，我科开展肝门胆管癌手术可切除性与安全性评估的临床课题研究，通过精准的术前影像与肝功能评估，精细的手术操作如cusa刀切肝，在明显减少出血的情况下，显著提高了切除率，使许多原本无法手术的Ⅳ型肝门胆管癌患者也得到根治，挽救了一大批晚期肝门胆管癌患者的生命。最近又开展围肝门切除术和特殊肝段切除术治疗肝门胆管癌，大大减少了术后肝功能衰竭的发生。</span><br />
