			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小儿换肝：“中国标准”从无到有 仁济医院十年实施500例小儿活体肝移植</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003489173658" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 11月10日，10个月大的重庆小女孩冉冉在上海交通大学附属仁济医院成功接受了活体肝移植。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 自2006年10月23日首例成功以来，仁济医院肝脏外科主任夏强带领团队在将近十载的岁月内完成了整整500例小儿肝移植手术，手术总量占全国半壁江山，连续五年位居世界第一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 相继填补多项空白</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 胆道闭锁是一种造成婴幼儿终末期肝硬化的常见先天性疾病。这些孩子的胆管是实心的，胆汁排不出来，全身出现黄疸。无论药物治疗还是手术，80%的患儿在两岁以内都会因为肝功能衰竭而死亡。让他们“起死回生”的唯一办法，就是肝移植。然而，器官移植的供体在全球范围都极度短缺。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2006年，夏强已是上海乃至全国公认的肝脏外科著名专家之一。可是在临床上，多少次看到或听闻患儿因为等不到供体而夭折，这让他痛感医生的无力。“传统的肝移植供体取自大小相近的尸肝，可是哪里找得到那么多适合孩子的供体？”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 当然办法也有，只是极难——活体肝移植。肝脏有很强的再生能力，如果从至亲身上取下一部分肝脏给孩子，并不会损害亲人的健康，而对于孩子而言，血缘关系也使患者术后排异反应较小。但患儿各主要器官发育尚不完全，血管胆管非常细小，婴幼儿肝移植手术技术难度极高。其中最大的难题是如何将大人与孩子的血管无缝对接，成人血管很细，孩子的更细，好比拼接两根直径不同的水管，要保证血液不会从任何缝隙处渗透。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 夏强和伙伴们探索从技术层面破解难题。团队积极与放射科进行合作，创建了三维立体的活体肝脏影像图形处理技术，使得手术前就彻底“摸清”患者肝内的解剖结构，为优化手术方案提供重要参考。手术技巧更是讲究，血管切面要严格按照术前规划的位置确定，不容有丝毫差错。不到2毫米的动脉在显微镜下被一点点剥开，手指必须稳准轻巧地覆上仍在呼吸的血管。成人与孩子的血管结构不同，两个口对接一个口的血管还涉及到拼接与缝补。遇到血管特别细弱的患儿，要搭起支架才能顺利完成手术。最初三年，他们小心翼翼地总共只做了31例，随着临床实例不断积累，夏强团队逐渐总结摸索出了适用于中国孩子的三种肝静脉解剖类型，并取得了多项重要技术创新，为提高手术成功率提供保障，如多模式肝动脉显微重建技术、复杂条件门静脉重建技术、婴幼儿肝移植胆道重建技术改进等，相继填补了国内多项空白。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 10年砥砺，硕果竟成。目前，夏强团队婴幼儿活体肝移植手术，时间从最初的13小时缩短到5小时至6小时，三分之一的手术病人不需要输血；手术中无肝期时间平均仅为48分钟。年龄最小的患儿不到5个月，最大的15周岁。手术成功率超过98%，一年生存率已超过90%，五年生存率达到80%。近两年来，更有从海外慕名而来的患者，5名来自马来西亚的患儿已经在仁济完成手术，费用比之前接洽的新加坡医院节省近90%。国际器官移植协会主席德尔莫尼克在今年8月的国际肝移植大会上盛赞：“仁济肝移植项目是中国乃至全球最出色的之一。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2010年，团队受卫生部委托协助制定了《中国活体肝移植临床应用技术规范》，中国活体肝移植就此有了属于自己的“行业标准”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 精准医疗全国推广</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “我们希望将技术推广至全国，让更多孩子得到及时救治。”夏强团队每每经过长时间奋战救回一名患儿后，在欣慰与喜悦之余，他总想着，“再多救一个就好了”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 由于技术限制，全国166家有器官移植准入资质的医院，真正开展累计超过20例的仅6家，与西方发达国家差距巨大，绝大部分患儿因得不到及时救治夭折。夏强坦言，“如今全国只有3家具规模的儿童活体肝移植中心，这对于数以万计的患儿而言，远远不够。”为此，夏强开创性地建起了一支跨医院、跨专业的队伍，使儿童肝移植能够在全国范围内可持续地健康发展，目前已在全国17个省(市)的33家三级甲等医院推广应用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为了将技术在全国推广，团队连续举办了8届国家继续教育学习班，为全国各地培养了千余位儿童肝移植骨干。许多个“第一次”就此诞生：第一个儿童器官移植专业委员会、第一份《儿童肝移植指南》、第一套中国儿童肝移植手术录像、第一届全国儿童肝移植论坛……中国儿童肝移植从2011年只占全国肝移植比重的1.8%，发展到今天占10%左右，夏强与他的团队功不可没。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 仁心仁术的慈善温度</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “几乎每一个患儿的背后都是一个家庭的辛酸故事。”夏强忘不了早年一个名叫琳琳的孩子。虽然母亲愿意割肝救女，却因承受不起高额费用，无奈忍痛放弃。这意味着，仅八个月大的琳琳将在高烧中煎熬，肚皮也会慢慢被腹水撑得鼓胀变形，而她的家人面对这一切，却无能为力。琳琳的母亲带着最后的希望和仅有的两千元积蓄，和奄奄一息的女儿一起来到了仁济医院，最终，夏强给她们实施了手术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 十几万元的医疗费用从何而来？在等待手术的病房里，二分之一患儿来自贫困地区，三分之二患儿家庭经济困难。肝移植手术缺乏医疗保障，除上海地区纳入儿童医保范围外，其余地区均为自费。为了拯救这些孩子和家庭，夏强和团队主动站了出来。“仁济肝移植的医生不单要救孩子，还要想办法帮孩子筹钱。每天这里都有生命的奇迹发生。”病房里陪伴病儿的志愿者吉红霞说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 从2006年起，夏强一边钻研技术，一边在各个平台为患儿们发声求援。从最早求助于报纸、电视，到如今的“仁济肝移植官方”微博，他的坚持从未改变。在他们的奔走下，2011年，国内首家也是唯一的“胆道闭锁儿童之家”正式成立。该组织与仁济肝脏外科长期合作，全程救助需要治疗的胆道闭锁患儿。各慈善基金会、公司和爱心人士目前累计捐款额已达2000余万元。目前，仁济肝脏外科完成的500例肝移植患儿中，70%以上得到了社会各界不同程度的救助。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 贴着维尼熊卡通的温馨病房里，夏强和助手们正在耐心询问着患儿家属情况。“疾病的存在让世界总有遗憾和不完美，而我看到的，却多是永不放弃、永不言败。看见那些面色蜡黄、爱哭爱闹的小脸，变成了今天白皙健康、天真活泼的笑脸，这是我们最大的幸福。”</span><br />
