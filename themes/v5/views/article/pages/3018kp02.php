		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>我国肺癌的治愈率仅为30% 肺癌要怎么预防？（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在发达国家，肺癌的治愈率达到近70%；在我国，肺癌的治愈率只有30%。这与我国近八成肺癌患者一发现就已进展到中晚期密切相关。中国医促会近日成立胸外科分会，希望能推动胸外科、呼吸内科、肿瘤内科等多学科联手，加强肺癌早期诊断，降低肺癌死亡率。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">据介绍，目前我国肺癌的发病率和死亡率均居恶性肿瘤之首。预计到2025年，我国肺癌患者将达到100万。中日医院胸外科主任刘德若说，近年来，我国肺癌的发病呈现出年轻化、女性化的趋势。以前肺癌的高危人群年龄锁定在65至70岁以上，认为肺癌是一种“老年病”。但现在的数据显示，我国肺癌的发病和死亡年龄从40岁开始迅速上升，70岁达到高峰，其中45至65岁患者占75%。从临床观察来看，现在四五十岁的肺癌患者比过去明显增多，过去40岁以下的肺癌患者很少见，现在却屡见不鲜，而且女性肺癌患者也越来越多。其中，女性肺癌增长趋势加快可能和现代女性吸烟人数增多、吸烟年龄低龄化、女性被动吸烟以及女性接触厨房油烟较多等密切相关。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">肺癌要怎么预防？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">肺癌日常预防</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1、戒烟。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">吸烟是导致肺癌的一个最重要因素，主动吸烟和被动吸烟对身体都会造成很大的伤害，因此戒烟是预防肺癌首选方法。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2、养成正确的饮食习惯。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">预防肺癌就要规律饮食，多吃菌类、豆类食品，少喝酒。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3、坚持体检。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">每次体检都要做胸片正侧位检查，尽量每年做胸片检查，对疑有问题的病例，再进行详细检查。坚持体检可做到早发现、早治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">肺癌患者健康食谱：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鸡汁薏苡仁粥：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">用料：黄母鸡150克，薏苡仁100克，粳米50克。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">制作方法：将黄母鸡去除脂肪及鸡皮，洗净；将薏苡仁、粳米洗净。先加清水适量，煮至鸡熟烂，再取鸡汁1000毫升煮薏苡仁及粳米成粥，加盐调味，随意饮用。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">功效：本汤补中益气，利水消肿。凡肺癌病人脾胃虚弱、神疲体倦、腹胀、双下肢浮肿者皆可饮用本品。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">禁忌：本方偏于滋补，凡外感未愈，或咳嗽痰多者慎用。汤中黄母鸡以不曾下蛋者为佳。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">冰糖杏仁糊</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">材料：甜杏仁15克，苦杏仁3克，粳米50克，冰糖适量。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">做法：将甜杏仁和苦杏仁用清水 泡软去皮，捣烂加粳米、清水及冰糖煮成稠粥，隔日一次。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">功效：具有润肺祛痰、润肠、止咳平喘等功效。</span>
