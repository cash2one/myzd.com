		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海前列腺癌10年增8倍</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;新闻背景：作为国家重点基础研究发展计划（简称“973计划”）立项以来泌尿系肿瘤领域的首个项目，“前列腺癌的分子机制与干预”课题研究近日落户上海长海医院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 中国前列腺癌怎么治，不能听由外国人说了算。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 然而，2009年中华医学会新出台的有关前列腺癌的诊疗指南中，参考文献竟有近90%来源于国外。上海长海医院院长孙颖浩教授说，研究中国人群特异性的、个体化的、适合国情的前列腺癌诊疗方案，迫在眉睫。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 因为我国前列腺癌具有明显种族差异性，极具中国特色。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 特色一：新发病例猛增长 前列腺癌在西方被称为男性第一癌，常年位于男性恶性肿瘤首位。虽然受遗传基因等因素影响，中国发病人数不及西方，但近年来迅猛增长。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; “上海前列腺癌发病率在过去的近10年内上升了约8倍。预计到2020年还将翻番，与肝癌的发病率相当。”孙教授称。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 究其原因，长海医院泌尿外科任善成博士认为，主要是前列腺特异抗原检查（PSA）筛查技术的逐渐普及，使众多“隐身”患者浮出水面，以及社会老龄化、民众高油高脂的饮食和久坐不动的生活方式导致。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 特色二：首诊患者多晚期 另一显著不同是，中国人约40%的前列腺癌患者初诊已属晚期。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 任善成认为，欧美国家能做到早发现，得益于在50岁以上人群中强制推行PSA筛查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 因此，专家建议，50岁以上或有前列腺癌家族史的45岁以上男性应每年做三项检查——PSA筛查、前列腺B超和直肠指检，以期尽早发现前列腺癌。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 一旦发现PSA指标异常，也不要过度恐慌。包括尿路感染、前列腺肥大、安插导尿管等都会干扰PSA指标，建议进一步到医院泌尿外科确诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 特色三：误诊过度治疗多 前列腺癌还有个特点，那就是，即便得了，也不必非得治。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 孙颖浩形象地把前列腺癌比作三种不同的动物——乌龟、猴子和兔子。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 乌龟，只要水桶不翻，它可能一直趴在桶底一动不动，与患者共存，因此，这种类型可以不用治；猴子，到处惹是生非，很快危害患者生命，必须迅速手术及做其他治疗；兔子，潜伏在体内，一旦受惊吓，便可能跑出来肇事。这一类，可以等等再手术。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 遗憾的是，这三类完全不同的前列腺癌状态，现有的诊断方法无法鉴别，只能是“龟、兔、猴一把抓”，造成了误诊或过度治疗。</span>
</p>
