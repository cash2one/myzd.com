			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>颅内海绵窦海绵状血管瘤手术治疗还是射波刀治疗？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';">颅内海绵窦海绵状血管瘤是一种极其少见的海绵状血管瘤，美国人把海绵窦海绵状血管瘤称为海绵窦血管瘤（英文：hemangioma in the cavernous sinus 或cavernous sinus hemangioma).。在华山医院神经外科，每年有1万台脑外科手术，海绵窦海绵状血管瘤大约10-13例左右，占脑瘤的0.1%-0.13%。因此这种肿瘤极其少见，但是亚洲人比欧美人多见。有的学者把海绵状血管瘤称为海绵状血管畸形，但是目前多数学者把海绵窦海绵状血管瘤当成一种血管性肿瘤。海绵窦、中颅底、眼眶附近的海绵状血管瘤与脑实质内的海绵状血管瘤在组织病理学上是一样的，但是两者的生长方式完全不同。海绵窦、中颅底、眼眶附近（脑实质以外）等部位（颅内脑实质以外）的海绵状血管瘤多见于女性病人，肿瘤生长缓慢，早期没有症状。当有临床症状时，多数肿瘤比较大，甚至巨大。在伽玛刀、射波刀出现之前，多数病人选择手术切除肿瘤，极少病人接受常规放疗。小的和中等大小的海绵窦海绵状血管瘤可以直接伽玛刀治疗，也可以射波刀治疗。2007年12月华山医院引进射波刀之后，我们用射波刀累计治疗了近50例海绵窦海绵状血管瘤，均取得了非常好的效果。射波刀治疗的海绵窦海绵状血管瘤多数是大肿瘤和巨大肿瘤。射波刀通过分次照射肿瘤，使肿瘤接受较高的治疗剂量，同时提高颅神经（视神经、动眼神经、三叉神经、外展神经和滑车神经）耐受放射的剂量（也就是说：减轻或降低颅神经的损伤）。通过随访每一位病人，发现射波刀的治疗效果非常好。华山医院神经外科的多数医生都能通过看脑部的磁共振（MRI）片子，正确诊断海绵窦海绵状血管瘤。他们一旦诊断为海绵窦海绵状血管瘤，会建议病人射波刀治疗。我们科的王鑫医生，对放射外科（伽玛刀、X刀）治疗海绵窦海绵状血管瘤进行的荟萃分析，结论是：放射外科治疗海绵窦海绵状血管瘤疗效好、副反应轻（论文发表在国外的神经肿瘤杂志，J Neurooncology)。射波刀在短短的4年里治疗了近50例此类病人，治疗后肿瘤缩小，一部分肿瘤几乎消失，病人视力改善。这些良好的结果和几乎没有治疗后副反应的良好效果证实射波刀治疗的优越性，在权衡手术风险、手术后的并发症时，射波刀将逐渐成为治疗海绵窦海绵状血管瘤的主要手段，甚至完全替代外科手术。</span>
</p>
