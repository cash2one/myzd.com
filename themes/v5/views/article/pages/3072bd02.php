		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>一岁娃脖子痛寰枢椎脱位在作怪</strong></span><span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong></strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">日前，全国年龄最小的上颈椎内固定手术在广州完成</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯 （记者莫冠婷 通讯员李冰洁）仅1岁8个月大的兰州男孩明明患有颅底凹陷症、寰枢椎脱位等多种先天性颅颈交界部复杂畸形，近日他在广州成功闯过了手术关，成为国内年龄最小的上颈椎内固定手术患者。专家提醒，明明是在婴儿期及早发现的颅底凹陷症、寰枢椎脱位例子，但临床上多数患者在成年后才被发现，越晚发现越有危险，可能随时一个走路不稳摔跤后就会瘫痪，甚至丧命。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">发病：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1岁及时发现歪脖子头痛</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1岁8个月大的明明是在婴儿期及早发现的案例。明明的妈妈说，今年1月，她就发现明明的头部经常向左歪。3月时，明明常常指着头部哭闹，妈妈发现明明脑袋常常耷拉着头抬不起来，有时甚至不能玩耍，只能躺在床上休息。妈妈觉察到明明头部有问题，马上带其到当地医院检查。结果发现，明明颅内压增高，有轻度的脑积水，但治疗半个月后病情仍未好转，辗转五六家医院后才查出寰枢椎出现问题。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5月，妈妈带小宁来到广州军区广州总医院骨科医院求医。经检查发现，明明患有多种先天性颅颈交界区复杂畸形，包括颅底凹陷症、寰枢椎脱位、Chiari畸形、先天性寰椎发育不良等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">手术：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">年龄幼小，风险极高</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“他是目前国内接受上颈椎固定手术年龄最小的患者。”手术主刀医生、广州军区广州总医院骨科医院副主任医师艾福志表示，由于年龄太小，身体各方面结构发育尚未完全，特别是需要手术的部位的第二颈椎（枢椎）椎弓根非常小，而且枕骨发育很薄，导致固定困难，再加上目前的手术器材主要针对成人，因此手术风险极高。术前，院内专家进行了充分的手术方案讨论，最终确定进行“寰枢椎后路复位枕颈内固定植骨融合术”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">艾福志表示，手术存在一定的风险，稍有不慎可能伤及上颈椎神经中枢和延髓，轻则术后瘫痪，重则术中丧命。如果螺钉进入了椎动脉孔，压迫了优势的椎动脉，则可能引起术后大面积脑梗塞，甚至死亡。所幸手术顺利完成，明明5~7天后出院，目前恢复良好。术后1~2个月枕骨和枢椎之间的骨头能长好，即可去掉头颈胸支具，过上正常人的生活。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">提醒：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">多数在成年后才被发现</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">艾福志表示，寰枢椎脱位能引起枢椎的齿状突进入枕骨大孔内，可对脑干、脊髓造成压迫，导致枕颈部疼痛、颈部活动受限，四肢无力，走路不稳，有的还会出现吞咽困难、饮水呛咳等颅神经损害的症状。随着压迫脊髓、脑干的程度越来越重，可引起呼吸困难，甚至瘫痪、死亡。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“明明算是早期发现的幸运例子，如果未及时治疗，可能幼年即会夭折，临床上更多见的是成年发病后才发现寰枢椎脱位。”艾福志说，不少先天性发育畸形在成人后被发现是因为小时候认识不到问题的存在，以为头痛脖子痛是疲劳或局部不舒服引起的，没有及时发现寰枢椎可能出现问题。直到长大后因为歪脖子或手脚没力、走路不稳常摔跤等，才发现问题。但是，一次严重的摔跤随时可能出现瘫痪或猝死。因此，家长应多留神，发现孩子出现头痛、脖子痛、歪脖子、走路常摔跤等症状时，应及时送院排查头颈部是否出现问题。</span>
