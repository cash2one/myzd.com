		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>(广州军区总医院名医榜)尹庆水：让瘫痪的人重新站立）</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 26岁的湛江女孩陈妹，受外伤后四肢麻木，两腿沉重无力，经常莫名其妙地抽筋和摔倒，后来瘫痪在床，被广州军区广州总医院确诊为“难复性上颈椎脱位、脊髓严重受压引起瘫痪”，必须及早进行手术。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">而脊髓部位的手术风险大，如不成功，轻者导致肢体麻木瘫痪，重者危及生命。因此，这被认为是脊柱外科手术的禁区，全国只有少数几家医院能完成。面对患者渴望的目光，全国知名脊柱外科专家、广州军区广州总医院骨科中心主任尹庆水教授向这一手术禁区发起了挑战。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 无影灯下，尹庆水采用独创的“寰枢椎即时复位钢板系统技术”，将压迫脊髓的瘢痕和骨组织彻底切除、松解，通过器械将脱位的椎骨完全复位。3周后，患者顽强地站立起来，1年后恢复正常生活。陈妹和家人含着热泪，手捧鲜花向尹庆水致谢。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 生命在于运动，而支配人体四肢复杂运动功能的正是脊柱中间的神经———脊髓。多年来，尹庆水在脊髓这一外科手术禁区默默探索，为众多患者带来了福音。他在脊柱、四肢和关节疾患诊治等方面积累了丰富的经验，特别是上颈椎损伤与疾患、严重痉挛性脑瘫和颈椎病的手术治疗等技术居全国先进水平。他研制的“经口咽前路寰枢椎即时复位钢板系统”为世界首创，已获3项国家专利，相关论文在国际脊柱外科权威杂志《SPINE》上发表。不久前，这本权威杂志编辑部寄来了一份热情洋溢的学术交流邀请函，认为尹庆水等发明的“上颈椎经口路复位钢板内固定技术”可以同时完成减压、复位和内固定，解决了以往开展这一区域的手术需要分期分步进行的难题，开创了一种系统治疗难复性寰枢椎脱位的最新方法。“能用自己发明的技术为广大患者服务，使瘫痪的人重新站立，是我终生追求的目标。”尹庆水如是说。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 名医档案：尹庆水，主任医师，教授，博士生导师，全军创伤骨科中心主任，广州军区广州总医院骨科中心主任。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 出版《临床颈椎外科学》等6部专著，发表论文50余篇。“寰枢椎即时复位钢板系统”、“复合珊瑚羟基磷灰石人工骨及其制备方法”等6项发明获国家专利。多项成果获军队科技进步二、三等奖。</span>
</p>
