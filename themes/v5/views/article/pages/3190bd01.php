	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>杨仕明：让世界不再“静悄悄”</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">杨仕明一直在做一件事，就是通过对聋病机制和防治的探索研究，找到让聋人重回有声世界的路径。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 经过长期攻关，杨仕明带领的团队通过对声损伤致聋分子机制的研究，形成了一整套聋病防治新策略，课题组成功筛选出中成药“骨参片”，并且成功研发出符合汉语声调特点的国产人工耳蜗，打破了国外对于这个领域的垄断局面，大幅降低了患者植入人工耳蜗的费用，使更多聋人受益。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 正是因为这一系列成就的取得，由中国人民解放军总医院和浙江诺尔康神经电子科技股份有限公司联合完成的“听觉损伤致聋机制及防治关键技术应用”项目获得了2015年国家科技进步二等奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在耳聋研究领域，杨仕明算得上领头人。作为中国人民解放军总医院耳鼻咽喉头颈外科主任，国家重点学科带头人，聋病教育部重点实验室主任，全军声损伤防护重点实验室主任，国家人工耳蜗项目技术指导专家，国家973项目首席科学家，他20多年来一直致力于研究耳蜗毛细胞从初步受损到表现出听力逐步下降以至最后失聪的过程，“抓住听觉损伤病理进程，也就找到了耳聋治疗的关键点，能够根据不同的时间节点"对症下药"。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 经过很长一段时间的研究观察之后，杨仕明课题组发现：听觉细胞纤毛上面有一串蛋白链接，即顶端链接(Tip-Links)，纤毛之间就像绳子一样被一根一根串起来的，如果纤毛顶端的分子蛋白断裂，神经的听觉功能也就不存在了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “一般认为耳蜗毛细胞死亡后不能自我再生，但为什么噪声损伤导致听力下降的患者过了几个小时有些人又能听见声音了呢？”带着这个长期困扰业界的难题，杨仕明指导课题组成员对毛细胞噪声损伤后的变化过程进行观察，最终惊喜地发现，24小时之后Tip-Links顶端链接又一个一个接了起来。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “这就是为什么噪声损伤以后24小时又能重新听到声音，回答了这个问题就是揭示了听觉损伤关键的分子机理之谜。我们进一步又发现，噪声刺激如果仅仅使纤毛顶端分子蛋白断裂，是有可能重新恢复听觉的，但如果连纤毛都断了，那就没有机会恢复了。而且一旦发生纤毛损伤，毛细胞和内耳损伤就会像多米诺骨牌一样持续加重，如果不提前干预，最终会导致听觉神经细胞彻底死亡。”杨仕明说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 正是这个重大发现，杨仕明团队和国外著名大学研究合作者首次建立了高倍显微镜下精准定位的毛细胞纤毛损伤模型，在国际上首次揭示了毛细胞纤毛损伤之后顶链接蛋白自我修复是形成暂时性阈移的关键机制，并依此提出了听觉损伤干预的时间窗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 杨仕明由此提出了听觉损伤的四阶段论：从最初的纤毛顶端分子蛋白断裂，到纤毛损伤进一步加重，再到毛细胞不可逆损伤阶段，到最后的毛细胞全部死亡。“从听觉细胞的损伤到细胞死亡是有一个过程的，这个病理进程就是我们怎么去预防去治疗的关键时期。”杨仕明说。这个创新性理论应用于临床实践对感音神经性聋的急性期诊治意义重大，杨仕明两次执笔起草关于中国突发性聋诊治指南由中华医学会已经在全国颁布实施。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “人工耳蜗是世界上迄今为止最成功的神经植入假体，也是目前恢复重度耳聋患者听力的唯一有效方法，”杨仕明说，“解放军总医院听觉植入中心已经为3000多例耳聋患者植入了人工耳蜗，99%患者手术后恢复了听觉，70%以上回归了工作和学习岗位，回到正常社会生活。但遗憾的是，进口人工耳蜗价格昂贵，阻挡了很多中国患者获益于这项先进医疗技术，而高性能、低价位的国产人工耳蜗临床应用成功，将彻底扭转这一局面。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 杨仕明表示，虽然目前研究成果取得很大突破但还仅仅是个开端，通过药物或者人工耳蜗植入技术治好的毕竟只有一部分病人。“我正在带领研究团队开展基因治疗、基因修复和干细胞治疗等新技术，有望修复和激活受损内耳细胞和听觉神经通路，即便是听觉细胞已经死亡的患者也可以实现"铁树开花"，帮助他们重新回到这个美妙动听的世界。”杨仕明说。</span>
</p>
