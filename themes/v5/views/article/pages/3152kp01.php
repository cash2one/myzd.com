			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何判断甲状腺肿物是良性还是恶性呢？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">区别甲状腺良、恶性肿瘤，主要依靠病史，体检，放射性核素扫描，多普勒超声和穿刺细胞学检查等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病史：①儿童出现的甲状腺结节５０％左右为恶性，②发生于年轻男性的单结节，也应警惕为恶性可能。③如病人突然发生结节，且短期内发展较快，则恶性可能较大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">体检：①甲状腺单结节比多发结节恶性概率大。②良性腺瘤特点：触诊表面光滑，质地较软，吞咽时活动度大。③恶性结节特点：表面不平整，质地较硬，吞咽时活动度较小，恶性结节常伴有颈部淋巴结肿大，有时癌肿很小，而同时颈部已有肿大淋巴结。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">核素扫描：应用放射性碘131扫描，比较结节与周围正常甲状腺组织的放射性密度。温结节：多为良性肿瘤，甲状腺癌的可能较小。热结节：几乎均为良性。冷结节：甲状腺癌均为冷结节，其边缘一般较为模糊，但冷结节不一定都是癌肿的表现。良性结节性甲状腺肿的结节内常由于血液循环不良发生退行性变形成囊肿，也可表现为冷结节，不过其边缘多清晰可见。而甲状腺瘤可表现温凉结节外，也可能是冷结节，</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">彩色B超检查：不仅可以辨别甲状腺肿块是囊性还是实质性外，还可以观察肿块的血流情况，血流丰富者有恶变可能。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">穿刺细胞学检查：可进一步明确甲状腺结节性质，诊断正常率达８０％以上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">建议发现甲状腺结节的患者及时就医，根据情况选择定期复查、药物治疗、手术或微波治疗。</span><br />
