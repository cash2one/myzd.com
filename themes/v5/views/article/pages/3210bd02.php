<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:1.5;"><strong>胡海应健康报邀请谈论保胆治疗&nbsp;</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">健康报2010年10月27日第8版: 医学论坛</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 胆囊结石的形成是遗传与环境共同作用的结果。其过程大致可以分为以下几个连续的阶段：第一期——遗传期，即先天性肝脏胆汁酸、胆固醇或脂类代谢异常。第二期——化学期，出现致石性胆固醇过饱和胆汁。第三期——物理期，出现光镜下可见的胆固醇结晶，涉及胆囊内成核。第四期——生长期，从小结晶成长为肉眼可见的结石。第五期——临床症状期，出现与结石相关的临床症状，包括胆囊壶腹或胆囊管梗阻引起的典型胆绞痛症状、胆囊慢性炎症的非特异症状，以及胆囊结石并发症所引起的症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"> 基于这一认识，可以广义地将胆囊结石预防分为四个层次：一级预防，防止胆囊结石易患人群产生结石。二级预防，对无症状的胆囊结石进行有效处理，防止并发症发生或结石进一步增大。三级预防，对有症状的结石患者进行治疗，防止或延缓胆囊失去功能或防止并发症。四级预防，消灭结石后，防止结石再生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 二级预防是对目前流行的观点——无症状结石无需治疗观点的挑战。对于某个个体，如果能够知道是否会产生症状和什么时候产生症状，那么可以不治疗或等到即将出现问题的时候再处理。然而事实并非如此，我们只能通过普查早期发现结石，却无法预测结石何时何地以何种方式发病。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"> 在腹腔镜胆囊切除技术出现之前，剖腹手术毕竟对人体的损伤较大，令人望而生畏，故大多主张出现症状才处理，结果病人在出现了明显的临床症状或并发症才入院接受手术。加上这种情况往往发生在中老年病人身上，手术风险自然大大增加，手术并发症和死亡率相对较高。以腹腔镜技术为代表的微创外科时代的到来，缓解了人们对于胆囊切除的恐惧心理，使得越来越多的病人在症状不重的情况下就接受胆囊切除。有利的是，从整体上看，胆囊结石病人接受手术治疗的时间提前了，减少了手术难度和并发症；不利的是，受腹腔镜胆囊切除优点的鼓舞，出现了一批无视胆囊功能的“切胆专业户”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"> 解决这一矛盾的最佳策略是微创保胆取石。胆囊结石的症状和并发症都是结石引起的，只要消灭结石，就能达到目的，无需切除胆囊。而且无症状期的胆囊功能比较好，保留价值大，手术难度低。因此，对于胆囊结石，我们主张一旦发现尽早处理，保留胆囊取出结石。</span>
</p>
