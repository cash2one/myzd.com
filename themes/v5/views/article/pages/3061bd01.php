			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>医师报——寻找“大医精神”--张骞：打响“肾脏保卫战”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2012年：完成200个保肾计划</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">12月25日，下午7点，武汉天河国际机场。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">离登机还有一个半小时，趁闲暇间隙，张骞梳理了一下当天的日程：上午9点会诊，10点半上手术台，11点缝合完毕，下午1点培训手术技能及理念……</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">考虑时间还充裕，他开始“检阅”自己年初制定的计划：“百肾计划”，也就是通过腹腔镜肾部分切术为100名患者保留肾脏。算上今天这例，已经超过200个了，算是超额完成任务吧。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可能在别人看来200只是一个没有任何感情色彩的数字，但对张骞而言，每一个数字背后都是一次高风险的挑战，都是一个生命甚至家庭的希望，都灌注着他的心血。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当前，肾肿瘤高发趋势已跃居泌尿外科第二位，且越来越年轻化。对于4～7公分、毗邻大血管的良性肿瘤或小于4公分、位于肾脏核心的恶性肿瘤，如果主刀医生技术不够精湛，他们只能肾脏全部切除。当很多人在高风险的肾部分切除术前考虑自己的名誉时，张骞选择将患者的生存质量放在首位——施行部分切除术为他们保留肾脏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">33岁的北大医院泌尿外科副主任医师张骞，已在泌尿领域年轻一辈中小有名气。过去的2012年，他奔波往返于全国10个省30个市之间，完成高难度腹腔镜手术400余例，给自己年初许下的承诺交出了一张满意答卷，也得到了患者与同行的广泛赞誉。2013年，他计划用自己微弱的力量号召更多的医生，共同完成“千肾计划”！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3年前：为农民工肿瘤患者保肾</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">提起他的第一台腹腔镜肾部分切手术，张骞至今仍记忆犹新。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3年前，一名33岁的农民工肾脏上长了个约4.5公分的肿瘤。在充分查体评估后，张骞考虑如果这名患者选择肾全切，对他而言虽然轻而易举，但对患者来说则影响甚大。考虑到他年纪很轻，又是一个贫困家庭唯一的经济支柱，张骞提议做腹腔镜肾部分切术。患者满是性命相托的信任，最终手术顺利地完成了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，这种手术风险很高，尤其对资历尚浅的年轻医生而言，挑战更大。但想到通过手术能为患者保留肾脏，能为一个家庭带来希望，张骞非常愿意挑起这份重担。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术的成功对他鼓舞很大，张骞至今仍对这名患者充满了感激，“有了这名患者的信任，才有了我的第一步。我应该以更大的努力来回报患者的信任。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下一个目标：挑高难度手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">年前，一名12岁的罕见异位嗜铬细胞瘤的先心病患儿前来就诊。这种肿瘤释放肾上腺激素，最高时血液浓度超过正常时的200倍。超强的毒性让患者随时会有生命危险。又由于她的心脏很脆弱，加上肿瘤位置紧邻大血管，手术的难度和风险极高，辗转多家医院没有医生愿意尝试。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经仔细查体和反复研究， 2012年圣诞节，张骞决定进行腹膜后镜左肾上腺嗜铬细胞瘤切除术。手术气腹压力需要严密控制，张骞特别下调了气腹压力，这让手术操作增加了难度。更要命的是，分离后发现肿瘤紧贴在肾动、静脉上，上方还包绕了一支异位肾血管，使得手术操作空间变得更加局促。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于患儿的嗜铬细胞瘤功能强劲，稍许的挤压和触碰都可能造成他血压的剧烈波动，进而危及生命。但张骞镇定地按照事先的计划，小心翼翼地对每一根支配肿瘤的血管进行阻断、分离……随着最后一根血管的阻断，肿瘤终于从肾动、静脉上被剥离下来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术的难度超乎想象，如果动作稍微粗暴，血压急剧升高，患者当时可能就会心力衰竭而死亡；如果动作速度过快也会引起血压急剧下降，进而危及生命。如果失败，即便患者家属不追责，对医生而言，自责、同行评议等带来的压力也很大。但在张骞看来，如果通过自己努力能给年幼的生命带来希望，承担再大的压力也是值得的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2013年：制定拯救千肾计划</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">新年伊始，张骞开始制定新的年度计划——“千肾计划”， 为符合保肾术适应证的1000名患者保住肾脏！他深知，完成这个计划靠一个人的力量是不可能的。从去年开始，他便通过各种途径与学术会议，分享自己在腹腔镜肾部分切术的心得体会，让更多医生了解并能够操作，共同完成“千肾计划”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刚开始，大家都觉得腹腔镜肾部分切术难度很高，很多人不敢碰。为改变他们的观念，张骞在演讲时做了大量的讲解和演示。张骞算了一笔账，去年自己共参加会议10多次，每次参会医生50多名，就意味着去年受培训医生至少500人，如果他们一人每年做2例手术就能实现“千肾计划”。今年计划到20个省60个市做手术演示和技术支持，那时数字的上涨可能不只是倍数的增加。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张骞每天都被梦想推着走，知道每天应该努力的方向，每天向目标前进了多少，距离目标还有多远。新的一年，他的计划开始启动，了解他的人无不充满了感动，因为在这个商业化的社会里，有一位拥有高尚品德力量的青年医生在坚守生命托付的责任， 用高尚的医德和医术抚慰着广大患者的身心，成就着一位青年医生的职业尊严和使命。</span><br />
