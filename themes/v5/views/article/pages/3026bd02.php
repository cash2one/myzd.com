		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中山医院泌尿外科团队27分钟为“孤肾”患者除瘤保肾</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">叶女士因为肾癌，20年前已切除了右肾。此次左肾又被查出长了大大小小5个肿瘤！中山医院泌尿外科团队技高胆大，仅用27分钟，就成功地实施了除瘤保肾手术。患者今天康复出院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 患者今年59岁，当年在中山医院泌尿外科接受过肾癌根治性切除手术，摘除了右肾。上个月，她在体检中又发现左肾多发肿瘤，在中山医院进一步检查后证实，左肾上竟然有最大5厘米、最小0.5厘米共5个肿瘤，而且其中一枚2厘米大小的完全内生性肿瘤，紧贴着左肾静脉。结合患者病史，医生高度怀疑叶女士所罹患的是一种罕见的疾病——VHL病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 中山医院泌尿外科郭剑明主任介绍，VHL病是一种罕见的遗传性疾病，发病率约为1/36000。患者常表现全身多脏器的多发良性或者恶性肿瘤：肾囊肿、肾细胞癌、中枢神经系统及视网膜成血管母细胞瘤、胰腺囊肿或肿瘤、肾上腺嗜铬细胞瘤和附睾囊腺瘤等。VHL的患者中，有近一半的患者会发生肾癌，且常常为多病灶和双侧性。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 由于叶女士只有一侧肾脏，如果简单地切除左肾，患者将变成“无肾人”，日后将面临长期的血液透析，生活质量大大下降。如何完整地切除叶女士左肾上的5个肿瘤，并最大限度地保留患者肾脏功能，是手术团队面临的最大难题。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 要保留患者肾脏，手术的挑战极大。最最关键一点，是需要在极有限的时间里逐个切除肿瘤。因为在手术过程中，为减少术中出血，必须阻断肾蒂。但阻断时间的上限为30分钟，一旦超时，患者术后肾功能将永久无法恢复。而要在30分钟内完成5个大小不等、深浅不一的肾脏肿瘤的切除，同时保证切除深部肿瘤时不能损伤相邻的大血管，并完成止血、修补、缝合等所有步骤，几乎是不可能的任务！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 为确保手术顺利进行，由泌尿外科郭剑明主任领衔，王杭副主任医师、胡骁轶主治医师组成的团队，在仔细研判术前影像资料的基础上，精心制定了手术方案。8月12日，他们信守“先难后易”的肿瘤切除原则，同时采取了“冰屑降温”、“浅表肿瘤开放肾蒂下切除”等措施，既保证完整切除了肿瘤，又最大限度地保留了患者的肾脏功能。结果，术中肾蒂阻断时间仅为27分钟，整个手术过程出血量不超过200毫升，手术获得了圆满成功。</span><br />
