	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>湖南医院成功为5个月“大头娃娃”切除颅内巨瘤</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">新华网湖南频道长沙９月１８日专电（记者帅才）５个月宝宝的脑袋里“藏”了一个乒乓球大小的“不定时炸弹”。近日，中南大学湘雅医院小儿神经专科主任刘景平教授，成功治疗了一例罕见的颅内巨大血管瘤患儿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> ５个月大的朵朵（化名）活泼可爱，与一般宝宝无异。奇怪的是，朵朵的头却比同岁的宝宝大了一圈。“从４个月开始，朵朵的头就开始慢慢长大了，连皮肤上的血管都看得非常清楚。”朵朵妈妈告诉记者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 来到湘雅医院检查才发现，原来朵朵的脑袋里竟然长了一个直径５厘米大小的血管瘤。“血管瘤长在朵朵脑袋最中心的位置，压迫了周围重要的脑组织。血管瘤已造成了患儿脑积水，造成脑室扩大，看上去就像大头娃娃，颅内压增高，可能造成脑萎缩。一旦血管瘤破裂，会造成生命危险。”刘景平教授说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 由于血管瘤位置较深，周围都被脑组织包围，如何找到最佳“切入点”是一大难题。既要精准切除血管瘤，又不能对周围脑组织造成损害，最重要的是，必须保证血管瘤完整，一旦破裂后果不堪设想。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 近日，刘景平教授领衔的团队顺利地找到了最佳手术切入口。首先，医生先将供血的２根动脉与血管瘤分离，成功切断“源头”。由于缺乏供血，血管瘤慢慢软化，张力变小，破裂的危险也有所降低。医生再将血管瘤牵引出来，尽量避免损伤周围正常的脑组织。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 经过６个多小时的奋战，湘雅医院专家团队成功为患儿朵朵摘除“不定时炸弹”。目前，朵朵情况良好，不日可出院。</span>
</p>
