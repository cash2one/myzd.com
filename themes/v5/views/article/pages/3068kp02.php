			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>他汀类药物使血管疾病低危人群获益</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血管疾病低危人群通常不被推荐应用他汀类药物，但是CTT协作组在医学杂志柳叶刀上发表文章称，5年血管疾病风险&lt;10%的人群应用他汀类药物使低密度脂蛋白胆固醇（LDL-C）每降低1mmol/L，5年内每1000人中11人可免于发生血管疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这项荟萃分析纳入了27项（174149名受试者）他汀类药物降低LDL-C的随机对照研究，其中22项（134537名受试者）为他汀类药物与安慰剂对照，5项研究（39612名受试者）是强化他汀治疗与低剂量他汀的比较。该荟萃分析观察了主要血管不良事件，包括主要冠状动脉（冠脉）事件（非致死性心梗或冠脉疾病导致的死亡）、卒中及冠脉血运重建。根据对照组（包括低剂量他汀药物组）的5年血管疾病风险，将受试者的5年血管疾病风险分成5组（&lt;5%,≥5%至 &lt;10%, ≥10% 至 &lt;20%, ≥20% 至 &lt;30%, ≥30%）。分析受试者LDL-C降低1mmol/L后血管疾病的降低情况。结果显示，27个研究中有5个平均血管风险&lt;10%，这5个研究都是一级预防研究。平均血管风险 ≥20%的研究，大部分患者都有血管病史，或者是透析患者。应用他汀类药物， 5年血管疾病低危（&lt;10%）和较高危（≥10%）的血管疾病显著降低。LDL-C每降低1mmol/L, 5组受试者的血管疾病风险分别降低了38%,31%,21%,19%和21%。显示了他汀类药物降低了血管疾病风险较低的人群的血管疾病风险。在这27项研究中，他汀类药物每降低LDL-C 1mmol/L，血管不良事件降低21%（P&lt;0.001），其中血管疾病引起的死亡降低了12%。应用他汀类药物使低危的两组主要冠脉事件降低了43%和39%，冠脉血运重建降低了48%和37%，和较高血管风险的受试者相比，冠脉事件和冠脉血运重建降低的趋势没有差异。低血管风险的两组卒中风险降低了24%。在低血管风险的两组中，排除了既往有血管疾病的受试者，血管疾病风险仍然有较大的降低（两组低危患者分别为39%和34%）。并且研究提示他汀类药物没有增加了癌症的发生和癌症患者的死亡。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">既往有研究证实，在没有血管病史的受试者中，应用他汀类药物降低LDL-C，能降低血管疾病风险20%。但是，他汀类药物在一级预防中的作用，还不是很清楚。一级预防中的受试者，有较低的血管发病风险，有必要对这些受试者的血管疾病风险获益进行评价。本研究提示，他汀类药物降低LDL-C,使血管风险低危的人群获益巨大，这可能会促使降脂指南的修改。</span><br />
