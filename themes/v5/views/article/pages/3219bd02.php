			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>安全用耳 保护听力——耳鼻咽喉科“爱耳日”义诊活动</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3月3日是第十六次全国“爱耳日”，本次的宣传教育活动主题为“安全用耳、保护听力”，旨在大力宣传非职业性噪声对听力健康的损害，提高全社会安全用耳，保护听力的意识。为了配合爱耳日的宣传教育活动，我院耳鼻喉科于年3月2日下午举行义诊活动，吴佩娜主任带领耳科团队参加义诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 此次活动得到了广大耳聋患友的热烈响应，短短的两个小时就有将近200名患友到现场进行咨询和问诊。耳鼻喉科专家们耐心的为每一位耳聋患友进行问诊和体检，并且给出专业的诊疗意见。通过“爱耳日”义诊活动，可以增强人们的爱耳护耳意识，普及听力保健的科普知识，以及推动耳聋疾病的防治工作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 我国现有听力残疾人将近3000万，而且每年新增的听力残疾人超过30万，是世界上听力残疾人最多的国家。近年来，随着手机、平板电脑等电子产品的普及以及KTV、酒吧等娱乐场所的增多，人们发生噪声性听力损伤的风险日益加大，噪声也成为导致听力残疾的重要原因之一。突发性耳聋是导致听力残疾的另一重要因素，主要与病毒感染和内耳供血障碍有关，尤其在连续熬夜或过度疲劳时容易发病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 听力残疾的关键在于预防，平时应注意保护听力，改变不良的生活习惯。一旦出现耳聋、耳鸣、耳胀、耳闷等症状应及时就诊，早期诊断，早期干预，尤其是急性的感音神经性耳聋患者，起病后一周内是最佳的治疗时间，一旦超过3个月再就诊，受损的听力很难恢复了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 我院耳科的诊疗水平近年来得到迅速发展，在原有中耳听力重建手术的基础上，还开展了人工耳蜗植入术和振动声桥植入术，实现了传导性耳聋、感音神经性耳聋和混合性耳聋听觉重建手术的全覆盖，对于保守治疗无效的听力残疾患者，可通过手术治疗来提高患者的听力，帮助他们重返美妙的有声世界。</span><br />
