			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海瑞金医院完成颅内外沟通巨大肿瘤切除手术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中新网上海4月12日电 (记者 陈静)记者今日获悉，上海瑞金医院攻克“医学难题”，成功为一位血友病患者成功实施了颅内外沟通巨大肿瘤切除手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，因为血友病病人本身缺少凝血因子，因此，为血友病患者实施手术难度要远远高于一般手术，向来是“医学难题”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今年55岁的许先生半年前发现自己右顶枕部头皮隆起，在随后数月中，肿块生长异常迅速，并出现剧烈头痛和呕吐症状。经诊断发现许先生患有巨大的颅内外沟通肿瘤，肿瘤最大直径约10cm，并向内严重侵蚀颅骨，突破脑膜，压迫脑组织。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">他在过去几个月的时间里走访了内地多家医院，但都无功而返。原来许先生生于一个血友病家庭，是一名凝血因子Ⅷ缺乏的血友病患者，自出生起就反复出现皮肤、肌肉、关节、牙龈出血。每次发作时都靠输注血制品止血。由于血友病患者手术风险大，难度高，多家医院都婉拒为他实施开颅手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最后，许先生辗转找到了上海瑞金医院神经外科主任赵卫国教授。术前赵卫国教授认真分析、制定了周密的手术方案。由于血友病患者手术需要多学科的合作，他又协调血液科，检验科和麻醉科商讨制定治疗方案。在精心准备，计算出围手术期所需要的凝血VIII因子剂量，并安排好用药时间后，手术如期进行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经过4个多小时的奋战，赵卫国教授带领手术团队顺利为许先生完成了左枕巨大颅内外沟通肿瘤切除手术，同时完成了一期颅骨重建修补手术，并取得了完全的成功。</span><br />
