			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>节后别乱给肠子“洗澡”</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 吃喝是节日不变的主题，但往往是饱了口福，累了胃肠。因此假期一过，不少人就开始了“清肠”计划。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2月9日、10日，《生命时报》记者从上海市杨浦区长阳路的几家药店了解到，清肠类保健品最近成了“销售主角”。“仅初六上午，某品牌肠清茶就卖了30多包。”药店工作人员介绍。一位正在买肠清茶的市民李女士说，过年吃得太油腻了，现在开始不吃饭光喝水，再喝些清肠的东西，给内脏“扫扫卫生”。记者发现，这样想的人还不少。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但上海交通大学医学院附属仁济医院消化内科主任医师戈之铮明确表示，不提倡健康人清肠。“清肠在临床上有明确的人群限制。如要做肠镜检查以及接受肠道腹部手术的人，才需要清，以避免影响检查结果或造成细菌感染。”戈之铮说，清肠即要排便，但排便是一个渐进的过程，讲究“先来后到”，代谢早、积累时间长的垃圾会先排出体外。因此，只要每天能正常排便，就不存在垃圾在肠道内长时间停留的问题。同时，有害和有益细菌在肠道内和平共处，达到一个平衡状态。若没有这些细菌，消化、分解就无法进行。过度清肠，反而会打破平衡，引起很多消化道疾患。“因此，无论是节食、喝水还是喝清肠类保健品，都不科学。”戈之铮说。短时间内饮水过量，血浓度会不断稀释，全身细胞的氧交换也会受限，脑细胞一旦缺氧，大脑反应就会迟钝。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，中国中医科学院广安门医院消化内科副主任医师周斌表示，清肠类保健品往往是苦寒之物，长期食用会进一步刺激肠道，降低肠道蠕动功能，加剧便秘，引发口臭，导致贫血等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">戈之铮提醒，要想给胃肠道减负，应该从调节生活习惯入手，多运动、饮食清淡。“饮食清淡要根据个体情况而定，首先应保证能量供应，不主张过分节食。如有一两天，喝点青菜白粥，刮刮油水。但如果工作强度大、耗费体力，就不太适合了，建议这类人可以多吃蔬菜水果、少吃肉。”</span><br />
