		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>左甲状腺素，优甲乐，雷替斯服药注意事项</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1、左甲状腺素片：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;目前市面上仅有两种，“优甲乐”和“雷替斯”；英文名：Letrox；两种化学成分相同（L-T4），制作上稍有不同，可根据购买的便利，选择一种服用。价格在30-40元一盒，约50天剂量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">目前市面上有50ug及100ug两种规格，购买时须注意，一般选用50ug便于调整剂量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">国产的药物也有，但其药物浓度不好控制，暂不推荐购买。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">经常有患者询问，主任，我切的是右边的甲状腺，您怎么给我开左甲状腺素，看完这里应该理解了吧。很多药物都有左旋和右旋，大多数左旋的有效，右旋的无效，当然就没有右旋的了。(^_^)</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2、那些人需要服用左甲状腺素片：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;1) 甲状腺乳头状癌、滤泡状癌患者必须服用，以预防肿瘤复发；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;2) 甲状腺全切除术后必须服用，以补充甲状腺素功能；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;3) 甲状腺部分切除术后，如甲状腺功能不足，需要补充；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;4) 一侧结节性甲状腺肿术后，建议补充，以减缓对侧结节形成；</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3、要服用多久，需要如何复查：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;大多数需要终生服药，但根据不同的原因，必须抽血复查甲状腺功能水平。推荐术后2周、2月、6月、1年随诊，以后每年随诊1次，共5年；此后可每2～3年随诊1次。正常甲状腺激素水平随季节有所变化，建议冬夏交替后调整。妊娠期甲状腺素需要量会大量增加，怀孕期间需要每个月复查甲状腺功能，逐渐增加剂量。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4、友情提醒：甲状腺肿瘤出现复发或者远处转移，多数患者是因为存在较长时间的停药，他们对自己的病情及服药重要性并不了解。因此为了患者的长期健康着想，我们不能对患者本人隐瞒甲状腺癌的任何病情。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5、如何服药，忘记服药怎么办：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 服药时间建议在清晨，餐前30min，这时候吸收效果最好。当然在其他时间服用亦可，最好养成定时服药的习惯，在每天的同一时间服用。如果清晨漏服，可以在其他时间补充，甚至可以在次日翻倍服用。停了太多天的话，请咨询医生。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">6、药物有何不良反应：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 过量服药：可能出现下列甲状腺功能亢进的临床症状，包括：心动过速、心悸、心律不齐、心绞痛、头痛、肌肉无力和痉挛、潮红、发热、呕吐、月经紊乱、震颤、坐立不安、失眠、多汗、体重下降和腹泻。在上述情况下停药1-3天后症状消失；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;长期服用：可能发生骨质疏松，建议配合服用钙剂，如钙尔奇D 0.6g 每日一次；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;药物不足：面部水肿，表情淡漠，体重增加；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;如发生以上情况，请及时就诊，建议复查甲状腺功能后调整药量。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">7、饮食上有无禁忌：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 医学上，甲状腺癌术后饮食没有禁忌；如后续需要碘131治疗，在治疗前2周需要禁碘饮食（禁止任何海产品，海带，紫菜等）；碘131治疗前后细节问题，需另外咨询，近期会逐步更新。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 如当地有自己的风俗习惯，可以按风俗进行；</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;我们治疗组推荐少服海带，紫菜这些含碘高的食物。</span>
</p>
