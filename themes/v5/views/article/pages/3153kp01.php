			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>谈谈蛛网膜囊肿</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今天我来聊聊蛛网膜囊肿。我在门诊经常遇到非常焦急的家长带着孩子来就诊：医生医生，我孩子的脑子里发现一个东西！仔细一阅读片子，原来是个蛛网膜囊肿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">蛛网膜囊肿在人群中的发生率并不低，约为0.1~0.7%。 儿童更多见，因为约75%的蛛网膜囊肿是在儿童阶段被发现。而且男孩的发生率高于女孩。多数的蛛网膜囊肿没有症状，只是在头部外伤后进行检查时偶尔被发现。所以多数来看蛛网膜囊肿的是调皮的男孩，尽管父母很焦急，但孩子看起来很健康。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前认为大多数的蛛网膜囊肿是在胎儿发育过程中形成。但也有少数囊肿和婴儿期头部外伤有关。我们目前对蛛网膜的自然过程还了解不多：多数蛛网膜囊肿可以长时间处于静止状态，既不变大，也不缩小；少数可以进行性增大，压迫脑组织产生症状；极少数可以自然缩小甚至消退。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">蛛网膜囊肿最常见的部位位于颞叶，约占一半。但孩子中，位于鞍区和小脑部位的囊肿明显多于成人。下面我们就谈谈位于颞部蛛网膜囊肿的症状。前面讲了多数蛛网膜囊肿没有症状，也不影响孩子的运动和智能发育，所以这些孩子一般都聪明可爱。但细细观察可以发现有些孩子的一侧颞骨有隆起。一般来讲隆起越高，囊肿越大，囊肿的局部压力也越大。颞部蛛网膜囊肿典型的症状是囊肿一侧的单侧头痛，以颞或眶部的疼痛最为典型。但因头痛不是很严重，所以年龄小的孩子很少有头痛的描述，一般8~9岁的孩子才有比较确切的此类头痛描述。约25%的颞部蛛网膜囊肿可以出现癫痫，可能和局部脑组织受压有关。应该值得注意的是，蛛网膜囊肿可以在轻微外伤情况下破裂，继而出现急性或慢性颅内出血。婴幼儿的巨大囊肿还可以导致头颅明显增大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下面来谈谈大家最为关心的治疗。目前对于蛛网膜囊肿的治疗尚有争议，尤其是是否需要治疗和采取哪种方法治疗。有位叫Galassi的学者将颞部蛛网膜囊肿分成I、II、III三种类型。I型和没有症状的II囊肿不需要治疗，可以每年复查头颅核磁共振1~2次。对于婴幼儿还需要监测头围。如果孩子依然没有症状、复查囊肿没有变化、孩子的头围也没有进行性增大，则继续随访复查。对于有症状的II型(局部颞骨明显隆起、头痛、癫痫、头围进行性增大)和III型囊肿则可考虑手术治疗。需要注意的是蛛网膜囊肿是良性病变，其病程相当缓慢，除非破裂，一般不会对孩子的生命构成威胁。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前对于蛛网膜囊肿的手术方式有3种：囊肿腹腔分流术、囊肿切除加造瘘术、神经内镜下囊肿切除加造瘘术。囊肿腹腔分流术是最有效的一种手术方法，其手术操作简单、创伤小，多数情况下是蛛网膜囊肿最常用的手术方式。但缺点是需要植入分流管，分流管往往可以在相当长的一段时间内产生多种不良合并症，堵塞、感染等等。如果选择了不合适的分流管，还可以导致小脑室综合症。囊肿切除加造瘘术，无论是采用显微手术还是神经内镜手术，优点是手术有效、不需要使用分流管，可以避免分流管的种种问题。但缺点是这二种手术的操作比较复杂、手术创伤相对也大、手术中发生意外的可能性也高。而且如果手术失败，仍然需要采用囊肿腹腔分流术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们科室这3种手术方式都采用，但更倾向于采用囊肿切除加造瘘术。尤其是我们不仅擅长神经内镜手术，还擅长锁孔手术。相对传统开瓣的显微手术，锁孔具有手术创伤小、手术后恢复快的优点。</span><br />
