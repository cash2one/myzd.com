		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>先天性小耳畸形与全耳再造手术（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">先天性小耳畸形的原因比较复杂，至今仍然不清楚，可能与父母近亲婚配，或者母亲在怀孕早期（2-3个月左右）受到药物、射线、有害气体等等的影响或病毒感染有关。主要的表现就是一侧或两侧的耳廓发育畸形，没有耳道口或者耳道狭窄，同时伴有该侧的听力低下。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">双侧耳畸形听力低下会严重影响孩子的言语发育，出现咬字不清的情况。孩子无法与家人和朋友正常交流，不能进入普通学校学习。这对孩子的身心发育都会有很大影响。所以如果两个耳朵听力都差，那就要尽早手术。一般在孩子六、七岁时手术，至少提高一侧听力，让孩子能够正常上学。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">单侧听力差会影响孩子形成双耳立体听觉，使言语辨别能力减低。而且头面部的明显不对称也会在孩子的心里造成很大的压力，引起自卑以至自闭心理的形成。所以我们建议，单侧外中耳畸形的孩子，在保护面神经、脑膜、颞颌关节等重要结构的前提下，尽量行听力重建，以形成双耳立体听觉。但是单侧外中耳畸形的孩子我们一般建议等到十五岁左右手术。一方面，再造的耳道再狭窄的可能性小一些。另一方面，孩子的肋软骨发育比较充分，对手术的承受力也强一些。不过手术也不能太晚，因为成年之后，肋软骨就会发生骨化，影响耳廓支架的形态。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">手术分两期，中间间隔至少三个月，半年至一年比较理想，最好不要超过两年，否则软骨可能发生液化吸收。由于大部分患儿都处于青春期皮脂分泌旺盛和活泼好动的年龄阶段，在整个两期手术过程中，孩子和家长都要特别注意避免挤压碰撞，以及耳部粉刺感染。如果有耳廓红肿、流脓、疼痛甚至表面皮肤破溃等问题发生，就要随时复查，避免出现更加严重的后果。</span>
</p>
