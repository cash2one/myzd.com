		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>浅析冻结步态(科普)</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“冻结步态”是怎样的一种体验呢？形象一点描述冻结步态是这个样子：突然出现的双足似是被一块巨大的隐形磁铁吸引故而像是被粘在或钉进地板一样，无法挪步。由于此时躯干前倾，双足“冻结”，极易跌倒。用学术些的言语来描述起来就是：发作性的向前有效迈步不能，多见于步态启动及转身时。</span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">冻结步态是我们大多数人从未感受过的，但却是帕金森患者常见的一种临床症状，并且是其跌倒和致残的重要原因，因此及时的发现与诊治十分重要。冻结步态较少见于帕金森病的早期，如若病人以冻结步态为首发症状，首先需考虑非典型帕金森的可能。此外，“冻结”并非仅局限于步态，还可出现于做交替重复动作的手指以及讲演发言时的言语。</span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">目前尚无有关冻结步态治疗的指南，现根据专家共识总结如下。冻结步态治疗的第一步是判断其是否对患者的生活造成严重的影响。严重影响通常被定义为严重干扰了患者的行动力及生活质量，比如频繁的跌倒发作。轻度的冻结步态患者，日常的生活及行走功能尚可代偿。鉴于冻结步态的阵发性出现特点，无论轻型还是重型患者，均需知晓其所致跌倒的风险、多种诱发因素及可能的预防措施。其预防手段主要包括迈步前先将重心移至对侧腿上、转身时增大转弯半径、在宽敞的环境中工作生活、保持情绪稳定、行走时专注于步态等。</span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">轻型患者首选物理治疗，比如有意识的增加步幅、保持规整的行走节律，均可减少“冻结”的发作。鼓励患者适度运动，尤其推荐骑行，因骑车时病人罕有“冻结”发作。药物治疗方面推荐选用单胺氧化酶B抑制剂（如雷沙吉兰、司来吉兰），多项临床研究表明其可减少冻结步态的发生。</span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">重型患者的治疗方案较复杂，需运动障碍专科医师（有时是康复治疗师）做详细的评判。治疗总体上包括三方面：药物及手术治疗、非药物疗法及合并症的治疗。根据对多巴胺的不同反应，冻结步态可被分为三类：多巴胺反应型（中枢神经系统缺少多巴胺所致）、多巴胺诱导型（由于摄入的多巴胺能药物所致）和多巴胺抵抗型（由非多巴胺能的脑部组织损伤所致）。多巴胺反应型是最常见的类型，其治疗方法包括增加左旋多巴的用量、缩短多巴胺能药物的给药间隔、减少多巴胺受体激动剂的用量（受体激动剂会增加冻结步态的发作）、加用金刚烷胺及适时手术，但各种治疗方式的选择及使用均有严格指征，需接受专科医生的评估。多巴胺诱导型及抵抗型均较少见，诱导型的治疗原则是减少多巴胺能药物的使用，抵抗型的治疗方法目前尚不确切，有报道提示联合应用屈昔多巴及恩他卡朋有效。非药物疗法主要是物理治疗，与轻型患者的物理疗法类似，但推荐更专业的职业治疗师指导并规划患者的各项生活活动细节。合并症的治疗包括多方面，诸如认知、焦虑、抑郁及体位性低血压等，及时的诊治焦虑抑郁被认为能有效的减少冻结步态的发作。</span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp; &nbsp;本文旨在能让读者对“冻结步态”有初步的感知，所谓“知己知彼，百战不殆”，希望通过医生及患者的共同努力，我们将来能够让帕金森症患者的步态不再冻结。</span>
</p>
