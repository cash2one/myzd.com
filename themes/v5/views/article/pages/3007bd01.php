			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>结肠癌肝脏转移患者的新希望</strong></span>
	</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 山西65岁的孟姓女患者 ，7个月前被当地医院诊断为“乙状结肠癌伴肝脏多发转移”，辗转多家医院后，均因肝脏转移瘤过多被判无法手术。经人介绍，来北医三院就诊。但由于患者左半肝体积过小，传统的手术方式因造成术后残肝体积不足而被否定；而且患者接受过6个周期的化疗，若放弃手术继续化疗，患者会因化疗不敏感，导致肿瘤进展而亡。北医三院普外科结肠癌肝转移的多学科（MDT）团队反复讨论，决定为患者实施“全腹腔镜ALPPS联合结肠癌根治术”。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3月13日，由普外科主任修典荣教授带领的医疗团队为患者实施腹腔镜手术。该手术是先切除左肝肿瘤局部、保留右肝，并通过结扎门静脉右支、劈离左右肝加大对左剩肝的营养供给，促使左肝体积快速增大,为二次手术创造条件。术后7天检查发现,左半肝明显增大、肝功能良好。3月23日，再次为患者第二次手术，在腹腔镜下切除右半肝和结肠癌。术后第二天患者返回普通病房。术后第4天拔除胃肠减压，并进食。复查肝功能、胆红素等正常。目前患者复查，恢复良好。</span>
</p>
<p>
