<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>与美国相比，中国的患者应该觉得很幸福</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3055/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨天，医脉通发布了一篇很有意思的文章，讲的是微博一位网友晒出亲属在美国做完手术后收到的账单后，引起了在外同胞集体吐槽国内医疗，有些评论令人捧腹，说的却是实情。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不得不说，“看病难”是近年一个炙手可热的词汇，一个“难”字道出了百姓对医疗现状的不满。从现实的情况看，的确有很多需要改善的地方。很多先富起来的中国人，纷纷将目光瞄准了国内提供高端服务的医疗机构，更有甚者，远渡重洋到海外就医。存在即合理，这些机构的存在为我们提供了一个多元化的选择，但是他们毕竟不是医疗市场的主体。对于普通的百姓而言，国外的发达国家是否也存在与我们类似的看病难问题呢？让我们以美国为例，看看中美百姓就医的差异吧。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、关于医保</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">去过美国的人都知道，下飞机必须做的第一件事情就是买保险。美国绝对不是福利国家，昂贵的美国医疗服务绝大多数是由保险公司进行支付的，如果你没有医疗保险，一个感冒花掉你上万美金绝对不是耸人听闻。可见如果没有医疗保险作为支撑，一个中产家庭就可能因病返贫。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由美国政府担负的医保主要覆盖四类人群：军人、退伍兵、老人和穷人，在奥巴马总统的医改之前，大多数工薪族的保险是由所在职的企业进行支付的，而且这个市场被美国四个盈利性保险公司所垄断。此外还有约4800万人没有医保，2014年美国医保在扩大覆盖的要求下，通过发起HealthCare保险项目，使得700万美国人才得以受惠。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于我们改革开放仅30多年的中国而言，虽然迄今为止关于医疗改革的呼声仍不绝于耳，但是在中国13亿人口的巨大基数下仍然实现了全民医保的基本覆盖，特别是基层县乡和广大农村地区的医保覆盖，这样的巨大成就举世罕有。今天走进医院就诊的绝大多数患者均有基本医疗保险，由国家主体进行负担的基本医疗保险制度，与西方国家高度发达的商业保险制度形成了鲜明对比。国人可在基本医疗保险的支持下，实现基本的特别是重大疾病的支付能力。而且百姓支付的保险金在可支配收入中的占比非常少。除了基本医疗保险托底，我们还可以通过购买商业保险进行灵活补充。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">美国此次医改的目标，与我们的医保思路又高度一致，“低成本、广覆盖”，由于美国的严重债务，预算委员会主席莱恩提出到2024年消减赤字减少医疗预算5万亿美元的法案获众议院通过，奥巴马的此次医改到底能否圆满开花结果，多方人士持观望态度。相比之下，不知有多少美国人羡煞中国的医保。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、家庭医生制度与分级诊疗</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">与保险制度紧密联系在一起的，是家庭医生制度。以美国南加州为例，医疗保险主要有两大类别：</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">PPO（Preferred provider organizations）和MHO（Health maintenance organizations）。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">PPO由保险公司提供一个医生名单，只要被保险人在此名单范围内均可选择医生就诊，但不能保证你选择的医生必须对你进行收治；</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">MHO指保险公司与医生团体结成联盟，由一个医生团体为被保险人提供服务，需要选择一个医生作为被保险人的主要医生，俗称“家庭医生”，如果出现需要手术情况，需要由家庭医生开具转诊证明或介绍信，才可以到医院进行进一步接受专家的治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在美国，医生可以凭借行医执照开设自己的门诊，虽然他们常归属一个医院管理，但远不像我国的严格人事管理制度，医院在很多情况下对医生仅是一个行医的场所。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我国的社区医疗体系在近几年得到了迅猛发展，在一些城市，基层医疗甚至覆盖到了社区。头疼脑热等小毛病和一些慢性疾病的管理，我们不用到大医院即可接受社区医生的治疗。虽然我国的医疗改革一直希望民众能在社区实现常见病的治疗，但是受制于我国全科医生培养的缺陷，百姓的就医仍然倾向于到大型的综合或者专科医院寻求专家的治疗。而我们常说的看病难，一般体现在这些顶尖人才和设备聚集的大型医院。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、就诊前如何选择医生</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于如何了解一位医生的医术，特别是罹患急重症时，这在任何国家都是非常重要的。有幸的是，我们身处信息化高度发达的世界，了解一个医生的教育背景、从业经验、患者的治疗效果等已经具备很多信息通路。这点中美并无差异。唯一需要注意的是对于肿瘤等方面的专家，手术成功率不能作为衡量医生专业水平的标志，因为一些好的医生接受高风险手术的几率要高很多。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不同的地方是，在美国选择医生，更倾向于听取家庭医生的建议，在行业里推荐专家；在我国，医生不具备独立行医资格，再加上专家的信誉往往由其就职的单位背书，国内的患者往往优先选择品牌医院，继而选择医生，而不像美国直接确定到专家个人。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、预约与挂号</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在美国看病必须要进行预约。一旦你确定了预约时间，必须准时到达，不能迟到。如果有事耽搁，必须提前一天打电话取消或重新预约，否则将被罚款。这一方面保证了医生的执业效率，另一方面也保证了整洁的就诊环境。但是对患者而言，可能会面临就诊前的长时间等候。据一项调查显示，美国人平均等候的时间根据疾病的不同，需等候几天至几十天。这就是我们为什么会常常看到美剧中感冒患者的形象：躺在自家床上、喝这热水、裹着毛毯。一方面，因为预约制度，等轮到你看病时，你的感冒也快好了；另一方面，即便你去医院，也很少机会会取得抗生素和输液的特权，在美国“买抗生素比买枪难”（我国抗生素滥用的问题我们不在本文展开讨论）。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我国施行就医挂号制度，由于没有强制的分级诊疗制度，患者想去哪家医院就医，基本是可以抬脚就走。 “抬脚就走”造成了百姓一拥而上到大城市大医院找专家看病的现状，一号难求应运而生，挂号难、住院难成了百姓对我国医疗吐槽的热点词汇。其实，从全国所有医院的范围看，除了这些三甲医院挂号难的问题尤为突出外，很多地区的二级甚至三级医院，挂号没有这么困难。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">即便如此，为改善医疗优质资源过度集中的现象，医生多点执业、打造医联体等举措皆是试图破冰看病难而推出的举措，虽然现实中还有种种的制约，短期内难以改变大医院人潮汹涌的现状，但是毕竟给出了一个可行的路径。我们也期盼着会在越来越多的基层医院看到专家的身影。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">五、检查项目多吗？</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">美国的患者在住院前后，一系列的医疗检查会让患者不堪重负，包括时间和经济支出。美国的法律施行“举证倒置”，如果医生出现误诊，不仅会吃上官司，还会要求出示检查证据以证实自己没有误诊。医生会为保护自己实施“防御性医疗”，进行大量检查。举例来讲，一个腹痛的病人可能会是胃炎、胰腺癌、心脏病造成的，即使有90%的把握可以判断是胃炎，但医生任然会让患者做胃镜、钡餐透视、心电图等检查，以避免可能的法律纠纷。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除此之外，美国众多的临床检查项目，不仅是医生主动防御的行为结果，还是盈利性保险公司、医疗机构、诊断试剂和器械厂家共同逐利的结果。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我国也同样存在检查项目多的问题，这是群众反映看病贵的另一个构成原因。我们的公立医院，由于一些众所周知的原因，是一个半公益半市场化的产物。无论是医院为生存发展，还是由于医患关系紧张进行的防御性医疗等原因，都在一定程度上造成了与美国一样的现象。 如何解决这个问题，中、美两国医疗机构市场化的主体地位差异，解决方案本应不同。但现在微妙的是，板子都打在了医院身上，医患双方为此付出了巨大的代价。试问观众，你看过家长生病，让孩子替你吃药的情况吗？</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">六、住院时长</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术后康复与住院，对于肿瘤等重大疾病，每天动辄一万美金的住院治疗费用，医保也不堪重负，美国医疗保险对于手术的患者最多报销5天的住院费用。所以，加上住院前的检查2~3天，能在医院住满10天的患者，已经是医院和普通中产家庭可以忍受的最大极限了。这对于需要术后康复，特别是放化疗的患者而言，必将在术后失去与主治大夫密切接触的机会。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我国在这方面则要人性化很多，只要主治大夫许可，你可以一直住到完全康复为止。保险报销不是问题。当然，大型的医院为发挥投资效率，在绩效指标上会以床位周转率来对科室进行考核，一般会要求患者术后尽早出院；而普通的医院则不会做出这类要求。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">七、医疗费用</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">以神经外科手术为例，在美国平均的价格范围是10万~50万美金之间，看医生门诊费用是以小时计算（100~200美金/小时），这样的价格，看上去好华丽啊，但请注意，这不是美国医院的单方面定价，而是美国保险集团作为患者代理人与医院博弈的一个结果。如果没有这个博弈过程，医疗价格在美国会高到你不可想象。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我国大多数医院都是公立医院，医疗服务的价格受到国家的严格指导，其详细程度能具体到手术中的每块止血纱布。以神经外科手术为例，价格从3万~15万人民币，比起国外动辄几十万美金的医疗费用来说，我们在国外同行面前总是显得有些“羞答答”，更别提十几元的专家号了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可能有人会说，美国人赚的是美金，我赚的是人民币，绝对数字并不能代表相对购买力而言的物价。我给大家出示一组数字， 2010年美国家庭净资产的中位数为7.73万美元，而当年我国的数据则为6.62万美元。家庭净资产常用来表示一个家庭的财富累积，即家庭所有资产减去家庭负债的净值。到2014年，这两个数据已经非常接近。美国由于是负债型经济，以大家对美国人消费的经验而言，虽然消费很高，但其家庭资产并不充裕。如果再考虑货币购买力，我们一点也不逊色于美国。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">再来看看这些费用美国能报销多少，依据企业购买的保险不同，个人需要支付的范围弹性很大，从50%~10%不等。而我们国家基本医疗保险，个人仅需要支付20%。</span><br />
<br />
<strong><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">八、就诊的环境及舒适度</span></strong><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不可否认，美国的医疗机构，自从它成立的那天起，都把患者作为医疗活动行为的主体服务对象，一切的流程和服务皆以满足患者的需要为出发点。无论从平均1小时的门诊咨询时间还是细致周到的服务细节，均提现了患者作为消费者的尊严。不可否认，这是我们公立医院需要努力提高的一方面。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是，我们不要忽视这些服务背后的代价，提供一组数字供大家进行对比：</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">美国医生的门诊量和手术量，在一流大型医院中，一个科室的手术量约为2~3台/天，门诊量最多20~25名/天，平均门诊咨询时间1小时/患者，医生月收入3万美金。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中国医生的门诊量和手术量，根据医院级别的不同差异很大，在知名医院中，一个科室的手术量约为5~10台/天，门诊量约为30~90名/天，平均门诊咨询时间小于10分钟；在一般医院中，一个科室的手术量约为2~3台/天，门诊量约10~20名/天，平均门诊咨询时间15分钟。一线城市博士毕业从业5年，工资加奖金6000元/月。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">看完这些对比，相信很多人会觉得自己作为中国医疗制度下的一名普通百姓，比美国人都要幸福。当我们幸福并发着牢骚着的时候，我们将有形无形中推动着已经有10年医改继续走向深入。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本文虽然选择了世界第一强国美国作为类比对象，但它是所有西方发达国家唯一还没有实现全民健康保险和国家卫生服务制度的国家，在全球整体经济下行的情况下除美国的医改结局难以预料外，还使得希腊等国破产，并在欧盟各国内部引起了大规模的预算消减和对医药生产企业、保险公司的严格管理。我们的未来会如何？笔者给予乐观的期待。</span>
</p>
