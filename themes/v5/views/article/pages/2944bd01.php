<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>点点滴滴民生情&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">郭华平 黄晓平 本报记者 鄢朝晖</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">喝上干净水、创业有门路、服务心贴心……近年来，定南县把民生摆上重中之重的位置，每年民生支出占财政收入的70%以上。点点滴滴的民生小事，体现了该县关爱群众的大情怀。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">饮水工程让山民喝上干净水</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“要不是政府出钱给我们铺好水管，引来干净的自来水，我们现在还要到村口的水塘里挑水喝。”家住定南县老城镇上洲村上屋组村民黄文河感激地说。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原来，上屋组地处偏僻，只有9户人家，祖祖辈辈只能从一口水塘取水，人畜共用。喝上干净的自来水，一直是他们的梦想。今年6月份，镇政府和驻村工作队了解实际情况后，筹集10多万元资金，把自来水引到各家各户。&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，该县把民生工程实实在在地办成民心工程。目前，该县农村饮水安全工程已全面完成，投资3156万元实施了县城管网延伸至天九镇、鹅公镇集中供水工程、县城管网延伸至老城镇等7项农饮工程，让6.65万名群众喝上了干净水。同时，县自来水厂新建万吨制水流程，安装了双回路专用电缆和自动发电机组，解决了城乡居民的饮水安全问题。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多轮驱动广铺就业创业路</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，该县始终把促进就业创业摆在民生工作的突出位置，坚持多点支撑、多轮驱动，不断扶持各类创业群体做大做强，努力开发就业岗位，实现以创业带动就业的倍增效应。出台系列创业就业实施方案和指导意见，针对不同创业创新群体的需求提供差异化的政策支持，全方位为创业者在政策、资金、场地、税务方面提供便利。同时，今年又新增小额担保贷款300万元，扩大贷款规模7000多万元。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该县采取积极的扶持政策，精准发力，为创业者提供贷款扶持、创业培训、社保补贴等保姆式创业服务，着力推动网络创业、电子商务、产品批发以及物流等创业群体的发展。同时，专门设立“大学毕业生就业创业服务窗口”，免费为大学毕业生提供政策咨询、实名登记、职业介绍等全程就业创业服务。依托“电商协会”“创业者协会”，为创业人员提供“一对一”创业指导服务。据统计，该县今年新增创业人员5000多人，新增就业人数15325人。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">贴心便民服务获群众点赞</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该县积极探索基层便民利民服务网络体系，在社区建设日间照料中心、“一站式”服务大厅、图书阅览室、志愿者服务中心等服务机构，推动基本公共服务项目覆盖全体社区居民。随着各项民生工程的加快推进，越来越多的群众在生活中积攒出信心，酝酿成了幸福。&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，该县坚持把民生工作做到群众心坎上，从全县各单位中抽调精干力量组成便民服务队，分别进驻119个村、居委会、社区，帮助群众解决各种难题。便民服务队的队员们与群众同吃同住同劳动，与困难群众“结亲戚”，当好政策的宣传员、矛盾的调解员、信息的采集员、经济发展的服务员、群众办事的代理员，构建起“群众张嘴，干部跑腿”的为民服务氛围。今年以来，该县便民服务队共走访群众15924户，化解信访矛盾纠纷357件，落实民生帮扶资金5000多万元，办理服务事项25838件，慰问群众1300余人次，群众纷纷点赞。</span>
</p>
