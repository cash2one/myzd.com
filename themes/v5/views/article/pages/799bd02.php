			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>达芬奇机器人有了"小白鼠" 医生术前可以反复做"模</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/799/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“打印”出来的胰腺</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">用3D技术打印骨骼、牙齿，这技术很先进吧？不过，医学发展是很快的。上周末，浙江省人民医院联合中国科学院长春应用研究所举办了一场“生物医用高分子研究与转化医学研讨会”。“高大上”的东西全来了，如能溶解血栓的水凝胶、将肿瘤的诊断和治疗合二为一的纳米粒子、与自体几近一样的胰腺、可降解的人工输尿管支架管膀胱等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一块“打印”出来的胰腺</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王知非教授是我国第一位外科机器人博士后，也是最早在美国获得机器人主刀培训资格证书的中国医生。研讨会上，王教授代表大会主席、省人民医院黄东胜院长做了“关于3D打印技术如何帮助达芬奇机器人外科手术训练”的特邀报告。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">他从口袋里掏出“新宠”，一块连着“管子”的“肉”。“这是用特殊的3D打印材料根据1:1的比例，按照人体胰脏的原型打印出来的，淡红色，长约12厘米，厚约2.5厘米，重约80克。”在王知非教授的介绍下，记者用手摸了摸，这块打印出来的胰脏软软的、黏黏的，就好像真的脏器一般。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“你可以把这块仿真器官材料理解成达芬奇机器人的御用‘小白鼠’，也可以理解成外科医生在重大手术前的‘模拟考试’。”3个月前，在芝加哥的美国机器人外科年会上，王教授把它带去了现场，让在场的美国医学专家都啧啧称奇，表示要合作研发这项技术和产品。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">他说，如果有需要，可以打印其他组织，如肝、胆、肺，甚至整个腹腔，让医生先来练手。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高大上的“达芬奇”也有弱点</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">众所周知，达芬奇机器人是外科领域中的一项重大发明，放大15倍、高清晰、3D成像、360度旋转、不抖动、微创……但达芬奇机器人用的毕竟是机械手，它是没有触觉的，而外科医生在手术时往往依赖触觉。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那可不可以用机器“练手”呢？面对价值2000多万人民币的设备，即使是在已经有2000多台达芬奇机器人(几乎每三到四家医院就一台)的美国，也没有几个中心能负担得起专门买一台来供外科医生在动物身上训练。中国目前连内地的医生机器人手术操作准入的资格培训都要到香港进行。而且此外，鉴于达芬奇机器人手术的费用和医保政策，仅在复杂的手术时才会考虑使用机器人。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王知非教授举了个例子：肝胆胰外科最复杂的胰十二指肠切除手术，切除后将手术后的胰腺残端和消化道接起来的胰腺重建，一直是最具有挑战性的技术之一，腹腔镜的局限性导致其成为微创外科多年来最难的堡垒，虽然达芬奇机器人帮助突破了人手的极限，使这种超精细的操作难度大大降低，但是以最经典常用的胰肠导管对黏膜吻合为例，“胰腺组织很脆弱，胰管通常非常细，2-3毫米直径，和肠子的小孔吻合的时候，力度和或缝针运针的角度不合适，就很可能造成术后的胰瘘，这极有可能带来灾难性的后果。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“从患者角度来说，目前全中国医院引进的达芬奇机器人总共不过4249台，浙江省才3台，有达芬奇机器人手术资格的医生也很少，患者会担心自己是医生经手的头几例，谁都希望自己的主刀医生身经百战。如果说机器人这个手术的学习曲线(技术达到较成熟稳定前需要完成的病例数)是50例，任何一位患者都希望自己是51例。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如何使医生在做第1例患者的机器人手术时就像是在做第51例呢？王知非想出了“模拟”的办法。他将想法告诉了3D打印专家们，于是请他们根据患者的CT等影像数据，做出了这套柔软的、中间还带着细细胰管的硅胶胰腺。更有趣的是，还可以根据每位患者的情况进行个体化3D打印，如调整胰腺软硬、胰管粗细等，几乎能做到一模一样。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高分子其实很“亲民”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">高分子？听上去蛮高大上的，其实它很“亲民”。例如，我们戴的隐形眼镜、心脏支架、种植牙等，都用到高分子材料。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1949年，美国首先发表了医用高分子的论文，第一次介绍了利用聚甲基丙烯酸甲酯作为人的头盖骨、关节和股骨，利用聚酰胺纤维作为手术缝合线的临床应用情况。20世界70年代，生物医学高分子的研究在全球进入了蓬勃发展期。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“实际上，无论是用作外科手术缝合线、注射微胶囊，还是靶向纳米药物、人工骨骼、皮肤等，医用高分子产品的研究与开发对现代医学的发展、现代临床诊断及治疗技术的提升至关重要，也与我们的健康事业息息相关，无论是做外科手术用的缝合线及人工皮肤的胶原蛋白，还是医疗整容整形用的硅胶材料、注射用微胶囊、靶向性的纳米抗肿瘤药物，以及三维打印技术制造的骨骼、牙齿等，都与高分子技术有关，与我们的健康息息相关。”黄东胜院长说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现场，省人民医院副院长、泌尿外科专家张大宏教授研发的可降解人工输尿管、人工膀胱等专利，还与企业达成专利转让，推动产学研结合。</span><br />
