		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>促进血管腔内修复技术不断发展与完善</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3136/1.png" title="" alt="demo.jpg"/>
</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><<img src="<?php echo $baseUrlImage; ?>article/doctor/3136/2.png" title="" alt="demo.jpg"/>
</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">破裂的腹主动脉瘤的腔内治疗（上图为术前DSA，下图为术后DSA）</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">腹主动脉瘤（AAA）治疗方式的变革，是血管外科由巨创到微创发展的最好范例。自Parodi 1991年首次报道血管腔内修复术( endovascular aortic repair，EVAR)以来，EVAR发展迅速，其治疗的有效性和微创性也得到肯定。目前，该技术已基本成熟，其进展主要体现在支架型人工血管（stent-graft，SG）的改进、操作技术的提高、术后并发症处理手段的完善以及影像学评估技术的进步。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">支架型人工血管的不断发展</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着EVAR技术的发展，SG性能和功能不断改进。合金支架和超薄覆膜的结合方式进一步优化，SG的柔顺性不断提高，器材导入系统的进一步微创化和简洁化。通过压缩SG的体积，缩小导入器具的直径，无需手术显露股动脉甚至髂动脉，最大限度地实现微创。改进释放方式，使释放、定位和调整操作更加简单。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">针对近端锚定区解剖不良（瘤颈短、成角、口径过大等），或累及内脏动脉开口的患者，采取“原位开窗”技术或使用开窗型SG以保持脏器血供。前者技术还有待继续研究，后者已经有成型产品。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">开窗型SG治疗肾上、肾周型AAA，甚至Ⅳ型胸腹主动脉瘤都取得初步成功，可使近端锚定平面超过肾动脉、肠系膜上动脉，甚至腹腔干水平，极大地扩展了EVAR 的适应证。但我们也要认识到：开窗型SG对术前影像学的评估和测量要求很高；同时，对操作的技术要求也大大提高，操作者既要有EVAR的成熟经验，也要有肾动脉等腔内治疗的良好技术。这类SG的中远期并发症也应引起重视，譬如移位、支架断裂、内脏血管闭塞等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">髂内动脉的处理是EVAR远端锚定区的处理的关键内容。鉴于多数AAA患者的肠系膜下动脉已闭塞，有时还需人为栓塞，一般认为应至少保留一侧的髂内动脉。对于合并双侧髂总动脉瘤或一侧髂内动脉已闭塞，若封堵双侧髂内动脉可能导致严重的臀肌缺血甚至肠坏死，如何保留髂内动脉就显得尤为重要。目前，已可以通过髂动脉分支型SG或髂内动脉重建等方法实现。如上所述，EVAR的技术成功，要求在术前良好的影像学评估前提下，根据不同的近、远端锚定区选择合适的SG。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">手术步骤的改进</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">以往术中首先进行腹主动脉造影定位肾动脉、瘤体上缘、腹主动脉分叉和髂总动脉分叉，再交换超硬导丝，导入SG输送系统。实践中发现，超硬导丝和输送系统导入后纠正了瘤颈、瘤体和入路动脉的扭曲，解剖位置发生改变，需要重新造影定位，增加射线摄入、造影剂用量和手术时间，因而改为一侧导入超硬导丝，对侧导入测量导管，超硬导丝侧导入主体输送系统至L1 水平，测量导管造影定位，避免上述的重复造影定位，简化步骤，减少射线摄入和造影剂用量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">并发症的预防和处理</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着时间的推移，初期EVAR的患者中，也开始出现一些特殊并发症，如内漏、内张力、SG移位、瘤体破裂、SG断裂或破损、SG感染、臀部间歇性跛行以及主动脉肠瘘。这些并发症已得到重视，处理手段也日趋完善。在这些并发症中，内漏的预防和处理格外受到关注。由于Ⅰ、Ⅲ型内漏往往是导致术后AAA破裂的最大危险因素，一旦发现，应及时处理通过球囊扩张、释放cuff或金属裸支架等腔内手段来纠正。鉴于I 型内漏多是与器具有关，且很多是继发于SG移位。可使用开窗型SG，因其肾上锚定、倒刺的设计可有效降低移位发生。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">同时，研制预防内漏的SG也成为研究热点。2003年来，复旦大学血管外科研究所与相关单位合作，研发了防I型内漏的功能性SG和防II型内漏的药物涂层SG，都已获成功，并取得专利。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">EVAR适应证的拓宽</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">EVAR的适应证不断拓宽，例如AAA破裂（rAAA）、肾动脉周围AAA、假性动脉瘤、炎性动脉瘤以及感染性动脉瘤等都不断见诸报道。其中，rAAA的腔内治疗越来越引起重视。相对于破裂AAA开腹手术的高风险（死亡率32%~ 80%），rAAA行EVAR可以有效降低失血量、ICU 住院时间、早期并发症，总死亡率接近17％（0~40％）。多数学者认为，在rAAA的诊治中，选择合适患者行急诊EVAR是可行的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">20世纪90年代EVAR的出现，对于传统外科手术治疗的地位产生了挑战。如今，大量的临床报道和循证医学研究，如欧洲的EUROSTAR、英国的EVAR trail 1、荷兰的DREAM试验等多中心随机对照临床实验都证实了EVAR围手术期安全性高于传统开腹手术，能有效降低动脉瘤相关的死亡率。对于高危患者，多数学者也认为，EVAR是合理的治疗选择。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在肯定EVAR的可行性和良好的近期疗效的同时，上述实验的随访结果也指出：与开腹手术相比，EVAR术后并发症率和再干预率也是相对较高的。但也需认识到，多数EVAR的并发症还是可以通过微创的腔内技术来再次干预，而且，随着SG的不断改进、操作技术和围手术期处理的日趋成熟，相信EVAR的适应证会不断拓宽。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1997年该技术进入中国，并迅速成为中国血管外科的热点。十几年来，上海、北京和广州等一些率先开展EVAR的主要单位已经积累了大量的治疗经验，中国EVAR的病例数方面也在全球遥遥领先。我们期望：在新技术推广和普及的同时，为完善技术、改进器具并组织进行国内、亚洲甚至世界范围内的多中心性、前瞻性和对照性研究，建立腔内血管外科医师的培训制度和各地区的培训中心，制定EVAR治疗的建议性规范尽微薄之力，并最终促进EVAR不断发展和完善，使更多的患者从中受益。</span><br />
