		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>手术超出“止血带时间”风险大增</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2970/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3D技术可帮助缩短手术时间。Gettyimages供图</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 3D打印技术可提前把骨折处的结构制作成模型，让医生做好功课再上手术台</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 本报讯 （记者伍君仪 通讯员李冰洁）严重受伤后的复杂骨折需要做手术救治，而手术的长短有“止血带时间”一说，即每过1~1.5小时要给骨折的肢体松绑放血以免坏死，但随之而来的是出血量、药物用量直线上升，手术风险也随之增加。为了避免手术超过一个“止血带时间”，现在患者可以通过3D打印技术把自己骨折处的结构制作成模型，让医生做好功课再上手术台。3D技术有一定的花费，究竟值不值？骨科专家分析认为，新技术缩短了手术时间，可大大减少许多手术的“隐性成本”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在近日举行的第三期全国数字骨科技术应用学习班上，广州军区广州总医院骨科医院副院长兼创伤骨科主任章莹介绍，严重的骨盆髋臼骨折、关节内骨折碎骨很多，骨折移位大，常见于交通事故、高处坠落等高能量暴力撞击损伤，偶见于骨质疏松的老年人摔倒受伤。如此复杂的骨折，往往要做很长时间的手术，特别是结构复杂的膝关节和踝关节。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 章莹称，手术的时间越长，患者承受的风险就越大。医生往往争取在1~1.5小时的“止血带时间”内结束战斗—手术时要用止血带扎住骨折的肢体以减少出血，但扎得太久会造成肢体神经损伤和肌肉缺血坏死，因此要定期松开止血带，那意味着患者出血将大大增加。随着手术时间延长，患者感染风险也会上升，卫生部规定手术超过3小时要在手术中追加使用抗生素。此外，手术麻醉时间延长也令医疗费用增加。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 为了缩短手术时间，最近几年临床上开始应用数字骨科技术。章莹介绍说，在手术前给患者的骨折部位拍CT，然后用计算机辅助3D打印技术制作出一模一样的模型，让医生更直观地了解手术部位的解剖结构，设计手术方式，安排钢板和钢钉的放置，省得在手术中慢慢摸索。这不但令手术操作更精确安全，还可缩短近1/3的时间—这令不少复杂骨折也能在“一个止血带时间”内搞定。目前这种操作需要一定的费用，但章莹指出，缩短手术时间减少了抗生素和麻醉剂的使用，又减少了出血，整体上降低了很多手术的“隐性成本”，实际上很划算。</span>
</p>
