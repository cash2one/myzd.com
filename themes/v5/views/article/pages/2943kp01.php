<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>甲状腺微创外科手术</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">得益于手术仪器设备的更新和外科技术的发展，现代外科手术发展迅速。各种传统术式都发生了很大的变化。修复重建和微创手术正是目前外科发展的主要方向，这两种方向在头颈外科体现得尤为突出。这里，我们讲讲甲状腺的微创外科手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">甲状腺位于颈前下方，对于一般的甲状腺肿瘤，传统手术一般需要长约6cm的切口，而且为了术中显露充分，需要向上分离颈阔肌皮瓣至少达环状软骨水平，有一部分病人术后有相当一段时间皮瓣位置肿胀较为明显，虽然最终都可以消退，但是这3个月左右的肿胀时间给患者带来了一定的负面心理影响。特别对于年轻女性患者，这种影响更是不可忽视的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">超声刀的出现给甲状腺外科手术带来了革命性的改进。超声刀不像普通电刀，它是利用一定频率和功率的超声波震动，使组织蛋白凝固变性而断裂，它可以更好地止血，而且局部温度不高，属于典型的冷切割，有利于保护正常组织；对于直径小于3mm的血管完全可以直接凝固切割而不需要结扎。因此，采用超声刀进行甲状腺手术可以做到零结扎，避免线结导致的异物反应。采用超声刀结合佩戴式手术放大镜，我们已经把常规甲状腺手术的切口缩短至4-5cm，皮瓣的水肿时间和程度也大大缩短，但是仍然满足不了年轻女性患者的需求。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">内窥镜的出现和应用成了微创手术发展的契机，目前很多外科手术可以借助内窥镜良好的视野和放大成像作用，通过自然空腔或者人造空腔来进行腔内操作；外科医生则是眼手分离，眼睛盯着显示屏，手里掌握为内镜手术专门设计的器械通过狭小的腔隙来完成手术。由于切口变小（只需要一个能容纳器械通过的洞或者可以通过自然腔隙），视野变得更清晰，使得手术更加精细，更加安全可靠。腹腔手术、胸腔手术、盆腔手术、鼻腔副鼻窦手术绝大多数可以通过内窥镜完成。内窥镜加上超声刀的应用更使这种腔镜手术精细化、无血化，带来的自然是术中创伤的减少和术后恢复时间的缩短。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早在1995年，Miccoli便率先使用鼻窦内窥镜进行小切口下甲状腺叶切除术，他能在3-4cm的切口下完成常规甲状腺叶切除术，并且可以显露喉返神经甚至甲状旁腺，这种手术方式已被全世界甲状腺外科医师接受，并称之为Miccoli手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经过十几年的发展，目前外科医师结合内窥镜和超声刀，已经可以在2-3cm的颈部切口下完成常规甲状腺叶切除术。这种术式的基本原理是利用内窥镜的良好视野和放大作用，利用超声刀超强的血管凝闭作用，准确地在甲状腺包膜间隙分离操作，解剖喉返神经和甲状旁腺，凝闭甲状腺血管，使手术更加安全精确、零结扎。由于切口小，而且不需要过分地分离颈阔肌瓣，术后皮瓣水肿不明显；此外，由于超声刀良好的血管凝闭和组织固化作用，术后创面渗出少，多数切口不需要放置引流管或引流条，术后恢复更快。这是一种真正意义上的甲状腺微创外科手术。目前世界最高水平的肿瘤中心，包括Sloan Kettering癌症中心和M.D Anderson 癌症中心均采用这种手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在汕头大学医学院肿瘤医院头颈外科，自从购进了足量的超声刀后，上述改良的Miccoli手术已经成为合适病例的甲状腺叶切除手术的常规做法。取得良好的临床疗效。实践证实，改良Miccoli手术能很好的显露喉返神经和甲状旁腺，切口仅2-3cm，切口不需要放置引流，术后恢复快，颈部切口肿胀不明显，美容效果良好；手术并发症率甚至低于传统手术；对于年轻女性患者尤为适合。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，任何新技术都有它的适应症，并不是所有的甲状腺叶切除术均可以采用改良Miccoli手术。当肿瘤较大（直径超过5cm），或者怀疑恶性时，改良Miccoli手术并不安全；当颈部淋巴结有转移需要颈清扫时，常规甲状腺癌根治术才是最终答案。而颈短肥胖会增加手术难度，切口可能需要适当加大，但是仍然较传统手术小。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于改良Miccoli手术还是在颈部留下切口，对于某些特殊职业的患者（比如说演员、模特等），尚不能完全满足要求。因此，甲状腺外科医生还发展了其它进路的甲状腺手术，包括腋窝进路、胸乳进路等，利用高清内窥镜的良好视野和特殊手术器械，通过腋窝，或者乳晕加剑突旁小切口，经皮下疏松组织间隙分离注气制造腔隙，达到甲状腺位置进行甲状腺叶切除。严格讲，这种进路的甲状腺手术并不能称为微创手术，因为建立皮下隧道和甲状腺术区腔隙会造成额外的创伤，但是，它能完全消除颈部切口，对于特殊职业或者爱美的年轻女性患者还是有一定的吸引力的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于甲状腺肿瘤患者来讲，接受手术的目的是彻底解决肿瘤疾患，传统甲状腺手术是最为安全的；对于肿瘤小（至少小于5cm），良性病变的患者，选择改良Miccoli手术是真正的微创甲状腺手术，既安全又美容，手术费用也仅仅增加了内窥镜使用的费用大约1000元，在有条件的情况下应该作为首选。而对于颈部无疤的腋窝或者胸乳进路甲状腺手术，则适合于少量有特别需求的人群。</span>
</p>
