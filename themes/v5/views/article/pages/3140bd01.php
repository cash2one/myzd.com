		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海中山医院完成世界首例应用达芬奇机器人微创手术</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据了解，2010年9月，中山医院许剑民教授领衔的手术团队开展了第一例达芬奇机器人手术系统辅助乙状结肠癌根治术。截至2014年3月，中山医院普外科已经利用达芬奇机器人辅助完成了近300例结直肠手术，居全国之最。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯（记者陈青）3月5日，复旦大学附属中山医院普外科、肝外科和胸外科联合，成功完成了世界首例应用达芬奇机器人[2.89% 资金 研报]微创技术，在一个病人身上同时完成了直肠癌根治术、肝转移灶切除术和肺转移灶切除术。57岁的患者龚先生术后恢复良好，于昨天顺利出院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据文献检索，此类手术在国内外尚无报道。该例联合手术的成功，标志着中山医院外科的微创技术水平和结直肠癌多学科综合治疗水平已跻身国际先进水平行列。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">同时实施肠、肝、肺3处病灶微创切除术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">同时实施肠、肝、肺3处病灶的机器人微创切除术，在避免了患者等待的恐惧和3次手术打击的同时，减少了因仅切除原发灶后，肝肺转移灶迅速增大的风险。该手术采用了世界上最先进的达芬奇机器人系统，仅需胸部3个1.2厘米小孔、腹部6个1厘米大小的小孔以及1个5厘米大小的切口取出肝、肠肿瘤的标本就完成了所有手术。而按原来传统方法，胸部需一个20厘米大小的切口、右上腹切一个20厘米切口、左下腹切一个18厘米的切口。整个手术耗时9个小时，基本与开腹手术相当。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">最重要的是，患者原本需要3次住院和3次全麻下才能完成的手术，在一次手术全部完成，术后7天即可顺利出院，大大缩短了住院天数，有利于患者早期的活动和早期的后续综合治疗。按传统手术方法每次手术需住院7天，手术1-2周后才能进行下一步手术，3个手术起码需2个月以上。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">龚先生因大便次数增多、大便变细伴腹胀、腹部隐痛半月余，到中山医院就诊，肠镜检查显示肛缘14厘米可见一巨大肿块，菜花状，表面糜烂坏死，肿块堵塞肠腔，肠镜不能通过。肠镜病理诊断为腺癌，进一步的检查显示为直肠癌同时合并肝右叶转移，右肺部转移灶。入住中山医院后，经过结直肠癌多学科诊疗团队的诊断和讨论治疗方案后，由普外科副主任、结直肠癌外科专家许剑民教授和韦烨副主任医师，联合肝外科王晓颖副主任医师和胸外科范虹、蒋伟副主任医师，以及麻醉科李敏和护理部汪菊、董晓燕等协同配合下，运用当今最尖端的微创技术——达芬奇机器人，为其实施了直肠癌根治术并同时间切除了肝脏和肺部的转移病灶。患者术后第二天即下床活动并进食流体食物。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">达芬奇机器人可将三维立体视野放大15倍</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据介绍，该手术依靠达芬奇机器人手术系统可放大15倍的三维立体视野，以及灵活、精确的机器手臂（每根手术臂仅一根筷子粗细），实现了在肺、肝脏和盆底这些狭小空间内的细致解剖，从而达到了减少术中出血、保护盆底自主神经的效果。该手术达到了完全的微创效果和手术根治彻底性的完美结合，同时又解决了长期以来同期手术切除结直肠原发灶和转移灶会造成极大创伤应激的困惑。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中华外科学会副主任委员兼胃肠外科学组组长、上海市外科学会主任委员、复旦大学普外科研究所所长秦新裕教授介绍，随着医学科学的发展，医学诊疗模式也发生了重大变化，目前国际上倡导的“多学科综合治疗协作组”肿瘤诊治新模式，一改原先“各自为政”的格局，能够为每位患者“度身定制”最具个体化和最合理的综合治疗方案；而专科化、微创化、基因化及数字化已成为外科及肿瘤外科的诊疗四大趋势。除传统的开腹手术外，内镜、腔镜、机器人手术将成为越来越多外科医生手中的“武器”，为患者提供更多的治疗方案。</span><br />
