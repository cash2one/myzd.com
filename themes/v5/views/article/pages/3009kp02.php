	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">微侵袭神经外科是21世纪神经外科的发展方向，"以最小的治疗创伤获得最大的治疗效果"的微侵袭外科理念需要借助手术前中的神经电生理技术、脑功能成像技术以及立体定向、神经内窥镜和神经导航、锁孔手术技术等先进方法得以实现。在癫痫外科领域，应用微侵袭神经外科手段可以使手术切口及脑组织暴露范围缩小，脑功能区得到更好的保护。例如采用神经导航方法，不仅可以将影像上的结构性改变作为导航目标，准确切除病灶，目前已经可以将癫痫术前的脑功能成像检查如脑磁图、功能核磁和神经导航系统结合，手术中将功能成像定位的癫痫灶和脑的重要功能区在导航指引下在皮层上标记出来，癫痫灶定位更准确具体，功能区可以得到很好的保护。神经导航下的锁孔入路不仅能够处理皮层癫痫灶，也能顺利完成内侧颞叶癫痫的手术治疗。术中唤醒的脑功能区电刺激定位可以更准确定位语言运动中枢，减少手术后功能障碍的发生。对于癫痫外科手术损伤较大的大脑半球切除术目前也可采用损伤更小的改良功能性大脑半球切除术来完成。癫痫手术作为一种功能性神经外科手术，以往由于手术前癫痫灶定位手段所限，传统上常需要大骨瓣开颅取得皮层大范围的暴露以便于手术中的电生理定位，随着癫痫术前定位手段和微侵袭神经外科技术的发展，对于手术前具有较明确可靠癫痫灶定位的病例，将有关技术结合使用完全可以实现癫痫的微侵袭手术治疗。</span>
