			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肺癌微创外科治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微创外科学革新了许多良性疾病的治疗模式，例如在病态肥胖症与胃食管反流症的外科治疗上，腹腔镜手术已经基本取代了常规开腹手术。然而在恶性肿瘤的治疗上，微创手术却仍存在很大争议。具体到肺癌方面，仅有为数不多的几家中心在开展胸腔镜肺叶切除术中积累了宝贵的临床经验。支持该项新技术的学者强调微创手术不但减少了手术创伤，也让患者得到了相同的长期生存获益。尽管如此，微创手术方式仍未获得广泛的肯定。在美国，每年约有40000例肺叶切除术，但胸腔镜肺叶切除术仅占其中5%[1]。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其中最重要的原因是支持胸腔镜肺叶切除术的数据几乎全部是回顾性的。虽然胸腔镜手术经验丰富的中心已经报告了该术式显著的优势[1,2]，但目前为止仍然未开展大型的多中心的临床试验。而且，仅有的少数几个胸腔镜与常规开胸的随机对照研究存在着样本量过小[3]或者手术操作不成熟的问题[4]。因此，反对该术式的学者认为支持胸腔镜下肺叶切除术的证据缺乏说服力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;制约胸腔镜下肺叶切除术推广的另一个潜在原因是大出血。尤其是中央型肺癌或淋巴结受累的病例，术中容易损伤肺动脉或肺静脉而导致大出血。当然这并不是指胸腔镜手术比常规开胸手术更容易损伤血管，问题是中转开胸的过程有可能错过重要的抢救时机。胸腔镜肺叶切除术的大型系列研究表明出血并发症的发生率是很低的，但在普及该术式之前，不管有哪个层次的证据支持，潜在出血风险仍将是一个顾虑。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;本章将阐明胸腔镜肺叶切除术的手术要点，评价其减少患者手术创伤的同时获得相同的长期生存率的证据。亦将简要地讨论具争议性的胸腔镜肺楔形切除术在治疗早期肺癌中的地位。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸腔镜肺叶切除术的手术要点</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诚然，目前并没有统一的胸腔镜肺叶切除术的定义将其与常规开胸手术区别开来，因为无论采用哪种术式都必须作一胸部切口为取出标本之用。对于胸腔镜肺叶切除术而言，胸部小切口一般限制在5cm以内，而且不用肋骨牵开器。外科医生可以使用标准的手术器械进行操作，但只能通过胸腔镜窥视手术术野。相比之下，“胸腔镜辅助肺叶切除术”则是指胸部切口（通常8-10cm）需要用肋骨牵开器撑开肋间，外科医师是在直视下进行手术操作，此时，胸腔镜只作照明之用。比较术后疼痛及住院时间方面，这两种术式是会有差异的。显然，这些概念的混淆会令评价微创手术的优势更加困难。因此本章定义胸腔镜肺叶切除术为非肋间撑开并仅通过胸腔镜窥视手术术野的术式。定义中没有规定操作孔的数量，但一般在2个到4个之间。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸腔镜肺叶切除术的手术操作与常规开胸手术有许多相似之处。例如在处理肺门时，两种术式都是一一解剖其中的结构，而且在淋巴结清扫范围上应该是没有区别的。主要的不同在于常规开胸肺叶切除术通常是从肺裂中辨认并分离出肺动脉的（图12.1），除非肺裂发育不良，一般需用剪刀或电刀切开覆盖血管表面的肺组织才可暴露肺动脉，但损伤的肺组织会引起术后漏气，从而住院时间也会相应的延长。相反，胸腔镜肺叶切除术不是从肺裂而是从肺门中辨认并分离出肺动脉（图12.2），然后再用切割缝合器处理肺裂的。这一点区别非常关键，因为它是胸腔镜手术可以减少术后漏气，早期拔除胸腔引流管和早期出院的理论依据。值得注意的是，这一点操作上的差异并不是绝对的，胸腔镜肺叶切除术亦可从肺裂中分离肺动脉，只是在常规开胸手术中更多见而已。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">图12.1 常规开胸肺叶切除术一般是从肺裂中暴露肺动脉的，切开覆盖其表面的肺组织容易引起术后漏气。（授权转载于General Thoracic Surgery，6th edn, Lipincott Williams and Wilkins, 2005.）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">图12.2 胸腔镜肺叶切除术一般从肺门中暴露肺动脉，图中所示右肺中叶静脉切断后即可见右肺动脉。待肺门处理完毕后便可用切割缝合器沿肺裂切除肺叶。（授权转载于Operative Techniques in Thoracic and Cardiovascular Surgery 2004; 9:98–114.）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸腔镜肺叶切除术的禁忌症</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸腔镜下肺叶切除术的绝对禁忌症不多。以前行过开胸手术并不是胸腔镜手术的禁忌症，事实上，胸腔镜比常规开胸更方便探查之前手术引起的胸膜粘连。广泛的胸膜粘连和不能耐受单肺通气均是胸腔镜手术的禁忌，但实际中广泛的胸膜粘连比较少见，除非病人之前行过胸膜固定术，或接受过胸部高剂量放射治疗，或曾患过脓胸。大多数微创外科医生都愿意先尝试胸腔镜探查，在胸腔镜手术不可行的情况下再中转开胸。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肿瘤侵犯胸壁也是胸腔镜肺叶切除术的禁忌症。虽然胸腔镜下可以很好的完成肺切除术，但当肿瘤累及胸壁要行胸壁切除术时，胸腔镜手术就没有优势可言了。胸腔镜探查可用于发现胸壁是否受侵犯，一旦证实则必须中转开胸。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸腔镜肺叶切除术的一些相对禁忌症也备受争议，有些胸腔镜手术经验丰富的中心正尝试打破这些禁忌症的限制，放宽胸腔镜手术的适应症。首先，对于中央型肺癌怀疑肺门结构受侵的病例，大多数外科医生认为胸腔镜手术不可行，原因有以下两点：（1）更容易损伤肺门血管，甚至引起危及性命的大出血；（2）对于可以行袖状切除术的病例，应避免由于胸腔镜下操作困难而改行全肺切除术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然也有一些中心在开展胸腔镜全肺切除术治疗中央型肺癌[5]，但积累的手术经验十分有限。至今最大型的系列研究一共报告了25例全肺切除术，仅其中7例尝试在全胸腔镜下进行，而且有1例需中转开胸[6]。目前的结论是胸腔镜全肺切除术仅适用于部分高度选择的病例。而大多数外科医生，甚至包括有丰富胸腔镜手术经验的医生都认为对于中央型肺癌或伴有团块状淋巴结肿大的病例，常规开胸手术更加合适。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">许多中心亦将术前诱导治疗列为胸腔镜下肺叶切除术的相对禁忌症之一，原因有以下两点：（1）术前的诱导治疗会使原有的外科平面变得模糊，以致在处理肺门时更加危险；（2）很多病人在接受完诱导治疗之后仍然残留有纵膈淋巴结病变，而在胸腔镜下很难完整切除这些病变。但有一项研究对97例术前接受诱导治疗的病人进行了回顾性分析[7]，其中12例在诱导治疗后接受胸腔镜下肺叶切除术，其余均接受常规开胸手术。胸腔镜组没有报道有术后并发症发生，并且比较两组的中位生存期没有统计学差异，表明即使术前接受了诱导治疗，胸腔镜下肺叶切除术仍然是安全可行的。值得注意的是，该报道来自于一个胸腔镜手术经验丰富的中心，而且对病例进行了选择。因此，对于大部分外科医生而言，胸腔镜肺叶切除术仅推荐用于治疗早期周围型肺癌。当然随着手术技巧的娴熟，胸腔镜手术的指证可望进一步放宽。</span>
</p>
