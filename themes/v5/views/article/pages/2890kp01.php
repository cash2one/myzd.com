			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肾脏肿瘤诊治的认识误区</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;根据近几年回复网上肾脏肿瘤病友的咨询和我几年来肾脏肿瘤诊治的经验，我发现有很多肾脏肿瘤患者对这个病的诊治存有认识上的误区。通过这篇文章，我可以减少重复回复相同的问题，也提高患者对这个疾病诊治的准确认识。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>1. 怀疑肾脏肿瘤急于治疗担心肿瘤会快速长大</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;俗话说：“病急乱投医”，有些患者体检B超发现肾脏肿瘤，非常紧张焦虑，夜不能寐，不加选择听从一些不专业的医疗机构提供的治疗方案。导致有些中晚期患者错过手术时机，也有些早期肿瘤患者进行过度治疗，增加病人痛苦和经济负担。对于体检B超发现的肾脏肿瘤最好到大的正规的医疗机构做增强的肾脏CT明确肿瘤的性质和相关情况，我们经常发现病人提供的CT质量很差，或者做的要求不符合肾癌治疗的一些细节（比如肾血管的情况）只能短期内重新进行CT检查，不光浪费金钱还增加病人X光的辐射量。肾脏肿瘤早诊断早治疗的观点是对的，但是一般小的肾脏肿瘤生长速度是不快的，等待一周，一月，甚至数月，肿瘤也不会明显长大到影响治疗方案的选择，所以没有搞清楚诊断前千万不要怕耽误病情草率选择治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2. 诊断肾脏肿瘤的检查越贵越好</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 有些患者体检B超发现肾脏肿瘤想明确肿瘤性质首先想到做PET-CT一步到位，昂贵的PET-CT对于肾脏肿瘤的淋巴结和远处转移的诊断是非常有帮助的，但是对于肾脏局部肿瘤的性质判断是没有优势的，不及普通的增强肾脏CT或增强磁共振检查，所以检查要根据需要一步一步进行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3. 增强CT和磁共振能明确肾脏肿瘤的良恶性</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;目前术前判断肾脏肿瘤的性质主要靠增强的CT或磁共振，但是肿瘤的良恶性最终还是要靠病理检查明确。特别对于一些小的肾脏肿瘤（小于3cm）靠增强的CT和磁共振检查判断肿瘤的性质的准确率只有80%左右，肿瘤越小准确率越低，所以对于CT或磁共振诊断的肾脏小肾癌选择根治手术一定要谨慎，以免不必要的切除肾脏。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>4. 保肾手术不能彻底治疗肾脏肿瘤</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 由于早期肾脏的发现率明显增加，保肾手术越来越普遍，还有很多患者怕保肾手术不能彻底治疗肿瘤选择根治手术。国内外的大量循证医学已经证实对于小于4cm的肾脏肿瘤只要肿瘤的位置合适保肾手术的方式也是完全可以彻底治疗肾脏肿瘤的，并且与根治手术的效果是相同的。最新进展的信息表明选择合适的4-7cm的肾脏肿瘤做保肾手术也可以达到彻底治疗肿瘤。当然保肾手术对医生来说手术难度是增加了，可能会增加一部分并发症的发生率，但目前都还是可控的，特别对于有经验的医生来说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>5. 腹腔镜手术治疗肾脏肿瘤还不成熟</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 腹腔镜作为当今微创治疗的潮流，在国内发展迅速，可以讲跟国外同步发展，一些开展较早的国内腹腔镜手术医生完全可以跟国外一流高手媲美，在肾脏肿瘤的手术治疗方面更是如此。目前80%以上的肾脏肿瘤的根治手术完全可以在腹腔镜下完成，到达与开放手术同样的效果，并且明显减少病人的创伤，这在国内外都已经达成共识。腹腔镜下保肾手术的技术也是成熟的，但是对手术医生的要求更加高所以普及率还不是很广，对于有经验的腹腔镜医生来说也是非常好的选择方法，这要根据实际情况而定。腹腔镜手术在肾脏肿瘤的手术治疗方面是非常成熟的，特别是大城市的三级医院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>6. 转移的晚期肾癌没有必要手术治疗</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;有很多转移的晚期肿瘤再进行比较大创伤的手术治疗确实是没有必要的，但是肾脏肿瘤与一些其他肿瘤不同。一些有淋巴结或肺转移等远处转移的肾癌患者，如果病人的全身情况许可，切除肾脏肿瘤难度不是非常大的话还是要选择切除肾脏肿瘤的原发灶，甚至是远处的转移灶，这对患者延迟存活时间和提高辅助治疗的效果是有帮助的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>7. 肾癌手术后都需要辅助治疗</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;肾癌治疗首先选择手术，手术是唯一能治愈肿瘤的方式。我们碰到有些患者由于担心肿瘤手术后会引起复发和转移而要求辅助治疗。有些医生也会推荐一些不正规的辅助治疗给患者。对于一般的早期肾癌已经做了根治手术或者保肾手术后是不需要特别的辅助检查的只需要定期复查就可以了，追加的辅助治疗不会降低肿瘤的复发和转移的发生率。早期肾癌没有必要辅助化疗和放疗，也没有必要进行干扰素或白介素的免疫治疗，更加没有必要辅助的靶向治疗，这些追加的辅助治疗不仅对肿瘤没有特别的作用还会产生药物的副作用，甚至影响肾功能。我们讲的大剂量的干扰素和白介素的免疫治疗和药物的靶向治疗是针对中晚期肾脏肿瘤，对中晚期肿瘤也要有选择的使用才对，避免对病人产生不利影响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 肾脏肿瘤诊治过程中产生的认识误区有的是病人对一些知识了解的不全面，也有一些是有些医生不会这种治疗方法误导病人选择治疗方法，更有一些可能出自医生的个人利益而诱导病人的选择。所以消除这些认识的误区要靠病人和医生共同努力，已达到最佳的诊治效果。</span><br />
