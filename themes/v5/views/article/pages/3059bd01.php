			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>达芬奇手术机器人进驻北大人民医院 顺利完成"首秀"</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2015年3月27日深夜，达芬奇(da Vinci)手术机器人正式进驻北京大学人民医院。经过安装调试，4月1日手术机器人正式投入使用。由胃肠外科叶颖江主任“主刀”的北京大学人民医院首例达芬奇机器人辅助下的直肠癌根治术取得圆满成功。达芬奇机器人使手术更加精准、微创，提高手术治疗效果，既避免了传统开腹进行腹部肿瘤根治术带来的创伤，又克服了腹腔镜手术器械的局限性，给外科领域带来了革命性的突破。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3059/1.png" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4月1日上午，北京大学人民医院中心手术室第十三手术间忙碌而有序。气管插管、消毒铺单、切口选择等常规操作后，庞大的机器人手臂与病人穿刺套管对接，当叶颖江主任坐到手术操作台，双手握住操作手柄时，北京大学人民医院第一例达芬奇机器人辅助下的直肠癌根治术正式开始。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 机器人机械臂的运动模式与医生保持手眼一致、手与器械端运动一致，实现医生对器械的有效控制，尤其在狭窄解剖区域中机械手比人手显现出更大的灵活性。机器人的“眼睛”是一部三维、高分辨率的立体腔镜，可获得放大5倍的高清晰立体图像，获得如开放手术般的定位，并实现术中实时信息的整合显示。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><<img src="<?php echo $baseUrlImage; ?>article/doctor/3059/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据叶颖江主任介绍：“在手术时，视野清晰开阔，感觉机器人就是自己的‘左膀右臂’，操作起来精确度更高，对于医生也有助于提高效率，拓宽了传统微创手术的应用。更重要的是，对于患者来说，减少手术创伤，术后并发症和失血明显减少，提高手术治疗效果。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中新网健康频道了解到，外科手术机器人，又称“达芬奇(da Vinci)手术机器人”、“内窥镜手术器械控制系统”，通过使用微创方法，实施复杂的外科手术，是目前世界上最先进的手术平台。机器人手术目前主要用于普外科(肝胆、胰腺、胃肠道、甲状腺等)、心脏外科、胸外科、泌尿外科、妇科、小儿外科、咽喉头颈外科等。在手术时，外科医生可以远离手术台坐在操纵台中，双手握住手柄，同步控制患者体内的机械臂手术器械进行手术，所以完全不同于传统的直接将手伸入患者体内进行手术的概念，给外科领域带来了革命性的突破。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据悉，达芬奇手术机器人从项目论证到经费申请，再到设备安装调试，医院都高度重视，相关部门全力协调。同时，达芬奇智能微创手术系统医生控制台也落户北京大学人民医院临床能力培训中心，标志着医院微创外科模拟教学工作进入智能化微创手术基本技能培训的新领域。医院将为广大外科医师打造更具前沿水平、更专业的微创模拟手术培训课程,为培养更多智能化微创外科手术医师。</span><br />
