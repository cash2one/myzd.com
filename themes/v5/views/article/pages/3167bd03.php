<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>三博林志雄教授主持上海脑积水沙龙小儿专场</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近日，首都医科大学附属北京三博脑科医院林志雄教授应邀出席了上海“2014年脑积水沙龙和病例分享”小儿专场，并做了《小儿分流管依赖综合征》学术报告，受到与会专家和同行的热烈好评。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">林志雄教授是国内知名的神经外科专家，尤其是在小儿神经外科疾病治疗方面具有丰富经验。擅长治疗小儿神经系统肿瘤、小儿脑外伤、小儿脑出血及小儿神经系统先天性疾病，本次会议林教授报告了小儿分流管依赖综合征，探讨了脑积水、蛛网膜囊肿腹腔分流术后并发分流管依赖的临床表现、影像学特征与治疗方法及可能的预防措施。该观点的阐述，提高了广大同行对小儿脑积水分流术后长期并发症的认识，明确了疾病的诊断方向，及时避免漏诊、误诊、推诿等情况。</span>
</p>
<p>
