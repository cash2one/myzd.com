		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>脑梗塞该看哪个科？（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;这个问题我几乎每年要回答上百次，随着医院分科的细致，越来越多的病人开始迷茫，不知道挂哪个科看自己的病，最要命的是，不同科室、不同医生观点不一致，有让吃药的，有让做支架的，有让做手术的，如果我是病人，我都会晕的。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 这主要表现在神经内科和神经外科，其实神经外科和神经内科的划分是一种传统的学科划分，每个国家都不一样，比如中国，神经内科负责不开刀的疾病，如炎症、变性疾病、脑血管病等等，神外负责开刀的疾病，如肿瘤、外伤等等，而随着医学的发展，纯粹学科的划分有时候是一个制约的因素，比如几十年前，癫痫、脑血管狭窄就是吃药，没有别的办法，那也就神经内科治疗了，但这几十年，手术或介入的办法使很多疾病得到更好的治疗，也就扩大了外科的范畴，所以外科也开始治疗这些疾病。最典型是日本，这是全球治疗脑血管病最好的国家，生存寿命大幅度提高，一个重要原因就在于政府引导神经外科在脑血管病方面占据主导地位，使得更多的病人预防为主而不再发病，但在中国，因为管理的缺失，一个新技术什么学科都可以抢，有点占山为王的味道，所以形成现在这种格局，就缺血性脑血管病而言，有神经内科、神经外科、血管外科、心脏内科、心脏外科、介入科等等都在做，每个地方都不一样，既造成了学科之间的敌对，导致了医院科室之间的矛盾，更重要的是使患者的利益受损。打个比方，一个严重颈动脉狭窄的患者来到医院，如果这个医生是神经内科医生不会介入和手术，他就会劝病人吃药，可能耽误病情；但如果一个比较轻的病人来到医院，接诊的是一个外科或介入科医生，他可能就会劝病人接受不必要的手术或介入治疗。总之，很多医生总是按照自己所会的知识给病人治病，而排斥其他的观点，尤其在少部分年纪大的医生身上表现的最明显，我不是说这些医生如何不负责任，因为大多数医生还是认真看病的，但都是凭经验看病，所以老大夫都信自己的经验，但在中国，有些基层医院医生可能较长时间不接触新的知识，不可能充分借鉴别人的经验，仅自己之经验总是有限的，所以患者就不能得到最理想的治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 现在这个问题是我们业界争论最激烈的，我们的主张一直都是摈弃学科的壁垒，以患者的病情为第一位，综合考虑合适的方法，但毕竟每个学科、每个医院、每个科室、每个医生都有自己的观点和利益，很难做到完全客观的对待这个问题，所以，这么多年来，我们一直做得主要是两方面工作，一是游说政府机构，尤其是卫生部，要针对技术和学科提出具体的培训和准入机制，泱泱大国60余年，全国各学科医生都没有专科的认证机制，不能不说是管理上的严重缺失，在中国，这对于任何一个医生都是不能完成的，只能依靠政府行政力量，有了这些培训和准入机制，我们就能够规范医师的行为、杜绝滥用技术或过度医疗；第二就是加强医生的学术培训，每年全国都有很多的学术会议，会像洗脑一样强化给医生很多国际先进的治疗理念，但这是一个任重道远的工作，不可能通过几年的教育就改变医生的固有观念，好在现在的年轻医生大多具有较好的素质和学习态度，这是中国医疗事业未来的希望。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">其实，这个问题真的很尖锐，引出我的诸多感慨，这是中国医疗体制上的一些弊病，作为一个医生，我很幸运赶上了治疗理念革新的时代，一方面能够用自己的力量治疗更多的病人，另一方面都为医生的教育做出一些贡献，但仅凭我们这一代为大家就做出很好的医疗体制模式是不现实的，即便是医疗体制比较完善的欧美，也存在很多致命的弊端，因此，我们能做的就是从自身做起，把每一步走扎实，少些牢骚，多些实干，只要对这个事业有利，对病人有利，我想政府和各级医疗机构都会向着更完善的方向努力的。</span>
</p>
