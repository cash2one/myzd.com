			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何判断是否为甲亢稳定期，尽快行眼科手术？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">问：我是甲亢患者，如何根据我的实验室检查结果来判断我是否为甲亢稳定期，能不能尽快进行眼科手术？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 答：甲亢患者的主要实验室检查有：总T3（TT3）、总T4（TT4）、FT3、FT4、TSH、TSAb等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">FT3、FT4不受血中TBG变化影响，直接反应甲状腺功能状态。其敏感度和特异性均高于总T3（TT3）、总T4（TT4）。甲亢时，FT3、FT4通常增高，若其指标在正常范围或较正常范围增高1倍以内，可视为甲功基本正常，若能维持半年，可初步判断为甲亢稳定期，</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">血TSH（促甲状腺激素）是目前测定甲亢最常用、最可靠的检测项目。甲亢时，血TSH降低。但并不将其作为判断甲亢是否稳定的主要指标，因只有甲亢彻底痊愈，TSH通常才会升至正常范围。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">TSAb（促甲状腺素受体抗体）是Graves病的重要标志物。未经治疗的Graves病患者，血TSAb阳性检出率可达80~100%，有早期诊断意义，对判断病情活动、是否复发亦有价值。若TSAb&lt;10IU/L，可初步判断为甲亢稳定期。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，FT3、FT4在正常范围内或较正常范围增高1倍以内，TSAb&lt;10IU/L，可初步判断为甲亢稳定期，需维持半年。还可行眼眶MRI或眼部A/B超检查，观察眼外肌的肌腹，与半年前相比有无明显变化，若变化不大，再结合患者眼部的变化，可判断患者为甲亢稳定期，可考虑行眼部手术。</span>
</p>
