			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>哪些胆囊结石适合保胆取石？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多胆囊结石的患者担忧切除胆囊后，会增加结肠癌的患病风险，这种担忧是可以理解的。胆囊毕竟是人体的一个器官，上帝创造了人，任何一个器官都不是多余的，都有它存在的道理，只是目前医学水平还有很多不能解释和解决的问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，很多人都提出能否保胆取石，即保留胆囊，单纯把结石取净，理论上讲，这个并非不可行，前提是必须取干净，保留的胆囊是有功能的。同时，开展此手术的医院需要具备纤维胆道镜、胆道硬镜及其它相关设备。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 那么，哪些胆囊结石的患者适合保胆取石呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 首先，取净结石，保留有功能的胆囊是保胆取石的主要目的，因此并非所有的胆囊结石都能保胆。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 适应症：（1）年龄在50岁以下，经B超或其他影像学检查确诊为胆囊结石，不合并胆管结石。（2）胆囊脂肪餐试验阴性，胆囊功能正常（胆囊壁厚&lt;4mm、ect检查胆囊显影良好，胆囊e≥30%）或腹腔镜下胆囊生理状态良好者。（3）无症状或有轻微症状者。（4）有强烈保胆意向，拒绝胆囊切除者。（5）无严重内科疾病能耐受麻醉与手术者。（6）无肝功能严重损害及凝血功能障碍者。其中（2）是保胆取石的必备条件。&lt; span=""&gt;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 禁忌症：（1）胆囊萎缩、胆囊壁明显增厚&gt;5mm或胆囊腔消失者。（2）胆囊结石诱发急性胆囊炎、胆囊坏疽、急性胰腺炎或其他严重并发症。（3）胆囊结石并发息肉隆起样变或疑有胆囊癌者。（4）胆囊收缩功能不良。（5）胆囊管完全梗阻者。（6）肝内胆囊术中不能提出者。（7）胆囊结石并发胆管结石者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 胆囊结石形成原因复杂，保胆取石还存在很大争议，结石复发率还有待进一步研究，因此，患者在选择保胆取石时，一定要清楚自己是否适合，开展此手术的医院是否具备资质，不要被人忽悠，导致最后再次手术，增加痛苦和不必要的费用。</span><br />
