		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>陆骊工：糖尿病足及时介入治疗可避免截肢</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中国是全球糖尿病人口最多的国家，发病率高达9.7%。且国内糖尿病患者的疾病检出率低、民众知晓率低、血糖控制率低的现状十分令人堪忧。糖尿病可以造成许多严重的并发症，其中就包括糖尿病足。目前，糖尿病足日益受到社会的关注，因为其截肢发生率、致残率高。患者一旦截肢，丧失正常的行走及劳务能力可严重影响生活质量；而且截肢并不能有效延长患者生存，截肢后5年生存率只有约25%；此外，截肢治疗的花费较高，增加家庭及社会的负担，每年国家都要投入了大量财力。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 随着材料医学和科学技术的发展，越来越多的先进治疗方法开始进入糖尿病足治疗领域，这其中，介入医学起到的作用是十分令人瞩目的。专家指出，糖尿病足患者如果及时进行血管介入治疗，可以延缓糖尿病足的进展，防止截肢的悲剧，提高生活质量，延长生存。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家简介：陆骊工，主任医师、广东省人民医院介入治疗科主任、医学博士、博士生导师。现任广东省医师学会介入医师工作委员会副主任委员、广东省中西医结合学会肿瘤分会常委、广东省中西医结合学会影像专业委员会常委。 擅长肿瘤介入及外周血管介入治疗。对于各种血管微创治疗（主动脉、肾动脉、颈动脉及髂动脉病变等）经验丰富。目前主持多项国内外大型科研项目研究。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 介入并非只能治疗肿瘤和心血管病</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 糖尿病足及时介入治疗可避免截肢</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 广东省人民医院介入治疗科主任陆骊工教授介绍，介入治疗目前在临床上的应用主要包括四个领域：心脏介入、神经介入、周围血管介入还有肿瘤介入。心脏介入由于专业性较强，目前医院都是由心内科来实施。其余3个领域都是介入治疗科实施的范畴。糖尿病足的介入治疗属于周围血管介入。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家表示，介入不仅仅可以用于诊断，也可以用于治疗，尤其是外周血管病变，相对于外科的血管搭桥，更是有其独特的优势。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据了解，周下肢血管属于外周血管，下肢血管病变包括动脉病变和静脉病变。糖尿病足的血管病变属于下肢动脉病变。下肢动脉的狭窄或闭塞是糖尿病足发生及发展的重要原因。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 陆骊工主任表示，糖尿病对于全身血管的影响是非常巨大的，可导致血管狭窄、硬化和闭塞，从而导致下肢和足部缺血引发糖尿病足，如不及时处理，最终难免截肢。应用介入技术，及时有针对性的对下肢动脉病变的直接干预，可有效改善下肢的血供，从病因上改善糖尿病足患者的症状，延缓病情的进展，可有效地避免既往“截完脚趾截足，截足之后截腿”的悲剧发生。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “及时介入治疗糖尿病足，可以通过改善足部的血液供应，从根本上延缓糖尿病足的发展，可明显降低糖尿病足患者的截肢风险。如果患者就诊较晚，病情已发展到必须截肢的程度，介入治疗还可以作为补救的措施，挽救一部分可以存活的肢体，降低截肢的平面，改善术后的生活质量。”陆骊工主任说。</span>
</p>
