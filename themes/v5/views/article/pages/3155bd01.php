			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>机器人微创前列腺根治术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; <img src="<?php echo $baseUrlImage; ?>article/doctor/3155/1.png" title="" alt="demo.jpg"/>　机器人就是通过这样的机械手臂完成手术操作的</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">□记者 蔡建华 实习生 黄克宇 通讯员 曹咏 程文杰</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上个月，迪士尼出品的动画电影《超能陆战队》，在获得6.19亿美元的超高票房后，又斩获了奥斯卡“最佳动画长片”奖。一时间，《超能陆战队》里的机器人“大白”成了影迷们关注的对象。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">事实上，影片中的医疗机器人、微型机器人的部分功能，人类已经实现了。去年10月，河南省首台达芬奇手术机器人落户郑大一附院，当年的10月7日，我省著名肝胆外科专家、郑大一附院的张水军教授通过“达芬奇”手术机器人，为两名患者做了肝脏手术，开启了河南省机器人做手术的新时代。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3月20日，《大河健康报》记者跟随郑大一附院泌尿外科张雪培教授参观机器人手术。与普通手术台相比，手术机器人的手术台多了几个机械臂，这些机械臂是机器人的手。手术台的旁边是操作台，主刀医生坐在操作台上，手脚并用，把手术指令发送给机器人，机械手臂完成手术操作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张雪培教授说，很多人认为，机器人手术时，所有步骤全是机器人做的，其实不是。手术前的摆体位、切皮，手术中的取标本，手术后的切口缝合等工作，都需要人工操作。也就是说，机器人做手术时，还是要有医生在手术台上辅助它完成一些工作的。只是，在手术台上的医生不是主刀医生，是助手。真正的主刀医生不上手术台，而是在手术台旁边的操作台向机器人发出指令，指挥机器人做手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">记者参观的手术是前列腺癌根治术，也是张雪培教授做的第52台机器人手术。手术采取的是微创方式，也叫钥匙孔手术，总共有3个手术切口，最大的也只有钥匙孔那么大。机器人手术和腹腔镜手术相似，三个手术孔各有分工，一个孔用于放镜子，负责把腹腔的情况显示在显示器上；机械孔则用于切组织和吻合器官；辅助孔用来送手术针、手术线和取标本。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">机器人手术臂通过三个微创的小口子进入高先生的体内，主刀医生张雪培教授坐在操作台上，开始手术。操作台由屏幕、手柄和脚踏板等部分组成，张雪培教授把双眼贴到可视区，能清楚地看到腹腔的三维图像。根据看到的情况，他用操纵杆做出各种手术动作，机器人把这些动作传输到手术台上，机器人的手臂在执行医生指令的同时，会自动过滤掉因为医生手颤等对手术带来的不利影响。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺的位置很特殊，它在膀胱下面，尿道从前列腺中穿过，它的位置比较深，常会影响医生视野和手术器械的进入，所以传统的开放手术并不好做。现在，用机器人手术，进入体内的镜子可以360度任意旋转，而且可以把看到的三维立体图像放大10倍。用于切、割的手术器械也很精密，像医生的手，却比医生的手更细更巧，手进不去的地方，它能进去。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在镜子的帮助下，张雪培教授很快找到了前列腺肿瘤，并把它切掉。切掉后，又迅速把膀胱和尿道进行了吻合。为防止复发，张雪培教授还对前列腺周围的淋巴组织和脂肪进行了清扫。接下来，在台上的手术助手取前列腺等标本、缝合……</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1个小时10分钟，手术完美结束。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">《大河健康报》记者发现，虽然手术是机器人用它的“手”完成的，但主刀医生才是关键，因为指令是由主刀医生通过操纵杆发出的，机器人只是被动地执行了主刀医生的动作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">机器人做手术，创口非常小，今天手术明天就可能下床，后天就能出院。总结一下，手术机器人的优点是可模拟专家手指的灵活度，同时消除不必要的颤动，而且可以进行人手不能触及的狭小空间的精细手术操作。</span><br />
