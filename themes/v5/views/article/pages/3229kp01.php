			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>风湿性心脏瓣膜病的防治</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">风湿性心脏瓣膜病是感染A型溶血性链球菌后引起心脏瓣膜病理改变而致的一组瓣膜疾病。多于青壮年发病，在我国是一种常见心脏病。最常见的是二尖瓣狭窄与/或关闭不全，其次是主动脉瓣狭窄与/或关闭不全，少见的是三尖瓣狭窄与/或关闭不全。为了增强对风湿性心脏瓣膜病的认识，从风湿性心脏瓣膜病病因、临床表现、诊断、治疗与预防对其作一概述。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一、病因</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 风湿性心脏瓣膜病患者一般先有风湿热病史，如风湿性咽喉炎、风湿性关节炎、风湿性心肌炎等。其致病微生物是A型溶血性链球菌。经济落后、生活水平低、卫生 条件差的地区较易发病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 二、临床表现</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 该病的临床表现因不同的病种而有差别。最常见的症状是活动后心慌、气促、胸闷，反复咳嗽及头晕等。严重者有咯血、晕厥、心前区痛、浮种、腹水等。晚期患者可因左、右心功能衰竭或心脏骤停而猝死。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 三、诊断</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 患者既往有风湿热病史。体检心前区可闻及心脏杂音等。抗链球菌溶血素“O”(抗O)，C反应蛋白(CRP)、红细胞沉降率(ESR)等化验检查对诊断有帮助。心电图、X线胸部平片、心脏超声心动图及彩色多普勒检查能明确诊断。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 四、治疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 风湿性瓣膜病早期多无症状，有些病人在体检时才发现患病。如果患者有症状，应立即到专科医院就诊，病情较轻者可口服强心、利尿补钾、扩血管等对症治疗的药物。药物治疗无效者需行手术治疗，有瓣膜成形术与瓣膜置换术二种，瓣膜病理改变较轻者可行成形术，病理改变重者需行换瓣术，行换瓣术者需终生抗凝治疗，抗凝期间应定期到医院检查凝血酶原活动度，以调整抗凝药的用量;早期尚有闭式二尖瓣狭窄扩张术，现已很少采用。少数二尖瓣狭窄病人可行球囊瓣膜扩张术。如患者处于风湿热急性期，需结合抗风湿治疗，如肌注苯唑青霉素，肌注或静注青霉素，青霉素过敏者可口服红霉素等措施。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;五、预防</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于风湿热多于人体抵抗力较低时，感染A型溶血性链球菌而引起。因此，平时加强锻炼身体，增加体质，生活规律及不吸烟 ，天气阴凉时注意穿衣盖被保暖等措施对预防该病是很有帮助的。定期到医院体检也利于对该病早发现、早诊断、早治疗。一旦确诊患有风湿热，应立即到专科医院应用苯唑青霉素、青霉素、红霉素等治疗。</span>
</p>
