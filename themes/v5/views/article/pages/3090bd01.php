<h1 style="text-align:center;margin-left:0pt;">
	<b><span style="font-size:18px;font-family:'Microsoft YaHei';line-height:2;">专访北京儿童医院张亚梅教授</span></b><b></b>
</h1>
<p class="p" align="center" style="margin-left:0.0000pt;text-indent:21.0000pt;text-align:center;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="p" align="center" style="margin-left:0.0000pt;text-indent:21.0000pt;text-align:center;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张亚梅北京儿童医院主任医师耳鼻喉教授硕士生导师中华医学会耳鼻咽喉分会常务委员；毕业于首都医科大学，曾先后在美国波士顿儿童医院和美国爱因斯坦睡眠中心进修；在小儿过敏性鼻炎、鼾症方面做了多项研究，在诊断和手术方式的选择上有丰富的经验；对小儿急慢性中耳炎、鼻炎鼻窦炎、扁桃体炎、各种气管支气管异物、食道异物的诊断治疗有较深入研究。</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">《华西·名医谈》栏目由《华西都市报》、成都仁品耳鼻喉专科医院联合制作，聚焦青少年健康问题。第三届“青少年耳鼻喉健康V计划”活动期间，特邀知名耳鼻喉专家定期讲解青少年常见疾病，为家长支招。敬请关注！</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">笔者：张教授您好！第三届“青少年耳鼻喉健康V计划”开展一个多月了，检查出患过敏性鼻炎的孩子比较多，您认为这是哪些原因造成的？</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张教授：我认为主要有这三方面的原因：一是近年来空气质量下降，尤其是在城市，过敏性鼻炎患者相对农村多一些；其次，现在的孩子生活环境较好，再加上家长的溺爱，对生活中的过敏原耐受力比较低；第三，家长对孩子过敏性鼻炎的认识不足，拖延了治疗时机，这也是造成青少年过敏性鼻炎高发的重要原因。</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">笔者：很多家长认为过敏性鼻炎是一种儿童常见病，不过是早上打几个喷嚏，等这个季节过去了，或者孩子长大后就好了，是这样的吗？</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张教授：显然不是的，对过敏性鼻炎“任其发展、等待自然好”，是很多家长都有的一种认识误区。大部分过敏性鼻炎患儿长大后并没有变好，反而加重了。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">孩子患上过敏性鼻炎不及时治疗，容易引起长期的鼻塞，导致孩子吸入氧不足，孩子记忆力减退，甚至影响身体发育和智力发育。而且过敏性鼻炎容易引起鼻窦炎、咽炎、鼻息肉、中耳炎等多种并发症，甚至许多过敏性鼻炎患儿最终会发展成哮喘，给孩子带来终身的麻烦。</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">笔者：既然小儿过敏性鼻炎是一个大麻烦，那么，请张教授给大家介绍下，现在医院在治疗方面有哪些好的方法？</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张教授：如果家长觉得孩子可能是过敏性鼻炎，应首先到专业医院进行过敏原检测，进行确诊并找出孩子是对什么过敏，然后尽量在生活中避免接触过敏原。对于轻度过敏性鼻炎，可以以药物治疗来控制症状，但不能根治。如果孩子症状较重，12岁以上我还是建议手术治疗，运用超微创技术，效果明显。而对于年龄较小不适合手术的尘螨过敏孩子，更适合进行脱敏治疗，通过2-3年的治疗提高孩子对过敏原的耐受力，终身受益。</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">笔者：最后请张教授给大家科普一下，小儿过敏性鼻炎到底有哪些征兆，家长在生活中应注意什么？</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">张教授：过敏性鼻炎有四大症状，第一是打喷嚏，打喷嚏不是光打一个两个，连续打一串喷嚏，而且一阵一阵的，第二，鼻子痒，痒的特别难受，有人使劲揉鼻子，有的人眼睛也痒甚至耳朵痒。眼睛痒到什么程度？会呈现出水汪汪的那种情况。第三，流鼻涕，鼻涕可以是清水样的鼻涕。第四，鼻塞，鼻塞可以非常厉害，影响到睡眠和学习。一般来讲，过敏性鼻炎四大症状里面，有两项符合就可能是过敏性鼻炎。</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p class="p" style="text-indent:21pt;">
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外需要注意的是，夏天很多家庭都喜欢开空调，导致室内外空气温差大，而且空调里面尘螨多，孩子吸入后容易诱发过敏性鼻炎。建议各位家长使用空调时，应注意控制儿童吹空调的时间和温度，还要注意清洗空调内部的尘螨，定时给房间通风。</span>
</p>
