<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">讨论：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、EUS引导下细针穿刺活检的诊断价值</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于内镜超声探头距离病变近，能清晰显示病变及其与周围组织器官的关系，穿刺距离短，活检穿刺成功率高，安全性高。Giovannini报道EUS－FNA对于胰腺、纵隔、淋巴结及消化壁外占位病变有较高的诊断准确率，约78–94%，诊断灵敏度和特异度分别为76 －91%, 84–100%，本研究中胰腺实性占位的诊断准确率为86.7 %，胰腺囊性占位中有6/7例诊断明确，腹腔淋巴结、胆囊、肝脏、纵隔等8例消化道邻近占位病变中有6例获成功诊断，而胃粘膜下占位病变中仅2/5明确诊断，均为间质瘤。Wiersema等报道EUS-FNA对于胃肠道粘膜下病变的诊断灵敏度为61％；本研究中仅2/5例取材成功，提示EUS-FNA对于胃肠道间质瘤取材成功率不高，可能与其组织特性有关。结合国内外的研究经验，我们认为EUS－FNA对于胰腺、纵隔、腹腔淋巴结等消化壁外腺体占位病变有较高的诊断准确率；而对于胃肠道间质瘤诊断准确率有待提高。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;EUS-FNA的诊断准确率受诸多因素的影响,如何提高其诊断准确率是目前EUS研究重点之一，文献报道病理专家现场进行细胞标本快速染色和检查，可明显提高取材成功率，减少盲目穿刺次数，减少并发症，金震东等报道由病理医师采用现场快速染色法，取材合格率由82.9%提高到100％，敏感性由67.6%提高到93.1％。穿刺时采用的负压也是取材成功与否的重要因素，Wallace等的一项随机对照试验显示过大负压可使吸取物中血液成分增加，致穿刺失败，Antillon等报道FNA是通过切割和刮取作用获取细胞学检查，不能单靠负压吸引。我们的经验是采用适当的负压并配合穿刺针在病变中来回穿刺多次并适当变换穿刺角度可获取较理想的活检组织，提高诊断准确率。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、EUS引导下腹腔神经结阻滞对癌性腹痛的治疗价值</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">晚期胰腺癌引起的剧烈疼痛是临床上非常棘手的问题,与传统的经皮CT或X光引导下阻滞治疗比较，内镜超声能清楚显示腹主动脉及腹腔干,对腹腔神经节进行准确定位，并实时监控进针路径，因此EUS 引导的细针能准确穿刺该神经丛, 发生截瘫、血管损伤等并发症更少，疗效增强。本研究中3例胰腺癌病员行EUS引导下腹腔神经节阻滞治疗后全部疼痛明显缓解，且维持时间较长，无任何严重并发症发生。Gunaratnam 等的一项纳入58例胰腺癌患者的前瞻性研究显示，EUS-CPN后2周疼痛评分下降78％，持续缓解时间为24周；杨秀疆等对29例胰腺癌、6例胆囊、肝脏、胃癌患者行EUS-CPN, 33 例疼痛明显缓解，2例疼痛减轻，疗效最长持续7个月，其中29例胰腺癌患者疼痛缓解，无严重并发症发生，提示EUS- CPN对癌性腹痛特别是胰腺癌疼痛的有效性及安全性。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本研究中3例患者症状均明显减轻，但缓解持续时间及缓解程度有差异，分析认为与下列因素有关：①3例患者均为晚期胰腺癌患者，治疗前疼痛时间较长，而CPN治疗时机的选择对疗效影响较大，胰腺癌早期疼痛多来源于腹腔神经丛，而晚期疼痛则可能伴有内脏和躯体神经的疼痛，对CPN的治疗反应差，Wong 及Vranken等亦认为癌性疼痛刚发生时进行阻滞治疗效果较好，而De Oliveira 则认为两者无明显差异。我们认为：为提高患者生存质量，减少镇痛药物副作用及成瘾性应尽早行EUS- CPN；②注射针部位也是影响镇痛效果因素之一，通常左侧和右侧腹腔神经节分别位于腹腔动脉干下方约0.9 和 0.6 cm，在腹腔干头侧注射酒精能起到较广泛的药物分布和神经溶解作用，疼痛缓解时间较长，因此操作过程中应准确地定位注射，并注射足量的酒精以达到充分消融神经作用，本文中第1例病员由于治疗经验不足，仅注射5ml酒精，可能是影响其疗效的原因之一，Giovannini等推荐酒精注射剂量为15－20ml；③本研究中3例患者为疾病晚期，未能进行辅助化疗，可能是疗效欠佳的因素之一。Gunaratnam等报道行CPN治疗同时进行辅助化疗能提高镇痛疗效，EUS－CPN 能有效地控制早期癌痛，辅助化疗则可在疾病晚期阻滞作用减弱时有效地维持镇痛效果。</span>
</p>
