<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、内镜超声引导下穿刺抽吸灌洗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胰腺、肝脏脓肿及囊肿既往多采用手术治疗、腹腔镜治疗或CT、B超引导下经皮穿刺的方法，其中经皮穿刺引流并发症发生率最低，但对于病变位置特殊病例，如肝脏腹腔面、靠近肝门及肝尾叶等，由于穿刺路径较长，经皮穿刺仍然可能导致腹腔内出血、腹膜炎、败血症等严重并发症，Kawaguchi等报道52例经皮肝穿刺引流的病人中3例死于大出血， Chung 等报道两例经皮肝穿刺引流肝脓肿后脓肿破入门静脉和肝静脉，并导致败血症，E US引导下穿刺引流途径更短，能避开血管，选择最佳穿刺路径，明显降低并发症发生率。对于内超引导下脓肿治疗国内尚未见有相关报道，国外Seewald等报道内镜超声引导下经胃壁穿刺安置鼻脓肿引流管成功治疗1例肝左叶腹腔面脓肿，Ang TL等报道采用内超引导下经胃穿刺置管引流，成功治疗1例经皮穿刺治疗失败且手术风险极高的肝左叶破裂脓肿，无不良反应发生。本研究中1例病变位于胰尾，2例肝脏脓肿位于肝尾叶及左叶，均行EUS介入治疗成功，由于本研究中脓肿壁较薄，为避免因安置引流管可能导致的脓液顺引流管周漏入腹腔导致腹膜炎的发生，因此我们采用反复抽吸灌洗方法，取得良好效果，且无并发症发生，但对于有条件病例仍建议尽量行置管引流。另外，治疗前进行EUS检查还能获取以下信息以决定治疗方法：囊肿或脓肿内有无坏死物、分隔、分隔厚度，囊壁厚度以及与胰管等结构是否相通，为治疗上述病变提供更为彻底、有效的治疗手段。</span>
</p>
