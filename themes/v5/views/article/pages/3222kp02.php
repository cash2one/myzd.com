			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>内镜超声引导下细针穿刺活检及介入治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前言：内镜超声检查术（endoscopic ultrasonography,EUS）目前已广泛应用于临床，早期EUS 仅作为一种诊断方法，EUS 引导下细针穿刺活检（EUS guided fine-needle aspiration，EUS-FNA）是近年来EUS 临床应用的重大进展；随着治疗内镜技术的发展及线阵超声扫描探头的应用，大钳道线阵超声内镜的不断完善、改进，使越来越多的EUS引导下介入治疗（EUS guided fine-needle injection,EUS-FNI）得以实现，其微创、经济、并发症少的独特优势，使它在消化系统疾病的诊治中发挥出越来越重要的作用，目前在临床上运用较成熟的EUS介入技术有EUS引导下细针穿刺抽吸活检技术、EUS引导下腹腔神经节阻滞，放射粒子置入和EUS引导下胰腺囊肿引流技术等；我们总结近年来我院对42例病员进行的内镜超声引导下介入诊疗，探讨内镜超声引导下细针穿刺活检，脓肿抽吸引流，腹腔神经节阻滞的诊疗价值。 </span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">
<div>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</div>
</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、资料与方法</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.1 研究对象</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2006年1月至2009年5月对诊断不明确的15例胰腺实性占位，7 例胰腺囊性占位， 4例肿大腹腔淋巴结，5例胃粘膜下占位，2例肝脏占位，2例纵隔占位，1例胆囊占位病变进行EUS引导下细针穿刺活检，对3例胰腺癌致顽固性腹痛患者进行内镜超声引导下腹腔神经节溶解治疗（celiac plexus neurolysis，CPN），对1例胰尾脓肿、2例肝尾叶、左叶脓肿患者进行EUS引导下脓肿抽吸灌洗引流治疗；所有患者年龄28－80岁，平均年龄65.4岁，男性26例，女性16例，所有患者均无严重心肺等脏器并发症，除外凝血功能障碍性疾病，术前2周内禁用非甾体抗炎药物及其他抗凝血药物。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.2 仪器及设备</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Pentax EPM-3500内镜主机，日立公司EUB-5500彩色超声仪， Pentax EG-3830UT线阵内镜超声仪，COOK公司EUSN-22-GA、19-G-T内镜超声穿刺针。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.3 操作方法</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.3.1EUS-FNA:</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者术前准备同无痛内镜检查，内镜超声清楚显示病灶后, 避开血管等重要结构，设计探头与病灶最近距离确定穿刺路径,连接10ml负压注射器, 保持恒定的负压，穿刺时始终在超声引导下观察针尖进入病变区域，拔出针内芯，穿刺针在病灶中反复穿刺6～8次后, 关闭负压开关后退针，获取的组织条以10％甲醛固定、脱水、石蜡包埋、切片后HE染色，行组织病理学检查,必要时行免疫组化，组织液涂片3－5张后以95%酒精喷洒固定，并行HE染色后作细胞学检查，必要时变换穿刺角度重复穿刺。穿刺后禁食24小时，常规补液，静脉使用抗生素、抑酸剂，并密切观察腹部体征、血压、脉搏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.3.2EUS-CPN:</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术前准备同EUS-FNA，超声探头置于贲门探测到主动脉及腹腔动脉分支处，用多谱勒显示血流确定为血管声像图后, 经内镜活检孔插入22G注射针,超声引导下，将穿刺针经贲门后壁穿刺至腹腔干起始部，回抽无血后，注入5ml 0.75%布比卡因及15ml无水酒精（其中1例注射酒精5ml），见局部呈低回声，随后呈云雾状高回声，注射毕,观察局部无出血后结束治疗，术后处理同EUS－FNA。各例患者术前术后采用疼痛视觉模拟评分(visual analogue scale,VAS): 由患者口述, 医师记录, 测定疼痛强度, 无痛时的VA S 值为0, 最剧烈疼痛时为10。VAS值1～3为轻度疼痛, 4～6为中度疼痛, 7～10为重度疼痛。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.3.3内镜超声引导下脓肿穿刺引流：术前准备同EUS-FNA，超声内镜清楚显示病灶后, 避开血管等重要结构，选择病灶最近距离确定穿刺径路, 经内镜活检孔插入19-G-T穿刺针，超声引导下进行脓肿穿刺，抽吸脓腔内脓液行常规、生化及细菌培养检查，依据病变脓腔大小以0.5％甲硝唑注射液分次注入和抽出灌洗治疗，最后一次尽量抽出腔内液体，术后处理同上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、结果</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.1EUS-FNA诊断准确率：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">15例胰腺实性占位病变中有2例未获得满意的组织及细胞，13例得到充足的组织及细胞并获诊断成功，穿刺成功率和诊断准确率均达86.7%，其中11例为胰腺癌，1例为炎性假瘤，1例为胰腺内分泌肿瘤，占位病变最大直径5.7cm，最小直径1.9cm。7例囊性占位中6例明确诊断，其中假性囊肿3例，囊腺瘤2例，导管内乳头状粘液瘤1例， 6/7例明确诊断，病变最大直径9.0cm，最小直径2.2cm。 4例腹腔淋巴结中3例穿刺取材成功，诊断为腹腔转移性腺癌，5例胃粘膜下占位中2例取材成功，诊断为胃间质瘤；2例肝脏占位中1例取材成功，诊断为肝脏炎性病变；2例纵隔占位分别诊断为胸腔恶性间皮瘤及神经鞘瘤；1例胆囊占位诊断为胆囊腺癌，详见表1。除穿刺时部分患者感上腹不适外，无疼痛、出血、消化道穿孔和感染等并发症发生。</span><br />
