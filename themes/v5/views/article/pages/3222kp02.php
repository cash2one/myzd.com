	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">综上所述，EUS 引导下FNA 及介入治疗对上消化道及消化壁外占位病灶的诊断和治疗均有较大的应用价值，有其它诊疗手段所不具备的优势，是一种安全、有效的诊疗方法，值得临床进一步推广应用。</span>
