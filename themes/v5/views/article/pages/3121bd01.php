<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">行走、跑步或跳跃时，跟腱承受着巨大的负荷。图片源自网络</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 华龙网2月29日19时20分讯（记者 周晓雪）这几天重庆天气转暖，不少人相约登高爬山，城市里的走走族、跑跑族也越来越多。运动虽好，可还需“量力而行”。今（29）日，重庆第三军医大学西南医院表示，据不完全统计，走走族、跑跑族最常见的抱怨之一就是后跟痛，跟腱炎就是其罪魁祸首。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 运动时跟腱承受巨大负荷 脚跟疼痛可能致跟腱撕裂</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “行走、跑步或跳跃时，跟腱承受着巨大的负荷，甚至是体重的数倍。”国际著名足踝外科专家、西南医院骨科运动医学中心唐康来教授表示，跟腱是人体最强壮的肌腱，由小腿后方腓肠肌和比目鱼肌共同延续融合而成。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在正常健康的跟腱组织中，跟腱在腱鞘内自由地滑动。反复、过度地滑动引起炎性反应，损伤的组织细胞释放出大量的化学物质，导致血管渗透性增加，血细胞渗漏到组织中，肌腱和腱鞘之间摩擦力增加，最终引起疼痛以及肢端的肿胀。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 而跟腱炎的早期症状并不明显，仅仅感到跟部轻度的疼痛或不适感，常常被人忽略。即使早晨起床足跟接触地面时，跟部出现紧张或疼痛，适度活动后即有所减轻，往往不会引起重视。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 但当运动量逐渐增加时，患者就会开始感到跟腱持续疼痛，呈进行性加重。观察到症状变化与持续性使用或活动有关，这点非常重要，如果没有得到合适的治疗，组织的损坏和肌腱炎加重，最终进展为跟腱撕裂。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 运动强度应逐渐过渡 早期发现跟腱炎可改善</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 导致出现跟腱炎的常见原因有哪些呢？唐康来分析，运动前没有合理的热身或牵伸训练、奔跑强度过大或频率太多、持续重复性的跳跃动作或不适合的鞋并没有起到保护的作用，都可能导致跟腱炎。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 唐康来表示，步行、跑步是一种理想的耐力训练，对心脏循环系统产生有益的影响，并能提高能量基础代谢。但在运动前，必须采用一些必要的措施。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “人体几乎可以适应任何环境，运动时应从低强度、低频率的训练到中强度、中频率的运动项目，逐渐进展到为一个平台期，继而过渡到合适的生理适应，最终成为一个自然的过程。”唐康来建议，市民可根据参与运动的内容和特点，改善运动所需的技巧，逐渐增加训练负荷和强度。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 另外，穿戴舒适合脚的鞋或护具、运动前做适当的热身运动，以及通过不同的牵伸训练和深部组织的按摩维持机体的弹性都很重要。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 如果出现跟腱炎，只引起酸胀和疼痛，并没有影响步态或运动状态，唐康来建议穿矫形鞋、抬高足跟，以减轻跟腱的张力，或是进行充分热身活动和常规牵伸训练，也可以改善跑步姿势或训练方式。</span><br />
