<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>颅内高压之平衡治疗</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">对于脑水肿、颅高压的病人强调脱水治疗在我国已形成多年的固定概念，湘雅医院神经外科主任袁贤瑞教授自上个世纪90年代开始对病人实行边脱边补，保持水电解质平衡、维持血液中晶体和胶体平衡，保证能量营养需求的治疗方法，经过10多年探索和总结，在前人基础上，在国内首次提出全面平衡治疗的原则，临床疗效显著，死亡率从当时的大于15%下降到现在的小于2%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 患者何某是来自湘潭市的72岁女性，在当地医院诊断为右额叶病变伽玛刀治疗后伴肿瘤边缘脑水肿。按照教科书多年的教条进行脱水治疗，在当地入院后一直给予甘露醇脱水和对症治疗，住院20多天病情不但没有好转，而且四肢出现凹陷性水肿、肺部感染、低钾低钠低氯血症、低蛋白血症等，病房已下病危通知单。3月10日转入湘雅医院，入院时发现病人体质虚弱，虽然脑水肿明显，但存在明显的脱水综合症，如低血钠低血氯、呕吐、乏力等。如果此时再予脱水，只能越脱水病情越重，病情越重越脱水而形成恶性循环。根据袁贤瑞教授提出的全面平衡治疗原则，果断停用脱水药物，迅速补充水电解质，纠正低血钠和低血钾，补充血浆和白蛋白，使体内的电解质和胶体达到平衡，同时给予静脉营养，供给能量，治疗4到5天左右，精神明显好转，基本可以扶床下地走路，四肢水肿也明显好转，治疗10天后病人水肿完全消退，并能下床活动，饮食起居可以自理，病情明显好转出院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 对于脑出血和重型颅脑损伤患者减轻脑水肿和降低颅内压，渗透性脱水剂是临床治疗的首选。其中甘露醇的应用最为广泛，脱水的目的首先是降低颅内压；其次是希望能够减轻缺血或出血引起的周边组织的继发性损伤；另外还通过纠正这些因素能够改善组织的灌注从而改善预后。据文献资料，绝大部分患者甘露醇的应用都超过了1 周，超过10天达到了51% ，有34.80% 的患者超过了16 天。对颅高压的病人进行脱水治疗是正确的，但应用脱水药物治疗是有指征的，目前国内很多医院特别是基层医院，对病人进行脱水治疗已经超出了正常范围而大大地滥用了，对头部外伤、脑中风或颅内病变的病人不分青红皂白，一律应用脱水药物，直到病人出院为止，部分病人因过分脱水而加重了病情。实际上，大剂量较长时间使用甘露醇及速尿等脱水剂并限制入水量，引起的不良反应在临床上不容忽视，主要有电解质紊乱、高渗昏迷、心肾功能损害、血容量不足等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 袁贤瑞教授提出的全面平衡治疗原则认为，在进行治疗时，应保持病人内环境的平衡，这些平衡包括水电解质的平衡、血液中晶体和胶体的平衡、营养和能量的平衡、激素的平衡、生命体征的平衡等。人体就是一个平衡的生态环境，一旦失衡就会生病，而生病后医生的任务就是恢复病人失衡的生态环境，中医的治疗非常讲究阴阳平衡，而西医的治疗原则也是一样，对于脑水肿，脱了多少水就应及时补充多少水，电解质丢失了，就是及时补充，像患者何某因右额叶占位出现脑水肿，形成颅内压升高，行走困难，如果单纯给予脱水治疗不仅不能达到治疗脑水肿的目的，而且会导致电解质紊乱，低蛋白血症，由此诱发四肢水肿，强行继续给予甘露醇脱水治疗，结果只能是加重低蛋白血症，电解质紊乱，从而造成全身内环境的紊乱，达不到治疗目的。以袁贤瑞教授为主的课题组从10多年探索和上千例病例治疗经验发现，给予病人全面的平衡治疗，能够迅速地纠正水电解质失衡，保持血液胶质和晶体的平衡，大大降低了应用脱水药物带来的各种并发症，促进了病人的康复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 袁贤瑞教授等人指出，目前通过不懈的宣讲，神经外科医生特别是经过专科培训的医生已能充分注意到水电解质的平衡在治疗中的重要意义，但对于能量和营养的平衡还重视不够，对血液中的胶体平衡及激素等方面的平衡只有较高专业水平的人才引起重视。这需要一个过程，只有重视了全面的平衡治疗，病人才会尽快地康复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 袁教授同时提出，对于颅压过高的患者在采用内科平衡治水如不能有效降低颅内压，应及时进行外科处理，如实行去骨瓣减压术等，以免引起脑疝。</span>
</p>
