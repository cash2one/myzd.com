			<div style="text-align:center;">
	<span style="font-size:18px;line-height:2;"><strong>“巧囊”巧治</strong></span>
</div>
<br />
<span style="font-size:14px;line-height:2;"><strong>一、卵巢巧克力样囊肿的主要形成机制</strong></span><br />
<span style="font-size:14px;line-height:2;">1、子宫内膜组织除了存在于子宫内膜以外，还可以存在于身体的其他诸多部位，如卵巢、子宫平滑肌、腹膜、肠系膜、膀胱壁、甲状腺、鼻粘膜、嘴唇粘膜等处，尤其以卵巢和子宫平滑肌层内最为常见，发生在子宫内膜以外的情形都成为子宫内膜（组织）异位，由此产生的相关临床表现统称为子宫内膜异位症。</span><br />
<p>
	<span style="font-size:14px;line-height:2;">2、异位子宫内膜（组织）同样具有正位子宫内膜的每月周期性变化，即增殖、脱落、修复等，经血从阴道排出，而异位的内膜组织发生出血时基本没有正常的排出通道，在局部愈积愈多，形成血液淤积为主体的结节样改变，积血时间久了就会发生变性，颜色似咖啡样，比较粘稠，所以称为“巧克力样囊肿”，简称“巧囊”，这在卵巢相当多见，有些人把它直接叫做巧克力囊肿是不妥的，因为这个囊肿不是巧克力做的，产品也不是巧克力。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;"><strong>二、卵巢巧克力样囊肿的主要危害</strong></span><br />
<span style="font-size:14px;line-height:2;">1、“巧囊”主要是随着月经周期发生新的出血，导致囊腔内张力增大，囊壁上的神经末梢受到张力影响而发生疼痛；</span><br />
<span style="font-size:14px;line-height:2;">2、“巧囊”形成占位效应可以阻碍或压迫输卵管，影响卵泡受精或者受精卵回归子宫腔内导致不孕。</span><br />
<span style="font-size:14px;line-height:2;">3、“巧囊”自己或者带着卵巢发生扭转，引起急性腹痛，甚至卵巢坏死。</span><br />
<p>
	<span style="font-size:14px;line-height:2;">4、“巧囊”还可掩盖某些合并的卵巢肿瘤，延误诊治。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;"><strong>三、卵巢巧克力样囊肿的主要外科治疗方法</strong></span><br />
<span style="font-size:14px;line-height:2;">既然巧克力样囊肿是由异位的子宫内膜组织出血引起的，那么原理上只要内膜丧失周期性变化的能力便可达到“止血”治疗的效果，如进入绝经期，内膜不再受雌激素的影响；或者在绝经期前异位内膜被破坏，丧失活性。如果想通过全身药物加以控制，势必对正常的子宫内膜的生理性周期变化也会产生影响，显然不是最佳选择。事实也证明药物治疗往往收效有限，对大多数患者而言，外科性治疗干预是必不可少的。从外科治疗技术的演进历史来看，微创化是发展的必然趋势。</span><br />
<span style="font-size:14px;line-height:2;">1、开放性手术，剖开腹腔，在肉眼直视下以剥离卵巢”巧囊“，多数情况下囊肿可以顺利剥除，但是也有连同同侧卵巢一起切除的。</span><br />
<span style="font-size:14px;line-height:2;">2、腹腔镜下手术，不剖开腹腔，只在腹壁（俗称肚皮）上开2~3个直径约1.5~2.0cm的孔洞，利用电视监视显像，指导腹腔镜器械完成囊肿剥除或者连同卵巢一并摘除。属于微创治疗，是目前仍较流行的微创技术。</span><br />
<p>
	<span style="font-size:14px;line-height:2;">3、超声引导下经皮穿刺抽吸积血联合硬化剂硬化治疗，这种方法也属于手术范畴。是在超声影像的引导和监视下，利用直径1.1mm的金属针准确穿刺到“巧囊”内，穿刺路径可以是经下腹部或者经阴道（视患者已婚否和囊肿的位置高低以及穿刺路径上的障碍大小等因素而定），先把长期淤积的陈旧性血液抽吸掉，用生理盐水充分清洗囊腔后再用无水乙醇（现已少用）或聚桂醇（硬化效果好，而且没有酒精刺激性疼痛的弊端）冲洗囊腔，使得囊腔的内皮细胞发生坏死，丧失再”出血“的能力。硬化治疗是不将囊肿剥除拿出体外的，而是使得囊肿内膜坏死，坏死组织逐渐被机体吸收达到治疗目的。穿刺硬化治疗的最大优势是真正的微创，在腹壁、囊肿壁上仅仅造成1.1mm的细微穿刺针眼，对聚桂醇硬化剂对卵巢组织的损伤十分轻微。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;"><strong>四、巧囊穿刺硬化治疗的关键技术环节</strong></span><br />
<span style="font-size:14px;line-height:2;">1、安全环节：穿刺路径分为经腹部和经阴道，经腹部穿刺一定要避开肠管和髂血管。经阴道穿刺一定要避开子宫动静脉和卵巢动脉，使用较好的超声设备和彩色多普勒超声严格检查，设计穿刺路径是非常重要的。</span><br />
<span style="font-size:14px;line-height:2;">2、疗效环节：“巧囊”内都是多时的血液，淤积的血液中水分被吸收掉，因此积血的浓度不断增加，形成浓稠的、甚至板结的“血泥”。穿刺时首先要彻底清洗掉这些血泥才能使用硬化剂，否则无水酒精一遇到血液就发生凝集，穿刺针被堵塞，无法抽吸，很难形成疗效。其次，注射硬化剂时不要让硬化剂静态地停留在囊腔内，这样不利于硬化剂与囊壁内膜组织充分地接触，发挥硬化作用，要让硬化剂流动起来，充分硬化囊壁。不能小瞧这样的技术改良，如果不按照这样的方法去做，治疗效果是要打折扣的。</span><br />
<p>
	<span style="font-size:14px;line-height:2;">大量的病例数据表明，在保持硬化治疗微创优势的前提下，通过技术改良和革新，完全可以使6~7以下的”巧囊“达到1次硬化而治愈的效果（囊肿缩小80%以上，甚至完全消失）。7~8以上的囊肿通常需要2次硬化治疗。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;"><strong>五、卵巢 “巧囊”治疗后的复发</strong></span><br />
<p>
	<span style="font-size:14px;line-height:2;">无论是开放性手术切除，还是腹腔镜下摘除，抑或是超声引导下的硬化治疗，”巧囊“都存在复发现象。但是，需要指出的是治疗过的囊肿再生长称之为复发，而原本存在的较小的甚或看不见的微小的囊肿”胚芽“持续生长则不能算作复发，因为它们根本没有受过治疗。开放性手术或者腹腔镜手术摘除可以存在内膜没有被清理干净，存在残留，因而复发。穿刺硬化治疗可以因囊肿内膜没有被硬化透彻而复发。那么不论是何种方法治疗后的囊肿复发，再次治疗时无疑应以微创为优先，穿刺硬化治疗的微创性、容易重复实施性的优势便凸显出来。如果经过2~3次硬化治疗后仍然复发，提示有必要联合使用抑制内膜周期性增殖、分泌的药物，即“停经”措施加以协同巩固疗效。</span>
</p>
