<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>（一）瓣膜病变及影响</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp;</span><span style="font-size:14px;font-family:'Microsoft YaHei';">在心房与心室之间，心室与大动脉之间都会有一个象阀门一样的结构，叫做心脏瓣膜。我们常把瓣膜比做房子的门。这些“门”也有门框，门扇，有的还有拉索，不过他们可精致多了。心脏上的“门”开放时会开的非常通畅，血流会毫无阻碍也通过这些门；他们关闭时，会关的十分严密，不能让通过的血流倒流回去。但是这些“门”用的久了，在各种因素的作用下，会发生病变，叫做瓣膜病。瓣膜的病变有两种，一种是狭窄，也就是这些“门”开放时不能完全打开，血液流过时不通畅；另一种病变是反流，也就是这些“门”关闭时不能完全闭拢，血流会沿原路返回。瓣膜狭窄时，心脏要用更多更大力量才能让血液通过狭窄的瓣膜，瓣膜反流时，心脏每次收缩前要准备更多的血才能抵消瓣膜反流的影响。因此，无论是瓣膜狭窄还是瓣膜反流，最终的结果都是心脏要干更多的活，长此以往，心脏不能承担过重的任务，病人就会出现心力衰竭的表现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>（二）瓣膜修复</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp;严重的瓣膜病变需要手术治疗。药物治疗只能临时缓解症状，不能解决根本问题。手术治疗有两种手段：修复与替换。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp;修复就是把坏的“门”修理一下再用，“门”还是自己的“门”。替换就是把原来的“门”卸掉，重新换一个人造的“门”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp;应该说，瓣膜修复是最理想的治疗手段。因为修复保留了自己的瓣膜，在二尖瓣和三尖瓣位，还保留了瓣膜的附属结构，这对保存损害的心脏功能非常有益，尤其是那些心脏功能严重受损的病人。其实，瓣膜修复的最大好处在于，修复的瓣膜不用终生抗凝，不用定期监测凝血，比起瓣膜替换，有更好的生活质量。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp; &nbsp;所以说，对于能修复的瓣膜，要尽量修复他，而不是换掉。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>（</strong><strong>三）生物瓣与机械瓣</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">虽然瓣膜修复比瓣膜替换好的多。但总有一些瓣膜不适合修复。那么，这些不适合修复的瓣膜最终需要替换。瓣膜替换术所用的瓣膜主要有两种，一种是机械瓣，一种是生物瓣。机械瓣可以认为用几十年不会坏，但是机械瓣有局部长血栓的风险，所以换了机械瓣得终生服用抗凝药，抗凝药用的少了不管用，用得多了就会造成出血。生物瓣膜术后抗凝3－6个月就够了，但是，生物瓣存在术后毁损情况，一般认为，生物瓣膜用10－15年就要坏了，需要再次手术。所以，一般是老年人选择生物瓣膜。当然，随着技术的进步，生物瓣膜的使用年限也在提高，但其耐受性仍比不过机械瓣膜。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><strong>（四）修复还是替换</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">每人病人都是一个个体，都要根据他自身的解剖条件决定最佳的治疗手段。另外，不同的外科医生也会有偏向，应该与自己的外科医生一同讨论，协商，制定最适合自己的治疗方式。</span><br />
