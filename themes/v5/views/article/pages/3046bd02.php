			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>2013年亚太微创外科年会-疝外科专题发言</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 刚刚结束的2013年度亚太内镜与腹腔镜医师年会上，我院疝外科副主任医师王明刚应邀进行了会议发言，在发言中报告了我院疝外科近五年来开展腹腔镜疝修补术的相关数据和经验，受到与会专家的高度关注和好评。我们所开展的手术种类和手术量在亚太地区均已达到领先地位，积累了丰富的临床经验。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3046/5.png" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 疝外科2007年底成立之后发展迅速，很快在国内就占据了领先地位，但是陈杰主任并没有停滞不前，而是预见到微创腹腔镜技术在疝外科应用的广阔前景。2008年在院领导支持下选派王明刚医师前往上海瑞金医院微创中心专门进修腹腔镜疝修补技术，2009年初进修结束后在科主任的支持下开始开展腹腔镜下疝修补手术，第一年就完成了352台的腹腔镜疝修补手术，到目前全科已经开展了4000余台，开展术式包括腹腔镜儿童、成人腹股沟疝、切口疝、造口疝、食道裂孔疝等十余个类型。同时疝外科在科室内逐步推行腹腔镜技术普及化，要求全科医师都要掌握这项技术术，目前全科主治医师以上的医生均已经掌握腹腔镜疝修补技术，可以熟练操作和独立完成绝大多数的手术类型，其中王明刚主任指导的学生朱熠林医师腹腔镜腹股沟疝手术视频还获得的医师协会视频大赛的一等奖，为院争光。目前我院疝外科手术中腹腔镜疝修补术比例已经突破60%，短短五年已经成为我科的主要手术方式。由于腹腔镜疝修补手术损伤小、恢复快、复发低，更得到了广大患者的认可和接受，受到了患者的广泛好评，取得了良好的经济效益和社会效益的双赢结果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 腹腔镜疝修补技术被2009年欧洲疝治疗指南作为首选术式推荐，目前发达国家腹腔镜疝修补手术比例在17-35%不等，在印度大型医院更是达到了60%以上，我国目前的初步统计仅仅在3%左右。因此这一技术急需在国内推广和培训。2012年在中华医学会腔镜外科学组和疝外科学组共同努力下组建了大中华腔镜疝外科学院，王明刚医生当选学院秘书长，全面负责学院的讲师管理、培训中心组建、以及培训课程制定等工作，在全国范围内开展腹腔镜疝修补技术的继续教育、培训教学、手术带教，力求更多的医生接受并掌握这一先进技术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3046/6.png" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近期在学组和学院院长李建文教授的指导下王明刚医师作为第二执笔人，组织参与了中国腔镜腹股沟疝修补手术操作指南的撰写，并于2013年7月发表在中国实用外科杂志。指南的发表标志着腹腔镜疝修补手术规范化推广进入全新的阶段。我院疝外科医生在全国范围内广泛的参与腹腔镜疝修补的培训教学工作，大大提升了医院和科室的学术地位和专科知名度，每年吸引大批各地医师甚至国外同道前来我院参观进修。此外，五年来疝外科共计发表腹腔镜疝修补技术相关学术论文20余篇，腹腔镜疝相关SCI论文一篇，这一成绩也是国内首屈一指的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 凭借五年来疝外科在腹腔镜疝修补技术方面的大力发展和取得的丰硕成果，不但巩固了北京朝阳医院疝外科在本专业国内的领先地位，同时依托先进技术的应用，使得科室的学术水平有了很大的提升，提高了医院、科室的社会知名度，确立了学术、学科地位，为今后更大的发展铺垫了道路。</span><br />
