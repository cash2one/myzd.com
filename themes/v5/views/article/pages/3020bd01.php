			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>一时“失足”，如何不成千古恨</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单位篮球比赛，小王一不小心又扭了脚，他像往常一样抹了点跌打损伤药油，却发现第二天踝关节越来越肿，两周后都还下不了地。他又急又怕，拄着双拐来到门诊，深怕自己象刘翔或姚明一样，肌腱或骨头出了问题。对于运动损伤与康复，热爱运动的小王却几乎一无所知。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. 平时走路及运动时做造成的脚扭伤是否一样？如何初步判断其严重程度？轻度损伤如何处理？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不论是行走、上下楼梯还是运动时，脚扭伤都是我们最常遇到的外伤。据统计，在美国，每天每一万人就有一个人会扭伤脚。走路与运动时造成的伤情是否相同呢？对于同一个人来说，跑跳时比行走时速度更快，冲力更大，受伤也就会更严重；但是，对不同个体来说，相同的外力造成的损伤可能差别巨大。一个运动员与一个经常不活动的老太太在下楼梯时扭了脚，运动员可能休息一下，贴块膏药就活动如常了；老太太却可能“伤筋动骨”，卧床数月却难以康复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果在速度不快的情况下我们扭伤了脚，脚不肿也不是很痛，可能我们只是软组织扭伤，并未伤及韧带；而当我们在跑跳中扭伤了脚，脚马上肿了起来，痛得不能站立行走，那我们可能扭伤了韧带。伤情比较严重，可能就要去看医生了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">若是轻度损伤，我们通常可以按RICE原则处理：即休息（Rest）、冷敷（Ice）、加压包扎（Compression）、抬高患肢（Elevation）。通常在两周内是可以复原的，年轻人则康复得更快，往往休息一两天就又可跑跳了。但在恢复期间如疼痛很久不消，不能下地，损伤那一定比较严重，不能轻视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.如何发现韧带是否断裂？当时怎么处理？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当我们脚扭伤后，如何才知道韧带是否断裂呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果是急性损伤，医生可能会给我们进行B超或MR检查，B超或MR对于诊断韧带或软组织损伤是较好的无创性手段。一般来说，B超经济快速，在门诊时更常被用到。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果我们在扭伤脚后，并没有得到及时的诊疗；拖了一段时间后，可能伤处也不是很痛了，也能慢慢走路了；再过一段时间，也能跑跳了，只是在走不平路时觉得脚发软，每年可能还会扭伤一两次。这时我们往往认为，伤已经好了，没事了，不必去看医生了。殊不知，这种情况往往就是韧带损伤的继发表现——踝关节不稳。如果不治疗，踝关节周围的韧带与关节软骨将较快出现磨损与退变。这就好比，当机器的皮带松动后不予以处理，轴承及发动机可能也会较快出现问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊断慢性踝关节不稳的方法有很多种，简单易行的是理学检查与X光。当我们发现患足比健足更容易内翻或如抽屉般（小腿固定）被前移时，可能我们的踝关节外侧韧带已断裂了。同理，我们也可判断内侧韧带是否损伤。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对待急性韧带损伤，与所有的急性运动损伤一样，初步处理原则是上述的RICE原则；在判断损伤的严重程度、病人的职业与要求后，可分别进行康复保守治疗或手术治疗。在慢性损伤时，我们就必须到医院去做相关的检查，准确诊断后及时治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.对于韧带断裂，治疗麻烦吗？如何康复？以后还能进行体育运动吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">按韧带损伤的严重程度，可将其分为三度。通常，Ⅰ到Ⅱ度的损伤可康复治疗好转。如果是Ⅲ度损伤，在系统保守治疗无效时，可采取手术治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">韧带、肌腱就是我们常说的“伤筋动骨”中的筋，如果完全断裂，手术修补后需要三个月才能完全康复。在这三个月中，康复可按三个阶段进行：在急性期，踝关节的出血肿胀还在继续，此时，按RICE原则处理；在康复前期，血肿正在机化，需要的是逐渐使韧带牢固的连接，在保护韧带等长的前提下有序锻炼，逐步消除肿胀；在康复期，韧带间已被成纤维细胞连接，比较牢固，需要积极的康复锻炼。在一个半月后，我们往往已可借助支具或在支具保护下下地行走，三月后我们可恢复到基本正常的活动水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于韧带损伤后的患者，在康复后，是完全可以进行体育运动的。如巴西的罗纳尔多，在股四头肌腱断裂后，经手术治疗与康复锻炼，很快又恢复到世界顶级运动员的水平；但对于韧带损伤后重返赛场的运动员或爱好者，在比赛或运动前，热身准备必须充分，初次运动必须量力而行，不可太过疲劳。如小王的脚扭伤，在经B超与MR诊断后，为踝关节外侧韧带的Ⅲ度损伤，最后，采取了手术治疗，三月后又恢复到正常的运动水平。</span>
</p>
