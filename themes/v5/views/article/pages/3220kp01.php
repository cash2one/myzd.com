			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>慢性便秘与甲状腺功能减退</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;典型病例：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;一位年过半百的女士，因为长期便秘来就诊。大便由原来的一天一次，渐渐减少为3天或4天一次，有时甚至一周或2周一次，并伴有食欲减退、腹胀、嗳气。为此看过不少医生，也进行过胃镜结肠镜检查，没有异常发现，被诊断为功能性消化不良、慢性便秘、肠道功能紊乱等。常服用泻药治疗，帮助通便。近来，感觉以前用的通便药也失灵了，没有以前见效了，心情非常郁闷烦躁。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">• &nbsp;经过仔细检查发现，发现甲状腺功能减退。给予相应治疗，原来的便秘、食欲减退、嗳气、腹胀也渐渐好转了，心情也渐渐开朗起来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 那么甲状腺功能减退是什么样的一种疾病呢？为什么会引起消化道的症状？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;甲状腺是什么器官呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;甲状腺是生长在喉部甲状软骨上的一种腺体，属于内分泌器官。虽然体积小，能耐却很大。当你解开领口，摸摸脖子正中皮下有一个硬硬的骨性小包，医学上称为甲状软骨，那里就是甲状腺借居的地方。别看甲状腺极小，重不过30克，能耐却大着呢！它的主要使命就是制造甲状腺激素，包括三碘甲状腺原氨酸（T3）与甲状腺素（T4）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;与其他激素一样，甲状腺激素对于人体生命正常运转至关重要，如促进蛋白质、脂肪、碳水化合物以及水、盐等物质代谢，保障大脑等神经系统发育良好，促进产热并维持正常体温等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;由于某种原因（如炎症或肿瘤侵犯甲状腺），造成甲状腺激素“减产”，或因外周组织对甲状腺激素的敏感性降低，继而导致一系列代谢性功能减退，这就是甲状腺功能减退，简称甲减。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;人体各个系统的生理功能必然下降，消化系统也不例外，会出现胃肠道功能减退、蠕动缓慢。伴随而来的便有食欲下降、腹胀以及便秘等症状。特别是便秘，经常较为顽固，一般治疗便秘的办法往往难以奏效。前面提到的那位女士，便秘长达3年之久，历经多位医生治疗而无效，直到确诊为甲减，给予治疗，才挣脱了便秘的困扰。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;甲状腺功能减退常见的临床症状有哪些呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;甲状腺功能减退，女性好发，尤其是40岁以上的女性好发，临床常表现为面颊及眼睑虚肿，面色苍白，呈现贫血貌或陈旧性象牙色，眼睑下垂导致眼裂狭窄如缝，鼻子、嘴唇增厚，舌头大而发音不清，说话缓慢，音调低弱，头发干燥、稀疏，部分患者的眉毛与睫毛脱落。表情淡漠、愚蠢、呆板甚至假面具样。此外，出汗少，怕冷，体温偏低，精神萎靡、疲乏、嗜睡，少言懒语，皮肤干燥。记忆力减退，注重力不集中，智力降低，重者精神变态甚至成天昏昏欲睡。脉搏缓慢，心音弱，血压低。食欲减退，腹胀，便秘。性欲减退，月经过多，病久者出现闭经。不过，上面所说的是甲减的典型表现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 由于甲状腺功能减退起病隐匿，发展缓慢，症状五花八门、光怪陆离，常常就诊于内分泌外的其他科室，医生患者常处于“雾里看花”的境地，漏诊误诊者居多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;所以提醒患者，当出现食欲下降、嗳气，尤其似乎顽固性便秘时，除了要及时到消化内科排查消化系统疾病以外，要想到甲状腺功能减退的问题，及时检查甲状腺功能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;如果诊断为甲状腺功能减退，也不要害怕焦虑，因为这是一个可治疾病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;治疗原则是补充足量的甲状腺激素。目前临床上应用的制剂主要有两种，一种是甲状腺片（含T3、T4），另一种叫左旋甲状腺素（含T4），须在医生指导下服用，不可擅自行事。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 用药期间要定期到医院检查，根据情况调整用药剂量。有一点需要牢记，须坚持服药，不可中断。假如不坚持治疗或中断治疗，病情会加重，还有发生黏液性水肿昏迷的危险，那可就危及生命了。</span>
</p>
