			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>关注孩子肚子上的怪包—儿童疝</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">现如今孩子都是一个，全家上下对孩子的关注那是无微不至，有些家长会发现孩子在哭闹的时候小肚子下面会出现一个鼓包，到医院检查医生会告诉家长这是儿童疝，在这里我想的和大家聊聊孩子小肚子下面的这个怪包到底是个啥情况。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 先说说症状，这个怪包一般都是家长在孩子哭闹、洗澡的时候无意间发现，表现为小肚子下面，靠近大腿根部一个突出的鼓包，一般很柔软，大小不一，用手一推大多可以退回去，孩子平躺的时候鼓包也会消失，还有相当一部分男孩子的鼓包可以向下突出进入阴囊。极个别情况会突然出现而且鼓包不能推回，这是个危险信号，必须尽快医院就诊。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼓包是什么呢？儿童疝！就是俗称的小肠串气。儿童之所以得疝气的原因是先天发育问题，儿童疝多见于早产儿和男孩，女孩子也有，相对较少。究其原因是因为男孩子在出生前1个月的时候，睾丸会从腹腔沿着腹股沟区下降至阴囊内，随之形成的腹膜孔洞叫鞘状突，会自行闭合，要是鞘状突闭合不良或者根本就没有闭合，在腹股沟区就会形成一个先天的孔洞，那么孩子出生后，伴随着身体的发育就会有腹腔内的肠管或者网膜从这个洞钻出来，在小肚子下面表现为一个以推回去的鼓包，这就是儿童疝，在孩子哭闹、站立、下蹲用力的时候腹压增高，鼓包会比较明显。女孩子在哪个部位是子宫圆韧带，个别情况也会发生疝。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 儿童疝的发病年龄不定，伴随着孩子的生长发育，几岁都有可能，还有一部分可以到成年后发病。发现孩子有这样的情况应该尽快到医院找医生予以确诊，对于儿童疝目前的治疗原则是1岁半以内不严重的可以保守治疗，就是自己制作一个类似于紧身小短裤样的疝气带给孩子穿上，在孩子哭闹时家长有意识的用手压迫一下鼓包的位置，予以保护，一部分孩子有自愈的可能。要是超过1岁半没有自愈或者鼓包又加重，那就要手术治疗了。</span>
</p>
