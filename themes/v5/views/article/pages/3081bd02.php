</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">解放军总医院泌尿外科主任医师张旭</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">张旭，男，中国人民解放军总医院泌尿外科主任，教授、主任医师，硕士生、博士生导师。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 张旭教授长期致力于泌尿外科手术技术研究，90年代提出并成功开展肾实质肾盂分层切口治疗复杂性肾鹿角型结石的方法，对尿道下裂修补技术进行了较系统研究，开展的新技术有改良乙状结肠球形正位膀胱术、改良十一肋间切口、保留性神经无出血前列腺癌根治术等。2000年后主要致力于腹腔镜技术在泌尿外科的应用研究及其推广，创立了以后腹膜入路为特色的腹腔镜手术技术，目前已经完成30余种、近4000例泌尿外科腹腔镜手术，并以手术演示和专题报告的形式在全国31个省市自治区的大学附属医院和核心医疗机构以及新加坡中央医院等东南亚国家的知名医院进行推广应用。通过技术攻关和推广应用，使部分泌尿外科后腹腔镜术式成为国内通用术式，迅速提高了我国泌尿外科腹腔镜水平，并赢得了国际微创泌尿外科学术界的认同和尊重，多次以特邀专家身份出席国际泌尿外科学术会议并作专题报告和手术演示，该项技术创新获2006年度国家科技进步二等奖，2007年度中华医学会泌尿外科分会金膀胱镜奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 张旭教授对泌尿系统疾病腹腔镜手术治疗有突出贡献，尤其在肾上腺疾病、乳糜尿、感染性无功能肾和肾癌等的腹腔镜治疗方面取得突破性创新，其中，以1000余例后腹腔镜肾上腺手术为基础，提“解剖性后腹腔镜肾上腺手术”，有效地解决了后腹腔镜手术中肾上腺显露和肾上腺血管处理的难题，改变了以往腹腔镜肾上腺手术仅能在国内外少数医疗机构开展的局面，使该术式易于被广大泌尿外科医生掌握，为广大肾上腺疾病患者提供了安全、简洁和有效的治疗手段，该项研究成果获2008年湖北省科学技术进步一等奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2009年开始研究单孔后腹腔镜技术在泌尿外科的应用，单孔腹腔镜技术是用腹腔镜器械通过皮肤一个小切口完成复杂手术，具有创伤小的优点，单孔后腹腔镜技术结合后腹膜入路和单孔技术的优势，使该项技术紧跟国际先进水平，目前已完成30余例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">现任中华医学会泌尿外科分会腔道泌尿外科学组副主任委员，湖北省医学会腔道泌尿外科分会主任委员，《中华腔镜外科杂志（电子版）》副总编，应邀担任Journal of Urology，Urology，Journal of Endourology，International Brazil Journal of Urology和Indian Journal of Urology 等国际专业泌尿外科杂志的特约审稿人。主译了《泌尿外科手术图谱》，主编《男科手术学》，《泌尿系内镜检查》，《腹腔镜泌尿外科手术学》等专著，参编专著十余部 。共培养硕士博士生40余名，博士后2名。获国家、省部级科技进步奖9项，其中《腹腔镜技术在泌尿外科的应用研究及推广》获国家科技进步二等奖。承担数项国家和卫生部课题，共获科研经费近500余万元。以第一作者和通迅作者发表SCI或ISTP收录期刊论著29篇，中文核心期刊近100篇。</span>
</p>
