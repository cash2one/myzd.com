		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>《CRI健康中国》特别报道—— 2012年全国泌尿外科微创新技术研讨会在京召开</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">初夏的北京，暖意融融。5月12日上午，2012年全国泌尿外科微创新技术研讨会在解放军301医院拉开了帷幕。没有隆重的开幕仪式，没有复杂的会议流程，研讨会采用圆桌会的形式，为参会的四百多位海内外医学专家创造了一个开放、融洽的交流平台。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 研讨会的主要内容是泌尿外科微创新技术的手术演示与观摩。来自欧洲和美洲的五位专家和二十位国内微创泌尿外科领域著名专家做现场手术演示。其中最重要的一位手术演示专家正是中国泌尿外科腹腔镜微创手术的泰斗级人物、解放军总医院泌尿外科主任张旭。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 说起张旭，国内国外同行没有不服气的；正是他，用时一个半小时完成腹腔镜前列腺根治切除术这一“不可能完成的任务”令外国专家叹为观止；也正因为他，享有"全球泌尿外科腹腔镜大佬俱乐部"美誉的欧洲泌尿外科腹腔镜学术大会将首次在欧美之外的国度召开——2013年度的第十届欧洲泌尿外科腹腔镜学术大会的举办地点被定在中国人民解放军总医院，张旭担任大会主席。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
