			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“不放弃”式抢救成功治疗血管炎消化道大出血患者</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个半月来，一位27岁患系统性小血管炎的女性患者的病情揪动着北大医院多个科室医护人员的心跳。她患有致死性消化道出血，经过高频度、不放弃式抢救，依靠坚强的毅力战胜了病魔。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">她的抢救过程危机重重，一波三折，不仅是高超的救治技术的全面展现，更突出体现了医务人员对于生命的敬畏与珍惜。笔者摘录出抢救过程的几个感人情节。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>多学科在第一时间会诊病情</strong></span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2月25日，该患者因消化道大出血收入普通外科一病房，血色素明显下降，仅为5.4g/dl，不足正常人的一半，因病情危重，在住院第二天转入重症监护室，并做了第一次肠系膜血管造影。随后我院消化科经验丰富的刘建湘副主任医师通过行结肠镜检查，发现结肠内弥漫性小血管持续渗血。在第一时间由消化科王化虹主任、普通外科杨尹默教授、重症监护室王东信教授和彭兵副主任医师、肾内科副主任周福德主任医师、张宜苗大夫等共同会诊病情，会诊结论是血管炎活动导致消化道出血，给予小剂量激素冲击治疗。从此以后，只要是病情有变化，无论是节假日还是夜间，多学科的会诊都能够及时到位。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>重症监护室是抢救的强大后盾</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">参加过抢救的所有人员都有这样的感慨，如果不是在重症监护室，如果不是这里医护人员及时的支持，这位患者的生命早都结束了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2月19日凌晨开始，患者便血加重，夜间出血2000多毫升。早晨医生交接班之后，在不足一个小时之内又出了将近2000毫升，病人随即进入到神志恍惚、休克、接近死亡状态。护士们给予了最大速度的输血、呼吸机辅助呼吸、升压药物回升血压，患者被从死亡线上暂时拉回来了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>普外科医生的担当</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">普外科杨尹默教授的团队和肾内科周福德主任马上到达了重症监护室共同会诊病情，商讨抢救方案。杨教授说，“我要上台给她做剖腹探查手术，否则她会死的”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在当前医患紧张的环境下，在这种病人随时有死亡可能的情况下，敢于做这种手术的普外科医生不多了。他的决定让在场人员非常感动，其他医生能够做的就是做好配合工作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在外监医生的紧密配合之下，患者被紧急送到了手术室。术中切开空肠后请消化科刘建湘大夫行全小肠镜检查，发现空回肠交界处可见一处活动性出血病灶，遂</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">切除出血段小肠约20厘米，手术顺利，患者平安返回重症监护室。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>再次致死性出血，她还能活吗</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经过这次开腹探查手术后，多数医生都认为患者的病情应该逐步好转了。可是，患者的病情化没有按照医生的预想状态发展。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在2-3个小时之后再次出血2000多毫升，患者随即又进入休克状态。经过生命支持治疗，患者血压稳定后，多个学科专家共同讨论认为，患者太年轻，如果就此离开人世太可惜了，我们还是不能放弃、还得救。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">专家们一致认为，应该再次请血管介入科行肠系膜血管造影检查。当时已是下班时间，我们就通知血管介入科的相关人员从家赶到医院，再次次行肠系膜血管造影，发现远端回肠有活动性出血至肠腔，予以钢圈栓塞。术后患者消化道出血量</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有所减少。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>重症病人抢救离不开的肾内科</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一位资深的老专家曾经说过，所有疾病在危重阶段常常会出现肾脏问题。本例患者的原发疾病是原发性小血管炎，北大医院肾内科对本病的治疗水平处于国内领先地位，因此肾内科医生积极参与方案的制定。从最初的根据病情的临床判断制定治疗方案、到病理明确诊断为血管炎肠道受累之后的精准性治疗，以及患者住院治疗的收尾工作，都转化为肾内科的任务。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在外科监护室给予血浆置换治疗，每1-2天一次，7次血浆置换后，患者血便有减少趋势，每日排血便500毫升左右，随后转入肾内科继续治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">面对历经磨难拯救而来的年轻而宝贵的生命，肾内科医生继续严密观察患者病情变化，及时调整治理方案。此后疾病进入僵持阶段，虽然出血量较前减少，但每日仍需输血维持血色素稳定，患者及家属曾一度伤心绝望，主管医生特意周末赶来给患者和家属做思想工作、鼓励患者，增强患者战胜疾病的信心。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">功夫不负有心人，患者消化道出血终于逐渐减少至停止。至今，已连续10天无消化道出血，患者和家属的脸上终于出现了久违的笑容。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正如周福德主任所言，此次急危重患者的抢救过程堪称是“不放弃”式抢救。患者数次徘徊在生死边缘，医生稍有迟疑可能就会断送患者的年轻生命。回顾救治经过，就像一场马拉松，不允许一刻的停歇，更像一次长跑接力，在普外科、外科监护室、消化科、介入血管外科、肾内科、皮科、烧伤科等多个科室的通力合作下，患者的生命被小心翼翼的传递着，直至安全达到治疗目标。这次危重症抢救，北大医院的医生表现出了令人称赞的专业素养，传承了老一辈建院时树立的优良传统。</span><br />
