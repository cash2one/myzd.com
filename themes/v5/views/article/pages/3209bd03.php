<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">嘉宾：解放军总装备部总医院306医院神经外科主任医师赵全军 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 概要：据相关数据统计显示，中国癫痫病患者目前约有900万，其中每年有40万的新增病例。癫痫让患病的儿童失去了天真浪漫的童年生活；让很多处于结婚生育年龄的青年变得无奈；让很多老年人生活不再平静……癫痫病是怎样的一种疾病，有哪些症状表现，怎样诊断？癫痫的外科手术治疗可以解决哪些问题？《健康中国》特邀解放军306医院神经外科主任医师、立体定向及功能神经疾病诊治中心主任赵全军教授做客直播间，和您说说癫痫的诊断和治疗。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 嘉宾简介：赵全军，主任医师，教授，博士，研究生导师。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 北京306医院神经外科 立体定向及功能神经疾病诊治中心主任 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 擅 长: 癫痫、原发性癫痫、继发性癫痫、复杂癫痫发作时的急救措施；小儿脑瘫、帕金森、扭转痉挛、小脑萎缩、脑出血后遗症、脑梗塞后遗症、肌张力障碍、遗传性共济失调、酒精依赖的功能神经外科治疗。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 用术语来讲，癫痫就是人脑神经元大面积同步化放电造成的一组临床症候群。通俗的说，人脑像一个社会，神经元就像社会中的每一个人。在社会当中，每人都各有职责，这样社会才会和谐发展。当癫痫发作的时候，人脑神经元就不像和谐社会那样各干各的事情了，它们大面积连接起来，同步化地进行放电，相当于社会里一群人交集起来同时在做违反社会的事情，引起社会骚乱，在人脑里面，就会引起人体机能的一些紊乱。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫最常见的表现就是意识丧失、肢体抽搐，老百姓管这叫羊角风。因为在抽搐之前，很多人都尖叫一声，尖叫就像羊急的时候发出的声音。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫遗传机率只有5% </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫主要是后天引起，遗传概率很小。据统计，有癫痫病的父母，生下来的小孩只有5%患有癫痫，如果在生活中再加以注意和防护，几乎是不可能遗传给下一代的，更别提隔代遗传了。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫临床表现分为三大类 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 按照国际抗癫痫联盟的分类，癫痫的临床发作分为三类。一类是急躁性发作，一类是全面性发作，还有一类是不能分类的发作。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 急躁性发作就是往往病人神志是清楚的，没有意识丧失，但是他会感觉到身体的某个部位不正常，比如有过电感、麻木感，这叫单纯感觉性的发作。还有一些急躁性发作，一开始是急躁的，但很快又拓展到全身，也叫杰克逊发作，这也是急躁性发作的一种。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 全面性发作，意识丧失，并且什么都不知道。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 不能分类的发作，没有办法区别他到底是全面性的，还是急躁性的，定位不能分类。有些病人发作起来就是头很疼，但又定位不了到底是哪一块疼，没有急躁性的特征。但他意识又不丧失，又不是全面性发作，这种把它定为不能分类的发作。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 用24小时的动态脑电图来鉴定是否得了癫痫 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 鉴定是否得了癫痫，最直接的方法是做脑电图。癫痫病人的脑电波里，能发现癫痫特有的波形，比如箭波、激波或者急慢波，以及其他的癫痫病特有的一些波形。如果没有发现特有波形，也并不排除癫痫病，可能因为检查时间短，波形没来得及发放。一般建议做24小时动态脑电图，持续监测24小时，比较容易发现癫痫波并作出诊断。除此之外，还要做一些其他检查，如磁共震，看看脑子里有没有器质性的毛病等。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 儿童期属癫痫高峰期 家长应提前预防 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 儿童期和老年期是癫痫发病的高峰期。儿童为什么好发？因为儿童脑功能不完善，容易受外界干扰，如说出生的时候，难产就会造成婴幼儿脑缺血、缺氧，功能受到影响容易发生癫痫； </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 还有一些外伤，比如孩子刚学走路的时候，可能的磕磕碰碰，也容易造成癫痫； </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 再次，小孩感冒发烧到40多度，也会造成神经元的损伤。因为孩子的脑神经元还未完全成熟，可能一点点高热就会造成损伤； </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 还有5%的病人有遗传。有遗传倾向、父母有癫痫病的孩子，在饮食方面，应少吃一些刺激性食物，尽可能避免癫痫发作触发因素。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫发作时如何急救 避免病人激发损伤 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 病人癫痫发作时，呼吸道可能有痰，痰堵塞可能会造成进一步缺氧，这时，一定要把病人侧过来，让嘴里的东西能流出，不能让癫痫病人口里白沫吸到气管里面去。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 其次，避免病人舌头咬伤。用纸、手绢之类的东西，塞到病人嘴里，让病人牙咬着软的东西，而不会咬自己的舌头。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 另外注意磕碰，癫痫发作的时候，如果周围有利器，如铁栏杆等，病人很强烈的运动，一旦撞上可能会受到一些损伤。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 有些病人癫痫发作已为常态，不过也不要太紧张，癫痫发作的持续时间不会超过5分钟，如超过5分钟就可能成为癫痫持续状态，就必须进医院，五分钟之内自行清醒的病人，不需要过多地治疗。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫手术的分类及治疗方法 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫手术分三类 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 一、直接处理癫痫病灶。通过磁共振检查、脑电图，发现局限性的病灶。局限性的病灶是导致癫痫发作的罪魁祸首，通过手术切掉。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 二、切断癫痫的传播途径。癫痫患者大脑的大面积神经原同步化放电，引起肢体抽搐，它一定要“传下来”。医生想尽办法，不让它“传下来”。在脑功能区病灶里架空抽搐，局限病灶可用这个方法。广泛的癫痫起源，可以做胼胝体切开，（人的大脑半球分为左半球右半球，两个半球之间的联系，叫胼胝体）使得大脑左半球的放电，传不到右半球，就不会导致临床发作。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 还可以通过微创手术，立体定向，把某些传导素刻意性阻断，以阻止癫痫放电向下传播。但是手术并不能够完全治愈癫痫，只是姑息性让症状有所缓解。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 三、加强抑制系统的功能。人脑跟社会非常像，人脑有两个系统，一个是兴奋性系统，还有一部分是抑制系统。抑制系统相当于社会警察，加强抑制系统，就是加强社会治安工作。抑制系统手术最早开展的是慢性小脑刺激术，小脑在整个中枢神经系统中相当于“警察”，小脑功能加强，能抑制大脑皮层的放电，不让大脑过度兴奋。国际上开展更多的是神经刺激术，在颈部迷走神经上放一个电极，定期给它刺激，加强功能，也会抑制整个大脑的癫痫发电。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 不是所有顽固性癫痫 都能通过手术治愈 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 顽固性癫痫的治疗手段，主要通过外科手术来进行，但手术之前需要对各项指标做评估。首先做头部磁共振检查，排除头部有没有器质性的病变，比如，血管畸形、肿瘤性病变。除磁共振以外还要做脑电，查看癫痫病灶在什么地方，如果发现起源病灶是局限的，则适合做手术。如果磁共振检查是阴性，没有发现任何病变，脑电图也没发现局限性病灶，这样的病人手术效果比较差。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 开颅手术VS微创手术 如何选择才科学？ </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 开颅手术、微创手术的区别在于： </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 如果开颅手术可以直接找到病灶、切除病灶，病人癫痫控制就会很好。就好比社会出现犯罪团伙，把团伙连锅端掉，就再也不会危害社会。微创手术相当于把犯罪团伙的领导者抓起来，但其他团伙成员还在，对癫痫控制的程度，相对要差一些。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 开颅的缺点在于，要切掉一大块脑组织，势必会影响到周围组织，风险较大，创伤也较大，而微创手术的创伤小。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 建议磁共振没有发现特殊病灶的患者，如果决定做手术，先考虑微创手术。即便效果不好，下一步还可做开颅手术。做了开颅手术，造成一些风险和损伤是不可逆的。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫术后 患者记忆力是关键 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 开颅手术把病灶切除，治愈力比较高。在顽固性癫痫里，面癫痫占很大一部分，如果把这个面癫痫连面带内侧的海马结构一同切掉，癫痫完全控制的比率应该在70%左右，其余类型的癫痫病人也会明显的改善症状，一些急躁病灶，切掉后效果也不错，基本一半左右的病人癫痫发作消失。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 微创手术，比如面癫痫微创手术，主要毁损的是海马结构，毁损导致癫痫放电的点，大概有50%的病人术后癫痫发作能够完全消失。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 但是海马结构掌管人类记忆，开颅手术需要把它完全切掉，术后病人的记忆力可能会受到影响。尤其是双侧癫痫，海马结构切掉一个，还有一个，还能诞生一部分记忆功能，如双侧都切掉了，人就没有记忆了。微创手术，保留整个海马结构，只把放电部位毁损，对病人记忆力影响非常小。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫术后应坚持用药 控制饮食起居 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 很多癫痫病人和病人家属觉得做了手术病好了，就不需要吃药，这是错误的。手术只是给癫痫病人创造机会，很多是不能治愈的，只是发作减少，发作程度降低。即便完全控制住的病人，也只是创造一个条件让他不发作。十年之内不发作才能宣布癫痫治愈。所以术后一定要继续吃药，至少坚持两年。两年以上如果不发作，可考虑慢慢减药，还要配合脑电图检测减药，看还有没有癫痫放电，如果有，减药要慎重，如没有，可按步骤地减。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫病人饮食起居三注意 情绪管理很重要 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 癫痫病人的饮食起居在三个方面要注意： </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 一、饮食方面：不要吃辛辣刺激性的食物、海鲜。甚至一些有异味的蔬菜，都要注意，比如香菜、韭菜、芹菜、葱、姜、蒜等。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 二、生活要规律：按时睡觉，按时起床。不到嘈杂的地方，比如歌厅、酒吧、舞厅，不刺激眼睛，不刺激耳朵，包括建筑工地的嘈杂声。电视和游戏也容易诱发癫痫，看电视，一天一个小时，不玩刺激的游戏，不看闪动、颜色鲜艳的画面。 </span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 三、控制情绪，有很多癫痫病人，尤其是面癫痫病人，本身脾气暴躁，情绪波动比较大。情绪上来的时候，大脑放电是增加的，生气的时候，大脑所有的神经原可能都在兴奋，都容易造成兴奋同步化，引发癫痫发作。保持情绪稳定，也是防止癫痫复发的很重要因素。 </span><br />
