<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3226/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">义诊现场专家教授合影</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 新华网湖北频道12月16日电（吴远征 田利华 夏春芬 郑巧）进入腊月，严寒逼近，气温降骤、冷风刺骨，是各种骨关节疾病高发的季节。12月14日，第二届武汉同济医院鄂东南地区骨科论坛暨现场义诊走进黄冈，在同济黄州医院隆重举行，武汉同济医院骨科主任李锋教授率领十余位同济医院骨科专家教授亲临现场进行学术讲座及现场义诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 当日上午8点半，同济黄州医院门诊5楼学术报告厅已是座无虚席。武汉同济医院骨科主任李锋等十余位同济医院骨科专家分别作了学术报告与交流。现场百余名骨科同行相互学习与互动交流，并对几位专家教授所讲内容作了笔记记录。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 李锋教授是武汉同济医院骨科主任，主任医师，教授，博士生导师，现为华中科技大学同济医学院附属同济医院骨科学术带头人，从事骨科临床及其基础研究工作二十余年，在脊柱和关节疾病的诊治方面水平颇高，尤其在治疗颈椎病、脊柱肿瘤、脊柱畸形以及在关节置换术方面有很深造诣。在国内率先开展了“畸形椎体切除后路楔形截骨治疗脊柱后凸畸形”、“髋臼周围截骨髋臼旋转成形术”等手术。目前在国内率先开展了计算机导航辅助下的脊柱微创手术，取得了满意的疗效。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 学术交流会上，专家们围绕近年来骨科医疗临床的典型病例，结合医疗技术和临床难题，对临床治疗新技术的应用等进行了深入的研讨。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 学术活动结束后，骨科专家还开展了现场义诊活动，来自武汉同济医院13名骨科专家教授为前来咨询的病人现场答疑解惑。义诊现场人头攒动，不少市民早早来到现场等候专家，大家听说是武汉同济医院骨科主任李锋教授率队前来义诊，都欣喜不已，两个小时的义诊，前来咨询和就诊的群众达500余人。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 自9月28日武汉同济医院正式托管同济黄州医院以来，同济黄州医院依托同济医院的先进管理理念及医疗技术，医院住院病人数、门诊量大幅上升，各项指标呈稳步上升趋势，受同济专家效应影响，医院门诊量同比增长110%，手术量同比增长10%，住院人次同比增长12%，开展的多项手术填补了黄冈市空白。</span><br />
