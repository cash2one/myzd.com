			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>专访耳鼻喉科专家单希征： 眩晕科学正在形成</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">9月12日，中国研究型医院学会眩晕医学专业委员会在北京成立。“这标志着全国眩晕医学领域的研究者、爱好者和实践者有了自己的交流平台，为大家开辟了更加广阔的发展空间。”武警总医院耳鼻喉科头颈外科主任、中美眩晕病研究所所长单希征说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“眩晕医学作为一个新学科，刚刚诞生。”作为该委员会的主任委员，单希征告诉健康界，他希望，通过眩晕医学研究专家的积极参与和共同努力，有效地改变目前中国对眩晕病研究和诊断治疗的滞后局面。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1983年，单希征毕业后被分配到海军总医院耳鼻喉科工作，并听从学科带头人的意见，在业余时间钻研起了眩晕病的诊断和治疗。经过30多年的潜心研究，他成功攻破了眩晕性疾病的诊断标准，并研制出眩晕诊断的医疗仪器。此外，他还发明了耳石症诊疗系统。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003658438520" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中国研究型医院学会眩晕医学专业委员会成立大会结束之后，单希征接受了健康界的专访，介绍了眩晕诊治的现状、误区及其研究进展。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征告诉健康界，眩晕性疾病是一类多发病、常见病，涉及多个学科，由于医院没有设专门的眩晕科，难以达到专业化的诊治水平。因此，有些就成为了疑难病症。眩晕作为临床症状，占据门诊患者主诉中的第2~3位，眩晕的发病率约为5%，一生中有过眩晕的人占28%。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“这是国际公认的常识数据。目前，中国对眩晕性疾病还未进行大样本的人群调查。”单希征坦言，但无论是国内还是国外，都还没有眩晕性疾病发病率等最新调查数据。下一步，他将通过眩晕医学专业委员会的平台，着手做这项工作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眩晕认知几个误区在哪里？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：眩晕性疾病的临床表现是什么？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：眩晕是临床常见的一种症状，是由于人体对空间定位障碍而引起的一种运动幻觉或者错觉，是平衡障碍在患者大脑皮层内产生的主观反映。常常表现为视物旋转，自身旋转、漂浮感等，有时还伴有恶心、呕吐、多汗、血压波动不定等症状，有的患者伴有神经系统定位体征。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：我记得您说，公众和医生对眩晕性疾病认知有误区？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：眩晕起病突然，症状较重，伴随症状复杂，患者首先想到的可能是脑血管疾病，因为医院没有眩晕科，患者只是根据自己的想象挂号，医院也无法指导。因此，大部分患者都去神经内科就诊。为此，患者常常得到的是对症治疗，而达不到病因治疗。常常是经过多个学科、多家医院就诊仍然诊断不清。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上述是患者的认知误区。目前，眩晕患者主要就诊于神经内科、耳鼻喉科、急诊科、骨科和老年科。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">各个学科的医生重视的是本学科的主流疾病。因此，他们都不专门研究眩晕，对眩晕的关注不够，缺乏全面的眩晕知识，致使临床医生对跨学科的眩晕诊治面临一定障碍，导致眩晕诊断泛滥和用药泛滥。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多年以来，临床医生常常把诊断不明的眩晕误认为“椎—基底动脉供血不足”而进行治疗，扩血管药、神经营养药成了眩晕的常规治疗方法，形成医生对眩晕的诊治误区。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于这两个误区的存在，形成了分科论治、对症治疗的局面，严重影响了眩晕性疾病的诊断治疗水平。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大多数眩晕与内耳有关</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：国内对眩晕性疾病的治疗有没有形成诊断标准？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：学者对眩晕的研究不断取得了可喜的进展，有些过去诊断不清的眩晕得到了明确，如上半规管裂综合征、前庭阵发症等。中国的神经科专家也达成共识，制定了眩晕的诊断流程。耳鼻喉科专家制定了梅尼埃病的诊断标准和良性阵发性位置性眩晕（Benigh paroxysmal positional vertigo，简称“BPPV”）的诊断治疗标准。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：您当初为何先突破耳石症，并发明了诊疗系统？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：在多年的临床实践中，我们发现，绝多大数眩晕与内耳有关联。耳石症又称为BPPV，其发病率非常高，大约占所有眩晕的1/3。过去这种疾病被误诊了，当成颈椎病或其它病治了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这种眩晕病是内耳有个管平衡的小石头脱落了，掉到了半规管里，引起眩晕，甚至恶心呕吐，通过复位治疗可立竿见影，非常神奇。国内外有少数医生在床上给病人手法复位治疗。但只限于非常有经验的医生，才能达到良好的治疗效果，远远不能满足临床的需要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：对于BPPV的研究取得了什么突破？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：对整个学科而言，眩晕医学的重要进展和突破就是对BPPV的认识和治疗，它解决了近乎1/3眩晕患者的治疗问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，我们对BPPV有三个突破性进展的认识：一是认识到BPPV发病率很高，约占全部眩晕患者的1/3，这是近几年来才有的一个概念。经过对该病的多年研究，以及诊断方法的规范化，我们对该病的认识越来越深入。除了原发性BPPV外，还发现了继发性BPPV，如突发性耳聋伴眩晕、梅尼埃病、前庭神经炎等，其发病非常广泛。此外，我们还发现Dix-Hallpike试验和滚转试验是诊断BPPV的金标准。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二是通过手法复位的方法治疗BPPV，并且能够得到立竿见影的疗效。目前，该方法在国内已经比较普遍开展，经验丰富的医生运用该方法可使临床效果达到80%~90%，这是经过多年的临床实践和研究结果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三是成功研制了全自动化良性阵发性位置性眩晕诊疗系统，实现了定位、定量、定速的全自动化的诊断和复位。由计算机指导的全自动化复位减少了医师的劳动负荷，能完成真正意义上的按照半规管的走行实现360度旋转复位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不过，上述诊疗系统对过度肥胖、存在颈腰疾患等不适宜转体或转颈的患者不易进行手法复位。对难治性和反复复发性的BPPV患者，难以精确调整复位时的角度值，但能够对他们进行个性化的方案设计，达到更好的治疗效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">提出眩晕医学专业化理念</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：怎么提高眩晕的诊断治疗水平呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：要提高眩晕的诊治水平，必须树立眩晕专业化的理念，建立专业化的眩晕诊疗机构。当然，还必须有专门的从事眩晕诊疗和研究的医师团队，而这个团队要具备眩晕的专业化知识、较高的诊疗水平、检查眩晕的专业设备、专业化的眩晕病房，对诊疗有困难的患者，能够提供住院治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">也就是说，拥有专业化的医师队伍、专业化的眩晕实验室、专业化的眩晕病房、专业化护理和专业化技师队伍，才能形成眩晕医学的专业化。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：您2007年成立中美眩晕病研究所也是因为这个原因吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：是的。目前，我们医院有专门的眩晕门诊，每年接诊来自全国各地的眩晕患者一万多人，我们研制成功了BPPV诊疗系统，实现了耳石症的全自动化和精确化的诊断和复位，取得了多项成果。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以，专业化的眩晕诊疗机构可以在耳鼻喉科，亦可以在神经内科或中医院的脑病科建立，将其视为三级学科。但若该学科想单独建立，就必须和其他学科一样，成为二级学科。不同级别的城市的三级医院均应设有眩晕诊疗机构，二线城市也应该有几个，三线城市至少有一个，这样便于眩晕患者到眩晕的专门诊疗机构就诊，改变患者目前盲目看病的现状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康界：在您的推动下，这项工作取得了什么样的进展？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单希征：目前，眩晕并不是一门独立科学，属于交叉学科和边缘学科的范畴，虽然很多医院并未专门成立该学科，但眩晕科学正在形成。在我们的推动下，目前中国已经有五六十家医院设眩晕科。此外，我们还成立了武警部队眩晕病研究所西南分所。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我认为，眩晕科的研究范围应该包括眩晕、头晕、不平衡和晕厥前失衡。眩晕学科要树立大学科观，不能只看眩晕，而不看头晕甚至头昏。</span><br />
<p>
