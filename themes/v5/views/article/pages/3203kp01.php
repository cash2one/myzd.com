			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>输卵管结扎复通术后再阻塞是否适合做介入再通术？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在临床工作中常常会遇到, 结扎后因为再婚或孩子意外死亡者行输卵管复通术,大部分人很幸运,输卵管成功复通,又幸福的做妈妈了。但是有一小部分结扎复通术后又出现一侧或双侧再阻塞，这种情况是否适合做介入再通术?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">要回答这个问题,我们必须首先要知道结扎的部位是在输卵管峡部,在输卵管峡部用银夹或丝线结扎输卵管,使输卵管管腔闭合使精子和卵子不能相遇受精而达到绝育目的。结扎处前后的输卵管都是正常的,是通畅的。在进行输卵管复通术时吻合的部位也是在输卵管峡部,复通腹痛术后,如果输卵管峡部通畅,一般输卵管就正常复通了。如果输卵管峡部阻塞了,可能是吻合处因纤维结缔组织增生引起阻塞,这样的阻塞就不适合做介入再通术。如果输卵管是峡部远端的壶腹部或伞部阻塞就可以试试介入再通术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">简言之, 输卵管峡部阻塞就不适合做介入再通术;如果输卵管是峡部远端的壶腹部或伞部阻塞就可以试试介入再通术。</span><br />
