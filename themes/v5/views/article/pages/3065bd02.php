<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>北京清华长庚医院姜泊教授获“何梁何利基金科学与技术进步奖”</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">清华新闻网9月23日电 （通讯员 韩冬野）日前，经何梁何利基金评选委员会全体会议评审通过，北京清华长庚医院内科部部长、消化内科主任姜泊获评“何梁何利基金科学与技术进步奖”。</span><br />
<img src="http://static.mingyizhudao.com/147004074083256" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">姜泊近照。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“何梁何利奖”由何梁何利基金设立，该基金是由何善衡基金有限公司、梁銶琚先生、何添先生、利国伟先生的伟伦有限公司共同捐资，旨在奖励我国取得杰出成就的科技工作者，倡导尊重知识、尊重人才、崇尚科学，激励科技工作者不断攀登科学技术高峰，加速国家现代化建设。何梁何利基金2015年颁奖大会将于10月中下旬举行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">姜泊教授现任中华医学会消化内镜分会副主任委员；中华医学会消化病学分会常委等职。自执业以来，姜泊长期从事大肠癌的早期诊治，胃肠道微生态系统与肠粘膜屏障，炎性肠病和功能性胃肠疾病等研究，对内镜下诊治胃肠早癌及癌前病变具有丰富经验，长期致力于推广应用染色内镜和放大内镜及大肠镜单人操作等方法，以提高早癌诊治水平。其在推动我国早期大肠癌诊治水平的研究方面做出的贡献，尤其获得了何梁何利基金的充分肯定和高度评价。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">未来，姜泊将带领北京清华长庚医院消化中心团队继续进行临床科研，深入开展社区早癌筛查项目，完成国家科研项目——肠道微生态系统研究等。“临床科研贵精，救患者于未然；洞察临床科研的盲区，救患者于存亡之间。”姜泊说。</span>
</p>
