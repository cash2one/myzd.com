		<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>从一个不幸的故事说起——走出颈椎病治疗的误区(转载)</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">王老师是某中学的模范教师，辛勤工作了30年，德高望重，桃李无数。然而，令人遗憾的是，半年前的一天晚上，他不慎重重地摔了一跤，造成四肢完全瘫痪，大小便失禁。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 也许大家都觉得奇怪，为什么仅仅摔了一个跟斗，就会导致王老师瘫痪了呢？这必须从王老师30年前的慢性颈部疼痛说起。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 30年前，王老师大学毕业后，怀着满腔工作热忱，登上了梦寐以求的三尺讲台，开始了自己的教师生涯。他爱岗敬业、多年如一日，每天连续伏案工作数小时，备课已经成为他的习惯。他很快就成了学生们崇拜的偶像，教师们学习的榜样。繁重工作的劳累，他逐渐出现了脖子酸困疼痛的症状，病情时轻时重，严重时竟彻夜难眠。为了不影响工作，他没有舍得花时间去医院看病。他忍耐着，忍耐是他顽强的标志，也是锻炼自己意志的良好方式，就这样，他忍痛工作了3年多。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3年后的某一天，王老师感觉到头有些晕，尤其是起床、卧床的时候眩晕加重。这才不得不去医院就诊。经检查，医生诊断为椎动脉型颈椎病。经过应用颈围领固定、卧床休息之后，他眩晕的减轻了，就又走上了心爱的讲台；然而，头晕的毛病却始终没有完全消失。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 10年后的某一天，王老师感觉到左手麻木，无力，在妻子的反复劝说下，他才又一次踏进医院的大门。经过骨科医生的检查、拍片，发现颈椎骨质增生，诊断为神经根型颈椎病，于是便开始了颈椎牵引治疗，妻子还到中医诊所为丈夫买了消除骨刺的中药，每天为他煎药。王老师的手麻症状逐渐减轻了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 25年后的某一天，王老师发现自己小便无力，走路时足底有踩棉花的感觉，头重脚轻，整个人轻飘飘的，如醉酒一般。在妻子女儿的一再催促下，王老师才再一次去看医生。然而，这一次就诊使他惊呆了，医生的诊断是脊髓型颈椎病，CT、MRI等检查证明，王老师的颈椎椎管内有大量的骨质增生和韧带钙化，椎管严重狭窄，脊髓已经受压变形，如同糖葫芦状。医生建议他尽早手术治疗。然而，由于担心手术风险大、害怕影响工作，他拒绝了手术治疗，重新回到了讲台。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 30年后的某一天，终于发生了我们文章开头那个不幸的故事，仅仅摔了一跤就使他坐上了轮椅。医生虽然为他及时做了手术，但是，由于脊髓已经发生了损伤坏死，半年来，他的四肢感觉和运动功能仍然没有得到良好的恢复，大小便依然失禁。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 许许多多的颈椎病患者都有着与王老师相似的经历。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 医生认为，王老师的瘫痪是一种必然。因为与大多数人一样，对于该病，他从来不做预防；因为在患了轻度类型的颈椎病之后，他始终没有坚持科学、规范和系统的治疗。加上传统的颈椎病治疗方法本身就存在着致命的弱点，他与许多患者甚至一些医生一样，从预防、治疗到康复的每一个环节，都存在着许许多多的误区。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 扁鹊治未病，千古成佳话。颈椎病的预防比治疗更加重要。如果王老师能够做到劳逸结合，积极调整生活姿势和节奏，选择正确的治疗方法，就可以有效防止颈椎病的发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 提高肌肉力量和耐力的治疗才是治疗颈椎病的根本方法</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在颈椎病的发病机制方面，一直以来，传统的观念将太多的眼球聚焦在椎间盘之上，认为所有类型的颈椎病都是椎间盘惹的祸；认为只要椎间盘没有问题，颈椎就没有问题；认为只有椎间盘有了问题，颈椎才有问题。这种观念沿袭至今。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 与传统观点不同，我们认为，颈椎病是一种多因素、多途径综合作用的结果。包括肌肉、韧带、椎间盘、骨关节在内的组织结构都会发生劳损与老化，也就是退行性改变。而且该病的发生和发展也同样符合由轻到重、循序渐进的规律。因此，将所有的责任都归咎于椎间盘的观点是不正确的。我们应该综合地、动态地、整体地认识颈椎病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在颈椎病的早期，可能有椎间盘的退变发生，但是更重要的责任组织应该是肌肉和韧带。由于长期伏案、高枕而眠、沉迷于电脑桌前、强大的工作压力等因素，颈椎周围的肌肉会发生劳损，使力量和耐力下降，再在致炎致痛机制的作用下导致颈部疼痛的发生；如果疼痛长期不愈，便形成颈型颈椎病。与此同时，韧带也会发生变性松弛，其维系骨关节结构的静态稳定作用下降，就会出现颈椎生理曲度的消失，甚至会有轻度的椎间不稳发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 因为颈椎周围肌肉的劳损、肌力下降、耐力的降低是颈型颈椎病的主要发生机制，所以只有提高肌肉力量和耐力的治疗方法才是治疗根本的方法；如果放任自流，或者治疗方法不当，就会使疾病迁延不愈，反复发作，逐渐加重，导致其他类型颈椎病的发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 传统的治疗方法，诸如药物、推拿按摩、针灸、牵引和理疗等等统统属于被动的治疗方法，没有一种治疗方法可以提高患者颈椎周围的肌肉力量和耐力，因此，这些治疗方法只能缓解症状，不能治疗根本。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 正如同我们肚子饿了必须自己吃饭才可以补充能量一样，要想提高颈椎周围的肌肉力量和耐力，就必须让患者自己进行主动的抗阻运动锻炼。我们把这一治疗方法称作“主动抗阻运动疗法”，并将该方法应用于临床，取得了满意的治疗效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 我们强调的是：要想拥有一个健康的颈椎，就应该预防颈椎病，将颈椎病消灭在萌芽状态。而要想达到预防和消除颈椎病的目的，首先必须拥有健康的肌肉。否则的话，预防和治疗都等于空谈。为什么传统的颈椎病治疗方法效果不佳？答案就在于缺乏对颈椎周围的肌肉力量和耐力的重视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 我们可以将颈椎病所有的治疗方法分为两大类，一类就是病人被动接受医生所给予的治疗，包括药物、牵引、按摩、理疗等等治疗方法，统称为被动治疗；第二类就是让患者主动锻炼的治疗方法，叫做主动治疗。尤其是让患者对抗一定的阻力进行肌肉功能锻炼的治疗方法叫做“主动抗阻运动疗法”，属于主动治疗的范畴。临床实践证明：通过长期的主动抗阻运动锻炼，能够达到重塑肌肉结构、增强肌肉力量和耐力、提高肌肉运动功能的功效，可以从根本上治疗颈椎病，而且该方法操作简便、易于推广。主动抗阻运动方法是一种理想的预防颈椎病的方法，也是颈型颈椎病、椎动脉型颈椎病等类型颈椎病的最佳治疗方法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 颈型颈椎病时期如果得不到合理治疗，或会逐渐向椎动脉型颈椎病、神经根型颈椎病、脊髓型颈椎病方向发展恶化。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 颈型颈椎病时期是最佳的治疗时机，在此时期如果加强颈椎周围肌肉的锻炼，是完全可以治愈的；如果失去最佳治疗时机或者治疗不当，颈型颈椎病就会在肌肉无力、耐力下降、韧带劳损松弛、椎间盘变性的基础上发生颈椎不稳，刺激供应大脑的血管，导致眩晕发生，发展成为椎动脉型颈椎病；如果在肌肉劳损、无力、耐力下降和椎间盘变性的基础上发生椎间关节松动、韧带受牵拉损伤钙化，就会发生骨质增生；如果增生的骨刺占据了神经通道，压迫神经根，就会产生肢体麻木无力等症状。这就是神经根型颈椎病；如果骨质增生、韧带骨化等压迫脊髓就会产生肢体无力、感觉障碍、大小便功能障碍甚至瘫痪，此时，就已经发展到了脊髓型颈椎病了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 事实证明，许许多多的颈椎病患者由于自己的疏忽或选择治疗方式不合理，遗憾地失去了最佳治疗时机，发展为十分严重的颈椎病阶段了。王老师的经历正是如此。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 对于椎动脉型颈椎病来说，最容易被误诊为高血压、美尼尔氏综合征等疾病。即使在明确诊断之后，传统的治疗方法是应用颈围领制动。然而，这一被动治疗方法往往会导致我们颈椎周围的肌肉萎缩，会使颈椎更加不稳，使眩晕愈演愈烈。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 因此，在椎动脉型颈椎病的治疗方面，传统的被动的治疗方法仍然存在着致命的缺陷。诸如药物、牵引、制动、按摩和理疗等等方法一直被一些医生不耐其烦地施予患者，而患者也乐于享受这些温和舒服的服务。毫无疑问，这样做患者的肌肉会仍然无力，颈椎会依然不稳，其结果是患者晕，医生更“晕”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 相反，如果在这一时期充分重视应用主动抗阻运动疗法，就可以有效提高患者颈椎周围的肌肉力量和耐力，使不稳定的颈椎趋于动态稳定，只有这样才能从根本上比较有效地保守治疗椎动脉型颈椎病，并有效防止疾病进一步的发展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 神经根型颈椎病是一种比较常见的颈椎病，在神经根型颈椎病的传统治疗方法中，也大都选择药物治疗和颈椎牵引，这都属于被动治疗。其中，颈椎牵引可以减轻神经压迫，缓解肢体麻木症状。然而，超重量、长时间的牵引会使韧带更加松弛，椎体之间更加松动，疾病更加易于复发和加重。令人遗憾的是，这种大重量、长时间的牵引疗法仍然在被广泛采用；与此相反，能够用来提高肌肉力量和耐力的治疗方法几乎没有被应用于临床。无怪乎患者的颈椎病反复发作和加重了。因此，临床实践证明：传统的治疗神经根型颈椎病的方法依然存在着致命的缺点。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 脊髓型颈椎病是一种最严重的颈椎病，它是在上述各型颈椎病的基础上发生发展而来的。此时，脊髓已经受压损害，必须尽早手术治疗，解除危害因素，防止瘫痪发生。当然，手术也属于被动的治疗方法。手术之后，在颈椎病的康复方面，肌肉功能的训练仍然是一个重要的环节，可遗憾的是，这一环节并不被患者甚至医生所重视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 提倡主动抗阻运动疗法</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 我们在这里强调，现在应该是重新认识颈椎病的发病机制的时候了，不要忽视颈部肌肉这一关键和重要的环节。必须认识到被动的治疗方法的致命缺陷就在于根本不能提高肌肉的力量和耐力。我们应该大力提倡和推广能够提高肌肉力量和耐力的治疗方法----主动抗阻运动疗法。只有防止肌肉劳损、提高肌肉功能才是预防颈椎病的关键。只有加强主动抗阻运动锻炼，提高肌肉力量和耐力才能够从根本上治愈颈型颈椎病。只有加强肌肉功能、提高其动态稳定能力，才能使椎动脉型颈椎病患者获得较为满意的疗效；也才能使神经根型颈椎病患者的病情不再复发和加重。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 总之，在过去的传统的颈椎病治疗方法中，被动治疗一直占据着主导地位，而主动运动治疗却一直被忽视。所以我们呼吁，预防、治疗和康复颈椎病，必须告别被动治疗，重视主动抗阻运动。只有如此，才能将颈椎病消灭在萌芽状态，降低颈椎病的发生率，提高颈椎病的治愈率，让颈椎病所导致的瘫痪发生率降至最低。</span>
