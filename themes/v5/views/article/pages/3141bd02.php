		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>面肌痉挛如何手术治疗？</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原标题：面肌痉挛如何手术治疗？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">身体周刊：神经外科都有哪些常见的肿瘤？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">张晓彪：神经胶质瘤是发生于神经外胚层的肿瘤，可起源于神经胶质、室管膜、脉络丛上皮等细胞。胶质瘤的病程依其恶性程度和所在部位长短不一，自出现症状至就诊时间一般多为数周至数年。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">症状主要表现在两方面：一是肿瘤引起颅内压增高，进而引起头痛、呕吐、视力减退；二是脑组织受肿瘤的压迫、浸润、破坏，造成相应的功能缺失。发生在额叶的肿瘤可出现性格改变、瘫痪或语言障碍；发生在顶叶的肿瘤可出现感觉障碍；发生在颞叶的肿瘤可出现幻觉；发生在枕叶的肿瘤可出现视力障碍；发生在小脑的肿瘤可出现共济失调，当然，以上所说的症状往往合并出现。此外，还可能有癫痫发作和精神症状。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">出现症状后需要进行头颅CT或磁共振检查，有时还需要做脑电图、视力和听力等检查。治疗的方法包括手术、放疗、化疗和多种方法联合治疗。手术是大多数肿瘤的一线治疗方法，现代神经外科越来越重视微创手术，在神经导航系统的引导下精确切除肿瘤而尽量保留原有的正常神经组织和功能。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">就目前的医学研究而言，还没有发现预防胶质瘤发生的方法和措施。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">神经外科常见的还有垂体瘤等颅底肿瘤。垂体瘤是起源于垂体前叶和后叶的肿瘤，前者为垂体腺瘤，即我们通常所说的“垂体瘤”，是最常见的颅底肿瘤之一，后者为垂体细胞瘤，较罕见。垂体瘤大部分为良性腺瘤，极少数为癌。垂体瘤的临床表现包括头痛、视力下降、内分泌障碍（性功能减退、女性闭经泌乳月经失调、库欣病、巨人症、肢端肥大症、甲亢等），肿瘤生长巨大时还会引起脑积水、下丘脑功能低下，危及生命。垂体瘤按大小可分为微腺瘤、大腺瘤和巨大腺瘤；按功能可分为无功能型、泌乳素型、生长激素型、促肾上腺皮质激素型、促甲状腺激素型。目前手术为最主要的治疗方法，其他还有药物治疗和放射治疗。手术大多数可以采用内镜导航微创经鼻入路切除。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">颅底肿瘤是生长在颅底的肿瘤，除垂体腺瘤外，常见的还有脑膜瘤、颅咽管瘤、脊索瘤、听神经瘤等，临床可表现为头痛、颅神经功能受损、内分泌障碍等。颅底肿瘤由于位置深、涉及重要神经血管多，手术难度巨大，是神经外科中治疗最为困难的疾病之一。根据不同部位采用内镜导航或显微导航技术切除常为首选治疗手段，其他还包括放疗和化疗等辅助手段。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">身体周刊：面肌痉挛、三叉神经痛，有什么微创手术方法治疗？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">张晓彪：面肌痉挛，即脸部肌肉抽搐，表现为一侧面部肌肉阵发性不自主抽动、眼睑抽动紧闭和口角歪斜等症状，可不断加重，甚至伴有耳鸣、听力下降。精神紧张时则症状更为明显，影响患者的阅读、视物、正常生活和社交活动，甚至影响睡眠。在明确诊断后，通常是先选择药物等保守治疗，一旦保守治疗无效或副作用大，即可选择神经外科的微创手术治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">神经外科微创手术治疗面肌痉挛，即显微镜下面神经微血管减压术，是目前国际公认的治疗方法，效果肯定，运用内镜辅助手术可以更加微创和有效。手术即在病人的耳后枕部做一个小的切口开颅，术中暴露出神经被血管压迫处，用小垫片（一般用Teflon材料）垫于两者之间，将神经血管分隔开来，消除病因。手术后，患者的症状可立刻或逐渐消失。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原发性三叉神经痛是发生于面部的剧烈疼痛。成年及老年人多见，发作时表现为以面颊上下颌及舌部明显的剧烈电击样、针刺样、刀割样或撕裂样疼痛，持续数秒或1-2分钟，突发突止，间歇期完全正常。患者口角、鼻翼、颊部或舌部为敏感区，轻触可诱发，称为扳机点或触发点。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">治疗方法首选药物治疗，无效或失效时选用其他疗法，包括经皮穿刺三叉神经半月节球囊压迫术、无水乙醇或甘油封闭三叉神经分支或半月神经节、经皮半月神经节射频电凝疗法，治疗原理都是破坏感觉神经细胞；治疗效果最好的是三叉神经显微血管减压术。近年来中山医院神经外科在手术中运用神经内窥镜，使手术治疗面肌痉挛和三叉神经痛更加微创和有效。</span><br />
