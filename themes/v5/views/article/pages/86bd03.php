		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>同仁医院除夕夜我值班心里踏实（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■卢海正在为病人进行检查■摄影/本报记者　袁艺 　　匆匆吃完年夜饭 赶往眼科急诊室 连续5年医院过除夕 医生卢海昨天说———</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">新春佳节，万家团聚，但是在这样一个日子里，却有一些基层工作者坚守岗位，默默奉献，为千家万户的幸福生活值守在一线。他们的付出、他们的努力令人感动。为此，本报记者春节期间走进基层，通过24小时全记录的方式，向您展示普通基层工作者平凡岗位上不平凡的春节。记者走进北京同仁医院，感受眼科医生平凡而又忙碌的一天。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■出场人物：卢海</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■岗位：北京同仁医院眼外伤科主任、医学博士</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">大年三十晚上，不论多忙碌的人此时都要与亲人围坐在丰盛的餐桌前，看春晚、放鞭炮。但是，北京同仁医院眼外伤科主任卢海却在昨天17点匆匆吃完年夜饭，18点赶到医院眼科急诊值班。他已经连续5年在医院里过除夕，“年三十晚上在医院里值班，我心里面踏实。”卢海说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">北京市从2006年春节开始解禁燃放烟花爆竹，到2011年的春节已经是第六个年头。据本市接收眼部爆竹伤患者的主要医院——同仁医院的统计，从2006年到2009年的受伤人数每年以不低于14%的速度增长。但是到了2010年春节，首次出现伤员人数下降的现象。“主要是政府在安全管理方面更细致、规范了，”卢海分析，“例如严查烟花爆竹质量，劣质产品减少，自然炸伤的患者就会少。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">19点20分</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■儿童伤员提早出现</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">位于同仁医院四层的眼科危重症诊室，12岁的河北肃宁男孩正在接受眼外伤检查。孩子左眼高高肿起，盖着纱布，脸上和手上的血液已经凝固。站在一旁的孩子妈妈说，下午1点多，儿子在家门口捡了个别人放过的礼花弹，“他剥开弹皮，把残余炸药装进一个玻璃瓶。”结果这个自制的玻璃爆竹就把孩子炸成了眼前这副模样。经过仔细检查，卢海告诉孩子父母，孩子除了眼皮被炸开了一个大口子外，眼球里也是一包血水，“可能是伤到眼球了，需要立即全麻手术。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">安排好河北男孩的手术之后，卢海又到眼科手术室查看手术记录。刚刚19点多，医院就已经接诊4名爆炸伤患者，其中3人都是儿童。和往年相比，儿童爆炸伤患者出现较早。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">21：00</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■重伤员多被礼花弹所伤</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">同仁医院眼科202诊室内挤满了被爆竹炸伤眼睛的患者和家属，突然，一辆平车被推进了诊室，车上躺着一位老年男子，双眼红肿泛着青紫，左手裹着纱布，右手还扎着吊瓶。陪护家属介绍，这位男子是在燃放礼花弹时，一个大火球扑面而来，衣服都被烧焦了。卢海立即安排：“肯定是要手术了，眼球要先做个CT。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">此时，医院已经接诊了13例眼外伤患者，据介绍，这些伤员主要来自于北京周边省份，重伤员多被礼花弹所伤。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">22点</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■楼上楼下跑了20个来回</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">正在二楼急诊室的卢海接到一个电话后马上跑向四楼重症室，一位廊坊的9岁男孩因为燃放“钻天猴”眼睛受伤。卢海用裂隙灯仔细检查了孩子的伤眼，他说，“眼睛里有异物，拍个CT，立即安排全麻手术。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">从晚6点上班到现在，卢海已经在医院楼梯上上下下跑了20多趟，二楼急诊室和四楼重症室都留下了他忙碌的身影。此时，他已经感到全身发冷，脑门冒出冷汗，“出门时有点发烧，现在可能病又重了。”卢海对身边的同事说，同事劝他回家休息，但他回答：“一会儿更忙，哪离得开啊。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">0点</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">■向媒体介绍救治情况</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在这个除夕夜，卢海还担负接待媒体记者。重症急救室外，卢海为京城十多家媒体召开了一个临时发布会，详细介绍了除夕夜救治伤员的情况。0点左右，北京上空的爆竹声就像是开了锅，卢海心里十分担忧，因为再过20分钟，很有可能病人会在短时间内蜂拥到医院急诊室，而且伤情严重，家属情绪激动。果然，正在进行中的媒体采访不得不中断，因为一位患者家属突然闯进来大喊：“大夫，那边有个病人正在等着缝合！”卢海听罢立即向患者家属指的那个方向跑了过去。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">虽然卢海除夕的夜班一直要忙到今早8点，但是下班了他也走不了，因为还有几项工作等着他去完成：把急诊室、手术室、病房巡查一遍；向院长汇报当天的工作情况。回到家里，最快也要下午1点了。</span>
</p>
