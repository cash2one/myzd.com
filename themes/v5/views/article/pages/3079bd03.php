<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>超声和钼靶</strong></span><br />
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">近年来，乳腺癌的发病率逐年升高，故女性对乳腺的体检越来越重视，通常采用超声检查和钼靶检查乳腺。然而，很多人并不太明白两种检查该选择哪一种。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 中山医科大学附属第一医院超声波科主任谢晓燕教授介绍，超声检查是将超声波发射到人体里去，通过组织的界面反射回来信息成像的检查手段。超声检查有黑白超声（简称B超）和彩色多普勒超声（简称彩超）检查两种。前者是超声检查的基础，但使用的仪器往往分辨率有限，反映的信息也比彩超较少，只是局限于对器官的大小、形态、结构和回声进行观察。彩超一般仪器的分辨率相对较高，可提供信息也更多，在黑白B超的基础上增加了器官的血流信息，如血流的速度快慢、血流阻力指数的高低等，特别是在发现有病变的情况下，彩色超声有利于进一步明确诊断。正是因为这一优势，当前多数大医院都采用彩超进行日常检查。谢晓燕建议，有条件者可直接做彩超检查，特别是乳腺肿瘤患者，做完黑白B超后再补彩超，就会造成不必要的浪费。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 有的超声检查需提前做准备：如检查腹部器官的，头一天晚上十点后必须禁食，早上做空腹检查；检查膀胱、输尿管的，应该提前憋尿，在膀胱充盈的状态下才能达到检查效果。但乳腺检查一般无需特别准备。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 与钼靶成像相比，简便易行、没有辐射是超声检查最大的优势，使超声检查被广泛应用。随着超声仪器分辨率的不断提高，图像分辨率也大大提高，对乳腺内肿块的检出非常的敏感，可检出像绿豆一样大小的病灶。但对钙化灶的检出仍不尽满意。尽管超声是常规检查，专家认为没有必要做太频繁的检查，普通健康人一年一次即可。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 与超声检查相比，钼靶照相的优势则在于：成像速度快，对检查者的技术依赖性较小，对乳腺组织内的钙化点的检出非常敏感，有助于捕捉到隐匿在乳腺内的早期乳腺癌，对腋窝淋巴结的检出也较敏感，但对乳腺内微小肿块的检出和图像的清晰度有待于提高。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 总之，两种检查各有优势，并不能简单地理解为“钼靶检查可以代替超声检查，或超声检查可以代替钼靶检查”。目前在我国超声检查是最常用的体检手段，但两者相互结合可提高早期乳腺癌检出的敏感性。</span>
</p>
