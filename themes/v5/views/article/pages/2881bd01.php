<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>（胡杰 复旦大学附属华山医院功能神经外科主任医师）</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在秋冬之际，由于气温的变化比较快，容易让人患上各种小疾病，这些小疾病有可能成为众多老年病的诱因。“帕金森病”是老年病中比较常见的一种。据统计，我国65岁以上老年人帕金森病发病率为1.7%。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">帕金森病初期症状一般表现为震颤，即手、脚会持续出现不受控制的震颤，尤其在静止时最为明显。震颤多由一边的手或脚开始，然后慢慢扩展到同一边身体；另外，手、脚肌肉变得僵硬，在伸直或弯曲手、脚时，会发觉该部位出现较大的阻力，动作好像转动齿轮般困难，这种僵硬会导致肌肉酸痛或者身体无法伸直，一旦有这些症状需要提高警惕。除此之外，如果出现动作迟缓、书写困难、长时间呆坐而不变动坐姿、起步停步困难等均有可能是帕金森的前兆。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医学发展到目前，帕金森仍然没有根治的方法。目前国内外临床医学上，对于帕金森病应用专业的评分标准来评估患者病情的严重程度，一般按照病情轻重程度分为五个阶段也就是五个分期。早期偶尔发生一些症状，对生活没有任何影响，这种情况下我们更多的是观察，然后做一些适当的康复锻炼和体育锻炼。症状再加重就需要服用一些多巴胺制剂。但是帕金森病有一个特点，药物治疗尽管可以暂时缓解症状，但并不能延缓疾病发展的进程，许多患者在服药一段时间后，会出现药物疗效逐渐下降、服药量不断增加的状况；也有部分患者会出现“不自主动作”（身体出现一些无法自我控制的动作）和“开关”的周期性药效变动这些副作用，这些副作用会对中晚期的帕金森病人造成极大的困扰。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在药物治疗出现“开关”症状，并且病人出现难以忍受的副作用的时候，神经外科手术脑起搏器治疗可以缓解帕金森病的各种症状，甚至可以使一些症状完全消失。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">帕金森病患者的一系列症状，都是由于大脑深部一些特定的神经细胞群过度活跃引起的。外科手术治疗就是找到这些细胞群构成的核团，并想办法抑制其过度活跃。手术方法主要是脑深部电刺激术（deep brain stimulation, DBS），也叫“脑起搏器”。原理是通过埋置脑刺激器发放电脉冲至相关神经核团，从而抑制脑细胞的异常活动，以达到改善症状的目的。该疗法已有20多年的历史，全球已经有超过十万例患者接受了“脑起搏器”治疗。</span>
</p>
