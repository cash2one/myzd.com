			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>孩子夏天游泳注意哦 要当心耳朵和皮肤患上炎症</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">华龙网8月14日21时45分讯（记者 郑懿）夏天很多人喜欢游泳，但因游泳引发的耳朵、皮肤等疾病也随之而来。今（14）日，华龙网记者从重庆医科大学附属儿童医院获悉，进入夏天以来，医院每天都会接诊到因游泳呛水、耳朵发炎和患皮疹的小孩。医生建议，游泳前，家长应先给孩子清理外耳道；而患上感冒鼻窦炎、耳朵发炎的儿童更不宜游泳。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当心</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">孩子游泳竟导致外耳道发炎</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今（14）日，家住沙坪坝区的9岁男孩牛牛(化名)，在父母陪同下来到儿童医院耳鼻喉科看耳朵。牛牛的妈妈说，孩子放暑假以后，每天吃过晚饭就喜欢到小区的游泳池游泳。前两天游泳回来后，孩子老说耳朵痛，很难受，听人说话像有层东西隔着。牛牛妈以为是孩子耳朵进水了，于是让他试试跳和拍，但孩子仍然听不清。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">耳鼻喉科主任姚红兵在检查后发现，原来牛牛是患上了外耳道炎。据了解，由于牛牛耳朵里的耳屎较多，游泳时水进入耳朵一泡，便膨胀开来，堵住了外耳道，引起发炎，到医院检查时已经开始化脓了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“进入夏季以来，平均每天都有十来个来看中耳炎的小孩，其中有一半都是因为游泳引起的。”姚红兵说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据姚红兵分析，游泳引起的耳部疾病主要有急性化脓性中耳炎和外耳道炎。游泳时耳道进水、呛水是主要原因。常见的症状就是耳朵闷胀、疼痛、听力下降、流脓等。“儿童的咽鼓管尚处于发育阶段，较之成人既短粗又平直，加上抵抗力低，位于耳鼻喉的细菌很容易侵入，造成发炎。”姚红兵说，另外，小孩在游泳池泡久了，容易感冒受凉，一旦出现上呼吸道感染，也易引起急性中耳炎。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，姚红兵还提醒，年幼的孩子患病时不会诉说症状，有时会耽误就诊，家长要多注意孩子会不会经常抓自己的耳朵，睡不安宁等。儿童急性中耳炎如果不彻底治愈，会对孩子的听力造成一定损伤，甚至引发一系列颅内外并发症。游泳后，家长要随时观察孩子的状况，有严重耳痛时应及时就诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">注意</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">孩子游泳比大人更易染皮炎</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同样因为游泳而患病的还有家住江北区的5岁女孩欣欣(化名)。据悉，欣欣在家附近的一个游泳池学游泳，可是去了一个星期以后，欣欣妈妈发现孩子身上除了泳衣遮住的皮肤外，其余的都红彤彤的，还又痒又痛，甚至出现了几个水泡。于是欣欣妈急忙把她带到儿童医院皮肤科就诊，经过检查，欣欣患的是夏季皮炎。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据儿童医院皮肤科副主任罗晓燕说，小孩夏季游泳主要会有2大病症。第一就是小女孩容易引发外阴炎、尿道感染，主要表现为外阴痒、小便痛等，这周就有一个小女孩因此来皮肤科就诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还有一个最常见的就是夏季皮炎，因为夏天紫外线太强，而游泳池通常设在户外，小孩游泳时露在外面的部分会直接接触到强烈的紫外线，由于小孩皮肤比成人抵抗力要差，直接接触紫外线照射的部分就易发红，并伴有痒、痛症状，严重的还会有水泡。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据了解，目前儿童医院每天都会收到大约10例因游泳而导致夏季皮炎的患儿。罗晓燕也提醒家长，要注意给孩子防晒，以免皮肤被晒伤。</span><br />
