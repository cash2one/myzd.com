			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>女性尿失禁就诊率仅一成 每天两次盆底肌肉锻炼</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">即将到来的5月11日是母亲节，每年这个时候，子女们都在想该如何回报母亲的养育之恩。其实，随着年龄的增长，健康是母亲最希望得到的礼物。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 浙医二院泌尿外科杜传军主任介绍，在众多疾病中，女性的压力性尿失禁是困扰众多中老年妇女的难言之隐，其实只要及时干预治疗，对生活不会造成影响。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 女性尿失禁，仅一成来就诊</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 50出头的张女士是社区里远近闻名的晨练明星，可是最近她的晨练伙伴们发现她“失踪”了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 原来张女士多年来在抱孩子或提重物时，总有少量尿液漏出，随着时间推移，张女士在大笑或咳嗽时，也有尿液漏出，而且最近漏尿量越来越大，每天要换四五条尿垫。为避免尴尬，张女士逃避各种社交活动。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 最终张女士在女儿的陪同下，找到了浙医二院泌尿外科的杜传军主任。经诊断，张女士得的是压力性尿失禁。针对张女士的情况，杜主任为她施行了单切口的新型微创吊带手术，手术很成功。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 压力性尿失禁是指在腹压突然增加时出现无法控制的漏尿，大笑、咳嗽、打喷嚏、跑跳等动作均可引发，特别多见于45岁以上的中老年女性。据不完全统计，压力性尿失禁的患病率为39.34%，但由于医学知识缺乏及传统观念的影响，女性就医倾向非常低，仅有10.92%的女性到医院就诊。不少女性觉得这不是病，是年纪增大后的自然现象，也不愿意去就诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 作为省内最早开展微创吊带手术的医院，杜主任提醒，女性朋友若偶有溢尿现象是正常的，若比较频繁发生溢尿，就是病理现象，需要及时就诊。否则不仅影响患者的生活质量与社交活动，还会严重影响个人卫生，导致皮肤感染、泌尿系感染、膀胱结石，严重时会影响肾脏功能。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 预防尿失禁，多做盆底肌肉功能锻炼</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 很多女性在发现患有压力性尿失禁后，都不知道如何就诊。其实很多大型综合三甲医院的泌尿外科，都设有女性压力性尿失禁专科门诊，方便女性患者进行就医和治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前治疗尿失禁有多种方法，医生会根据病人的不同情况选用。轻度患者可运用行为锻炼或药物治疗等保守方式缓解症状。对于保守治疗无效或者症状比较严重的患者，最有效的方式是手术治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 杜主任提醒，女性尿失禁最好的预防办法就是多做盆底肌肉功能锻炼，及时就诊很必要。在女性一生中，要非常重视妊娠和分娩及绝经这两个时期的尿失禁防治。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在妊娠和分娩期，女性要定期孕、产后期检查，避免产后的重体力劳动和咳嗽、便秘等使腹压增加的疾病；如孕期或产后发生尿失禁，及早进行盆底肌肉的锻炼治疗，尤其在产后一年内进行盆底肌肉的锻炼治疗，多数患者可以康复。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 盆底肌肉功能锻炼最简单的方法，就是每天晨醒下床前和晚上就寝平卧后，各做45～100次紧缩肛门和上提肛门活动，不仅可以预防压力性尿失禁的发生，对轻症患者还有一定的治疗作用。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 围绝经时期应治疗慢性支气管和便秘等腹压增加疾病，定期妇科检查，平时不要憋尿，还要注意减肥，饮食要清淡，多食含纤维素丰富的食物，防止便秘。发生尿失禁应尽早去专科医院就诊，得到有效而个体化的治疗。</span><br />
