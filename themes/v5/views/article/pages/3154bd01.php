	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>儿童不宜过多使用电子产品</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着平板电脑逐渐走入家庭，很多孩子都成了重度使用者。但在临床实践中医生发现，手机、iPad等电子产品对儿童视力的影响很大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">南京儿童医院眼科副主任医师 &nbsp;乔彤：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">家长就明确告诉我，我家孩子就是前天玩了3个小时的iPad之后，眼睛就眨个不停，视力就下降了，会有这样的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">长时间玩iPad的话（儿童）的睫状肌会痉挛，会导致一部分假近，就是通常所说的假性近视，视力一过性地下降。而且小孩3到6岁是视力发育最关键的时期，可塑性非常强，可能会迅速一下变好、一下变坏，最严重的影响就是让近视提前发生，或者说视力急剧下降。然后就是频繁眨眼导致神经性眨眼。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乔医生说，除了iPad等平板电脑会加重孩子视力下降外，智能手机、掌中宝等电子产品的频繁使用，也会对孩子视力和身体有不良影响。建议不要让年龄较小的孩子接触这些电子产品。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">南京儿童医院眼科副主任医师 &nbsp;乔彤：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果能切断源头，不玩iPad这是最好的，但如果家里实在是有了，小孩又玩上瘾怎么办，那么就是间歇性地用，每次玩不要超过半个小时，让眼睛有足够的休息时间，恢复到正常状态。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼科专家同时建议，家长要提醒孩子保持良好的坐姿，并做好心理疏导工作。如果孩子已经出现视疲劳的问题，可以适当使用一些眼药水防止眼疲劳。</span>
