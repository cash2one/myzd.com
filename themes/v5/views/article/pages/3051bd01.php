			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑膜瘤有哪些治疗方法？&nbsp;</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因脑膜瘤绝大部分为良性肿瘤，故治疗以手术切除为主，复发脑膜瘤的首选方法仍是手术。并可以配合放射治疗，细胞因子治疗，基因治疗（主要为脑膜瘤病，尚处于研究阶段），化疗很少（主要针对于脑膜肉瘤可以考虑化疗）等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑膜瘤是否需要治疗主要决定于脑膜瘤部位、大小，对于周围组织有无压迫，有无颅内压增高症状。若脑膜瘤相对较小（如1cm）以下，对脑组织及颅神经无明显压迫症状，可以观察，建议每年复查一次，有无变化。脑膜瘤直径小于 crn可行x-刀或伽玛刀治疗 若肿瘤相对较大，对于周围脑组织及颅神经有明显压迫、或者有颅内压增高表现，只要患者无手术禁忌症，能够耐受手术，建议行手术切除。由于肿瘤接受来自颈内颈外动脉的双重供血 术中出血较多 彻底切除应包括受侵犯的硬脑膜及与之相邻的颅骨 否则容易复发</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.手术全切肿瘤，降低神经损害发生率：随着显微神经解剖学的研究深入，各种手术入路已比较成熟，手术关键是如何全切肿瘤。困扰脑膜瘤切除有以下问题：①肿瘤包绕重要血管和神经，造成全切困难；②肿瘤质地坚韧，无法全切；③肿瘤血供极为丰富，造成手术危险；④肿瘤侵及硬脑膜及其形成物，术中不易处理。针对以上问题，要求制定个体化手术方案，精练手术技巧，选用便利精巧的手术器械，力争肿瘤切除达到SimpsonⅠ～Ⅱ级。这样，可大大降低术后肿瘤复发。当然，评估肿瘤切除和神经损伤风险亦非常重要，这需要非常有经验的医生进行评估。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.正确选用立体定向放射外科或放疗以控制肿瘤：放疗包括光子束和质子束治疗或者分次外照射治疗。绝大多数的医疗中心使用改良的直线加速系统或者伽马刀来给患者实施治疗。在一些远离重要结构的小型肿瘤中，放疗可以用来替代手术。此类脑膜瘤主要是肿瘤位置相对较深，肿瘤体积较小（一般为3cm以下）对于脑组织及颅神经无明显压迫症状。这种方法主要用于脑膜瘤术后残留的小瘤体，或小的病变及高龄病人，要考虑及时进行放射外科治疗。对未能处理的受侵蚀硬膜、硬膜形成物和静脉窦，选用常规放疗也是防止肿瘤复发的重要措施。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">针对于放射治疗，可以在手术不能全切，而且减压充分后行放射治疗（主要为伽玛刀治疗），可以控制或减慢肿瘤复发；或者患者不能耐受手术，可以直接考虑行放射治疗（无颅内压增高及颅神经或脑组织受压）。对于次全切除的WHOⅠ级的脑膜瘤的患者，往往需要术后放疗。对于WHOⅡ级和Ⅲ级的脑膜瘤患者，目前标准的治疗方案是术后辅以放疗，不管手术切除的程度如何。这是因为这些高级别肿瘤的术后局部复发率相对较高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">伽马刀治疗脑膜瘤的目的是控制肿瘤的生长，其治疗依据是：①脑膜瘤为良性肿瘤，生长缓慢，可以用较低剂量治疗，有足够的时间让肿瘤缓慢坏死；②脑膜瘤易于在CT或MRI上显影且边界清晰，便于进行剂量计算，并可有效保护周边组织；③脑膜瘤依靠影像多能作出诊断，多数并不需要病理学论断；④老年人发病率较高，不适宜手术治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大量治疗经验表明，大脑凸面脑膜瘤，特别是矢状窦旁脑膜瘤，即便体积较小，也容易产生长期、严重的放射水肿反应，应首选手术治疗，残留部分再行伽马刀治疗。颅底脑膜瘤放射反应较轻，体积较大者可采用分次伽马刀治疗。对于高龄病人，由于肿瘤本身生长缓慢，可采用低剂量治疗，目的以控制生长为主，即所谓“人瘤共存。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.其他治疗：①细胞因子治疗。如应用Suramin可干扰细胞信号传递，应用后发现可减少脑膜瘤细胞增生。其他还有应用IL-1β，IL-6和IFN等治疗脑膜瘤。②基因治疗。如将腺病毒基因导入到脑膜瘤供应血管中，观察到瘤中有明显基因表达。主要研究集中在NF-2和p53基因等方面。③化学治疗，效果尚需要进一步探索。主要针对于脑膜肉瘤可以考虑化疗。④激素治疗。某些激素与脑膜瘤发生和发展有关。曾经使用过抗孕激素药物，但是结果各异。近来研究发现羟基脲类药物可以使无法切除的肿瘤和复发的脑膜瘤减小，但这些还需要进一步的评估。</span>
</p>
