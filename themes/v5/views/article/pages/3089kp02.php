<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;春天来了，万物复苏，各种感染性眼病也处于高发阶段。这个季节尤其重视急性结膜炎的发生，也就是俗称“红眼病”。该病好发于各年龄段。眼红是基本特征，伴有眼异物感，伴有或不伴有眼分泌物、流泪、畏光及视力下降等。因感染的微生物不同而分为病毒性和细菌性。病毒性感染除了眼红外往往表现为水性分泌物及耳前淋巴结肿大；细菌性感染的特点是分泌物多为黏性或脓性。不同微生物的感染治疗上是不同的。得了“红眼病”一定要引起重视！首先，它有较强的传染性。其次，它会引起其他并发症，严重的使视力不可逆的下降。另外，得了“红眼病”不能乱用眼药，一定要对症。因此，一旦出现眼红、眼异物感等不适等症状，要及早就医，以免耽误病情贻误治疗时机。</span>
</p>
