<div style="text-align:center;">
	<strong><span style="line-height:2;font-size:18px;font-family:'Microsoft YaHei';">梅杰综合征的诊断和治疗</span></strong>
</div>
<br />
<br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">什么是梅杰综合征</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp;梅杰综合征（Meige Syndrome)是由法国神经病学家Henry Meige于1910年首先描述的一组锥体外系疾病，也是一种累及头面部的特发性肌张力障碍，偶可累及颈部、上肢、躯干。治疗上较为困难，常常导致患者心理自卑自闭、妨碍行走和阅读、妨碍与他人正常交流，从而严重影响患者的正常生活和工作。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">&nbsp;梅杰综合征以中老年女性较多见。首发症状多数是双侧眼睑痉挛，可以逐渐累及口、面、下颌等部位。患者表现为双眼不自主的频繁眨眼动作、睁眼困难、口面下颌等部位肌肉不自主运动，呈“挤眉弄眼”状。少数患者可以单侧起病，逐渐累及对侧。患者症状往往在紧张、疲劳、强光下、注视等时诱发和加重，在睡眠、讲话、唱歌、张口等时改善。梅杰综合征主要需与面肌痉挛相鉴别，面肌痉挛往往仅单侧累及，双侧受累者罕见，主要表现为单侧眼睑不自主跳动和抽搐，可以累及同侧口角面颊，但不会出现双侧口面下颌的不自主运动。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">梅杰综合征的非手术治疗</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">梅杰综合征的非手术治疗主要包括肉毒素注射和药物治疗。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">肉毒素注射可以使部分患者症状获得改善，但一般3-6个月内复发，反复注射后效果进一步减退。 &nbsp; &nbsp; &nbsp; &nbsp;药物治疗的效果总体并不理想。常用药物有安坦、氯硝安定、巴氯芬、氟哌啶醇、氯氮平等。药物对很多患者无效，仅能使部分患者症状获得部分改善，而且药物所引起的副作用往往使患者难以耐受。左旋多巴可加重病情。卡马西平、丙戊酸钠无效。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">梅杰综合征的手术治疗</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">手术治疗是目前治疗梅杰综合征相对效果最好的方法。我们近年来采用脑深部电刺激术（Deep Brain Stimulation, DBS）治疗梅杰综合征取得了较好疗效。术后绝大多数患者症状都取得了明显改善。有的患者手术后第一天就能轻松地睁开平时几乎紧闭的双眼。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脑深部电刺激术（DBS）是当今科技发展的最新成果。DBS通过立体定向技术将微电极准确植入脑内相关神经核团，微电极通过导线经皮下隧道与埋置于锁骨下胸部皮下的刺激器相连接，刺激器通过发出微弱的高频电脉冲经微电极刺激神经核团，使基底节神经核团环路之间失去平衡的功能获得纠正，从而改善患者的症状。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;DBS手术具有微创、安全性高、可逆可控、疗效持久等优点。微电极、导线、刺激器均埋置于患者皮下，术后患者外观如常人，不影响患者的日常工作和生活。相对于其他疗法，DBS是目前治疗梅杰综合征疗效最好的方法。</span>
</p>
