			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>下腰痛的诊断和治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下腰痛是骨科临床最常见的就医原因。从专业术语看，下腰痛属于症状学范畴，其实际所指是以腰痛为根本临床表现的一类疾病的统称，但不包括伴有其他临床表现的腰椎疾病如椎间盘突出症、椎管狭窄、腰椎滑脱等。下腰痛是影响人类健康的重要疾病，是医疗支出增加、缺勤、失能（躯体的、心理的因素）的主要原因，成为倍受关注的社会经济问题。文献报告，成年人每年下腰痛的发生率为15~45%，一生中有70%以上成年人受下腰痛困扰，男性发病率为73％ ， 女性为88％， 男女均以3 5～ 5 5岁为疾病高发期。下腰痛的原因众多，发病机理复杂，临床处理棘手，骨科医生难免有这样的感叹“患者腰痛，医生头痛”。鉴于下腰痛的高发病率及其危害性，关于下腰痛的健康教育日益受重视。本文就下腰痛的诊断和治疗作一综述，以供参考。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、关于下腰痛的定义</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从疼痛部位看，下腰痛分布在腰或/和骶部解剖区域。按症状时间分为：急性疼痛≤3月；慢性疼痛≥3~6月及发作性疼痛等。对下腰痛的描述应包括：疼痛特征（强度、持续时间、发作频次）；特异性或非特异性诊断；体能和功能情况；一般特征如性别和年龄及治疗史。下腰痛的评价方法包括：1、 自我报告，被认为疼痛评价的金标准，因为可以如实反映疼痛特征，但有一定主观性，如视觉模拟评分法（VAS），此法简单易行，不需要语言描述。2、问卷调查法MPQ ，从情感、感觉、评估性方面计分，主要用于临床研究。3、疼痛画图，画出疼痛区域和强度等。此外还包括体能、功能性的测试等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、下腰痛的危险因素</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下腰痛的危险因素很多，主要可分为以下三个方面：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、个人因素：包括①年龄，35～55岁是下腰痛的好发年龄。随着年龄的增加，椎间盘及小关节退变不可避免，同时腰背肌肌力下降、韧带劳损，严重影响了脊柱的稳定性，使其容易发生下腰痛。②健康状况，一般认为，身体健康者，其四肢尤其是躯干肌肌力较强，脊柱稳定性好，而且精神状况也佳，发生慢性下腰痛的概率相对较小。③畸形，伴有脊柱后凸、侧凸畸形和下肢不等长的患者，躯体平衡性下降。因为要维持相对良好的姿势和步态，就必须通过脊柱和腰背肌进行功能代偿，其结果是导致脊柱退变和腰肌劳损引起下腰痛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">２、职业因素：①重体力劳动，下腰痛的发生与从事重体力劳动有关。Matsui等调查的结果表明，下腰痛的发生与体力劳动的强度呈正相关。②频繁弯腰和扭转，要求经常弯腰和扭转的职业，如推、拉、抬重物等会加速椎间盘、小关节退变和腰部肌肉、韧带劳损，产生下腰痛。③ 重复性工作，不仅会加速椎间盘、小关节及韧带劳损，而且由于反复做同一动作，容易引起肌肉尤其是腰背肌疲劳，削弱了其对脊柱的稳定作用，发生下腰痛。④ 静止性姿势，腰椎所受负荷的大小与体位有关。在要求长时间坐位或站立位的职业中，下腰痛的发生率一般较高。如果在坐位或立位超过2小时，下腰痛的发生明显增多。这是由于在此体位时腰部所受负荷较大，椎间盘及后部韧带容易发生劳损；另外要维持这一姿势需要腰背肌参与，而腰背肌长时间收缩会出现疲劳，更容易发生腰痛。有研究认为，驾驶员容易发生腰痛与其长期处于坐位有关。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">３、心理及社会因素：随着社会发展，心理及社会因素对慢性下腰痛发病的影响正日益受到重视。心理测试已成为腰椎功能检查的一部分，由于个体心理异常者容易发生非器质性下腰痛，还由于这些患者长期受疼痛困扰，疼痛使其精神脆弱，对治疗失去信心，产生忧虑和恐惧，以至夸大症状，增加痛感。另外，社会因素也是影响慢性下腰痛发生的一个重要因素。一般认为，工作与生活环境所造成的心理应激与下腰痛发生有关。有学者报道，对所从事职业不满意者下腰痛的发生率比满意者要高出2．5倍，而与上司和同事间的关系如何也与下腰痛的发生明显相关。也有研究证实，工作单调、时间长、注意力高度集中也可能与下腰痛的发生有关。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、下腰痛的来源</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于小腰痛的原因也是由来已久的探索课题，二十世纪三十年代，腰椎间盘突出症被认识确立，从此神经压迫被认为是腰痛主要原因。随着研究发展，椎间盘、小关节和骶髂关节被认为是主要的疼痛潜在来源，此外包括肌、筋膜、韧带等软组织来源及牵涉痛、非器质性疼痛（精神因素引起的躯体症状化）等。前三者发生率各占39%、15%、13%，上述结构均有神经支配，当伤害感受器受到有害的、机械的及化学性的刺激，就会产生疼痛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、下腰痛的诊断和治疗原则</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前对下腰痛的诊断尚无统一标准，主要的参考标准是麻醉或激发性注射，但有侵袭性、费用问题、可操作性等故临床上未常规采用。病史采集应包括：①疼痛的性质；②疼痛部位；③疼痛程度；④ 以往有无发作；⑤疼痛持续时间；⑥有无胃肠、盆腔或泌尿系症状；⑦有无伴随精神症状。除了病史和体格检查，常用的辅助检查包括：X线，能直接观察腰椎的骨性结构，如感染、肿瘤、滑脱等病变，反映部分间接征象如椎间隙狭窄、软组织肿胀等；脊髓造影，是一种侵袭性检查，可以明确诊断椎管占位、椎管狭窄等疾病；CT：主要反映骨性结构变化，椎管形态、骨质破坏等；MRI：无辐射损害，可以多方位成像，对椎间盘、神经结构等软组织敏感，但费用贵。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对下腰痛的治疗应该因人而异，其目的是尽早恢复工作。对于急性下腰痛，可通过适当的卧床休息和对症治疗达到治愈。而慢性下腰痛的治疗最为困难，除对症治疗外，还需要长期的腰腹肌锻炼和心理治疗进行配合。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">１、一般治疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">包括①卧床休息；②消炎镇痛剂、肌肉松弛剂；③理疗、按摩；④封闭疗法；⑤牵引等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">２、功能锻炼</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">功能锻炼对治疗慢性下腰痛有重要作用，其主要目的是增强躯干肌肉力量，增强脊柱稳定性。腰椎位居躯干中部，是脊柱运动的枢纽，其稳定与否直接影响着脊柱活动及下腰痛的发生。在静止时，脊柱的稳定结构主要是椎间盘、小关节及后部韧带结构，运动时的动力稳定结构主要是腰背肌。腰背肌锻炼能大大减小后部韧带的张力，增强脊柱稳定性，从而避免或减轻韧带及椎间盘发生劳损，有效防治下腰痛；同时，腰背肌锻炼还能促进肌肉及其筋膜的血循环，有利于肌肉酸性代谢产物排除，消除肌肉疲劳和肌筋膜炎症，缓解下腰痛症状。另外，腹腔对脊柱的支持作用也非常重要。保持挺胸直腰姿势，可使腹肌有一定张力，而进行腹肌锻炼又使腹肌有一定肌力，减轻脊柱负荷，防治下腰痛。还有人认为进行需氧锻炼增强人的适应性和耐力，对防治下腰痛也很重要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">３、腰骶支具</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰围是骨科医生治疗下腰痛的最常用工具。佩戴腰围不仅可通过其制动和保护作用缓解肌痉挛，减轻症状，还可保持或增加腹腔压力，分担脊柱负荷，有助于防治慢性下腰痛。因此，如果使用恰当，佩戴腰围可减轻或治愈下腰痛。但必须同时进行有效的腰腹肌锻炼，并使之成为“肌肉支架”，否则会出现废用性肌萎缩，加重腰椎不稳。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">４、劳动保护</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下腰痛的发生与脊柱的稳定失衡有关。要维持脊柱稳定，除与椎间盘、小关节、肌肉韧带等结构有关外，保持良好的姿势非常重要。长期处于站立位的人，其腰部前凸加深，加重了小关节及椎间盘的压力，可引起下腰痛。如果能通过姿势调整，使髋、膝关节轻度屈曲，身体稍前倾，这样腰部负荷就会减小，腰痛的发生也减少。在坐位尤其是前倾坐位时，由于骨盆后倾，身体重心前移，脊柱弯矩加大，更增加了椎间盘及后部韧带的负荷，容易引起下腰痛。如果能采取正直坐位，使骨盆前倾，腰部负荷将减小。如果坐椅有扶手、靠背，则不仅上肢重力的影响减小，而且因腰背部有所依靠，其负荷也减小，下腰痛的发生也大为减少。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">抬重物是一种外来负荷，其对脊柱负荷的影响，除与物体的重量、大小及形状有关外，还与腰椎的弯曲程度有关。采用伸膝弯腰法抬物时，由于躯干前倾，腰椎前屈，力臂加长，腰椎负荷最大，容易引起腰部劳损，而采用屈膝直腰法抬物时，由于物体离脊柱较近，且起立活动主要分散在下肢关节，脊柱负荷及劳损减轻。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">５、教育与训练</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有人主张开办“腰背痛学校”，采取个别辅导或配合录像集体上课的方式进行教育。学习内容包括：①相关解剖学知识；②采用什么姿势和体位最有利；③顽固性疼痛的危害性；④腰痛的发生原因和治疗方法。训练的重点是加强肌肉的强度和耐力锻炼，并指导其采取合</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">理的姿势和健康的行为。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">６、心理治疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对有心理异常的慢性下腰痛患者，其治疗需要医师、理疗师及心理学家等多方面协作，进行多方位的功能康复训练。因此，如果患者经手术或其他治疗无效，应及时进行心理学测试，以明确诊断。在治疗中，应充分了解精神因素在慢性下腰痛发病中的作用，要与患者密切交流，获得患者的信任与合作；还要向患者说明其不存在组织结构病患，疼痛的发生主要是由于情绪紧张引起的机体变化。有人认为，对治疗持乐观态度且期望值较高者，疗效较好。必要时也可配合使用抗抑郁药物进行治疗。另外，对于由生活压力或工作环境引起的心理异常，也应采取相应的措施加以改善。总之，乐观向上，举止健康合理，并经常保持运动，对维护身体健康和防治慢性下腰痛极为重要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">７、手术治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术主要适应于保守治疗无效，排除心理因素，存在组织结构上器质性损害者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">椎间盘源性疼痛</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">椎间盘源性疼痛(discogenic pain)是近年来脊柱外科的热门研究之一。首先由Crock等描述，它的主要病理学特点是纤维环撕裂，它是慢性下腰痛的最多见类型。目前可接受的定义是 ：除外影像学检查神经根受压迫的慢性腰背痛，由椎间盘本身所致的下腰痛即椎间盘源性下腰痛。明确椎间盘内神经分布走行及其功能是理解椎间盘源性下腰痛的基础：(1)椎间盘由髓核、纤维环、软骨板组成。纤维环的后部由窦椎神经分布，而纤维环的侧方和前部由来自脊神经前支和交感神经系统的分支支配；(2)Crock 认为腰椎间盘退变髓核变性致纤维环应力分布失去平衡和内层纤维环撕裂是腰椎间盘内紊乱的病理学基础。内层纤维环破裂后，神经纤维、血管和肉芽组织侵入裂隙进行修复，侵入纤维环内层的窦椎神经末梢多为无髓纤维，裸露在间质液体中，所以易感受间质内化学物质(如SP、CGRP、VIP)和机械性变化，出现椎间盘源性下腰痛。研究发现，在病变椎间盘的外层纤维环中，神经纤维的密度明显高于正常椎间盘，并且80％的病变椎间盘内层纤维环有神经分布，进一步证明了此发病机理。(3)Suseki用CGRP免疫组化的方法证明大鼠的L5S1椎间盘由L1-2 的背根节通过椎旁交感干支配，后者与窦椎神经通过灰交通支联系。因此，可以说明腰部椎间盘的交感神经分布应与L2或更高节段平面相关，临床上椎间盘源性下腰痛也主要表现在L1-2皮肤支配区，表现为下腰部、臀部、腹股沟区以及大腿外侧等部位的疼痛和这些部位的感觉异常。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">椎间盘源性疼痛伴或不伴牵涉痛，通常缺乏客观的神经学体征，临床检查难以判断。腰椎间盘外形正常，无腰椎间盘突出，X线和CT扫描正常。常继发于明显的创伤，主诉下腰正中深在的疼痛，休息缓解、活动可加重，下肢症状体征不明显，很少累及膝关节下。椎间盘源性疼痛的检查方法包括MRI、振动试验和椎间盘造影等。MRI表现：高信号区（HIZ）、椎间盘退变、终板变化、纤维环破裂和椎间隙狭窄，不同程度提示盘源性腰痛的存在，尤其是黑椎间盘（Black Disc），被认为与盘源性疼痛有密切相关。MRI显示的椎间盘异常也常见于无症状人群，Modic等发现近乎30%的正常人有异常的MR I信号强度改变。因此，MRI在诊断椎间盘源性下腰痛方面起着重要作用，但不是确定性作用。振动试验操作简单，与MRI结合对椎间盘源性疼痛有一定诊断价值。椎间盘造影：是盘源性腰痛的最重要的诊断方法和金标准。其评价标准包括四部分：被注射椎间盘的形态学、椎间盘内压力和(或)接受注射的液体容量、病人对注射的主观反应、邻近椎间盘缺乏疼痛反应。正常的椎间盘能接受0.3～1.0ml液体容量，椎间盘内峰压应在400～500Kpa。通过造影描述病人的疼痛强度、疼痛复制、疼痛行为，正常邻近椎间盘无疼痛反应。但该检查具有侵袭性，在X线透视下进行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">椎间盘退变性疾病的良好治疗必须根据疾病的自然病史进行预测，椎间盘源性下腰痛的自然病史目前还了解很少。大部分急性下腰痛发作很少超过2周，仅7%下腰痛病人成为慢性，其中仅1／3将发展成慢性失能性下腰痛，需进一步对病情进行检查。研究结果提示椎间盘源性下腰痛可能是一种自限性疾病，随着年龄增加、腰椎僵硬和整个腰椎的退行性改变，症状可能减轻。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">非手术方法治疗下腰痛的方法包括卧床休息、锻炼、牵引、针灸、经皮电刺激、支具、生物反馈、药物治疗和按摩等。对椎间盘源性下腰痛病人至少要进行3个月的非手术治疗，包括非类固醇抗炎药物、肌肉松弛剂和物理康复程序。物理康复程序包括温水疗法、有氧泳疗、等长躯干肌锻炼。未完成康复程序的病人应行心理测试和疼痛门诊治疗。完成3个月康复程序且疼痛减轻者应进行功能评估后再恢复工作。如果完成康复程序仍有下腰痛，可考虑进行手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术治疗椎间盘源性下腰痛首选融合手术。虽然已有其他方法，如椎间盘切除术、经皮椎间盘切除术、木瓜凝乳蛋白酶髓核溶解术等治疗椎间盘源性下腰痛的报道，但这些方法的疗效还没有得到肯定。人工椎间盘置换术和人工髓核置换术还处于起步阶段，长期疗效有待</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">观察。椎间盘切除和椎体间融合术可能是治疗椎间盘源性下腰痛的最有效方法。其机制有二，一是切除了疼痛源，二是消除了节段的运动。手术融合方式目前主要有3种：前路融合、后路融合和前后路联合融合。前路融合具有直接切除累及的椎间盘和避免后路椎旁肌剥离及去神经支配的医源性创伤等优点。Saal等于2000年首次报道应用椎间盘内电热疗法(intradiscal electrothermal therapy，IDET)治疗椎间盘源性下腰痛的初步临床结果，引起了国际脊柱外科界的广泛重视。IDET是一种针对椎间盘源性下腰痛的微创疗法，近年来发展较快，并已被临床证明有效。一定程度的热量能使胶原纤维收缩并凝固神经纤维。当IDET过程中组织被加热至一定温度(60～65度)时，维持胶原纤维三螺旋结构的共价键断裂，胶原分子收缩变厚，纤维环裂隙重新连接、加固，从而使纤维环的生物力学状态得到改善，提高了脊柱运动节段的稳定性；此外，热能可凝固分布于纤维环和后纵韧带上的神经纤维及椎间盘内的肉芽组织，改善椎间盘的炎性环境，减少刺激的传入。IDET的初步临床疗效令人鼓</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">舞，它为难治性椎间盘源性下腰痛提供了一种微创的治疗手段。与椎间盘融合术相比，IDET具有明显的优势，如微创性、费用低、疗效显著及并发症少等，特别对多间隙或椎间融合术后邻近节段出现的椎间盘源性下腰痛病人是一种较好的治疗选择，但IDET 尚处于起步阶段，缺乏长期的临床随访资料，目前尚无随机双盲、前瞻性对照的临床研究，临床疗效有待严格评价。对于盘源性腰痛的生物学治疗的基础研究也在广泛深入展开，包括基因治疗、干细胞移植等达到椎间盘功能再生的目的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">小关节源性疼痛</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰椎小关节是脊柱连接的重要关节，具有限制脊柱前屈前移运动，抵抗压缩、剪切及旋转等各种形式负荷的功能。外伤、异常的应力等因素均可导致退变和损伤。小关节即关节突关节，为滑膜关节，滑膜面和关节囊分布有丰富的神经末梢，感受和传递疼痛信息。发病机制：关节软骨的损伤和炎症；继发于椎间盘退化的小关节功能失代偿，引起柱体节段进行性结构不稳，加速小关节退变、增生，压迫刺激神经根引起疼痛。临床表现：缺乏特异性，主要位于臀纹以上的下腰部疼痛，伴或不伴有牵涉痛，小关节处有深压痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于小关节源性疼痛的检查主要是CT和MR,CT可以发现关节突肥大、增生、硬化，关节面凹凸不平，软骨下骨囊性变，关节腔内真空现象，关节半脱位及小关节骨质增生引起的侧隐窝狭窄等；MRI显示增生小关节对椎管及神经根管的压迫，关节囊的水肿，关节囊内软组织突出及关节腔内的滑膜变化等。小关节阻滞是诊断小关节源性下腰痛的金标准。根据疼痛缓解时间与局麻药作用时间匹配与否来判断药效。但该检查方法属有创性检查，可增加感染的发生机率，且不能用于对局麻药过敏的患者。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗上除了腰痛的常规治疗手段，就是关节腔内注射，小关节内侧神经支阻滞，内侧支神经变性、破坏（物理、化学、手术）等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骶髂关节源性疼痛</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骶髂关节是微动关节，有坚强的韧带、肌肉和筋膜共同固定和限制关节活动，维持关节的稳定性，以传导和平衡躯干至下肢的应力。骶髂关节常见的病理改变主要包括畸形、损伤、劳损、退变及炎症等。第五腰椎以下部位疼痛、可伴有放射痛，缺乏特异性。临床检查和骨扫描有助于诊断。X线透视下骶髂关节内封闭术，如注射后15～45分钟内患者疼痛缓解75%，则一般可诊断为骶髂关节源性下腰痛，是最可靠有效的方法。非手术治疗无效，可考虑行骶髂关节融合术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肌肉源性疼痛</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脊柱椎旁肌群是脊柱的动态稳定结构，维持人体直立姿势。骨骼肌纤维包括I型和Ⅱ型，其中I型纤维收缩速度慢，收缩持久耐疲劳，抗重力作用强，对维持机体的生理姿势和完成某些精细活动具有重要意义；Ⅱ型纤维收缩速度快，易疲劳，抗阻力作用强，对完成机体的快速粗大运动有重要意义。因此，腰背肌中I型纤维占优势是与其功能相适应的。下腰痛患者往往伴有腰背肌肌力或耐力下降。长期坐位工作，活动少，可以造成椎旁肌的废用性萎缩。研究显示，椎旁肌I型纤维所占比例小、横切面积小，抗疲劳的能力降低。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">骶棘肌的收缩抵抗前屈力矩，当腰背肌疲劳时，骶棘肌功能减弱，从而使椎间盘和韧带承受较大的应力，使之易于损伤。肌肉疲劳其协调性和控制能力下降，腰椎动态稳定性下降，腰椎过度活动、腰椎不稳。腰痛引起椎旁肌的保护性痉挛，活动减少，加重椎旁肌的易疲劳性，形成恶性循环，可能是下腰痛拖延难愈和反复发作的原因之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，椎旁肌骨筋膜间隔间隔综合征被认为是肌源性腰痛的发病机制。由于各种原因引起的骨筋膜室内压力增高，肌肉缺血，产生慢性腰痛。临床上通过骨筋膜室切开减压可以缓解或消除疼痛，从侧面证实这一病因的存在。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">结语</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰痛是全球性的医疗和社会经济问题，值得医患双方及社会公共机构的共同关注。普通民众通过健康教育、职业培训、劳动防护等获取相关知识和信息，可以预防腰痛发生。腰痛患者对自身疾病的了解，有助于提高治疗效果，早期康复。医生方面应当不断提高认识水平，足够重视腰痛这一常见病，为患者造福。</span><br />
