	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>张旭：把微创腹腔镜手术做成艺术</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2011年6月11日，德国东部城市莱比锡，一位前列腺癌患者接受了难度很大的“二次手术”，为他进行手术的是一位中国医生。半个多小时后，这位中国医生及其助手将乒乓球大小的癌变组织完全切除并取出体外。接着，这位中国医生又采取自创的单针吻合技术，用十几分钟将病人的膀胱和尿道连接起来。整个手术历时仅一小时，而通常情况下仅缝合这一个程序就要一两个小时。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 手术结束时，在场观众向这位中国医生报以了热烈的掌声。这场手术就是2011年欧洲腹腔镜手术大会上特殊的手术表演，这位中国医生就是中国人民解放军总医院泌尿外科主任张旭教授。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 为微创腹腔镜手术改路</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 张旭把微创腹腔镜手术比作“枪”，把传统开放式手术比作“刀”。在上世纪90年代中期，在华中科技大学同济医学院附属同济医院做泌尿外科医生的张旭，就已经把开放式手术演绎得出神入化。但随着世界上第一例腹腔镜手术以微创优势在普通外科迅速普及和推广，张旭敏锐觉察到微创腹腔镜手术在泌尿外科的前景。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; “腹腔镜手术创伤小、并发症少、术后痊愈快、住院时间短。开放手术要在病人腰腹部切一个20~30厘米的口子，常常还要切掉一截肋骨，由于肌肉和部分神经被切断，有的病人术后一个月都直不起腰。而腹腔镜手术只打三个小孔，每个孔0.5~1厘米，患者术后第二天即可下床。”张旭说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 于是他毅然改变了研究方向，开始主攻腹腔镜手术。那段时间，张旭仿佛又回到了住院医生的阶段，从最简单的腹腔镜体外模拟训练到动物实验，重新认识腹腔镜下解剖结构，重新设计腹腔镜手术步骤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 在西欧等国家，泌尿外科的腹腔镜手术都是经腹腔途径，原因是腹腔解剖标志显著，操作空间大，但缺点是容易损伤其他器官。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 如果改变腹腔镜的入路，效仿传统开放手术，从腹膜后进入则距离目标器官较近。但另一个问题又出现了，腹膜后空间狭小，无明显解剖标志，手术难度显著增加。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 经过多年的探索，张旭通过优化腹膜后操作空间的制备方法，又对腹膜后的镜下解剖进行深入研究，形成了一整套以腹膜后入路为特色的泌尿外科腹腔镜手术技术。在欧洲腹腔镜大会上，张旭是首个被邀请做手术演示的亚洲医生。他设计完成的手术，还多次被引入美国专业医学教材。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; “一台传统的开放肾部分切除手术，要将近两个小时才能完成，而张旭在腔镜下只用20分钟就完成了。整个手术没有一个废动作，行云流水，他把高难度的手术变成了艺术。”解放军总医院泌尿外科张旭的同事们这样说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 推广技术，服务百姓</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 至今，张旭创立的腹腔镜手术术式已近30种，涵盖了泌尿外科所有体腔内的脏器，完成手术5000多例，包括肾上腺手术、肾脏手术、输尿管手术、前列腺手术以及膀胱手术等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;他主持的“腹腔镜技术在泌尿外科的应用研究及推广”项目，曾获2006年度国家科技进步奖二等奖。如今，国内泌尿外科的标准术式是后腹腔镜术式，而不是欧美的经腹腔术式，这种技术特色也赢得了国际微创泌尿外科学术界的高度认可。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; “好的技术应该为广大老百姓服务，况且，一个学科的完善和发展，靠的绝不是某一个医生。”近年来，张旭不辞劳苦地奔忙于全国各地推广这项技术，主办全国腔道泌尿外科学术会议；到全国数十所大学附属医院和近31个省市自治区的核心医疗机构进行手术演示和举办学习班，让更多同行分享这项技术，让腹腔镜技术更多更好地服务于患者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 2008年，张旭作为优秀人才被解放军总医院特招入伍，肩负起泌尿外科学科带头人的使命。3年来，解放军总医院泌尿外科的微创手术比例逐年稳步升高，2010年达到87%，位居国际先进水平。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; “在他的带领下，解放军总医院泌尿外科快速推进了20年，避免了很多学习阶段出现的错误，简化了以往的手术方案。”张旭的同事这样说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 在张旭看来，这个学科的发展需要的是医学科学家，而绝不仅是开刀匠。这也是他以及更多同行努力的方向。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 优化手术方案，为患者省钱</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 虽然腹腔镜技术已经成为当今外科的首选手术方法，但它的高额费用却让许多病人望而却步。这主要是因为这项技术的一些关键配套医疗器械只有国外才能生产。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 如何降低医疗费用，让更多患者能够接受这样的微创手术？为此，张旭花了不少心血来设计新的手术方法和相应的手术器材。比如自制气囊以替代进口扩张气囊，自制标本袋以替代进口标本袋，设计新术式以减少手术耗材的使用等。整个过程算下来，使每台肾脏、膀胱或前列腺腹腔镜手术降低了4500元左右的手术费。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 张旭说，做个好医生，首先心里要装着病人。张旭的手术做得好，慕名来到解放军总医院找他看病的患者非常多。为了尽量减少患者等待手术的时间，张旭提出实行连台手术，甚至一天要连续做6~8台手术。接受记者采访前，张旭从早晨9点到下午3点已完成了三台腹腔镜前列腺癌根治术和两台腹腔镜肾部分切除术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; “每天我的工作都像是在走钢丝，因为我面对的是一个个的生命，而生命不能重新再来。”张旭说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 张旭，1962年出生，现任解放军总医院外科临床部泌尿外科主任，主任医师，教授，博士生导师，担任全军医学会泌尿外科分会副主任委员等职务。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 长期致力于泌尿外科手术技术研究，提出并成功开展肾实质肾盂分层切口治疗复杂性肾鹿角型结石的方法；对尿道下裂修补技术进行了较系统研究。开展改良乙状结肠球形正位膀胱术等新技术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 创立泌尿外科后腹腔镜手术技术，完成30余种、5000多例泌尿外科腹腔镜手术，在全国31个省市自治区的大学附属医院和核心医疗机构及新加坡中央医院等东南亚国家医院推广应用，获2007年度中华医学会泌尿外科分会金膀胱镜奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 获国家、省部级科技进步奖8项，其中“腹腔镜技术在泌尿外科的应用研究及推广”获2006年度国家科技进步奖二等奖。（《科学时报》 2011-08-19 王璐 王继荣 罗国金 王佳斌）</span>
</p>
