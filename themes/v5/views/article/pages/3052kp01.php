		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>陈晓明：理想的医院 应缩短患者等待时间（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">7月31日，川内知名眼科专家、前华西医院眼科教研室主任、博士生导师陈晓明教授离开工作了20多年的“前东家”，并迅速以一个全新的身份——华厦眼科医院院长出现在世人面前。一些市民、医生在此次“四川名医口碑榜”中看到陈晓明新身份时，还纷纷打进热线质疑是否写错陈晓明教授就职医院的信息。知名博导的这一举动让业界、患者感到意外。对此，陈晓明表示，他有一个朴素的想法：缩短患者等待时间，避免失明的灾难。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 创办医院</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 挽救更多人的视力</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 作为四川眼科行业领军人物、国内知名的青光眼专家，从三甲公立医院离职，创办一家民营医院，这样的举动让很多人都不理解，包括他的家人。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 老伴抱怨：“你辛辛苦苦一辈子，这两年好不容易轻松一点，你又不图钱，干吗把自己搞得那么累？”陈晓明说：“我就是不能这样轻松，想多看几个病人；一想起那些挂不上号、预约号至少要等两个月、一见到我就苦苦哀求的病人，我就心里沉甸甸的轻松不起来；让更多人能看得到病，保住自己的视力，对我来说，就是最好的事情、最该去做的事情！”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 据了解，陈晓明教授即使一天的门诊中放出120多个号，还是有很多病人挂不上号、看不了病。看病难，手术更难，因为住院床位紧张，想要排到他的手术往往病人需等两三个月。“人能等，但病不能等，青光眼往往是很急的病，每一周、每一天，病人的视野都在高眼压的威胁下一点点地被蚕食、缺损，越来越小，直至最终完全坠入黑暗。那将不单是患者本人，甚至是一个家庭的灾难。”陈教授说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 远道而来的病人和家属拦路下跪，成了陈教授心里永远的痛。他唯一能做的，就是遇到挂不上号又病情紧急的病人，常常免费给他们看；但是手术排期的问题，依然很难解决。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 毅然转身</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 突破自我束缚</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 一说起那些被青光眼折磨在失明的恐惧中的病人和家属，陈晓明教授满脸沉重，“我搞了一辈子的青光眼，研究了几十年，传承了老一辈专家教授们的经验和心血，就是希望能造福更多的病人……”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 近年来，随着国家医改的逐渐深入，扶持民营医院发展的政策越来越多、越来越好，这让陈教授看到了医疗资源走向合理分配的契机。他对自己的同事，另一位博士生导师、著名眼眶病专家罗清礼教授说：“在国外，人们对民营医院的印象、认可与国内完全是两种不同状态，民营医院的社会地位、学术地位都非常高。现在，国内民营医院越来越成熟，专业眼科医院拥有更多的床位、更集中的医疗资源，这就是它的发展优势，我们要充分地将它运用起来，为更多的患者做好服务。”于是，两位临到退休的知名教授毅然离开了公立医院，创办了成都首家三级眼科医院——成都华厦眼科医院。“把公立医院医术、医疗质量管理带到民营医院，更多更好地服务病人，希望我们能做一个表率，为医改探索更多的经验，为老百姓带去更多的福利。”陈晓明说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 薪火相传</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 培养更多基层好医生</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 慕名从省内各地赶来成都找陈教授诊治的病人说：“陈教授手术做得漂亮，诊断水平高，县里医院的医生看不好，让我们上成都找陈教授！”对此，履新前夕，陈晓明教授对自己在华厦眼科医院的工作已经有了清晰的计划和安排。陈教授表示，除了看更多病人，还希望今后能够培养更多具备扎实基本功的年轻医生，帮助基层医生提高青光眼的早期诊断和治疗水平。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 早年在湖南湘雅医学院师从国内著名青光眼专家吴振中教授、蒋幼芹教授，攻读硕士、博士研究生，以及数年转科、实习的经历，无疑让陈教授打下了无比坚实的临床工作基础。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 上世纪80年代末，尚在攻读博士研究生期间，陈晓明发表的多篇视野研究论文被《中华眼科杂志》系列发表，这让他早早在国内眼科界崭露头角。彼时，国内还没有统一的视野研究规范，他和导师一起从国外引进了先进的检查技术，大大提高了青光眼早期发现和诊断的几率。在湘雅医学院举办的先后两期全国视野研究班，他作为主讲讲师之一，为来自全国各地的基层眼科主治医师们讲解视野研究的新进展和临床技术应用，推动我国首次在临床上开始了标准化的视野检查。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 在美国约翰·霍普金斯大学医学院WILMER眼科研究所做博士后研究的经历，让他深切感受到国内青光眼诊治和科学研究十年以上的落后和差距。二十多年过去了，他已能欣慰地说：“以前我们买不起设备，降压药也只有一两种，现在我们在青光眼的诊断、治疗、科研上的水平已经与国际同步。”他表示，现在需要做的，就是让两代眼科人艰苦努力好不容易获得的技术、学术进步，能够造福更多的老百姓，让更多的患者能够保住赖以生活的视力。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 多年来，因为他的科研成果和卓越贡献，陈晓明教授历任或现任“中华医学会眼科学分会常委”、“中华医学会眼科学分会专家会员”、“中华医学会眼科学分会青光眼学组委员”、“中华医学会眼科学分会防盲学组委员”、“中国医师协会眼科医师分会常委”、“国家自然科学基金委员会评审专家”、“四川省医学会眼科专委会主任委员”、“四川省医师协会眼科专委会主任委员”、“美国约翰·霍普金斯内外科协会会员”、“美国眼科学会会员”。2007年获中华眼科学会奖，今年获“2015年中国优秀眼科医师”殊荣。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 并肩战斗</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 两大专家挑战“看病难”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 随着我国医改政策的不断出台，医生流动的潮流在四川医疗界已经日渐明显。此次与陈晓明教授共同离职创业的还有一位他的老同事——国内知名眼眶病、白内障专家罗清礼。四川眼科界两大领军人物、两位知名博导从公立医院离职，只为探索一条医改创新之路。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 目前全国80%的医疗资源集中在大城市，其中30%又集中在大医院。正是这种医疗资源结构的失衡，让病人过度集中求诊，很多三级医院不堪重负。经过一段时间的考虑，两位教授最终选择走出体制，创办了一家全新的大型专业眼科医院：成都华厦眼科医院。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 据了解，新建的医院按照国家三级眼科标准，斥资近亿元，引进了一大批国际一流的眼科设备，科室配备齐全，开设病区近10个，床位近百张。除拥有先进的硬件配置外，医院还吸引了多名在西南地区乃至全国都有影响力的知名专家加盟，“打造辐射西南地区的疑难眼病诊疗中心”成了这家医院的发展目标。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 对此，省内某公立医院的一位眼科医生对记者表示，若换作几年前，年轻的医生或许根本不会考虑“选择哪条路”这个问题——当时“公立”、“三甲”就是最好的选择。但如今，随着陈晓明和罗清礼等先行者探索出另一种理想模式，他们也将有了更多的选项。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 有业界评论认为，陈晓明和罗清礼两位眼科大腕离职创办医院，正是医改政策引领下的成功创新与实践，是积极顺应国家医改浪潮的举动，主动跳入市场化的河流，成为我国医改一线的实践者、推动者，也是为缓解“看病难”、“看病贵”等探索一条新路子。（黄文娟）</span>
