			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“沉默的杀手”前列腺癌</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不久前，日本著名作家渡边淳一因前列腺癌去世，引起人们对前列腺癌的关注。提起前列腺疾病，人们认识更多的是前列腺炎和前列腺增生，对前列腺癌却所知甚少。它是一种什么样的疾病，有什么危害，该如何防治呢？日前，中华医学会北京泌尿外科分会、天津泌尿外科分会与西安杨森制药有限公司在京举行了“泽被生命，创造可能”前列腺癌高发趋势及前沿治疗媒体沟通会，与会专家呼吁公众要提高对前列腺癌的认知，增加对日益攀升的前列腺癌发病率的关注。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp;</strong><strong></strong><strong>早期难发现</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中华医学会北京泌尿外科分会主任委员、北京协和医院泌尿外科主任李汉忠教授表示：“前列腺癌是全球范围内男性中第二位常见的癌症，2012年新诊断的前列腺癌患者为110万人，约占新增癌症病例总数的15%。近10年来，我国前列腺癌发病率呈现高发趋势，大城市更成为前列腺癌发病率的‘重灾区’。”数据显示，北京市前列腺癌发病率由2001年的5.53/10万上升至2010年的16.62/10万。天津市20年间的前列腺癌发病率增长约4倍。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前列腺癌也被形容为“沉默的杀手”，早期不易被发现，通常确诊时病情已发展至晚期，主要症状表现为尿血、尿痛及骨痛。中华医学会天津泌尿外科分会主任委员、天津医科大学第二医院院长徐勇教授表示：“早期前列腺癌确实很难有蛛丝马迹，因此，很多患者被发现时已到中晚期。晚期前列腺癌最主要的治疗方法是内分泌治疗，也叫雄激素阻断治疗。在前列腺癌中，雄激素能够促进肿瘤生长，而雄激素阻断治疗通过降低雄性激素水平从而延缓疾病的进展。该疗法虽然具有一定疗效，但是因为大多数前列腺癌细胞会演变成性激素非依赖型，所以大部分患者的病情缓解仅能维持1.5年到4年。”随后患者病情会进一步发展为去势抵抗性前列腺癌(CRPC)，甚至转移至前列腺以外的其他器官(比如骨骼)，成为转移性去势抵抗性前列腺癌(mCRPC)。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<strong>治疗待突破</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一项针对前列腺癌患者的调研表明，晚期前列腺癌患者的健康相关生命质量(HRQoL)评估结果显著劣于其他阶段的患者，这主要由于晚期前列腺癌患者饱受疼痛、疲劳、睡眠障碍等疾病症状的严重困扰。北京大学第三医院泌尿外科主任马潞林教授指出，“随着治疗理念的不断改进，晚期前列腺癌的治疗目标已经不仅仅局限于延长患者的生存时间，同时需注重减轻患者的疾病症状并改善他们的生活质量。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 北京大学第一医院泌尿外科主任周利群教授强调，“转移性去势抵抗性前列腺癌一直是晚期前列腺癌治疗的难点。数据显示，转移性去势抵抗性前列腺癌患者的生存期通常小于2年。国际研究提示，如果雄激素水平能够进一步降低，患者的生存期可进一步延长。因此，全面阻断睾丸、肾上腺和肿瘤本身合成的雄激素，已成为前列腺癌治疗的新思路。”据介绍，目前，新一代的抗雄激素治疗药物已在国外上市并得到广泛应用。临床数据表明，该药物能有效延长患者总生存时间，并改善他们的生活质量。值得期待的是，这些新药也会被陆续引入国内，为我国转移性去势抵抗性前列腺癌患者带来全新的治疗选择。</span>
</p>
