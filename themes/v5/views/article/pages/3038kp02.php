			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>斜视手术有什么风险和后遗症？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">斜视矫正手术目前是一种成熟并相对安全的手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术最常见的问题是矫正不到位。由于斜视是根据病人的斜视类型、斜视度和其他因素对运动眼球的肌肉的位置和长度进行调整，由于个体差异，可能手术会发生矫正不足或矫正过度，但可以再次手术矫正。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">斜视手术在眼球外进行，所以一般不会影响视力，即使手术失败，也不会导致视力下降。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般常见的问题是手术后的复视，但孩子视觉系统可塑性很强，所以复视一般都会消失，如果斜视完全矫正，复视可能促进孩子双眼视的发育。对于成年人，手术后的复视绝大部分人都可消失，极个别人不消失也会逐渐适应，不至于影响生活和工作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">斜视手术在白眼仁表面的结膜上切开进行，不会在眼皮上留下瘢痕影响外观。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，比起斜视本身的危害来说，手术的风险和后遗症算不上什么！</span><br />
