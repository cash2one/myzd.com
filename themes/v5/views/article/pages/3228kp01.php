			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>儿童中耳炎</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">半夜里，睡眠中的小宝宝忽然尖声啼哭，如果将他抱起则哭声减弱或停止哭泣，一旦将他放下平卧哭声又剧。这常常是孩子得了中耳炎的表现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 婴幼儿出现呕吐、上呼吸道感染，尤其是伴鼻塞时，鼻腔内的分泌物携病毒、细菌就会导流入中耳，引起中耳炎。表现为局部疼痛和耳腔有液体渗出，严重时会流脓，并常伴发热。当孩子平卧时，渗出物集聚在耳室内，使之内压升高，引起剧痛；当将宝宝竖起时，渗出物流出到鼻腔内，耳室内压减低，疼痛减轻。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 由于中耳炎是儿童的常见病和多发病，而冬春季节是高发期，并且与儿童上呼吸道感染有着密切的关系，一般继发于上呼吸道感染48小时内发病，并且病程不超过12周。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 儿童急性中耳炎的治疗包括病因治疗和症状治疗。其病因治疗主要是抗菌药物的应用，结合其他对症治疗等，进行综合性治疗。根据国内外临床实践经验，使用广谱阿莫西林类，疗程通常为7-10天。局部治疗包括有滴耳剂、喷鼻剂等的使用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 中耳炎如不及时治疗将引起宝宝听力下降甚至耳聋。婴幼儿由于不会言表，等到被发现时常常已较严重，故应预防为主：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 平时孩子一旦有呕吐，注意不要让呕吐物流入中耳。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当孩子感冒伴鼻塞时，虽无发热，也应及时保持宝宝鼻腔通畅。可用口服滴剂，它能有效解除宝宝因感冒等原因引起的鼻塞，能预防上呼吸道感染并发的中耳炎，又没有明显副作用。</span>
</p>
