<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小肠移植技术亚洲领先</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 中国工程院院士黎介寿12月24日表示，继1994年完成亚洲首例单独小肠移植后，该院成功完成的第14例小肠移植已度过一周年生日，这位病人手术半个月后肠功能完全恢复，现在口服正常饮食，身体状况良好，每日还进行登山运动。这标志着我国小肠移植技术已从临床实验性使用发展到了临床常规技术，也使得中国小肠移植技术在亚洲领先。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 小肠移植排斥反应及感染发生率高达90.5%，是比肾、心、肝等器官移植更为困难的器官移植手术，目前仅有美国等少数几个欧美国家能进行此项手术，南京军区南京总医院是国内唯一仍在开展小肠移植的医院。南京军区南京总医院解放军普通外科研究所副所长李宁说，与以往有突破性进展的是，过去病人恢复进食需要两三个月时间，现在病人手术后十几天就能口服饮食；过去病人需要终身静脉营养，且不能出院，现在一个月不到就能停止静脉营养，而且口服药减少到只需一种抗排异药。专程赴美国匹兹堡大学Thomas. E Starzl器官移植研究所学习小肠移植关键技术的李元新副主任医师介绍，小肠移植将成为肠衰竭病人的最为理想治疗方式，一旦病人不能依赖肠道吸收营养维持生存，应尽早进行小肠移植。&nbsp;</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 山东“换肠人”迎来周岁生日</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 2008年平安夜，对于山东“换肠人”22岁的小吴来说，注定是刻骨铭心的日子。昨天在南京军区南京总医院普通外科研究所，这个帅小伙在鲜花和烛光中迎来了手术后存活一周年“生日”。现在他每天爬小山坡、慢跑800米，一日三顿，过着常人一样生活。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 这是南京军区南京总医院全军普外研究所继1994年完成亚洲首例小肠移植(至今共完成15例小肠移植手术)以来，取得的又一个标志性成果。全小肠移植存活时间1年、肠功能完善，拥有良好生活质量。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 7年前的冲撞成了无肠人</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 今年22岁的小吴，是山东济南人。上初二时，一次在操场上跑步，他和一位同学发生冲撞，小吴的腹部重重撞上了同学的膝盖，在医院，小吴的脾脏被全部切除。一晃7年过去了，小吴已大学毕业，正准备踏上工作岗位，谁料7年前早就被遗忘的手术竟出现了罕见的并发症，差点要了他的命。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 去年中秋节，小吴因为一时高兴，多吃了东西，第二天就感到腹痛胃胀，剧烈腹痛竟持续了15天，不能排便。他从山东被转运到南京军区总医院急诊室的时候，已经处于休克状态，生命迹象越来越弱，医院一连下了5份病危通知书。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 小吴患的是脾切除术后并发症——身体处于高凝状态，最终导致门静脉系统血栓形成，该病症起病隐匿，若不及时发现处理，可使肠道静脉回流发生障碍，严重者出现肠坏死，危及生命。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 当天晚上给小吴做急诊手术的李元新教授告诉记者，在手术台上，发现小吴的小肠全部坏死，还有急性弥漫型腹膜炎，必须先进行小肠切除手术。李元新主任在全麻下行“坏死小肠切除、近端空肠插管造口、末端回肠插管造口、胃造口、腹腔冲洗引流术”。术后给予禁食、抑酸、抑制消化液分泌、抗感染、静脉营养支持等治疗。以后又出现了静脉感染，“胆泥淤积”、真菌感染等症，经历多次对症治疗。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 9小时手术移植小肠300cm</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 小吴两个多月不能进食，完全依赖静脉营养维持生命。小肠移植是最理想的治疗方式。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 2007年12月24日下午5点，人们都沉浸在平安夜的喜悦中，小吴在经过充分的术前准备后，再次被推进了手术室，在全麻下进行“同种异体小肠移植术”，移植小肠约300cm。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 在黎介寿院士、李宁主任的统一领导下，全院多个科室10多位医生大协作，紧张而有条不紊地进行手术。手术分了多个小组，李元新主任带领的供体组成功获取全腹脏器移植物，并对移植小肠进行修整。与此同时，李宁主任、朱维铭主任带领的另一组医生开始移植前血管和消化道的受体手术准备，黎介寿院士始终在手术第一线给予指导与指挥，每当遇到困难时，黎院士的决策与经验是克敌制胜的关键。随后，李元新主任紧张地进行移植小肠的血管吻合，这是一项精细工作，需在手术放大镜下完成。“当血管吻合完成，血液贯通的刹那，移植小肠立刻由苍白转为红润，并出现肠蠕动，有清亮的肠液流出。”李元新主任对此记忆犹新。李宁主任随后完成移植小肠的消化道重建工作，这是小肠移植的另一个难点。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 9个小时过去了，</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">手术组的医生们看着已经开始自主蠕动的小肠都松了一口气。此时已是午夜12点钟，圣诞钟声响起，小吴的手术成功完成，年轻人在平安夜迎来了新生。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 术后两周看完6本武打小说</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 这次小肠移植手术借鉴了全球最顶尖的美国匹兹堡大学小肠移植中心手术外科技术和术后抗排斥、抗感染综合处理技术，使得病人恢复异常迅速。术后第4天开始给予肠内营养，手术后第15天，他就停止静脉营养，开始吃饭了，术后两周，小吴竟一口气看完了6本武侠小说。术后1年，他能正常饮食，除了每天吃几粒抗排异药，不再用任何药物，治愈出院。&nbsp;</span>
</p>
<p>
	<br />
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 小肠移植研究发展历程</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 黎介寿院士从1968年肠外瘘的临床研究开始，开启了肠功能障碍的系列研究，上世纪70年代在国内率先进行肠外营养研究，80年代中期，全球尚没有成功临床小肠移植先例的前提下，便开始进行小肠移植的大动物实验，90年代初成功建立大动物小肠移植模型。1994年完成亚太地区第一例小肠移植，当时全球仅欧美国家少数几个移植中心完成三十余例小肠移植，在小肠移植起步阶段的环孢霉素时代取得了存活近一年的疗效，成为当年国家十大医药新闻之一。2003年完成国内首例肝小肠联合移植。先后完成包括肝小肠联合移植在内的共15例临床小肠移植，其中3例为亲体小肠移植。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 小肠移植的进步是伴随着抗排斥治疗方案的改进，2003年以来，外科技术、免疫抑制方案、排斥反应的监测与治疗、感染防治等主要技术的进步，使得小肠移植术后生存率大大提高，目前美国匹兹堡大学的小肠移植病人一年的生存率达到90%。作为医学大家的黎介寿院士敏锐地把握了这一重大临床技术的发展脉搏，2006年初选送在小肠移植领域积累丰富经验的江苏省重点医学人才（省卫生厅组织的“135”工程）——李元新副主任医师赴美国匹兹堡大学Thomas. E Starzl器官移植研究所学习小肠移植最先进的关键技术，师从全球最著名的小肠移植和腹腔多脏器移植专家Kareem教授，学习全球最先进的小肠移植和腹腔多脏器移植技术。李元新副主任医师在2007年3月学成归国，在黎介寿院士和李宁主任直接领导下，在总结既往小肠移植的经验基础上，借鉴匹兹堡大学先进经验，对外科技术和围手术期处理等关键技术进行重要改进，使研究所的小肠移植的关键技术上达到国际先进水平。尤其值得一提的是研究所在国内大器官移植中率先使用应用CD52单克隆抗体(Campath 1H)，其目的是诱导部分免疫耐受，术后不用激素，仅单用低剂量普乐可复维持，这是器官移植术后免疫抑制理念的一种革命性的改变，术后免疫抑制剂用药量比肾移植、肝移植还小，不仅减少了移植术后感染和免疫抑制剂药物毒性的影响，而且大大减低了病人的经济负担。2007年后完成的小肠移植病人术后短期内便恢复口服饮食，拥有良好的移植肠功能和生活质量。研究所副主任、江苏省领军人才李幼生主任医师近期又从美国学成归来，不仅带来了美国匹兹堡大学的先进经验，还带来了其他国际先进小肠移植中心的经验，为研究所小肠移植事业的发展注入了强大的生命力。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南京军区总医院普通外科研究所</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南京总医院全军普通外科研究所，是国家、军队、江苏省重点学科（实验室），南京大学博士学位授权学科，第二军医大学、南方医科大学博士研究生联合招生点，国家博后工作流动站。是国家卫生部临床药理基地（营养支持专业），全军胃肠外科重点实验室。在黎介寿院士的领导下，学科立足于医学发展前沿，融医疗、教学与科研为一体，更新教学观念，深化教学改革，改善教学条件，完善培养规划，逐渐形成为“具有一流的师资队伍、一流的教学条件、一流的教学质量和一流的教改成果的国内一流的医学创新人才培养工作的示范点”。&nbsp;</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 学科带头人，</span><span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">黎介寿，中国工程院院士，著名普通外科专家，医学教育家。主任医师。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 专业特长：</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">肠外瘘的治疗，同种异体小肠移植，临床营养支持及各种普通外科疑难杂症。1968年开始肠外瘘治疗的研究，1971年在国内最早开始应用肠外营养支持，1985年起研究同种异体人小肠移植，1994年研究胰十二指肠移植，1996年研究生长激素与营养支持对肠功能的康复、促进蛋白质合成与组织愈合的作用，均都获得国内领先的结果。曾获全国科学大会奖一次，国家科技进步二等奖二项、三等奖一项，军队科技进步一等奖四项、二等奖五项，国家教委科技进步二等奖一项，江苏省科技进步一等奖1项、二等奖2项、江苏省教育成果一等奖1项。</span>
</p>
