<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 专家介绍：</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 李宁，南京军区南京总医院解放军普通外科研究所主任、副所长，教授，博士生导师，博士后联系导师，主任医师；中华医学会肠外与肠内营养支持学分会主任委员，中华医学会外科学分会常务委员，营养支持学组组长。从事普通外科近30年，在复杂胃肠道疾病、小肠移植、肠瘘、临床营养支持等方面均有很深的造诣。</span>
