<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 李幼生，南京军区南京总医院普通外科副主任，教授，博士生导师，博士后联系导师，主任医师，江苏省医学重点人才、江苏省医学领军人才。从事小肠移植临床与研究近二十年，在小肠移植和复杂胃肠病治疗积累了丰富经验。2007年到2008年间赴美国Pittsburgh大学，Nabraska大学及Miami大学等国际先进的小肠移植中心学习。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 李元新，副主任医师，副教授，硕士生导师，江苏省医学重点人才。从事小肠移植临床与研究十五年。2006年到2007年赴美国Pittsburgh大学学习国际先进的小肠移植和腹腔多脏器移植技术，回国后在黎介寿院士领导下，对小肠移植关键技术加以改进，使小肠移植关键技术在我国实现突破。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 哪些人可以做<span style="font-family:'Microsoft YaHei';font-size:13.63636302947998px;line-height:25.454544067382813px;">小肠移植</span></span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">问：临床小肠移植包括哪些？</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 答：现代临床小肠移植的概念已不仅仅局限于传统意义上的单独小肠移植，而是包括三类：①单独小肠移植；②肝小肠联合移植：适应于肠衰竭合并全肠外营养（TPN）所致的肝功能衰竭；③腹腔多器官联合移植：移植包括胃、胰腺、十二指肠、小肠及肝脏。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 问：小肠移植的适应征有哪些？</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 答：小肠移植的主要适应症包括：</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 1.各种原因所致小肠广泛切除术后的短肠综合征：包括先天性小肠闭锁、肠扭转所致小肠广泛坏死、坏死性小肠结肠炎、创伤、肠系膜血管或门静脉系统血栓形成或缺血、克罗恩病反复手术所致小肠广泛切除；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 2.消化道动力障碍：包括慢性假性肠梗阻、内脏神经病变、消化道神经节细胞缺如（Hirschsprung’s病）；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 3.先天性肠粘膜病变导致的严重吸收不良病，如微绒毛包涵体病（microvillus inclusion）、绒毛状肠病(tufting enteropathy)；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 4.放射性损伤；</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 5.难以控制的分泌性腹泻；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 6.自身免疫性肠炎；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 7.先天性消化道畸形，如腹裂、先天性小肠闭锁；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 8.局限性硬纤维瘤；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 9.多发性息肉病，如Gardner’s综合征；&nbsp;</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 问：小肠移植现在疗效如何？</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 答：占全球近40%小肠移植例数的全球最大的小肠移植中心——美国匹兹堡大学小肠移植中心，病人和移植脏器的一年存活率分别达92%和89%。目前全球共有69个移植中心对1608例病人进行了小肠移植手术，存活909例，最长一例病人拥有良好功能的移植肠生存了19年。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南京军区总医院普外研究所于1994年完成亚洲首例小肠联移植，随后又完成十例小肠移植，其中包括肝小肠联合移植、亲体小肠移植。最近开展的小肠移植在总结前十例小肠移植经验的基础上，借鉴了全球最先进的小肠移植和腹腔多脏器移植中心——美国匹兹堡大学最先进临床技术，包括外科技术、围手术期处理技术及免疫抑制方案。尤其是免疫抑制方案目的已将由过去应用强大的免疫抑制剂抑制病人的免疫功能转变为提高移植小肠被病人机体接受的可能性，即诱导部分免疫耐受，使得病人术后仅单独使用低剂量FK506免疫抑制药物，无需使用激素维持病人的免疫状态。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 问：如何评价肠衰竭的另一种治疗措施——全肠外营养，它与小肠移植的关系如何？</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 答：目前，肠衰竭治疗的天平已由家庭肠外营养向小肠移植倾斜。肠功能衰竭而需终身依赖全肠营养支持的病人，在其漫长的全肠外营养支持过程中，可发生反复全身感染、肝功能衰竭或失去静脉通道的建立而危及生命，甚至由于肝功能衰竭而需行肝小肠联合移植或全腹多脏器移植。由于近年来小肠移植多种关键技术的进步，小肠移植的病人和移植脏器的存活率大大提高，同时小肠移植病人的生活质量高，可以正常饮食，正常工作，小肠移植和家庭肠外营养的关系如同慢性肾衰病人选择肾移植和血透治疗一样。美国学术界进行过严格的科学的比较，小肠移植的价效比优于家庭肠外营养支持。因此，肠衰竭治疗的天平开始由家庭肠外营养向小肠移植倾斜，一旦病人不能依赖肠道吸收营养维持生存，应尽早进行小肠移植。</span>
</p>
