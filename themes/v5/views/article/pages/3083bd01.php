<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>余新光:“微创”你理解了吗？</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">上一期做肿瘤专题的时候提到过一次“对微创的误读”，从各个渠道的发声反馈来看，大家对微创的解读还存在很多误区，因此本期我们来到神经外科继续微创这个话题。神经外科对普通人来说非常陌生，任何一个常见病都能牵扯出很多需要检索的学术名词，为了不给读者造成不必要的困扰，我们不探究某个具体病症，而是请神经外科专家以本学科的角度向非医学工作者这一受众浅谈微创、精准医学等前沿的医学概念。</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3083/1.png" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;余新光，解放军总医院神经外科主任，博士生（后）导师。担任中国医师协会神经外科医师分会副会长、专科医师规范化培训基地认证与建设专家委员会主任委员、专业英语教育与培训专家委员会主任委员、中华医学会神经外科学分会神经肿瘤学组副组长、中国颅底多学科协作组神经外科副组长、《中华神经外科杂志》等多种学术刊物编委。从事神经外科30年，在脑血管疾病外科、颅底和脑深部肿瘤这两类公认的复杂疾病的治疗上取得较大成绩。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;正确认识微创</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;神经外科比较早提出了“微侵袭”这个概念，后来翻译成微创，逐渐普及开被大家所熟知。微创的基本含义是指手术对正常组织器官的最小创伤，主要包括内镜、介入、立体定向、锁孔手术等技术。与传统手术相比，显微外科手术更早地体现了微创的理念。微创的概念也逐渐在外科手术中得以普及，形成趋势。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;神经手术最重要的就是保护正常的神经功能、提高手术后的生活质量、减少手术对脑组织的创伤，医生都希望以最小的代价治好患者，但每种手术都有自己的适应症。微创虽然发挥了重要作用，但并不能取代传统的开放手术。如果单纯追求外表的小切口，由于操作不便而造成了对深部神经血管的损害，或者不能妥善处理病变，就会给患者带来更大的创伤，这样便远不如开放手术效果好，这种“微创”选择就是盲目且危险的。余新光主任提醒道，创口小的手术并不等于微创，在安全、妥善处理病变的基础上，缩小暴露范围，减少组织损伤，才是微创的根本，所以不要只为追求微创这个名头而忽略其他。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;神经调控治疗神经外科疾病</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;科技的发展给医生提供了新的治疗思路，一些药物治疗效果不理想的病开始转向外科治疗。神经调控治疗功能性神经疾病便是通过在脑内安装脑起搏器，把电极插到脑深部特定的核团，进行持续的脉冲刺激（英文简称DBS），来改善神经症状的一种新尝试。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;目前运用DBS治疗帕金森病的经验已经比较成熟，效果也非常好，但大众对帕金森症的印象可能还停留在只能用药物治疗的阶段，并不太熟悉这种治疗手段。其实，这种方法已经扩展应用到到肌张力障碍、抽动症、强迫症、重度抑郁症的治疗上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;解放军总医院最近在国内最先用DBS方法治疗了两例早老痴呆患者，效果比较满意，结合国际上前期的临床试验来看，研究值得进一步推进。随着社会老龄化的日益严重，老年痴呆病的患者数量不断升高，由于药物治疗效果有限，期待新的疗法能够缓解部分患者的症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;脑起搏器的发明人是来自法国格勒诺布尔约瑟夫•傅立叶大学的Alim Louis Benabid，他也因此获得2014年拉斯克医学奖。前不久，这位科学家来解放军总医院做学术报告时，特地看望了首例DBS治疗的痴呆病人。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;在不断探索神经调控技术治疗功能神经外科疾病的同时，解放军总医院神经外科也致力于参与清华大学国产脑起搏器的临床研发试验，争取让更多患者在不久的将来“用得起”这项技术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;创新是一个学科发展的必经之路，在巩固传统治疗方法的同时，用新的技术对传统疾病进行更深入的研究，借助高科技手段，将传统疾病治疗做得更精细，打造精品医疗；同时，探索新的治疗方法，改善疑难复杂疾病的预后，这是解放军总医院神经外科学科发展的主流。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;“精准”理念给患者带来福音</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;最近，“精准医学”这个词出现频率较高。通俗解读，就是在基因水平寻找病因和防治方法。神经外科领域的应用范例，就是针对脑胶质瘤这种常见肿瘤，从肿瘤分子病理学（分子标记物）特性入手，找出每个患者的肿瘤特点，进行个体化的靶向治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;前几年，我们提出“精准神经外科”的概念，和上述含义有所不同。同样以脑肿瘤为例，手术之前，可以通过影像学，把大脑重要功能区标注出来，在手术中加以格外的保护。以前医生切除肿瘤，只能凭肉眼、经验判断，现在有了导航系统、术中磁共振和电刺激，便可精准定位病灶位置，验证切除的程度，避免损伤神经传导纤维等等，有这些高科技的辅助，很大程度提升了手术的精准度，做到最大程度的安全切除。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;媒体对养生保健类的知识普及较多，对医学的进展和局限性宣传相对较少，这使得一些患者及家人对现代医学理解出现偏差，也间接导致出现较差的依从性。我们期待媒体引导民众关注医疗科学技术的发展，唤醒大众对医学进步的兴趣，让更多的人能够听到医学前沿科技的声音，使科学思想、方法得到公正的“对待”。同时，充分宣传疾病的复杂性和医疗技术的局限性，有利于构建正常的医患关系。</span>
</p>
