<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>这样治癫痫病情容易反复！</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癫痫是导致短暂的大脑功能障碍的一种慢性疾病，癫痫病的危害大、影响大、持续的时间长，癫痫病复发的原因，多种多样，引起癫痫发作的原因也即是其临床复发的原因。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病情没有记录没能提供正确的病史资料</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">许多病人到医院检查治疗，医生问及病情大多人都用不知道、不清楚来回答，在这里提醒患者由于癫痫病史对癫痫诊断有很重要的作用，因此专家在进行癫痫诊断中要了解患者的癫痫病史，这就需癫痫患者的家属在患者每一次癫痫发作的时候进行记录，包括记录癫痫发作的时间、症状表现等等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物仅仅控制病情，停药反弹病情加重</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据统计，大多数病人都尝试过抗癫痫药物治疗，并且稍见病情有所好转便相信药物治疗有效，等吃到一定程度病情见轻，便停药，停药之后，神经元放电立马反弹，病情会逐渐加重，再吃药，效果便不明显，于是加大药量，一段时间后，大脑损伤明显，严重至即使不发病时，精神也不再正常。诸如目前很多家医院都采用的抗癫痫药物治疗，这个只能从表面控制和抑制病情，遇到情绪上有任何波动都有可能导致病情复发。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癫痫复发与未能经过系统彻底治疗有关。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有些病人经过一段时间的中、西医治疗，发作得到基本控制后，往往自行中断治疗。这些患者及其家属认为只要癫病不再发作，生活能够自理疾病就算好了，殊不知发作的控制是由于药物作用的结果，并不意味着大脑神经元功能完全恢复正常。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此在停止治疗后、发作又出现，有时比以前发作还要严重，以致导致恶性循环。尽早、全面、彻底、系统的治疗，症状控制较快，间隔的时间也较长，有些甚至能痊愈。而治疗不彻底、用药不规范、反复复发的患者其效果往往较差。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"></span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">小提示</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">癫痫病是一种长期较难治愈的疾病，发作得到控制后一般仍需很长时间的治疗，但患者有时不能坚持治疗。有的病人认为“凡药自有三分毒”，认为长期服用会造成大脑功能和身体损害，有的从眼前的经济利益出发，中断治疗。诸如此类的问题往往导致疾病复发。</span>
</p>
