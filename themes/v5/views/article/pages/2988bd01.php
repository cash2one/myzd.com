		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>楼文晖：阻碍我国外科创新的桎梏在哪？</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">编者按：「大众创业、万众创新」的浪潮下，在医疗领域，医生们创新的热情也持续高涨。为了响应国家号召，鼓励并支持医生创新，由《中华外科杂志》发布，中华医学会外科学分会担任战略合作伙伴，广东天普生化医药股份有限公司和贝壳社主办的「天贝杯」医生创新大赛于日前启动。「天贝杯」医生创新大赛医疗导师团，不仅医学造诣深厚，而且极具创新精神。但与国外相比，我国的创新起步较晚，为此我们邀请到了复旦大学附属中山医院普外科、上海医学会外科分会副主任委员楼文晖教授为大家解读其中的原因。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">丁香园：楼教授您好，关于《外科创新的过去、现在、未来》的主题报告，能否请您简单介绍下国内外创新最大的不同点，或者谈谈您对此的感受。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">楼文晖教授：外科创新实际上应分成两大部分，一是手术器械及设备的创新，二是手术方式的创新。就手术器械及设备的创新而言，如今我们跟发达国家相比，最大的问题在于两个方面，第一是我们的创新理念和意识尚缺乏，第二是我国专利相关的法律法规不健全。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">举例而言，很多外科医生在临床上对血管钳、拉钩都有所改进，但却并没有想到要将其申请专利、产业化并去推广。并且如果有医生发明了一个引流管，申请了一个专利，找到一个公司或厂家制作生产，这时另外一个厂商觉得这个产品不错，就未经发明者同意也开始生产。这种现象非常常见，而这种对专利、对知识产权的忽视，很大程度上制约了外科创新，制约了产业化的进程。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">再就外科技术的创新而言，目前外科技术的创新是不太受到重视的。举例来谈，如果内科医生发明了一种新药或新的治疗方法，按照传统评价方法一期、二期、三期临床，则会获得很多直观的数据。而外科医生的术式更新无法量化，如外科医生做胰腺的手术，原来的方法是间断缝合，现在改为连续缝合，所使用的缝合材料也有变化，但最后所看到的是个体病人的优化结果，并没有转化成大数据。从这个角度来说，目前我国学术评价体系对外科术式的创新是非常不利的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">丁香园：本次大赛是将医生和企业做了一个结合和衔接，在整个医疗方面也是一个创新，您对此怎么看？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">楼文晖教授：实际上作为中华医学会外科学分会和企业，包括和投资方合作，初衷都是希望为外科医生搭建一个长久的平台。最终能入选的项目需要有较好的前景进行产业化，其能在一定程度上改善临床实践，从而为病患服务。本次大赛是顺应了整个社会的需求。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">丁香园：我们传统认为，医生最主要的精力应该都在临床上，如何在临床之余兼顾创新呢？在这个方面，是否有国外医生的技术转化成产业化的经验可以借鉴？</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">楼文晖教授：从创新角度来说，西方有完整的法律和服务体系，支持医生做创新，而不会占用医生的很多精力。如某医生有了一个新的创新想法，一旦被评估为有市场前景，会有一整套体系为其服务，包括提供法律服务、产品转让费用等。目前我国的问题在于，医生欠缺一个完整的配套体系。所以，希望通过「天贝杯」平台，摸索出一条路，能够为外科医生分担一部分其不擅长的工作，从而为整个外科界带来极具意义的变革。</span>
</p>
