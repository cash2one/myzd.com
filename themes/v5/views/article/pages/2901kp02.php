			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>面肌痉挛</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">面肌痉挛，又称面肌抽搐。为一种单侧面部不自主抽搐的病症。面部肌肉是由面神经控制的,该神经由脑干发出,经内听道下方出颅.面神经主要为运动型神经,控制眼睑、口腔和嘴唇的活动。面肌痉挛比较少见，在美国，每100000中有8例患者，女性多见，男、女比例约2：3。平均年龄为45岁。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">症状</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">90%的患者起病从眼轮匝肌开始抽搐，然后向下逐渐涉及整个面。早期症状眼睑肌肉的间断抽搐，可导致眼睛关闭。10%的患者从下巴开始，向上进展。常不伴疼痛，但导致患者窘迫和影响患者的正常表情和形象。痉挛可扩散到一侧脸部，也可能局限在上部或下部。可能出现流泪现象。睡着时症状也可出现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊断</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病史和常规的神经病学检查可以诊断面肌痉挛，MRI可以排除因肿瘤、动脉瘤或动静脉畸形引起的面神经压迫。肌电图和神经传导速度的检查可以测判面肌和神经的电活动。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有药物、肉毒素局部注射和微血管减压三种治疗方法，但到目前为止，只有微血管减压手术是有效治疗。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物治疗：可采用安定类药物如地西泮、肌肉缓解药巴氯芬、抗癫痫药物卡马西平或大仑丁等。这些药物对症状较轻的患者可能有效，但副作用较大，如嗜睡、行走不稳、恶心、皮肤红斑和成瘾等。各种药物还有各自的副作用，患者需定期检查，防止严重的副作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肉毒素局部注射：肉毒素是由肉毒细菌产生的可阻断神经肌肉电传导引起肌肉麻疲剧毒蛋白质物质。神经肌肉之间传导的神经递质为乙酰胆碱，肉毒素可阻止其释放，肌肉失去收缩信号。经注射器向发生抽搐的面肌注射肉毒素，一般3天后出现效果，可持续3个月。该治疗可反复进行，但效果逐渐减小，因为随时间延长，患者体内产生肉毒素抗体，影响其疗效。副作用包括面部无力、眼睑下垂、眼睛感觉过敏不适等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术：药物和肉毒素治疗无效或副作用大，患者不能忍受这些副作用的，微血管减压手术可治愈该疾病，由于该手术创伤小，效果显著，越来越多的医师提倡该手术治疗（短暂药物治疗无效，影像学检查明确面神经受压的，为手术指证）。尽管手术治疗是目前面肌痉挛唯一有效治疗，但从来不是第一治疗选择。采用耳后（枕下乙状窦后径路）小切口，做3×2.5cm骨窗，切开脑膜，进入桥小脑角，找出Ⅶ、Ⅷ颅神经，如发现有占位性病变或蛛网膜粘连即进行切除和分解，如有压迫性血管，可在显微镜下利用显微器械给以分离开，如果分不开，可用Silicone或Teflon片隔垫开，亦可用肌肉片填塞在血管与神经之间。超过90%的患者恢复正常工作与生活。手术发现95%以上患者面神经有血管压迫，该手术的主要并发症是短暂单侧面瘫（11%）和耳聋（3%）。治疗效果：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">85%术后立即消失；10%术后痉挛明显减少；2%患者手术1月后再次出现痉挛；7%患者复发。早期有效率95%，长期有效率90%。</span><br />
