<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海长征医院助13岁重疾男孩圆“站立梦”</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">13岁的男孩陈嘉辉（化名）因身患重病，不得不离开学校，在医院里接受治疗。小陈在出生时即患有脊髓栓系综合征，随着个头长高，小陈逐渐出现会阴区麻木，双腿疼痛，左脚无法勾起，行走拖地，排小便也变得很费力。全家为给小陈治病，辗转北京广州多地，花费几十万积蓄，但病情却仍无明显好转。小陈的父母告诉记者，这两年里小陈只能瘫痪在床，由于长时间躺在床上，母亲只能辞去工作，每天在家不间断地为他按摩双腿，以免出现肌肉萎缩的现象。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 值得庆幸的是，辗转多地的小陈最终来到上海长征医院，第二脊柱外科史建刚教授为他明确诊断，并确定手术方案。经过5个小时手术，目前小陈恢复良好，会阴区感觉异常消失，下肢肌力有所恢复，左脚背屈（勾脚）力量明显增加，数周之后小陈就有望能恢复正常行走。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据了解，脊髓栓系综合征多伴发脊柱裂，是一种先天性脊柱脊髓畸形，在中国据不完全统计约有400万病人。患者除背部外观异常外，还常伴有大小便功能异常、下肢及会阴部疼痛或感觉异常、下肢运动功能障碍等表现，这些症状给不幸罹患该病的患者及他们的家庭带来了巨大的影响。近年来，虽然残疾人联合会及社会各界已对这类患者给与诸多照顾，如发放轮椅、导尿管、尿垫等物品及建立云朵之家、中途之家等组织，但仍有部分脊髓栓系综合征患者因各种因素导致最终放弃治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前对于脊髓栓系综合征，传统的外科治疗方法是终丝切断及马尾神经松解手术，这对许多伴发椎管内脂肪瘤的患者效果不佳，尤其对复发的脊髓栓系综合征束手无策。史建刚教授对于该病发病机制提出了神经骨骼发育不匹配学说，在治疗上创新出脊柱均匀短缩脊髓轴性减压技术，治疗效果令人振奋。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 史教授介绍，脊柱均匀短缩脊髓轴性减压术是采用在多个节段均匀短缩脊柱的方法，对受到栓系的脊髓进行轴性减压的技术，传统终丝切断马尾松解手术仅仅对圆锥局部的神经组织起到减压的效果，但是栓系导致的神经损伤范围远大于减压的范围，故传统终丝切断马尾松解手术无法对神经进行充分减压。相对传统终丝切断术的优势在于其在多个受栓系节段的脊髓、马尾神经进行整体均匀的减压，减压范围同脊髓损害的范围相统一；还有就是通过改变脊髓周围的结构，达到脊髓的直接轴性减压，降低了神经损伤、椎管内感染以及再栓系的可能性低。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 同时史建刚主任表示：我们了解到他们一家的情况之后，觉得他们非常的不容易，经请示院领导后所以决定对其减免医疗费用。”史主任还表示：上海长征医院一直非常重视对脊髓栓系综合征患者的援助，并且许多经治患者的家长团体正积极筹备建立“长征医院脊髓栓系综合征基金”，该基金会的成立是为了减轻贫困脊髓栓系综合征患者及其家庭的经济压力，得到正规权威的治疗，摆脱疾病的困扰。脊髓栓系综合征病友经申请可领取援助基金用于脊髓栓系综合征的治疗。</span>
</p>
