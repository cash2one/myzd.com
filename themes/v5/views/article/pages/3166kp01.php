<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑胶质瘤手术后 还会复发吗？&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">得了脑胶质瘤，人们的第一反应往往是，我还能活多久？怎么才能活下来？针对这生死攸关的大问题，医生也不能轻易就下结论。而且，脑胶质瘤有那么多厉害程度不同的“兄弟姐妹”，要分情况才能找出答案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般来说，胶质瘤的级别越高，越容易复发（详见《脑胶质瘤兄弟姐妹一大家 厉害程度各不同》）。肿瘤切除得越彻底，越能推迟复发时间。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1级脑胶质瘤算是这类脑瘤里“最温柔”的了，它本身的恶性程度不高，肿瘤细胞的发展较为温和。如果这种低级别的脑胶质瘤生长在大脑的非功能区，比如大脑额头边际的脑区、颞骨边际的脑区等，就可以彻底切除。术后就不用放化疗，只需要定期做好复查，密切监测防止复发就可以了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是如果肿瘤生长在大脑的重要功能区，那就没法做到彻底切除（如果彻底切除，就会损伤重要的脑功能区，术后会导致严重的功能障碍，比如没法行走、没法说话等），手术过程中要进行病理活检明确病理分期，术后根据病理分期分型进行辅助放疗。由于放疗可以定向治疗“指哪打哪”，因此这一级别的胶质瘤就不需要化疗了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总体来说，1级胶质瘤的治疗结果比较好，一般能够达到治愈不复发。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2级脑胶质瘤是一种低度恶性的肿瘤，但比1级的要厉害许多。如果能够全部切除，说明肿瘤的风险程度比较低，一般情况下不需要辅助放化疗，定期复查即可，通常平均生存期在8~9年左右；如果不能够完全切除，说明肿瘤的风险比较高，那么就需要辅助放化疗，尽管如此，平均生存期也会缩短到大概3年。</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，虽然这种级别的胶质瘤和1级胶质瘤同属低级别，但是风险程度不一样，复发的情况也不一样，寿命也会受到较大的影响。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、4级都属于高级别的胶质瘤，也是恶性程度比较高的脑肿瘤，其疾病发展的结局就不容乐观了。比如4级的胶质母细胞瘤，就算进行了手术和术后的放化疗，70%的患者在6个月以内就会复发。而且复发的部位大部分是新的病灶，比如原来肿瘤长在大脑前面的额叶，复发时可能就跑到了其他脑区，甚至脊髓、腰椎等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，总体来说，3级脑胶质瘤的平均生存期是18个月左右。4级脑胶质瘤比较凶恶，做完手术+综合放化疗之后的平均生存期一般是1年左右，到两年还活着的人一般只占5%左右。而如果不做手术，往往只能活两三个月了。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">虽然合理的选择手术以及放化疗方案对于防止肿瘤复发有一定的效果，但同时也要注意做好定期复查工作，来监测病灶的动向。如果等到患者出现头疼、呕吐、抽风，甚至视力下降等症状时，往往说明肿瘤复发很严重了，这个时候再进行二次处理就比较晚了。</span>
