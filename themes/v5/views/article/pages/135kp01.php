			<h1 style="text-align:center;">
	<span style="font-size:18px;font-family:'Microsoft YaHei';">主动脉瓣关闭不全外科治疗时机的选择</span>
</h1>
<p class="MsoNormal">
	<br />
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">1．临床表现：AR的病因主要有风湿热，感染性心内膜炎，先天畸形，主动脉根部病变等。临床表现分急性和慢性两种。前者多因感染性心内膜炎，主动脉夹层或外伤，表现为乏力，严重呼吸困难，低血压等；后者主要表现为劳力性呼吸困难，阵发夜间呼吸困难等。</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">自然史：慢性AR者约75%可生存5年，50%生存10年，一旦出现症状则进展较快。如不手术治疗则出现心绞痛后4年内死亡，出现心衰后2年内死亡。预后不良的因素有：高龄，症状进展，房颤，收缩末内径&gt;25mm/m2体表面积。急性AR者危险远高于慢性AR者，一旦血流动力学稳定即应尽早手术治疗。</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">2.内科处理：轻中度AR者若无症状且心脏不大或轻大，则不必治疗，每12～24 月进行临床和超声复查，应予抗生素治疗以预防感染性心内膜炎；慢性重度AR左室功能正常者6个月随访一次；左室舒末内径&gt;60mm者每4～6月随访一次。治疗收缩期高血压应慎用?-阻滞剂；高血压及左室功能不全者应使用血管扩张剂；积极预防和及时控制房颤和心动过缓；患有心绞痛者可使用硝酸盐类药物；左心衰竭者应予以洋地黄制剂，限盐和利尿剂治疗。&nbsp;&nbsp;&nbsp;</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">3. 外科治疗:&nbsp; 主要是采取主动脉瓣置换术（AVR），术前无需常规心导管检查；高龄患者（》60）术前常行冠造检查。适用于：</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">A.有症状，左室功能不全（EF&lt;50%）,左室明显扩大(舒末内径&gt;70mm，收缩末&gt;50mm)；</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">B.重度AR且有症状，无明显禁忌症和伴发病症者也应行AVR术。因症状严重（NYHA Ⅲ或Ⅳ级）及左室功能不全(EF&lt;40%）者术后生存率低，故应在心功能尚好（nyhaⅱ级）时手术。</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">C.EF&lt;25%者应视患者情况而定手术治疗抑或内科药物治疗。</span>
</p>
<p class="MsoNormal">
	<span style="font-size:14px;font-family:'Microsoft YaHei';">D.主动脉根部病变所致AR者，主动脉根部内径&gt;50mm时应行手术治疗。</span>
</p>
