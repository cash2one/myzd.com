	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>让大爱继续：医疗队在艰苦条件下抢救生命</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">【专题】汶川地震抗震救灾特别报道</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “5·12”地震已经过去了半个多月了，灾后重建正在展开。一方面，前方灾区医疗救援还在进行，另一方面灾区伤员正在转往后方各省大医院接受康复治疗。前线和后方步伐一致的救援行动，用成都军区总医院神经外科主任屈延的话来说，都是为了给因灾受伤的患者提供顶级医疗救助。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在艰苦条件下抢救生命</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">汶川映秀镇原本是一个山青水秀的小镇，马路、河堤和通向山坡小村的公路都是水泥路面，一个小镇内就可见有四座桥。然而，一场突如其来的天灾使所有道路都被破坏了。这座受毁灭性破坏的小镇，给救援、搜索、救治工作带来了极大的困难。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 作为首批进入映秀镇的医疗队之一，广东医疗队的巡诊、抢救都是在瓦砾中跨过。在现场抢救发生余震时，队员们在坍塌的现埸仍然临危不惧，继续进行截肢和输液等抢救工作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 阿坝州医疗指挥部曾要广东医疗队派遣20名男医生轻装步行前往距离映秀23公里外的耿达。听到这个决定，作为领队的广东省卫生厅副厅长廖新波很是担心，这并不是轻松的事情。滑坡掩埋了道路，蜀道更多险阻。廖新波并没有毅然、轻松地接受任务，因为在他看来，只有保证医护人员的安全才有可能有效地抢救伤者。正当他犹豫的时刻，南方珠江医院的副院长徐如祥带头响应，有的队员还写了遗书。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 震后的映秀，所有人都无房可居，作为先遣医疗队的广东医疗队，与抗灾大军一样，几乎没有一块平地可驻扎。幸好他们是较早到达的队伍，与“友军”42医院的医疗队在映秀漩口中学的篮球场搭起了野营帐篷。这种帐篷不能抵雨，廖新波和大部份队员们深夜常常被雨浸醒。有两个医疗队的队员，为了防雨，不得不睡到马路旁的人行道上。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “在灾区经历的艰辛远远不止这些，如果你看了，就会深深地震撼。我很心酸，很心痛。但这些难、这些苦，只要能换回受灾民众的生命，换回他们的健康，又算什么？”廖新波说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 他们是伤员最大的福音</span><br />
<p>
	<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> </span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在成都军区总医院，短时间内收治大量伤员，每位医生及护士承担的工作量比平时大了5～8倍。恰在这时，留在该院的解放军总医院的专家们帮上了大忙，使得对患者治疗的及时性和治疗水平并未降低。这些专家中包括两位院士及8名顶级外科专家(均为文职将军)，为医院在医疗上提供了强有力的技术援助和支持。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “他们不仅缓解了本院医护人员的压力，更重要的是，这些在国际上享有盛誉的专家的到来，为地震后的患者所提供的医疗服务治疗，无庸置疑都是顶级的。”屈延说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 65岁高龄的301医院教授周定标，享誉国内外的神经外科专家，经过数十个小时的旅行到达医院后，不顾疲劳，首先便到病房询问科里伤员的情况，对几名重伤员进行重点查询，在询问了他们的治疗情况后，提出了自己对患者的诊疗意见。第二天，又带领全科医护人员，对所有震后伤员进行查房，在肯定前期工作的同时，针对目前医疗及护理上存在的问题，提出了自己的诊疗意见，并督促科室尽快加以落实。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 此时，前一天到达的301医院神经外科的乔广宇医生，早在到达医院当天即投入到工作中，并完成了两例患者的开颅手术，第二天又为一名因地震受伤的儿童进行了长达9个小时的手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “生命奇迹的产生有诸多因素，但少不了医生们的妙手回春。他们是伤员最大的福音！”有人感叹说。</span>
</p>
