</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疝气手术虽然不大，但也不是可以掉以轻心的“小”手术，而是要“小心开”的手术，事实上，疝气的样子千变万化，腹股沟区解剖极为复杂且常有变异，需要仔細地保护好输精管、血管、神经等，可能发生的并发症包括睾丸缺血萎缩、输精管受损、复发、血肿、感染、慢性疼痛、膀胱损伤、肠损伤等等。根据世界各地的报道，疝专家与一般外科医生手术的复发率及并发症率有较大的差别。疝专家所做的无张力修补手术复发率在 0.1% 左右，而非疝专家的外科医生所做手术的复发率可达5%。所以要找疝气手术经验丰富的疝专科医生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疝气的本质其实质是腹壁的物理缺损，因此，只有手术方可治好疝气。目前临床应用的主流疝修补手术方法有数十种，到底哪种手术方法最好？国际上的权威说法是，没有哪种术式最好，对不同病人而言，只有最适合自己具体病情的手术方式，即手术方案个体化，包括具体麻醉方式、手术方式、修补材料等。对医生来说，最好使用最擅长的手术方式，可以缩短手术时间并减少并发症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 目前临床上主要有以下三大类共数十种手术方式：1.传统有张力疝修补术：创伤稍大，术后恢复时间较长，慢性疼痛率和复发率较高，仅适用于少数特殊情况的病人。2.无张力网片疝修补术：占各类疝修补术的80%以上，复发率远低于传统疝修补，是目前主流的手术方式。3.腹腔镜下无张力疝修补术：有3种具体的腹腔镜手术方式，医生用不同的手术器械插入病人的腹腔，借助摄象机把一张补片放入病人腹腔或者腹膜前间隙。医生通过一个电视屏幕上的成像进行手术。这类手术近期的效果良好，但是费用较高，有可能发生罕见的严重并发症如内脏损伤、大血管损伤等。这类手术比较适合发病时间较短的双侧疝患者或部分复发疝患者。根据医生的经验和技术水平，腹腔镜修补后的疝复发率在 0.5% 到 11% 之间。</span>
</p>
