		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>眼科专家呼吁：不要在最喜庆的日子里，让最亲的人失去光明（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;新华网北京２月２５日电（记者 李亚红）“不要在最喜庆的日子里，让最亲的人失去光明。”这个提醒来自著名眼科医院北京同仁医院院长伍冀湘。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;近年来，各地烟花爆竹致伤患者中，有一些是未成年人，有的孩子甚至为此摘除眼球，永失光明。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;同仁医院眼外伤科主任卢海介绍，烟花爆竹炸伤的部位多是眼睛和手，其中眼睛又尤为脆弱，轻则角膜烧伤、感染、溃疡，重者异物进入、烧伤严重，导致眼球摘除。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;北京市烟花爆竹致伤患者中，曾约有五分之一为青少年。因为儿童的危险意识尚未建立起来，好奇心又强，一些淘气的孩子喜欢把爆竹放在玻璃瓶、可乐罐里燃放。一旦爆炸，不但有爆竹伤还有异物伤。卢海说，如果单纯是纸片崩到眼睛里，伤害相对比较小，但是玻璃、塑料等碎片崩到眼睛里，更难处理。加上孩子在救治时又不配合，容易哭闹，就更加大了救治难度。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;卢海讲述自己曾救治过的一个孩子的故事。孩子当时是奶奶看管，可一时没有看住，孩子把爆竹放到玻璃瓶里点燃了，孩子的眼球被炸伤，经过抢救仍没能保住孩子的那颗眼球。一起事故给一个家庭带来的惨痛，让卢海印象深刻。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;伍冀湘说，想给家长们提一个醒，要好好地保护好自己的孩子，让他们远离烟花爆竹伤害。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;北京急救中心提示，要安全有序燃放烟花爆竹，特别要做到不燃放超标烟花爆竹。小孩须在成年人陪同下燃放烟花爆竹，不能单独燃放；带领孩子观看燃放烟花爆竹时也要注意自身安全，注意燃放现场的环境和风力、风向、障碍物等因素，保持安全距离。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;卢海还提醒，一旦发生炸伤后，切记四个“不要”：不要自己用清水清洗眼睛；自己不要处理已被炸裂的眼球，因为在压力下，已破损眼球的眼内容物可以脱出，医术再高超的医生，对已大部分眼内容物脱失的眼球，也无能力挽救，只能遗憾地行眼球摘除术；不要舍近求远，要就近诊治；不要喝水、吃东西。严重受伤后可能需要接受全麻手术，术前必须禁食禁水６小时以上。</span>
</p>
