		<p style="text-align:center;">
	<span style="line-height:2;font-size:18px;font-family:'Microsoft YaHei';"><strong>癫痫患者最“怕”电闪雷鸣</strong></span>
</p>
<p>
	<br />
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><img src="http://static.mingyizhudao.com/147004181216417" title="" alt="demo.jpg"/>
</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';">打雷闪电对癫痫病患者是一种剧烈的感官刺激，容易引起癫痫发作</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 图/陈秋明</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 受访专家/南方癫痫研究所所长、南方医科大学第三附属医院癫痫外科主任 张新伟</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 文/羊城晚报记者 陈辉 通讯员 陈晓霖</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 广东地处雷电频发地带，每年雷暴日都在80日左右，雷暴持续时间之长居全国之首。根据广东全省闪电定位监测，今年初至7月24日，全省闪电次数高达283万次，其中广州市32万次。就在上周六，广州市民再次看到城市上空骇人的闪电。对于普通市民来说，电闪雷鸣最多会让人吓一跳，但对于癫痫患者来说，就可能诱发一次癫痫大发作，甚至有致命危险。医学专业人士提醒，癫痫患者在雷雨天气最好待在室内，万一诱发了大发作，家人要注意保护他们，避免头外伤和舌咬伤，发作结束后要及时就医。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 癫痫患者为何“怕”雷电</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 癫痫，俗称“羊角风”或“羊癫风”，是大脑神经元突发性异常放电，导致短暂的大脑功能障碍的一种慢性疾病。南方医科大学第三附属医院癫痫外科主任张新伟说：“在中国，癫痫已经成为神经科仅次于头痛的第二大常见病，发病率是5-7％。，小孩的发病率更高。”</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 为何癫痫患者“怕”雷雨天气？张新伟解释说，雷雨天气对癫痫患者的影响是多重的。首先，雷雨天气常常是一道巨大的闪电后就伴随一声巨大的炸雷，这种电闪雷鸣对癫痫患者无疑是一种剧烈的感官刺激，容易引起患者大脑异常放电，使大脑出现短暂的功能性障碍，从而导致癫痫病发作。另外，雷雨天气压会比较低，空气的含氧量较低，这种变化会导致包括癫痫在内的多种疾病的发作。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 对于癫痫患儿来说，他们的脑部神经网络发育不完善，调节能力较成人差，更容易受到雷雨天气的影响，易诱发癫痫大发作。由于癫痫患儿平时以“小发作”居多（表现多为愣神几秒钟，症状不明显），所以一旦大发作时，家长会惊慌失措，不知该如何应对。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 雷雨天待在室内并增加药量</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 雷雨天气频繁时，癫痫患者应采取哪些措施预防发作？张新伟介绍说，临床上医生会建议癫痫患者在雷雨天气最好不要出门，尽量待在室内，以减少雷电产生的听觉刺激和视觉刺激。室内还要保持开窗、通风，保持室内的含氧量。另外，在雷雨天要避免活动（特别是剧烈活动），以免增加身体的耗氧量。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 此外，正在服药的癫痫患者，可以在雷雨频繁的天气里适当增加药量，比如以前一天服药一次的，雷雨天可以增加到一天两次，这样发作的可能性会大大降低。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 有人癫痫发作，你怎么做？</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 如果你身边有人突然癫痫大发作，他的样子可能有些吓人，可能会出现突然意识丧失、倒地、头后仰，甚至口吐白沫、面色青紫等表现，不过你不要惊慌。癫痫病人在发作时并不会伤害别人，但他们很可能会伤害到自己。所以，这时特别需要身边有人帮助他们。张新伟提示说，当你看到身边有人癫痫发作时，你可以这样做——</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> ① 马上上前扶住他，然后慢慢地让其顺势躺下，避免摔伤头部引起脑出血。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> ② 趁患者的嘴巴未紧闭之前，迅速拿些东西塞进他嘴里（不管什么东西都行），垫在病人的上下齿之间，防范牙关紧闭时咬破舌头，咬破舌头后的出血可能会导致窒息的危险。此时不用担心患者难以呼吸，因为鼻子还是可以呼吸的。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> ③ 保持呼吸道通畅，解开患者的衣服纽扣，把头歪向一边，免得呕吐物被吸入气管而引起窒息。救助者应始终在病人的身旁，随时擦去病人的吐出物。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 七成癫痫患者能找到病因</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 大部分患者的癫痫发作一般会持续5分钟，当发作结束后，无论有无不适都应当就医。有的患者会在癫痫发作后头痛，有的会因为缺氧而出现脑水肿，有必要做进一步的检查并及时处理。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 第一次癫痫发作的病人，发作后尽快就医，有助于查找导致癫痫发作的病因。以往，癫痫患者中只有30%能够找到病因，现在这个比例已经提高到70%。患者通过合理用药大多能控制病情，用药物无法控制的癫痫患者，仍有外科手术的机会，可通过迷走神经刺激手术和胼胝体切开术来控制癫痫发作。</span>
</p>
