			<div style="text-align:center;">
	<strong>肿瘤患者手术后要忌发物吗？</strong>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常有肿瘤患者和家属在手术后问医生是否要忌口，是否吃发物会导致肿瘤复发并影响伤口愈合。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">发物属于传统医学的概念，现代营养学中并没有与之准确相应的术语。简单来说发物就是会诱“发”疾病的食物，在经典流传版本中往往包括鹅肉、老母鸡、羊肉、牛奶、鸡蛋、螃蟹、虾、豆制品、草鱼、海带、韭菜、香椿、香菜、香菇等等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">古人没有现代医学知识，只能笼统地把在某些人出现不良反应之前吃过的食物通通归结为发物以警示后人。他们难以分析出事件与食物是否成因果关系、是否个人体质问题、是否因为食物不洁、是否因为加工失当等，而如果从现代医学的角度来解释的话，“发”的症状类似于机体出现变态反应。首先，对于某种食物过敏的人，无论患肿瘤与否都不该吃它，也很少有人会在手术后突然变得对羊肉、虾等过敏。其次，鱼虾在腐败变质的情况下容易产生组胺等胺类物质，吃了这些不卫生的食物容易使人出现荨麻疹等症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肿瘤手术后复发的原因是患者的低免疫力状态、肿瘤的微转移和手术中的肿瘤残留，而影响伤口愈合的因素有局部感染和缺血、年龄、营养状况、血糖、免疫力情况以及激素等药物的使用，目前尚无证据证明忌口能够防止肿瘤复发并有助于伤口愈合，反而会因为严格的忌口导致营养不良降低免疫力，影响伤口愈合并增加肿瘤转移的机会。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，发物的定义较为模糊，不应作为一种食物的固有属性，我们市场上常见的各类食物也早已经过了无数人的亲身使用，其成分研究得较为透彻，只要了解自己的身体情况，不吃患病前已知的自己过敏的食物，注意选择新鲜食品和烹调方法，没必要为了没有证据的传统说法而因噎废食。</span><br />
