<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;许多病人询问什么样的脑膜瘤可以不手术？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 一般来说，如果诊断正确，脑膜瘤直径小于3厘米，没有脑膜瘤引起相应的症状，可以动态观察，暂不手术。开始常常在第上次检查后2-3月复查核磁共振，如果没有什么变化，继续观察。随后，在上次核磁后半年再复查，如果仍没有发现脑膜瘤明显增大，可以将复查间隔定为一年。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">特殊部位比如鞍结节脑膜瘤 、桥脑小脑角脑膜瘤等，可能肿瘤比较小就会出现症状，如视力下降视野缺损等症状，肿瘤直径1-2CM，甚至几个毫米，也需要手术。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">观察期间，如果发现脑膜瘤明显增大或瘤周水肿范围扩大，出现相应症状等肿瘤进展迹象，应该尽快安排手术治疗。</span>
</p>
