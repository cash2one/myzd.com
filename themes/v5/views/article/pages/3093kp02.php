<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>Rasmussen 脑炎（Rasmussen综合征）的症&nbsp;</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Rasmussen综合征(RE)，也称Rasmussen脑炎，由Rasmussen于1958年首次报道，此后各国相继报道，此病临床少见，但症状严重，是由免疫介导的脑功能障碍导致的单侧脑半球萎缩，同时合并进行性神经系统功能障碍以及难治性癫痫。至今本病病因不明，有论点认为本病与病毒感染相关，但至今并未确定何种病毒感染可以引起本病。特征性表现为各种发作形式的癫痫、进行性的偏瘫以及智力减退。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>症状表现:</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Rasmussen综合征主要发病时期为儿童期，平均发病年龄6岁，但也可见于青春期和成人。最常见的是首发症状是肌阵挛发作，也可以是简单部分性发作、复杂部分性发作或者继发全身化发作，经常表现为部分性发作持续状态，发作时意识清楚，可累及任一小肌群，如一侧的口角、面部、手指或下肢等。但癫痫发作并不是Rasmussen综合征必有的症状。特别是艾滋病导致的脑损害也可以表现为部分性癫痫持续状态 。部分性癫痫持续状态还可见于森林脑炎、非酮症昏迷、多发性硬化、脑肿瘤、MERRF、以及L-K综合征等疾病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>诊断标准:</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当患儿出现部分性癫痫持续状态时，至少存在下列一条才能做出诊断：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、癫痫开始或以后出现进展性的神经功能损害;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、头颅CT或MRI提示有进行性大脑半球萎缩，CT显示有低密度，MRI显示有异常信号</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、脑脊液检查中寡克隆区带阳性;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、脑活检病理证实慢性脑炎。此外Rasmussen综合征发作间期脑电图表现为患肢对侧大脑皮质的弥漫性慢波或多灶性棘波，有时也能见到双侧性独立的痫性放电。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、MRI检查可见一侧大脑半球萎缩，额、颞、顶叶长T2信号或部分脑叶发育不良。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此全面地评价患者的临床特点、脑电图表现、神经影像学和血清学资料，对于明确诊断是非常关键的。</span><br />
