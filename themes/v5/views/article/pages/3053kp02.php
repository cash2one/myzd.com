			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>漏斗胸术后为什么会胀肚子？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">阿明在周一（２０１３年１０月２０日）接受了ＮＵＳＳ手术，手术非常成功，畸形矫正也非常满意。阿明和他的家人本来是非常开心的，但从昨天起，阿明的妈妈忧心忡忡地找了我好几次，说阿明的肚子不舒服，很胀，不想吃东西，是不是肚子出了什么问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 漏斗胸手术操作的部位在胸部，胸部手术却出了肚子的问题，家属不清楚，很多外行的人也不会理解。其实病因还是在胸部，罪魁祸首就是术后的疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 漏斗胸术后的疼痛是最多见的病症，其病因主要来自两个方面：其一是切口的疼痛，其二是强力撑起的漏斗胸壁的疼痛。由于疼痛有明确的定位，很少会累及其他的部位。而术后的疼痛虽然普遍存在，却并不是难以忍受的绞痛，所以多数患者不会因此频繁使用止痛药。但是，疼痛却可以引起一系列相关的麻烦。其一，疼痛可能使患者惧怕必要的活动，比如卧床起床的动作，多数患者在很长的时间里都是靠家人扶着完成这样的动作的。早期这种帮助没有什么不妥，但如果持续时间较长的话，无疑会增加家人的负担，也不利于患者的康复。其二，疼痛可能限制腹部肌肉的运动。由于术后的疼痛主要位于下胸壁，而此处又是腹部肌肉在胸部的附着点，腹肌紧张必然会牵拉胸壁加重疼痛，所以很多患者不敢使腹部用力。其三，疼痛可能限制呼吸运动。呼吸运动意味着胸廓的活动，呼吸活动会加重下胸壁的疼痛。所以患者术后往往不敢大力进行呼吸，这样必然限制正常的呼吸运动。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在术后疼痛引起的麻烦中，每一种麻烦都可能直接或者间接地诱发另外一种麻烦，那就是腹胀。患者惧怕疼痛而减少的运动，使腹部必要的活动明显减少，这对肠道功能的恢复非常不利。肠道功能不恢复，腹胀就很容易形成。疼痛对腹部肌肉运动的限制，则可能直接导致腹胀。疼痛限制正常呼吸的同时，可能使气体过多地吞咽入肠道，从而引起胀气。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 由此可以看出，阿明出现的腹胀，真正的原因是术后的疼痛。要消除腹胀，最有效的方法就是让他自己坚强起来，不再过分地惧怕疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 阿明今年１６岁，个头１７８cm，在我们工作人员的眼里基本上算是个大人，但在其爸爸妈妈的眼里却还是个孩子。我亲眼看见阿明对着妈妈撒娇，亲眼看见娇小的妈妈扶着硕大的儿子躺下、坐起。阿明真的很不让家人省心。像这样的情况，阿明越是娇气，就越会出现不该有的麻烦，康复起来就非常吃力。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我把阿明腹胀的原因向阿明的妈妈和他本人详细做了说明。阿明还算个懂事的孩子，在我的搀扶下勇敢地下了床，然后照我的吩咐走出病房，到走廊和大厅里走了几圈，很快便上下通气不再胀肚子了。</span><br />
