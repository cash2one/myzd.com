			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>认识胃肠道-胰腺神经内分泌肿瘤</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二十世纪初就有学者注意到一类生长相对缓慢的肿瘤，并将它命名为“类癌”，随后的研究证实这类肿瘤来源于神经内分泌细胞，这些细胞具有调节功能，并广泛分布于全身各系统。绝大多数的神经内分泌肿瘤（NET）发生于胃肠道和肺，在胃肠道中发现的肿瘤现在被称为胃肠道-胰腺神经内分泌肿瘤（GEP-NET）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 神经内分泌肿瘤一度被认为比较罕见，但流行病学研究发现，过去30年中其发病率增加了约5倍，目前发病率约为5/10万，发病增加也与诊断技术的进步有关。胃肠胰神经内分泌肿瘤约占所有神经内分泌肿瘤的70％。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 过去认为这类肿瘤生长相对缓慢并且绝大多数均为良性肿瘤，但现在看来，几乎都具有恶性潜能，并且在某些情况下具有高度转移性，最常见的转移部位为肝脏。诊断必须有病理确诊，具有特定的细胞形态，并且有特异的免疫组织化学指标，现在我们国内病理学界也与国际同步公布了相关病理诊断规范标准。另外还有相对特异的影像学检查，即奥曲肽核素扫描，不仅可以用于诊断，也对指导治疗可能有一定意义，目前这项检查只有北京肿瘤医院（北京大学肿瘤医院）和协和医院可以进行。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;现阶段胃肠胰神经内分泌肿瘤的治疗可以选择化疗，生长抑素类似物（奥曲肽等）治疗，以及分子靶向药物舒尼替尼（索坦）和依维莫司治疗。北京肿瘤医院消化肿瘤内科已开始进行相关研究。</span><br />
