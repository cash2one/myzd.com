<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>乳腺癌的分子靶向治疗</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">如果要说乳腺癌的分子靶向治疗，应该先简单说一下乳腺癌的治疗。大家所熟知的乳腺癌治疗方法包括手术治疗、放射治疗和化疗，稍微陌生一点的是内分泌治疗和靶向治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术治疗和放疗是对肿瘤的局部控制。目前，随着医学的进步，疾病发现得越来越早，局部控制的效果也越来越好。然而，仍有少数患者在治疗后会复发，甚至死亡。究其原因，是这些患者的体内可能存在微小的转移灶，而这是局部控制无法顾及到的，因此就需要辅助治疗。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">从理论上来说，人体正常细胞和肿瘤细胞在很多遗传学表达上存在不同，这些差异都可能作为靶点。所谓的靶向治疗，就是通过分子靶向药物抑制这些靶点，阻断肿瘤细胞或相关细胞的信号转导，从而抑制或杀死肿瘤细胞。在临床中，找到能影响肿瘤生长增殖，并有生物学效应的理想靶点还是比较困难的。但幸运的是目前针对乳腺癌的靶向治疗，已经发现了一个有效靶点——“人表皮生长因子受体2”，又名HER2。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">随着近年分子生物学研究的深入，人们根据免疫组化检测把乳腺癌划分为三个不同类型。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">第一类，激素依赖型乳腺癌，即雌激素受体阳性的乳腺癌，此类患者适合内分泌治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">第二类，就是HER2阳性乳腺癌。此类患者适合分子靶向治疗。HER2会导致细胞表面HER2蛋白过度表达，刺激癌细胞增殖。靶向药物通过对HER2的抑制，达到治疗乳腺癌的目的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">第三类，三阴性乳腺癌，是指雌激素受体及人表皮生长因子受体2（HER2）均为阴性的乳腺癌，这类乳腺癌患者以化疗为主。</span>
</p>
