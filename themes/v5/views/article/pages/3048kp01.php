			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>确诊扩张型心肌病，要做“排除法”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆什么是扩张型心肌病？有什么危害？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所谓的扩张性心肌病并不是传统意义上的冠心病，虽然当今冠心病的发生占了心脏病人中的大部分（主要是心脏上的大血管发生了病变）， 但扩张性心肌病则不同，扩张型心肌病是心肌本身而不是大血管发生了病变，心脏的形态以心室腔扩大、整体的心脏功能降低为主要表现，病人可表现为活动耐量降低、脚肿、气急、胸闷等一系列不适，一定要在排除了其他可以引起心脏扩大的病因后才可以诊断扩张性心肌病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆扩张型心肌病是由什么原因引起的？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">扩张性心肌病的病因至今并不十分清楚，有报道认为与急性或慢性反复感染累及心肌引起，因此在临床上医生会比较关注患者在心脏扩大的疾病过程中有无时间较长或较为严重的感冒、腹泻病史。各种病毒、细菌的感染影响到心肌，病人可能出现胸闷、心慌、头晕、乏力等不同症状，临床上则表现为轻重不一的早搏、心脏传导阻滞或心肌缺血表现。当然，缺硒是另一个可能的原因，硒是一种微量元素，早期尤其是在克山地区常常发现因病人缺硒而导致扩张性心肌病，因此也称为克山病。对于扩张性心肌病，往往起病隐匿，多数病人在起病早期自身并没有明显不适，往往等到心功能不全的症状出现、心腔已经明显扩大时才来就诊，此时绝大多数病人的心腔大小及心功能已经较难逆转了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆ 扩张型心肌病有没有什么特征性的检查做完就可以基本确诊的？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">单凭体格检查并不能确诊为扩张性心肌病，仅仅能发现心脏扩大，必须要做一整套辅助检查来排除是否有其他疾病引起的心脏扩大才能最终诊断，这包括心脏彩超明确心脏是否增大及心功能情况、冠脉CT或心血管造影排除冠心病引起的心肌损伤、近来，在专门的心脏中心还有心脏磁共振检查明确是否存在如左室致密化不全或结节病等原因引起的心脏扩大，甄别出哪些是扩张性心肌病、哪些不是，然后对于不同的病因进行不同的治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆ 根据心脏彩超可以确诊吗？EF值是什么意思？低了说明什么？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">心脏彩超并不能明确是否就是扩张性心肌病，但心脏彩超还是可作为无创手段方便迅速的提供心脏大小、结构、功能。 当心脏彩超提示心室收缩普遍下降伴LVEF显著低于正常则说明病人的心脏功能显著减退，减退程度直接与病情严重程度成正比，在此基础上医生知道患者的心脏问题严重，接着将对病人的病人进行进一步筛查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆ X线检查结果有什么参考意义？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">X线检查在确诊扩张性心肌病中的价值已经逐渐下降，因为不论是通过体格检查还是心脏彩超都可以及时准确的明确心脏大小，X线诊断“心影增大”并不可靠，同时X线检查也不能给出心脏功能。X线唯一额外可以提供的信息是病人的胸部是否有炎症，这可能是加重病人近期症状的原因之一。由于X线本身的射线及分辨率原因，其在诊断扩张性心肌病中的作用已经在减退。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆ 心内膜穿刺活检能否最终确诊？比起其他检查是不是更准确？是不是所有的患者都需要做？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">心内膜活检作为诊断扩张性心肌病有一定的价值，但并不是必须的，因为它的组织学表现并不是特异性的，从扩张性心肌病的定义中我们就可以知道，任何没有其他原因引起的心脏扩大、心功能不全都可以被称为扩张性心肌病。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从活检所见来看，首先，在扩张性心肌病的活检组织中，可见心肌细胞肥大、大小不一，有心肌纤维化和心肌细胞坏死；但值得注意的是，并不是所有的病人都需要做活检，活检为创伤性操作，存在一定的风险，尤其是当病人已经心功能降低、心肌壁薄的情况下风险与获益是首先需要考虑的问题，而且可以做活检的部位非常有限，活检所取的组织量也十分少，必然会存在未取到有病变的组织的可能。相应的，现在技术提供了更为简单有效的方法，我们可以通过最新的心脏磁共振技术无需住院就可以无创的观察患者整个心脏是否存在着急性炎症水肿，心肌厚度及心脏功能；更为理想的是，可通过磁共振清晰的看到心脏不同部位是否存在心肌坏死、纤维化，可以称得上是“无创的组织活检”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在我们的心内科，我们通过心脏磁共振检测了一批被诊断为扩张性心肌病的病人， 用它来进行心脏扩大的心肌病病因的筛查，我们发现其中有部分病人其实是肥厚扩张性心肌病、左室致密化不全、结节病等其他病因造成的心脏扩大，这部分病人的治疗及对不同治疗的反应与病因密切相关，因此可能改变整个后续的治疗策略及病人的预后。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>☆患者症状比较轻，是不是可以不在意不治疗呢？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于一些症状比较轻但心脏已经扩大、甚至心功能已经减退的病人决不能停止治疗，如果是一过性感染引起的急性心肌扩张，我们应该预防反复感染，避免心脏再一次扩张；对于已经是慢性病变的病人，则应长期应用相应的药物延缓心脏扩张的进程。一旦扩张的程度、心功能下降到了一定的程度，心脏再同步化治疗或心脏减容术都是可以考虑的手段。</span>
</p>
