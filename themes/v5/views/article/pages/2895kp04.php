			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么样的胆囊容易变癌？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近年来随着人民生活水平的提高，我们发现胆囊结石、胆囊炎的发生率逐年增加，甚至越来越多的年轻人和小朋友也深受其害。胆囊结石、胆囊炎一旦发展成为慢性胆囊炎，就面临着新的隐患―――癌变，胆囊也会癌变，这个严重问题往往不为很多人所知，胆囊恶变的过程非常隐匿，其导致的右上腹隐痛病人往往以为是胆囊结石或炎症引起的，B超检查往往也是只见石头未见癌变，等到肿瘤长大甚至“原形毕露”时，绝大多数人已失去根治的机会，即使进行手术，效果也不理想。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，也不是所有的胆囊结石和慢性胆囊炎都会癌变，不过我们的研究发现有一些比较特别情况的慢性胆囊炎或胆囊结石比较容易癌变。那么，什么样的胆囊有癌变的可能？有什么办法可以进行预防呢?下面我将这些比较特殊的情况简单介绍一下：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1、50岁以上的老年患者，慢性胆囊炎反复发作的病人，尤其是女性，其癌变率急剧增加，占总病例的80％左右，平均年龄为65岁左右。但从来未有腹痛症状的患者其胆囊癌变的比率并不升高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2、患病时间较长（&gt;5年）、腹痛反复发作的慢性胆囊炎，尤其是萎缩性胆囊炎（即胆囊大小小于正常范围）。胆囊内有结石，尤其是多发型或充满型结石者。结石性胆囊炎的癌变率是非结石性胆囊炎的30倍，说明结石刺激因素在癌变中的重要作用。时有腹痛发作的病人尤其要注意定期检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3、瓷胆囊。即胆囊壁钙化，多见于65岁以上的女性，它是慢性胆囊炎的终末阶段，瓷胆囊癌变率高达20％。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4、合并有胆囊息肉、胆囊腺瘤和腺肌增生症者。息肉≥10毫米者癌变率高达23％。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、B超提示胆囊壁有明显增厚或局限性增厚者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 6、胆囊结石直径大于2.0厘米。随着胆石体积的增大，胆囊癌的发生率相应升高，直径大于2.0厘米结石者比小于1厘米者胆囊癌的发生率高10倍。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 7、以前曾接受过胆囊造瘘术者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 既然知道哪些情况会引起胆囊癌变，那么我们就有办法去预防它：&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1、有反复腹痛发作症状者，应尽早通过手术来彻底治疗胆囊炎与胆石症。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2、注意饮食习惯，三餐要定时，少吃油腻的食物，尤其是烧烤、油炸的食物。此外，增加新鲜蔬菜和水果的摄入，能减少胆囊癌发病的危险。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3、体重严重超标者胆囊癌发生率也高，肥胖者应积极减肥，控制体重。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 总之，我们可以通过一系列的办法来预防胆囊癌，关键在于大家是否重视它。</span>
</p>
