<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/14701039184449" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">图片说明：瑞金医院足踝外科团队进行INBONE全踝关节置换手术操作</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 东方网通讯员丁燕敏、记者刘轶琳10月30日报道：瑞金医院骨科足踝团队日前成功实施了Inbone全踝关节假体置换手术，这是我国首例Inbone全踝假体置换术，术后患者将保留关节的活动能力，该手术的成功开展标志着我国治疗终末期踝关节炎的水平再上新台阶。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据悉，患者为一名57岁女性，患类风湿性关节炎已经18年，近1年来出现右踝关节肿胀疼痛难耐，活动受限，已不能正常行走，经过各种药物治疗效果均不理想。经检查发现该患者右踝关节已经完全破坏，完全丧失活动度，医生给予建议做关节置换手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 瑞金医院骨科徐向阳主任医师带领骨科足踝团队精心制定手术方案，清理了踝关节内外侧炎性组织及骨赘，将患足置于专门的定制支架中，在透视下精确调整足踝及导针位置，在截骨导板的定位下进行踝关节胫骨及距骨的截骨，试样假体，置入合适匹配的踝关节假体及垫片完成全踝关节假体的置换，全程手术时间90分钟，手术成功达到预期效果。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据介绍，患者在休养四周左右以后可以下地行走，去除踝关节炎疼痛畸形的同时能够重建踝关节的活动功能，为生活和工作提供极大便利。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主刀医生徐向阳表示，以往对于严重踝关节炎的治疗主要依靠踝关节融合手术，但融合后丧失了踝关节的活动，引起周围关节的加速退变，不但在下蹲爬坡等日常生活受限制，周围关节的继发损伤会引起新的疼痛等病症，这些现象在类风湿等踝关节炎患者中尤为明显。全踝人工关节置换术能够在清除踝关节炎症解除疼痛的同时重建踝关节的活动功能，大大提高生活质量。</span><br />
