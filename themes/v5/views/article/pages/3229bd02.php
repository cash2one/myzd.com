			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>给心脏搭4座“桥”，刀口可以多小？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯（记者黎清 通讯员雷薇）心脏搭桥是大手术，需要切开胸骨、使心脏停跳并建立体外循环，不少患者因为惧怕，对手术望而却步。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 上周，在武汉亚洲心脏病医院，来自日本大和成和医院的心外科主任菊地庆太教授和亚心医院心外科专家共同配合，成功为两名冠心病多支病变的患者做了小切口心脏搭桥手术，一次性搭建4根“血管桥”，刀口仅5厘米。令人惊叹的是，手术全过程心脏不停跳。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据了解，传统搭桥手术是在冠状动脉狭窄的近端和远端之间重新建立一条血管通道，使血液绕过狭窄部位到达远端，就像在堵塞的道路上架上一座立交桥。人们将这种手术称之为“心脏搭桥”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 亚心医院副院长陶凉教授介绍，过去，微创搭桥是心脏外科新技术，2010年，亚心医院心外科率先开展湖北省内第一例微创搭桥手术，截至目前已完成100余例微创搭桥手术，占所有搭桥手术的20%。</span><br />
