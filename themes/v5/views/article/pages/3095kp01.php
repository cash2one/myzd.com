<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑膜瘤压迫视神经 三博脑科教授捡回丢失视力</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">千龙网北京讯（通讯员 李亚静）近日，首都医科大学附属北京三博脑科医院闫长祥教授为一位患者恢复了光明，手术后第二天患病的朱女士视力就明显好转，此前几乎失明的她格外珍惜这次再见光明的机会并激动地说“是闫教授捡回了我的视力和眼睛。”&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 40岁的朱女士来自河南，以前身体一直很健康，半年前开始出现间断性的头痛，因为头痛可以忍受，能够自行缓解，就没有引起注意。两个月前，她发现视力下降很厉害，看东西模模糊糊，以前在很远的地方就能辨别出哪个人，现在要走到跟前才看清，报纸和书上的字也模糊一片，无法阅读，朱女士赶紧到当地医院检查，医院按“青光眼”给予治疗，症状没有好转逐渐加重，后来到郑州某大医院检查发现大脑内长有肿瘤。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 经人介绍朱女士来到北京三博脑科医院，找到了脑肿瘤专家闫长祥教授，闫长祥教授为朱女士做了脑肿瘤切除手术，术中看到脑肿瘤已经显著压迫左侧视神经，并与视神经粘连紧密，右侧视神经及视交叉也被肿瘤压迫并有粘连，血运很差，双侧大脑前动脉被肿瘤挤压到一边，闫教授在显微镜下切除了脑肿瘤，保护了周围正常脑组织。闫教授说，患者得的是脑膜瘤，肿瘤很大，已经严重压迫双侧视神经，如果患者晚些时候来手术，双眼很可能失明。脑膜瘤以头痛和癫痫为首发症状，还可以出现视力、视野、嗅觉或听觉障碍及肢体运动障碍等。尤其当视力出现障碍的时候，一定要提早检查，避免造成不良后果，错过诊疗最佳时期。</span>
</p>
