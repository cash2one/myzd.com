	<span style="font-size:16px;font-family:Microsoft YaHei;line-height:2;"><strong>微访谈预告：刘庆教授谈脑膜瘤外科诊疗要点</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脑膜瘤分为颅内脑膜瘤和异位脑膜瘤，前者由颅内蛛网膜细胞形成，后者指无脑膜覆盖的组织器官发生的脑膜瘤，主要由胚胎期残留的蛛网膜组织演变而成。好发部位有头皮、颅骨、眼眶、鼻窦、三叉神经半月节、硬脑膜外层等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在颅内肿瘤中，脑膜瘤的发病率仅次于胶质瘤，为颅内良性肿瘤中最常见者。虽然近年来肿瘤的全切除率相对提高，手术死亡率显著下降，但仍存在术后并发症多、致残率高及生存质量差等系列难题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
