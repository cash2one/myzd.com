			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>深层次合作探索改革发展之路</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯（记者祝琳）11月10日，由中科院院士、北京协和医学院校长、肿瘤专家曾益新率领的医疗卫生专家（院士）团来梧考察，并举办医疗卫生学术讲座与交流会。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">市长朱学庆会见曾益新一行，并聆听了讲座。朱学庆说，梧州历史人文优势、区位交通优势、生态环境优势明显。近年来，随着自治区“双核驱动”战略的深入实施，梧州经济社会加速发展，为医疗卫生事业发展带来重大机遇。希望各位专家一如既往地关心和支持梧州医疗卫生事业，尤其在鼻咽癌防治研究、肿瘤微创手术、妇幼保健事业、医疗卫生机构交流合作等方面，提供更多的科研指导与技术支持，进一步探索和开展多渠道、深层次的合作，共同促进梧州医疗卫生事业加快发展。梧州市将认真落实专家提出的意见和建议，积极打造广西医疗卫生事业发展的“试验田”，努力推动医疗卫生工作取得新进展。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">曾益新表示，在深化医药卫生体制改革的进程中，国家鼓励地方开展具有当地特色的探索实践，梧州应该加强与上级部门和有关医疗机构的沟通联系，探索符合本地实际的改革思路和举措，加强基层医疗卫生体系建设。近年来，梧州在鼻咽癌防治研究等方面取得了良好成绩，带动了全市医疗卫生事业提升。专家们将以此次活动为契机，充分发挥专业优势和经验特长，共同为梧州医疗卫生事业发展贡献力量。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨天上午，曾益新院士以及美国胃肠专家、美国爱荷华大学医院教授乔梁，中山大学附属肿瘤医院医学影像与微创介入中心主任吴沛宏等医学专家，围绕医药卫生体制改革、城乡医疗卫生服务、医疗卫生人才队伍建设和有关医疗技术课题，分别作专题演讲或学术讲座。下午，专家（院士）团到市红会医院、中医医院和城市规划展览馆、苍海新区等地考察。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">副市长彭健铭参加了学术讲座和考察活动。</span><br />
