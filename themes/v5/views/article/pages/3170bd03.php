	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>首例3D打印椎体置换术成功</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">■科学与生活</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 科技日报讯 （记者俞慧友 通讯员王玉林 戴瑜亮）日前，记者从中南大学湘雅二医院获悉，该院脊柱外科吕国华团队应用胸腔镜技术，完成了世界上首例胸椎体肿瘤切除和定制的个体化3D打印人工椎体置换术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 吕国华介绍，脊髓是人体运动和内脏器官发挥正常功能的传导通路，被喻为人体“信息主干道”。今年10月，27岁的袁姓患者因肿瘤侵犯椎体，挤压脊髓，导致截瘫。而且，这一部位肿瘤与大血管、脊髓等重要器官相邻，稍不慎即会造成患者脊髓神经损伤导致永久性瘫痪，甚至由于大血管损伤危及生命。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 对此，吕国华团队制定了有别于传统治疗的先进治疗方案，应用现代3D技术根据患者病椎的形态和大小，术前成功“克隆”出胸7椎假体，再凭借娴熟的胸腔镜微创手术技巧，在胸腔镜辅助小切口下成功完成肿瘤椎体和3D人工椎体置换，并同时辅以后路椎弓根钉内固定。术后，患者脊髓压迫和胸背部剧痛症状消失，3天实现下地行走。</span>
</p>
