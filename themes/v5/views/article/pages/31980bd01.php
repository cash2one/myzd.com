		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>中国介入治疗前景广阔 水准接轨国际（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">中新网上海5月23日电 (记者 陈静)首届东方介入放射学大会23日拉开序幕。大会执行主席、第二军医大学附属长海医院介入科主任杨继金当日接受采访时表示，当前，在临床治疗领域，中国介入治疗水准和效果已经接轨国际，在一些介入治疗方式上，比如神经介入治疗，甚至走在国际前列。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 据悉，在国际上，发达国家于上世纪80年代初开始推广介入疗法，中国始于从80年代中期。经过30多年的发展，介入治疗已成为与外科、内科并列的第三大支柱性学科。据杨继金介绍，介入治疗以影像诊断为基础，在医学影像设备的引导下，在血管、皮肤或生理管道(如阴道、直肠、胃腔等)作几毫米的微小通道，对疾病作独立诊断和治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 本次大会主席、东南大学附属中大医院院长、中华放射学会副主任委员滕皋军介绍，除了恶性肿瘤的介入治疗外，其它良性病变如子宫肌瘤、子宫肌腺症、肝脏血管瘤、颜面部血管畸形、甲状腺机能亢进及老年性骨质疏松等现在均可以通过微创介入的方法得到治疗，效果显著而副作用轻微。除了人们熟知的冠心病、心肌梗死的介入治疗外，能引起病人猝、死严重危及人类生命健康的如主动脉夹层、脑动脉血栓形成及脑动脉瘤破裂出血，只要及时采用微创介入手术，都可以得到有效的治疗，从而避免悲剧发生。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 杨继金对记者解释，介入治疗将放射性例子植入实体肿瘤，使射线得以持续释放，因为射线距离短，局部能量强，所以，这样的治疗方式不仅疗效显著，且创伤小；与一般的放疗不同，其对健康细胞损伤极小。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 杨继金告诉记者，对一些放疗无效或者传统方法无法治疗的肿瘤，介入治疗大有可为。比如，对肿瘤堵塞人体管道，以往的治疗方式仅仅是在管道中设立支架，而随着肿瘤长大，支架渐渐失去作用。如果加入介入疗法，就可同时使肿瘤萎缩，达到治愈的目标。杨继金表示，除了实体肿瘤，一些原本需要开大刀才能治愈的疾病，如心脏瓣膜置换、脑动脉瘤等，如今使用介入疗法，也可轻松治愈。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 虽然，介入治疗功效显著，给患者带来创伤少，但是，一般民众，甚至部分医务人员也对此知之甚少。杨继金坦言，对于普通老百姓甚至大部分医务工作者来说，介入治疗的概念可能还停留在肝癌的经动脉化疗栓塞术(TACE)，事实上，介入医学已远远超越了这一点。据了解，第二军医大学附属长海医院是全中国最早开展介入手术的医院之一。杨继军说，即使在他所在医院，也存在上述现象。他建议，不同学科的医务人员应当对相关彼此业务有所涉猎，以便以更加综合的治疗手段面对疑难杂症，给更多患者带来福音。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 当日举行的首届东方介入放射学大会邀请十余名国际知名专家及数十名国内著名学者出席，包括美国宾夕法尼亚大学医学院放射学副主席Scott,俄勒冈健康与科学大学介入治疗专家Frederick,日本东海大学医学院放射学教授Terumitsu,澳大利亚介入放射学会主席KennethThomson等。本届论坛以“规范与安全”为主题，内容涵盖所有介入医学领域的临床与基础研究，包括分子影像、纳米医学及分子靶向药物治疗等前沿热点领域。主办方表示，东方介入放射学大会以后每年将轮流在华东地区举行。</span>
</p>
