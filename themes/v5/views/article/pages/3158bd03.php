		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>河大一附院用新技术治疗85岁老人的前列腺疾病好了</strong></span> 
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报开封讯近日，河南大学第一附属医院为患有前列腺增生疾病的85岁高龄老人实施了绿激光前列腺汽化术。据了解，该院是豫东首家开展此术的医院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据了解，这位患者今年85岁，患有严重的前列腺增生，还患有多种心脑血管疾病。泌尿外科主任卜宏民组织专家进行会诊，决定对患者采用这项新技术。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术中，卜宏民从患者尿道插入膀胱镜和光纤，操纵着高功率绿激光慢慢地汽化前列腺增生部分，白色的增生组织如棉絮般挥发消散，残留物被水冲走，30多分钟后，增生的组织大部分被消除。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 据卜宏民介绍，这是一项国际先进的技术，经半小时左右的手术就可有效治疗前列腺增生，创伤极小、基本不出血、手术并发症少、住院时间短。</span> 
</p>
