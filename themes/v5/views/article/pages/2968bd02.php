		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>黄华扬：治骨关节病偏方慎防激素 关节镜微创技术成熟有效</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着我国人口老龄化的程度不断加剧，老年骨关节病的发病总人数也是不断在攀升。因此，如何及早认识骨关节病？如何正确治疗骨关节病？骨关节病的最先进治疗方式是什么？知晓这些问题已不仅限于发病人群，作为家庭成员的中坚分子更应该及早了解相关的骨关节知识，一来可以有效避免自己“中招”各式骨关节病；二来可以携带家中患者正确治疗骨关节病，避免走近治疗误区。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 骨关节病发病率高，集中于压缩骨折和膝关节</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 名医简介：黄华扬 主任医师，教授。在脊柱、骨病及关节病损等方面有着丰富的临床经验，尤其在人工关节置换、关节镜微创技术方面（包括膝关节交叉韧带修复）居国内领先水平，在关节软骨损伤及骨坏死方面的研究取得了突破性进展。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 骨关节病从发病机理上来说，主要分为两种类型。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 第一种是因其他疾病导致的骨关节病变;例如内风湿性关节炎、强直性关节炎和外伤引起的关节炎等；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 第二种是指骨关节退行性病变导致的骨关节病；其主要表现在老年骨关节退行性病变引起的骨关节的疼痛，造成骨关节功能性障碍。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 关节骨病科黄华扬主任指出：“目前常见的病人还是以骨关节退行性病变的为主，尤其是绝经期的妇女因其内分泌的改变往往表现得更明显，其骨关节病的发病几率也比男性高得多。值得注意的是，老年骨关节病集中表现在两个部位。一个是脊柱骨质疏松引起的压缩骨折，表现为腰痛、下肢麻木等;另一个是表现在膝关节上面，膝关节是骨关节退行性病变中发病率最高的部位，大概八成的绝经妇女都会出现膝关节的相关病症。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 如果骨关节病处理不当，其不仅仅影响患者自身的工作能力和生活质量，也将给家庭带来沉重的负担，使得家庭成员不仅需要花费大量的金钱予以治疗，而且还需付出巨大的精力照料饮食起居。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 温馨提示：由于其在工作和家庭方面肩负的任务较为繁重，加之闭经导致的内分泌变化较大，中老年妇女是骨关节病的高危人群，因此应该及早重视，及早治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 伤筋动骨或不止一百天，天天看医生反而影响康复</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 俗话说“伤筋动骨一百天”，人的身体里大大小小共计206块骨头，如果哪天一个不留神让两块相邻的骨头打架了，那么关节可是最遭罪的角色。为何如此说呢?那是因为关节是连接骨头与骨头的纽带，稍有不慎随时可以制约人体的正常活动。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 如果你不小心骨折了，是否要老老实实静养100天呢？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 黄华扬主任提醒：“‘伤筋动骨一百天’这句话说得一点没错，有的情况康复起来甚至不止一百天。从病理、生理发展的过程中可以发现人体拥有一套自我修复的系统，而这个自我修复、愈合的系统从启动到完成是需要一定的时间的。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对于“一百天”应该怎么对待才更有利于伤势的好转康复呢？由于大部分人们对骨科知识的认知比较粗浅，在门诊中经常可以看到心急骨折患者频繁就诊，希望医生能够帮助其快速康复。其实，这种做法是错误的！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“不少患者误以为看一回医生，吃几付药就可以大功告成。这些都是不正确的认识，根本不需要天天看医生，应该动静有度。对于骨折最关键的是康复锻炼，一定要在专业医生的正确指导下进行正确的功能锻炼，如果锻炼不正确可能或导致致残。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 温馨提示：骨折患者的动静有度，因为“动”有利于血液循环，“静”有利于骨骼生长。但是有的患者需要完全“制动”，有的患者不能完全“制动”，这应该根据具体病情做到“动静结合”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 慎选祖传、跌打、秘笈，一贴止痛见效小心掺激素</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 腰痛、腿痛、关节痛……说起疼痛，或许是大部分中老年人挥之不去的惆怅。谈及骨关节病变带来的疼痛，从事关节骨病科工作多年的黄华扬主任坦言心痛：“门诊中所接待的绝大部分患者都有一个共同的期盼，那就是‘如何才能不再疼痛’。有的患者实在疼得受不了的，见到医生第一句话就是说‘请医生给我开多点止痛的药’。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 正是因为疼痛带来的困扰，不少骨关节病患者在求治过程中往往“舍本逐末”，盲目强调“止痛”。对此，黄华扬主任经常遇到一些“为难”的情况——“一些老患者往往中途停止了继续就诊治疗，然而时隔几个月他又再度回来，还带来了一堆‘祖传偏方’问我作用和功效”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 由于众所周知的原因，江湖跌打偏方和虚假医疗广告依然以不同的广度影响着骨关节病患者，尤其对于上了年纪的老人家来说，口口相传的“偏方”往往比正规医院正规医生的治疗意见更为信服。黄华扬主任提醒：“不少患者说他亲自试过那些偏方或膏药，也真切地发现疼痛减退甚至消失。这种情况下要特别注意，因为如此见效快、效果好的偏方往往添加了激素。而一旦长期使用含有激素的偏方，其副作用也将陆续浮出水面。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 温馨提示：长期大量使用激素容易导致三方面的恶果——1、脂肪代谢紊乱、糖代谢紊乱，形体上容易出现“满月脸、水牛肩”；2、骨质疏松；3、加剧骨关节软骨的破坏。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 微创关节镜0.8—1.0cm创面，手术量年900多台属 华南地区前例 </span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 从关节的承载分工和使用频率方面来看，关节受损几率最高的肩关节和膝关节，两者都属于运动损伤，主要是损伤关节的韧带。以膝关节为例，倘若前交叉韧带或后交叉韧带出现损伤之后，膝关节则会出现不稳将可能引起功能障碍，如果不及时治疗将会引起关节的运力集中导致骨软骨损伤，继而引发骨关节病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 因此，黄华扬主任强调：“对于膝关节受损者，早期地进行运动关节的稳定手术非常有必要，而目前主要是利用关节镜微创技术来进行治疗。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 谈及“微创技术”，大部分人可能都会略有所知其共同点——创伤少，并发症少，康复速度快。那么，对于关节镜微创技术，其特色优势又是体现在哪里呢？将其与以往的传统手术相比或许更有说服力。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 同样以膝关节受损为例，在关节镜微创技术面世前，手术时须直接切开手术部位，而这个伤口最少也达到12cm。这往往容易造成大出血，也容易引发并发症。此外，当手术结束后需要打石膏，石膏固定的时间约为6至8周，而往往容易造成关节僵硬，事后患者如果没有强大的毅力和正确的康复指导，往往难以恢复关节正常功能。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 然而，关节镜微创技术的应用将一改此境况：首先，关节镜的开口面积就像笔头这么大，介于0.8——1.0公分之间，患者只需缝一针即可；其次，对于一些简单的手术，只需要局麻，患者的出血量也只有几毫升；再次，患者无需打石膏固定，只需佩戴膝关节角度支具即可下地行走。</span>
</p>
