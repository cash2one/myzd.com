		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻窦炎应怎样治疗?（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻炎与鼻窦炎的区别有哪些?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻腔和鼻窦的粘膜是连续的、有鼻窦炎肯定伴随有鼻炎，有鼻炎不一定有鼻窦炎。主要的区别就是部位的区别，鼻炎就在鼻腔的粘膜炎症，鼻炎指的是鼻腔粘膜和粘膜下组织的炎症，是由于急性或慢性的鼻粘膜如病毒、病菌感染，或刺激物的作用下受损而导致的，鼻炎的症状是由过高的组织胺导致的。鼻窦就在鼻窦的粘膜炎症，鼻窦炎是鼻窦粘膜的非特异性炎症，为一种鼻科常见多发病，在各种鼻窦炎中，上颌窦炎最多见，依次为筛窦、额窦和蝶窦的炎症，鼻窦炎可以单发，亦可以多发。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻窦炎应怎样治疗?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">两个方面：药物治疗和手术治疗。药物治疗是首选。其次是手术治疗：急性鼻窦炎在药物控制不满意或出现并发症时可采用鼻内窥镜手术，通过内镜引导直达病灶，开放鼻窦口，清除病变，改善局部引流，进而恢复鼻窦正常的生理功能。手术的适应症有以下几点：1、存在着一些影响鼻窦引流的一些解刨学的异常。我们叫做变异。影响窦口引流，里面的分泌物出不来。 2、正规药物治疗3个月后没效果的。3、出现鼻息肉，鼻窦炎往往同时跟鼻息肉在很大情况下是并发的，从专业来讲，鼻窦炎分为两个类型，一个是干性鼻窦炎，还有一个是鼻窦炎伴有鼻息肉的。 4、出现眼眶、颅内的并发症等等需要采取手术的治疗方法。</span>
</p>
