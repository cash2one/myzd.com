<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑膜瘤复发几率有多高？</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 不少病人问脑膜瘤手术后复发几率有多高？如何有效预防复发？以下资料来自于国外多篇文献。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;一级脑膜瘤(WHO I)占脑膜瘤总数的80%，辛普森一级切除后平均复发时间7.5年，复发比例为：5年复发率5%，10年复发率25%、15年复发率32%、20年复发率37-55%。不能达到一级切除的病人复发率很高，比如脑膜瘤次全切除复发率高达85%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;二级脑膜瘤(WHO II)占脑膜瘤的15-20%，即使一级切除，5年复发率40%，随时间延长复发率增加。平均复发时间为2.4年。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;三级脑膜瘤（WHO III)占脑膜瘤1-3%，为高度恶性脑膜瘤即使一级切除加放疗，也会比较快复发，预后不佳，平均存活时间为2年。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 目前，除手术外还没有有效手段预防复发，没能全切的脑膜瘤和三级脑膜瘤可以放疗延缓复发时间。</span>
</p>
