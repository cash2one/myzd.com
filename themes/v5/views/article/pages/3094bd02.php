<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>关注耳健康宜早不宜迟 —三博脑科将耳健康送进学校</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一年一度的“全国爱耳日”将至，来自河北石家庄的江丽在这一天前在北京三博脑科医院实施了听神经瘤摘除术，从此她的一侧耳朵的将失聪。在生命与单耳失聪之间，江丽不得不选择后者。江丽说，早在读中学时，她就发现自己有听力下降和耳鸣的现象，至今已有11年之久。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据为江丽主刀的于春江教授介绍，江丽如早几年手术治疗，完全有机会保住听力。目前，经他手治疗的听神经瘤已有近六百余例，绝大部分患者都是在听力下降多年后才发现是因为听神经瘤导致的。因此，做好听力障碍相关知识普及与听障查因的工作非常有意义。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2011年3月3日“全国爱耳日”宣传教育活动主题确定为“康复从发现开始”。为了响应这一活动，提高孩子们关注耳健康的意识，普及爱耳护耳科学知识，避免和减少耳聋发生，首都医科大学第十一临床医学院北京三博脑科医院组织以于春江教授、闫长祥教授、张宏伟教授为首的专家团队免费为北大附属中学香山学校的学生们进行了听力普查、听力障碍查因活动，并为学生们举办“爱耳护耳”的知识讲座。同时，三博脑科医院张阳院长表示，医院有意无偿为此次公益活动中听神经瘤导致听力下降的学生们提供治疗上的援助。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在活动中，除了给学生们普及爱护耳朵的常识外，于春江教授在给孩子们的知识讲座中特别指出，在患听力障碍的患者中，约有3―4%的比例是由听神经瘤所导致的，且这个比例呈上升的趋势，而这类患者往往初诊都在耳鼻喉科，容易忽略听神经的筛查而延误治疗，确诊时大都已经发展到中晚期，最后导致错失治疗的最佳时机而造成听力不可逆的损失，严重时甚至危及生命。因此，出现单侧耳鸣或听力下降等症状一定要引起重视，应及早进行影像学检查筛查听神经瘤以明确诊断后及时治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 新闻资料链接：据了解，目前我国患听力障碍人数已有2780万，其中0至6岁听障儿童约13.7万，听力障碍严重影响了他们的学习和生活。我国政府十分重视听力语言康复事业的发展，“十二五”期间，我国还将进一步加大投入，帮助更多听障儿童特别是贫困家庭的听障儿童实现康复。</span>
</p>
