	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>消化道肿瘤治疗：从开膛破肚到内镜微创切除</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">案例一】美籍华人李先生离乡背井30多年，3年前回到上海，亲朋好友争相接待，觥筹交错中突感胸口烧灼样绞痛，第二天胃镜检查发现食管壁有一个直径约1.5厘米大小的浅溃疡，医师诊断为早期食管癌。是接受传统的开胸大手术，还是有其它创伤小的治疗方法？李先生上网搜查发现，复旦大学附属中山医院可以不开胸进行胃镜下的切除手术。于是，李先生来到中山医院内镜中心，通过胃镜进行早癌的微创切除，住院3天出院。手术后复查3年，伤口痊愈没有复发。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 【案例二】近日房产大亨朱先生因母亲贲门癌去世接受胃镜检查，发现胃底贲门下方的胃黏膜下有一鸡蛋大小肿块，医生建议开腹切除肿瘤。联想到母亲贲门癌术后不能平卧睡觉，朱先生求治于复旦大学附属中山医院，结果胃镜下切除了肿瘤，同时保留了贲门。朱先生逢人便讲，今年打了一场漂亮的保“胃”战。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 近年来，随着消化内镜检查的普及，消化道肿瘤、尤其是早期癌和黏膜下肿瘤的发现迅速增多。对于消化道早期癌和黏膜下肿瘤，传统的治疗方法是外科手术切除，疗效确切，但手术创伤大，患者恢复慢，住院时间长，治疗费用高，手术后生活质量也大为降低。随着内镜治疗器械的不断开发和新技术的逐步开展，内镜发现的大部分早癌和黏膜下肿瘤可以实现内镜下的切除，达到外科手术的疗效。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对于胃肠镜发现的早期食管癌、胃癌和大肠癌，首先用染色剂给肿瘤点“颜色”使肿瘤着色，再在肿瘤周围做一环状标记，在肿瘤下方注射生理盐水使肿瘤明显抬起。沿肿瘤边缘标记好切除范围后切开周围正常黏膜，使肿瘤与周围正常组织分离，再用特殊的剥离器械将肿瘤从其下方的正常组织上完整切除下来。这种内镜切除方法医学上称为“内镜黏膜下剥离术(ESD)”，也就是消化道上“铲”肿瘤。而对于胃肠镜发现的黏膜下肿瘤，首先应用圈套器掀掉肿瘤表面的正常黏膜，露出肿瘤的庐山真面目，再用特殊的器械将肿瘤完整挖除出来，这种内镜切除方法医学上称为“内镜黏膜下挖除术(ESE)”，也就是消化道上“挖”肿瘤。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 复旦大学附属中山医院内镜中心在主任姚礼庆教授的带领下，以周平红副教授为主的治疗团队于2006年在国内率先开展ESD技术，至今已完成3000多例食管癌、胃癌、大肠癌等消化道早癌的ESD治疗，是国内开展时间最早、治疗病例数最多、国内发表ESD论文最早、进行ESD大会现场操作演示最早的医疗单位。在成功开展ESD的基础上，中山医院积极探索消化道黏膜下肿瘤的内镜微创切除治疗，创造性地开展ESE治疗消化道黏膜下肿瘤，目前已完成ESE治疗近1000例，微创切除范围和治疗适应征进一步扩大，由原先的表浅层(黏膜层)深入到消化道全层(黏膜下层、固有肌层，甚至浆膜层)。一大批原先需要外科手术的患者经ESD微创切除免除了外科手术之苦，住院时间缩短为1-2天，治疗费用也仅为外科手术的1/3-1/2，取得了较好的经济效益和社会效益。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 与传统外科手术相比，ESD和ESE治疗具有微创、恢复快、预后好的独特优势。既能保证肿瘤的彻底切除，又能最大限度地保留正常消化道，生活质量明显提高。由于创伤小，病人易耐受，住院时间短，有些病人治疗后当天就可回家。内镜下还可以把较大面积、形态不规则的肿瘤一次完整切除下来，明显减少肿瘤的残留和复发；可以在不开刀的情况下切除病变，获取完整的切片化验标本，明确肿瘤的性质和程度；同一病人可以多次接受ESD治疗，一次也可以同时切除多个病变。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 由于中山医院内镜治疗在国内的领先地位，姚礼庆教授和周平红副教授多次受邀出席国内学术大会，进行大会演讲和内镜操作演示。周平红副教授多次受邀参加国际大会，出访美国康奈尔大学、日本昭和大学、香港中文大学和台湾大学等进行大会演讲，并主持大会。为造福更多的患者，该院连续召开3届中日ESD高峰论坛和全国消化道早癌大会，举办5届全国ESD培训班，培养ESD学员200余人，在国内30多个省市推广内镜微创切除新技术。姚礼庆教授、周平红副教授还主编国内第一部《内镜黏膜下剥离术(ESD)》，在国内外期刊发表相关论文70余篇(其中SCI论文6篇)。相关科研项目连续获2006年上海市优秀发明选拔赛二等奖、2009年“恩德思”医学科技进步一等奖、2010上海市医学科技进步一等奖。</span><br />
