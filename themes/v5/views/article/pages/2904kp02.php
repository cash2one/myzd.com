<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>乳腺癌高发，因为这么多年在穿胸罩？(转载)&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">文胸与乳腺癌究竟有没有关系？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">没有关系，乳腺癌主要和激素有关</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸罩变成了“凶罩”，这样的论断一直的传播，在微信上，很多人在说，有钢托的文胸，会阻止淋巴液流向腋下的某个区域，而胸部的细菌和其他废物通常在那里清除，于是乳腺癌发生了。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还有人佐证说，中国女性这些年为什么乳腺癌高发？上个世纪50年代之前，很少人带胸罩，所以以前发病率很低，现在的女性，整天戴胸罩，才出现了这个问题。也有人继续“联想”，非洲女性就很少得乳腺癌，就是因为他们不戴文胸。难道乳腺癌高发，是因为这么多年在穿胸罩？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对此，江苏省人民医院副院长、江苏省妇幼保健院院长、江苏省人民医院乳腺疾病诊疗中心主任、著名乳腺外科专家王水教授说，这种说法是不成立的。正因为网上有太多类似的流言，早在 1991 年曾经也有过佩戴文胸与乳腺癌关系的研究，当时得出的结论是不戴文胸的女性患乳腺癌的较少，但这种差别在统计学上差异性不大，也就是说，这种假设不成立。后来，研究人员分析结果时考虑，可能是因为那些女性较瘦，而众所周知，肥胖是导致乳腺癌的风险因素。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近期，又有专家研究，文胸与任何一种乳腺癌的发病都没有关系，乳腺癌的发病和体内激素异常有关系。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸罩佩戴不好，会有怎样的弊端？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胸罩佩戴不当会诱发皮炎</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺增生、乳腺癌和佩戴文胸其实没有太大关系。不过，东南大学附属中大医院普外科胡浩霖主治医师认为，胸罩佩戴不当会诱发皮炎。如果胸罩佩戴不当，的确会引起一些不适，比如过紧过窄的文胸会导致皮肤瘙痒，甚至是接触性皮炎。医院的皮肤科会收到因为文胸佩戴不当导致的皮炎女孩。16岁的小丽（化名）最近跟同学一起逛街时，看到内衣店里款式漂亮、“效果显著”的加厚钢圈文胸，忍不住就买了几件。但是没穿几天，胸部老是痒痒的。于是，妈妈带着小丽来到中大医院皮肤科看看。结果是皮炎。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺癌和大胸有关系吗？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺癌面前，大胸小胸是平等的&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;现实生活中，一些“太平公主”很得意，总觉得自己得乳腺疾病的风险较小，得乳腺癌的机会就更少了。对此，江苏省人民医院肿瘤科主任医师殷咏梅教授认为，这种说法是错误的，乳腺癌面前，大胸小胸是平等的。 </span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;乳房大小和乳腺癌是没有关系的，因为乳腺是由二十到二十五个小叶组成的，而乳腺癌就是在这些小叶的腺上皮上长的肿瘤，乳房大是因为有脂肪和纤维集体组织。乳房大得乳腺癌的机会更多，这是不科学的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么人才是乳腺癌的高发人群？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">45-55岁的高压人群、高龄未育族&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中大医院自2003年启用江苏省第一台全数字化平板乳腺机以来，目前已接诊乳腺检查者约10万例，其中发现乳腺癌5千例，统计显示其年龄分布特征符合年轻化特点，高发年龄段主要分布在45-55岁。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王水教授说，国外曾经对未婚未育的女性与正常生育女性做过比较，发现未婚未育的女性患上乳腺癌的几率是正常人的2－3倍，有明显增加的趋势。专家指出，其实，乳腺癌变多，和很多因素有关系，现在营养好了，女性的乳房发育提前了，但结婚的年龄普遍在推迟。结婚后，很多女性为了保持身材不愿生育，做丁克族。有的女性虽然生了孩子，却不愿意哺乳，或者只哺乳一两个月就算完事。这其实都“助长”了乳腺癌发生。另外，压力大的白领人群也是乳腺癌的高发人群。</span>
</p>
