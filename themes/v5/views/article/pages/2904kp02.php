</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">穿胸罩的几个细节要注意&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、肩带过细或导致肩背疼痛&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对年轻女性来说，佩戴较细肩带式的胸罩，会显得窈窕健康，但很多人并不知道，胸罩穿不对还会伤颈椎。胸罩带过紧、过细可压迫颈部肌肉、血管、神经，使其受累，可诱发颈椎病，产生上肢麻木、颈部酸痛、头晕、恶心等症状。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、合体舒适最重要&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生提醒，胸罩的主要作用是支撑和保护乳房，在选购和佩戴时一定要以合体、舒适为原则，选择内里最好是纯棉的胸罩，易吸汗。如果清洗文胸的方法比较好，用中性的洗衣液，不伤害它的纤维，不放在阳光下暴晒。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、戴胸罩时间最好不要超过8小时&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">戴胸罩的时间最好不要超过8个小时，一般情况下下班回到家后，要尽量释放胸部，尤其是晚上睡觉，一定要解下胸罩，保证淋巴液的正常流动。</span>
</p>
