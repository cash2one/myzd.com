		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>章莹：“数字骨科”让手术时间至少缩短三分之一</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“数字骨科”实现影像三维重建及术前模拟 手术时间至少缩短三分之一</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 数字骨科技术的逐渐发展，使临床诊断和治疗水平得到大幅提高。但是，对于数字骨科科这项新技术鲜为人们所了解，为此，编辑特邀广州军区广州总医院骨科医院副院长章莹教授，详细介绍数字骨科技术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专家简介：章莹，广州军区广州总医院骨科医院副院长，兼创伤骨科主任，教授，医学博士，硕士生导师。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 疾病擅长：一直从事骨科专业的基础与临床研究，熟练掌握骨科各类伤病的诊断与治疗，尤其擅长于创伤骨科、显微外科及运动医学，熟练掌握关节镜技术及人工关节置换术。曾在德国雷根斯堡大学创伤中心及中国运动医学研究所访问学习，精通各类复杂骨折、骨不连、周围神经损伤及各类关节的关节镜微创手术治疗。近年来又在临床开展开计算机辅助技术，使骨创伤修复手术更加精确。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 出诊时间：星期二下午</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 数字骨科技术让手术时间缩短三分之一</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着数字骨科技术的日渐发展，复杂、高难度的骨手手术成功率得到提高。章莹教授介绍道：“对于复杂的疾病，与传统手术相比较，数字骨科技术有着巨大的优势，可以在不增加患者手术费用的前提下，提高手术精度，减少手术的风险。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 传统手术一般手术时间长，无法准确的定位，一般是依据医生的临床经验。而数字骨科技术则将手术时间至少缩短了三分之一，减少了患者的出血量，同时也降低了麻醉时间。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 比如5～6处的复杂多发骨折，传统手术需要7～8小时，而数字骨科技术则可把手术时间至少缩短2～3小时，单一部分的复杂骨折可以一小时内顺利完成。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专家提醒：数字骨科技术对于设备的要求比较高，所以能够应用的医院数量也有限。并不是哪家医院都具备把数字骨科技术运用到实际手术中的能力，据了解，广州军区广州总医院骨医科医院是目前在全国做这类手术数量最多的单位之一，质量也名列前矛，所以提醒广大患者，具在一定难度和风险的骨科手术要选择正规和有实力的医院进行。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 你对数字骨科技术了解多少？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 目前，数字骨科技术较广泛应用于骨科临床，并且融入了传统骨科手术治疗，数字骨科技术如何在临床进行运用的呢？章莹教授指出，数字骨科技术是一种复杂的过程，主要体现在以下五个方面：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1. 在诊断方面，从影像上实现了3D重建；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2.在影像上进行模拟手术，对于病情复杂的患者来说大大减少手术的风险；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 3.利用仪器塑造出比例为1：1的三维立体实体模型，把病变的骨骼甚至血管非常直观的呈现，并做出实物在体外进行模拟矫形、钢板预置等；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 4.通过电脑模拟，打印出适应矫形后骨骼的钢板，但是，由于一些客观因素的限制，目前并未广泛应用；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 5.进行假体设计，如人工膝关节、髋关节，可以精确的计算出患者出需要的假体形状和大小，然后交给工厂进行“量身”制作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 越复杂的疾病和手术越适合运用数字骨科技术</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 并不是所有的骨科疾病都需要用数字骨科技术，对于简单和低难度的手术，采用传统的方法就可以。目前，数字骨科技术主要应用于复杂和高难度的疾病，例如脊柱侧弯、后凸、畸形、O形腿、X形腿、关节内的骨折、骨盆骨折等疾病。</span>
</p>
