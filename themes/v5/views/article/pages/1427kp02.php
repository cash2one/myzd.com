		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>不开刀的甲状腺结节消融术</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">作为一个外科医生，经常会被患者问到甲状腺开刀时是否用微创方式。普通人的观念中，外观刀口越小，即越微创。我在前一篇文章中已经讲述过开放性和腔镜两种术式之差异，今天再来介绍一种真正微创兼美容的手术方式——消融术。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">门诊一</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：大夫，我甲状腺的这个结节好大，影响外观，是不是要开刀了？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 医生：你这是良性的，可以继续观察，如果心理上承受不了，可以处理掉。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：我很想处理，但对开刀实在害怕，既怕全麻，又怕疼，还耽误工作，每次都下不了决心，唉！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 医生：你这种情况，不用开刀，不用住院，局麻，观察几个小时就可以离开，接受吗？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：哇，有这么好的事？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 医生：是啊，现在超声技术进步了，我一边给你做彩超，一边插根电极细针到你的结节里，用热力把它消融掉，却不破坏甲状腺其它组织。你不放心的话，我还可以给你做多个穿刺以确定结节是良性还是恶性。做完消融，你休息一下就可以走了，过几天，针口都看不见，也不用吃药。你唯一需要的是耐心，等上几个月，让结节慢慢变小，被自身吸收。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：如果是这样的话，实在很方便！但不开刀，结节以后是否还会再长呢?</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;医生：有这种可能，因为甲状腺没有被切掉，所以结节复发的几率和常人一样。消融术肯定没有开刀这么彻底，但好处就是微创，美容，省时，安全。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：那么费用大概要多少呢？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 医生：和开刀差不多，一万多吧。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：好吧，大夫，那么请给我预约消融的时间，谢谢！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 门诊二</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;患者：呜呜呜，大夫，我的甲状腺癌术后发现淋巴结转移了，可是我真的不想再做手术啊！上次的经历太痛苦了！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 医生：你可以尝试一下做个消融，只针对你的淋巴结，不用再开刀。甲状腺癌是发展非常缓慢的疾病，把病灶处理掉，几年内是不会有问题的，可以慢慢观察再做决定，有些人可能一辈子都不复发了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：消融不是只针对良性结节的吗？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 医生：甲癌术后复发或者转移的都可以，但你要承担再复发的风险。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; 患者：只要不用开刀，我愿意尝试新方法！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">以上提到的消融术就是现在治疗甲状腺结节的一种新方法。即在彩超的全程引导下，将一根1mm粗的电极针插入结节内部，通过发射高频电磁波，微波，甚至激光使病灶温度升高，发生凝固坏死，然后人体会慢慢将这些坏死的组织吸收掉，结节（肿瘤）因此消失。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">消融治疗具有适应范围广，效果好，微创，恢复快，美容等优点，受到部分患者的欢迎。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">说了这么多消融的优点，那么，它的缺点是什么？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1． &nbsp;不如常规手术彻底，有复发风险，原因在于没有切除甲状腺。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2． &nbsp;术后结节需要3-6个月才可以被慢慢吸收，不如常规手术般立竿见影。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3． &nbsp;怀疑是原发癌并转移的病灶不推荐使用此方法。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着科技的进步，患者对生活质量的要求会越来越高，令患者满意就是医学发展的动力。只要医生严格把握手术适应症范围，谨慎操作，相信甲状腺结节消融这种治疗方式在国内将会得到越来越多人的欢迎和接受。</span>
</p>
