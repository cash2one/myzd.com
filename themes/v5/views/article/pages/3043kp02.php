			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肝脏里的水泡泡</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“医生，我感到肚子很胀，而且越来越胀，上腹部好像摸到一个好像球一样的东西，最近增大许多，好像还有弹性，这个肿块要紧伐？”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊经有时可以遇见这样的病人，一般情况良好，躺下后可以发现肚子隆起，有时可以摸到具有弹性和波动性的囊性肿块，这时作个超声或者CT就可以发现肝脏上出现了体积较大的囊性肿块，有时可以发现大大小小好几个。绝大多数的肝囊肿都是先天性的，肝囊肿，通俗点说就是肝脏中的“水泡”，即因先天发育的某些异常导致了肝肾囊肿形成。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">囊肿可以是单发的，就只一个，小至0.2厘米，临床上经常可以碰见肝囊肿，以孤立性囊肿多见。孤立性肝囊肿通常无任何症状，如果肝功能正常，不需服药，一般也不要手术切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">也可以多到十来个、几十个，甚至也可有一个是大至几十厘米的。上海中山医院资料中，有一位77岁男性肝囊肿病人，其肿块大至从腹腔延伸到盆腔。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除非巨大囊肿，出现压迫症状者可以手术切除。若囊肿较大可出现压迫症状，如腹痛、恶心、腹泻等。囊肿的发生部位以右肝居多。一般情况下，病人全身情况良好，肝功正常，可作开窗术，以减轻压力，缓解症状，促使肝细胞再生，只要不产生症状，也算治愈了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有些病人常以上腹肿块为首发症状，终末期出现腹水，门脉高压等肝功能不全的症候。当囊肿长大到一定程度，可能会压迫胃肠道而引起症状，如上腹不适饱胀。也有因囊肿继发细菌感染而有腹痛、发热的。如果一旦囊肿并发细菌感染，可先用抗生素治疗，如果还是不能控制炎症，可作所谓“开窗”术。即将囊壁切去一块，引流其中液体。这种手术对身体健康影响不大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">B超对肝肾囊肿的检出率可达98%，所以很多人会在体检中发现肝脏囊肿。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">更有甚者这个肝脏长满了密密麻麻，大大小小的囊肿，称之为多囊肝。多发性肝囊肿又称多囊肝，有半数以上的病人合并有多囊肾，多囊肝常侵犯整个肝脏，也有少数多囊肝病人的病变局限于肝脏的一叶或半肝范围。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">囊肿多而且大时，除了上腹部不适，压力增高会使肝脏实质萎缩，由此需要手术治疗。多囊肝的囊肿可满布肝脏，有些病人常以上腹肿块为首发症状，终末期出现腹水，门脉高压等肝功能不全的症候。病情严重者，肝脏密密麻麻全是大大小小的囊肿，无法切除，肝实质很少，肝功能损害，这时需要进行肝移植，以彻底根治本病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">后天性的因素少有，如在牧区，如人们染上了包囊虫病，在肝脏中便会产生寄生虫性囊肿，笔者在援疆工作期间就碰到多例肝包囊虫病，由于上海地区罕见，由此不复赘述。此外外伤、炎症，甚至肿瘤也可以引起肝肾囊肿。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">囊肿是在肝脏上面长出来的一个肿瘤，尽管没有多大的症状，也很不放心，会不会变肝癌呢? 先天性肝肾囊肿是不会癌变的。肝肾囊肿常见的并发症是破裂出血、细菌感染、瘘及穿透，而罕见癌变。据上海中山医院资料，对65岁以上45例肝肾囊肿随访5年，经B超及CT复查，无有一例癌变。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝囊肿的治疗不需要吃药，手术的原则也是症状明显，影响肝功能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">以往开腹手术是常规的治疗方法，随着微创技术的进展，对于刚才门诊的病人，由于他肝功能正常，囊肿属于可切除的范围，我们选择了最适合他的治疗，腹腔镜下肝囊肿切除和去顶，术中注意胆管和血管，病人术后腹胀消失，肝功能恢复良好，三天出院。</span><br />
