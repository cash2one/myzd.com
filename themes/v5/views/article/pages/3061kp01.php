<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，我国的前列腺癌发病率呈逐年上升的趋势。在西方国家，前列腺癌已成为仅次于肺癌的第二大肿瘤。随着PSA筛查等检测手段在临床的广泛应用，早期前列腺癌诊断率大大提高，其治疗也成为现今医学研究的一大热点。 前列腺癌根治术是治疗早期前列腺癌的标准方法。早在1997年，就有腹膜外途径腹腔镜前列腺根治术治疗局限性前列腺癌的报道。1999年Guillonneau建立了标准化的腹腔镜前列腺癌根治术。目前腹腔镜前列腺癌根治术已经成为一种成熟的治疗方法，在美国及欧洲等发达国家和地区，其在临床实践中正逐步取代传统的开放前列腺癌根治术，成为治疗局限性前列腺癌的首选方法。 和传统的开放手术相比，腹腔镜前列腺癌根治术具有很多独特的优势。 1）肿瘤控制良好。 腹腔镜前列腺癌根治术和开放手术清除肿瘤的疗效相当。腹腔镜前列腺癌根治术切缘阳性率、肿瘤残留率与开放手术相比没有统计学差异，两种术式术后远期生存情况一致。 2）术中损伤小，出血少。 据报道，腹腔镜前列腺癌根治术术中出血量仅开放手术出血量的四分之一。这为手术提供了良好的视野，同时也可减少术中周围组织损伤的风险。 3）术后恢复快。 开放手术后患者常常需要留置导尿15天左右，而行腹腔镜前列腺癌根治术的患者往往在术后1周左右就可以拔除尿管。另外，腹腔镜前列腺癌根治术明显缩短了患者的平均住院天数，在减轻患者痛苦的同时，节省了医疗开支。 4）患者术后生活质量影响小。 尿失禁是影响前列腺癌根治术后患者生活质量的主要的因素之一。大样本临床研究提示，腹腔镜前列腺癌根治术与开放前列腺癌根治术两种手术方式真性尿失禁的发生率基本一致。对于较多见的术后压力性尿失禁，腹腔镜前列腺癌根治术后6个月，约95%的患者可以恢复至理想水平。 综上可知，腹腔镜前列腺癌根治术的手术治疗效果和术后远期并发症与开放手术相类似，但是其围手术期并发症少，术后恢复快的特点是开放手术无法取代的。目前腹腔镜前列腺癌根治术的应用范围已经十分广泛，特别是在大型医疗中心已作为常规手术开展，中转开放率仅为2%左右。</span>
