	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>一针见效别轻信</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">本报讯 （记者 唐江澎通讯员魏惊宇）“微创手术有很强的局限性和应用范围，不可能什么病变都‘微创’。否则微创也会酿成巨创，甚至给患者带来灾难性后果。”昨日，第二届全国微创脊柱外科学术会议暨2007国际内镜脊柱外科技术高峰论坛在长闭幕。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">会上，来自美国、德国、意大利以及国内的众多专家表示，微创脊柱外科技术不仅要在学术上明确其先进性，更应体现医者对患者的关爱，切不可成为临床追求的某种时尚，所谓的“一针见效”更是不可轻信。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据中南大学湘雅二医院脊柱外科主任、省脊柱外科中心主任吕国华教授介绍，所谓微创手术，是指比传统手术创伤更小的手术技术，具有疼痛轻、恢复快、创伤小、住院短等优点。但由于我国目前对开展微创骨科缺乏必要的准入制度，一些不具备微创骨科条件的医院或医生纷纷开展该项目，以往简单、有效的传统手术也被冠以微创骨科的美名，或者错误地认为切口小就是微创。目前，全国每年大约有10万人接受微创骨科手术，其中只有一半人享受真正意义的微创。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 专家们表示，由于商家的炒作，目前有些医生既没有开放手术的基础和经验，又没有经过规范化微创技术培训；有些医院不具备开展微创手术的条件，也盲目开展微创外科手术，导致手术的失败和医疗资源的浪费。因此，一方面要尽快建立准入制度，防止出现技术滥用，同时，患者也要正确认识微创。</span>
</p>
