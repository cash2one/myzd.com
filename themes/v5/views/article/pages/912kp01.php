			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>慢性化脓性中耳炎：手术效果怎样？</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>（1）流脓</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">流脓是慢性化脓性中耳炎最常见的症状，可以反复发作。通过手术可以清除病变，修补鼓膜，将中耳与外界隔绝，避免受感染后流脓。目前通过手术，90%以上都可以达到这个目的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>（2）听力</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">听力是患者非常关心的问题，但是要具体问题具体分析。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果是单纯的因为中耳传音功能下降导致的传导性聋，一般通过鼓室成形术、听骨链成形术都可以有一定程度的提高，至少可以维持现状或避免听力进一步下降。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是当合并耳蜗、听神经等内耳疾病造成的感音神经性耳聋时，通过手术是难以彻底恢复听力的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>（3）胆脂瘤</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆脂瘤并非一种真正的肿瘤，而是鳞状上皮生长于中耳腔内的肿块，可以因破坏骨质、中断听骨链、侵犯迷路、累及面神经等而引起耳聋、眩晕、面瘫等症状，因而必须予以坚决清除，目前手术也基本可以保证彻底清理。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是胆脂瘤需要面对的一大问题是复发！！！</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">理论上，只要有上皮细胞残留于中耳，就有可能导致胆脂瘤复发！因而国外部分医疗单位常规对做过胆脂瘤手术的患者专门再做一次手术，看看中耳里面有没有胆脂瘤复发。在国内，由于经济条件、观念的限制，没有办法常规开展，只能靠患者定期复查，确实有胆脂瘤复发时，再次手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>（4）耳鸣</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">耳鸣对患者的生活质量有很大的影响，可造成焦虑、失眠、抑郁等精神症状。造成耳鸣的原因有很多，而且很大一部分与中枢神经活动有关。因为耳鸣发病原因不明，因而慢性化脓性中耳炎手术治疗的目的也不包括消除耳鸣！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一般而言，由于局部病灶引起的短期耳鸣，通过清理病灶、营养神经等正规治疗，有可能消失；对于长期神经性耳鸣，无论手术还是药物，疗效都不是非常确切。</span><br />
