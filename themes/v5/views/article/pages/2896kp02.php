<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>美国人喂养唇腭裂患儿的经验</strong></span> 
</div>
<br />
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>一、前言</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人人都希望给孩子最宝贵的东西：健康、快乐的生活。初为父母，如何喂养新生婴儿，受到家庭传统、社会习俗、文化价值观以及既往喂养经验的影响。要满足唇 腭裂患儿的营养需求，在喂养方面要综合考虑喂养食物、喂养姿势和喂养技巧。本文旨在帮助您了解唇腭裂患儿在喂养时可能遇到哪些问题，以及如何克服这 些问题，使喂养过程既能保障营养，又能身心愉悦。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>二、喂养经验</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于所有婴儿来说，喂养都有重要的作用，其中最重要的就是提供生长发育所需的营养。不仅如此，喂养过程还能加强亲子关系，锻炼婴儿口腔肌肉，帮助婴儿获得愉悦感。所以在选择喂养方式的时候，这几点都要考虑到。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">进食是宝宝的主要活动。喂养时发生的亲子互动，促进了亲子间的积极情感，强化了亲子关系。吮吸动作需要舌头和口腔其他肌肉的主动运动。这些肌肉的发育可 以帮助宝宝将来顺利完成用杯子喝水、咀嚼、语言和发音等动作任务。另外，喂养过程应使宝宝因吮吸而产生愉悦感，所以最好的喂养技巧就是让宝宝主动地吮吸。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>三、宝宝的营养</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一个足月、健康的新生婴儿每天需要2~3盎司（50~90毫升）每磅体重的母乳或配方奶才能获得足够的营养增加体重。因此，一个体重8~10磅 （3.6~4.5公斤）的婴儿每天需要16/24~20/30盎司（450/680~570/850毫升）母乳或配方奶，相当于一罐超市卖的即食配方奶。 新生儿在出生第一周体重下降10%是正常的，不要心急，只能逐步增加婴儿的进食量。到出生第二周，婴儿平均每天要进食18~24盎司（510~680毫 升）母乳或配方奶。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">喂养唇腭裂患儿的问题包括鼻腔返流（医学术语，就是食物从鼻孔流出来）、喂食时间长以及影响患儿实际进食量的个体差异（医学术语，就是搞不清楚原因 ^_^|||）。因此，要保持平和的心态。在出生的头4~6周，每周给宝宝称一次体重，最好是在每周的同一天、同一天的同一时间、用同一个称来称。这样，你和 医生都能准确知道患儿的发育是否正常了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">喂养习惯建立以后，多数婴儿一次进食不超过30分钟。如果超时，说明宝宝进食过于吃力，反而是影响生长的，应考虑改进方法。就进食的间隔时间而言，隔 3、4个小时吃一顿的宝宝要比不到2小时就吃一顿的宝宝长得好。进食太勤的宝宝吃饭就像吃零食，只是吃到不饿，而没有满足身体发育的营养需求。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最后，宝宝的喂养问题多和可靠的医生讨论，不要凭个人经验或者想当然。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>四、腭裂宝宝存在的喂养困难</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">只有唇裂或者唇裂伴有牙槽裂的宝宝喂养方面一般没有太大问题。象所有新生宝宝一样，唇裂宝宝一生下来很快就能学会含住乳头。不过，在腭裂宝宝的喂养技巧、营养供应、喂养体位等方面就有所不同。这是因为腭裂宝宝的口腔和鼻腔没有正常分隔开，造成他们的吮吸能力较弱、可能有食物鼻腔返流、进食时间过长以及吞咽和呼吸动作不协调。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腭裂宝宝要想吸到奶，就要用舌头和缺损的上腭把乳头夹住才能进行吮吸。因此他们容易在吸奶时吸进过多空气，经常需要打嗝排气。很多类似问题都需要合适的技巧来解决。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>五、如何喂养腭裂宝宝（重点啊^_^）</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不论你选择什么喂养方式，大多数医生都认为母乳是新生儿的最佳食物。美国儿科医学会建议母乳可以吃到一岁（编注:不是只吃母乳）。如果母乳不足，医生可以帮你（编注:不是替你）选择营养成分配比最适合宝宝消化系统的配方奶（编注:人家不是奶粉!伤不起啊!）。如果所用的配方奶需要调制或稀释，请认真阅读产品说明书，确保正确配制。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">市场上有几种转为唇腭裂宝宝设计的奶瓶和奶嘴。挑选的时候，要注意以下几点:</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.薄而软的奶嘴容易吮吸;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.奶嘴的出奶量适中，不太快也不太慢;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.奶嘴不能影响正常吞咽功能和口面部正常肌肉的运动。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多家长还希望奶瓶外形不要太特别，容易买到，不贵。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不管你选择什么样的奶瓶和奶嘴，目的都是为了让你喂养起来更容易，宝宝也能更好地进行吮吸。因此，相对软的奶嘴要好过相对硬的奶嘴。如果特制的奶嘴不合用，可以选择早产儿专用奶嘴，因为很软。NUK的矫形奶嘴也是一种选择。等宝宝长大些、变壮了，奶嘴也要随之升级，不然宝宝一吸奶嘴就瘪了，吃不到奶。</span><br />
<p>
	<span><span style="line-height:25.454544067382813px;"><img src="http://static.mingyizhudao.com/147002416494324" title="" alt="demo.jpg"/></span></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（NUK的矫形奶嘴）</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有些腭裂宝宝喂养不好的原因既可能是奶嘴太短，也可能是奶嘴太长。奶嘴太短，宝宝的舌头和上腭和奶嘴接触面积过小，含不住；奶嘴太长，宝宝含不到奶嘴膨大的部分，就含不紧。有时候可以选用Ross奶嘴，这种奶嘴比较软，外形像管子，宝宝只需轻轻吮吸就可让奶直接通过裂开的上腭。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多医生都会推荐十字口的奶嘴，这种奶嘴可以利用宝宝正常吮吸、吞咽的节律控制奶的流量，因为十字口奶嘴只有在宝宝吮吸的时候才开放。而普通较大的圆形奶嘴开口会使奶液持续流出，宝宝无法自行控制，增加宝宝协调呼吸和吞咽动作的困难，容易造成呛咳。如果自己动手，任何奶嘴都能用单面刮胡刀片切割十字形开口。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">增加奶流量的另一种方法就是使用挤压式奶瓶或者塑料瓶衬（plastic bottle liner）。用塑料瓶衬的话，在喂奶前要排空瓶衬里的空气，然后根据宝宝吮吸的节奏间断性的压迫瓶衬。这种方法可以最大限度地减少宝宝吞咽时吞进胃里的空气，也使宝宝吮吸得更轻松。挤压式奶瓶也叫辅助输送系统（还有比这更学术的名字吗?-_-||）,腭裂宝宝专用的已知有三种: 美赞臣（Mead Johnson Cleft Palate Nurser)、希柏（Haberman Feeder）、贝亲（Pigeon Cleft Palate Nurser）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">美赞臣的腭裂奶瓶配有一个薄而软的奶嘴，十字形开口。相对长的奶嘴设计可以使奶液顺利通过裂隙部分。瓶身很软，很容易根据宝宝吮吸和吞咽的节奏按捏奶瓶。这样的设计可以克服宝宝吮吸能力不足的问题。按捏奶瓶的时候动作要轻柔、有节奏，不要一直按着不放。根据宝宝自己吃奶的节奏按捏可以确保宝宝不会把奶吸进肺里，也就是误吸。</span><br />
<img src="http://static.mingyizhudao.com/147002416694817" title="" alt="demo.jpg"/><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">希柏的奶瓶配有一个大型的、可按捏的奶嘴，一字形开口而不是十字形开口。在奶嘴柔软的基部，有一圈标记标出了一字型开口相对于宝宝嘴巴的位置，同时也让喂奶的人可以知道奶的流速。奶嘴的基部设计了一个垫片，作用类似于单向阀门，令奶液只能单向流动，进入奶嘴（不能流回奶瓶里面）。同时垫片也可以减少奶嘴里面的空气，以免宝宝吞入过多。希柏奶嘴有两种型号：普通型和迷你型。普通型奶嘴是标准长度，迷你型短一些，和早产儿用的差不多。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147002416989723" title="" alt="demo.jpg"/></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">贝亲的奶瓶配有一个带“Y”型开口的奶嘴。和其他奶嘴相比，贝亲奶嘴稍大、稍膨一些，更适合宝宝的口腔。它的头部较硬，基底部较软，宝宝的舌头容易吮吸。当宝宝吮吸的时候，还有一个空气阀门防止奶嘴瘪陷。拧松或拧紧瓶颈部可以控制奶液流速。回流控制阀门可以防止奶嘴中的奶液返回奶瓶，也能减少奶嘴里面的空气，以免宝宝吞入。尽管贝亲奶瓶不是软质的，但是有弹性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">无论采用什么奶嘴或喂养器具，奶嘴要和宝宝的嘴巴成一定角度，让奶液直接越过裂隙，这样喂起来更容易。因为这样宝宝可以用舌头和上牙床夹住奶嘴进行吮吸。你也可以让医生来帮你选择最适合你和宝宝的奶瓶和奶嘴。</span> 
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span> 
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>六、其他建议</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">喂奶时让宝宝处于合适的体位十分重要。把宝宝竖起来喂可以减少奶从鼻子里反流出来的情况。喂奶的人可以采取坐姿，一只脚或者两只脚踏个小凳子，用腿垫高宝宝；也可以用枕头撑住宝宝的身体，让他/她更舒服。要记住，即便采用合适的姿势，有时候还是会有奶从鼻孔反流的。如果出现奶液从鼻孔反流的情况，暂停喂奶，让宝宝自己把奶咳出来或者擤出来。只需短暂停顿宝宝就能自行清理干净鼻子里的奶液，可以接着吃了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">宝宝在适度饥饿的时候进食比较好，而不是非常饥饿的时候。因此，家长应该在宝宝刚刚醒来就喂奶。宝宝从睡梦中醒来的标志有：闭着眼皮眼珠转动；嘴巴蠕动；把手靠近嘴边。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大部分腭裂患儿采用本文介绍的喂养技巧就能得到很好的喂养。不过，对于裂隙过宽的患儿，医生会建议使用一些腭裂专用喂养辅助器具，比如腭护板，腭阻塞器等。腭护板是医生在宝宝嘴里取出口腔上腭模型，然后制作的塑胶上腭护板。戴在宝宝嘴里可以和宝宝上腭贴合，起到部分封闭口腔-鼻腔裂隙的作用。口腔科有专业的医生来制作、调试以及定期检查腭护板。</span> 
</p>
