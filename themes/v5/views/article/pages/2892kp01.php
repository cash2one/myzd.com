			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>话说脑垂体肿瘤</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">垂体腺瘤是一种生长缓慢的颅内肿瘤，发病率约1／10万人，占颅内肿瘤的10％。近十年来，垂体肿瘤的临床和基础研究有了飞速的发展。放射免疫测定和各种激素的兴奋／抑制试验的应用，以及CT、MRI等影像学检查的进步，使垂体腺瘤的诊断准确性较之过去以视野、视力改变、X片上的蝶鞍骨质的变化等诊断标准前进了一个时代。加之现代显微外科、放射神经外科的广泛开展，各种疗法的不断改进，使垂体腺瘤的全切除率大大提高。根据国内外报道，小型或微型腺瘤切除后，症状改善率达70％～90％，垂体大腺瘤如切除彻底，有效率亦达30％～70％。又由于分子生物学的迅速发展，垂体肿瘤单克隆发生及致瘤基因理论的研究，使人们对垂体肿瘤的认识不断深化，从而为临床疗法的改进，开辟了广阔的前景。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>1 &nbsp;垂体腺瘤的发生机制</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">过去学者们对垂体腺瘤发生机制的认识，主要包括二种假说：一是垂体细胞自身缺陷学说；二是下丘脑调控失常学说。目前由于分子生物学的发展，垂体瘤发生的多步学说逐渐将二种学说统一起来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2 &nbsp;垂体腺瘤的分类</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">以往按光学显微镜下病理染色的不同将垂体腺瘤分为嗜酸性、嗜碱性、嫌包性、混合性四种。由于内分泌学研究进展，这一分类法已不满足临床需要。Mosa和Baroni于1963年首先提出根据内分泌功能分型，后经许多学者改进，现分型如下：①有分泌功能的腺瘤：包括单激素分泌腺瘤和多激素分泌腺瘤。②无分泌功能的腺瘤：包括未分化腺瘤和瘤样细胞瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3 &nbsp;垂体腺瘤的治疗方法</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">垂体腺瘤的治疗方法有手术治疗、放射治疗及药物治疗等。手术治疗仍是垂体腺瘤的主要治疗方法。垂体腺瘤的手术途径曾有许多变更，大体可分为经额颞开颅和经蝶窦入路两种，分述如下。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3.1 经蝶窦垂体腺瘤切除术</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.1.1 术式发展 经蝶窦入路手术始于Schloffer，1909年Cushing成功地应用经蝶垂体腺瘤切除术治疗了一例肢端肥大症患者。在当时缺乏抗生素的条件下，其手术死亡率低于经颅手术，故而在很长时间内被视为垂体腺瘤的首选入路。但由于其对鞍隔上瘤块切除不彻底，加之术野深，照明差，故又渐为经颅手术所代替。直至60年代，Hardy等利用手术显微镜，使术野显露大为改观，使经蝶入路既能切除腺瘤、又可保持正常垂体功能。70年代以来，随着CT、MRI等先进的影像学检查及放射免疫测定法的出现，使垂体腺瘤特别是微小腺瘤的早期诊断成为可能。垂体手术亦可以达到对机体内分泌紊乱予以彻底根治。从此垂体腺瘤的经蝶入路手术方式得到了空前发展。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">逐渐好转。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3. 1.2 内窥镜在垂体腺瘤切除中的应用</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经内窥镜垂体腺瘤切除术，是近年来发展应用的一种手术方式。近十年来国内外报导日渐增多，而国内在这方面研究尚不多见。Matulac等〔18〕认为通过神经内窥镜，可以发现一般手术显微镜无法观察到的结构。更重要的是，利用内窥镜使显微手术的范围扩大，可以看到后方和周边结构，在神经外科中是一种安全、有效的手术方法。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，使用鼻腔内窥镜，避免了传统的切开方法及术后鼻腔填塞，使患者更快痊愈。内窥镜能使术者看清蝶窦全景，避免损伤周围结构。并且通过改进照明及放大设备，能提供蝶鞍及蝶鞍上区的极佳视野。这为彻底切除肿瘤和保存垂体功能，避免神经血管损伤提供了良好前景。</span>
</p>
