</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海交通大学医学院附属仁济医院神经外科葛建伟副主任医师近年来采用最新一代高清神经内窥镜，为许多垂体瘤患者施行内窥镜肿瘤切除术，为广大患者解除了病痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3.2经颅垂体腺瘤切除术</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近20年来，由于经蝶入路垂体瘤切除术的普及，经颅手术的比例逐渐减少。Massimo等统计932例手术患者，仅有48例（5.1％）行经颅垂体腺瘤切除术。但由于垂体瘤的生长及扩展特点，这两种入路仍各有其存在价值。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp;4 垂体腺瘤的早期筛查</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 生育期女性如出现停经、泌乳、不孕，健康男性出现性功能下降，成年人出现肢体明显变粗、口唇厚、鼻形增大，体重不明原因上升、视力不明原因下降等情况均提示脑垂体疾患的可能，须到医院及时就诊。</span><br />
