		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>咽喉癌的临床表现及发病原因（科普）</strong></span>
</p>
<p>
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">咽喉癌是鼻咽癌，口咽癌，喉咽癌、喉癌的总称。喉咽又称下咽，下咽癌多发在梨状窝，其次为下咽后壁。咽喉癌治疗方法有单纯放疗、单纯手术、手术加放疗、化疗和免疫治疗等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(1)声音沙哑—喉癌最常发生于真声带会影响发音，下咽癌末期侵犯喉部时也有沙哑现象。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(2)咽喉异物感—尤其下咽癌常有咽喉单侧异物感。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(3)疼痛—吞咽疼痛久治不愈。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(4)血痰—咽喉癌表面溃疡出血时痰中带血。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(5)吞咽困难—下咽肿瘤持续长大阻碍食物通过。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(6)呼吸困难—喉部肿瘤太大时呼吸道阻塞之故。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(7)颈淋巴肿大—发生于淋巴转移时。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(8)其他—如莫明之体重减轻，食欲不振。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(9)头痛—鼻咽癌破坏颅底，可以导致头痛。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(10)耳闷、听力下降—鼻咽癌导致咽鼓管阻塞，可以导致中耳腔积液之故。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">咽喉癌发病的原因是什么?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(1)化学致癌物烟草中尼古丁;酒精可作为致癌物的溶剂，促使致癌物进入黏膜。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(2)物理致癌因素不合适的牙托、义齿、龋齿、残缺的牙嵴等;不良的口腔卫生习惯，长期机械性损伤,经常卡鱼刺、骨头等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(3)生物致癌因素人乳头状瘤病毒与喉癌、下咽癌、口咽癌发病有关;鼻咽癌发病与EB病毒感然有关，遗传、个体易感性、营养代谢障碍、种族及放射线等。</span>
</p>
