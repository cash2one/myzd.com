<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>国内外专家聚宁研讨妇科微创术</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报讯(通讯员 小河) 在昨天召开的《妇科微创技术新进展学习研讨班》上，自然通道手术—未来的腹部手术成为一大亮点。这次国际性的学习班由南京医科大学第二附属医院妇产科主办，来自德国的著名妇科专家Dr. Stark 教授在大会上专题介绍了“十步法阴道子宫切除术法”,并亲自作了手术演示。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前在我国，腹腔镜、宫腔镜等微创技术已广泛应用于妇科疾病的临床治疗，包括：腹腔镜广泛子宫切除术+淋巴结清扫术治疗妇科恶性肿瘤；宫腹腔镜联合应用治疗不孕症；宫腹腔镜电切术在妇科的应用；腹腔镜治疗慢性盆腔痛手术；PFP盆底重建手术思考及盆底重建手术并发症的处理等等，而以“十步法阴道子宫切除术”为首的自然通道手术可谓是微创技术在妇科应用中的一个突破性的进展。受到了与会各大医院妇产科专家们的大力追捧。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">德国的著名妇科专家Dr. Stark 教授是“横切口美容剖宫术”的创始人，当他得知他的这一技术早在十年前就被南医大二附院引进应用并且获得了省卫生厅新技术引进奖时，风趣的对记者说：你看，我的技术竟然比我先到中国了！我相信，以中国同行的聪明和敬业，自然通道手术也一定会在中国发扬光大的。采访时，Dr. Stark 教授反复强调，作为医生，一定要让患者遭受的痛苦和伤害减少到最低限度，“十步法阴道子宫切除术法”正是秉承了这一理念的结晶。Dr. Stark 教授的讲座图文并茂，在南医大二附院妇产科主任、留美博士应小燕教授翻译下，更显得深邃、精彩，赢得了与会代表们一阵阵热烈的掌声。当天下午，Dr. Stark 教授即为代表亲自进行了手术演示，令代表们获益匪浅。随后两天，研讨班还将安排几十台现场手术演示。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"></span>
