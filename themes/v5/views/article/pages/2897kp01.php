<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>漏斗胸的微创治疗&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2897/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">漏斗胸</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">漏斗胸(funnel chest)是一种先天性并常常是家族性的疾病，俗称塌胸脯，男性较女性多见(4:1)。漏斗胸的原因不明，认为与遗传有关，属伴性显性遗传；此外由于维生素D缺乏造成钙、磷等矿物质不能沉积在骨骼上的佝偻病，有的造成双腿的畸形，有的则造成小儿胸廓的畸形。漏斗胸属渐进式病变，在出生时可能就已存在，但往往在几个月甚至几年后才愈来愈明显而被家长所发现。外型特征为前胸凹陷，肩膀前伸，略带驼背及一个突出的上腹。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、 漏斗胸有什么症状？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) &nbsp; &nbsp; &nbsp; &nbsp;漏斗胸在临床上非常容易发现，畸形一目了然，即胸骨、肋软骨及一部分肋骨向脊柱凹陷形成“漏斗状”。儿童往往表现为一种独特的虚弱姿势：颈向前伸，圆形削肩，罐状腹。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2) &nbsp; &nbsp; &nbsp; &nbsp;轻微的漏斗胸可以没有症状，畸形较重的压迫心脏和肺，影响呼吸和循环功能。小儿肺活量减少，易发生反复呼吸道感染，使小儿的体格发育受到影响；年龄较大的可以出现活动后呼吸困难、脉快、心悸，甚至心前区疼痛，部分患者还可以出现心律失常，以及收缩期杂音。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3) &nbsp; &nbsp; &nbsp; &nbsp;漏斗胸有时合并肺发育不全、Marfan综合征、哮喘等疾病，这些疾病合并存在时常常成为患者不可耐受的畸形。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、漏斗胸的诊断检查：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) &nbsp; &nbsp; &nbsp; &nbsp;X线胸片检查：下段胸骨向后凹陷，与脊柱间的距离缩短；心影多向左侧胸腔移位，心影的中部有一个明显的放射线半透明区，右心缘常与脊柱重叠；侧位胸片可以看到胸骨体明显向后弯曲，有的胸骨下端可以抵达脊柱前缘。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2) &nbsp; &nbsp; &nbsp; &nbsp;胸部CT片：能清楚地显示胸廓畸形的严重程度及心脏受压移位程度。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3) &nbsp; &nbsp; &nbsp; &nbsp;心电图(ECG)：表现为V1的P波倒置或双向，也可以有右束支传导阻滞。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4) &nbsp; &nbsp; &nbsp; &nbsp;心导管检查：可以描记至舒张期斜坡和平台，与缩窄性心包炎所见相同。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5) &nbsp; &nbsp; &nbsp; &nbsp;心血管造影：显示右心受压畸形和右室流出道受阻。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、漏斗胸为什么需要治疗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) &nbsp; &nbsp; &nbsp; &nbsp;严重影响身体美感，在幼儿会造成自卑感、自我封闭。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2) &nbsp; &nbsp; &nbsp; &nbsp;影响心肺等机体功能。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、漏斗胸治疗方法：轻度的漏斗胸通过加强营养，加强锻炼可得以改善；若为维生素D缺乏引起的，可以补充钙和维生素D，并多晒太阳；若为严重的漏斗胸并影响小儿的心身发育，则需到医院检查，中度和重度漏斗胸的儿童，宜手术治疗，矫正畸形。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(一) &nbsp;传统手术： </span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) &nbsp; &nbsp; &nbsp; &nbsp;肋骨成型术：适用于单侧较深而不涉及胸骨的漏斗胸。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2) &nbsp; &nbsp; &nbsp; &nbsp;胸骨抬高术：这种方法术后可能出现反常呼吸，不太受欢迎。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3) &nbsp; &nbsp; &nbsp; &nbsp;胸骨肋骨抬高术：特别适用于肋软骨肋骨骨质都比较柔韧的较年轻的患者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4) &nbsp; &nbsp; &nbsp; &nbsp;带上下血管带蒂胸骨翻转术：本法术中不切断胸廓内动静脉和腹直肌胸骨的血液循环能够保持正常，确保了术后胸骨的正常成长发育，手术后胸壁稳定，无反常呼吸患者可以早日下地活动，畸形纠正效果满意。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5) &nbsp; &nbsp; &nbsp; &nbsp;带腹直肌蒂胸骨翻转术：本法切断胸廓内动静脉，只保留腹直肌蒂作为血液供应的来源。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6) &nbsp; &nbsp; &nbsp; &nbsp;此外还有无蒂胸肌翻转术、胸骨翻思索加重叠术等等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(二) &nbsp;微创手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微创Nuss手术治疗漏斗胸是在胸腔镜导引下手术植入量身塑造的金属板(Pectus Bar)，将胸骨凹陷往外推出来做矫正手术所有向内凹变形的肋软骨也用金属板向外推出，既不需切断肋骨，也不需切开胸大肌。此一金属板，约留置体内2至5年后再移除。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微创Nuss手术具有切口小而隐蔽、手术时间短、出血少、活动早、不需游离胸壁肌肉皮瓣，不需肋软骨或胸骨的切除、长期保持胸部伸展性，扩张性、柔韧性和弹性等优点，被称为胸壁矫形手术的革命。由于Nuss手术是一种微创、易于掌握的外科技术，与传统的漏斗胸手术治疗方法(如胸骨翻转术)开胸相比，在保证了手术效果的前提下，最大可能减少了手术创伤、术后疼痛减轻、术后恢复时间缩短、手术费用低、出血少，不用输血等，从而快速地被全世界小儿外科和胸外科医生所接受。手术适应年龄范围在3岁到50岁之间的漏斗胸患者，以及采用传统手术治疗失败患者。</span>
</p>
