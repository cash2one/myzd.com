<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>张玉琪：“筑巢”清华 开建完整神外学科体系&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;新华网北京11月4日电（晏珊）目前医院“患多医少”已成常态，而小儿神经外科，医生紧缺的程度更是让人“堪忧”。“我国有15000名神经外科医生，但专门从事小儿神经外科领域研究的医生却仅有百余人，这意味着大量的儿童得不到有效的治疗。比如脑肿瘤，每年患儿达5万余人，即使100名神经外科医生全力以赴，也显得力不从心。”近日，我国著名神经外科专家、清华大学玉泉医院副院长张玉琪在做客新华网“十三五”院长论坛时表示，我国小儿神经外科人才缺口巨大，加强小儿神经外科建设迫在眉睫，并透露将力推完整神外学科体系建设。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<img src="http://static.mingyizhudao.com/147003964108519" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">图为清华大学第二附属医院（玉泉医院）副院长张玉琪。新华网 杨锘摄</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 构建以神外为特色综合医院</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 解决“燃眉之急”，先从人才培养和学科建设入手。“我院是脑神经科的博士生、硕士生培养单位以及国家人事部博士后流动站，在脑神经外科人才队伍建设方面形成了健全的学科培养体系，培养了大量神经外科人才。另外，我院拥有较强的医疗服务能力，比如神经外科设有床位160张，仅次于天坛医院。”张玉琪说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据悉，近年来，清华大学玉泉医院先后开展显微血管减压术治疗面肌痉挛和三叉神经痛，分流术和脑室镜治疗小儿脑积水，难治性癫痫病的评估和综合治疗等等，每年完成神经外科手术近3000台，患者来源覆盖了全国31个省、市、自治区的病人，另有来自中亚、蒙古国等海外患者慕名前来就医。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 作为清华大学的附属医院，在学科建设上清华大学为医院的发展提供了必不可少的支持。张玉琪举例：“比如医学影像学，清华大学医学影像研究中心具有国际先进的颅脑医学影像技术，开发了很多颅脑影像软件，同时脑电图功能研究可以将微电极插入脑内部研究颅脑功能，这些都给治疗小儿颅咽管瘤、脑室内肿瘤带来了新的‘武器’。”此外，据张玉琪介绍，人才方面清华大学同样给予了强大的支持，比如世界级神经科学专家施一功教授、鲁白教授都会经常来院指导授课。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 除了脑神经外科，清华大学玉泉医院还着重打造妇产科及手外科，形成了“大专科、小综合”的综合医院。作为著名神经外科尤其是小儿神经外科专家，张玉琪希望在为病患服务的同时，也能把自己几十年的从医经验传授给学生，这样才能把医疗技术、医疗思想、医疗服务最大化，培养更多的神经外科人才。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “精准”有助降脑外手术创伤至最小</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “精准、微创、个性化”是当今国际医疗发展的主流方向，脑病的治疗也逐步迈进“精准”时代，对于脑细胞而言，精度的提高不仅能提高疗效，更能减少并发症。“颅脑外科手术不仅要追求把病变切除，更要把对脑功能伤害降到最低。比如治疗帕金森病，不需手术，将微电极插入脑部便可使帕金森病得到有效控制”张玉琪补充说：“内科治疗也如此，比如如何用最少的药发挥最大的作用，减少最多副作用也是内科追求精准用药的一大方向。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在小儿神经外科临床治疗领域，张玉琪表示目前无论从技术还是治疗方面也日趋精细。“比如小儿脑肿瘤，很多家长都关心小孩做完手术之后会不会傻？今后的学习和发展是否受到影响？对于这些问题，我们采取脑认知功能评估，即在术前、术后用国际的标准来评测患儿智商，跟踪个体发展，消除家长的疑虑。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 随着“健康中国”国家战略落地，</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">深化医改有望得到全面推进，促进分级诊疗是其中一项重要举措。作为院长，张玉琪认为：“分级诊疗并不是将医院分成一二三等级，而是形成由基层社区医院、社区门诊、县级医院转诊至大城市大医院‘由下而上’的诊疗机制，这符合医学发展规律，一定程度上解决百姓‘看病难、看病贵’的问题。”</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 最后，张玉琪建议，希望从制度方面取消对医院的评审，因为它既不能给基层医院“雪中送炭”，也不能给大医院“锦上添花”，反而让患者一有感冒发烧都直奔三甲医院，造成“看病难”的现象。</span>
</p>
