			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑干胶质瘤的诊断和治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑干肿瘤在儿童期较成人常见，有人估计发病率为成人的9-10倍。Ingraham 及Jackson分别报告313例和273例小儿脑瘤中，脑干肿瘤均占8.8%。肿瘤的性别差异不大，发病的高发年龄为8-9岁，6-11岁的患儿占67.7%。肿瘤部位虽有中脑、桥脑及延髓之分，实际上多呈浸润生长及沿神经纤维束向上下蔓延，以桥延部最为多见。肿瘤的性质几乎皆为胶质瘤，以星形细胞瘤和多形胶质母细胞瘤最多见，部分可为神经节胶质瘤和室管膜瘤。位于脑干上段主要是低度恶性星形细胞瘤而脑干下段则以胶质母细胞瘤居多。本组报道53例儿童脑干肿瘤中星形Ⅰ-Ⅱ级38例（71.7%），多形胶质母细胞瘤14例（26.4%），混合型胶质瘤1例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 脑干肿瘤临床表现：一个或多个颅神经麻痹常为脑干肿瘤的重要特征，首发症状为颅神经麻痹者占24%。最常见的颅神经损害为外展神经，其次为面神经和舌咽、迷走神经，症状可表现为眼球内斜及复视；面瘫；吞咽反呛；上睑下垂、瞳孔扩大光反射消失等。肿瘤同时损害锥体束时会出现特征性的交叉性麻痹（同侧颅神经损害合并对侧肢体偏瘫），锥体束征常为双侧性，颅神经损害则对侧较同侧严重。肿瘤侵犯小脑-齿状核-红核-丘脑束时可导致小脑损害征（64.6%），表现为步态不稳、肢体共济障碍及眼震。国外文献报道颅压增高发生率为15%-23.3%，而本组为53.1% ，与就诊时已发展至疾病晚期有关。少数患儿有智力减退及精神改变（强哭或强笑等）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2 脑干肿瘤辅助检查：①腰椎穿刺：脑脊液蛋白量正常或稍高，细胞数正常。②脑干听觉诱发电位（BAEP）：有人报告7例儿童脑干肿瘤中有6例听觉电位异常。③CT：表现为脑干部位的低或等密度占位，也可为混杂密度，肿瘤多实性少囊变，不均匀强化。由于受后颅窝伪迹影响，肿瘤的显像效果不佳。④MRI :星形细胞瘤多为长T1长T2信号，脑干形态膨大，边界不清，呈不均匀强化，程度与肿瘤的恶性度相关，可伴瘤内出血，偶有囊变。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3脑干肿瘤诊断：学龄儿童若出现眼球内斜（复视），周围性面瘫，言语不清，吞咽反呛，走路不稳应想到此病的可能，若检查有一侧颅神经麻痹和对侧（或双侧）锥体束征者基本可明确脑干占位的诊断，需行进一步神经放射的检查。但是影像学检查并不能完全确诊，因为部分脑干脱髓鞘、感染炎症、血管炎病例可能会误诊为脑干胶质瘤，并采取了不必要的放射治疗，反而加重了临床症状，因此立体定向引导的脑干病灶活检在诊断和治疗方面显示出优势，不但可以明确诊断，而且在诊断同时，采取碘125籽粒植入内放疗技术，可以达到普通外放疗所不能达到的放射剂量，并减少放射性副损伤。海军总医院神经外科采取次方法活检诊断治疗脑干病灶90余例，取得比较满意的治疗效果，至少诊断是明确的。而且手术创伤和风险均低于开颅切除。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4脑干肿瘤治疗：颅内压不高、边界不清的实质性肿瘤首选放射治疗，一般放射总量应达50-55GY，超过此剂量会造成放射性坏死，照射部位通常为后颅凹瘤床局部，也有人认为放疗应包括脊髓。多数患者放疗后出现临床症状的好转，但缓解期通常不超过8月。近年，随着放疗技术的发展，单纯放疗的5年生存率已显著提高，最高已达40%。对于突出于脑干表面或有囊变者可考虑手术治疗，手术的目的为解除脑干的压迫、恢复脑脊液循环的通畅及明确肿瘤性质。应在不增加神经功能损伤的情况下，尽可能显微镜下多切除肿瘤，术后辅以放射治疗。术中进行脑干神经功能监测，利用超声吸引器(CUSA)和激光(Laser)切除肿瘤可减少术后并发症降低死亡率。本组手术探查47例，活检5例，部分切除35例，大部分切除6例，次全切除1例，术后1月死亡率为17%。有人对无法切除的小儿脑干肿瘤进行放疗后多元联合化疗，选用的药物有长春新碱、卡氮介(BCNU)、环己亚硝脲(CCNU)、5-氟尿嘧啶等，但疗效并不肯定。此外可以考虑立体定向内放疗，肿瘤控制效果尚满意。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
