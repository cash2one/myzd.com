<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肾结石小于8mm的肾结石，可以保守治疗，大于8mm肾结石，估计不能自然排出，可以选择体外冲击波碎石。对于碎石无效的病人，结石又小于50px，推荐做输尿管软镜碎石术。手术可以将结石击碎，可以取出部分结石颗粒，但大部分碎石颗粒需要自然排出。排石的过程，大部分病人没有感觉，个别人会出现肾绞痛，一定要到医院复查。</span><br />
