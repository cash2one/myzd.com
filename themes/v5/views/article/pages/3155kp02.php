	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肾上腺疾病：你怎么看</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肾上腺是人体极为重要的内分泌器官,我们每个人生命的维持,体能物质的代谢,无不同肾上腺密切相关连。我们可能经常听说胰腺，甲状腺等内外分泌的腺体，但对肾上腺了解却不多，随着医疗检查技术的发展与提高，肾上腺疾病已经越来越多的走进了普通大众的视线，对于肾上腺，你了解吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、肾上腺的解剖 </span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肾上腺因其位于肾脏的上方，和肾脏共同位于肾周筋膜之内，因此得名。同时因为和肾脏解剖位置极为密切，所以需要外科手术处理的肾上腺疾病归属泌尿外科。其实在国外大部分医院有专门的内分泌外科专业，像甲状腺，肾上腺等这些腺体的疾病由这个专业收治。肾上腺外表呈金黄色，大小５＊３＊0.５CM大小，常常分有内枝和外枝，像一个厚实的分叉的树叶。肾上腺的位置非常的深在和隐蔽，左侧位于肾脏前上方，膈肌下方，腹主动脉前外侧，胰腺后方。右侧位于膈肌下方，肾脏上方前方比邻肝脏，右侧紧靠下腔静脉。肾上腺血液供应非常丰富，主要有膈下动脉，腹主动脉和肾动脉的分支分为肾上腺上，中，下动脉进入肾上腺，而静脉回流则主要是有肾上腺中央静脉一支血管左侧回流至左肾静脉，右侧直接注入下腔静脉。肾上腺的解剖位置和周围结构决定了其在外伤中很少祸及，同时传统的开放手术暴露困难，手术难度大。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、肾上腺的组织及生理 </span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肾上腺从组织学上分为外层的皮质和内部的髓质，肾上腺皮质自外向内在显微镜下可分为球状带，束状带和网状带．分别分泌肾上腺盐皮质激素也就是我们常说的醛固酮激素，束状带分泌皮质醇激素，网状带分泌性激素．内部髓质分泌儿茶酚胺类物质包括肾上腺素，去甲肾上腺素和多巴胺。醛固酮类激素作用于肾脏的肾小管细胞起到保钠和排钾的作用，过多的醛固酮分泌比如在醛固酮瘤时高钠导致高血压，低钾导致全身无力甚至出现四肢麻痹等症状。肾上腺血糖，束状带分泌的皮质醇激素除了有轻度盐皮质激素作用外，主要用于人体内三大物质的代谢，可以促进糖原异生升高，促进蛋白分解和脂肪合成及再分布。产生于束状带的肿瘤由于分泌过多的激素可导致糖尿病，肌肉蛋白分解致四肢肌肉萎缩，脂肪过多堆积在背部和面部引起所谓的满月脸和水牛肩等改变。肾上腺网状带肿瘤少见，过多的性激素往往会引起女性男性化，男性性早熟等改变。肾上腺髓质增生或肿瘤导致儿茶酚胺类物质过多，而该类物质主要是收缩血管升高血压，因此会引起高血压等改变。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、常见的肾上腺疾病 </span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">了解了肾上腺的生理功能，对了解肾上腺疾病颇有帮助。１皮质醇增多症　肾上腺皮质束状带的增生或者是肿瘤导致过多皮质激素分泌引起该病，患者从外形上来看脸如满月，从前面看几乎看不见耳朵，从侧面看几乎看不见鼻子，更是因为血色素的升高患者满面红光，正是因为脂肪的再分部导致肩背部肥胖形成所谓的水牛肩，肌肉的分解导致四肢消瘦，腹部脂肪堆积形成所谓的向心性肥胖，高血压，糖尿病女性患者月经不调等在这类患者中都可能见到。２原发性醛固酮增多症　多见于肾上腺醛固酮腺瘤，患者高血压多见，最典型表现为周期性麻痹，因为血钾降低，患者经常出现四肢麻痹，有时早上一觉醒来，发觉不能动弹，仅仅能动动眼珠子。症状周期性发作。３儿茶酚胺症　主要包括肾上腺髓质增生和嗜铬细胞瘤。过多的升压物质引起严重的高血压，有时是阵发性高血压，有时是持续性高血压，发作时头痛头晕，血压可能升高至２００ｍｍｈｇ以上，重者可引起心功能衰竭，脑出血等。当然肾上腺疾病还有很多，如肾上腺无功能腺瘤，囊肿，转移瘤，髓样脂肪瘤等等，都需要认真甄别。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、肾上腺疾病的诊断和治疗 </span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着影像技术的发展普及，越来越多的肾上腺疾病被发现。对于年轻人高血压患者应该高度警惕肾上腺疾病的可能，对于出现上述典型症状的患者也应该尽早行肾上腺检查。ＣＴ肾上腺扫描可以发现５ＭＭ以上的病变是检查的首选，敏感性大概在９０％以上。血液或者尿液中肾上腺激素方面的检测对疾病的定性诊断相当重要，可以区分不同性质及类型的肿瘤。一旦发现了肾上腺肿瘤，患者往往都是急切要求手术切除，殊不知对于肾上腺肿瘤，充分的术前准备甚至比手术本身更加重要。例如嗜铬细胞瘤，大量的升压物质使全身外周大量毛细血管收缩，体内血管内液体减少，在手术过程中肿瘤受到挤压，升压药物释放入血血压会急剧升高甚至达到３００ｍｍｈｇ以上，而在肿瘤切除以后，全身毛细血管扩张血液滞留在外周，回心脏血流减少，血压会急剧下降到０ｍｍｈｇ。所以术前充分地降压输水扩充血容量对保证手术的成功非常重要，这个过程往往需要２周甚至更长时间。对于皮质醇增多症腺瘤因为过多的皮质醇抑制了脑垂体促肾上腺皮质激素的分泌，对侧正常的肾上腺萎缩了，手术后会出现急性和慢性肾上腺皮质功能不全，急性肾上腺皮质功能不全又称肾上腺危象，患者可出现血压下降休克，昏迷等严重情况，需要补充激素等紧急处理。慢性肾上腺皮质功能不全患者在术后往往全身无力厌食，需要小剂量补充激素，这个过程需要数月到２年左右，因此术后随访至关重要。对于醛固酮瘤，术前用药升高血钾，稳定血压对于手术中间安全也是不可或缺的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">五、微创手术在肾上腺疾病治疗中的价值 </span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，外科手术切除是治疗肾上腺肿瘤唯一有效及可靠的手段。大部分肾上腺肿瘤体积较小，传统的开放手术需要切开腹部或者腰部多层肌肉，创伤大，手术暴露困难。我本人自２００３年２月开展河南省首例腹腔镜肾上腺肿瘤切除手术，至今已经有８００余例。而手术时间也从最初的２个小时降到２０分钟左右。目前腹腔镜已经成为肾上腺手术的最优选择，较传统开放手术比较，仅需要在腰部或者腹部打上３～４个５ｍｍ到１０ｍｍ的小洞，不切断肌肉，创伤小，恢复快，伤口美观，技术经验的积累和进步，使我们现在也能够完成１０ＣＭ大小的大肾上腺肿瘤，相信随着技术设备的进步，经验的积累，微创手术必将能够造福更加广大的患者。</span><br />
