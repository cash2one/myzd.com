<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;脑肿瘤治疗后 间隔多久复查</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;目前，脑肿瘤的治疗方法有观察随访、手术、放疗、伽马刀治疗、化疗、靶向治疗和生物治疗等。其中，手术是治疗颅内肿瘤最常用最有效的方法，良性肿瘤经手术大多可治愈；恶性肿瘤通过手术治疗，可以缓解症状，并为术后放化疗提供病理学甚至分子病理学依据。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;良性肿瘤术后2年内，每3—6个月复查1次，2—5年每6—12个月复查1次，之后可每1—3年复查一次或根据病情变化复查。恶性肿瘤术后2年内，每3个月复查1次，2—5年每6个月复查1次，之后可每年复查一次。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;复查主要以影像学检查为主，推荐选择增强磁共振，最好保证在同一家医院，相同条件下进行，这样有利于前后对比。对于垂体瘤，还需要检查垂体激素，接受化疗的患者还应做血液化验，如血常规、肝肾功能等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;患上脑肿瘤后如何防止复发</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;首先，要完成所有规范化治疗，并按期复查随访；其次，要正确认识脑肿瘤，用健康、阳光的心态面对脑肿瘤，要有战胜脑肿瘤的信心；再次，要有健康的生活方式，如戒烟限酒、合理膳食、适量运动、增强体质等。没有完成规范化治疗，整天心情郁闷、情绪低落、势必削弱人的抵抗力，会给肿瘤复发创造机会。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;脑肿瘤预防分三级</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;脑瘤的一级预防是病因学预防，指对一般人群消除或降低致瘤因素，促进健康，防患于未然的预防措施。如节制饮酒、免疫接种、防止职业病、全社会肿瘤预防的健康教育。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;二级预防是发病学预防。指对特定高风险人群筛检早期肿瘤病例，从而进行早期发现，早期预防和早期治疗，其措施包括筛查和干预实验。如对脑外伤患者需检查头颅CT，对家族中有神经纤维瘤病发生的人群进行头MRI检查，筛检脑膜瘤及神经鞘瘤；对有癌症病史的患者定期复查MRI筛检转移瘤；对艾滋病患者、老人及免疫力低下人群作CT或MRI筛检颅内淋巴瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;三级预防是指对现患肿瘤患者防止复发，减少其并发症，防止致残，提高生存率和康复率，以及减轻由肿瘤引起的疼痛等措施。包括定期随访、及时复查影像、早期功能锻炼及康复治疗、完善治疗手段减少治疗副反应、早期预防并发症发生等。（中国医学科学院神经外科主任 万经海）</span>
</p>
