			<div style="text-align:center;">
<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>认识“慢性硬脑膜下血肿”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">慢性硬脑膜下血肿，顾名思义是一种慢性发生的情况，但是也可能急性发作而发生危险。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">慢性硬脑膜下血肿的发生有以下几个特点：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、容易发生于老年人。老年人往往有脑萎缩的情况，硬脑膜下腔往往显得比较宽。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、外伤史不明显。一般认为，慢性硬脑膜下血肿往往还是由于头部外伤引起的，但这种外伤往往比较轻，或者患者和家属根本就想不起在头几个月内受过外伤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、容易误诊。慢性硬脑膜下血肿的病人的症状往往也不典型，有的病人因为头昏、头痛，以为是“感冒”；有的出现肢体活动不便，以为是脑梗塞。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">慢性硬脑膜下血肿诊断很容易，关键是做个头部CT，一般都能明确诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗首选局麻下钻孔引流术，这种方法创伤较小，绝大多数病人都能够成功解决问题。少部分病人需要全麻开颅清血肿，这种方法创伤相对较大。手术后注意要多躺一段时间，利于残余血肿吸收，也可预防复发。</span><br />
