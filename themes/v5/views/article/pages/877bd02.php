<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">哈哈大笑，肚子给笑破了！</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据武汉晚报报道,武汉一位胖婆婆开怀大笑，结果肚脐鼓鼓地“掉”了下来。肚子笑破了，原来是脐疝惹的祸。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 北京朝阳医院疝和腹壁外科主任陈杰说，脐疝是疝气的一种。疝就是人体组织或器官一部分离开原来部位，通过人体间隙、缺损或薄弱部位进入另一部位。脐疝就是因为小肠连同腹膜等腹腔内容物钻到皮下，由脐部薄弱区突出造成的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 疝本身症状不是特别重，可能有点酸胀。有的会发现腹股沟或阴囊部位出现柔软的突起，仰卧时会变小或消失。当咳嗽、弯腰、拎东西、用力时，肿块增大。由于这些症状并不明显，所以也很少引起人的重视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 引发疝气，就两个危险因素，一是腹压增高，二是腹壁薄弱。而“胖婆婆”三个字，就囊括了这两大危险因素。胖婆婆肚子大，脂肪多，腹腔压力就大，加上年龄大，腹壁比较薄，所能承受的压力就有限。一旦开怀大笑更增大了腹部压力，造成“兜不住”的情况发生，腹腔内容物会跑出来，引发脐疝的发生。除了脐疝，还有腹股沟疝、股疝等，其中腹股沟疝最常见。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一老一小都是疝气的高发人群。陈杰形象地比喻，疝气就像衣服破了一样。孩子得疝气，就像新衣服的线没缝好，哭闹、剧烈运动时就易引发疝气，大多只需缝一缝就可。而老人发生疝气，就像旧衣服穿久难免会破。所以基本上每位老人到一定年龄都可能发生疝气。老人疝气不仅要把裂缝缝上，为了更坚固些，多需打个“补丁”。所以治疗上，手术才是治疗疝气的好办法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 疝通常无法自愈，长期不治病情会恶化，最终只能接受更大的手术，提醒一旦发现疝，尽快去医院就诊。老人预防疝气的发生，尽量减少腹压增大，如腹部减肥、控制好老慢支、便秘、避免跳高、跳远等运动。</span>
</p>
