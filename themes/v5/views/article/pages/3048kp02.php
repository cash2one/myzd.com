			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>房颤个体化治疗的重要性</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">心房颤动，也称心房纤颤，简称房颤，是临床上最常见的心律失常之一。房颤的发病率随着年龄的增加而增长。在之前的一篇文章里面已经大概介绍了房颤的一些临床相关事宜，详见相关文章：《房颤那些事儿》。而本文主要介绍房颤个体化治疗的重要性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经常会有病患及家属问“某某人多少岁了，是否适合进行抗凝治疗或者射频消融治疗？”等等问题。其实这个不是绝对的，年龄不是绝对的，每个人的合并疾病以及身体状态都不一样，因此很多治疗方式的选择还是需要结合具体情况来达到个体化治疗的目的。就比如我们为不少患者进行了射频消融治疗，最小的患者在进行手术的时候是12岁，而最大的患者当时年龄则达到了91岁，期间的跨度非常之巨大。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然房颤是一个疾病的名称，但是，房颤其实并不是一个单独的疾病，而常常是不同基础疾病导致的一个临床表现，而这个表现就是临床心电图上所看到的心房电活动紊乱。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床上的很多疾病状况可以诱发房颤，常见的有甲状腺功能亢进（甲亢）、年龄增大、瓣膜病、高血压、心肌病、左心室肥厚、糖尿病、肺部疾病、冠心病、急性心肌梗死、心功能减退（心衰）、左心房扩大、呼吸睡眠暂停综合征（鼾症）、药物、酒精、肥胖、离子通道疾病等等。从上面列出来的一些病因来看，有些是可以通过调整生活方式来改善的，如酒精、肥胖等，而有些是可以通过治疗原先的疾病而得到治疗的，如甲亢、瓣膜病、急性心梗等，当然，还有一些因素是没有办法改变的，比如年龄增大等。因此，房颤的病因是多种多样的，每个个体的情况是不一样的，房颤的治疗需要针对一些可能的病因进行个体化治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，虽然多种病因最终都导致心脏发生相应的改变，出现心脏纤维化（心脏的老化）后引起房颤；但是不同疾病引起房颤的进展过程是不一样的。也就是说，有些病因需要很漫长的过程才最终导致心脏老化引起房颤，而有些则会比较迅速的引起心脏老化而导致房颤。此外，不同患者的状态不一样，比如是否同时有多种因素并存，而每个因素的存在时间也不同。因此，病因不同，病情差异，病程不等，各个方面都要求对房颤的治疗应该能够个体化，这样才能够更好的符合患者的病情，而将治疗的利益最大化。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，对于不同患者的治疗，需要医师结合患者的具体情况综合判断方能给出对患者最为有益的诊疗方案。这也就是为什么我在回复患者咨询的时候会先询问患者具体情况，同时需要患者进一步完善相关检查并提供相关资料的原因，目的是为了能够使得治疗个体化，让患者获益最大化。</span><br />
<p>
	<br />
</p>
