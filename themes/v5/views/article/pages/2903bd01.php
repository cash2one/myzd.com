			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>单孔腹腔镜胆囊切除术</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">自1987年世界首例腹腔镜胆囊切除开展以来，腹腔镜胆囊切除成为胆囊切除术的金标准，并被广大患者和医生接受。创伤越来越小、疼痛越来越轻、达到快速康复和美容效果是腔镜外科医生追求的目标。随着腹腔镜技术的不断成熟和发展，腹腔镜胆囊切除从最初的四孔发展到现在常规的三孔技术，以至目前国内只有少数医院开展的经脐单孔腹腔镜胆囊切除。我们医院开展此项技术三个月，已有十几例病人成功开展单孔腹腔镜胆囊切除，病人均顺利出院，受到病人的认可和欢迎。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 经脐单孔腹腔镜技术是目前国内临床刚刚开展的一种新型微创手术，是一种“隐匿性”手术，是微创外科向无疤、自然腔道手术方式的演化。单孔腹腔镜胆囊切除术经人体固有的通道--脐孔，紧贴脐孔下缘做50px切口,切口愈合后被脐孔皱襞所掩盖，因而具有令人满意的美容效果，基本能达到体表无瘢痕。单孔腹腔镜手术在一个切口置入三个的穿刺管，通过操作孔道置入手术器械完成手术操作，手术中器械间的夹角由三孔改为一孔后角度变为了零呈平行状态，操作难度大大增加，因此此类手术只能在熟练掌握腹腔镜技术的医生中开展，而且需要专门培训才能开展此类手术。目前开展此项手术的医院不多，但是随着技术的熟练和器械的更新换代，单孔腹腔镜胆囊切除会成为胆囊切除术的优先选择，微创外科无疤手术的时代即将到来。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>单孔腹腔镜胆囊切除有哪些优缺点？</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经脐单孔腹腔镜手术的最大优点首先就是体表无明显首先手术疤痕，具有令人满意的美容效果，这点尤为广大女性患者所青睐；其次可以减轻术后疼痛，缩短康复时间和住院天数。单孔腹腔镜胆囊切除由于依赖可转弯器械、超声刀等高精尖设备，所以费用比一般腹腔镜手术昂贵，另外操作器械之间没有角度手术难度很大，目前暂时难以广泛开展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>&nbsp;哪些病人适合做单孔腹腔镜胆囊切除呢？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于胆囊切除的病人现在首选都是腹腔镜胆囊切除术，但是哪些病人合适做单孔腹腔镜胆囊切除呢？患有胆囊息肉、慢性胆囊炎伴胆囊结石和胆囊良性肿瘤的病人比较适合，但是胆囊充满结石、萎缩性胆囊炎以及胆囊结构不清周围粘连严重的病人不合适单孔腹腔镜手术，甚至有些病人需要传统开腹手术。我们目前在手术开始时会先以腹腔镜经脐孔入内探查，凡是胆囊解剖较为清晰、无明显粘连的患者才这样做，这样既保证手术安全又提高手术成功率。</span><br />
