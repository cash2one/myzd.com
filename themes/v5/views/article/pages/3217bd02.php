			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>机器人做手术，你信不信得过？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一说起机器人，大家自然会联想到高科技、智能……可是如果由手术机器人“操刀”你的手术，你还会那么淡定吗？事实上，广州已有中山大学附属第一医院和中山大学附属肿瘤医院开始在临床上使用手术机器人，其中一家医院目前有三分之二的前列腺癌手术都是由“机器人”操刀，且应用领域越来越广泛。这就是说，不久的将来，越来越多的患者将接受手术机器人做手术。不过很多读者对手术机器人仍有许多疑问和顾虑，就读者担心的问题记者采访了两位有丰富机器人操控经验的医学专家。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q：和医生相比，机器人这种“庞然大物”做手术有那么精细吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> A：手术机器人并不像大家想象中模拟人类外形的机器人，而是有着4个灵活的机械臂，能够模拟医生的手臂手腕操作的手术平台。机械臂最大的优势是可以任意角度旋转，这样在腹腔内非常狭小的空间，能够完成以往在大空间才能完成的精细动作，特别是准确分离及缝合，这是开放性手术和传统腹腔镜手术所不能比拟的。这样在很小的开口，也能把手术做得很漂亮，出血量也非常小。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q：每个人的病灶千差万别，机器人如何知道哪里该切哪里不该切？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> A：其实手术机器人还是由医生操控的。医生坐在操控台前，两手各操控一个控制柄，一边看着显示器一边操控控制柄，他在控制柄上的动作同步传递到手术机器人的机械臂上。动作在传递的过程中，机器人还可以过滤人手的疲劳颤抖，使得手术中的精细操作的稳定性得到保证。机器人的视频处理系统把患者腹腔内情景放大10到13倍用裸眼3D的效果呈现在显示器上。血管和组织纤毫毕现，清晰、放大的视野可以让医生对患处进行细细切割、缝合，犹如雕刻，手术的精细程度自不言而喻。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q：什么样的医生做机器人操控手？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> A：手术要做的漂亮，医生的临床经验是至关重要的，一般是临床手术经验非常丰富的医生经过严格培训，才能操作手术机器人来给患者实施手术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q：手术机器人越来越多，医生会不会下岗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> A：借助手术机器人，医生不用长时间站立在手术台旁，手抖也可以过滤掉，放大的视野让医生不怕眼睛老花，这样外科医生的职业寿命还会更长。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q：机器人手术除了精细，还有别的优点吗？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> A：中山一院从2015年3月份开展第一台机器人手术开始，到目前为止，医院共已开展200多例，接近二分之一是泌尿外科手术，有三分之二的前列腺癌手术都是由机器人“操刀”，可以说手术机器人在泌尿外科疾病治疗中已经相当成熟，90%的泌尿外科疾病可以用手术机器人完成。通过这么多台的手术观察，总结出手术机器人的优点是：科技含量高、手术安全性高、操作精准、手术精细、患者出血量少、并发症少、功能保存与重建好。几年前，一台前列腺癌根治术，输血量从几百毫升到上千毫升，而用手术机器人，病人几乎可不用输血。传统前列腺癌根治手术后，大部分病人会出现尿失禁和性功能障碍等问题，用手术机器人能实现精准操作，对控尿和性功能的保护大大提高。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q：机器人手术费用会不会更高？</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> A：手术机器人由于造价高，每部机器人价值约两千万，那么每台手术要比传统手术的费用高出2万到3万。费用高，且这笔费用需剥离出来目前不能纳入医保，是当前手术机器人的主要缺点。不过从临床观察看，患者由于并发症少、恢复快、住院时间短，且不用输血等因素，总费用不一定会更高。</span>
</p>
