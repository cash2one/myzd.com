			<div style="text-align:center;">
	<strong><span style="font-size:18px;">冠心病的“40”现象</span></strong>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上周，两位40多岁的导演冯小刚和古榕相继猝发心脏病，使这一老年病患者年龄前移的趋势再次成为人们关心的话题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 12日，记者为此采访了北京阜外医院心血管外科教授许建屏，他认为，心血管疾病的40岁现象已经越来越明显。据介绍，心脏病属于“富贵病”，是由于摄入过多的高脂肪、高蛋白和胆固醇造成的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 许教授分析，现在40岁左右的人群的生活特点，使得他们猝发心脏病的高危因素越来越多：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 一是喝酒、抽烟、熬夜，使得肌体调节紊乱;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 二是太注重滋补，吃肉、吃高营养的食物，造成体重偏高、胆固醇偏高和血压偏高;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 三是科学技术和现代化办公设备，使他们越来越缺乏身体锻炼;四是这一年龄段的人大多工作压力大，精神紧张，体力透支，成为诱发心脏病的因素。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 已有医学专家发现，心脑血管疾病的突发和人的疲劳程度、体质有很大关系。更重要的是，中、青年人对这一病症的预防重视不够。许教授说，现在他在门诊接触到的心血管疾病患者的年龄越来越低，“最年轻的一位才28岁”，这些人在发病的时候往往没有任何先兆，可是一发作就是大面积心梗。“而且他们的身上往往都没有备用药物”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对此，许教授建议40多岁的中年人群，应该注意适当休息，锻炼身体。过于肥胖和高血脂、高血压患者，应该及时将体重、血脂、血压降下来，少抽烟、少吃盐，让自己身上猝发心脏病的高危因素减到最低程度。</span>
</p>
