		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>只允许成功 绝不许失败(组图)</strong></span>
</div>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003875168263" title="" alt="demo.jpg"/></span>
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003875419340" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003875854080" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 屈光手术，这个人们口中常说的激光矫正近视眼手术，在眼科手术风险评估中，凭借其高风险难度，在所有眼科手术中，位列榜首，在医学界被称为 “眼科背后的眼科”。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在中央厚度不足1毫米的角膜上，用激光进行精准的切削、雕琢、重塑，矫正视力。四川大学华西医院眼科副主任邓应平教授，作为中国第一代屈光手术医生，从医30年，在这个被他誉为 “能且只能一次成功”的屈光手术台上，不仅交出了成功完成4万余只眼睛的屈光手术，且无一例感染病例的华丽成绩单，还意外收获了“眼球上的工程师”的赞誉。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 人物名片</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 邓应平教授，四川大学华西医院眼科党支部书记兼副主任，近视眼激光治疗中心负责人。中华医学会眼科角膜病学组委员，四川省眼科学会主任委员，四川省器官移植学会委员，成都市眼科学会副主任委员。先后发表论文80余篇，国家及部省级科研成果3项、参加5本眼科专著的编写，2012年荣获 “中国名医百强榜”上榜名医。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不能出一点错</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “屈光手术能且只能一次成功”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 精神的小平头，脸上并没有笑容，透着一股认真与专注。谈到自己从事的屈光手术事业，邓应平稍稍放松了些，“屈光手术就是借助一些高精尖仪器，通过手术矫正近视、远视、散光。”邓应平顿了顿，“说得通俗点，就是一个摘下眼镜的过程。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 角膜，这个指甲盖大小的地方，是邓应平教授的施展空间，角膜的轻薄脆弱，更是让医生在手术过程中如履薄冰，“中间最薄处大概是500微米到550微米，周围厚度大概是1mm，整个角膜的跨度也就在10.5mm到12.5mm之间。”正因如此，对屈光手术医生的要求，也高于一般眼科医生。“不允许你犯错误。一点都不行。手术能且只能一次成功。”邓应平教授坦言，体内脏器的外科手术也许还能承载再一次的手术修复，可眼睛本就敏感脆弱，不可能承受得起第二次手术修复，对屈光手术医生而言，“机会只有一次这句名言，在我们这里体现得淋漓尽致。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 量化标准近乎苛刻</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “不希望我的学生仅仅是匠人”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 学术上，邓应平对学生是出了名的严格，一些量化的标准，甚至可以用苛刻形容。最基本的角膜缝针，邓应平要求，缝针的跨度一般1mm~1.5mm之间，针与针的距离在0.5mm之间，缝合的深度必须达到百分之九十。而在考查显微镜操作熟练程度时，需要在眼睛不离开显微镜的情况下，把手伸到其他地方拿手术器械，拿完之后，手还可以快速准确地回到显微镜下的手术视野。“听起来似乎容易，但是没有个三五年沉淀，是完全做不到的。”邓应平似乎看出了记者眼中的疑惑，认真地解释起来。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在学生眼中，工作时的邓应平似乎有些不苟言笑。如果能够获得邓教授的亲口称赞，绝对会是一件值得庆祝的事情，“连邓老师都说好，说明你真的没问题了。”邓应平教授就意味着标准。听着学生的评价，邓应平忍不住笑出声来。玩笑过后，邓应平很快平静了下来，“会手术，那仅仅是一位匠人。眼科医生有很多，但不是所有的眼科医生都能成为屈光手术医生。屈光手术并不是简单的机械操作，真正的屈光手术医生对于可能出现的感染并发症，必须具备快速反应和处理的能力，“我希望我的学生都能成为真正的医生。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 小助手有大学问</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “橘子对我来说，是‘玩’的对象”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 助手是医生成长的第一步，但现实是，很多并不重视这个过程，觉得倒倒水、缝缝角膜什么的，毫无技术含量可言，“小助手，可是有大学问的。”邓应平狡黠地笑了笑，“我当时可以想尽办法找机会当助手呢。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1984年，毕业不久的邓应平成为了一名助理医生，“医生们的手术虽然结束了，可是显微镜、器械都还在手术室里。”趁着这个空当，邓应平见缝插针地熟悉显微镜操作。“在那个时候，橘子对我来说，不是用来吃的，而是‘玩’的对象。”邓应平所谓的“玩”，就是在显微镜下，用专业的显微手术刀“剥”橘子，将橘子一瓣一瓣地剥开，将里面的瓤一根一根理出来，力度、下刀的位置，都要经过深思熟虑，到最后，就连橘子里的经络、瓤等细小组成，就像一个个拆开的零件，完好无损地分离在手术台上。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 触类旁通之下，邓应平练就了精湛的显微镜技术。从医30年，在这个被称作“眼科背后的眼科”的屈光手术中，成功完成4万余只眼睛的矫正，无一例感染病例，是邓应平交出的华丽成绩单。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 人物特写&gt;&gt;&gt;</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 时间观念特别强 喜欢接受新事物</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在见到邓应平教授之前，从未想过，一个人的时间观念，竟可以做到那样精细。采访时间预约的是在上午9点，可是当8点55分，我们已经抵达华西医院，正准备乘坐电梯时，同事杨丹却赶紧拿出手机，给邓教授发了个短信，“已到。采访9点准时开始。”对于认识邓教授多年的记者，杨丹很清楚邓教授的习惯，“他的时间观念，特别强。”“时间对我来说，真的很重要。”性格细腻、做事细致的特点，也是当初邓应平选择专攻眼科的重要原因，“大外科，比较大刀阔斧，不太适合我。”而邓应平喜欢接受新事物的特别，也让他在学科发展和建设方面一直走在最前沿。就在上周，四川大学华西医院宣布正式开机运行今年4月引进的目前全球最先进的VisuMAX全飞秒激光手术系统，这也是四川省唯一的眼科全飞秒（1秒的千万亿分之一）手术系统，而最早提出引进新设备这个想法的，正是邓应平。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当初读书选择眼科方向，是因为当时眼科还属于新兴学科。如今，作为最早的屈光手术医生，他见证了我们国家4代屈光手术的变更发展，引进了“全飞秒”后，他欣喜地看到屈光手术真正实现手术微创化。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 温馨提示&gt;&gt;&gt;</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 隐形眼镜、美瞳不要天天戴</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 邓教授表示，隐形眼镜与角膜亲密接触，使用时一定要注意卫生习惯，及时进行清洗、消毒，周末时，最好不戴隐形眼镜，让眼睛充分休息。</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 另外，邓应平教授提醒各位爱美女士，美瞳中含有色素成分，透氧性比一般隐形眼镜差，长时间佩戴容易造成角膜缺氧，引发眼部不适，“有需要的条件可以戴一戴，平时尽量少戴。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 使用电脑一小时到户外活动一下</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 电脑屏幕光线调整到合适的亮度，平时可以把电脑屏幕调整到视线以下，俯视屏幕，这时，眼睛的开合度较小，可以减少泪液蒸发，保持眼部湿润，缓解干涩的现象。邓教授表示，使用电脑一小时，最好到户外稍微活动一下，让眼睛休息一下。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不允许你犯错误。一点都不行。手术能且只能一次成功。”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 邓应平教授</span><br />
