			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>感染性心内膜炎二尖瓣成形手术</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">感染性心内膜炎在心脏外科是个常见病， 传统上因感染而造成的二尖瓣功能障碍需要进行二尖瓣置换手术，用人工瓣来替代自身瓣膜。这种方法虽然有效，但也带来了问题：如果患者比较年轻，机械瓣长期抗凝可能对其远期生存质量有负面影响。是否有变通的方法来解决这个问题，改善预后？最近我收治了一例患者，25岁，发热近一月，血培养为草绿色链球菌，二尖瓣前叶靠近内侧交界有赘生物。患者强烈表示不愿意做瓣膜置换手术。因此在术前，我们为其做了经食道心脏彩超，仔细评估二尖瓣受感染的范围和程度，希望可以进行二尖瓣成形修复手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 手术中，我发现二尖瓣的A3区有赘生物，主要累及瓣叶边缘和临近A2区的两根腱索。于是将感染瓣缘剪除，尽量不要去除太多健康组织，病变腱索也剪掉。然后在A3靠近A2处植入2根人工腱索，A3近交界处与对缘缝合。最后植入32号成形环。TEE检查成形效果非常好，术后心脏杂音消失。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; 随着二尖瓣成形修复技术的发展，一些原本无法进行修复的二尖瓣病变（风湿性、感染性）都可以做修复手术了，而且效果很好，当然前提是找到这方面有丰富经验的医生。</span>
