			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>武汉医院兼职政策“内外有别”</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">荆楚在线消息（湖北日报）　记者胡蔓、通讯员曾念军、邱琼报道：3日，北京安贞医院心外科主任黄方炯，正式走马上任武汉市第一医院心外科主任。黄方炯说，自己是到市一医院兼职的。此外，他还从北京带来了麻醉师、护士等5位医务人员。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼下，武汉本地医院正大开院门，吸纳省外名医兼职，以提高自己的技术力量。此前，安贞医院教授马长生出任一医院心内科主任，市二医院挖来美国俄勒冈州医学及科技大学心外科教授何国伟。此外，在亚洲心脏病医院，也有多名重量级名医兼职。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是，武汉许多医院对院内职工兼职却没开口子。医生兼职有政策支持。2000年，中组部、人事部、卫生部联合下发《关于卫生事业单位人事制度改革的实施意见》，指出：“医疗机构可根据工作需要采取专职与兼职相结合的方式，聘用部分兼职技术骨干。”对此，卫生部的解释是，兼职需经医生所在医院同意，它不同于“走穴”。“走穴”是未经单位允许而行医的个人行为，应该予以制止。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不容回避的事实是，武汉许多医生都在私下“走穴”，他们为何难以光明正大地踏出院门？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">协和医院人事处夏家红说，医院有苦衷。现在，医生在外院行医获得的报酬普遍高于本院。一旦兼职，很可能将主要精力放在院外，影响本职工作。同时，允许兼职或许会成为医生“走穴”的借口，一旦发生医疗事故，究竟谁来负责？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">市十一医院院长张必伦担心，让本院医生给外医院帮忙，可能是“搬石头砸自己的脚”。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">市卫生局有关人士认为，兼职相关细则尚未制定，医院操作起来有困难。</span>
</p>
