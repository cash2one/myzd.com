		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>&nbsp;走路总摔跤原是颅底凹陷作怪</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3072/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">孩子走路总摔跤，父母应留意。（CFP供图）</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">●颅底凹陷症是一种常见的枕颈部畸形，约占枕颈部畸形的90%</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">●患者早期可无明显症状，但有外伤等诱因时可发生瘫痪甚至猝死</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">●孩子脖子比正常孩子短、发际低，同时出现颈部疼痛、颈部活动受限等时应提高警惕</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">10岁女孩熙熙上体育课时不小心摔了一跤，虽然只是擦破了一点皮，但随后几个月却不仅脖子疼痛，而且两只手臂都没有力气，甚至走路时还经常摔跤。辗转多家医院后，熙熙最终被诊断为“颅底凹陷症”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">医生提醒父母们，当孩子出现颈部疼痛、活动受限，或四肢无力、走路不稳等症状时，一定要及时带孩子就医。虽然颅底凹陷症患者在早期可能不会表现出明显症状，但一些不可预知的外力，如跌倒、扭伤脖子等，却可能成为疾病发作的诱因，严重时甚至可造成瘫痪、猝死。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">文/记者饶贞</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">通讯员李冰洁、银春林</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">医学指导/广州总医院骨科医院脊柱一科副主任医师艾福志</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">案例：10岁女孩走路总摔跤</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据熙熙的家人介绍，熙熙出生3个月左右家人就发现她的头部无法旋转，只能轻度地前屈后伸。不过，家人对此没有太在意，也没有带熙熙进行任何治疗。两年前，上小学的熙熙开始觉得脖子疼，身体乏力，有时起床还会觉得头晕，而且症状逐渐加重。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">大概半年前，在一次上体育课时，熙熙不小心摔了一跤，前额被轻微地擦伤，老师和家长起初都觉得问题不大。但随后几天，熙熙几乎每天都会在学校不小心摔跤。到后来，熙熙走路越来越不稳，逐渐发展到需要人搀扶才能行走，最后甚至无法自己拿碗筷吃饭。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在辗转了香港和内地的多家医院后，熙熙近日在广州军区广州总医院骨科医院被确诊为“重度颅底凹陷症”，并伴有扁平颅底、寰枢椎脱位等枕颈部畸形。经过一场近3小时的高难度手术，熙熙终于逐渐康复，几天后开始能独立行走，也能自己吃饭了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原因：家族遗传或先天因素造成</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“颅底凹陷症是一种常见的枕颈部畸形，约占枕颈部畸形的90%。”广州军区广州总医院骨科医院脊柱一科副主任医师艾福志介绍，由于颅底凹陷可使患者的脑干、脊髓受到压迫，随着压迫脊髓的程度越来越重，患者不仅可出现四肢无力、走路不稳等情况，如果有外伤等诱因还可发生瘫痪甚至猝死。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“颅底凹陷症既可能是家族遗传，也可能是其他先天因素造成的，比如母亲在孕期吃药或接受放射线照射等都可能有一定的影响。” 艾福志表示，一些较严重的患者可能在很小时就发病，还有一些患者则随着年龄的增大才逐渐出现症状。以该科为例，每年从全国各地前来接受手术的近200位颅底凹陷症、寰枢椎脱位等颅颈交界伤病患者中，既有四五岁的儿童，也有六七十岁的长者。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">症状：四肢无力、步态不稳</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">艾福志介绍，除颅底凹陷外，扁平颅底、枕骨髁发育不良、寰枕融合、寰枢椎前后弓发育不良等都属于常见的枕颈部畸形，“这类患者往往有多种畸形合并发生，而且往往合并有寰枢椎脱位。当枕骨基底凹入颅腔，齿突尖突入枕骨大孔时，可压迫脑干、脊髓或椎动脉，使脑脊液循环受到影响，并引起相应的症状。”艾福志举例说，枕颈部疼痛、颈椎活动受限、一侧或双侧上肢麻木、酸痛、无力等都是颅底凹陷症的常见症状；此外，有些患者还可能出现舌咽部感觉障碍、吞咽障碍等表现。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“如果小脑受到影响，患者还会出现步态不稳、共济失调、眼震等表现；如果延髓及上颈段脊髓受压，患者还会出现轻重不一的四肢瘫痪、无力，手指精细动作障碍等表现；一旦椎动脉供血不足，则会出现眩晕、视力障碍、恶心呕吐等表现。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">发现：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脖子短、后脑勺发际低</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">要警惕</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">艾福志提醒说，从外观上来看，颅底凹陷症患者往往有脖子短粗、后脑勺发际低、枕颈部扁平等特点，“约半数患者同时伴有斜颈，也可能有面部、颅骨不对称等特征。”他表示，由于脊髓有一定的代偿空间，患者在早期可能不会表现出明显症状，但一些外力的作用，如跌倒、扭伤脖子等，可能成为疾病发作的诱因。“正常孩子摔一下可能没事，但患有颅底凹陷症的孩子，摔跤后轻则可出现手脚无力，重则可造成瘫痪。”他表示，延髓是呼吸和心血管的中枢，一旦受到压迫，患者甚至可能当场停止呼吸。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“如果发现孩子出现持物无力、走路不稳、颈部活动受限等症状，家长一定要及时带孩子到医院就诊。”艾福志表示，颅底凹陷症一般可通过X光片、三维CT重建及颈椎磁共振检查进行确诊。他同时提醒说，颅颈交界部的手术是一种高风险手术，患者一定要到正规医院的脊柱外科专科接受治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">提醒：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">孩子摔伤后应特别留意颈部</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">虽然摔跤是每个孩子在成长过程中都避免不了的，但孩子摔跤后家长的应对和处理其实也有讲究。艾福志提醒说，如果发现孩子经常摔伤，家长应询问孩子究竟是因为被东西绊倒，还是因为下肢无力才摔伤的。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">此外，发现孩子摔跤后，家长还应特别留意孩子的颈部是否出现活动时疼痛和活动障碍的情况，“如果现场有条件，可以给孩子戴颈围保护制动；如果没有条件，则应避免屈曲孩子的颈部，最好维持同样的颈部直立或轻度后伸姿势送往医院就诊，以免加重脊髓压迫。”</span>
</p>
