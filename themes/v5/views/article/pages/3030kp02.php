			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>这些症状提示你得了垂体瘤</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑垂体瘤为良性脑肿瘤，比较常见，如何及早发现及早治疗呢？我详细个大家介绍一下。　脑垂体为重要的内分泌器官，内含数种内分泌细胞，分泌多种内分泌素，如果某一内分泌细胞生长腺瘤，则可发生特殊的临床表现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3030/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 　1．不同种类垂体腺瘤的内分泌表现</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; (1)生长激素细胞腺瘤:早期瘤仅数毫米大小，主要表现为分泌生长激素过多.&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> (2)催乳素细胞腺瘤:主要表现为闭经、溢乳、不育，重者腋毛脱落、皮肤苍白细腻、皮下脂肪增多，还有乏力、易倦、嗜睡、头痛、性功能减退等.男性则表现为性欲减退、阳萎、乳腺增生、胡须稀少、重者生殖器官萎缩、精子数目减少、不育等，男性女性变者不多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;(3)促肾上腺皮质激素细胞腺瘤:临床表现为身体向心性肥胖、满月脸、水牛背、多血质、腹部大腿部皮肤有紫纹、毳毛增多等.重者闭经、性欲减退、全身乏力，甚至卧床不起.有的病人并有高血压、糖尿病等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; (4)甲状腺刺激素细胞瘤:少见，由于垂体甲状腺刺激素分泌过盛，引起甲亢症状，在垂体瘤摘除后甲亢症状即消失.另有甲状腺机能低下反馈引起垂体腺发生局灶增生，渐渐发展成垂体腺瘤，长大后也可引起蝶鞍扩大、附近组织受压迫的症状。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;(5)滤泡刺激素细胞腺瘤:非常少见，只有个别报告临床有性功能减退、闭经、不育、精子数目减少等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; (6)黑色素刺激素细胞腺瘤:非常少见，只有个别报告病人皮肤黑色沉着，不伴皮质醇增多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; (7)内分泌功能不活跃腺瘤:早期病人无特殊感觉肿瘤长大，可压迫垂体致垂体功能不足的临床表现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> <img src="<?php echo $baseUrlImage; ?>article/doctor/3030/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2．视力视野障碍:早期垂体腺瘤常无视力视野障碍.如肿瘤长大，向上伸展，压迫视交叉，则出现视野缺损，外上象限首先受影响，红视野最先表现出来.以后病变增大，压迫较重，则白视野也受影响，渐渐缺损可扩大至双颞侧偏盲.如果未及时治疗，视野缺损可再扩大，并且视力也有减退，以致全盲.因为垂体瘤多为良性，初期病变可持续相当时间，待病情严重时，视力视野障碍可突然加剧，如果肿瘤偏于一侧，可致单眼偏盲或失明。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3．其他神经症状和体征:如果垂体瘤向后上生长压迫垂体柄或下丘脑，可致多饮多尿;如果肿瘤向侧方生长侵犯海绵窦壁，则出现动眼神经或外展神经麻痹;如果肿瘤穿过鞍隔再向上生长致额叶腹侧部，有时出现精神症状;如果肿瘤向后上生长阻塞第3脑室前部和室间孔，则出现头痛呕吐等颅内压增高症状;如果肿瘤向后生长，可压迫脑干致昏迷、瘫痪或去大脑强直等。</span><br />
