			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>尿失禁：咳嗽、大笑，尿漏的尴尬</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">每年6月的最后一个星期，有一个值得女性朋友关注的日子，就是“世界尿失禁周”。郑大一附院泌尿外科张雪培教授特别提醒：尿失禁发病率较高，由于医学知识缺乏及传统观念的影响，仅有10.92%的女性患者愿意到医院就诊。这不仅影响患者的健康，更严重影响她们的生活。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">咳嗽、大笑，尿液漏出</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杜女士在所有人眼中都是很完美的，长相漂亮、事业成功，可就在一年前，她莫名其妙地出现尿失禁的情况，让她的生活备受困扰。当她咳嗽、大笑、打喷嚏，或者提重物时，尿液会不由自主地溢出，运动时更加厉害。后来情况越来越严重，让她不敢应酬、不敢运动，甚至连上班时都提心吊胆的，怕尿液不知不觉又溢出……无论是工作还是生活都受到很大影响。而她一直也以为自己得了怪病，不愿去医院救治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前不久，在家人的劝导下，杜女士到郑大一附院就诊，被确诊为压力性尿失禁，需手术治疗。张雪培教授通过微创手术，很快就让杜女士远离了尿失禁的困扰，重新回归正常生活。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尿失禁为何被称为“社交癌”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据张雪培教授介绍，杜女士的情况并不是个案。据统计，全世界40岁以上的女性中，每四个人中就会有一人出现不同程度的尿失禁。尿失禁除了令人身体不适，容易引发泌尿系统感染之外，更会长期影响患者的生活质量，严重影响患者的心理健康，许多患者因此承受着巨大的心理压力，精神高度紧张，不愿参加社交活动，产生焦虑、尴尬和沮丧等不良情绪，因此国际上又把尿失禁称为“社交癌”。“社交癌”虽不致命，但长期无法控制的漏尿、滴尿带来的生活不便、心理挫折以及家庭负担等问题，却不亚于癌症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微创手术，治好尿失禁</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于许多患者对尿失禁存在认识误区，她们或认为“漏尿”不是大病没必要治，或害羞不愿说出口，有些人仅仅将其视为身体老化的生理表现，并不知道这也是一种病。有调查显示，由于医学知识缺乏及传统观念的影响，女性患者就医倾向非常低，仅有10.92%的女性愿到医院就诊。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果是尿失禁症状较轻的患者，可以通过增强盆底肌肉和尿道肌肉的张力，提高肌肉对压力作用的反应性收缩力，从而改善尿道括约肌功能。患者坚持1～2个月后开始有效果，坚持一年以上则可明显改善症状。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中、重度患者以及保守治疗效果不佳的患者，则建议像杜女士那样采取微创手术治疗：在女性尿道下方植入一个小吊带，即可达到治疗目的。</span><br />
