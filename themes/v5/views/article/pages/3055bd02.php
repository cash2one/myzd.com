<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中国小儿神经外科的发展现状</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">核心提示：根据2012年底的统计数据，我们全国从事神经外科医师约有1.3万名，由于儿童神经外科手术技术要求高的特点，因此造成国内优秀的小儿神经外科医生匮乏，能做儿童脑肿瘤手术的医师则少而又少。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 张玉琪，清华大学玉泉医院副院长，教授，博士生导师。曾任天坛医院小儿神经外科主任，擅长小儿脑积水、小儿神经系统肿瘤(颅咽管瘤、鞍区肿瘤、三室内肿瘤、丘脑肿瘤、颅底肿瘤、脑干肿瘤、脑血管病等)的外科治疗，特别在儿童颅咽管瘤的研究和治疗方面享有国际盛誉。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 根据2012年底的统计数据，我们全国从事神经外科医师约有1.3万名，由于儿童神经外科手术技术要求高的特点，因此造成国内优秀的小儿神经外科医生匮乏，能做儿童脑肿瘤手术的医师则少而又少。结合目前儿童脑肿瘤的发病率，可以说：目前我国的相当多的儿童脑肿瘤没有得到应有的正确诊断或治疗而死亡。因此，这是每一名神经外科医师、各级政府部门和全社会必须面对的重大问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 尽管在50多年前，北京市神经外科研究所就成立了国内第一个独立病房的小儿神经外科，但目前在全国综合性医院中，也只有少数医院有独立病房的小儿神经外科和专职的小儿神经外科医师(如：清华大学玉泉医院、北京天坛医院，新疆医科大学附属第一医院，广州医学院附属第二医院，福建医科大学附属第一医院等等)。其他绝大多数医院没有独立的小儿神经外科，只有个别神经外科医师兼职做儿童患儿的神经外科手术。在全国儿童医院系统内，有独立小儿神经外科或床位超过30张的医院(如：南京儿童医院，重庆医科大学附属儿童医院，上海儿童医学中心)也极少。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 诚然，不仅是小儿神经外科，整个从事儿童临床工作的医务人员数量均在近十年下滑严重。虽然这种现状的形成有其复杂的成因，但作为从事儿童神经外科工作近30年的我个人来讲，儿童才是我们整个社会的明天和希望，关注他们的健康是我们这代医生义不容辞的责任，也是社会赋予我们这些从事小儿神经外科工作者的历史责任。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如何更好的提高我们小儿神经外科的发展?个人认为下述三个方面对小儿神经外科的发展可能有所帮助：第一，高年神经外科医师应主动关注小儿病人，在条件较好的医院(病人来源、硬件条件和技术水平)要设立小儿神经外科。第二，在全社会普及小儿神经外科的科普教育，使每一个人都认识到儿童颅脑疾病的重要性，特别是媒体的宣传作用。第三，政府和社会组织的经费投入，支持医学科研和减免治疗费用。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">与儿童白血病治疗相比较，开展小儿神经外科所需要的硬件条件要求不高，儿童神经系统疾病的治疗费用低、早期治疗成功率高、长期疗效好。不高的经济投入，较好的治疗效果，每个单位拨付一些经费可使更多的儿童病人受益。</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近些年社会对小儿白血病投入了较多的关注，取得了很多的成绩，但作为位列小儿神经外科发病率第二位的小儿脑肿瘤，却迟迟未得到全体社会的重视，所以在这里我大声呼吁我们的医务工作者、媒体工作者、其他社会各界人士对儿童的颅脑肿瘤倾注更多的热情和关注。</span>
</p>
