<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 据报道，目前全国帕金森病患者约有200万，已占到全球患者总人数的一半，北京地区的患者已达3万。随着城市老龄化的到来，中国65岁以上老年人口中，大约有1.7%的人患有此病，防治形势极为严峻。上海交通大学医学院附属瑞金医院功能神经外科主任孙伯民做客新华访谈，为您专业解读秋冬季节帕金森患者的疾病管理。图为嘉宾与主持人交流。</span>
