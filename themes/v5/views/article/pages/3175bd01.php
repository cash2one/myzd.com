<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在介绍我国帕金森病发病现状时，孙伯民说，我们国家在很多年前做了流行病学的调查，认为发病率跟国际上是接近的，并不是发达国家就少，发展中国家就少，老年55岁以上患病率是3%到5%。以前是50、55岁以上患病率就成倍增加了，但是从这些年的现象来看发病越来越早，以前如果30岁发病我们会考虑是其他的病，但是通过这些年观察，发现30多岁的这些人也有可能是帕金森病，发病率趋向年轻化。</span>
