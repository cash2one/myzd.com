<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">新华网北京9月29日电 上海交通大学医学院附属瑞金医院功能神经外科主任，世界功能神经外科学会常务委员孙伯民29日应邀做客新华网，就许多老年人关心的帕金森患者秋冬季节疾病管理问题，与网友进行在线交流。&nbsp;</span><br />
