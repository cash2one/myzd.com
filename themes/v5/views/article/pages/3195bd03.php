		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>我国首例利用数字技术修复颅骨手术获得成功（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">我国首例采用数字技术修复颅骨手术日前在天坛医院获得成功。这项技术是由北京工业大学与天坛医院合作，采用吉林大学无模成形技术共同研制完成的。这标志着我国颅骨修复术已经走出了手工制作的阶段，数字化技术已成功地应用于医学工程领域。&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 目前国外颅骨损伤修补术，一是采用铸造，另一采用传统模具，在术前制作出与患者缺损部位相吻合的修复体，但制备时间长、费用昂贵。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 北京工业大学与天坛医院合作采用数字制造技术制备颅骨修复体。他们首先完成颅骨CT数据的三维重建，然后进行修复体的数字设计、模拟装配、优化工艺参数，最后将设计好的修复体数据直接传输到数控无模多点成形设备上制造出修复体。这项技术因不需要铸造，也不必制造模具，制作效率很高，从获得CT数据开始到制作出颅骨修复体只需两小时左右。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 据天坛医院该项目负责人赵继宗教授和赵元立博士介绍，此项技术的成功应用解决了我国目前颅骨修复技术水平偏低的现状，大大缩短手术时间，减少手术风险，减轻病人的痛苦，同时也为颅骨快速修复提供了坚实的技术基础。&nbsp;</span>
