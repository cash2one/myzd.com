		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小家坤上午在儿科医院“收心”</strong></span>
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003645103993" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147003645200722" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">11:50 上午首家报道</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 地点：复旦大学附属儿科医院第二手术室。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">时间：上午8时</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">病例：山东枣庄的7岁男孩小家坤胸口“长”有一个梨状的凸出物，10来厘米直径，深褐色，有搏动，咳嗽或屏气时会鼓胀得更厉害。诊断证实，这个“肿块”是由薄薄一层皮肤包裹着的心脏以及部分肠子。原来，小家坤的胸部、腹部先天性畸形，胸骨自第三肋骨起往下大段缺失，整个胸骨“裂”成了一个倒Y字形，加上分隔胸腔、腹腔的膈肌以及腹壁肌层都有缺损，他的心脏和一部分突入胸腔的横结肠便仅隔一层皮肤而凸现在胸前。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">手术背景：低于百万分之五的发病率，全球迄今不足百例的相关病例。反反复复的呼吸道感染、肺炎和食物后呕吐，令孩子的生长发育情况明显落后。目不识丁的父母向当地政府求助。一对在上海工作的台湾夫妇试着与儿科医院联系，并决定捐助8万元手术费用。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术方案：从两侧截断患儿部分肋骨，用“自身肋骨移位”的方法重建一个合拢的胸廓，将原本裸露在外的心脏覆盖保护起来。国内尚无为这种Cantrell五联症患者进行一期纠治手术的成功先例。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 手术现场：由胸外科贾兵教授、普外科郑珊教授领衔，儿科医院此番决定修补胸骨、腹壁、膈肌、心包，将7年来一直悬挂于患儿胸前的心脏和肠子“回纳”到它们原来的位置。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “在患儿自己的肋骨上做足文章！”儿科医院专家们的大胆设想有些类似于“大桥合龙”——自下而上各分离出3对肋骨并在左右两侧截断，再由外而内向正中方向作“平移”，横跨移位后的肋骨最终用钢丝固定缝合于胸前。心脏将被稳妥地“藏”于肋骨之后。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 5台电视摄影机被允许轮流“进场”拍摄，更多记者则在一楼会议室收看5楼手术室的实时转播。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 8时50分完成全麻，在患儿身上划开一道10多厘米长的胸腹联合切口。随后，切除凸出胸前的深褐色“囊袋”，分离周围组织进行心包、膈肌修补。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 9时45分起，贾兵教授开始从胸骨肌损处向两侧大范围地游离皮下组织、肌肉，暴露出第4至第10肋骨及肋软骨。在估算了移动尺寸、覆盖面积之后，他果断地在其中3对肋骨与肋软骨的交界处作了截断。扁平状的肋骨被一一向中间移位3至5厘米距离，在胸前正中位置由直径1毫米左右的钢丝作交错固定。然后是皮肤及皮下组织整形修剪缝合。完整的前胸“屏障”建成了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">11时50分，郑珊教授“接台”实施腹壁重建，最后再新“做”一个美观的人造肚脐眼。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">小家坤的心脏与结肠终于归回原处，各得其“所”。本报记者 施捷</span>
</p>
