			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>眩晕医学 — 治疗篇之短暂性脑缺血发作</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 、什么是短暂性脑缺血发作？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 短暂性脑缺血发作（transient ischemic attack, TIA）是指伴有局灶症状的短暂的脑血液循环障碍，以反复发作的短暂性失语、瘫痪或感觉障碍为特点，症状和体征在24小时内消失。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一过性脑缺血发作，是指颈内动脉或椎-基底动脉缺血导致的相应区域一过性局灶性脑或视网膜功能障碍，每次发作持续数分钟，通常在30min内完全恢复，但常有反复发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、哪些病可以引起TIA发作？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 本病多与高血压动脉硬化有关，其发病可能有多种因素引起。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）微血栓：颈内动脉和椎一基底动脉系统动脉硬化狭窄处的附壁血栓、硬化斑块及其中的血液分解物、血小板聚集物等游离脱落后，阻塞了脑部动脉，当栓子碎裂或向远端移动时，缺血症状消失。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）脑血管痉挛：颈内动脉或椎一基底动脉系统动脉硬化斑块使血管腔狭窄，该处产生血流旋涡流，当涡流加速时，刺激血管壁导致血管痉挛，出现短暂性脑缺血发作，旋涡减速时，症状消失。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> （3）脑血液动力学改变：颈动脉和椎一基底动脉系统闭塞或狭窄时，如病人突然发生一过性血压过低，由于脑血流量减少，而导致本病发作；血压回升后，症状消失。本病多见于血压波动时易出现本病发作。此外，心律不齐、房室传导阻滞、心肌损害亦可使脑局部血流量突然减少而发病。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）颈部动脉扭曲、过长、打结或椎动脉受颈椎骨增生骨刺压迫，当转头时即可引起本病发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3 、TIA有哪些临床特征？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 60岁以上老年人多见，男多于女。多在体位改变、活动过度、颈部突然转动或屈伸等情况下发病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> （1）颈动脉系统的TIA较椎一基底动脉系统TIA发作较少，但持续时间较久，且易引起完全性卒中。最常见的症状为单瘫、偏瘫、偏身感觉障碍、失语、单眼视力障碍等。亦可出现同向偏盲及昏厥等。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> （2）椎基底动脉系统TIA较颈动脉系统TIA多见，且发作次数也多，但时间较短。主要表现为脑干、小脑、枕叶、颞叶及脊髓近端缺血。神经缺损症状，常见为眩晕、眼震、站立或行走不稳、视物模糊或变形、视野缺损、复视、恶心或呕吐、听力下降、球麻痹、交叉性瘫痪，轻偏瘫和双侧轻度瘫痪等。少数可有意识障碍或猝倒发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、 TIA的预后如何？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本病常系脑血栓形成的先兆，颈动脉TIA发病1个月内约有半数、5年内约有25～40%患者发生完全性卒中；约1/3发作自然消失或继续发作。高龄体弱、高血压、糖尿病、心脏病等均影响预后，主要死亡原因系完全性脑卒中和心肌梗塞。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、 TIA的治疗方案？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本病可自行缓解，治疗着重于预防复发。应调整血压，改善心功能，保持有效血液循环，纠正血液流变异常，避免颈部过度屈伸活动，并长期口服抑制因小板聚集剂，如阿斯匹林0.05～0.1克，1～2次/天。</span><br />
