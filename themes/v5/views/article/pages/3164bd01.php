			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小胶囊解决大问题</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 今年4月在浦东举行的上交会上，美国纯电动汽车特斯拉和中国安翰光电胶囊机器人这“一外一中”两个参展项目受到最多关注。记者日前从上海金桥经济技术开发区管委会获悉，继特斯拉落户金桥后，引进胶囊机器人的工作也获得突破性进展，有望成为金桥转型发展的又一代表性项目。 </span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">
<div>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</div>
</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">跨越多项技术领域&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">记者了解到，武汉安翰光电技术有限公司自主研发的NaviCam遥控胶囊内镜机器人系统，是一种可精确定位、可主动控制的第二代胶囊内镜，主要应用于消化道胃部和小肠系统疾病的筛查，具有检查方便、无创伤、无痛苦、无死角、无交叉感染等特性，因此被视作是世界上最先进的同类技术。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“多年临床经验告诉我们，早期胃癌在术后5年的存活率可达95%以上，而中晚期的术后存活率仅有20%。因此，定期胃镜检查对于发现胃癌来说至关重要。但传统方法不仅存在交叉感染的风险，检查过程中所产生的疼痛感也让许多人打了‘退堂鼓’。”上交会期间，安翰光电董事长吉朋松在接受记者采访时表示，遥控胶囊内镜机器人是与传统胃镜进行技术互补的产品。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据他介绍，与传统胃镜不同，患者只要用水服下一颗直径不到10毫米、长度不到20毫米的胶囊，再穿上一件连接记录仪的马甲，就可以施行无痛检查。当胶囊机器人通过吞服进入人体后，由于其体积很小，进入体内毫无异物感和不适感，能够消除患者的紧张情绪，极大提高受检者对检查的耐受性。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">值得一提的是，尽管胶囊并不大，但内含了300多个零件，整套系统跨越电磁控制、传感、光电成像、图像处理、无线通信等技术领域。吉朋松告诉记者，在集成了各种各样的传感器后，系统就能采用独创的磁场精确控制技术，把胶囊内镜变成“有眼有脚”的机器人，医生则能把隐藏在胃和小肠里的病灶看得清清楚楚。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床应用获得好评&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，自2013年1月获得国家药监局颁发的医疗器械注册证以来，安翰光电遥控胶囊内镜机器人已先后在北京、上海、广州、武汉等地十多家医院临床应用。其中不乏北京协和医院、北京军区总医院、上海长海医院、上海瑞金医院、上海中山医院、上海仁济医院等，并得到使用单位认可。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海交通大学医学院附属仁济医院消化内镜中心主任戈之铮教授认为，消化内镜是目前消化道恶性肿瘤诊断与治疗中必不可少的工具，也是早期消化道恶性肿瘤检出的关键。然而，国内早期消化道恶性肿瘤，如早期胃癌的检出率较低，与国外差距明显。因此如何进一步发展内镜新技术，提高早期消化道恶性肿瘤的检出率就显得十分迫切和重要。安翰光电遥控内镜机器人的出现，对消化道尤其是胃部健康筛查意义重大。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第二军医大学长海医院消化内科主任李兆申教授则指出，安翰光电遥控胶囊内镜机器人在多中心的临床试验中，展现出与国外的胶囊内镜相似的诊断力，并且工作时间更长，能有效地发现病灶，可作为胃病筛查手段，替代部分胃镜检查。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，正是广阔的市场前景，推动安翰光电有意向安装新的生产线，扩大产能，并建立展示体验中心，让更多普通百姓了解胶囊机器人。吉朋松曾在上交会期间表示，正在和全国各地多个开发区商谈合作事宜，浦东金桥也在接洽范围内。而根据记者日前从金桥开发区管委会了解到的信息，双方有望在近期正式牵手。</span>
</p>
