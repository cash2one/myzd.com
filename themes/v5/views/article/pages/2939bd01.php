			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>应《健康报》之约谈“肺部结节临床诊治中的困境和出路”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2004年7月15日《健康报》“寻医问药”栏目曾刊登了笔者的采访文章《不容轻视的肺内结节》，几年来这篇文章受到了很多刊物和网站的转载或转引，增加了群众对这一疾病的认识。当年的文章主要面向患者。此次应《健康报》医学论坛版之邀，笔者再次撰文，面向医生，尤其是非本专业的医生，介绍有关肺部结节的现状和问题。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（此文经报社编辑后业已刊登在2012年7月19日《健康报》第8版学科展望栏目。）</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>1. &nbsp; 发现越来越多，诊断困境依然存在；癌的概率较高，医患均需重视。</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着经济的改善，人们健康意识的提高，以及胸部CT的普遍应用，临床遇到的胸部小结节病例越来越多。这些小结节，有些呈密度较高的实性，有些呈淡片状的“磨玻璃影”。以往应用胸部X线片，只有病灶较大时才被发现，较大的病灶往往特征明显，诊断起来相对容易；近年来，胸部CT广为应用，由此检出了很多肺部结节，尤其是一些直径在10mm以下的微小结节或“磨玻璃影”。这些结节其病变性质多种多样，可能是早期肺癌、腺瘤样增生、炎性假瘤或炎性淋巴结增生、结核瘤、硬化性血管瘤或错构瘤等等。微小的病灶其影像学特征常常很不典型，因此确诊十分困难。但文献报道，在非选择人群中，孤立性肺结节的恶性比例约为20%-40%；即使小于10mm的肺部微小结节，其恶性比例也在15%左右。尤其值得重视的是呈“磨玻璃样”的肺部结节，原发肺癌的可能性在90%以上。这提示对于这种检查发现的肺部结节，医生和患者都务必高度重视！一方面病例越来越多，确诊十分困难；另一方面由于不能除外癌，尽早确诊又十分必要，这是当前针对肺部结节的困境之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2. &nbsp; &nbsp; &nbsp;治疗时机常难以拿捏。恰当的诊疗决策目</strong></span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>前仍主要依靠有丰富经验的专业医生。</strong></span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于不好定性，故到底是观察，还是手术？治疗的时机怎么把握？常是另一个困境。目前一贯的普遍做法是，临床医生根据病人的年龄、性别、个人史、家族史、临床症状，以及病变的影像学特点等信息，结合个人经验，做出一个良恶性的初步判断，如果高度怀疑肺癌，则建议尽早手术；如果认为良性可能性大，则建议观察，定期复查胸部CT，假以时日，根据病灶的形态变化决定治疗的措施和时机。然而，个人经验千差万别，由于医生不恰当的建议延误治疗的肺癌尚不在少数。医学和社会的进步使肺内病灶的检出大大提前，这是当今肺癌领域很重要的新进步。然而如果检出后未能及时治疗，这种进步的带来的效果势必大打折扣！因此，对于肺内结节，一般建议“宁左勿右”，亦即强调早期处理，不要使癌症耽误。为了更科学地预测病变性质，国际上有学者根据大宗病例分析结果，提出了能够计算肺内病灶恶性概率的数学模型，虽能在一定程度上弥补经验医学的不足，但目前还不能很好地指导临床。迄今，恰当的诊疗决策仍有赖于有丰富经验的医学专家。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3. &nbsp; &nbsp; &nbsp;胸腔镜手术是当前解决诊治困境的最佳选择，但术中病灶定位有时充满挑战。</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">电视胸腔镜技术用于肺部结节的诊治是指，利用2-3个1cm左右的胸壁小切口局部切除肺内病灶，手术中送快速的冰冻病理，如为良性，则结束手术，对病人来讲相当于立刻解除了心头之患；如为恶性，进一步行胸腔镜下的肺叶切除+淋巴结清扫术，创伤性小且效果好，使肺癌早期诊断的获益真正落到了实处。癌症治疗强调“早诊断，早治疗”。胸腔镜手术为肺癌患者架起了一个从早期诊断通往早期治疗的桥梁，是迄今解决肺部结节诊治难题的最佳选择！</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">然而在肺部结节的胸腔镜手术中有时会遇到一个棘手的问题，如果结节非常微小，位置深在或质地柔软，术中要准确找到会很困难。医生的经验十分重要。北京大学人民医院王俊教授是我国最具权威的肺部小结节专家，他能够通过手指触诊定位出98%以上的肺内结节。但并非所有的胸外科医生都有此“功夫”。近年来，人们发明了各种协助定位的方法，如术前CT引导下经皮穿刺置带钩导丝、注射染色剂、术中肺组织B超等，一定程度上解决了这一问题，但均不十分满意。目前对于肺内微小结节的术中定位，外科医生的经验和技巧仍起着十分重要的作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>4. &nbsp; 针对“微小癌”的手术方式是否可以改良尚有一定争议。</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当前呈现在外科医生面前的肺部结节越来越多，也越来越小。针对这些小结节，已有的共识是，尽可能先局部切除（楔形切除或肺段切除），如为恶性，再进一步行扩大切除。但有争议的是，对于一些十分微小的恶性病变，如5mm左右的微小癌，腺瘤样增生合并局灶癌变，或多发的局灶性的肺泡细胞癌，是不是一定要做肺叶切除？是不是要做淋巴结清扫，还是仅做淋巴结采样即可？目前还存在一定争议。就在几年前，这么小的肺癌临床还很少碰到，可是近年越来越多。对于这些“小肺癌”，传统术式固然没错，且轻车熟路，但是否可以减小切除范围，局限切除会不会影响治疗效果，还需要一个探索过程。这是新时期的胸外科医生需要面临的新课题。寄希望未来能够有很好的随机对照研究予以解答。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>5. &nbsp; 当前科学决策和恰当治疗尚主要依赖于专家经验，未来发展大有空间。</strong></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如前所述，当前肺部结节越来越多，从诊断到治疗均存在很多挑战和异议。如何依据临床信息做出更为准确的诊断，如何选择恰当的手术时机，如何确定最佳的手术方案，如何获得最好的治疗效果，对于这些问题，尽管已有一些章法和原则可循，但主要还都依赖于专家经验。同样是查体发现的一个肺部小结节，在不同医院，按照不同医生给予的诊疗流程走下来，其结果可能大不相同。未来，这些都是需要不断完善，不断走向科学化的问题。所以说，关于肺部结节的诊治，未来发展大有空间！</span>
</p>
