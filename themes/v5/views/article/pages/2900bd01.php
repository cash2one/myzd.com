	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>美国进修学习的体会</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';">我本人对出国并不陌生，但是这次来美国进修学习还是感到比较激动。我曾经于2000年8月-2001年8月在伦敦大学学院（University College London)的英国国立神经内科和神经外科医院进修学习一年，那次进修学习对促使我成长为一名优秀、认真负责的好医生起关键作用。在医院的大力支持下，今年10月3号来美国费城进修学习射波刀新技术和美国医疗先进经验。现将进修学习的体会分享给各位。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 一、著名放疗专家、医生和物理师共同创建独立的射波刀中心</span><br />
<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';">&nbsp; &nbsp; 我进修学习的第一站是美国费城射波刀中心，它是费城射波刀治疗病人数量最多的中心。由一位美国最著名的放疗专家、几位知名放疗医生、2名中国的物理师共同创建的。这种独立射波刀中心的优势是：经营成本低、服务好、治疗精心（因为他们在为自己工作），随访及时。这个射波刀团队配合默契、团结互助，像一个和睦家庭，他们用精湛的技术和真诚的关爱治疗每一位病人。他们从病人病情出发，为病人提供合理的个体化治疗方案。经常治疗一些高难度病人，特别是一些病人已经做过放射治疗，肿瘤复发无法再进行放射时，他们为病人做射波刀治疗，取得良好效果。如果病人适合手术治疗，他们会帮助病人联系手术医生。如果病人适合其他放射治疗，他们会安排病人做其它的放疗而非唯一的射波刀治疗。有一位女医生，她是一位知名的放疗专家，看病时经常会像关心孩子一样地关爱病人，而且经常工作到很晚。那位最著名的教授是一位奇才，92岁高龄，记忆力超强，主编多本放疗书籍和美国放疗的百科全书，从他身上真正的体会到活到老学到老。这里另一位医生，看病时自己坐在一个小圆櫈上，很亲切地问候病人，再加上非常温馨诊室，病人战胜病魔的信心大增。这里的治疗技术员是两位女同胞，非常可爱，对病人非常热情，经常和病人聊天拉家常，病人的精神放松了，治疗非常愉快。女同胞做射波刀治疗技术员的优势很多，服务好又温柔。这里的2位中国物理师都是国内著名大学的高才生，是全能选手，做治疗计划、传输各种图像资料，电脑的维护和射波刀小故障的维修，收入不错。可惜国内没有真正重视物理师的工作。我非常赞同由专家医生和物理师等专家团队共同组建的射波刀治疗中心，这些专家团队经营的治疗中心（或医院）与华尔街投资人经营医院完全不同，专家团队经营的医院注重医院品牌和长期成长价值，更注重病人的治疗效果，质优价廉。而投资商经营的医院主要是快速收回投资，快速赚钱。这个独立的射波刀中心治疗费用在2万元美金左右，比大医院便宜。在美国的医疗收费没有统一标准，治疗同样的病，每家医院的收费不完全一样，因为可以打折收费。</span><br />
