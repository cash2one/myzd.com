			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>刘迎龙委员：建议利用混合所有制经济形式加快医改</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人民网北京3月7日电 （记者马晓慧）“民营医院面临着整体结构不合理，发展空间受限，遭遇各种有形无形政策壁垒，专业人才匮乏，社会信誉度不高等问题。这些问题既有体制机制和政策上的原因，也有行业发展中产生的原因。”全国政协委员、北京安贞医院小儿心脏中心主任刘迎龙认为，可以利用混合所有制经济形式，促进医药卫生事业的改革与发展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">十八届三中全会提出：国有资本、集体资本、非公有资本等交叉持股、相互融合的混合所有制经济，是基本经济制度的重要实现形式，有利于国有资本放大、保值增值、提高竞争力，有利于各种所有制资本取长补短、相互促进、共同发展。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，刘迎龙建议在医疗领域也可以采取混合所有制经济形式进行改革，并提出了具体建议：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.各个部门统一思想，积极引导社会资本办医，促进民营医院良性持续发展。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.合理规划民营医院发展空间，满足群众多样化、多层次的医疗卫生服务需求。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.鼓励各地积极探索，社会资本通过联合、参股、兼并、收购、托管等多种形式参与公立医院改制，在总结各地好的经验做法基础上，中央各有关部门应尽早研究出台相关政策，确保其健康发展。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.加强民营医院人才队伍建设，加快公立医院人事制度改革，完善医师多点执业管理办法，促进医务人员在公立、民营、混合所有制等各类医疗机构之间流动。</span>
</p>
