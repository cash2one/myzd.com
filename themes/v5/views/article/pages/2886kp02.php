			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是慢性排尿功能障碍？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">慢性排尿功能障碍主要分为三大类：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1、尿频、尿急及急迫性尿失禁——患者通常会表现为无法控制地尿急，小便次数频繁但尿量很少，永远有解不干净的感觉，有时会伴随着解尿疼痛或解尿困难，严重时还会发生漏尿的情况。患者生活品质相当差，可能每隔半小时上一次厕所，晚上需要起床三至四次，无法开车至较远的距离，社交活动几乎暂停，虽然不是所有病患都会疼痛，但耻骨或会阴附近的不适感也是常见的问题。一般而言，病患会自行以少喝水，从社交工作上退缩来试图处理，但最后可能会觉得被孤立，甚至产生沮丧忧郁的情绪。目前关于其治疗主要包括：药物治疗、膀胱功能训练、生物反馈治疗、膀胱灌注治疗、经阴道电刺激理疗和骶神经电刺激治疗等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2、压力型尿失禁——是指当咳嗽、喷嚏、大笑或体位突然改变时，由于腹压骤然上升，尿液不处自主地外流的情况。原因主要是经产妇尤其是有过难产者，由于盆底组织、膀胱颈部和尿道等因分娩损伤或者患有子宫脱垂、膀胱和尿道膨出，导致尿道膀胱所形成的后角消失的解剖性缘故。另外一个重要的原因就是绝经后妇女体内雌激素的下降等一系列因素导致尿道固有括约肌功能的障碍，使尿道不能产生合适的关闭压从而产生漏尿。目前关于其治疗主要包括：药物治疗、盆底功能锻炼、电刺激理疗和手术治疗等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3、神经原性膀胱功能障碍——是指控制排尿的中枢神经或周围神经受到损害之后引起的排尿功能障碍，称为神经原性膀胱功能障碍。是一种较为常见的疾病，在临床上有时被误诊为尿路感染。肾盂积水，尿路结石或原因不明的排尿困难、尿潴留和尿失禁等，而忽略了原发病变的存在。目前关于其治疗主要包括：非手术治疗（间歇导尿或长期导尿、药物治疗、封闭治疗、去神经治疗和电刺激治疗等）和手术治疗（尿流改道、经尿道膀胱颈切除、外括约肌切开术、膀胱颈部Ｖ－Ｙ成形术以及骶神经电刺激治疗等）。</span><br />
