		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>一阵酸痛过后脖子歪了三年</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">晨报讯一位患上歪脖子怪病3年的女孩，在经历了3年的折磨后，终于找到病因，恢复了原本亭亭玉立的美丽形象。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 今年24岁的周某，3年前在没有任何诱因的情况下，突然感到一侧颈部酸痛，头不由自主地歪向左侧。起初，并没有引起她的注意，可是过了一段时间，头颈歪斜得更厉害，以致无法正常行走和生活，走起路来只会顺着头歪的方向打转转。她跑了不少地方看这个怪病</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">，花光家里的积蓄，仍然不见任何疗效。去年年底，周某到南京市鼓楼医院就诊，经神经外科主任医师梁维邦仔细检查，诊断其怪病为痉挛性斜颈。磁共振进一步检查发现，应当在脑底正中的一个粗大血管明显偏向了头部左侧，压迫了患者颈部肌肉的神经，正是这根异位血管的压迫造成她颈部歪斜。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医生决定采取微血管减压术为其治疗。术后，患者痉挛的颈部肌肉立刻放松，在医护人员精心制定的康复训练下，饱受“歪脖子”痛苦的姑娘终于端正了头颈，与恋人喜结良缘。</span><br />
