		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>疼痛规范化管理让患者在"轻痛中加速"康复</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">新华网南京12月19日电（记者林凯 卢辉）经历过手术治疗的患者都有深刻体会：不但要在术前、术后“忍饥挨饿”，更要忍受“钻心”疼痛。19日，记者从江苏省人民医院“疼痛规范化管理基地”了解到，应用疼痛规范化管理，可以在保证医疗安全的前提下，实现外科手术病人减少疼痛、降低风险、缩减费用、快速康复的目标。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 让患者安全、快速地渡过手术期并快速康复出院是我国未来3-5年的医改方向之一。江苏省人民医院胰腺中心主任苗毅教授表示：术后加速康复（ERAS）应用于胰腺手术的围手术期管理能够显著缩短患者术后住院时间，降低住院费用。同时，由于应用了规范化的疼痛管理，配合个体化的镇痛方案、多模式镇痛、超前镇痛理念等，患者的术后疼痛少、并发症少，整体就医满意度显著提高。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 苗毅说，“术后加速康复”理念不是一种手术方式，而是一门需要外科、麻醉科、护理学科等多学科相互配合的一系列围手术期（即术前、术中和术后）处理程序和方法的创新；指通过全面优化的围手术期处理及治疗方法，采取一系列规范化的措施和疼痛管理方法，最终实现外科手术病人少疼痛、低风险、少花费并且快速康复的目标。</span>
</p>
