<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>乙肝患者每年查查甲胎蛋白和肝脏彩超</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">受访专家:中山大学附属第一医院超声波科主任医师 谢晓燕</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;肝癌(原发性肝癌)是我国常见的癌症，临床上注意到许多肝癌患者有急性肝炎—慢性肝炎—肝硬化—肝癌的病史，可见肝炎与肝癌关系极为密切。医学专业人士建议，乙肝患者应该每年查查肝脏彩超和甲胎蛋白，有助于发现早期肝癌。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;乙肝患者不能满足于普通体检</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;中山大学附属第一医院超声波科主任医师谢晓燕教授介绍说，一些乙肝患者参加了单位的常规体检，做了B超检查没有问题，就以为万事大吉，其实对于他们来说，这种大规模的筛查性检查是远远不够的。乙肝患者除了肝脏彩超外，最好每年做肝炎四项、H BV-D N A检测以及甲胎蛋白的动态监测。甲胎蛋白是肝细胞和生殖细胞肿瘤的标志物，是原发性肝癌最灵敏、最特异的一种指标，甲胎蛋白在怀孕早期孕妇的血清中可以查到，婴儿出生后很快就消失，如果成人血中查到甲胎蛋白，就怀疑是肝癌。对于45岁以上，肝功能异常的人群，以上检查可能需半年做一次。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;谢晓燕介绍说，如果乙肝患者在以上检查中发现有指标异常，特别是甲胎蛋白异常的，建议再进行超声造影和CT检查。CT检查可以发现很小的病灶，超声造影没有放射线，是鉴别良性肿瘤还是恶性肿瘤的有效方法。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;两厘米内原发性肝癌首选消融</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;对于早期原发性肝癌，除手术外，还可以选择超声引导下消融治疗。谢晓燕介绍，随着科学技术的发展和医学经验的积累，肝癌治疗指南提倡两厘米内的原发性肝癌，临床上可首选消融治疗。即医生在超声的引导下，用一根针（比缝衣针稍粗）穿过皮肤和肝组织直达肿瘤处，通过激光、射频、微波，来把肿瘤细胞“烫死”。坏死的肿瘤没有了生命力，在肝脏上形成一个“疤痕”，随着时间的推移会慢慢缩小。用超声介入进行肿瘤消融创伤很小，大部分患者在治疗时不需要全麻（局部麻醉+静脉镇痛即可），不过对于75岁以上老人、儿童以及肿瘤过大者则需要全麻。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;不能耐受手术的肝癌患者也可选择消融治疗。因为我国70%—80%的早期肝癌患者是合并有肝硬化的，有的肝功能储备能力差，无法耐受手术。如果勉强手术可能对肝功能带来进一步损伤。此外，肝癌的复发率很高，无论是手术还是消融，初次治疗后5年内的复发率高达50%-80%。复发后怎么办？绝大多数肝癌患者都无法耐受第二次手术。一般认为，肿瘤大小不超过5厘米，肿瘤个数不超过5个的复发型肝癌也可选择消融治疗。</span>
</p>
