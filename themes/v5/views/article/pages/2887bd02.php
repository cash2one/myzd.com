		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>前列腺癌基金会扩大全球影响力，首次在中国新增两名PCF青年学者</strong></span>
</p>
<p>
	<br />
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">北京--(美国商业资讯)--前列腺癌基金会（Prostate Cancer Foundation，PCF）今日公布其在中国首次新增的两名青年学者，从而展开该基金会在中国确定、资助和促进创新研究项目的举措。PCF中国计划（PCF China program）与该基金会在全球资助的所有研究共同肩负着终结前列腺癌所导致的死亡和病痛的最终目标。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">PCF中国计划的首届年度前列腺癌研讨会（First Annual Prostate Cancer Symposium）将于9:00-15:00在北京大学吴阶平泌尿外科医学中心召开，在接下来的特别颁奖晚宴中，将对PCF在中国首次新增的两名青年学者进行颁奖。颁奖典礼将于18:00-21:00在北京海淀区远大路25号1楼福朋喜来登酒店举行。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2012年PCF中国青年学者奖获得者将得到一位长期PCF赞助者的匿名资助，获奖人员是：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">任善成，医学博士、理学博士</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">上海长海医院</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">导师：孙颖浩，医学博士、理学博士</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">基因融合是基因组上的两个通常不相邻的基因错误地并置在一起。作为两个基因错位的结果，它们的表达发生改变，从而可能会导致癌症的发生和恶化。TMPRSS2-ERG基因融合是前列腺癌（PCa）的一种标志，在约50%的白种人患者中检测到这一基因融合。近期的研究表明，TMPRSS2-ERG基因融合在中国前列腺癌患者中的发生率相对较低，约为15-20%。潜在的遗传异质性/不同种族人群之间的差异可解释该观察结果。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">任善成博士已在中国前列腺癌患者中发现新的基因融合，即SDK1和AMACR基因的并置。任博士提出研究这一SDK1-AMACR基因融合在中国PCa患者中的相对发生率及其临床意义。此外，任博士还提出对SDK1-AMACR基因融合作为一种用于中国前列腺癌患者检测的新型、无创标志物进行考察。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">任博士及其研究团队最近在《细胞研究》（Cell Research）刊物上发表了一篇论文，描述了在中国患者中特异性基因融合的作用。阅读该发表论文，请点击本链接。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">张宇曦，医学博士、理学博士</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中国医科大学附属第一医院</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">导师：孔垂泽，医学博士、理学博士</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">雄性激素（雄激素）刺激前列腺癌的进程，因而前列腺癌的一线治疗通常采用雄激素阻断治疗（ADT）。然而不幸的是，大多数前列腺癌患者最终会出现ADT抵抗。这一阶段的前列腺癌被称为去势抵抗性前列腺癌（CRPC），预示着转移和死亡的危险性提高。研究者最近确定出前列腺癌特异性干细胞（PrCSC），认为它在前列腺癌进程和治疗抵抗中起着主要作用。对PrCSC的研究表明，这些细胞能够自我更新，具有促进肿瘤诱发的能力，其生长和生存并不依靠雄激素，因此比其他癌细胞对治疗具有更强的抵抗性。在张博士以前的一项研究中，已确定出了一个PrCSC亚群，在ADT治疗中该亚群数目增加。他观察到，当采用雄激素治疗去势抵抗性肿瘤时，这一特异性干细胞亚群降低，肿瘤中另一不同的PrCSC亚群则变得更加突出。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">张博士提出研究导致疾病进展和治疗抵抗的前列腺癌特异性干细胞。这些研究将有助于甄别出可抑制CRPC进程的潜在药物靶标。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在中国设立的青年学者奖包括一个为期三年的课题资助，每年的总资助金额为 37，500美元。前列腺癌基金会共资助91名青年学者，他们分别来自于7个国家和44个研究机构，资助总金额高达2，025万美元。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">PCF首席科学官兼执行副总裁Howard SOULe博士指出："PCF所支持的青年学者已变革了前列腺癌的研究范畴，推进了治疗科学，并提高了全球患者的生活质量。我们今年首次向在中国的青年学者提供资助，这一扩张举措将有助于加快我们响应日新月异研究变革的使命，资助那些可促进数据共享以及提供更好患者治疗效果的高风险、高回报项目。"</span>
</p>
