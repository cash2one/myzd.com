<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腹腔镜手术是先进的科学技术与现代医学的完美结合，成为微创外科的象征，改变了传统思维观念、技术路线、和操作技巧，在妇科领域的应用具有广阔的前景，对于腹腔镜手术方式及新技术等多角度多层面的研究将为保障患者的手术安全、制定最佳治疗方案提供科学依据。腹腔镜手术治疗妇科恶性肿瘤不仅可以达到开腹手术的效果，且有以下优点：腹腔镜的淋巴结清扫可通过腹腔镜的放大作用，准确，全面取材，为治疗提供病理依据，并准确分级，避免过度治疗，腹腔切口小，对血管损伤小，出血少，手术完全在封闭的腹腔内进行，避免了脏器的暴露及手套，纱布等对组织的损伤，不干扰肠管，术后粘连少，肠蠕动恢复快，进食早，减少了输液和抗生素的用量，伤口愈合好。许多学者对腹腔镜与开腹手术治疗妇科肿瘤进行对比研究发现腔镜手术出血少、疼痛轻及恢复快,住院时间短及伤口美观。选择适当的病例按肿瘤治疗原则及要求进行腹腔镜恶性肿瘤根治术是安全、有效、彻底的，近期疗效优于开腹手术，远期疗效满意。</span>
