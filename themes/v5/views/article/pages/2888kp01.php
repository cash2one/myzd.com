	<strong>镜下魅影，刀过无痕——腹腔镜手术可以治疗哪些妇科肿瘤疾病</strong>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 爱美是人的天性，许多女性更是把美貌看做比生命还重要，在妇科肿瘤门诊经常会遇到这样的病人——王女士患有子宫肌瘤多年，已达到5-6公分的直径，医生告诉她需要开刀将肌瘤摘除，甚至有可能会将子宫整体切除。但王女士一想到肚子上要开一个大口，还有可能会失去子宫，始终下不了决心做这个手术，这一拖就是几年。但几年下来，患者体内的肌瘤越来越大，经期经常大出血甚至导致了贫血。最终王女士来到医院时患者仿佛4个月的孕妇，有迷信的人说她像是怀了“鬼胎”。像这么大的子宫肌瘤，一般都会采用开腹手术，但由于王女士不愿意在腹部留下疤痕，最后医生采取了腹腔镜下微创的方法，将肌瘤在肚子里打碎再取出，术后仅有微小疤痕（或无疤痕）。真可谓”镜下魅影，刀过无痕“。</span>
</p>
<p>
