<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多年来妇科恶性肿瘤治疗一直以手术为主，经腹广泛子宫切除和盆腔淋巴结清扫是目前治疗妇科恶性肿瘤的主要术式。近10年来，随着腹腔镜设备的不断改进和技术的不断发展完善，腹腔镜手术不仅应用于妇科良性疾病的治疗，如子宫内膜异位症、附件肿物、子宫肿瘤及异位妊娠等，在妇科恶性肿瘤诊断与治疗中的应用也越来越多，通过这些年的不断尝试腹腔镜手术的适应症逐渐扩展，禁忌症极大的减少，甚至有学者认为腹腔镜手术是没有绝对禁忌症的。手术领域已从对早期的宫颈癌、子宫内膜癌，卵巢癌的治疗向高难度宫颈癌、晚期卵巢癌手术扩展。腹腔镜手术对术者的手术操作水平要求较高，术者必须特别对盆腔各脏器、血管和神经的解剖位置及比邻关系等盆腔解剖熟悉，其次要有娴熟的腹腔镜操作技巧和配合默契的手术组人员，同时注意术中轻柔操作和正确使用各种腹腔镜手术器械。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
