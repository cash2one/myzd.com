		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>宝宝出生记得看看背部 警惕脊柱裂</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong></strong></span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>1、脊柱裂都会有明显症状吗？隐性脊柱裂患者的腰骶部皮肤是否没有任何异常？</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脊柱裂根据是否伴有椎管闭合不全分为隐性脊柱裂和显性脊柱裂：显性脊柱裂患儿出生时背部有局部包块，单纯脊膜膨出可以无明显症状，脊髓、脊膜膨出伴有脊髓末端发育畸形、变性、形成脊髓空洞者，其症状多较严重，常有不同程度的双下肢瘫痪及大小便失禁，少数患儿可合并脑积水和脊柱侧弯等其他畸形；隐性脊柱裂的症状出现有早有晚，有的在成年后才出现症状，主要表现为不同程度的双下肢无<strong>力和大小便功能障碍。隐性脊柱裂患者的腰骶部皮肤可有毛发增多，小凹，毛细血管瘤，色素沉着或皮赘等，也可以没有异常。</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>2、显性脊柱裂能否通过肉眼观察得到？肉眼观察患者的腰骶部，会有什么典型特征？</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">显性脊柱裂一般可以通过肉眼观察到，患儿背部中线的颈、胸或腰骶部可见囊性包块，其大小可从枣大至巨大不等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>3、家长应如何及早发现小儿的脊柱裂？</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于胎儿可通过产前超声，母体血清以及羊水中的AFP值来早期发现脊柱裂，近年来产前诊断不断发展，胎儿脊柱裂的筛查指标及方案不断更新，脊柱裂的检出率越来越高。出生后的宝宝可通过检查背部是否有包块，腰骶部皮肤是否有异常来及早发现脊柱裂，必要时可到专科医院就诊让医生帮助辨别。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>4、除了患者腰骶部的变化，患者还会出现其他哪些症状？</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">除了腰骶部的异常，脊柱裂患儿还可能出现神经损害症状，主要表现为不同程度的双下肢无力或瘫痪，大小便功能障碍甚至失禁，少数患儿还可伴有脑积水或脊柱侧弯等其他畸形。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>5、脊柱裂都会导致患儿腰痛、排便困难以及肢体功能障碍吗？</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脊柱裂并不一定都会导致腰痛，排便困难或肢体功能障碍，只有当脊髓或脊神经功能损害时才会出现上述症状。脊柱裂的症状与是否合并脊髓栓系、受压和神经损害的程度有关。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>6、脊髓栓系综合征是什么？脊柱裂都会导致脊髓栓系综合征吗？</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">先天性的脊髓被固定在某一个点，使之不能随脊柱的生长而发生移动，脊髓像皮筋一样被逐渐拉长，患儿进行性出现神经功能和膀胱功能障碍称为脊髓栓系综合征。脊髓栓系综合征的原因很多，如终丝紧张、脊柱裂、骶管内皮样囊肿和腰骶部脂肪瘤等，脊柱裂并不一定都会导致脊髓栓系综合征。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>7、脊柱裂合并脊柱脂肪瘤是否会导致脊髓栓系综合征加速出现？</strong></span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">脊柱裂和脊柱脂肪瘤都可以引起脊髓栓系，所以如果脊柱裂合并脊柱脂肪瘤可能导致更早的出现脊髓栓系，并较早的出现神经损害症状。</span>
</p>
