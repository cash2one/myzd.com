</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由四川肿瘤医院承办的全国微创食管癌根治术培训班于2015年07月26日-28日在四川肿瘤医院学术厅成功举办。此次培训班内容丰富、实用性强，课程设计模块化，实施流程科学化，采用理论教学专题、实践模块操作训练、手术观摩深入学习、学习体会总结升华等进阶式培训体系，生动而成系统地传授规范的微创食管手术技能。来自全国多家三甲医院近30名学员参加了此次培训。</span><br />
