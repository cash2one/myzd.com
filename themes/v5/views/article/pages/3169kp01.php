	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>垂体腺瘤早期诊断应注意的几种现象</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">俗话说：“头痛医头，脚痛医脚。”人体哪些部位感到不适，人们首先考虑的一定是到与这些部位相应的科室去就诊，但事实上，一些疾病的症状往往表现在其它的看似并不相及的部位上，比如我们脑外科收治的脑肿瘤病人就是这样。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 视力下降、性功能障碍、月经不调……出现这样的症状，你会到哪里就诊？一定是眼科、妇科。但是当久治不愈后，你有否考虑到，这样的症状也意味着你可能得了脑肿瘤。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 垂体腺瘤是颅内最常见的良性肿瘤之一，人群发生率一般为1/10万。如能获得早期诊断和早期治疗，治疗效果会相对较好。但由于早期就诊科室选择不当，相当一部分患者被延误诊断。笔者结合门诊中遇到的一些病例，就垂体腺瘤的早期表现症状作个提示，希望引起相关患者的重视。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 病例A&nbsp; 患者郑某，男性，44岁，两年来自觉双眼看东西模糊，自己未予重视。视力下降逐渐加重，曾在眼科就诊，一直当眼科疾病治疗，视力也没有好转。最近左眼视力下降更加明显，眼科医师告诉他左眼视神经萎缩了，才查CT发现颅内鞍区肿瘤，肿瘤直径3cm，明显压迫视神经，手术证实是垂体腺瘤。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">病情分析&nbsp; 视力下降是由于垂体腺瘤肿瘤向鞍上生长压迫视神经引起的。部分患者可以只表现为视力下降，一般肿瘤长得较大压迫视神经时才出现。如视神经长期受压，视力也明显下降，这时肿瘤已经长得较大，手术虽能给视神经减压，以改善视力，但肿瘤治愈已比较困难。因此视力下降的早期应该引起重视。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 医生提示&nbsp; 在排除了眼科疾病后，应想到有颅内鞍区肿瘤的可能性。此外，还有一种表现应该引起重视，就是病人两侧的视野变窄了，走路的时候容易撞到两侧的物体，部分病人可以是一侧眼睛的表现。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 病例B&nbsp;&nbsp; 患者王某，男性，40岁，患者说自己性功能不行已经10余年了，早期是性欲望下降，后来是勃起不行，总感觉乏力，胡须稀少。因难以向人开口提起，头几年一直未就诊。近两年性生活严重障碍后，才到处就医，吃过各种中草药和西药，都不管用。后来邻居有人得垂体腺瘤，两人症状很像，到脑外科就诊， CT提示是鞍区肿瘤，垂体腺瘤可能性大。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 病情分析&nbsp; 垂体腺瘤有两种机制可以引起性功能障碍：一是泌乳素腺瘤患者（垂体腺瘤的一种），高泌乳素分泌抑制下丘脑-垂体-性腺轴的功能，引起性功能下降；二是其他垂体腺瘤，肿瘤较大，压迫正常垂体引起垂体功能低下，导致性功能下降。由于病人自己不愿主动向医师说明，因此临床上垂体腺瘤引起性功能障碍远比我们看到的要多。性功能下降可以有各种不同程度的表现，男性患者如表现性欲望下降、勃起不行、严重者阳痿（2%阳痿病人是由泌乳素腺瘤所致）。女性患者表现为性欲望下降，阴道分泌物的减少等，严重者可以引起不育。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 医生提示&nbsp; 性功能下降虽然并不都是由垂体腺瘤引起的，但部分垂体腺瘤患者最早的症状就是性功能下降，这时患者在就医时要主动向医师说明，可大大提高诊断的准确性。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 病例C&nbsp;&nbsp;&nbsp; 患者林某，女性，35岁。近5年月经一直不正常，开始是月经稀少，不规则，有时2至3个月才来一次，最近1年停经，伴有双侧乳房的泌乳。患者由于长在农村，经济条件不好，头两年月经不正常时未给以就医。后来在当地医院给以药物来了几次人工周期，最近才由我们的妇产科医师建议查头颅CT发现垂体腺瘤。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 病情分析&nbsp; 人们很难想象，把脑子的毛病和月经不好联系在一起。垂体腺瘤也是由于高泌乳素分泌或肿瘤压迫正常垂体引起性腺轴功能的改变，从而导致月经周期和量的改变。大约5%的原发闭经和25%的继发闭经是由泌乳素腺瘤（垂体腺瘤的一种）引起的。如果闭经伴有泌乳，患有泌乳素腺瘤的概率增加到70%-80%。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 医生提示&nbsp; 妇女如有月经症状的改变，在排除了妇科疾病后，应该想到有脑垂体腺瘤的可能性。尽管出现症状时间的长短和肿瘤的大小不成正比率，但如能早期诊断，早期治疗，病人的预后相对较好。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 当然，垂体腺瘤也不仅仅限于上述几种表现，2/3的病人早期可有不同程度的头痛，主要位于眼眶、前额和双侧太阳穴的位置。而且不同的类型的垂体腺瘤有各自不同的表现特征，如生长激素腺瘤表现为巨人症或手脚肢端的肥大；促肾上腺皮质激素腺瘤可表现为特有的肥胖症等，比较容易早期发现肿瘤。上述症状中的几个可以一起出现，比如头痛、视力下降又伴有月经的改变，则高度提示垂体肿瘤。而仅出现单一的症状则容易引起漏诊和误诊，这将影响治疗效果。在症状出现的早期发现垂体腺瘤，肿瘤小于1厘米，属于微腺瘤，对有经验的神经外科医师，手术治愈率达到90%左右，而大腺瘤或巨大腺瘤，单纯手术的治愈率分别为30%-50%和0%-5%，这充分说明的早期发现垂体腺瘤的重要性。病人自身的医疗常识和医生的诊治水平同等重要，结合上述三个病例，希望能给垂体腺瘤的早期诊断有所帮助。</span>
</p>
