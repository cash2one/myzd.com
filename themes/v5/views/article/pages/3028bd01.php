		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>治房颤：射频消融有了"升级版" 更简便安全</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="/ke4/attached/11385343fbf2b211e9875a05ca8065380dd78ed4.jpg" alt="" />img1</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 资料照片</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 老龄化社会扑面而来，心律失常发病率也急剧上升。在各种心律失常类型中，房颤是继室性早搏后第二位常见类型，约占整个心律失常的15%。初步统计显示，我国约有房颤患者526万人。科学研究发现，房颤与卒中等有密切关系，与非房颤患者相比，房颤患者卒中发生风险增高5倍，所有卒中患者中，15%至20%由房颤产生。什么是房颤？治疗房颤的最新技术有哪些？记者就此采访了复旦大学附属中山医院心内科朱文青教授。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 心房颤动发病</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 随年龄增长而增长</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 房颤的学名叫心房颤动，朱文青解释，房颤是指规则有序的心房电活动丧失，取而代之的，是快速无序的颤动波，房颤也是最严重的心房电活动紊乱。房颤时，心房激动的频率为300至600次/分，心跳频率也出现快、不规则的症状，有时甚至达100至160次/分。在一般人群中，房颤的总患病率为0.4%，成人患病率为0.5%至0.95%之间，60岁以下人群患病率总体为1%，随着年龄的增加，房颤有逐渐增加趋势，75岁以上人群可达10%。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家介绍，诱发房颤的原因多种多样，例如，高血压、冠心病、心脏外科手术、瓣膜病等。房颤的症状可表现为心脏跳动紊乱、心跳加快、疲劳、头晕眼花、晕倒，更有的患者会表现为胸部疼痛、压迫、呼吸困难等。房颤的最主要危害包括卒中（风险是正常人的5倍）、心力衰竭（风险是正常人的2倍），由此引发患者死亡率增加。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 应对房颤，当前医学界普遍认为，导管射频消融术式最常用、最可靠的治疗手段之一。朱文青介绍，导管射频消融术，是通过心导管将射频电流（一种高频电磁波）引入心脏内，消融特定部位的局部心肌细胞，融断我折返环路、或消除异常病灶，以此来治疗房颤。导管射频消融术是相当成熟的介入治疗方法，其手术成功率高、对心脏损伤小、术后并发症少。专家建议，对于阵发性房颤患者，经过药物治疗控制不佳，可考虑进行射频消融治疗。首次消融成功率一般在70%至80%左右，患者可开展多次射频消融术，两次以上消融成功率可达80%至90%。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “升级版”射频消融术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 更简便安全</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 射频消融技术的问世，为大量房颤患者带来福音，可部分患者仍难享受到技术利好。年纪较大、合并心功能不全的患者，消融可能会带来隐患。冷冻球囊导管消融新技术，随着应用的需要而问世。朱文青说，传统冷盐水灌注射频消融，手术时间较长，术中需要注射1000毫升的生理盐水，射频消融过程中的烧灼感、疼痛也会为患者带来紧张和痛苦。冷冻球囊导管消融，原理是通过液态制冷剂的吸热、蒸发，带走组织热量，在球囊与心脏接触处进行消融。这种新技术使目标消融部位温度降低、病变的心肌细胞遭到破坏、阻止了异常电信号的传导，继而达到治疗房颤目的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 中山医院心内科于2013年针对2例阵发性房颤患者，开展了冷冻球囊消融术。术中，医生通过长鞘管送冷冻球囊，将Achive环形标测电极进入肺静脉前庭处，造影剂显示：肺静脉前庭贴靠良好，冷冻消融共消融了480秒，整个手术依次完成了左上肺静脉、左下肺静脉、右下肺静脉、右上肺静脉的电隔离，过程相当顺利。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这种“升级版”的射频消融技术，优势在哪里？朱文青表示，相比冷盐水灌注射频消融，冷冻球囊导管射频消融的手术时间、X线曝光时间都显著缩短。由于术中无需输注大量生理盐水，患者没有以往常见的疼痛症状，心脏穿孔、心包填塞等严重并发症的发生率明显降低，因此，这种技术具有更安全、更简便、更有效的优势，患者耐受性也更好。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 三维腔内超声技术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 减少辐射应用</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医学技术的探索，可谓永无止境。从冷盐水灌注，到冷冻球囊射频消融，直至利用三维超声技术，更精准完成手术，技术的升级为患者带来更大福音。今年4月，由中科院院士葛均波教授领衔，中山医院心内科团队运用Carto3三维标测系统、三维腔内超声技术，成功完成阵发性房颤射频消融术。这也是华东地区首例开展三维腔内超声技术治疗房颤的病例。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 三维超声技术辅助，为原有射频消融术的操作锦上添花。朱文青说，手术过程中，三维腔内超声技术用于左心房解剖模型的建立、辅助房间隔穿刺、左心耳血栓检查，同时实现CT影像和超声三维影像的精准融合。整个手术中，因为新技术的辅助，有效降低了60%的X线曝光时间（从平均25分钟降到9分钟），减少了50%的造影剂使用（从平均80毫升降为40毫升），有效减轻了对患者与医生的辐射，减少了肾功能不全、造影剂过敏的发生风险。此外，三维腔内超声技术还缩短了手术时间，缩短了房颤消融曲线，提高了手术成功率，对导管头端血栓、穿刺并发症（如心包积液等）做到了尽早发现、实时监测。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家表示，未来50年，房颤很可能成为最为流行的心血管疾病。目前，医生可根据临床症状、体征初步诊断房颤，但确诊仍需要心电图检查；对于房颤短暂发作、难以捕捉到的患者，需要进行动态心电图检查。预防疾病，市民应从戒烟、限制饮酒、限制或不饮用咖啡因，谨慎应用某些治疗咳嗽、感冒的药物，这些药物可能会含有刺激物，促使不规则心律的发生。此外，部分患者可能需要避免饮用含有咖啡因的饮料、可乐以及非处方药物，以免疾病复发难愈。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 链接</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 射频消融治疗小知识：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1、避开月经期，射频消融术中会使用抗凝剂，可能会引起患者失血过多。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2、避开孕期，怀孕期间不能接受经导管射频消融术，X线对胎儿易造成伤害。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 3、术前禁食，对于情绪比较紧张的患者，术前、术中可适当给予镇静及止痛药物。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 4、术后康复，患者需卧床12小时方可下床活动，住院时间约5至7天。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 名医档案</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">朱文青，复旦大学附属中山医院心内科主任医师，教授，博士生导师。复旦大学上海医学院临床诊断学系主任；复旦大学附属中山医院临床诊断教研室主任。担任中华内科学会委员；卫生部介入培训中心导师；中华起搏与电生理学会房颤工作组委员；上海市生物医学工程学会起搏与电生理学会常委；上海市心血管病学会起搏与电生理学组委员。擅长心血管内科各种疾病的诊治。尤其擅长室上性心动过速、室性心动过速、心房颤动等各种疑难心律失常的诊治。高级专家门诊时间为周二、周四上午。 </span><br />
