			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>从你我眼中看：咱们家门口的省级医疗服务</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“浙二专家来咱们建德了，而且就在市一医院，听说是天天都有呢”。现在在建德的街头巷尾，大家茶余饭后聚在一起总会互相讨论着咱们建德发生的大事件。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这几年，我市经济迅速发展，人民生活水平日益提高，与之相对应的，老百姓对医疗服务水平的要求也在提高。为了全面提升医疗服务水平，浙江大学医学院附属第二医院建德分院应运而生！“让人民群众满意，就是最好的服务”，这句话在市一医院得到了最生动的诠释。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">走进市一医院普外科病房，你会发现有一群仪表端庄、举止大方、动作快捷、朝气蓬勃的白衣天使，不管多忙多累，她们都毫无怨言，热情地护理着每一位病人。正是她们的付出，才有了来自广大患者的赞誉。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵女士，65岁，我市杨村桥人，有10余年的高血压病史。今年5月，因为上腹部灼伤般疼痛、反酸，并且连续5天伴有呕吐症状，其子女陪她来我院就诊。胃镜检查之后，提示：胃窦肿块伴溃疡形成、幽门梗阻，反倒性食管炎。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">入住普外科之后，6月6日上午9时，赵女士准时被送进手术室。手术由来自浙医二院派驻专家陈健副院长亲自操刀，直至下午1:30手术顺利结束，赵女士被推出手术室送回病房。参加医保的她自付手术费用为4421.25元。然而同样是这台手术，同样的医生，放在浙医二院本部进行的话，加上交通转运等开销，赵女士自己要自付的费用就将超过8000元。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">数字虽然是枯燥的，但最能说明此次浙医二院专家进驻建德分院给我们建德老百姓带来的实惠。以在浙医二院本部和建德分院分别做胃癌根治手术为例：同一种手术应用的术中材料不同，仪器设备不一；且医院等级和收费标准不同，相同的手术，却是不同的治疗与护理费用；由于医保、农保报销比例的不同，同样的手术和治疗，农保病人在本地医院报销比例至少高20%；此外，患者在建德市本地进行治疗的话还可以节省陪护费、交通费等支出。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据测算，在我市进行胃癌根治术手术的平均费用12000多元，参加医保自理自费部分不到5000元，而在省级三甲医院手术平均费用则需2万元。可以说，省级医院专家的入驻切实减轻了患者的医疗负担，一定程度上缓解了群众看大病贵的难题。专家团队的进驻，让老百姓切实享受到了用低廉的价格“在家门口看名医”的实惠。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这一周来，在我院门诊大楼挂号处可以看到，每天来挂浙医二院专家号的患者早早就来到了医院，并且都很自觉地排着长队。李女士就是其中之一，她76岁的妈妈腰椎间盘突出腰腿疼痛半年多了，大清早赶来想请浙医二院的骨科专家吴琼华医师看看。早上6点多，她就已经来门诊挂号了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“总算挂到了，因为不懂怎么在网上预约，所以很早就来了，可是一来就看到很多人已经在排队了，真担心挂不到号啊。”李女士说，“在以前，我肯定会直接把老妈送到杭州大医院去治疗的，现在浙医二院的专家来建德分院坐诊了，我就不用两地跑了。”</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医院信息科的统计数据显示，自从今年6月3日浙二专家正式进驻我院以来，门诊量已经比上月同期增加了将近600人次。网上预约也变得空前火爆，自从专家进驻的消息公布以后，医院的网上预约量也成倍增加，而医院院办的咨询电话一天下来也有几十个。“浙医二院专家的门诊号一开始挂号马上就会放光号子，非常火爆。”医院挂号收费处的工作人员说。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“因为1年前的小中风，至今右手活动还不是很灵活，所以上个礼拜特地去浙医二院看神经内科专家，其它不说，一到医院大门，不仅车子要排队进去，而且最看大的感受就是看病的人特别多，车子停得特别满。”第二次来门诊楼挂盐水的张大伯说道！</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不少科室的医护人员告诉我，浙医二院专家团队的工作作风也深深地感染着大家，因为慕名而来的病患很多，无论是出专家门诊还是上手术台，这些专家从上午8点上班后就开始马不停蹄地工作，到下午一两点没吃饭也是很平常的事情，但他们从来没有一句抱怨的话。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同时，在医院医护同事间，专家团队丰富的临床经验和严谨踏实的工作作风也完全征服了大家。现任建德分院副院长陈健是浙医二院普外科专家，医学博士，对于外科疾病，尤其是微创手术，综合治疗，基础研究等有着丰富的临床经验。在这两周时间里，除了出专家门诊、做手术外，他还带领科内医护人员开展病例讨论。“陈主任来科室以后，进一步拓展了我们普外科的诊治范围，并通过手把手的‘传帮带’，让我们的年轻医生开阔了眼界，当然也提高了科室的治疗水平。”普外科王文荣主任说。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，浙医二院派驻了9人的管理和技术团队常驻建德分院。在行政管理、学科建设和人才培养等方面进行全方位渗透，可以说投入了巨大的力量。此外，呼吸内科、妇产科、急救中心、神经内科、肾内科、普外科、骨科、脑外科8个科室的主任也全部由浙医二院的专家担任。而接下来的每隔一周的周六，浙医二院都会派出相关科室的专家来开展周末专家门诊。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“以前自己去省级医院看病，自己又不会坐车，不仅花费高，报销少，而且又晕车非常难受，最主要的是子女都在建德上班照顾也不方便。现在好了，在咱家门口就能享受到浙二专家的服务，真是既方便又实惠。”沈大妈高兴的说着。</span><br />
