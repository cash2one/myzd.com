			<div style="text-align:center;">
	<span style="font-size:18px;line-height:2;"><strong>慢性中耳炎术后 咽鼓管功能如何恢复？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;line-height:2;">有的患者手术后会感觉耳朵痛，原以为是术后恢复期，伤口正在愈合，伤口的疼痛在所难免。可是，过了一段时间，还是会有耳痛、耳闷，这到底是怎么回事呢？</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;">慢性化脓性中耳炎常常会损伤鼓膜，因此手术解决的第一个问题就是修补鼓膜。但实际上，对于一些咽鼓管功能也不好的患者，仅仅修补鼓膜远远还不够。咽鼓管是平衡鼓室内外压力的重要通道，具体可参见文章《耳朵总流脓、听力下降 当心慢性化脓性中耳炎》，如果手术后咽鼓管依旧没法调节鼓室内外压力，就会出现鼓室外压力＞鼓室内压力，中耳形成了相对的负压，就会造成耳痛、耳闷。当然，中耳的炎症没有充分清除也有可能导致耳痛。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;line-height:2;">那么术后该怎么促进咽鼓管功能的恢复呢？有两种方法，第一种是捏鼻子鼓气，第二种就是吹气球。基本上患者在家就可以做。</span><br />
<p>
	<span style="font-size:14px;line-height:2;">而且，术后为了保护鼓膜，擤鼻涕要特别注意。如果两个鼻孔一起屏气擤鼻涕，就会在鼻咽部产生巨大的气压，咽鼓管功能失调的情况下，巨大气压就会经由咽鼓管，到达中耳。这样对尚处于修复过程中的鼓膜造成巨大压力，从而影响鼓膜的修复，另外还有导致植入听骨移位的可能。因此术后擤鼻涕时，要一个鼻孔一个鼻孔做，防止对中耳造成巨大压力。同理，打喷嚏也是一样的，要张开嘴巴打喷嚏。不仅如此，飞机起降过程中也对咽鼓管有很大影响，一般术后一个月之内不建议坐飞机。</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;line-height:2;">在饮食上，尽量吃软食，保持排便通畅，防止大便干燥。手术后不要吸烟喝酒，烟酒对咽鼓管的恢复有很大影响，也要避免上呼吸道感染，以免鼻咽部分泌物堵塞咽鼓管咽口。</span>
</p>
