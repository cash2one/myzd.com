		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>小儿患神经原性膀胱须尽早治疗</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">晚报讯 怀孕前不注意补充叶酸，过度泡桑拿接触热环境，经常抽烟等，或致新生儿脊髓栓系症，导致儿童患上神经原性膀胱！记者从复旦大学附属儿科医院主办的“神经原性膀胱儿童之家”活动上了解到，这类病孩终身有严重排尿或尿控制障碍，若不及时干预治疗，可能诱发肾脏感染或相关疾病，最严重的甚至发展为肾功能衰竭。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在“神经原性膀胱儿童之家”活动上，有相似经历的家庭、病孩聚集在一起，在彼此交流中互相借鉴经验，互相鼓励。“孩子10岁以前裤子没有干的时候，浑身尿臭，上学都不愿意去。现在他自己每天导尿四五次，原来的尿失、尿臭等问题都解决了，学习成绩也好了。”一位母亲说。另一位家长感叹：“我原以为孩子没指望了，现在看看他们，我也有信心了。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 临床上，有的病孩来就诊时发现，肾脏已经开始出现萎缩。如果不及时关注并治疗，将来可能引发肾功能衰竭。一旦慢性病造成肾脏积水、尿毒症等，病情将不可挽回。复旦大学附属儿科医院泌尿外科主任毕允力分析说，神经原性膀胱患儿主要表现为严重的排尿和尿控制障碍，他们中有的病孩不能控制排尿，由于尿液不自主地流出，衣裤长年潮湿发臭；还有不少病孩不能排尿，造成肾积水或尿路感染，严重的感染或肾功能衰竭等，甚至可能威胁到生命。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “脊髓脊膜膨出是儿童神经原性膀胱最常见的病因！”毕允力主任说，对成人来说，神经原性膀胱的主要原因是外伤或糖尿病，但在小儿中，往往由于胎儿期神经管发育异常引起的脊髓脊膜膨出、椎管内脂肪瘤、脊髓栓系症等所导致。据不完全统计，儿童脊髓脊膜膨出发病率大约为千分之一左右，在怀孕前和怀孕期注意及时补充叶酸，发病率约可下降一半。此外，正常母体在妊娠前三个月缺乏叶酸，泡桑拿、睡电热毯等过度接触热环境，以及抽烟等，都可能是导致小儿脊髓栓系症的原因。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 神经原性膀胱是因控制或协调排尿功能的中枢神经系统受损引起，是一类慢性病，需长期导尿，终身随访，对家长和患儿来说，不仅精神压力大，还对其社会活动造成严重困扰。因此，早期治疗和干预对患儿有至关重要的作用。在治疗上强调个体化的综合性治疗，其中，清洁间歇导尿是治疗这类疾病的基础，通过实现尿控使患儿能融入到日常生活和学习中，同时预防尿路感染，以保护肾功能。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 此外，儿科医院肾脏泌尿系统疾病诊治中心还建立了“神经原性膀胱儿童之家”QQ群，及时解答患儿在导尿等方面的困惑，方便患儿家长交流护理经验，帮助患儿重新找到自信，找回笑容。</span>
</p>
