		<p align="center" class="p" style="background:#FFFFFF;text-indent:36pt;margin-left:36pt;">
	<span style="background:#FFFFFF;color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>科学认识先天性白内障</strong></span><span style="background:#FFFFFF;color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>（科普）</strong></span><span style="background:#FFFFFF;color:#333333;font-family:宋体;font-size:16.5pt;"></span>
</p>
<p class="p" style="background:#FFFFFF;">
	<span></span><span style="background:#FFFFFF;color:#666666;font-family:宋体;font-size:9pt;"></span>&nbsp;
</p>
<p class="p" style="background:#FFFFFF;">
	<span style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;">先天性</span><span><a href="http://www.haodf.com/jibing/baineizhang.htm"><span class="15" style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;text-decoration:underline;">白内障</span></a></span><span style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;">是儿童期影响视力发育的一种</span><span><a href="http://www.haodf.com/jibing/yanbujibing.htm"><span class="15" style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;text-decoration:underline;">眼病</span></a></span><span style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;">，是指婴幼儿出生时或出生后第一年内发生的晶状体混浊。病因可以是家族遗传性或者散发性；同时可以伴有或者不伴有其他眼部异常，是造成儿童</span><span><a href="http://www.haodf.com/jibing/shiming.htm"><span class="15" style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;text-decoration:underline;">失明</span></a></span><span style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;">和弱视的重要原因之一。</span><span style="color:#333333;font-family:Arial;font-size:10.5pt;"></span>
</p>
<p class="p" style="background:#FFFFFF;">
	<span style="color:#333333;font-family:Arial;font-size:10.5pt;"><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">婴幼儿出生后的前</span><span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6</span></span><span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">个月是视觉发育的关键时期，固视反射、立体视觉和色觉等都是在这个时期形成。正常情况下这一阶段婴幼儿的视力会迅速提高，如果这时发生白内障，不仅会造成视力损害，同时也会破坏视功能和色觉的发育，发生形觉剥夺性弱视，所以先天性白内障的治疗不同于成人白内障，其治疗目标是恢复视力、减少弱视和视力低下的发生。</span></span></span><span style="color:#333333;font-family:Arial;font-size:10.5pt;"></span>
</p>
<p class="p" style="background:#FFFFFF;">
	<span style="color:#333333;font-family:Arial;font-size:10.5pt;"><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">先天性白内障手术后视力一直是患儿家长关注的焦点，临床治疗证明根据不同情况有不同的结果。对于单眼遮挡视轴明显的白内障应在出生后</span><span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2</span></span><span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">个月内手术，否则会发生形觉剥夺性弱视；对于双眼白内障已明显影响视力的，应该尽早手术，同时为缩短单眼因白内障受抑制的时间，两眼手术间隔时间尽量安排得近一些，一般在第一眼手术后</span></span><span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3</span></span><span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">天内且没有明显手术反应的情况下就可以行第二眼的白内障手术；如果白内障不严重，或局限性晶状体混浊，患儿尚有一定视力，就要根据患儿的具体视觉发育状况慎重考虑，如果能正常生活，不要急于手术，但必须采取促进视觉发育的措施（如弱视矫正训练等），待患儿稍大些后再手术。先天性白内障的情况复杂，一般要咨询白内障专家根据病情进行相应治疗。</span></span></span><span style="color:#333333;font-family:Arial;font-size:10.5pt;"></span>
</p>
<p class="p" style="background:#FFFFFF;">
	<span style="color:#333333;line-height:2;font-family:Microsoft YaHei;font-size:14px;">婴幼儿白内障眼的视力恢复情况，除了取决于一个成功的白内障手术，术后屈光矫正和弱视治疗是一个非常重要环节，但是一个长期而艰苦的过程，需要家长有超常的耐心并高度重视，而这点往往被忽视，所以在此应着重强调。但总体上，先天性白内障即使手术治疗成功，术后视力的恢复与老年性白内障相比也往往是不满意的，患儿家长对此要有客观的认知和适度的期望。</span>
</p>
