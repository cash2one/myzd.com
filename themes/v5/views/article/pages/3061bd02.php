			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>张骞：让手术结果更完美</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1978年出生的北大医院泌尿外科副主任医师张骞还很年轻，但在泌尿外科领域中，他已小有名气。对于张骞来说，让手术结果更完美，让患者获益最大是他不懈的追求。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 当前，肾肿瘤高发，且越来越年轻化。对于4厘米～7厘米、毗邻大血管的良性肿瘤，或小于4厘米、位于肾脏核心的恶性肿瘤来说，如果主刀医生技术不够精湛，就只能将肾脏全部切除。但是，张骞将患者的生存质量放在首位，尽量为他们保留肾脏，施行难度高、风险大的腹腔镜部分切除术。他还偷偷给自己定下了一个“百肾计划”，即每年拯救200个肾脏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147003991711827" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为了让更多医生了解并熟练操作腹腔镜肾部分切除技术，张骞又有了新计划。他开始筹划“泌尿外科腔镜万里行”公益行动，一场“肾脏保卫战”随之打响。张骞通过到全国各地为经济困难的患者义务手术，并亲身讲解、实际演示等，普及推广腹腔镜技术，尤其是腹腔镜肾部分切除手术。他说，只要影响到200名医生，每名医生一年做10例肾脏部分切除术，即可为2000名患者保留肾脏。如今，“千肾计划”已变为现实。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我国泌尿外科领域的泰斗、中国工程院院士郭应禄说，从手中有刀到手中无刀，张骞的拯救肾脏计划让人肃然起敬。在医患互信缺失的今天，这种将困难留给自己的挑战和坚守难能可贵。如何将技术的最大化，转变为患者利益的最大化，值得每个行医者反思。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “成绩的取得，离不开一位患者。”张骞对他的第一台腹腔镜肾部分切除手术记忆犹新。2010年，一位33岁的农民工前来就诊，经检查发现肾上长了个约4.5厘米的肿瘤。如果对他进行全肾脏切除，手术会相当轻松，但考虑到患者比较年轻，又是一个贫困家庭的唯一经济支柱，张骞提议做腹腔镜肾部分切除术。这种手术风险很高，尤其对当时资历尚浅的张骞而言挑战更大。但想到保肾手术能为一个家庭带来希望，张骞决定挑起这份重担。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术非常成功，张骞也受到了莫大的鼓舞。他至今仍对这名患者充满了感激：“正是有了这名患者的信任，才有了我的第一步。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 张骞表示，医者仁心，每个手术技巧的进步，都是源于对患者的“仁心”。作为外科医生，要有扎实过硬的技巧和丰富的经验，以及应对危急情况时处变不惊的心理素质。只有这样，才能最大程度减小手术创伤，缩短手术时间，从而让患者更快得到康复。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在这种源动力的推动下，张骞在泌尿外科肿瘤微创手术技术上有了一些突破。腹腔镜肾部分切除从最初的两三个小时，缩短到最快13分钟；腹腔镜前列腺癌根治术从最初的两三个小时，缩短到现在最快40分钟。而这些手术的进步，都是基于把患者的创伤降到最低的想法得以实现的。在手术的操作细节中，他力求做到增一分则多、减一分则少的境界。以往肾癌患者和前列腺癌根治术患者术后，往往要住院7天～10天，而他的患者通常术后3天即可出院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “作为医生要有‘胆商’，但决不意味着可以鲁莽行事。”张骞强调，“我们从事的职业与生命息息相关，因而一定要在充分准备的前提下进行创新、尝试。”在张骞看来，“胆商”不仅要有胆量，更重要的是有面对困难的勇气，以及坚持不懈的精神。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前不久，“顶天立地——泌尿腔镜中国行”在上海第十人民医院启动，这是由中华医学会泌尿外科分会主任委员孙颖浩倡议，北京郭应禄泌尿外科发展基金会提供资助的爱心公益活动，旨在让年轻医生挑大梁，传播先进的腔镜理念和技术。所谓“顶天”，就是要和国际接轨，追求学术高峰；“立地”则是要将所掌握的先进技术，切实地应用于患者身上。而张骞，再次开启了新的征程：“作为一名年轻医生，我深知前方还有很长的路要走，希望自己始终心存善意，志存高远。”</span><br />
