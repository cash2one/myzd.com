		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻塞都是鼻炎引起的吗</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="http://static.mingyizhudao.com/147004463084840" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">采写/本报记者 陈俊珺</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 读者来信</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 近来，我一直被鼻塞困扰，特别到夜里会影响睡眠。一开始我以为是感冒引起的，可吃了感冒药仍然不见效。那么，鼻塞是不是和鼻炎有关？应该怎么治疗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 闵行区 张女士</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 名医解答</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 会引起鼻塞的病因有很多，常见的有鼻窦炎伴鼻息肉、鼻中隔偏曲、过敏性鼻炎等，儿童腺样体肥大、少数鼻腔肿瘤、鼻腔黏连等原因也会造成鼻塞。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 鼻息肉是一种炎症性疾病，其病因尚不明确，大多与上呼吸道慢性感染等相关。早期单发性鼻息肉只要用皮质类固醇激素类气雾剂喷鼻治疗，就能得到控制。较大的鼻息肉及伴发有鼻窦炎的患者需在鼻内窥镜下进行手术治疗。术前，患者须进行全身检查，如患有严重的心血管疾病或处于哮喘发作期等，应待病情稳定后再行手术。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 鼻中隔偏曲是指两个鼻腔间隔的骨质和软骨在发育过程中受某些因素影响，导致结构上的畸形。若病情影响到鼻腔通气等生理功能，通过鼻内镜下手术矫正可获得良好的疗效。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 过敏性鼻炎若持续时间较长，可能会引起鼻腔黏膜高度水肿，从而导致鼻塞。世界卫生组织推荐，“四位一体”疗法是对付过敏性鼻炎的最佳方案即：明确诊断，包括避免接触过敏原；对症的药物治疗，包括抗组胺剂、减充血剂、低剂量类固醇喷鼻剂等；及时就诊，建立良好的医患关系；进行标准化特异性免疫治疗，即俗称的脱敏治疗。采取此方案治疗可有效控制过敏性鼻炎的各种症状。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 腺样体肥大多发生于3到7岁的儿童，若孩子反复出现夜间打鼾、鼻塞和呼吸暂停等症状，就应该到专科医师处就诊，排除腺样体肥大的可能性。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 需要注意的是，鼻塞也有可能是鼻腔肿瘤引起的，这种鼻塞往往会日益加重，伴有血涕、鼻臭等。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 鼻腔的各种疾病都会导致鼻塞。所以，长期被鼻塞困扰的患者，不能听之任之，最好到专科医师处检查和诊断，明确病因，再进行针对性的治疗。</span>
</p>
