            <h3 align="center">引起癌症的常见因素有哪些</h3>
            <!-- <h5 class="text-muted" align="center">全网发布：2015-01-14 11:43 发表者：王智雄</h5>  -->
            <ol class="mt30">
                <li class="mt20">
                    <b>病毒感染因素</b>
                    <p>
                        EB病毒与传染性单核细胞增多症、Burkitt淋巴瘤、鼻咽癌、多发性B细胞淋巴瘤的发生有密切联系；乳头状瘤病毒和单纯疱疹样病毒是二种与宫颈癌密切相关的病毒；乙型肝炎病毒与肝癌的发生有密切关系；人类T细胞白血病毒Ⅰ型与成人T细胞白血病有病因学上联系；免疫缺陷病毒均与恶性肿瘤密切相关。
                    </p>
                <center>	
                    <img src="../upload/article/article1.1.jpg">
                </center>
                </li>
                <li class="mt20">
                    <b>烟草致癌因素</b>
                    <p>
                        点烟中的烟焦油具有化学致癌性。据统计美国、英国全部肿瘤的30%由烟草引起。吸烟者发生肿瘤的危险性为非吸烟者的10倍，其危险性随着吸烟量的增加而增大。吸烟与呼吸道、上消化道、胰腺、肾盂和膀胱的癌证有关，而鼻烟和咀嚼烟草则与口腔、鼻咽、肾与膀胱的癌症有关医学教|育网搜集整理。
                    </p>	
                <center>
                    <img src="../upload/article/article1.2.jpg">
                </center>
                </li>
                <li class="mt20">
                    <b>酒精致癌因素</b>
                    <p>
                        酒在人体的主要代谢产物乙醛是已经肯定的致癌物。已证实饮酒能诱发肝癌、食管癌；过量饮酒能导致肝癌、食管癌、贲门癌、胃癌、口腔癌、上喉癌等，并且与吸烟有协同联合作用。
                    </p>
                <center>
                    <img src="../upload/article/article1.3.jpg">
                </center>
                </li>
                <li class="mt20">
                    <b>环境致癌因素</b>
                    <p>
                        环境被致癌物污染，如空气、土壤，亦可导致癌证的发生。目前已知约80%左右的肿瘤发生与环境因素有关，尤其肺癌更为密切。大气污染越重，肺癌发病率越高。城市高于农村，都市越大发病率越高。据报导肺癌发病率最高者依次为：北京、上海、沈阳等。
                    </p>
                <center>	
                    <img src="../upload/article/article1.4.jpg">
                </center>
                </li>
                <li class="mt20">
                    <b>职业因素</b>
                    <p>
                        人类劳动生产环境中，存在着可引起劳动者发生癌症的因素。如石棉工人肺癌和间发瘤多与从事石棉工作有关；氯乙烯聚合工人多见肝血管肉瘤；从事橡胶工业者与髓性白血病关系密切；接触电离辐射能增加肿瘤的发病率，可致白血病、乳腺癌、甲状腺肿瘤、皮肤癌等，已知的紫外线照射诱发皮肤癌、舌癌、黑色素癌等。
                    </p>	
                <center>
                    <img src="../upload/article/article1.5.jpg">
                </center>
                </li>
                <li class="mt20">
                    <b>遗传因素</b>
                    <p>
                        肿瘤与遗传有关的证据越来越多。如视网膜母细胞瘤、肾母细胞瘤、神经母细胞瘤等。是当前肿瘤研究的重要课题之一。
                    </p>
                <center>	
                    <img src="../upload/article/article1.6.jpg">
                </center>
                </li>
            </ol>

