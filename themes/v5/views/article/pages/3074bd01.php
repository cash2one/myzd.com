		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>陆骊工：发展肝癌的多学科综合治疗</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一个肝癌病人进入医院，到底是选择肝胆外科治疗还是介入治疗？这是大部分人的疑问，在第三届亚太肿瘤介入大会上，广东省人民医院肿瘤中心介入治疗科主任陆骊工教授告诉我们，多学科合作才是有效治疗肝癌的关键，团队建设和如何合作处理肝癌病人是我们亟待解决的问题。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/14700418802631" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肝癌的治疗应根据病人的具体情况，如机体情况、病理类型、侵犯范围（病理分期）和发展趋势，科学地、合理地、有计划地应用现有的治疗手段，如介入治疗，外科手术，放化疗，靶向治疗等的最佳组合，以期最大幅度地提高治愈率、延长生存期、提高病人生活质量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肝癌的治疗方法包括：1、局部治疗方法：肝切除术、消融治疗、放射治疗（包括外照射和粒子植入）、高聚焦超声2、针对全肝的治疗方法：肝脏移植、肝动脉灌注化疗和TACE（超选择导管置管时TACE可为局部治疗）3、针对全身的治疗方法：化疗、靶向药物、免疫生物治疗、中医药治疗4、其他：抗病毒、抗炎、护肝等</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 目前，国际上一般遵循巴塞罗那肝癌治疗标准，2011年国内卫生部也组织专家制定诊治规范，并达成共识，认为小肝癌小于3cm的外科切除，肝移植，射频消融这些方法处理都能达到根治的目的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 肝癌综合治疗方案的探索</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 门静脉癌栓（PVTT）是影响肝癌预后的重要因素，其发生率高达62.2%-90.2%，肝癌并发门静脉癌栓若不积极治疗，存活期一般不超过6个月，平均为2.7个月，多在3个月内因食管癌底曲张静脉破裂出血或肝功能衰竭死亡。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不论是TACE，还是外科切除取栓，由于入肝的门脉血流减少，肝功能储备受损，并且癌栓的存在使门脉高压恶化，均增加了治疗风险，降低了疗效，门脉支教联合碘—125粒子条能够在恢复门静脉供血的同时对癌栓进行治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 另外，我们也不断探索新药物对癌栓的作用，如雷替曲塞——TACE治疗肝恶性肿瘤，研究表明它和奥沙利铂灌注治疗一样，对肝癌中晚期病人有一定疗效。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 如果肝癌出现肺转移，肺转移病灶小的可做消融治疗，但如果多的话，则加大治疗难度，最近发现TACE联合三氧化二砷全身化疗某种程度上能解决这一问题，临床发现，有病人接受这种治疗手段，于21天后肺转移病灶消失。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 陆骊工教授指出，实现多学科的会诊模式，从诊断到治疗的随访，能为肝癌患者的预后提高更好的治疗方案，延长生命，提高生存质量。肝癌诊疗规范是实现多学科综合治疗的基础，多学科综合治疗模式是肝癌治疗的必然趋势。</span>
</p>
