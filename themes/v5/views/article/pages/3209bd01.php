<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。其实有很多病人和他的家属呢有一个误区，就是他想着西药的副作用比较大。都去找中医治疗。找中医治疗呢，这个中医啊是咱们的国宝，咱们不能说中医一定没有效果，因为凭肩治风（音）也是中医的一个强项，但是呢中医药我们绝对不能把它作为一个主要的抗癫痫治疗。有很多中药的成品里面我们通过化验呢，它是有西药的成份的，而且都是一些比较低档次的西药，比如像鲁米那就是我们俗称的安眠药，那么还有一些苯妥英钠，苯妥英钠呢就是它的有效剂量和中毒剂量非常接近的，我们现在了一般有条件的话都不用这个，但是它可以掺在中药里面吃。这样的话一个是它本身的效果不太好。另外一个呢，因为它掺了西药呢，对我们医生再给他配合用药的时候呢就造成了很多的麻烦，很多困难。因为我们不知道他掺的是什么，因为他掺药之后他也不写，我们只是通过化验才发现他原来有这个药，这样的话他就是治疗起来也不正规，也不系统。那么慢慢慢慢的，有很多病人是因为不正规的治疗，不系统的治疗，最后导致成本来是药物可治的，变成药物难治的了。那么更可笑的是，我曾经接诊过两个病人。那么第一个病人来了以后呢，因为我刚才说了来了以后我们首先得做磁共振，这个病人做不了，为什么呢？他满脑袋都被埋了磁珠。这个有磁性的东西，你身上带了有磁性的东西去做磁共振，人家肯定是不给你做的。</span>
</p>
