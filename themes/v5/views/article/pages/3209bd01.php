<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：网友朋友大家好，这里是央视网《健康有约》。我是今天的主持人，常婷。今天我们的《健康有约》节目当中要跟大家一起来聊一下癫痫这种疾病。很多人都知道癫痫这种疾病它是一种慢性病，有一些患者呢可能会数年、数十年都患有这种病。对于患者的身体和精神来说，是双重的伤害。那么社会上对于这种疾病普遍存在一些误解，因此呢导致有许许多多五花八门的偏方，很多的患者盲目的乱用药，错用药，最后导致呢，身体无法得到一个健康的状态。今天我们节目当中，就要跟大家一起来聊一聊，如何正确的治疗癫痫这种疾病，避免大家进入到癫痫治疗的误区当中。给您介绍一下坐在我身边的，是来自解放军第306医院神经外科主任医师，国务院政府特殊津贴获得者，赵全军，您好赵主任。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：主持人好，网友朋友们大家好。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：赵主任，您首先先给大家解释一下，究竟什么是癫痫？究竟是哪个年龄段的人群比较易发？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：癫痫，用我们的术语来讲，就是人的大脑的神经元，大面积、同步化放电导致的一组临床症候群。我这样解释，可能网友不大理解。我可以打一个比方，我们人类的大脑，就相当于一个社会，那么大脑的神经元呢，就相当于我们社会当中的每一个人。在这个社会的平稳发展过程当中，每一个人必须各司其责做它自己该做的事情。这样的话我们社会才能稳定、和谐的向前发展。同样，对我们的大脑也是一样的。大脑的每个神经元都有它自己的职责，有它自己的功能。它每个神经元都在进行放电，但是呢它都是按照这个系统的规划来系统的进行放电的活动。如果要是有什么异常情况，它们不做自己的事情了，而做其它的事情的时候，就会出现一些意外，像我们的社会，比如说这个如果有一群人不做他们自己的事情聚集在一起，那么这就相当于我们的神经元，大面积的神经元同步化了，那么异常放电，就相当于我们的社会上的人在做一些违法乱纪的事情，如果社会上的人聚集起来一起做违法乱纪的事情呢，那么这个社会就会引起一个动荡，那么对社会的发展就造成了极其不良的影响，这个不良的影响呢就相当于我们大脑所出现的这种癫痫发作的这种情况。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么根据这个癫痫发作的类型呢，我们一般把它分为三种：一个是局灶性的，一个是全面性的，还有一个就是暂时还不能分类的发作。那么局灶性的发作指的是什么呢？是指的我们的大脑神经元的某一个部位，它这个神经元同步化了，异常放电。然后导致了一些症状。这个它的特点就是，病人一开始他是清醒的，他能够叙述他所感受到的异常的情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全面性的放电指的是，一开始就整个大脑所有的神经元那么都是同步化放电，那么这个它的特点就是意识丧失。病人发作完了，你再问他你有什么感觉，他可能一点感觉都没有，他甚至不知道他已经发作过了，有些有经验的常常发作的病人，他会根据他自己发作完了以后比较劳累，感觉到这个头痛什么，他知道我可能是发作了，但是具体怎么发的，他一点都不清楚，这是全面性发作。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">还有一些发作呢，这目前还不能分类。比如说有些头痛性癫痫，病人发作起来他就是头痛，但是你说他这个是全面性的还是局灶性的呢？不好说。那么就是说目前是有这三种类型。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：它的表现是不一样的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。表现是不一样的。局灶性发作，要根据剧作性发作的这个类型它又分为感觉性的，有人可能发作的时候就感觉我一侧肢体发麻，这叫局灶感觉性。有的是局灶运动性。发作的时候可能就是一侧肢体它不停抽动，但是病人自己控制不了，他知道，但是控制不了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么还有一种呢就是比较复杂，我们叫复杂运动性的发作。那么这个发作起来，就是类似于精神病发作一样，我们打一个比方叫登高而走，弃衣而歌，就是这个病人发作起来可能就出现一些很古怪的行为，站在高处，因为他没有畏惧心理，他不害怕。弃衣而歌，就是把衣服脱下来唱歌，那就是他没有羞耻心了，就是和人类的正常心态不一样，行为也不一样，但是他发作以后，你再问他刚才做什么了，他也不知道。所以这个就是一种。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：完全断片了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。这是一种叫精神运动性发作。那么局灶性发作呢，往往有的时候是一开始是局灶的，有感觉，感觉肢体麻木，或者是一些异常运动。逐渐逐渐的时</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">候，他就慢慢慢慢的扩展，扩展到全身以后他也完全神志不清了，也有这种情况，这种情况我们叫局部扩展到全身的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么全面性的发作呢我们刚才已经讲了，就是一开始就什么都不知道了。至于全面性发作它这个原理，它这个怎么就大面积，全脑的神经元就同步化放电了，这个目前还不太清楚。有人研究它还是应该有一个起始点，但是这个点的话它的传播速度非常的快，所以来不及达到意识水平的时候就出现了这个发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：也就是说，就是在发作之前，这个患者本身他并不知道我下一秒可能要发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。他还没来得及知道，他就已经发作了。这个是全面性的发作。不能分类呢，刚才说了头痛性的，还有一些像痴笑性的，发作的时候病人哈哈哈一笑，笑完了然后呢，你再问他你刚才为什么笑，他说不知道，他说我为什么笑了，不知道。我没笑。这个就是有一些是不能分类的。这个把癫痫发作的分类简单的给网友们介绍一下。其实呢，癫痫发作的形式是各种各样的，我只是简单的说这么几个典型的。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">至于这个癫痫它好发于哪个年龄段呢？实际上这是有两个极端。一个是婴幼儿时期，一个是老年时期。那么大部分是在婴幼儿和儿童时期发的比较多。那么目前，因为咱们老龄化的现象越来越严重，很多老人都长寿，那么就发现很多长寿的老人，但是他可能会有一些癫痫性发作。这种就是新发的癫痫性发作出来了。但是。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：那我也想问一下，如果说我们看到生活当中可能有一些朋友他有这个癫痫病，他可能是的时候就没有医治完全，然后导致说这个癫痫病一直维持了这么多年，是这样吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。这样有很多。尤其是没有经过正规治疗的。很多他可能是很轻微的癫痫症状，但是越来越重，越来越重，导致最后终身无法至治愈的也可以。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：那你给大家说一说，大家肯定会有一个疑问，为什么婴幼儿的癫痫患病率这么高？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这个患病率，癫痫的患病率确实是很高，它可以达到千分之七，这是2005年WHO统计的。那么在我国来讲的话，粗算下来也应该有一千万的患者。为什么是在婴幼儿和儿童时期发病率那么高呢？这个还是拿我那个比方，就是婴幼儿的大脑，就相当于一个刚刚开始建立社会制度的一个社会，它很多制度还不完善，所以这个社会刚刚建立的初期呢往往它是动荡不安的，那么对于婴幼儿来讲也是这样的，他的大脑发育是不完全的，很容易受到他自身的不足以及外界的影响来导致他的功能上的紊乱，出现这种大面积神经元同步化的异常放电，也就是出现这个癫痫发作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：那您刚才讲了可能是由于自身的不足或者是外界的因素导致的，那么有哪些可能性呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这个可以分为先天性和后天性两种。先天，主要有些遗传因素。这个可能在癫痫发病率当中占到10%左右，不是主要的，这种遗传的因素，因为他本身有这个遗传基因，所以说他生下来以后他可能会带有这个癫痫疾病。那么还有一些，就是他脑子里面有一些结构上的异常，比如说有这个局灶性的皮层发育不良，我们叫FCD，它有一块皮层它没按照正常的去发育，就像我们这个社会制度刚建立起来可能有一个犯罪团伙还在那里，我们没有把他铲除掉一样。另外还有一些像灰质异位，本来这个大脑皮层所在的位置它变了，它换到另外一个位置去了，那么它还要发挥它原来的功能，但是在那个地方它不能发挥那个功能，但是它发挥了，那么就造成了这种癫痫发作的状态。另外还有一些像海绵状血管瘤，什么等等有一些器质性的病变，都可以导致这个癫痫病的发作。这是先天性的因素。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么还有后天性的因素。后天性的因素主要是在生产当中，比如说产伤，大脑直接受损伤了，那么它可能导致癫痫发作。那么还有就比如说出生以后，这个孩子比如窒息、脑缺氧，还有一些是因为小孩出生以后几天他有一个黄疸期，有时候黄疸比较严重，出现核黄疸了，大脑的神经元受到黄疸的影响，变性了，那么也会出现癫痫发作。另外还有比如说高热啊，这个脑炎啊，出生以后的外伤啊，比如说有时候生产的时候，孩子产下来没接住摔在地上了，这都有可能。等等很多这个后天的因素，致使大脑造成了损伤。因为婴幼儿的脑子刚才说了，他还没有发育完全，所以他受到外界的冲击以后很容易产生它的功能异常。像这个很多见的一种常见的这个癫痫叫婴儿痉挛症，它一般是在一岁之前发病。还有一些，就是说你没有这些后天的因素，就是说也没有损伤，也没有遗传因素，只是脑发育不好，那么有一种癫痫呢叫儿童良性癫痫，它是伴有这个颞叶中央区的基波的。这个发病呢他一般是在两岁以后，然后到了十四岁青春期开始的时候呢他自己就好了，这就是随着大脑发育的完善，就像我们一个社会制度，刚开始建立的时候有不完善的地方，逐渐逐渐完善以后呢它就会很稳定。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：所以它这个叫良性的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。叫儿童良性癫痫。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：我估计听完这些之后，我们的网友朋友一定觉得养一个孩子太不容易，从婴幼儿一直到儿童时期都得要关注，要时刻小心，看着他这个脑部发育一定要加强重视。那么很多人就想问，说这个孩子如果说是初次发病的时候，肯定很多家长都没法判断他这到底是不是癫痫还是其它的疾病，怎么判断呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这个很重要。有的家长确实现在大多数都是一个孩子，现在政策放宽了，可以单独俩孩儿了，总的来讲对这个后代还是非常的金贵。一旦出现问题以后都很紧张，一抽了就觉得好像是癫痫，实际上有很多情况它不是。首先我觉得，如果遇到类似情况的话要排除一下，要排除一个假性癫痫，假性癫痫有一些情况，比如说低血糖，比如说孩子贪玩，他饿了以后他不吃东西，他没来得及吃东西，他还在玩儿，那么这个脑子这个血糖供应不上了，那么他就可能会出现晕厥，有的时候还类似于抽搐的一些症状，看起来很像癫痫，但是这个不是，这个查一个血糖就应该能鉴别出来了。那么还有一些孩子是一种就是像类似于癔病这个状态。因为现在这个孩子，他比如说有一些要求得不到的时候，或者在情绪上受到影响，比如生气了或者什么的这些方面他没法表现出来，他可能就采取这种方式，可能他自己也意识不到，就是说他是在装病，但实际上他就是出现了这种让大家觉得很紧张的状态。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：还有这种状况呢。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。这个也好鉴别。因为这个癔病他是在人越多的时候他越发病，他是发给你们大家看的。再一个就是说，因为他是装病，所以他是不敢面对大家，直视睁开眼睛看。所以说他发病的时候眼睛一般都是闭着的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：这其实从某种意义上来说，不能算发病。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。不能算。但是我们也要重视。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：它是一种表现形式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。我们要去鉴别他。我们可以看到他，他闭着眼睛，但是我们透过他的眼皮可以看到他的眼球在骨碌骨碌，比如他这个想让谁重视他的眼球会转向那一方。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：立刻可以告诉大家小坏蛋别装了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这个是要鉴别一下假性癫痫。那么还有一个呢就是要注意一下这个有高热惊厥。因为婴幼儿时期大脑还没发育完全，他如果体温过高可能也会刺激到大脑皮层，引起这个过度放电。那么这个时候呢他会出现一种惊厥的症状。所以这个要看他，如果他是每次都是发烧的时候发作，那么可能就是一个高热惊厥，这种高热惊厥呢一般是在四岁以前比较多。这个只要你看到他是因为发烧引起的，那么你就不要紧张，那么要注意以后尽量不要让孩子发烧。因为有些高热惊厥的患儿，他发的时间多了以后，次数多了以后呢，他即便是不发烧，他有可能也会发现，那么这有可能就转成癫痫了，这个要提醒家长注意的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：所以尽量要控制孩子的体温，不要让他一直高烧不退。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：但是不管怎么说吧，孩子有了这些异常的状况了，我还是建议家长带孩子到医院去看一看。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：那就是说到如果说是家长怀疑这个孩子有可能是癫痫，那么要上医院去做哪些检查来确认呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：首先要做一个头的磁共振检查，我们看一下这个孩子的脑子的状况什么样，有没有器质性病变，比如刚才我说的这个脑皮层的局灶性皮层发育不良，还有一些灰质异位，还有这个海绵状血管瘤等等，用磁共振是能看出来的。要排除一下他有没有器质性病变，这是第一个要做的。第二个就是要做一个长程的脑电图，咱们现在大多数医院里面做的脑电图也就是15分钟-20分钟，这个基本上我感觉它的诊断意义不大。要做就做长程的。一般至少要3、4个小时，最好能做24小时的脑电监测，而且最好导电多一些，现在咱们常规的都是16导，那么能做32导、64导，最多的现在咱们做到128导，就是电极给它密集一些，就是把它放电的地方不要漏掉，这样的话可能诊断更清楚一点。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么还有一个就是我要提醒大家注意，而且往往有些我们的医生也没有重视，就是对于癫痫的患儿要做一些神经心理学方面的检查。神经心理学方面的检查，它往往可以揭示孩子脑功能的状态，有的时候对确定这个癫痫病灶在什么部位很有用处。我前一段在美国克利夫兰医院癫痫中心做访问学者的时候，我就发现他们的癫痫中心，它是有好几个科室的人员组成，有神经内科的，有神经外科的，有影像科的，那么还有心理科的。就是说这几个科一起来对这个患者进行诊断那是比较科学的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：好的。那我们现在想知道，就是说针对目前这个癫痫，其实我也提到了，因为社会上普遍对它存在一些误解，所以导致现在很多的患者都轻信一些五花八门的偏方，那究竟应该如何正确的来治疗癫痫呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：首先，得了癫痫并不可怕，家长不要过度地担忧。可以说70%以上的癫痫患者，是可以通过正规的药物治疗来控制癫痫发作的。那么第一步，那么就是应该去进行正规的药物治疗，不要去找那些乱七八糟的偏方。那么正规的药物治疗如果没有效果了，那么我们还可以进行手术，手术完了以后我们还可以做一些辅助式的，有些病人手术效果不是很好，那么还可以做一些辅助治疗。那么在这三个方面呢，我先说药物治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：好。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：药物治疗，首先我们主张是单药治疗，尽量的用一些副作用小，有明确的抗癫痫效果，而且不影响患儿的智力和记忆力等等这方面的药物。用单药，先用单药，一般单药治疗这个70%的病人就能够起效。如果单药治疗无效的话，在这个无效的病例里面，你再给他加第二种药的话，那么还有13%的患者他可以起到良好的控制效果。如果两个药治疗还不好，那么再加第三个药，它的效果就没那么好了，可能也就百分之几，如果三个药治疗效果还不好，再加第四个药那么他控制好的几率就不到1%了。那么在这种情况下，那我们就可以考虑去做手术评估了，是不是可以考虑做手术了。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么手术是这样的，我们分为这个做开颅手术，大家听起来比较可怕，还有一种就是微创手术。这个开颅手术它的特点就是风险相对来讲大一些，可能对孩子会有一些影响，因为它要切除一部分脑组织，但是它对于癫痫的控制效果要好一些。那么微创手术创伤比较小，可以说头上就打两个毫米的一个小孔，我们把一个电极进去探测到病灶以后，给它做一个热凝的毁损，创伤非常小，对孩子不会有太大的智力、记忆力各方面的影响，不会有太大的影响。但它的特点，它不能够完全彻底的把癫痫病灶处理掉，所以它的抗癫痫控制效果可能要差一些。但是我还是建议如果你这个病人必须要做手术的话，如果有条件，最好先做微创，微创效果不好，我们再去做开颅手术，再去切大块的癫痫病灶。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：对于很多患者的家属来说，肯定也是希望先从小的手术入手，一听到开颅手术就特别害怕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。因为这个微创手术也是，我在306医院开展了这个项目，还是挺受欢迎的。但是，大家要选择微创手术的时候，一定要有一个心理准备，不是说我做完了一定要好，这个减轻了就达到目的了，如果实在不行咱们还有后一手。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：很多人想问您一个问题是，我是不是做完这个手术之后就可以没有任何的忧虑，也不用再吃药了，是这样吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这是一个极端错误的观念。手术只是帮助控制癫痫发作，手术并不能保证完全治愈。从我们的临床实验当中，就是说药物治疗无效的癫痫患者，这个药物治疗有效率大概百分之七八十吧，那么有将近有1/4的患儿是药物治疗无效的。那么这部分患者，我们进行评估给他做手术，手术完了以后能够完全控制住不发作的，也只能占一半。占一半，而且这一半大多数还是要吃药的。就是说，我们为什么去做手术，就是说你是因为药物治疗无效我才做的手术，手术做完了，药物治疗有效了，就达到手术的目的了。这个是作为患者或者患者的家长一定要了解的一个情况。并非所有的病人术后癫痫发作都消失的，而且所有的病人手术以后都要继续服用抗癫痫药，这个至少两年以上。那么有一部分病人，他是需要终生服药的，这个要记住。这种情况就是说，比如你这个磁共振看上去，脑子里面没有明确的器质性的病灶，做完手术了，那么这样的病人，往往他复发的可能性要多一些，然后需要终生吃药，他的脑电图复查还不正常。还有一部分病人，两年以后可以逐渐减药。那么这两年，比如说病人手术以后他没有发作，脑电图他原来非常不正常，现在慢慢开始越来越好了，有一些轻度的异常，那么可以逐渐的减，减到一个维持量，也需要终生吃的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：所以根据每个人不同的情况，他这个手术之后的方案也是不一样的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。那么还有一部分就是最幸运的病人，手术以后两年没有发作，脑电图也正常了，那么这样的病人才可以慢慢的减药，最后到停药。要说彻底治愈，目前有一个标准，手术以后十年没有发作，也没吃药了，那才能算治愈，所以说大家不要想着我一做完手术癫痫就治愈了，因为这部分患者呢，大多数都是颞叶癫痫，比如伴有海马硬化的，就是有一些器质性病变，而且又在颞叶，这个脑电图呢通过手术以后呢恢复正常了这种情况下还可以。另外还有一些像我刚才说过海绵状血管瘤、局灶性皮层发育不良，这些器质性的病变你手术都给它切掉了，它对脑子没有影响了，这样的情况下可能会达到术后停药的这种效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：所以看你是哪种类型，这个很重要。之前我在开场白当中也提到了，就是说现在社会上有很多的偏方，那在您接诊的病例当中，有没有因为轻信偏方，而导致自己的病情越来越严重的？有没有这种病例？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这个其实太多了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
