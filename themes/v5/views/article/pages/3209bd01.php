<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：那赵主任，最后您给大家讲一下，就是癫痫患者他在日常的生活当中有哪些是需要注意的地方？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：在三个方面需要注意吧。一个呢，癫痫患者我说了，一定要正规的药物治疗，有些病人呢可能开始轻微的时候不把自己的病当回事，药有一顿没一顿的吃，这样的话有可能药物对他是有效的，那么一开始他能控制住，但是他经常落顿，落顿，最后药物就没有效果了，那么就会出现发作了。但发作以后，有的时候你再吃药，可能效果就不那么理想。你要是经过正规的药物治疗，能够连续下来，他可能能治好的，能够完全控制住的，但因为你吃药不规律，那么就会出现这个最后变成难治的了。这是就是说吃药要按时，按系统这个不要断顿。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：要坚持。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：这是一个方面。另外一个方面呢，就是要尽量避免一些导致癫痫发作的因素。那么这个因素呢我总结有三个方面。一个是在饮食方面，一个是在生活规律方面，一个是在情绪控制方面。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在饮食上面，要尽量的不要去吃一些辛辣刺激性的食物，包括一些饮料。什么是辛辣刺激性的食物呢？川菜咱们不用说了，那都是不放辣椒不成席的，尽量的不要去吃。那么还有就是有一些有异味的，比如说海产品，它有腥味，实际上它为什么有腥味呢，它就是有一些小的短肽类的这个物质它是有刺激性的，对大脑有兴奋性的，这些不要去食用。还有就是一些蔬菜里面，比如说韭菜、芹菜、葱、姜、蒜这些有异味的东西，尽量的少吃，尤其不要吃生的，比如炒菜你放点葱花提个味什么的，这个倒也无所谓，但是别吃生的葱，比如山东的网友朋友们愿意吃大葱蘸酱卷煎饼。这些如果是癫痫患者尽量就不要去吃了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么饮料上面呢就是像酒，肯定是不能喝的。这是有刺激性的。浓茶、咖啡，甚至连可乐、雪碧这种碳酸饮料带气泡的，都是有刺激性，尽量不要去食用。如果是抽烟的话，那么还是尽量的要戒掉，这是在这个饮食方面。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那么生活规律方面呢，要注意就是不能过度疲劳。疲劳呢有两个方面：一个是身体的疲劳，比如你要是跑步，或者是做打球运动过度了以后，让自己呼呼带喘这种情况的话容易诱发癫痫。因为我们在这个检查当中有的时候希望病人能够出现发作，我们好看他的具体放电的情况，我们就有一种检查手段就叫过度换气，让他过度换气以后，他就有可能会诱发癫痫了，所以你这样过度去运动的话，也可能会出现这种换气状态，所以身体上面不要过度的劳累，不要去熬夜。就是说我有些病人呢，往往就在这个过年的之后就给我打电话，哎呀，我又发作了。一问什么，大年三十儿看春晚。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：熬夜了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：熬夜。一宿没睡觉。还有就是不能到人声嘈杂的地方去，这个过年了放鞭炮那种声音的刺激，也对这个癫痫发作可能有一定的这个激发作用。还有呢就是说要注意看电视、电脑，包括看手机，玩儿游戏，这些都尽量要节制一些。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：不要太疲劳了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。电视、电脑，尤其画面的闪动，画面的闪动，尤其是有一些比较暴力的游戏。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：比较激烈的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：枪战什么之类的，对眼睛，对脑都是有刺激的，因为眼睛直接跟脑是相连的。这个我有时候开玩笑跟癫痫的小朋友说，我说你要去玩游戏你就去偷偷白菜什么的吧，不要玩那种枪战片。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：电影上也不要看恐怖片。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">赵全军：对。即便是平和的这种片子，一天要控制不超过一个小时。你让他一点不看这也不现实。现在社会发展到这个状态了，电视是一个很重要的媒体。你不看也不行，但是控制一下，别超过一个小时。这是生活规律方面。那么还有最后一个就是情绪控制方面。我们这个癫痫病本来就是脑功能的一些异常，如果你过分激动的话，那本能大脑的兴奋就增加了，那么放电就增多了，那么如果它们一同步化，那就成癫痫发作了。所以说呢对于癫痫病的患者的话，自己为了我们的身体健康不要发火。那么作为这个癫痫病人的亲属和朋友来讲，那么为了他的健康，咱们不要去跟他经常发生这种口角上的冲突，或者什么。如果发现他情绪有点不对头，我们离他远一些，不要把他的火激起来，这样对他的这个身体健康状况不好。这是一方面。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">主持人：好的。今天也非常感谢赵主任来到我们的演播室给大家讲解了一下如何正确的治疗和正确的对待癫痫这种疾病，避免大家走入癫痫治疗的误区。那么其实得了癫痫这种疾病并不可怕，比如说我们历史有很多的名人，他都是癫痫病的患者，有牛顿、诺贝尔、拿破仑，这些人他们都是癫痫的患者，但是依然取得了许许多多不可磨灭的成就。即使是癫痫的患者发现了之后要及早的积极就医，另外控制发作，其实对于我们的生活，对于我们的工作都不会有太大的影响。感谢您收看我们本期《健康有约》，我们下期节目再见。谢谢您，赵主任。</span>
</p>
