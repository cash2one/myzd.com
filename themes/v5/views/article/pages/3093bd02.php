<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>准确预测大脑半球切除术患者的术后风险很重要&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2015年的元月，宽敞明亮的首都医科大学三博脑科医院功能神经外科病房里，“白日依山尽，黄河入海流……”这清晰的声音来自于近日接受了大脑半球切除术的湖南小朋友小莉。三博脑科医院周健主任提醒：大脑半球切除术用于治疗半球性癫痫疗效确切，准确预测患者术后风险很重要。小莉今年6岁，两年前无明显原因的就出现眨左眼，口角左歪，左侧肢体强直阵挛，口唇青紫，意识丧失等症状，到当地医院检查后发现脑电图异常，开始口服抗癫痫药物，由于效果不佳，服药半个月后自行停药。半年后到湖南省某医院就诊，药物治疗后，一个月无发作出院，半个月后再次出现发作，不仅发作次数逐渐增多，还出现了新的发作形式，药物治疗后效果仍不理想。不久后小莉智力明显下降，左侧肢体精细活动差、运动及语言减少，于是来到首都医科大学三博脑科医院寻求进一步的治疗。周健主任发现小莉脑组织出现进行性萎缩的特点，结合病史及脑电图，考虑患者的癫痫为一种极其罕见的脑炎所致，RE脑炎。对于RE脑炎所致癫痫，目前国际上唯一的治疗办法就是行大脑半球性手术，而且手术越早对于患者的智能发育的改善及功能的恢复越有利。1月13日周主任为小莉实施了右额颞顶开颅大脑半球离断术，手术非常顺利。术后癫痫消失了，小莉的生活质量及智力也都显著得提高了。周健主任指出大脑半球性手术是一种疗效非常高但同时又是风险性、难度系数都非常大的手术，全面的术前评估、严格掌握手术适应证，准确预测患者术后的受益和风险特别重要。</span>
