<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>关节镜手术的优点&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;关节镜手术是运用显微照相技术和相关器械进行，这些器械安置在一条细长管道的末端，通过一个皮肤小切口置入关节进行关节内直视。通过另一个小切口伸进操作器械来修复关节损伤以及退变性疾患。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 关节镜手术适应症很多：最常见应用于膝，肩，肘，髋，踝关节</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 最常见应用广泛的是膝关节半月板撕裂修整，关节内软骨碎片、游离体去除，髌骨脱位矫正</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前后交叉韧带损伤重建 ，膝关节脱位后引起的多发韧带损伤，关节内韧带止点撕脱骨折的固定</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 膝关节滑膜炎炎症或损伤。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">关节软骨损伤或者关节内骨折， &nbsp; &nbsp;一些不明原因关节疼痛等。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肩关节镜手术适应症包括：肩关节脱位，肩关节不稳定，肩峰撞击综合征，肩袖损伤，肱骨大结节骨折，肩锁关节脱位。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术在全麻或者腰麻下进行，患者处于深睡眠状态且丧失痛觉，医生通过几个关节周围的小切口施行手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 将关节内窥镜的观察镜头和其它器械插入关节。医生通过监视器观察关节的其它部分，然后修整切除损伤组织，进行损伤组织的修复重建。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 由于关节镜手术创伤小，术后恢复快，越来越受到医生和患者的青睐。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;尽管关节镜为微创手术，但是有部分患者在术后常常存在一段时间不适，其原因首先与手术有关，即使再小的手术对组织也有创伤，手术当中修复损伤韧带、半月板，肩袖等组织，需要建立骨隧道。此外关节镜手术需要液体灌注保证手术视野，一般采用生理盐水，对关节滑膜组织有刺激。由于这些问题，患者术后可能存在一些不适，而且患者个体存在很多差异，这也就是为什么术后需要一段时间进行康复的原因。</span>
</p>
