		<p align="center">
	<span style="font-family:Microsoft YaHei;font-size:18px;"><strong>用3D技术摘除骶骨足球大肿瘤（科普）</strong></span>
</p>
<p>
	<br />
<span style="font-family:Microsoft YaHei;font-size:14px;">臀部上顶着似“足球”样大小的肿瘤，令35岁的田先生走路似企鹅一摇一摆，更令他痛苦不堪的是，由于肿瘤压迫，无法正常排便，只能在腰上系着一个导尿袋。近日，第二军医大学长征医院骨肿瘤科专家采用3D打印技术，成功将盘踞在骶骨上巨大的脊索瘤整体“拆除”，同时实施腰椎骨盆重建术，并通过爱心募捐，为患者减免了部分手术费用。&nbsp;&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;来自贵州山区的田先生只身一人在广州打工，妻子在家务农，照顾两个年幼的孩子，全家的生活就靠他一个人打工支撑。二年前田先生出现骶尾部疼痛，大小便障碍，在广州各大医院检查后均确诊为骶骨肿瘤，需尽快手术治疗，却因家境窘迫无力支付昂贵的手术费用，病情一再拖延。今年2月，田先生希望能多攒点钱，拖着病体前往浙江温州打工，期间肿瘤迅速增长，腰部疼痛让他走路都有成了问题，夜晚更是难以入眠，才去当地医院求诊，检查发现他的骶骨上有一个足球大小的肿瘤，当地医院认为肿瘤巨大，无力手术。而此时田先生小便完全失禁，只能插导尿管解决燃眉之急。&nbsp;&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">田先生揣着打工积攒的几万元钱，在当地医院的推荐下，来上海找到了长征医院骨肿瘤科。肖建如主任率领严望军副教授治疗组立即对田先生进行全方位检查，确诊田先生罹患的是骶骨脊索瘤，巨大肿瘤几乎占满了整个盆腔，骶骨几乎已被肿瘤“蚕食”殆尽，并侵犯神经，挤压直肠和膀胱。&nbsp;&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">肖建如教授介绍，唯有将肿瘤一起整块完整切除，才能根治田先生的病，而骶骨承载和支撑整条脊柱，是人体上下体的连接部位，切除后人体上下就脱节了，连坐都坐不起来。此外，各种器官、大血管和神经组织在骶骨周围形成了严密的网络，手术切除时很可能侵犯大血管引起大出血。常规外科切除方式通常采用分块切除术，但术后复发率极高；而采用肿瘤整块切除技术，是降低术后局部复发率的重要手段，但患者的肿瘤直径达20厘米，潜在盆腔底部，周围血管缠绕且与神经粘连，要实施完整切除术无论手术难度及风险对医生是极大的考验。所以，过去骶骨肿瘤被视为手术禁区，即使现在，做骶骨手术也如“走钢丝”般惊险。&nbsp;&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">在高难度与高风险面前，以严望军副教授为首的专家团队决定采用国际上最先进的ＭＲＩ与CT三维重建图像融合和3D打印快速成型技术，为田先生“再造”一个仿真患病部位，医生通过模型来制定完全个性化的手术方案，决定“从后路实施骶骨肿瘤完整切除与重建术”，并进行术前模拟手术，以提高手术的“精准度”和“安全性”。&nbsp;&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">正当治疗组紧锣密鼓地准备手术时，田先生却提出放弃手术：因为家境贫困，无法承受昂贵的医药费。&nbsp;&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">“放弃手术，必死无疑。”治疗组申请为田先生争取减免部分费用，同时在肖建如主任和严望军副主任带头下，发动科室人员募捐，加上患者工友的帮助终于凑足了手术费用。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">为了给手术保驾护航，医生在手术中为田先生实施了腹部主动脉球囊阻断技术，将带有小球囊的导管送入病人体内，暂时阻断了腹主动脉，关闭了通往下肢的血流，防止后期手术大出血。在惊心动魄的6个小时手术里，医生小心翼翼地保护患者的每条血管和神经，从腰骶椎后路将一个大小为23厘米×18厘米的肿瘤完整摘除，并为其重建脊柱“底座”，内固定植骨，重建腰骶椎功能支撑起脊柱。术后，田先生大小便功能经过锻炼后恢复，双下肢感觉保留，已能正常运动。</span>
</p>
