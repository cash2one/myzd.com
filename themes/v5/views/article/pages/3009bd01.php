		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>专家：早期诊断、规范治疗是关键</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">■新快报记者　刘流　实习生　谢小明　通讯员　王大勇&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">癫痫，就是人们通常所说的“羊癫疯”、“羊角风”，是由于脑部疾病导致皮层异常放电引起的一种发作性疾病，实质上和头痛一样是多种脑科疾病均可产生的一种症状。癫痫是多发病之一，我国现有癫痫病人1000多万人，发病率在8‰左右，每年新增发病人数35万人。据一份调查显示，目前我国大部分地区，尤其是广大农村地区，癫痫治疗的状况还很不理想，仅约35％的病人癫痫发作得到较好的控制。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“我们每周要接诊癫痫患者100多人，其中还有少数顽固性癫痫患者，病人通常会伴有认知功能障碍，给学习生活带来困难，给家庭带来沉重负担。”广东三九脑科医院癫痫中心朱丹主任担忧地对记者说。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">早期确诊规范治疗&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">朱主任向记者介绍，由于癫痫的症状可能会表现为意识、感觉、运动、植物神经、精神等方面的不同障碍，患者常常被误诊为精神疾病，延误了最佳治疗时机。而癫痫对人体健康造成危害的程度与癫痫病的严重程度有关，包括发作频率、持续时间、病史长短、临床表现、心理素质等方面。一般来说发作频率越高、持续时间越长、病史越长、临床表现越复杂、心理素质越差则危害越大、后果越明显。因此，癫痫病的早期诊断对患者来说显得异常重要。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">朱主任表示，癫痫的确诊需要病史和有关电生理检查来判断，典型的反复发作性症状和脑电图癫痫样放电是确诊的依据。普通脑电图检查记录时间短，对于发作不频繁的癫痫病人很难记录到癫痫发作期脑电图，对于发作间期癫痫放电记录也不能令人满意，诊断价值较低。目前癫痫诊断的基本电生理检查是行长时间视频脑电图监测，通过长时间的连续记录病人的临床表现和同步脑电图，提高检查阳性率。癫痫诊断需要完成癫痫分类和病因诊断，为制定治疗方案提供依据。&nbsp;</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">广东三九脑科医院癫痫中心周锦华副主任指出，癫痫病人一经确诊，除了有明确颅内疾病引起的继发性癫痫如脑肿瘤、脑血管畸形需要手术治疗外，首先要采用药物治疗。&nbsp;</span>
</p>
