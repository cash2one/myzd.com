	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>慢性化脓性中耳炎鼓膜穿孔了怎么办？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大多数中耳炎症消退后中耳腔的结构可以完全恢复。但在部分病人中，会遗留一耳膜穿孔。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鼓膜修补术就是一种修补穿孔耳膜的手术，当然前提是中耳感染已经控制并且听小骨功能完好。这种手术可以修补耳膜并提高听力。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术多在全麻下完成，通常利用耳部肌肉膜或软骨膜修补耳膜。通常病人在术后一周可以正常工作，术后六周应该复诊了解耳膜愈合情况并检查听力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术时通常在耳膜内侧放置一片软骨以避免术后疤痕并改善中耳功能，增加耳膜活动性。如果中耳内充满疤痕组织或听小骨完全破坏，这时为了改善听力，可以采取二期手术的方法。第一次手术目的在于清除病变组织并减少疤痕，第二次手术的目的在于重建听力。是否选择二期手术要根据第一次手术情况来决定。</span>
