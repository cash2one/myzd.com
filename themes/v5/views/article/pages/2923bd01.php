		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>女童六年来梦中经常痴笑1分钟 疑母亲孕期服药</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2923/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">丽雅/漫画</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 东快讯（记者黄淑平通讯员丁心栓朱秀梅）福州长乐有个“怪”女童小粉（化名），连续六年来，晚上熟睡时经常会突然发出两三声爆发性笑声，笑声约持续1分钟。近日，导致怪笑的病灶“下丘脑错构瘤”被切除，这种怪象才消失。医生说，出现这种现象，可能和小粉的母亲在怀孕期间吃药有关系。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 据接诊的医生南京军区福州总医院神经外科主任医师王守森介绍，2008年初，小粉在出生后5个月左右，父母无意中发现，孩子夜间熟睡时会出现两到三次的爆发性笑声，每次发作均在睡眠中，持续约1分钟后才自动缓解。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 起初，“怪笑”的现象一年会发生两三次，小粉的父母也特别注意在夜间固定的时间进行观察。但奇怪的是，女儿莫名大笑后并没有苏醒过来，且白天均与其他小朋友一样正常饮食、玩耍。家人认为可能是孩子的正常行为，就没有给予重视。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 随着小粉渐渐长大，“怪笑”的频率也开始增多，最多时一个月达十几次。直到去年7月的一天，小粉在睡觉时突然出现了四肢抽搐发作，上肢伸直、下肢屈曲，并伴有眼睛上翻的异常症状，长达20多分钟。家人才赶紧带她去医院检查，然而跑了不少大医院均无果，最后来到了南京军区福州总医院神经外科就诊。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 经医生初诊，小粉神志清楚，语言交流正常，反应略显迟钝，生长发育较正常同龄儿迟缓。不过，经磁共振头颅检查显示，小粉头部鞍上区出现了与大脑灰质相同的一场信号肿块影，大小约3.6×1.8厘米。医生给出了明确诊断，小粉的病症为下丘脑错构瘤及痴笑样癫痫。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 小粉进行开颅手术后，伤口愈合良好，四肢肌力张力均正常，生长发育、智力都在逐步改善中。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 医生建议</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 孕妇最好不要服药</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 孩子出现异常应及时就医</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; “以前从未接触过此类病人。”据该院神经外科医师荆俊杰介绍，“下丘脑错构瘤”实际上不是肿瘤，而是灰质结构。小粉是因为脑灰质异位引起的异常放电，导致了痴笑癫痫发作。类似小粉这样脑灰质出现异位的病人在临床上实属罕见。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 家人回忆，小粉的母亲在怀孕中期曾有服药史，家人怀疑孩子发病与母亲孕期服药有关。但荆俊杰医师说，导致小粉每夜痴笑的病灶已经明确，不过这与其母亲在怀孕期间服药有无直接关系目前还不好说。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 荆俊杰提醒，孕期服药会导致胎儿出现畸形、容易流产及胎儿出生后致病等危害，因此孕妇最好不要服用药物或在医生的指导下服用；此外发现孩子有任何不正常的特征表现，应尽早就医。</span>
</p>
