			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胆囊结石与胆囊息肉</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疾病介绍：胆囊结石主要见于成年人，女性多于男性，40岁后发病率明显增高，结石多为胆固醇性或以胆固醇性为主的混合性和胆色素结石；胆囊息肉样病变是泛指胆囊壁向腔内呈息肉状生长的所有非结石性病变总称，主要包括胆固醇性息肉、炎性息肉、腺瘤样息肉、腺肌症、混合性息肉等。临床上最为多见的是胆固醇性息肉。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床表现：胆囊结石可以无任何症状，称为“安静结石”，合并慢性胆囊炎时，表现为右上腹进食后隐痛，易与胃部疾病混淆。当结石嵌顿胆囊壶腹或胆囊管时，可出现右上腹绞痛并向右肩部放射，伴恶心、呕吐及胆囊张力增大。胆囊结石也可排入胆总管形成胆总管结石，出现急性胆管炎甚至急性胰腺炎的症状。胆囊息肉一般没有明显临床症状</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">诊断：根据临床表现，结合影像学手段可确诊，首选B超检查。超声造影对鉴别胆囊息肉的良恶性有帮助。必要时可结合CT、MRI对息肉的性质作出鉴别，同时明确有无合并胆总管结石。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗：胆囊结石若无症状，结石较小时可选择内科治疗，定期复查B超（一般为6个月），必要时可口服利胆药物如胆宁片、胆维他、舒胆合剂等。对于以下情况可考虑行胆囊切除：胆囊充填型结石、胆囊萎缩、胆囊结石合并胆囊息肉、大于2.5cm无症状的胆囊结石、反复发作的胆囊炎影响生活质量、胆囊结石并发胆源性胰腺炎和胆囊壁增厚胆囊无功能者。胆囊息肉小于10mm者一般建议随访，大于10mm或短期内迅速增大或伴有明显症状者应行胆囊切除。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我科特色：腹腔镜胆囊切除术已是胆囊结石治疗的金标准，我科每年实行腹腔镜胆囊切除术约2000例，大部分采用当天入院当天手术，次日出院的日间手术模式。对于胆囊息肉患者，我们制定了严格的鉴别筛查和随访流程，确保在最恰当的时机进行手术治疗。对于合适病例我们还进行经脐单孔腹腔镜胆囊切除。B超定位下的经皮胆囊穿刺（PTGBD）也是我科的特色之一。对于无法耐受手术或胆囊炎症状严重的患者，可先行PTGBD穿刺控制胆囊急性炎症，待炎症消退后再行腹腔镜胆囊切除术。</span><br />
