<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>揭秘汤淼治疗团队 妙手回春曾救助过受伤王燕</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在中国男排主教练周建安探望汤淼之前，利用在上海采访体操世界杯的机会，记者曾经专门到上海长征医院进行了探访。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海长征医院历史优秀，他准确的称呼是中国人民解放军第二军医大学第二附属医院，曾经是著名的“上海同济医院”。&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医院靠近繁华的人民广场，南面就是上海的南京路，交通非常便利。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">汤淼在俄罗斯受伤后，上海长征医院得到通知，立即组织专家组前往俄罗斯，将汤淼接回了国。在此期间，急救科单红卫副主任、骨科史建刚副教授、呼吸科陈吉全副教授等组成的转运医疗小组一路保护着汤淼。记者了解到，史建刚副教授的医疗专长正是颈椎病、腰椎病等。身为医学博士，他长期从事脊柱外科临床工作和相关研究，对颈椎病、腰椎病的诊断和治疗有着丰富的经验。汤淼的另外一个重要医生名叫林兆奋，是急救科的副主任医师、副教授，他也是医学博士、硕士生导师，擅长治疗严重创伤并发症等。&nbsp;</span>
</p>
