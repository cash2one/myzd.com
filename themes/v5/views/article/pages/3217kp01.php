			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>浅谈保肾手术之四：术后注意事项</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">以前保肾手术术后需要卧床半月甚至更长时间才能下床活动，现在观念发生了改变，术后卧床3天甚至更早即可下床。但是下床活动，仅仅是指床边散步等，剧烈活动还是会增加术后出血的风险。所以现在还是建议术后3月避免剧烈活动，一般的散步是可以的。 &nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 术后最需要注意的事项是：及时发现和处理术后出血，因为术后出血在术后三月内仍有可能出现。术后出血的主要表现是血尿，少量出血仅表现为尿液颜色变红，严重出血者可排出大量鲜红色血块，出血量大时血块填塞于膀胱无法排出，表现为急尿却无法排出。还有部分患者出血时表现为腰腹胀痛，出血量大时会出现头晕眼花等症状。一旦保肾手术后的患者出现上述出血症状时，应立即与相关医疗机构联系，避免错过最佳处理时机。 &nbsp; &nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">保肾手术术后患者需要定期复查，复查的主要内容包括：影像学检查（肿瘤是否复发？），肾功能检查（肾脏功能如何？）。一般术后第一次复查安排在术后1-3个月，此后根据病情确定后续复查时间，肾癌患者需终生定期随访。</span><br />
