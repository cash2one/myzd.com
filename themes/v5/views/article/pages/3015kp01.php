			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>股骨颈骨不连怎么办？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;股骨颈骨折并不是罕见病，大雪封山、山体滑坡、飞来横祸、跌打损伤等等，都有可能导致股骨颈骨折。股骨颈一旦骨折，到了医院，医生第一句话就会告诉你，这个病有可能导致股骨头坏死，病人吓得不得了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 股骨颈骨折和人体四肢的其他部位骨折相比较，确实并发症比较多，因为这是一个特殊的部位，局部血管的天生的解剖比较特殊，因为股骨头是个终端，光溜溜的，股骨颈骨折后，那个头就断了供，所以容易发生的头坏死是第一位的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 当然，股骨头坏死的相关因素很多，除了解剖学上的天生易发以外，还有其它因素左右，比如和骨折后移位的程度也很相关，移位大的坏死率高，此外，手术和复位的时机，内固定的技巧等也是关键因素，总之，股骨颈骨折后应该尽早手术，复位的质量要高。术后半年之内应该尽量不下地，否则，头受压过度，也会坏死。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 说道这里，我话锋要转，因为，已经有点跑题了，股骨头坏死医生、病人双</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">方都很在意，了解的自然多，我说多了没意思。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 本帖的重点是讲解股骨颈骨折后骨不连的问题，就是股骨颈骨愈合，可以说，这个问题和前者同等重要。可是，为什么，现在这个问题很少被提及呢？我个人认为原因有二，一个是对60岁左右的股骨颈骨折病人，相当一部分被直接人工关节置换了，当然也就没有后来的股骨颈不连了。另一个是，现在，股骨颈骨不连治疗的手段和器械都很先进，比如以前，就用三根普通的钢针固定，把持力不够，也有用三翼钉的，骨折缝反而更大，股骨头内部也破坏的很严重，也有顶板固定的，口子切的老大，加上，手术过程中，缺乏牵引床和C型壁透视机器，这样手术的效果很难保证，术后发生骨折固定不牢，位置复位不好，最终股骨颈骨不连的挺多。今非昔比了，目前内固定多用特制的标准空心钉，手术的配套设备也很好，局部微创，病人受罪少，骨头复位好固定牢，当然，骨不连的发生率明显降低。这样一来，股骨颈骨不连，当然提得人就少了，就像我们现在很少提旧社会地主算盘响，收租逼债来抢粮。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 可是，我国地大物博，人口众多，各地的医疗保障水平是不一样的，股骨颈骨不连确实存在，一旦发生，治疗比较棘手，很多患者不知所措，很有介绍的必要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;目前各种治疗的方法都有，常见的是各种带血管的骨瓣移植手术，效果评价不一。我这里强调的是，一个历史悠久而被人忽视的技术，即粗隆下外展截骨术（附图），只要措施的当，效果优良。迄今为止，这一方法，在欧美国家仍然是公认效果最好的手段。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 粗隆下外展截骨的技术原理有三个，一个是可以恢复因为股骨颈不连短缩、下沉导致的腿短缩；二是，能让股骨颈的断面从斜向改变为水平位，消除断端间的剪力，有益于骨不连愈合；三是，能紧张臀中肌，改善患者的跛行步态。因为这些术语太专业，我就不费笔墨了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;其实，对此类手术，我并非第一次介绍，在另外一篇文章中，我已经泛泛的做了介绍，本文只是这类方法的一个具体描述，其行话叫做，病灶旁截骨术。就是，对骨不连的位置不做处理，而是在病灶的旁边，做截骨，达到治愈的目的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;粗隆下外展截骨治疗股骨颈骨不连，历史悠久，效果确实，今日重新描述这一技术，目的在于，当新技术、新方法涌现时，我们不应忽视或忘记那些行之有效的所谓“旧”方法。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 革命导师，列宁说，忘记过去，就意味着背叛。</span><br />
