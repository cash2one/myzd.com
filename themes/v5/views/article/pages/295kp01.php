			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>乳腺派杰氏病</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 乳腺paget’s（派杰氏）病，其本质是乳腺癌的一种类型，与常见的乳腺癌不同主要是因为它发病于乳头乳晕区，临床表现为乳头乳晕区的反复脱屑、渗液、糜烂、结痂破溃、刺痛等，表面上与乳头乳晕皮肤的湿疹表现有相似之处，故又称为乳腺湿疹样癌。但该病与真正的湿疹区别在于前者乳头和乳晕会逐渐破坏、呈鼠咬状、无法自愈，病程长者甚至出现乳头乳晕的完全缺失，而湿疹则不同，而且针对湿疹治疗后症状可以改善。Paget’s病患者除乳头乳晕区的表现外可伴有或不伴有乳房内的病灶，不伴有乳房其他病灶的单纯的paget‘s病属于非常早期的乳腺原位癌，可以通过手术治愈。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 很多患者发现乳头瘙痒脱屑就很紧张是否患了乳腺癌，上网搜罗了一堆资料越查越害怕。乳房paget’s病虽然呈现湿疹样的改变，但和湿疹还是有差别的。很多妇女都患有小叶增生，由于月经周期、情绪紧张、工作压力等影响，有时会感觉乳头区瘙痒，但通常都没有乳晕区的其他症状，这种瘙痒一般能够自行缓解。当瘙痒明显，伴有乳头乳晕处脱屑，严重时候还可能引起皮肤的增厚、皮疹，则这些症状首先考虑乳头乳晕区湿疹，对于这类疾病建议注意乳头乳晕区的清洁，尽量穿戴棉质内衣和内衬全棉的文胸，避免长时间穿戴不透气或质地较厚的文胸。对哺乳期妇女而言更需要注意哺乳前后的乳晕区清洁，湿疹、乳腺炎都是哺乳期好发的疾病。这些疾病虽然发生于乳房，但其实属于皮肤的疾病，可以通过针对性治疗治愈。这类良性的疾病除外部因素，例如抓破或感染等，一般较少引起皮肤的缺损，并且有一定的自愈性。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 当然如果出现类似的乳头瘙痒、刺痛、糜烂、渗出等情况时，我们不能凭这些来自我诊断，容易产生误诊，建议尽快到正规医院乳腺外科就诊，请乳腺专科医生判断，从而避免延误病情。临床上当这些症状不易愈合，或皮肤科针对性治疗短期内不能好转，尤其是存在乳头乳晕区皮肤的破坏，甚至出现缺损时应引起高度警惕了，应首先排除派杰氏病，乳腺的影像学检查如常规的乳腺超声、钼靶检查可以提示有无乳腺内是否合并有其他乳腺癌病灶存在，以利于乳腺癌伴派杰氏病的诊断。对于可疑的乳头处皮肤病变可进行刮片细胞学检查或活检病理学检查，若发现paget’s细胞则可以明确诊断本病。paget’s病也可能发生于乳头乳晕以外的皮肤区域，而非乳头乳晕区的paget’s病属于皮肤恶性肿瘤的范畴，和乳腺癌还是有一定的差别，处理方式也不同。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 乳房paget’s病在各年龄层的妇女中都可能发生，理论上男性也同样会发生，但实际临床上较为罕见，它和普通的乳腺癌一样并不会引起人群间的传染。一旦诊断乳房paget’s病不用过于慌张，其分期主要看乳腺内伴发的乳腺癌的情况而定，如果仅单纯的乳头乳晕paget’s病不伴乳腺内癌灶、或仅伴发同侧乳腺内导管内癌成分，则属于原位癌，是非常早期的乳腺癌，绝大多数腋窝淋巴结不会转移，原则上手术后不需要化疗（可能需要其他治疗，如内分泌治疗），多数及时就诊的患者属于这一类；如果同时伴发乳腺内浸润性癌，分期则以该浸润性癌的大小和淋巴结状态综合分期，可能需要进一步依据该浸润性癌的分子分型依照治疗指南进行综合治疗（可能需要化疗）。手术治疗是首选治疗方法，由于病灶发生在乳头乳晕区，也可能累及到后方的大导管，或同时有部分患者合并有乳腺内癌灶，一般不建议行保乳手术，对于有外形要求的患者可考虑全乳切除、前哨淋巴结活检加乳房重建，如果前哨淋巴结没有转移，原则上不必淋巴结清扫。国外也有进行派杰氏病保乳手术的小样本临床研究，但目前尚为达成共识。</span><br />
