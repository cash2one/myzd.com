	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>儿童耳聋的病因和治疗方法</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">病案介绍：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 患儿朱某，4岁，出生时即有双眼亮蓝色虹膜，后发现患双侧感音神经性聋，目前可观察到颜面部少量雀斑分布。诊断为Waardenburg 综合征，在解放军总医院耳鼻咽喉头颈外科由科主任杨仕明教授主刀给予人工耳蜗植入治疗。目前听觉言语康复效果佳，上正常幼儿园。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">导致孩子耳聋的原因主要有以下几种：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一、综合征性耳聋：临床上常见的综合征性耳聋包括大前庭水管综合征，Usher综合征，阿耳斯特雷姆综合征，以及前面病案中提到的Waardenburg 综合征等等。这部分患儿常表现为包括耳聋在内的身体不同器官的一组临床表现。以Waardenburg 综合征为例介绍，Waardenburg 综合征(Waardenburg syndrome，ws)是临床上常见的引起综合征性耳聋的疾病之一，遗传方式为常染色体显性遗传，至少有2％的先天性耳聋由Waardenburg综合征引起。该病的发病机制为神经嵴起源的黑色素细胞缺失造成的神经嵴细胞功能异常而导致的症候群。Waardenburg综合征主要表现为感音神经性聋及色素异常，后者包括虹膜异色、白额发、早白发、皮肤色素减退或雀斑沉着；其他表现还有内眦异位、高宽鼻根、多毛症、一字眉或眉毛中部潮红等。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：疾病大多为遗传性，耳聋多表现为双侧重度感音神经性聋，治疗可根据耳聋程度选择助听器或植入人工耳蜗并收获一定效果。解放军总医院耳鼻咽喉头颈外科听觉植入中心为1300余例重度耳聋患者成功植入人工耳蜗，其中包括很大一部分综合征性耳聋的患儿，效果明确。16名Waardenburg综合征患儿在我科接受耳蜗植入手术并收获良好听觉言语康复效果。产前诊断可避免一部分综合征性耳聋患儿出生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">二、药物中毒：一些研究表明， 目前耳毒性药物已成为造成儿童听力损伤的主要原因之一。致聋的药物主要包括氨基糖甙类、水杨酸类、抗肿瘤类、酒精类。常见的有链霉素、双氢链霉素、新霉素、卡那霉素、庆大霉素、万古霉素、紫霉素、托布霉素、巴龙霉素、多粘菌素B、奎宁、阿斯匹林等，此外还有部分抗肿瘤的药物和利尿剂也会伤害听神经，引起听力障碍。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：母亲在孕期(特别是怀孕3个月以内)就应当禁用一些损害胎儿内耳的药物。如链霉素等。必须使用耳毒性药物时，应密切观察。特别是注意有无耳鸣、头晕等现象。做到早期发现，早期采取措施。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">三、幼儿耳朵生理结构特点引起耳聋：幼儿在伤风感冒、以平仰位置吸吮乳汁或恶心、呕吐时，常常会引起中耳腔的细菌感染，发生急性化脓性中耳炎。当中耳腔内脓液不断增多时，孩子会因耳痛加剧而大哭．中耳腔内脓液增多的压力也会引起鼓膜穿孔、破裂。如果不及时就医或治疗不彻底，会造成慢性化脓性中耳炎，致使鼓膜不断遭到破坏。穿孔越来越大，对听力的影响将日趋严重。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：急性中耳炎是中耳黏膜的急性化脓性炎症。感冒后咽部、鼻部的炎症向咽鼓管蔓延，致病菌乘虚侵入中耳，引起中耳炎。常见的致病菌主要是肺炎球菌、流感嗜血杆菌等，因此预防感冒就能减少中耳炎的发病几率。擤鼻涕方法不正确也可导致中耳炎。有的人擤鼻涕时往往用两手指捏住两侧鼻翼。用力将鼻涕擤出。这种擤鼻涕的方法不但不能完全擤出鼻涕，而且很危险。鼻涕中含有大量的病毒和细菌，如果两侧鼻孔都被捏住。则压力迫使鼻涕向鼻后孔挤出，到达咽鼓管引发中耳炎。正确的擤鼻方法。是用手指按住一侧鼻孔，稍用力向外擤出对侧鼻孔的鼻涕，用同法再擤另一侧。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">四、噪音：相对于成人，孩子们更容易受到噪声的伤害。并且在没有任何痛苦的情况下使他们的听力逐渐减退。这是由于，高分贝的噪声过度刺激了孩子们内耳里弱小但却非常精密的“感应接收器”，一旦“接收器”受到损伤，就再也不能把声音传送到大脑了。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：避免使孩子长时间处于啃杂环境中，避开生活中常见的噪音污染源，比如电视，或者高音量的立体声音响。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">五、耳周病变：耳朵周围邻近器官的病变，有时也会涉及到中耳腔，从而引起听力减退。如鼻炎、副鼻窦炎、扁桃体炎、腺样体肥大等。在儿童传导性耳聋的病因中，腺样体肥大是一个极其重要的因素。腺样体因反复炎症刺激而发生病理性增生肥大，可堵塞并压迫外侧的咽鼓管咽口引起分泌性中耳炎，出现传导性耳聋及耳鸣症状，导致患儿听力下降。若治疗不及时，患中耳炎时间太长，虽然切除了腺样体，听力也不能恢复正常。故小儿腺样体肥大一旦影响全身健康或邻近器官者，值得引起重视。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：当发现此类疾病时，家长要带孩子去医院及时治疗，绝不可掉以轻心。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">六、耳外伤：原因包括：孩子淘气时，有些父母盛怒之下，给孩子几个巴掌；喜庆佳节，燃放的鞭炮突然在孩子耳边爆炸，巨大气浪直冲耳道内鼓膜；游泳时，孩子一侧耳朵先撞击水面。以上外伤都会造成鼓膜破裂、穿孔，直接造成听力减退。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：耳朵不能揪也不能打。更不能往耳里塞放豆子、小球等类物品。过节放鞭炮时。家长应嘱咐孩子站远些。以免爆炸气浪损伤孩子鼓膜。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">七、病毒感染：流行性腮腺炎并发感音神经性耳聋，大多发生在学龄前及学龄儿童，是儿童单侧感音神经性耳聋常见病因之一。听力检测腮腺炎致耳聋患儿以单耳极重度和全聋多见，其次为单耳部分听力下降，双耳聋少见。其典型临床表现为腮腺炎发病后约4 5d后出现听力下降，同时可伴眩晕、耳鸣。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：儿童流行性腮腺炎并发感音神经性耳聋应遵循早期综合治疗的原则。治疗措施包括抗病毒、改善内耳微循环、营养神经、高压氧等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">八、先天性耳部发育畸形：包括耳廓、外耳道、中耳、内耳先天性发育畸形，会造成不同程度的传导性耳聋和感音神经性耳聋。内耳畸形是儿童先天性感音神经性耳聋常见致病因素之一。内耳畸形包括骨性畸形、膜性畸形以及细胞水平的异常。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp; 应对策略：在儿童耳聋病因确诊过程中，行颞骨CT和MRI检查尤为重要。明确儿童耳聋的病因对其相关治疗方案的选择，尤其对人工听觉植入手术的预后，手术禁忌证，及装置的选择都有重要的指导意义。人工耳蜗植入是目前治疗先天性内耳畸形导致重度感音神经性耳聋的最佳方法，此外还可根据患儿耳聋性质和耳聋程度选择其他听觉植入装置，如振动声桥（VSB），骨锚式助听器（BAHA）等等。VSB和BAHA是最近一两年才进入我国的听觉植入装置，我科主任杨仕明教授和其带领的听觉植入团队在前期做了大量的手术方式探索和适应证选择的工作，并由杨教授亲自操刀为数十名患者成功完成手术。</span>
</p>
