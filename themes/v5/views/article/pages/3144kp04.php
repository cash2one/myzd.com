		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>Vibrant SoundBridge：小耳畸形患者听力重建的新手段（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;小耳畸形是耳部的先天性畸形，通常表现为耳廓的发育不良或者缺失，同时伴发外耳道狭窄或闭锁、中耳结构畸形。小耳畸形患者的耳聋大部分是传导性耳聋。小耳畸形在欧美地区发病率低于亚洲地区，我国每年新增大量的小耳畸形病例。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 小耳畸形患者的治疗包括两个方面：耳廓再造和听力重建。单侧小耳畸形和双侧小耳畸形的患者，其治疗方案略有不同。双侧畸形的患儿，必须尽早地进行听力重建，以保证患儿言语功能的正常发育。因此双侧畸形患儿，应该尽早进行听力干预，然后在10-12岁之间进行耳廓整形及再造。单侧畸形患儿，其言语功能通常能正常发育，因此听力重建不是那么紧迫，可以择期进行；耳廓的整形通常也在10-12岁之间进行。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp;&nbsp;听力重建是双侧小耳畸形患儿的治疗中最重要的一环，因为它关系到患儿的语言发育，并对日后的学习、工作能力造成长远的影响。传统的听力重建术（即外耳道重建和鼓室听骨链的重建）因存在以下不足，目前已逐渐被新的听力重建技术取代：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">-&nbsp;只适合于小耳畸形评分（Jharsdorfer评分）在7分以上的患儿（8分以上效果较好）。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">-&nbsp;如果先进行外耳道重建及鼓室成形，破坏了耳廓重建区的皮瓣，因此日后重建耳廓的位置必须后移，从而影响耳廓美观。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">-&nbsp;重建的外耳道及听骨链，术后可能出现闭锁或者固定、脱位，造成听力再次下降，从而需要再次全麻手术修复。尤其是小耳畸形评分在7分以下的患儿，该可能性更大。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 综上所述，通过传统的外耳道和鼓室听骨链再造来重建听力，主要存在两方面的不足：影响后续的耳廓重建；可能需要反复多次手术。目前小耳畸形治疗领域的一个探索方向，就是寻找一种新的方式进行听力重建，既不影响后续的耳廓重建，又不需要反复手术进行外耳道和鼓室听骨链的重建。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 同仁医院从2010年5月开始，陆续给很多小耳畸形的患儿植入了Vibrant SoundBridge(振动声桥)。这是一项新的技术：它能重建听力，但不影响日后的耳廓重建，同时也不需要反复手术重建外耳道和鼓室。那么，振动声桥是一个什么样的医疗设备？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 振动声桥由处理器Amade和植入体VORP两部分组成。Amade（图1）是处理器，它的作用是把声音信号编码为电磁信号，跨过皮肤发送到VORP。VORP（图1）是植入体，它的作用是根据接收到的电磁信号，驱动漂浮质量传感器（FMT）产生振动。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 植入了振动声桥之后，小耳畸形的患儿这么听到声音的：外界的声音，被处理器的麦克风采集，然后被编码成精密的电磁信号，跨过完整的皮肤发送到植入体。植入体接收到信号以后，就指令漂浮质量传感器FMT（植入在内耳的开口处，图2）振动。因此振动不用经过外耳道和鼓室听骨链，就直接传入了患儿的内耳——患儿从而听到了清晰而自然的声音。同仁医院采用特殊的手术技术，在植入声桥时完整地保留耳廓重建区的皮瓣，使患儿日后的耳廓能重建在正常位置；同时，在整个重建听力的过程中，不需要重建外耳道和鼓室听骨链，患儿术后因此不会再面临反复手术的风险。很多小耳畸形的患儿，都在同仁医院植入振动声桥重建了听力，而且日后的耳廓重建不受任何影响。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;&nbsp;&nbsp; 振动声桥在欧美已经临床应用10余年，在全球已有数千名植入用户；2010年5月进入中国，在小耳畸形患儿群体中得到了迅速的应用。中国地区的小耳畸形发病率和患者总量都远高于欧美地区。在这样的病群背景下，在小儿畸形的振动声桥植入领域，我们同仁医院不论在病人数量和手术技术，都已位于国际前列。我们希望国内的患儿能早日得到治疗，健康成长。</span>
