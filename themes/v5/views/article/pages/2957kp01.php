		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻窦炎的预防需要做到哪些（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻窦炎的手术治疗是在内镜下的微创手术治疗，目前的治疗效果在90%以上，总有5%-10%左右预后不是太好，这部分的病人可能跟他的体质以及免疫系统有关，但是，具体的病因还不是很清楚，这是我们要研究的部分，为什么总有那么一小部分病人没办法治愈，我们叫他们难治性鼻窦炎，有鼻窦炎、鼻息肉、哮喘，这部分病人治疗效果会比较差，大多数急性鼻窦炎通过合理的药物治疗都可以在短期内痊愈。也有一部分是难治性鼻窦炎患者预后不是特别好。原因是比较复杂的，可能跟个人过敏体质、免疫系统都有关。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻窦炎的预防需要做到哪些?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">对于像慢性咽喉炎、鼻炎等一些邻近组织的感染病症要积极的做好预防和治疗的工作。日常生活中要做到有规律的饮食起居，要尽量的戒烟戒酒，平时多注意口腔的卫生。平多锻炼身体，提高自身免疫力，增强抵抗力。平时工作学习居住的环境要经常保持通风换气。多吃水果蔬菜，避免辛辣刺激性的食物。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在鼻窦炎的治疗或预防方面的建议或心得</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1、国内内镜手术适应症可能放的比较宽，大家应该严格的掌握手术的适应症。2、有一部比较难治性的鼻窦炎应该投入更多的科学研究。对于难治性鼻窦炎的病因病理研究应该更加加强。3、目前内镜手术开展的比较普遍，应该更加严格要求技术人员的培训。</span>
