			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>肩痛科普</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩痛是中老年人群中常见的病症，疼痛会影响肩关节活动，夜间疼痛会影响睡眠，而且肩痛的病程常常很长，会持续数周甚至数月。我们平时认为肩痛主要原因是肩周炎或者颈椎病，其实引起肩痛的原因有很多，这里，让我们来说说“肩痛”那些事！</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩痛可能是肩部疾病引起的，也可能不是肩部疾病引起的，哪些不是肩部的疾病会引起肩痛呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、颈椎病</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">较常见，颈椎病引起肩部疼痛范围较广，疼痛不适最明显会在颈肩结合部或肩背部（如图），疼痛会向整个上肢放射，甚至会放射到手部，引起手指麻木。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、肺癌</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不常见，但每年在我们肩关节专科门诊都会发现， 因为肺尖癌压迫或侵犯臂丛神经时，会出现该侧肩部及上肢放射状灼热疼痛。但是这类患者的肩部一般是没有压痛的，而且肩关节活动也基本不会受限。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、胆囊炎</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不常见，胆囊炎引起的牵涉痛会导致右肩背的疼痛，如果左肩疼痛就肯定不是胆囊炎引起。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、冠心病</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不常见，冠心病会引起左肩的放射痛，这种放射痛不是一个点的疼痛，而是从肩膀到手臂的一个面，这种疼痛常常伴随着压迫感，大多令人感到沉重不安。但如果是右肩疼痛就不会是冠心病引起。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，绝大多数的肩痛是由于肩部本身的疾病引起。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩关节是我们人体中活动度最大的关节。它是由肱骨头、肩胛盂和围绕它们周围的肌肉、肌腱（肩袖）和关节囊等组成。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩关节内任何结构出现问题，就可能引起肩部疼痛或不适，因此，许多人一旦出现肩痛就会自我诊断为“肩周炎”是完全错误的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“肩周炎”这个诊断是140多年前一位美国医生（1872年Dr.Duplay）提出的，当时对肩关节结构的研究不多，而且没有CT、MRI等检查设备，因此他把肩部疼痛原因同归为“肩周炎”，但随着对肩关节研究的逐渐深入，发现“肩周炎”这个诊断是极其不准确的，最新版的医学教科书上已经取消了“肩周炎”这个诊断。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">那肩部有哪些疾病会引起肩痛呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、肩袖损伤</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">最常见，肩袖是围绕肩关节的四条肌腱的总称，这四条肌腱像个袖口一样包绕肱骨头，所以称之为肩袖。肩袖的主要作用是带动肩关节的活动，还有稳定关节的功能。肩袖比较容易损伤，有文献表明，在60岁以上人群中，肩袖全层撕裂的比例可达到30%，肩袖上本身没神经，因此许多人即使肩袖撕裂可以没有症状，只有因为肩袖损伤后引起肩关节周围力偶不平衡，引起炎症，才会出现疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩袖撕裂的疼痛常常会向上臂的外侧放射，但不会超过肘关节。肩袖撕裂的疼痛一开始不剧烈，主要是做某些肩部活动时出现。随着病情的严重，会出现夜间痛、静息痛，影响睡眠，而且还伴有上举无力甚至肩关节活动受限的症状。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、肩峰撞击征</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">较常见，一说起肩峰撞击征，许多人会认为说“我并没有和别人撞击，怎么说我是撞击征呢？”。其实，肩峰撞击征是指上臂的肱骨大结节和肩胛骨的肩峰撞击导致产生炎症引起肩痛。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">许多人在上举活动肩关节时能听到或感受到肩部的“咔、咔“的声音，这其实就是肩峰撞击的表现，但是只有反复撞击产生肩峰下滑囊炎症才会引起疼痛。撞击征有可能是肩袖撕裂的一个原因，疼痛初时不重，主要是上举内旋肩部活动时出现。随着病情的严重，会出现夜间痛、静息痛，影响睡眠，甚至肩关节活动受限的情况。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、肱二头肌长头肌腱炎</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">较常见，它多发于45岁——55岁，有人认为它可能是“冻结肩”的始动因素。它常常与撞击征和肩袖损伤并发。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肱二头肌长头肌腱炎病人常常主诉肩部前上方的疼痛，疼痛可沿着上臂的前方向下延伸到上臂的二头肌肌腹，常常会出现夜间疼痛，肩关节外展、外旋、后伸时（在前座向后伸至汽车后座拿物）可出现肩前方疼痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、冻结肩——我们老百姓说的“肩周炎”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这是“一种没有明确病因的、排除肩关节故有疾病的疾病，表现为肩关节主动和被动活动均明显受限。”——冻结肩的定义。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">冻结肩的疼痛主要为突然活动时的剧烈疼痛，而且会有较严重的夜间痛，而且一定伴随肩关节的活动受限，尤其外旋和上举。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果您的肩能完成（上图）这个动作，那您的肩关节再痛也不会是“冻结肩”——也即是传统说的“肩周炎”！</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、钙化性肌腱炎</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">疼痛是钙化性肌腱炎最主要的症状，在钙化形成期，疼痛一般较轻，而在钙化吸收期时的急性疼痛是非常剧烈的，以致病人因为疼痛拒绝活动肩关节，而不自觉地将手臂紧贴胸口，但一般急性期只持续2—3周。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其他引起肩部疼痛的肩部疾病还有“肩锁关节炎”、“盂肱关节骨性关节炎”、“肩关节不稳定”、“肩关节内游离体”等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，如果您的肩痛已经持续一段时间的话，请您不要自我诊断，最好寻求专科医生，以免误诊、误治！</span><br />
