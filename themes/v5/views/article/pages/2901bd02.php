			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>微血管减压治疗老年三叉神经痛患者的安全性</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微血管减压（MVD）是三叉神经痛（TN）患者最有效的治疗方法。TN的发生率随年龄的增大而增加，因此老年患者的安全性是手术医师面临的问题。美国学者最近通过国家数据系列，分析患者年龄对MVD手术并发症发生率的影响。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 通过回顾1999年到2008年住院患者（NIS）,统计患者在住院期间的并发症，包括死亡率、并发症、住院时间及费用等。并发症包括急性疾病。本研究采用2个老年年龄时间点，65岁和75岁。共3273例TN患者作MVD，年龄6~93岁，平均为57岁。超过65岁患者占31.5%，超过75岁患者占10.9%。65岁以下的TN患者为2241例，65岁以上患者为1032例。75岁以下患者为2923例。75岁以上患者为350例。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. &nbsp; &nbsp; &nbsp; 以65岁为分界 &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">65岁以下患者的死亡率为0.13%，65岁以上患者死亡率为0.68%（P=0.0087），后者是前者的5.1倍，并发症的发病率：心脏（0.95%对1.94%，P=0.0087），肺脏（1.09%对2.23%，P〈0.0001），血栓性疾病（0.27%对1.16%，P=0.0097），脑血管病（1.38%对3.29%，P=0.0003）。年龄与手术后颅内感染无明显关系。65岁以下患者平均住院时间3.3天，65岁以上的为4.0天（P〈0.0001）。 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. &nbsp; &nbsp; &nbsp; 以75岁为界</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;75岁以下患者住院死亡率为0.21%，75岁以上为1.16%（P=0.0021），后者是前者的5.6倍。并发症的发病率：心脏（0.79%对2%，P=0.0244），肺脏（1.13%对4.0%，P〈0.0001）。血栓性疾病（0.39%对2%，P〈0.0001），脑血管病（1.74%对34%，P=0.0044）。年龄与术后感染无关。住院天数老年为4.3天而非老年为3.4天（P〈0.0001）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;微血管减压（MVD）是治疗TN最有效的方法。尽管最近有报道随年龄增长并不增加MVD的风险，本文通过大数据病例分析得随着年龄的增长，MVD的死亡率和并发症明显增加，虽然发生率仍较低，仍应引起临床医师的注意。</span><br />
