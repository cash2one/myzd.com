		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>治疗消化道早期癌和巨大息肉有新招 ——内镜黏膜下剥离术</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">近年来随着人们健康意识的增强和消化内镜检查的普及，消化道早期癌和癌前病变——息肉的发现迅速增多。对于消化道息肉和早期癌，传统的治疗方法是外科手术切除，疗效确切，但手术创伤大，患者恢复慢，住院时间长，治疗费用高，手术后生活质量也大为降低。近年来随着内镜治疗器械的不断开发和新技术的逐步开展，内镜发现的大部分消化道病变可以实现内镜下的切除，而不再需要传统的外科手术治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 如果内镜发现的病变很小或像香菇一样有茎（长蒂），可以很简单的应用圈套器套住蒂部切除，这叫息肉切除术。有的病变长得很平像皮萨一样，就必须先在病变下方注射生理盐水抬起病变，再用圈套器电切病变，这叫黏膜切除术。但对于超过2厘米的平坦病变，黏膜切除术只能通过分块切除的方法来进行，其结果是切除了较大病变但有病变的遗漏，肿瘤很快就会复发，切下来的病变破碎也不能进行准确的切片化验。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 为了达到完整的切除肿瘤、减少肿瘤复发、微创根治消化道早期癌的目的，内镜黏膜下剥离术（ESD）应运而生。内镜发现肿瘤后首先用染色剂使肿瘤着色，再在肿瘤周围做一环状标记，在肿瘤下方注射生理盐水使病变明显抬起。沿病变边缘标记点切开正常黏膜，使肿瘤与周围正常组织分离，再用特殊的剥离器械将肿瘤从其下方的正常组织上完整切除下来。目前在日本，绝大多数条件具备的医院已普遍开展ESD，而国内仅极少数医疗单位才刚刚开始尝试ESD治疗工作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 对于没有转移的消化道局部病变理论上都可以进行ESD切除。现时ESD主要治疗以下消化道病变：（1）早期癌：根据医生经验，结合染色、放大和超声等其它内镜检查方法，确定肿瘤局限在黏膜层和没有淋巴转移的黏膜下层，ESD切除肿瘤可以达到外科手术同样的治疗效果。（2）巨大平坦息肉：超过2厘米的息肉尤其是平坦息肉，推荐ESD治疗，一次、完整的切除病变。（3）黏膜下肿瘤：超声内镜诊断的脂肪瘤、间质瘤和类癌等，如位置较浅（来源于黏膜肌层和黏膜下层），通过ESD可以完整剥离病变；如肿瘤较深（来源于固有肌层）， ESD剥离病变的同时往往伴有消化道穿孔的发生，不主张勉强剥离，有丰富内镜治疗经验的医生可以尝试运用。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; ESD治疗后一般要住院观察是否有发热、腹痛和消化道出血。第2天如果没有腹痛和出血，可以下床活动，进软食，住院3～5天出院。2个月后复查内镜了解伤口愈合情况，局部是否有肿瘤的残留，以后每年复查内镜了解病变是否有局部复发。和其它内镜治疗一样，出现和穿孔是ESD治疗的两大主要并发症。一般来讲，ESD术中都有少量出血；术后出血通常表现为呕血或解血便，一般术后1周左右出现，内镜下电灼或止血夹夹闭出血点可以有效止血。ESD治疗中发生的穿孔一般较小，也能及时发现，内镜下应用金属夹可以缝住穿孔，一般不需要剖腹手术修补。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 复旦大学附属中山医院内镜中心于2006年在国内率先引进ESD技术，至今已完成500多例消化道病变的ESD治疗，是国内开展时间最早、治疗病例数最多、能同时对早期食管癌、早期胃癌、早期大肠癌进行ESD治疗的医疗单位。总结经验，我们体会ESD治疗具有以下独特优势：（1）微创治疗。既能保证肿瘤的彻底切除，又能最大限度的保留正常消化道，生活质量明显提高。由于内镜治疗创伤小，病人易耐受，住院时间短，有些病人治疗后当天就可回家。（2）可以把较大面积、形态不规则的肿瘤一次完整切除下来，明显减少肿瘤的残留和复发。（3）可以在不开刀的情况下切除病变，获取完整的切片化验标本，明确肿瘤的性质和程度。（4）同一病人可以多次接受ESD治疗，一次也可以同时切除多个病变。</span><br />
