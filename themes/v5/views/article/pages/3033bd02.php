		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>一封迟到14年的回信能记起多少流年？</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1991年</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 一名胃癌患者在华西医院接受胃切除手术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1999年</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医生发出随访信，了解患者术后情况</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2013年</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 当年写信的医生见到了患者对这封随访信的回信——</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 近日，新浪微博网友 “舞w冰”的一则微博，在不少华西人心中激起层层涟漪。一名胃癌患者在1991年做过手术后，为回访愈后效果，医院于1999年发出一封随访信，那时的称呼都还是“同志”。然而，由于病人搬家，信被朋友代收了，直到上个月朋友见面才有机会交还这封信。如今治疗手段更新变换，曾经的青葱医生已然成长为医院的中流砥柱，看到泛黄的信纸，医生们不禁唏嘘不已……</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 有一种意外迟到14年</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 原来并非遗忘，而是光阴荏苒，地址更迭，才让这封本该在14年前到达的信件姗姗来迟……</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 昨日，华西医院第二住院大楼8楼，天府早报记者见到了这封迟到了14年的随访信回信：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 信纸有些泛黄，折痕很深，由于年代久远，摸上去甚至有些发脆，然而信封的邮戳，回复的字迹却是崭新的。开头那句“您于（19）91年1月在我院接受了胃切除手术”，将人们的记忆瞬间拉回上个世纪的华西医院。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “那是我们1999年发出的随访信，我们也是前几天才收到（回信）的。”发信人是四川大学华西医院胃肠外科中心博导胡建昆教授，1999年发信时，他刚升为主治医师，如今已经是华西医院普外科胃肠外科专业的中流砥柱。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 胡建昆说，看到这封随访回信，他本人也惊讶了许久，“时间太久远，不仅是患者，就连我自己都快记不得曾经发过这样一封随访信了。”然而，读到病人的亲笔回信，胡建昆心里不由泛起阵阵暖意。原来并非遗忘，而是光阴荏苒，地址更迭，才让这封本该在14年前到达的信件姗姗来迟。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 得知曾经的病人术后22年健康依旧，作为医生，胡建昆心中倍感欣慰：“这封迟到的回访信也告诉我们，这个胃癌患者已经临床治愈。”因为恶性肿瘤手术患者5年以上生存就表示临床治愈。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 有一种书信曾让人落寞</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 信件内所有的手写文字，是无数个夜晚医生们一笔一划写上去的，然而回信的人却并不多……</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 1999年发信时的情景，胡建昆仍记忆犹新，“科室的老主任带着我们四个新人一起做（随访），给400多位病人发去了随访信。”信件内所有的手写文字，是无数个夜晚医生们一笔一划写上去的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 然而，在胡建昆看来，这还并非最难的事。“我们要把过去所有患者的手术病历挨个整理，查完病例后，记录每一个病人的住址和联系方式，然后一个个通知。”随访的信件连同贴好邮票、写好回信地址的信封，一起装在医院专用信封内，寄往患者家中。“不过似乎回信的人数都不是很多。”说到这里，胡建昆的眼神有些许落寞，在他印象里，为了做深入的临床科学研究，医院曾于1993年、1999年、2004年，总共进行了三次大规模的病人随访，有些遗憾的是，回复的病人并不多。“收到的回信数量大概只占到了发出量的20%~30%，但根据要求，随访率要达到85%以上才有统计学价值。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 胡建昆表示，在中国，每年新发病的胃癌人数约为50万，对患者的治疗，还有许多方式方法需要改进，“我们需要从自己做过的手术中，做一个评价和改善。”他坦言，这个过程需要医生的努力，也需要患者的配合，“我想谢谢这位患者，他的回信，在很大程度上也是在造福后人。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 回信</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 因已搬家，迁至成都市芳沁街××号，最近返回川化，友人才将此信交予。一封迟 到 的 回信，抱歉！”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 医者，患者我们的关系可以不仅仅是手术台上一场冰冷器械的相遇济世救人，本就是一种温暖何况我们间还有一封书信是对温暖最好的维系尽管迟到了但只有书信才能穿过时间证明重新变得健康的生命，</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一晃，22年。</span><br />
