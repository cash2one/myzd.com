			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是脑动脉瘤？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">动脉瘤是动脉壁出现非正常的薄弱点并由此引起的动脉壁向外突出成球形病灶。这种血管壁的薄弱点如累及到动脉壁的全层称之为梭形动脉瘤（fusiform aneurysm）；累及到动脉壁的一层称之为囊性动脉瘤（saccular aneurysm）；而将动脉壁分离开称之为夹层动脉瘤（dissecting aneurysm）。其中，发生于脑部的动脉瘤破裂后的出血性中风可引起导致严重脑损伤甚至导致死亡。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑动脉瘤的发生率据估计为6-7%，即15个正常人中在其一生中会发展成为脑动脉瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 脑动脉瘤通常在其破裂后导致出血流入脑组织周围的腔隙内（蛛网膜下腔）才被发现。脑动脉瘤破裂后可以导致出血性中风、脑损伤甚至死亡。10-15%的动脉瘤破裂患者在到达医院以前就已死亡；超过50%的病人在出血后三天内死亡；而剩余的患者将有一半会残留永久性的神经功能损伤；动脉瘤好发的年龄为40-60岁；动脉瘤在性别发生比为女性：男性=3:2；20%的动脉瘤病人有多发的动脉瘤（2个以上的动脉瘤）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 与脑动脉瘤发生有关的危险因素有过度吸烟、酗酒和长期高血压。而患有纤维肌发育不良和多囊肾的人群中，其罹患脑动脉瘤的风险也升高。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">破裂动脉瘤的治疗的主要目的是放止再出血和减少再复发的危险。而未破裂的动脉瘤治疗目的是预防其破裂。</span>
</p>
