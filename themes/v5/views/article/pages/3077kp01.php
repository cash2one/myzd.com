			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>早期喉癌，选择手术还是放疗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">喉癌，以声带癌最为多见，会厌癌（声门上区癌）和声门下区癌相对较少。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早期声带癌的最早的（也是唯一的）症状就是声嘶。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">声嘶十分常见，其可能原因为：声带小结、声带息肉0000000000000、声带囊肿、声带乳头状瘤、声带白斑（喉癌前病变）、喉炎、喉癌。是否为喉癌，只有通过专科检查才能明确诊断，才能及时发现喉癌，才能早期诊断，早期治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一旦被确诊为喉癌，也不管是否是早期喉癌，病人及家属常常要求马上住院手术切除喉癌，但他们并不知道放疗同样能治好早期喉癌。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早期喉癌，到底是手术好，还是放疗更好？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前早期喉癌治疗存在误区：已确诊为早期喉癌，耳鼻喉科或头颈外科医生常常建议病人手术治疗，因为手术治疗效果好，放疗科医生常常建议病人行放疗，因为放疗效果很好，都说自已的治疗效果好。其实两种说法都正确，但也都有一定的片面性，不够客观。任何治疗都不会十全十美，手术治疗和放疗也一样，都有各自优缺点。医生应该很客观地把手术和放疗两种治疗方法的优缺点告诉病人及家属，让病人及家属再根据自身情况选择适合自已的治疗方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">放疗（俗称电疗）是目前治疗喉癌的有效方法之一，放疗设备和放疗技术越来越先进，为早期喉癌治疗创造了有利条件。目前普遍采用的放疗设备为直线加速器，所采用的放疗方法为三维适形调强放疗技术。喉癌以鳞状细胞癌为主，鳞状细胞癌分化程度分为好、中等、差，分化程度越好对放疗敏感性越差，分化程度差，恶性程度高，但对放疗敏感。喉鳞状细胞癌分化程度以分化中等多见，对放疗比较敏感。另外，团块型或外生型喉癌对放疗相对比较敏感，溃疡型坏死伴严重感染或水肿的喉癌对放疗的敏感性相对较差。目前放疗是早期喉癌的最好治疗方法之一，治愈率达90%。一旦放疗失败、放疗后复发，还能进行手术补救治疗，再手术切除喉癌。早期喉癌放疗的最大优点是：声音保持较好，因为没有手术，没有切除声带，所以没有手术所引起的较严重的声嘶现象。但放疗也有缺点：放疗期间会有放射性口腔粘膜炎，表现为疼痛，影响吃饭；放疗会引起产生骨髓抑制、白细胞下降，对人体免疫功能有一定影响。放疗远期并发症：颈部变硬；白细胞下降；饭不容易咽下（放疗后唾液腺功能受到严重破坏损伤，唾液腺分泌大量减少，因缺少唾液润滑作用，所以饭不容易咽下），要有些汤或水才能容易咽下；难忍的干燥性、萎缩性咽喉炎；,放疗还存在将来（放疗10年以后）诱发放射性恶性肿瘤的可能 。另外，放疗时间长（近2个月），放疗费用大，要3万～5万元。放疗能很好地保护发音功能，对发音要求很高的病人可选择放疗。全身情况差，不能耐受手术的病人，也可放疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术治疗早期喉癌的优点：治愈率高（90%以上），费用低。激光切除喉癌费用约5千～7千元，喉部分切除术约1万5～2万元。手术治疗早期喉癌治疗时间短，激光切除喉癌只需住院2天，喉部分切除术住院约10天～2周。手术治疗早期喉癌的最大的优点：没有放疗并发症及后遗症。手术治疗早期喉癌的缺点：声音嘶哑要比放疗明显得多。如果对声音要求不是很高、能耐受手术的病人，可选择手术治疗早期喉癌。</span>
</p>
