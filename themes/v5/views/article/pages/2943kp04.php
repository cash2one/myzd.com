<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>外科器械和外科技术的辩证关系</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">每一次生产力的发展必将导致一场生产关系的革命，从旧石器时代到新石器时代再到青铜器时代，从工业时代到信息时代… 每个时代的发展都来自生产工具的革命性变化。如果说科技是第一生产力的话，那么，生产工具是推动生产力发展的第一要素。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果没有纳米技术的发展和应用，那么，现在的计算机还是由晶体管组成的几十吨重的庞然大物，谁还能想象电脑会成为家用电器？笔记本电脑更是不可想象的东西… 随便举举例子便可以反复论证生产工具是生产力发展的第一要素这个论断。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医疗技术提高也离不开医疗工具——医疗器械、医药的发展。每个学科的发展都离不开医疗器械的发展和更新。放射治疗专业的产生和发展史其实就是放射治疗设备的更新史，从深部X线到Co60再到电子线、中子线等等，每一次更新都标志着治疗水平的一次跳跃性提高。心导管的产生和应用对心血管内科来讲是有划时代意义的；没有体外循环机器的产生很难想象心脏外科的存在，更不用说发展了…</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">外科器械对外科医生有多重要的意义不言而喻，然而，外科器械与外科医生的关系是辩证的！外科器械的产生往往来自外科医生的需求，其根源来自外科技术水平的提高而旧的外科器械已经不能满足这种需求了；新的外科器械的产生则可以刺激外科医生技术的提高。因此，对于一个不断挑战极限，追求进步的外科医生来讲，提高技术水平的关键在于对外科器械的熟悉和掌握以及技巧的提高。满足现状的外科医生永远达不到更高的境界。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">九十年代以前的耳鼻咽喉科被普外科医生瞧不起，因为他们普外技术稍差，只能做一些诸如扁桃体摘除、鼻息摘除、乳突根治术等等，内窥镜技术的发展彻底地改变了这种局面，功能性鼻窦内窥镜手术技术的提高使得鼻腔、鼻窦手术上升到另外一个台阶，原本不可能的手术成为可能，原本不精确的手术好像装上了精确制导的“魔手”，就连原本属于神经外科的垂体瘤也开始向耳鼻咽喉科倾斜，因为，高水平的耳鼻咽喉科医生经过鼻腔便可以施行垂体瘤切除，术后第一天就可以下床活动，比起经前颅窝开放性手术不知道轻松、安全多少倍！手术显微镜和电磨钻的应用使乳突根治术得到了改良，听骨链重建、耳蜗植入等等使得耳鼻咽喉科彻底告别“三炎一聋”还治不好的尴尬局面，使古老的学科焕发新的光彩。一切都是得益于外科器械的更新和发展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二十世纪五十年代以前，普外科医生对小血管、神经损伤无可奈何，这种需求促使显微外科器械的产生，显微器械的问世催生了显微外科专业，八十年代显微外科器械和显微缝线的更新使显微外科技术开始普及，血管和神经的重建再也不是显微外科医生的专利了，如今，一个普外科医生如果不掌握一点血管外科和显微外科技术或者理念的话，他已经不是一个出色的外科医生了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">试问，如果没有腹腔镜器械的发展和普及，微创外科能得到这么多人的青睐么？如果没有超声刀的应用，腹腔镜手术能普及吗？超声刀的使用不仅提高了腹腔镜外科的水平，也催生了甲状腺、腮腺微创手术的产生，使这个在1997年首先在国内介绍就被认为是“疯狂”的想法的技术逐渐被人接受。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">作为外科医生，如果不花点心思去研究熟悉外科器械，如果对新生器械一点都不感兴趣，那么，其手术水平是不可能提高到更高境界的。这就要求外科医生在实践过程中不断地去探索提高，去开拓新领域，去突破极限。局部晚期肿瘤的外科手术往往涉及联合脏器的切除和重建，神经血管侵犯时需要精细解剖甚至血管神经重建，使用常规器械是不可能完成的。只有想突破极限的外科医生才会去想办法解决问题，这就使促使其开始使用显微外科技术和器械，实际上，他的水平得到了提高，最终结果就是患者得益。而另外一些医生可能会采取回避的办法，让肿瘤残留，甚至关起来。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">固步自封的外科医生往往对新生事物不感兴趣，对新技术新器械第一反应是否定，然后拒绝。十年前，当欧美欧美学者开始使用腹腔镜进行直肠癌根治术的时候，许多老专家就提出很多“担心和忧虑”，现如今在欧美发达国家腹腔镜下直肠癌手术已经不是什么新鲜事了。同样是十年前，当耳鼻咽喉科医生采用鼻窦内窥镜行鼻腔恶性肿瘤切除术时，许多头颈肿瘤外科专家提出很多反对意见，如今这种反对的声音越来越小了，因为，人们已经发现，其生存率不低于甚至优于传统手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不管对于任何级别的外科医生，花点时间去熟悉常规外科器械，花点心思去认识新器械，花点精力去研究如何使“困难”变成“简单”，如何借他山之石为己用，花点功夫去探索如何使手术变得完美，都是有益的。当然，既不能被新器械抛弃，也不能被新器械牵着鼻子走。这才是辩证的。</span>
</p>
