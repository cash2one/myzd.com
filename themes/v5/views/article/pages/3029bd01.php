	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>通大附院：胸腔镜手术根治食管癌 国内领先视频直播</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">胸腔镜手术，是通过现代摄像技术和高科技器械，在微小切口下完成的微创胸外科新技术。目前这项技术在北京、上海等地得到成熟运用。昨天，通大附院举办了一场别开生面的胸腔镜手术学习会，来自省内一百多名胸外科医生，通过观看视频直播，学习和掌握这项新技术，造福病患。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 下午1点，通大附院手术室内，正进行着一场国内先进的胸腔镜食管癌根治手术，坐在一墙之隔的会议室的省内各大医院的胸外科医生，通过高清视频观看了手术演示。胸腔镜手术仅需做1到3个不超过1.5厘米的胸壁小孔，将带有微小医用摄像头的手术器械伸进病人体内，胸腔内的情况便实时反映在显示幕上。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这台手术特邀复旦大学中山医院胸外科教授谭黎杰主刀，而通大附院心胸外科主任医师严煜，还将为同行们带来全胸腔镜下肺叶切除肺癌根治手术。食道癌和肺癌，在南通地区属于高发病，胸腔镜手术的应用推广，将为通城病患带来福音。</span><br />
