		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>内镜治疗“中山标准”赢得世界掌声(图)</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3202/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中山医院内镜中心医生为患者诊治 /院方供图</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 晨报记者 张昱欣</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">复旦大学附属中山医院内镜中心—全球完成内镜手术治疗数量最多、质量最高的中心。其中，内镜黏膜下隧道切除术属国际独创。世界各国、包括梅奥等国际顶级医疗机构的医生，纷纷来中山医院进修学习。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">从当初“无教授、无设备、无论文、无课题、无研究生、无进修医生……”的“十无”科室，到现在多项“全球领先”技术获得公认，这一医学史上颠覆性的变化，源于中山医院内镜治疗团队二十多年的刻苦钻研和持续创新。作为团队带头人的姚礼庆教授感慨道：“这轮医学创新，中国医生不仅没有落下，还打破了欧美专家一统天下的局面。在国际内镜治疗大会上，我可以很自豪地说，Im　Chinese.Im　from　Shang－hai！”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">平均每分钟完成一例检查</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">有多少人想到，这个如今受到国际医学同行瞩目的团队，在1992年成立伊始，不过是个没人愿意去的小科室。“我是外科开刀出身的，让我去做肠镜怎么能甘心？”姚礼庆回忆，他到内镜中心担任主任时，面临需要白手起家的困境—三名医生和三名护士、一台仪器，中心就是在这样的基础上跌跌撞撞起步了。成立之初，姚礼庆与他的助手忙不过来，只好加班加点。也正是从那时起，姚礼庆再没有过星期六，工作日也经常要到下午三点半以后才能吃到中饭。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据了解，如今的内镜中心每天人山人海，导医台护士平均每30秒就要接诊一位病人，每天20台机器同时开动，平均每分钟就完成一例内镜检查。为了减少病人排队等候的时间，中山医院内镜中心推出了每天提前一小时上班的制度。无论每天任务有多重，每天下班都要再加做30个病人。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">如今“中山标准”名扬业界</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">医学界也是“武林江湖”，没有过硬的“武艺”和绝技，要获得同道中人的认可非常困难。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">内镜中心的周平红教授是第一个被邀请参加德国国际消化内镜大会做内镜治疗的中国人。当时，日本教授先登台演示，人气很旺。等看到周平红这张陌生的中国面孔后，会场上就有人离场休息。手术关键是医生要在食管夹层中打开一条隧道，切断肌肉，难度系数很高。5分钟后，周平红以最快速度打开食管夹层隧道，寂静的台下忽然掌声响起。紧接着，周平红麻利地切开肌肉，活动性出血也用最快的速度止住。最后，当其他专家还在埋头手术时，他已经大功告成。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">大会主席问他，“您的团队为多少名病人开展过内镜下治疗贲门失弛缓症（POEM）术？”周平红回答说：“1500人！”大会主席惊讶地说，“占世界的二分之一，太了不起了！”这时候全场响起第5次掌声。姚礼庆回忆道，“当时我马上拿出手机打给秦新裕书记，我对他说，我们中国医生，终于在世界舞台上站起来了！”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“如今我们的‘中山标准’，也已成为世界内镜学的‘金标准’。”姚礼庆表示。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在“提早一小时上班”的规定外，姚礼庆还开通了“24小时+365天”的抢救绿色通道，内镜中心的医生无论年资多高，一律轮流夜间值班。“我们整个内镜中心的医护人员，每天天不亮就得出门，晚上数着星星回家。”姚礼庆表示，绿色通道开通迄今已有10年，每年约抢救600多个病人，每晚少则一两个病人，多则四五个病人。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“让病人有尊严地活下去”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“让病人有尊严地活下去”的信条，也促使着姚礼庆和他的团队不断寻找创新的“落脚点”。对于大肠癌肠梗阻患者，过去的做法是先做个人造肛门。直到有一天，一个老病人对姚礼庆说：“生了这样的病倒不如死了爽气！”原来，这名患者自从做了人造肛门，人整天都是臭烘烘的，老伴让他去阳台睡，孙子不让他抱。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">姚礼庆听了以后很不是滋味，他查阅资料后，有了先采用肠镜置入支架治疗肠梗阻，将排泄物排清，再手术切除病灶的想法。1999年冬天，一名身患晚期肠癌并发肠梗阻的病人来就诊，当时她的肚子胀得像皮球，没有一家医院肯接收她，姚礼庆通过肠镜把一根金属支架植入她的体内，这是国际上首次。“我清晰地记得，支架植入病人肠道的那一瞬间，粪水喷到了我的脸上、头上，臭气熏天，但我却很兴奋！从此以后，肠梗阻病人再也不用做人造肛门了。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">中山医院党委书记秦新裕表示，医院未来计划建成区分于内镜检查的独立的内镜手术室，给中山内镜团队的创新发展注入新的动力。</span><br />
