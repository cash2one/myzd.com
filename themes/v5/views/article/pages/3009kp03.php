		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>癫痫的急救方法及注意事项</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1.癫痫发作时，迅速让病人仰卧，不要垫枕头，把缠有纱布的压舌板（或牙刷把）垫在上下牙齿间，以防病人自己咬伤舌头。随即松开衣领，将病人头偏向一侧，使口腔分泌物自行流出，防止口水误入气道，引起吸入性肺炎。同时，还要把病人下颌托起，防止因窝脖使舌头堵塞气管。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2.发作时不要强行喂水或强行按压肢体，应刺激或点压人中、合谷、足三里、涌泉等穴位。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">注意事项&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1.如癫痫连续发作，要将病人送到医院继续抢救。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2.癫痫病人在平时要按医嘱用药，不要自行减药、停药或换药。那样会引起癫痫连续发作。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3.抗癫痫药对癫痫有刺激作用，要在饭后服用。服药期间注意口腔卫生，经常刷牙。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4.癫痫病人在日常生活中要避免情绪激动和劳累，不要登高、骑车、游泳，不宜在机器旁工作，以免癫痫病发作时发生意外。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5.病人如有假牙，应在每日睡觉前摘下。癫痫病人睡单人床时，要在床边增加床档，以防发病时坠床跌伤。&nbsp;</span><br />
