<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">昨晚11时，上海的救护车在常熟新区医院外待命特派记者陈意俊摄</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在获知上海旅游车发生重大交通事故后，上海市卫生局于第一时间与江苏省卫生厅和苏州市、常熟市卫生局取得联系，表示将全力配合做好伤员救治工作。由市卫生副局长瞿介明教授带领的上海医疗小组一行16人于昨晚8时30分抵达常熟，其中包括来自市第六人民医院骨科、重症监护科的两名专家，以及市医疗急救中心派出的1辆急救指挥车、4辆救护车。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 事故当晚至今天凌晨近2时，医疗小组逐一查看了分别收治于常熟市新区医院、常熟市第一人民医院、常熟市第二人民医院3家医院4个院区的所有上海伤员，了解伤情和治疗情况。另有长征医院的两名专家也应邀赴常熟参加了会诊。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据市卫生局医政处副处长倪卫杰上午在现场介绍，上海医疗小组的重症监护专家王学敏、骨科专家周祖彬今天一早同当地医生一道，对3名在重症监护病房的交通事故受伤者进行了全面会诊，随后即与赶至常熟的伤者家属作了深入沟通。鉴于有家属希望伤者能够转回上海医院持续治疗，医疗小组又对伤者转运中可能发生的风险进行了评估。上海市卫生局对可能来沪的后续治疗作出具体安排，首批转运回沪伤者的接诊单位市第六人民医院目前已开通绿色通道，并预留出病房。</span>
