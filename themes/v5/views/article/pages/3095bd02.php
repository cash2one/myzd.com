<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>北京民营医院三博脑科医院建立异地医保直通车</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据了解，在北京市三级医院里，有30%-40%的患者来自外地，专科医院的这一比例接近80%-90%，其中住院患者的比例还要高一些。这些外地医保病人因为种种原因，在北京就医时遇到了不少困难。“是否能很快找到合适的专家就诊”、“出院回家医保是否能报销”、“等待办理医保手续的时间太长，报销起来是否会有障碍”诸如此类的问题是很多来北京看病的外地医保病人最关心的焦点。而这类问题在民营医院更加凸显。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2011年3月14日，来自濮阳的病人贾先生在北京三博脑科医院顺利完成了垂体瘤摘除手术，准备出院。在考虑是否来北京就医时，因自己是外地医保，贾先生曾担心来京就医会很不方便。后来得知在北京三博脑科医院看完病后可以直接结算，统筹部分由医院垫付，自己只需要缴纳自费部分就行，而且可以在第一时间里预约到想找的神经外科专家闫长祥教授，贾先生毫不犹豫地选择了来北京。贾先生说，北京民营医院的外地医保也可以这么方便地结算，医保“绿色通道”真是方便了患者。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 三博脑科医院于2009年被认定为北京市基本医疗保险神经专科三级定点医疗机构，且已取得北京市新农合定点医疗机构的资格，是民营医院里少数取得医保通行证资格的幸运者之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 为了保证外地医保的费用报销更便捷、更省事，三博脑科医院与河南、山西、黑龙江、青海、辽宁等省份的当地医保部门签署了医保工作服务协议。据张阳院长介绍，不但这些地区的病人可直接由当地市属三级医院转诊就诊，这些地区的病人也可以在最短的时间内预约到三博的专家就诊，减少在北京等待的时间。这些举措为这些外地医保病人建立了零等待的“绿色通道”。此外，医院还与当地医保部门建立一系列防止骗保的举措。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在异地医保真正实现跨省报销之前，三博脑科医院建立的这种与外地医保部门的合作模式，为外地医保病人提供了极大的方便，同时也为北京市未来建立完善的医保异地报销体系提供了具体的实践经验。</span>
</p>
