		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>区第二人民医院麻醉科精准配合河南专家四台“人工膝关节置换术”顺利完成</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">当接到河南省中医药大学第二附属医院关节中心主任、主任医师、博士生导师、全国知名专家孙永强教授将受邀前来医院，并拟定于10月23、24日实施四台“人工膝关节置换术”的通知后，地区第二人民医院麻醉科就开始了紧锣密鼓的准备工作。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“人工膝关节置换手术”是较大的关节重建手术，在国内属较前沿的手术技术，整个哈密地区都较少开展。此次开展的四台手术，尤其是23日下午的三台手术，在麻醉、器械和人手配备等方面，对麻醉科都是个不小的考验！科主任韩刚、援疆副主任梁冰及护士长梁海宏事先在物品准备、科室配合、人员协调等方面作出妥善安排，并提前制定了各种应急预案。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">即将进行手术的四名患者年龄均较大，基础病较多，韩刚主任和梁冰副主任亲自前往病房进行麻醉术前访视。针对患者的自身情况，选择了合适的麻醉方式；做了ASA风险评估，认真做好解释工作，取得患者的配合。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">护士长梁海宏组织护士们提前翻阅了大量的相关资料，了解手术流程，熟练掌握各种特殊手术器械的安装和使用步骤；组织术前讨论，制定周密的手术配合计划，做到每一步都心中有数；提前对每位患者介绍手术室的环境和手术中要配合的注意事项，建立良好的医患关系，消除其紧张情绪。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨科手术对环境的无菌要求很高，麻醉科严格执行无菌操作规程，手术医生及器械护士均戴双层手套。消毒、铺巾、贴膜、连接吸引器……手术有条不紊地开始了。孙教授的手术速度非常快，从划下第一刀开始，一台高难度的膝关节置换术40分钟左右就可以完成，而孙教授缝合时的迅疾更是让人惊叹！手术进展快，对麻醉、术前准备和术中配合的要求也进一步提高。但一切忙而不乱，凭借着充分的准备和干脆、利落的配合，四台膝关节置换术全部顺利完成！术后，孙教授对地区第二人民医院的麻醉和手术配合给予了高度赞誉！</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">此次手术的成功开展，使地区第二人民医院麻醉科的麻醉技术和手术配合能力得到了一定的提高，而高难度手术的顺利完成，进一步增强了哈密地区的医疗技术力量！</span><br />
