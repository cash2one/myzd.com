			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“上引下连”促专科发展——市中心医院特聘六位全国著名胸外科专家为客座教授</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为进一步提高商洛市微创外科技术水平，持续推动医院“上引下连”业务发展模式走向深入，8月8日，市中心医院隆重举行了客座教授聘任仪式暨第二届商洛微创胸外科高峰论坛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 8日上午，市中心医院多功能会议室里高朋满座，蓬荜生辉。天津医科大学肿瘤医院于振涛教授、中山大学肿瘤防治中心范卫君教授、上海交通大学附属胸科医院罗清泉教授、西安交大一附院付军科教授、四川省肿瘤医院韩泳涛、彭林教授、唐都医院张涛教授、陕西省肿瘤医院雷光焰教授等多位全国顶尖胸外科专家齐聚一堂，通过手术演示和学术交流分享国内微创胸外科领域最新成果，来自商洛各区县基层医疗卫生单位的医务工作者200余人共同见证了这一历史时刻。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 副院长李银山代表医院向远道而来的各位专家教授表示热烈的欢迎。市卫计局副局长朋双成表示，市中心医院作为全市医疗卫生服务体系的龙头单位，近年来立足本市，放眼全国，不断优化医院功能定位，整合学科，精尖技术，积极开展“上引下连”，形成稳定有序的协同发展新机制。市中心医院院长杨智哲代表医院先后为于振涛、范卫君、罗清泉、付军科、雷光焰及张涛六位教授颁发了客座教授聘书。于振涛教授表示，商医胸外科近几年取得了长足的发展和进步，他将尽自己最大的努力为医院胸外科水平提升，为商洛广大患者做出贡献。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专家们就食管癌早期淋巴结转移、肺癌微波消融、机器人微创肺手术等前沿学科内容进行了精彩的学术交流。为了让大家对食管癌微创技术有一个更加清晰、全面、直观的了解，韩泳涛教授及我院胸外科汤志刚主任医师先后通过视频现场进行了手术演示。与会代表一致认为继韩泳涛、彭林、许樟荣、蔡英全教授及美国休斯敦大学刘钰、易敏博士之后，于振涛等六位国内著名胸外科专家获聘商医客座教授，极大推动了胸外科业务能力提升和专科学术发展，扩大了医院品牌关注度及影响力，让专家来到了家门口，切实造福了商洛人民。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，市中心医院在以杨智哲同志为核心的领导班子带领下，上下一心，锐意进取，迈上了跨越式发展的快车道，无论从整体规模、技术实力、人才建设、硬件设施、文化建设等方面都发生了翻天覆地的喜人变化。现代化的20层外科住院楼建成使用，学科精细化发展走向深入，清华大学高级管理研修班、西安交大硕士班开班，质量内涵建设不断加强。在“学术年”活动背景下，为持续提升医院发展“软实力”，2014年11月，杨智哲院长高瞻远瞩，提出“上引下连”的业务发展模式，并将胸外科“食管癌微创治疗”项目确定为突破口。半年多来，胸外科发展迅速，已发展为医院样板科室。杨院长鼓励医院更多科室更新观念，拓展思维，学习“上引下连”发展模式，加强专科建设。下一步，市中心医院将通过多渠道、多方式与国内外不同领域的更多顶尖专家进行广泛合作，为商洛与国内乃至世界一流医学技术接轨搭建平台，为广大市民提供更加专业、便捷、高效的健康服务。</span><br />
