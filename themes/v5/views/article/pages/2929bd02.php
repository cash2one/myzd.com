			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>国安官网：瑞典国脚加盟国安 将身披21号战袍</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腾讯体育讯 北京时间2014年7月29日，国安俱乐部官网发布公告，宣布费祖拉乌正式加盟。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">北京时间7月29日，瑞典外援埃尔顿•费祖拉乌从家乡斯德哥尔摩飞抵北京，正式开启了自己的中国生涯。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上午10时，费祖拉乌在经纪人的陪伴下抵达了首都国际机场，北京国安足球俱乐部总经理高潮与副总经理武又文等在机场迎接了这位瑞典外援。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在短暂的接受了媒体的采访后，费祖拉乌便驱车前往了北京大学第三医院，由著名运动康复专家胡跃林大夫亲自上阵为瑞典人体检把脉。结果在对费祖拉乌的关节和韧带等进行了一系列的检查后，胡大夫认为费祖拉乌的身体并没有明显的伤病，这也为费祖拉乌的加盟扫清了最后障碍。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">下午5点，费祖拉乌出现在了北京国安足球俱乐部，在对合同进行了最后的确认之后，他在北京国安俱乐部总经理高潮的注视下在合同上签下了自己的名字。至此，北京国安在巴塔拉受伤后火速完成了和瑞典人埃尔顿•费祖拉乌的签约，而费祖拉乌将会身披21号战袍征战下半赛季的联赛。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随后，费祖拉乌接受了北京国</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">安官网的采访，他在采访中表示自己非常高兴能加盟北京国安这样一家拥有如此众多球迷的俱乐部，如果在主场能看到数以万计的绿色身影自己将会非常开心。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而众所周知，在巴塔拉受伤后，国安队更换外援的时间非常有限。而在如此短的时间之内就敲定了这一桩转会，费祖拉乌也是有着自己的看法：“这一切发生的真是很快，能够来到北京这样一座美好的城市，每场比赛都会有四到五万的球迷来为自己加油，真的是很让人激动。而另一方面，北京国安这样的一家俱乐部给我报价实在很难以令人拒绝，毕竟这样的机会错过了就不会再经常出现了。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于这座他即将展开新生活的城市，费祖拉乌也是一直充满着期待。当被问到对北京这座城市的第一印象，“太热了”费祖拉乌打趣道，随后当他说出自己从来没有在大街上见过这么多人的时候在场的所有人都笑出了声。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">13场打进9球，正在进行的瑞典联赛中，费祖拉乌发挥神勇，就在10几天前刚刚和诺尔雪平的比赛中他还梅开二度。对于自己的身体状况，瑞典人也信心满满。“我上一周依然在瑞典联赛中参加比赛，我的身体没有任何问题，已经随时可以准备登场亮相。”至于在下半赛季自己在北京国安队中会有怎样的表现，费祖拉乌显然也对国安队做了一些了解：“我知道现在国安队排在联赛的第二名，这是一个非常好的位置，球队可以坐二望一，对此我也非常期待。”至于瑞典人本人对自己的期望和目标则更加具体：“我的目标就是在下半赛季用最好的表现证明自己，希望明年还可以继续留在这里”。</span>
