<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>解密让人“头痛”的烟雾病</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q 烟雾病患者应该注意哪些事项？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 段炼：烟雾病早期确诊进行外科手术干预是最主要的治疗方式。另外，烟雾病病人在日常生活中要避免过度换气的行为，可以通过二氧化碳口罩、或者牛皮纸袋等来避免。就是病人将呼出的二氧化碳再吸回来，这样身体内的化学感受器感觉到体内二氧化碳浓度升高，会协调大脑血管扩张，可以提供更多的氧。而过度换气造成体内二氧化碳浓度低，大脑认为不缺氧，会让血管收缩，这对本来供血量不足的大脑是非常危险的。避免过度换气和卒中后病人进高压氧舱吸氧是不矛盾的，卒中病人进高压氧舱吸氧是通过氧浓度的提升来减少缺血神经元的损害，但要注意吸氧同时保证二氧化碳的平衡。另外，病人一定要避免去高危地区或从事高危行动，如攀高、游泳等运动，因为人发生一过性肢体无力，如果在攀高、游泳、开车中就是非常危险的。另外，病人维持健康的生活方式很重要，家里有烟雾病病人的，家属需要进行TCD筛查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 血管扩张剂可能增加出血风险</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q 内科使用的一些血管扩张剂对烟雾病病人有用吗？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 段炼：目前为止，因为病因和发病机理不明，内科没有一种药物对烟雾病肯定有效，血管扩张剂对烟雾病病人来说，没有相关的临床试验和研究证据证明其功用，只能根据大夫的临床经验，针对病人个体特点来决定。我个人的经验是，烟雾病的血管结构跟正常人不一样，使用血管扩张及可能造成患者本就已经十分脆弱的脑供血平衡被打破，反而引起危险症状。包括对烟雾病病人使用阿司匹林会不会增加出血风险，现在还没有前瞻性研究，只能根据病人自身情况采取个体化治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 不是所有患者都适合搭桥</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q 烟雾病病人有典型的病人，也有非典型的烟雾病病人。这两类病人需要区分对待吗？是不是所有的烟雾病病人都要进行外科搭桥手术？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 段炼：这两类病人都应该采取积极的治疗办法，但是手术要慎重。典型的烟雾病病人确诊后，需要进行外科手术。但外科搭桥手术是有风险和有创的方法，手术前应首先考虑病人的安全，最大程度保证病人术中和术后的安全，第二，病人应该能从这种治疗方式中获益，手术要有效。所以要通过各种因素判断，哪种治疗方式对病人安全、有效，就使用哪种治疗方式，如果没有足够的证据，对于非典型的烟雾病病人，就应密切观察病情发展。有些人不需要、不适合就不能做手术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 经过外科手术后，烟雾病病人的自身代偿性的血管会减少，从理论上来说，会让患者出血的风险减少。但这也需要进一步研究。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> Q 什么样的烟雾病患者适合做外科手术？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 段炼：外科手术有适应症，是否适合外科手术，第一条是要诊断明确，明确是不是烟雾病。第二，一定要有缺血症状。对于没有症状的疑似患者，要通过定量的脑血流储备检测手段来判断患者脑血流的储备能力。比如说平时需要脑血流量100毫升，在动脑、活动时需要150毫升，健康人可以通过调动身体其它因素来拿出需要的血量，短暂性增大供应，而对于烟雾病患者，像汽车用尽了自己油箱里的油，无法进一步加大供应，导致缺血症状的出现。如果通过判断发现脑血流储备能力降低，就能判断患者可能在病程的进展中出现脑梗塞或脑出血，这时应该提前做外科手术，而不是等脑梗塞后再做手术。对于烟雾病的易感和高危人群，则可以通过经TCD来判断脑血管是否狭窄、堵塞，血流量和流速多少，这项检查大约200元一次，对人体无创，医生操作容易，再经过磁共振血管成像一般就能确诊。</span>
</p>
