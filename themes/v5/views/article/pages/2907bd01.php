<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（中山大学肿瘤医院肝胆科主任医师 陈敏山）有肝炎病史或者有肝硬化的人，每六个月必须做一次肝脏检查，因为乙肝病毒感染者在40岁以上得肝癌的几率比正常人要高两百倍。肝癌是环境污染、家族基因、肝炎病毒感染、众多不良的生活习惯叠加的结果。对于普通人来说，需要在自己可控的范围内尽量减少叠加因素，以降低肝癌的发生几率。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="http://static.mingyizhudao.com/147003107756610" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">普通人防肝癌首先要预防感染乙肝、丙肝病毒。肝病的表现多数以消化道症状为主，就是说一旦出现不明原因的恶心、呕吐、腹胀、食欲不振、乏力、厌油腻等症状时，要意识到可能是肝脏出了问题。另外，观察大小便也是简单的自我检测方法，如果发现尿色明显发黄，大便颜色浅白，一定要到医院就诊。</span><br />
