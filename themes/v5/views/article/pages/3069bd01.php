		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>爱眼 从新生儿筛查开始</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">南都讯 记者王道斌 通讯员周密易灵敏 今天是世界爱眼日，记者从广州市妇女儿童医疗中心眼科获悉，自广州市8年前启动市区三级早产儿视网膜病变筛查以来，已为广州减少了数百名盲童。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 广州稍早前曾对广州市的盲校儿童进行原因调查，结果显示，全部盲童中67人致盲原因为早产儿视网膜病变，占全部盲童的37.9%，为最主要原因。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 市妇儿医疗中心眼科主任项道满表示，“早产儿视网膜病变已经成为全国范围内儿童致盲的首要因素，此外就是先天性白内障，也属于可避免的盲症，但很多患儿错过了最佳手术时机，成了低视力人群。”据透露，未进行全市范围内的视网膜病变筛查前，广州每年新增的盲童数为40-50人。但自从8年前广州启动了该项以妇儿医疗中心为核心的三级筛查网后，大大减少了新增盲童的数量，累计根治了数百名潜在盲童。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 另一个和手术时机把握非常相关的视力疾病，是先天性白内障。该病自然发病率为0 .4‰，以广州的新生儿数量每年15万-20万计，该类患儿的数量为60-80人左右。“这个疾病如果在出生1个月至1个半月期间及早手术，视力的恢复程度是最好的。”项道满说。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; “类似的筛查工作，在美国、英国是普及开展的，目前国内天津也已开展这一项目。”项道满表示，希望广州也能尽快建立新生儿眼病体系，尽早发现早期先天性白内障患儿。</span>
</p>
