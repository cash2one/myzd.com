		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>耳聋患者恢复听力的又一选择—骨锚式助听器</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨锚式助听器（BAHA）是一种通过骨传导方式改善听力的一种助听设备，也是唯一直接经过骨传导方式工作的植入式助听装置。由于BAHA性能优越，植入简单，至今已为全球超过10万名患者提供了听力帮助。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>植入装置及原理</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; BAHA主要包括三个组成部分：一个钛质的植入螺钉—通过手术植入到耳后颅骨上，经过一段时间钛螺钉与骨组织融为一体。一个外部连接桥基—穿过皮肤连接颅骨内的钛质螺钉和外置的声音处理器。一个可拆分的声音处理器—置于体外，与桥基相连接，接收和放大声音。BAHA的工作原理：声音处理器通过其麦克风接受外界的声音，声能经过一个电磁转换装置转换为机械振动引起颅骨内的钛螺钉高效振动，振动可通过颅骨和颌骨传到内耳，引起内淋巴液波动，从而刺激了感觉毛细胞，毛细胞经过机械—电转导将这种刺激转换为电化学信号，通过听觉通路逐级传递到听觉皮层，产生听觉。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>什么样的患者适宜植入？</strong></span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">1. 双侧传导性或混合性耳聋患者</span>
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">BAHA对于双侧传导性或感音神经性耳聋患者（包括先天性外耳道闭锁、双侧中耳炎、双侧耳硬化症等）改善听力状况十分有效，适用于那些不宜接受听力重建手术以及传统的骨传导助听器效果不理想的人群。国外学者曾研究了40例先天性外耳道闭锁患儿，其中20例接受了BAHA植入，20例接受了听力重建手术，结果显示接受BAHA植入听力改善明显优于接受听力重建手术。BAHA植入在双侧慢性化脓性中耳炎患者中的疗效也是明显的，患者术后听力及生活质量有明显改善。双侧耳聋的患者建议双侧植入BAHA，可以提高听觉灵敏度和言语接受率，以及噪声环境下的言语识别。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2. 单侧传导性耳聋患者或混合性耳聋患者</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">单侧传导性或混合性耳聋患者通常面临的问题是声音定位、噪音环境中的言语识别能力较差。BAHA 能够解决这类问题，还可使其获得双耳立体听力。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3. 单侧感音神经性耳聋患者</span>
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">包括听神经瘤（未接受治疗、接受手术治疗或放疗的患者）、先天性耳聋、突发性耳聋以及梅尼埃病。将BAHA植入这些患者患耳一侧，通过颅骨传导起到信号对传的作用。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">4. 伴中度智力缺陷的传导性或混合性耳聋患者</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5. 耳鸣患者</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">BAHA可通过骨导产生掩蔽声音，同时不影响通过外、中耳获得正常声音，从而减轻耳鸣的症状。</span><br />
