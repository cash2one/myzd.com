			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“健步行走慈善活动”惠及忻州市民</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（山西忻州传媒网12月8日电） &nbsp;12月4日—5日，山西省忻州市人民医院与北医三院骨科专家倾力合作，成功为3位患者施行了4台全膝关节置换手术，至此，由忻州市人民医院、北医三院骨科、北京爱康宜诚医疗器械有限公司共同发起的“ 健步行走慈善活动—忻州行”活动圆满落下了帷幕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “健步行走慈善活动—忻州行”活动是由北医三院骨科张克主任倡议，忻州市人民医院骨科二病区具体承办，北京爱康宜诚医疗器材有限公司无偿捐赠十台人工全膝关节假体，北医三院骨科张克教授等作为自愿者三方的爱心合作，在为期8个月的活动中，专家们为近百位关节病患者进行了义诊，全市有50多名关节病致残病人受益，特别是3位经济条件困难的关节病患者（两例为骨性关节炎、一例为双膝严重屈曲畸形的类风湿性关节炎）获得了成功治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 活动的宗旨是为患有膝关节疾病，需要进行人工关节置换手术但又不能全额负担手术费用的病人提供免费治疗；同时对当地骨科医护人员进行关节置换手术方面最先进治疗技术的培训和学术研讨。活动期间，北医三院专家就国内外关节外科最新进展等课题，进行了专题学术讲座和手术观摩。随行的该院护士长黄萍更是不辞辛苦，手把手的帮助、指导市人民医院骨科的护理工作，尤其是对全膝关节置换术后护理作了悉心指导，极大地提高了科室护士的业务技能和整体素质。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据介绍，忻州市人民医院还将继续开展这样的“慈善活动”，在扩大病人筛选的基础上，提前预约即将上马的关节镜病源，同时选出6例膝关节病患者进行“健步行走慈善活动”二期手术，届时，该院科室关节外科疾病的诊治水平将获得提升，更加有利于人才培养和医院特色科室建设。</span><br />
