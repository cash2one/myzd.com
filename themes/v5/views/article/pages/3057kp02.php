<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近日诗人汪国真因肝癌在302医院逝世，我们如何预防肝癌发生呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝癌在早中期没有症状，所以体检按筛查尤为重要。有以下高危因素的人群尤其警惕。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝癌家族史、常饮酒、糖尿病、乙肝、丙肝感染,有肝硬化基础。上述人群在40岁后就要每4-6月体检。包括甲胎蛋白，彩超。 因为彩超对操作者要求高，部分小肝癌彩超难识别，如果觉得自己当地医院水平有限，可直接做增强CT更加敏感。 很多患者问磁共振和CT那个看得更清楚，这个取决于所在医院医师、设备、经验。按照当地医生意见比较好。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;普通人群则需避免大量饮酒、避免食用霉变花生。目前很多变质花生做成小包装油炸花生，所以尽量减少袋装花生食用。了解当地饮用水质量。避免毒物污染水源。适当地运动，减少脂肪肝；合理休息，免疫增强，积极治疗肝脏疾病。都是需要注意的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 甲胎蛋白升高，很多人问到升高是否都是肝癌，答案是否定的。甲胎升高代表肝脏恢复生长。小孩出生时也是高的。肝一次损害后甲胎升高是好事。但是持续高于150U/L。则需使用增强CT明确肝上有无长东西。若无异常，单纯升高则需定期复查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;从正常肝组织到肝癌转变需要一个过程，经历癌前病变，所以肝硬化患者每半年做个增强核磁共振是能发现很多肝癌早期状态。</span><br />
