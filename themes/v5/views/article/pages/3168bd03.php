	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>湖南医院采用神经内镜技术切除脑内肿瘤</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;新华网长沙７月１０日电（记者帅才）近日，湘雅医院神经外科专家采用神经内镜技术，为一名６７岁患者成功切除了深部脑室内肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;湖南娄底新化县的陈女士由于持续头痛、呕吐等颅内高压症状，于近日前来湘雅医院就诊。经磁共振检查，神经外科专家发现陈女士右侧脑室内长有一枚直径约２厘米的肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;据湘雅医院神经外科副主任、小儿神经外科主任刘景平教授介绍，以往针对这类脑深部肿瘤，无论瘤子大小均采取开颅的方法。即通过一个近６厘米的大切口来充分暴露视野，然后在显微镜下切除肿瘤。但显而易见的是，该方法对患者的创伤较大且影响美观。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;通过反复研究，刘景平教授及其团队决定利用刚刚从德国引进的全球最新神经内镜，为患者实施微创手术切除肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;据了解，神经内镜技术是“微创神经外科”理念的突出代表，其具有创伤小、恢复快、费用低和安全性高等优点。目前，该技术在发达国家已广泛开展，但在国内尚未普及，绝大多数大型三甲医院还不具备开展此类手术的条件。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;尽管神经内镜微创手术的操作难度要高于传统的开颅手术，但是凭借十余年来积累的丰富内镜操作和解剖经验技术，刘景平教授认为眼下是尝试突破的最佳契机。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;&nbsp;&nbsp;近日，刘景平教授率领团队成员，由一个直径约２厘米的小切口置入神经内镜，并透过身旁清晰的大屏幕，准确地找到肿瘤，从而迅速将其“连根拔起”。目前，患者恢复良好，不日即可康复出院。</span><br />
