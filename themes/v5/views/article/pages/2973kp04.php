	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>神经鞘瘤该怎么治疗？</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">如果把神经比作电线的话，那么神经鞘就是包裹这根电线的塑料绝缘层。当这层绝缘塑料上长了肿瘤，就是神经鞘瘤了。这是椎管内硬膜外肿瘤中最常见的一类肿瘤，通常分布在颈椎或者腰椎节段的神经鞘上。成年人容易得，特别是中老年人。但是也不必过于担心，虽然这种肿瘤比较常见，但几乎95%以上都是良性的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">得了神经鞘瘤，最典型的症状就是疼痛，后期还会出现手脚麻木、大小便的障碍。但是由于这些症状很容易与颈椎病、腰椎病的症状相混淆，因此出现腰腿疼痛、麻木，就要及早就医，警惕有可能是神经鞘瘤在作怪。如果误把神经鞘瘤当做颈、腰椎病来治疗，不仅达不到治疗效果，还会耽误肿瘤的最佳治疗时机，一旦肿瘤长大了，压迫神经和血管，造成不可逆的损伤，患者就容易出现运动功能障碍甚至瘫痪。当然，患者出现这些症状时还不能诊断，往往还需要做核磁进行排查；而CT和X线能更清楚地看到肿瘤对骨头的破坏程度，所以必要的时候还要补充做CT和X线检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由于这种肿瘤绝大多数都是良性的，因此首选手术切除，往往切干净了就能治好了。一般手术费用是3~5万元，根据肿瘤的位置、大小不同，手术的费用也不尽相同。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">当然，术后症状缓解的情况还需要一段时间。疼痛会消失得快一些，而肢体麻木、无力、大小便障碍的情况会缓解得慢一些。而且，手术后症状的缓解程度还与肿瘤的位置和大小、供血情况等有关系。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">可是通过手术，术前的哪些症状何时能缓解呢？每个人恢复的时间不一样，有的可能一两周就能改善，有的可能需要一年半载才能完全缓解。特别是对于那些有神经功能损伤的患者，手术后还要做康复训练，轻度的神经功能损伤一般不需要康复训练。</span>
</p>
