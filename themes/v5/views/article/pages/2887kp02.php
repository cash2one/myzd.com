<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2、经直肠超声(TRUS)检查。经直肠超声的原理和直肠指诊相似，只不过医生不是用手指，而是用超声仪伸入到患者直肠中，以此来观察前列腺是否有癌变。由于超声仪比手指的触感更客观，所以这个检查可能也比直肠指诊更准确。但是，在超声发现的结节并不都是前列腺癌，其中低回声结节得癌的机率更大。&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">3、前列腺特异性抗原(PSA)检查。前列腺特异性抗原（PSA）是前列腺组织中所独有的一种蛋白。前列腺被癌组织破坏后，这种蛋白就会跑到血液中去，所以通过检测血中前列腺特异抗原的水平，我们就能知道是否有癌变。可惜的是，影响前列腺特异抗原（PSA）水平的因素很多，前列腺炎、前列腺增生以及急性尿潴留、射精、前列腺活检、膀胱镜检、经尿道手术等均可导致前列腺特异抗原的升高。也就是说，前列腺特异性抗原异常也不一定就是前列腺癌。&nbsp;</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这三面照妖镜各有千秋，因此我们医生会联合应用这些方法，以增加诊断的准确性。必须说明的是，这些检查都存在误差，并不能够完全告诉我们前列腺是否“干净”。特别是对于前列腺特异抗原（PSA）检查异常的病人，存在“虚惊一场”的风险。如果想要确定是否真的有癌变，需要请出一位铁面法官，那就是“前列腺穿刺活检”。由于篇幅所限，对于这位公正的法官，我们只好放在下一节介绍了。</span>
