			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中国科学家开创新术式登国际顶级杂志</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">南方日报讯（记者/曹斯 通讯员/黄金娟 欧晓芳）在我国，乳腺癌列女性恶性肿瘤发病率的第一位。乳腺癌病人大多会遇到腋窝淋巴结和内乳淋巴结转移的情况，但由于内乳淋巴结转移往往较小，缺乏有效评估手段，难以制定准确治疗方案。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 笔者近日获悉，2012年12月，中山大学肺癌研究所、中山大学肿瘤防治中心胸科龙浩教授的“胸腔镜内乳淋巴结清扫术”，发表在最新一期肿瘤外科学顶级杂志《Annals of Surgical Oncology》上。这一术式通过微创方式进行，从诊断上保证了内乳淋巴结评估与病理分期的准确性，从治疗方面考虑，避免了让患者承受过度放疗、修复诊断与治疗的缺陷，得到国际认可。据专家透露，随着研究深入，这一术式或改变国际临床指南。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 麻烦的诊断</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在相当长一段时间，人们只留意到乳腺癌病人出现的腋窝淋巴结转移，而忽略了“隐蔽”在胸骨后的内乳淋巴结转移。直到20世纪40年代末，人们才认识到内乳淋巴结同腋窝淋巴结一样，为乳腺癌转移的第一站。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 内乳淋巴结转移的发现为何那么难？原来，内乳淋巴结的解剖位置深，转移也往往比较小，常规检查方法难以准确判定其转移情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 那么是否可以用B超？有人在1989年就想到了。在对7例内乳淋巴结肿大（包括6例乳癌淋巴结转移，1例淋巴瘤）的患者行胸骨旁超声探测后，医生们发现超声可检测出直径≥0.7cm的乳癌转移性肿大淋巴结。但由于乳腺癌内乳淋巴结转移一般都较小，B超的分辨率实在不足。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 要么用CT？这种横断面扫描成像技术密度分辨率高，空间定位准确，可同时显示病灶和周围脂肪间隙、骨骼、肌肉等组织，不但能观察乳腺腺体内病灶情况，在造影剂增强后，还可以分辨胸廓内动静脉及内乳淋巴结，能观察有无淋巴结的肿大及邻近其它组织的浸润。不过，还是因为内乳淋巴结太小，直径约0.1cm—0.5cm（除非极晚期已形成明显内乳区肿块，否则直径多在0.3cm），CT也使不上劲。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 还有更牛的MRI可以一试。这是一种多参数、多平面、多层成像的技术，具有很高的软组织分辨率，通过动态增强扫描再加时间—信号强度曲线、减影等图像后处理，对乳腺癌的原发灶诊断具有重要意义。但由于MRI的空间分辨率有限，只能分辨0.5cm以上的软组织肿块，便难以对付小的内乳淋巴结转移。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 尴尬的治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 检查起来那么难，不处理却不行！“如果内乳淋巴结转移没被发现，很可能2到3年乳腺癌就会复发。”龙浩说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 随着研究深入，人们发现是否有内乳淋巴结转移对乳腺癌的临床、术后病理分期及术后化疗、放疗方案的决定有重要意义，对乳腺癌患者的预后判断也有着重要意义。龙浩列举了一项临床研究数据，在50例患者中，有40%的患者因发现内乳淋巴结转移而改变了病理分期，而其中6例患者，不仅改变病理分期，还改变了后续治疗方案，这一研究结果已发表在乳腺癌领域国际著名杂志《Breast》上。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 龙浩介绍，根据目前世界最权威的癌症治疗指南——美国国家癌症综合网推荐，对病理证实或临床高度可疑的内乳淋巴结转移，应考虑加做内乳区的放射治疗。不过新问题又来了，内乳淋巴链的放射治疗很容易导致严重心脏放射损伤，许多病人多年后不是死于肿瘤，而是死于相关心脏并发症。放疗的广泛应用受到限制。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “在肿瘤的个体化治疗时代，既要反对治疗不够，也要反对治疗过度。内乳淋巴结转移的诊断与治疗的缺陷导致其一直处于尴尬的境地。”龙浩表示，结合目前乳腺癌流行病学的特点看上世纪的临床研究的设计与内容，当时的研究结果未必全部适用于现代。因此，为内乳淋巴结进行准确的评估与治疗探索新方法，显得尤为迫切。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 全新的术式</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “我们一直在思考，能不能在同时满足拥有最佳内环境稳定状态、最小切口、最轻验证、最少瘢痕等条件的情况下，进行内乳淋巴结清扫。”龙浩及其研究团队想到通过微创技术，开创了“胸腔镜内乳淋巴结清扫术”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; “这一术式首先解决了活检的问题，在胸腔镜下对整个内乳淋巴引流区行连续整块切除，从诊断上保证了内乳淋巴结评估与病理分期的准确性。”龙浩介绍，此外，新术式对内乳区进行清扫，属于治疗性手术，即使内乳淋巴结存在癌转移亦可免去进一步放射治疗，避免了放疗导致的心脏损伤。而从微创技术本身优势而言，这一术式还具有创伤小、恢复快等优点。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 为了进一步了解患者的感受和体验，团队还特地增加了手术损伤因子分析，“前期已完成的随机研究证实，患者并未因为增加手术操作而从生理上增加损伤。他们甚至未感觉到附加的不适。”龙浩说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2012年12月，新手术录像发表在最新一期肿瘤外科学顶级杂志《Annals of Surgical Oncology》上，获得了杂志主编推荐和同行关注。有专家在评语中写到，十分期待该手术进一步临床结果的发表。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据龙浩介绍，至今已有超过100例患者成功接受并获益于该项新手术，“特别是乳腺癌病灶位于乳房内侧或中央区的早期病人，他们很容易发生内乳淋巴结转移。这一手术有助于明确分期，避免治疗不够或过度治疗。而对于单纯内乳淋巴结转移的病人，将因此而获得最佳的个体化治疗，避免不必要的放射性心脏损伤。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
