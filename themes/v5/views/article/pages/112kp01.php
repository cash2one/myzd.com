		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我们“被”不孕了吗？（科普）</strong></span>
</p>
<p>
	<br />
</p>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/112/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 一对有正常性生活而没有避孕的配偶，在第一年的自然受孕率为86%左右，第二年达到92%，第三年达到95%，也就是说，一对生育年龄的夫妇，在三年内可以自然获得&gt;95%生育目的，仅有约5%的夫妇真正需要求助医生进行治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 国际卫生组织（WHO）和一些国际协会，将不孕症的年限标准定为一年，我国自2008年开始，也将不孕症的标准从两年改为一年。这样的定义显然人为提高了不孕症的发病率，因为其中有约10%的夫妇可能会在第二、三年期待中自然受孕，而无需治疗。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “不孕不育”名词的由来</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对不孕症的定义和名称，长期以来有很多争议。英文里“Infertility”被翻译成“不孕症”，原意中并无男女之分。但对于中文理解来说，一般认为“不孕症”这个名词仅对于女性而言，因为男性不能“怀孕”，男性原因导致的生育障碍，则称为“不育症”。因此有了“不孕不育”的名词之说，统统包括了男女双方的生育问题。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 但是又对“不育”有另外的解释。有的概念认为不孕的意思就是无法受孕，有些患者怀孕没有困难，但却频频发生自然流产和宫外孕，最终没能将孩子抱回家，这样的没有结果的怀孕，也被称为“不育”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这样的定义有点绕呢，我们在说一对夫妇不能生育的时候，对妻子原因的要说不孕症，对丈夫原因的要说不育症，对不明原因的，或双方都有原因的，就说是不孕不育症。问题是，我们还常常真的不能确定不孕的病因，看样子还是说“不孕不育症”较为稳妥了。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 原发和继发的不孕和不育</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不孕症还根据怀孕的历史，分为原发性不孕和继发性不孕，相对应的对丈夫的不育也要分原发性不育和继发性不育两种。原发性是指：女性从来没有怀孕过，或男性从未使任何女性怀孕过；继发性是指女性过去有过怀孕史而现在发生不孕，无论是分娩、人工流产、自然流产和早产、或是宫外孕的历史，男性曾经有过使女方怀孕的历史，也应归属为继发性不育。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这样的划分似乎很清晰，但是细一想，还是有许多问题的。一对夫妇的生育状况，我们真的能这样简单地加以界定吗？试想，一对夫妇就诊，女方如果和前男友曾经有过怀孕和人工流产史，而丈夫为初婚，从未有过致女方怀孕的历史，我们就不能简单定义为不孕症，而应该将诊断写为：女方继发性不孕，男方原发性不育。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这些情况对于这对不孕不育夫妇来说其实并不重要，也许他们永远也不需要面对原发性和继发性的纠结。但是对医生来说，这些信息却是至关重要的，因为在考虑不孕不育病因和诊断的时候，需要根据患者怀孕的病史进行分析和推断。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 例如，如果这位妻子有过宫外孕和人工流产史，医生会更多地怀疑她的盆腔和输卵管的不孕因素，对输卵管造影或腹腔镜检查会侧重提出建议。当然，医生对于属于个人隐私的病史，是充分尊重患者意愿的，绝不应该随意将怀孕和生育史透露给别人，包括夫妇配偶。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 随着辅助生殖技术的发展，一些对不孕不育的概念的变化也凸显出来，使用中有些困惑。例如一对原发性不孕的夫妇，在进行辅助生殖技术的过程中怀孕又流产了，不到一年的时间再来进行助孕治疗，这时医生是应该把她当成是继发性不孕，还是原发性不孕呢？</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不孕不育的行业诊断标准</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 我国卫生主管部门颁发了《不孕不育症诊断标准》，这个行业标准充分考虑了对不孕症的准确定义，将不孕不育症这一名词作为诊断的标准术语，涵盖了男性和女性生育障碍的潜在可能，在国内生殖医学界将会被广泛使用。《标准》中将不孕不育症分为三大类：女方的原因，其中包括排卵障碍和盆腔因素两类，男方的原因，和不明原因。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 对于生育来说，不孕不育永远是个复杂的问题，而且是个动态的过程，除了特别界定的绝对原因：例如男方无精症、女方卵巢功能衰竭、女性生殖道无子宫，或输卵管完全梗阻或切除，在其它的不孕不育状态下，生育力常常很难评估，一般会用期待怀孕的时间来证实。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 实际上一对夫妇多年不孕，一朝偶然怀孕的案例也并不少见，所以我们对不孕症的一般治疗，如果不是高质量大样本的随机对照研究，是无法计算其“成功率”的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/112/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 在某些情况下，不孕不育夫妇还是应该早些看医生的。例如：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ①女方年龄超过35岁的不孕不育夫妇；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ②女方有月经不调或闭经的病史；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ③怀疑或确诊为子宫输卵管病因，或子宫内膜异位症的女方；</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ④男性配偶被诊断出肯定的不育病因。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 生育是自然赋予我们的使命，准备做父母的年轻夫妇，还是将怀孕当成是自然的事情来对待吧。在正确的年龄、合适的环境、耐心的期待、理性的求治中获得孕育。</span><br />
