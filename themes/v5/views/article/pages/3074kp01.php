		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>原发性肝癌规范化诊治的专家共识</strong></span>
</div>
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>一、前 言&nbsp;</strong></span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong><br />
</strong></span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原发性肝癌(PLC,以下简称肝癌)是临床上最常见的恶性肿瘤之一，全球发病率逐年增长，已超过62.6万/年, 居于恶性肿瘤的第5位; 死亡接近60万/年，位居肿瘤相关死亡的第3位。PLC在我国高发，目前，我国发病人数约占全球的55%；在肿瘤相关死亡中仅次于肺癌，位居第二。因此，肝癌严重威胁我国人民健康和生命。为了推动我国临床肿瘤学事业的发展，提高PLC多学科规范化综合治疗和研究水平，积极学习和应用国内、外的符合循证医学原则的高级别证据，制订符合我国国情的PLC临床实践指南，中国抗癌协会肝癌专业委员会（CSLC）、临床肿瘤学协作专业委员会（CSCO）和中华医学会肝病学分会肝癌学组共同发起，组织多学科专家参与，制定了本“原发性肝癌规范化诊治专家共识”。&nbsp;</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2007年11月10日、2008年4月5日和8月30日，先后在上海召开了三次专家共识研讨会。会议由叶胜龙和秦叔逵教授共同主持，吴孟超、汤钊猷、孙燕院士和管忠震教授亲临指导，国内有关PLC诊治领域的60多位著名专家出席会议。在会上，系统地复习了当前PLC的国际指南与共识，讨论了PLC的诊断、手术治疗（肝切除与肝移植）、介入治疗、局部消融治疗（主要包括射频消融、微波消融和高强度聚焦超声治疗）、放射治疗、生物治疗、分子靶向治疗、系统化疗以及中医药治疗等一系列问题。各位专家认真准备、积极参与，基于尊重循证医学证据的原则，与国际上的诊断治疗理念接轨，特别是针对我国PLC诊治的现状和发展，大家各抒己见、集思广益，提出了许多良好的建议。会后，由部分专家执笔，广泛征求意见，反复多次修改，最终形成了本共识。&nbsp;</span>
</p>
