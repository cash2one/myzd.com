	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>大龄男性应更加谨“肾&nbsp;</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">新华网上海３月４日电 每年3月份的第二个星期四是世界肾脏病日。今年世界肾脏病日的主题是“防治老年肾病”。从复旦大学附属中山医院了解到：在世界肾脏病日来临之际，由中国医师协会、中国健康促进与教育协会、复旦大学附属中山医院联合发起的 “护佑肝肾•因爱而行” 肾癌健康知识讲座在该院举行。针对肾肿瘤患者及家属关心的肾肿瘤诊疗、护理过程，与会专家从多学科的角度进行了权威、详尽的解答，帮助患者和家属共同抗击肿瘤，应对困难，树立生活信心。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>&nbsp; &nbsp; 肾脏健康不容忽视</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 人体就像一台精密的仪器，人体的各个脏器如同各个部件，某一个部件坏了，就可以影响人体这台“仪器”，使之不能正常工作。肾脏起着过滤血液中的杂质、维持体液和电解质的平衡、调节血压等诸多重要功能。肾脏的病变可以影响到全身其他器官，同时全身其他器官的疾病，在一定程度上也可以影响并导致肾脏疾病。然而，现代人尤其是男性，吸烟、喝酒、肥胖和环境污染等诸多不利条件严重影响着肾脏健康。最新统计数据显示，在过去的20年间，我国肾癌发病率以平均每年6.5%的速度飞速增长，已跃升至常见泌尿系统肿瘤发病的第二位，成为严重威胁国民健康的恶性肿瘤之一。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;<strong> 大龄男性应更加谨“肾 ”</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 复旦大学附属中山医院泌尿科主任郭剑明教授介绍：“肾细胞癌是最常见的肾恶性肿瘤，发病可见于任何年龄，高发年龄在50至70岁之间，男性发病率是女性的两倍，患者就诊时有25%已经出现远处转移。转移性肾癌患者的中位无进展生存期在7至10个月之间，且对化疗和激素治疗并不敏感。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; <strong>发病初期不明显，坚持体检防肾病</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 郭剑明教授在介绍肾癌时说到:“与糖尿病、高血压类似，肾癌是一种“富贵病”，可以说生活条件好了人容易得这个病。广大男性朋友无论是出于工作原因还是个人习惯，对平时的饮食不加控制，不规律的饮食很容易造成肥胖，进而导致高血压，再加上吸烟、喝酒都对肾脏产生不良影响。生活中，许多人当出现了血尿、疼痛、肿块三大症状时才去医院就诊。殊不知，这三个就是肾脏恶性肿瘤的三大典型症状，称为肾癌的“三联征”，同时还存在不少非泌尿系统的肾外表现，如精索静脉曲张，可同时有下肢水肿出现。”“在临床上，我们遇到很多患者在发现肾癌时，这些患者的病情已发展至中晚期，错过了最佳的治疗时间。另外值得注意的是，肾癌早期多无症状或呈多种多样的“忽悠性”症状，容易误诊，因此，出现血尿、疼痛、肿块这三联征其中任何一个症状都应引起高度重视。有条件的市民每年进行一次体检，50周岁后尽量半年体检一次，这有助于及早发现疾病。”郭教授补充道。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; <strong>&nbsp; 肾癌治疗：分子靶向药物少不了</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 对于如何治疗肾癌，郭剑明教授给出了以下建议：“肾癌的治疗手段包括外科治疗、免疫治疗、分子靶向治疗等。现阶段，外科治疗主要包括肾切除术和保留肾单位手术。而分子靶向治疗是一种新型的全身性治疗手段，它能特异性的针对肿瘤细胞，抑制其生长，促进其死亡，在提高生存质量的前提下延长晚期患者的总体生存期。目前，在肾癌治疗领域有代表性的是索拉非尼。它是第一个被美国FDA和中国的SFDA批准的，在中国地区上市治疗晚期肾癌的分子靶向药物。而针对中国人群进行的临床试验研究也提示，中国人群在索拉非尼中的获益可能更为明显，可有效控制疾病进展，延长患者生命总体生存期，使患者受益更多。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; <strong>健康好身体，肾癌远离你</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 谈及肾癌的预防方法，郭剑明教授强调：养成良好自身习惯很重要。首先应不吸烟或尽早戒烟，而水是最好的预防“药物”，多饮水可以降低毒素的有效浓度。要少吃高脂肪、高热量食物，控制体重，避免肥胖、高血压。最后，要坚持体育锻炼，增强身体素质，增加机体免疫力。</span><br />
