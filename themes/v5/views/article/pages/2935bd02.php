	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>英雄抓贼动静脉被割断 受助人称会负责到底</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">雷锋精神今何在？</span><br />
<br />
<span style="font-size:14px;"><img src="http://static.mingyizhudao.com/147003328688735" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">英雄抓小偷时很勇敢，拍照时很腼腆。</span><br />
<br />
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">南方网讯因制止小偷盗窃，被小偷同伙砍伤右臂动、静脉住院的北京青年刘浩，10月30日输血后，发一夜高烧，体温达38.4摄氏度，10月31日早晨，体温恢复正常，脱离生命危险。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 英雄挺身抓小偷 右臂被砍</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 10月28日18时30分左右，在北京市公主坟环岛东南角，22岁的刘浩看见一小偷正在偷李小姐的手机，他冲上前去大吼一声?押“快打110，小偷偷你手机！”刘浩左手按住李小姐的包，右手扯着小偷的衣领，大声喊：“快报警！快报警！”小偷拼命挣扎，惊慌失措地大叫“放手，放手！”就在两人厮打的过程中，从旁边又蹿出两个人，其中一个人一拳打掉刘浩的眼镜，另外一个人抽出一把折叠刀，照着刘浩的右臂砍去，小偷趁机逃脱。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 刘浩捂着失去知觉的右臂跑到马路边躺下来。李小姐报完警后，跑过来摁住刘浩的伤口，此时，一位过路者也过来帮忙。三四分钟后，翠微派出所的民警赶到，但三名犯罪嫌疑人已不见踪影。随后120和999及时赶到，将刘浩送到304医院。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 医生：幸亏送治及时</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 10月29日10点，在304医院病房里，刘浩的床头上摆放着鲜花，母亲在床边照顾。刘妈妈介绍，送鲜花的除了刘浩的朋友，还有刘浩所在的公司——益环通商贸有限公司的高经理和办案派出所的民警。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据304医院的唐家广医生介绍，刘浩送进医院时伤势特别严重，处于休克状态，右前臂有横向15厘米长的伤口，失血较多，桡动脉、静脉、神经断裂，并有7根屈指腱断裂。“我们抢救5个小时，幸亏送治及时，如果稍慢一点就会有生命危险。”唐医生告诉记者，“手术做得很成功。”并表示，刘浩需要一个月的时间可以康复，生活上可以自理，但抓握功能需要长时间才能完全恢复。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 英雄刘浩：我不后悔</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据刘浩的母亲介绍，10月29日早晨，经过抢救醒来的刘浩第一句话就是：“那贼抓到没有？”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 记者问他是否想到小偷带刀时，他表示：“想过，我必须冲上去。我不后悔！”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 记者说想给他拍照，他腼腆地笑笑：“现在是不是有损形象？”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 英雄的母亲：做人就应该这样</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在刘浩手术后，刘妈妈一直守护在儿子床边。她对记者感叹：“幸亏路人和警察送得及时，要不后果不敢想，真要谢谢他们！”同时，她也想谢谢李小姐的家人，“他们抢着给刘浩付了1万多元的医药费。”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据刘妈妈介绍，中学毕业后，刘浩当了3年兵。“做人就应该这样！”刘妈妈说。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 受助人李小姐：我会负责到底</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 李小姐告诉记者，看到刘浩受伤，当时她就吓傻了。“我是一名医护人员，但当时都蒙了。他让我掐住他的胳膊，防止失血过多。我特别感激他，也特别佩服他的勇敢！小偷没得手，但他仍坚持要抓。”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 当记者问及医药费时，她说：“这件事与我有关，我会负责到底！”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 翠微派出所：可申请见义勇为基金</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 10月29日，翠微派出所的政委胡又维告诉记者，刘浩可以申请见义勇为基金奖。“刘浩应该受到嘉奖。他的见义勇为行为起了一个很好的榜样作用。”</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 胡又维还表示，派出所已经成立重案组，连夜查找线索，追捕犯罪嫌疑人。他认为社会治安和良好风气的维护和形成，不仅需要公安人员的努力，更需要像刘浩这样的人。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 刘浩所在公司：要嘉奖刘浩</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 10月31日，益环通商贸有限公司的高经理告诉记者，作为业务经理的刘浩，平时就非常正直，有很强的正义感。“有一次上班迟到，他解释去抓小偷。没想到这次却不幸受伤了。”他表示很佩服刘浩的勇气，并表示，这段期间刘浩不仅不会因为误工而没有工资，“公司还要对他进行褒奖。”</span>
</p>
