			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>大数据时代的泌尿肿瘤精准医疗</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在11月7日举行的第五届上海泌尿肿瘤国际论坛上，复旦大学附属肿瘤医院副院长、泌尿外科主任叶定伟教授向媒体介绍了一种适用于中国人群的初诊前列腺癌骨转移预测模型。据悉，这种模型已获得国家发明专利。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">统计发现，全国肿瘤登记地区前列腺癌的发病率为32.23/10万、肾癌的发病率为14.75/10万、膀胱癌的发病率为17.44/10万，三大泌尿系统肿瘤合并发病率高达64.42/10万，已位列男性恶性肿瘤发病率的前三甲。叶定伟领衔的研究团队运用大数据模型，优化泌尿男生殖系统肿瘤的诊疗，并结合国人发病特点，提出了泌尿肿瘤精准医疗的“复旦经验”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大数据为前列腺癌患者“减负”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌发病率在我国城市地区呈现逐年上升的趋势，目前已位列男性肿瘤第5位，严重危害了男性的身体健康。叶定伟表示：“过度治疗是在前列腺癌治疗过程中尤为突出的，因此，进一步深化中国人群的前列腺癌研究，真正为中国前列腺癌患者减负意义重大。”</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在对早期前列腺癌患者诊治的过程中，为了更加精确地确诊病人是否需要进行骨扫描，叶定伟率领的研究团队基于中国人群600余例的数据，发明了适用于中国人群的初诊前列腺癌骨转移预测模型。该模型根据患者年龄、病理诊断报告、前列腺特异性抗原（PSA）及临床分析四项指标就可有效区分病患骨转移风险度。叶定伟介绍说：“这个模型对骨转移风险度的预判，不仅能够减少病患做不必要的骨扫描检查，还能根据预判百分比筛选出高风险人群并给予警示作用。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">叶定伟指出，前列腺癌多发于60岁以上的老年男性。家族史在前列腺癌的发生中扮演着重要角色。叶定伟和他的团队针对1000余例中国人群的研究发现，对于年龄在60-70岁的怀疑患有前列腺癌的人群，GRS（遗传学评分）可以在常规检查的基础上提高前列腺癌及高分级前列腺癌的诊断效力，因此，对于怀疑有遗传背景的该年龄人群可以从该模型中预知患前列腺癌的风险。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着精准医学时代的到来，目前唯一用于前列腺癌诊断的血液学指标前列腺特异性抗原（PSA）在临床上的应用也出现了一定的局限性。叶定伟和他的团队通过分析肿瘤医院的近700例前列腺穿刺数据并在同期的香港人群中进行验证后发现，前列腺健康指数（PHI）在预测前列腺癌发病风险方面明显优于PSA。该指标在不增加漏诊的前提下，可以使15.6%的患者避免前列腺穿刺，使患者减少了过度诊疗，避免承受不必要的经济负担和治疗痛苦。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三分之一肾癌患者肥胖</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当肥胖成为一种常见病，其对健康的影响远远没有得到应有的重视。据全球一项近30万病例的大数据分析发现，体重指数（BMI）每上升5kg/m2，则肾癌的发病率将上升1.24倍。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“我国的比例和欧美数据相当接近。”叶定伟指出。他发现我国34%的肾癌患者同时也是肥胖患者。可见肥胖因素在我国肾癌的发病因素中起到了很大作用。“我们所说的肥胖已经不再单指体型上的肥胖，有的人看着很瘦，但可能体内的内脏脂肪含量较高，这些内脏脂肪的堆积往往会导致肿瘤恶性程度的比例加大。”叶定伟肯定地说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“我们对1500余例肾癌患者的血清学分析发现脂联素的点突变可能与肾癌发生有关。”叶定伟表示，内脏脂肪百分比越高，肿瘤的恶性程度也越高，“脂联素点突变”的发现有希望成为肾癌检测与治疗的新靶点。“降低内脏脂肪含量一定是未来减少肾癌的发病率，降低恶性程度的重要思路。”叶定伟指出。而保持健康绿色的饮食、良好的生活习惯并且坚持锻炼是保障健康的不二法宝。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">基因谱预测新模型推进膀胱癌的精准化治疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">膀胱癌是泌尿系统最常见的恶性肿瘤，其发生与日常的生活习惯、所处环境都息息相关。在医疗进入大数据背景下，叶定伟和他的团队创新地将系统综述原理和生物信息学分析相结合，利用公共平台大数据及肿瘤医院的本地数据库，将原有的膀胱癌基因谱预测模型在600余名患者中进行整合和校正。整合后的新模型不但检测指标更精简，费用降低，而且预测效能较以往提高了10%，更有利于膀胱癌的精准化治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">叶定伟教授表示，目前膀胱癌的精准治疗着眼于基因谱研究，并从多种角度尝试将膀胱肿瘤分成不同的亚型。同样是膀胱癌，患者之间的病情特点和治疗效果区别很大。在大数据时代的背景下，预后模型的重新整合不仅使指标更加精简化，而且更具实战性。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由叶定伟教授研究团队整合的预后模型项目正在进行后续的多中心、跨检测平台的验证，有望在不远的将来应用于临床实践。不仅使精准治疗真正落地，对未来的推广也有很大程度上的积极作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对医疗而言，在大数据时代的背景下，更加精准地预测风险对于医疗人员及病患，乃至整个社会都有至关重要的意义。叶定伟教授表示，肿瘤没有完美的治疗方式，但多学科协作（MDT）综合诊治模式是当前最先进的治疗理念，对医疗研究提供了科学基础和交流平台，并且能够给患者带来实实在在的益处。特别是复杂患者，通过多学科协作可以得到最适合的个体化治疗，不仅就诊流程更加简便，还可使个体化诊疗成为现实。</span><br />
