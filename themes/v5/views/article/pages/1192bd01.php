		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>防治慢性肾病，沪上专家来支招</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">地点：第一七四医院住院大楼一层学术会议厅</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 内容：上海解放军第八五医院张景红教授，讲座题目为《慢性肾脏病的心血管问题》</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 上海第二军医大学附属长征医院叶朝阳教授，讲座题目为《血液透析相关并发症的防治》</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 上海第二军医大学附属长征医院朱有华教授，讲座题目为《肾移植的康复与保健》</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 本报讯 （记者 楚燕通讯员李金才）时值解放军第一七四医院肾病中心成立十周年之际，厦门市科学技术协会将和第一七四医院联合举办大型肾友联谊会。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">届时，三位国内著名的肾病专家将应邀来厦门开展慢性肾脏病防治健康讲座。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 第一七四医院肾病中心主任梁萌介绍，随着糖尿病肾病、高血压肾损害、老年人肾病的患病率增加，城市人口尿毒症发病率明显增加。据统计，厦门尿毒症患者占全市参保人员比例约万分之三，但医疗费用占医保总支出百分之八左右，是普通人群的300倍。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 梁萌说，厦门市肾脏疾病的诊疗水平近十几年来取得了长足进步。作为厦门驻军医院肾病专科，第一七四医院肾病中心在厦门率先开展了数十项新业务新技术，肾脏病诊治及尿毒症肾替代治疗技术不断完善，为数以千计的肾友们提供了优质的医疗服务，成为闽西南地区主要的肾脏病诊疗中心之一，曾被福建省肾脏病学会评为“血液透析先进单位”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 梁萌提到，肾病中心的医生们在临床工作中发现，群众对肾脏病常识的了解很匮乏，甚至部分基层医院的医生在这方面的知识和经验也十分有限。比如，相当一部分体检项目不包括常规尿液检查；有的人对长时间尿检或肾功能异常置之不理；有的人相信各种偏方制剂或夸大宣传的广告；有的滥用感冒药、止痛药，导致肾功能衰竭……</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 为了增强人们的健康意识，肾病中心多次举办肾友联谊会，组织健康科普讲座，编辑科普杂志《闽南肾友》免费赠阅，并建立科普博客提供网上健康咨询服务，帮广大肾友走出肾脏病诊治的误区。</span>
</p>
