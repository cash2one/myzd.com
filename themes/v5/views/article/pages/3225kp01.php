			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>如何发现膀胱癌？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么是膀胱癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">膀胱癌即膀胱恶性肿瘤，以尿路上皮细胞癌多见，占90%以上，其次为鳞状细胞癌和腺癌。膀胱癌发病率较高，主要发病年龄在中年以后，儿童、年轻人少见；男性多于女性，城市多于农村。膀胱癌特点是多中心，易复发。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如何发现膀胱癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">膀胱癌早期症状为间歇性全程无痛性血尿，可表现为肉眼血尿或镜下血尿。血尿出现时间及出血量与肿瘤恶性程度、分期、大小、数目形态并不一致。少数病人也有以尿频、尿急、排尿困难和盆腔疼痛为首发症状。晚期膀胱癌患者可能出现体重减轻、肾功能不全、腹痛或骨痛。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">需要做哪些检查确诊膀胱癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于可疑膀胱癌患者必须做相关检查。B超检查可以发现绝大多数膀胱病变，能够发现小于0.5cm肿瘤，准确率较高。CT和MRI检查能了解膀胱癌大小、浸润深度、周围侵犯情况以及与输尿管的关系，同时能与血块鉴别。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尿细胞学查即是采集尿标本，显微镜下找寻癌细胞，阳性意味着患者泌尿系统存在尿路上皮癌的可能；此项检查敏感性低，也就是说尿细胞阴性并不能排除尿路上皮癌的存在。恶性度高的肿瘤敏感性高于恶性低的肿瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">膀胱镜检查和活检是诊断膀胱癌最可靠的方法。通过膀胱镜检查可以明确膀胱肿瘤的数目、大小、形态、部位以及周围膀胱粘膜的异常情况。同时可以对肿瘤和可疑病变进行活检以明确诊断。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为什么对早期膀胱癌患者可以直接行经尿道切除而省去膀胱镜检查？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果影像学检查发现膀胱内有非肌层浸润的早期肿瘤占位病变，可以省略膀胱镜检查，直接行经尿道电切术，这样可以达到两个目的，一是切除肿瘤，二是明确肿瘤的病理诊断和分级、分期，为进一步治疗以及判断预后提供依据。</span>
</p>
