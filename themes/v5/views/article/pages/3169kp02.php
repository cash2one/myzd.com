	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>刘庆教授关于脑膜瘤诊治相关问题访谈内容</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1&nbsp;提问：海绵窦区无症状的脑膜瘤，需要治疗吗，还是随诊?</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：&nbsp;对于海绵窦区脑膜瘤，最新国外综述指出其治疗的原则已由全切肿瘤转变为在尽可能降低治疗和肿瘤占位效应相关致残率的前提下控制和缓解症状。对于该部位脑膜瘤的治疗方式主要包括保守观察、放疗、部分切除肿瘤并进一步随访观察或进一步放疗。对于外科治疗，目前多数研究关注于肿瘤部分切除后肿瘤控制率的情况。目前治疗策略多种治疗方式下的控制肿瘤进展。&nbsp;我们认为，对于无症状小型（≤75px）起源于海绵窦内的脑膜瘤，首先采取保守观察的态度，在定期复查过程中出现临床症状或是发现肿瘤增大，则再采取手术治疗或放疗。手术的目前在于肿瘤减压，缩小肿瘤体积的同时尽可能保留颅神经功能，术后辅助于放射外科治疗。对于大型海绵窦外起源累及海绵窦内的脑膜瘤，可先采用积极的手术方式，尽可能在保留颅神经功能的前提下全切除肿瘤。若肿瘤质地硬韧，与颅神经关系不清，不必勉强切除，术后进一步采用放射外科治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2&nbsp;提问：良性脑膜瘤第一次手术全部切除，但增生的脑膜没有处理彻底，连着三年复查都没有复发，最后一次是今年一月份的CT都一切正常，7月份复查时在原肿瘤后约75px处，肿瘤复发，大小约7*200px，切突破颅骨，生长至皮下，病理回报：间变型脑膜瘤，这种良性恶变多见吗？第二次术后，还应怎么治疗?</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：良性脑膜瘤约占颅内肿瘤的13%~26%，其中非典型脑膜瘤约占5%~7%，而间变型脑膜瘤的发病率为1%~2.8%。对于非典型脑膜瘤和间变型脑膜瘤,&nbsp;手术仍是首选治疗。即使复发,&nbsp;只要患者条件许可,仍应考虑再次手术。脑膜瘤手术的最终目标是完全切除肿瘤的同时切除受累的硬膜和颅骨。&nbsp;我们在临床上也注意到复发性脑膜瘤的生物学行为已发生改变，尤其是反复复发的脑膜瘤，具有潜在的恶变倾向，因此，特别强调首次手术时尽可能彻底切除肿瘤及其周边受累的硬膜及骨质。&nbsp;非典型脑膜瘤和间变型脑膜瘤的预后多不良。间变型脑膜瘤术后可考虑行放疗，但疗效尚不确切。立体定向放射治疗应用于残留脑膜瘤或复发的小型肿瘤的治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3&nbsp;提问：脑膜瘤大多为良性肿瘤，分型也较多，但术后复发的确令人头痛。第一次手术的时候病理证实为某种亚型，但复发时病理证实为另一亚型，更甚者出现恶变。有报道称这是由于首次手术造成的。这种观点正确吗？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：一部分脑膜瘤（WHO I&nbsp;级）复发时可能出现生物学行为改变，病理级别升高，广泛侵袭周围硬膜及骨质，甚至存在远隔部位再生或种植转移等情况，不一定与首次手术治疗直接相关。但可能与手术操作破坏原有正常蛛网膜界面等有关，影响了肿瘤的生长方式。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">4&nbsp;提问：矢状窦中段旁脑膜瘤在切除过程中如何保护皮层静脉和矢状窦？矢状窦切开后出血您有哪些处理经验？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：对于皮层静脉的保护，剪开硬膜时必须避开粗大的大脑上引流静脉，必要时需游离皮层静脉几毫米或避开静脉的走行。切除肿瘤时，可优先处理部分肿瘤基底，阻断肿瘤血供，再行肿瘤内充分减压，使肿瘤与受累静脉自然松解，再沿肿瘤与静脉的蛛网膜界面切除肿瘤。切不可在未行减压时强行分离受累静脉。&nbsp;对于肿瘤累及上矢状窦时应将骨瓣跨越中线至对侧，以达到充分显露肿瘤。特别是硬膜侵润区，使肿瘤周围的正常组织得到充分显露。视肿瘤累及矢状窦程度以不同方式处理肿瘤。若矢状窦完全闭塞，可结扎肿瘤两端的上矢状窦，彻底切除肿瘤；若矢状窦部分闭塞，则沿肿瘤一端逐渐分离窦内肿瘤，同时行矢状窦边缘连续缝合；若矢状窦壁受累严重，无法缝合，则可取自体大隐静脉或人工硬膜修复。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">5&nbsp;提问：如何从术前的影像资料，判断脑膜瘤的质地，硬度，血供情况？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：脑膜瘤质地判断：质硬的肿瘤MRI上T2WI信号强度多呈低信号或稍低信号，肿瘤边界清楚，有完整包膜；质软的肿瘤T2WI信号强度多呈高信号或稍高信号，肿瘤边界欠清，包膜不完整。血供丰富的肿瘤在MRI增强像上往往显著增强，肿瘤血供一般来自受累硬膜，具体血供来源因肿瘤具体位置不同而异；部分肿瘤存在双重供血，往往在T2WI上存在明显瘤周水肿，肿瘤表面形态不规则。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6&nbsp;提问：根据您的分型，蝶岩斜脑膜瘤和岩斜蝶脑膜瘤是有区别的，您从何鉴别？总的来说，手术全切率都不是太高，您怎样做到功能保护和手术切除程度的统一？对于未切除的部分，您如何后期治疗？谢谢。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：蝶岩斜脑膜瘤和岩斜蝶脑膜瘤鉴别：二者主要强调的是肿瘤基底起源与生长方向的不同，蝶岩斜型指肿瘤基底起源于鞍旁-海绵窦，经岩斜裂向斜坡区发展；岩斜蝶型指肿瘤基底起源于中上斜坡硬膜，向外向上延伸至岩骨斜坡裂、岩骨嵴、蝶鞍旁，经Meckel腔到达海绵窦后壁，甚至侵犯海绵窦，向中颅窝发展。&nbsp;对于不同分型的肿瘤针对性的选择合适的手术入路，术者自身的显微手术技术，根据患者自身的特点制定个体化的治疗方案，才能尽可能实现功能保护和手术切除程度的统一。我们仍然强调，首次手术时应尽可能有效切除肿瘤，即争取全切除肿瘤，或术后放射外科可能控制肿瘤。&nbsp;对于未切除肿瘤部分，可以在患者术后2~3月后限期行伽玛刀治疗，尽可能的控制肿瘤的复发或进展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">7&nbsp;提问：对术中侵犯的颅骨如何处理，能自体颅骨消毒一期修复吗？如何消毒？如何减少术后复发？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：国内外研究一般认为被侵犯的颅骨，存在肿瘤细胞浸润，切除肿瘤过程中需要连同受累骨质一并切除，对于缺损颅骨可采用人工材料修补；自体骨高温高压消毒可考虑但不推荐。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">8 &nbsp;提问：请问术中您怎么判断受压迫的神经或包裹的神经是否有功能，术中怎么对神经的保护？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：可根据术前患者症状体征及神经电生理检查结果判断颅神经功能情况，但术中所有神经均应尽可能的完整妥善保留。熟悉解剖结构是神经保护的基础，根据肿瘤的病理特点、起源、生长方式并结合术前影像学判断神经的走行；术中优先处理安全区域的肿瘤，充分减压，使肿瘤与神经自然松解，为肿瘤与神经的剥离创造条件。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">9&nbsp;提问：另外，不同手术入路都有其相应的优势和局限，请问在具体临床工作中比较优先推荐哪种方式？各类手术方式注意事项有哪些？</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">答：不同的手术入路选择由多种因素共同决定，诸如肿瘤的性质、位置、大小、质地、与周围神经血管的关系，以及患者的年龄、术前身体状况、术者经验等因素综合考虑选择手术的入路。一般而言，手术入路的选择应尽可能的简单化、创伤小、手术区域显露充分，目前颅底外科中的额下入路、翼点入路、颞下入路及枕下乙状窦后入路已基本能完成绝大部分颅底手术。</span>
</p>
