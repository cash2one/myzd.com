			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>口腔癌应该怎样治疗？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">口腔癌在亚洲的发病率很高，这和吸烟，饮酒，饮食习惯，HPV病毒感染等因素有关。临床上经常碰到许多患者朋友因为治疗不当，使得本来可以得到根治的口腔癌失去了治愈的机会。另外，我国临床分科同时存在头颈肿瘤外科和口腔颌面外科，这为患者就医该选择哪个专科造成了一定的困惑。本文就想简单讨论一下口腔癌的合理治疗问题，希望给患者朋友提供一个知识普及和就医参考。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. 口腔癌的治疗应该以手术切除为主。早期，中期和部分晚期患者都应该首次治疗就选择根治性手术切除，在此基础上配合其他治疗，根治疾病的机会就会大增。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 口腔癌的手术通常很大，而且会影响咀嚼，吞咽，言语，美观等重要功能。这时修复重建手术通常需要与根治切除一起完成。正是这个因素限制了很多患者的救治，因为修复重建手术通常比较复杂，需要显微外科和整形外科技术，并不是哪个医院都具备这些条件。造成很多需要手术的患者因为担心术后功能的影响而没有进行根治性切除手术，首次治疗仅选择化疗或者放疗，结果造成本来可以简单根治的疾病不但治疗花费大增，还没能得到根治。所以，口腔癌的手术治疗通常比较复杂，需要更专业的医生和技术才能得到好的治疗效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. 晚期口腔癌的治疗应该综合应用手术，放疗和化疗。诱导化疗加手术，同期放化疗，序贯治疗，靶向治疗都是不同情况下的治疗方式。如何选择需要个体化，每个病例都是不同的，应该和医生仔细讨论选择最佳的方案。</span>
</p>
