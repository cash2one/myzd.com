		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>甲状腺结节，切还是不切？--《中国科学报》（科普）</strong></span>
</p>
<p align="left">
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;近日（2013-3-15）接受《中国科学报》记者采访，题目是“甲状腺结节，切还是不切？”。有兴趣者可以参考以下链接&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">相关链接：</span><br />
<a href="http://news.sciencenet.cn/sbhtmlnews/2013/3/270680.shtm?id=270680"><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">http://news.sciencenet.cn/sbhtmlnews/2013/3/270680.shtm?id=270680</span></a><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">本报记者 张思玮</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“一旦发现甲状腺结节，也不必过分紧张。因为大多数甲状腺结节都是良性的。但同时，也不能忽视甲状腺癌发病率节节攀升的趋势。”日前，北京协和医院基本外科主任医师刘跃武教授在接受《中国科学报》记者采访时表示，随着大众健康意识的增强，体检逐渐普及，很多人都会在体检中被查出患有甲状腺结节。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">据统计，在超声检查中，约有20％以上的正常人均可发现甲状腺中有结节。其中女性发病率是男性7倍，而女性中又以育龄期妇女、中青年女性为多见。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">甲状腺是人体最大的内分泌腺体，负责合成甲状腺激素，调节机体代谢，对人体的生长发育具有重要作用。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“当甲状腺激素生产过剩时，人体代谢加快，容易出现呼吸、心跳加速，大汗淋漓等症状，称为甲亢；反之，当其供应不足时，人体代谢减慢，便会出现怕冷、嗜睡、皮肤粗糙等症状，称为甲低或甲减。”刘跃武说。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">通常早期无症状</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">其实，人的甲状腺里面本来是质地均匀，没有结节。但如果长了结节，有可能是癌或腺瘤，也有可能是增生的结节，但在没有定性之前，都统称为结节。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“这就好比我们看一个人，假设光线不好，没有看清楚他是一个什么样的人时，我们只知道这是一个人，等走近一看，才能确定是一个男人、女人、老人或者小孩。”刘跃武介绍，甲状腺结节根据B超的检查结果可分为单发结节和多发结节；根据同位素的检查结果，可分为热结节、凉结节和冷结节。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">早期的结节，只是在甲状腺体上的单个或多个大小不一的肿块，呈圆形或椭圆形，表面光滑，可随吞咽上下移动，质地较正常腺体略硬，无压痛的肿块可长期维持原状或缓慢长大。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“患者此时通常没有任何症状。”刘跃武说，随着结节的增多或增大，有些患者可能会出现面部潮红、腹泻、肿块侵犯邻近组织和器官，引起声音嘶哑、呼吸困难、吞咽障碍等情况。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">不过，一旦甲状腺结节产生恶变，患者则会有颈部胀痛、紧迫感、肿块增大迅速等不适感。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">究竟什么原因会导致甲状腺疾病的高发呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">刘跃武认为，这主要与工业化的进程加快、辐射增加等有一些关系。工作环境有辐射、情绪长期处于压抑状态，或者情绪反复、剧烈波动；生活规律明显颠倒紊乱，都会引起内分泌和甲状腺代谢的异常，出现甲状腺结节。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">此外，研究还显示，缺碘及过量碘摄入也可能导致甲状腺结节。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">须综合判定</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">那么，一旦在体检中，发现甲状腺结节，我们该怎么办呢？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">刘跃武建议，首先去一个比较专业的医疗机构，尤其是第一次就诊，最好能得到确诊。“通过甲状腺B超、甲状腺功能、细针穿刺活检等检查确定甲状腺肿块的大小及性质。其中，彩超对结节的良恶性鉴定的准确率接近85%。”</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">如果彩超显示结节是实质性包块、点状微钙化、边界不规则、其内血流丰富、周围组织浸润或伴有淋巴结转移，便可高度怀疑为甲状腺癌。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">其次，要检查甲状腺的功能。抽血查甲状腺激素是判断甲状腺功能的量化指标。此外，核素显像也对判断甲状腺功能有一定价值。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">刘跃武告诉记者，一般只要结节超过1厘米，就应该引起重视。一旦超声提示出现低回声、微小钙化或边缘不规则等任何恶性的蛛丝马迹时，必要时可以进行穿刺检查。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">不过，在很多情况下，检查结果往往提示，甲状腺结节为多发的，且大小并不太规律，但功能却是正常的。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“这时候的处理，就会显得有些犹豫，需要超声定期检查结节的状况。”刘跃武说，有些多发性的良性结节，最好等一等再手术，等到结节超过4厘米，或者具有颈部压迫症状后再行手术。因为这个结节容易再长，倘若一有结节就切除，那么很可能有些人一生中要接受3~4次这样的手术，这显然不值得，还容易损伤神经导致声音嘶哑等并发症。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">定期检查不可少</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">由此看来，甲状腺结节的定期检查必不可少。刘跃武觉得，定期检查可以观察到甲状腺结节的大小以及性质的变化。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“有一些结节虽然长得很大，但可能是良性的。但也有一些结节可能长得非常缓慢，但随着时间的推移，结节中间若出现钙化点，很可能出现癌症的特征。”刘跃武说，如果没有结节，一年查一次即可；如果已经有结节了，且是良性的，半年或者大半年查一次即可；如果结节总体像一个良性的，但有一些癌的特征，最好3个月复查一次。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">毕竟，甲状腺癌的高发趋势也让人不容小视。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">据北京协和医院资料显示，1986年该院全年甲状腺癌手术例数仅为8例，而2012年该院全年甲状腺癌手术例数为1125例，是1986年的140倍。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">多数可治愈</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“一旦证实了结节是恶性肿瘤，也不必谈癌色变。因为常见的甲状腺癌多为乳头状癌，其治愈率较高。”刘跃武坦言道。即便是有些病人术后又有复发，但只要及时发现和手术，对生活质量和寿命没有太大影响，不必太紧张和怕耽误治疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">据了解，甲状腺癌可分为四大类：有乳头状、滤泡状、髓样癌和未分化癌。其中最常见的是乳头状癌，约占甲状腺癌的85%以上，其次有10%左右的是滤泡状癌，还有5%左右是髓样癌、1%~2%是未分化癌。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">刘跃武说，在甲状腺癌的四种类型中，乳头状癌发生率最高、治疗效果最好；其次是滤泡状癌，但它能在早期通过血管转移到其他脏器，比如说骨骼、肺和大脑；而髓样癌既通过淋巴转移，又通过血液转移，治疗起来比较棘手。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">“治疗效果最差的是未分化癌，目前几乎没有好的治疗手段，从发现到去世也就是三个月到半年左右。”刘跃武觉得，早发现、早诊断、早治疗仍是有效治疗甲状腺癌不可或缺的手段。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">而对于有些人担心甲状腺切除后，身体会因缺乏分泌的甲状腺激素而影响新陈代谢，刘跃武语气坚定地说：完全没有必要。因为现在人工合成的甲状腺素纯度已经很高了，服用非常方便。</span>
</p>
