			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>盲目打“针”治疝，反被治成“排球”</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">本报2月25日讯（记者 樊伟宏 通讯员 勾翠华） 24日，记者从北大医疗鲁中医院获悉，医院普外二科疝诊疗中心近日成功为一位高龄患者实施疝修补术，患者因为多次注射硬化剂不见效果，差点延误治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">83岁的周先生家住张店，5年前发现得了腹股沟疝，因同时患有高血压、冠心病，周先生害怕手术会有风险，就听人介绍去打“针”，也就是所谓的打硬化剂。为了尽快治愈，周先生每半个月去打一次，半年下来共打了10多次，但效果甚微，不只如此，周先生腹股沟区反而多了好多硬块，感觉非常难受，疝也越来越大，已接近排球大小，不仅进入阴囊，有一次还并发了嵌顿疝，老人备受煎熬，辗转多家大医院都因为风险大、病人年龄大等原因而遭到拒绝。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">机缘巧合下，周先生来到北大医疗鲁中医院就诊，该院疝气专家梁存河教授仅用局麻就成功给老人完成了手术，不仅取出了所有的硬化剂，还修补了巨大的疝缺损，老人术后当天就进水进食，下地排尿，没有感觉到明显疼痛，恢复的如此之快让老人一家喜出望外。</span>
</p>
