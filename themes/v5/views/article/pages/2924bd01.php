		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>两个“10”的故事</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">10岁男孩，颅脑内长出举世罕见肿瘤；历10余时手术，脑肿瘤专家生死搏弈成功切除。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 10岁男孩的脑袋里就长了个鸭梨大小的间叶性软骨肉瘤，头痛欲裂，痛不欲生。今年9月，湖南省肿瘤医院神经外科主任何正文教授率领手术团队，历经10多小时，成功对其实施左侧大脑额顶叶巨大肿瘤切除术。经文献检索，近10年国内外未见相关报道，实属举世罕见。12月28日，何正文电话回访，男孩未见任何神经功能损害，与正常儿童无异。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 患儿周怡（化名），今年8月间出现头痛、喷射性剧烈呕吐、左眼视物不清伴意识模糊等症状，周怡的父母带着他就诊于省内多家医院，因其症状重，基础状态极差，有极高的术后死亡率及神经功能损伤风险，诸多医院望而却步。最终，患儿辗转就诊于湖南省肿瘤医院脑肿瘤专家何正文教授，何正文详细查看患儿及相关检查资料，发现肿瘤大小约为10厘米×8厘米×7厘米，相当于鸭梨大小，犹如一颗“定时炸弹”，随时威胁患儿生命，而手术排除是唯一办法。然而，患儿肿瘤所在位置特殊，且体量巨大，加之患儿身体瘦弱，手术存在极大风险，术后很可能出现右侧偏瘫，偏身感觉障碍，且失去语言功能，抑或手术失血过多，致人财两空。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 面对错综复杂的病情，何正文精心设计手术方案，同时做了多份预备方案，完善术前各项准备。9月23日，患儿被推上手术台。开颅可见肿瘤位于左侧额顶叶，肿块实质部分质中偏硬，囊性部分为多房，囊液约80毫升，肿瘤包膜不完整，与周边脑组织粘连较重，并侵入左侧脑室，部分囊壁与侧脑室壁融合。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 何正文和他的团队凭借娴熟的显微神经外科解剖技术、精准的手术操作，细致分离瘤壁包膜，经过10余小时生死搏弈，以最小的损害，干净利落切除肿瘤。患儿术后恢复良好，头晕、头痛、恶心的症状明显减轻，术后不到1周就能下床，四肢活动自如，语言功能正常，无其他神经功能损害。周怡的父亲千恩万谢：“感谢何教授妙手救命之恩！”</span>
</p>
