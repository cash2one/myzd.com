	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>26岁女子右肾长8斤巨瘤 2小时手术成功切除</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">红网长沙5月30日讯（记者 董雷 通讯员 罗闻 姜丽）你听说过的肿瘤中最大的有多重？26岁的汤小姐右肾上一颗重达8斤的巨瘤，或许会刷新你的知识库。这颗足球般大小的瘤子如同一枚“定时炸弹”，不仅令她终日不堪重负，还随时可能危急她的生命。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 近日，中南大学湘雅医院泌尿外科齐琳教授领衔的泌尿生殖肿瘤团队为汤小姐成功实施了右肾巨大肿瘤切除术，让患者终于如释重负。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 汤小姐在怀胎7个月时便发现右肾有一较大占位病变，孩子还未出生，却迎来如此噩耗，令汤小姐一家顿时措手不及。为了孩子，她毅然决定冒险生子后，再进行手术切除肿瘤。。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3个月后，汤小姐虽顺利产下一子，但肿瘤也越长越大，肚子显得依然“坚挺”，手术难度和风险也随之增大。面对其他医院退避三舍的巨大肿瘤，湘雅医院泌尿外科主任齐琳教授给了她一颗“定心丸”。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 术中，专家发现那颗巨大肿瘤已经占据了汤小姐的半个腹腔，并与腹主动脉和腔静脉等大血管关系密切，稍有不慎便可能危及患者生命。凭借精湛的手术技艺，历时不到2小时，专家便成功将那颗足球般大小、重达8斤的肿瘤完整切除。术后，躺在病床上的汤小姐不仅恢复顺利，也放下了那颗悬着的心。</span>
</p>
