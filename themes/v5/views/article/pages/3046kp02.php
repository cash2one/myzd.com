			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>关注腹壁肿瘤</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 腹壁肿瘤是腹壁外科常见的占位性病变，从病理分型可有良恶之分。腹壁良性肿瘤有硬纤维瘤、纤维瘤、神经纤维瘤、血管瘤、乳头状瘤、皮样囊肿等。恶性肿瘤有纤维肉瘤、隆突性皮肤纤维肉瘤、黑色素瘤、皮肤癌和转移性癌等。。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 【临床表现】</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 临床上表现为腹壁内无痛性卵圆形肿块，质地坚硬，生长缓慢。在大多数病人，肿瘤直径达数厘米时即被发现。当腹肌收缩时，仍能清楚地扪及肿瘤轮廓，可以与腹腔内肿瘤相鉴别。少数延误就诊者，肿瘤向四周呈片状浸润性生长，大片腹壁僵硬，直径可达10几厘米。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 【病理说明】</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肿瘤多来源于腹直肌、腹外斜肌、腹内斜肌、或腹横肌的肌鞘或腱膜，向肌肉内呈浸润性生长。肿瘤无包膜，边缘不规则，呈蟹足状；质地坚韧如橡皮；切面灰白色，呈交错编织状。显微镜下，肿瘤由丰富的胶原纤维和较少的纤维细胞组成，呈平行排列；细胞无异型性和核分裂相；在肿瘤的边缘，常可见到被肿瘤包绕的横纹肌组织小岛。肿瘤组织形态属良性，但极易复发。本病以青壮年多见。肿瘤通常发生于真皮，呈隆凸状。镜下见肿瘤由梭形细胞组成，细胞长短、胖瘦不一，多呈席纹状或漩涡状排列，有些细胞可有较显著的异型性，核分裂相少见，属一种低度恶性的肿瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 【治疗说明】</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 治疗上应手术切除。范围包括肿瘤及其附近的正常肌肉、肌膜和腱膜，如累及腹膜亦应部分切除。切除不彻底易致复发。如肿瘤切除后腹壁缺损较大，应采取局部组织转移结合人工材料修补的办法进行腹壁修复，以防形成切口疝。肿瘤范围很广不能切除时，可试用放射治疗。</span>
</p>
