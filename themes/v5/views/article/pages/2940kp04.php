<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>为什么后尿道吻合术中要凿除部分耻骨？&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在结合了阴茎海绵体中隔切开和耻骨下缘部分凿除两项操作技术后，不仅手术视野暴露清晰，而且缩短了后尿道吻合的走行途径。其目的在于：1、切除耻骨下缘与游离远端尿道在会阴一个切口中完成，弥补了单纯会阴径路无法处理4cm以上复杂性后尿道狭窄的缺陷，又减少了因尿道缺损较长需经耻骨途径的手术创伤；2、术中采用锲形切除耻骨下缘，可以充分暴露后尿道所在的空隙，有利于器械的精细操作；3、切除耻骨下缘后，可同时切开阴茎海绵体中隔使远端尿道从中穿过，或远端尿道穿过一侧阴茎海绵体脚，再通过已切除的耻骨下缘，尿道可内隐于该空隙中，可使前后尿道间距离缩短约4～5cm，基本达到无张力吻合。同时避免了因过分游离远端尿道，出现阴茎短缩的现象；4、一旦该径路手术失败，以后仍可通过经耻骨径路进行后尿道手术；5、尿道端端吻合的关键之一是充分切除尿道周围的疤痕组织，直至正常健康的尿道黏膜。由于本研究中涉及的病例尿道周围疤痕严重，切除的范围较长，以上两项技术可保证大部分病人的尿道进行无张力吻合。</span>
</p>
