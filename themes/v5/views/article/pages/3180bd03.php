<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>断足成功寄养大腿 湘雅名医为女童再施保肢绝技</strong></span><br />
</div>
<br />
<br />
<br />
&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3180/1.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 断肢X光影像（欧原 摄）</span><br />
&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3180/2.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 医务人员为小慧实施手术。（欧原 摄）</span><br />
&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3180/3.png" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 成功将断足寄养在大腿上。（欧原 摄）</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 红网长沙3月3日讯（时刻新闻记者 易征洋 通讯员 黄辣 吴攀峰）在成功创造了3例断手寄养小腿和腹部保肢的“奇迹”后，近日，中南大学“湘雅名医”、湘雅医院手显微外科主任唐举玉教授率领科室全体医务人员，为车祸导致左小腿完全离断的2岁女童成功将断足寄养在大腿上，攻下了保肢第一关。唐举玉教授团队接下来还将为小姑娘进行寄养断足回植和小腿牵伸再生延长等两次大型手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 车祸致女童左小腿离断，保肢手术难度超过以往</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2月28日，2岁9个月的小慧不慎被运煤卡车撞倒，左下肢被车轮严重碾压，从腹股沟到足踝部整段皮肤套脱损伤、小腿毁损性离断，伤后发生创伤失血性休克，病情危重。小姑娘随即被送往湘雅医院救治。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “如果按照传统治疗方法，为了保命只能舍弃保肢，可以从髋关节处离断进行高位截肢。”手显微外科主任唐举玉教授接诊小慧后，对小慧的病情进行了精准判断。截肢手术虽然相对简单、手术风险小，但这也意味着两岁的小慧要一辈子依靠假肢行走，而且一生要多次更换假肢，同时还可能因为高位截肢带来巨大的心理创伤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 与此同时，唐教授发现虽然小慧的小腿严重损毁，但足踝比较完整，膝关节虽有部分开放，但大腿的肌群尚在、神经没有撕脱缺损。于是，在反复权衡评估后，唐举玉教授决定再施保肢绝技，通过将断足寄养到对侧大腿上，也许可以为她觅得一线保肢生机。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “此次寄养足部与前三次寄养手部原理相同，但由于患者只有两岁多，是其中年龄最小的一位，使得血管吻合的难度系数增大，因此对术者的要求也更高。”唐举玉教授介绍道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 在为小慧制定手术方案时，根据患者的病情，专家们需要将血管吻合和组织覆盖两大问题综合考量，最终决定将断足寄养到右侧大腿，将右侧大腿的血管与左足吻合，用大腿的皮瓣覆盖血管足背皮肤缺损，等断足成活后，再将它回植到小腿残端。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 全科医生“兵分三路”，攻下保肢第一关</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2月28日，湘雅医院手显微外科医务人员在唐举玉教授的带领下，分为三组为小慧进行断足寄养手术。由于小慧伤情极重，因失血过多、创伤严重发生了休克，因此首先为小慧进行了抗休克处理。之后，第一组进行断足的清创处理，第二组进行残肢的清创处理，第三组将撕脱的皮肤反取皮回植。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 通过全体医护人员的分工合作，小慧的断足成功寄养到右侧大腿，以最快的速度重建了断足血运，目前生命体征平稳，已经转入ICU进行为期一周的密切监护。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 然而，断足寄养成活还只是保肢的第一步，小慧接下来还需要经历两次大型手术，包括寄养断足回植，同时要结合皮瓣移植重建组织缺损，这将是整个手术中最难的一步。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 唐举玉教授表示，即使小慧最终能顺利度过这最难关，她的小腿还是会比正常小腿短很多。如果左足能顺利成活，足部神经功能恢复好，后续又一关键将是通过Ilizarov技术将小腿牵伸再生延长，造出一条小腿，以恢复正常肢体长度和行走功能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “小姑娘才两岁，未来还有很长的路要走。我们将动用最好的技术、尽我们最大的努力再一次创造奇迹，让她避免截肢的厄运。”对于小姑娘接下来的治疗，唐举玉教授充满了信心。</span>
</p>
