	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>腰痛的预防和自我锻炼&nbsp;(转载)</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">锻炼需要注意什么？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 如果你之前有过腰部外伤史或其他腰部疾患，需要在你开始腰肌锻炼之前，咨询医师。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 缓慢地拉伸，避免太过突然的动作改变；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 注意你的拉伸动作，在你能力范围之内做这些姿势（产生轻微拉伸的感觉即可，避免产生疼痛等不适感），坚持5~10秒，再缓慢的恢复初始姿势；如果刚开始练习的时候，觉得肌肉酸痛，可以将保持姿势的时间减少为2秒钟，然后循序渐进地增加，直到5~10秒。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 在你锻炼的时候，深而慢地呼吸；</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 每次锻炼中，单个练习动作可重复5~10次，循序渐进。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 五步锻炼法：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 缓慢开始至让你感觉到肌肉轻度紧张的某一姿势；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 保持这个姿势5秒；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 还原；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 放松2秒；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 再次开始，重复3~5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 12个腰部锻炼法，可以：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> l 通过拉伸动作增加腰部肌肉的柔韧性；</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> l 通过对抗运动增加腰部肌肉的强度。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 一、拉伸运动</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （一）骨盆运动法</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 可以拉伸腹部及腰部肌肉，增加髋部柔韧性。</span>
</p>
<p>
	<img src="http://static.mingyizhudao.com/147003444018514" title="" alt="demo.jpg"/>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，屈膝，两膝内收，足底贴地或垫子上，两足间距离同肩宽，双手放在身体两侧；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 使腰部紧贴地面，使得髋部前倾；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持姿势几秒钟，然后放松；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 重复5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （二）单腿拉伸法</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 可以拉伸你髋部，下腰部及臀部的肌肉。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，一只腿屈曲，将脚面贴在地板或者垫子上，另一只腿可伸直；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 两手抱住屈曲的膝，向上拉伸至胸部，动作连贯，同时你的下腰部及另一个膝紧贴地面；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持这个姿势5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 放低活动腿，恢复初始动作，然后放松；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 同一侧腿重复做5次；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 6. 另一侧腿交替。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （三）双膝胸运动</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 这个练习可以拉伸髋部，臀部及腰部肌肉。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，膝部屈曲，双足底贴地，双手置于身体两侧；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 抬高双膝至胸口，可以用手轻轻拉双膝至胸口；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持姿势5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 放低双膝至初始姿势，稍作休息；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 重复5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （四）编麻花(Pretzel)</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 编麻花动令你大腿内侧及髋部得到延伸。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，双腿弯曲；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 把一只脚敲到另一只腿的膝盖上；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持这个姿势，然后两腿一起向胸部拉伸；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 保持姿势5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 放松，还原姿势；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 6. 换另外一只腿，重复这个姿势。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （五）侧弯练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 侧弯运动拉伸一侧及背部的肌肉。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 高举一侧上臂过头，连腰一起向对侧侧弯；另一只手可以叉在腰。侧弯的时候不要扭曲或者猛地用力拉伸；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 保持这个姿势5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 还原，两手置于身体一侧；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 重复5个来回；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 换另一边，重复1~4步骤5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （六）背伸练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 背伸运动能够拉伸你肩部、背部及髋部的肌肉。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 直立，双脚分开同肩宽，脚尖向前。将两手掌置于你下腰部；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 缓慢地吸气与吸气，让自己放松；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 两手叉腰，两腿伸直，呼气，上身向后仰；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 保持5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 慢慢还原；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 6. 重复做5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> (七)伸腿练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 伸腿运动可以拉伸大腿后侧肌群。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，一只腿屈曲，足底贴地，另一只腿伸直；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 将伸直的腿缓慢抬高，直到你感觉到大腿有轻微受牵拉的感觉。手可放在膝后以帮助固定和抬高运动的下肢。如果你的手够不到膝，可以把毛巾绕在膝盖或者大腿后，然后拉毛巾；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持姿势5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 慢慢将抬高的腿放低至地面上，并放松；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 重复如上动作5次；</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 6. 换另外一只腿，重复做如上动作。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 二、强度练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （一）挺腰练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 挺腰动作可以增强臀肌的力量。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，双膝屈曲，双足稍分开，同肩宽，足底贴地，双手置于身体两侧；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 一点点抬高髋部，不要拱起背。注意不要让你的腹部凸出来，这样可以避免拱背。保持肩和膝处于同一直线；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 缓慢放低髋部，复原；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 重复5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （九）不全仰卧起坐</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 不全仰卧起坐可以增强腹肌的力量。</span><br />
<img src="http://static.mingyizhudao.com/147003444576270" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 仰卧，屈膝，双足底贴地，同肩宽；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 双手在胸前交叉，保证你整个后背贴在地面上。将头及肩部稍稍抬离地面，缓慢抬高直至肩完全离开地面。刚开始抬的高度有限也没关系；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持姿势几秒钟，随着练习增多，你慢慢可以保持这个姿势5至10秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 慢慢将姿势还原；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 重复5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （二）拱背练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 弓背练习（像猫伸懒腰）可以锻炼背部及腹部的肌肉。</span><br />
<img src="http://static.mingyizhudao.com/14700344497193" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 跪在地板上，双手撑地，颈部平行于地面；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 通过收紧腹部及臀部的肌肉，拱起背部，稍稍低头；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 双手伸直，让拱起的背部向地板方向运动。记住将身体的重量均匀地分配到双手和双腿上；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 保持姿势5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 6. 重复5次。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （三）够手练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 够手练习可以增强肩部及上背部肌肉的力量。</span><br />
<img src="http://static.mingyizhudao.com/147003445636252" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 四肢着地，颈部与地面平行；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 伸出一侧手臂，注意不要抬起头。尽量将身体重量均匀分布到双膝及贴地的那只手上；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 还原；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 重复做5次；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 6. 换另一只手，重复如上动作。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （四）腿后伸练习</span><br />
<img src="http://static.mingyizhudao.com/147003445950027" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 四肢着地，颈部与地面平行；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 缓慢将一只腿平行于地面后伸。脚可以伸直或者弯着，只要你觉得舒服。注意在你后伸腿的时候，不要让你的腰部、头或者腹部下垂。做这个动作的时候，要确保你身后没有人以防踢伤。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 还原动作，并重复5次；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 换另外一只腿，重复如上练习动作。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> （五）滑墙练习</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 滑墙练习可以增强腰部、髋部及腿部肌肉的力量。</span><br />
<img src="http://static.mingyizhudao.com/147003446284449" title="" alt="demo.jpg"/><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 1. 直立，背贴墙，双足分开同肩宽，双手置于身体两侧。头中立位，两目前视；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 2. 背部沿墙慢慢向下滑动直至你膝弯曲呈90°，就好像坐在凳子上一样。如果你暂时不能降至这个水平，可以先练习滑动至一半的高度；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 3. 保持5秒钟；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 4. 缓慢甚至双腿，恢复初始姿势；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 5. 重复5次</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 结语：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 绝大部分人一生中都会出现腰痛。但是腰痛是可以预防的，脊柱和椎间盘的退行性变是可以推后的。预防的方法包括：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 锻炼腰部，使它更具柔韧性；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 平常就保持良好的身姿，养成良好用腰习惯；</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 避免意外和外伤；</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 如果超重的话，一定要减重。 </span>
