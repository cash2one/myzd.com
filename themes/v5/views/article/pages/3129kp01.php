			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>心脏瓣膜病的常见原因和治疗方法</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;人的心脏中主要有四组瓣膜，它们是左侧心房和心室间的二尖瓣，左心室和主动脉间的主动脉瓣，右心房和右心室间的三尖瓣，以及右心室和肺动脉间的肺动脉瓣。心脏是人体的发动机，而心脏瓣膜所起的作用象单向阀门，瓣膜规则地启闭运动，保持血流沿一定的方向顺序循环。心脏瓣膜发生病变，会严重影响心脏功能，甚至危及生命。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;心脏瓣膜病变的主要原因有以下几种：1. 风湿性心脏病 （风心病），是链球菌感染后继发的自身免疫性疾病，主要引起瓣膜的进行性纤维化，增厚，甚至发生钙化。2. 感染性心内膜炎，常见于细菌和霉菌感染，主要引起瓣膜穿孔，撕裂，甚至毁损。3. 缺血性心脏病，多见于心肌梗塞后，由于瓣膜乳头肌缺血坏死，或发生断裂，导致二尖瓣关闭不全。4. 瓣膜退行性病变，多见于老年人，瓣膜发生纤维化，钙化的改变，近年有增加趋势。5. 先天畸形，瓣膜的先天畸形多种多样，可以是缺如、狭窄、闭锁、裂缺等等。6. 其它如肿瘤侵犯、外伤等也可以导致瓣膜病变。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;无论何种原因，瓣膜的改变可以表现为：1. 瓣膜开启受限，瓣口狭小，血流通过受阻；2. 瓣膜关闭不全，血流返流；3. 以上两种情况并存。感染性心内膜炎和心肌梗塞所致的瓣膜功能障碍，起病急骤，病人可在短期内明显加重。但常见的风心病和瓣膜退行性病变，具有慢性、渐进性和隐匿性的特点，大多数病人在早期可无症状，只是在体检或自觉活动后心慌气急而就诊时，才被发现患有该病。而事实上，这类患者可能已经历了数年、乃至十余年的心脏瓣膜病变过程。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;瓣膜病导致心功能不全的内科治疗主要包括强心、利尿、扩血管和针对病因的药物治疗等方面，以改善心功能和延缓病程。如瓣膜病变为单纯的纤维粘连性狭窄也可进行经皮穿刺心导管球囊扩张术。外科手术治疗可进行瓣膜成形修复，或进行瓣膜替换手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;心脏瓣膜病病人若拖延接受手术的时间，瓣膜病变会渐趋加重，导致心肺和全身其他重要脏器功能进行性损害，严重者可发生顽固性心力衰竭、心源性肝硬化和全身恶病质，丧失手术机会。心瓣膜病变严重者，还经常并发心房纤颤和左心房血栓形成，病人有发生脑栓塞或其他器官栓塞的危险。所以，已有明显瓣膜病变的病人，且有明显临床症状者，应尽早手术治疗，以免贻误治疗时机，酿成严重后果。</span><br />
