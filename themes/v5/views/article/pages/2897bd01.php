<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>2014ESMO肺癌治疗新进展(转载)&nbsp;</strong></span>
</div>
<br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2014年欧洲肿瘤协会年会（ESMO）于9月26日-9月30日在西班牙马德里召开，本文介绍本次年会中关于肺癌诊疗的新进展</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. SABR治疗早期非小细胞肺癌</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">立体定向消融治疗（SABR）为采用外照射技术，分1到数次（低于8次），将放射治疗的高剂量精确照射到肺部肿瘤病灶，从而使肿瘤受到高剂量，肿瘤周围正常组织受到低剂量照射的一种特殊放疗技术。对于不适宜手术或拒绝手术治疗的患者，ESMO指南推荐行SABR治疗。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">SABR的毒副反应较少，发生率低于10%，主要有放射性肺炎，胸壁疼痛，肋骨骨折等，SABR对于肺功能的损害较小。但对于存在基础间质性肺病的患者，发生致命性放射性肺损伤的危险性显著增加。目前SABR治疗早期非小细胞肺癌的数据主要来自于两项临床研究数据，另有两项临床试验正在进行中。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">门诊SABR治疗有效地提高了高龄肺癌患者的治疗率。对于手术风险高的患者，SABR是一种很好的治疗替代选择。三项前瞻性随机对照临床研究，比较可切除肺癌患者使用SABR或手术治疗的优劣，目前已完成病例收集工作，该结果的公布将明确SABR在可切除肺癌治疗中的价值和地位。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 多发肺结节的诊治</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床上存在很多患者表现为肺部多发结节，其中一个为主要病灶，往往诊断考虑肺癌，其余则为次要肺结节。IASLC肺癌分期指南将恶性多发肺结节根据结节所在肺叶进行分期，同叶多发肺结节为T3，同侧非同叶多发肺结节为T4，不同侧肺叶多发肺结节则为M1a。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前早期肺癌存在额外多发肺结节的几率尚未明确。部分研究认为早期可切除肺癌，经影像学评估存在20-25%的额外肺结节，其中25-30%为恶性结节。上述IASLC分期指南关于恶性多发肺结节的分期不应适用于同期第二原发肺癌（SPLC），以及良性可能性大的肺结节，如亚厘米肺内淋巴结等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同期第二原发肺癌与肺内转移可经Martini-Melamed标准鉴别区分。最近，区域癌化（field cancerization）理论部分解释了第二原发肺癌现象。因同期第二原发肺癌独立于另一原发肿瘤，故两者均应行独立TNM分期。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多发肺结节的患者需经临床多学科讨论后确定诊疗方案，不同病灶间的生物学联系也影响着肿瘤的诊断和治疗，还需详细地评估患者是否存在远处和纵隔转移，建议PET/CT和头颅MRI/CT评估胸外转移，纵隔镜或超声内镜评估纵隔转移，对于同侧多发肺结节，若行手术切除治疗，无需进一步的创伤性检查，而若为双侧多发肺结节，行单侧手术前需行内镜或经皮穿刺活检明确对侧病灶是否为转移（M1a）。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. 早期非小细胞肺癌治疗的靶向治疗</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于弥漫性的微小肿瘤转移灶的存在，肺癌局部治疗方法生存效果欠佳。围手术期化疗对于提高生存效果有限，且毒副作用较大。同期化疗能提高部分局部进展期肺癌的放疗效果，对大部分患者作用有限。目前研究表明肺癌存在多种驱动基因，50-80%的肺癌患者存在明确的驱动基因。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">针对驱动基因信号通路的治疗可以抑制肿瘤细胞生长，该治疗方法改变了晚期肺癌的传统治疗模式，如EGFR突变肺癌患者使用EGFR靶向药物治疗效果显著，反应率达到60-80%，中位无进展生存期达到8-13个月，生存质量也远高于化疗患者。而对于早期肺癌患者，个体化的全身治疗方案可能会有助于减少毒副反应，提高患者生存。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">EGFR靶向药物的作用在早期肺癌外科治疗患者中已经评估，一项回顾性研究显示靶向治疗可以提高早期肺癌患者无疾病生存，而另一项前瞻性研究（RADIANT）则显示早期肺癌使用EGFR靶向药物治疗未能提高治愈率。另有多项研究认为早期肺癌患者辅助性靶向治疗并未能提高肿瘤治愈率，但可以延缓肿瘤复发。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前研究证据表明，放化疗后使用吉非替尼或塞妥昔单抗治疗局部进展期肺癌效果欠佳。目前多项相关前瞻性临床试验正在进行中，如EGFR，ALK靶向药物辅助性治疗早期肺癌的临床试验（ALCHEMIST），以及关于局部进展期肺癌放化疗前诱导靶向治疗的临床试验（RTOG1210/Alliance31001）。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. 肺癌微创治疗</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肺癌筛查项目使得早期肺癌患者大量出现，多数肿瘤直径小于15mm，其中25%的患者侵袭性非常低（肿瘤倍增时间&gt;400天），部分患者因筛查项目致过度诊断。大部分的临床Ia期肿瘤患者无淋巴结转移，近期有研究发现多个危险因素可预测临床Ia期出现淋巴结转移的可能性，不具备相关危险因素时，则不需行淋巴结清扫。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于无明显临床症状，筛查出的Ia期肺癌患者，传统的肺叶切除+淋巴结清扫术可能是一种过度治疗，可以考虑缩小手术切除范围，减少手术的创伤，还可以尝试使用非外科治疗方式。传统开胸方式行肺叶切除、肺段切除的方法逐渐被胸腔镜微创（VATS）方法所取代。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">机器人手术应用也逐渐增多，相较于VATS，由于其放大的三维视野，器械移动精度佳，除颤等多方面的优点，使其更便于外科医生掌握，也利于复杂手术操作。手术风险较高的患者（存在合并症，年龄&gt;75岁）考虑行亚肺叶切除，亚肺叶切除同样适用于&lt;2cm的周围型肺癌患者。&lt; p=""&gt;</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前有三项随机对照试验正在比较亚肺叶切除和肺叶切除治疗T1aN0M0肺癌患者的生存效果，待亚肺叶切除的价值确定后，下一步的研究将评估比较立体定向放射治疗与外科手术治疗早期肺癌患者的生存状况，另外还需要基于分子检测，影像学检测指标的准确风险预测模型以利于指导个体化治疗。</span>
</p>
