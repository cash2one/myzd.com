		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>垂体瘤的最佳治疗是什么?（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">最佳的治疗要根据肿瘤的类型。泌乳素瘤多可以成功通过药物治疗（口服药）。超过90%的患者，药物治疗（口服药）可以缩减肿瘤体积和降低血泌乳素水平。但药物治疗只能控制肿瘤，不能治愈肿瘤。约8-10%的患者，药物治疗效果欠佳，可能需要手术和放疗。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">对于其他类型垂体瘤的最佳治疗要由经常进行垂体手术的有经验的神经外科医生进行手术切除。尽管多数神经外科医生具有一些垂体手术经验，但由一部分专攻于垂体手术并且有着“最好的”成功手术记录的医生来做手术可以达到更好的治疗效果。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">目前最好的手术方法是在内镜下经鼻腔入路切除垂体瘤，创伤比显微手术还小，术后痛苦更小，鼻腔甚至不用填塞，术后鼻腔通气。</span>
