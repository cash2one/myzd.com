			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>原发性胆囊癌的预防</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">两千多年前，《黄帝内经》中提出“上医治未病，中医治欲病，下医治已病”，即医术最高明的医生并不是擅长治病的人，而是能够预防疾病的人。胆囊癌是最常见的胆道恶性肿瘤，一直以预后极差、生存率极低而备受外科学关注。近年来原发性胆囊癌在我国部分地区发病率有上升趋势。原发性胆囊癌发病隐匿，无特殊的症状和体征，早期诊断率仅为19．1％，一旦发现，多处于中晚期。其术后5年生存率不到5％。下面我就临床工作中经常会被患者问及的一些问题，和大家一起探讨一下原发性胆囊癌诊治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.哪些地方原发性胆囊癌发病率高呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">无论是全球范围，还是在一个国家内，胆囊癌发病的最明显特征就是其地域性的差别。就我国来讲，西北和东北地区发病比长江以南地区高，农村比城市发病率高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.老年女性更容易患原发性胆囊癌吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发性胆囊癌的流行病分布存在着明显的性别差异，女性明显高于男性。这种两性别间发病率差异的原因目前还不甚明确，有研究发现女性的多孕多产可明显增加患胆囊癌的风险，可能与妊娠期间女性体内的孕激素和内源性雌激素水平增高有关。我国大陆原发性胆囊癌的发病年龄分布在25~87 岁，平均57 岁，50 岁以上者占70%～80%，发病高峰年龄段为50～70 岁。尤以60岁左右居多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.原发性胆囊癌和职业有关吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多调查显示事炼油、造纸、化工、制鞋以及纺织等行业的人群中胆囊癌的发病率较高，但由于研究例数较少，又缺乏相关的研究来进一步验证，职业与胆囊癌的联系仍有待于进一步研究。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.我患有胆囊炎胆囊结石疾病多年，会导致胆囊癌吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前对于原发性胆囊癌发病的高危因素已基本达成共识：①胆囊结石病程大于5年；②50岁以上的女性胆囊结石患者；③B超提示胆囊壁有局限性增厚；④胆囊颈部嵌顿结石；⑤结石直径大于2．0 cm；⑥胆囊萎缩或囊壁明显增厚；⑦瓷器样胆囊；⑧合并有胆囊息肉样病变；⑨合并异常胆胰管连接；⑩既往曾行胆囊造瘘术者。临床上如遇到具有以上高危因素的患者时，应采取更加积极的诊治措施，以提高原发性胆囊癌的早期诊断率。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.很多人体检都查出患有胆囊息肉，为什么医生只建议其中一些人进行手术治疗呢？胆囊息肉与胆囊癌有关系吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">体检发现有胆囊息肉的患者，大多数无明显临床症状。鉴于少数胆囊息肉可能为早期胆囊癌或可能发生癌变。是否有癌变可能及是否需要手术治疗，视息肉形态而定。目前较公认的手术指征：直径超过25px的单个病变，年龄超过50岁，连续B超检查发现息肉有增大趋势，腺瘤样息肉或基底宽大，合并胆囊结石或胆囊壁增厚。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6.日常生活中，那些症状提示患有原发性胆囊癌的可能？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发性胆囊癌临床表现根据病变的部位和深度可有不同的症状。早期无特异性症状，如原有的慢性胆囊炎或胆囊结石引起的腹痛、恶心呕吐、腹部压痛等，部分病人因胆囊切除标本病理检查意外发现胆囊癌。当肿瘤侵犯至浆膜或胆囊床，则出现定位症状，最常见为右上腹痛，可放射至肩背部，食欲可下降，胆囊管受阻时可触及肿大的胆囊。能触及右上腹肿物时往往已到晚期，常伴有腹胀、体重减轻或消瘦、食欲差、贫血、肝大，甚至出现黄疽、腹水、全身衰竭。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7.B超发现有胆囊占位病变后，为什么医生还建议做了其他很多检查，这些检查有什么用吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在谈这个问题前，我们首先要明确一个“意外胆囊癌”的概念。所谓意外胆囊癌是指临床诊断为胆囊良性疾病而行胆囊切除术,在术中或术后经病理检查确诊为胆囊癌。意外胆囊癌在胆囊癌的诊治过程中很常见。这种情况使得临床医师在治疗过程中很被动，解决这个问题的关键在于怎样尽可能将意外胆囊癌变成术前诊断的胆囊癌，从而能在一次手术中就能进行符合病理分期要求的恰当手术，避免第二次手术给患者带来伤害。所以术前尽量明确胆囊癌高危人群的诊断是非常重要的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在各种影像学检查中，超声检查的诊断率较高，并以简便、无创和可重复性而成为仪器检查的首选。对胆囊癌的诊断符合率可达80%以上，但对早期胆囊癌的诊断率较低，对定性诊断和分期帮助不大，易受到肥胖和胃肠道气体干扰。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">超声内镜检查法检查对微小病变确诊和良恶性肿瘤鉴别诊断均有较高的价值。由于B超易受腹壁肥厚、肠管积气的影响，早期胆囊癌较难检出。而超声内镜采用高频探头仅隔胃或十二指肠壁对胆囊进行扫描，使胆囊癌的检出率进一步提高，尤其对早期胆囊癌，还可以评估胆囊癌浸润范围，对临床分期具有指导意义。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">CT对于胆囊癌的定性优于B超，对胆囊癌确诊率高于B超，且可以明确胆囊癌的浸润范围及是否有肝动脉及门静脉浸润。增强CT可以更加明确肿瘤与周围血管的关系。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来磁共振（MRI)及磁共振血管成像(MRA)也用于胆囊癌的诊断，MRI与MRA联合可以显示血管浸润、胆管浸润、肝脏浸润、淋巴结转移|。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着正电子发射断层扫描(PET)广泛开展，对CT、B超不能鉴别的胆囊息肉样病变，PET可较好的鉴别良恶性，更重要的是，PET对胆囊切除术或胆囊癌根治术后残余胆囊癌、胆囊癌的复发及远处转移具有较高的诊断价值。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">实验室检查方面，最近研究表明CA-242在胆囊癌特异性的肿瘤标记物中诊断价值较高，诊断灵敏度达84％，明显优于CEA、CA-19—9、CA-125。细针穿刺胆囊胆汁行肿瘤标志物检查更有诊断意义。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着分子生物学技术的发展，现在已可以直接检测和鉴定缺陷基因，使胆囊癌的诊断从传统形态学诊断上升到基因诊断。目前，研究得较多的与胆囊癌有关的癌基因有K-ras、survivin、cerbB-2、hcl-2、c-mvc、bax等，抑癌基因有p53、PTEN、p27、Rb基因等。检测以上基因变化可为胆囊癌早期诊断提供更加重要的辅助手段。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7.因慢性胆囊炎胆囊结石行腹腔镜胆囊切除术，但术后医生告知术后病理为胆囊癌，需要二次手术，这合理吗？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">LC（腹腔镜胆囊切除术）已成为常规的、代替传统外科手术治疗良性胆囊疾病的方法。由于LC的广泛开展和手术前诊断胆囊癌很困难，LC术中或术后发现意外胆囊癌的数量逐渐增加。然而，在缺乏已制定好的指南下处理意外胆囊癌是一个难题，意外胆囊癌是否、何时、如何履行再次手术，对于胆囊癌在不同病理分期阶段再次切除的有效性一直存在争议。手术切除是目前胆囊癌唯一有效的治疗方法，大多数学者认为胆囊癌的治疗方式根据胆囊癌的分期决定。在良性疾病行单纯的LC术后被发现的胆囊癌，为了改善预后，应该对肿瘤和胆囊切缘进行再次取材分析，判断肿瘤的分期，如果初次手术无法达到根治术的要求，则有必要进行二次根治术。有文献报道，推荐对T2期(癌肿浸犯囊壁全层，无淋巴结及远处转移)的胆囊癌或更晚期患者进行第二次根治性手术。</span>
</p>
