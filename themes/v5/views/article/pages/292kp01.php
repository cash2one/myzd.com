			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>有胆囊结石但没有症状的女性怀孕前需要切除胆囊吗？</strong></span>
	</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我个人的建议是：切除胆囊</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">理由是：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.尽管暂时没有症状，但在将来出现腹痛等症状的机会逐年增大，而且胆囊结石时间越长，胆囊发生病变的可能越大，胆囊结石一般也会逐渐增大，而即使始终没有症状，如果胆囊结石增大到2.5cm或病史长于10年以上，由于癌变的机会相对增大，也建议行胆囊切除。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.怀孕后人体内雌激素和孕激素大量增加，胆囊的收缩功能下降，胆汁易于淤积于胆囊内，加上国内传统上会在孕期加强营养，往往是大鱼大肉，这些都是急性胆囊炎发作的高危因素。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.一旦在孕期出现急性胆囊炎，处理将投鼠忌器，用药上由于害怕对胎儿产生不利影响，因此受到很大局限，如果药物不能控制，面临着需要胆囊穿刺引流甚至急诊手术，这对孕妇和家属来说往往是个两难选择，即使穿刺引流有效，一般都建议到切除胆囊时再拔除引流管，那样就面临着将在整个孕期甚至哺乳期都带着引流管，对于生活将造成很大困扰。如果面临手术，那手术创伤和麻醉对胎儿将产生多大影响是完全无法预测的。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.当然，如果预防性胆囊切除，手术本身也是有风险的，但目前腹腔镜胆囊切除术已经非常成熟，发生严重并发症的机会远远小于带着胆囊结石怀孕可能带来的困扰。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.最后，值得提醒大家的是，这只是作者个人的观点，目前国内外尚无相关的治疗指南推荐在怀孕前行预防性胆囊切除。本人计划做一个课题，相信几年后会有相关更准确可靠的信息和意见。</span>
</p>
