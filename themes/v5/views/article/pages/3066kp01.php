			<p>
	<div style="text-align:center;">
		<strong><span style="font-size:18px;">准妈妈能做胆囊切除术吗？</span></strong>
	</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">怀孕期间的准妈妈身体里的激素水平会出现明显改变，其中孕酮和雌激素是最主要的两种激素，可引起许多生理变化，比如胃食管反流病的发病率会增加，便秘也是准妈妈们很常见的症状。在肝胆系统方面，胆囊中发生变化的主要是胆汁的化学成分。在妊娠的中晚期，胆囊体积可以增大到怀孕前的两倍，胆囊排空的速度也减慢了很多。因此可以有高达4％的孕妇通过超声检查会发现存在胆囊结石，但大约每1000人中只有1例出现胆囊结石的相关症状。实际上在妊娠期有症状的胆石症发病率与同年龄段非孕妇的发病率是相同的。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;没有症状的胆结石，没有外科处理的必要。但患胆石症的准妈妈们由于出现了症状而行胆囊切除术的情况也是并不少见的，准妈妈们最常做的外科手术中排第一位的是阑尾切除术，第二位的就是胆囊切除术了。胆绞痛是常见的胆结石症状，通常会突然发病，出现右上腹痛或上腹的疼痛，轻重不一，重者疼痛难忍，呻吟不止，面色苍白伴大汗。这种疼痛多为阵发性的绞痛，也可为持续性痛，疼痛还可向右肩或右上背部放射，常伴恶心和呕吐。怀孕与非妊娠患者中，胆绞痛的症状没有什么区别。出现症状的准妈妈，依靠超声检查可以明确。使用超声对怀孕患者的胆结石以及胆囊炎症的判定与非妊娠期患者同样准确。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 患胆绞痛的准妈妈行胆囊切除术的时机取决于孕龄以及症状的严重程度。初次发作而且症状不重的，或经非手术治疗症状迅速缓解者，可以先不考虑手术。常用的非手术疗法主要包括卧床休息、低脂饮食、抗感染解痉止痛和输液支持治疗。如果已经合并了急性胆囊炎的情况，还是应该认真考虑手术的问题。在妊娠早期行开腹胆囊切除术后的流产率为12％，而在妊娠中晚期则分别降至5.6％和0％。妊娠中期行胆囊切除术后出现早发性宫缩的机率分别是0％而在妊娠和晚期则升至40％。所以行胆囊切除术的最佳时期为妊娠中期，此时发生自发性流产和早发性宫缩的机率均为最低，但患者已出现胆石症的其他并发症的情况时除外。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 如果准妈妈的胆囊结石引发了其他比较严重的问题，如梗阻性黄疸、胆源性胰腺炎以及胆管炎时，无论其孕期早晚均应手术。因为很多母亲与胎儿出问题往往是由疾病本身导致的，另外胆石症的非手术疗法所引起的并发症可导致更高的母亲与胎儿的死亡率，而及时的手术能够终止疾病的发展，这一观点已得到越来越广泛的认同。有报道孕妇若患有胆源性胰腺炎可导致15％的母亲死亡率以及60％的胎儿死亡率，因此现在将手术治疗作为妊娠期胆石症的首选治疗方式。另外在综合医院里，为孕妇进行手术前都有专业的产科医生的会诊，帮助外科大夫进行围手术期的产科治疗，所以准妈妈们大可不必惧怕手术了。</span><br />
