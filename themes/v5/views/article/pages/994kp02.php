			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>心脏搭桥患者的术后保养和用药指导</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>1、 保持积极主动的康复态度</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很难想象，影响患者术后恢复最主要的因素，并不在于医药，而更多地取决于患者的康复态度。比如说，很多患者认为，熬过了那么大的手术，一定要好好卧床休息，加上伤口疼痛，以致动也不敢动，甚至不敢咳嗽；食欲不好，也就不愿进食，或者干脆要求医生使用静脉营养。这种消极的观点只会贻误病情。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">研究表明，术后尽早的身体锻炼有助于患者早日康复，因此，我们要求病人一旦转入普通病房，就要及早下床，开始功能锻炼。同样的，即使食欲不振，也要尽量进食（当然不是暴饮暴食），甚至“把饭当药吃”，才能更快的恢复。如果患者没有积极主动的康复态度，只是等待医生开药，反而易于出现各种并发症。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>2、 住院期间的主动康复</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病人离开监护室，意味着围术期危险已明显降低，也意味着主动康复阶段的到来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在饮食方面，患者应尽量保证充足的营养摄入。如前所述，术后食欲下降是常有之事，有时甚至要多少强迫自己，才能增加一点点食量。或者采取少食多餐的办法，多吃几顿，也是行之有效的手段。一般来说，如果术后早期的进食量能达到术前的一半，已经可以达标了。糖尿病的患者，如果身体情况允许，可以早一点从半流食过渡到普通饮食，这样更便于调节血糖。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">进入普通病房后，病人就可以开始下床活动，并根据自身体力状况循序渐进地增加活动量。如果顺利的话，在出院前（术后5~7日），多数患者可以自行在走廊里短时间行走。活动量的调整以病人不感觉太累为准。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于术中留置气管插管，患者术后多多少少都会有些痰。应积极咳嗽、咳痰，切勿因害怕疼痛而不敢咳。咳嗽的时候，可以怀抱枕头等，限制胸廓运动，家属也要积极拍背，协助排痰。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>3、 建立良好的生活习惯</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从某种程度上讲，冠心病是一种社会病，也是与个人行为密切相关的疾病。出院以后，患者应该利用这个机会，重建良好的生活习惯。这方面的著作已经汗牛充栋，只能就几个方面简单说说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一是健康饮食。大原则就是适当减少碳水化合物（也就是主食）的摄入，尽量少吃高脂肪、胆固醇饮食，丰富进食品种。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二是适当锻炼。饮食与锻炼结合，才能收到好的效果。出院以后，还是要逐渐增加活动量。根据自己的感觉，选择适合自己的运动方式。增加活动不要急于求成，一定要循序渐进，比如从室内到室外，从短距离到长距离，从慢步到快走等等。如果在锻炼过程中，出现胸痛、气短、哮喘等，或者特别疲劳，应立即停止。另一个方面，患者也不要有心理障碍，认为做过手术，人就废了，其实，大多数病人仍可以达到正常或接近正常的运动量和生活质量。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">第三是规律生活，充分休息，戒除不良嗜好（特别是吸烟）。其实，不独冠心病，各种疾病的康复都离不开这一点。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>4、 术后药物治疗</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 冠心病的全称是“冠状动脉粥样硬化性心脏病”，顾名思义，其病因是动脉粥样硬化。冠状动脉搭桥手术虽能恢复冠状动脉血运，但并不能去除动脉粥样硬化这个病根，从这个意义上说，冠心病是不能根治的。因此，患者在术后仍需坚持服药，防止动脉硬化的继续发展。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;这些药物中，一部分是血管扩张剂，包括硝酸酯类药物（比如硝酸甘油、单硝酸异山梨醇酯等），钙离子拮抗剂（如硫氮卓酮等），出院后早期还要规律口服，外出时常需携带，以备心绞痛发作时控制症状，如果术后半年以上无复发，可以减停。其次是β受体阻滞剂，如美托洛尔等，长期应用，具有保护心脏功能的作用；抗血小板药物，目前常用肠溶阿司匹林、玻利维，以及降脂药物，如各种他汀类药，都有稳定粥样斑块，降低心血管事件发生率的作用，应长期、规律服用，如身体条件允许，甚至终生服用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;还有些药物，根据患者各自的病情和既往病史，也需要服用。例如，心功能较差的病人，可以使用洋地黄类强心药物（如地高辛等）和利尿药（比如呋塞米、罗内酯等）。既往有高血压病史的病人应服用降压药物，糖尿病患者要注意控制血糖。总之，除直接作用于心脏的药物外，术后用药大多针对冠心病的各种危险因素，希望籍此达到稳定患者病情，延缓病变发展，延长手术有效期限的目的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;另外，患者出院后，一定要注意复查，与医生建立长久的复诊关系，在医师的指导下，调整药物剂量，增减药物种类。切勿自作主张，随意停药。</span><br />
