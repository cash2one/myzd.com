		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>引起鼻窦炎的病因及症状（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">众所周知，鼻窦炎是一种在人群中发病率极高的疾病，影响患者生活质量。那么，引发鼻窦炎的原因有哪些呢?鼻窦炎发病的时候都有什么症状表现呢?鼻窦炎对人体的危害是非常大的，因此，做好鼻窦炎的预防工作至关重要。引起鼻窦炎的病因有很多，鼻窦炎中致病菌是导致鼻窦炎的一种病因。生活与工作环境不洁是诱发本病的常见的原因。过度疲劳、受寒受湿、维生素缺乏等引起机体抵抗力降低、特应性体质、全身性疾病(贫血及糖尿病)、内分泌疾病(甲状腺、脑垂体或性腺功能不足)、上呼吸道感染及急性传染病。鼻腔的一些疾病，如鼻中隔偏曲、中鼻甲肥大、鼻息肉、变态反应性鼻炎、鼻腔异物或鼻腔肿瘤，也可引起鼻窦炎。创伤性、其他如鼻腔填塞物留置过久、气压骤变引起非阻塞性航空性鼻窦炎等都是鼻窦炎的病因。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻窦炎的主要症状有： ①脓涕：鼻涕多为脓性或黏脓性，黄色或黄绿色，量多少不定，可倒流向咽部，单侧有臭味者，多见于牙源性上颌窦炎或真菌感染。②鼻塞：轻重不等，多因鼻黏膜充血肿胀和分泌物增多所致。③嗅觉减退：鼻塞和炎症反应可导致嗅觉障碍。④头痛：慢性鼻窦炎一般无明显局部疼痛或头痛。如有头痛，常表现为钝痛或头部沉重感，白天重，夜间轻。前组鼻窦炎多表现前额部和鼻根部胀痛或闷痛，后组鼻窦炎的头痛在头顶部、后枕部。患牙源性上颌窦炎时，常伴有同侧上列牙痛。</span>
</p>
