			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>生后脑出血造成婴儿痉挛的手术治疗&nbsp;</strong></span>
	</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我院又成功完成一例复杂的难治性婴儿痉挛症手术治疗。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我院癫痫中心在方铁主任和方方主任的带领下，近期完成了一例复杂的难治性婴儿痉挛症患儿的手术治疗，术后患儿癫痫发作完全消失，身体恢复良好。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患儿来自河北省石家庄市，目前2周岁。患儿生后2个月出现颅内血肿，当地医院给予患儿实施了颅内血肿清除术，术中发现存在脑血管畸形，但是因为血管畸形复杂，未能给予处理。患儿6个月时出现癫痫发作，主要表现为点头痉挛样发作，每天几十次甚至上百次，并逐渐加重；患儿辗转河北和北京多家医院治疗均无效果，尝试过多种抗癫痫药物和激素治疗也没有疗效。患儿家属几乎放弃了治疗希望！幸亏北京儿童医院建立医疗集团后神经内科方方主任定期到石家庄儿童医院国际部出特需门诊。当方方主任在石家庄儿童医院了解到患儿的病情后立即建议患儿家属带患儿到北京儿童医院癫痫中心进行手术评估。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经过北京儿童医院癫痫中心评估确定患儿的婴儿痉挛发作主要是因为患儿脑出血造成的，但是癫痫网络范围广泛，涉及到多个脑叶，因此建议首先进行颅内电极植入确定确切的癫痫网络范围。因为患儿年纪小，以前有过颅内出血病史造成颅内脑组织粘连严重，颅内电极植入手术难度很大。在癫痫中心方铁主任领导的全体癫痫外科手术团队的努力下，经过6个多小时的艰苦手术，终于顺利完成患儿的颅内电极植入手术。术后明确患儿癫痫网络主要位于患儿脑出血区域，颞叶和枕叶区域。经过癫痫中心的再次讨论，确定患儿的最终手术切除方案为病灶和多脑叶切除术，同时切除患儿的脑血管畸形。5天后患儿再次接受手术，经过11个小时的艰苦努力，方铁主任带领手术团队为患儿切除了脑血管畸形和脑软化灶，此外还进行了颞叶和颞叶内侧结构切除和后头部多脑叶的离断术，手术一直持续到凌晨才结束。</span>
</p>
