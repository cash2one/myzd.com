			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>附属仁济医院李可为团队为罕见“镜面人”成功取石</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 日前，上海交通大学医学院附属仁济医院胆胰外科李可为主任医师仅用时40分钟，便顺利完成1例高难度内脏全反位（俗称“镜面人”）的胆总管结石ERCP取石术，目前世界上此类报道仅9例。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">该例患者为55岁女性，胆囊已切除十余年，一个半月前右上腹痛、同时发热伴轻度黄疸，上腹部磁共振显示胆总管内结石，经保守治疗度过急性炎症期后，于2016年2月19日全麻下ERCP取石。由于是内脏位置与正常人完全相反，治疗团队查阅了相关文献，作了充分的技术探讨和多套临床预案后，决定采取仰卧位下ERCP取石术。术中克服了背向监视器、反向进镜、曲镜位操作、“镜面式”插管和切开、反位放射图像、附件出入困难、取石方向相反和结石偏大等诸多不利因素，手术按准备预案顺利完成。术后患者恢复良好，3天后出院。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近年来，仁济医院胆胰外科在科主任王坚的带领下，在肝外胆道结石的微创治疗上取得了长足的进步。单纯的胆总管结石首选ERCP取石，年平均实施400余例，平均手术时间27分钟，插管成功率接近97%，严重并发症率仅1%。近年还实现了ERCP日间化，开通了急诊ERCP绿色通道，开展了全覆膜金属支架技术纠正胆总管良性狭窄。此外，团队针对胆囊结石和息肉开展了单孔腹腔镜胆囊切除术，美容效果显著。针对胆囊结石合并胆总管结石的患者，采用腹腔镜胆囊切除胆总管探查一期缝合术既保留了Oddi’s括约肌的功能，又把治疗周期从2个月缩短至1周；针对急性胆道感染采取穿刺外引流和ERCP内引流后再综合运用腹腔镜、胆道镜和十二指肠镜技术来分期实现全程微创治疗，基本摒弃了既往创伤巨大的开腹手术。目前，团队肝外胆道结石的总体微创手术成功率在92%以上，即使是上腹部手术后的患者，也基本可以实现治疗的微创化。</span><br />
