		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>鼻腔生理学功能（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻腔主要有呼吸、嗅觉功能，另外还有过滤清洁、吸收排泄、加温加湿、共鸣、反射等。此外，鼻腔黏膜上皮能分泌溶菌酶、防御素等一系列生物活性物质，具有一定的生物学作用。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">鼻腔生理学功能：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1、呼吸功能。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(1)呼吸道的门户和通道。呼吸气流对于机体与外界的接触其重要作用。外界气体吸入鼻腔，通过鼻腔一系列生理机制的调节后，符合机体需要交换入肺部器官。否则易引起呼吸道疾病。外界气流换到达鼻腔，经过处理后换进入肺部，进行气体交换 。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(2)鼻阻力对于维持胸腔负压、便于气流在肺泡内充分停留，保证正常呼吸功能的实现有重要作用。鼻气道阻力简称鼻阻力（nasal airway resistance，NAR）,约占整个呼吸道阻力的一半。鼻阻力产生的关键部位在软骨性鼻前庭与骨性鼻腔的交界处，即鼻中隔方形软骨前下端、鼻外侧软骨前端和鼻腔最前部的梨状孔底部构成的鼻内孔区域与两侧下鼻甲区域，以通过鼻腔的气体流速和压力比值来表示。鼻阻力值随生理状态的改变和病理状态的发生而变化，病因年龄、人种而异。正常人两侧下鼻甲黏膜内的血管呈交替性收缩与舒张，表现为双侧鼻气道开闭的周期性变化，称为鼻周期（nasal cycle）。鼻周期受左右交感神经交替性兴奋和抑制的影响，正常情况下不宜察觉。鼻周期个体差异较大，健康人约为2小时~6小时，鼻周期对单侧瞬时鼻阻力有影响，但不影响鼻气道总阻力。鼻周期的存在有利于缓解疲劳，睡觉时能够及时反复调整睡眠姿势。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(3)温度和湿度的调节。由于鼻黏膜面积大约160平方厘米，黏膜内毛细血管和腺体丰富，起到温度和湿度的调节。而且与吸入鼻腔气体的温度、湿度、数量、速度和分泌物的性质（浆液性、粘液性和化脓性）有关。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">(4)过滤清洁作用。鼻毛具有阻挡外界气流中的尘粒及细菌，气流净化处理。而且鼻黏膜纤毛系统是维持鼻腔正常生理功能的重要组织，发挥重要作用。黏膜表面体液流变学的改变对其有变化，受干燥、渗透压、温度、酸碱度、药物等多种因素影响。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2、嗅觉功能。嗅觉是一种复杂的心理、生理反应，也是鼻腔的一种保护性功能。影响因素很多，诸如性别与年龄、局部机械性压迫、局部与全身某些感染以及长期吸入有害气体等。严重程度可分为嗅觉减退与失嗅。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3、反射功能。鼻腔内神经分布丰富，当黏膜受到机械性、化学性、物理性刺激时产生，可引起呼吸和心血管方面影响。主要包括：鼻肺反射（nasopulmonary reflex）、喷嚏反射（sneeze reflex），鼻心反射、鼻睫发射以及嗅觉反射，体表温度的升高与降低，会导致鼻黏膜的充血与肿胀。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4、吸收功能。鼻黏膜内丰富的血液微循环是其重要的解剖学基础。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5、排泪功能。鼻泪管是常规状态下泪液引流排泄的途径，开口于下鼻道。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">6、免疫功能。分泌特异性免疫物质（免疫球蛋白等）与非特异性免疫物质（溶菌酶、乳铁蛋白、蛋白分解酶等），发挥免疫机制。</span>
</p>
