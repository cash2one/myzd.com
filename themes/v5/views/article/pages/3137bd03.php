<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海交大附属六院骨科专家赴川救援表现出色</strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 小长假不休息，对上海交大附属第六人民医院骨科的孙玉强医生来说是寻常事。不过这个五一劳动节还是有点特别，因为他身在四川成都，身为上海交大医学院骨科专家组专家，救治芦山地震伤员。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 昨天一清早，他和另两位六院医生从住处出发，搭公交车赶到四川省人民医院。与前几天略显紧张忙乱相比，医院显得安静了许多。病房里，正在康复或择期手术的伤员投来信任的目光，问候“上海医生好”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “‘紧急状态’已经过去了，危重伤员都已得到了妥善处置。”不过与记者连线时，谈起10天前从上海领命出发，孙玉强还是难掩激动。地震当天，他正好在医院值班，当时就想着如果要派医疗队，以骨科创伤救治闻名全国的市六医院一定有份，自己当仁不让。可等了一天没有消息，隔了一天再到医院看门诊时，却接到了紧急通知——当晚飞赴成都参与救援！孙玉强想，“这个任务终究还是我的”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 23日凌晨到达成都，8时，孙玉强和同伴们就精神抖擞地到了人民医院。初步了解情况后，上海专家们迫不及待地请战，要求马上上手术台。救护车不断呼啸而来，伤员源源不断，各种骨折、复合伤都有，大多是前方简单处理后的重伤员。看着他们强忍痛楚的焦灼神情，“我们不能等”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 回到驻地已是深夜。此后几天，上海专家组保持着每天四五台复杂手术的高效率。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有个孩子被山石压砸伤，足踝部毁损，地震当天在前方医院做了截肢。转到省人民医院后，孙玉强发现创口愈合不好，有一半部位发生了感染，更麻烦的是保留的残肢太长，不利于将来佩戴假肢和功能康复。与全国各地专家交流后，上海医生调整了治疗方案，清创后重新做了残肢修整手术，还设计了一个残端切口，方便日后戴假肢。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有个小伙子双前臂骨折，原定4月30日结婚，医院得知后打算立即手术，让他能赶上婚礼，当地电视台也准备报道。孙玉强仔细检查，发现他前臂肿胀程度较大，不宜马上开刀。大家有点失望，上海医生却坚持，治疗要讲科学，要为小伙子一辈子着想。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “现在有的医疗队已经回程，我们上海医疗队仍旧在川随时待命，随时接受调遣，参与疑难病例会诊救治。”孙玉强说，救死扶伤是医生的寻常工作，但能为灾区救援尽更多的力，“作为上海医生，我们自豪”。</span>
</p>
