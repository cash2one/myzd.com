			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>耳聋的治疗</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">突发性耳聋&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">突发性耳聋又称特发性暴聋，是指听力突然减退，1～3日即可达到耳聋最高峰乃至全聋。大多数为单耳发病，多由全身或局部因素所引起的一种感觉神经性耳聋，少部分病人伴有头晕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[临床表现]</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1．耳聋：听力突然明显减退(多为单侧耳)，大部分病人伴有耳鸣，部分病人有耳内发闷、胀满及阻塞感。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．发病原因：多数病人无明显发病原因，不少是发生在睡眠当中，于起床时自感耳鸣、耳聋。一些病人有较明显的劳累、情绪过于激动、精神紧张及感冒病史，这些可能与发病有一定关系。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3．眩晕：有一少部分病人有耳聋同时伴有眩晕，自觉有旋转感，常伴恶心、呕吐，多于发病一周左右眩晕减轻，耳鸣、耳聋无好转，伴有眩晕者听力损失多较重，也不容易恢复。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[诊断]</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1．听力突然明显下降，以单侧耳聋多见，部分患者可伴眩晕，电测听检查为感音性耳聋。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．鉴别：单纯的突发性耳聋病较易诊断，由于症状突然发作，听力损失明显，多为单耳发病，故不容易与其它耳聋相混；但如果伴有眩晕的病人则应与很多病相鉴别，此时应及时到医院请专科医生检查，因为鉴别眩晕症状，需要借助于各类仪器，所以不是病人自己能够办到的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">[治疗]</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">突发性耳聋治疗越早效果越好，发病一周内开始治疗者80％以上可以恢复或部分恢复听力，发病两周后再治疗效果就差一些。有资料表明，发病超过一个月，听力已经基本定型，治疗的效果就会大打折扣。有的患者因为没有及时治疗而变成了永久性耳聋。所以应将突发性耳聋作为急症对待。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物治疗：低分子右旋糖酐，扩张血管（钙通道阻滞剂，组胺衍生物，活血化淤中药），抗血栓形成及纤维溶栓剂（东菱克栓酶，尿激酶），维生素，改善内耳代谢药物（都可喜等），糖皮质激素等；中医中药如复方丹参等. 对耳鸣、眩晕的病人应给予镇静剂.</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">理疗及针灸：高压氧、耳周穴位注射等</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">外科治疗：鼓室内药物注射，带蒂颞肌瓣营养内耳等</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物性耳聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗应早期进行，以神经营养药物为主，如维生素A、复合维生素B、ATP、辅酶A、高压氧等，中药苍术、生地、枸杞等对内耳亦有一定的保护作用。早期轻度中毒者，听力多可恢复，对于中毒时间较久的耳聋，一般治疗方法无明显效果，可选配适宜的助听器或行电子耳蜗植人后进行听觉语言康复。预防方面可行耳毒性药物敏感个体基因检测，筛查耳毒性药物敏感人群；避免耳毒性药物的使用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">噪声性耳聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">噪声性耳聋目前西医尚无有效治疗方法。应用扩血管药后养神经药及促进细胞代谢药物可能有一定帮助，但一般效果不明显。听力损伤严重者可选配助听器。最重要的是一旦确诊，尽快调离噪声工作环境，或改善工作环境的噪音污染，佩带防噪音耳塞及噪音检测仪。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">老年性聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可以佩戴助听器，保守治疗以营养神经改善微循环为主。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全身系统性疾病，传染病源性聋引起的耳聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">针对病因治疗，保持残余听力，可行扩张血管，改善内耳代谢药物等治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">创伤性聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">尽量恢复和重建声音传导系统，可行手术治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">自身免疫性聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">环磷酰胺、强的松龙等免疫抑制剂疗效较好，但停药后可复发。再次用药仍有效。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人工耳蜗植入术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">感音神经性耳聋的治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、药物治疗：因为感音神经性聋的致病原因较多，机制与病理改变不尽相同，故讫今尚无一个简单有效、适用于任何情况的药物与治疗方法。目前多在排除或治疗病因性疾病的同时，宜早选用血管扩张剂，降低血液粘稠度的药物、维生素B族、能量制剂，以及必要时在一定期间内应用类固醇激素等进行治疗。如罂粟碱、肝素、654-2、氢麦角碱、地巴唑、川芎嗪、葛根黄酮等血管扩张剂口服或注射。维生素B1、B6、B12，三磷酸腺苷、辅酶A、细胞色素C、低分子右旋糖酐，碳酸氢钠，高压氧等治疗，药物治疗无效者可配助听器。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、助听器：是帮助聋人听取声音的扩音装置。它的主要由微型传音器、放大器和耳机、耳模、电源等组成。助听器种类很多，单供个体用者就有气导和骨导、盒式和耳级式（包括眼镜式、耳背式、耳内式），单耳与双耳等助听器，需经耳科医生或听力学家详细检查后才能正确选用。一般讲，语频平均听力损失35—85dB者可使用，若两耳损失程度大体相同者可用双耳助听器或将单耳助听器轮换戴在双耳。若双耳听力损失程度差别较大，但都不超过50dB者，宜给听力较差耳配用；若一耳听力损失超过50dB，则应给听力较好耳配戴助听器。此外，还应考虑听力损害的特点，例如助听器应该用于语言识别率较高，听力曲线平坦，骨气导间隙较大或动态听力范围较宽的耳。传音性聋应用气、骨导助听器均可，外耳道狭窄或有炎症等患者只能用骨导型助听器。感音神经性聋者多用气导型。有重振者需配用具备自动增益控制或自动重振控制装置的助听器。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、耳蜗埋植：又称电子耳蜗或人工耳蜗。适用于中青年双侧极度耳聋，使用高功率助听器无效，耳内无活动性病变。X线断层拍片或CT检查证明内耳结构正常，耳蜗电图无反应，鼓岬或圆窗电刺激可诱出脑干反应者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性耳聋的治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">先天性耳聋可分为遗传性和非遗传性两大类。又可分为传导性、感音神经性和混合性三类。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、先天中耳畸形应行手术矫治，手术时机最好在15岁以上进行；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、先天感音神经性聋病变为不可逆性，无有效药物或手术矫治方法；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">①先天聋儿应做到早期发现、早期诊断；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">②聋儿无论年龄大小，一旦被发现确诊，有残余听力者应尽早戴助听器，进行听力言语康复训练。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">③双侧极度耳聋者若有条件者可行人工耳蜗植入</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">传导性耳聋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">又称传音性耳聋，由耳廓到中耳甚至内耳的病变均可引起，常见疾病有：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、（常见传导性聋）：由耳廓到中耳病变引起</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">①耳郭畸形或缺失</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">②外耳道堵塞</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">③鼓膜穿孔</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">④鼓室病变：鼓室外积液、积脓，听骨链粘连、硬化，听骨链断裂等</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常见传导性耳聋大多数是可以手术治疗的，如取出外耳道异物、听骨链重建、鼓膜修补、耳郭再植等，目的就是恢复声音的正常传导途径，均可使听力得至部分或完全恢复。随着各种助听器的出现，不宜手术者可选择佩带助听器。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、（非常见传导性聋）：由内耳病变引起</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">梅尼埃病、上半规管裂隙综合症</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">耳硬化症</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">耳硬化症是一种常染色体显性遗传疾病，好发于20~40岁,女性多见，孕妇可加剧进展</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">病因：与维生素、遗传因素、代谢障碍导致骨迷路营养障碍有关，其典型的病理组织学改变为骨迷路原发灶性海绵状变性，进而骨质吸收或硬化。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床表现：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、双耳进行性听力下降 2、低调耳鸣 3、出现Willis误听 4、眩晕 5、家族史6、听力学特点：早期为传导性聋，晚期可出现混合性或感音神经性聋；Gelle试验(﹣)提示足板硬化固定；可在2KHz处出现Cahart切迹。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">分型</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">w &nbsp; 按病灶累及部位</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;1、镫骨性耳硬化：病变位于前庭窗，病灶好发部位为前庭窗前区和圆窗边缘</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;2、耳蜗性耳硬化：病变位于迷路或内听导，引起听觉感受器或听神经病变</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">w &nbsp; 按病灶累及范围 ：Portmann 4型</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1型：耳硬化早期，仅有镫骨前方韧带硬化</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2型：前庭窗前区</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3型：前庭窗其他区</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4型：底板全部发生硬化病变或呈阻塞型</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">w &nbsp; 按是否出现临床症状</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、临床耳硬化：镫骨固定而出现临床症状</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、组织学耳硬化：终身无自觉症状，仅见于尸解病理学检查，白人8~10%，而临床耳硬化仅占其中12%</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">w &nbsp; 按影像学（CT）表现</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、窗型：累及卵圆窗和圆窗， CT表现为卵圆窗消失，硬化骨质代替</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、窗后型：累及骨迷路包括耳蜗、前庭和半规管， CT表现为围绕耳蜗的低密度晕圈</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(1)药物治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 氟化钠抑制海绵化病灶内水解酶及蛋白酶活性、减少破骨细胞吸收、促进成骨细胞形成，减慢或终止耳硬化进行性听力下降。研究表明氟化钠45mg/d或更少不会增加骨质形成，只涉及酶的活性，认为适量氟化钠（15~45mg/ d）能阻止耳蜗变性而不致加重镫骨固定。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 1、成人重度耳蜗型或经证实为Schwartze征阳性者氟化钠45mg/d,5d/w,持续6~8m</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;2、已手术且伴随进行性耳蜗症状者氟化钠20mg/d，2y</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;(2)手术治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、单纯开窗术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、镫骨撼动术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、镫骨抬提高术</span><br />
