			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>我的子宫肌瘤需要手术吗？</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; ☆ 1、子宫肌瘤可能会造成患者出现哪些症状？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、月经淋漓不尽，2、痛经，3、经期延长，4、月经量多，甚至过多，5、贫血，6、大小便障碍，7、性功能障碍等典型症状。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8、B超发现子宫肌瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; ☆ 2、体检发现子宫肌瘤但没有症状，是不是也要手术？决定是否手术的主要依据是肌瘤大小、位置还是是否出现症状？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术指针：1、症状明显的，比如，出血太多，难以忍受；贫血；2、痛经难受，3、注意：肌瘤大于5厘米，笔者在其他问章中已经反复强调！4、肌瘤出现液化、坏死等，5、大小便障碍，6、性功能障碍等。7、突然长大，增长速度快，就要考虑手术，因为有恶变可能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; ☆ 3、手术能根治子宫肌瘤吗？不手术会恶变吗？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、手术可以根治肌瘤，但是术后会复发，因人而已，复发指新发，原来手术取出的肌瘤已经不在了。2、肌瘤恶变率低，如果没有上述典型症状，可以不手术，绝经以后会自行消失。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; ☆ 4、子宫肌瘤可以药物治疗吗？效果如何？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">药物治疗没有一点作用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
