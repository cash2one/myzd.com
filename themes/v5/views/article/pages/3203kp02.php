<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; ☆ 5、暂时观察的子宫肌瘤患者多久复查一次？增长速度多快的时候就需要注意了？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、最好每3个月、6个月、一年复查一次。2、突然长大，增长速度快，就要考虑手术，因为有恶变可能。</span><br />
