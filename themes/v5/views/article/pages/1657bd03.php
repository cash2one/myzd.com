<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>双肾长动脉瘤？专家“拆弹”再植肾</strong></span><br />
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">信息时报讯 （记者 李楠楠 通讯员 李绍斌 彭福祥） “等于把肾脏摘出来逐一进行‘拆弹’，拆完再安装回人体内”，记者昨日从中山大学附属第一医院获悉，该院通过自体肾移植等完成一例复杂双侧肾动脉瘤手术，成为国内首例同期完成双侧肾动脉瘤及自体肾移植手术案例。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 双肾动脉瘤随时可夺命</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 据中山一院器官移植科主任王长希介绍，患者为一名38岁男性，广东顺德人。日前因右下腹疼痛到当地医院检查，结果显示患者右肾动脉真性动脉瘤，双肾动脉假性动脉瘤。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 其中，左侧肾假性动脉瘤直径超过10厘米，随时可能破裂导致大出血死亡。在辗转了多家医院后，患者被送到了广州。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 摘肾后再重新植入体内</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 接诊的中山一院专家面临一个棘手的问题，患者要么双肾切除进行终身透析，要么冒险保留右肾。王长希介绍，在与家属充分商议后，医院联合多科室进行手术，右肾被取出半个多小时，对动脉瘤进行“拆弹”后，重新植入到距离原本右肾20厘米处的髂内动脉位置，并进行血管重建。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 手术共进行了10小时，术中出血很少，患者无需输血。随着自体肾的植入，在手术台上患者就有尿液排出，说明肾功能已恢复正常。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 专家：已完成十多例自体肾移植</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 中山一院血管甲状腺外科主任医师常光其介绍，肾动脉瘤是一种罕见的肾血管疾病，国内外报告发病率为0.01%~1%，出现双侧巨大肾动脉瘤的更罕见，“肾动脉瘤一旦破裂，死亡率高达70%以上。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “对于严重性的肾动脉瘤，自体移植是比较理想的手术方法”，王长希介绍，中山一院通过自体移植的已进行了十多例。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 王长希最后表示，除了肾动脉瘤外，小儿肾动脉疾病、血管外腹主动脉瘤、腹膜后肿瘤等复杂性疾病在治疗过程中，也可用自体肾移植的手段，保证手术正常完成。</span>
</p>
