<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">9月22日，河南省卫计委组织的“中原名医哈密行”活动正式启动，河南大学第一附属医院消化内科主任韩大正、泌尿外科主任卜宏民作为全省选派的医学专家成员赴新疆参加学术交流活动。 东方今报记者 曹东方 通讯员 王雷</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&lt;p xmlns=""&gt;9月22日，河南省卫计委组织的“中原名医哈密行”活动正式启动，河南大学第一附属医院消化内科主任韩大正、泌尿外科主任卜宏民作为全省选派的医学专家成员赴新疆参加学术交流活动。此次活动由省卫计委主任李广胜亲自带队，抽调省人民医院、河南大学第一附属医院等六家省直医院的共十名顶级专家，开赴新疆哈密市、兵团十三师、巴里坤县、伊吾县进行学术交流、开展巡回义诊、专家讲座、示范手术等活动，对当地进行重点支援和智力支持。韩大正主任和卜宏民主任都是本地区医学领域的知名专家，他们一致表示将把河南大学的优良传统和浓厚感情带到新疆，用最好的技术服务新疆人民，为河南大学争光。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据悉，作为三家受邀的河南省高水平大学附属医院之一，河大一附院一直以来都积极支持和参与援助新疆的工作，目前已派出呼吸内科、消化内科、泌尿外科等多名专家支援新疆医学发展，多次获得上级部门的好评。</span>
