			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>子宫颈癌科普知识</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">宫颈癌又称子宫颈癌，常发生在子宫颈鳞状上皮细胞及宫颈管内膜的柱状上皮细胞交界处的恶性肿瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 宫颈癌在早期可无任何症状，或仅在阴道检查时，可见表浅的糜烂，或有一般慢性炎症如少量白带等；中期常有白带增多、腥臭，阴道不规则出血，骶尾、臀部及大腿部的持续性疼痛，膀胱、直肠浸润时可出现排尿、排便障碍等表现。&nbsp;</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：宫颈癌发生的位置在哪里？宫颈癌的发生占妇科肿瘤中的第一位吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：如图：阴道与体外相通，宫颈上是子宫，宫颈癌发生在宫颈的位置，进一步发展会向其它的地方扩散。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 它在最常见的妇科肿瘤中仅次于乳腺癌，排第二位，全世界每年约有50万人新发宫颈癌，80%发生在发展中国家，我国占13万，占约1/3。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：宫颈癌前病变有什么症状吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：1、性生活后出血。70％-80％的宫颈癌症患者都有这一症状；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 2、宫颈糜烂。年轻女性宫颈糜烂经久不治，或是更年期后仍有宫颈糜烂，应该引起重视；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 3、接触性出血，性生活后出血，或是妇科内诊检查后子宫出血，都是宫颈癌前病变的征兆；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 4、白带混血，除上环引起子宫出血外，女性长期白带混血应及时检查。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：筛查和确诊宫颈癌有哪些方法？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：根据病史和临床表现，尤其有接触性出血者，应想到有宫颈癌的可能，应做详细的全身检查及妇科检查，并采用以下辅助检查：①子宫颈细胞学检查；②碘试验；③宫颈和宫颈管活体组织检查；④阴道镜检查；⑤宫颈锥形切除术。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：什么年龄的妇女容易诱发宫颈癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：20-70岁之间的妇女都可能会得宫颈癌，高发年龄是40-50岁，20岁以前得宫颈癌的妇女比较少，但是近年来年轻妇女诱发宫颈癌有上升的趋势，有报道称发现有16、7岁的处女罹患宫颈癌的病例。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：目前宫颈癌的治愈率多高？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：宫颈癌治愈后一般恢复会比较好，主要是看宫颈癌处于什么期，在原位癌发现后愈后效果相当好，五年的生存率可达100%，中期五年生存率约70-80%，晚期五年生存率大约只有30-50%。所以宫颈癌只要早期发现，早期诊断，愈后是相当好的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：宫颈癌有哪些治疗措施？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：早期患者可行手术治疗（广泛性子宫切除＋盆腔淋巴结清扫术），中晚期或有手术禁忌者可行放射治疗，一些患者则需进行手术及放疗联合治疗。化学治疗主要用于晚期或复发转移的患者，或作为手术或放疗的辅助治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：宫颈癌一定要切除子宫吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：不一定。对要求保留生育功能的早期宫颈癌患者可行宫颈锥形切除术或广泛性子宫颈切除术及盆腔淋巴结清扫术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：宫颈癌复发有哪些症状？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：宫颈癌复发癌的主要症状为一侧下肢疼痛，腹和盆部酸痛，阴道出血和恶臭白带。另外随复发部位而有不同表现，如咳嗽、胸痛、血尿、直肠出血等。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：预防HPV感染＝预防宫颈癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：HPV（人乳头瘤病毒，Human PaoillomaVirus）是引起宫颈癌的必要因素。没有HPV感染，几乎不会发生宫颈癌。目前，人类对付HPV仍然缺乏确切的办法。正在研制中的HPV疫苗是最有希望的预防和治疗手段，但目前只有预防性疫苗，治疗性疫苗还未能进入临床试验阶段。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：HPV感染就必然导致宫颈病变吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：HPV感染不一定导致宫颈发生病变，就相当于乙肝病毒携带者一样。HPV感染很常见，感染后并无任何症状，只有持续的HPV感染才会发生癌前病变（CIN）或宫颈癌。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前为止，对于HPV病毒尚没有明确的特效药。但HPV病毒就像感冒病毒一样常见。如果身体免疫力好的话，绝大多数可以自动清除。所以HPV感染者无须恐慌，但如果持续感染，就需要定期的随访监测。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Q：HPV病毒是通过什么途径传播的？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">A：HPV病毒可以通过唾液、性接触及皮肤接触传播，即使避孕套也不能有效地防止HPV的传播，因为该病毒可以在肛门和生殖器周围任何区域存活，包括避孕套无法覆盖的区域，而且可以存活数年。</span><br />
