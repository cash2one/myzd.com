			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>马潞林主任谈：是不是所有的肾病都会发展成尿毒症呢?</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">马潞林， 男，1961年生，主任医师、教授、博士生导师，现任北京大学第三医院泌尿外科主任。现任中华医学会泌尿外科分会常委，肾脏移植学组组长兼腔道学组委员， 中国医师协会泌尿外科分会委员,中华器官移植委员会北京分会委员，中华泌尿外科委员会北京分会常委，《中华泌尿外科杂志》、《中华外科杂志(电子版)》常 务编委，《中国微创杂志》、《中国组织工程研究与临床康复》和《世界泌尿肿瘤杂志》编委，《中华外科杂志》特约审稿人，北京市及海淀区医疗事故技术鉴定专 家。主要擅长于泌尿外科疑难手术、肾移植和腹腔镜手术。包括腹腔镜下前列腺癌根治，膀胱癌根治，活体供肾切取，肾部分切除术，肾癌合并腔静脉癌栓，后腹膜 淋巴结清扫术等。熟悉取肾、修肾、肾移植和术后免疫抑制剂的合理应用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147010648973757" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 各种肾脏疾病引起慢性肾功能衰竭导致尿毒症发生，经一般治疗无效，都会考虑做肾移植来挽救生命。肾移植手术的成功率如何?肾衰患者该如何选择治疗呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<strong>一、是不是所有的肾病都会发展成尿毒症呢?</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：有一位网友提出，慢性的肾功能不全和慢性的肾衰竭是一个概念吗?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：是一回事。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：那么，进一步讲慢性的肾衰竭跟尿毒症是什么关系呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：尿毒症是慢性肾衰发展到最后的情况。肌酐在130umol/L至800umol/L之间，就叫慢性肾功能衰竭，若是达到了800umol/L以上就叫尿毒症。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：肌酐从正常到异常，从800umol/L以下到800umol/L以上，是有一个数月到数年的过程的，这个过程分几个时期?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：慢性肾炎最初是尿蛋白，病人眼睛肿、腿肿，这是慢性肾炎早期的表现。再损害严重会导致肌酐升高。 之后再严重病人会恶心、呕吐、心衰，这个时候应该做治疗了。人的体质不一样，有时候肌酐五、六百的病人也需要开始透析或者做移植了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：中末期肾病指的是什么呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：慢性肾炎到最后，肌酐在800umol/L以上，就算是中末期肾病，或者也叫尿毒症。有两种疾病最容易导致尿毒症，一个是慢性肾炎，另外一个是多囊肾。以前慢性肾炎比较多见，现在随着医学治疗水平提高，慢性肾炎导致的尿毒症越来越少了，多囊肾多起来了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：对。肾移植是中末期肾病最有效的措施。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：是不是说所有的肾病都会发展到尿毒症的地步呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：肾病概念比较广泛，有许多类型。实际上肾脏病大部分都不会有尿毒症。但是肾脏病的肾炎大部分都会发展到尿毒症。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：是不是所有到了中末期肾病的病人都能够做肾移植呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：应该说90%以上的病人都可以做肾移植。反过来说，哪些情况不能做移植呢?第一存在活动性感染，比如活动性肝炎、肺结核。第 &nbsp;二，严重的心脑血管疾病。第三，癌症。癌症的病人以前的观点是手术后5年以后可以做肾移植，现在的观点是两年以后如果没有局部复发就可以做肾移植。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：从得上肾炎到发展为尿毒症，这期间的时间大概有多长?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：平均是10年。有些病人发病快的几个月就到了尿毒症阶段，有些也可能20年、30年才到尿毒症阶段。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<strong>二、肾炎的患者怎样减缓肾功能的衰竭呢?</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：对于患了肾炎的患者来讲，他们能够通过哪些措施减缓肾功能的衰竭呢?换句话说，从患上肾炎到最后发生尿毒症的这段时间，他们如何做才可以想尽办法延长这段?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：首先对肾炎要有一个正确的认识：目前国际上肾炎进入慢性肾功能衰竭阶段就基本上没有特别好的办法了，因此不要有病乱投医，尤其不要杂七杂八的什么药都吃。还 是应该去一些正规的大医院，根据情况采取合理的治疗方案。第二，遇到感冒和感染，这里所说的感染包括腹泻和发烧等等，各种感染都会导致机体的抗体升高，这 个抗体就会破坏肾脏的基底膜，最后导致肾功能的进一步损害。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：在平常生活、起居、运动上都有哪些需要注意的呢?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：生 活、起居上应该注意适当饱暖，尤其预防感冒和腹泻，这是我们常见的感染。一般来讲感冒一次感染的可能会升高一些。腹泻和感冒都容易加重肾功能的损坏。另外 在运动量上注意不要做过大量的运动，一般地来讲，肾功能不好的病人心脏功能比正常人都会差一些，承受不了过大量的运动，同时过大量运动也有可能突然发生心 衰。不应该做大量的运用，要适当的休息。可以早晨散步、晚上散步都可以。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：肾病患者的饮食，什么样的东西可以吃，什么样的东西不能吃? 马潞林：实 际上我们正常人能吃的东西他都可以吃，这个是不存在区别的。但是一定要注意，肾病患者一定在量上要控制。正常人比如说一顿饭吃一只鸡问题不大，可是，对于 尿毒症的病人来讲负担就过重了。不要吃的过饱，要七成饱。对我们正常人来讲吃七成饱营养已经够了，对尿毒症病人来讲吃多了还有一个过剩的营养产生的废物排 不出去的问题，就会进到血液里面去，尿毒症的发展就会更快，从慢性肾炎到尿毒症所经历的时间就更短了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：很多患者认为肾脏不太好的要少吃植物类的蛋白，相比较而言要多吃动物类的蛋白。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：对。这是因为植物类的蛋白里含的蛋白质的成分不如动物更全面，植物蛋白里面还缺少一些必须的氨基酸,而动物的蛋白质的成分更接近人体。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：如果吃过多的植物蛋白会对肾脏加重负担吗?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：会加重负担。蛋白质代谢产物是肌酐，那毒素就会升高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 主持人：对于这个网友来讲首先要注意饮食。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 马潞林：对。适当的吃一些蔬菜，补充一些维生素。再看尿量，肌酐400umol/L多一般尿量问题不大，如果浮肿要适当的控制饮水量，根据尿量来喝水。应该遵医生的指导，因为夏天出汗多，一般水也要多补充。 但有条件的话尽可能遵医生的指导。</span><br />
