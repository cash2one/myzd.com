		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>睡眠呼吸暂停低通气综合征(科普)</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">睡眠呼吸暂停低通气综合征（Obstructive sleep apnea/hypopnea syndrome, OSAHS）是睡眠过程中反复上气道暂时性狭窄塌陷、阻塞，导致呼吸暂停或通气不足，伴有睡眠时打鼾，引起频发夜间血氧下降、高碳酸血症、睡眠片段化及睡眠结构紊乱、白天嗜睡、生活质量下降等病症。其最主要的病理生理变化是在上气道狭窄的基础上，神经调节功能下降，造成睡眠过程中反复发生上气道塌陷，进而形成低通气血氧饱和度下降，终致全身多脏器功能损害。OSAHS发病的病理生理学机制主要包括：解剖结构异常、炎性反应和神经调节功能障碍三大因素。解剖结构异常是造成上气道狭窄的基础，炎症因素可加重上气道狭窄的程度，久而久之神经调节咽部开大肌的功能下降，进一步加重阻塞。三个因素之间长期相互作用和转化，形成了上气道通气功能障碍的发病基础。在长期OSAHS临床研究中发现，咽腔阻塞是引起临床症状的主要部位。然而随访中发现，部分患者解除咽腔阻塞后，并不能完全缓解睡眠中上气道通气功能障碍。通过鼻腔解剖学、影像学、病理生理学等研究发现：鼻腔狭窄时呼吸气流增快、鼻通气阻力加大、软腭位移、咽腔塌陷、阻塞、引发张口呼吸。由此推测鼻腔狭窄可能是上气道阻塞的源头性启动因素。</span>
</p>
