			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>计算机精确导航下的关节置换</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在计算机导航技术问世之前，骨科医生为病人进行手术主要依靠解剖学知识、术前所得到的病人的影像资料（X光片、CT、MRI）、术中的X射线透视图像和临床经验。然而，一旦出现一些很小的失误就可能导致术后严重并发症或肢体功能下降等问题，增加病人痛苦，造成医疗资源的浪费。在医疗质量不断提高的今天，许多手术对术中操作精确性要求更高，先进的计算机导航辅助手术技术是解决“精确操作”这一问题的良方，它可以使骨科手术更安全、更有效。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 应用计算机导航手术，可进行相应定位手段，对手术部位及术中的手术器械进行实时跟踪、显示、定位、引导，其工作原理犹如在航空、航海中为飞机和舰船进行导航一样，在手术中可以对每一步操作进行导航。利用导航辅助进行骨科手术，是以计算机图像处理工作站及影像跟踪设备为核心，将医学影像设备提供的图像进行信息化处理，并结合立体定位系统对人体肌肉、骨骼解剖结构进行显示和定位，借助计算机进行手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 在髋关节置换方面，使用计算机导航技术可使臼杯放置的角度达到十分理想化的程度；在全膝关节置换术上，可不需进入髓腔，截骨更加准确，特别是在术中可随时模拟截骨状态测量和调节两侧软组织的平衡。计算机辅助人工关节置换手术系统的出现，突破了传统术式的局限性，在下肢力线的正确重建、假体的准确置入、屈伸间隙和韧带平衡等方面获得了传统手术难以达到的效果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 总之，计算机辅助导航技术在提高病灶定位精度，减少手术损伤，执行复杂外科操作，提高手术成功率方面都有优越性。</span><br />
