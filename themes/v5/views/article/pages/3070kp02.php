		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>呼吸道异物堵塞易致死 5岁前小儿别吃危险食物</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147004113408424" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 小孩的反射机能差，吃东西的时候一不小心卡到咽喉、气管、支气管的情况时有发生。由于呼吸道是生命通道，一旦卡住容易造成堵塞，导致呼吸不畅甚至死亡，对孩子而言极其危险。专家指出，呼吸道异物堵塞在5岁特别是2岁以下的小儿中发生率非常高，为小孩的生命安全着想，最好在此年龄段前不吃容易导致呼吸道堵塞的食物，比如花生、瓜子、豆子、果冻、口香糖、骨头等。尤其是果冻和口香糖具有粘性，往往一堵就堵得很死，孩子还没来得及送医院就没命了，要绝对禁止食用。此外，对于一些有零碎部件的小玩具，也不宜给孩子买来玩，以免他们拿到嘴巴里吃，造成异物堵塞。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 果冻、口香糖卡住咽喉最容易致死</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 小儿呼吸道异物包括了咽、喉、气管、支气管异物，其中，以喉异物堵塞的危险性最大。由于喉部中央的声门是整个呼吸道最狭窄的地方，一旦有东西卡在这个位置，孩子短时间内就会发生呼吸困难甚至窒息。不过，临床中有不少食物卡喉的案例，有的孩子被抢救回来，有的却很快没了命，这是怎么回事呢？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 广州市妇女儿童医疗中心耳鼻喉科主任刘大波教授说，卡住的异物种类不同，其后果也可能不同。像果冻和口香糖，比较容易吸附或粘在咽喉壁上，上不去，下不来，把声门口堵得死死的，这种情况发生窒息的几率最大，孩子可能在几分钟内丧命，根本来不及送医院抢救。但是如花生、瓜子、骨头、鱼刺一类的食物，即使卡住总是留有一点缝，就给抢救争取了时间。不过，卡住的要是骨头、鱼刺，也必须及时处理，否则咽喉壁被刮伤，还会出现脓肿、出血等并发症。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 剧烈咳嗽是呼吸道异物的典型症状</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 2岁半的小玲有天吃面条，突然满脸涨红，不停干呕，还好赶紧喝了几口汤，才慢慢恢复了正常呼吸。可是到了晚上，小玲突然咳嗽起来，声音嘶哑，呼吸也有点困难。难道是之前吃的面条卡在喉咙里？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 刘大波说，这种表现一般不太像呼吸道异物，因为呼吸道异物有一个典型症状，即吃东西剧烈咳嗽、脸红发绀，大概持续1-2分钟之久。有的人可能因此导致呼吸困难和窒息，也有的人可能过会能缓过来，那是因为异物从呼吸道掉入了肺里，没了堵塞感，呼吸就恢复了平顺。不过，如果异物长时间没被取出，接下来还会导致发烧、咳嗽等并发症。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “无论如何，出现异常马上就医还是很明智的做法。”刘大波说，到医院做一个内镜检查基本就能判断是炎症还是异物。但有些家长未必留意到孩子呛咳，如果孩子长期间歇性、阵发性咳嗽，或肺炎反复治疗不好，有肺不张和肺气肿，也要考虑曾经有过呼吸道异物堵塞。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 预防之道</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 这些食物最危险</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 呼吸道异物堵塞有性命之危，为防止意外发生造成终生遗憾，花生、瓜子、果冻、口香糖、骨头等危险食物孩子5岁前应禁止食用，在喂食和吃东西的过程中，也要注意不要让孩子大声哭笑、打闹，在平和安静的状态下就餐。一旦出现呼吸道异物堵塞，靠家长急救如倒提、轻拍多数都不会有效，最好赶紧到有资质的医院就诊，并尽快做手术。(刘大波 宋导 马军)</span>
</p>
