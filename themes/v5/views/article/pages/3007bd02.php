			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>修典荣：肝癌误区详解</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">谈到癌症，我们首先还是要简单给大家讲解一下一些常见名词。关于癌症的名词分类是不断在变化的，并且在医学术语和普通大众的叫法上也有些区别。一般而言，肿瘤分成良性和恶性两类（也有少部分肿瘤在开始诊断时分不清是良性还是恶性，可以叫做恶性潜能未定，有少部分些肿瘤介于良恶性之间的称为交界性肿瘤）。恶性肿瘤统称是癌症，其中包括各种恶性实体肿瘤和恶性血液病（白血病），恶性的实体性的肿瘤中又根据组织起源不同分成癌和肉瘤，白血病又有很多分类。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3007/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">修典荣：肝癌误区详解</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常有病人问医生自己得的癌是良性的还是恶性的，这个问法是因为对于肿瘤得定义不适很清楚：肿瘤有良性和恶性之分，恶性肿瘤，也就是癌症的主要特点就是具有不断生长和复发转移的特点，这也是为什么不是所有癌症都能彻底治愈，也就是普通大众说得彻底去根儿。相对而言，多数得良性肿瘤是可以通过手术彻底治愈的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝癌的来源就是肝脏？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">就肝脏而言，我们常说的肝癌一般是指肝细胞肝癌。其实，生长在肝脏的癌症可以分成很多种，胆管细胞来源的胆管细胞癌也并不少见，。还有一些血管来源的其他肝脏恶性肿瘤也属于肝癌范畴。除这些肝脏原发的肿瘤以外，肝脏还是很多其它部位的肿瘤常见的转移部位，这些称为肝脏转移性肿瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">同所有癌症一样，对于肝癌的具体病因并不是非常清楚，但是，在我国和很多东南亚国家，多数得肝癌病人是在有肝脏疾病基础上发生的，最主要是和乙型肝炎相关，我们国家肝癌病人中绝大多数都是乙型肝炎患者或者是乙型肝炎病毒携带者或者有过乙型肝炎感染病史，在西方国家则丙型肝炎更常见。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝癌都是缓慢发展的？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">各种肝硬化都容易发生肝癌，包括酒精性肝硬化、胆汁性肝硬化、自身免疫性肝硬化或者原因不明肝硬化。这些肝脏疾病发展到肝癌的典型过程应该是肝炎然后肝硬化然后肝癌，而临床上有很多年轻病人根本没有像这样慢慢发展而来，常常没有任何不适，突然发现为肝癌晚期。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝癌无法治愈？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">跟许多癌症一样，肝癌如果能够及时早期发现，手术治疗后效果很好，而一旦晚期，则各种方法疗效均大打折扣。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝癌无法早期发现？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果病人能够具备一些肝癌相关的常识，规律检查早期发现，很多都有治愈机会。定期检查内容最简单的就是超声和血夜甲胎蛋白测定，虽然这两种方法均不是最准确的，但是简单、无创，多数医院均可以完成，有异常时再进行其他复杂影像学检查即可。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">年轻人不会得肝癌？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床上经常碰到的就是病人很年轻，不认为自己会这么快得肝癌，所以，不太愿意规律查这些项目，还有一些因素也影响病人查体的积极性，那就是怕让别人知道自己有肝炎。同很多癌症一样，肝癌在早期也是没有任何症状的，尤其是年轻人精神体力均比较好，更不容易早期发现。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;肝癌检查每年一次即可？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肝脏超声和抽血查甲胎蛋白非常方便，费用也不高，应该每三个月进行一次。检查结果正常只是表示目前可能没有肝癌发生，由于肝癌发展一般比较快，因此，间隔时间不能太长。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">多年以前我们一个病人不到40岁，工作上成绩斐然，前途无限，虽然自己知道有肝炎病毒，但是工作较忙的原因仅有每年的定期常规健康查体，并且没有针对肝炎进行的特殊检查，直到偶一天突然右上腹疼痛就医才发现为巨大肝癌，已经没有了切除机会，虽然积极进行了肝移植手术，还是术后一年发现肺部复发转移而英年夭折。我们国家另一个广为人知的例子是某知名演员也是同样一个非常相似的过程。其实，如果他们能够将查体更有针对性一些，检查的周期能更短一些，很有可能在肝癌还是很小的时候发现，则无论肝脏肿瘤的切除或者射频消融等均有很好的效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有慢性肝炎的病人一定要进行积极的抗病毒治疗，在肝炎内科医生的直到下积极治疗也可以有效的预防和延迟肝炎病人发展到肝癌的过程。由于酒精主要是在肝脏解毒处理，因此饮酒对于肝炎病人的危害要明显大于对正常人，在肝炎基础上喝酒无疑于雪上加霜，会明显加快加重肝炎肝硬化肝癌的发展，所以有肝炎的病人必须彻底戒酒。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">随着我们国家乙型肝炎疫苗接种的普及，在整个国人中乙型肝炎感染者明显下降，相应肝癌发病率也会逐渐下降。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">没有肝炎就不会得肝癌？</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而随着生活方式的改变，目前结肠直肠癌的发生逐年增加，而这些病人的最大特定就是容易发生肝脏的转移，有统计认为近半数的这些病人会在不同时期发生肝脏转移。结直肠癌发生肝脏转移与其他肿瘤发生肝脏转移的最大不同则是虽然是四期（晚期）肿瘤但是积极手术切除以后仍然有高达半数的病人可以长期存活。转移性肝癌与一般肝癌，也就是肝细胞性肝癌的主要区别在于这些病人多数没有肝炎基础，虽然常发生肝脏多发转移，但是积极切除，只要能切除干净再辅以辅助化疗和一些其他药物治疗，治癒率仍然较高，5年存活率超过50%。由于对于肝脏转移性肿瘤到底是不是属于可切除性的在不同的医院和医生的判断标准不同，因此，在很多指南上都提到了一定要多学科讨论决定，其中一定要有一个经验丰富的肝脏外科医生的参与。除了最常见的结直肠癌发生的肝脏转移在经过积极的以手术为主的综合治疗可以获得令人满意的效果以外，神经内分泌肿瘤（苹果公司前CEO乔布斯即是因为胰腺的神经内分泌肿瘤发生肝转移以后做了肝移植然后存活近8年）同样也可以经过积极的手术治疗获得非常令人满意的长期效果。由于这些病人的病情涉及到多个学科，因此，一定要多个学科的大夫一起讨论后再给病人制定出一个最有效的治疗方案，这不光是对病人同样也是对很多各个专科的医生的要求。正是由于当今医学发展越来越专，才需要各个专科一起讨论才能对于一个病人的病情做出最有利于病情的评估。在临床工作中，仍然经常会碰到类似一些病人在各级医院当作晚期肿瘤而放弃了根治性治疗机会而仅仅给予化疗等姑息治疗方案。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 总之，肝脏作为人体一个重要器官可以发生各种各样的肿瘤，其发病机制并不非常清楚，积极科学的随访，早期发现，在专科医生的积极治疗下才会获得最佳的治疗效果。</span>
