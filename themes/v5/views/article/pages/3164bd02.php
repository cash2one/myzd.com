			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>国际权威消化病学专业杂志刊登附属仁济医院最新研究成果</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 血管畸形导致的消化道出血素来是临床诊治的难点，而一度因严重致畸副作用退出临床一线的老药沙利度胺却在该病的治疗中焕发新的活力。7月25日，国际消化病学专业最具权威性的杂志《胃肠病学》（Gastroenterology，IF：12.032）在线发表了附属仁济医院消化科戈之铮教授的最新研究成果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 血管畸形导致的消化道出血常常因为诊断困难，病情迁延反复而成为消化专业医师面临的挑战。在上世纪九十年代初，这种疾病尤其是小肠血管畸形所致消化道出血缺乏有效的检查手段。直到双气囊小肠镜和胶囊内镜的问世使得这种疾病诊断率不断提高。但在治疗方面，学界曾先后尝试不同的方法试图改善治疗效率，如糖皮质激素因能抑制全身免疫反应，但因长期应用而产生诸多严重副作用，而且疗效并不确实；内镜下治疗因病变部位不定而鞭长莫及；而手术切除又因病变在各个肠段多发，如全部切除常常导致短肠综合症等严重并发症，导致生活质量下降。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 沙利度胺又名反应停，上世纪五十年代作为治疗癫痫药物问世。后被发现具有镇静止吐作用而广泛应用于治疗妊娠妇女的早孕反应，但在此后因被发现可导致胎儿畸形，而被撤出市场。而后数十年，世界各地的学者一直没有放弃对沙利度胺的研究，其先后被用于治疗麻风结节性红斑、多发性骨髓瘤等疾病，而且效果良好。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 当年一篇简单的病例报道描述了沙利度胺在治疗血管畸形所致消化道出血上具有一定疗效。戈之铮教授研究团队敏锐地感觉到该疗法可能将给这种疾病的治疗带来新的突破。于是，一项历时八年的开放、随机对照、前瞻性研究至此展开。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戈之铮教授认为，沙利度胺治疗血管畸形所致的消化道出血，其作用机制可能是通过抑制血管生成，抑制机体免疫应答和局部炎症反应而实现。研究发现，沙利度胺是一种血管生成抑制剂，其致畸作用也与抑制胚胎期血管生成有关。沙利度胺抑制血管生成的机制之一是通过抑制血管内皮生长因子（VEGF）的表达而得以实现。同时，沙利度胺也是一种免疫抑制剂，能通过活化核转录因子NF-κB，抑制肿瘤坏死因子TNF-α的生成和表达，抑制局部炎症反应，改善血管壁通透性，减少渗出，修复和重塑血管，以达到止血的目的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 从一开始的大量基础研究、体外实验和动物试验，沙利度胺就显示出良好的效果。研究小组自2003年起开始通过严格的筛选标准，先后纳入55例反复出血的胃肠道血管畸形患者（定义为每年消化道出血次数大于六次），其中28例患者被随机进入沙利度胺治疗组，27例患者进入口服铁剂治疗的对照组。治疗持续4个月，所有患者至少随访1年，平均达到39个月，再以大便潜血试验来判定患者的出血情况。研究最终发现，沙利度胺治疗组和对照组的止血有效率（定义为一年出血次数下降50%）分别为71.4%和3.7%，而完全止血率（一年无一次出血情况发生）为46.4%和0%。此外，在患者总住院率，输血率，因出血所致的住院率以及年出血次数，出血持续时间，血红蛋白量，红细胞数量，因出血住院次数和住院天数等各参数改善程度方面，治疗组均优于对照组。而在长期随访中并未发现沙利度胺导致的严重不良反应。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戈之铮教授表示，此项研究为血管畸形导致的消化道出血临床治疗带来了新的思路和方法，将来进一步的工作将试图纳入更多的研究样本量，在全国开展多中心、安慰剂对照研究，更全面系统地评价该项药物的临床治疗价值。</span>
</p>
