<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>武警总医院和天坛医院承办 研究型医院脑血管病学专委会成立</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">科技日报讯 （李冬梅）12月15日，由武警总医院和北京天坛医院承办的，中国研究型医院学会脑血管病学专业委员会成立大会在京召开。吴中学任大会主席，毛更生任执行主席。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由解放军总医院、北京协和医院等医院发起的中国研究型医院学会是国家一级学会，武警总医院原院长王发强当选首届会长。脑血管病专业委员会隶属于中国研究型医院学会，是国内第一家以脑血管病复合开放手术、介入治疗为核心特色内容，并涵盖脑血管病重症干预、药物治疗、影响评估、神经康复及基础研究等专业的多学科学术交流平台。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在专委会成立之际，同时举办了第四届脑血管病长城论坛北京峰会，这是前三届脑血管病武警长城论坛的延续。与往届论坛以病例分享为主不同的是，今年论坛形式以专题演讲为主，内容涵盖了脑血管病的手术、介入治疗和相关学科最新进展，同时还开设了脑血管病急症沙龙，集中探讨卒中院前急救、绿色通道、院内救治、专科护理等相关问题，分享最新理念及技术成果。</span>
