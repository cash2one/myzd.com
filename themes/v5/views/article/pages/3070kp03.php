		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>为何胖宝宝睡觉容易鼾声如雷</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一名“重量级”儿童，年仅11岁就有80公斤，体重相当于一名壮汉。在过去的6年时间里，这个孩子只要一睡觉就会鼾声大作。去医院就医时，医生说他已经患有严重的睡眠呼吸暂停综合症，睡觉打鼾的病因就是过度肥胖。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">今天是睡眠日，据流行病学调查，中国每五个儿童中就有一个患有睡眠障碍，其中睡眠呼吸暂停综合症是比较严重的表现。儿童患上睡眠呼吸暂停综合症的人数并不少，甚至一些出生不久的小BB也会打鼾。专家介绍，肥胖以及喉软骨发育不良是儿童睡眠呼吸障碍疾病的原因，另外，扁桃体或腺样体肥大也会引起小孩子睡眠呼吸障碍。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肥胖儿6年间睡觉如“打雷”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">今年初，广州市妇女儿童医疗中心耳鼻喉科—睡眠中心接诊了一位肥胖的儿童。这个孩子年纪只有11岁，体重却高达80公斤。因为肥胖，他不仅日常生活不便，而且也很难睡一个好觉。家人说，在过去的6年时间里，这个小孩只要一睡觉就会打鼾，经常鼾声如雷，严重的时候还会出现口唇发青。在医院，医生趁这个小胖墩睡眠时，给他测了血氧，结果低于50%(正常人血氧应该在95%以上)，综合其他表现，医生诊断这个孩子患有严重的睡眠呼吸障碍(即阻塞性睡眠呼吸暂停低通气综合症)。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">我们了解到，中华医学会儿科协会曾经组织对全国20多个省市进行儿童睡眠质量调查，结果显示，儿童睡眠障碍的发生率超过25%。在儿童睡眠障碍中，够得上疾病的占10%，睡眠暂停呼吸综合症是儿童睡眠疾病常见也是比较严重的表现。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肥胖、发育不良均可引起睡觉打鼾</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">广州市妇儿医疗中心耳鼻喉科—睡眠中心主任刘大波介绍，睡觉打呼噜是一种疾病，即睡眠暂停呼吸综合症。这种病可以导致患儿多系统发生损害，引起儿童生长发育不良，智力下降，影响患儿的听力、社交能力等等。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">那么，儿童睡觉打鼾是什么原因引起的呢？刘大波说有以下几种原因：</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">一是先天性喉软骨发育异常，宝宝刚出生就打鼾。刘大波说，在耳鼻喉科门诊，医生常常可以见到一些焦虑的父母亲抱着刚刚出生1个多月的孩子来看病，孩子一出生开始，喝水就呛，睡觉时打鼾、鼻塞甚至张口呼吸，喉咙里经常可以听到响声，有时睡觉的时候常常憋醒，难以入睡。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">照电子喉镜检查，往往发现这些孩子有较严重的“先天性喉喘鸣”，原因通常为先天性喉软骨发育异常，影响患儿睡眠和喂养，最终影响患儿的生长发育。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">二是肥胖，肥胖以可以引起严重的睡眠障碍，如上面提到的80公斤小孩就是典型例子。肥胖是睡眠障碍的危险因素之一，肥胖儿童比正常儿童患上睡眠呼吸暂停的机会高几倍。在严重肥胖儿童中约有30%～50%会出现异常睡眠，常见表现就是打鼾。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">三是扁桃体或腺样体肥大，是儿童打鼾最多见的原因。扁桃体或腺样体肥大导致呼吸通道狭窄，引起打鼾甚至呼吸暂停。</span>
</p>
