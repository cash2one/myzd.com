<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>中年人肩袖损伤的治疗&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中年人肩袖损伤，可以分为功能性肩袖损伤和无功能性肩袖损伤。功能性肩袖损伤指的是尽管有肩袖撕裂，但是肩膀还能抬举起来，只是抬肩时疼痛、无力；无功能性肩袖损伤指的是肩膀抬举不超过90度。对于无功能性肩袖损伤，肯定要通过手术来争取恢复肩关节的功能。对于功能性肩袖损伤，在治疗上存在一定争议。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">功能性肩袖损伤如果疼痛、无力明显，实际上日常生活和运动能力还是明显打折扣的，建议积极治疗。如果疼痛、无力症状轻微，可以暂时不手术；但是要定期做核磁共振检查，了解肩袖损伤的发展，一旦发现损伤有明显加剧，则建议尽早手术干预。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总体来讲，对于中年人的肩袖损伤，建议积极治疗，也就是手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">我们的肩袖分三组：前肩袖--肩胛下肌、上肩袖--冈上肌、后肩袖-冈下肌和小圆肌。每组肩袖都有一定的宽度和厚度。肩袖损伤可能为整组肩袖全部断裂，也可能为部分纤维断裂。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">肩袖断裂后，不会自行愈合。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果是整组肩袖完全断裂，肩袖裂口会迅速增大，时间越长修补成功的可能性越小，修补后愈合的可能性也越小。肩袖完全断裂超过三个月，肩袖的肌肉部分就会发生变性，以后即便勉强修补起来，肩袖也因为肌肉不会收缩而没有功能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果是一组肩袖中部分纤维断裂，肩袖裂口不会迅速扩大。但是残留连接的部分纤维就要承担原来所有纤维的负荷，更容易断裂，最终发展成完全断裂。尽早把断裂的部分纤维修复，有助于恢复肩袖的完整性和整体功能。</span>
</p>
