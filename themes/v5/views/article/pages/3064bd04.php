<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>清华长庚医院成为我国华北泌尿结石病防治基地</strong></span><span style="font-family:'Microsoft YaHei';font-size:18px;line-height:25.454544067382813px;"><strong>举办清华大学首届泌尿系结石论坛</strong></span><span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong></strong></span>
</div>
<br />
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">清华新闻网6月12日电（通讯员 徐中海 刘宇保）日前，由清华大学主办，北京清华长庚医院承办的第一届清华大学泌尿系结石论坛在医院成功举办。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">大会主席、北京清华长庚医院泌尿外科主任李建兴主持论坛，来自全国泌尿医学领域的专家和学者500余人参加论坛。</span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经中华医学会泌尿外科学分会审议，在北京清华长庚医院设立“中国华北泌尿结石病防治基地”。开幕式上，中华医学会泌尿外科分会前主任委员、结石学组组长叶章群教授与清华大学副秘书长、北京清华长庚医院党委书记王志华共同为基地揭牌。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">论坛演讲环节，与会专家围绕尿路结石的基础及预防研究进展、体外冲击波碎石相关问题、经皮肾镜和输尿管硬软镜结石治疗进展、复杂结石总额和治疗相关问题、与尿路结石治疗相关的器械进展等进行了交流和学术讲解。</span>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">学术性演讲之后，来自北京清华长庚医院、清华大学一附院、广州医科大学附属第一医院、上海长海医院、中山大学孙逸仙纪念医院泌尿外科医师，先后进行了10台泌尿系结石碎石取石术，分别演示了软性输尿管、孙氏镜、经皮肾镜等多种方式泌尿系碎石取石技巧与操作经验。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为带动青年医师培养，展现我国青年泌尿外科医师在泌尿系结石研究、治疗、预防等领域的最新工作进展，清华大学泌尿系结石论坛特设“青年论坛”环节，经过层层评审，浙江省人民医院的俞蔚文医师的《B超定位下球囊扩张建立通道经皮肾镜碎石取石术（PCNL）》获得手术操作视频第一名；上海交大医学院附属新华医院的黄云腾医师，则以《连续性肾脏替代治疗（CRRT）在泌尿系结石并发感染性休克患者的临床应用》获得了论文第一名。</span>
</p>
