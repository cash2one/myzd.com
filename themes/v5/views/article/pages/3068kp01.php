<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然健康饮食的重要性已被大众熟知，但饮食控制主要存在于成年人群中，仍有大量少儿饮食过量或营养配比不均衡。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 有调查显示，少儿的肥胖率在世界范围内成倍增长，与正常体重儿童青少年相比，超重、肥胖儿童青少年罹患心血管疾病的危险性显著增加，其肥胖程度和冠心病独立相关。青少年往往以向心性肥胖为主，腰围是评价青少年腹部脂肪含量的实用指标，是青少年心血管疾病的重要危险因素。有研究证实，腰围值大的少儿腹部内脏脂肪含量是正常体重青少年的2—3倍，其患高血压和血管形态学的改变、早期心肌和冠状动脉损害的危险显著大于腰围小的孩子。</span>
