	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>湖南：湘雅专家运用3D技术成功切除肿瘤</strong></span><br />
</div>
<br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">随着3D技术逐渐渗透进入人们的日常生活，让人感觉新鲜之余，更多地是体会到这项技术的实用和高效。然而，将3D技术应用于医学领域，在手术室内通过3D眼睛和立体画面开展腹腔镜手术，又会呈现出怎样的效果呢？</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 近日，中南大学湘雅医院泌尿外科主任、湘雅名医齐琳教授领衔泌尿肿瘤亚专科团队采用3D腹腔镜技术，为一名75岁的膀胱癌患者实施腹腔镜膀胱全切术。在3D高清腹腔镜的帮助下，屏幕上腹腔内的器官似乎变得触手可及，令术者们仿佛置身于3D影院一般。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 术中，高清立体影像取代了以往的平面视图，超声刀所到之处，组织层层分离，血管立体清晰可见；腹腔内缝合的时，就好像缝线都拉到了眼前……如此逼真、清晰的场景，令专家们仿佛置身于3D影院一般。</span><br />
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “手术只花了不到两个小时便顺利完成，大大缩短了手术时间，且患者出血量仅为100毫升。”齐琳教授说，3D高清腹腔镜技术还原了真实视觉中的三维立体手术视野，为微创手术提供了立体视觉效果，使腹腔镜下的术野如同开放手术时一样直观、清楚，令手术操作更为精细和准确，可以最大限度地减少血管、神经损伤，较常规腹腔镜进一步减少了出血和并发症。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “医生带上3D眼镜后，通过监视器清楚地辨认组织器官周围的结构，高清立体视野可以极大地帮助术者解剖、缝合，且定位更加精准，使得技术发挥得淋漓尽致。”齐琳教授说。</span>
