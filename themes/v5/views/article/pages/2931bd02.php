		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>为患儿争取更美好的明天</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">复旦大学附属儿科医院泌尿外科主任医师毕允力，从医廿余载，或许是一直与患儿打交道，他一如大男孩般可亲。事实上，在许多患儿心中，毕医生不仅是医生，更是心中最亲近的大哥哥。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">从初入行的忐忑紧张，到如今小儿泌尿外科领域权威专家，毕允力坦言，自己也曾彷徨，面对许多长期带病生存的泌尿道、生殖道先天性畸形患儿，他为无法彻底改变患儿命运而失落。但随着对疾病认知的不断深入、医术的不断提升，而今，毕医生更相信：医生应勇于突破，不敢冒险、固步自封，难以为患儿争取更美好的明天。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">承受手术压力送给男孩未来</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对毕允力来说，一名17岁少年，是他从医生涯中不可错过的病人。男孩患有先天性脊髓脊膜膨出，症状随年龄增长日渐严重：排尿反流，导致反复尿路感染，伴有发烧，肾功能受到影响，总尿湿裤子。当爸妈带着男孩来就诊时，毕允力觉察到男孩因受疾病折磨，已出现严重自卑、焦虑情绪。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">摆在眼前的传统治疗方式弊端很明显：要扩大膀胱、改善反流状况、治疗输尿管……意味着男孩腹部将留下很长的永久疤痕。“还有什么好办法吗？以后留那么大的疤痕，孩子找工作、找对象都难免受影响。”面对家长渴望，毕允力大胆设计了新方案，全部手术实现腹腔镜下操作。而之前，儿科医院对扩大膀胱、改善反流等单个手术实现了腹腔镜下操作，但所有步骤一并完成，从未尝试过。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">术前，毕允力承受了很大精神压力：腹腔镜下要针对好几个器官做治疗，稍有不慎，便会影响男孩未来生活功能。他反复斟酌、衡量利弊，最终认定：冒这个险，值得！腹腔镜下连续操作12个小时后，手术顺利完成，男孩身上只留下几个微创打孔洞。术后，孩子体力虽虚弱，眼神却如点亮的星星般，充满希冀。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为“中间地带”患儿送上福音</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“17岁少年的病例总让我反思，医生的能量可以很大。你选择保守或冒险，选择固步自封或创新进取，对病人意义大相径庭。”长期从事小儿泌尿外科治疗，毕允力深有感触：有的病可以彻底治愈、有的病毫无办法，但作为“中间地带”，大多小儿泌尿疾病属于慢性病，孩子未来能否获得更好生活质量，很大程度上取决于医生。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">要不要迈出冒险的一步？每每临床上遇到选择时，17岁少年的那张脸，总会浮现在毕允力脑海里。少年脸上洋溢着笑容，似乎过往生活阴霾正被渐渐扫除。自2009年顺利完成手术后，少年每年秋季都会返回儿科医院，参加神经源性膀胱患儿俱乐部，用自己的故事鼓励弟弟妹妹们。而今，曾经自卑的男孩已变成阳光、自信的大学生。他由衷感激毕哥哥：“要是没有您当年肯冒险为我开刀，或许我现在仍在病床边等待治疗。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">技术的进与退，真真切切可以改变人生。在跨出关键创新的一步后，毕允力与小儿泌尿外科一发不可收拾：从最初的每年完成2至3例，到而今每年完成10例左右，腹腔镜下的娴熟操作，已为数十名患儿送去康复福音。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">为膀胱外翻患儿设计新手术</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“纵观医学史上，每一次技术进步，都离不开冒险，我能做的只是充分准备，将风险降至最低。”毕允力常这么说。在应对罕见病膀胱外翻过程中，他从少年身上启示而来的冒险创新精神，再次发挥作用。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">作为最先进修复术――盆底软组织完全松解膀胱外翻修补术，同时也有最大风险，手术要分离患儿一侧阴茎海绵体，稍有不慎，患儿未来生活质量一落千丈，还有可能遭致医患矛盾。毕允力深知，如果自己不担风险，相安无事；但许多因膀胱外翻遭到遗弃的患儿，就此没有治疗转机。反复设计方案、探索6例新术式后，毕允力发现技术仍不够成熟、手术时间过长，直至他请来英国权威专家亲临授课，将最新技术娴熟掌握。至今，儿科医院已利用新术式成功救治8例膀胱外翻患儿，手术效果十分理想。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">“孩子不似大人，他们不会直白的语言表达；不过孩子不会骗人，他舒服了，脸上就会有笑容、眼神就会亮起来。”患儿眼中的大哥哥，医术精湛，却不乏一颗单纯的童心，“看到点亮孩子眼神的那一刻，我获得了身为医生的最大满足感。”</span>
</p>
