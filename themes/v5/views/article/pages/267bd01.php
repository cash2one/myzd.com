<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海华山医院可行所有肝段肿瘤腹腔镜手术</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">上海华山医院普外科肝胆外科专业组成功开展所有肝段肿瘤的腹腔镜下切除手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 6月19日，上海华山医院普外科陈进宏教授、贾户亮副教授、陆录和朱文伟主治医生组成的医疗团队，在钦伦秀主任的指导下，为1例结肠癌术后肝转移的患者成功施行了腹腔镜下肝脏转移瘤切除术。该患者的病灶分别处于肝脏尾状叶和Ⅶ、Ⅷ段交界处，其中尾状叶处肿瘤位置深，紧邻血管。由于患者首次结肠癌手术为开放手术，腹腔内粘连较严重，因此实施腹腔镜手术难度极大。钦伦秀主任带领团队术前拟定详尽的治疗方案，术中精细操作，顺利完成手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 同日，该团队还为1例结肠癌肝转移患者施行了腹腔镜下结肠癌和肝脏转移灶同期切除术。两名患者术后恢复顺利，近日顺利出院。此次成功施行的肝脏尾状叶肿瘤腹腔镜切除术，标志着上海华山医院普外科肝胆外科专业组已能开展所有肝段肿瘤的腹腔镜手术，居全国领先行列。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肝脏手术传统上被认为是“巨创”手术，手术创伤大，出血量多，部分患者影响肝功能，导致住院时间长，恢复时间长。近年，腹腔镜肝癌切除术因其“微创性”而逐渐兴起，但多数局限应用于肝第Ⅱ-Ⅵ段等表浅部位肿瘤切除。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 位于肝脏Ⅶ、Ⅷ段的肿瘤由于位置深，暴露困难，能开展此类手术的医院并不多。而肝脏尾状叶位于肝后下腔静脉的前方，第一肝门的后方，三支肝静脉的下方，亦即夹于三个肝门结构之间，位置深在，显露困难，以往甚至被认为是开放手术的禁区。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 直至上世纪90年代，有关肝尾状叶开放切除术的报道才逐渐增加，但开展腹腔镜下尾状叶肿瘤切除术更是寥寥无几。至今为止，全世界范围内单中心腹腔镜下尾状叶肿瘤切除术最多病例数仅为8例。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 华山医院普外科肝胆外科专业组成立1年多来，已成功实施159例完全腹腔镜肝脏肿瘤切除术，包括左右半肝切除、Ⅶ、Ⅷ段肿瘤切除等等。</span><br />
