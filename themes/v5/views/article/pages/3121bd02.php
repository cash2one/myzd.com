	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:1.5;"><strong>重庆真实版“霍比特人”：最大的心愿是能穿双新鞋</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"><img src="http://static.mingyizhudao.com/147004376831993" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 手术前刘会长的左脚和右脚对比。　西南医院供图　摄</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';">&nbsp;<img src="http://static.mingyizhudao.com/147004376901364" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 奶奶给刘会长做的布鞋。　西南医院供图　摄</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 家住四川渠县21岁的刘会长有一个独特的“大脚板”。这只“大脚版”43厘米长，14厘米厚。从出身到现在，刘会长一直没有穿过正常的鞋，所有的鞋都由奶奶给他做。现在，70岁的奶奶老了，眼睛看不见了，刘会长的大脚板以后怎么穿鞋成为奶奶最大的心病。刘会长说，希望手术之后，能让自己的脚恢复正常，奶奶也会安心。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 奶奶给“大脚板”做了21年布鞋</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 刘会长的爸爸刘运江告诉记者，刘会长出生的时候，家里人已经发现他右脚要比左脚稍大一些。刘运江把孩子带到当地医院，医生说，这是一种罕见病，要治疗只有截肢。刘会长的奶奶不忍心，把他抱回家，说以后这孩子的鞋我来做。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 刘运江随后和妻子前往外地打工，刘会长平时就跟奶奶相依为命。奶奶平时要忙农活，闲暇时，就一针一线的给刘会长做鞋子。由于刘会长只有右脚是大脚板，左脚是正常的，所以奶奶只需要做一只“大脚板”的鞋。随着年龄的增长，刘会长的大脚板越长越大，奶奶的鞋也越做越大。以前，奶奶一年要做两只。现在奶奶已经70岁了，眼睛已经看不清楚了，差不多两年才能给刘会长做一只。现在奶奶最担心的是自己过世之后，谁来给孙子做鞋子。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> “大脚板”的心酸</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 除了走路痛之外，这个“大脚板”给刘会长带大了太多的痛苦回忆。在学校的时候，同学们都爱嘲笑他。特别是一些调皮的同学，最爱在他的“大脚板”上跳几下。自己的弟弟妹妹看不过去了，为了不让他受同学的欺负，还跟他的同学打了几架。刘会长说，自己是大哥，却要弟弟妹妹来保护，自己心理一直觉得过意不去。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 在找工作的时候，刘会长的“大脚板”也受尽了鄙视。中专毕业，学数控专业的他，面试了好几家单位，一看到他的“大脚板”，老板都摇头拒绝。后来，还是他的中专老师，托了熟人的关系，联系到一家单位，刘会长才找到这个包装工的工作。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 这是巨肢巨趾症世界罕见病</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 四处求医的刘会长，得到的答案都是截肢。奶奶心疼得不行，流着泪对刘会长说，不去看医生了，奶奶还给你做鞋。但是刘会长不甘心，又来到第三军医大学西南医院。西南医院骨科唐康来副主任给刘会长进行了会诊，并且查阅了国内外的大量文献。可惜的是，并没有这方面的报道。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 唐康来教授把刘会长的检查结果，发给了全球著名的骨科医生。之后，有18位教授进行了邮件回复，其中17位医生建议截肢，只有一位教授表示可以考虑手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 经过反复论证，唐康来教授还是决定手术。据唐康来教授介绍，刘会长患的巨肢巨趾症，是先天性的罕见病，同时刘会长的脚还患有平足畸形、中足的关节脱位等多种疾病。“刘会长的右脚的2、3、4趾特别大，只有大脚趾是正常的。手术将分三期进行。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';"> 据了解，目前唐教授刚刚为刘会长完成的第一期手术。3个小时的手术中，医生主要对刘会长的巨趾进行手术。唐康来介绍，手术要循序渐进的进行，是为了让刘会长的脚部血液循环正常，一但血液循环不正常，刘会常的脚部将会坏死。从手术后恢复的情况来看，刘会常的状态非常好。</span><br />
