<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>内镜黏膜下剥离术 微创治疗消化道早期癌</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1月13日，解放军第153医院消化内科黄锦副主任医师在解放军总医院消化内镜中心主任卢忠生的指导下，成功为5名患者进行了内镜黏膜下剥离术（ESD），这是该院消化内科针对消化道早期癌，在河南省内率先开展的一项技术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;多年以来，外科手术切除是治疗消化道早期癌的首选方法。然而，外科手术风险、创伤大以及存在近、远期并发症的可能，使人们梦想着能有一种微创且能完整切除病变的治疗方法。经过几十年的探索，内镜技术不断进步，从早期的内镜息肉摘除术，到内镜黏膜切除术（EMR），再到最近的内镜黏膜下剥离术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;内镜黏膜下剥离术的独特优势在于将较大的平坦病灶，像“片鸭皮”一样完整地剥离下来，主要用于消化道息肉、早期癌及癌前病变和黏膜下肿瘤的治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;内镜黏膜下剥离术的主要适应证：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;1.早期癌及癌前病变。根据医生经验，结合染色、放大和超声内镜等方法，确定肿瘤局限在黏膜层和没有淋巴转移的黏膜下层，ESD切除早期癌可以达到与外科手术同样的治疗效果，同时避免外科手术较大的创伤和术后并发症。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;2.巨大平坦息肉。直径超过2厘米的息肉尤其是平坦息肉，推荐ESD治疗，一次性、完整地切除病变。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;3.黏膜下层包括固有肌层的良性肿瘤。脂肪瘤、平滑肌瘤、间质瘤和神经内分泌肿瘤等。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">&nbsp;&nbsp;据悉，目前在河南省仅有两家医院开展此项技术，解放军第153中心医院就是其中的一家。</span>
</p>
