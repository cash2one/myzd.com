<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多家长咨询癫痫患儿能不能接种疫苗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这个问题是大家非常关心的，因为国内多种疫苗说明书均提示：癫痫患儿禁忌。然而非常遗憾的是没有任何指南能简单的回答说可以还是不可以。在2013年最新版英格兰公共卫生组织颁布的感染性疾病免疫接种绿皮书（Immunisation against infectious disease, the Green Book）中提到，癫痫本身不是疫苗接种的禁忌症。癫痫患者接种疫苗后发生神经系统副作用偶有发生，但没有统计学意义。预防接种有效保证了绝大多数儿童的健康，因此不应当拒绝让儿童接受可能救命的疫苗。有些疫苗接种后会导致体温升高，常见的有乙脑疫苗、流脑疫苗、白百破疫苗，当患儿有癫痫、热性惊厥、癫痫家族史，疫苗接种后可能诱发癫痫或加重癫痫发作，但长期随访没有显著的副作用，因此仍然建议正常接种。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">但对于接种前尚未发病的某些先天缺陷病患儿，疫苗接种则可能诱发发病，出现难以预料的意外，甚至猝死。有机酸尿症、线粒体病、脂肪酸代谢障碍、尿素循环障碍在发热、疲劳、药物、饥饿、疾病等应激状态下都可以急性起病。这些疾病通常起因于基因或本身的缺陷，也就是即便不接种疫苗，下次碰到发热、饥饿或外伤迟早会发病。如果不能意识到这些问题，医生可能会诊断为“脑膜炎”；而家长就会错误理解为“打疫苗把孩子打傻了、打死了”。</span>
<p>
