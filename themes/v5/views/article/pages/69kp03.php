<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前我们中心的意见是：癫痫发作稀少，或癫痫完全控制时间在1年内，将利弊向交代家属，建议接种，如果家长恐惧接种诱发癫痫发作，可以等到癫痫完全控制1年后正常接种；小于1岁的患儿，如果癫痫发作未控制，建议推迟到病情稳定后再接种；7岁以上的小儿癫痫患者若癫痫未能控制，不应再补种百日咳疫苗；癫痫患儿在第一次接种后3天内出现抽搐，就不应再进行加强接种；若第一次接种后7天内出现脑病表现，也不应再进行第二次接种；患儿癫痫发作频繁或有原因不明的逐渐进展的脑病时，建议暂缓。</span>
