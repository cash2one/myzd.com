			<div style="text-align:center;">
	<strong><span style="font-size:18px;">提高胆囊癌的早期诊断和治疗</span></strong>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原发性胆囊癌是最常见的胆道恶性肿瘤,近年来发病率有所增加,因其预后极差而逐渐受到大家重视。近30年来虽然外科手术技术不断进步和发展，但是胆囊癌恶性程度高、临床症状出现迟、早期诊断困难早期发现者太少、术前误诊率太高、治愈性手术切除率太低，导致预后极差。临床上能术后长期生存的胆囊癌患者,大多是胆囊手术中无意间发现的早期癌症患者。因此，早期发现、早期诊断及合理手术是目前提高胆囊癌术后生存率的关键。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>如何早期发现胆囊癌？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胆囊癌的诊断目前尚缺乏特异性的肿瘤标志物,主要依赖影像学诊断。近年来随着超声、CT、MRI和ERCP等技术的广泛应用,胆囊癌的诊断率有所提高,但早期诊断率依然较低。提高胆囊癌的早期诊断率, 首先提高基层医院的诊断水平, 因为这类患者绝大多数曾首先在基层医院检查。B 型超声和CT是当前最常用的检查方法,然而诊断与手术发现的符合率有的时候却很低，因为影像诊断特别是B超检查, 其结果对检查者有很高的经验依赖性。繁忙的门诊工作条件以及检查者缺乏对胆囊癌诊断的警惕性时, 可能造成漏诊和误诊。故加强对胆囊癌诊断警惕性的宣传教育,对提高早期诊断的一条重要途径。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 重视胆囊癌高危人群的随诊工作，可望发现早期病例，这有着重要的临床意义。哪些患者是胆囊癌的高危人群呢？主要有: (1)年龄大于55岁；(2)有明确的、病史较长的胆道疾病，病程&gt;5年；(3)右上腹痛由间歇性转变为持续性；(4)胆囊结石大于&gt;2.5cm；(5)B超提示胆囊有萎缩、钙化、局部增厚，陶瓷样胆囊；(6)直径&gt;1.0cm的胆囊息肉；(7) 胆囊的腺肌增生症；(8) 胰胆管汇合处畸型；(9)继往曾行胆囊造瘘术。对具有以上情况的患者, 应认为是胆囊癌的高危患者, 需要作进一步的检查并密切随访。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>胆囊癌患者如何规范治疗？</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;那么一旦确诊或者高度怀疑胆囊癌该怎么办呢？&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;(一)手术治疗 &nbsp; 早期胆囊癌以往认为行胆囊切除术即可达治疗目的，但现在大多学者支持早期胆囊癌也应该做根治性的手术。中晚期胆囊癌应行胆囊癌扩大切除术，指将胆囊及周围2cm肝组织扩大切除，清扫胆囊周围淋巴引流区如门静脉、肝动脉和肝外胆管周围等淋巴结,做到切缘无癌残留。如果肝脏局部侵犯可加相应肝叶切除甚至肝左右多叶切除，由于胆囊的淋巴引流途径很广,若有远处淋巴结转移甚至加做胰头十二指肠切除术。对于伴有肝内广泛转移灶、转移灶过大或侵犯肝门、肝转移合并其他脏器广泛转移、全身状况较差不能耐受肝切除手术和合并肝硬变的晚期胆囊癌无法手术切除者，可行经股动脉穿刺插管肝动脉化疗栓塞、经皮B超引导下无水酒精注射等。对于合并肝门或远端胆管侵犯所致的各种梗阻性黄疸,应积极采取多种方式的引流术；有幽门梗阻，则作胃空肠吻合术，以减轻患者痛苦、提高生存质量。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(二)放化疗 &nbsp;为防止和减少局部复发,放疗作为胆囊癌的辅助治疗，可以提高五年生存率。胆囊癌的放疗包括外照射、术中放疗和近距放疗。目前尚无公认的能有效控制胆囊癌的化疗方案,而以5-FU 为基础的联合化疗FAM( 5-FU , 阿霉素和丝裂霉素) 对胆囊癌的治疗疗效可达30%。最近研究发现新出现的抗肿瘤药物健泽联合5-FU或顺铂具有协同作用, 而毒性作用无叠加，联合化疗者总的缓解率为60 % ，是目前疗效最好的报道。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（三）其它 除了上述治疗以外，胆囊癌患者还可以应用免疫治疗和中药治疗。免疫增强剂如干扰素、白介素和胸腺肽等提高机体免疫力,改善病情,延长生存期,提高生存质量。中药治疗通过扶正祛邪、调和脾胃，减少化疗药物的毒副作用和放疗的损伤，提高机体免疫能力，增强体质，提高疗效。</span><br />
