<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>腰椎滑脱的介绍&nbsp;</strong></span> 
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰椎滑脱是指因椎体间连接异常发生的上位椎体于下位椎体表面部分或全部的滑移。简单地说腰椎滑脱是指一个椎体在另一椎体上向前或向后移位。腰椎滑脱一般为前滑脱。后滑脱好发于腰5和腰4椎体，其中腰5椎体发生率高，其他腰椎少见。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰椎滑脱的原因可以是先天性的（出生时就存在），也可能是后天性的，在儿童时期或更晚些发生。主要是因各种过度的机械应力引起，诱因一般是有过剧烈的运动。还有一种腰椎滑脱是退行性的，即由于腰椎各种结构老化而发生结构异常，通常发生于 50 岁以后，这种滑脱通常伴有腰椎管狭窄，多需要手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">发生腰椎滑脱后，患者可以没有任何症状，仅仅在是拍片时发现；也可能会出现各种相关症状，如腰痛、下肢疼痛、麻木、无力，严重时可出现大小便异常。滑脱较重的患者可能会出现腰部凹陷、腹部前凸，甚至躯干缩短、走路时出现摇摆。如果腰椎滑脱没有明显的加重，可以采取保守治疗，定期复查腰椎 X 线，了解滑脱情况。如果严重那么就考虑手术的治疗了。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">腰椎滑脱的病因至今尚不十分明确，大量研究表明先天性发育缺陷和慢性劳损或应力性损伤是两个可能的重要原因，一般认为以后者为主。</span> 
</p>
