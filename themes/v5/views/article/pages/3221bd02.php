<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">刘思德教授在接受家庭医生的采访时表示，早期的大肠癌是无特异症状的，当患者一旦感觉身体有明显不适时，往往已到中晚期，错过了最佳的治疗时间。所以想要防治大肠癌，关键是早发现早治疗。</span><br />
