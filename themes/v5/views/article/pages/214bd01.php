		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>翁习生教授：人工关节置换术后假体周围感染问题（资讯）</strong></span>
</p>
<p align="left">
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="<?php echo $baseUrlImage; ?>article/doctor/214/1.png" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><img src="<?php echo $baseUrlImage; ?>article/doctor/214/2.png" title="" alt="demo.jpg"/></span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">中华医学会骨科学分会主任委员田伟教授在美国骨科医师学会（AAOS）学术年会的世界骨科交流论坛上提到，1921 年中国第一个西医骨科在北京协和医院成立。今天，在 AAOS 会议的中国展台，丁香园有幸对北京协和医院骨科主任翁习生教授进行了专访。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">中国作为特邀嘉宾国参与本次 AAOS 会议</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">翁习生教授称，中国骨科协会（COA）国际学术会议在国际上的影响力不断扩大，AAOS 希望可以与中国合作，加强两个国家在骨科临床和学术领域的交流与互动。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">翁教授指出，以往中国医生参加 AAOS 年会是本着学习的态度，学习国外临床的先进理念和技术。在前期的准备工作中，AAOS 的中国会员人数不断增加，国内外交流不断加强，COA 在国际上的影响力也不断扩大，在今年 AAOS 会议上，我们有了更多的交流和合作，学术讨论越来越平等，让大家都可以看到中国骨科的研究成果。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">国外临床科研值得学习的地方</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">翁习生教授谈到，国内对临床科研的重视不够，国外临床科研早已开始使用电子文档，并建立了相对完善的注册数据库，而目前国内对资料保存还停留在纸质文档阶段，即使更新了电子病历系统，往往也无法对病历数据进行系统的统计和分析处理，大量临床资料都没有得到有效的积累。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">无源之水，无本之木，临床数据的缺失阻碍了中国临床科研的发展。但是临床科研光靠一家医院是不行的，需要志同道合的医院共同协作，建立统一的标准，统一的数据记录和管理模式，加强合作，尽快追赶，实现大数据化管理。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">关节置换感染</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">人工关节置换术后假体周围感染是关节外科医生最为关注的话题之一，据翁教授介绍，国外对关节置换术后感染的问题非常重视，国内仍需进一步加强工作。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">关节置换失败的原因很多，包括假体松动、不稳、脱位、感染等。在美国，感染导致翻修的病例数目不断下降，但在中国，感染仍是人工关节置换失败的首要原因，感染发生无论对病人还是医生而言都是重大灾难。因此，我们需要高度重视。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">首先，要选择合适的病人和合适的时间进行手术，高度关注导致病人感染的高危因素，比如糖尿病、皮肤病变、糖皮质激素的使用，随后精心准备手术实施方案；同时也需要获得其他专科的协作。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">另外，关节外科医生需要有严格的无菌观念，细致规范的操作，精心的围手术期护理。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">国内医生已经意识到人工关节感染发生的情况与国外的不同，但是我们缺乏相关的基础研究数据，包括感染发生率、发生时间和机制来支持这样的结论，所以我们已经在做相关的临床基础研究来制定相关指南。</span>
</p>
