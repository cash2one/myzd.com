		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>ASCO2014：钟文昭发现生物标志物指导下的新辅助治疗可用于 NSCLC 治疗</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于 IIIA-N2 期非小细胞肺癌患者，表皮生长因子受体酪氨酸激酶抑制剂（EGFR-TKI）新辅助疗法及个体化辅助疗法都是极具吸引力但又颇有争议的治疗策略。广东省人民医院的钟文昭等进行了一项 II 期临床研究试验，目的是研究生物标志物在基于 EGFR 基因突变状态分型的 IIIA-N2 期 NSCLC 患者新辅助治疗策略引导中的作用。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">根据 EGFR 突变状态，研究人员把可切除的、经组织学证实为 IIIA-N2 期的 NSCLC 患者分配到新辅助治疗厄洛替尼或吉西他滨 / 卡铂（GC）组。该临床研究的主要终点是缓解率响应（RR）。次要终点是无进展生存期（PFS）和总生存期（OS）。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2008 年 1 月至 2011 年 5 月，研究共纳入 24 例 IIIA-N2 期 NSCLC 患者。患者总响应率为 42%，厄洛替尼组 EGFR 突变患者响应率为 58.3%（7/12），吉西他滨 / 卡铂组 EGFR 野生型患者响应率为 25.0%（4/12）。厄洛替尼组有 6 例患者接受切除术治疗，吉西他滨 / 卡铂组为 7 例。厄洛替尼组和吉西他滨 / 卡铂组患者中位无进展生存期分别为 6.9 个月和 9.0 个月。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">两组中位总生存期分别为 14.5 个月和 29.3 个月。厄洛替尼组和吉西他滨 / 卡铂组接受切除术治疗患者的中位总生存期分别为 28.9 个月和 55.8 个月。所有患者人群无进展生存期和总生存期分别为 7.9 个月和 24.1 个月。此外，厄洛替尼组接受切除术治疗的 6 例患者在疾病进展后接受酪氨酸激酶抑制剂（TKI）再次治疗时出现部分缓解，第 2 次中位无进展生存期值为 11 个月。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">对于 IIIA-N2 期非小细胞肺癌患者，生物标志物指导的新辅助治疗策略是可行的。虽然厄洛替尼可提高患者响应率，但并没有带来该亚组患者无进展生存期或总生存期改善。研究人员认为，要想确定围手术期 TKI 疗法对 IIIA-N2 期非小细胞肺癌患者治疗效果，需对新辅助疗法联合 EGFR-TKI 进行进一步随机对照研究。临床试验信息：NCT00600587。</span>
</p>
