<br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 近日，上海第九人民医院整复外科杨军主任医师团队主刀，南北院区多学科协作，成功为一位腰背部患有17公斤巨大神经纤维瘤的患者进行了全切除修复术。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
