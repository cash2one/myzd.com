	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 日前，由整复外科杨军主任医师主刀，大家齐心协力奋战11小时，为患者完整切除了瘤体。该神经纤维瘤覆盖患者整个肩、背、腰部，重达17公斤。为解决肿瘤切除后的大面积创面，杨军采取将瘤体表面皮肤反取皮回植的方法，成功覆盖创面。术后九院(北院)烧伤整形科派出多名具备大面积创面管理经验的医生为患者轮流值班，精心护理。目前，陈先生恢复良好，植皮完全成活，已康复出院。</span>
