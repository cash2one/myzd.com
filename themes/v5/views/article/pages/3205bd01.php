<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>有个小毛病 最好去看医生</strong></span>
</div>
<br />
<br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 春节即将到了，许多心脑血管、醉酒、急性消化道、烫伤的病人也逐渐增多，专家提醒，及时了解发病诱因，平安度过新年。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 暴饮暴食易得肠胃病</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南京市中医院金小晶主任提醒：过节期间要注意饮食的规律性，不要让零食代替主食，更不能暴饮暴食。</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">人的消化器官的活动有一定规律，如果突然吃得太饱，特别是摄入大量的油腻食物，加上过量饮酒，就需要更多的消化液来进行消化，加上胃胀得很大，肠胃蠕动困难，影响了正常的消化机能，情况严重的话，可导致急性胰腺炎、肠胃溃疡等。如果饮食不干净的话，还会引起肠胃炎。</span>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 肾病患者避免过度劳累</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南京市中医院孔薇主任提醒：肾病患者春节期间要避免过度劳累。如果天气变化再感染风寒容易引起慢性肾炎、慢性肾衰竭的发作或者病情的加重。饮食上要避免肥、甘、油、腻，还有酒的摄入，会引起痛风或者痛风性肾病。尿毒症患者特别是少尿的人不要摄入过多液体，每年过节都会有很多病人由于液体摄入过多而导致心力衰竭症状送入医院，所以患者朋友一定要小心。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 少吃辛辣防痔疮</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 南京市中医院王业皇主任提醒：过度疲劳会使肠胃受伤，以致浊气不能排出，淤积于血，最后流到肛门也会发生痔疮。饮食变化加上忙碌而忽视饮水，可能会出现大便干燥、排便困难的情况，所以便秘也是诱发痔疮的一个因素。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 玩通宵小心疱疹</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 江苏省人民医院皮肤科主任范卫新提醒：节日期间尤其要注意生活起居有规律，最好不要熬夜。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 节日期间一些人纵情欢娱，甚至通宵达旦，会使人在过度消耗体力的时候仍感受不到疲劳。这种反常的生活方式，势必导致机体免疫力下降，可能引起带状疱疹的发生。</span>
</p>
<p>
	<br />
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 主食可以多样化</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 以往过年，人们经常是只吃副食不吃主食，这是不科学的。主食不仅包括米面，还有杂粮、薯类等，它们含有丰富的碳水化物，是最经济、最直接的能量来源。而粗制米面及杂粮是人们获得维生素B1、矿物质和膳食纤维的最好来源，像燕麦、大麦、荞麦、粟米、玉米、高粱米等杂粮，都含有精白米面中所缺乏的营养素，可起到有益的补充作用。不过要提醒高尿酸血症、慢性胃炎、痛风的病人慎食杂粮。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 吃法：春节期间可将定量的米和粗杂粮放在一起煮，做成“五谷饭”或“八宝饭”（非市售加糖产品），也可以将红薯、南瓜等薯类与米、面混合制成南瓜饭、蒸南瓜饼等，正所谓“粗细搭配，过节不累”。烹调方法最好用煮、蒸，避免油炸，如油条、煎饼等。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 肉类食物要量化</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 春节期间是不是可以随意地吃肉呢？李群主任认为，肉类食物在提供优质蛋白质的同时也带来了“丰厚”的脂肪，尤其是一些畜禽类的肥肉、肉皮和荤油，对于肥胖、高血脂、高血压、冠心病、高尿酸、糖尿病的病人来说非常不利，所以春节时尽量不要以肉类食物为主。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 吃法：选择肉类品种的时候要定量并多样化，正常人每日可摄入肉类食品1～3两。最好将鱼、禽、畜类都搭配在一日三餐中，如中餐可以吃1两瘦猪肉、1两青鱼肉、1两鸡肉，尽量少用肥肉、肉皮和荤油。烹调方法以蒸、炖、煨为主，不宜煎、炸，如能搭配一些蔬菜不仅提高了菜肴的营养价值，控制了肉的用量，还能在颜色上进行搭配，就是“锦上添花”了。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 蔬菜水果要丰富</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 蔬菜能够为人体提供丰富的膳食纤维、维生素和矿物质。水果不同于蔬菜的是它含有很高的糖分，所以正常人饭前吃水果可以有助减肥，饭后吃就要在吃饭时为自己留一点“肚子”，以免能量过剩。春节人们喜欢吃水果沙拉，丰富的水果配上高能量、高脂的沙拉酱对于患糖尿病、肥胖、高血脂的人来说等于“雪上加霜”。</span><br />
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 吃法：可以选择多种颜色的新鲜蔬菜制作成蔬菜拼盘，如莴苣、胡萝卜、平菇、木耳、番茄等，也可以用涮的方式去除部分草酸，这样既保证了营养素的丰富又能做到色香味俱全。吃水果最好在两餐之间，一般在下午3点合适，选择含糖量低的水果，如猕猴桃、柚子、台湾大枣、火龙果、圣女果等。</span>
</p>
<p>
	<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"><br />
</span>
</p>
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 饮酒有度坚果不多吃</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 节日里欢聚把盏，确有兴致，但是饮酒过量，不但会酒精中毒也会引起情绪的变化，好事变坏事。此外，春节期间，一般人家里都准备了各种各样的瓜子、花生、开心果、核桃、糖果、巧克力等小食品招待客人或自己享用。因而节日里常常吃掉平时好几倍甚至是十几倍的坚果和糖果。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 方法：饮酒时要注意量，健康成人每人每日可饮2两红酒或1两白酒。而脂肪肝、糖尿病、痛风、高血压、高血脂、冠心病的病人可以用“以茶代酒”的方式表达心意。而坚果类食物中含有很高的糖分脂肪，虽然可口但过多食用就容易造成血糖和血脂的波动。</span><br />
<span style="line-height:2;font-size:14px;font-family:'Microsoft YaHei';"> 近期，很多市民咨询最多的一个问题就是“过年了，我该怎么安排我的饮食？”江苏省人民医院营养科主任李群认为，过年吃美味不是一个难题，关键在于如何在美味的基础上做到相对“健康”，如果市民能把握好“四大饮食原则”，就可以达到美味加健康的效果。</span><br />
