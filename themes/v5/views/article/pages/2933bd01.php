		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>医生称儿童睡眠打鼾多发生在3到6岁 需引起重视（资讯）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">丈夫天天晚上睡觉打呼噜，妻子王女士不胜其烦，日前，王女士听从朋友的建议，将丈夫带到医院检查了一番，诊断结果出人意料：丈夫打呼噜也是病。医生提醒，打呼噜并不是日常人们理解的睡得香的表现，它不仅影响别人休息，还是睡眠障碍的警告。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 北京同仁医院耳鼻咽喉科，咽喉科学专业主任医师王军指出，在打鼾的人群中，约有20%的人睡觉时经常出现憋气症状，医学上称之为阻塞性睡眠呼吸暂停低通气综合征(OSAHS)。有这种病症的人易产生白天困倦、性格急躁、乏力、工作效率下降、睡眠后不能解乏、口干、夜尿增多、返酸、性功能减退等症状，并易发高血压、冠心病、脑血管疾病等。研究表明，OSAHS患者的猝死发生率明显高于正常人群，重度OSAHS患者五年死亡率是15%左右。同时由于OSAHS患者有明显白天嗜睡现象，所以OSAHS也是导致交通事故发生的重要原因之一。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 王军提醒，胖人明显容易打呼噜，对儿童来说更加需要注意。儿童睡眠打鼾伴张口呼吸多发生在3到6岁，这类儿童往往体重和身高低于同年龄组儿童，而睡眠时呼吸道阻力的增加可使胸部的负压增大，久而久之则造成鸡胸；张口呼吸还有可能使颅面骨发育受到影响；严重时还可能因长期通气不足产生肺源性心脏病。一旦儿童睡眠受到严重影响，白天嗜睡，学习成绩会明显下降，还易有噩梦，遗尿等症状。家长们一定要看好自己的孩子，千万别让睡眠误了孩子的正常生活和健康成长。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"> 不过，保持良好的生活习惯对打呼噜的人群是很好的补充。不良的生活习惯可能诱发OSAHS的发生或使病情加重，如吸烟可导致上呼吸道炎症、使局部黏膜干燥、局部感受器受到损害；饮酒不仅刺激上呼吸道，而且可使呼吸中枢受到抑制。一旦怀疑自己睡眠时有问题，应尽快到有条件的医院做详细的检查，以明确诊断、判断病情的严重性、确定下一步的治疗方案。尤其对儿童来说，只要睡眠时有明显的呼吸不畅就应该积极治疗。(杨珺易瑛)（来源：京华时报）</span>
