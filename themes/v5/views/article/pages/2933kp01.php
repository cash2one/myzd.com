	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>我是白内障吗？（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">患者：描述：右眼网脱，做了垫压手术，术后无需趴着时间：2013-04-25 至 0000-00-00&nbsp; 科室：未填,未填药物名称：思考林、广乐</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">服用说明：</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">都是一次一片，一天三次，无不良反应</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4月25日做的右眼网脱手术，恢复一直良好，但是6月1日左右，左眼出现模糊现象，好像有一团白雾遮挡，视力有影响，到医院做了电生理一系列检查，都没有大问题，检查晶体，说是有白内障。我很奇怪白内障不是要很长时间才能形成，我得左眼一直很好，我得怎么手术后几十天就形成了。因为网脱手术前晶体肯定要检查的，那时还没问题。我一直高度近视左眼770，右眼800多想问一下，我这有可能是什么情况？大夫说左眼是白内障的可能性很大，我就奇怪，几十天白内障就形成了。我还需要做些什么检查？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">患者：特别是做了造影后，大夫看了说是白内障</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">患者：当时是右眼孔源性视网膜脱落，做的是右巩膜外垫压手术</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">北京同仁医院白内障中心王军：左眼的白内障跟右眼的手术没有关系。白内障不是几十天内形成的，但是白内障在不同的时期进展速度不一样。在正规医院的眼科完善白内障的相关检查。应了解玻璃体、视网膜、视乳头、黄斑区，是否正常及脉络膜有无病变。可应用视力干涉仪检查未成熟白内障的黄斑功能。</span>
