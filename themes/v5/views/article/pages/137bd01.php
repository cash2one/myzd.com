		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>孙立忠：有些事，我们这代人不做就来不及了</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;主笔：大医生兵器谱戴志悦</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 孙氏手术以前，医生不敢碰主动脉弓</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 戴志悦：在没有孙氏手术之前，大夫们怎么做手术？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;孙立忠：在没有孙氏手术之前，国外做急性夹层，只是换主动脉弓近端的血管，而不敢换主动脉弓。最早日本有一位大夫试着换过主动脉弓，从文献上看，报了13例，死了5、6例，结果不好，他就没再系统往下继续做。当时全世界的医生都觉得这个手术难度太大，风险太高，所以大家都不去触碰。后来，有了孙氏手术之后，推动大家慢慢开始去换主动脉弓，欧美国家也逐渐接受了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：国外已经有很成熟的象鼻手术，当初也是您引进中国的，为什么这种手术不适合中国人，孙氏手术与这个有什么不同？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：传统的象鼻手术主要是用于主动脉弓降部的动脉瘤，都是用软的象鼻，里面没有支架，这就要求主动脉里有一个空间能把血管放进去。国外的主动脉疾病主要是以主动脉瘤为主，所以他们适合的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 而中国很少有弓降部的真性主动脉瘤，我做了2000多例主动脉弓部的手术，但弓降部的真性主动脉瘤连200例都不到。所以，中国人主要还是夹层，导致弓部损害，血管里假腔把真腔空间挤占得很小，软的象鼻很难放进去，就算放去了，也很很容易扭转阻塞血管。所以，我在传统的象鼻上加了一个支架，先用支架把血管撑开，重建血管就容易多了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 把做过手术的患者一个个找到</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：您还记得第一例孙氏手术吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：这名患者，我们一直在做随访，女性，现在50多岁，生活一切正常，还能做家务。第一例是我从非典前线回来以后在宾馆隔离一个月时研究出的方法，当时没有别的更好的手术方法，我们就试着做了一例，做了以后效果非常好。后来又继续研究血管的材料，并申请了国家专利。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：孙氏手术10年了，这些做过手术的患者后来的情况怎么样？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：我们一直在做随诊，从2003年以后，我做了近2000例，陆陆续续全国很多医院都开始做，巴西、阿根廷等很多国家也在推广，全世界的病例数加起来有8000多例。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我的2000多例，一直都在做随访，尽管随访的成本很高，我们要通过各种方式去找患者的联系方式，甚至专门派人上门去了解他们的情况。到目前为止，还有200多例没有找到，我们还会继续找。找到的患者，情况都还不错，说明这个手术的确是有效的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 急性主动脉夹层能撑到医院做手术的人比例很小</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：主动脉疾病中能用上孙氏手术的比例有多少？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：要换主动脉弓的手术大部分可用孙氏手术，其中主要是A型主动脉夹层的病人。这个手术可以提高手术的安全度，降低再次手术的机会，如果患者需要二次手术，二次手术的难度也下降，因为胸降主动脉这一段有了支架血管在里面，下一次手术可以直接在人造血管上做阻断缝合，手术风险就下降了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> B型主动脉夹层因为不累及升主动脉，风险本身就没有A型高，死亡率也没有这么高。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 不管夹层病人还是非夹层病人，主动脉破裂的部位，升主动脉发生的机率最高，因为心脏收缩的时候，血流对血管产生的冲击力最大，升主动脉承受的压力也最大，所以它破裂的机率大，A型夹层往往要急诊手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：从病人发病情况看，主动脉夹层是A型多还是B型多？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：在临床上，我们见到的病人A型多，但从总体发病来说B型的多，因为B型风险没有A型高，治疗起来比A型相对简单，很多患者不需要到北京来看病，所以在我们的患者群体里，我们见到的A型多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 不过，现在我国还没有流行病学调查数据，调查也难，因为很多A型病人，很多还没来得及到医院检查确诊就死亡了，国外的死亡病例都会做尸检，所以能搞清楚死因，而我国没法做到这一点，许多人猝死之后家人也不会去寻找原因。所以，很多A型病人在早期就死亡了，真正能撑到做手术到医院来看病的人，比例很小。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中国患者做一台手术要管几十年</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：从医生的角度看，中国人的主动脉疾病患者与国外有什么不同？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：来找我们看病的患者，基本都已经是撑过了2天急性期的，病人手术的条件比较好，而且我们的患者年轻，平均40多岁，国外平均60多岁。另外，我们的患者预期寿命比国外长，40多岁做手术，过20年才到60多岁，而他们的患者60岁做手术，活20年就80多了。所以他们和我们追求的目标不一样，他们只要管10年、20年就行，我们却要管的时间更长，就像今天做手术的12岁的孩子，我们得管他能活上60年、70年。所以我们对长期效果更为追求。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：国际学术界如何看待孙氏手术？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：这几年大家逐渐认可我们的手术方式了，今年11月意大利、纽约分别有学术会，都邀请我去讲孙氏手术。以前刚开始的时候，人家问为什么要把手术做得这么大，因为他们一次只解决一个局部问题，保证低死亡率，过几年有问题再说。我说，因为我们的国情不同，我们尽量要给患者一次尽量做彻底，否则反反复复做手术，对患者的身体和经济都是很大负担。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 现在看来，有了孙氏手术以后，换主动脉弓，手术虽然大了，但操作简单了，难度降低了，死亡率反而降低了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：目前孙氏手术在国内和国际上使用的情况怎样？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：在国内已经有100多家医院开展孙氏手术了，全国各地有110多名医生可以独立开展这种手术。全世界范围来看，南美洲的一些国家，如巴西、阿根廷、委内瑞拉都在推广孙氏手术，他们的国情和我们差不多。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 大血管外科医生有天生的使命感</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 戴志悦：以前与您的团队接触得也比较多，能体会大血管外科医生辛苦，作为这个领域医生的学术带头人，您有什么感受？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 孙立忠：做心血管外科的医生的确与其他外科医生不一样，其他任何脏器手术之后，都需让脏器休息一段时间以后再工作，但心脏不行，心脏停跳时间长了，人活不了，所以我们既要在很短时间内修复它的解剖结构，还得恢复它的生理功能。简单地说，要在很短时间内把病治好了，还得让它功能恢复到正常。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 我们在手术中最怕两件事，第一是心脏跳得不好，第二是大出血。心脏跳得不好或跳得没有力量，病人是下不了手术台的；出血也不行，只要有出血，病人也下不了手术台。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 所以我们做心血管外科医生和其他外科医生不一样。所以对我们心血管外科医师技术要求非常高，你缝得不好就漏血，人活不了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 国外的心血管外科医生成长的时间也比别的专科医生都长，做心脏外科医生之前，一定要做普外科、普胸科，把基础打得很牢靠才能做心脏外科医生。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 而要成为大血管外科医生，成长时间更长，因为大血管医生是以心外科为基础，基础操作是一样的，只是大血管手术技术要求比心外科要更高，一般来说，心外科手术做好以后才能做大血管的手术。所以，大血管外科医生平时还是要经常做心脏手术，就像练基本功一样。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 大血管外科医生的压力大、风险高，但成就感也来得更高。我国主动脉外科整体水平还很落后，尤其是我们这一代人，作为中国的第一批大血管外科医生，责任就是把主动脉外科技术推广到全国去。我们这代人不做这个事情，就来不及了。</span>
</p>
