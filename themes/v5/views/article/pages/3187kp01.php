	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>用错眼药水，当心青光眼</strong></span><br />
</div>
<br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">临床上曾有患者因患结膜炎后双眼瘙痒、红肿，听人推荐用了地塞米松眼药水，症状迅速消失。但不久后，他感觉视力下降，到医院检查后，医生告诉他这可能是误用激素眼药水引起的青光眼。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 主要成分带“松”字的眼药水，如地塞米松、氟美松等多属于激素类眼药水。它们有很好的抗炎、抗过敏作用，适用于过敏性眼病、角膜炎、结膜炎、视神经炎等眼病的患者。虽然起效快，但临床上，长期用激素类眼药水引起的激素性青光眼患者有逐年增加的趋势。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 长期用激素类眼药水易损害眼睛小梁网功能，阻碍房水流出，使眼压增高、压迫视神经，严重时会导致视神经萎缩和视野缺损（即眼睛看到的范围变狭窄）。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 有青光眼家族史和中高度近视的人对激素类眼药水比较敏感，应慎用。眼压升高的程度与药品种类、浓度、用药频率和持续时间有关。如果症状发现较早，停药后可逐渐恢复正常，一旦造成视神经损伤和视野缺损，恢复的可能性就不大了。建议用这类眼药水2周以上者定期测量眼压。</span>
</p>
