<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 名医档案</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 郑珊 主任医师，教授。复旦大学附属儿科医院副院长、中华医学会小儿外科上海分会副主任委员。擅长小儿消化道畸形如先天性肛门直肠畸形、先天性巨结肠、胆道闭锁等手术治疗。曾获上海市科技成果三等奖，上海市医疗成果三等奖、上海市青年医务工作者“银蛇奖”一等奖等。迄今在国内外杂志共发表论文50余篇。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家门诊时间：每周四下午（需预约）。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 毕允力 副主任医师，擅长小儿泌尿外科先天性畸形的腹腔镜手术治疗，在国内最早开展腹腔镜肾盂成形术治疗肾积水。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 普通门诊时间：每周二下午，每周四上午（需预约）。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 儿童先天性畸形的发病率正逐年上升，需要接受外科手术治疗的孩子越来越多。怎样将手术对孩子造成的创伤降到最低？这是患儿父母最关心的问题。全新的小儿微创技术，为解除这一困扰带来新解。借助腹腔镜等现代医疗器械及相关设备，在患儿身上开0.5厘米至1厘米的小孔，由掌握技术的医生操作，孩子术后几乎没有伤疤、恢复快，更没有明显的疼痛感。而今微创技术，正成为越来越多小儿外科专家的选择。本期周刊，我们便邀请了权威儿科专家，为您讲解小儿微创技术的发展与应用。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> ——编 者</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 小儿微创术优势在何处</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 美容效果是微创手术独特的优势。小儿微创手术只需在腹部打上几个“钥匙孔”般的小洞，借助腹腔镜的巧妙操作，术后几乎不留疤痕。除了外形美观，微创手术的术后伤口疼痛感较小，患儿短时间内就可以下地活动，一般3至5天内便可完成检查、治疗、康复的全过程，有效降低了传统开膛剖腹手术的风险，伤口感染率也大幅下降。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 其次，微创手术减少了疾病为孩子带来的心理创伤。不少医学人士认为，创伤不仅是伤疤的长短，更应该包括精神和心理上所受到的伤害。因手术产生的疤痕，会使孩子产生焦虑等不良情绪反应，甚至会给孩子将来的人生道路带来重重困难。研究表明，极端严重的创伤打击后，儿童会对生活的态度发生明显改变，具体表现为对未来没有信心，对人回避，难以形成较亲密的感情等。由此可见，腹腔镜手术所具有的切口美观、术后疼痛减少、恢复快等优点，对患儿心理的影响无疑会降至最低。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 除此之外，微创手术单价虽比常规手术费用高，但若以入院过程总费用来算，微创手术因住院时间少、并发症少，费用反倒更实惠。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 穿过小孔动刀，先天畸形孩子“新选择”</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 哪些疾病适宜微创手术</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 哪些小儿外科疾病适宜使用微创技术？</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 目前，微创技术几乎涉及了小儿外科各个专科领域的各种常见病。比如，腹腔镜技术治疗阑尾炎、斜疝，已成为各个儿科医疗机构常见的治疗手段；小儿普外科领域的疾病，如幽门环肌肥厚、先天性巨结肠、胆总管囊肿，以及小儿泌尿外科领域的高位隐睾、精索静脉曲张、肾发育不良、肾积水、膀胱输尿管反流等，均可在腹腔镜下完成。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 不过，由于小儿疾病的种类繁多，各种疾病存在不同类型，不同类型又会合并其他问题。因此，是否选择微创手术，还应根据具体病情、孩子的体质、医院和医师的技术来决定。近年来，申城各医疗机构均掌握了严格的规范应用和操作原则，由此开展的各种小儿腹腔镜手术尚未发现严重并发症及死亡病例。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> “经肚脐”微创成新趋势</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 传统的微创手术需要三、四个独立切口来完成。现在，经肚脐或其他部位进行的腹腔内窥镜单一切口手术 （简称LESS手术），已荣登医学技术领域最重要的创新项目之一，这一手术在临床上称之为“单孔腹腔镜手术”。凭借新的手术工具，医生可将二、三种医疗器械通过单一的插入点，伸入患者腹内治疗疾病。数据显示，仅在欧洲每年用单孔腹腔镜进行的胆囊手术高达90万台。专家预计，在接下来的几年里，包括复杂手术在内的 “经肚脐”微创手术将会大幅激增。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 自2009年，复旦大学附属儿科医院开展单孔腹腔镜下肾切除、肾积水肾盂成形、重复肾切除、腹内睾下降固定、美克尔憩室切除等手术，手术均获得成功。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 微创手术≠微风险</span><br />
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 很多人将微创外科手术简单地理解为：创伤就一点点，不碍事。这其实是严重的认知误区。微创并不等于“微风险”，真正的微创手术必须在完成手术之时，尽可能少留创伤，尽可能保护患者的组织和器官，远非简单的以小切口方式动刀这么简单。因此，微创手术具有和开放手术一样的手术风险。</span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 微创手术不能盲目开展。需要微创的患儿，就诊时应选择大型正规的儿童专科医院、有微创手术经验的小儿外科医生，这样才能保证手术的微创效果。如果病例选择恰当，医生技术熟练，仪器设备优良，手术前的各项准备充分和规范，微创手术确实能达到患者创伤小、痛苦少、恢复快的目的。相反，如果病例选择不恰当，手术技巧不熟练、手术中遇到问题不能应付自如，微创手术也可能会发生严重的并发症，甚至“微创”有可能变成“巨创”，为患儿带来不必要的痛苦。 (本文来源：解放日报 )</span>
</p>
