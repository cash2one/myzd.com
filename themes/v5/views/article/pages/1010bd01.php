<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 快嘴仁心</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “孩子现在有夜间睡眠缺氧憋气的症状，如果只保守治疗不做手术的话，将来还有可能复发。至于你担心的以后免疫系统发育问题，那不会受手术影响的。”肖水芳一边给一位来自黑龙江佳木斯的小女孩看病，一边向一旁的孩子父亲解释。他的两个学生则一个在旁边观摩，一个帮着记录病历。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肖水芳在病人中口碑极好，除了精湛的医术外，他对病人总是很和蔼。在进门之前面色紧张的患者，似乎都在他的诊疗之后轻松不少。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 中午12点40分，第32位病人走出诊室。而通常的一个门诊单元大夫是挂15个号。如果不是还有会议，他通常要到下午两三点才结束门诊。“能帮我孩子爸也看一下吗？他鼻子也不太舒服。”一旁一位30多岁的妇女不好意思地问道，她们一家一大早从河北赶过来，刚给儿子看完病、拿完药。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肖水芳简洁地询问了一下孩子爸爸患病的情况,然后开始给他做检查。“儿子跟老爸长得挺像的嘛，都挺帅！”肖教授的一句话，让诊室里的几个人都微微笑了起来。“你之前用过什么药？……我给你开一些药。”肖水芳一边把用药告诉学生，一边开始洗手换衣服，他还要赶去参加下午一点半的会议。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “有时候常会碰到这种情况，其实只要我有时间，都可以给他们看一下的，来一趟医院都不容易，更何况还是从外地赶来的。”一上午的门诊，他一口水都没顾上喝，嗓子已经不像早晨那样清爽，但还一如既往地用略带湖南口音的普通话笑着说。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1962年，肖水芳出生在湖南益阳的一个小镇上，尽管已经离开家乡三十多年，他依然保留着湖南人的快语速。说话快，走路也似脚下生风，这也让身边的其他人都不知不觉地紧跟他的快节奏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 尽管肖水芳快人快语，但对待患者，他一定是放慢语速、温和可亲，从医几十年来从未发生过一起纠纷。他身边的人都说，几乎没有看到过他发脾气。“即便有了争执，他也不急着争辩，总是耐心地解释。”他的同事李天成说道。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 肖水芳从医是受母亲影响，他的母亲“肖家婆”是镇上为数不多的几位“接生婆”。“她是接受过专业培训的，我们镇上一两万人都是她接生的。”肖水芳自豪地说，年幼的他看到母亲很受人尊敬，又觉得能够帮助别人很有意义，便在高考时义无反顾地选择了他心中最好的医科―北京大学医学部。</span><br />
