		<div style="text-align:center;">
	<span style="line-height:2;font-size:18px;font-family:'Microsoft YaHei';">白内障“特聘主任”卢奕来厦开白内障患者分享会</span> 
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2014年4月26日，厦门眼科中心积极响应厦门市卫计委推行的“双主任制”，正式引进上海复旦大学附属眼耳鼻喉科医院博导、国内知名白内障专家卢奕教授担任该院白内障专科特聘主任，成为该科的“双主任”。卢奕教授定期来厦，与厦门眼科中心白内障专科张广斌主任带领的白内障专家组共同为白内障患者会诊，让患者不出厦门就能享受到国内顶级医学专家的诊疗服务。本月17日下午，卢奕教授将坐诊厦门眼科中心，患者可提前咨询预约。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">另外，笔者从厦门眼科中心获悉，本周六该院将举办第七期白内障患者分享会，由白内障专科主任医师李立梅博士为广大市民做白内障科普讲座，现场还将有公益眼疾筛查。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">创新医院医疗技术交流 沪上眼科名医定期来厦</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">所谓“双主任制”，即在该医院的重点专科设立特聘主任和执行主任两个岗位，前者由柔性引进的国内外知名医院学科带头人、知名专家担任，被赋予全面管理科室的权力，并承担学科发展、科室管理、临床诊疗、科研教学、人才培养等责任；后者由医院选聘本院专家担任，配合特聘主任开展工作，负责科室的日常管理工作。近一年来，厦门眼科中心白内障专家联合卢奕教授已为众多白内障患者提供了高端会诊和治疗。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据悉，厦门眼科中心除了引进卢奕教授实行“双主任”制外，还正在积极研究与上海复旦大学附属眼耳鼻喉科医院建立双向转诊机制，更大程度方便福建地区的患者。未来双方将加强在医疗设备共享，医疗会诊、病源转诊等方面的合作，更大程度方便福建地区患者和上海乃至华东地区患者就诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">白内障疑难杂症中心 患者可享一站式服务</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">众所周知，白内障是致盲的主要眼疾之一，其导致的失明率占致盲率的首位。福建闽南地区由于日照时间长、紫外线辐射强等原因更是白内障高发区。厦门眼科中心白内障专科学科带头人张广斌主任介绍，手术才是治疗白内障的有效方式。一般而言，只需十几分钟就能顺利完成一台正常白内障手术，多数患者都能通过手术重获光明。然而，白内障患者中，也有不少是由青光眼、色素膜炎等引起，这些患者多伴有并发症，手术风险高、难度大。厦门眼科中心已于2011年1月成立“白内障疑难杂症中心”，集合国内专家力量，为白内障疑难病症患者，提供来自各方专家的会诊和治疗，至今已接诊数千位白内障疑难杂症患者。</span><br />
