			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>结直肠癌基础知识8问</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、大多数的结直肠癌是怎么发现的?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;大部分结直肠癌患者是由于出现症状后就诊是诊断的。 在此情况下所检查出来到结肠癌大约仅有一半是早期的，是可以治愈的，所以如果有症状，及时就诊断很重要。然而最好监测结直肠癌的时机是在症状出现之前进行筛查，特别是50岁以上的人群。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、我应该在什么时间开始筛查？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;理论上对于年龄超过50岁的人群就因该开始筛查。如果一级亲属中有患有结直肠癌的患者，筛查时间应该提前。如果有家族遗传息肉病或遗传性非息肉病性结直肠癌患者或炎性肠病的患者，筛查试验更应该尽早，具体由医生制定筛查方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、如果筛查我应该注意哪些？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;在进行筛查前有些准备可以使检查结果更准确，如行粪常规+潜血试验前几天不要进食含有动物血液的食物，行肠镜检查前应该如何准备肠道才能让医生观察更清晰，以免遗漏病变。在检查前应咨询医生，了解准备事项及注意事项。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、在筛查过程中什么疾病最多?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;息肉!特别是腺瘤性息肉，如果在筛查过程中发现，并行摘除术，那么结肠癌的发生率就会大大降低，起到预防癌变作用。结肠镜检查的随访至关重要。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、结肠镜检查时会感觉疼吗?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;随着无痛内镜的开展，结肠镜检查时根本不会感觉到疼痛。如果做普通结肠镜，可能会有些腹胀及腹部不适的感觉。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、如果检查出结肠癌，要行结肠造瘘术吗？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;一般不会。仅有不到5%的患者行该手术，该手术只是结肠的改道手术。外科医生会按照肿瘤的分期及发生部位行不同方式的手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7、治疗结直肠癌会有哪些方法?</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp;结肠癌的治疗方法包括手术、化疗、放疗和最新的靶向治疗(单克隆抗体) 通常医生会采用联合治疗方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8、女性结直肠癌的发病率也高吗？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; 高！有些人错误地认为女性不可能患结直肠癌，这是不科学的。事实是性别在结直肠癌的发病率差别并不大。为此，在美国FDHN组织还专门写了一本关于女性和结直肠癌的科学读物。</span><br />
