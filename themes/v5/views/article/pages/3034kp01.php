			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胃肠外科手术术前特殊准备</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对耐受力不良的病人，除了要做好本报前述一般准备，还需做各种特殊准备，具体如下：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.营养不良：营养不良病人蛋白质缺乏，耐受失血和休克等的能力降低，易引起组织水肿，影响愈合，且易并发严重感染，应在手术前予以纠正，争取达到正氮平衡状态。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.高血压病人血压在160／100mmHg以上时，可能在诱导麻醉或手术时出现脑血管意外或急性心力衰竭危险，需应用降压药，使血压降到上述范围以下，但不必降到正常后才做手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.心脏病：心脏病人的手术死亡率是一般病人的2.8倍。心脏病的类型不同，其耐受力也各不相同。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）耐受力良好的心脏病包括：非发绀型先天性心脏病、风湿性和高血压心脏病。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）耐受力较差的心脏病包括：冠状动脉粥样硬化性心脏病，房室传导阻滞易发生心脏停搏。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）耐受力甚差的心脏病包括：急性心肌炎、急性心肌梗死和心力衰竭，除急症抢救外，手术应推迟。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">心脏病患者手术前准备的注意事项：①长期使用低盐饮食和利尿药物、水和电解质失调的病人，手术前需纠正；②贫血病人携氧能力差，手术前可少量多次输血矫正；③有心律失常者，根据不同原因区别对待，对偶发室性期前收缩，一般无需特别处理；④急性心肌梗死病人，6个月内不施行择期手术。心力衰竭病人，最好在心力衰竭控制 3～4周后再施行手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.呼吸功能障碍：呼吸功能不全的主要表现是稍微活动就发生呼吸困难，哮喘和肺气肿是最常见的两种慢性病。对严重肺功能不全者，术前应做血气分析和肺功能检查，对伴有感染者，必须得到控制方可手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术前准备：（1）停止吸烟2周，鼓励病人深呼吸和咳嗽。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）应用麻黄素、氨茶碱或异丙肾上腺素雾化吸入。经常咯脓痰的病人，手术前3～5天开始应用抗菌药物，并做体位引流。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）经常发作哮喘的病人，可给口服地塞米松。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）麻醉前给药量要少。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5.肝脏疾病：常见的是肝炎和肝硬化。肝轻度损害，不影响手术耐受力；肝功损害较严重或濒于失代偿者，手术耐受力显著削弱，必须经过长时间严格准备，方可施行择期手术；肝功能有严重损害，表现有明显营养不良、腹水、黄疸及凝血功能障碍者，一般不宜施行任何手术。急性肝炎病人，除急症手术外，多不宜施行手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6.肾脏疾病：凡有肾病者，均应进行肾功能检查，肾功能损害程度可根据24小时内生肌酐廓清率和血尿素氮测定值判断。分为轻、中、重度，轻、中度肾功能损害，经过内科处理，都能较好地耐受手术；重度肾功能损害者，在有效的透析疗法处理下，仍然能相当安全地耐受手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7.肾上腺皮质功能不足：除慢性肾上腺皮质功能不足病人外，凡是正在应用或在6～12个月内曾用激素治疗超过1～2周者，可在手术前、当日、术后给予氢化可的松，直至手术应激过去后，便可停用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8.糖尿病病人手术耐受力差，手术前应适当控制血糖，纠正体液和酸碱平衡失调，改善营养状态。凡施行有感染可能的手术，术前都应使用抗菌药物。施行大手术前，要将病人血糖稳定于正常或轻度升高状态（5.6～11.2mmol／L）、尿糖+～++，如果病人应用降糖药物或长效胰岛素，均改为短效胰岛素。手术中、后可在输液中给予胰岛素，比例为5：1，术后胰岛素用量可据4～6小时尿糖测定给予。</span><br />
