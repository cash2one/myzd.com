		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>肝肾囊肿的介入治疗（科普）</strong></span>
</p>
<p>
	<br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">肝、肾囊肿是临床上常见的良性病变，绝大部分患者并无任何症状，通常是在做B超或CT检查时发现。有些人在体检时发现有肝、肾囊肿后可能会比较紧张，尤其是觉得肝脏上“长”了东西总是不好的事，担心哪天会恶变，千方百计要去治疗。其实，这种担心完全没有必要。肝、肾囊肿绝大多数是先天性的，也就是说是与生俱来的，通常没有症状，虽然会慢慢长大，但非常缓慢，并且基本上不会恶变，所以通常不需要处理。只有囊肿大到相当程度（一般要大于5cm），引起相关器官的压迫症状或有隐痛时，才予以处理。过去需要手术处理，如开窗去顶、内引流等，但目前通过微创介入治疗的方法即可达到治疗目的，简单、安全有效，而费用非常低廉。具体说，就是在B超或CT引导下，用专用套管针穿刺囊肿，将囊液吸出，然后用一定量的无水酒精注入囊腔，将囊壁上的分泌囊液的上皮细胞“烧死”，这样囊肿就会变小并且不再长大，万一不彻底，可以重复治疗，一般单个囊肿治疗1～2次即可！多发病灶，可以分次治疗。</span>
</p>
