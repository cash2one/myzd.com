		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>周六：如何兼顾肾癌治疗与生活质量</strong></span>
</div>
<br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在过去20年间，肾癌发病率以平均每年6.5%的速度迅速增长，其中晚期肾癌患者由于对放疗、化疗、免疫或激素治疗均不敏感，因此采用传统手段的治疗效果并不理想。为了更好地帮助医生和患者建立先进的肿瘤治疗管理模式，提倡及早持续的治疗，为患者取得最大生存获益，由中国癌症基金会主办的“爱伴坦途--中国癌症基金会患者关怀计划”今年继续与第二军医大学附属长征医院合作，希望促进全社会关注肾癌患者的治疗管理。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">第二军医大学附属长征医院泌尿外科徐丹枫主任表示，在对晚期肾癌患者的治疗中，靶向药物仍然是目前晚期肾癌治疗的主要手段和有效方案。靶向药物不仅可经阻断肿瘤生长所需的血液和营养物质供给而“饿死”肿瘤，并同时具有杀死肿瘤细胞活性这两种作用机制，为肾癌的治疗带来了明确而深刻的变化，减少了肾癌患者在治疗过程中产生的副作用和精神上的痛苦。从总体上看，靶向药物治疗利大于弊，但患者需合理安排药物使用，以尽可能地使靶向药物的疗效持续较长的时间，减少副作用出现的机会。同时，不良反应的观察与护理是患者康复过程中重要的一环，要及早监测，有效管理不良事件。对不良反应须“三步走”，不要担心和恐惧，及时跟医生取得沟通，大部分的不良反应是可控制和处理的，也有许多不良反应是暂时性的，患者不要擅自停药要尽量坚持治疗以获得更大的益处。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据悉，“爱伴坦途——中国癌症基金会患者关怀计划”有效地建立了疾病管理机制，通过提供医生管理手册，让医生更好地规范化管理患者，提供患者资料包和热线回访进一步提高患者信心，对于靶向治疗有良好认知，能够自我检测，定期复查，在各医院中召开患者教育会有效的搭建医患沟通的交流平台。 9月28日（周六）9:30-11:30，肾癌患者关怀讲座将在长征医院门诊楼7楼会议中心盛大举行。届时，长征医院泌尿外科主任徐丹枫教授将与泌尿外科崔心刚教授、任吉忠教授、高轶教授、姚亚成教授和干思舜医师共同科普肾癌疾病与治疗方案，肾癌患者饮食调理和心理护理，以及展开患者交流活动，欢迎广大病友及其家属前来参加。&nbsp;</span>
