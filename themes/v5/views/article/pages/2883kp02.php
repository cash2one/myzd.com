			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>什么是立体定向脑电图SEEG</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">立体定向脑电图SEEG是近年来在国际上兴起的一种全新的癫痫病灶定位技术，该技术把定位方法从2D引入3D层面，对大脑进行全方位立体覆盖，从而到达准确定位病灶、提高治疗效果的目的。立体定向电极可以直接放置至颅内任何靶向部位，如额叶深部、大脑内侧面、扣带回、颞叶内侧等无法常规皮层电极无法达到的部位，术前能够设置电极的路径，从而规避颅内动脉、静脉，大大降低手术风险。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">立体定向电极的主要优势有：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、定位精确：每个患者都有个体化的手术路径，电极的每个触点对应不同的解剖部位；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、手术创伤小：仅需在局麻下在颅骨上钻一3mm小孔即可完成，术后当天即可下地行走。而常规硬膜下皮层需要在全麻下进行大骨瓣开颅手术；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、监测范围广：可监测双侧、多个脑叶，有助于散在脑区的鉴别诊断；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、功能全面：除查证癫痫病灶外，还可以进行大脑皮层功能定位和诱发试验等；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、热凝损毁功能：对于小范围的病灶，可以利用电极的热凝功能进行损毁，避免开颅手术。</span><br />
