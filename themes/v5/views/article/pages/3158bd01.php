		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>高龄体弱前列腺增生患者可考虑做“绿激光”</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">王先生今年85岁，患有前列腺增生多年，曾有心梗病和高血压病。几年来，每天晚上起夜次数频繁，排尿困难，最近突然又排不出尿，于是家人马上带他到医院急诊，医生诊断其为前列腺增生合并膀胱结石和急性尿潴留，需要靠插尿管维持生理需求，老人感觉非常痛苦。听说河南大学第一附属医院泌尿外科开展的“绿激光”可以让老人轻松摆脱前列腺增生的痛苦，于是就带老人找到了该院泌尿外科主任卜宏民。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">卜宏民主任为老人做了仔细的检查，最终决定采用创伤小、出血少、痛苦小、恢复快的“绿激光”进行治疗，治疗后，老人很快摆脱了痛苦。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">卜宏民主任介绍，随着我国人口老龄化程度加剧，老年男性朋友长期受到前列腺增生困扰的情况普遍存在。前列腺增生的发病率随年龄的增长而增加，通常发生在40岁以后，到60岁时高于50%；80岁以上的男性，患病几率高达83％，前列腺增生成为高发性疾病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">卜宏民主任介绍，在“绿激光”手术系统诞生之前，很多男性通过药物控制前列腺增生，但效果并不理想，手术的传统做法是医生用电刀来操作，也就是平常泌尿外科所说的经尿道前列腺电切术。相较于传统电切术，“绿激光”手术系统的优势显而易见，他就像我们外科医生的“核武器”，具有创伤小、无血或出血极少、适应症广、效果好、副作用小、住院时间短等特性，对有心脏病、高血压、糖尿病等合并症的高危病人无手术风险，高龄体弱的前列腺增生患者、许多以往传统观念上不能耐受手术治疗的前列腺增生症患者都可以接受“绿激光”手术，并能得到满意的治疗效果。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">卜宏民，主任医师、教授、硕士生导师、河南大学第一附属医院泌尿外科主任。他从事泌尿外科工作20余年，专注于泌尿系结石、肿瘤、肾上腺疾病及男科学方面的研究，尤其在微创治疗前列腺疾病、泌尿外科内镜技术方面，在豫东地区享有盛誉。在省内率先开展“经尿道前列腺汽化电切术”、“输尿管镜/经皮肾镜取石术”等多项微创技术，主持省部、地厅级科研3项，发表国家级论文30余篇。在他的带领下，河南大学第一附属医院泌尿外科开展了多项高难度手术，技术水平达国内领先。</span>
