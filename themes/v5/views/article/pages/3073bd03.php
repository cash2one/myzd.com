	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>发呆愣神也可能是癫痫发作</strong></span>
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">南医三院南方癫痫研究所近日成立</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">本报讯（记者伍君仪 通讯员孙健、荣华）我国目前约有900万癫痫患者，仅广东省就有60万，这一庞大的群体有65%以上未经正规治疗，给家庭及社会造成了巨大伤害。近日，南方医科大学第三附属医院成立南方癫痫研究所，在揭牌仪式暨中国抗癫痫进展高峰论坛上，有专家指出，癫痫不一定都有抽筋的典型症状，有时发呆、愣神也可能是癫痫发作所致。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据南医三院南方癫痫研究所所长张新伟副教授介绍，癫痫是大脑神经元突发性异常放电，导致短暂的大脑功能障碍。癫痫有多种多样的表现，大部分患者的典型表现是全身抽筋、口吐白沫，也有的人只是一只手抽筋，有的人突然倒下，有的人出现梦游现象，有的人甚至只是发呆、愣神“这种失神发作也非常危险，例如一个出纳员可以一愣神就把钱给了别人，这些人最好尽早去检查癫痫。”</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">由于胎儿缺氧、出生难产、成长环境受到污染等原因，目前约有6%。的孩子出现癫痫，以3~8岁多见。张新伟称，如果癫痫得不到控制，病灶每天放电会让大脑功能越来越差，直到整个脑子废掉，因此要早期治疗。病情较轻者只要进行内科治疗便可，8岁以后癫痫就会消失。但三分之一的患者需要接受切掉癫痫病灶的手术，术后还需吃一两年的药。他表示，一些中药声称能够对癫痫起到调理作用，实际上作用很小，而且不少中药里还混有西药成分。</span>
</p>
