			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>刘思德：春节胃肠疾病高发 常备药物可自治自救</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">春节期间亲朋聚会，美食当道，因此在每年的春节期间便是“节日病”胃肠疾病的高发时期。在这个合家欢乐的节日里如何避免患上肠胃病，家中可常备什么药物作为不时之需的自治自救呢？南方医科大学南方医院消化内科主任刘思德教授在接受家庭医生在线的采访时为各位网友的肠胃健康支招。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="http://static.mingyizhudao.com/147010492174672" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">南方医科大学南方医院消化内科主任刘思德教授</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 节假日预防胃肠疾病 把握三大要点</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刘思德教授在接受家庭医生在线的采访时表示，节假日是胃肠疾病高发的时期。其中最高发的是急性肠胃炎，第二是急性胰腺炎，第三是由喝酒引起的疾患，比如胃肠道损害、或是肝脏损害，甚至是胃黏膜出血等，这些疾病和症状在节假日尤其是春节会比平时高发很多。刘思德教授指出，春节期间胃肠病高发与人们不良的生活习惯有很大的关系。其中饮食不当和进餐不规律是诱发肠胃病的主要原因。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刘思德教授介绍，节假日的胃肠疾病其实也是可以预防的，把握好三大要点即可安全度过。首先，注意食物的来源，保证安全、卫生、清洁的食物，避免食用来源不明的食物；其次，食物的加工一定要有保障，应将食物煮熟，尽量少食用生冷食物，造成肠胃不适，同时，在处理食物时，生熟食物必须分开处理；另外，在进餐时，要把握量，切忌吃得过多，摄入过多易诱发急性胰腺炎。在就餐时间上，刘思德教授也提醒，需要有规律的进食。由于春节假期较长，如果长期不规律的饮食，易使消化功能紊乱，产生胃肠不适等问题。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 预防胃肠疾病 常备药物可自治自救</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 在节假日中，往往一不小心就会吃得太多，吃得太油腻，吃得不规律。而在假期，也有较多网友会选择外出旅游，食物的多样性，摄入不可保障等都容易诱发肠胃病。那么，当患者在发生胃肠疾病，又不便就医时，可以通过哪些药物进行自治自救呢？</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 刘思德教授指出，从胃肠道来看，在节假日及旅途中常诱发的胃肠疾病一般是急性胃肠炎。应对这样的疾病，一般建议服用胃黏膜保护剂、制酸剂、或是OTC类抗生素，比如达喜、吗叮咛等。如果出现呕吐、腹泻等症状时，多数跟细菌感染有关，所以有必要提前准备一些抗生素类药物。刘思德教授提醒家庭医生在线的网友，抗生素的使用一定要谨慎，最好提前请医生开具，并在医生的指导下使用。如果一定要自己选择，可挑选不良反应低，副作用较小的类型。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如果春节期间患者身体出现严重的腹泻，并且无法立即前往医院就诊的话，很可能会出现脱水并危及生命。在此，刘思德教授表示，严重腹泻者如无高热现象，建议服用一些止泻类的药物。先止泻，再尽快就医。因此，还需提前准备一些止泻类药物。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 每年因喝酒引发的胃肠疾病也不在少数。对于国人心中根深蒂固的饮酒习俗，节假日的狂饮也是无可避免的。刘思德教授告诉家庭医生在线的网友，喝酒前可先摄入一些主食，如馒头、米饭等，用于延缓酒精的吸收，保护肠胃。另外，最好能在饮酒前服用保护胃粘膜的药物，如达喜，对胃提前进行预防性保护。对于醉酒后的醒酒食物，如蜂蜜水、茶等，已经证实作用不大，所以，提醒各位网友，为了健康，请适量饮酒。</span>
</p>
