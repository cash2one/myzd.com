			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>有这些症状，别忘了神经外科</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">30多岁经产妇，素日身体健康，但在怀二胎孕27周时，突发小脑大量出血继发脑疝，经过急诊开颅手术成功的保住了孕妇和胎儿，孕妇当天清醒，一周后再成功剖宫产生下一名婴儿，同期切除了卵巢成熟畸胎瘤。一周之内三次抢救、两次手术，回想起来，这是多么惊险的一幕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">孕妇为什么会突发脑出血，这成了很多人的疑问。据了解，孕妇在妊娠期发生脑卒中的机会约为32 /10 万，妊娠高压综合征（妊高症）是引起孕妇脑出血的最常见病因。但主刀抢救该孕妇的中山一院神经外科杨超副教授表示，妊高症引起的脑出血95%以上都是发生在大脑，但该孕妇出血部位在小脑，而且并无妊高症病史，故高度怀疑脑血管畸形破裂出血的可能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">脑血管畸形，一般是先天性的，平时往往无任何症状，一旦破裂出血，则十分危急。其实除了脑血管畸形以外，还有很多神经外科的疾病在早期没有症状或症状不典型，因而往往容易出现漏诊或者误诊，比如说以下的一些情况：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">老年人痴呆有可能是脑积水引起</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">很多老人，随着年龄的增长，出现记忆力下降、走路变慢等症状，有的还伴随小便失禁。不少家属误以为这是年龄变大的原因、是正常现象。其实，这些可能是老年痴呆的表现，而很多人又认为老年痴呆是无药可治的，因此不去看病，但事实并非如此。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">其实，并不是所有痴呆的老年人得的都是阿姆茨海默病，有一部分患的是血管性痴呆，还有的病人是出现了脑积水，这部分发生率虽然比较低，但由于我国老年人口比例越来越高，发病率也越来越高。但大部分人会忽略这一点，所以出现漏诊的可能性比较大。杨超提醒，“脑积水引发的老年人痴呆是可能被治愈的。通过脑室腹腔分流手术，将过量的脑脊液引入腹腔中，手术可以明显改善患者的反应能力、记忆力和运动功能，痴呆也可以明显好转甚至治愈。”至于怎么知道是否由脑积水引起呢，一般作个MRI检查便能知晓。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手指麻木：小心Arnold-Chiari畸形（音：阿齐氏畸形）</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当出现肩膀麻木，进而延伸到手臂，甚至是手指麻木，很多人会想到可能是颈椎病引起；去医院看病，医生一般会建议作CT检查；若CT显示正常，或许有些人就选择吃些药，先观察一下，但如果半年之后仍然没有好转，此时应该警惕，可能不是脊椎的问题，而是脊髓的问题，应该去做一个核磁共振（MRI）。MRI相比于CT，分辨率比较高，可以清晰看到脊髓状态。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杨超特别提醒，“出现非对称性的手指麻木，特别是单根手指麻木逐步扩散到其他手指，要保持警惕，应尽早去看神经内外科。”他介绍，“双手手指麻痹可能是由糖尿病等内科疾病引起的，但非对称性的手指麻木，可能是由于颈髓疾病引起，譬如由阿齐氏畸形引起的脊髓空洞症。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Arnold-Chiari畸形，也叫小脑扁桃体下疝畸形，可以通过手术进行治疗，发现越早，治愈可能性越大。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">屁股长“尾巴”，不能掉以轻心</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当婴儿出生后发现臀部中线有“小尾巴”、皮下肿块、毛发、或者有小坑、小窝，家长通常会找小儿外科，有的大夫会把它当作脂肪瘤直接切掉。但事实上，“屁股长尾巴”绝对不是小事情，很大的可能是患了脊髓栓系。杨超指出，脊髓栓系如不及时治疗，小孩长大后会出现下肢疼痛、跛行、脊柱侧弯、大小便困难甚至肾功能不全等症状，称为“脊髓栓系综合征”；但通过手术，阻断脊髓继续受到牵拉，可以保存神经功能，防止病情继续进展。</span>
</p>
