		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>《健康报》：“为病人着想，一直是我们的宗旨”</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">郭剑明，复旦大学附属中山医院泌尿外科主任，在泌尿外科各类疾病的诊断和规范化手术治疗方面，尤其在尿路结石、肾肿瘤、前列腺癌、膀胱肿瘤等疾病的诊治，以及对术后并发症的处理及危重急症的处理方面，具有独到的经验。他曾荣获恩德思医学科学技术奖二等奖、上海市优秀发明选拔赛银奖，目前是国家卫生计生委住院医师规范化培训教材和中国泌尿外科疾病诊疗指南编委，在肾癌研究方面已获得国家自然科学基金和市科委自然基金。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">既要技术精湛，更要为患者考虑</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">临床上，郭剑明每年完成肿瘤、结石、前列腺等手术达800台，有复旦大学附属中山医院“一大能人”之美誉，但他心中还有一个原则：“为病人着想一直是我们的宗旨”。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">肾癌的诊治一直是复旦大学附属中山医院泌尿外科的一大重点和医疗特色，每年收治的肾癌手术患者一直高居上海市最前列，每年手术患者超过600人。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在处理各种复杂性肾癌上，复旦大学附属中山医院早在上世纪90年代就在国内较早开展“进腹入路处理法”，改变了以往“从腰入路法”；之后，他们紧跟国际新理念，开展肾癌局部切除的保肾手术，在国内最早开展腹腔镜手术的基础上，5年前成为国内第二家、上海市第一家率先开展了目前依然是最先进的达芬奇机器人辅助腹腔镜肾癌手术（包括保肾的肾癌手术和根治性肾癌切除手术等）。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在郭剑明20多年的临床工作中，他一直认为在治病过程中，医生既要技术精湛，也要敢于冒风险，更要为患者多考虑。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">郭剑明告诉记者一件事：十多年前，他曾经碰见一位来自上海金山远郊的30多岁年轻男性患者，体检时发现他双侧多发肾癌。当时，国内保肾手术开展较少，经验不多。医生如果为了方便，可以切除病人的肾，但是这将会严重影响其今后数十年的生活质量。为病人着想一直是中山泌尿外科的宗旨。经过精心的准备，郭剑明分别对他进行了两次手术，只对两侧肾脏的恶性肿瘤病灶作了肾部分切除，保留了双肾。这种处理方法医生承担的责任很重大，手术难度也提高了，如果处理失当，并发症的风险更高，但将明显提高患者的生活质量。手术后经随访，该患者至今未有复发，肾功能良好，并一直健康地工作着。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">尽可能多花点时间和病人交流</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">郭剑明说，看病时尽可能多花点时间与病人交谈很必要，因为除了诊疗外，病人的社会、家庭情况，甚至有些特殊情况、心理活动，对医生的判断都有用。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">郭剑明说，有位60岁的女性，经多家医院B超和CT检查显示该患者的左肾有个3厘米良性可能的占位。她来到门诊看病时，郭剑明依据影像学图像认为不能排除恶性的可能，建议手术。此时，患者显露出较大顾虑，深入交谈时，她才告诉郭剑明：女儿工作很忙，她要帮女儿带小孩；再说几家医院认为良性可能大，她自己也想过几年再说。郭剑明考虑到该病人肿块完全长在肾内，不像通常大部分肿瘤那样长在肾的表面，保肾难度较大，一旦手术，肾切除的可能很大，这将大大影响她的生活，帮女儿带孩子的愿望也会落空。于是，郭剑明告诉她，虽然目前还是小肿瘤，但需密切随访观察，二、三个月做次B超，一旦有变，立即手术，并答应尽量给她做保肾手术。听了这一番耐心的解释，患者接受了郭主任的建议。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">此后每3个月，该患者都找郭剑明看她的检查报告，密切随访了一年多，这位病人利用这段时间将外孙女上学等事都安排好了。此时，检查提示，病人肾脏的肿块长大了2毫米~3毫米。病人对郭剑明说：准备好了，现在我没有后顾之忧了，决定可以住院了。这台手术难度确实很大，但郭剑明没有忘记自己的承诺，最终既为她切除了肿瘤，又保留了肾脏；术后病理报告显示为肾细胞癌。患者非常感动，感谢郭剑明不仅为她治疗了疾病，还实现了她照顾外孙女的心愿。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">郭剑明还遇到过一位刚退休的病人，是位内科医生，8年前因右侧肾癌，切除了右肾，后来发现左肾又长出一个约6厘米的肿瘤，肿块贴近肾门的大血管。如果为了根除肾癌，为他切除左肾是最方便的，且风险较低，医生也不用担负责任，但术后病人将终生依赖血透过日子。郭剑明思忖：科技进步了，出现了治疗转移性肾癌的新型靶向药物，如果用药使肿瘤缩小后再进行肾部分切除可能会最终达到保肾目的。但服用靶向药物可能有副作用，且部分患者可能无效。经过再三权衡，郭剑明还是想在这位患者身上试试。于是，郭剑明查阅了大量文献，并了解到世界上确有少量的这方面的成功案例报道；但文献同时也指出，用药后肾部分切除的创面愈合可能会受影响，手术中可能会增加出血、漏尿等并发症。郭剑明与病人进行了充分了沟通，决定先用药。两个月后，病人的肿瘤缩小了1厘米，但病人出现了比较严重的鼻出血情况，被迫停药。新的问题出现了：肿瘤还没有缩小到最适于手术的大小，但如不马上手术，等肿瘤再次长大就很难处理了。这对郭剑明的手术技术确是个挑战。为了抓住这稍纵即逝的机会，郭剑明当机立断，立即手术。正是因为郭剑明的果断及精心的准备，最终成功地为该病人施行了较大肿瘤的孤立肾的保肾切除术。术后未发生出血、漏尿等并发症。目前，该患者已术后随访一年半，情况良好。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">这就是郭剑明，对病人，他总是这样精心细致，也正是如此，他才能够攻克一个又一个难关。</span><br />
