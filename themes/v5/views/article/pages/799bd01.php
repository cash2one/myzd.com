			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>只漏了一次体检，他肾脏就长出了拳头大肿瘤</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">家住山东的王先生，退休不过半年，便查出了拳头大小的肾脏肿瘤。“退休前的单位体检时，我肾脏还是好好的，现在怎么会有肿瘤，还长这么大了？”王先生迷惑而着急。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“肾脏肿瘤在早期、中期的时候，病人其实很难察觉到的。”浙江省人民医院泌尿腔镜中心副主任祁小龙告诉他说。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">原来退休前，王先生的单位每年会组织体检，但2014年底的那次体检与他的出游计划冲突了，他就放弃了。“之前体检单上没有‘红色的箭头’，平时身体挺好的，从来没有住院过，就疏忽了。”王先生很后悔出游回来没有及时去补查。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">与上次体检相隔一年多，再去体检时，泌尿系B超提示右肾上长了十几公分的巨大肿瘤，王先生着实慌神了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">于是动员全家，急寻对策。王先生辗转济南、北京等地的多家知名大医院，医生们认为，肿瘤实在太大了，建议他做个开放的肾癌根治术，也就是需要切开一个十几公分的口子。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正准备去挨一大刀，王先生的一位亲戚告诉他，最新的手术方法不需要开那么大的刀了。于是王先生慕名找到了浙江省人民医院张大宏医师团队。张大宏教授问诊后，认为做微创手术就可以。</span><br />
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">果然张大宏医生只通过钥匙孔般的小切口，就完整地取出了这个大肿瘤，整个手术时间还不到一个小时。手术后第一天，王先生就能下床活动了。不到一周，王先生术后的引流管也顺利拔除了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">昨天，王先生出院。对他和家人们来说，终于能踏踏实实过个年了。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">祁小龙告诉钱江晚报记者，肾脏肿瘤有时会长得比较快，如果等有了尿血再做检查，往往肿瘤到了中晚期。要尽早发现这些肿瘤，最好的办法就是一年做一次体检，“做一个泌尿系B超，90%多的肾脏肿瘤都能检查出来。”</span><br />
