<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼科是检查和治疗眼病的专科，凡是眼睛的疾病，都应该找眼科医师诊治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; 眼睛虽小，疾病的种类却很多，医学上将眼科疾病分为以下若干大类：视野、色觉、光觉的异常，眼的屈光异常，眼睑疾病、泪器疾病、结膜疾病、巩膜疾病，葡萄膜疾病，视网膜疾病，视神经疾病，青光眼，晶状体疾病，玻璃体疾病，眼眶疾病，眼球运动异常，眼外伤，遗传性眼病。中枢神经系统或其他全身性疾病引起眼的症状等。其中比较常见的疾病有：近视、远视、色盲、睑缘炎，麦粒肿、泪腺炎，结膜炎，角膜溃疡，先天性角膜异常，先天性白内障，眼外伤及遗传性眼病等。家长如果发现孩子视觉异常、眼部感染、眼部异物或外伤等不正常情况，都应该及时去眼科就诊。</span><br />
