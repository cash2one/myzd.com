			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>乳腺癌内分泌治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 乳腺癌是最常见的女性恶性肿瘤，其发病率在发达国家始终居于首位。在我国，乳腺癌的发病率也呈逐年上升的趋势。乳腺癌的确切病因目前还不十分清楚，但已知的相关因素包括月经初潮早、绝经晚、不生育或晚生育、不哺乳、乳腺癌家族史、乳腺良性肿瘤等。经研究发现，乳腺癌发生的危险性与女性体内性激素的水平相关，而这些性激素是通过激活乳腺癌相关基因来达到致癌目的的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然乳腺癌也属于恶性肿瘤，但它却是其中治疗效果较好的一种。现在，有多种治疗手段可应用于乳腺癌的早期诊断及治疗，其中较新的诊断方法包括超声、钼靶、核磁共振、PET-CT、穿刺活检等，治疗方法包括手术、放疗、化疗、内分泌治疗、分子靶向治疗、中医中药治疗等，而其中的内分泌治疗在乳腺癌的综合治疗中占有越来越重要的地位。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺癌内分泌治疗的历史，可以追朔到1896年Beatson首先报告3例绝经前晚期乳腺癌，在切除双侧卵巢后2例肿瘤明显缩小。期后一百年以来，随着对乳腺癌了解的逐步深入，乳腺癌内分泌治疗经历了肾上腺切除、卵巢切除、雄激素、雌激素、孕激素、抗雌激素、芳香化酶抑制剂治疗等发展过程。而其中的雌激素受体拮抗剂他莫昔芬（三苯氧胺）一直是激素受体阳性乳腺癌内分泌治疗的金标准。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">已证实他莫昔芬对乳腺癌有多方面的作用：① 乳腺癌复发转移，对ER（＋）患者可有50％～60％疗效，ER（－）者可有5％～10％的疗效；②乳腺癌术后辅助治疗，特别是ER（＋）的绝经后患者， 疗效优于化疗；③乳腺癌术后，服用他莫昔芬可降低对侧乳腺癌发生率；④对乳腺癌高风险妇女，服用他莫昔芬可以预防乳腺癌的发生。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">研究显示乳腺癌患者术后口服三苯氧胺5年，可使雌激素受体阳性乳腺癌5年复发率和死亡率分别相对减少47％和26％；辅助化疗后序贯应用5年三苯氧胺可进一步降低死亡率和复发率；使对侧乳腺癌的发生率下降47％。而且，口服三苯氧胺5年的乳腺癌患者，在停用后仍可显示出复发和死亡危险的降低。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三苯氧胺具有疗效确切、服用方便、价格便宜等优点但其副作用也不容忽视。口服三苯氧胺的副作用是颜面潮红、发热、皮疹、阴道出血、子宫内膜增厚、消化不良、脂肪代谢紊乱、乳房疼痛，嗜睡、头痛、头晕、痒、脱发等。其中比较严重的是三苯氧胺可以导致子宫内膜上皮细胞的增长和孕激素受体的表达。与健康人群相比乳腺癌三苯氧胺治疗和非三苯氧胺治疗患者子宫内膜癌的发病率都要高，服用三苯氧胺的人群中子宫内膜癌的发病率是对照组的4-6倍，约占服药人数的1%左右，说明三苯氧胺刺激子宫内膜癌的生长，且三苯氧胺治疗患者子宫内膜癌的病理分期的恶性度要高于非三苯氧胺治疗组。口服三苯氧胺除对子宫内膜有影响外，还不同程度影响子宫平滑肌、卵巢的结构和功能。服用三苯氧胺可以导致血清雌激素和孕激素水平上升，从而增加子宫肌瘤和卵巢囊肿的发生几率。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">鉴于三苯氧胺的主要副作用均体现在女性生殖系统，因此在口服三苯氧胺治疗的过程中应定期检查妇科彩超，一般在没有症状的情况下每半年检查一次，而有症状时应随时检查。如果在服药前后出现明显的变化，如子宫内膜明显增厚(厚度大于1cm)、大的或多发的子宫肌瘤、逐渐增大的卵巢囊肿等我们就要考虑到更换药物或进行妇科手术干预。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">除了妇科的副作用外，口服三苯氧胺另一个常见的不良反应就是对脂肪代谢的影响，少部分病人可患有严重的脂肪肝，肝功明显改变，主要是转氨酶升高。但通过护肝、降酶治疗后一般都不会造成严重后果。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，三苯氧胺仍然是激素受体阳性乳腺癌病人最有效、最方便的治疗方法之一，同时也是预防乳腺癌高危风险的有效方法。尽管其有各种各样的不良反应，但通过定期的监测、预防及早期不良反应的及时处理，能够使三苯氧胺治疗更加安全、有效。</span>
</p>
