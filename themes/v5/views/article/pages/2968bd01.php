		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>受访专家：广州军区广州总医院骨病关节科主任黄华扬</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">原标题：受访专家：广州军区广州总医院骨病关节科主任黄华扬</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">骨关节疾病要及早治疗</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">随着我国人口老龄化的程度不断加剧，老年骨关节病的发病总人数也是不断在攀升。骨关节病从发病机理上来说，主要分为两种类型。一种是因其他疾病导致的骨关节病变;例如内风湿性关节炎、强直性关节炎和外伤引起的关节炎等；另一种是指骨关节退行性病变导致的骨关节病；其主要表现在老年骨关节退行性病变引起的骨关节的疼痛，造成骨关节功能性障碍。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">广州军区广州总医院骨病关节科黄华扬主任指出：“目前常见的病人还是以骨关节退行性病变的为主，尤其是绝经期的妇女因其内分泌的改变往往表现得更明显，其骨关节病的发病几率也比男性高得多。值得注意的是，老年骨关节病集中表现在两个部位。一个是脊柱骨质疏松引起的压缩骨折，表现为腰痛、下肢麻木等;另一个是表现在膝关节上面，膝关节是骨关节退行性病变中发病率最高的部位，大概八成的绝经妇女都会出现膝关节的相关病症。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">从关节的承载分工和使用频率方面来看，关节受损几率最高的肩关节和膝关节，两者都属于运动损伤，主要是损伤关节的韧带。以膝关节为例，倘若前交叉韧带或后交叉韧带出现损伤之后，膝关节则会出现不稳将可能引起功能障碍，如果不及时治疗将会引起关节的运力集中导致骨软骨损伤，继而引发骨关节病。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">因此，黄华扬主任强调：“对于膝关节受损者，早期地进行运动关节的稳定手术非常有必要，而目前主要是利用关节镜微创技术来进行治疗。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">关节镜微创手术优势明显</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">谈及“微创技术”，大部分人可能都会略有所知其共同点——创伤少，并发症少，康复速度快。那么，对于关节镜微创技术，其特色优势又体现在哪里呢？将其与以往的传统手术相比或许更有说服力。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">以膝关节受损为例，在关节镜微创技术面世前，手术时须直接切开手术部位，而这个伤口最少也达到12cm。这往往容易造成大出血，也容易引发并发症。此外，当手术结束后需要打石膏，石膏固定的时间约为6至8周，而往往容易造成关节僵硬，事后患者如果没有强大的毅力和正确的康复指导，往往难以恢复关节正常功能。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">然而，关节镜微创技术的应用将一改此境况：首先，关节镜的开口面积就像笔头这么大，介于0.8——1.0厘米之间，患者只需缝一针即可；其次，对于一些简单的手术，只需要局麻，患者的出血量也只有几毫升；再次，患者无需打石膏固定，只需佩戴膝关节角度支具即可下地行走。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">广州军区广州总医院骨病关节科主任黄华扬介绍说：“关节镜是一种观察关节内部结构的直径5mm左右的棒状光学器械，是用于诊治关节疾患的内窥镜。关节镜在一根细管的端部装有一个透镜，将细管插入关节内部，关节内部的结构便会在监视器上显示出来,有“把眼睛和手指放入关节内”之称，可对关节进行动力性检查。关节镜不仅用于疾病的诊断，而且已经广泛用于关节疾病的治疗。关节镜开始主要应用于膝关节，后相继应用于髋关节，肩关节，踝关节，肘关节及手指等小关节等。”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据悉，广州军区广州总医院早在1985年就开展关节镜微创技术，现骨病关节科拥有51张病床，约三分之一的患者接受关节镜微创技术的治疗，年均手术量介于800—900台之间，位居华南地区同类手术第一。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">关节镜是多种骨关节疾病诊断“金标准”</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">关节镜是诊断关节内伤病的一种重要和有效的方法，尤其是对于长期关节疼痛、肿胀，功能障碍，诊断不明确，保守治疗无效的患者，关节镜是诊断的“金标准”。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">黄主任介绍，目前广州军区广州总医院骨病关节科可用关节镜手术治疗的病症主要有以下几种：急慢性膝关节运动损伤；膝关节退行性骨性关节炎（骨软骨损伤）；膝关节滑膜病变（包括类风湿关节炎、滑膜软骨瘤病、滑膜血管瘤、血友病性关节炎、痛风性关节炎、色素沉着绒毛结节性滑膜炎、结核性滑膜炎以及化脓性关节炎等）；膝关节其他病变（如关节游离体、髌骨半脱位、膝关节滑膜皱襞综合征等）；肩关节运动损伤；慢性肩周炎、肩关节感染及肩关节内骨折等；肘关节滑膜疾病、骨刺、游离体及“网球肘”（肱骨外上髁炎）等；踝关节反复扭伤、滑膜疾病、骨刺及游离体等；髋关节滑膜疾病、股骨头坏死等；腕关节滑膜疾病、正中神经卡压综合征。黄主任说：“以上多数疾病术前难以确诊，关节镜微创手术同时起到了诊断与治疗的作用。”</span>
</p>
