			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>龙浩教授在胸腔镜外科领域取得创新性进展</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">全新手术入路与手术方式评估与治疗乳腺癌病人的内乳淋巴结转移，一个新的乳腺癌微创术式得到国际承认——胸腔镜内乳淋巴结清扫术</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2012年12月，中山大学肺癌研究所、中山大学肿瘤防治中心胸科龙浩教授的胸腔镜内乳淋巴结清扫术，发表在最新一期肿瘤外科学顶级杂志《Annals of Surgical Oncology》上，这标志着国际上对此新术式的认可。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">乳腺癌病人的淋巴结转移途径，除腋窝淋巴结外，位于胸骨后的内乳淋巴结具有同等重要的地位。为追求更好的治疗效果，上世纪60年代开展了关于乳腺癌扩大根治术的临床研究（乳腺癌标准根治术+包括切除2条肋软骨的内乳淋巴结清扫），遗憾的是，至8 0年代，20多年的研究结果表明，这种的术式，创伤明显性加大，但并不能给乳腺癌患者带来生存获益。自此业界基本上摒弃了这种术式。但是，近十来年，越来越多的研究结果证实内乳淋巴结转移确实是一项不容忽视的预后不良的指标。有没有内乳淋巴结转移，直接影响其临床分期，预后及进一步治疗决策。因此，目前世界最权威的癌症治疗指南，美国国家癌症综合网（National Comprehensive Cancer Network, NCCN）推荐对于病理证实或临床高度可疑的内乳淋巴结转移，应考虑加做内乳区的放射治疗。但由于内乳淋巴结即使有转移也往往较小，目前缺乏准确有效的评估手段，活检困难而且创伤较大，影象学检查不准确。并且，内乳淋巴链的放射治疗很容易导致严重心脏放射损伤，许多病人多年后不是死于肿瘤，而是死于相关心脏并发症。从而限制了其广泛应用。在肿瘤的个体化治疗时代，既要反对治疗不够，也要反对治疗过度。内乳淋巴结转移的诊断与治疗的缺陷导致其一直处于尴尬的境地。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">事实上，结合目前乳腺癌流行病学的特点，反观上世纪的那项临床研究的设计与内容，其研究结果未必全部适用于现代。分期不精细，术前常规检查不全面，手术适应证的把握上存在一定缺陷，导致许多已经发生转移的晚期病人亦接受了手术治疗；另外，当时术后辅助治疗以及术前新辅助治疗并未发展起来，在缺乏有效全身治疗的支持下，局部治疗的生存获益或许被掩盖。进入新世纪后，随着乳腺癌筛查的普及，临床上可见越来越多早期的病人；另外，化学治疗、内分泌治疗及靶向治疗等全身治疗亦得到了长足发展，学者们才开始重新提出内乳淋巴结状态的评估与治疗的重要性。但乳腺癌扩大根治术因其创伤性过大已基本被弃用，而影像学检查和内乳区前哨淋巴结活检的敏感性与特异性均不理想，因此，探索新方法对内乳淋巴结进行准确的评估与治疗显得尤为迫切。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">龙浩教授研究团队正是在此背景下探索开创了胸腔镜内乳淋巴结清扫术这项新术式，其主要的优点包括：1，新术式区别于淋巴结采样活检，在胸腔镜下对整个内乳淋巴引流区行连续整块切除，从诊断上保证了内乳淋巴结评估与病理分期的准确性；2，新术式对内乳区进行清扫，属于治疗性手术，即使内乳淋巴结存在癌转移亦可免去进一步放射治疗，从而避免了放疗相关的心脏损伤；3，新术式结合了胸腔镜微创技术，具有创伤小、恢复快等优点。前期已完成的随机研究证实，患者并未因为增加手术操作而从生理上增加损伤。患者甚至未感觉到附加的不适。前期50例患者的研究结果已发表在乳腺癌领域国际著名杂志《Breast》（2011;20:129-33）上，40%患者因发现内乳淋巴结转移而改变了病理分期；更有意义的是，其中6例患者因发现单纯内乳淋巴结转移不但改变了病理分期而且改变了后续治疗策略。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2012年12月，龙浩教授的新手术录像，发表在最新一期肿瘤外科学顶级杂志《Annals of Surgical Oncology》上，获得了杂志主编的推荐和同行广泛的关注。作为一个新的术式在该杂志以录象发表，该杂志十分重视与谨慎！经过了多批相关的国际顶级同行专家的反复审定。该录像的发表。奠定了该全新术式在乳腺癌领域的地位。有专家在评语中写到，十分期待该手术的进一步临床结果的发表。至今已超过100例患者成功接受并获益于该项新手术。特别是对于乳腺癌病灶位于乳房内侧或中央区的早期病人很容易发生内乳淋巴结转移。该手术将明确分期，避免治疗不够，或过度治疗。而对于单纯内乳淋巴结转移的病人，将因此而获得更为准确的个体化治疗。避免不必要的心脏损伤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">龙浩教授研究团队将进一步开展临床试验，对新术式的手术创伤以及其对患者生活质量的影响作深入的研究与总结；更为重要的是，希望通过临床研究验证该新术式可能给乳腺癌患者带来的生存获益。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微创与根治的完美结合-------手辅助胸腔镜双肺转移瘤切除术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">中山大学肺癌研究所龙浩教授团队取得的另一项重要成果是通过对肺转移瘤的系列研究，特别对肺转移的外科治疗进行了深入研究，完善和发展了胸腔镜肺转移瘤的手术技巧与适应症。自主发明了经胸肋三角入路手辅助胸腔镜双肺转移瘤切除术。全新的手术入路在最小损伤的原则下对双侧胸腔的探查和双侧肺转移瘤的完整切除。新的术式使相当多的原发病灶已切除而只有肺转移的病人获得长期生存。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">国际肺转移瘤注册机构（The International Registry of Lung Metastases，IRLM）早在1997年就公布了其5206例患者的回顾研究，初步奠定了肺转移瘤外科治疗的地位。大量回顾性分析证明，选择性患者可以从肺转移瘤的彻底切除中获益的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">彻底切除肺转移瘤是影响患者预后的独立因素。保证彻底切除需双侧开胸探查，传统双侧开胸或经胸骨正中劈开，虽可达到一次手术双侧转移瘤切除的目的，但其创伤大不适用于已经发生转移的晚期患者。胸腔镜手术因其微创优势而被应用于肺转移瘤患者的外科治疗，但因其难于定位、较高的遗漏风险而备受争议。因此，胸外科医生开始探索新的手术入路，以期减少手术创伤的同时，达到彻底切除的目的，手辅助胸腔镜手术的概念正是该时期提出的。1999年Tommaso Mineo教授首次报道了经剑突途径行手辅助胸腔镜双肺转移瘤切除术，但其心脏压迫较明显；2003年Gavin Wright教授改良为经膈肌途径行手辅助胸腔镜双肺转移瘤切除术，成功避免了心脏压迫但存在膈肌损伤而且要两侧作切口。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在此基础上，龙浩教授通过解剖学的研究，开发了一条全新的手辅助入路。通过一个切口，不损伤膈肌经双侧胸肋三角途径可以全面探查双侧胸腔，并在手辅助下经胸腔镜行双肺转移瘤切除术。相比以往的手术方式，该手术利用腹部单一手术切口，利用人体自然腔隙通道，完成双侧肺转移瘤切除。损伤减到最小，而能够最大程度的切除转移瘤。甚至有相当多影像学未能发现的转移瘤。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">自2001年开展该项新技术以来，现有超过100例患有肺转移瘤的恶性肿瘤患者成功接受了手辅助胸腔镜双肺转移瘤切除术，术后并发生发生率3.6%，3年累积生存率59.8%，5年累积生存率47.2%，达国际先进水平。而最新的统计5年累积生存率已超过60% 。相关研究的成果已经发表在胸外科领域的顶级杂志美国《Annals of Thoracic Surgery》（2011;91:852-8.）上。该杂志是目前临床影响力最大的杂志。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经胸肋三角入路手辅助胸腔镜双肺转移瘤切除术主要的优点有：1，可以避免术中两次转换手术体位，一次完成双侧肺转移瘤切除；2，因为手进入了双侧胸腔探查，可以最大程度地发现双肺转移瘤，并予以精确定位切除；3，结合了胸腔镜微创外科的理念，单一切口，手术创伤少，出血少，患者恢复快。意大利Fondazione Policlinico Universitario Tor Vergata胸外科的Tommaso Mineo教授给予该项新技术极高的评价：“龙浩教授团队对手辅助胸腔镜入路的改良（经胸肋三角）取得了突破性进展。满足微创与彻底切除的完美结合，同时充分利用了人体的自然间隙，消除了其他阻碍手辅助手术推广的弊端。是他1999年首次报道经剑突途径行手辅助胸腔镜双肺转移瘤切除术以来的最大进步。15年的经验积累，充分证明了该手术的安全性与生存优势。因此，手辅助胸腔镜手术值得列入肺转移瘤诊断与治疗的常规项目。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前龙浩教授团队已总结了近60年国际上肺转移瘤外科治疗的经验，正进一步开展多中心和前瞻性研究。并与国际先进的相关研究所合作开展有关的分子基础研究。</span><br />
