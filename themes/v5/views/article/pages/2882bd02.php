		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>巧手脑中取“活虫”</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>2014年11月19日,新华医院小儿神经外科马杰主任在术中导航及术中B超下成功取出11岁男童脑中近8公分寄生虫。</strong></span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>病例重现</strong></span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">患儿何XX,男,11岁,汉族,生长于安徽省天长市釜山镇。近日,因时常感觉双脚没力、麻木,自主甩腿后就伸不直了。2014年11月9日,突然意识不清四肢抽搐,甚至面色发紫、小便失禁,这种症状持续了2分钟后自行缓解。当晚,何XX的爸妈顾不得心中的疑虑就焦急的带着孩子去了当地医院看病,经过检查后,发现孩子的病情并不简单,可能需要动手术治疗。爱子心切的他们遂及四处打听为孩子求医,最终决定带他来上海新华医院就诊。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2014年11月12日,患儿因“突发意识不清伴四肢抽搐一次”收入我科小儿神经外科治疗。入院后即刻行头颅MRI检查,结果示;左侧额顶叶占位性病变,大小约2*75px,病灶边界清楚,可呈不均匀强化,周围脑组织水肿较重,局部占位征象明显。孩子爸妈心中的疑团终于揭开了,难道孩子是得了脑肿瘤?这可怎么办?</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>考虑脑中寄生虫可能</strong></span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">在我科医生向孩子和家属采集病史过程中,发现了一个重要的信息,何XX 5-8岁间,曾因脸上反复生长“白斑”,蚕豆样大小,未感特殊不适,按照民间方法考虑应该是寄生虫病,孩子父母认为是吃了不干净的食物导致了肚子里长了虫,就在当地医师医嘱下服用了“驱虫药”一周左右,服药后病情好转,无特殊不适,之后也就没再发生这类症状。谈话期间,孩子不免透露着自己平时非常爱吃烧烤、麻辣烫等食物,平时饮食中常有鱼虾蟹等海鲜类食品。而且,6岁时曾在家中食用“乌蛇肉”一次,爸妈因为知道他爱吃“野生黄鳝”而常常烹饪这类食物。这让医生更加确信孩子可能是颅内寄生虫感染,脑袋中长得可能不是肿瘤而是小虫子。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">人大脑中会长寄生虫，裂头蚴和猪肉绦虫等寄生虫会以囊虫或卵的形态进入血管，从而有可能通过血液循环进入大脑内生存，而这些寄生虫病，大多是由喝了不干净的水或吃了不熟的肉所致。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>裂头蚴寄生男童左额叶</strong></span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">孩子住院后,完善各项检查化验、查体等,发现孩子的状态不错,马杰主任尽早地安排了手术,因为孩子的病情不能拖,早治疗早康复不仅是个口号,而是真正意义上的和时间赛跑。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">2014年11月19日上午,患儿在静脉复合型麻醉+气管插管下,行左额叶病变切除术。马杰主任在术中神经导航及B超辅助精确定位下进行,显微镜下探查中央前回,皮质下约25px见病变,内有益白色长带样虫体,宽约5px,长约125px,质硬,周围可见胶质增生,小心取出病灶组织后见其不停蠕动。虫体头端膨大,有小分叉,体端细长,有弹性,通体白色,离体后仍能生存。手术顺利。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">术后经寄生虫抗体筛查确诊为“裂头蚴”,明确术后诊断:左额叶寄生虫。患儿术后转PICU继续治疗。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>脑寄生虫病的常见症状——癫痫</strong></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">马杰主任说道脑寄生虫病的临床表现酷似脑瘤，可造成阵发性头痛、喷射状呕吐、癫痫、瘫痪甚至昏迷，而癫痫是最为常见的症状。由于该虫活力极强以及血脑屏障的存在，一般内科驱虫药物治疗收效甚微，往往需要借助精确导航定位下显微手术取出活虫，外科治疗预后良好。而小儿神经外科有着先进的仪器设备和资深的专家学者,一直致力于儿童神经系统疾病专业。手术顺利,马主任也表示深感欣慰,对于孩子能康复出院的希望也更加殷切。何XX的案列是极具代表性的,孩子的父母也深深内疚于对疾病知识的缺乏,不断表示今后一定健康饮食。</span><br />
<br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><strong>专家提醒:管住自己的嘴,放弃对野生蛙类蛇类的爱好</strong></span><br />
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">所谓“病从口入”所言不无道理,长期良好的饮食习惯和饮食质量是健康基础的保障。马杰主任强调预防应加强宣传教育，改变不良习惯，不用蛙肉、蛇肉、蛇皮贴敷皮肤、伤口，不生食或半生食蛙、蛇、禽、猪等动物的肉类,不饮用生水等是预防裂头蚴寄生虫病的有效措施。作为孩子的家长也要督促监管孩子的饮食情况,切勿因为一时贪嘴,导致疾病加身。裂头蚴病的病症危害巨大且不易治愈，如何预防就成了关键。裂头蚴是最常见于青蛙、蛇等野生动物体内的寄生虫，出现在人体内的现象非常罕见。当裂头蚴通过体表或者消化道进入人体内后，为了找到一个适合的生长环境，它会四处游走，在人的四肢、躯体、眼部、皮下等处寄生，而一旦进入人脑，不仅代谢的产物会对大脑带来损害，更可怕的是随着它不停游走，会对大脑带来不可修复的破坏。</span>
</p>
