	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>让大脑深处跃动新希望</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">6月7日，星期六[-1.98%&nbsp;资金&nbsp;研报]，早上9点。在患者身旁陪伴了一夜的余新光回到了办公室，开始了一天的工作。不分日夜，没有周末，这样的工作节奏对这位军医来说已是家常便饭。</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">坐在科技日报记者面前，对话中遮掩不住脸上的疲惫。然而说起钟爱的神经外科事业，他的脸上却重新泛起了光彩，思维的火花频频闪耀。3个小时的采访中，从自身专业谈到世界最前沿的脑研究，再到灾难医学和战创伤救治，他侃侃而谈。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">余新光，解放军总医院神经外科主任、教授、博士生导师，始终攻关在脑神经外科的最前沿，在人类大脑的最深处不断探索着医学的奥秘。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">转文从医，自有前瞻思维</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术案例和书本资料摆满了文件柜，电脑屏幕闪动着看至一半的文档，墙上老将军刘振华手书“忠诚敬业，医者仁心”格外醒目。余新光办公室不大，简单却不失整洁。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">余新光在北京长大。1969年，年仅9岁的他，随父母下放到河南信阳五七干校。两年的农村生活，让他更能体会百姓生活的艰辛；中断学业下地劳作的经历，也让他更懂得珍惜学习的机会。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1979年，余新光高中毕业。本是文科生的他，听从父亲的建议，把志愿都填上了医科大学，最终上了第二军医大学军医系。由于父亲的影响，他在英文上下了苦工夫。这为他日后发展和成为专业英语教育与培训专家奠定了基础。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">本科毕业后，余新光被分配到一家军队医院的神经外科。一年后，到解放军总医院神经外科，跟随时任科主任段国升教授进修。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一次，法国专家来访，余新光用流畅的英语与他们交流，并表现出较高的专业知识水平，引起了段国升的关注，并把他招收为研究生。之后，他在这里一口气读完硕士、博士。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1992年12月，刚刚博士毕业、只做了3个月主治医生的余新光，由于在血管病和颅底手术方面表现突出，被破格晋升副主任医师，成为该院当时最年轻具副高职称者之一。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">1996年，余新光到美国亚利桑那大学神经科学研究所做访问学者。本来看看即可的他，却一待就是3个月，积极参与病例讨论，表现十分出色。研究所负责人好奇地问他为什么，他回答：“既然来到这里，我就一定要学到真本事再回去。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">这位负责人大受感动，将他推荐至圣路易斯华盛顿大学医学院实用手术入路解剖学习班学习，这是该院最著名的商业化学习班，专业性强，学费高昂，而他因为那位负责人的推荐获得了免费学习的机会。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在那里，作为唯一发现并大胆指出教授教案中错误的学生，他的勤奋好学赢得了老师的器重，因而成为在学习班中学习时间最长的学生，并获得了头颅标本免费使用权。他以一个提问，让专家立即停止手术，转而专门为之进行现场演示。他深厚的理论功底让国外顶尖专家叹服。这样的经历，让他连续在这里免费上了9次学习班，让他能熟悉掌握从头部到腰椎的手术技巧。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术刀下，演绎无数传奇</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">上世纪八九十年代，脑静脉畸形是一种少见的脑血管畸形，在国内医学界还未被认清，不仅国内各大医院无人报告，就连教科书上也缺乏较为系统地描述。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一个偶然的机会，刚刚在该院担任主治医生的余新光接触到了这种病例，并开始留心。当时病例特别少，只收集到6例，但每一例他都亲自随访，并作了系统研究。1988年，他开始在会议上向专家们介绍这种病例。两年后，他据此在《中华神经外科》杂志上发表了第一篇学术文章，第一次向全国报告这种罕见的病例，同时对该病症的发病机制、临床特点及治疗方法进行全面描述，杂志发表时几乎一字未改。这让他迅速受到学界关注。当年，这项成果使他获得军队医疗成果三等奖。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">因为这篇文章，越来越多的专家开始重视起这种发病率其实并不低的疾病。就连著名神经外科专家王忠诚院士也在自己的书中引用过这篇文章。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“这类血管畸形不能手术”，这是国外权威专家下的结论。可余新光却不认可这个说法。直到今天，余新光依然在关注着这种疾病，并大胆尝试对部分病例进行手术切除并获得成功，突破了手术的禁区，更新了救治观念。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">脑膜瘤约占颅内肿瘤的15%，仅次于胶质细胞瘤，也是最常见的颅底肿瘤之一，长期以来一直被视为难治之症。经过深入研究，余新光创新了国外专家提出的“鞍旁间隙”为中央颅底的概念，认为颅底的前中后三部分往中央汇聚，事实上这个中心才是中央颅底（或颅底中央区）。在此基础上，他将其概念外延拓展，在国内首先提出中央颅底的概念，并于2004年最早在《中国临床神经外科杂志》发表文章介绍中央颅底。恰巧，最早提出“中央颅底”的这位外国教授，在同年出版的书中也将中央颅底的概念进行了拓展，与他的提法一致。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">依据这一概念，余新光制定出个性化手术方案，改良了手术入路和步骤，大大提高了中央颅底区脑膜瘤的治愈率。近10年来，他累计治疗颅底中央区脑膜瘤和其他高难度病变500余例，效果良好，其对颅底最难处理病变之一的岩斜区脑膜瘤，采用了经前部岩骨入路和后部岩骨入路，肿瘤全切除率61％，死亡率仅2％，达到国际先进水平。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">从事神经外科30年来，余新光一次又一次为神经外科学注入新鲜活力——</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">在国内神经外科业界最先报告“脑静脉畸形”“巨长椎—基底动脉”和“脑静脉窦血栓”等少见脑血管病；最先提出中央颅底的概念，率先采用新的颅底入路完成海绵窦、岩斜区和脑深部肿瘤切除等高难度复杂手术4000余例，降低了死亡率和致残率；在国际上最先采用术中CT导航下复杂颅颈交界畸形的减压和钉棒内固定治疗，提出“个性化治疗”的理念并改良了手术技术，提高了这种在我国发病率较高的疾病的疗效。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">紧盯前沿，大脑深处刨根问底</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2013年4月2日，奥巴马发起了“脑计划”，试图勾画个体在快速思考时脑细胞和复杂神经环路工作时的动态图谱。这一计划是继人类基因组计划之后又一国际性的科研计划，揭示大脑的奥秘成为21世纪人类面临的最大挑战。殊不知，早在2004年，余新光就已经开始密切关注脑高级功能。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">一次，一个从事IT业的小伙子受脑外伤后，到余新光这里接受救治。手术后，患者的脑外伤顺利得到康复。但在随访中，家属告诉他：以前十分内向的患者在手术之后，竟变得外向活跃起来。脑手术改变了性格吗？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">不久后，又有一个病例。一名患者因颞叶出现血肿，脑表面也出现大血肿。余新光手术后，患者成功出院。半年后，余新光在随访时，病愈者已因抑郁跳楼自杀。抑郁是由于出血破坏脑组织导致，还是手术等其他原因导致？很难弄清。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">如何避免手术带来的副损伤，将损害降到最低，首先就必须弄清大脑各个部分所对应的功能。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">该科上任主任许百男教授曾提出“精准神经外科”理念，意思就是要精确手术，将对患者脑组织的伤害降到最低，并率先引进了术中导航设备，提高了手术的安全性和治疗效果。如今，余新光又对这一理念进行了更深入拓展。他收集了所有与额叶有关的病例，联合北京师范大学的心理学专家，进行心理分析后初步发现，人脑额叶底面眶回可能与恐惧情绪管理有关联，它有可能是人类的“恐惧司令部”。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">余新光决定进入到脑高级功能研究领域，并申报相应的国家自然科学基金项目，却遭到了评委的拒绝：“一个神经外科的医生搞什么心理学研究！”但余新光坚定地认为，心理学研究的心理行为，都是有物质基础的，而这个物质基础就是大脑。心理学研究，通过收集、分析大量数据，建立起理论模型，但这种理论模型只是未经临床证实的假设，没能真正揭示脑功能深层的运行机制。神经外科作为临床医学，能够直接接触大脑，通过功能核磁共振和微电极植入等现代技术，能够真正弄清楚脑高级功能深层的运行机制。在他的坚持和努力下，关于前额叶脑损伤与认知的国家自然科学基金项目终于立项。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">经过几年研究，余新光在这个全新领域的名气越来越大。担任科主任后，他更加注重科室向研究型科室的转型。短短半年里，他从科研临床的全局高度，提出了“高难度病变、高科技、高级脑功能和基础研究、基本技能以及整合为一”的“321工程”。他还要把医院与神经科学有关的神经外科、神经内科、影像科、老年神经科心理科、麻醉科等科室联合起来，与清华大学等工科、工程类大学联合起来，将研究开展得更深入。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">采访间隙，正巧著名认知科学和实验心理学家陈霖院士来访。交流和讨论中，他对余新光开展的研究和科室大量的病例以及医疗设备产生了浓厚兴趣，本来定好半个小时的交流，延长到一个小时。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“额叶是恐惧情绪的‘司令部’。大脑的奥秘已经露出了冰山一角，深层的奥秘正在向我们招手，痴呆、抑郁、焦虑等情绪的‘司令部’也终究会被我们发现。”谈及脑高级功能的研究前景，以及“脑机接口”技术的应用前景，余新光信心满怀！</span>
</p>
