<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">优秀甲状腺外科中心的标准</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;随着人民生活水平和健康体检意识的提高，环境因素和生活工作压力的改变，甲状腺疾病的发病率不断升高，特别是甲状腺癌的发病率提高，手术病例不断增多，建立以甲状腺外科为核心的地区级甚至国家级甲状腺疾病诊治中心，很有必要。优先建立高质量的优秀甲状腺外科（专科或中心），提高甲状腺疾病的诊治水平，造福广大病患，提高无病生存率，提高生活质量，减少并发症，非常重要。</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1. 常规的腺叶切除或甲状腺全切（或近全切）+/-中央区（一侧或双侧）清扫，质量过关，术后出血、术后永久性喉返神经损伤、术后永久性甲状旁腺功能减低、术后感染，在1-2%以下；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2. 颈侧区淋巴结清扫（选择性或全颈清）规范、可靠、彻底； 淋巴瘘、舌下神经损伤、付神经损伤、血管损伤等并发症低；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3. 部分较年轻的病例，能够规范顺利完成腔镜辅助或全腔镜颈外途径的甲状腺和淋巴结清扫手术，满足病人的微创美容需求；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4. 对侵犯喉返神经、特别是气管喉、食管下咽、血管、胸部、皮肤能够单科或者多科协作完成挽救性手术；</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5. 对原发性甲状旁腺、继发性甲状旁腺手术能够较好完成；</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6. 良好的多科协作，如规范的同位素碘131治疗，高质量的超声检查、细针穿刺细胞学检查、高质量的病理学检查、良好的CT、MRI检查，必要的外放射治疗。</span>
</p>
