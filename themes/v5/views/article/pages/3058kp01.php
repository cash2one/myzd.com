			<div style="text-align:center;">
	<span style="line-height:1.5;font-size:18px;"><strong>乳腺癌患者的生育问题</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';">乳腺癌患者是否可以生育是一个比较复杂的问题，如果该患者的雌激素受体（ER）和孕激素受体（PR）检测为阴性，也就是说不需要内分泌治疗的患者（如果ER和PR中有一项或两项检测为阳性，患者需要接受内分泌治疗），我们建议她在手术及化疗结束后的三年之内最好不要怀孕，因为肿瘤的复发转移在前三年内的发生率相对较高。如果三年后，患者的身体状况很好，又有生育的需求，我认为是可以怀孕的。</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';"> 对于接受内分泌治疗的患者，情况可能比较复杂。因为这项治疗需要持续服药五年甚至十年，这些药物对胎儿可能会造成一定的影响，所以在服药期间是应该严格避孕的。但在治疗期间是不是一定不能怀孕？目前还存在着争议。激素受体阳性，需接受内分泌治疗的患者，治疗期间我们不鼓励生育。但是假设患者有这个需求，并且目前肿瘤比较稳定、没有复发转移的迹象，如果考虑怀孕，需要患者仔细权衡利弊。因为如果要怀孕就得停药，中断肿瘤的治疗，这个风险到底有多大，是否值得为怀孕冒这个险，从医生的角度很难权衡。如果生了孩子的同时，肿瘤也没问题，那将是一个很完美的结果；但最差的结果可能是没能保住孩子，同时肿瘤复发或者转移。所以这部分患者需要谨慎考虑其中的风险。同时也得考虑患者的年龄，比如患者25岁，治疗后观察5年也才30岁，之后再考虑生孩子也不晚；但如果患者年纪比较大，35岁左右还没孩子，做完治疗就40岁了，此时女性怀孕的几率会大幅下降，这时需要患者自己来权衡风险，决定要不要怀孕。</span>
</p>
<p>
	<br />
</p>
<span style="font-size:14px;line-height:2;font-family:'Microsoft YaHei';"> 还有部分患者担心乳腺癌治疗后可能会停经或者绝经，担心是不是没有月经就不能怀孕了，这得分情况来看。绝经的患者基本不会再怀孕了；停经的患者虽然怀孕的几率比较低，但如果停经之后卵巢功能还可以恢复，那么还有再次怀孕的机会，但是可能会有困难。得强调一下，月经并不是怀孕的必备条件，有些患者可以悄悄地排卵，子宫内膜没变化也排卵（没来月经也可能会排卵），也可能恰巧能够受孕。当然没有了周期性的月经，子宫内膜可能会不够肥沃，那么卵子可能不容易成功着床，就会导致不孕甚至流产。这也从另一个侧面提醒乳腺癌患者，术后治疗期间是可能意外怀孕的，虽然当时可能没有恢复月经，但是卵巢功能恢复了就有怀孕的风险，所以治疗期间需要做好避孕措施。</span><br />
