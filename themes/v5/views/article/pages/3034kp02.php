			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胃肠间质瘤外科治疗</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一、GIST良恶性区别：</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;判断目前仍存争议, 比较一致的意见是良恶性的区分主要以肿瘤大小和核分裂为准, 对临床上已出现肿瘤浸润、转移的GIST定为恶性并无异议. 但文献报道, 一些体积小, 组织为良性的GIST, 术后仍有复发转移的可能, 故认为所有的GIST均应看作是一种有复发转移潜能的肿瘤, 并提议以“危险程度”来描述GIST的性质, 参照Fletcher等提出的GIST恶性潜能分级标准, 分为极低危险性(肿瘤直径≤2 cm, 核分裂相&lt;5/50 HPF); 低危险性(肿瘤直径2-5 cm, 核分裂相&lt;5/50 HPF); 中危险性(肿瘤直径&lt;5 cm, 核分裂相5-10/50 HPF或肿瘤直径5-10 cm, 核分裂相&lt;5/50 HPF); 高危险性(肿瘤直径&gt;5 cm、核分裂相&lt;5/50 HPF或肿瘤直径&gt;10 cm、任何核分裂相或核分裂相&gt;10/50 HPF、任何肿瘤大小。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">二、治疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;GIST的治疗主要有手术、药物治疗及其他物理治疗手段, 常根据病变的性质和浸润范围分别处置, 强调综合治疗. GIST对放疗、化疗等辅助性治疗效果均不理想.</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 手术切除 手术切除是最好的治疗方法, 一旦GIST诊断成立, 外科治疗的目的就是完整切除肉眼可见及显微镜下病灶, 避免肿瘤破裂的发生和获得阴性切缘. 手术中确保肿瘤完整切除及避免肿瘤破裂较肿瘤广泛切除更有意义.对瘤体较大或术中瘤体有破损者, 关腹前可行温热化疗(45 ℃-47 ℃蒸馏水加丝裂霉素10 mg浸泡10 min), 以预防术后复发</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、手术方式: 应根据肿瘤的大小、部位及良恶性等选择手术方式. 最佳的手术方式应该是包括肿瘤组织和周围部分正常组织在内的整块切除, 如有周围组织或脏器受累, 应一并切除</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、手术范围: 不宜过大亦不宜过小. 主要通过肿瘤良恶性来决定手术范围. 对于良性患者局部切除已无异议, 只要能完全切除肉眼可见肿瘤, 行局部楔形切除就能达到与规则胃肠切除相似的效果, 但仍须做切缘冰冻检查. 而近年由于对间质瘤潜在恶性的认识, 局部及楔形切除的较少, 而胃大部切除被视为比较稳妥、有效的方法. 目前多数学者认为恶性以腹腔种植和血行转移为主, 鲜有淋巴结转移, 故大多认为无需行淋巴结清扫, 行胃肠的局部切除即可,如果区域淋巴结有转移, 淋巴结清扫能减少局部复发率和提高存活率时, 可以进行淋巴结清扫. 小肠GIST恶性程度高, 要多切除肠管, 所属淋巴结不宜扩大清扫, 术后因淋巴转移复发的病例并不多见。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、不同部位间质瘤的手术特点:</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(1)胃: 肿瘤&lt;3 cm可行局部或楔形切除, 3-5 cm应根据所在部位且胃楔形切除或胃大部切除, 而&gt;5 cm者均应行胃大部切除. 但切缘距瘤体应&gt;3 cm, 以保证切缘阴性&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(2)小肠: 由于小肠间质瘤恶性程度高, 所以首次切除至关重要. 另外小肠间质瘤要较胃间质瘤更易破溃, 手术进腹后常有多发及自行破溃现象或包膜不完整. ;少数浸润性生长的恶性GIST, 胰十二指肠切除或范围更大的手术也不可避免; 空回肠GIST两端切缘应距肿瘤10 cm以上. 有时肿瘤与肠系膜血管成为一体, 肿瘤可能无法切除, 可用药后再考虑二次手术. 回肠易见多发肿瘤, 有时肿瘤易在腹腔、直肠膀胱陷窝内多发转移结节.这些小结节术中应尽量肉眼下摘除, 残余者服用药物治疗.&nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">(3)结、直肠间质瘤: 结肠肿瘤往往位于黏膜下, 位置相对固定, 可行左半或右半结肠切除术. 直肠间质瘤较为复杂, 一般来讲位于距肛门5 cm以上者可以行前切除, 切缘1 cm已足够, 几乎不会出现黏膜下浸润. 位于距肛门5cm以下肿瘤诊断及处理均较困难, 肿瘤&lt;2 cm可行局部切除, &gt;2 cm原则上能保肛尽量行保肛治疗, 但病理冰冻切片常不能给予确切诊断, 此时保肛还是弃肛是外科医生所难决断的难题, 腹腔镜手术治疗GIST还是以小于5 cm的肿瘤为宜.</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">三、分子靶向治疗</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1 新辅助治疗: 新辅助治疗指的是在外科手术之前采用的术前辅助治疗, 在GIST的治疗中,新辅助治疗主要指新辅助应用选择性酪氨酸激酶抑制剂(TKI)制剂的靶向治疗, 以甲磺酸伊马替尼为代表. 对于较大的难以切除的肿瘤通过新辅助治疗可以获得良好的切除机会, 间质瘤在进展期或复发、转移性GIST患者, 利用TKI制剂进行新辅助治疗取得了很好的效果</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、术后辅助治疗: 大约85%的原发GIST患者可接受根治性手术切除, 但术后2年内复发率高于50%, 5年生存率在50%左右. 所以如何降低复发率成为我们关注的问重点. 研究表明: TKI新辅助治疗及手术切除后, 应继续TKI辅助治疗.确诊为GIST且直接手术后, 高复发风险的患者应考虑行TKI辅助治疗, 但持续时间目前尚无定论.</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、甲磺酸伊马替尼耐药及其对策: 尽管KIT,PDGFRα受体都是酪氨酸激酶抑制剂STI571(甲磺酸伊马替尼)的作用靶点, 然而不到20%的患者最初就对治疗抵抗, 即原发性耐药; 还有部分患者在接受STI571治疗最初有效后2年相继出现继发性耐药. KIT, PDGFRα的突变类型与耐药密切相关.</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、对于耐药患者主要措施: (1)提高STI571的剂量, 虽然不能增加客观缓解率, 但是有证据表明可以延长无进展生存时间(PFS), 当然不良反应也增加. (2)二线药物的应用, 2006-01, FDA新批准Sunitinib(SUI1248)用于STI571耐药GIST的二线治疗.</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、 甲磺酸伊马替尼的不良反应: 可发生水肿、恶心呕吐、白细胞减少、皮疹、贫血、转氨酶升高、肿瘤出血和流泪等. 有报道称其可以导致骨髓增生异常综合征. 尽管许多患者对伊马替尼有反应, 但不应该中断该药的治疗, 除非肿瘤进展、患者无法耐受以及患者拒绝</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">四、复发转移处理</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;85%的GIST术后复发或转移, 多局限于腹腔, 以肝脏和腹膜多见, 罕见于肺、胸膜及骨骼. 研究发现: 至今为止, 男性比女性更易发生转移. 对于肝脏转移者, 因肝转移瘤具有多发性、跨肝段及隐匿性等特点, 大多无肝硬化背景, 行手术治疗时常行较大范围的规则性切除. 且由于GIST属于富血管肿瘤, 可进行肝动脉栓塞化疗术(TACE), 常用介入化疗药物有顺铂、多柔比星和丝裂霉素, 栓塞剂如: 明胶海绵、液碘油和聚乙烯乙醇颗粒等. 对于肝脏转移还可实施局部减瘤, 如注射无水乙醇、射频消(RFA)、微波消融及手术切除. 对于腹膜浸润转移, 腹腔灌注化疗有一定疗效, 可选用多柔比星、米托蒽醌及顺铂等药物, 2-3 wk 1次, 共4-6次. 术后复发转移者再次手术切除可以延长生命, 不要轻易放弃手术. 对无法手术切除的复发转移GIST, 可应用伊马替尼治疗, 某些患者可能重获手术机会. 若能手术, 尽可能完整切除肿瘤, 并获得1 cm的无瘤切缘.</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">五、预后</span><br />
