			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>结肠冗长症的外科治疗</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">经钡灌肠发现结肠冗长的病人并不少，但常没有症状，因而不需要治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但对有合并症的结肠冗长则需要积极治疗，如肠扭转和肠梗阻常需要急诊手术。对结肠冗长所致的顽固性便秘，首先应行保守治疗，包括积极调节饮食和排便习惯，进行生物反馈治疗，服用肠动力促进剂、菌群调节制剂、容积性泻剂和不含刺激性泻剂的中药制剂，但尽量不用含蒽醌类物质的刺激性泻剂，必要时也可行灌肠治疗。约40％-50％的病人可得到明显的改善。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对经积极保守治疗1年以上而无效的病人应考虑手术治疗，切除冗长的传输缓慢的结肠肠段。早在1909年Lane即尝试用结肠切除来治疗冗长结肠和特发性便秘，取得了比较好的效果。Hushes于1981年报告17例，5例行乙状结肠切除，12例行结肠次全切除，术后13例效果良好。Golisherl984年报告10例，8例行结肠次全切除，2例行全结肠切除，术后6例效果极佳，每日排便1-3次，余4例也有明显改善。Penal992年报告105例，成功率89％。国内近年大量资料也显示手术效果比较理想，有效率在75％-86％。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">关于结肠切除的范围，由于冗长结肠与正常结肠之间没有明显界限，因而主张行扩大范围的切除。对乙状结肠冗长，应行乙状结肠全切除而不行乙状结肠部分切除，要游离脾曲，行降结肠直肠吻合，必要时行左半结肠切除。对横结肠冗长应行扩大的左半结肠切除，消除脾曲的锐角，然后于小肠系膜后方行横结肠近端与乙状结肠远端的吻合，必要时行结肠次全切除。对全结肠冗长则应行全结肠切除，回直肠吻合。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">术中可见结肠冗长、扭曲、转圈、粘连。部分结肠扩张、膨胀、肠壁变薄呈半透明状，部分结肠则痉挛、肠壁肥厚。切除的结肠经普通病理检查常无明显异常，但嗜银染色和电镜检查可发现肠肌间神经节细胞减少、变性、空泡化。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">由于病人长期便秘，结肠中积存大量粪便，因而一定要作好术前肠道准备，充分清洁肠道。一般需洗肠多日，直至干净。手术的并发症主要有吻合口瘘、术后肠粘连、肠梗阻、严重腹泻等，全结肠切除后常有较严重的腹泻。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前认为，对于有严重便秘、无便意、排便次数少于每周2次的结肠冗长病人，结肠运输功能试验显示有结肠慢传输，5-7日后仍有标记物未排出，经积极保守治疗1年以上无效时，在注意鉴别特发性巨结肠和成人型先天性巨结肠等病后，应行手术治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总的来说，结肠冗长所致顽固性便秘经手术治疗后，近期1-3年的效果是十分理想的，但远期尚有一定的发率。另外，部分病人还可能同时伴有直肠出口型梗阻，术前应注意查明并予以相应处理，以免影响疗效。</span>
</p>
