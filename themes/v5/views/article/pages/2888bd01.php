			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>刘开江“精准、无瘤、人性”的微创手术折服全国同行</strong></span>
</div>
<span style="font-size:14px;"><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">11月21日上午，成都、济南、上海妇科恶性肿瘤的微创专家们集聚三地，为全国同行开展了一场妇科恶性肿瘤三地手术转播活动，大家以技会友，本着“精准化”、“微创化”、“人性化”的现代手术理念，群英荟萃，各显神通，来自全国各地的专家同行400余人参会。上海交通大学医学院附属仁济医院(以下简称“仁济医院”)院长李卫平教授到会致辞，仁济医院副院长王育教授与江苏省人民医院程文俊教授共同担任上海会场手术点评。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/2888/1.png" title="" alt="demo.jpg"/></span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">仁济医院妇瘤科科主任刘开江在此次会议演示的手术为妇科腹腔镜手术中难度最大的手术术式之一：腹腔镜下子宫内膜癌再分期手术。该手术不仅要求术者在腹腔镜下对盆腔淋巴结清扫，同时还要求行达到肾血管水平的腹主动脉旁淋巴结的清扫。由于手术范围大，风险高，国内大多数医院妇科无法在腹腔镜下实施。刘开江主任在常规妇科腹腔镜手术的基础上，通过对穿刺孔、体位方面的变化，应用丰富的腹腔镜经验，娴熟的技巧，摸索出一套适合妇科医师行腹腔镜下腹主动脉旁淋巴结清扫的手术技巧。在3个小时的手术直播中，刘开江主任延续他一贯的细腻、准确的手术风格，在手术室、麻醉科以及手术团队的配合下，顺利的完成该手术。这台手术充分体现了刘开江主任的精准、规范、无血的手术风格、行云流水般的手术和令人叹为观止的高超技巧，使参会人员受益匪浅。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，在手术演示中，刘主任始终坚持“精准化、无瘤化”的理念，他再三强调：“只有手术更精准，病人才会更安全，这样才能体现‘人性化’的服务理念。”刘开江主任还以其特有的幽默、风趣、睿智的手术解说，把腹腔镜下行恶性肿瘤微创手术的无瘤原则，按照解剖、间隙手术的原则以及肿瘤治疗的微创化、人性化、规范化、个体化等理念一一介绍给三个会场的各位同道，他毫无保留的传授经验，各地同行们感到受益匪浅。此外，王平教授演示的腹腔镜下宫颈癌根治术加盆腔淋巴清扫、荣丰年教授的腹腔镜下大子宫切除也受到各地同行的一致好评。</span><br />
<br />
</span>
<p>
	<span style="font-size:14px;"><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据悉，刘开江主任近10年来带领团队完成各类腹腔镜手术近万例，包括腹腔镜下“子宫内膜癌分期手术”、“早期卵巢癌全面分期手术”、“保留生育功能宫颈癌根治术”、“保留神经的广泛子宫颈癌根治术”等高难度的恶性肿瘤手术4000余例。在他的带领下，仁济医院妇瘤科目前针对妇科达到良性肿瘤98%以上、常见恶性肿瘤90%以上的腹腔镜手术率，居全国领先。科室还紧跟国际腹腔镜微创发展趋势开展各种新术式，严格按照国际妇科恶性肿瘤诊疗规范和指南开展包括手术治疗、化学药物和放射治疗的综合治疗，并取得良好的治疗效果。</span></span>
