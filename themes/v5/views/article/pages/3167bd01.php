<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>祝贺三博脑科神外六病区荣获“北京市模范集体”称号&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近日，2015年北京市庆祝“五一”国际劳动节暨表彰劳动模范和先进工作者大会上，首都医科大学三博脑科医院神经外科六病区被授予“北京市模范集体”荣誉称号。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在闫长祥教授、林志雄教授的带领下，首都医科大学三博脑科医院神经外科六病区践行“技术、品质、服务”的经营理念。2014年完成相关手术628例，其中高难度4级手术占60%以上。神经外科六病区是卫生部国家临床重点专科(神经外科)八大亚专科之一——幕上肿瘤专业。依托该病区组建了国内第一个国际脑胶质瘤会诊中心暨北京医师协会脑胶质瘤会诊中心。该中心由国际顶级的脑胶质瘤专家组成诊疗团队，患者在国内即可享受到欧美一流水平的脑胶质瘤诊疗技术。该病区也是垂体瘤不孕不育中心，由闫长祥教授联合多家医院专家团队，主要治疗由垂体因素所造成的难治性不孕不育。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">注重医技的同时，高度重视医德医风建设。近三年来，神外六病区共收到患者感谢信件200余封，锦旗30余面，拒收红包176万余元。2014年患者满意度高达98%(第三方数据)，药费仅占全部治疗费用的16%左右。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">正如辽宁患者蔡先生说的“你们以精湛的医术、热情周到的精心服务，温暖了患者的心”。</span>
</p>
