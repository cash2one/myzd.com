			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>叶定伟教授：前列腺癌治疗的关键在“早”</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp;肿瘤患者一旦发生骨转移，代表着肿瘤已经进入晚期阶段，病人的生命以及生活质量严重下降。如果能够控制住肿瘤病人的骨转移，将能明显延长患者生命，提高生活质量。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “在前列腺癌中，有65%-75%左右的几率发生骨转移，给病人带来极大痛苦，因此骨转移在前列腺癌的治疗中非常重要。”复旦大学附属肿瘤医院副院长、泌尿外科主任、泌尿男生殖系统肿瘤多学科协作组首席专家叶定伟教授在接受新华网采访时强调了控制前列腺癌骨转移的重要性。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;前列腺癌成男性泌尿系统头号杀手</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 前列腺癌是当今欧美国家最为常见的恶性肿瘤。中国人群的发病率低于西方国家，但当下随着生活方式的改变和老龄人口的增加，我国前列腺癌的发病率迅速攀升，目前已经成为我国老年肿瘤疾病中的第五位，是男性泌尿系统发病率第一位的肿瘤，堪称男性泌尿系统肿瘤的头号杀手。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 除了人口老龄化加快增加了前列腺癌高发人群的基数外，生活方式的转变也是加剧前列腺癌的重要原因。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “现代人对高脂肪、高蛋白、高热量的‘三高’食品摄入增加，又疏于运动，增加了患前列腺癌的风险；开始性生活的时间提前、性伴侣增多，感染的几率就增加了；此外随着宣传教育的深入，人们对于前列腺癌的了解增多，增加了就诊率。”叶定伟教授分析了我国前列腺癌发病率升高的几个原因。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> &nbsp;据了解，前列腺癌多起源于前列腺的周边带，起病较为隐匿，生长较为缓慢，所以早期前列腺癌的症状不明显，一旦出现症状，往往已经遗憾地进展到晚期阶段，发生转移和扩散，这也是前列腺癌首诊便出现较高骨转移比例的原因。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 因此，早发现、早治疗是治疗前列腺癌、遏制骨转移的关键。叶定伟教授建议，50岁以上的男性每年都要定期筛查肿瘤指标，早发现、早诊断、早治疗可以有效的遏制甚至避免骨转移的发生。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;肿瘤多学科协作提高疗效</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据叶定伟教授介绍，肿瘤疾病因为其复杂性和特殊性，哪怕是同一类型的肿瘤，病人的病情都不一样，医生要根据不同病人的身体状况、肿瘤的病理特征、发病进展等进行有针对性的综合治疗，达到遏制肿瘤、改善患者生活质量的目的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “肿瘤的综合治疗需要采用多学科协作综合诊治（即MDT）的方式，这也是目前国际上被普遍认为最先进的治疗模式。通过对病人进行多学科的综合考量，制定一个相对最适合患者、最有效的治疗方案。”叶定伟教授说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着医学的不断进步，对抗肿瘤的新药物和新治疗手段不断涌现，对肿瘤患者的术后功能保护和生活质量的关注也不断增加。“今后，肿瘤的治疗将越来越重视多学科的结合，这将给病人带来更多的生存机会和更好的生活质量。”叶定伟教授说。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据叶定伟教授介绍，我国目前尚未出版肿瘤骨转移临床诊疗方面的“指南”，在没有“指南”的情况下，《恶性肿瘤骨转移及骨相关疾病临床诊疗专家共识》（以下简称“专家共识”）是目前比较具有指导意义的文件。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “新的药物、治疗手段不断出现，治疗的证据越来越多，‘专家共识’也在不断更新中”，叶定伟说，“‘专家共识’是由高端专家达成的统一治疗意见，具有指导性意义。但如何大范围的推广、宣传，让其他单位、专家了解‘专家共识’，还有很长的路要走。”</span><br />
