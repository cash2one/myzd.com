<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胸腰椎骨折的分类&nbsp;</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1单纯性契形压缩性骨折:这是脊柱前柱损伤的结果。暴力来自沿着X轴旋转的力量，使脊柱向前屈曲所致，后方的结构较少受影响，椎体通常成契形。该型骨折不损伤中柱，脊柱仍保持其尊稳定性。此类骨折一般为高空坠落伤、足、臀部着地，身体猛烈屈曲，产生了椎体前半部分压缩。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2稳定性爆破型骨折:这是脊柱前柱和中柱损伤的结果。暴力来自Y轴的轴向压缩。高空坠落伤，足臀部着地，脊柱保持正直，胸腰段脊柱的椎体受力最大，因挤压而破碎，由于不存在旋转力量，脊柱的后柱则不受影响，因而仍保留了脊柱的稳定性，但破碎的椎体与椎间盘可以突出于椎管前方，损伤了脊髓而产生神经症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3不稳定性爆破型骨折:这是前、中、后三柱同时损伤的结果。暴力来自Y轴的轴向压缩以及顺时针的旋转，可能还有沿着Z轴的旋转力量参与，使后柱亦出现断裂，由于脊柱不稳定，会出现创伤后脊柱后突和进行性神经症状。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4Chaece 骨折:为椎体水平撕裂性损伤。以往认为暴力来自沿着X轴旋转的力最大，使脊柱过伸而产生损伤，例如从高空仰面落下，着地时背部被物体阻挡，使脊柱过伸，前纵韧带断裂，椎体横形裂开，棘突互相挤压而断裂，可以发生上一节椎体向后移位。而目前亦有人认为是脊柱屈曲的后果，而屈曲轴则应在前纵韧带的前方，因此认为是脊柱受来自Y轴轴向牵拉的结果，同时还有沿着X轴旋转力量的参与，这种骨折也是不稳定性骨折。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">5屈曲-牵拉型损伤:屈曲轴在前纵韧带的后方，前柱部分因压缩力量而损伤，而中、后柱则因牵拉的张力力量而损伤，而中、后柱则因牵拉的张力力量损伤，中柱部分损伤表现为脊椎关节囊破裂，关节突脱位，半脱位或骨折，这种损伤往往还有来自Y轴旋转力量的参与，因此这类损伤往往是潜在性不稳定型骨折，原因是黄韧带，棘间韧带和棘上韧带都有撕裂。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">6脊柱骨折-脱位:又名移动性损伤。暴力来自Z轴，例如车祸时暴力直接来自背部后方的撞击，或弯腰工作时，重物高空坠落直接打击背部，在强大暴力作用下，椎管的对线对位已经完全破破坏，在损伤平面，椎沿横面产生移位，通常三个柱均毁于剪力，损伤平面通常通过椎间盘、同时还有旋转力量的参与，因此脱位程度重于骨折，当关节突完全脱位时，下关节突移至下一节脊椎骨上关节突的前方，互相阻挡，称关节突交锁，这类损伤极为严重的脊椎损伤难免，预后差。 另外还有一些单纯性附件骨折如椎板骨折与黄突骨折，不会沉重脊椎的不稳定，称为稳定型骨折，特别是横突骨折，往往是背部受到撞击后腰部肌肉猛烈收缩而产生的撕脱性骨折 。</span>
</p>
