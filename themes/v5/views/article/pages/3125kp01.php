			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>胃肠道神经内分泌肿瘤有哪些肿瘤标志物？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胃肠道神经内分泌肿瘤，包括发生在胃、小肠、直肠、阑尾等部位的神经内分泌肿瘤。根据最新的WHO 2010版分类，胃肠道神经内分泌肿瘤分为高分化的神经内分泌瘤（包括G1和G2）和低分化的神经内分泌癌（G3）。胃肠道高分化的神经内分泌瘤（G1），也叫类癌，恶性度较低。类癌患者出现的内分泌激素相关症状叫类癌综合征，典型的类癌综合征包括发作性皮肤潮红、腹泻、腹痛、喘鸣、心瓣膜病变等。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">胃肠道神经内分泌肿瘤比较少见，常用的血液肿瘤标志物CEA、CA125、CA199往往不高，针对神经内分泌肿瘤的血液标志物叫CgA（嗜铬粒蛋白A），可以用于胃肠胰腺神经内分泌肿瘤的筛查、诊断和病情监测。胃神经内分泌肿瘤的患者还应该检测血清胃泌素，对于分型诊断及病情监测都有意义。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5-HIAA（5-羟吲哚乙酸）是5-羟色胺的代谢产物，从尿中排出。神经内分泌肿瘤伴类癌综合征时，尿5-HIAA水平明显升高。检测24小时尿5-HIAA，可用于类癌综合征病情的监测及治疗疗效的评估。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">因此，胃肠道神经内分泌肿瘤复查时，除了化验血CEA外，还应该化验血清CgA和尿5-HIAA。胃神经内分泌肿瘤患者在治疗前应先检测血清胃泌素，以便对其进行分型（1型、2型和3型）。胃泌素水平正常者属于3型，恶性度较高，治疗策略与1型2型不一样。</span><br />
<p>
