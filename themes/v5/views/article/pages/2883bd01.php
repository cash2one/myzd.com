			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>外科治疗癫痫有了新选择</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在6月28号第九个“国际癫痫关爱日”之际，上海交通大学医学院附属仁济医院功能神经科主任徐纪文教授向我们讲述了外科手术治疗癫痫的最新进展，立体定向脑电图。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">徐纪文说，随着现代医学的进步，除了通过服用单一抗癫痫药物控制发作外，外科手术也成为治疗癫痫的一种重要手段。外科手段治疗癫痫大体上可分为两类：切除性手术和辅助性手术。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当引起癫痫发作的病灶很局限并能够明确定位，且不在脑的功能区时，可以选择进行切除性手术。将异常放电的病灶切除，患者很可能就痊愈了。最新研究结果显示，颞叶内侧癫痫手术完全控制率超过70%，是最适合手术的一种癫痫。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当病灶多发或无法确定时可以进行辅助性手术，如迷走神经刺激术。这种治疗目前约有10%的难治性癫痫患者可达到完全控制，近一半患者发作次数可减少90%以上。值得一提的是，这种疗法在能提高患者的生活质量，使患者的警觉性、语言技巧、思维能力、独立性等方面都有提高。迷走神经刺激术(VNS)的手术无需开颅，安全性高，目前在国内外儿童难治性癫痫患者中的应用越来越广泛，对儿童的记忆力、情智改善尤为明显。不但可以用于药物无效的患者，手术后无效或复发者也适合进行此手术治疗。目前，在仁济医院功能神经科徐纪文主任、瑞金医院功能神经外科孙伯民主任和华山医院静安中心神经外科胡杰主任能够为患者进行这种治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“但是，相当一部分患者、家属、甚至是医生，对手术心存疑虑和恐惧，宁可历经多年反复更换或添加药物进行治疗尝试，或企图寻找神医、秘方等来达到目的也不愿接受手术治疗”，对此，徐纪文深表无奈，他强调，癫痫患者中有20%-30%患者为药物难治性癫痫，是需要借助外科手术来达到治愈或改善的目的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医疗技术在不断提高，抗癫痫药物和手术手段治疗癫痫的疗效也在不断提高，但仍有相当一部分患者接受这些治疗后效果不佳，深受病痛困扰。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据徐纪文教授介绍，立体定向脑电图（SEEG）是近年来国际上兴起的一种新的癫痫病灶查找方法，该方法可以进一步明确癫痫病灶的部位，到达准确切除病灶、治愈患者的作用。立体定向脑电图能帮助人们在手术前确定这一看不见、摸不着的所在。通过精细化的微创手术，精确定位切除癫痫灶、完整保护功能区以及术后抗癫痫药物的有效合理应用，难治性癫痫患者可以获得比较满意的疗效</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">徐纪文教授强调，癫痫是神经系统的一种慢性疾患，治疗的周期相对较长，需要患者和家属的密切配合，克服急躁心理，才能取得最佳效果。</span><br />
