			<p>
	<div style="text-align:center;">
		<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>先天性后鼻孔闭锁如何治疗</strong></span>
	</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">行后鼻孔闭锁成形术，是其根治性的有效方法。用手术方法去除闭锁间隔，有经鼻腔、经腭、经鼻中隔、经上颌窦4种途径，应根据患儿年龄、症状程度、间隔性质与厚度，以及全身情况而定。为了安全，可以先做气管切开术。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.鼻腔进路 适用于鼻腔够宽，能够看到闭锁间隔者，膜性间隔或骨性间隔较薄者，新生儿或患儿全身情况较差而急需恢复复鼻呼吸者。&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）麻醉 儿童用全身麻醉，成人用局部表面麻醉。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）切口 左侧鼻腔间隔做“[”形切口，右侧鼻腔做“]”形切口，分离粘膜，露出骨面。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）切除间隔 用骨凿、刮匙或电钻头去除骨隔，保留骨隔后面（咽侧）粘膜，以覆盖外侧骨创面。术中须切除鼻中隔后端，以便两侧造孔相贯通。造孔大小以能通过食指为度。然 后放入相应大小的橡皮管或塑料管，或以气囊压迫固定，留置时间视间隔性质而定，膜性间隔两周即可，骨性间隔则须4～6周。为了防止再次狭窄，可于一年内定 期进行扩张术。此种手术若在鼻内窥镜下进行则更方便。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;对新生儿可用小号乳突刮匙沿鼻底刮除，在骨隔处用旋转刮除法去除骨隔至足够大小，后面粘膜仍须保留，可行十字形切口，用橡皮管自鼻咽逆行拉出，以固定粘膜瓣于骨面上。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">采用鼻腔进路，在术中需注意避免损伤腭降动脉、颅底及颈椎。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.经腭进路 优点是手术野暴露良好，可直接看到病变部位，能将间隔彻底切除，并可充分利用粘膜覆盖创面，适用于闭锁间隔较厚者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）体位及麻醉 患儿仰卧，头向后伸，用0.1%肾上腺素棉片塞于鼻腔深部闭锁间隔前壁，再于硬软腭交界处注入少量含肾上腺素的1%普鲁卡因，以减少术中出血，经气管切开给全身麻醉。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）切口 做Owens硬腭半圆形切口，切开粘膜，切口两端向后达上颌粗隆。分离粘骨膜瓣至硬腭边缘。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）硬腭后缘显露后，用粗丝线穿过已游离的粘骨膜瓣，以便向后牵引。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）去除闭锁间隔 分离硬腭后面（鼻底面）的鼻底粘膜，用咬骨钳去除患侧腭骨后缘部分骨壁，即可发现骨隔斜向蝶骨体，分离骨隔后面粘膜，凿除骨隔，然后再于梨骨后缘按鼻中隔 粘骨膜下切除的方法去除的方法去除一部分梨骨，使后鼻孔尽量扩大，保证通畅。骨隔前后和鼻中隔后端粘膜可以用以覆盖骨面。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（5）缝合切口 将硬腭切口的粘骨膜瓣翻回复位，用细丝线严密缝合，其手方接近软腭处若有撕裂，也应严密妥善缝合，以免术后穿孔。最后经前鼻孔置入橡皮管或塑料管，固定修 整后的鼻内粘膜，4周后取出橡皮管，预约定期随访。若有后鼻孔术后粘连，应及时处理，必要时可进行扩张。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.经鼻中隔进路 此法仅适用于治疗成人后鼻孔闭锁。单侧、双侧、膜性、骨性，皆可使用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（1）体位和麻醉 同鼻中隔粘骨膜下切除术。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（2）切口 用killan切口，或稍偏后做切口。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（3）剥离粘骨膜 范围要尽量扩大，特别是向上、向下剥离的范围要大，可包括双侧鼻底粘膜，以便向后扩大视野。 &nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（4）切开鼻中隔软骨，剥离对侧鼻中职粘骨膜，范围要尽量扩大。剥离到后方时，可将鼻中隔软骨和筛骨垂直板去除一部分，发现骨隔时用骨凿去除，直到能看到蝶窦前壁为止。最后经前鼻孔插入橡皮管或塑料管，预防后鼻孔粘连。必要时术后定期扩张。</span>
</p>
<p>
	<span><span style="font-size:14px;line-height:28px;"><br />
</span></span><span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;4.经上颌窦进路 此法仅适用于成人单侧后鼻孔闭锁，是利用de Lima手术，自上颌窦开放后组筛窦，达到后鼻孔区，进行闭锁间隔切除。</span><br />
