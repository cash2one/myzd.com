<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">10月16日下午16:00，大渝健康微访谈特意策划了一期与乳腺疾病有关的话题：乳腺增生到乳腺癌到底有多远？请来了重庆医科大学附属第一医院乳腺外科主任厉红元教授做客大渝网，就女性乳腺健康问题进行交流，300多条的互动评论让厉教授惊叹，我们必须警惕乳腺癌这个女性第一大杀手给众多女性所带来的危害。&nbsp;</span>
