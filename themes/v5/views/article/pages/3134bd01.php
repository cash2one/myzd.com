<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">早发现早治疗，4个检查有效防范乳腺癌</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.钼靶X线摄片适合40岁以上女性：乳腺照相是乳腺癌诊断的常用方法，特别是乳腺有既往良性病变、长时间服用雌激素的人群。检查机构：医院、体检中心。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2.乳腺B超适合40岁以下年轻女性：乳腺B超无损伤性，对明确肿块大小常较正确，特别是20多岁的女性，乳腺仍在发育中，射线可能促使正常的细胞发生癌变，所以 40岁以下的年轻女性更适合用B超筛查乳腺癌。检查机构：医院、体检中心。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3.细胞学及组织学诊断用于乳腺癌最终诊断：细针穿刺抽吸细胞学检查（FNAB）正日益被广泛采用，其操作安全，诊断迅速。检查机构：医院。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4.乳腺癌基因检测非常重要：基因检测是一种最前沿的检测方法，可以在人们没有患病的情况下，了解有无基因缺陷和患病风险。通过BRCA1和BRCA2基因的基因突变检查，及时掌握罹患乳腺癌、卵巢癌的风险。检查机构：权威基因检测机构，体检机构也是送往基因检测机构，不要选择普通的基因检测机构，保证检测准确度。</span><br />
