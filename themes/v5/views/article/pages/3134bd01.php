<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">什么问题最火？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">NO1.乳腺增生有哪些症状，如何有效防治乳腺增生？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">厉教授解答：月经前乳房胀痛，可能会有肿块，月经后疼痛减轻或消失，肿块变小。如果有一个肿块，一定要到医院看医生做超声和钼靶检查。平时可以自己在淋浴时候自我检查，具体到重医附一院内分泌乳腺外科门诊去领取如何自检的手册免费提供。或者10月31号下午在重医附一院学术报告厅有专题讲座，下午14点30，报名电话：89011122</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">NO2.乳腺增生到乳癌有多远，纤维瘤到乳癌又有多远？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">厉教授解答：乳腺增生是指乳腺上皮和纤维组织增生，乳腺组织导管和小叶在结构上的退行性病变及进行性结缔组织的生长，一般单纯乳腺增生不会变为乳腺癌，只有非典型性导管上皮增生是可以变为乳腺癌，纤维肌瘤变为乳腺癌的机率极低。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">NO3.乳房发现有硬块，还伴有胀痛是怎么回事？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">厉教授解答：当发现乳房有硬块或肿块时，不一定代表患乳癌，但是，乳房硬块往往是乳房疾病的征兆，一旦出现乳房硬块，不论其大小及性状，都应立即就医检查。当然，这种情况与女性的生理周期变化也有关，平时注意安排好生活节奏，调节心情，忌酒和少吃辛辣食物，如果疼痛不严重，不需要吃药和其他治疗，每年定期到医院检查。</span>
</p>
