			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>眼耳鼻喉科医院戴春富教授出席第30届Politzer学会会议并发言</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近日，第30届Politzer学会会议在日本新冩召开。来自世界各地的耳神经颅底外科医师近八百余人参加了本次会议。眼耳鼻喉科医院戴春富教授作为本次会议的国际组织委员会委员（中国唯一代表），应邀作了30分钟题为《累及颈静脉孔区肿瘤的手术处理》的大会发言，介绍了眼耳鼻喉科医院开展颈静脉孔区肿瘤手术的经验及手术效果。另外，由戴春富教授主持的颞骨恶性肿瘤圆桌会议也得到Polizter学会主席Angel Romos-Macias的高度评价。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3004/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">Politzer学会以“现代耳科之父”Politzer医师命名，由最权威的国际耳神经颅底外科学会组织。本次会议有大会发言16个、指导课程6个、圆桌讨论28个、论文发言60个，内容包括中耳炎、中耳胆脂瘤、耳硬化的手术处理、感音神经性听力下降、侧颅底肿瘤的干预策略等。会议重点强调乳突黏膜的保留对中耳气体交换的作用以及耳窥镜下中耳微创手术；再次强调影像学的进展对中内耳疾病诊断及治疗革命性影响；首次介绍了类似上半规管裂的耳蜗骨裂的临床表现和处理；关注眩晕患者和焦虑情绪障碍的相互作用及对治疗的影响；在侧颅底手术方面，强调在完全切除肿瘤的同时，需尽量保留相应颅神经功能（听功能、面神经功能和后组颅神经功能）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3004/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">戴春富教授作为圆桌会议“颞骨恶性肿瘤处理新理念”的主持人，邀请了在该领域中负有盛名的专家参与其中。来自英国剑桥大学、法国巴黎南部医学院、西班牙阿斯图里亚斯中央大学附属医院的同行分享了各自的经验，分别介绍颞骨鳞癌、腺样囊性癌、内淋巴囊肿瘤的临床处理策略，颞骨肿瘤切除后的功能重建和缺损的修复。同时对该领域中的争议热点进行讨论，发表各自的观点。颞骨恶性肿瘤圆桌会议使与会者受益匪浅。Polizter学会主席Angel Romos-Macias不仅给予高度评价，还邀请戴春富教授继续参与第31届Polizter学会会议。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，眼耳鼻喉科医院侧颅底肿瘤手术的数量和质量在国际处于领先水平。通过积极参与并在国际平台展示相关成果，得到国际同行的认可。戴春富教授还担任Journal of Neurological Surgery: Skull Base和Otology &amp;Neurotology这两本国际著名刊物的编委。</span>
</p>
