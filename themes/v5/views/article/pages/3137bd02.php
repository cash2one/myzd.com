<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>上海骨科专家今天为灾区“准新郎”骨科手术</strong></span>
</div>
<br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">重病期间女友陪伴 “病房婚礼”如期举行</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">晚报讯 今天（4月27日），作为国家卫计委增派的上海骨科专家组，将在四川省人民医院为一位“特殊”的病人进行手术。下周二（4月30日），这位术后的“准新郎”，将与最近一直坚持在病房里日夜陪伴他的女友举行一场特殊的“病房婚礼”。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">准新郎原定今年4月30日结婚，但不幸的是，在雅安地震发生后，他的双前臂骨折，且伴有锁骨骨折，伤情比较严重。在转入四川省人民医院后，他的女友一直陪伴在病床边。当医院得知小伙和女孩的情况后，为了使小伙如愿以偿，院方计划于4月30日在医院的病房里，为这对新人举办一场特殊的“病房婚礼”。经过检查，考虑到患者的前臂比较肿胀，前几天还暂时不适宜手术，因此，专家组先制定了“先行抬高患肢、消肿”等治疗手段。但为了婚礼能顺利如其地举行，今天，来自上海第六人民医院和第九人民医院的骨科专家组，将为准新郎尽早进行手术。根据患者的恢复情况，专家组计划在今天为小伙实施手术，确保在4月30日前为准新郎完成手术内固定，使婚礼能够如期地举行。据第九人民医院骨科副主任医师龚伟华预计，术后患者将于一周左右即可出院。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在重症伤员中，一位20岁的小伙在所住的三层小楼房屋中，被巨石击中倒塌砸伤，父母和邻居20多人用双手将他扒出，并及时将他送往当地医院，不仅接受了脾脏切除手术，而且患者还是罕见的RH阴性血，又伴有重度颅脑损伤、脾破裂、全身九处骨折，病情危重，重度昏迷，使用呼吸机维持呼吸。在震后的108个小时的连续抢救中，坚强的小伙一直坚持与“死神”赛跑！这位内心坚强的伤员转入四川省人民医院重症监护室病房抢救，上海第六人民医院孙玉强主任医师听取了重症监护室医生的病情介绍，还查看了病人，认为目前主要是抢救生命，尽快苏醒，还对伤员全身多处的骨折给予了颈托、石膏等固定。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">记者了解到，随着从地震重灾区转入医院的伤员数量不断增加，为了提高工作效率，专家们还将病人按照“手术后”、“拟手术”、“新收治”这三大类病人的不同侧重点，针对性地制定了治疗方案。据悉，从昨天起，四川省人民医院已经挪出四间手术室，可以供上海的骨科专家同时使用，方便尽早地为更多的患者及时进行诸如股骨骨折、跟骨骨折等不同的多部位骨科手术。&nbsp;</span>
</p>
