		<p style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>新技术治疗脑胶质瘤 生存率明显提高</strong></span>
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">新华网湖南频道8月1日电（彭 萍、任年军）近日，湖南省肿瘤医院神经外科采用将化疗药物与高分子载体制作成植入剂，直接作用于脑胶质瘤，取得良好治疗效果。目前，这项技术在湖南仅有该院成功开展。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 51岁的谭大爷来自永州东安，两年前因恶性胶质瘤在外地医院做了胶质瘤切除术，术后做了一段时间放疗、化疗。今年3月17日，他因反应迟钝、记忆力下降3个多月来到湖南省肿瘤医院，入院检查时神志清楚，但部分运动性失语，反应迟钝，记忆力、定向力、计算力均下降，被确诊为胶质瘤术后复发。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 4月1日，神经外科专家为他在全麻插管下做了开颅探查左颞枕叶复发胶质瘤切除术。术中，医院将其病灶分块切除后，植入卡莫司汀缓释植入剂180mg。经过对症治疗后，谭大爷恢复顺利，出院时患者神志清楚，精神、食欲、睡眠等都已正常，无发热、呕吐，生活能自理。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 据神经外科何正文主任介绍，脑胶质瘤是颅内发生率最高的恶性肿瘤，占45％左右。由于其生物学特点，手术难以彻底切除，特别是恶性脑胶质瘤单纯手术效果不佳。目前主张手术辅以放化疗的综合治疗。虽然在近几十年中显微神经外科学、神经影像学、放射治疗、化学治疗等均取得了长足的进步，但胶质瘤病人的预后仍没有明显的改观，主要是由于血脑屏障的存在及药物的毒副作用，胶质瘤对全身化疗反应很差。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 该院神经外科不断探索脑胶质瘤诊断与治疗的规范化和个体化，并紧跟世界医学发展前沿，采用目前世界上治疗脑胶质瘤 特别是复发性恶性脑胶质瘤的最新技术，即将治疗脑胶质瘤的化疗药物与一种生物相容性良好、可降解吸收的高分子载体结合，做成缓释植入剂直接作用于肿瘤局部，使化疗药物绕过血脑屏障作用于肿瘤，保证局部较为持久、稳定药物浓度，同时减轻全身毒副作用，改善化疗效果，提高病人生存质量，延长生存期。 湖南省肿瘤医院这项新技术的开展使湖南省脑胶质瘤的综合治疗达到了国内先进水平，该项技术全面提高了肿瘤的局控率和生存率，70%以上的复发恶性脑胶质瘤患者均可利用该技术而获益。（完）</span><br />
