	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>武汉同济医院采用人工椎间盘置换手术治疗颈椎病</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">武汉同济医院日前为患有颈椎病的王先生实施了人工椎间盘置换手术，这是湖北省首例人工椎间盘置换手术。 　　今年４０岁的王先生由于长期低头伏案工作，近来经常觉得颈、肩背、前胸疼痛，手臂麻木，在武汉同济医院被确诊为脊髓型颈椎病。７日上午，同济医院骨科李锋教授主刀为他做了人工椎间盘置换手术。术后第二天，王先生就能下床活动了。 　　与传统手术治疗方法相比，人工椎间盘置换手术更符合人体生理功能。传统手术是将病变的椎间盘和增生的骨赘切除，然后植入患者自体骨或填充有自体骨的融合器。但这会使椎间盘作为“关节”的功能丧失，影响颈部正常活动，而且，取自体骨会给患者增加痛苦，并有可能引发后遗症。同济人工椎间盘置换术，不仅能保证术后颈部正常活动，而且患者恢复快，痛苦小。 　　颈椎是连接人体头部与躯干的支撑部位。李锋教授介绍说，颈椎病是由于颈椎间盘退行性变、颈椎骨质增生所引起的一种综合症。主要症状为头、颈、臂、手及前胸等部位的疼痛，重者可致肢体软弱无力，甚至大小便失禁、瘫痪。 　　李锋说，颈椎病多发于４０至６０岁间的成年人，５０岁左右的人群中有２５％的人患过或正患此病。其发病率随年龄增长而增多，男性多于女性，近年来还有年轻化的趋势。他建议，长期伏案工作者，应每隔１－２小时有目的地让头颈部向左右转动数次，向远方眺望半分钟左右，这既可消除疲劳感，又有利于颈椎保健。</span><br />
