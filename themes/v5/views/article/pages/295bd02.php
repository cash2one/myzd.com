			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>乳腺癌治疗同时别忘了骨健康管理</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 每年10月为“国际乳腺癌宣传月”，旨在提高人们对乳腺癌的关注与认识，早预防、早发现、早治疗。随着对乳腺癌临床研究的深入，综合手段治疗已成为临床中治疗乳腺癌的主要理念。为了防止乳腺癌复发、转移，手术后的辅助治疗显得非常重要，其中内分泌治疗就是乳腺癌辅助治疗的方法之一。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 所谓乳腺癌内分泌治疗，就是通过切断雌激素的来源或抵消雌激素的作用来阻止或延缓乳腺肿瘤细胞的生长。而由于雌激素在身体内一个重要任务就是阻止骨骼中钙质流失，因此，乳腺癌内分泌治疗在抑制肿瘤生长的同时，也势必进一步加速乳腺癌患者体内钙质的流失速度，意味着增加了患者骨质疏松和骨质疏松性骨折（脆性骨折）发生的风险。</span><br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 上海交通大学医学院附属仁济医院乳腺中心主任陆劲松教授认为：“近些年，乳腺癌内分泌治疗在临床广泛应用，尤其是芳香化酶抑制剂（AI）的使用，改善了患者的生存比例，延长乳腺癌患者的生存时长。与此同时，乳腺癌患者骨丢失、骨质疏松、骨折等骨骼健康问题也应该给予足够的重视。因此，乳腺癌患者在接受内分泌治疗的同时，应采取一定的骨安全管理措施，两者‘双管齐下’，以维护患者的骨骼健康。”</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 临床上，根据患者骨丢失和骨质疏松风险分级，推荐采取补充钙剂和维生素D、使用双膦酸盐治疗等相应的预防和处理方法。此外，需要特别指出的是，在预防乳腺癌患者骨丢失和骨质疏松时，合理的选择AI药物也非常重要，有助于降低和预防骨相关事件的发生。陆劲松教授强调道：“总体而言，甾体类AI 的早期骨安全性较好。骨丢失和骨质疏松风险评估为高度或中度危险，并伴有多个危险因素的早期绝经后乳腺癌患者，可考虑选择对骨丢失影响较小的甾体类AI进行治疗。”</span><br />
