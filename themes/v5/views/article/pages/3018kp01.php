		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>降低肺癌死亡率重在早诊早治（科普）</strong></span>
</p>
<p>
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">目前我国肺癌的发病率及死亡率均居恶性肿瘤之首，已成为世界第一肺癌大国。预计到2025年，我国肺癌患者将突破100万。有分析称，在未来30年，我国因肺癌致死的入数将高达1800万，这意味着每分钟就有1入死于肺癌。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">受人口老龄化、环境恶化、烟草以及新病源与耐药病源等诸多因素影响，全球的肺癌、食管癌等胸部肿瘤以及肺气肿、肺纤维化等呼吸系统良性疾病发病率、病死率持续增加，成为全球最大的公共卫生问题之一。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">中国工程院院士、中日医院院长王辰教授指出，肺脏是三大开放脏器之一，且首当其冲接受外在挑战，呼吸疾病发病率、致残率与死亡率高，病患及家庭负担重，未来呼吸疾病发病形式将更加严峻，在这样的前提下，呼吸相关的临床学科应更注重相互交融，建立多学科呼吸疾病防治体系，加强联动抵御呼吸系统疾病的能力建设。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">近年来我国肺癌的发病呈现出年轻化、女性化的趋势。中日医院胸外科主任、大外科主任刘德若介绍，以前，我们将肺癌的高危入群年龄锁定在65N70岁以上，认为它是一种“老年病”。但现在数据显示，我国肺癌的发病和死亡年龄从40岁开始迅速上</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">升，70岁达到高峰，其中45N65岁患者占75%，且发病年龄平均每5年降低一岁。从临床观察来看，现在四五十岁的肺癌患者比过去明显增多，过去40岁以下的肺癌患者很少见，现在却屡见不鲜，而且不抽烟的女性肺癌患者也越来越多。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">早诊可提高肺癌治愈率</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">本报记者田晶</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">引人精准医疗观念、利用大数据技术提高胸部疾病诊疗效果、规范胸外科微创医疗行为、提高肺移植应用水平、发展胸外科移动医疗、建立我国胸外科数据平台、组织多中心研究、开展国际交流，培养基层胸外科医师、为广大患者提供胸外科良恶性疾病科普服务。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">肺癌在全世界范围都是发病率比较高的一种癌症，但是我国与发达国家相比最大的差距在于，欧美等发达国家在肺癌的治愈率上达到60%B70%，而中国只有30%左右。主要原因在于我国早期诊断、早期治疗的筛查体系还不健全，导致肺癌的发病率以及死亡率一直以来都位居癌症榜</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">首，肺癌防治问题尤为突出。为此，世界肺癌联盟将每年的11月定为全球肺癌关注月，让更多的入关注肺部健康。</span>
