			<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>椎管内肿瘤会出现哪些症状？</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">椎管内肿瘤往往并不像甲状腺结节、乳腺结节那样广为人们所知，但是椎管内肿瘤若未能及时发现、及时治疗，带来的危害是非常严重的。那么，椎管内肿瘤都有哪些典型的症状呢？</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">①疼痛</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">由于椎管内位置有限，随着肿瘤渐渐长大，就会压迫脊髓、神经和血管，导致患者出现一系列症状。其中最常出现的症状就是疼痛，尤其是神经鞘瘤引起的神经根痛最为典型，这种疼痛沿神经根分布区域扩散。神经根主要是脊髓与周围神经的连接部位（如下图），传出、传入的神经冲动都要经过神经根。神经根痛主要是由于肿瘤占位，使得感觉神经不断受到刺激，从而出现感觉过敏，这种过度的神经冲动就会给人体带来一种刺痛的感觉，而且是沿着神经根的分布范围而产生的疼痛。一般来说，如果脊髓外的肿瘤直接压迫神经根或者周围的神经，更容易出现神经根痛；若肿瘤长在脊髓里，神经根痛的症状反倒不明显。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><img src="<?php echo $baseUrlImage; ?>article/doctor/2973/1.png" title="" alt="demo.jpg"/>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">神经根（图片来源于网络）</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">不仅如此，有的患者还会感觉到酸痛或烧灼痛，也可出现各种感觉异常，如麻木感、好像有蚂蚁在后背幽幽地乱爬，后背被束缚的感觉，后背凉飕飕的，等等。有的患者甚至感觉不到疼痛、温度，或者触觉减退甚至消失。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">然而就有人提出疑问了，这些症状跟腰椎病其实挺像的，怎么区分呢？其实，腰椎病导致有一个比较典型的表现就是间歇性的跛行，也就是患者站的太久或者走路太多，疼痛的症状会加重，躺下休息就会缓解。而对于椎管内肿瘤导致的疼痛，往往是静卧休息时明显，活动之后反倒减轻了。但是，单单通过这些症状很难确定是什么疾病，往往还是需要临床医生专业的查体以及影像学的检查。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">②压迫症状</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">有的先天性椎管内肿瘤压迫到神经，还会导致大小便功能障碍，比如便秘，尿频、尿无力、尿潴留等；或者运动障碍的表现，比如肢体僵硬、无力、活动不便、肌肉萎缩和肌束颤动等，甚至随着个体的生长发育，出现双下肢的畸形。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">③脊柱畸形</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">虽然椎管内肿瘤和脊柱肿瘤生长位置不一样，但是别以为椎管内肿瘤就一定不会造成脊柱畸形。有的椎管内恶性肿瘤，生长速度非常快，也能从椎管内突出来，累积脊柱。如果严重的累及到关节突就容易引起脊柱畸形，但这种情况比较少。往往是肿瘤比较大，足以破坏脊柱的稳定性时，才会造成脊柱畸形，一般来说椎管内肿瘤导致脊柱畸形的情况非常少见。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">然而，有些先天性椎管内肿瘤的患儿，在生长发育过程中，不仅椎管内长了肿瘤，同时还伴有脊柱的发育畸形，这就是肿瘤与脊柱畸形共存的一种情况。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">④脊髓栓系</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">有的椎管内肿瘤患者在检查过程中，医生还会说有脊髓栓系。那么这是怎么回事？说起脊髓栓系，非常复杂，读者可以先参考尚爱加大夫文章《脊髓栓系综合征是怎么形成的？》，先了解一下脊髓栓系是怎么回事。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">值得注意的是，椎管内肿瘤也会导致脊髓栓系，特别是先天性的肿瘤，在胚胎生长发育过程中就逐渐形成了。随着脊髓脊柱的发育，在腰骶椎附近的椎管内肿瘤就会牵拉到脊髓下端的马尾神经，造成栓系。这种就是继发形成的栓系，与原发性的脊髓栓系有所不同。如果是原发性的脊髓栓系，孩子出生后，随着生长发育，会逐渐出现大小便障碍、双下肢发育异常等问题与肿瘤大小无关。而对于椎管内肿瘤导致的继发性脊髓栓系，往往出现症状较晚，一般是肿瘤长得足够大，足以牵拉到脊髓时，才会出现明显的症状。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">有的先天性椎管内肿瘤患者，同时伴有脊髓栓系。这种情况下，肿瘤和脊髓栓系没有直接的联系。</span>
</p>
