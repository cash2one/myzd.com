			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>“大人物”爱中招前列腺癌 它是不杀人的杀手</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">今年2月份，63岁的新加坡总理李显龙因患前列腺癌接受了机器人手术治疗，仅仅在三个月前，新加坡前任总理吴作栋同样也是因为确诊早期前列腺癌，接受了机器人手术治疗。新加坡前后两任总理先后确诊前列腺癌这个事实也许是个巧合，但这也从侧面反映了前列腺癌发病率高这个事实。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 专家指出，因为公众以及一些医务人员对前列腺癌的认知缺乏，前列腺癌的治疗还存在一定的过度治疗问题。对于早期前列腺癌，理论上是能够通过根治性手术治愈的。但相比其他一些恶性肿瘤，前列腺癌自然病程较长，他建议低危、高龄患者可实施“等待观察治疗”，不着急手术，先密切监测。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “大人物”爱中招?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前列腺作为男性最大的附属性腺，虽只有栗子样大小，但对人体的作用不容小觑。可恰恰是这弹丸之地的“叛变”——前列腺癌，成为了老年男性的“头号杀手”。但因对此癌种的关注和认知度不够，我国前列腺癌的5年生存率仅有53%，与欧美等国90%以上生存率的状况相距甚远。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 随着我国人口老龄化、生活方式改变等，使我国前列腺癌发病率急剧上升，已跃居男性恶性肿瘤的第六位。中山大学附属第一医院泌尿外科主任丘少鹏教授介绍，北、上、广等发达城市的调查数据显示，近些年这些地区的前列腺癌发病率以7%-8%的速度在增长，近几年前列腺癌已发展成为男性最常见的泌尿外科肿瘤。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 另外，其实，还有很多广为人知的“大人物”都曾患前列腺癌，这里面包括“资本大鳄”巴菲特、“传媒巨头”默多克、美国前国务卿鲍威尔将军、湾著名作家李敖等。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前列腺癌是不杀人的杀手?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据悉，前列腺癌虽然也是癌症，但它同其他的癌症相比有很大的区别。一般来说，前列腺癌是一种相对惰性的肿瘤，进展缓慢，多数患者的死亡原因并非该病所致。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 曾经发表在权威医学杂志《新英格兰医学》的一项研究论文提出，尸检结果显示，大于50岁的死者中有30%患有隐匿性前列腺癌;这个比率在70岁以上的死者中升高到了70%。可见，尽管前列腺癌非常常见，但多数情况下似乎并不影响患者的自然寿命。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前列腺癌还像一个“馋”嘴的孩子，在它发生、发展过程中的大部分时间内，它只“爱吃”一种东西，就是雄激素，一旦把雄激素阻断了，就像给前列腺癌断了“粮草”，癌细胞就会纷纷“饿死”，在临床上能够产生很好的治疗效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前列腺癌还有一个“顽固”的特性，是指虽然前列腺癌具有很高的雄激素依赖性，但是当对其进行激素治疗时，由于机体内肿瘤细胞“爱吃”的粮草(雄激素)没有了，癌细胞就会逐渐适应新的环境，慢慢地转变为不依赖雄激素也能生长的肿瘤。一旦前列腺癌发展到这个阶段，目前的医疗技术就很难控制它的生长了。也正是因为如此，前列腺癌患者才应该牢牢地把握住根治手术的时机，及时随访和治疗，否则前列腺癌迟早会进入这种“顽疾”的状态，直至危及患者的生命。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 预期寿命达十年患者可考虑手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 谈到前列腺癌的治疗，对于早期前列腺癌，理论上是能够通过根治性手术治愈的。但相比其他一些恶性肿瘤，前列腺癌自然病程较长、较缓慢。鉴于此等因素，如果患者预期寿命较短，手术的治疗意义不太大，如果预期寿命达到十年的患者，考虑做根治性手术则更合适。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 丘少鹏还谈到，《前列腺癌治疗指南》中提出，对低危前列腺癌和预期寿命短的患者，建议实施“等待观察治疗”，主动监测前列腺癌的进程，在出现肿瘤进展或临床症状明显时给予其它治疗。患病早期的患者便属于低危情况。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 对于晚期的前列腺癌患者，医学证明已经没有彻底治愈的可能了。但是根据前列腺癌“馋”的特点，临床上可以通过睾丸切除的手术，还可以应用打针、吃药的方法达到去除雄激素的效果，进而有效地治疗前列腺患者。当癌细胞慢慢地转变为不依赖雄激素也能生长的肿瘤，也并非无药可治，包括放疗、同位素治疗、化疗以及中医中药治疗等，还是能够比较好地缓解前列腺癌患者的症状。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 前列腺癌自然病程长，不管是前期的根治性治疗还是晚期的辅助性治疗之后，前列腺癌患者都需要定期随访，对病情进行监测、评估，定期监测前列腺特异抗原 (PSA)，以判断病情是稳定状态还是发展状态，然后针对情况对治疗方法进行及时调整。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 机器人手术可避免性神经损伤</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 上述新加坡两位领导人接受的机器人手术，在近年医学界十分热门。该手术机器人是目前世界上最先进的外科手术机器人。专家表示，机器人手术尤其适合泌尿外科。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 很多接受前列腺切除术的病人，承受了很大的痛苦，因为这台手术极其容易伤到紧贴着前列腺后外侧的性神经，很多病人术前，就做好了术后丧失性功能的准备。没有性神经，不光是性功可能丧失，性神经也是控制排尿的神经，性神经保留得差，也会影响排尿。而达芬奇机器人大大发挥了优势，由于可放大十倍视野，机械手上有稳定器，尤其是在狭窄解剖区域中，机器手比人手更稳定，可避免伤及一条无辜的血管、神经，几乎没有出血。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是，机器人手术也存在其天生的缺点，比如说在力学方面，无法用手感受脏器组织的弹性，对组织的钳夹力度无法控制，其次光源有局限性，有光线的地方才能实施手术。还有，机器人手术价格较昂贵，香港地区，一台这样的手术花费大概是8万元人民币左右，比一般普通的腹腔镜微创手术花费要多。</span>
</p>
