<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在我国心脏瓣膜病是一种非常普遍的心脏疾患，而我们的生活中患有心脏瓣膜病的也着实不少。然而大家了解心脏瓣膜有哪些临床表现吗?</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">从临床表现来看，瓣膜性心脏病患者最容易出现活动后疲乏和倦怠，活动耐力明显减低，稍作运动便出现呼吸窘迫(即劳力性呼吸困难)，严重者夜间频发阵发性呼吸困难甚至无法平卧休息。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">部分患者(特别是二尖瓣狭窄人群)会在胸闷、憋喘的同时伴有呼吸道出血，轻者痰中伴有血丝，重者一次性咯出大量鲜血。此外，长时间的肺部淤血可导致患者频繁罹患支气管炎，特别在冬季尤为高发。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">而对于某些患者(特别是主动脉瓣狭窄)，常常会感到在活动后出现头晕或眩晕，并且随着年龄的增长，心前区不适或心绞痛症状的发生日益频繁。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">另外，部分人群虽无上述典型表现，但如果近期出现心悸、存在既往血栓栓塞、胃肠道出血、皮肤瘀点或瘀斑以及不明原因发热等病史，也为诊断瓣膜性心脏疾病提供了重要的线索而不应予以忽视。</span><br />
