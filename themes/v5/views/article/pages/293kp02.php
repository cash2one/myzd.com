<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 儿童肝移植是与人类临床肝移植并肩诞生的。1963年，Thomas Starzl对一名3岁的胆道闭锁患儿Bennie Solis进行了全世界第一次肝移植的尝试，虽然手术以失败告终，但其毫无疑问地开创了移植外科的新纪元。经过此后近半个世纪的发展，儿童肝移植在手术技巧、麻醉管理、供肝获取与保存、免疫抑制以及术后并发症的处理等方面不断得到改善，已经成为儿童终末期肝病、肝功能衰竭、遗传代谢性肝病及常规治疗手段无效的肝脏恶性肿瘤的最佳治疗手段。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">儿童肝脏移植手术根据供体来源的不同分为尸体肝移植和活体肝移植。尸体肝移植的供体主要来自脑死亡或脑死亡后的心脏死亡后的器官捐献，通过外科手术的办法获取器官捐献者的整个肝脏或部分肝脏植入患儿的体内取代患儿整个或部分病肝的方法称为儿童尸体肝脏移植术。通过外科手术的办法切取身体健康的患儿三代以内的直系亲属的部分肝脏植入患儿的体内以取代部分或整个病肝的治疗方式称为儿童活体肝脏移植术。中国公民死亡后的器官捐献事业起步较晚，器官捐献比例较低，由器官捐献来源的尸体供肝数量有限，并且儿童肝移植中尸体供肝也时常会遇到和供受体大小不匹配的情况，上述因素是制约并影响儿童尸体肝移植发展的主要因素。活体肝脏移植手术技术成熟，治疗效果明确，缓解了器官短缺的问题，在中国、日本、韩国、新加坡等亚洲地区是最为常用的儿童肝移植手术方式。</span><br />
