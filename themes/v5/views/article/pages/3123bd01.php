			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>2014爱尔眼科（成都）学术研讨暨微创手术交流会隆重召开</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5月17日，2014爱尔眼科（成都）学术研讨暨微创手术交流会在成都爱尔眼科医院学术报告厅隆重召开。第三军医大学西南医院谢汉平教授，四川大学华西医院范玮教授、张军军教授、刘谊教授、李惠平教授，爱尔眼科医院集团泪道学范金鲁主任，成都爱尔眼科医院周进院长、巫雷主任等知名专家受邀作重要学术演讲。成都、攀枝花、西昌、自贡、绵阳等160多位省内基层医生参会。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3123/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据悉，本次会议延续往年学术会专业性强、实用性高的特点，拓宽研究范围，覆盖青光眼、白内障、眼底病、屈光手术以及斜弱视手术等全眼科领域，围绕前沿知识、领先技术、创新理念展开，以专家讲解、视频直播等形式进行学术研讨和微创手术交流。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 会议期间，第三军医大学西南医院谢汉平教授就《翼状胬肉手术治疗新进展》作主题演讲，展示自体游离结膜瓣移植术、纤维蛋白胶固定与缝线固定法等前沿技术在复发几率控制方面取得的新进展；成都爱尔眼科医院周进院长就《飞秒激光在角膜屈光手术中的应用与发展趋势》作详细解说，她将屈光（近视）手术中角膜手术的治疗方法和技术进行了深入的剖析和讲解，尤其将该院即将引进的国际领先设备全飞秒作充分展示，将学术氛围推向高潮。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3123/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 会议还搭建起西南地区眼科手术临床操作实践交流平台，通过现场视频直播，一边是手术专家亲自主刀，一边是权威专家详细解说，现场医生视频观摩手术细节，实现高新技术分享。目前，白内障是基层眼科工作中常见多发疾病，手术是唯一有效根治手段，但由于技术与经验的缺乏，分核和撕囊是基层医生普遍存在的难题。交流会上根据白内障手术中核的软硬程度，成都爱尔眼科医院白内障科主任巫雷、攀枝花市中心医院眼科主任李平、四川大学华西医院范玮教授分别主刀“1.8mm同轴微切口超声乳化白内障手术”，体现先进手术方式优势，并对分核和撕囊的处理技巧作剖析。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 成都爱尔眼科医院任辉主任主刀、四川大学华西医院张军军教授讲解“微创眼底玻切手术”，该项手术中所采用的微创技术，其治疗效果及安全性都居于国内同类手术前列。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术观摩让交流会学术氛围分外浓烈，与会医生踊跃提问，交流中就切实操作问题作深入探讨，这对促进基层眼科临床诊治水平的提高起到导向作用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3123/3.png" title="" alt="demo.jpg"/></span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> “本次会议非常成功”，爱尔眼科四川省区CEO陈应城先生会后表示，会议将业内最新的研究成果作了充分的交流和探讨，充分展示爱尔眼科致力于眼科研究与学术创新的精神，这对于医疗事业的进步具有非常积极的意义。他说，以交流、合作、创新、共赢为会议精神，每年一次的学术研讨暨微创手术交流会已成常态，爱尔眼科将会把这种精神继续延续下去，致力于人类眼健康事业。</span>
