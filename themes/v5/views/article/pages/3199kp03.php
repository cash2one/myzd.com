			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>消化内科用药禁忌</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1.胃复安在上消化道出血时禁用，而且药典上只有肌肉注射的用法，无静脉注射或静脉滴注的用法。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2．胃溃疡患者禁用培他啶。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3．胃肠动力药（常用胃复安．吗丁啉．莫沙必利）不宜联合应用，锥体外系反应增加。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4．胃肠动力药不宜与抗胆碱药合用，作用降低。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5．胃肠动力药不宜与制酸剂．铝剂合用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 胃肠动力药促进胃肠蠕动，影响上述两种药物的吸收，若合用服药时间至少间隔１小时。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6．西咪替丁不宜和硫糖铝合用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 硫糖铝需经胃酸水解后才能与胃蛋白酶络和而发挥抗溃疡作用，而西咪替丁抑制胃酸分泌，从而使硫糖铝药效降低。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7．枸橼酸铋钾不宜和氢氧化铝合用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 枸橼酸铋钾需在胃液酸性条件下形成氧化铋胶体，形成一层保护膜，而氢氧化铝使胃内ph值升高，造成枸橼酸铋钾的作用减弱。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">8．西沙必利禁用于低钾血症者。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 如肝硬化病人易发生低钾，有时因胃肠蠕动差而合用西沙必利，但低钾时用西沙必利可引起严重的心律失常。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">9．西沙必利禁用于病态窦房结综合征。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 西沙必利可延缓心脏再极化，可引起Q-T间期延长．晕厥和严重的心律失常，心动过缓者禁用西沙必利。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">10．活菌制剂不宜和抗生素及收敛剂合用。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 抗生素使活菌制剂（如乳酶生．丽珠肠乐）灭活，收敛剂（如鞣酸蛋白．次碳酸铋）可吸附活菌，药效降低。</span><br />
