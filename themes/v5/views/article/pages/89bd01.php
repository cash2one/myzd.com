<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>微创心脏外科的现状和进展</strong></span>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">微创外科概念的提出是外科学的一场革命，它深刻地影响了外科学的根本理念，并渗透于外科学的各个具体专业，由此出现的微创心脏外科也极大地推动了传统心脏外科学的发展。1997年5月，国际上成立了国际微创心脏外科学会，并出版了学会的官方杂志《The Heart Surgery Forum》，在心脏外科界产生了很大的影响。微创心脏外科手术的目的，就是通过改变手术途径，减少手术创伤，增加美学效果，促进患者康复。目前，微创心脏外科的观念已经被多数心脏外科医生接受，并且在相关技术的支持下迅速得到发展。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>一、微创心脏外科的种类和现状</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（一） &nbsp; 小切口心脏外科手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">自1992年Wilson等报道了部分胸骨切口后，逐渐有大量小切口被用于临床，由于这类切口术后疤痕小，较美观，因此，受到病人的好评。病人强烈的愿望，是医生实施小切口手术直接的动力。由于小切口手术不需要特殊的辅助设备，因此，临床应用相对容易，这便使小切口手术得到了广泛的开展。目前，国外已经有大量关于小切口心脏手术的报道，国内多数开展心脏手术的单位也开展或尝试过小切口心脏手术。目前国内开展的小切口手术大多采用胸骨上段小切口、胸骨旁小切口、胸骨完全横断法、胸骨下段法、右胸骨窗法和左胸骨窗法等方法，这些径路的小切口手术都有其明显的局限性，应用范围有限。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（二） &nbsp; 电视胸腔镜辅助下的心脏手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人们对手术切口及手术入路创伤的认识是胸腔镜用于临床心脏外科的主要原因，这种手术被称为电视胸腔镜辅助的心脏手术。其用途可以有两个方面，一是作为辅助的手段，仅用于改善显露；另一个用途是全胸腔镜下的心脏手术。这种手术最早由Chang等在1996年报道，以后不少作者也进行了尝试，完成的手术包括房间隔缺损修补术、室间隔缺损修补术、单纯主动脉瓣手术或二尖瓣手术及冠脉旁路移植术等。胸腔镜能提供良好的视野，使手术操作可以通过非传统切口完成，从而使损伤进一步减小。这种手术的优点包括切口小、比较隐蔽、损伤小、出血少等，多数作者认为这种手术是一种较为肯定的微创手术。但就目前国内报道的文献显示，仅一个房间隔缺损修补术就需要三个切口，切口总长度也在5cm左右。电视辅助的心脏手术需要一些特定的技术支持，这些技术包括：（1）熟练的胸腔镜操作技术；（2）特定情况的体外循环建立及维持技术；（3）紧急情况出现时的应急处理方案等。正是由于这些特殊的技术要求，是这种手术开展有一定的难度，手术的推广相对有限。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（三） &nbsp; 机器人心脏手术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 在美国，每年有1700例病人应用机器人操作系统进行心脏微创手术，并且每年增加400例或每年增加25%。大多数心脏手术为二尖瓣手术和內镜下冠脉搭桥术。机器人冠脉手术的范围包括机械手取乳内动脉人工冠脉吻合，小切口或正中开胸停跳或不停跳搭桥，直至完全胸腔镜下的冠脉旁路移植术（TECAB）。吻合已成功运用于所有冠脉，甚至是序贯桥和使用吻合器。机器人手术的病人输血和住院时间明显少于开胸病人，但住院费用显著增加，目前国内还不能大规模地开展该项手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">（四） 介入方法在心脏外科中的应用</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">杂交手术、主动脉瘤支架植入术</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><strong>二、目前我们开展的工作&nbsp;</strong></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/89/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 自2010年4月开始，我们在国内率先开展直视下微创心脏手术，目前已完成各类微创心脏手术50例，均痊愈出院，其中30例患者未输血（60.0%），切口长度4~6.5cm（4.85±0.58）。50例手术中微创二尖瓣主动脉瓣替换术+三尖瓣成形术、二尖瓣主动脉瓣替换术、Ebstein畸形矫治术、二尖瓣替换术+冠脉搭桥术、PECD矫治术均为国内首例报道，标志着安贞医院微创心脏外科已达到国内领先水平。该微创手术的优点在于切口少，隐蔽美容；术后出血、输血量明显减少，术后疼痛轻，恢复快；不需要胸腔镜等特殊器械，手术切口小，为直视下手术，操作相对简单，手术适应范围较大，易于在有一定心外科基础的医院推广应用。</span><br />
