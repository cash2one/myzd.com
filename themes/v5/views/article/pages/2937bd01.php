		<p align="center">
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:18px;"><strong>拯救小巨人--关于脑垂体瘤（资讯）</strong></span>
</p>
<p align="left">
	&nbsp;
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;"><strong>[嘉宾]首都医科大学宣武博医院神经外科陈革副主任医师</strong></span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">[提问回答]</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">1.孩子一年内长高了15厘米这正常吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">在正常的青春期前后，如果长15厘米是完全正常的。如果在青春期后，如果再每年长15厘米就不正常了。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">2.服用激素类的药物会导致垂体瘤吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">不会。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">3.饭量大与垂体瘤有关吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">垂体瘤会导致饭量大，饭量如果异常大，并且体貌、容貌和其他的一些内分泌症状的话，需要考虑是不是垂体瘤。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">4.内分泌紊乱会导致垂体瘤吗？</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">这个问题应试是垂体瘤大多数是导致内分泌紊乱的原因，而内分泌紊乱不会导致垂体瘤。</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">5.糖尿病和垂体瘤之间有什么关系?</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">糖尿病和垂体瘤之间没有直接的关系。但是垂体瘤的病人，尤其生长素腺瘤，很容易导致糖尿病。但是糖尿病本身不会导致垂体瘤。</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">［要点回顾］</span><br />
<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">脑垂体分泌生长激素促使身高增长</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">脑垂体瘤典型表现：身高异常、肢端肥大、闭经、异常泌乳</span>
</p>
<p>
	<span style="line-height:2;font-family:Microsoft YaHei;font-size:14px;">脑垂体瘤是一个良性肿瘤、及早治疗效果好</span>
</p>
<p>
