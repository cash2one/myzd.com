			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>老年人应注意前列腺癌</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌知识问答</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、什么是前列腺癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌98%为腺癌，起源于腺上皮细胞。前列腺的结构分外周带、移行带和中央带。前列腺剖面如切成圆片的橙子一样，橙子皮部分相当于外周带，橙子核心部分相当于移行带和中央带，尿道贯穿其中，前列腺癌最常发生于外周带。由于前列腺位置隐匿，早期前列腺癌病人常无症状或症状不明显，疾病进展缓慢，但晚期病人疾病进展较快，主要表现为下尿路梗阻症状，甚至以转移症状如骨痛就医。前列腺癌的发生可能与种族、遗传、环境、食物、吸烟、肥胖和性激素等相关。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、哪些人容易患前列腺癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患前列腺癌大多年龄超过65岁，高发年龄在70～74岁，而50岁以下的男性在体检中普查血清前列腺特异性抗原（PSA）项目也筛查出少数病人，80岁以上的男性则为数不少。由于前列腺癌与遗传相关度非常高，直系亲属中如有患前列腺癌的，本人患前列腺癌的风险要比其他人高一倍；如果有两个患前列腺癌的，本人患前列腺癌的风险更大，而且更容易早年发病，确诊年龄要比普通人提前6～7岁。此外，高动物脂肪饮食是比较确定的危险因素，病人往往长期摄入较多的动物脂肪。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、前列腺癌有何预兆和症状？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">虽然前列腺癌有起病隐匿的特点，但只要做个有心人，还是可以察觉一些“蛛丝马迹”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1) 早期前列腺癌可无任何预兆症状，仅仅是体检时发现PSA升高或直肠指检前列腺有异常。 2) 出现排尿障碍，表现为尿频、尿急、排尿不尽甚至尿潴留或尿失禁。这些排尿困难症状与前列腺增生相似，容易误诊。3) 出现乏力，体重减轻，全身疼痛，可能是晚期进展性前列腺癌。4) 出现骨痛、骨折或瘫痪，可能是前列腺癌已经转移到骨。5) 出现腿部肿胀，可能发生淋巴结转移，淋巴结肿大压迫，而导致下肢血循环障碍。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、如何早期发现前列腺癌？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 直肠指检与PSA是目前公认的早期发现前列腺癌的简单、方便的初筛方法。建议50岁以上男性每年接受例行直肠指检、PSA检查。但是家族中有前列腺癌病例者，应该提早自45岁开始进行每年一次检查。当怀疑患前列腺癌时可进行的检查如下。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1) 前列腺特异性抗原检查（PSA）; 2) 直肠指检（DRE）; 3) 经直肠超声检查(TRUS); 4)超声引导下前列腺穿刺活检; 5) 前列腺癌其他影像学检查（如CT、MRI、全身骨扫描）</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">5、什么时候需要进行PSA的检查？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 《中国泌尿外科疾病诊断治疗指南》建议：1) 50岁以上男性每年应接受例行的PSA检测和直肠指检。2) 对于有前列腺癌家族史的男性人群，应从45岁开始进行每年一次的检查。3) 对于直肠指检异常，有临床征象（如：骨痛、骨折）男性应进行PSA检查。4) 对初次PSA异常者建议复查。5) 提醒：PSA检查应在前列腺按摩后1周，膀胱镜检、导尿操作后48小时后、射精24小时后，前列腺穿刺1个月后进行。同时应无前列腺炎、尿潴留等疾病。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">6、防治前列腺癌，生活上应注意些什么？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 1) 合理饮食，让食物多样化，注意多食植物类食物，每天食物中谷类、蔬菜、水果、豆类应占2/3以上，主食粗细粮搭配，荤素兼有。食用西红柿、西红柿制品，以及豆制品（低脂豆奶、豆腐、豆类蛋白粉等），可降低患前列腺癌风险。2) 饮食中高蛋白以鱼类为主，摄入有益脂肪酸，尽量避免高动物脂肪的摄取。3) 多喝绿茶是有益的。4) 戒烟限酒。5) 保持合适的体重，坚持适当活动，拥有积极向上的心态。6) 适量的钙质和维生素D，预防骨质疏松。</span><br />
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">7、前列腺癌与前列腺增生症、前列腺炎之间有没有关系？</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌与前列腺增生症、前列腺炎虽然同是前列腺器官发生的疾病，但却是三种完全不同的疾病，三者之间完全没有必然的关联。前列腺癌与前列腺增生症大多数发生于50岁以上男性，症状有或无，轻或重，都可以加以区分，而前列腺炎主要发生于中青年男性，症状与癌、增生也不相同。从病变部位、性质来看，三者也互不相同，前列腺癌的主要病变部位在前列腺外周带，是腺细胞的恶性病变；前列腺增生症的病变部位在前列腺移行带和中央带，主要是前列腺间质增生，而前列腺炎则是生殖系统的感染，不是单独的疾病，而是前列腺炎综合征。临床上前列腺增生症手术后，仍可患前列腺癌，也证明二者之间的区别。因此，目前医学上对前列腺癌与增生、炎症之间是否相关尚无循症医学依据。</span>
</p>
