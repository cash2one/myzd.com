<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;当人们听不清声音，感觉听力下降的时候，第一就诊科室通常是耳鼻喉科，首都医科大学附属北京三博脑科医院于春江教授提醒大家，听力下降也要查查大脑影像，别让脑肿瘤“偷走”了我们的听力。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
