			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>得了漏斗胸要不要做手术?&nbsp;</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">漏斗胸是一种先天性并常常是家族性的疾病，漏斗胸属渐进式病变，在出生时可能就已存在，但往往在几个月甚至几年后才愈来愈明显而被家长所发现。得了漏斗胸要不要做手术呢?</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于小年龄患儿，因小儿骨质较软，肋软骨易变形，在患儿用力呼吸及哭闹时会导致暂时的胸壁下陷畸形，而且对两岁以内小儿由于体弱、骨质较软、肋软骨易变形(佝偻病活动期)，只要无明显心肺功能障碍应先行保守治疗，适当的补钙、加强营养、适当的户外活动等处理后多可自行恢复正常胸壁外观。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">漏斗胸影响心肺功能及有精神负担的，应该手术漏斗胸治疗。大多数学者认为手术矫治比较好的年龄为7～12岁。只要看到明显的畸形，无论年龄大小都应立即手术，而不应该等到有严重的临床症状以后再手术，年龄越轻，治疗效果越好，需要手术的范围越小。漏斗胸的治疗手术指征:对称性漏斗胸进行性加重,HallerCT指数≥3.25和(或)漏斗胸导致呼吸道症状、心肺功能异常。(1)CT检查Haller指数大于3.25。(2)肺功能提示限制性或阻塞性气道病变。(3)心电图、超声心动检查发现不完全右束支传导阻滞、二尖瓣脱垂等异常。(4)畸形进展且合并明显症状。(5)外观的畸形使病儿不能忍受。</span><br />
