<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>不明原因耳鸣11年 竟是听神经瘤作祟</strong></span>
</div>
<br />
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">千龙网北京讯 (通讯员 彭雁) 郑女士是河南郑州人，11年前在上中学时出现右耳耳鸣，耳朵深处总是隐隐约约地传来嗡嗡嗡的声音，似乎有一窝蜜蜂深藏在她的大脑深处，时不时地出来叨扰她。慢慢，郑女士发现自己右侧听力也开始下降了，有时还会耳背、头晕。郑女士开始四处求医。去各大医院的五官科看耳朵，医生说她有神经性耳鸣，口服药物治疗后，没有什么效果，也有医生说郑女士是因为颈椎有问题，中医则说她是气血两虚，折腾来折腾去，郑女士的症状非但没有好转，反而越来越严重了。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 最近，郑女士发现头晕转变为头疼了，隐藏在大脑深处的那群蜜蜂飞出来的次数越来越频繁。有时，有人在右侧很近的地方跟她说话，她也听不到。郑女士在医院门诊看病时，作一个头颅CT的检查。结果一出来，问题就发现了，这群蜜蜂原来是听神经瘤所导致的。郑女士的听神经瘤已经有鸡蛋那么大，压迫到脑干了。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2011年7月，郑女士在北京三博脑科医院实施了听神经瘤摘除术，术后，据该院为其主刀的于春江教授介绍，郑女士的听神经瘤如再晚几个月就医，有可能会危及生命，所幸手术很顺利，完整地剥下了一个最大处直径近3厘米大的听神经瘤。于春江教授说，遗憾的是她的听力虽然已经最大限度地得到了保留，但已经无法复原到正常水平，如在早期就能手术的话，她的听力完全有机会保留下来。&nbsp;</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<strong><span style="font-family:'Microsoft YaHei';font-size:13.63636302947998px;line-height:25.454544067382813px;">单侧听力下降、耳鸣应及早就医&nbsp;</span></strong><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 听神经瘤为神经外科最常见的良性肿瘤之一，多发病于30~50岁的中年人，人们会觉得这种肿瘤长在脑子里，一旦发病肯定很严重，能够迅速引起患者重视。其实不然，听神经瘤有一件温和的“外衣”，极易被忽视或误诊。&nbsp;</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">于春江教授介绍，听神经瘤病人最早出现的症状是患侧耳鸣、听力减退和眩晕，随着病情发展可表现为进行性耳聋及头晕，最后听力完全丧失；还有面部麻木、鼻唇沟变浅、声音嘶哑、吞咽困难、走路步态不稳等；病程晚期可出现头痛加重、呕吐、视力下降等，严重时危及生命。因为很多患者初诊都在耳鼻喉科，因此听神经瘤的早期诊断率很低，确诊时大都已经发展到中晚期，出现了上述很多非听力症状，延误了最佳治疗时机。&nbsp;</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 于教授强调：要想早期发现听神经瘤，必须对耳鸣或听力下降等症状引起重视，有时单纯在耳鼻喉科就诊往往会延误病情，应及早进行影像学检查以明确诊断后及时治疗，目前听神经瘤的最有效治疗方法是手术切除。&nbsp;</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">听神经瘤在全切除后可以治愈，由于肿瘤位于桥脑小脑角这个区域，肿瘤越大对脑干的压迫就越明显，过去的手术以切除肿瘤、保证脑干功能（保证生命）为主要目的，死亡率较高，更谈不到面神经的成功保留了。随着显微手术技巧的成熟和微创理念的引入，术中监测技术的应用，改变了以往以切除肿瘤保留生命为目标的手术方式。据临床数据统计，目前于春江教授所在北京三博脑科医院听神经肿瘤全切率已超过96%，面神经保留率达90% 以上。&nbsp;</span>
