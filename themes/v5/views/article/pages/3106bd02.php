<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">CroweⅣ型髋关节脱位是先天性髋关节发育不良中最严重的一种类型，表现为股骨头完全脱位、下肢明显短缩、髋臼及股骨上段发育严重不良，甚至畸形，成年后可因假臼的骨性关节炎而需行人工全髋关节置换术。但手术难度大、并发症多。如将臼假体重建于真臼位置，因肢体延长较多，可发生坐骨神经或股神经损伤 。因此，如何在重建髋关节的同时避免损伤坐骨神经或股神经，是关节置换手术面临的难题。我院骨科朱振安教授已对上百例CroweⅣ型先天性髋关节脱位行全髋关节置换术，绝大多数病例均在真臼部位重建髋关节，无一例发生神经损伤，取得良好疗效。</span><br />
