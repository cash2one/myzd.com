<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">九院快讯：</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者，张**，女，46岁，来自江苏昆山，入院诊断为CroweⅣ型髋关节脱位。患者自幼髋关节脱位，步态蹒跚，行走如鸭摆样，成年后继发骨关节炎，髋部疼痛，行走困难。体检发现双下肢缩短达7cm。经人工关节置换术治疗后，脱位的关节恢复原位，髋部疼痛消失，患者步态正常，而且“长高了”7cm。门诊复查时，患者高兴的说：“现在很多人看到我，都说长高了，走路姿势漂亮了，就像换了一个人，都不认识了！真的谢谢您，朱医生！”。</span>
</p>
