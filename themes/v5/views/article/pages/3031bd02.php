			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>脑胶质瘤患者的康复护理</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">绝大多数患者出院时头皮上都留有手术疤痕，在术后一个月内，可以用温水毛巾擦拭头部，一个月后可以洗头了，但忌用手抓。手术中作去除骨板的患者，注意骨窗部位的保护，外出需戴帽，出院后避免去公共场所，防止意外，出院3～6个月后可到医院作颅骨修补术。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">颅脑手术患者容易发生便秘，在康复护理中须注意三点：一是让患者宜多吃带皮的水果和各种蔬菜；二是适当饮水，建议早餐前喝一杯热水；三是排便时不能太用力，必要时使用开塞露；对两天以上未排便的正常饮食患者，可口服缓泻忌剂帮助排便。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">颅脑手术患者的功能锻炼方式，一是对瘫痪的肌肉用柔软、缓慢的中等力度进行按摩、揉捏；二是让患者自己，或由家属帮助完成对患肢的被动运动；三是主动运动，先利用各种本体反射（如浅伸反射、屈曲反射）进行训练，以诱发主动运动；再对瘫痪肌肉做助力运动；然后对患肢主动运动；最后通过坐起锻炼，逐步使患者摆脱他人辅助，做到下地、座椅。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">部分胶质瘤患者带有癫痫史，对他们的护理要视情况而定。一是癫痫发作时，应解开患者的衣服，就地仰卧，头偏向一侧；同时于两齿间置入上手帕、手套或帽子等物，以防咬伤；禁止向患者嘴里灌汤灌药；禁止对抽搐肢体暴力施压，以防骨折。二是日常护理中，要帮助患者养成有规律的生活习惯，按时注意，科学进食，外出应有人伴行或随身携带能证明身份的诊疗卡，便于及时获得帮助。三是抗癫痫药物的使用必须在医生的指导下进行，不可自行停药、换药、加量、减量；定时测量药物血浓度，以调整药剂量，测量体温时，禁用口表。四是即使康复，也应禁止从事驾车、高空操作等危险工作。</span><br />
