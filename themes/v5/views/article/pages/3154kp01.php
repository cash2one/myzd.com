			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>家长对儿童近视眼矫正的四大误区</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">误区1：孩子太小别戴近视眼镜</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 经常有眯眼看人、视物的儿童。不少父母会说：“别让孩子那么小就戴眼镜，戴了就摘不下来了。”这种认识是极端错误和非常有害的！儿童正处于视觉发育的关键时期，保证完整、健全的视觉，最重要的就是双眼能同时看到一个清晰的视网膜（眼底）像。近视眼如不及时矫正，长期的视网膜模糊像，一方面会诱发眼轴的进一步加长，使近视度增加，甚至引发弱、斜视；另一方面还会影响孩子的心理发育，表现为对外界新事物兴趣降低，生活范围缩窄，重者影响智力发育。 误区2：近视眼镜越换度数越高　　青少年时期近视度数增加的原因，除遗传因素外，最主要的是过重的用眼负担和不科学的用眼习惯。身高快速发育期，往往近视度数也增长较快。因此，在科学、合理矫正的基础上，近视度数是否增加与是否更换眼镜无关。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp;建议青少年应每6～12个月做一次视光学检查，定期监测视力变化。矫正视力过低者须重新验光，并更换合理屈光度的矫正镜片</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">误区3：近视眼镜不能配得太清楚</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近视的完全矫正是将近视矫正为正视，用以恢复调节与集合之间固有的正常关系。长期戴低矫眼镜，眼睛看近物时不用调节或少用调节，为了维持双眼单视，两眼视轴一定要集合起来，这样调节和集合之间的关系就会发生紊乱。如果使调节向集合靠拢就要产生过度调节，因而引起睫状痉挛，即人为地增加了近视度数；另一方面是集合向调节靠拢，即向低集合方面发展，产生眼外肌肌力不平衡，当肌力不平衡不能维持时，双眼视功能就会被破坏，只靠一眼看物，另一只眼偏向外侧，成为暂时性间歇性交替性外斜视。对于配镜度数的高低，验光师会根据孩子的视功能情况合理给予，家长不要盲目干预。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">误区4：为不戴眼镜或少长度数而盲信各种“治疗”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 社会上不断出现的“治疗近视”仪器、疗法（按摩、针灸等）已经让众多的家长上当、受骗。从科学的角度看，这些治疗手段往往对舒缓眼疲劳可能有所帮助，对假性近视的恢复视力有一定作用，而对真性近视（近视眼）无任何帮助。到目前为止，近视眼最有效的矫治手段仍然是光学矫正——佩戴合适的眼镜。</span><br />
