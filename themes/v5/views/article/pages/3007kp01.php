			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>当心这样吃药会伤肝</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">服用药物是治疗疾病的一种常用方法，国际通行做法把药物分为非处方药、处方药两大类，处方药需要专科医师开具处方或特殊处方才能使用和购买，而非处方药则不需要医师的处方就可以在药店买到。目前我们可以接触到的药品多达3万多种，其中可以明确产生肝损伤的药物多达1000多种，三十分之一的几率已经不低了，所以中医上常说“是药三分毒”，也有人形象的比喻“药是纸包枪、杀人不见伤”。在一项调查中显示：药物性肝炎占非病毒性肝炎比例的20-50%，在我国中药所致的肝损伤占20%左右，世界卫生组织统计的结果显示药物性肝损伤占住院患者的5%，而且近20年来由药物导致的肝损伤有上升的趋势，由此可见药物在治疗疾病的同时也会给我们的身体带来很多难以预料的伤害。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对于没有相关医学常识的人群来讲，怎样确定哪些药物是安全的呢？其实任何一种药物是没有绝对安全的，无论处方药还是非处方药都会发生肝损害，因为发生药物性肝损伤主要取决于两种因素，第一种因素：药物本身的毒作用，大部分药物在治疗剂量下都是有毒的，只不过治疗下的毒作用带来的治疗效果是机体可以承受的，并不会带来严重的伤害，所以西医将药物按照药物的半数有效量和半数致死量来区别药物的治疗剂量和毒作用强弱。第二种因素：自身代谢差异，每个人对药物的反应千差万别，并不是一种药对大部分人群没有伤害，就不会对某个个体就没有伤害。因为有些药物对个体的遗传、免疫、代谢以及年龄、性别、疾病等是有很大差异的。所以即便是没有毒性的药物在某个特殊的个体中也可能发生严重的毒副反应。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">常见的几种可能增加肝损害的原因</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">1、用药超量：这是常见的不按药品说明书服用药物导致肝损伤原因，单次用药量大、超次数服药，长期用药都是常见的用药超量，更有些人会有“多吃点、好的快”、“我吃过、害不死人”的做法，其实正是这种做法害了很多人。临床中常见的感冒药、安眠药、平喘药、止痛药、抗生素、抗结核药等最容易用过量，也是最常见的肝损伤药物。我们身边就有很多这样的例子，感冒了，吃了没见好，马上多吃点或多吃几遍，还有人干脆换种药或换几种药；尤其在偏远落后地区，扑热息痛是万能的，无论什么病都吃这个药，其实24小时超过6片就有可能导致肝损伤发生；而正确的做法是用药前先仔细看药品说明书，看不懂的问专业的医师或药师，没有说明书的不服用。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">2、多药同服：我们都知道，吃头孢不饮酒，因为近几年媒体上报道了很多因此丧命的例子，于是很多人真的吃头孢不饮酒了，可是却在吃青霉素、替硝唑或其它药物时开始喝酒了，熟不知，这些药物一样可能发生双硫仑样反应。多药同服是老年人常犯的错误用药，很多老年人吃药像吃饭，大把大把的各类药物服用，其实这是很危险的。因为很多药物之间是有配伍禁忌的，也许单服某种药物并不会发生什么样不良反应，但是一旦和某种药物合用，很可能就发生了严重的不良反应。建议需联合用药的，一定要在专业的医师和药师指导下进行，切莫听信非专业人士的经验论主义，更不能轻易听信广告的夸大宣传，药没有多少钱，可是身体吃坏了是多少钱也换不会来的。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">3、中药偏方：经常听人讲“不爱吃西药，西药有毒，中药无毒”，更是总有人相信“偏方治大病”。这种观念对吗，很显然是不对的，我们不否认中医在很多疾病的很多方面有着西医西药无法达到的疗效，2015年的诺贝尔医学奖青蒿素就是对祖国传统医学的最大肯定。但是我们也必须承认中药也是可以造成肝损伤的，中医讲究的是辩证施治、配伍减毒，正确的服用中药可以治病，错误的则可害人。前几年沸沸扬扬的天麻胶囊、龙胆泻肝丸事件，都是滥用药物的教训。更是有些唯利是图之人，以祖传秘方为名，做什么专治疑难杂症、不孕不育，实质是中药掺西药，坑害可很大一群人。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">4、孕妇、儿童乱用药：孕妇、儿童与成人的身体有很大的区别，一旦乱用药，很难想象结果是什么样。尤其是儿童服用成人用药，发生肝肾损害的例子数见不鲜，儿童肝脏代谢能力不如成人，且易发生爆发性肝衰竭，如扑热息痛、红霉素、尼美舒利等等，都被证实较容易引起婴幼儿的肝损伤。建议14岁以下的孩子用药时要选择儿童专用剂型，而且要根据孩子的体重、年龄精确用药，千万不要给孩子乱吃药或轻易用成人药。20多年前的四环素、氯霉素伤害了几代人，今天医疗水平提高了，我们更不能让药物残害孩子的未来。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">总之，治病用药是大事，不能草率和想当然，更不要不懂装懂，现在医疗机构已经覆盖了绝大多数社区，互联网医疗也蓬勃发展，走几步路，甚至动动手指就可以查到正确的用药信息或咨询专业的医师或药师，千万不要乱用药，以免花了钱还伤了身。</span><br />
