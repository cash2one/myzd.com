			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>警惕 | 我国每10秒就有1人死于心血管病 患者近3亿</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据国家心血管病中心统计，我国心血管病患者近3亿，每10秒就有1人死于心血管病，每年大约350万人因此死亡，占总死亡原因的41%，居各类疾病之首。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3139/1.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">健康的心脏源于健康的环境</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">专家指出，不健康的心脏源于不健康的生活、工作环境。比如办公室温度过高或过低，都会对心脏造成影响。在嘈杂环境中工作的人罹患心脏病的几率比普通人高两倍以上。睡眠不足、精神压力大与心血管疾病年轻化密切相关。研究表明，每天睡眠不足6小时的人发生高血压的风险达35%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">至少80%的心血管疾病导致的死亡可通过戒烟、限制食盐摄入、控制高血压等干预手段来避免。比如高糖饮食影响体内脂肪消耗导致高血脂，盐分摄入过多容易诱发高血压，吸烟是心血管病的重要诱因，持续饮酒者比不饮酒者，4年内发生高血压的危险增加40%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">目前，治疗心血管病较为常见的方法就是采取介入治疗，安装心脏支架便是其中一种。心脏支架又称冠状动脉支架，是心脏介入手术中常用的医疗器械，具有疏通动脉血管的作用。心脏支架最早出现在20世纪80年代，它的出现让很多心脏疾病患者重新获得了健康。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但是，前段时间，内蒙古发生了一起医疗纠纷，一位冠心病患者，被一次性植入了价值15万元的9个心脏支架，手术医生被怀疑“过度医疗”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp;<img src="<?php echo $baseUrlImage; ?>article/doctor/3148/2.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">△冠心病示意图</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">一次植入9个支架被疑过度医疗</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">患者黄金海说，4年前因为冠心病做了心脏支架手术，现在症状不但没有减轻，反而加重了，“现在一翻身里面像扎的似的”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">为黄金海治病的医生王东方，是当地一家三甲公立医院心内科的主任医师，做心脏介入手术已有10多年的经验了，在他看来，黄金海从手术前不能动，到现在基本可以正常生活，“说明手术是成功的，我们的治疗策略是正确的”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王东方说，支架植入后并不能根治冠心病，只能保持血流通畅，依然面临再狭窄、血栓形成等问题。医疗纠纷发生后，他希望患者到医院复查，但患者拒绝了他的建议。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">据王东方回忆，2010年10月，患者黄金海通过朋友介绍找到他看病。检查发现，黄金海的三支冠状动脉严重病变，需要做心脏支架或心脏搭桥手术。心脏支架属于微创手术，操作方便创伤小；心脏搭桥需要开胸，创伤大风险高。一般来说，如果需要植入的支架超过3个，就要考虑心脏搭桥手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">但患者及家属由于担心“下不了手术台”，于是选择了进行支架治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">手术过程中，因为病人长期患有严重的高血压、糖尿病，有两支血管非常脆弱，出现了内膜撕裂的情况。王东方表示，本着救命优先的原则，他为病人一共放了9个支架，“这是术前没有预料到的，我做了10几年的支架治疗，这也是唯一的一次，最多的一次”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">这9个支架到底是病情需要还是过度医疗，还需医疗鉴定才能得出结论，目前这起医疗纠纷还正在调解中。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">△记者调查：一次植入9个支架为哪般？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">支架由国家定价 医院提成3%</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">心脏支架进入我国30年来，让数百万冠心病患者免去了开胸手术的痛苦。但这种危急时刻救人一命的心脏支架，近年来却屡屡遭到质疑。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">近段时间，一篇名为《可怕的心脏支架》的文章，在网络上大量转发。文章说，做完心脏支架手术，就意味着在身体里埋了一颗定时炸弹，突然发作起来3分钟内就会死亡；这种手术在国外就是500~800美元，在中国则需要3~5万元，因为暴利，医院特别喜欢为病人做支架手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">对此，业内专家表示，这样的评价是不公正的。中国工程院院士、阜外心血管病医院教授高润霖，曾经参与制定了心血管疾病介入诊疗技术规范。他认为心脏支架让冠心病患者遭受最小的创伤，达到最好的效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">针对网上所说的装了心脏支架就等于埋下定时炸弹的说法，专家表示，装完心脏支架确实可能出现血栓，造成心肌梗死，但这种情况发生率极低，不足百分之一。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">临床统计，不装支架，急性心梗患者死亡率在15%左右，装了支架死亡率可降低到3%到6%。对于心脏支架暴利的说法，高润霖说：“在2001年只有唯一的支架进入到中国，定价是四万二；当国产支架问世以后，现在进口最好的支架只有一万八千块钱左右，国产的一万到一万二左右”。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">在北京三甲医院做心脏支架手术，如果没有并发症的话，放一个国产支架的医疗总费用大概两三万块钱。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">北京大学人民医院心脏中心副主任王伟民说：“支架属于消耗品，消耗品由国家定价，医院只是提了3%左右，没有一个医院有权利去更改这个费用问题。”</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">△救命的支架还是可怕的支架？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">人均植入1.6个支架 数字逐年下降</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">专家介绍，在心脏支架刚出现的10多年中，由于病例少、临床经验不足等原因，一些医生曾希望治疗效果接近完美，建议病人植入更多的支架。另外，由于搭桥手术风险高、创伤大，一些患者主动要求放支架。目前我国建立了心血管介入治疗准入制度和网上直报系统，是世界上为数不多的从国家层面来规范心血管介入治疗的国家。2012年国家卫生行政部门曾对10省市27家医院进行督导，对置入支架数超过5枚的患者进行分析，其中92%的情况被认为是合理的。2013年全国共有45万人接受冠状动脉介入治疗，平均每个病人植入了1.6个支架。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">王伟民说，美国人均植入支架的数量是1.5个左右，根据卫计委的统计数据来看，我国人均植入支架数量从2012年的1.7个，到2013年的1.6个，正处于逐渐下降过程中，且与发达国家是非常接近的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><img src="<?php echo $baseUrlImage; ?>article/doctor/3148/3.png" title="" alt="demo.jpg"/></span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">△心脏支架安装示意图</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">冠心病患者该如何科学应用支架</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">冠心病是在长期动脉粥样硬化的基础上造成血管狭窄而引起的心肌供血不足，冠心病最大的危险就是容易猝死。对于冠心病患者来说，应该如何选择最佳治疗方案，科学应用心脏支架呢？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">专家介绍，治疗冠心病，改善其心脏血管狭窄或堵塞的症状，可以通过药物治疗、搭桥手术、支架介入等三种方法来实现。临床统计，大约三分之二的冠心病患者没有及时接受正规的心脏介入或外科手术治疗，而是认为吃吃药挺一挺就好了。专家指出，出现心肌梗死、心绞痛、药物治疗效果欠佳、血管狭窄面积大于75%的冠心病患者，都应该及早接受心脏介入治疗。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">不适合装支架的患者包括，冠状动脉弥漫狭窄、严重钙化以及心脏病变复杂的患者，肾功能不全、对抗凝药物过敏的患者也不适合放心脏支架。一些患者认为经过介入或外科手术后就可以根治冠心病了。其实，冠心病是由高血压、高脂血、糖尿病、吸烟等多种危险因素造成的，冠心病介入治疗或外科手术治疗后仍存在血管再狭窄的可能性，患者仍需继续药物治疗和生活方式的调整，防止发生新的病变。</span><br />
