		<div align="center">
	<span style="font-family:Microsoft YaHei;font-size:18px;"><strong>增长最快的恶性肿瘤--甲状腺癌</strong></span><br />
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:Microsoft YaHei;font-size:14px;">生活实例</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">李女士在某外企工作，上周单位体检发现甲状腺结节，第二天，她去了肿瘤医院甲状腺外科进一步检查，被确诊为甲状腺乳头状癌。李女士震惊之余，百思不得其解：自己性格乐观，平时注意饮食健康，并有规律地锻炼身体，近期身体也没有任何不舒服，为什么突然得了甲状腺癌？一打听才知道，近年来，有不少人看到或听说过身边同事朋友被诊断为甲状腺癌。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">甲状腺癌发病率不断增加</span><br />
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;">2011年&nbsp;&nbsp;&nbsp;北京市卫生与人群健康状况报告显示，甲状腺癌患病率9年间增长225.2%。</span>
</p>
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;"> 2012年&nbsp;&nbsp;&nbsp;国家卫计委统计报告显示，甲状腺癌发病率上升至女性恶性肿瘤第3位。</span>
</p>
<span style="font-family:Microsoft YaHei;font-size:14px;">2014年&nbsp;&nbsp;&nbsp;北京市发布，甲状腺癌已成为北京市增长最迅速的恶性肿瘤。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">事实上，不仅是北京，全国各大省市甲状腺癌的发病率都在增加。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">&nbsp;</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">增长最快的恶性肿瘤－－甲状腺癌</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">中国医学科学院肿瘤医院头颈外科教授&nbsp;&nbsp;张&nbsp;彬</span><br />
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;">甲状腺是人体重要的内分泌器官，位于颈部正中，成“蝴蝶形”趴在气管前方，它分泌的甲状腺素是维持人体正常运转的重要物质。甲状腺癌的发病率不断增加与多种因素有关，其中最主要的是与B超早期发现癌肿有关。此外，甲状腺癌的发生还与电磁辐射、饮食碘过量或缺碘、甲状腺良性病变、女性雌激素水平、肥胖以及家族遗传等有关。其中，明确的病因只有电磁辐射，其他仅仅是相关因素。</span>
</p>
<span style="font-family:Microsoft YaHei;font-size:14px;">1．医学检查技术提高&nbsp;&nbsp;&nbsp;&nbsp;随着生活水平的不断改善，人们的健康意识逐渐增强，规律体检的人数不断增加，特别是近年来体检项目中增加了甲状腺超声检查，过去不被发现的早期甲状腺癌，尤其是直径小于1厘米的微小癌，现在也能够被发现，在很大程度上提高了甲状腺癌的检出率，因此可以说，医学检查技术的提高，是直接导致甲状腺癌发病率上升的主要原因。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">2．电磁辐射&nbsp;&nbsp;&nbsp;&nbsp;甲状腺癌的发生与甲状腺受电磁辐射，如高能γ、χ等放射性损伤有关，最典型的案例是1986年前苏联切尔诺贝利核电站发生核泄漏事件后，当地儿童甲状腺癌发生率明显攀升。临床调查也显示，一些患者有在儿童时期做过颈部X线治疗或多次X线检查的病史。而普通的电磁辐射，如手机信号、无线Wi-Fi、微波炉等，到目前为止，并没有证据证明与甲状腺癌有关。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">3．碘过量或缺乏&nbsp;&nbsp;&nbsp;&nbsp;民间有传言认为，食用加碘盐或含碘海产品会引起甲状腺癌。其实，国内外流行病调查均表明，食用加碘盐与甲状腺癌发生没有明确关系。目前认为，沿海地区甲状腺癌发病率较内陆地区明显增高的主要原因是沿海地区生活水平与医疗技术发达，早期发现甲状腺癌的人群较多，并非食用海产品导致。相反，缺碘地区和井水中含大量碘地区，甲状腺癌发生率均明显升高。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">4．甲状腺疾病&nbsp;&nbsp;&nbsp;一些甲状腺病变，如甲状腺腺瘤、慢性甲状腺炎、结节性甲状腺肿，或其他毒性甲状腺肿患者合并甲状腺癌的可能性增加。需要强调的是，这些甲状腺病变并非癌前病变，只不过这些良性病变发生的因素与甲状腺癌发生因素有重叠现象，如低碘和女性激素等，导致甲状腺良性疾病与甲状腺癌“相伴”发生。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">5．女性激素&nbsp;&nbsp;&nbsp;&nbsp;甲状腺癌“偏爱”女性，特别是中青年女性，男女发病率大概为1:3.5，说明女性激素与甲状腺癌发生有关。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">6.&nbsp;家族史&nbsp;&nbsp;&nbsp;甲状腺癌的发生与家族也有一定的关系，大约10%～15％患者的直系亲属可能患甲状腺癌，有甲状腺癌家族史人群患甲状腺癌危险度较一般人高5～6倍。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">7．肥胖&nbsp;&nbsp;&nbsp;&nbsp;流行病证实，超重和肥胖会增加患癌风险，体质指数高(也称BMI，体重除以身高的平方)是甲状腺癌发生的相关因素，肥胖可能仅仅是现象，内在可能与激素和内分泌失调有关。</span><br />
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;">与所有癌症发生一样，甲状腺癌发生可能是多种因素综合作用的结果，包括多个癌基因突变，目前已经发现癌基因包括BRAF、RET、RAS等。</span>
</p>
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;"><br />
</span>
</p>
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;">需要强调的是，虽然甲状腺癌发病率增长很快，但是治疗效果很好，甲状腺癌中，绝大多数是疗效最好的乳头状癌，20年生存率达到90%以上，其中小于1厘米“微小癌”大多数呈惰性状态，有可能伴随我们终身。因此，患者不必过分担心，只要到正规医院诊治，大多数患者的寿命及生活质量不会受到较大影响。</span>
</p>
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;"><br />
</span>
</p>
<span style="font-family:Microsoft YaHei;font-size:14px;">预防甲状腺癌5措施</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">早期甲状腺癌几乎没有任何症状，因此，预防和及早发现甲状腺癌很重要。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">1．儿童应尽量避免颈部医源性辐射，如颈部CT、颈椎X片和放射性碘131甲状腺扫描等；邻近部位必要的检查如胸片和牙片等，也要采用“铅围脖”进行甲状腺防护。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">2．内陆地区居民建议食用加碘盐。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">3．减肥和控制体重。</span><br />
<span style="font-family:Microsoft YaHei;font-size:14px;">4．直系亲属中有甲状腺癌患者的人群，每年至少做1次甲状腺B超检查。</span><br />
<p>
	<span style="font-family:Microsoft YaHei;font-size:14px;">5．患慢性甲状腺炎和结节性甲状腺肿的患者，建议每年至少做1次甲状腺B超检查，及时发现变化。甲状腺功能低下者，需要在医生指导下补充甲状腺素。</span>
</p>
