			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>哪些前列腺癌患者适合做内分泌治疗？</strong></span>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">前列腺癌是男性最常见的恶性肿瘤之一，多数情况下是在雄激素的刺激下缓慢生长，若体内的雄激素大幅度下降，肿瘤就会萎缩。通过手术切除睾丸或药物注射，可以抑制患者的雄激素水平，达到阻止肿瘤发展的目的，这种方式被称为内分泌治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">内分泌治疗在前列腺癌根治手术后进行，可以预防疾病再发。但不是手术后尽快做，而是要定期检测PSA，等PSA上升到一定程度后再开始治疗——前列腺癌根治术后，PSA要一个月一查，多数情况下，第一个月是低于0.2的，如果没有降到0.2以下，还可以再等一个月，接下来的两个月若持续下降，降到0.2以下，那么患者就可以放心了，之后每三个月一查即可；但如果后续检查发现PSA上升，大于0.2，称为生化复发，就要考虑做内分泌治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果不做根治性手术，前列腺癌还可以做放射治疗，又叫根治性放疗，也能起到抑制肿瘤发展的目的。放疗患者的PSA检测值，相对手术患者，要求会松一些，可以大于1的时候再进行内分泌治疗；内分泌治疗时，放疗也不要停，两种方式综合治疗可以起到更好的抑制效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如果前列腺癌恶性程度较高或发生骨转移，手术和放疗不能解决问题，就要采用内分泌治疗，阻止肿瘤发展。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">当然，内分泌治疗不是非要等到前列腺癌手术、放疗失败后才使用，也可以在疾病早中期开始。比如患有心脑血管病或其他严重疾病，程度比前列腺癌更加危险，导致预期寿命不长，或者患者由于某些原因难以接受或不能接受手术，为了更好的生活质量，也适用内分泌治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">有说法提出，可以在前列腺</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">癌手术前三个月做内分泌治疗，即新辅助治疗，使肿瘤变小，边缘清晰，让手术更加顺利。但从实际操作的角度来讲，内分泌治疗会使组织粘连加重，反而增大手术的难度。根据外国文献报道也可以证明，新辅助治疗并不能降低手术难度，推迟肿瘤复发时间的效果也不明显。因此，我不建议在前列腺癌手术前做内分泌治疗。</span>
</p>
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">此外，不是所有的前列腺癌都能用内分泌治疗。比如恶性程度较高的前列腺肉瘤，患病后PSA并不高，因为这种肉瘤不依赖雄激素生长，所以控制雄激素不能抑制其发展，也就不能用内分泌治疗</span>
</p>
