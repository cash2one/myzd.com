<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>男人年过五十别忘给前列腺“年检”</strong></span><br />
</div>
<br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"></span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“最有男人味的癌症”近年来发病率快速增长，症状易与前列腺增生混淆</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 医学指导/广东省泌尿外科学会主委、中山大学附属第三医院泌尿外科高新教授，中山大学附属第三医院泌尿外科邱剑光主任医师</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 随着年龄的增长，男人很难摆脱慢性前列腺炎和前列腺增生的困扰。然而，泌尿外科专家指出，出现排尿费劲、夜尿增多、排尿不尽等症状，未必都归咎于前列腺炎和增生，在其背后，有可能还隐藏着前列腺癌的身影。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 有“最有男人味的癌症”之称的前列腺癌，近年来发病率快速增长。以广州地区为例，近十年来该病发病率翻了一番。由于我国老年男性前列腺增生发病率很高，症状容易与前列腺癌混淆，而且前列腺癌在早期发展缓慢隐蔽，不去做检查的话，也很难被及时发现，导致过半患者确诊时已到晚期。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 专家呼吁，年过五十的男性应该每年进行一次筛查，即抽血查前列腺特异性抗原（PSA）加直肠指检。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 文/广州日报记者任珊珊&nbsp;通讯员江澜</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 警惕：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 前列腺增生易“掩护”癌症踪迹</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 前列腺癌好发于60岁以上的老年男性。&nbsp;“20年前，前列腺癌的发病率大概是十万分之二到三，2008年的统计数据约为十万分之十，而去年，北上广地区进行的筛查显示，前列腺癌发病率接近十万分之二十，发病率上升得很快。”广东省泌尿外科学会主委、中山大学附属第三医院泌尿外科高新教授指出，导致前列腺癌发病率快速增长的最主要原因是老龄化社会的到来。其次，人们接受了西方的饮食方式，摄入过量的脂肪、红肉，特别是烧烤过的肉，也被认为是前列腺癌发病率升高的原因。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 早期前列腺癌的根治率很高，可达90%，约有6%~10%的病人会出现转移，转移性前列腺癌的5年生存率还不到10%。和许多癌症一样，我国大部分前列腺患者在确诊时已到晚期。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “约有一半的病人因排尿不顺利等症状来就诊，这时已经晚了，有不少人出现了转移。”高新表示，之所以会出现这个问题，一方面是我国普通健康体检大多没有纳入前列腺癌筛查项目，老年男性也缺乏给前列腺做“年检”的意识，无法及早筛查出来。另一方面则与前列腺癌的特点有关。“统计显示，六十岁以上的男性有40%~50%的人患有前列腺增生，到了70~80岁则上升至50%，80岁以上人群中，发生前列腺增生的比例为100%。”高新指出，早期前列腺癌没有特异性表现，部分患者会表现为排尿不畅，与前列腺增生症状非常相似，如果患者本身就有增生，相当于给癌症打了“掩护”，常让患者放松警惕。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 治疗：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 前列腺癌并不都是“懒癌”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 众所周知，癌症有“好”“坏”之分，有的癌症如甲状腺癌，进展缓慢，有的则如肺癌和肝癌等，增长速度很快。前列腺癌总的来说比较温和，但并不都是“懒癌”。“‘好’的前列腺癌很懒，又称惰性癌，就像冬眠的动物一样，很多年都没动静。但‘坏’的前列腺癌生物学行为比较活跃，进展很快。”高新说，有病人半年就会出现肿瘤转移。前列腺癌是对激素敏感的肿瘤，采用内分泌治疗的效果较好。而容易转移的病人，肿瘤对激素并不敏感，内分泌治疗的效果差，就像普通肿瘤一样，更易出现全身转移。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 前列腺癌的处理手段包括主动监测、手术治疗、放疗、内分泌治疗、免疫治疗、化疗和姑息治疗。前列腺癌的手术治疗是指手术切除前列腺，是根治癌症的最佳手段。早期前列腺癌有机会通过手术切除前列腺治愈。手术治疗尽管有尿失禁、勃起功能障碍（ED）、出血、感染等风险，但这些风险在大多数情况下是可控的，因此，不能因为担心手术风险而害怕接受手术。高新指出，在国外，大约四分之一的病人采取主动监测的治疗方案。当PSA小于10，穿刺针数小于2针，可以先不切除肿瘤，而采取主动监测。如果病人高龄，且癌症分化较好，恶性程度不高，也可以采取主动监测。高新指出，对于PSA大于20，穿刺针大于6针、肿瘤穿破了前列腺但没有发生远处转移的局部高危患者，过去一般不进行手术切除治疗，而采取保守的放疗和内分泌治疗。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 防复发：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 术后每月都要检查PSA</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 前列腺癌最经典的筛查手段当属前列腺直肠指检结合血清PSA(前列腺特异性抗原)检测。高新强调，两项检查在筛查中的作用都十分重要，任何一项有异常都需要进一步检查。大多数前列腺癌病人由PSA&nbsp;异常增高而发现，约有25%是由直肠指检异常发现的。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 邱剑光指出，PSA不光是前列腺癌筛查的指标，更是前列腺癌治疗效果的预测指标。前列腺癌患者术后每月都要检查PSA，做好监控。做完前列腺癌根治手术6周以后，如果体内仍能检测出PSA，且数值仍然升高，说明体内有残留的前列腺癌病灶，仍在不断产生PSA。手术后，如果连续两次血清PSA水平超过0.2ng/ml，提示前列腺癌生化复发。如果血清PSA缓慢升高，提示肿瘤很可能出现了局部复发。如果血清PSA值快速升高，还意味着肿瘤可能出现了远处转移。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 不过，PSA经常会曝出“假情报”。前列腺炎症、性生活、局部按摩甚至骑车等，也会引起PSA升高。“前列腺癌的病灶并不都能在影像检查中发现，有些早期肿瘤的PSA指标并不高，等到PSA数值增高时，可能病情已进展到二期。”邱剑光表示，近年来，随着研究的进展，融合基因检测也被引入前列腺癌的筛查中。当发现PSA升高时，或者当PSA数值并未升高，前列腺直肠指检已发现结节时，或者B超、CT、MR等影像学检查怀疑有前列腺肿瘤时，患者都需要进行前列腺穿刺活检。活检中一旦发现癌症，则可用病理蜡块进行融合基因的检测，并依据检测结果来预测治疗效果，量身定制个性化治疗方案。</span>
</p>
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 筛查：</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 四类男性早点做“年检”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 高新强调，年过五旬的男子，应该每年抽血查一次血清PSA结合前列腺直肠指检。前列腺高危人群应该更早开始定期筛查，包括有前列腺癌家族史的男性，即父亲、叔父或兄弟患过前列腺癌，生活中经常接触亚硝胺、铅、X射线、紫外线等致癌物，有前列腺增生且症状严重，性生活长期不协调等。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 和抽血检查PSA相比，许多人更抗拒直肠指检，认为检查很痛苦。实际上，对熟练的医生来说，前列腺直肠指检只需要几分钟左右，病人并不会感到多大痛苦。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 而要预防前列腺癌，平时应注意少吃红肉，避免高脂肪饮食，多吃富含膳食纤维的食物，适量补充富含维生素E、硒、番茄红素等抗氧化剂的食物，经常锻炼，保证充足睡眠时间，经常给心理减压。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 坊间流传一些号称“前列腺卫士”的食物，究竟靠不靠谱？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> “西红柿等富含番茄红素的食物，的确能对前列腺组织起到保护作用。”&nbsp;高新指出，有研究显示，番茄红素对预防前列腺癌、结肠癌和直肠癌等癌症有显著功效。不过，番茄红素是脂溶性的营养素，因此生吃西红柿没什么用，最好煮熟吃。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;"> 豆制品不仅含有丰富的蛋白质，可以提升抵抗力和免疫力，同时大豆中还含有一种叫做三羟基异黄酮的抗氧化剂，具有抗癌功能。西蓝花、白萝卜等十字花科蔬菜以及核桃也有助抑制前列腺癌细胞生长、扩散。</span>
</p>
