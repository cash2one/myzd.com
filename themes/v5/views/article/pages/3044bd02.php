			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 飞秒激光近视眼手术：爱的很多，怕的不少</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">眼睛是我们获得信息的源泉，其重要性不言而喻。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">然而，我国是世界上近视人数最多的国家。近年来，我国近视发生率达33%，是世界平均水平的1.5倍，人数已近4亿。其中，青少年为2.7亿，近视发病率为50%～60%，年轻化趋势明显。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近视患者们都有这样的感受：戴框架眼镜不方便、不美观；戴隐形眼镜怕感染、不舒服。因此，做近视手术成为摘掉眼镜的唯一办法。但是，有不少患者对手术的安全性和术后效果却心存疑虑。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 暑期又要到了，最近到同仁医院屈光中心做手术的患者也越来越多，今天是全国爱眼日，结合我二十多年的屈光手术临床经验，借此机会，对患者最关心的几个问题进行分享。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 角膜屈光手术安全性高</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 近视眼的手术种类很多，如果按照手术解剖结构来分，主要有晶状体屈光手术和角膜屈光手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 晶状体屈光手术主要是在眼内植入镜片，又叫人工晶体植入术。对于高度近视的患者，做角膜屈光手术可能会存在残余度数，人工晶体植入术就可以解决这个问题。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 角膜屈光手术主要是在角膜上，利用冷激光照射矫治。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 由于人工晶体植入术是眼内手术，安全性不如角膜屈光手术。因此，各医院选择最多的还是角膜屈光手术中的飞秒激光近视手术。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 飞秒激光是当前最流行的术式</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 角膜屈光手术经过30多年的发展，已日趋成熟。从最早的放射状角膜切开术（PK），到准分子手术（包括PRK、LASIK、Epi-LASIK、LASEK），再到目前最新的结合飞秒激光、波前像差技术和虹膜定位技术的准分子激光手术等，手术安全性越来越高，并发症逐渐减少，手术效果更令患者满意。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 据2009年Solomon等在角膜世界文学报道《LASIK World Literature Review》中的研究统计，在全球范围内，飞秒激光近视手术患者的术后满意率已经达到95.4%。其中，在美国的满意率为95.2%，在美国之外的其他国家满意率为95.6%。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2013年，美国总统奥巴马还宣布，授予Lasik近视手术创始人吴拉姆•佩曼“国家技术和创新奖”，以表彰他在近视领域做出的努力和卓越科学成就。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 飞秒激光是微创手术，无明显痛感</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 飞秒激光手术是一种微创手术。如果患者配合得好，飞秒激光近视手术需要5～10分钟。手术一般不会流血，也不会有很明显的疼痛感。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 这是由于正常人的角膜厚度约为0.6毫米。角膜可分为五层，飞秒激光近视手术是在角膜中间层上进行照射治疗塑形。因此，整个手术分为三步：制角膜瓣；掀开角膜瓣，用准分子激光照射塑型；角膜瓣复位（如图1所示）。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 术前、术后都需要做严格的检查</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 虽然是微创手术，但也不是人人都能做。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术前需要经过严格的术前检查，排除禁忌症。如眼部活动性炎症及病变；眼周化脓性病灶；已确诊的圆锥角膜；严重干眼病；严重的眼附属器病变；全身结缔组织病及严重自身免疫性疾病等。部分患者感到术后效果不理想，甚至出现反弹或并发症，一定程度上与术前检查不到位有直接关系。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 此外，术后还要定期做复查，确保手术效果。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 手术不会导致失明角膜会逐渐修复</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 说到手术，难免让人紧张害怕，在眼睛上做手术就更是如此了。很多患者都会问，飞秒激光近视手术到底安不安全，会不会导致失明？</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 飞秒激光使近视手术真正成为无刀手术，可以通过控制时间将激光运行距离的精度控制在微米级，手术的安全性和之前的术式相比，有了显著性提高。飞秒激光通常不会带来严重的并发症，更不会导致失明。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 只要是手术就会有风险。由于任何角膜屈光手术都会不同程度地切断角膜神经，改变角膜的形态。因此，少数患者手术早期会出现眼干、眩光等不适症状。但是，随着伤口的修复，这些问题都会逐渐得到改善，角膜也会恢复。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> （作者为华北区屈光手术专家会诊中心专家、北京同仁医院眼科中心屈光专科主任周跃华）</span><br />
