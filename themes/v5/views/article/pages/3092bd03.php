<div style="text-align:center;">
	<strong>北京优秀中青年医师评选揭晓民营医院专家首次榜上有名</strong>
</div>
<br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">“优秀医师不是我们身上披着名牌医院的白大衣，而是我们每一天踏实的工作和每一位我们救治过的患者对我们的认可和尊重。”这是北京三博脑科医院脊髓脊柱专家范涛教授在“北京优秀中青年医师”表彰大会上的发言。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 近日，由北京医师协会和北京医学奖励基金会共同主办的2012年度“北京优秀中青年医师”表彰大会隆重召开。该奖项是北京市医师行业的最高荣誉奖。与往年不同的是，本次评选首次将民营医院、驻京部队、中外合资医疗机构纳入评选范围，实现了北京地区所有医疗机构全覆盖。评选委员会共召开了五次全委会，对各单位推荐的优秀中青年医师候选人进行反复评审，从264名候选人中选出127名优秀中青年医师，另有60名医师获提名奖。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">&nbsp; &nbsp; 据悉，在127名优秀中青年医师中，有4人来自民营医院。其中，被选为医师代表在大会上发言的三博脑科医院专家范涛教授贡献突出，采用显微神经外科技术结合脊柱固定技术全方位开展脊髓脊柱手术，已有几千脊髓脊柱疾病患者受益于范教授的特色技术。</span>
