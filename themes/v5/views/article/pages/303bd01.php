			<div style="text-align:center;">
	<strong>上海利用人体自身组织为患者重建食管</strong>
</div>
<div style="text-align:center;">
	<br />
</div>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">新华网上海5月23日电（记者仇逸）记者从上海交通大学医学院附属仁济医院获悉，医院胸外科主任曹子昂联手头颈外科主任王家东日前突破性地采用人体自身组织，为一咽部完全闭塞患者重建颈段食管，并利用该段食管成功修复上消化道。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 32岁河南患者陈某2013年因误服强碱导致咽部及全食管严重化学性烧伤，口底以下完全闭锁，无法进食，并实施了永久性胃造瘘术。从此，病人只能通过腹管、无法通过口腔进食。经过一年的痛苦生活，2014年10月患者辗转至曹子昂教授处求治。为了恢复进食，患者表示宁可放弃自己的发声功能。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 考虑到患者还年轻，又是孩子的母亲，如果因为恢复进食而破坏了患者发音功能，必然会影响与孩子的交流。所以，曹子昂设计了一个大胆而创新的手术方案：利用自身组织建立一个人工食管通道，确定通道形成后进行消化道重建。</span>
</p>
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"><br />
</span>
</p>
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;"> 2014年11月3日，由曹子昂及郑家豪两位主任医师执刀，先在患者颈部做一个切口，利用颈部肌肉及原有闭锁食管的肌肉层，沿口腔方向构建一个管状结构。然后头颈外科科主任王家东从患者口腔底部做一人工隧道，与管状结构相贯通，再内置两根胃管作为支撑，形成一个开放性的备用人工食管。术后４个月，经过碘水造影检查，备用人工食管通畅性良好。于是，在2015年3月23日，曹子昂教授再次执刀，为病患实施了胃代食管消化道重建术，将胃部与人工食管进行通路吻合，同时封闭人工食道构建时留在颈部的开口，以及病人腹部原有的进食插管口。术后10天患者开始经口进食流质，半个月后开始半流质饮食。目前，这名患者已经康复出院。据文献检索，国内外相关资料鲜有类似案例的报道。</span><br />
