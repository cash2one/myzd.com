			<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>交医生朋友的利与弊</strong></span>
</div>
<br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生可以有很多朋友，可是和医生交朋友很难。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生没有和朋友聊天的习惯。即使参加聚会也通常要迟到。朋友约他吃饭，要么早退要么席间溜号，说是病人有事，医院呼叫。对于朋友的发财、提升，他并没有多大兴趣，似乎还没有他的病人是否发烧、术后有无排气更重要。人家是舍身为朋友，而医生是舍朋友为病人。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">你去找医生，十有八九要落空。在门诊，他没空儿出来;在实验室，他不能出来;在手术室，他无法出来。就是想去他的医院看病，给你搞个号就不错了，休想让他陪你跑前跑后。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生可以收到病人或家属一沓一摞的名片，其中不乏大官、大腕、大款，可他很少去理会。他很少给你名片，找医生有什么好事!别人道别说“再见”是真想再见，可谁愿意跟医生说再见?</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生生活枯燥，兴趣索然，除了那点医学，不知道还能想些、说些什么……</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">所以，别跟医生交朋友，医生朋友不好玩。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">可是，医生自有医生的哲学。至少他对朋友坦诚、真挚，也是一种职业本能。他少有势利，无论年迈的、贫穷的、丑陋的，他不会嫌弃与计较，因为他善于透视生命，尊重人性与人格。他不太在意别人对他的态度，冷暖或亲疏，譬如今天千恩万谢，明天忘之脑后，甚至翻脸不认。他能深刻理解人的本质，甚至细胞和基，他太了解人的理性与理智、情感与情绪的缺陷了。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生的宽容、友爱、仁慈和善良是其他职业者无法比拟的。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生也会调皮和幽默，在餐桌上，他大讲心肝脾肺，全然不顾医学局外人望着肉食菜肴而目瞪口呆，自个儿依然朵颐大开、甘之如饴。不过，他会告诉你防病的秘密、减肥利弊、“干净”与“不干净”的科学界限。</span><br />
<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">医生很少求人、麻烦人，喜欢淡如水的君子之交，也并非是自命清高，有时也是求人无觅处。朋友真遇有急事、要事，特别是涉及看病治病的事，他可是雷厉风行、分秒必争，而且帮忙到底，还要“追随”结果。</span><br />
<p>
	<span style="font-size:14px;font-family:'Microsoft YaHei';line-height:2;">如此看来，交个医生朋友也不错!</span>
</p>
