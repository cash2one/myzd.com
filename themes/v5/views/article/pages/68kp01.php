		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>表现为占位效应的颅内未破裂动脉瘤</strong></span>
</div>
<br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;</span><span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp;一、虽然随着目前无创诊断技术的进步，未破裂动脉瘤的发病率呈降低趋势。但Solomon等人报道，以占位效应为主要的表现的颅内未破裂动脉瘤的比例仍达到8%。脑神经麻痹是未破裂动脉瘤占位效应的典型表现。眼球运动障碍与动眼神经受累关系密切，而来自后交通动脉的动脉瘤常侵犯动眼神经，约7.2%--94%的未破裂后交通小动脉瘤会表现为动眼神经麻痹。虽然脉搏搏动冲击脑神经也是造成其麻痹的重要原因，但传统的观点仍认为脑神经麻痹主要是由于机械挤压造成的。</span>
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;1、血管内治疗存在占位效应的动脉瘤，虽然完全恢复的比例为32%-50%，但是，在血管内治疗后达75%的病例的占位效应会得到改善。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; &nbsp;2、减轻脑神经因脉搏搏动造成的挤压，可能是经血管内栓塞后，患者占位效应得到改善的原因。有趣的是，有3/4的患者经动脉瘤栓塞后其的脑神经麻痹症状会得到改善，即使动脉瘤的体积在栓塞后会明显增大，其脑神经麻痹也会得到改善。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 3、不论是开颅手术还是介入治疗，脑神经麻痹的时间越短，其症状恢复越好。一系列研究表明，症状在出现5天内进行治疗，脑神经麻痹症状甚至可完全恢复。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 二、后交通动脉动脉瘤造成的动脉神经麻痹</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;瞳孔对光反射可作为一种典型体征来鉴别动眼神经麻痹是因类似于后交通瘤压迫造成的，还是由于因糖尿病等因素造成神经内部损害造成的。位于神经表层的副交感神经很容易受到压迫而造成损害，而这些神经表纤维的损害肯表现为瞳孔对光反射消失。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp;反过来，糖尿病造成的动眼神经麻痹的典型表现，是由于供应神经的小血管缺血，由于供应神经的小血管是从外向内供血，因此神经中心部分更容易受到缺血造成损害，而神经外层的纤维不容易受伤损害。。因此，动眼神经麻痹造成的瞳孔散大一般不是由神经受到外部压迫（如后交通动脉瘤）造成的。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 术后眼球运动功能的恢复过程是可预测的。上睑下垂是最先好转的。但是完全恢复往往需要数个月时间。</span><br />
<p>
	<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">&nbsp; &nbsp; 而一些情况如糖尿病病史、高龄、延迟治疗以及完全动眼神经麻痹等，是造成栓塞术后动眼神经恢复不良的原因。</span>
</p>
