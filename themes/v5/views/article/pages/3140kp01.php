		<div style="text-align:center;">
	<span style="font-family:'Microsoft YaHei';font-size:18px;line-height:2;"><strong>许剑民：肝动脉灌注化疗可延长中晚期结直肠患者生命</strong></span>
</div>
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;">据了解，大肠癌包括结肠癌和直肠癌，临床上又被统称为结直肠癌。除了家族史，其他危险因素则包括高脂肪、高蛋白的饮食，缺乏运动等，因此，大肠癌又被称为“富癌”之一，在我国近年来，结直肠癌的发病率和死亡率呈现上升趋势，复旦大学附属中山医院许剑民教授在近日举行“POST ASCO GI 2015”会议上表示，早期治疗结直肠癌的5年生存率高达90%，但是一旦延误至晚期，生存率只有11.7%。遗憾的是，目前我国结直肠癌患者多为中晚期，因此如何进一步提高结直肠癌的临床疗效，成为了我国乃至世界胃肠专家攻克的目标。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"><img src="http://static.mingyizhudao.com/147010245017714" title="" alt="demo.jpg"/></span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 专家介绍：许剑民，卫生部大肠癌早诊早治专家组成员，卫生部《结直肠癌诊断和治疗标准》和《结直肠癌诊断和治疗规范》制定专家组成员，中国抗癌协会大肠癌专业委员以及大肠癌肝转移学组和遗传性大肠癌学组委员，国家自然基金和上海市科委基金评审专家，上海市疾病控制中心大肠癌中心组成员。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 直肠癌行辅助化疗VS术后辅助放疗谁更有效？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 术前放化疗是局部晚期直肠癌的标准治疗，在病人依从性、毒性、降期和保留肛门括约肌方面似乎优于术后放化疗。德国研究人员发现，在长期结局上术前放化疗生存优势并不明显。最新的长期随访结果发现，10年后，术前和术后放化疗的总体生存率类似，无疾病生存率也类似。但是，术前放化疗的局部控制率明显提高，与之前的研究符合。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 之前的研究来自于德国直肠癌研究组，是建立术前放化疗作为局部晚期直肠癌的标准疗法的重要依据。在这项之前的研究中，术前放化疗的使用，使局部失败率从13%下降到6%，保肛率从20%上升到39%，急性和晚期毒性发生率都有所下降。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 长期随访的结果证实了局部晚期直肠癌的标准治疗依然是新辅助放化疗，随后是全直肠系膜切除和辅助化疗。尽管新辅助治疗的一般方法得到了证实，但是直肠癌治疗还有进一步改善的空间。一个目标是，对于某些挑选的病人，在不损害局部控制和生存的情况下，应用较低强度的治疗方案修正新辅助治疗以提高生活质量。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 许剑民教授表示，最初的研究中，纳入了823名II到III期直肠癌患者，随机分配到有氟尿嘧啶（FU）的术前放化疗，全直肠系膜切除术，辅助FU化疗，或者同样的治疗时间表，但是放化疗在术后。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 5年随访结果在2004年公布，两组的整体存活率无差异。到2010年12月，平均随访134个月，有450名病人存活，随访期间死亡的341名病人中，223名病人与直肠癌有关。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 397名接受术前放化疗的病人中，23名局部复发，393名接受术后放化疗的病人中，37名局部复发。在意向性治疗人群中，5年和10年局部复发累积发生率情况为，术前放化疗为5.0%和7.1%，术后放化疗为9.7%和10.1%（P=0.048）。疾病转移的10年累积发生率两组间无差异。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 结直肠癌切除术后肝动脉灌注化疗对中晚期直肠癌有何帮助？</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 此次会上，许剑民教授还详细讲解了今年自己团队在ASCO GI研讨会上发表的研究，许教授表示，这项旨在评价一项多中心试验中，增加术前肝动脉和区域动脉灌注化疗（PHRAC）对II期和III结直肠癌预后的影响。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 许教授介绍，将临床II期或III期结直肠癌（CRC）患者随机分为PHRAC加辅助治疗（PHRAC组）或单独辅助治疗组（对照组）。主要终点指标是无病生存率（DFS）。次要终点指标是出现异时性肝转移，总生存期（OS）和安全性。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 研究发现，来自中国5家医疗中心的688例患者随机分到各组。PHRAC组和对照组的五年DFS分别为75％和61％（HR 0.60;95％CI，0.45-0.80; P &lt;0.001）。PHRAC组和对照组的五年肝脏转移率分别为8％和18％（HR0.39;95％CI，0.24-0.64; P &lt;0.001）。PHRAC组和对照组的五年OS分别为81％和72％（HR0.59;95％CI，0.42-0.84; P = 0.003）。两组间的发病率和死亡率没有显著差异。符合条件患者的结果几乎没有什么变化。亚组分析显示III期患者的DFS，肝转移率和OS存在显著差异，但II期患者则未见差异。</span><br />
<span style="font-family:'Microsoft YaHei';font-size:14px;line-height:2;"> 增加PHRAC可减少异时性肝转移率，提高III期CRC患者的DFS和OS，但不影响患者的安全性。</span><br />
