	<div align="center">
	<span style="font-size:18px;font-family:Microsoft YaHei;line-height:2;"><strong>切了半个大脑还能活人？</strong></span><br />
</div>
<span style="font-size:14px;font-family:Microsoft YaHei;"></span><br />
<p>
	<br />
</p>
<p>
	<br />
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">半脑人？还当了妈妈？什么人只有半个脑袋呢？</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">3月初，笔者随同解放军总医院第一附属医院神经外科副主任梁树立，驱车7小时，去辽宁阜新回访他的半脑切除患者—刘俏。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">初春的东北还相当寒冷，但敲开刘俏的家门后，立马被温暖所包围。“梁医生，你可是我们家的大恩人呀！没想到刘俏恢复得这么好，这真是比中一千万元的大奖还高兴呀！”刚一进门，刘俏妈就紧紧拉住梁树立的手，边说边流眼泪。刘俏不到两岁的儿子长得活泼可爱，在房间跑来跑去，刘俏紧跟着保护儿子……</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">故事还得从头说起。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">老摔跤原来是癫痫</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“人们常说小孩是三翻、六坐、八爬、周岁走，我们家刘俏七个多月就会爬了。” 刘俏妈妈挺骄傲的。可万万没想到，一次发烧彻底改变了孩子的命运。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">那是一个晚上，刘俏突然发烧。妈妈先给孩子喂了退烧药，第二天一早就抱着孩子跑到阜新市中心医院。这时孩子烧是退了，可直仰脖子，呼吸也很微弱，身上出现许多高粱米大小的紫癜，医生诊断是病毒性脑炎。经过23天的治疗，刘俏终于死里逃生。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">后来，孩子两三岁了还不会走路，左手左腿伸不直。13岁那年，刘俏先后给两条腿做了矫正手术。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">17岁那年，有一天刚走到学校门口，她突然摔倒在地。开始一直以为是腿的事，可在这之后，摔跤就成了常事，甚至听到汽车鸣笛也会摔倒。到医院检查，诊断为癫痫。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“怎么会是癫痫呢？癫痫不是都抽搐、吐白沫吗？”刘俏妈妈着急地问医生。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“引起癫痫病的发病原因有很多，颅内感染是诱发癫痫病发作的常见因素，尤其是成年以后所谓的晚发性癫痫，颅内感染所诱发的癫痫病所占比例就相对更高些，刘俏是典型的病毒性脑炎引起的颅内感染。”医生解释说，病毒性脑炎引起的癫痫是一种继发性癫痫，由于患者病毒性脑炎的病变部位和受累的程度不同，临床表现也不一样。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">家里的生活完全被打乱了，跑遍了大半个中国，吃了中药、西药、蒙药、偏方，刘俏就是不见好转，而且她的脾气也越来越大，经常发火、摔东西。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">需要切掉已经软化的右大脑</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2010年4月，爸妈带着刘俏到北京寻医。“白天在各医院来回跑，晚上回到租住的地下室，吃不好，睡不好。”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">跑了10多家，有医生拍胸脯保证说99%可以治好，只有解放军总医院第一附属医院神经外科副主任梁树立说治愈率有百分之六十几，好转率90%。梁树立说，刘俏的右侧大脑已经软化，需要做半脑切除术。这种手术他们已经做了20多例。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“切掉半个大脑，那人还能活吗？就算死不了，成了植物人怎么办……”</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">爸爸妈妈下不了这个决心。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">回到阜新后，他们专程到当地梁树立治疗过的癫痫患者家详细地了解手术情况，发现恢复得挺好。一家人合计后，第二次来到解放军总医院第一附属医院准备手术。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“我们复查了癫痫不发作时的脑电，还进一步做了发作期的脑电，结果发现这些癫痫电波来自右侧已经软化的大脑半球”，梁树立分析说，可我们依旧不能因为定位了癫痫病灶就贸然去切，因为大脑跟我们的行为活动，甚至呼吸心跳都息息相关，右侧大脑管着我们左侧肢体的活动，这也就是为什么刘俏从小左手左腿都伸不直的原因。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“这样的患者，除了做常规的功能核磁检查大脑各功能区的分布情况外，还必须要做Wada 氏试验。”梁树立介绍说，“我们先麻醉刘俏正常的左大脑半球，看右侧大脑是否放电，语言、思维以及两侧肢体的活动情况，然后再麻醉她软化了的右大脑半球，看左侧大脑情况。” 目的是精确定位并检测切除右侧大脑后会不会导致功能性损伤。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">Wada 氏试验证明，“刘俏的右侧大脑基本功能已经消失，而且90%以上的脑功能已经发生了转移。但是右侧大脑却还在时不时放电，干扰、影响着左侧大脑也不能好好工作。”梁树立说。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">成人大脑半球切除术成功了</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">人出生时，大脑的代偿能力是很惊人的。美国弗吉尼亚州瀑布教堂市已经40多岁的米歇尔·迈克，在母亲子宫中孕育时就曾遭遇中风，导致左半边大脑彻底摧毁，是名副其实的“半脑人”，右脑一直掌管着全身的功能，正常生活。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">当梁树立将右侧大脑切除的手术方案告诉刘俏一家人的时候，他们再度犹豫了。“纠结，煎熬，20年了，我觉得自己已经被活生生推到了崩溃的悬崖边上了。”刘俏想起了《中国达人秀》里用脚弹钢琴的刘伟的座右铭，“我的</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">生中只有两条路，要么赶紧死，要么精彩地活着。”最终，她选择了手术。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">2010年11月12日，梁树立亲自主刀。手术从早上8点半一直持续到下午5点。刘俏被推出手术室，轻轻叫了一声妈就又睡过去了，之后就推进了重症监护室。之后的几天里，分分秒秒都是揪心的煎熬。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“她爸一直待在地下室不出来，抽着闷烟，饭菜都得我端进去。虽然医生说了，需要时间恢复，可我们怎么能不担心呢？我每天都会到重症监护病房，隔着玻璃看躺在病床上的刘俏，祈祷刘俏能快一点醒来！”妈妈轻轻地抹着眼角的泪水。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“半脑切除会在颅内留下一个空腔，最容易发生周围脑组织复位移动塌陷，进而牵拉血管断裂出血或血管扭转形成梗塞缺血，引起脑功能障碍，所以需要给患者注射镇静剂，固定卧床位置。经过一段时间的恢复，颅内缝合的硬脑膜会逐渐愈合，人体脑脊液也逐渐将大脑空腔填满，这样健侧大脑就安全了，我们再将病人逐渐唤醒。” 梁树立非常理解家人焦急的心情，耐心地向家人解释。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术后第九天，妈妈刚走进科室走廊就被护士拉住，招呼着去看醒了的刘俏。刘俏躺在病床上冲妈妈笑。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">手术后第11天，刘俏在妈妈的搀扶下开始下地走路。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">20天后，刘俏康复出院。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">半脑切除术主要用于治疗幼儿半球性癫痫</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">刘俏手术成功，在阜新当地成了头号新闻。</span><br />
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">亲朋好友纷纷到家探望，发现刘俏脑子清楚，说话流畅，过去不听使唤的左手现在还可以干活，脾气也变好了。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">刘俏术后服用了8个月的抗癫痫药物后就彻底停药了，至今没有再犯过病。2012年春，刘俏结婚，年底生下聪明漂亮的儿子。刘俏妈在第一时间把喜讯告诉了梁树立医生。</span>
</p>
<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">其实，刘俏并不是梁树立的第一位接受半脑切除的成人患者。“在国外，半脑切除术主要用于治疗幼儿半球性癫痫，这是因为半球性癫痫多是在幼儿时期造成的，同时也因为幼儿的神经可塑性强，代偿功能转移快，即便术中出现偏瘫等并发症，术后也可以恢复好转。而成人神经代偿性较差，术后一旦出现偏瘫很难恢复，所以国内外对大脑半脑切除术治疗成人半球性癫痫少有研究。”梁树立介绍说，我们在临床实践中发现，成人半球性癫痫患者约占全部癫痫患者的1%-3%。这些病人发作频繁，药物难以控制，功能进行性损害，有的难以行走甚至瘫痪，本人和家人都非常痛苦。于是，从2006年开始，我们联合其他医院施行了成人大脑半球切除术的前瞻性研究。在25例手术患者中，22例术后无癫痫发作，认知与运动功能改善11例，无认知下降，运动功能受损2例，死亡率0。这表明，经过技术改良和熟练精细操作，可大大减轻半脑切除后的严重并发症。和幼儿相比，不仅预后和安全性无明显差异，而且成人术后对语言、运动、认知功能的损害相比幼儿更轻。</span><br />
<p>
	<span style="font-size:14px;font-family:Microsoft YaHei;line-height:2;">“当然，大脑半球切除术效果虽好，但主要适用于个别存在偏瘫和严重癫痫的特殊病人，所以成人手术应慎之又慎。”梁树立强调。</span>
</p>
