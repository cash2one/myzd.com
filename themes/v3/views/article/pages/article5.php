            <h3 align="center">8种常吃食物对应预防8种常见癌症</h3>
            <div class="mt30">
                肺癌、肝癌、胃癌、乳腺癌、肠癌、胰腺癌、皮肤癌、宫颈癌„„日常生活中经常看到这些真实的癌症实例，如何才能通过日常饮食可以有效预防癌症的发生呢？对于这8种常见癌症而言，我们平时吃的食物中就有它们的“克星”。
            </div>

            <li class="mt20">
                <b>肺癌</b>
                <br>抗癌明星：菠菜 
                <p>
                    上榜理由：菠菜中含有多种抗氧化物，有助于预防自由基损伤造成的癌症。每天吃1碗菠菜可使患肺癌的几率至少降低一半。此外，番茄、胡萝卜、南瓜、梨和苹果也都可以预防肺癌的发生。
                </p>
            <center>	
                <img src="../upload/article/article5.1.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>乳腺癌</b>
                <br>抗癌明星：海带 
                <p>
                    上榜理由：海带不但含有丰富的维生素E和食物纤维，还含有微量元素碘。而科学家认为，缺碘是乳腺癌的致病因素之一，因而常吃海带有助于预防乳腺癌。      日本女性乳腺癌发病率低，可能与经常摄入海带等海藻类食物有关。同时，红薯、番茄、菱角、荸荠、豆类食品也是能预防乳腺癌的美食。
                </p>	
            <center>
                <img src="../upload/article/article5.2.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>肠癌</b>
                <br>抗癌明星：茭白
                <p>
                    上榜理由：茭白、芹菜类食物富含纤维，食物进入肠道后，可加快其中食物残渣的排空速度，缩短食物中有毒物质在肠道内滞留的时间，促进胆汁酸的排泄，对预防大肠癌极为有效。此外，经常食用大蒜，也可使患结肠癌的风险降低30%。同时，红薯、卷心菜、麦麸也是极其重要的预防肠癌的食物。
                </p>
            <center>
                <img src="../upload/article/article5.3.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>胰腺癌</b>
                <br>抗癌明星：菜花
                <p>
                    上榜理由：西兰花等十字花科食物，能够降低患胰腺癌的风险。研究指出，这可能与食物中含有的天然叶酸有关。同时，由于萝卜中含有芥子油，它和萝卜中的酶能互相作用，具有促进胃肠蠕动、增进食欲、帮助消化的功效。近年来，研究发现，萝卜因此也有助于抵抗胰腺癌。
                </p>
            <center>	
                <img src="../upload/article/article5.4.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>宫颈癌 </b>
                <br>抗癌明星：黄豆
                <p>
                    上榜理由：用黄豆制成的豆腐、豆浆，可以补充植物雌激素，它所含有的异黄酮、木质素都被认为有抗氧化的作用，能抑制宫颈癌的生长，减少癌细胞的分裂，同时有效阻止肿瘤转移。此外，酸梅、西红柿也是很好的预防宫颈癌的食物。
                </p>	
            <center>
                <img src="../upload/article/article5.5.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>胃癌 </b>
                <br>抗癌明星：大蒜
                <p>
                    上榜理由：常吃生大蒜的人，胃癌发病率非常低，原因是大蒜能显著降低胃中亚硝酸盐含量，而这是胃癌非常重要的诱因。而经常吃洋葱的人，胃癌发病率比少吃或不吃洋葱的人也要低25%。此外，少吃腌渍食品、熏鱼烤鱼也是预防胃癌的好方法。
                </p>
            <center>	
                <img src="../upload/article/article5.6.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>肝癌 </b>
                <br>抗癌明星：蘑菇
                <p>
                    上榜理由：有“抗癌第一菜”的美誉，比如菜蘑、口蘑、香菇等，由于含有多糖体类的抗癌活性物质，可以促进抗体形成，使机体对肿瘤产生免疫能力，抑制肿瘤细胞生长，可以抵抗包括淋巴瘤、肠癌等在内的多种癌症，特别是对肝癌病人，很有益处。
                </p>
            <center>	
                <img src="../upload/article/article5.7.jpg">
            </center>
            </li>
            <li class="mt20">
                <b>皮肤癌</b>
                <br>抗癌明星：芦笋
                <p>
                    上榜理由：芦笋是近年风靡世界餐桌的蔬菜之一，含有丰富的维生素、芦丁、核酸等成分，对淋巴瘤、膀胱癌、皮肤癌有一定疗效。
                </p>
            <center>	
                <img src="../upload/article/article5.8.jpg">
            </center>
            </li>

