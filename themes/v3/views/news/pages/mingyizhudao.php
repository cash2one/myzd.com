        <div class="col-sm-9 news-content pt10">
            <h3 style="text-align: center;">
                <span style="font-family: 宋体; font-size: 20px;">名医主刀：移动医疗手术</span><span style="font-size: 20px; font-family:Times New Roman;">O2O</span><br/>
            </h3>
            <p style="text-align: center;">
                <span style=";font-family:宋体;font-size:14px">本报记者&nbsp;刘涌</span>
            </p>
            <p style="text-align: center;">
                <span style=";font-family:宋体;font-size:14px">实习记者&nbsp;林鸣&nbsp;北京报道</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">见面时，苏舒一脸兴奋。在过去的一个星期当中，苏舒创办的“名医主刀”收获颇丰。先是获得了真格基金<span style="font-family:Times New Roman">500</span><span style="font-family:宋体">万人民币的天使投资，随后又在北京之行中与数位大牌专家达成了战略合作。签约的文件已然装满了苏舒手里的文件袋。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">名医主刀看上去像是又一个医生集团概念的组织：在自己的平台上汇聚愿意多点执业专家资源，为患者提供医疗服务。连形式看起来都一样，苏舒现在也忙着与各地的医疗专家签约。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">但苏舒的思路却完全不一样。他是要借助互联网的方式，以患者的手术需求作为切入口，打造一个移动医疗手术<span style="font-family:Times New Roman">O2O</span><span style="font-family:宋体">的平台。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">按照当下移动医疗的惯常思路，要做手术<span style="font-family:Times New Roman">O2O</span><span style="font-family:宋体">平台，需要“圈占”足够数量的大医院和优秀医生，拥有足够多的医疗资源后便不愁患者上门。这也正是当前国内移动医疗领域变得愈发没有生气的主要原因。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">作为“<span style="font-family:Times New Roman">80</span><span style="font-family:宋体">后”，长期在海外学习工作且仅仅在一年前才回国的苏舒，虽然出身在一个有着深厚医疗背景的家庭，但手中所能“掌握”的医疗资源甚至不足以帮助他身患癌症的外公找到可以可供治疗的肿瘤医院。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">这个故事情节简单得有点像心灵鸡汤。但就是在此之后，金融行业出身的苏舒决定创业，为了改变医疗现状做些努力。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">2014<span style="font-family:宋体">年</span><span style="font-family:Times New Roman">10</span><span style="font-family:宋体">月份，名医主刀成立，各个部门加在一起的创业团队有</span><span style="font-family:Times New Roman">20</span><span style="font-family:宋体">人左右。从成立至今的大半年时间里，名医主刀总共帮助</span><span style="font-family:Times New Roman">200</span><span style="font-family:宋体">位患者完成了手术。这个数量级相比动辄烧钱过亿、用户千万的互联网巨头而言尚微不足道，但仍然证明一点：苏舒的思路可行。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">简单的说，苏舒希望在医疗领域借鉴优步、滴滴打车的模式。名医主刀作为一个平台，为有不同需求的医院、医生、患者三方提供对接和服务，且不与三方发生利益联系。苏舒希望，通过最简单的<span style="font-family:Times New Roman">C2C</span><span style="font-family:宋体">模式来实现医疗资源的优化配置。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="line-height: 1.5em;">
                <span style=";font-family:宋体;font-weight:bold;font-size:14px">1<span style="font-family:宋体">、满足医院、医生、患者三方需求</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">虽然看病难、看病贵是当前新医改所着力解决的问题，但真正人满为患的只是极少数大医院。不少医院虽然硬件条件好、医疗等级高，但却受多种因素的影响而缺少患者。对于这类医院而言，基于存在大量的闲置医疗资源，最主要的需求自然是吸引患者。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“在医疗机构的选择上，我们的主要标准是二甲以上的医疗机构。”苏舒对<span style="font-family:Times New Roman">21</span><span style="font-family:宋体">世纪经济报道记者表示，“这些医院有些是政府举办的公立医院，有些是很优秀的民营医院，但他们仍然有大量空置病床可供利用。”</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">目前，“名医主刀”已经与北京和睦家医院、上海天坛普华医院和南京医科大学第二附属医院等一批优质的公立及私立医院建立了战略合作关系，随时可以提供大量的床位和手术室资源。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“在北京、上海、杭州、南京等这几个我们已经布点的城市，每天基本都有<span style="font-family:Times New Roman">1000</span><span style="font-family:宋体">张床位可供我们使用。”苏舒说。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">医生的需求则包括两个方面，一个是经济价值的体现，医生需要在扭曲的定价体系外，通过自己的努力来获得应有的劳动价值；另一个是社会价值，为更多有需求的患者提供必要的医疗服务，也是大多数医生的愿望。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">实际上，大部分医生集团的成立、自由执业医生的出现等，都是受到了上述两个因素的影响。但名医主刀在短时间里获得的进展速度仍然显得出人意料。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">截止今年<span style="font-family:Times New Roman">7</span><span style="font-family:宋体">月份，名医主刀已经与北京、上海等一线城市数千名三甲医院主任级别的专家签订了战略合作协议，包括肝胆领域泰斗中国工程院院士王学浩，甲状腺顶级专家北京协和医院普外科主任刘跃武，白内障专家北京同仁医院朱思泉主任等国内顶尖的名医专家。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">苏舒毫不含糊的说，名医主刀现在的合作专家已经基本涵盖了北京所有顶尖三甲医院的各个科室，以及上海大多数三甲医院的主要科室。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">不过，与动辄宣称万名医生加盟的众多医生集团相比，苏舒的“数据”看起来并没有那么惊艳。但是从操作思路的角度，与众多医生集团明显不同，名医主刀的可以称得上是“超轻模式”。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“我们与签约的医生之间不存在任何利益上的关联。这些专家向我们提供的主要是他们可供利用的业余时间。这样的话，患者通过我们这个平台就可以找到想看的专家，并且在专家的空余时间获得治疗。”苏舒说。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">患者角度的需求则非常明显，就是希望能够找到针对自身的病情提供最好治疗的专家。而在看病难的大背景下，这几乎是一个十分奢望的需求。看病难也成为新医改、互联网医疗所要解决的主要痛点之一。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="line-height: 1.5em;">
                <span style=";font-family:宋体;font-weight:bold;font-size:14px">2<span style="font-family:宋体">、好看病到看好病</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">看病难首先表现在挂号难，大医院、大专家“一号难求”让众多求医患者叫苦不迭。不少互联网医疗公司便以此为切入点，解决患者挂号难的问题。比如提供在线咨询、电话咨询、加号服务等，帮助患者能够找到需要的专家看病。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">但问题是，对于很多需要做手术的患者而言，即便是真的能够看到需要的专家，由于公立医院的医疗资源有限、没有空余的床位，依然无法顺利进行手术而获得治疗。这就是名医主刀希望推动解决的另一个层面的问题。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“现在大家都在解决的是‘好看病’的问题，就是如何能够让患者看上想看的专家。而我们要解决的是‘看好病’的问题。”苏舒认为，“看好病”才是更加根本的问题，尤其是对于需要手术的患者来说，“看好病”才是真正核心的需求。“所以，我们选择手术作为进入医疗领域的切口。”苏舒说。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">面对都有各自需求的医院、医生和患者三方，名医主刀为三方资源的合理匹配搭建一个平台。在这个平台上，患者找到需要的医生，医生找到需要的床位，医院找到需要的患者。而名医主刀作为一个平台，则不与三方中的任何一方发生利益上的联系。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">不过，对于名医主刀来说仍然有一个问题需要解决，谁才是真正需要手术的患者？如果这个问题不解决，名医主刀很可能沦为又一个挂号问诊平台。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“我们自己建立了一个分诊的团队，由两名具有长期医生执业经历团队成员负责。对于前来就医的患者，我们会首先做一个甄别，挑选出真正需要做手术的患者。”苏舒说，“另外，这里面也有一个经济杠杆在起作用，毕竟做手术、请专家的费用要比看一次普通门诊的费用高得多。”</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">某种程度上也是在经济成本上的降低，帮助名医主刀能够迅速获得医生专家的认可。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">通常情况下，医生利用业余时间出诊的医院往往是在外地，这极大增加了医生出诊的成本。而名医主刀利用互联网的模式进行资源匹配，能够帮助医生在同城范围内获得出诊机会。这极大的降低了医生出诊的成本。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">这种模式也是苏舒所理解的互联网医疗。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“我们显然不能颠覆医疗技术本身，也无力改变现有的医疗体制。”苏舒说，“但我们能够做的是利用互联网的工具，在医疗资源的优化配置反面做出改进，让更多的人能够获得优质的医疗服务。”</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="line-height: 1.5em;">
                <span style=";font-family:宋体;font-weight:bold;font-size:14px">3<span style="font-family:宋体">、一个电话决定下来的投资</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">&nbsp;</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">从解决看病难的角度，目前互联网医疗公司大体存在着三种思路。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">一种是以春雨医生为代表的模式，利用移动互联网的“众包模式”，通过解决患者的普通病、常见病以及基本的健康需求，来减少大医院的就诊压力；</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">一种是大量互联网医疗公司的思路，利用互联网高效便捷的特点，来提高医疗服务的效率，包括医生在诊前、诊中、诊后的效率，医院在服务流程中的效率，以及患者在就医过程中的效率；</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">一种是名医主刀的思路，通过将闲置资源进行优化配置，来解决看病难的问题。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">三种模式的优劣很难简单评价，虽然作为新生事物且受多种因素的束缚，互联网医疗公司至今没有产生盈利模式，但不能否认，这些技术在医疗领域的应用的确给患者就医的便利程度带来了很大的改进。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">总体上看，第二种思路目前在互联网医疗领域占据主导地位，即以医疗资源为中心的模式。但这种模式也存在一定的隐忧。这些互联网医疗公司往往“圈占”的是大医院，在医疗体制没有改变的情况下，大医院服务效率的提高是否反而会吸引更多前往就医的患者。这种趋势则是与整个新医改的方向相悖的。</span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">目前，第三种思路则并不常见。尤其是，与大多数互联网医疗公司<span style="font-family:Times New Roman">B2B</span><span style="font-family:宋体">、</span><span style="font-family:Times New Roman">B2C</span><span style="font-family:宋体">模式不同的是，名医主刀本质上是</span><span style="font-family:Times New Roman">C2C</span><span style="font-family:宋体">的模式，即患者直接与医生发生联系。但苏舒正是凭借着这个思路，在一个电话里打动了真格基金的创始人徐小平。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">看了《中国合伙人》超过<span style="font-family:Times New Roman">10</span><span style="font-family:宋体">遍的苏舒，对徐小平有着特别的景仰。而他自己的创业项目，在如此短的时间里获得真格基金的投资，更是让苏舒异常惊喜。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
                <span style=";font-family:宋体;font-size:14px">“我当时去一个投资大会现场，有<span style="font-family:Times New Roman">100</span><span style="font-family:宋体">多位投资人在。我把名医主刀的模式讲给当时在现场的真格基金另一位创始人王强，只用了</span><span style="font-family:Times New Roman">10</span><span style="font-family:宋体">分钟就决定投我们。”苏舒仍然觉得这有些不可思议。后来在通了一个小时电话之后，徐小平正式拍板，投资名医主刀项目。</span></span>
            </p>
            <p style="text-indent: 28px; line-height: 1.5em;">
